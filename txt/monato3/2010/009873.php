      <h3>Turismo</h3>
      <h3 class="tem">BELGIO</h3>
      <h2>Bunkro remalkovrita</h2>
      <!-- komenco de la enhavo -->

       

       <p>La belga urbeto Ieper [ipr], situanta en sud-okcidenta Flandrio, troviĝis dum la unua mondmilito en la batalkampo. Ĝi estis komplete detruita kaj post la milito rekonstruita. Nun ĝi estas turisma centro lige kun militaj memoraĵoj. Baldaŭ al ili aldoniĝos plia allogaĵo. Dekon da kilometroj for de Ieper, en la vilaĝo Kemmel (parto de la municipo Heuvelland), estas monteto, ĉirkaŭ kiu troviĝas kelkaj bunkroj kaj aliaj restaĵoj de la unua mondmilito. Tamen, sub la monteto ankaŭ estas apenaŭ konata bunkro, pli nova kaj multe pli granda.</p> <h3>Malvarma milito</h3> <p>Post la dua mondmilito ekestis <i>malvarma milito</i> inter Usono kaj Sovetio, en kiu partoprenis ankaŭ iliaj aliancanoj. Dum tiu milito estis elspezitaj grandegaj sumoj por armado. Enkadre de tio en 1948 Francio, Britio, Belgio, Nederlando kaj Luksemburgo subskribis la Traktaton de Bruselo por kunordigi sian aerdefendon. Por loki unu el ties komandejoj, en 1953 estis fosita grandega truo sub la Kemmel-monteto. En ĝi oni konstruis trietaĝan bunkron sur areo de 2500 kvadrataj metroj.</p> <p>La bunkro estis uzata nur dum mallonga tempo, ĉar kelkajn jarojn poste fondiĝis NATO. Ĉi-lasta kreis sian propran aerdefendan sistemon. En 1963 la belga armeo ekuzis la bunkron por instali sekuran komandejon, uzotan dum milito. Ĝi estis konektita al civilaj kaj militistaj komunikadsistemoj per la tiutempe plej modernaj ekipaĵoj disponeblaj. Kelkfoje jare en ĝi okazis ekzercoj. Post la fino de la malvarma milito en 1995 ĝi estis definitive forlasita.</p> <h3>Sekreto malkaŝita</h3> <p>En la maldense loĝata regiono nur malmultaj homoj sciis pri la bunkro kaj post 1995 ĝi estis preskaŭ forgesita. En 2003 loka komitato sukcesis aranĝi viziton por manpleno da civiluloj. Dum kelkaj jaroj oni diskutis pri ebla reuzo de la komandejo. Finfine la respondeca ministro decidis, ke ĝi iĝu muzeo pri la malvarma milito, prizorgota de la belga militmuzeo (oficiala nomo: Reĝa Muzeo de la Armeo kaj Milita Historio, <a href="http://www.klm-mra.be">www.klm-mra.be</a>).</p> <p>En la muzeo eblos vidi kiel funkciis la administracio (ĉar la armea komandejo ja estas administracio) en la 60aj jaroj, sen komputiloj, nur kun skribmaŝinoj, telefonoj, grandegaj murmapoj kaj – kiel plej avangardaj komunikiloj – kelkaj teleksoj por sendi kaj ricevi ĉifritajn mesaĝojn.</p> <p>La malfermo de la muzeo jam kelkfoje estis prokrastita, sed antaŭ nelonge oni firme promesis, ke fine de 2009 oni oficiale anoncos ĝin sur la retpaĝo de militmuzeo.</p> <div style="text-align:right">Roland R<span class="mm">OTSAERT</span></div>   
