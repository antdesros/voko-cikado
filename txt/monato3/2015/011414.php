      <h3>Scienco</h3>
      <h3 class="tem">MAMUTOJ</h3>
      <h2>Glaciaj mumioj sorĉas Aŭstrion</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Fascinaj kiel dinosaŭroj: estas tiel priskribeblaj la mamutoj, kiuj vivis ĝis antaŭ
      3700 jaroj en la nordaj regionoj de Eŭropo, Azio kaj Ameriko. Aparta ekspozicio kun
      la titolo „Mamutoj, glaciaj mumioj el Siberio” okazas ĝis la komenco de marto 2015 en la Naturhistoria Muzeo de Vieno.
   </b>
</p> 
<p>En la ĉefurbo de Aŭstrio, – fosante norde de la „Stefan-katedralo”, germane 
   <i>Stefansdom</i>, – oni trovis en la jaro 1443 femuran oston: tiu, je unu metro longa, apartenis al mamuto.
   Tiam oni pensis: ĉu restaĵo de giganto (germane 
   <i>Riese</i>)? Kaj la osto estis sekve ekspoziciata ĉe la „Gigantopordego” (<i>Riesentor</i>) de la menciita katedralo.
</p> 
<h3>Donacita elefanto</h3> 
<p>Pasis jarcentoj ĝis la loĝantoj de Vieno vidis unuafoje en sia urbo elefanton, kiu
   estis nomita Soliman. Tiu naskiĝis ĉirkaŭ la jaro 1540 en Hindio, estis donacita al
   Aŭstrio kaj mortis en 1553 – unu semajnon antaŭ Kristnasko – en Vieno. Ekzistas tiurilata artikolo en Vikipedio (serĉu per „Soliman (elefanto)”).
</p> 
<h3>Genaj sciencistoj</h3> 
<p>Mamutoj, nun jam malaperintaj, apartenas al la familio de la tiel nomataj elefantedoj
   (latinlingve 
   <i>elephantidae</i>). Ekzistis proksimume dek specioj. Iliaj alteco kaj maso estis tre similaj al tiuj
   de la hodiaŭaj afrikaj elefantoj. Ankoraŭ ne sukcesis genaj sciencistoj revivigi mamutojn:
   la DNA-ĉeno disfalis al multaj mallongaj pecoj kaj estas malfacile ilin kunigi.
</p> 
<h3>Dentoj kaj skeletoj</h3> 
<p>Ĝis la 2a de marto 2015 la Naturhistoria Muzeo (mallonge 
   <i>Nhm</i>) en Vieno montras al la publiko sesdek objektojn: temas pri dentoj, skeletoj, mumioj
   de mamutoj. 30 objektoj venas el Sankt-Peterburgo (Ruslando), dum 15 el Vieno mem.
   La objektoj estas duone originalaj, duone kopioj.
</p> 
<h3>Siberia rivero</h3> 
<p>Antaŭ 45 000 jaroj vivis la ina mamutido Kroma, kies kadavron oni malkovris en 2008
   en la norda Jakutio. La nomo aludas al la siberia rivero Kroma, kiu fluas tie apude.
   La korpo de Kroma estis fiksita en la ĉiamfrosta grundo. Kelkaj supraj partoj mankas:
   verŝajne manĝis ilin korvoj kaj vulpoj. La cetera parto de la korpo restas ĝis nun
   profunde frostigita kaj admireblas en vitrino je -22 celsiaj gradoj. Kiam Kroma mortis,
   ŝi aĝis nur du monatojn. En ŝiaj naza kavo, faŭko kaj traĥeo oni trovis ŝlimon. Verŝajne
   ŝi sufokiĝis pro ŝlima lavango. En ŝia stomako oni trovis digestitajn plantojn kaj
   iomete da patrina lakto, do ni komprenas, ke Kroma estis bonorde nutrita.
</p> 
<h3>Falinta mamuto</h3> 
<p>Antaŭ 40 mil jaroj vivis la vira mamutido Dima, kies korpo estas ĝis nun komplete
   konservita. Dima malsanis, kiam li mortis en la aĝo de duona, maksimume unu, jaro.
   La mumio enhavas ĉiujn internajn organojn; krome konserviĝis ĝiaj oreloj, rostro,
   peniso kaj parto de la felo. La longeco estis 90 cm, la maso 110 kg. Multaj parazitoj
   kaj vundo malfortigis la beston. En ĝiaj intestoj oni trovis ne multe da manĝaĵo.
   Verŝajne Dima falis en truon kaj ne povis sin liberigi.
</p> 
<h3>Ĉiamfrosta grundo</h3> 
<p>En la ĉiamfrosta grundo de la norda Siberio certe kuŝas multaj miloj da frostigitaj
   mamutoj. Ĉiujare oni trovas kelkajn. Estas permesite libere vendi la eburon de mamutoj,
   sed ne tiun de elefantoj: tial elefanta eburo estas ofte vendita kiel mamuta.
</p> 
<h3>Studenta rabato</h3> 
<p>Por pliaj informoj, oni vizitu la retejon de la muzeo: 
   <a href="http://www.nhm-wien.ac.at/presse">www.nhm-wien.ac.at/presse</a>. La ekspozicio estas malfermita ĉiutage krom marde, inter la 9a matene kaj la 6a
   kaj duono posttagmeze; merkrede ĝis la 9a vespere. La eniro kostas 10 eŭrojn, sed
   studentoj ĝuas 50-procentan rabaton. Oni uzu la metroliniojn U2 kaj U3 ĝis la stacio
   
   <i>Volkstheater</i>.
</p> 
