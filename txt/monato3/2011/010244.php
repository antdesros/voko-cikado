      <h3>Politiko</h3>
      <h3 class="tem">TERORISMO</h3>
      <h2>Mitralo en la kuirejo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kiam la germana ministro pri internaj aferoj, Thomas de Maizière, kristandemokrato, avertis en novembro, ke Germanio troviĝas antaŭ atako de islamismaj ekstremistoj, la polico gardis laŭsupoze endanĝerigitajn lokojn kiel stacidomojn kaj flughavenojn.</p> <p>En la federacia lando Berlino Ehrhart Körting, la ministro pri berlinaj internaj aferoj, kaj socialdemokrato, ne volis aspekti malpli zorgema ol la konkurencpartia ŝtata ministro.</p> <h3>Instancoj</h3> <p>Körting siaflanke avertis la publikon, maksimume laŭvorte, tiel: „Se ni en la najbaraĵo rimarkas ion, kiel jen subite estas lokitaj tri iom strange aspektantaj homoj, kiuj neniam vidigas sin aŭ simile, kaj kiuj parolas la araban aŭ fremdan lingvon, kiun ni ne komprenas, tiam oni pripensu, mi kredas, ke oni informu la ŝtatajn instancojn, kio tie okazas.”</p> <p>Pro tiu eldiro la politikisto rikoltis abundajn reagojn, ofendiĝojn, indigniĝojn kaj mokegojn. Kial ĝuste tri personoj? Kiam homo aspektas strange? Kiu germano tuj ekkonas la araban lingvon? Kiun fremdan lingvojn „ni” ne komprenas? Kio okazas ĉe tri homoj, kiuj ne vidigas sin?</p> <h3>Fremduloj</h3> <p>Ĉu bavaro en tipa kostumo en Berlino ne aspektas strange, kaŝas sin precipe en tribaj bierdrinkejoj, parolas lingvon, kiun prusoj ne komprenas? Kaj se tiaj fremduloj aperas eĉ triope, okupiĝante pri la hejma ŝatokupo, la sekreta fenestrumado (kuraĝa noktomeza tragrimpado de fenestro al la ĉambro de amatino)? Ĉu teroristoj?</p> <p>Poste Körting per „ĝustigo” koncedis, ke la eldiroj estis „eble malkonvenaj”. Li klopodis precizigi sian alvokon: „Mi parolis pri seriozaj indikoj pri armiloj aŭ suspektindaj pakaĵoj, ne pri denuncado. Ne temas pri denuncado, se oni vokas la policon, vidinte novajn najbarojn umantajn kun mitralo en la kuirejo. Tio cetere validas ankaŭ por muzulmanoj en Berlino. Kiu hazarde aŭskultas en moskeo suspektindan interparoladon, tuj anoncu sin ĉe la sekurigaj instancoj.”</p> <h3>Kolegoj</h3> <p>Ankaŭ tiuj „klarigoj” ricevis la saman akcepton de la publiko kaj ne povis forviŝi la impreson, ke tiu ĉi funkciulo ne estas kapabla efike agi en kriza situacio. Kaj li vekas suspekton ankaŭ kontraŭ la kolegoj, kiuj pli lerte povas komuniki (aŭ kaŝi) siajn ideojn.</p> <p>Estas malfacile distingi inter insida denuncado kaj informo al la polico, kiu povas eviti krimon. Pli malfacile estas taksi homon laŭ aspekto, konduto kaj lingvo. Plej malfacile, almenaŭ por politikistoj, ŝajnas esti komprenigi sin nemiskompreneble, eĉ se ili parolas „nian” lingvon.</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div>   
