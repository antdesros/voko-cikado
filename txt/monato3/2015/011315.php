      <h3>Lingvo</h3>
      <h3 class="tem">NORDA EŬROPO</h3>
      <h2>La islanda: revo de ne plu juna lernemulo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En julio 2013 mia edzino Máire [majre] kaj mi pasigis naŭ agrablajn tagojn en Rejkjaviko,
   la pitoreska ĉefurbo de Islando. Ni havis la eblon ekskursi al kelkaj el la ĉirkaŭaj
   vidindaĵoj, kaj la sperto tiom ravis mian edzinon, ke ni devis revojaĝi al Islando
   en la aŭtuno de la sama jaro por konatiĝi kun alia parto de la lando. Ĉi-foje post
   unu nokto en Rejkjaviko ni flugis per malgranda aviadilo al Akureyri, en la nordo,
   multe pli proksime al la Arkta Cirklo. Tie Máire esperis spekti la borealan aŭroron,
   sed, kvankam ni dufoje eliris dumnokte kun grupeto da turistoj, ni ne sukcesis vidi
   ĝin. Malgraŭ tiu eta disreviĝo, ni ne malĝojis, ĉar ni havis la okazon ekskursi sur
   ŝipeto kaj spekti grandajn balenojn en la longa fjordo, apud kiu staras Akureyri,
   la dua plej granda urbo en Islando.
</p> 
<p>Akureyri estas bela, bonveniga kaj vere ĉarma urbeto, kaj ni povas varme rekomendi
   la simpatian stabanaron de la tiea turisma entrepreno 
   <i>Saga</i>. En oktobro, kiam ni estis tie, la vetero tute ne estis malvarma kaj la plantoj (alpaj
   edelvejsoj kaj moder-klimataj trikoloretoj kune!) bunte floris sub maldensa polvaĵo
   de neĝo en la malgranda botanika ĝardeno, unu el la allogaĵoj de la urbo. (Alia vidindaĵo
   estas la apuda arbaro, unu el la malmultaj en Islando.)
</p> 
<h3>Sagaoj</h3> 
<p>Dum Máire entuziasmiĝis pri la naturo kaj pri la urbo, kun ĝiaj granda biblioteko
   kaj hejmeca kafejo 
   <i>La Blua Kruĉo</i>, kie ni kaftrinkis ĉiumatene, mi eĉ pli interesiĝis pri la fascina islanda lingvo.
   Por mi, tiu fascino baziĝas sur la fakto, ke la islanda lingvo apenaŭ ŝanĝiĝis ekde
   la tempo de la sagaoj, kaj krome ĝi firme rezistas la enkondukon de fremdaj vortoj,
   preferante formi novan terminaron el siaj propraj radikoj. Bedaŭrinde, la malfacila
   gramatiko kaj iom bizara fonetiko fortimigas iun ajn, kiu volas studi ĝin. Oni sentas
   sin same fortimigita, kiam oni provas lerni kaj uzi iun ne tre disvastiĝintan lingvon,
   ĉar – kutime – la denaskaj parolantoj de la idiomo kutime respondas al la fremda lingvoentuziasmulo
   en pli monde konata lingvo, kaj en Islando temas ĉiam pri la usonangla.
</p> 
<p>En Rejkjaviko ni renkontis norvegan esperantiston, kiu plendis, ke islandanoj konstante
   respondas angle, kiam li alparolas ilin en la nova norvega, kiu estas speco de simpligita
   islanda. Simila estis mia senkuraĝiga sperto en Skandinavio kaj en Nederlando. Mi
   ofte demandas min, ĉu tiuj landoj, kie laŭŝajne preskaŭ ĉiuj parolas la anglan, ne
   finfine perdos siajn naciajn idiomojn. Ĉie en la mondo minacas la senkolora, malbunta
   monokulturo.
</p> 
<h3>Monaĥoj</h3> 
<p>Eble mi devus klarigi, ke ni estas skotdevenaj kaj kutime parolas la skot-gaelan (ofte
   kune kun Esperanto) en nia hejmo kaj ene de nia familio. La gaela (ĉu skota, ĉu irlanda)
   tute ne similas la islandan, sed en Skotlando la gaela estis iom influita de ĝi. La
   gaela apartenas al la kelta lingva familio, dum la islanda estas ano de la nord-ĝermana,
   kaj specife de la skandinava subfamilio.
</p> 
<p>La rilatoj inter la skandinavoj kaj la gaeloj estis daŭraj kaj intimaj. Inter la 8a
   kaj la 15a jarcentoj, la vikingoj iom post iom okupis la nordajn kaj okcidentajn insulojn
   de Skotlando, kaj aldone parton de la nord-orienta ĉeflando. Kiam norvegoj atingis
   Islandon en la 9a jarcento, ili jam renkontis gaelajn monaĥojn, kiuj venis al la insulo
   proksimume en la jaro 800. Do, la unua lingvo parolata en Islando estis la gaela,
   ne la antikva norvega.
</p> 
<h3>Sklavinoj</h3> 
<p>Plie dum siaj rabekskursoj, la vikingoj ŝtelis virinojn el Irlando kaj Skotlando kaj
   retenis ilin kiel sklavinojn. La rezulto de tiu movado de homoj inter la skota kaj
   la skandinava teritorioj jenas: kaj en la Hebridoj kaj en Islando estiĝis miksita
   norvega-gaela popolo. Supozeble en Islando, la gaelaj genoj, konstateblaj en la hodiaŭa
   loĝantaro, devenas de la virina (mitokondria) flanko. Mi ne sukcesis trovi restaĵojn
   de la gaela lingvo en la islanda lingvo, sed kelkaj gaelaj personaj nomoj troviĝas
   inter la islandanoj. Inverse, kelkaj skandinavaj vortoj (kvankam nemultaj) eniris
   la gaelan: temas precipe pri terminoj, kiuj rilatas al la maro. Plie, kelkaj gaelaj
   personaj nomoj estas gaeligitaj formoj de skandinavaj nomoj.
</p> 
<p>La loĝantoj de la nordaj insuloj (Ŝetlandaj kaj Orkadaj) dum jarcentoj retenis sian
   skandinavan dialekton, sed la vikingoj en la okcidentaj Hebridoj perdis ĝin kaj adoptis
   la gaelan. Sekve, ni konstatas la strangan fakton, ke, dum la lingvo de la hebridanoj
   estas la gaela, preskaŭ ĉiuj el la tieaj loknomoj estas skandinavaj, kvankam en gaeligita
   formo. Sur la skotlanda ĉeflando, tamen, la plimulto de la loknomoj estas pure gaelaj.
   Eĉ en la sud-orienta parto apud la landlimo kun Anglio gaelaj loknomoj ne forestas.
   (Ekzemple la lasta skota urbo antaŭ la landlimo havas la klare gaelan nomon Dunbar.)
</p> 
<h3>Lernolibro</h3> 
<p>Sed mi iom deflankiĝis de mia temo, nome la islanda lingvo. Sur mia librobreto troviĝas
   lernolibro pri la islanda, verkita de iu P. J. T. Glendening, supozeble angla kompilinto.
   En la sekcio pri la prononco li asertas, ke en la islanda ekzistas iu sono, kiu havas
   duoblaĵon en neniu alia eŭropa idiomo, nome la aspiracio de vokalo antaŭ duobla konsonanto,
   ekzemple en la vorto „<i>uppi</i>”, kiu sonas kiel „uhpi”. Li evidente ne sciis, ke tiu sono estas komuna en la gaela, precipe en la dialekto
   de la okcidentaj Hebridoj. Supozeble, la antaŭkonsonanta aspiracio estas nur unu el
   la sonoj en la gaela, kiuj devenas de la vikingaj koloniistoj. Iom simpligita priskribo
   de la skotgaela lingvo povus esti, ke ĝi estas varianto de la irlanda, prononcata
   kun islanda akcento. (Tiu prononco ne aŭdiĝas en la irlandgaela.)
</p> 
<p>Mi ne povas pretendi, ke mi ĝis nun faris grandan progreson en mia lernado de la islanda.
   Ĝia gramatiko estas eksterordinare komplika por eksterlandanoj, kiel mi. Ĝi estas
   tre fleksia lingvo, kiel la latina kaj la pragreka, kun abundaj konjugacioj kaj deklinacioj,
   kaj antikvaj karakterizoj kiel la supino, la tiel nomata meza voĉo, kiuj cetere troviĝas
   nur en la klasikaj lingvoj. Kelkaj islandaj sonoj, ekzemple la duobla „-nn” kaj „-ll” estas malfacilaj por neislandanoj. Aliflanke, aliaj islandaj sonoj estas facilaj
   kaj tute normalaj por gaellingvanoj. Finaj voĉaj konsonantoj kiel „d” estas elparolataj senvoĉaj (samkiel en la islanda) en la skotgaela, kvankam ne en
   la irlanda. Aliaj sonoj, maloftaj en eŭropaj lingvoj, okazas kaj en la gaela kaj en
   la islanda. Ofte, internaj konsonantoj, kiel „g” (ĝenerale „gh” en la gaela), estas modifataj aŭ malaperas en la prononco.
</p> 
<h3>Mutacio</h3> 
<p>Kiam mi menciis al mia edzino, ke mi konsideras malfacila la gramatikon de la islanda,
   ŝi rimarkigis, ke ankaŭ la gramatiko de la gaela havas komplikaĵojn. Kelkaj el tiuj
   komplikaĵoj estas similaj al la islandaj; aliaj tute unikaj. Ekzemple, ambaŭ lingvoj
   havas internan mutacion de vokaloj, dum la gaela (sed ne la islanda) havas ankaŭ komenc-konsonantan
   mutacion. Nur la gaela posedas la vokativan kazon kaj la distingon inter dependaj
   kaj sendependaj verboj. Aliflanke, tamen, la formo de gaelaj verboj ne ŝanĝiĝas laŭ
   la akompananta persono aŭ pronomo, kaj en tiu senco ili estas similaj al la esperantaj.
   Plie, en la gaela estas nur dek unu neregulaj verboj, krom la du verboj por „esti” kaj malgranda nombro da nekompletaj verboj. Do, oni povus disputi, kiu el la du lingvoj
   estas la pli facile lernebla, kvankam verŝajne la gaela gajnus la konkurson. Dum la
   lastaj jaroj la gaela iom simpliĝis, preskaŭ tute perdante, ekzemple, la dativan kazon
   kaj kelke da silentaj literoj.
</p> 
<p>Se oni komparas samajn tekstojn en la islanda kaj la moderna norvega, estas mirinde,
   kiom similaj estas la du idiomoj. La norvega aspektas tre simpligita kaj pli konciza,
   sed evidente samdevena, dum ĉiu vorto en la islandaj frazoj devas esti analizita,
   kiel tiam, kiam oni studas la klasikan latinan lingvon.
</p> 
<h3>Aviadilo</h3> 
<p>Se mi estus pli juna, mi ŝatus partopreni kursojn por eksterlandanoj pri la islanda
   lingvo, kiuj okazas en diversaj universitataj centroj en la lando, sed mi ne plu estas
   tia. Kaj vojaĝi per aviadilo hodiaŭ ne estas agrabla sperto. Do mi restos hejme kaj
   parolos nian prapatran lingvon. Aŭ Esperanton!
</p> 
