      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Dekstraj tendencoj</h2>
      <!-- komenco de la enhavo -->

       

     <p>La sukcesoj de la sin tiel nomanta „te-partio” en Usono alarmas ne nur demokratojn en tiu lando. Ke tiu movado denuncas prezidanton Obama kiel „socialiston” estas nur nuanco en ties batalo por dekstregaj ideoj. En Usono vera „socialismo” ekzistis neniam, alimaniere ol en Eŭropo, do la riproĉoj kontraŭ Obama estas ridindaj en la oreloj de eŭropanoj. Sed en la malnova kontinento la fenomeno de dekstrismo aperis jam antaŭ multaj jaroj. Pli kaj pli fortiĝis en multaj landoj dekstregaj partioj; en iuj landoj – ekzemple en Hungario – ili eĉ regas aŭ kunregas. En Aŭstrio, Italio, Danlando, Belgio, Nederlando, Svedio, Pollando, Rusio, Serbio, Kroatio, eĉ en Svisio dekstregaj popolistoj gajnis multajn adeptojn. Ili nutras sin per sloganoj kontraŭ „fremduloj” kaj precipe ofte kontraŭ islamanoj.</p> <p>Ankaŭ la franca prezidanto Sarkozy [sarkozí] denove ludas la dekstran karton. Kiam li estis ministro pri internaj aferoj, li batalis kontraŭ alĝerianoj, nun li malpermesas al islamaj virinoj porti vualojn kaj laste amase forpelas romaojn el Francio. Dekstregaj popolistoj batalas kontraŭ moskeoj kaj denuncas ĉiujn islamanojn kiel „teroristojn”.</p> <p>Mirigas tamen unu granda escepto: en Germanio ja ekzistas ankaŭ kelkaj dekstregaj popolistoj, sed ili neniam sukcesis en tiu demokrata lando vere mobilizi tiom da homoj, ke oni povus paroli pri dekstrega movado, kvankam ankaŭ ne malmultaj germanoj malŝatas ekzemple islamanojn. Sed ili ne subtenas dekstregajn partiojn, kiuj en balotoj kutime gajnas nur kelkajn procentojn. Tion oni ne ŝuldas al la demokratiaj partioj, sed ĝi estas historia sperto, kiu tre profundas en Germanio. Post la terura nazia reĝimo kaj malvenko en la 2a mondmilito, germanoj absolute ne volas denove persekuti minoritatojn kaj fremdulojn kiel Hitler, kiu murdigis milionojn da judoj, ciganoj, samseksemuloj, komunistoj, handikapitoj, kristanoj, eĉ esperantistoj. Se nun iuj malmultaj novnazioj manifestacias sian ideologion en iu urbo, tuj amase protestas homoj kontraŭ la bruna hordo. Faŝismo do en la hodiaŭa Germanio ne havas tian ŝancon kian ĝi denove gajnis en multaj aliaj pli-malpli demokrataj landoj.</p> <p>Kiam oni lernos ankaŭ tie, ke libereco validas ne nur por propraj popolanoj, sed por ĉiuj homoj?</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
