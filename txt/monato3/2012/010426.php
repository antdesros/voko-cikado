      <h3>Turismo</h3>
      <h3 class="tem">ITALIO</h3>
      <h2>Poluri la bildon de Pompejo?</h2>
      <!-- komenco de la enhavo -->

       

       <p>En eksterlando oni prave konsideras Italion muzeo sub libera ĉielo pro ĝia fakte enviinda arta kaj historia heredaĵo. Bedaŭrinde tiu posedaĵo estas kelkfoje malbone mastrumata. Tiu konsidero rilatas eĉ al Pompejo apud Napolo – la mondfama urbo, kiu estis detruita en 79 p.K. pro erupcio de la vulkano Vezuvio. Tamen la urbo ne estis superŝutita de ĝia lafo – kiel foje oni legas – sed de 10 metroj da cindro kaj lafoŝtonetoj. La konservado de la pompejaj prifosoj ne estas kontentiga. Tial oni ĉiam pli ofte aŭdas novaĵojn pri terfaloj kaj eĉ kreskanta ĉeesto de senlicencaj ĉiĉeronoj. Tio ne konvenas al urbo, kiu estas en la listo de Monda Kulturheredaĵo de Unesko.</p> <p>Lige kun tio la komunumo de Pompejo kaj la itala Ministrejo pri kulturo proponis projektojn kun la celo poluri la bildon de tiu antikva urbo en Italio kaj en la mondo. Unu el la iniciatoj, tute senpaga kaj eble la plej alloga, estas la tiel nomataj „artaj mardoj”. Ĝi antaŭvidas, ke ĝis decembro 2011 la lastan mardon de ĉiu monato la pordoj de la prifosoj estos malfermataj por vizitantoj dum tuta nokto. Por viziti ilin sufiĉas anonci sin pli frue. Gvidate de arkeologo, oni malkovros ĉi-maniere la komplekson de suburbaj termoj, kun belegaj dekoracioj kaj murpentraĵoj trafe iluminataj.</p> <p>Alia iniciato ebligas viziti la prifosojn bicikle. La itinero ekas de la Placo de la amfiteatro kaj atingas la tiel nomatan Mister-vilaon, kiu situas je distanco de 5 km. Tiun veturon la ŝatantoj de bicikloj certe ĝuos. La aliro al tiu itinero ne estas senpaga, sed estas inkluzivita en la prezo de ordinara bileto. Instruistoj ne pagas la eniron, kondiĉe ke ili kunportas atestilon pri profesio. Oni povas uzi propran biciklon aŭ lupreni iun ĉe la enirejo. Biciklantoj estas alvokataj respekti la unikan lokon kaj uzi ŝirmkaskon.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div>      
