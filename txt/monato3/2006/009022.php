      <h3>Moderna vivo</h3>
      <h3 class="tem">MALARMADO</h3>
      <h2>Pedale pacen</h2>
      <!-- komenco de la enhavo -->

       

       <p>La norvega organizaĵo <i>Biciklado por la paco</i> planas novan kampanjon en 2007, anoncis la iniciatinto Tore Nærland. La sloganoj de la rajdado estas: 1. Ne al atomarmiloj okcidente/oriente, norde/sude; 2. Subtene al la viktimoj en Semipalatinsk, Nevada kaj Ĉernobil; 3. Bicikle por paco kaj malarmado; 4. Bicikle por sano, ĝojo kaj mediprotektado; 5. Sporto por ĉiuj.</p> <p>La rajdo daŭros de la 1a de majo ĝis la 3a de junio 2007: komence de Novjorko al Vaŝingtono, poste fluge al Bordozo, Francio, kaj tiam al Parizo, Amsterdamo kaj Berlino. Tamen oni ne nur biciklos. Survoje, en ĉiu urbo, kie oni tranoktos, okazos kunvenoj en lernejoj kaj institutoj por handikapitoj, kaj kun pac- kaj sport-organizaĵoj, politikaj aŭtoritatoj, registaroj kaj komunumoj. Okazos ankaŭ kulturaj aranĝoj kun kantado kaj muziko.</p> <p>La partoprenontoj venos ĉi-foje el Germanio, Norvegio kaj aliaj landoj – la rajdo estas malfermita por biciklantoj el la tuta mondo. Oni celas grupon de 20-60 partoprenantoj. Kerngrupo biciklos la tutan vojon. Kunbiciklos lokaj biciklantoj de unu urbo al la sekva.</p> <h3>Amikeco</h3> <p>La unua tia „pacbiciklado” okazis en Nord-Irlando en 1978. Poste <i>Biciklado por la paco</i> aranĝis grupbicikladojn en pli ol cent landoj cele al paco, malarmado, solidareco, mediprotektado, amikeco kaj sporto por ĉiuj.</p> <p>En 1983, jam dum la malvarma milito, realiĝis biciklado (en kiu partoprenis la aŭtoro) Moskvo – Oslo – Novjorko – Vaŝingtono kun kerngrupo el Sovetio, Skandinavio kaj Usono, kaj en 1999 azi-eŭropa rajdo Pekino – Alma Ata – Moskvo – Bergeno, 102-taga.</p> <p>Tra la jaroj la biciklantoj renkontis plurajn el la mondaj gvidantoj kaj gajnintoj de pacpremioj: papo Johano Paŭlo la 2a, ŝtatestro Andrej Gromiko, ĝeneralaj sekretarioj de UN Boutros Boutros-Ghali kaj Javier Pérez de Cuéllar, prezidantoj Lech Wałęsa kaj Mary Robinson, patrino Teresa kaj Walter Mondale.</p> <p>Vidu ankaŭ la TTT-ejon <a href="http://www.bikeforpeace.no">www.bikeforpeace.no</a>.</p> <div style="text-align:right">Douglas D<span class="mm">RAPER</span></div> 
