      <h3>Rakontoj</h3>
      <h3 class="tem"></h3>
      <h2>Budapeŝta aŭtuno</h2>
      <!-- komenco de la enhavo -->

       

      <p>Eva ne sciis, kio estas milito. Ŝi naskiĝis post la lasta mondmilito, kiu por ŝi estis
   io malproksima kaj nekomprenebla, sed Eva memoris kaj eterne memoros ion, kio similis
   al milito kaj kio postlasis en ŝia animo vundojn, kvazaŭ de tranĉilo, kiuj delonge
   cikatriĝis, sed ankoraŭ estas malglataj; tial Eva neniam forgesos, kion ŝi travivis.
</p> 
<p>Ĉio komenciĝis en frua novembra mateno. Eva vekiĝis pro foraj obtuzaj tondroj. En
   la unua momento ŝajnis al ŝi, ke komenciĝas ŝtormo. Estis malhela nuba mateno kaj
   eble pluvis. Eva kuŝis en la lito, rigardante la grandan fenestron de la ĉambro. Ŝia
   patrino estis en la kuirejo, kaj Eva sciis, ke panjo baldaŭ venos en la dormoĉambron.
   Ĉe Eva kuŝis la pluŝa urseto Gaŝpar, kiu jam estis ĉifita, sed ŝi amis ĝin kaj ne
   kutimis ekdormi sen ĝi.
</p> 
<p>Ŝajnis al Eva, ke longan tempon jam ŝi estis sendorma, sed panjo ne venis, kaj Eva
   ne komprenis, kial panjo ne venas. Ĉiun matenon, kiam Eva vekiĝis, ŝi vidis la bluajn
   okulojn de panjo kaj ŝian rideton, varman kiel ĵus boligita lakto. Eva ligis la rideton
   de panjo al varma lakto, ĉar matene el la kuirejo senteblis la aromo de boligita lakto.
   Tamen hodiaŭ matene panjo ne venis en la dormoĉambron, kaj Eva kuŝis senmova, strabante
   al la griza fenestro. Kvazaŭ peza griza kurteno estis ekstere, kiu kovris la fenestron.
   Eva frostis kaj tremis sub la dormokovrilo. El la kuirejo ne alflugis eĉ plej eta
   bruo. Kiam Eva konstatis, ke panjo ne baldaŭ alvenos, ŝi ekstaris de la lito kaj piedpinte
   ekiris al la pordo, malfermis la pezan pordon, kaj nudpiede ekpaŝis sur la ŝtonplata
   kuireja planko. La patrino staris ĉe la fenestro kaj rigardis al la korto de la granda
   domo.
</p> 
<p>Silente Eva ekstaris malantaŭ la patrina dorso kaj provis same alrigardi tra la fenestro.
   La korto estis ĉirkaŭita de la alta konstruaĵo, kaj en ĝin oni eniris tra grandega
   ligna pordo. La rigardo de la patrino estis direktita al la pordego, nun larĝe malfermita,
   ĉe kiu staris kanono, granda kaj timiga. Ĉirkaŭ la kanono estis kelkaj viroj, sed
   ne soldatoj. La viroj direktis la kanonon al la kontraŭa monteto Gelert, sur la alia
   bordo de Danubo. Nun Eva komprenis, ke la tondroj, kiuj vekis ŝin, estis pafoj desur
   la monteto Gelert. Iuj pafis, sed kial?
</p> 
<p>La patrino ne komprenis tuj, ke Eva staras malantaŭ ŝi. Kiam ŝi rimarkis Evan, ŝi
   tuj levis ŝin kaj portis en la dormĉambron.
</p> 
<p>– Restu ĉi tie! – severe diris la patrino. – Ne venu en la kuirejon! Tie estas danĝere. Mi portos ĉi tien la matenmanĝon.</p> 
<p>Eva restis sola en la obskura dormoĉambro. La pafoj ekstere aŭdiĝis pli forte kaj
   pli forte. Eva komencis plori. Ŝiaj varmaj larmoj fluis sur la vangojn kiel riveretoj.
   Ŝi ne sciis, kiom da tempo ŝi estis sola en la ĉambro. Subite la pordo malfermiĝis,
   kaj eniris la patrino kun la matenmanĝo – teo kaj biskvitoj. Hodiaŭ ne estis lakto kun buterpano. Eva demande alrigardis la
   patrinon, sed tiu silentis kaj aspektis ege maltrankvila. Ŝi eĉ kvazaŭ ne vidis Evan.
   La pleto kun la matenmanĝo tremis en la patrinaj manoj, kaj la metala tea kulereto
   tintis kiel sonorilo. La patrino lasis la pleton kaj senvorte viŝis ŝiajn larmojn.
   Neniam Eva vidis la patrinon tiel maltrankvila. Verŝajne ekstere okazis io terura.
   Eva penis kompreni, kio okazas, kial estas pafoj kaj kial en la korto staras kanono.
   Pro la timo ŝia koro estis kiel frostigita pasero. La patrina maltrankvilo obsedis
   same Evan, kaj ankaŭ ŝi komencis tremi, kvazaŭ en la ĉambro estus ege malvarme. La
   timo kiel serpento ekrampis en ŝian ventron, kiu konvulsiis. Kio okazis?
</p> 
<p>La patro de Eva estis en la laborejo, kaj ĉu li sukcesos reveni hejmen? La tondroj
   ekstere plifortiĝis. La fenestraj vitroj tremis, tintis, kvazaŭ subite ili dispeciĝus
   en sennombrajn kristalajn neĝerojn.
</p> 
<p>La patrino denove iris en la kuirejon. Kion ŝi faras tie, demandis sin Eva. Ĉu ŝi
   kuiras aŭ gvatas tra la fenestro? La patrino ŝatis trinki kafon, kaj matene la kafaromo
   odoris en la tuta loĝejo, sed ĉi-matene ŝi verŝajne forgesis kuiri kafon. Eva perceptis
   paroladon en la kuirejo, streĉis la orelojn kaj rekonis la voĉon de la najbarino,
   onklino Agi. Ŝi kaj la patrino vigle diskutis ion. La scivolo de Eva varmigis ŝian
   ventron. Ŝi ne eltenis, atente kovris Gaŝpar per la dormokovrilo, kaj diris al ĝi:
</p> 
<p>– Restu ĉi tie! Mi baldaŭ revenos.</p> 
<p>Eva proksimiĝis al la pordo kaj provis aŭdi, kion diras la du virinoj en la kuirejo.
   La patrino kaj onklino Agi konversaciis ege maltrankvile. Certe ili estis forte timigitaj.
   De tempo al tempo ili flustris, kvazaŭ ili supozus, ke iu subaŭskultas ilin, tamen
   Eva sukcesis kapti kelkajn vortojn. Plej ofte ili ripetis la vortojn „revolucio”, „laboristoj”, „studentoj”. Eva ne komprenis la vorton „revolucio”. Ŝi konis la vorton „laboristo”. Ŝia patro estis laboristo kaj laboris en granda uzino. Tamen ŝi ne estis certa,
   kion ĝuste signifas la vorto „studento”. En la najbara loĝejo loĝis Piŝta, kaj oni diris, ke li estas studento. Ĉu panjo
   kaj onklino Agi ne parolas pri Piŝta?
</p> 
<p>Li ĉiam estis tre serioza, kun okulvitroj, kaj havis longan helan mantelon. Eva sciis,
   ke Piŝta estas bona homo, neniam ion malbonan li faris kaj li certe ne faris revolucion,
   tial panjo kaj onklino Agi ne devas timi lin. Ambaŭ virinoj tamen ege maltrankvilis,
   kaj tra la pordo Eva klare perceptis iliajn veojn. Eva same estis maltrankvila, sed
   ŝi sciis, ke Piŝta ne estas kulpa. Ŝi eĉ deziris tuj malfermi la pordon kaj diri al
   panjo kaj al onklino Agi, ke Piŝta estas bona homo kaj nenion malbonan li faris. Eva
   tamen ne kuraĝis malfermi la pordon, ĉar ŝi sciis, ke panjo alrigardos ŝin severe
   kaj riproĉos ŝin. Ja, la patrino avertis ŝin resti en la dormoĉambro kaj ne eliri
   el ĝi.
</p> 
<p>La memoro pri Piŝta forte okupis la meditojn de Eva. Li aspektis iom ridinda kun la
   okulvitroj kaj la longa mantelo, kies rando estis preskaŭ ĝis la ŝuoj. Tamen estis
   io, kio forte impresis Evan kaj kio konstante tiklis ŝian scivolon. Piŝta ofte portis
   ian grandan nigran ujon, kaj Eva tre deziris ekscii, kio estas en ĝi. Ŝi supozis,
   ke en ĝi estas pupo, tamen Piŝta estis serioza homo kaj Eva ne kredis, ke li portas
   pupon.
</p> 
<p>Foje nokte Eva sonĝis pri Piŝta kun la ujo. Li ridetis, malfermis la ujon, kaj en
   ĝi Eva vidis miraklan lumon. Tion Eva ne komprenis. La ujo ŝajne estis malplena, sed
   el ĝi radiis lumo, multkolora kaj blindiga. Eva decidis, ke la sekvan tagon, kiam
   Piŝta preterpasos ilian loĝejon, ŝi petos lin malfermi la ujon kaj montri, kio estas
   ene. Ja, ĝi aspektas ege stranga ujo, kaj Eva nepre devas vidi, kion ĝi entenas.
</p> 
<p>Post nelonge onklino Agi foriris, kaj panjo restis sola en la kuirejo. Nun Eva kuraĝis
   malfermi la pordon.
</p> 
<p>– Kial vi venas? – riproĉis ŝin la patrino. – Mi diris, ke vi restu en la dormoĉambro!</p> 
<p>– Mi timas esti sola – ekploris Eva.</p> 
<p>– Ja, mi estas ĉi tie – provis trankviligi ŝin la patrino, sed ŝia voĉo tremis kaj tute ne estis trankvila.</p> 
<p>Ekstere ŝajne la tondroj jam ne estis tiel oftaj. La kanono tamen daŭre staris ĉe
   la pordego, sed ĉirkaŭ ĝi ne videblis la viroj. Eva rigardis tra la fenestro kaj demandis
   sin, kial la viroj forlasis la kanonon en la korto kaj malaperis. Kvazaŭ neniu estus
   en la tuta granda domo, tamen Eva sciis, ke la homoj estas en siaj loĝejoj kaj same
   kiel ŝia patrino gvatas tra la fenestroj.
</p> 
<p>Subite de la strato aŭdiĝis pafoj kaj krioj. En la korton enkuris du knaboj. Verŝajne
   iuj persekutis ilin. Unu el la knaboj estis Piŝta. Eva tuj rekonis lin. Li surhavis
   la longan helan mantelon kaj estis kun la ridindaj okulvitroj. Post la knaboj kuris
   viroj, vestitaj en verdaj soldataj uniformoj, kiuj kriis ion en nekomprenebla lingvo.
   Piŝta kaj la knabo rapidis trapasi la korton kaj kaŝi sin en la domo. Ekkrakis pafoj,
   kvazaŭ iu dispecigus sekajn lignojn. La resono de la pafoj estis surdiga. Piŝta stumblis
   kaj falis. Eva supozis, ke li surpaŝis la randon de sia mantelo. Piŝta falis vizaĝe
   kun etenditaj brakoj.
</p> 
<p>La alia knabo sukcesis eniri la domon. La viroj kun la pafiloj eniris post li. Piŝta
   restis kuŝante en la korto. Eva ne komprenis, kial li ne stariĝas. Ŝi certis, ke li
   tuj ekstaros kaj alrigardos la ĉielon, kiu videblis super la korto. Antaŭ Piŝta kuŝis
   la okulvitroj, kvazaŭ liaj etenditaj brakoj dezirus preni ilin.
</p> 
<p>Subite terura krio skuis la tutan domon. Ĝi venis el la dua etaĝo. Kriis la patrino
   de Piŝta. Ŝi kriis kaj kuris malsupren laŭ la ŝtuparo. Post sekundoj ŝi estis en la
   korto, klinita super Piŝta. Ŝiaj krioj estis teruraj kaj doloraj. Nun Eva rimarkis,
   ke ĉe la hela mantelo de Piŝta estas ruĝa makulo, simila al lageto, kiu iĝas pli kaj
   pli granda.
</p> 
<p>– Ĉu iu helpos min? – ploris onklino Marta, la patrino de Piŝta, sed neniu kuraĝis eliri el la granda griza
   domo.
</p> 
<p>Ekpluvis. Forte ekpluvis, sed onklino Marta restis klinita super Piŝta.</p> 
<p></p> 
<p>Tion memoras Eva de la fora budapeŝta aŭtuno de 1956. Tiun ĉi teruran bildon ŝi neniam
   forgesos.
</p> 
