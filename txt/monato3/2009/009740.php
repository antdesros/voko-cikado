      <h2>Plia heroo por Esperantujo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Jen kvin eroj el itala bildstrio, prezentitaj en Esperanto. La unua (el 1989-96) titoliĝas <i>La skuegaj originoj de Rat-man</i>, el kiu oni rapide komprenas, ke temas pri parodio de la usona komikso Bat-man. Tiu kutimas funkciigi serĉlumilon, en kies lumo aperas vesperta silueto; ĉe Rat-man tio fariĝas formo, simila al la kapo kaj oreloj de Miki-muso.</p> <p>La dua rakonto nomiĝas <i>Rat-man kontraŭ la araneo</i>. Tiu sekvas la tradicion de la originalo, ke Bat-man kaj liaj kontraŭuloj akiris siajn unikajn kapablojn pro eksterordinara evento – toksiĝo je mistera substanco aŭ mutaciiĝo pro nuklea akcidento aŭ simila, kun aludoj pri obstakloplena kaj maljusta juneco por klarigi la nunan kontraŭnorman konduton.</p> <p>La minaco verda atakas en la tria rakonto – temas pri io pli organika ol esperantistaj brigadoj. Ĝi eskapas de la laboratorio de freneza sciencisto kaj Rat-man devas konfronti ĝin per sia kutima heroeco.</p>  <p>En la kvara rakonto rajdas en la vivon de Rat-man Ĉincja la Barbara, kiu emas esprimi sin per kantado de fragmentoj el popularaj anglalingvaj amkantoj. Cetere, konvene alloga virino sukcesis enplekti sin en ĉiujn el la aventuroj, sed Rat-man (kiel Bat-man) neniam sukcesas trovi tie daŭran kontentiĝon. Ĉi-okaze Ĉincja trudas sian volon al la malvolema Rat-man.</p> <p>La lasta rakonto, eble la plej facile digestebla, temas pri Titanic 2000 – aludante la eventojn de la jaro 1912 kaj al la novjarmila apero de la samnoma eposfilmo. Ĉi-foje la ĉefa virina rolo estas plenumata per vivgranda blovpupo, kun speciala gasta rolo por la glacimonto. Adiaŭan bison provizas rakonteto pri tio, kiel Rat-man klopodas, sed malsukcesas aliĝi al sia unua Esperanto-kurso – verkita de Daniele Binaghi kaj desegnita de Andrea Rosetto, kiu estas intervjuita sur la sekvaj paĝoj.</p> <p>La originalo havas italajn vortŝercojn, foje bone redoneblajn en Esperanto, dufoje klarigitajn per piednoto, en unu-du aliaj lokoj oni sentas, ke la originalo eble estis iom pli forta. Rat-man rompas ĉiujn el la kondut-reguloj por herooj; li ne estas aparte kuraĝa, liaj savplanoj kutime malsukcesas kaj li ĝuas preterkutiman malbonŝancon – en parodia stilo simila al tiu de Maxwell /maksvel/ Smart el la populara TV-serio el la 80aj jaroj – kaj retrovebla en nia E-mondo per kontrastigo de la spionoj 007 kaj 005.</p> <p>La tuto estas pretigita per stila fideleco al la Bat-mana fonto, granda imagpovo, desegno-lerto kaj konvene densa spriteco.</p> <div style="text-align:right">Stefan M<span class="mm">AC</span>G<span class="mm">ILL</span></div> <h6 class="bib">Leo Ortolani: Rat-man. El la itala tradukis Christina de Giorgi, Daniele Binaghi (projektestro), Filippo Zanozer, Luisa Oberrauch Madella. Eld. Projekto RoMEo, Espero por IEJ kaj JEFO (itala kaj franca TEJO-sekcioj), 2007. 128 paĝoj.</h6>  
