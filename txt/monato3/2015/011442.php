      <h3>Scienco</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Fukuŝima kvar jarojn post la granda tertremo</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>La verkintino, japana medicinistino, partoprenis en kunveno okazinta la 7an de novembro
      2014 ĉe la Japana Esperanto-Instituto kaj ricevis de Yamakawa Syuiti, la iniciatinto
      kaj gvidanto de la okazaĵo, la permeson transdoni al la legantaro de M<span class="mm">ONATO</span> la ĉefajn kolektitajn informojn pri la nuntempa vivo en Fukuŝima. Ŝi aldonas multe
      da donitaĵoj kaj fotoj pri siaj propraj domo kaj kvartalo kaj prezentas detalan interesan
      raporton ĝuste kvar jarojn post la granda tertremo, kiu frapis ŝian landon.
   </b>
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kie situas la urbo Fukuŝima en la gubernio? Kaj kiujn sekvojn havis la akcidento,
      kiu frapis la areon en 2011?
   </i>
</p> 
<p>La urbo Fukuŝima situas en la norda parto de la gubernio Fukuŝima kaj ĉirkaŭ 280 kilometrojn
   norde de la japana ĉefurbo Tokio. La atomcentralo F-1 troviĝas ĉirkaŭ 65 kilometrojn
   sud-oriente de la urbo. La tertremego okazinta la 11an de marto 2011 damaĝis plej
   grandan parton de la gubernio, precipe ĝian orientan parton. La katastrofa akcidento,
   kaŭzita de la cunamo, ĉe la atomcentralo 
   <i>Fukuŝima-dai-iĉi</i> (F-1), nemezureble influis (kaj verŝajne daŭre longe influos) la vivon de la loĝantoj
   en la tuta gubernio.
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kien difuziĝis la radioaktivaj substancoj post la akcidento ĉe F-1?
   </i>
</p> 
<p>Radioaktivaj substancoj liberiĝis en la aeron pro la hidrogenaj eksplodoj, kiuj sekvis
   la tertremon kaj la cunamon. La tiama vento portis ilin nord-okcidenten. Tuj post
   la akcidento ĉefe la radioaktivaj substancoj jodo 131, cezio 134 kaj cezio 137 precipitiĝis;
   plejparto de la radioaktiva jodo baldaŭ malaperis, ĉar ĝia duoniĝ-tempo estas nur
   ok tagoj, sed radioaktivaj cezioj, kiuj adheras al la tero, malaperas pli malrapide:
   la duoniĝ-tempo de cezio 134 estas du jaroj kaj tiu de cezio 137 estas tridek jaroj,
   kvankam estas rimarkinde, ke en la homa korpo ambaŭ cezioj duoniĝas post nur 100 tagoj,
   ĉar ili eliras en urino iom post iom.
</p> 
<p>Tuj post la katastrofo detektiĝis granda dozo da radiado en kelkaj kvartaloj de mia
   urbo. Sed ŝajnis, ke la dozo malpliiĝas jaron post jaro, verŝajne pro tio, ke radioaktivaj
   cezioj spontane malaperas: pluvo kaj vento iom post iom forportis ilin, kaj aldone
   oni senaktivigis la loĝ-terenojn. En majo 2011 detektiĝis valoro de 3,44 μSv/h (mikrosivertoj
   ĉiuhore) sur la gazono de mia ĝardeno, kaj en la posta monato mia edzo novigis la
   gazonon. En marto 2014, la dozo ĉe la gazono estis malaltiĝinta ĝis 0,259 μSv/h. Sed
   eĉ en mia nevasta loĝ-tereno detektiĝas dozo iuloke pli alta kaj aliloke malpli alta:
   ekzemple sur la manĝotablo 0,118 μSv/h, sed ĉe la kovrilo de la kloako por pluvakvo
   1,280 μSv/h(!).
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kiom danĝeraj estas la radioj?
   </i>
</p> 
<p>Ni ĉiuj ricevas radiojn ĉiutage, kie ajn ni estas sur la terglobo, mondaveraĝe 1,26 mSv/jaro
   (milisivertoj ĉiujare) el la aero, 0,29-0,48 mSv/jaro el manĝaĵo, 0,36 mSv/jaro el
   la kosmo ktp, sume proksimume 2,4 mSv/jaro. Krome ni estas elmetataj al ili okaze
   de radiografia ekzameno aŭ komputila tomografio. Laŭ statistiko, ĉirkaŭ 30 elcentoj
   el la japanoj, kiuj mortis en la daŭro de unu jaro, perdis sian vivon pro kancero.
   Estas kalkulite, ke, se oni ricevus 100 mSv/jaro da radioj, la nombro de la mortintoj
   pro kancero plialtiĝus je 0,5 %. Sed estas konate, ke, se la mortintoj ricevis malpli
   ol 50 mSv/jaro da radioj dum ili vivis, ne eblas determini la kialon de ilia morto,
   ĉu pro radioj aŭ pro alia kaŭzo. La malbona influo de la radioj al la homa korpo estas
   ankoraŭ ne tute klarigita: ĉiuokaze estas konsilinde, ke ni ricevu kiel eble plej
   malmulte da radioj.
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kiel oni senaktivigas la loĝ-lokojn en via urbo?
   </i>
</p> 
<p>La urba registaro senaktivigis la loĝ-lokojn, celante, ke radioj estu malpli ol 1 mSv/jaro,
   nome 0,23 μSv/h, laŭ la decido de la japana registaro. Sed en aŭgusto 2014 la registaro
   nuligis tiun ciferon kaj decidis, ke oni laŭeble malplimultigu la radiadon, konsiderante
   la radiadan dozon, kiun la loĝantoj ricevis/ricevas efektive. Fakte, rezulte de esploro
   klariĝis, ke oni ne ricevas tiom grandan dozon, eĉ se oni loĝas en loko, kie detektiĝas
   pli ol 0,23 μSv/h. Krome, eĉ post la senaktivigado la radiada dozo ne malpliiĝis ĝis
   0,23 μSv/h en pluraj kvartaloj. La urba registaro senaktivigis unue parkojn, lernejojn,
   infan-ĝardenojn kaj la lokojn, kien multaj homoj venas; poste oni pritraktis loĝ-terenojn
   laŭ la ordo de grandeco de la dozo. La metodo de senaktivigo estas unue forpreni poluitajn
   tersurfacon, foliojn ktp kaj ankaŭ purigi domojn, due ŝirmi homojn kontraŭ radioj
   el forprenitaĵo kaj trie konservi ĉi-lastan malproksime de loĝantoj.
</p> 
<p>Mi montros al vi, kiel oni faris ĉion ĉi en la parko apud mia loĝejo kaj aliaj lokoj.
   En decembro 2013 unue oni forprenis la surfacon de la tero, eltirante arbustojn kaj
   fortranĉante branĉojn de la arboj. Due oni metis la forprenitaĵojn en grandajn plastajn
   sakojn, kiuj siavice estis enterigitaj en grandan truon ĉirkaŭ kvin metrojn profundan,
   kaj kovris la truon per pura tero. Post la laboro la trunkoj de la arboj blankiĝis,
   ĉar oni purigis ilin per premakvo. Antaŭ la senaktivigado la radiada dozo estis 0,79 μSv/h
   en la parko kaj en la enirejo estis ŝildo, sur kiu estis skribite, ke oni ne ludu
   tie pli ol unu horon kaj ke post la ludado oni nepre purigu siajn manojn kaj buŝon.
   Post la senaktivigado la ŝildo estis forigita, kaj la dozo malpliiĝis pli kaj pli.
   Se mankas spaco por enterigi la sakojn, oni metas ilin apud domo. La sakoj metitaj
   aŭ en la teron aŭ ĉe domo estos transportitaj al la provizoraj konservejoj, kiam la
   ejoj estos pretaj en ĉiu kvartalo, poste de tie al ununura provizora konservejo kaj
   fine al definitiva konservejo. Sed neniu scias, kiam tio efektiviĝos. Tio estas unu
   el la grandaj problemoj koncerne la senaktivigadon.
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kia estas la sekureco de manĝaĵoj en Fukuŝima?
   </i>
</p> 
<p>Oni vendas manĝaĵojn bone kontrolitajn, kaj precipe oni ekzamenas ĉiun rizan sakon
   en mia gubernio. Se temas pri legomoj, kiujn oni kreskigis aŭ rikoltis sur monto por
   si mem kaj ne por vendi, la urba registaro senpage mezuras la dozon. Mia edzo jam
   kelkfoje petis la ekzamenon pri iuj legomoj, kaj la rezultoj estis absolute bonaj
   ĝis nun. Mi ĉiutage manĝas legomojn rikoltitajn en mia ĝardeno. Tial en oktobro 2014
   mi ekzameniĝis pri la dozo en mia korpo. Ne detektiĝis radioj. Por mezuri radiadan
   dozon ĉirkaŭ si oni povas ankaŭ uzi porteblan detektilon, sed mi ne mendis ion tian.
   Notinde estas, ke pli ol 3000 realtempaj detektiloj estas starigitaj en lernejoj,
   infan-vartejoj, parkoj kaj aliaj lokoj en la gubernio Fukuŝima, kaj ke monitoroj por
   kontroli la radiadan dozon en la aero estas instalitaj en pli ol 600 publikaj lokoj
   de la gubernio. 
</p> 
<p>
   <i>M<span class="mm">ONATO</span>: Kion fine pri la alarmemo rilate al la vivo en Fukuŝima?
   </i>
</p> 
<p>Nu, la japanoj loĝantaj ekster mia gubernio ofte diras: „Fukuŝima estas poluita de radioaktivaj substancoj.” Aŭdinte tion, mi senvoĉe demandas: „Pri kiu Fukuŝima temas?” kaj murmuras, ke, se la atomcentralo havus alian nomon, oni ne emus aserti la samon.
   Fakte ja ekzistas tri Fukuŝima-j: la gubernio, la urbo kaj la atomcentraloj 
   <i>Fukuŝima-dai-iĉi</i> (F-1) kaj 
   <i>Fukuŝima-dai-ni</i> (F-2). Mi volus, ke oni distingu ilin kaj sciu, ke la gubernio estas tre vasta kaj
   ke almenaŭ en Aizu, la okcidenta regiono de la gubernio, la influo de la radioaktivaj
   cezioj estas treege malgranda. En majo 2014, cetere, la mangao 
   <i>Oiŝimbo</i> elvokis polemikon pri la influo de radioj al la homa korpo post la akcidento de F-1.
   Kiel mi diris, oni ne povas tute nei la influon de eĉ malgranda dozo, sed oni ja povas
   preskaŭ ignori tiun de malpli ol 100 mSv/jaro, ĉar oni povas diri, ke tiu risko estas
   tiom rimarkinda kiom ajna alispeca danĝero. Ni neniam forgesas aŭ forgesos, ke povas
   iam okazi ĉe F-1 refoje grava akcidento pro ventego, pluvego, tertremo, cunamo kaj
   aliaj tiaj eventoj. Sed nun mi vivas pli-malpli trankvile en la urbo Fukuŝima. Mi
   opinias, ke oni devas taksi surbaze de scienca esplorado la riskon kaj la danĝerecon
   de radioaktivaj substancoj el F-1. Mi dezirus, ke oni ne tro timu radiojn krom en
   la lokoj, kie estas klare preskribite, ke oni restu tie nur mallonge, aŭ ke oni portu
   protektan veston.
</p> 
