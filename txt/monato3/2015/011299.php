      <h3>Turismo</h3>
      <h3 class="tem">ITALIO</h3>
      <h2>La Loreta Vojo: Laŭ la spuroj de antikvaj pilgrimantoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pilgrimi piede, per biciklo aŭ – por tiuj, kiuj deziras alproksimiĝon des pli tradician kaj allogan – eĉ per ĉevalo: oni faru libere sian elekton antaŭ ol ekiri la tiel nomatan Loretan
   Vojon. Situanta en la centra Italio, ĝi estas riĉa je preĝejoj kaj aliaj belaĵoj,
   kaj spite al la paso de multaj jarcentoj, ankoraŭ kunligas Asizon, la urbeton de Sankta
   Francisko, kaj Loreton, vilaĝon en la provinco de Ankono.
</p> 
<p>Granda estas la nombro de la etaj pitoreskaj komunumoj, kiujn oni povas „tuŝi” laŭlonge de itinero longa entute 150 km. Oni povas eluzi tiujn vilaĝojn por medita
   halto, por etapo ripoziga ankaŭ por la eventualaj ĉevaloj, aŭ – kial ne? – por simple ĝui la montetan pejzaĝon de la centra-itala teritorio, jene priskribita
   en Esperanta elsendo de Radio Romo, okazinta la 23an de novembro 1940. Pro sia unika
   stilo, ĝi vere meritas reviviĝi sur niaj paĝoj.
</p> 
<p>„Italio, la koro de la kristanismo kaj patrujo de sanktuloj, havas la privilegion posedi
   grandegan nombron da sanktejoj. El ili, grandaj kaj malgrandaj, iuj estas en la unua
   rango pro sia luma karaktero, kiu distingiĝas de la ĉirkaŭa profana medio, aŭ pro
   la interrilato pli-malpli intima kun la naturo. Monumentoj de kredo – de historio kaj de arto, en kiuj la supernatura interveno estas kunligita kun la
   homa elemento – estas kvazaŭ ĉirkaŭverŝitaj je mistera fluido, elradianta el ili, plena de ravo kaj
   mistika poezio religia.”
</p> 
<p>Lasu vin allogi de la certigo, ke preskaŭ nenio en la regiono ŝanĝiĝis, kaj vizitu
   la lokojn tiel fascine priskribitajn, meze de la pasinta jarcento! Ĉu vi volas iri
   kiel la mezepokaj pilgrimantoj sur ĉevalo? Tio nun eblas. Unua ĉeval-pilgrimo, kutima
   agado en la pasintaj jarcentoj, sed longe ne praktikata, lastatempe estis reorganizita
   en la Loreta Vojo. Ĉi-lasta estas strato, kiu ekde la Mezepoko kunigas Asizon („En la luma brilo de la umbria pejzaĝo, Asizo konservas neŝanĝita en la jarcentoj la
   dolĉan poezion de la franciskana epopeo. Malmultaj lokoj kapablas veki tiel riĉajn
   trezorojn de sentoj, kiel ĉi tiu malgranda urbo de paco kaj sonĝo, soleca kaj silenta,
   trapenetrita de spiritualismo kaj de mistika poezio, dolĉe loga”, oni radie dissendis en Esperanto el Italio al la tuta mondo) kun Loreto, sidejo
   siavice de unu el la plej famaj sanktejoj de Maria, la patrino de Jesuo.
</p> 
<p>Ambaŭ regionoj – Umbrio kaj Markio – sin organizas ĉiam pli bone pere de turismaj projektoj, kunligitaj kun la kredo.</p> 
<h3>Asizo</h3> 
<p>Oni komencu la itineron de Asizo. Kion penvaloras vidi tie? Ni tuj agordiĝu laŭ la
   esperantlingva frekvenco de 
   <i>Radio Romo</i> (1940).
</p> 
<p>„En la ebeno sub Asizo kuŝas la 
   <i>Sanktejo de Santa Maria degli Angeli</i>, bela kaj granda preĝejo, konstruita laŭ desegnoj de Vignola. Ĝi estas samtempe Sanktejo
   omaĝe al Maria kaj al Sankta Francisko kaj kie ĉio ankoraŭ parolas pri la Serafulo,
   la sanktulo de la mistiko intense agema kaj asketo de la plej pezaj humiligoj, kiu
   aliigis sian ekziston al kanto de ĝojo. La Sanktejo estas tre adorata, ĉar Sankta
   Francisko mortis ĝuste en tiu loko, kie li estis vidinta la vizion pri la Sinjoro.
   La malgranda 
   <i>Preĝejeto de Santa Maria degli Angeli</i>, tiom kara al la koro de la Sanktulo, estas nun sub la kupolo de la vasta templo.
   <br></br> 
   <br></br> Granda estis la amo de la Sanktulo al tiu sia preĝejeto, Porziuncola, apud kiu floris
   la komencoj de lia fervora apostolado kaj batadas por ĉiam nova vivo de tiom da dolĉaj
   franciskanaj memoraĵoj. El tiu senornama kapeleto, kiu ankaŭ nun konservas sian tutan
   primitivan aspekton de severa malriĉeco, Francisko el Asizo dissendis en la mondon
   kaj en la jarcentojn sian ordenon. La eta preĝloko troviĝas nun en la centro de la
   Sanktejo. Ĝi estas kruda kaj tre simpla ortangula konstruaĵo el blanka ŝtono, al kiu
   estas aldonita, sur la frontono, gotikstila tabernaklo. La supra parto estas dekorita
   per fresko de Overbeck, dum sur la flanko estas konservitaj restaĵoj de fresko el
   la 15a jarcento. La interno, je ogiva volbo, estas tute nigrigita de la fumo kiel
   la tabulpentraĵo de 1293, staranta super la altaro. Tie estas ankaŭ la kapelo de la
   ‚Transiro’, kie la Sanktulo ‚kun la brakoj, krucitaj je la agonio’ finas sian teran vivon, glorante Dion.”
</p> 
<h3>Loreto</h3> 
<p>La fina loko de la proponita itinero estas Loreto, konata pro la Baziliko de Sankta
   Domo, kie estas gardata la domo de la Sankta Virgulino (Maria), mistere translokita
   laŭkrede en 1294 el Nazareto. Se oni havas amikinon kun la nomo Loredana, oni sciu,
   ke ĝi rilatas ĝuste al devoteco pri la Madono de Loreto. Ni klopodu, dum la legado,
   reaŭskulti per niaj oreloj la jam menciitan elsendon, pretigitan por esperantistoj
   tutmonde.
</p> 
<p>„Loreto estas la perlo de la sanktejoj de Maria, fama en la tuta kristana mondo pro
   la konservado de la Sankta Domo de Nazareto. Pia tradicio diras, ke ĝi efektive estas
   la dometo, loĝita de Maria en Nazareto, kie ŝi estis salutata de Anĝelo Gabrielo en
   la Anunciacio. Dum la nokto inter la 9a kaj la 10a de majo 1291 okazis la mirakla
   translokiĝo de la dometo de Nazareto ĝis Tersatto apud Fiume; de tie la 10an de decembro
   1294 la dometo estis transportita per la sama miraklo, en lokon apud Recanati, ĉirkaŭitan
   de arboj; kaj post kelkaj monatoj en lokon apud la supro de la monteto kaj fine centon
   da metroj malproksime de la supro. Oni neniam povis klarigi la misterajn translokiĝojn
   alie ol per supernatura interveno. Ĉirkaŭ la Sankta Domo por anstataŭigi la humilan
   originan preĝejeton ekestis majesta templo, en kiu, per monoferoj de popolo, de princoj
   kaj de papoj, estas amasigitaj gloraj artotrezoroj. En la konstruado de la giganta
   baziliko laboris artistoj kiel Bramante, Sangallo, Sansovino. La interno de la baziliko
   estas tre riĉa je artaj verkoj, statuoj, bronzaĵoj, portaloj, belegaj ornatoj. Grandan
   intereson elvokas la freskoj de Melozzo da Forlì, de Luca Signorelli, de Pomarancio.
   Tre riĉa estas la trezorejo, kvankam parte rabita de francoj en 1797. Pluraj milionoj
   estas la piuloj, kiuj iris pilgrime al Loreto. Inter ili kvindek papoj kaj multaj
   sanktuloj, inter kiuj Sankta Luigi, Sankta Francisko, Sankta Carlo Borromeo, Sankta
   Ignazio, Sankta Alfonso de Liguori.”
</p> 
<h3>Pilgrimantoj bonvenaj</h3> 
<p>Kreskas la religia turismo en tiu areo. Al Loreto venas ankaŭ vizitantoj lige kun
   mediaj, sportaj, kulturaj kaj aliaj okazaĵoj. Ne temas cetere pri novaĵo lastatempa.
   Io simila devis okazi jam antaŭ 74 jaroj, kiam la Esperanta radio-voĉo diris, ke „ĉiuj ĉi sanktejoj estas potenca alvoko ne nur por la kredanto, sed ankaŭ por ĉiu ajn,
   kiu de la religio ŝatas nur la valoron moralan kaj estetikan, kaj por ĉiu amanto de
   naturaj belaĵoj kaj mistika enpensiĝo”. Do kredantoj kaj nekredantoj libere renkontiĝu en la Loreta Vojo, kiu, desegnita
   de etruskoj kaj poste de romianoj, kaj kiel dirite plena de plurspecaj allogaĵoj,
   atendas kun etenditaj brakoj ĉiujn homojn, kiuj serĉas lokon por forgesi la metropolan
   kaoson kaj streĉon, al kiu nin devigas ĉiam pli ofte la nuntempa vivo.
</p> 
