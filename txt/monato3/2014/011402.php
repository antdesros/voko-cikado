      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Ne interveni signifas ne solvi</h2>
      <!-- komenco de la enhavo -->

       

        <p>Sendube la nuna milito de politikaj kaj religiaj fanatikuloj en Sirio kaj Irako zorgigas
   la tutan mondon kaj precipe en Proksima Oriento kaj Eŭropo. Ĉiutage aperas raportoj
   pri la lukto kontraŭ Islama Ŝtato en Irako kaj Levanto (ISIL), en Eŭropo konata kiel
   Islama Ŝtato aŭ IS.
</p> 
<p>Politikistoj longe hezitis fronte al amasmurdo, forpelo, perforto en norda Irako kaj
   Sirio. Tamen realan danĝeron oni ekvidas ankaŭ en eŭropaj landoj. Islamdevenaj civitanoj
   vojaĝas orienten por soldatiĝi: ili revenas agresemaj kaj batalharditaj.
</p> 
<p>En Germanio oni komencas malhelpi revenintojn tiajn kaj ne donas pasportojn al danĝeruloj.
   Ĉu tio helpos solvi la problemojn? Iamaniere oni reagas per maltoleremo kaj pli-malpli
   kaŝa ksenofobio.
</p> 
<h3>Naive</h3> 
<p>En sia 
   <i>Fejsbuk</i>-paĝo antaŭ nelonge membro de Eŭropa Parlamento Fabio De Masi (Maldekstruloj) notis:
   „Mi ne estas pacemulo. Mia avo batalis kun armilo en norda Italio kontraŭ la faŝistoj.
   La kurdoj en Sirio bezonas nian subtenon. Sed mi ne naive kredas, ke Usono finos la
   sangoverŝadon. Ĉio, kion Usono komencis, finiĝos en fiasko kun centmiloj da mortintoj.”
</p> 
<p>Kiam antaŭ tri jaroj komenciĝis la interna milito en Sirio, Eŭropo, Unuiĝintaj Nacioj,
   Nord-Atlantika Traktat-Organizaĵo decidis ne interveni, agnoskante la suverenecon
   kaj timante pliintensigon de la konflikto. Sed Usono baldaŭ forgesis la „ruĝajn limojn” difinitajn de Obama: kreskis perforto. Oni nun vidas, ke ne-interveno kondukas al
   ne-solvo.
</p> 
<h3>Pasive</h3> 
<p>Netaŭgajn armilojn sendis Eŭropo kaj Okcidento al sin defendantaj kurdoj, jezidoj,
   peŝmergoj ktp en norda Irako. Ili pasive rigardis, kiel Turkio subtenis kontraŭkurdajn
   atakojn en Sirio. Ŝajnas, ke Turkio volas aŭ almenaŭ akceptas malfortigon de la kurdoj
   por mem profiti en norda Sirio.
</p> 
<p>La sorto de homoj en la sieĝata landlima urbo Kobani decidiĝos eble en Turkio. La
   kurdaj batalantoj ne fidas ŝtaton, kiu lasis la loĝantojn de la urbo en dilemo. Temas
   pri vivo aŭ morto por tiuj homoj, kiuj eĉ ne povas fuĝi, ĉar Turkio fermis la landlimon
   kun Sirio.
</p> 
<h3>Urĝe</h3> 
<p>En la turka ĉefurbo Ankaro estas kuracataj vunditaj batalantoj de Islama Ŝtato: turkoj
   dum longa tempo malhelpis kurdajn sindefendantojn. Nur fine de oktobro Turkio permesis,
   ke 150 peŝmergo-batalantoj atingu Kobani kun pezaj armiloj, urĝe bezonataj kontraŭ
   la kirasveturiloj de la IS-terormilico.
</p> 
<p>En Kolonjo, Germanio, futbalhuliganoj kaj ekstremdekstruloj dum manifestacio kontraŭ
   salafistoj – anoj de islama sekto – batalis kontraŭ la polico, vundis 49 policanojn kaj renversis policaŭtomobilon. Evidentas,
   ke maltoleremo kaj strebo al unukultureco kreskas en la mondo kaj videblas ne nur
   ĉe militemaj islamistoj.
</p> 
<h3>Hejme</h3> 
<p>Rafik Schami, german-siria verkisto, naskiĝinta en Damasko kaj delonge loĝanta en
   Germanio, ĝue sin sentas hejme en ambaŭ kulturoj. Li diras: „Multkultureco estas daŭra defio ... Kultura toleremo estas pli ol paca kunekzistado
   de diversaj manĝokartoj”.
</p> 
<p>Pli komplika estas paca kunekzisto de religioj. Multaj religioj estas ligitaj al lingvo:
   Torao uzas la hebrean, la biblia Jesuo parolis la aramean, kristanismo komence uzis
   la grekan kaj longtempe la latinan, la Korano estas en la araba, pli precize en dialekto
   – la kuraŝita (aŭ kurajŝita) – parolita en la regiono de Mekka en la sepa jarcento.
</p> 
<h3>Regione</h3> 
<p>Ne nepras lingvistike aŭ etimologie detali, ĉar ĉiu scias, kiel lingvaj esprimoj varias
   regione kaj ŝanĝiĝas dum la tempo. Ĉiu lingvo, ĉiu dialekto, ĉiu tempo, ofte eĉ unuopaj
   grupoj, havas siajn diraĵojn, idiomojn kaj metaforojn. Oni nur aŭskultu grupon da
   junuloj en lerneja korto aŭ ĉe tramhaltejo. Nekompreno, miskomprenoj kaj eraroj multas.
</p> 
<p>Ekzemple, la greka „<i>λόγος</i>” [logos], laŭ la evangelio laŭ Johano la komenco de la mondo, kaj eĉ dio mem (prologo),
   havas multajn signifojn kiel „parolo”, „vorto”, „informo”, „onidiro”, ankaŭ „senco”, „racio”, „temo”, „enhavo”, „doktrino”, „ideo”.
</p> 
<h3>Malfacile</h3> 
<p>La araba vorto „<i>ĝihad</i>” ofte estas tradukata kiel „sankta milito”. La vorto signifas nek „sankta”, nek „milito”. Ĝi malfacile tradukeblas kaj signifas lukton, penon, sinapogon kontraŭ peko kaj
   strebon je morala vivo. Ĝi signifas uzi ĉiujn siajn fortojn, riski ĉion, venki sian
   timon, egoismon, personajn interesojn.
</p> 
<p>Lukto por paco kaj justeco estas 
   <i>ĝihad</i>; sen 
   <i>ĝihad</i> tiu lukto estus malsincera kaj duonkora. Multe da islamaj eruditoj kaj iliaj studoj
   tion konfirmas. Sed estas ankaŭ aliaj, kiuj mistradukas kaj misinterpretas la vorton
   kaj alvokas aliajn al milito. Kiu batalas kun arda fajro, ne estas brulofajranta kaj
   mortiganta soldato, sed iu, kiu fervore klopodas. Tion ordonas ankaŭ la Korano, uzanta
   la vorton „<i>ĝihad</i>”.
</p> 
<h3>Egale</h3> 
<p>Nek Torao, nek Biblio, nek Korano rekomendas militon, kvankam ĉiu el ili mencias kruelaĵojn
   kaj malicajn leĝojn el pratempoj. Milito neniam bonas, egale pri la celo, ĉar ĝi alportas
   malfeliĉon, suferon, morton.
</p> 
<p>Interpreti religian tekston postulas ne nur lingvajn kaj kulturajn konojn, sed respondecon.
   Povas esti kontraŭ la origina signifo eĉ laŭvorta traduko, sed sen konsidero de la
   senco.
</p> 
