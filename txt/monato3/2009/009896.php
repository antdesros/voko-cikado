      <h3>Ekonomio</h3>
      <h3 class="tem">POLLANDO</h3>
      <h2>Kvin jarojn en Eŭropa Unio</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Pasis kvin jaroj ekde la membriĝo je la 1a de majo 2004 de Pollando en Eŭropa Unio (EU). Eblas jam fari la bilancon de la polaj avantaĝoj. Unu el la plej gravaj estas la aliro al financado por la ekonomia disvolviĝo: teknikaj strukturoj, entreprenado, sciencaj esploroj, kreado de novaj laborlokoj, financado de agrikulturo kaj de kamparaj regionoj.</b></p> <p>Ekde la 1a de majo 2004 ĝis la fino de majo 2009 ĉiuj transpagoj de EU al Pollando atingis sumon de 31 miliardoj da eŭroj. Aliflanke Pollando dum tiu periodo kontribuis al la buĝeto de EU per 14 miliardoj da eŭroj. La bilanco do sumiĝas je pluso de 17 miliardoj entute, aŭ preskaŭ 3,5 miliardoj da eŭroj jare, kio en polaj zlotoj donas imponan sumon de 14 miliardoj. En la jaroj 2004-2007 Pollando ricevis la maksimuman sumon kaj tre bone profitis la eŭropuniajn monrimedojn (PHARE, ISPA, NPR 2004-2006, Fonduson por Kunteniĝo, rimedojn por agrikulturo kaj por kamparaj regionoj). Pollando faris grandan salton antaŭen en la jaro 2008, kiam ĝi povis akiri grandan kvoton (preskaŭ 20 % de la tuta buĝeto de 70 miliardoj da eŭroj por la periodo 2007-2013) por la realigado de la koheriga politiko.</p> <p>Pollando ekde sia membriĝo ricevadis ĉiujare pli da mono. En la jaro 2008 tiu tendenco estis haltigita kaj Pollando ricevis la saman sumon kiel en 2007. Ankaŭ la membrokotizo de Pollando en EU ĉiujare kreskis. Finfine la saldo de la kontoj kun EU en la jaro 2008 estis je preskaŭ 600 milionoj da eŭroj pli malbone ol en 2007.</p> <div style="text-align:right">Stanisław Ś<span class="mm">MIGIELSKI</span></div>   <p>La <i>Fonduso por Kunteniĝo</i> (angle <i>Cohesion Fund</i>, germane <i>Kohäsionsfonds</i>, france <i>Fonds de cohésion</i>) estas unu el la financaj iloj, per kiuj EU celas malgrandigi la diferencojn en la disvolviĝo de la regionoj kaj membroŝtatoj. Por la jaroj 2007-2013 la fonduso disponas pri entute 70 miliardoj da eŭroj.</p> <p>La helpo estas destinita al landoj, kies laŭpersona malneta enlanda produkto (MEP) estas malpli ol 90 % de la EU-mezumo, kaj estas uzota por infrastrukturaj investoj en mediprotektado kaj transporto.</p> <p>Ekzistas kelkaj kondiĉoj. La profitanta lando devas mem pagi parton (kutime 50 %) de la subvenciata investo. Se la buĝeta deficito de la lando superas 3 % de la MEP, novaj projektoj ne estas akceptataj.</p> <div style="text-align:right">rr</div> 
