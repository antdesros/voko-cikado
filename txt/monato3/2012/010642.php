      <h3>Politiko</h3>
      <h3 class="tem">GERMANIO</h3>
      <h2>Perditaj prezidantoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ malpli ol du jaroj unuafoje abdikis germana prezidanto. Horst Köhler [kéler], vizitinte germanajn trupojn en Afganio kadre de la UN-mandato, diris, ke eblus milite interveni ankaŭ pro ekonomiaj interesoj.</p> <p>Kritikite kaj de opozicianoj kaj de politikistoj de la registara koalicio, Köhler deklaris, ke lin tiel ataki estas malrespekte. Li abdikis en majo 2010.</p> <p>Malpli ol du jarojn poste abdikis dua germana prezidanto. Christian Wulff, la posteulo de Köhler, rezignis pri la prezidanteco en februaro 2012.</p> <h3>Gastoj</h3> <p>Ne ĉiutage oni fariĝas prezidanto kaj la eventon volis festi Wulff. La feston la nova prezidanto deklaris privata, tamen ĝi estas organizita de profesiulo, kiu similajn eventojn aranĝas por eminentuloj. Kelkaj gastoj pagis 3000 eŭrojn por ricevi enirbileton.</p> <p>Wulff agis ŝajne laŭ la principo „konatecoj kaj rilatoj”, kiel en la iama Sovetio. Volonte li akceptis invitojn al senkostaj feriovojaĝoj al Hispanio, Italio kaj Usono.</p> <p>Rolon ludis ankaŭ la filmisto David Groenewald. Tiu mendis kaj pagis feriojn, pruntis monon, aĉetis poŝtelefonon por sia amiko Wulff. Nun prokuroroj priesploras la kontraŭleĝajn avantaĝojn kaj profitojn. Ekzemple, la federacia ŝtato Malsupra Saksio (kies ministroprezidanto estis Wulff) promesis garantii kvar milionojn da eŭroj por filmentrepreno, en kiu estas implikita Groenewald.</p> <h3>Donacoj</h3> <p>En Germanio al ĉiu ŝtatoficisto estas malpermesite akcepti donacojn. Nepre oni volas eviti suspekton pri korupto aŭ subaĉeto. Eĉ kristnaskaj donacetoj al poŝtisto aŭ stratpurigisto estas kontraŭleĝaj: nur esceptokaze ili estas permesitaj.</p> <p>Dum longa tempo Wulff nur duonklare respondis demandojn kaj eĉ provis influi la gazetaron. Kreskis suspektoj. La 16an de februaro prokuroro petis nuligi la juran imunecon de la prezidanto. Unu tagon poste Wulff abdikis.</p> <p>Perdinte duan prezidanton, kelkaj germanoj nun demandas, ĉu necesas la posteno. Aliaj germanoj opinias, ke ekzistas pli gravaj aferoj ol persekuti prezidanton pro donacoj de amikoj. Ĉiaokaze, prezidanto – la ŝtatestro, posedanto de la plej alta posteno en la lando – modele sin prezentu al siaj kuncivitanoj.</p> <div style="text-align:right">Jomo I<span class="mm">PFELKOFER</span></div> 
