      <h2>Malnova monumento de la serba kulturo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Sendube la plej memorinda monumento de la cirillitera skribmaniero estas la tiel nomata Evangelio de Miroslav (Miroslavljevo Jevanđelje) el la dek-dua jarcento.</p> <p>Ĝi ekestis je mendo de Miroslav, princo de Hum (teritorio koincida kun la nuntempa Hercegovino) ĉirkaŭ 1185, por servi la bezonon de preĝejoj. Tia libro estis uzata por la mesa liturgio kaj estis aranĝita por legado dum unu jaro. La evangelio estis skribita sur pergameno kaj ornamita de 296 miniaturoj. Tiuj miniaturoj estis pentritaj per plumo kaj poste kolorigitaj per peniko en ruĝa, verda, flava kaj blanka koloroj, ornamitaj kun oro. La libro konsistas el 181 folioj, binditaj en ledo, proksimume A3-formata.</p> <p>La artisma pentraĵo montras kombinon de orient-eŭropa kaj okcident-eŭropa kulturelementoj: bizanca tradicio en la ikonografia ornamiteco de la serba teksto kaj miniatura stilo, devena de italaj kopiejoj. La pentrada ornamado de la manskribaĵo per tiuj miraklaj miniaturoj kun neniam ripetitaj motivoj el flaŭro kaj faŭno faras la libron majstraĵo, unika en sia speco. La beleco kaj la unika karaktero de la evangelio de Miroslav poste influis aliajn manuskriptojn en la regiono kaj en mezepoka Eŭropo.</p> <p>La grandvalora dokumento estas en preskaŭ perfekta stato kaj atestas la povon, kiun la kristanaj princoj de Balkanio disponis je la fino de la 12a jarcento.</p> <p>Dum la serba regado de Aleksandar Obrenović (1876-1903) ĝi troviĝis en la monaĥejo de Ĥilandar en Grekio. En marto 1896 li ricevis ĝin kiel donacon de la monaĥaj fratoj kaj alportigis ĝin al Beogrado. Ĝi estas la plej grandvalora kaj la plej grava dokumento de la kultura heredaĵo de Serbio.</p> <p>La Evangelio de Miroslav listiĝas en la Internacia Registro „Monda Memoro”, grava programo de Unesko, starigita en 1992. Ĝi postsekvas du aliajn listojn, tiun de la Monda Heredaĵo, kiu entenas jam 851 materiajn objektojn kaj tiun de la „Buŝaj kaj nemateriaj heredaĵoj de la homaro”. La registro „Monda Memoro” celas konservi la kolektaĵojn arkivajn kaj bibliotekajn kaj disvastigi ties priscion. Ĝi enhavas 122 memoraĵojn, inter kiuj tri slavlingvajn evangeliaĵojn: tiujn de Arĥangelsk (11a jarcento), Miroslav (12a jarcento) kaj Ĥitrovo (15a jarcento).</p> <div style="text-align:right">Svetlana M<span class="mm">ILANOVIĆ</span></div>  
