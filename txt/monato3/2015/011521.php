      <h3>Scienco</h3>
      <h3 class="tem">ITALIO</h3>
      <h2>Festanta la revenon de Astrosamantha</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>La plej fama itala virino – tiu, al kiu la itala gazetaro dediĉis la plej larĝan spacon ekde la pasinta novembro
      – estas nuntempe nek aktorino, nek kantistino, nek modelulino, nek koramikino de iu
      dikmuskola piedpilkisto. Temas (kiel malofta afero) pri sciencistino nome Samantha
      Cristoforetti [samanta kristoforeti]. Ĉu vi ne konas ŝin? Guglu ŝian nomon kaj aperos
      oceano da fotoj, artikoloj, filmoj pri ŝi kaj ŝia agado: Interreto igis tiun seriozan
      kaj senŝminkan junulinon vera stelo amaskomunikila.
   </b>
</p> 
<p>Ni unue diru, ke ŝi estas unu el la kvar italaj astronaŭtoj dungitaj ĉe la Itala Kosma
   Agentejo. Naskiĝinta en Milano antaŭ 38 jaroj kaj poste kreskinta inter la valoj kaj
   la montoj de la norda regiono Trentino – Alto Adige, Samantha Cristoforetti estas ege pli konata en kaj ekster Italio kiel
   Astrosamantha: tiel, menciante la pli kaj pli popularan pseŭdonimon de ŝia 
   <i>Twitter</i>-konto, la itala ĉefministro Matteo Renzi [mateo renci] mem salutis la italan „kapitaninon” okaze de ŝia alteriĝo.
</p> 
<p>La sciencistino, magistriĝinta dufoje kaj poste distingiĝinta inter 8500 aspirantaj
   astronaŭtoj, partoprenis ekde la 24a de novembro 2014 la sciencan mision 
   <i>Futura</i>, efektivigitan ĉe la Internacia Kosmostacio (IKS). Por obteni ĉi tiun postenon, ŝi
   devis montri sin kapabla plenumi plej diversajn taskojn kaj alfronti iujn ajn problemojn.
</p> 
<p>„Dum mia restado en IKS”, ŝi deklaris al la itala ĵurnalistino Gaia Giorgetti antaŭ sia forveturo, „mi devos okupiĝi pri simpla bontenado, elektraj problemoj, mastrumado de la provizoj,
   kiuj alvenos per ŝarĝveturiloj. Sed mia ĉefa rolo estos efektivigi tutan serion da
   sciencaj testadoj, ok el kiuj organizas Italio. Mi pristudos materialojn, brulaĵojn,
   kaj mi faros fiziologiajn testojn, kiel eksperimenta kobajo. La kosmo ebligas observi
   fenomenojn kaj procezojn okazantajn sen la tera gravito.”
</p> 
<h3>Rekordo</h3> 
<p>Ŝi ne estis sola en tiu ĉi misio: ŝiaj du kunvojaĝantoj el la tero al IKS estis la
   rusiano Anton Ŝkaplerov kaj la usonano Terry Virts. En IKS ili renkontis tri kromajn
   sciencistojn. Samantha, kiu regas la anglan kaj la rusan (kio ebligis al ŝi bone komunikiĝi
   kun siaj kolegoj; ofte ili uzis miksaĵon de la du idiomoj), revenis teren post ekzakte
   200 tagoj.
</p> 
<p>Ŝi alteriĝis la 11an de junio, fiera ne nur pri la tuta ĝenerala sperto travivita,
   sed ankaŭ pri la rekordo de ŝi freŝe atingita: oni fakte memorados ŝin, ĝis alia esploristino
   refoje rompos la rekordon, kiel la ino, kiu restis plej longe en la kosmo. La tuta
   revena operacio estis spektata televide kaj interrete en Italio kaj ne nur. Kiam ŝi
   alvenis al la bazo, situanta en Bajkonur (Kazaĥio) (el la sama loko ŝi estis ekfluginta
   en novembro), ŝi esprimis du dezirojn: manĝi finfine freŝan miksitan salaton kaj duŝiĝi:
   en la kosmo ĉi tio ne eblas, kaj ŝi devis lavi sin per spongo kaj aliaj provizoraj
   rimedoj.
</p> 
<p>„Mi ĝojas esti ĉi tie kaj havi la eblon fari veran duŝon, sed mankos al mi la ideo
   povi ŝvebi libere”, asertis Cristoforetti el Houston post sia alteriĝo. Tie, en Usono, ŝi estas devigita
   pasigi periodon de readaptiĝo al la vivo sur la tero kaj aparte al ĝia gravito. Pri
   la famo, kiun ŝi iom neatendite (almenaŭ en tiaj dimensioj) gajnis en la lastaj monatoj,
   ŝi deklaras sin kontenta, sed jam preta kaj ema reveni al sia normala esplorista vivo:
   „La tasko de astronaŭtoj ne estas fariĝi famuloj: mi certas, ke tiu ĉi granda bruo
   baldaŭ finiĝos.”
</p> 
<h3>Fotografaĵoj</h3> 
<p>Restos longe tamen la mesaĝoj, la fotografado, la intervjuoj al italaj televid-elsendoj
   (ekzemple en februaro, dum la historia muzika festivalo de Sanremo, kiam ŝi plurfoje
   rotaciigis la mikrofonon, respondante al la demandoj de la prezentisto), kiuj igis
   infanojn (kaj ne nur) revi. Samantha aperigis ĉiutage en Interreto plurajn multkolorajn
   fotojn faritajn el la kosmo: jen insularoj, montoj, riveroj, lagoj, maroj, urboj kaj
   aliaj homaj konstruaĵoj, kiajn neniu estis antaŭe vidinta. Ĉiu foto estis komentata
   en 
   <i>Twitter</i> dulingve, en ŝia gepatra lingvo (la itala) kaj en la angla.
</p> 
<p>Aldonis Samantha: „Kiel lernejano, mi neniam elstaris pri geografio, sed iom post iom la tero iĝis al
   mi konata, kaj mi sukcesis rekoni ĉe la unua rigardo la diversajn areojn de la planedo.” Post 200 tagoj ŝvebadi iĝas plej normala ago: „Estas eble transdoni objekton al kolego, simple lanĉante ĝin al li. Antaŭhieraŭ mi
   estis ĵetonta poŝtelefonon al mia kuracisto, sed poste mi haltis, kaj mi ekkonsciis,
   ke mi ne plu estas en la kosmo!”
</p> 
<h3>Litsakoj</h3> 
<p>„Mi neniam forgesos la vivon en IKS, je 400 kilometroj super la tero (kiujn veturilo
   
   <i>Sojuz</i> laŭiras ene de apenaŭ 8 minutoj je la nekredebla rapido de 27 000 km/horo), dum ĉiuj
   ĉi monatoj. Mi sentos la mankon de tiu privilegiita fenestro por rigardi la teron.” Kion ŝi manĝis? Ĉefe kvinoon (herbecan jaran planton de la familio amarantacoj kultivatan
   precipe pro la grajnoj riĉaj je proteinoj), skombron, sed ankaŭ kelkspecajn legomojn.
   Oni faris teston pri nutrado; pli precize pri la rilato inter nutrado kaj sano. Pri
   dormado, la astronaŭtoj dormis ene de ŝvebantaj litsakoj alkroĉitaj al muro. Filmo
   kun detala priskribo haveblas ĉe la retpaĝo 
   <a href="http://https://youtu.be/YlUzva6lRQQ">https://youtu.be/YlUzva6lRQQ</a>.
</p> 
<h3>Sunleviĝo(j)</h3> 
<p>Kiujn karakterajn ecojn devas, fine, havi astronaŭto? „Li/ŝi devas elteni la longecon kaj la malfacilecon de la trejniĝo: ĝi estas ne malsama
   ol maratono, do bezonatas daŭraj sinkoncentrado kaj sindediĉo.” La trejniĝo, al kiu Samantha Cristoforetti aludas, daŭris ĉirkaŭ 2,5 jarojn: en la
   unuaj ok monatoj oni studas la teorion, la funkciadon de la diversaj IKS-aparatoj.
</p> 
<p>Poste venas la praktika parto: oni devas scipovi interveni kun la celo savi la vivon
   de la kolegoj kaj ĝenerale gardi la kosmostacion. Oni imitas rutinon kaj neantaŭviditaĵojn,
   lernigante la manieron savi, se necese, la deĵorteamon (en januaro Samantha kaj ŝiaj
   kolegoj devis vere alfronti iun danĝeran situacion) kaj solvi eventualajn averiojn.
</p> 
<p>La plej signifoplenaj momentoj estis, laŭ Samantha, la alveno al IKS, la komenca konstato
   pri la foresto de pezo, samkiel la fina vojaĝo, „kiam la veturilo 
   <i>Sojuz</i> disiĝis kaj ni vidis nian lastan kosman sunleviĝon”. (Penvaloras tie ĉi atentigi, ke pro la konstanta moviĝo de la IKS la astronaŭtoj
   spertis 16 ektagiĝojn kaj 16 sunsubirojn ĉiutage.)
</p> 
<p>La sciencistoj nun priesploros ĉiujn kolektitajn donitaĵojn kaj tio bezonos kelkan
   tempon.
</p> 
