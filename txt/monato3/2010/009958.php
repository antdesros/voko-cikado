      <h2>Kosmologio de nia jarcento</h2>
      <!-- komenco de la enhavo -->

       

      <p>Dum jaroj mi konas Manuel Halvelik. Ofte mi renkontis lin en nia loka Esperanto-klubo. Mi sciis, ke li estas erudito, pro liaj intervenoj dum debatoj pri iu ajn temo.</p> <p>Antaŭ nelonge mi hazarde trovis en la reto tekston lian kun la titolo „Sotragogismo” (<a href="http://www.sotragogismo.net">www.sotragogismo.net</a>). Temas pri pli ol 400-paĝa dokumento pri kosmologio. Baldaŭ evidentis, ke la enhavo transiras miajn digesteblojn. Mi konfrontiĝis i.a. kun fakvortoj el filozofio, natursciencoj, matematiko ..., kiujn mi ne komprenas, kun mencioj de sciencaj leĝoj, kiujn mi ne konas ... Malgraŭ tio mi daŭrigis trafoliumadon kaj legadon de partoj. Mi konfrontiĝis kun aro da tute novaj ideoj, ĉiufoje argumentitaj per citaĵoj de aŭtoritataj figuroj aŭ kun mencio de nuntempaj sciencaj malkovroj. Halvelik ne timas montri erarojn aŭ mankojn ĉe plej renomaj sciencistoj. Evidente mi ne povus prijuĝi la enhavon, sed per tiu ĉi artikoleto mi esperas instigi sciencistojn konatiĝi kun tiu ĉi riĉenhava verkaĵo.</p> <p>Jen kion Halvelik mem skribis pri ĝi:</p> <p>„Temas ja vere pri nova paradigmo, t.e. nova mondbildo kiel siatempe la teorio de Copernicus, kiu ŝanĝis la mondpercepton kaj kune la homsocion. Granda diferenco kun aliaj filozofiaj sistemoj estas ke la mia estas firme apogata de sciencaj donitaĵoj kaj de matematiko.”</p> <p>Ĝi aperas ĝustatempe en nia homa kulturhistorio, ĉar nur ĵus fizikistaj medioj konstatis kaj certigas, ke la du grandaj teorioj, kiuj regis la pasintan jarcenton, <i>Kvantumfiziko kaj Relativeco, plu ne validas pro internaj sinkontraŭdiroj</i>. Kiu mondbildo ilin anstataŭigu, ili neniel scias kaj tion ankaŭ ne hezitas konfesi. La materiismaj amaskomunikiloj ankoraŭ turnas surdan orelon al tiu katastrofa mesaĝo, ĉar ili ja staras aŭ falas per „La Scienco”. Tio facile eblas, ĉar teknologio plu produktas kaj produktos mirigaĵojn, sendepende de teoriaj fundamentoj. Sed ja frue aŭ malfrue la ŝirma veziko de la „publika opinio” krevos. Ne forgesu: „ideoj regas la mondon”. Do ne timu; kvankam mia verko estas malfacile alirebla, ĝi baldaŭ venos al la surfaco. Fakte, jam relative multaj homoj vizitis mian retejon kaj ankaŭ elŝutis materialon, sed (provizore) sen videbla rezulto. Kredeble enparte ankaŭ pro tio, ke eĉ esperantistoj mem neniel atendas renkonti ion historie decidan en nia verda medio.</p> <p>Halvelik ankaŭ spertis, ke Esperanto ne sufiĉe precizas por la ĉiam pli rapide aperantaj novaj terminoj. Li proponas adaptojn de Esperanto por la terminologio kaj por la ortografio kaj nomis sian projekton <i>Uniespo</i> (universala Esperanto). Sed la tekstoj enhavas klarigojn pri ne-Zamenhofaj vortoj kaj pri la adaptita ortografio.</p> <div style="text-align:right">Ivo D<span class="mm">URWAEL</span></div> 
