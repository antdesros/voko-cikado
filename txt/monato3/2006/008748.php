      <h2>Ruzo en rifuzo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Al la serio <i>Oriento-Okcidento</i> de UEA aldoniĝas ĉi tiu bele bindita kaj eldonita libro. La jam abunde tradukita verko aperas nun komplete en Esperanto. Hašek mem travivis la unuan mondmiliton, en kiu mortis 15 milionoj da personoj, inkluzive de miliono da soldatoj el la aŭstra-hungara armeo. Lia reago estis ĉi tiu libro, verkita en 1921-1922, sed li mortis en 1923, fininte nur kvar el la ses planitaj partoj.</p> <p>La heroo, kvankam laŭte proklaminta deziron lojale batali por la imperiestro, renkontas serion da malliberejoj, malsanulejoj kaj internigejoj, vidas homojn, kiuj ne povas aŭ ne volas batali, kaj diversajn absurdajn vizaĝojn de la milita maŝinaro kaj devas iel defendi sian identecon. La verko tiom penetris en la ĉeĥan konscion, ke ekzistas la verbo <i>švejkovat</i> kun la signifo „per ruzo travivi”. Kvankam plejparte rakonta kaj intim-scena, fojfoje la verko levas sin al pli alta rigardo, kiel fine de unu el la komencaj ĉapitroj: „Kaj ie en la foraj malproksimoj de la historio, ŝvebas al Eŭropo la fakto, ke la morgaŭo detruos ankaŭ la planojn de la nuntempo.”</p> <p>Hašek pioniris novan verkstilon, analogan al tiu de la tiutempaj dadaismaj pentristoj. Iuj opinias, ke tiu rekta, senornama stilo estis komenco de tradicio, sekvata poste de Hemingway, Fitzgerald, Faulkner kaj aliaj. Aserteblas, ke ĉi tiu estas pionira kontraŭ-milita romano. La aŭtoro de la difina tiuspeca romano pri la dua mondmilito Joseph [jozef] Heller mem legis la libron Švejk kaj konfesis, ke sen tiu sperto lia romano <i>Kaptilo 22</i> neniam estus verkita.</p> <p>Mi ne povas kompari la tradukon kun la ĉeĥa originalo, sed ĝi bone legiĝas kaj klare transdonas sentojn de ironio kaj satiro. Ĝi rajtas teni sian kapon alte, kompare kun fragmentoj el diversaj tradukoj al la angla. Gratas nur la kelkfoje ripetita „ke mi igos vin malliberigi” – ĉu eraro, ĉu klopodo redoni ĉeĥan misparolturnon?</p> <p>La reagoj de Švejk ridigas pro la ŝajna manko de logika ligo inter burokrataj demandoj kaj liaj respondoj. Mezurilo de traduk-kvalito estas, kiom traduko lasas al vi konkludi, ke Švejk estas efektive stulta aŭ ke li ruze ŝajnigas/uzas tiun stultecon kiel traviv-teknikon. La esperanta traduko konvene starigas la demandon, sed lasas ĝin malfermita. Ankoraŭ plia literatura klasikaĵo nun havebla nialingve.</p> <div style="text-align:right">Stefan M<span class="mm">AC</span>G<span class="mm">ILL</span></div> <h6 class="bib">Jaroslav Hašek: Aventuroj de la brava soldato Ŝvejk dum la mondmilito. Trad. Vladimír Váňa. Eld. Kava-Pech, Dobřichovice, 2004. 712 paĝoj. Firme bindita. Ilus. Josef Lada (nepo de la samnoma origina ilustristo). ISBN: 80-85853-76-0.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=vejk">la Retbutiko</a>.
        </h6>
