      <h3>Ekonomio</h3>
      <h3 class="tem">MINADO</h3>
      <h2>Trepça, pomo de malkonkordo inter Kosovo kaj Serbio</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <b>Lastatempe la rilatoj inter Kosovo kaj Serbio ekkonis novan tension pro la minejoj
      de Trepça, apud la urbo Mitrovica en Kosovo. La laboristoj strikas, ĉar ili timas
      pri sia estonta sorto pro disputo pri la proprieto de la minejoj.
   </b>
</p> 
<p>Temas pri tre malnova aro da minejoj de plumbo kaj pluraj aliaj valoregaj kaj maloftaj
   mineraloj kaj miliardoj da tunoj da lignito, ekspluatita jam de iliroj (la antaŭantoj
   de la albanoj) kaj poste de romianoj, bizancanoj, otomanoj, britoj, germanoj dum la
   dua mondmilito, jugoslavoj post la milito kaj nun de albanoj, almenaŭ parte. Inter
   la jaroj 1945 kaj 1990 tie oni jare produktis 600 000 tunojn da plumbo. Antaŭ la jaro
   1999 en la 40 branĉoj laboris pli ol 22 000 personoj.
</p> 
<p>Ne nur estis la minejoj, sed estis diversa industrio pri prilaborado de la ercoj kaj
   liverado de rilataj produktoj. Menciindas la ercoj kaj koncentraĵoj de plumbo kaj
   zinko, la rafinita plumbo, la arĝento, la oro, la bismuto, la elektrolita zinko kaj
   kadmio, la sulfata acido, la fosfata acido, la superfosfata sterkaĵo, la elektraj
   akumuliloj, la nikelkadmio, municioj por ĉasado, plumbaj tuboj kaj dratoj, zinkita
   lado kaj produktoj el ĝi, elektrokontaktaj materialoj, vagonetoj kaj elektrolokomotivoj
   por ercejoj, ekipaĵoj kaj vicpecoj por metalurgio kaj ercejoj kaj aliaj.
</p> 
<h3>Trepça laboras</h3> 
<p>Dum Jugoslavio Trepça enspezigis ĝis pli ol 100 milionoj da usonaj dolaroj jare danke
   al la eksporto de plumbo. Pro tio tiutempe cirkuladis la onidiro, ke „Trepça laboras, dum Beogrado konstruas”. Kelkaj eĉ diras, ke ĝi estis la kaŭzo de la eksplodo de la konflikto inter albanoj
   kaj la serba ŝtato en la jaroj 1998-1999. Aktuale la produktado estas tre limigita,
   ĉar la minejo bezonas multe da investaĵoj por la renovigo. Plia problemo estas, ke
   parto de ĝiaj entreprenoj troviĝas en la teritorio regata de serbaj municipoj ene
   de Kosovo. Post la milito la tiama UN-institucio pri Kosovo, UNMIK, deklaris la minejon
   ne ŝtata, sed administrota de aparta institucio, Agentejo de Kosovo por Privatigo.
</p> 
<p>Antaŭ nelonge la registaro de la sendependa Kosovo, estrata de Isa Mustafa, deklaris,
   ke Trepça devas aparteni al Kosovo kaj helpi ĝin en la evoluigo de la ekonomio kaj
   la mildigo de la amasa senlaboreco. Tion kontraŭstaras la registaro de Serbio. Dume
   la albanaj ministoj strikas, timante pri sia estonta sorto kaj dezirante, ke la minejo
   estu redonata al la ŝtato de Kosovo.
</p> 
