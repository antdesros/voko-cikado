      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Nova leĝo ... sed malleĝeraj demandoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kroatio tuj enironte Eŭropan Union komence de julio kaŭzis gravan skandalon. Tri tagojn antaŭ la eniro la kroata parlamento akceptis laŭ insisto de la registaro leĝon pri ekstradicio de krimuloj.</p> <p>La leĝo validas nur por krimoj faritaj post 1991 (do post la ekesto de Kroatio kiel memstara ŝtato). Tio kontraŭas al la ekstradicia leĝo pri livero de krimuloj inter EU-ŝtatoj, kiu ne havas tempolimon.</p> <p>Tamen Kroatio klare subskribis traktatojn, en kiuj ĝi sin devigas akordigi ĉiujn siajn leĝojn kun la eŭropaj. Do ĝi agis kontraŭeŭrope jam en la komenco de sia EU-membreco.</p> <h3>Kunvokis</h3> <p>Kolere reagis la Eŭropa Komisiono. Ĝin reprezentis Viviane Reding, kiu postulis tujan nuligon de la leĝo. La kroata registaro gvidata de Zoran Milanović kunvokis la parlamenton meze de septembro por ŝanĝi la leĝon, por ke tiu validu senkonsidere pri la tempo, kiam la krimo estas farita.</p> <p>La parlamento decidis prokrasti la aplikon de la leĝo ĝis oktobro 2014. Tio eĉ pli kolerigis la Eŭropan Komisionon. Tiu proponis sankciojn kontraŭ Kroatio (ne doni la planitajn 600 milionojn da eŭroj por prepari la Schengen-limon aŭ enkonduki konstantan observadon de la politiko de Kroatio). Sekvis alia kunsido kun EU kaj deklaro, ke Kroatio tre baldaŭ solvos la problemon.</p> <h3>Enplektita</h3> <p>Ĉiu en Kroatio nomas la leĝon Lex Perković. La leĝo estas farita por ŝirmi iun Josip Perković kaj eventualajn kunlaborantojn. Lin volas juĝi en tribunalo Germanio, pro tio, ke li estas enplektita en la murdo en la 70aj jaroj de Stjepan Đureković, kroata azilpetanto en Germanio.</p> <p>Perković estis ĉefo en la sekreta polica servo en Jugoslavio ankoraŭ en la komunisma erao. Li restis en la posteno ankaŭ poste kaj liajn servojn akceptis kaj la dekstra registaro dum kvar mandatoj kaj la maldekstra dum du mandatoj.</p> <p>Ne estas klare, kial la nuna socialista registaro volas ŝirmi lin. Verŝajne li posedas skandalan sekreton pri la nun reganta Socialdemokrata Partio, kiun prezidas la nuna ĉefministro.</p> <h3>Malstabiligi</h3> <p>Kiam Perković estos tamen ekstradiciita al Germanio, la sekreto verŝajne aperos kiel novaĵo, kiu povos malstabiligi la registaron. Intertempe estas interesa la manipulado de la popolo rilate la aferon.</p> <p>Milanović aŭdacis aserti, ke li ne obeos iun Viviane Reding, kiu diference de li ne estas demokratie elektita. Tamen nek li nek la ĵurnalistoj diris la simplan veron, ke Kroatio, subskribante la traktaton pri aliĝo al EU, akceptis, ke leĝoj de EU superas kroatajn leĝojn.</p> <p>Tiel manipulante la popolon, kiu plejparte ne komprenas tiajn faktojn, Milanović volis kolekti favorajn politikajn poentojn por si kaj la partio, ŝajnigante sin granda defendanto de Kroatio kontraŭ la diktatora EU.</p> <div style="text-align:right">Zlatko T<span class="mm">IŠLJAR</span></div> 
