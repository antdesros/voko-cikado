      <h3>Moderna vivo</h3>
      <h3 class="tem">RUSIO</h3>
      <h2>Profilo paradoksa</h2>
      <!-- komenco de la enhavo -->

       

       <p>La meza loĝanto de Rusio havas 37 jarojn kaj tiel – almenaŭ statistike – „maljuniĝis” je tri jaroj kompare al 1989. Li aŭ ŝi estas edz(in)o kaj havas unu infanon, sed fakte plej verŝajne estas ino, ĉar en Rusio vivas je dek milionoj pli da virinoj, ol viroj. Probable pro tio la vortoj <i>patrujo, lando, naskolando</i> in-genras en la rusa lingvo.</p> <p>Loĝas la statistika mezulo en negranda distrikta urbeto. Nur ĉiu kvina loĝas en urbego. La averaĝulo havas mezan profesian kleron kaj laboras en industrio. La meza salajro egalas al 260 usonaj dolaroj monate: tiel rusiano estas duoble pli malriĉa, ol la averaĝa terloĝanto. Paradokse, tamen, Rusio okupas la trian lokon en la mondo laŭ la nombro de miliarduloj. La rusianoj ne kredas, ke riĉuloj dividos sian monon kun ili. Pli ol triono el ili ĉagreniĝas, ke en Rusio estas tiom multe da miliarduloj. Nur 15 % revas mem iĝi miliarduloj.</p> <h3>Komputiloj</h3> <p>En Rusio la plej riĉaj 10 % estas 15-oble pli riĉaj, ol la plej malriĉaj 10 %. La averaĝa rusiano aĉetas nutraĵojn en negrandaj magazenoj, ne povas permesi al si pagi por medicina servo, ne havas komputilon, uzas publikan transporton, pli ofte busojn, kaj ne vojaĝas eksterlanden, feriante hejme.</p> <p>La averaĝa rusiano ne fumas, ŝatas bieron, legas, sportas, kredas je Dio, ne kredas je neidentigitaj flugantaj objektoj, fidas al sia prezidanto (66 %), ne fidas al la parlamento (68 %), ne deziras fordoni siajn filojn por servi en la armeo, tamen amas kaj volas esti amata.</p> <h3>Telefonoj</h3> <p>Tamen la vivo iĝas pli bona. Aleksandro Gorjanin, la aŭtoro de la libro <i>Mitoj pri Rusio kaj spirito de la nacio</i>, diras, ke ne ĉio estas malbona, kaj argumentas, ke grandas la gamo de varoj, ke libroeldonejoj prosperas, ke 20 milionoj da rusianoj veturas eksterlanden. Antaŭ 20 jaroj oni ne povis imagi ion similan. Kaj jam en Rusio estas uzataj 79 milionoj da poŝtelefonoj.</p> <p>Krome kreskas aŭtomobiligo de la loĝantaro: la kvanto de aŭtoj por 1000 homoj en Rusio egalas nun al tiu en Pollando. Oni multe konstruas privatajn domojn kaj loĝejojn. En bankoj kreskas deponoj kaj kontoj. En la nuna Rusio vivas pli da studentoj, ol iam en Sovetio (416 kontraŭ 200 por 10 000 loĝantoj). Ekde 1999 kreskas la naskokvanto. Aliflanke daŭre postrestas la lando rilate konkuradon en mondaj merkatoj.</p> <div style="text-align:right">Sergei P<span class="mm">AĤOMOV</span></div> 
