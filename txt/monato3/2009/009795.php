      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Parolas enketo, sed ne fremdlingve</h2>
      <!-- komenco de la enhavo -->

       

       <p>En marto la ĵurnalo <i>Mainiĉi</i> publikigis enketon pri la planoj de 100 ĉefaj kompanioj por dungi novajn universitato-finintojn. Laŭ la enketo, la kompanioj alte taksas jenajn punktojn ĉe la kandidatoj:</p> <p> <table> <tr> <td>Nombro de kompanioj</td> <td>Ecoj</td> </tr> <tr> <td>80</td> <td>Kapablo pri komunikado</td> </tr> <tr> <td>64</td> <td>Aktiveco</td> </tr> <tr> <td>34</td> <td>Fervoro por la kompanio/laboro</td> </tr> <tr> <td>32</td> <td>Bona karaktero</td> </tr> <tr> <td>27</td> <td>Respondeco</td> </tr> <tr> <td>26</td> <td>Harmonio kun aliaj homoj</td> </tr> <tr> <td>10</td> <td>Adaptiĝo-kapablo</td> </tr> <tr> <td>2</td> <td>Pacienco</td> </tr> <tr> <td>2</td> <td>Scio pri la fako</td> </tr> <tr> <td>2</td> <td>Sperto pri klubaj aktivoj / volontula agado</td> </tr> <tr> <td>0</td> <td>Lingvo-kapablo</td> </tr> </table> </p> <p>Surprizas du aferoj. Unue, la kompanioj ne alte taksas sciencajn atingojn. Junuloj pasigas kvar jarojn aŭ pli en universitato, tamen la kompanioj ne volas konsideri ĉi tiujn jarojn. Ŝajnas, ke la kompanioj volas homojn, kiuj obeeme aŭskultas kaj obeas ordonojn, kaj kiuj energie, sindone kaj ĝismorte dediĉos sin al la laboro.</p> <p>Due, la firmaoj ignoras lingvo-kapablon. La ministrejo pri edukado kaj scienco (kaj la registaro) ĉiam diras, ke japanoj havas malaltan kapablon uzi eksterlandajn lingvojn (ĉefe la anglan) kaj planas enkonduki anglajn lecionojn en elementajn lernejojn. Tamen laŭ la enketo neniuj kompanioj interesiĝas pri lingvoj.</p> <h3>Ministrejo</h3> <p>Kia diferenco inter la ministrejo kaj la kompanioj. La unua trudas al la popolo la anglan, sed la lastaj neglektas ĉi tiun kapablon. Kial la ministrejo insistas pri la angla? Por ke Usono povu profiti, vendante anglajn librojn kaj sendante instruistojn al Japanio? Por igi Japanion la 51a usona ŝtato? Kial la kompanioj ne taksas lingvo-kapablon? Ĉu ili ne plu volas agadi internacie? Ĉu jam sufiĉas tradukistoj kaj interpretistoj?</p> <p>Por ellerni la anglan, multaj lernantoj enverŝas grandegan energion, elspezas grandan monsumon kaj eluzas sian cerbon, ofte perdante esperon kaj memfidon pri sia kapablo. Multaj studentoj, kredante, ke la kvalifiko pri la angla donos bonan ŝancon pri sia dungiĝo ĉe bona kompanio, vizitas lernejon de la angla post sia studo en universitato.</p> <p>Leviĝas diversaj demandoj ankoraŭ sen taŭgaj respondoj. Tamen certas, ke la angla ne garantias laboron en la 100 ĉefaj kompanioj. Tio signifas, ke lerni la anglan kaj tial elspezi energion kaj monon, senutilas.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
