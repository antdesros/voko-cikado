      <h2>Senmita biografio de Zamenhof (2)</h2>
      <!-- komenco de la enhavo -->

       

       <p>Laŭ mia vidpunkto, la reago de Angelos Tsirimokos (M<span class="mm">ONATO</span> 2010/4, p. 23) al mia recenzo de <i>Zamenhof. Biografia Skizo</i> ne plene pravas. Certe, povas esti, ke mi misinterpretis vortojn de la aŭtoro Aleksander Korĵenkov, kaj se tiel, do mi pardonpetas. Sed kelkaparte Angelos Tsirimokos, verŝajne, parolas nur pri nesamaj vidpunktoj.</p> <p>Pri la antaŭnomoj: sur p. 4 Korĵenkov skribis: „La nomoj Eliezer en la hebrea, Ludwik en la pola, Louis en la franca k.a. estas nacilingvaj formoj en la respektivaj lingvoj.” Mi multe cerbumis pri la celo de tiu komento, ĉar ankaŭ al mi Lazaro kaj Ludoviko ne ŝajnas du formoj de unusola nomo. Verŝajne, Aleksander Korĵenkov nur iom tro kurte sin esprimis. Sed kvankam la historio de la nomo Lazaro estas relative klara, la origino kaj frua historio de la nomo Ludoviko estas tre neklaraj.</p> <p>Pri la naskiĝatesto mi mem havas nenian rektan scion. Aleksander Korĵenkov simple diras sur p. 4: „Kiel Ruslanda civitano li estis nomata Lazarj Markoviĉ Zamenhof, kiu estas la sola oficiala nomformo”. Interesas, ke Aleksander Korĵenkov ne skribis la familian nomon „Samenhof”; pri la ŝanĝo de Samenhof al Zamenhof mi supozis, ke temis ne pri oficiala nomŝanĝo, sed nur pri ortografia afero, ĉar ne konata al mi estas la cirila skribformo de la nomo.</p> <p>Ĉu ruso, ĉu polo? Dependas de onia vidpunkto. Laŭ mia propra sento, „polo” kaj „Pollando” estas politikaj vortoj. Kiu estis civitano de Ruslando, tiu estis ruso. Same kiel, kiu estas civitano de Britio, estas brito. La uzo kaj signifo de vortoj multe varias de epoko al epoko kaj de loko al loko. La vortoj „popolo”, „gento”, „nacio” havas nuntempe signifojn tre diversajn. Mi parolas kaj pensas laŭ la nuntempa signifo kaj uzado de tiaj vortoj laŭ mia propra sperto, kiu ja estas limigita al Usono kaj Aŭstralio. En sia <i>Skizo</i> Aleksander Korĵenkov nenie indikas, ke Zamenhof uzis la polan lingvon. Sur p. 6 estas skribite: „La hejma kaj lerneja lingvo de la juna Lazarj estis la rusa, kaj ĝi restis lia hejma lingvo dum la tuta vivo. Li revis fariĝi ruslingva poeto.”</p> <p>Do, miavidpunkte, temas pri tre subjektivaj aferoj (escepte de la nomoj Lazaro kaj Ludoviko).</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> 
