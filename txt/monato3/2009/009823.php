      <h3>Moderna vivo</h3>
      <h3 class="tem">MONDPACO</h3>
      <h2>Postaj tempoj eble pli bone komprenos min</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la sama jaro, kiam Zamenhof publikigis sian internacian lingvon, naskiĝis en sud-orient-germana vilaĝeto Max Josef Metzger [maks jozef mecger]: 1887. Post lernejaj jaroj li studis katolikan teologion, doktoriĝis kaj laboris kiel vikario en hejma regiono.</p>
<p>Kiam Germanio deklaris militon kontraŭ Francio en aŭgusto 1914, la juna sacerdoto tuj patriotece sin registrigis kiel volontula militpastro. Kun la soldatoj li celebris mesojn kaj preĝis por ilia glora venko. Por liaj religiaj admonoj kaj subteno de la milit-entuziasmo li ricevis ordenojn. En 1915 li ĉeestis kruelajn batalojn en Francio, konsciiĝis pri la mortiga fataleco de milito. Samtempe li ege malsaniĝis kaj poste estis sentaŭga por militservo.</p> 
<h3>Laborlingvo</h3> <p>En oktobro 1915 li translokiĝis al Graz, Aŭstrio, kiel ĝenerala sekretario de la Asocio de Abstinemaj Katolikoj, kaj fondis la Mondpacan Ligon de la Blanka Kruco, memorigante per nomo kaj simbolo je la Ruĝa Kruco. Baldaŭ li tie uzis Esperanton kiel oficialan laborlingvon. Ankaŭ de siaj kunlaborantoj li postulis, ke ili lernu la zamenhofan lingvon. Tiutempe multaj homoj esperis, ke ĝi baldaŭ iĝos vaste uzata vera mondlingvo.</p> 
<p>Post la milito kaj la pactraktato de Versailles [versáj] Metzger estis la unua germana civitano, kiu oficiale ricevis la rajton publike paroli en Francio. Kaj li sekvis la inviton partopreni intertraktadojn preparantajn la fondiĝon de la Ligo de Nacioj, el kiu jardekojn poste sekvis Unuiĝintaj Nacioj. Dume tamen reprezentantoj de la katolika eklezio suspekteme observis liajn agadojn kaj publikaĵojn.</p> 
<h3>Toleremo</h3> <p>Li – ne tute memvole – translokiĝis al la urbeto Meitingen en Bavario, Germanio, kie li daŭrigis sian laboron, gvidante la Asocion de Reĝo Kristo (<i>Societas Christi Regis</i>). En letero al papo Pio la 12a li proponis ekumenan koncilion. Li fondis la kristanan asocion <i>Una Sancta</i>, kiu postulis kaj preparis la reunuiĝon de katolika kaj protestanta eklezioj, kaj certe ankaŭ subtenis toleremon kaj kunlaboron inter ĉiuj aliaj religioj.</p> 
<p>Venis tiranoj: Hitler kaj nazi-reĝimo. Metzger plurfoje estis arestita. En oktobro 1943 li estis kondamnita je morto en fia pseŭdoproceso gvidita de „furioza Rolando” (germane: <i>rasender Roland</i> – Roland Freisler, vidu <a href="http://eo.wikipedia.org/wiki/Roland_Freisler">eo.wikipedia.org/wiki/Roland_Freisler</a>). Li estis skribinta kaŝleteron al tiama sveda episkopo de Upsala kun propono, post fino de tiu Hitler-milito, konstrui Germanion demokratan en unuiĝanta Eŭropo.</p> <h3>Muzikaĵoj</h3> <p>Katenite Metzger atendis pli ol duonan jaron en karcera ĉelo, kie li skribis en malgrandegaj literoj sur la randoj kaj malplenaj paĝoj de biblio, kiun oni permesis al li kiel solan legaĵon. Li verkis poemojn kaj komponis muzikaĵojn.</p> <p>La 17an de aprilo 1944 oni gvidis lin el la ĉelo en la mortoĉambron, kie oni demandis pri lia nomo, oficiale kontrolis lian identecon kaj senkapigis lin per gilotino. La proceduro daŭris entute sep sekundojn – tiel oni zorge protokolis.</p> <p>Okaze de la 65a datreveno de tiu mortigo okazis memorkunveno, kiun partoprenis diversaj organizaĵoj: katolika <i>Pax Christi</i> (paco de Kristo); sindikato de edukistoj kaj sciencistoj; alianco por homa digno; kaj Esperanto-lingvoklubo Augsburg. Interalie estis parolado ankaŭ en Esperanto: Max Josef Metzger „estis patro de la mondpaca movado”, diris la parolanto, kaj „multflanke ekzemplodona, tiom bunta, ke ĉiu el ni speguliĝe sin retrovas”.</p> <h3>Filmo</h3> <p>Inter la partoprenantoj estis katolikaj pastroj, anoj de Asocio Reĝo Kristo, viktimoj de nazireĝimo, antifaŝistoj, sindikatanoj, politikistoj kaj esperantistoj. Eminentulo estis la holokaŭstpostvivanto Mietek Pemper, 89-jaraĝa, kiu konsilis la reĝisoron Steven Spielberg [stivn spilberg] dum la farado de la filmo <i>Listo de Schindler</i> [ŝindler] en Krakovo.</p> <p>Post depono de florbukedoj la ĉeestantoj iris al la proksima kinejo Thalia, kie estis montrata filmo pri Max Josef Metzger, kiu adiaŭis el sia vivo per la vortoj: „Postaj tempoj eble pli bone komprenos min – ĉar estis ĉiam mia fatalo, ke mi antaŭis la tempon kaj tial ne povis esti komprenata”.</p> <div style="text-align:right">Jomo I<span class="mm">PFELKOFER</span></div>  
