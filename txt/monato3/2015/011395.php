      <h2>Urbo kun forta studemo</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>Jen la dua el tri artikoloj, kiuj fokusiĝas pri Svislando.</b>
</p> 
<h3>Inter lago kaj Ĵuraso</h3> 
<p>Neocomium estas la latina nomo de la urbo Neuchâtel, urbo kun lago kaj 33 000 loĝantoj.
   Situanta 425 m super la marnivelo ĝi pli malgrandas ol la pli alta urbo La Chaux-de-Fonds
   en Ĵuraso (1000 m). Tamen kun la ĉirkaŭaj vilaĝoj Neuchâtel enhavas la ĉefan parton
   de la kantona loĝantaro.
</p> 
<p>Ĝi estas la ĉefurbo de la kantono Neuchâtel, konata oficiale kiel la Respubliko kaj
   Kantono de Neuchâtel. Unue francaj familioj, poste la reĝo de Prusujo regis en la
   regiono (1707-1848). En 1814 la kantono aliĝis al la svisa konfederacio.
</p> 
<p>En la montara parto vivis ĉiam nur liberaj homoj, kiuj finfine ribelis kontraŭ la
   eksterlanda jugo. La kulmino estis la jaro 1848, kiam eksplodis revolucio kaj forpeliĝis
   la reprezentanto de la prusa reĝo.
</p> 
<p>Nuntempe ĉiam pli alte sur la ĵurasa deklivo super Neuchâtel oni konstruas novajn
   kvartalojn, de kie eblas ĝui perspektivon sur la tegmentoj de la urbo. De la flankoj
   de la montaro Ĵuraso oni povas ĉefe en aŭtuno admiri grandan parton de la Alpoj. Monto
   Chasseral, la plej alta kantona pinto, atingas 1550 m.
</p> 
<h3>Kastelo sur monteto</h3> 
<p>Vizitindaj estas kastelo kaj preĝejo. La kastelo estas la sidejo de la kantona registaro
   kaj parlamento. La urbo transiris al protestantismo pro Guillaume Farel (1489-1565)
   kaj do la granda preĝejo 
   <i>collégiale</i> apud la kastelo estas protestanta. En la urbo vidiĝas fontanoj kaj malnovaj domoj
   en piedir-zonoj.
</p> 
<p>Du patriciaj familioj, De Pury (la ĉefa placo de la urbo tiel nomiĝas) kaj DuPeyrou
   (palaco en francstila parko staras oriente de la centro), riĉiĝis per posedaĵoj en
   diversaj mondopartoj en la 18a kaj 19a jarcentoj kaj beligis sian urbon. Tamen la
   tradicia, aristokrata kaj dekstrema Neuchâtel ŝanĝiĝis kaj nun la urba registaro estas
   centra kaj iom verdula.
</p> 
<h3>Universitato kaj kulturo</h3> 
<p>En Neuchâtel troviĝas universitato. Dum jardekoj ĝi estis la plej malgranda en la
   tuta Eŭropo. Tamen multaj studentoj elektas ĝin pro la tiea favora studetoso. En 1975
   la universitato kreis Instituton pri Mikrotekniko kaj en 1984 naskiĝis Svisa Centro
   pri Elektroniko kaj Mikrotekniko. En 2009 kreiĝis ligo inter ĝi kaj la Politeknika
   Altlernejo de Lausanne.
</p> 
<p>La kantona muziklernejo ligiĝas al la muzika akademio de Ĝenevo. Do observeblas emo
   al kunlaboro kaj kunigado de fortoj. Krome la svisa konfederacio klopodas ne konservi
   ĉiujn servojn en la ĉefurbo Berno. Pro tio Neuchâtel ricevis la federan oficejon pri
   statistiko.
</p> 
<p>La urbo proponas ankaŭ teatrojn, inkl. de malgrandaj, avangardaj, diversajn muzeojn
   (pri artoj kaj pri etnologio), koncertejon kaj restoraciojn. Eblas promeni tra apudlagaj
   parkoj. Naĝejo, stadiono „La Maladière”, kaj sketejo staras je dispono de sportemuloj.
</p> 
<p>La aŭtoro Friedrich Dürrenmatt (1921-1990) vivis en Neuchâtel; ankaŭ la hungardevena
   artisto Viktor Vasarely (1906-1997). Ĉe la universitato profesoris la esperantisto
   Edmond Privat (1889-1962). En Neuchâtel naskiĝis la psikologo Jean Piaget (1896-1980)
   kaj la nobel-premiito pri medicino Daniel Bovet (1907-1992).
</p> 
