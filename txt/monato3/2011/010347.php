      <h3>Medio</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Energio de la tegmento</h2>
      <!-- komenco de la enhavo -->

       

        <p>Aŭstroj uzas la tegmenton de sia domo kiel energifonton. Jam 4,5 milionoj da m<sup>2</sup> da sunkolektiloj estis instalitaj en la lando, 250 000 hejmoj jam havas sunkolektilojn sur la tegmento. Al ili aldoniĝas ĉiusemajne miloj. Tiamaniere estas produktataj 1400 gigavat-horojn da energio – tiom multe, ke tutan vintron oni povus hejti ĉiujn hejmojn en la federacia lando Vorarlberg (okcidenta Aŭstrio), deklaris Roger Hackstock, afergvidanto de la sun-energia asocio <i>Austria Solar</i>.</p> <p>Tamen la sunkolektiloj kovras ankoraŭ apenaŭ 1,5 procentojn de la bezonata energio por varma akvo kaj hejtado en Aŭstrio. Ekzistas granda potencialo por evoluo en tiu sektoro, ĉar laŭ esploro ĝi povus atingi 40 procentojn, informis la oficejo de la ministro pri naturmedio. Tamen nuna celo estas altigi la hodiaŭajn rezultojn ĝis 10 procentoj post kelkaj jaroj, diris Hackstock. Lastatempe post multjara kresko la branĉo spertis „historian malaltiĝon”. En Aŭstrio malpliiĝis la vendoj de kolektiloj je 20 procentoj kaj entute, kune kun la eksportoj, duoniĝis la negocoj pri kolektiloj en du jaroj. En la branĉo oni devis maldungi laboristojn, krome 3 el la 70 produktantoj kaj komercistoj devis deklari bankroton.</p> <h3>Nova kresko</h3> <p>Nun oni esperas je nova kresko. Konsiderante la eksplodantajn petrolprezojn kaj la rean diskuton pri kurento el la nukleaj elektrejoj, la energiŝparado fariĝis aktuala temo. La interesiĝo pri la sunkolektiloj ege plialtiĝis. Tiun fakton konfirmas ankaŭ instalistoj. „Okazas ĝenerala ŝanĝo en la pensado direkte al konsciiĝo pri la naturmedio”, opinias Theodor Seebacher, ĉefo de entreprenista fakasocio en Salcburgo. „La sunradio-instalaĵoj hodiaŭ estas ĉe la domkonstruado por ni teknika bezonaĵo.” Postulataj estas ĉefe instalaĵoj, kiuj povus kovri krom la bezonoj de varmakvo ankaŭ parton de la hejtbezonoj.</p> <p>Se 250 000 hejmoj uzis ĝis nun 90 procentojn de la sunenergio por varma akvo, nuntempe tri kvaronoj de la aĉetataj instalaĵoj povas kovri ankaŭ parton de la hejtbezonoj, laŭ Hackstock. Por unu familia domo la instalaĵo kun 15 m<sup>2</sup> da kolekt-areo kaj 1000 litroj da varmakvujo, kune kun la instalado, kostas entute 12 000 eŭrojn. Oni povas kovri per ĝi trionon de la hejtbezonoj, la ŝparo estas 350 ĝis 700 eŭroj jare. Notindas, ke ĝis 30 procentojn de la instalaĵprezo konsistigas investo de federaciaj landoj kaj komunumoj.</p> <p>La situacio ĉe la sunĉeloj estas alia ol ĉe la sunkolektiloj. Ĉe ĝi la sunradioĉeloj ne varmigas akvon, sed rekte produktas elektran kurenton. Danke al la plialtigo de la financa subteno, altiĝis lastatempe ilia parto. Tamen ĝi estas minimuma – nur 0,47 procentoj de la tuta elektroproduktado.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>    
