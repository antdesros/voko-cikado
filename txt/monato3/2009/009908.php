      <h2>Vojo al demokratio</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ 20 jaroj la detruita berlina muro signalis la finon de la malvarma milito en Eŭropo. Tamen, du monatojn antaŭe, en aŭgusto 1989, ĉirkaŭ du milionoj da litovoj, latvoj kaj estonoj staris homĉene inter Vilniuso, Rigo kaj Talino je distanco de ĉirkaŭ 600 km.</p> <p>Tiel ili atentigis pri la 50a datreveno de la Molotov-Ribbentrop-pakto, laŭ kiu Sovetio kaj Germanio sekrete konsentis dividi mezan Eŭropon. Samtempe la homoj volis montri, ke sen la tri baltaj popoloj la kontinento ne povos esti unueca kaj libera. Laŭlonge de tiu ĉi „balta vojo” flirtis nigraj funebraj rubandoj kaj brulis kandeloj de homoj, kiuj esprimis sian deziron sendepende vivi. En 1991 la revo realiĝis.</p> <p>Ankaŭ la aŭgusto de 2009 ne trankvilis en Baltio. En ĉiuj tri landoj okazis jubilea celebrado de la „balta vojo”, kiun akompanis koncertoj, diversaj manifestacioj kaj aliaj aranĝoj.</p> <h3>Koro</h3> <p>Ĉirkaŭ 60 000 homoj partoprenis maratonon inter Vilniuso kaj Talino, transdonante unu al alia simbolajn krucetojn. Aliaj kuris po unu kilometron kadre de la kampanjo <i>Mia koro batas por Baltio</i>. Aviadistoj disĵetis florojn, kaj engaĝiĝis ankaŭ glisparaŝutantoj, biciklantoj, motorciklantoj kaj velŝipantoj.</p> <p>La ĉefaj jubileaj aranĝoj okazis la 23an de aŭgusto. Tiam tra Litovio estis portata torĉo de la Balta Asembleo, okazis meso en la katedralo de Vilniuso, kaj estis inaŭguritaj fotoekspozicioj. Samtempe en Vilniuso, Rigo kaj Talino, en la lingvoj litova, latva kaj estona, estis kantata kanto <i>Baltio jam vekiĝas</i> populara antaŭ du jardekoj. Ĉe la limpunkto latva-estona estis starigita monumento: sep metalaj manpreme starantaj siluetoj.</p> <h3>Memoro</h3> <p>Samtage la ĉefministroj de la tri baltaj landoj (la litova Andrius Kubilius, la latva Valdis Dombrovskis kaj la estona Andrus Ansip) subskribis deklaracion, ke en Eŭropo estu konscience kaj senkaŝe pritaksata komuna memoro pri la totalismaj reĝimoj de la 20a jarcento – stalinismo kaj naziismo. Ili interkonsentis, ke la 23a de aŭgusto fariĝu tago de rememoro de stalinismaj kaj naziismaj viktimoj.</p> <p>Honoron al la partoprenintoj de la „balta vojo” esprimis la estro de la Eŭropa Komisiono José Manuel Barroso. La direktoro de Unesko Koichiro Matsuura rimarkis, ke la homa ĉeno devigis Sovetion deklari la Molotov-Ribbentrop-pakton ne plu valida, tiel atestante pri la influo de etaj ŝtatoj al mondaj aferoj. La usona ŝtatsekretariino Hillary Clinton diris, ke pro la „balta vojo” la tri landoj fariĝis ĉampionoj pri demokratio kaj homaj rajtoj.</p> <div style="text-align:right"><span class="mm">LAST</span></div>   
