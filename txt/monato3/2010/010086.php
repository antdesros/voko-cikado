      <h2>Perversa pedagogio - drameca didaktiko</h2>
      <!-- komenco de la enhavo -->

       

     <p>Jen konciza unuakta drameto kun nur tri roluloj. La unua – iu ŝajne milda erudicia profesoro kun signifa reputacio, kiu akceptas lernantojn unuope en sia hejmo, la dua – lia dommastrino kaj la tria – juna studentino, kiu fariĝas nova lernanto ĉe la profesoro.</p> 
<p>La komencaj paĝoj de la verko donas enscenigajn instrukciojn kaj gvidon por la aktoroj kiel prezenti kaj modifi la personecojn de la roluloj dum la dramo. Sekve, se vi legas la verkon en libra formo, vi jam havas ideon pri la verŝajne atendebla misiro de eventoj. Spertante la verkon kiel spektanto en teatro, tiuj informoj fariĝos (en lerta surscenigo!) nur laŭgrade evidentaj al vi dum la disvolviĝo de la drameto. Se vi konas la verkiston, vi tamen havas antaŭsuspektojn pri la atendeblaj bizaraĵoj!</p> 
<p>Oni scivolas, ĉu celo de la aŭtoro estis fakte moki iujn troigojn kaj kritikindaĵojn de la tiama instruado aŭ simple krei teatraĵeton plena je absurdaĵoj, logikaj kontraŭdiroj kaj kalemburoj. Ni supozu, ke li efektive volis kritiki la instruadon – jen instruisto, kiu plene ignoras la bezonojn aŭ dirojn de la lernanto, kiu turnas la nociojn de motivigo al lerndeziro sur sian kapon, mortigante anstataŭ spronante tion; kiu kreas malfacilaĵojn, kie ili ne devus ekzisti por aspektigi la lernadon apenaŭ sekvebla mistika procezo kaj diras nekompreneblaĵojn, kiuj levas harfendaĵojn ĝis la nivelo de vivnecesaj lernaĵoj.</p> 
<p>La servistino envenas de tempo al tempo, dirante zorgigajn avertojn, kiuj pruviĝas ĉiam pli trafaj. La kulmino okazas, kiam la profesoro – kiel bone dresita Cseh-instruisto – prenas fizikan objekton por klarigi sian tezon. Tiu objekto fakte estas minacilo, kies precizan tipon ni ne malkaŝos, kiel same la teatraĵan finon. Ege defia tradukotasko – lerte plenumita, kune kun adaptetoj por konvenigi la verkon por esperantista publiko. Se la verko abunde legiĝos, ni devas timi pri la varbsukceso ĉe estontaj kursoj de ILEI.</p> 
