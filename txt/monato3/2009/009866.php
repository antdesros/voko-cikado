      <h3>Ekonomio</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Litovio fermiĝas!</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Multaj gastoj de Vilnius, kiuj venis celebri la miljaran ekziston de Litovio, estis ŝokitaj, kiam en la hoteloj je la 22a horo de la 5a de julio 2009 malaperis la lumo. Ĝis la 1a horo matene hotelaj haloj, restoracioj kaj kafejoj dronis en mallumo. Nur kandelaj lumoj helpis orientiĝi.</b></p> <p>La mallumiĝo de multaj hoteloj ne signifas, ke ili spertis ajnan difekton de la elektra instalaĵo. Ĉirkaŭ 300 hoteloj de Vilnius kaj de aliaj grandaj urboj de Litovio tiel partoprenis la agadon „Litovio fermiĝas! Ni revenas 1000 jarojn”. Tion atestis afiŝoj en restoraciaj fenestroj, sur kiuj videblis ŝercportreto de la litova ĉefministro Andrius Kubilius, la kapo de blinda kaj surda registaro.</p> <p>Evalda Šiškauskienė, prezidantino de la asocio de hoteloj kaj restoracioj de Litovio, klarigis la sencon de tia protesto: „Altigitaj impostoj kaŭzis, ke la alvena turismo malkreskis kaj ke multaj hoteloj kaj restoracioj de la lando trafis en bankrota situacio. Jam 10 000 personoj estis maldungitaj. Tamen la registaro estas surda rilate al niaj argumentoj. Ĝi parolas pri instigo de la entreprenado kaj eksporto, tamen tion penas fari per altigo de imposta ŝarĝo por entreprenoj.”</p> <p>Similaj protestagadoj jam estis organizitaj junie, tamen ili estis senefikaj. Šiškauskienė aldonis, ke la agadoj daŭros ĝis estos atingitaj rezultoj favoraj por la entreprenado. Gastoj de la hoteloj estos daŭre informataj pri la protestaj agoj kaj malfavoraj kondiĉoj per ekspono de diversaj karikaturoj de la aŭtoroj de tiel stultaj decidoj.</p> <p>La hotelojn kaj restoraciojn kaj la tutan alventurismon ĝenas tri ĉefaj problemoj: la malkomforta atingo de Litovio (mankas rektaj flugoj), la impostoj je nemoveblaĵoj ne estas konformaj al la reala situacio, kaj altigita aldonvalora imposto malpermesas konkuradon en la eŭropa merkato. Cetere, ĉi-jare nur du landoj de Eŭropo – Litovio kaj Latvio – altigis aldonvaloran imposton por loĝiga servo ĝis pli ol 10 %. En Eŭropo 20 landoj el 27 aplikas imposton inter 5 kaj 10 %, dum en Litovio hodiaŭ ĝi estas eĉ 19 %.</p> <p>La ĉefministro ne plu estas optimisma pri la tendencoj de la ekonomio. En majo 2009 li asertis, ke tuj la vivo de la litovoj pliboniĝos, ĉar la registaro sukcesis solvi esencajn problemojn. Tamen li ŝanĝis sian sintenon. Nun li asertas, ke la ekonomio falos anstataŭ je antaŭviditaj 5 % minimume je 15 % aŭ eĉ pli. La Eŭropa Komisiono en majo prognozis falon de la litova malneta produkto je 11 % por la nuna jaro kaj je 4,7 % sekvontjare.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
