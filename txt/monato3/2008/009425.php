      <h3>Politiko</h3>
      <h3 class="tem">SLOVENIO</h3>
      <h2>Muso kaj elefantaro</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 1an de januaro 2008 Slovenio, lando kun 0,4% de la loĝantaro de Eŭropa Unio, ekprezidis dum sekva duonjaro la Konsilion de Eŭropa Unio. Temas praktike pri gvidado de la unio, kiun transprenas unuafoje unu el la novaliĝintaj landoj de 2004. Slovenio fariĝas la unua slava lando, kaj la unua eks-socialista, kiu tiel respondecas pri Eŭropo. Ŝajnas, ke muso gvidas elefantaron.</p> <p>La komencon signis solenaĵo dum la 7a kaj 8a de januaro. Unue kunsidis en Ljubljana la slovena registaro kun la Eŭropa Komisiono, sekvis konferenco pri Interkultura Dialogo, kaj la tuton finstampis granda solenaĵo en kulturdomo, kiun partoprenis 1500 personoj reprezentantaj slovenan eliton, inkluzive du reprezentantoj de Eŭropa Esperanto-Unio.</p> <h3>Danĝero</h3> <p>Dum la konferenco gvidantoj de unuopaj sekcioj plurfoje menciis lingvojn kaj kulturon. Zvonko Kovač diris: „Mi esperas, ke en EU ni ne devos komuniki nur en la angla lingvo.” Laŭ la filozofo Edvard Kovač: „La nuna dulingvismo (uzo de la gepatra lingvo kaj la angla) estas granda danĝero. Necesas atenti, ke la anglan oni ne proklamu kiel ununuran lingvon de la scienco.” La slovena ministro pri kulturo Simoniti opiniis: „Kulturo ne estas kontraŭulo de la ekonomio, sed ĝi ankaŭ ne estas malriĉa parenco, kiun oni devas ĉiam finance helpi.”</p> <h3>Kulturfestivaloj</h3> <p>Tiam, en la kastelo de Ljubljana, estis subskribata deklaro pri kunlaboro de eŭropaj kulturfestivaloj. Subskribis krom reprezentantoj de dudeko da festivaloj ankaŭ la eŭropa komisionano pri instruado kaj kulturo, Jan Figel, kaj la urbestro de Ljubljana, Zoran Janković. Fine, dum la grandioza ferma solenaĵo, parolis i.a. la slovena ŝtatprezidanto, Danilo Turk, la prezidanto de la Eŭropa Parlamento Hans-Gert Poettering, kaj la prezidanto de la slovena registaro Janez Janša.</p> <p>Eŭropa Esperanto-Unio transprenis la taskon eldoni Informilon pri eŭropaj okazaĵoj dum la slovena gvidado de la konsilio. Raportoj aperos monate en Esperanto kaj pere de Esperanto en kelkaj aliaj lingvoj. Paĝaro en la tri oficialaj lingvoj (slovena, angla, franca) troveblas ĉe <a href="http://www.eu2008.si">www.eu2008.si</a> kaj en Esperanto ĉe <a href="http://europo.eu/index.php?art=3&amp;art2=30">europo.eu/index.php?art=3&amp;art2=30</a>.</p> <div style="text-align:right">Zlatko T<span class="mm">IŠLJAR</span></div> 
