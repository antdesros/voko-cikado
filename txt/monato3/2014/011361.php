      <h3>Medio</h3>
      <h3 class="tem">EŬROPA UNIO</h3>
      <h2>Verdaj mitoj kaj herezoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>Dum la pasintaj jardekoj la ekologia movado kaj la al ĝi rilatantaj verdaj politikaj
      partioj en multaj landoj iĝis sufiĉe influhavaj kaj eĉ kelkfoje partoprenis en registaroj.
      Indas demandi sin, kiomgrade la verdulaj starpunktoj vere estas utilaj al la medio.
      Pri tiu temo aperis lastatempe pluraj libroj.
   </b>
</p> 
<p>En majo la loĝantoj de Eŭropa Unio denove elektis la membrojn de la Eŭropa Parlamento.
   Estos 38 verduloj en la renovigita parlamento (plus kelkaj kiuj estas pro administraj
   kialoj en la sama parlamenta frakcio, sed ne nepre estas verduloj). Legante la verdajn
   programojn (mi konsultis flandran kaj germanan versiojn), oni apenaŭ rimarkas diferencojn
   kun socialismaj programoj. En ambaŭ estas ekzemple promesoj pri ekonomia kresko kaj
   pli da laborlokoj. Cetere, la samaj promesoj aperas ankaŭ en aliaj programoj (sed
   laŭ verduloj kaj socialistoj intervenu por tio la ŝtato, dum ĉe aliaj „la libera merkato” faros). Troveblas nur kelkaj ekologiaj temoj, ekzemple pri verdaj energifontoj, sed
   ne detale ellaboritaj.
</p> 
<h3>„Verda” fojfoje signifas „dolora”</h3> 
<p>La plej videblaj eroj de la verdiĝanta energiproduktado estas la ventoturbinoj en
   la kamparo kaj la sunpaneloj sur la tegmentoj. Tiuj energifontoj devos anstataŭi la
   nukleajn centralojn, kiuj iom post iom estas elŝaltataj. Tamen, vento kaj suno ne
   estas fidindaj. Por garantii ke ĉie kaj ĉiam elektro estas je dispono, necesas tute
   restrukturi la distribureton. Antaŭe la distribuo okazis, kiel por akvo kaj gaso,
   en unu direkto: de la centralo al la konsumanto, kun kelkaj konektoj inter la centraloj
   kaj trans la landlimoj. Nun la elektro venas el multaj fontoj kaj oni devas transporti
   ĝin al pli foraj lokoj. La mastrumado de la novspeca distribusistemo estas multe pli
   kompleksa kaj riska kaj estas bezonataj novaj alt-tensiaj lineoj.
</p> 
<p>Jen aperas problemo: multaj homoj ne ŝatas, ke pilonoj kaj kabloj perturbas la pejzaĝon
   aŭ timas, ke la lineoj kaŭzos iun radiadon nocan por ilia sano. Ili postulas, ke la
   kabloj estu metitaj subtere. Teknike eblas, sed ĝis nun tio estas farata nur por etaj
   distancoj, ĉar estas multe pli kompleksa kaj kosta solvo. El ekologia vidpunkto, ĝenas
   la bezono de pli dikaj kabloj, kio por longaj distancoj signifas konsumon de ege granda
   kvanto da naturaj resursoj. Iam superkondukiveco povos esti solvo, sed nun tio ankoraŭ
   estas eksperimenta.
</p> 
<p>Ĝis nun ne eblas stoki grandan kvanton da elektro. Estas unu iom racia solvo, uzebla
   nur en montaraj regionoj: kombino de supra kaj malsupra lagoj. Se estas tro da elektro
   (kio oftas ĉe ventoturbinoj) oni uzas la troon por pumpi akvon de la malsupra al la
   supra lago. Se mankas elektro, akvo fluas de la alta al la malalta lago tra la pumpiloj,
   kiuj funkcias kiel generatoroj. Kie eblas, tiaj lago-kombinoj jam estas uzataj. Nun
   urĝe necesas konstrui novajn, sed por tio estas bezonataj grandegaj konstrulaboroj
   neakcepteblaj por la loĝantoj de la trafitaj regionoj.
</p> 
<p>Verduloj ne volonte parolas pri tiaj konsekvencoj de nova energipolitiko. Kontraŭe,
   ili kutimas subteni la protestantojn, tiel subfosante la propran idearon.
</p> 
<h3>Biobrulaĵoj</h3> 
<p>Krom ventoturbinoj kaj sunpaneloj, biobrulaĵoj estas tria verda energifonto. Laŭ informilo,
   kiun la belga elektro-produktanto kaj -liveranto Electrabel (filio de la franca GDF
   Suez) ĵus dissendis al siaj klientoj, 80 % de ĝia verda energio devenas de biobrulaĵoj.
   Tamen tiuj brulaĵoj estis kaj estas kontestataj. Kvankam la produktometodoj dum la
   pasintaj jaroj pliboniĝis, restas multaj duboj pri la reala utilo. Plejparto de la
   brulaĵoj (sukerkano, maizo, ligno ...) estas kultivata industriskale en evolulandoj
   kaj tie forprenas agrojn bezonatajn por la loka konsumado.
</p> 
<h3>Ekologia dorsosako</h3> 
<p>Ĉu mi anstataŭigu mian dekjaran, ne ofte uzatan, bone funkciantan aŭton per nova,
   konsumanta malpli da energio? Ĉu mi tuj forigu ĉiujn ampolojn kaj ekuzu LEDajn aŭ
   fluoreskajn lampojn en mia domo, aŭ ĉu el ekologia vidpunkto estas pli favore anstataŭigi
   ilin nur kiam ili difektiĝas? Jen demandoj al kiuj la kutima respondo estas „faru tuj”, sed kiuj fakte bezonas zorgan kalkuladon.
</p> 
<p>La germana veterana ekologiisto Friedrich Schmidt-Bleek (naskiĝinta en 1932) kreis
   la koncepton de „ekologia dorsosako”. Kutime oni prijuĝas la efikecon de aŭto aŭ lampo komparante la energikonsumon. Schmidt-Bleek
   adicias ĉion, kio dum la produktado, la uzo kaj la forigo de iu aparato estas konsumita:
   la bazajn materialojn (metalo ...), la kromajn konsumaĵojn dum la produktado (akvo,
   pakumo, transporto ...), la energikonsumon dum la utila vivo, kaj fine la kostojn
   (kaj eble la enspezojn) por „malprodukti” la aparaton. Laŭ tiaj kalkuloj la uzo de plejparto de la nuntempaj elektraj aŭtoj
   estas frenezaĵo, ĉar por iliaj baterioj estas bezonataj multe pli da bazaj materialoj
   ol por kutima aŭto kaj dum la uzado, la pezo de la baterioj pligrandigas la energikonsumon.
</p> 
<p>Similajn kalkulojn eblas fari por pesi kaj kompari la ekologian dorsosakon de papera
   aŭ plasta pakumo, de vitraj aŭ plastaj boteloj, ktp. La konkludo de Schmidt-Bleek:
   facilaj respondoj ne ekzistas.
</p> 
<h3>Karbona dioksido</h3> 
<p>Ĉar por la ĉiutaga vivo estas bezonataj simplaj komparmetodoj, la kutima kriterio
   por prijuĝi la ekologian aspekton iĝis la emisio de karbona dioksido (CO
   <sub>2</sub>). Ĝi originas el la Protokolo de Kioto, internacia traktato, alprenita en decembro
   1997. La protokolo enkondukis sistemon de emisio-rajtoj de CO
   <sub>2</sub>, distribuataj al poluantaj industrioj. La intenco estis fiksi maksimumon de emisioj,
   jaron post jaro malpliiĝantan. Kompanioj, kiuj sukcesis malpliigi sian emision, povis
   vendi siajn troajn rajtojn al aliaj. Anstataŭ ekologia stirilo, la rajto-sistemo iĝis
   financa produkto, kun ĉiuj eblaj misuzoj. Restas la burokrataro kaj la fraŭdo; pridubindas
   la utilo por la medio.
</p> 
<h3>Greenpeace</h3> 
<p>Se la medio-protektantaj asocioj ne sukcesas honeste kaj akurate informi la publikon,
   oni almenaŭ povus esperi, ke interne ili zorge kaj etike funkcias. Eĉ pri tio devas
   esti duboj ...
</p> 
<p>En sia numero de la 16a de junio 2014 la germana revuo 
   <i>Der Spiegel</i> anoncis, ke la ĉefsidejo de Greenpeace en Nederlando perdis 3,8 milionojn da eŭroj
   pro financa spekulado. La asocio intertempe konfirmis tion. Kiel kutime, la respondeca
   oficisto estis tuj maldungita, sed evidente li ne povis fari tiajn transakciojn sen
   scio kaj konsento de siaj superuloj. La afero konfirmas, ke la bankokrizo de antaŭ
   kelkaj jaroj estas tute forgesita. Kompleksaj kaj riskaj financaj konstruoj denove
   iĝis tute akcepteblaj, des pli ĉar la rento el normalaj ŝparkontoj estas preskaŭ nula.
</p> 
<h3>Konkludo</h3> 
<p>Danke al la verda movado la publiko nun estas (iomete) konscia pri ekologiaj temoj.
   La verdaj ideoj intertempe estas transprenataj de plejparto de la aliaj politikaj
   partioj, sed ofte kiel simpligitaj sloganoj. Se la verduloj deziras daŭre progresi
   (ne nur laŭ nombro de voĉdonoj okaze de balotado, sed ankaŭ laŭ realigo de sia idearo),
   ili devos repripensi sian strategion. La nuna publiko estas pli bone edukita ol la
   antaŭa generacio kaj deziras kredindajn informojn.
</p> 
