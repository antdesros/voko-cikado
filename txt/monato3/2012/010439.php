      <h2>Svingante la ŝtatan flagon</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Oficialiĝis kiel muziko de la usona ŝtato Teksaso la tiel nomata okcidenta svingo. Tio estas nur la dua fojo, ke usona ŝtato oficialigis „sian” muzikon.</b></p> <p>Okcidenta svingo estas ĵaz-subspeco, en kiu intermiksiĝis ritmoj de tradiciaj svingo, bluso, diksilando kaj eĉ polko-danco. Ĝi aperis kaj populariĝis kiel teksasa popola danco dum la jaroj 1920aj kaj 30aj.</p> <p>Tiu ĉi tipa dancmuziko akiris adeptojn en Teksaso, Oklahamo kaj Kalifornio ĝis la 1940aj jaroj. Aldone al tradiciaj ĵazinstrumentoj alvenis ankaŭ gitaroj kaj violonoj. Cetere, unu el la pioniroj de rokenrolo, Bill Haley, substrekis la influon de okcidenta svingo en la evoluo de rokenrolo en la 1950aj jaroj.</p> <h3>Klasikaĵoj</h3> <p>La plej populara okcident-svingisto estis Bob Willis kun sia bando <i>Texas Playboys</i> (Teksasaj ludemuloj). Kantoj, kiel <i>San Antonio Rose</i> (Rozo de San Antonio) kaj <i>Take me back to Tulsa</i> (Revenigu min al Tulsa) restas klasikaĵoj de la ĝenro.</p> <p>Nuntempe la centro de tiu ĉi ĵaz-subspeco troviĝas en la teksasa ĉefurbo Austin. Tie hejmas la plej populara okcident-svinga bando <i>Asleep at the wheel</i> (Ĉestirile dormanta).</p> <div style="text-align:right">AM<span class="mm">IK</span></div> 
