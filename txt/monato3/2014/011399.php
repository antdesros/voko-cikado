      <h3>Politiko</h3>
      <h3 class="tem">BRAZILO</h3>
      <h2>Post venko - dialogo</h2>
      <!-- komenco de la enhavo -->

       

         <p>En oktobro 2014 renoviĝis la estraro kaj la duĉambra parlamento de Brazilo. Post streĉa
   kampanjo estas reelektita Dilma Rousseff [diŭma ruséf], kiu nun proponas dialogon
   por repacigi la landon.
</p> 
<p>Ĉi-jare ĉirkaŭ 140 milionoj da brazilanoj elektis ŝtatajn kaj federaciajn deputitojn,
   senatanojn, guberniestrojn kaj la respublikestron. Por ĉi-lasta posteno prezentis
   sin 11 kandidatoj, el kiuj nur tri havis realan ŝancon fariĝi prezidanto: Dilma Rousseff
   (Partio de la Laboristoj), Aécio [aesju] Neves (Partio de la Socialdemokratio), reprezentanta
   la opozicion, kaj Eduardo Campos [kampus] (Partio socialista), eksministro, reprezentanta
   disidentojn.
</p> 
<h3>Aviadila akcidento</h3> 
<p>Datumoj publikigitaj en aŭgusto prognozis jenan rezulton: Rousseff – 38 %, Neves – 23 % kaj Campos – 9 %. Tamen abrupte mortis Campos en ankoraŭ ne plene klarigita aviadila akcidento.
   Anstataŭis lin Marina Silva, kiu, samkiel la mortinto, antaŭ nelonge estis ministro.
   Tamen malkiel Campos, fidela socialisto, Silva trifoje migris de partio al partio
   en la lastaj kvin jaroj.
</p> 
<h3>Promesoj</h3> 
<p>Por la fina baloto du proponoj dividis la publikan opinion. La koalicio de la reganta
   Partio de la Laboristoj prezentis atingojn dum 12 jaroj da regado: 22 milionoj da
   homoj eliris el stato de mizero; la lando ne plu videblas en la mapo de malsato (laŭ
   Organizaĵo pri Nutrado kaj Agrikulturo); 37 milionoj leviĝis el malriĉa al meza socia
   klaso; laborpostenoj rekordas, eĉ dum la malfavora internacia krizo; estas konstruitaj
   milionoj da domoj (eĉ por malriĉuloj) kaj inaŭguritaj pli ol 400 teknikaj lernejoj.
</p> 
<p>Promesite por venontaj jaroj estas pli intense lukti kontraŭ korupto kaj reformi la
   regularon por malaltigi balotajn elspezojn. Siavice la opozicio promesis plibonigi
   la administradon laŭ novliberalismaj postuloj, batali kontraŭ korupto, bridi la inflacion
   kaj stimuli la ekonomion.
</p> 
<p>La moralisman tezon de la opozicio malavare subtenis konservativaj amaskomunikiloj,
   praktike funkciantaj kiel monopolo en Brazilo. Du tagojn antaŭ la fina balotado aperis
   sur revua kovrilo skandalaj raporto kaj foto tiel prezentita, ke Rousseff kaj eks-prezidanto
   Lula da Silva aperas kvazaŭ krimuloj. Mankis sufiĉe da tempo por konvene reagi aŭ
   sin defendi antaŭ la publika opinio.
</p> 
<h3>Apenaŭ komfortaj</h3> 
<p>La rezulto de la plej streĉa kampanjo en Brazilo konfirmis la venkon de Rousseff,
   sed per apenaŭ komfortaj 51,64 % de la validaj voĉoj (sume 54 501 118). Ricevis Neves
   48,36 % (sume 51 041 155).
</p> 
<p>Jam en sia unua postbalota parolado diris Rousseff, ke plej urĝas dialogi. Tiel la
   prezidanto esperas repacigi la dividitan landon kaj interrilati kun la parlamento,
   kiu moviĝis signife dekstren.
</p> 
