      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Profitoj, punoj kaj privatigo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Fine de aprilo akcidentis trajno en la urbo Amagasaki en gubernio Hyogo. Ŝajnas, ke la trajno tro rapidis kaj elreliĝis. Mortis 105 homoj kaj vundiĝis 461, kiam la vagonoj parte volvis sin ĉirkaŭ apudtraka apartamentaro. Jen la sepa plej grava trajnakcidento de la pasintaj 60 jaroj.</p> <p>Unue, por senkulpigi sin, la trajnkompanio JR-Okcidento indikis, ke surtraka ŝtono elreligis la trajnon. Poste ĝi agnoskis, ke la 23-jaraĝa kondukisto tro rapide veturis. Li volis tiel korekti 90-sekundan malfruiĝon kaŭzitan de malĝusta haltigo ĉe la antaŭa stacio. Dume altagiĝis diversaj dubindaj labormetodoj de la privatigita trajnkompanio.</p> <h3>Premhoraj trajnoj</h3> <p>Evidentiĝis, ke JR-Okcidento ege dense kaj rapide trafikigas siajn trajnojn por superi aliajn privatigitajn kompaniojn. Inter Takaraduka kaj Osaka trajno de JR-Okcidento veturas en 23 minutoj, dum tiu de Hankjuu-kompanio en 30. Dum matenaj kaj vesperaj premhoroj trajnoj venas ĉiun 3an-5an minuton. Iuj trajnoj haltas nur 15 sekundojn en stacio Takaraduka. Ne estas permesate al kondukisto malfruiĝi.</p> <p>Kiam fuŝas kondukistoj, tiujn atendas severa punkorekta kurso. Kondukistoj devas pasigi tagojn, skribante sinkritikajn raportojn. Ili ne rajtas kontraŭdiri al la kontrolistoj, kiuj blasfeme insultas ilin. Ili devas promesi, ke, se ili denove fuŝe kondukos, ili eksiĝos el la posteno. Ankaŭ la salajro estas pune malaltigita. Eĉ pli draste: en 2001 kondukisto, kiu malfruigis trajnon je 50 sekundoj por kontroli ĝian sekurecon, estis sendita al la trajnkompania „punejo”, kie tri tagojn poste li sin mortigis.</p> <h3>Ses kompanioj</h3> <p>En 1987 la japana nacia fervojo estis privatigita kaj dividita en ses kompaniojn. Komence JR-Okcidento havis 53 000 dungitojn: ĝi nun havas 32 000. Krome la privataj kompanioj anstataŭigis siajn konstantajn dungitojn per portempaj, malpli bone salajrataj laboristoj. Gravajn taskojn, ekz. kontroladon de la trakoj kaj vagonoj, ili donis al aliaj firmaoj. Multaj stacioj estas nun senstabanaj.</p> <p>Diris eksa dungito de la nacia fervojo: „Post privatigo la devizo iĝis ‚servo kaj profito’. Batalemaj sindikatoj estis tiel subpremataj, ke la laborejo fariĝis malgaja kaj laboristoj ne povis paroli libere. Malantaŭ ĉi-foja akcidento ekzistas malmoderna dungitara gvidado.”</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo/pg</div>  
