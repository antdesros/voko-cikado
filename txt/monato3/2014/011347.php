      <h3>Ekonomio</h3>
      <h3 class="tem"></h3>
      <h2>Konsumantoj agitiĝas kaj konsciiĝas</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <b>Pro la ekonomia krizo de la pasintaj jaroj, grandskala trompado, fraŭdo kaj ekologia
      konsciiĝo, pli kaj pli da homoj serĉas alternativojn. Plurloke ekestas iniciatoj,
      kiuj protestas kaj faras proponojn, sed ili ne atingas la amasojn kaj estas malpli
      potencaj ol la industrio kaj burokrataro. Ankaŭ la asocioj de konsumantoj fojfoje
      ŝajnas esti ĉe la kontraŭa flanko. Kiel ofte, iu kompromiso akceptebla por la produktantoj
      kaj la konsumantoj estus la plej bona solvo, sed interparolo inter ambaŭ flankoj malfacilas.
   </b>
</p> 
<h3>Ekonomia krizo </h3> 
<p>Antaŭ kelkaj jaroj okazis pluraj bankokrizoj, kun konsekvencoj ĝis hodiaŭ senteblaj
   en multaj landoj. Reage al la malbona direktado de la bankoj, la subteno fare de registaroj,
   la multaj politikaj skandaloj, indigniĝis multaj civitanoj, kiuj ne apartenas al la
   pli altaj sociaj klasoj kaj kiuj perdis ĉiun fidon en la nuna kapitalisma sistemo,
   kiu avantaĝigas ĉefe tiujn, kiuj jam havas monon, koste de tiuj, kiuj havas malmulte.
   Naskiĝis la protest-grupo 
   <i>Indignados</i> en Hispanio kaj la 
   <i>Occupy</i>-movado en Usono, kaj similaj grupoj en multaj aliaj landoj.
</p> 
<p>La ofte vidata slogano „Ni estas la 99 %” protestas kontraŭ la fakto, ke „99 % de la bonhavoj estas posedataj de 1 % de la popolo”. La aliaj 99 % de la homoj do kune posedas nur 1 % de la bonhavoj. Eĉ se la proporcio
   1/99 ne estas preciza, restas la granda socia malegaleco: la pli riĉa malplimulto
   (la 1 %) posedas aŭ mastrumas la plej grandajn bonhavojn. Ili direktas komercajn entreprenojn
   kaj bankojn kaj diktas la leĝojn – kompreneble favorajn al la propraj industrioj kaj interesoj. La neriĉaj homoj (la
   99 %) povas nur voĉdoni kaj esperi, ke ili ne perdos sian ŝparitan monon. Estas tute
   klare, ke la klasbatalo de la 19a jarcento daŭre vivas, sed en pli moderna vesto.
</p> 
<h3>Trompado al klientoj</h3> 
<p>Se kompanio produktas bonegajn fridujojn, kiuj uzeblas dum 30 jaroj, logike, ĝi post
   kelkaj jaroj ne plu havos vendojn, se ĉiuj homoj efektive aĉetis tian fridujon. En
   la nuna epoko kompanioj devas havi laŭeble multe da profito kaj laŭeble grandan vendociferon
   por atingi altan kurzon de siaj akcioj en la borso kaj pliriĉigi siajn akciulojn.
   Bonaj, longdaŭre funkciantaj aparatoj do ne estas ekonomie senchavaj. Registaroj ne
   vere kontraŭas tion. Ili nepre volas krei laborlokojn por siaj elektantoj, kaj estas
   alloge doni laboron al multaj homoj, eĉ se ili produktas malbonajn aŭ neuzeblajn varon.
</p> 
<p>Kompanioj diversmaniere – bedaŭrinde ofte maletike – agadas. La plej „honesta” estas la industrio pri poŝtelefonoj kaj aliaj porteblaj aparatoj. Multaj homoj preskaŭ
   ĉiujare aĉetas novan poŝtelefonon, ĉar oni daŭre surmerkatigas pli potencajn aparatojn,
   kiuj igas la „malnovajn” aparatojn malaktualaj. La ĉie distrumpetata reklamo konstante instigas homojn aĉeti
   la pli novajn aparatojn. La socie malfortaj homoj estas facilaj viktimoj de la intensaj
   kampanjoj, ĉar ofte ĝuste tiuj volas kaŝi sian malriĉecon aĉetante la plej novajn
   (kaj multekostajn) poŝtelefonojn, kun la konsekvenco, ke unu semajnon poste ili ne
   plu havas monon por mendi picon per sia nova aparato.
</p> 
<p>Truko de la industrio estas vendi aparatojn ne tro kostajn, sed kiuj konstante bezonas
   kartoĉojn aŭ ion alian por garantii konstantajn enspezojn. Ekzemplo estas la novaj
   kafomaŝinoj, kiuj funkcias per apartaj kartoĉoj aŭ aliaj kafpulvorujoj por ĉiu unuopa
   taso. (Aldone estas la ekologia aspekto: kompare kun normala pakumo, la kartoĉoj foruzas
   amason da materialo kaj kaŭzas montojn da rubaĵoj.) Alia, malpli etika ekzemplo estas
   tiu de la printiloj, kiuj ofte estas malmultekostaj, sed bezonas inkon tiel multekostan,
   ke oni parolas pri la „nigra oro”.
</p> 
<p>Antaŭ nelonge televida elsendo malkaŝis veran trompadon: ĉar profiti daŭre vendante
   novajn kartoĉojn ŝajne ankoraŭ ne sufiĉas por la monavida industrio, oni trompas la
   klientojn vendante ne tute plenajn kartoĉojn unuflanke, aliflanke intence produktante
   la aparatojn tiel, ke ili difektiĝas pli frue ol normale, pere de „delikataj” partoj, kiuj do rompiĝas iom post la fino de la garantio-periodo. Iuj firmaoj ankaŭ
   programas la printilojn tiel, ke ili aŭ rifuzas ne-aŭtentikajn kartoĉojn, aŭ simple
   anoncas ke la kartoĉo estas anstataŭigenda post difinita tempo, eĉ se ĝi ne estas
   jam malplena. Okazas ankaŭ, ke printiloj simple ĉesas funkcii post iu (tamen sufiĉe
   granda) kvanto da printitaj paĝoj.
</p> 
<h3>Kiu kulpas?</h3> 
<p>Parte kulpas la konsumanto. Anstataŭ plendi pro la multekosta inko, li povus nur printi
   ion, kiam estas vere bezonate (tiel samtempe ŝparante paperon) kaj ĝoji, ĉar li nun
   povas akiri printilon multe pli kvalitan kaj ege malpli kostan ol antaŭ 20 jaroj.
   Kiu bezonas multajn printaĵojn, aĉetu pli kostan, sed ankaŭ pli ŝpareman printilon.
</p> 
<p>Kulpas ankaŭ la protestantoj. Ili ne sukcesas konigi, kiuj printiloj havas kiajn mankojn
   aŭ kiel solveblas problemoj. Tiaj informoj troveblas, sed nur post longa serĉado kaj
   sen garantio, ke ili estas fidindaj.
</p> 
<p>La germana ekonomikisto Christian Kreiß [krajs] ĵus aperigis libron „Geplanter Verschleiß” („Planita eluziĝo”, ISBN 978-3-944305-51-6), en kiu li asertas, ke la industrio perfekte povas prognozi
   la vivodaŭron de ĉiu ero de aparato kaj – kondiĉe, ke oni uzas erojn kun la sama vidodaŭro – ankaŭ la vivodaŭron de la aparato. La industrio replikas, ke efektive ĝi produktas
   diverskostajn kaj diverskvalitajn produktojn: por uzanto, kiu nur malofte bezonas
   ĝin, la malmultekosta dum longa tempo eltenos, sed profesiulo, kiu multe uzas ĝin,
   aĉetu la multekostan.
</p> 
<h3>Defendantoj de la konsumantoj lamas</h3> 
<p>En ĉiuj landoj ekzistas asocioj, kiuj analizas la produktojn vendatajn en la merkato,
   testas ilin kaj aperigas la rezultojn, sed ili estas perdantaj la fidon de siaj membroj.
   En 2008, tuj antaŭ la granda banko-krizo, islanda banko reklamadis en la belga radio,
   proponante tre favoran renton je ŝparkontoj. Kvankam en la financa mondo estis ĝenerale
   sciate, ke la islanda banksistemo estas kolapsonta, la belga 
   <i>Test-Aankoop - Test-Achats</i> (= aĉetaĵo-testo, cetere fikonata pro siaj agresemaj varbkampanjoj) ne avertis kontraŭ
   tiu banko, sed anstataŭe konsilis uzi la favoran investeblon, ĉar „ŝpardeponoj de privataj personoj ja estas asekuritaj”. Ĉu vere saĝa kaj etika konsilo?
</p> 
<p>ADAC estas la plej granda asocio de aŭtomobilistoj en Germanio. Ĉiujare ĝi disdonas
   premion por la plej ŝatata aŭto en Germanio. Komence de 2014 iĝis sciate, ke la gajninto
   ricevis ne 34 299 voĉojn kiel anoncite, sed nur 3409, ridindan kvanton por asocio,
   kiu havas 18 milionojn da membroj. Poste iĝis konataj ankoraŭ kelkaj aliaj skandaloj
   ĉe ADAC.
</p> 
<p>Ankaŭ la gazetaro kaj televido malmulte utilas. Prezentadoj de novaj produktoj tie
   estas farataj laŭde, rapide kaj senkritike. Ŝajne la plimulto de la homoj ŝatas tion,
   ĉar ekzemple la brita aŭtomobil-televidprogramo 
   <i>Top Gear</i> (<a href="http://www.topgear.com">www.topgear.com</a>) estas ege populara ankaŭ ekster Britio; ĝi prezentas belajn spektaklojn, sed apenaŭ
   utilajn informojn.
</p> 
<h3>Ekologia konsciiĝo</h3> 
<p>Estas klare, ke la konsumsocio atingis sian kulminon kaj ke (multaj, sed ne ĉiuj ...)
   homoj konsciiĝas pri la rubo, kiun ili ĉiutage produktas. Apenaŭ troveblas kvadratmetro
   da naturo sen spuroj de homa civilizo.
</p> 
<p>En la oceanoj flosas plastaj rubo-tapiŝoj. La nord-pacifika rubovortico estas la plej
   granda el la kvin rubocirkuloj (angle 
   <i>North Pacific Gyre</i>). La surfaco de tiu ruboinsulo havas la grandon de Teksaso aŭ Mez-Eŭropo. Laŭ kalkuloj,
   komence de 2008 cirkulis ĉirkaŭ 100 milionoj da tunoj da plasta rubo en la rubovortico.
   Tiu plasto kaŭzas la morton de multaj marbestoj. Ĉirkaŭ 70 % de ĉiu plasta rubo sinkas
   al la margrundo. 
</p> 
<p>Pli kaj pli da homoj konscias, ke manĝaĵoj estas tro-pakitaj. Multaj kukoj estas unuope
   pakitaj per plasto, tiuj kukoj siavice estas ankaŭ grupe volvitaj en plasto, kaj finfine
   tio estas pakita en kartona skatolo. Kaj tiuj kartonaj skatoloj dum transporto ankoraŭfoje
   troviĝas en pli granda pakaĵo! Pro tio, Catherine Conway malfermis la vendejon 
   <i>Unpacked</i> (nepakita) en Londono en 2007. Ekzistas ankaŭ similaj vendejoj en Teksaso kaj Berlino.
   Tamen estas ankaŭ kontraŭa evoluo: pro skandaloj pri manĝaĵoj dum la pasintaj jardekoj
   ekestis striktaj reguloj pri higieno kaj pakumo. Eĉ ne plu estas permesate uzi restaĵojn
   el restoracio kiel bestomanĝon.
</p> 
<p>Sekve de la (programita) paneo de elektraj aparatoj, konsumado de kartoĉoj kaj ĉiaj
   aliaj aferoj por unufoja uzo, kreiĝas pli kaj pli da rubo. Laŭ esploro, kostas sep
   litrojn da akvo por produkti unu plastan akvobotelon. Pli bone trinki akvon de la
   krano do, ĉie, kie eblas! Same pri forĵetataj trinkujoj por kafo, teo, kaj tiel plu:
   kostas multege da materialo, akvo kaj energio produkti ĉion tion, dum la rubo-monto
   grandiĝas. Sume: unu forĵetebla objekto havas trioble malbonan efikon sur la medion:
   akvomalŝparon, energiomalŝparon kaj rubokreadon.
</p> 
