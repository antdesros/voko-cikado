      <h3>Moderna vivo</h3>
      <h3 class="tem">TELEFONADO</h3>
      <h2>Servo alternative fiksita</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ankoraŭ aŭdeblas plendoj – sed telefonservoj en Kubo iom post iom pliboniĝas post gravaj ŝanĝoj trafintaj dum 2008 la tutlandan telefonentreprenon ETECSA.</p> <p>Plej gravas la zorge pripensita decido transiri al libera programaro. Ekde printempo 2008 oni malpli aplikas vindozajn solvojn. La decido estas logika kaj komprenebla. Temas pri malriĉa lando kun severe limigitaj kapabloj komercaj, kaj preskaŭ neniu instalita kopio de la operaciumo <i>Vindozo</i> estas laŭleĝa. Ne eblus agi alimaniere, pro memkompreneblaj kialoj. Krome, la deŝuton de programoj el interretaj serviloj ofte baras leĝaj malpermesoj, ĉar Kubo troviĝas en „nigra listo”.</p> <h3>Tutlanda reto</h3> <p>Gravas due la plimultiĝo de instalaĵoj (nun pli ol 30 000) de la tiel nomata Alternativa Fiksa Telefonservo, kiu uzas la teĥnologion de poŝtelefonoj por komunumaj telefonservoj. Ĝis nun pli ol 3500 publikaj, fiksaj telefonaparatoj en malfacile alireblaj komunumoj estis instalitaj, kaj oni esperis atingi la ciferon 4200 fine de 2008. Tiamaniere 581 komunumoj (kun pli ol 300 loĝantoj) kaj 1531 loĝlokoj, malfacile kaj nur multekoste atingeblaj per kutimaj, fiksaj telefonservoj (ekzemple montaraj lokoj), estas alligitaj al la tutlanda reto. Se ne, oni devus atendi ankoraŭ jarojn.</p> <p>Aliflanke, eĉ en la ĉefurbo la kutima, fiksa telefonservo estas celobjekto de plendoj, ĉar la atendolisto de dezirantoj havi propran, hejman telefonaparaton ĉiam kreskas. De tempo al tempo oni disponigas limigitan kvanton da pliaj instalaĵoj hejmaj por difinita urboparto, kun rigora selektado de bonŝanculoj laŭ difinitaj kriterioj (la graveco de la laborposteno de la deziranto, ekzisto de malsanuloj ktp).</p> <p>La solvo bazita sur la Alternativa Fiksa Telefonservo postulas limigitan tempon por uzantoj, ĉar mankas rimedoj. La afero komplikiĝis pro la plimultiĝo de la mesaĝoj SMS (servo de mallongaj tekst-mesaĝoj) senditaj ĉefe de kuracistoj laborantaj eksterlande. Tio devigis la entreprenon ĉesigi la servon, verdire ne difinitan en la kontrakto. Tamen, oni anoncas por 2009 reaperon de la servo, sed nur por tiuj, kiuj rilatas al sanserva kunlaboro en eksterlando.</p> <p>La publika telefonservo, aliflanke, daŭre anstataŭigas la malnovajn monerajn aparatojn per kartaj. Tamen telefonkartoj ne kontentigas: nur la tiel nomata <i>Propia</i>-karto (esperante: propra) estas reŝargebla.</p> <h3>Poŝtelefonoj</h3> <p>Tria grava decido rilatas al poŝtelefonoj. Ekde aprilo 2008 kubaj civitanoj rajtas kontrakti pri servoj de poŝtelefonado (antaŭe oni devis trovi eksterlandanon, kiu aperis en la kontrakto kiel uzanto), sed nur kontraŭ alta tarifo en konverteblaj kubaj pesoj (CUC), do afero nepensebla por la plimulto. Bonŝance fine de 2008 oni decidis duone rabati la tarifon. Kvankam ĝi restas alta (60 CUC) kaj ankaŭ la monata tarifo altas, la decido estas bonaŭgura.</p> <div style="text-align:right">Alberto García F<span class="mm">UMERO</span></div> 
