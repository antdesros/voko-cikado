      <h3>Turismo</h3>
      <h3 class="tem">UNUA MONDMILITO</h3>
      <h2>La unua pafo okazis en Aŭstralio</h2>
      <!-- komenco de la enhavo -->

       

       <p>Britio proklamis militon al Germanio la 4an de aŭgusto 1914 je la 23a horo. En Melburno,
   ĉefurbo de la federacia ŝtato Viktorio, tio estis je la 9a horo la 5an de aŭgusto.
   La informo atingis Aŭstralion telegrafe per la morsa kodo el Britio.
</p> 
<p>Eksciinte pri la milito, germana vaporŝipo „Pfalz” provis fuĝi el la golfo de la haveno Phillip ĉe Melburno. La enirejo de tiu haveno
   estas mallarĝa kaj tiuloke estas rapida fluo, kiu estas tre danĝera por ŝipoj. Por
   trapasi ĝin oni bezonas piloton. Por gardi la havenan enirejon, Britio instalis kanonojn
   A1 kaj B1 en Fuorto Nepean en la dua duono de 19a jarcento.
</p> 
<p>Do, akurate posttagmeze la 5an de aŭgusto, la kanono B1 sur la orienta flanko de havena
   enirejo elpafis obuson, kiu pasis preter la vaporŝipo „Pfalz”. Poste la piloto, kapitano Robinson, persvadis la kapitanon de „Pfalz”, ke la ŝipo revenu en la havenon. Tio estis la unua pafo de la unua mondmilito.
</p> 
<p>La 100-jariĝon de la unua pafo de la unua mondmilito en la Brita Regno oni celebros
   la 5an de aŭgusto ĉe Fuorto Nepean.
</p> 
<p>Interesa fakto estas, ke ankaŭ la unua pafo de la dua mondmilito okazis sur la sama
   loko. Kanono A1 pafis obuson preter ŝipo, kiu ne respondis demandon de Fuorto Nepean.
   Temas pri la aŭstralia vaporŝipo „Woniora”. Tamen poste la kapitano rapide donis respondon.
</p> 
<p>Post la dua mondmilito oni vendis ambaŭ kanonojn por ferrubo. Nun oni planas restarigi
   ilin por allogo de turistoj. La tuboj de la kanonoj estis konservitaj. Fuorto Nepean
   troviĝas en la Nacia parko „Point Nepean”.
</p> 
<p>La duoninsulo Point Nepean (Pinto Nepean) situas sur la orienta flanko de la enirejo
   de la golfo de la haveno Phillip. Melburno troviĝas je ĉirkaŭ 70 km for de la norda
   marbordo de la golfo.
</p> 
