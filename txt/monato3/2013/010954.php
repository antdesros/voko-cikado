      <h3>Arto</h3>
      <h3 class="tem">PENTRARTO</h3>
      <h2>La testamento de Karavaĝo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉu ekzistas tempomaŝino? Retorika demando: oni scias, ke vojaĝi pasintecen estas ankoraŭ utopio. Tamen lastatempe la pasinteco ŝajnas fojfoje etendi la brakojn al ni, aŭ pli precize iuj trovigas al ĝi la manieron nin atingi: oni pripensu ĉi-rilate la kantojn de mortintaj kantistoj, kiuj estas kelkfoje eldonataj eĉ jarojn post ilia forpaso.</p> <h3>400-jara salto</h3> <p>Pli malofte tiaj postmortaj malkovroj (aŭ foje veraj „reklamaj artifikoj”) rilatas aliajn artajn fakojn, kiel ekzemple pentrarton. Pro tio la novaĵo verŝajne havis tiel grandan resonon. En la pasintaj monatoj oni fakte „malkovrigis” al la homaro nekonatan pentraĵon de la mondfama itala artisto Michelangelo Merisi da Caravaggio, aŭ pli mallonge Karavaĝo.</p> <p>Tiun artaĵon – post 400-jara salto – la publiko povis lastatempe unuan fojon admiri en ĝia tuta beleco. Temas pri verko, kiun, ekde ĝia malkovro (okazinta en 1976), oni alskribis al iu malfrua sekvanto de Karavaĝo, sed ne al la majstro mem. Ties ekzisto estis ĝis nun konata nur al malgranda aro da „laborkoncernatoj”, kiu inkluzivis ĝian proprietulon – kiu preferis resti anonima pro privatecaj kialoj –, iujn teknikistojn kaj kelke da spertuloj. Antaŭ kelke da monatoj oni donis al la tuta mondo la eblon ekscii pri ĝi.</p> <h3>Interesa komparo</h3> <p>La <i>Sternitan Johanon la Baptiston</i> oni povis admiri dum pluraj monatoj inter 2010 kaj 2011 en la amsterdama Muzeo Domo de Rembrandt (<i>Museum Het Rembrandthuis</i>, <a href="http://www.rembrandthuis.nl">www.rembrandthuis.nl</a>), kio interalie ebligis interesan komparon inter la verkoj de Karavaĝo kaj tiuj de Rembrandt, la t.n. „baroka genio”, konsiderata la vera posteulo de la itala pentristo.</p> <p>Tiu ĉi verko estas preskaŭ certe identigebla kun unu el la du verkoj, kiujn Karavaĝo kunportis en la ŝipeton, per kiu li navigis al la vilaĝo Porto Ercole. Temas sendube pri verko el lia lasta arta fazo. Ĝi estis plenumita en periodo, kiam la pentristo sin turnis al nova, miriga direkto, sed kurioze tiutempe reelmergiĝas ankaŭ pluraj temoj el lia junaĝa produktado.</p> <h3>Senco de la vivo</h3> <p>Melankolia kaj aparte malkvietiga, tiu „nova” karavaĝa pentraĵo estas difinita de pluraj fakuloj kiel probable lia lasta verko, pentrita en 1610 en eta toskana vilaĝo (Porto Ercole), en kiu la baroka pentristo nelonge poste forpasis. Oni hipotezas, ke pere de tiu ĉi pentraĵo li volis proponi specon de resumo pri la centraj temoj de la propra arto, kvazaŭ idean testamenton aŭ originalan „adiaŭan leteron”.</p> <p>Ankaŭ la sankta Johano videbla en la pentraĵo ŝajnas fakte, same kiel Karavaĝo, primediti la finan sencon de la vivo. Kvankam plejparto de la fakuloj jam kredas, ke ĝi estis verko de Karavaĝo mem, la montrado de ĉi tiu majstroverko al la publiko en Nederlando parte reflamigis polemikojn kaj debatojn pri la pravigeblo de tia atribuo.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div>  
