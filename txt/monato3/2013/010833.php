      <h3>Spirita vivo</h3>
      <h3 class="tem">EŬROPA UNIO</h3>
      <h2>Kredo gravas</h2>
      <!-- komenco de la enhavo -->

       

       <p>Unuafoje en la historio de Eŭropa Unio, la traktato de Lisbono en 2007 oficialigis malferman kaj regulan dialogon inter eŭropaj institucioj, eklezioj, religioj kaj komunumoj de diversaj kredoj. Spite la ĝeneralan percepton, ke religiaj kaj kredaj demandoj forestis de la eŭropa integriĝo, tamen estas konataj longedaŭraj kontaktoj inter religiuloj, politikistoj kaj eŭropuniaj ŝtatoficistoj ekde la Deklaro de Schuman<sup>1</sup> de 1950 ĝis hodiaŭ. Analizo de la religiaj kontaktoj faritaj dum tiu periodo malkaŝas, ke 120 grupoj reprezentantaj katolikajn, protestantajn, anglikanajn, ortodoksajn, judajn, islamanajn, hinduajn, budhanajn kaj framasonajn komunumojn dialogis kun la Eŭropa Komisiono.</p> <h3>Personaj interesoj</h3> <p>Publikprivataj interrilatoj estas rezulto el personaj religiaj interesoj de politikistoj kaj oficistoj koncernataj en la traktado de eŭropa kunlaboro, kaj ne el sistema politiko pri religio. Malgraŭ la ĝenerala opinio, ke la romkatolika eklezio estis la unua apoganto de la traktado pri eŭropa integriĝo post la dua mondmilito, fakte estis la ekumena movado dum la intermilita periodo, kiu starigis la unuan transnacian grupon por konsiderado de religio, formitan de protestantaj kaj anglikanaj ekleziuloj kaj politikistoj konsilantaj ekleziojn pri eŭropa integriĝo.</p> <p>Provaj rilatoj disvolviĝis el la decido de la Eŭropa Komisiono interrilati kun eklezioj kaj religioj en 1982. Tiuj interrilatoj fariĝis formalaj en 1990, post kiam la prezidanto de la Eŭropa Komisiono lanĉis publikan debaton. Rezulte de tio, kreskanta nombro de eklezioj kaj komunumoj de diversaj kredoj starigis oficejojn en Bruselo. Tiel ili iniciatis la interrilatojn kun eŭropaj institucioj, kiuj efektive okazis ekde 2007, kiam veraj instituciaj interrilatoj komenciĝis. La instituciigo stimulis regulajn publikajn kunsidojn inter altrangaj religiaj gvidantoj kaj eŭropuniaj oficistoj.</p> <p>La Komisiono estas la sola eŭropa institucio rekte direktanta la interkredan dialogon, sed estas propono krei similan mekanismon en la Eŭropa Parlamento.</p> <p>Kvankam religiaj demandoj restas sub la jurisdikcio de la eŭropuniaj membroŝtatoj, tamen la kreskanta nombro de oficejoj de religiaj institucioj ĉe Eŭropa Unio estas indiko, ke la aferoj pri fido gravas en la koridoroj de la eŭropaj institucioj kaj en la konstruado de la eŭropa politika sistemo.</p> <div style="text-align:right">Lenio M<span class="mm">AROBIN</span></div> <h6 class="pied">1. La Deklaro de Schuman de la 9a de majo 1950 estis registara propono de Robert Schuman, la tiama ministro pri eksterlandaj aferoj de Francio, por krei novan organizan formon de ŝtatoj en Eŭropo. (red.)</h6> 
