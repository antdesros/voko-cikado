      <h3>Moderna vivo</h3>
      <h3 class="tem">FRANCIO</h3>
      <h2>Ekzameno ekzamenata</h2>
      <!-- komenco de la enhavo -->

       

       <p>Bakalaŭriĝi – t.e. en Francio akiri lernejfinan abiturientan diplomon – okupas gravan lokon en la tiea socio. La diplomo ebligas eniron al universitato kaj rolas kiel atesto pri bona studfino.</p> <p>Ĝi estas iel simbolo de sukceso, kaj gepatroj taksas ĝin multe pli grava ol ĝi fakte estas. Antaŭ 20 jaroj la ministro pri instruado celis, ke almenaŭ 80 % el finstudintaj gimnazianoj sukcese akiru la diplomon.</p> <p>Trafita estis la celo, sed nepras memori, ke ekzistas pluraj specoj de bakalaŭroj, ĉu ĝeneralaj (kun tendenco matematika, scienca aŭ literatura), ĉu teknikaj, ĉu profesiaj. Diversaj estas ankaŭ la niveloj, kiuj montriĝas ĉe ekzameno-sesioj.</p> <h3>Kandidato</h3> <p>Estas du partoj: skriba kaj parola. Kandidato ekzameniĝas pri la francaj lingvo kaj literaturo ne en la lasta sed en la antaŭlasta jaro de gimnazia studado.</p> <p>Lastatempe la ministerio pri instruado komisiis al instruistino kaj ĵurnalisto, Martine Marie Muller, priraporti ĉi tiun parton de la ekzameno.</p> <p>Frapis ŝin la socia diferenco inter samgeneraciaj liceanoj. Junuloj, kiujn ŝi ekzamenis, devenis de du liceoj: publika liceo en popola kvartalo de Parizo, kaj privata franc-usona liceo en pli riĉa kvartalo.</p> <h3>Kablo</h3> <p>Ŝi observis miskonojn ĉe ambaŭ grupoj, ekzemple ĉe liceano, kiu asertis, ke la verkisto Honoré de Balzac aktivis en la 18a jarcento (fakte la 19a), aŭ ke Victor Hugo „rompis kablon” kontraŭ Napoleono la 3a – junulara slango por eldiri, ke iu perdis memregadon.</p> <p>Muller notis, ke ne ĉiuj gejunuloj primajstris la francan lingvon: kelkaj grave miskonjugaciis francajn verbojn. Sed plej ŝokis, ke liceanoj el la malriĉa kvartalo ofte fuŝis la lingvon kaj montris sin malkapablaj klare paroli kaj esprimi personan penson.</p> <p>Male, liceanoj el la privata liceo parolis kun facileco, memfido, kaj bone uzis la lingvon, kio tutcerte provizis ilin per pli da poentoj en la ekzameno. Laŭ Muller, la diferencoj inter la du grupoj rezultas el hejmaj sociaj medioj.</p> <h3>Gepatroj</h3> <p>„Tiuj junuloj montras ne tion, kion ili lernis, sed tion, kio ili socie estas”, raportis Muller. „Mi havis la impreson, ke mi pritaksas ne ilin, sed iliajn gepatrojn. La pli bonan nivelon de la dua grupo havigas ne la lerneja sistemo, sed la devena socia medio.”</p> <p>Jam de multaj jaroj oni scias pri tiaj diferencoj . Tamen ŝanĝiĝas nenio. Ke la lerneja sistemo donu al ĉiu egalajn ŝancojn, bone konscias la nova franca prezidanto François Hollande. Verŝajne tamen mankas sufiĉe da mono por enkonduki reformojn.</p> <div style="text-align:right">Pierre G<span class="mm">ROLLEMUND</span></div>  
