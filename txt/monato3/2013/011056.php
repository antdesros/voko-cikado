      <h3>Scienco</h3>
      <h3 class="tem">ANDROLOGIO</h3>
      <h2>Armitaj ĝis la dentoj  kontraŭ impotenteco</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Sciencaj esploroj kelkfoje sukcesas trovi strangajn, sed ne ridindajn rilatojn inter plej malsamaj kondutoj, situacioj kaj san-problemoj. Certe la jena novaĵo ne estos memorata kiel la malkovro de la jarcento, sed ĝi povus interesi parton de la legantaro. Mallonge: la viroj, anstataŭ viziti (inkognite kaj multekoste) farmakologojn aŭ andrologojn, fiksu regulajn rendevuojn – ĉefe meze de pasia amhistorio – kun la propra ... fid-dentisto!</b></p> <p>Ni klarigu la koncepton: antaŭ nelonge aperis esploro, fare de turkaj fakuloj el la universitato de İnönü, en Malatya, surbaze de kiu oni hipotezas la ekziston de kunligo inter tiel nomata erektiĝa misfunkcio kaj la ĉeesto de kronikaj dent-problemoj, kiel akuta gingivito aŭ periodontito. La esploro estas publikigita en <i>Revuo pri Seksa Medicino</i> (angle: <i>Journal of Sexual Medicine</i>). Kiu spertas kronikan periodontiton, ja tiu havas preskaŭ 3-oblan probablon (53 % de la okazoj) suferi pro erektiĝa misfunkcio, kompare al (el buŝ-sana vidpunkto) bonfartulo. Ĉi-lasta kategorio spertas fakte erektiĝajn problemojn nur en 23 % de la okazoj.</p> <h3>Koronariaj arterioj</h3> <p>Konsiderante diversajn parametrojn, kaj klinikajn kaj soci-kulturajn (ekzemple: klerec- kaj riĉec-nivelon, socian prosperon, salajrojn, aĝon, dik-ventrecon), la esplorintoj rimarkis, ke viroj trafataj de gravaj dent-patologioj havas ekzakte 3,29-oblan probablon sperti erektiĝan deficiton ol la ceteraj. Aliflanke estas memorigende, ke ne unuafoje oni substrekas interligon inter erektiĝo kaj kora funkcikapablo. Oni jam sciis, ke kronikaj periodontitoj estas potenciale kapablaj startigi iom da vaskulaj patologioj, ne malofte ekzemple problemon ĉe la koronariaj arterioj. Alia reala fakto estas, ke du trionoj de la viroj, krom probleman erektiĝon, priplendas ankaŭ korajn problemojn. La turkaj sciencistoj simple kunligis la du jam konatajn faktojn, prezentante baze de ili trian interesan (kaj probable trafan) interligon.</p> <h3>Bluaj tablojdoj</h3> <p>Oni ne rajtas scii ekde nun, ĉu la prezentita teorio estas tute fidinda, ĉar la nombro de la viroj submetitaj al la esploro estas relative malalta (proksimume okdeko da uloj kun, kaj okdeko da uloj sen erektiĝaj problemoj). Estontece do ja nepros aldona esplorado ĉi-rilate. Ĝis tiam, tamen, andrologoj kaj iliaj pacientoj – kadre de ordinara anamnezo – povos klopodi konsideri tiun aspekton por klarigi la kialojn de ekzistantaj erektiĝaj problemoj. Ege pli simple kaj nature, regula ŝanĝado de dentobroso kaj, almenaŭ 2-foje ĉiun jaron, purigado de la dentoj ĉe kompetenta dentkuracisto povus do havi similan efikon al tiu de la tro disvastiĝintaj – kaj ankoraŭ tro multekostaj (kvankam tre baldaŭ la koncerna patento eksvalidiĝos, kun sekva malpliiĝo de la prezoj) – bluaj tablojdoj.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
