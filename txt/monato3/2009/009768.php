      <h3>Lingvo</h3>
      <h3 class="tem">AMERIKO</h3>
      <h2>Kia lingva ekologio por daŭripova evoluo?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Sendube la amerika kontinento estas granda lingva mozaiko kaj tial multaj prilingvaj fakuloj sin demandas kiel administri tiun ĉi multlingvismon. Kiajn kriteriojn sekvi por garantii konservadon de la amerika lingva ekologio ene de integriĝinta spaco? Kaj konsekvence, al kiaspeca integriĝo ni devas aliĝi: ĉu al inkluziva aŭ ekskluda? Oni devas scii, ĉu niaokaze plurlingvismo konsistigas faktoron evolupovan aŭ retroiran.</p> <p>La fakto estas, ke multaj fakuloj daŭre esploras kaj verkas pri tiu ĉi temo kun la celo konsciigi la civitanaron pri la reala lingva malekvilibro, kiu damaĝas la lingvan ekologion de tiom vasta regiono.</p> <p>Tiurilate oni devas atenti, ke „lingvo akompanas imperion” laŭ la esprimo de Antonio de Nebrija [antónjo de nebriĥa], aŭtoro de la unua gramatiko de la kastilia lingvo en siaj dediĉaj notoj al la Katolikaj Gereĝoj en la jaro 1492. Por granda parto de Ameriko, kies integriĝo al la okcidenta mondo tiam komenciĝis, tiu frazo estas ligita al la enrompo de la kastilia lingvo, kiu kaŭzis la unuan grandan skismon en iliajn hejmojn. Ankaŭ la lingvoj angla, franca kaj portugala, kiuj akompanis siajn respektivajn imperiojn, produktis aliajn skismojn en la regionoj, kie ili altrudiĝis.</p> <p>Nuntempe tiuj kvar lingvoj, klasifikitaj inter la plej parolataj en la mondo, apartenas al politikaj institucioj forte strukturitaj kaj konsistigas oficialajn lingvojn en internaciaj organizaĵoj. Apriore, ne ekzistus kialo por pridubigi kvarlingvan projekton de kontinenta integriĝo, tamen, ĉu tiuj lingvoj per si mem spegulas la kulturan kaj lingvan profilon de Ameriko?</p> <p>La realo estas, ke oni apenaŭ havas proksimuman scion pri la nombro de lingvoj ankoraŭ parolataj en la kontinento. Aktualaj raportoj kalkulas, ke estas proksimume 1500 aŭ 2000 lingvoj, klasigitaj en 20 lingvistikajn familiojn, kiuj prezentas riĉan genetikan kaj tipologian diversecon kaj estas uzataj en la jenaj regionoj: Nord-Ameriko (de Alasko ĝis centra Meksiko), Mez-Ameriko (inter centra Meksiko kaj Nikaragvo), Karibio kaj Sud-Ameriko.</p> <p>La situacio de la lingvoj en la tri menciitaj geografiaj regionoj prezentas trajtojn klare diferencajn, kiuj inkluzivas, jen danĝeron pri evidenta malapero, jen subitan kreskon. Oni povas tamen diri, ke la plimulto de la indianaj lingvoj, forte markitaj de la stigmato de longa koloniado, trovas sin reduktitaj ĝis la kategorio de minoritataj lingvoj, kondamnitaj al socia periferio de ties parolantoj, diskriminaciataj, aŭ en plej bonaj okazoj, transformitaj en ekzotikaĵojn, forajn eĥojn de pasinteco konkerita kaj venkita.</p> <div style="text-align:right">Maritza G<span class="mm">UTIÉRREZ</span></div> 
