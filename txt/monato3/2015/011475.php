      <h3>Eseoj</h3>
      <h3 class="tem"></h3>
      <h2>La klasa fototabulo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En urboj kaj urbetoj kun hungara loĝantaro, ĉe la fino de lernojaro, ekaperas klasaj
   fototabuloj en montrofenestroj. Ili estas grandaj, enkadrigitaj tabuloj kun la fotoj
   de abiturientoj kaj de iliaj profesoroj.
</p> 
<p>Preterpasantoj ekhaltas antaŭ la montrofenestro kaj trarigardas la fotojn de gelernantoj
   kaj geinstruistoj. Ili serĉas konatajn vizaĝojn aŭ simple rigardas la ridetantajn
   junajn vizaĝojn. Post la abiturientaj ekzamenoj la fototabuloj estos pendigitaj sur
   la murojn de la koridoroj en la institucioj, kie lernis la fotitoj.
</p> 
<p>Estas la komenco de somero, floras kaj bonodoras la tiliarboj, kaj mi rememoras miajn
   mezlernejajn jarojn, miajn iamajn instruistojn, miajn klaskamaradojn, amikinojn. Pasis
   jam 50 jaroj, de kiam mi estis abituriento. Kelkaj el inter miaj samklasanoj ne plu
   vivas. Kelkaj el inter miaj instruistoj ankoraŭ vivas.
</p> 
<p>Laŭdire, se oni rememoras ankoraŭ la nomon de instruisto post 50 jaroj, tio pruvas,
   ke la memorata persono estis vere bona instruisto. Mi povas rememori preskaŭ ĉies
   nomojn, kaj mi kredas, ke mi havis bonajn geinstruistojn. Ni respektis ilin, kelkajn
   eĉ amis. Kiel plej amatajn mi mencias mian klasestron kaj la instruistinon pri la
   hungara lingvo.
</p> 
<p>Mi havis klaskamaradon, kiu tre lerte imitis homojn, ankaŭ niajn instruistojn. Dum
   paŭzo ni restis en klaso, sed tiu eliris, kaj poste enpaŝis, kvazaŭ iu el niaj instruistoj.
   Li ne devis diri nomon, ni estis sorĉitaj, ĉar ŝajnis al ni, ke li vere fariĝis la
   imitata persono. Ni krevis pro ridado. Ĉiujn li lerte imitis, nur du instruistojn
   li neniam imitis: nian klasestron kaj la instruistinon pri la hungara lingvo. Eĉ tiu
   impertinentulo ilin respektis.
</p> 
<p>Niaj geinstruistoj plejparte estis bonaj pedagogoj. Tio signifas, ke ili sciis regi
   la klason, dum lecionoj estis disciplino. Ho, disciplino! Kion signifis en tiu tempo
   maldisciplino? Ne kiel nuntempe, kiam en lernejoj okazas, ke instruanto kaj instruato
   estas batataj, humiligataj, eĉ mortigataj de agresema knabo. Antaŭ 50 jaroj maldisciplino
   estis, se ni kaŝe babilis dum leciono, ridis aŭ sendis letereton al alia. Mi mem kelkfoje
   tiel kulpis.
</p> 
<p>La instruisto pri la latina lingvo leone muĝis, kiam ni estis „sendisciplinaj”. La instruisto pri la germana lingvo estis alia. Li ne estis „reganto” de la klaso. Dum liaj lecionoj la petolemaj knaboj priĵetis unu la alian per ŝmirita
   pano kunportita por manĝi dum tagmeza paŭzo. Kompreneble, nur kiam la instruisto skribis
   sur la tabulon, kaj ne vidis, kio okazas malantaŭ lia dorso.
</p> 
<p>La aŭtoritato de instruisto dependas ne nur de fakaj scioj, sed ankaŭ de tio, ĉu oni
   tiun iomete timas aŭ ne. Mi ripetas, ni neniam estis malkaŝe impertinentaj fronte
   al niaj instruistoj. Nu, tiu instruisto pri la germana lingvo tute ne estis timiga
   persono. Kiam li estis tre malkontenta pri nia konduto, li minacis: „Tuj mi kriegos!” Sed tio neniam okazis. Li estas delikata homo, maldika, grizhara, ĝentila, kaj ne
   tro severa.
</p> 
<p>En tiu tempo oni povis spekti en kinejo ĉefe sovetiajn filmojn. Lernantoj ne rajtis
   viziti kinejon solaj, nur organizite, kun la tuta klaso, akompanata de instruisto,
   aŭ kun gepatroj. Mi supozas, ke la instruistoj organizis komunan viziton tian, se
   la filmo estis „instrua”. La plej multaj filmoj estis sovetiaj, pri la dua mondomilito, kun malicaj faŝistoj,
   kontraŭ kiuj heroe batalis sovetiaj ruĝarmeanoj.
</p> 
<p>Subkonscie por mi la germana estis lingvo de faŝistoj, kaj mi lernis ĝin ne tro diligente.
   Eĉ se mi ne tro diligente lernis, mi akiris mezbonajn notojn, kaj tio estis sufiĉa
   … por faŝisma lingvo. Kompreneble, tiu mia malemo ne estis tiel konscia, kiel nun.
   Nur nun mi komprenas la kialon de mia malsimpatio al la germana lingvo.
</p> 
<p>Kial mi rememoras eĉ nun tiun instruiston, kiu mem estis germano? Foje okazis, ke
   ni havis skriban taskon. Li diris al la klaso: „Mi ne kontrolos, ĉu vi trompos. Se mi kontrolus, tio signifus, ke mi pridubas vian
   honestecon.” Kaj li ne prispionis nin.
</p> 
<p>Iuj feliĉe malfermis sub la benko kajeron, kaj senĝene kopiis. Inter ili estis ankaŭ
   eminentaj lernantoj, kiuj povintus akiri bonan noton, eĉ nekopiinte. Sed ili deziris
   esti certaj. Mi estis tuŝita de liaj vortoj. Neniam mi aŭdis ion tian. Ĉiuj niaj geinstruistoj
   estis tre atentaj, ke ni ne trompu dum skribaj taskoj. Mi eĉ rememoras junan instruiston
   pri fiziko, kiu diris komplice kaj ridetante, ke ankaŭ li estis lernanto, kaj konas
   la trukojn de trompado.
</p> 
<p>Tiu germana instruisto diris ion nekutiman, sed mi komprenis lin. Ĝenerale mi ne kutimis
   trompi, ĉar mi timis, ke mi estos hontigita antaŭ la klaso. Sed ekde tiam mi lernis
   diligente la germanan, precipe antaŭ la skribaj laboraĵoj, por ke mi ne bezonu kopii.
   Mi lernis parkere multajn versojn el 
   <i>Faŭsto</i> de Goethe, kaj mi ricevis pro la skriba laboraĵo pli bonan noton, ol ĝenerale dum
   lecionoj.
</p> 
<p>Nun mi kredas, ke li eble supozis, ke kopiis ankaŭ mi, ke ne estis tiel bona en la
   germana lingvo ja mi. Li ne sciis, ke ĝuste la eminentaj lernantoj kopiis, mi ne.
   Eĉ nun mi kredas senvalora rezulton malhoneste atingitan.
</p> 
<p>Mia instruisto forpasis antaŭlonge. Li neniam muĝis leone, li estis modesta, mallaŭtvoĉa
   homo. Tamen lia voĉo sonas eĉ nun en mia memoro: „Mi ne kontrolos, ĉu vi trompos. Se mi kontrolus, tio signifus, ke mi pridubas vian
   honestecon.”
</p> 
