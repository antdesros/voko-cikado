      <h2>Kancelierino</h2>
      <!-- komenco de la enhavo -->

       

     <p>Tra Germanio iras nova sento: Por la unua fojo la landon regas virino! Tio sendube estas grava cezuro en la historio de tiu ĉi ŝtato. Angela Merkel, partiestro de kristandemokratoj, nun estas ĉefa registo de Federacia Respubliko Germanio. Angela, cetere, signifas „anĝelo”; ĉu ŝi estos anĝelo por la lando, tion ni vidos. Laŭ la konstitucio ŝi havas la ofic-titolon „federacia kanceliero” (germane: <i>Bundeskanzler</i>). Sed la protokol-ofico en Berlino decidis (certe laŭ postulo de Merkel), ke la ĝusta titolo estas „<i>Bundeskanzlerin</i>” (federacia kancelierino), analoge al „ministrinoj”, kiujn la lando havas jam dekomence. Por sia unua vizito al eksterlando Merkel elektis Parizon, kaj eĉ la francoj obeis al la germanaj kolegoj kaj titolis ŝin ne „Sinjorino la Federacia Kanceliero”, kiel postulus la franca lingvo, sed salutis ŝin kiel kancelierinon.</p> <p>Sed kiel ni procedu en Esperanto, ekzemple en nia revuo? Ĉu ankaŭ nia protokolo postulas skribi „kancelierino”? La germana <i>Bundeskanzlerin</i> estas relative facile prononcebla, almenaŭ por germanoj. En la lingvo internacia jam por la vorto „kanceliero” oni bezonas iom da ekzerco por ĝuste prononci. Sed „kan-ce-li-er-ino” estas vere langorompa, ĉu ne? Mi kompatas niajn komplezajn helpantojn, kiuj ĉiumonate voĉlegas la revuon por blinduloj!</p> <p>Bonŝance pri ŝia nomo ni ne havas problemon, ĉar oni prononcas ĝin same en la germana lingvo kaj en Esperanto (sola diferenco: en la germana la akcento troviĝas sur A). Do ni ne devas aldoni prononcindikon. Germanoj, cetere, jam havas karesnomon por ŝi: Angie. Sed atentu, oni ne prononcas ĝin germane „angi”! Germanoj, adorantoj de la angla lingvo, diras „ejnĝi”. (Kiel prononcas franclingvanoj?) Do eble elturniĝo por nia revuo estus skribi pri „kanceliero Ejnĝi”. Kion vi opinias? D-ro (aŭ d-rino?) Merkel laŭprofesie estas fizikisto – aŭ ĉu ankaŭ ĉi-okaze ni devas skribi „fizikistino”? Kiaj neatenditaj problemoj nur pro tio, ke virino regas Germanion ... Cetere, anĝeloj laŭdifine ne havas sekson, do tutcerte ni ne devas diri Anĝelino Merkel.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
