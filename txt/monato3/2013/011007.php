      <h3>Moderna vivo</h3>
      <h3 class="tem">SVISLANDO</h3>
      <h2>Plurlingveco plurproblema</h2>
      <!-- komenco de la enhavo -->

       

       <p>Svislando havas 3,5 oficialajn lingvojn. La romanĉa estas nur duone oficiala. La germana estas parolata de 70 % de la loĝantoj, la franca de 20 % kaj la itala de 10 %.</p> <p>Laŭ la svisa leĝo ĉiu teritorio, ĉu vilaĝo, urbo aŭ regiono, havas oficialan lingvon. Tio signifas, ke tie la registaro alparolas la civitanojn per tiu lingvo. Publikaj lernejoj uzas tiun lingvon, la ŝildoj surstrataj aperas en tiu lingvo. Ne eblas iel eskapi la lokan lingvon. Alvenintoj, ĉiuj novaj loĝantoj lernu ĝin.</p> <p>Ekzistas nur du lokoj oficiale dulingvaj: la urbo Biel/Bienne (germana/franca) kaj la apuda vilaĝo Evilard/Leubringen (franca/germana).</p> <h3>Kolero</h3> <p>En marto 2013 la urbo Fribourg/Freiburg (franca/germana) aperigis sian novan emblemon. Tio naskis grandan koleron ĉe germanlingvanoj de tiu ĉi teorie franclingva urbo, ĉar legiĝas nur la franca.</p> <p>Germanlingvanoj asertas, ke tiu regionoteorio estas mezepoka, kaj ke oni adaptiĝu al la nova erao, kie miksiĝas multaj homoj el plej diversaj originoj. La kantono Fribourg/Freiburg estas dulingva, la lingva limo trairas ĝian teritorion, tamen la urbo Fribourg situas en la francparola parto.</p> <p>En marto la horloĝokompanio <i>Swatch</i> eldonis raporton en la svisgermana dialekto. Tiu provoka faro intencas insisti pri la ideo <i>Swiss made</i>, do farita en Svislando kaj do aŭtenta produktaĵo svisa.</p> <p>Tamen la oficiala lingvo laŭ la svisa konstitucio estas ne la alemana dialekto, sed la germana, la norma germana. Aliflanke la alemana estas aŭtenta svisa lingvo, la germana ne.</p> <div style="text-align:right">Mireille G<span class="mm">ROSJEAN</span></div>    
