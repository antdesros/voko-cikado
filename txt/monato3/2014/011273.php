      <h3>Scienco</h3>
      <h3 class="tem">MUZIKO</h3>
      <h2>Aŭskultu simfonion, aŭskultu la universon</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>La informoj, kiujn ni de tempo al tempo ricevas rilate al astronomio kaj al kosmaj
      esploroj, estas fojfoje kapturnaj pro la grandaj distancoj, kiujn la menciitaj fakoj
      traktas. La kreemo de la sciencistoj, kiuj okupiĝas pri nia universo, povas esti tamen
      same (se ne pli) kapturna!
   </b>
</p> 
<p>Ĉi-foje esploristo produktis ion vere nekutiman, eksterordinaran. Temas pri la itala
   fizikisto kaj – en la malmulta libertempo – muzikisto Domenico Vicinanza [doméniko viĉinanca], kiu laboras ĉe la esplora centro
   
   <i>Géant</i> de Cambridge (Britio) kaj kies familinomo ironie signifas „proksimeco”-n en lia gepatra lingvo, la itala.
</p> 
<h3>Sondiloj</h3> 
<p>La 37-jaraĝa sciencisto konceptis la ideon produkti muzikon surbaze de la realaj donitaĵoj,
   kiuj alvenas al nia planedo el plej malproksimaj meandroj de la kosmo. Kiel? Danke
   al la perado de la sondiloj 
   <i>Voyager I</i> kaj 
   <i>Voyager II</i>. Ĉi-lastaj, lanĉitaj en la kosmon kun esploraj celoj en 1977, antaŭ kvardeko da jaroj,
   jam „emeritiĝis” (se tiel diri), sed daŭrigas sian kuron en la kosmo miliardojn da kilometroj for
   de la tero. Por ŝparigi al ili energion, oni malaktivigis la plimulton de iliaj instrumentoj,
   kiel fotoaparatoj kaj kameraoj. La troviloj de kosmaj korpuskloj male plufunkcias
   kaj pere de plutoniaj baterioj ili eltenos dum ankoraŭ 20 jaroj.
</p> 
<p>Spite al la tempo pasinta kaj al la distanco trairita, ili do ne ĉesis sendadi al
   la tero la signalojn plurspecajn kaptatajn. Estas rimarkinde, ke la sondiloj ne estas
   proksimaj unu al la alia: la unua jam eliris el la suna sistemo (ĝi nun distancas
   de la tero je 18 miliardoj da kilometroj), tiel ke ĝiaj donitaĵoj, kvankam vojaĝante
   je la luma rapido, bezonas proksimume 17 horojn por atingi la teron. La dua troviĝas
   male ankoraŭ ene de la sunsistemo, je distanco de 22 milionoj da kilometroj de sia
   ĝemelo.
</p> 
<h3>Dueto</h3> 
<p>D-ro „Proksimeco” do kolektis la „malproksimecajn” signalojn samtempe senditajn de la du sondiloj laŭlonge de sia vojaĝo. Li poste konvertis
   ĉiun el ili en apartan sonan signalon. Kompreneble la sono mem ne trairis materiale
   la kosmon, sed per komputila programo la menciita fakulo sukcesis transskribi tiujn
   samajn signalojn, sen modifi aŭ aldoni ion, kiel la itala ŝatanto de fiziko kaj muziko
   fiere rimarkigas. Li fine transformis la tre longan serion da kolektitaj sonoj en
   veran muzik-komponaĵon por piano kaj arĉmuzikiloj. Temas pri bela dueto, en kiu eblas
   distingi du malsamajn melodiojn, kiuj tre bele kongruas, danke al tio, ke la signaloj
   estis senditaj de la sondiloj en la sama momento.
</p> 
<p>Estas neeble ne trovi mirindan harmonion en tiu alloga muziko, ekskluzive por ni ludita
   de planedoj, steloj kaj stelfiguroj, kiuj ŝajne tiom silente brilas en la firmamento.
   Por aŭskulti ilian voĉon, la dolĉan voĉon de nia universo, oni vizitu la retejon 
   <a href="http://soundcloud.com/geant-sounds/sonification-of-voyager-1">soundcloud.com/geant-sounds/sonification-of-voyager-1</a>. Alia komponaĵo (<i>Voyager I eliranta el la suna sistemo</i>) aŭskulteblas ĉi tie: 
   <a href="http://soundcloud.com/geant-sounds/voyager1-exiting-the-solar">soundcloud.com/geant-sounds/voyager1-exiting-the-solar</a>. En la retejo 
   <a href="http://soundcloud.com/geant-sounds">soundcloud.com/geant-sounds</a> haveblas aldonaj tiaj komponaĵoj, surbaze de plurspecaj sciencaj donitaĵoj.
</p> 
