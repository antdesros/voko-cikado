      <h3>Medio</h3>
      <h3 class="tem">ELEKTRONIKO</h3>
      <h2>Pli kaj pli da rubo al la tria mondo</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La konvencio de Bazelo, kiu ekestis en 1989 kaj ekvalidis en 1992, havis kiel ĉefan celon eviti, ke la landoj de la tria mondo iĝu la amasrubejo de toksaj forĵetaĵoj de la riĉa nordo. Bona intenco certe, sed la praktiko montras, ke intencoj ne ĉiam plene realiĝas.</b></p> <p>Laŭ la usona <i>Basel Action Network</i> (<a href="http://www.ban.org">www.ban.org</a>) en la haveno de la niĝeria ĉefurbo Lagoso ĉiumonate alvenas 500 konteneroj plenaj je forĵetitaj elektronikaj aparatoj, ĉefe komputiloj kaj malnovaj televidiloj. En 2007 la usonanoj aĉetis probable 30 milionojn da novaj televidiloj kaj forĵetis preskaŭ same multe da malnovaj aparatoj. Ili grandparte iris al Niĝerio, Barato kaj Ĉinio por „recikligo”, sed plej ofte recikligo tute ne okazas. La aparatoj venas sur grandajn rubmontojn. Klasika televidaparato povas enhavi ĝis kvar kilogramojn da plumbo, kiu iom post iom enpenetras en la teron. Oni taksas, ke ĉi-momente ekzistas unu miliardo da privataj komputiloj en la mondo, ĉefe en la industriaj landoj, kaj ke en 2008 oni forĵetos 180 milionojn el ili.</p>
<p><a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2511953"><img src="http://u1.ipernity.com/7/19/53/2511953.e82369ca.500.jpg" width="500" height="375" alt="Elektronika rubo" border="0"/></a></p> <p>Kvankam Usono subskribis la konvencion de Bazelo, ĝi ne ratifis ĝin. Ne ekzistas tie federacia leĝaro, kiu regulas la recikligon de elektronika rubo. Pro tio la organizaĵo <i>Electronics Takeback</i> (Reprenu elektronikaĵojn) lanĉis kampanjon, kiu celas stimuli la produktantojn libervole repreni la malnovajn elektronikajn aparatojn okaze de aĉeto de novaj. Laŭ niaj informoj nur la giganto <i>Sony</i> ĝis nun pretas transpreni malnovajn televidilojn. Cetere, estas ankaŭ <i>Sony Ericsson</i> kaj <i>Sony</i>, kiuj aperas pinte en la listo de „verdaj” entreprenoj laŭ listo de <i>Greenpeace</i> (<a href="http://www.greenpeace.org/international/campaigns/toxics/electronics/how-the-companies-line-up">www.greenpeace.org/international/campaigns/toxics/electronics/how-the-companies-line-up</a>). Tute fine en la ruĝa parto de la listo aperas <i>Nintendo</i>, <i>Microsoft</i>, <i>Siemens</i> kaj <i>Philips</i>.</p> <p>En Eŭropa Unio (EU) la repreno de difektitaj aŭ malnovaj elektronikaj aparatoj iĝis deviga ekde la 13a de aŭgusto 2005 pro la regularo 2002/96/EC pri elektra kaj elektronika rubo. Produktantoj kaj distribuantoj de elektraj kaj elektronikaj aparatoj devas proprakoste repreni ilin post finuzado kaj informi siajn klientojn pri tio. Tamen ankaŭ tie ĉi la nobla celo kaj la nigra realo diverĝas. La firmaoj ne rajtas sendi tiun rubon al la tria mondo, sed neniu leĝo malpermesas al ili malmultekoste vendi „brokantajn” aparatojn al Afriko kaj Azio por dua uzo. Cetere, la plimulto de la aparatoj ne estas reportita al la vendisto post uzado. Poŝtelefonoj, MP3-ludiloj kaj aliaj etaj aparatoj plej ofte estas ĵetitaj en rubsakon kaj atingas la rubfornon.</p> <p>Kvin elcentoj de la eŭropa rubo konsistas nun el elektronika kaj elektra rubo, same multe do kiel plasta pakmaterialo, sed multe pli danĝera ol pakmaterialo, kaj tiu elcentaĵo ĉiujare kreskadas. Sed ne nur en Eŭropo kaj Usono ekzistas tiu tendenco. En la venontaj du jaroj aldoniĝos 200 milionoj da komputiloj en la ĉinaj hejmoj, kaj Barato antaŭvidas en tiu periodo 80 milionojn da novaj uzantoj. Se oni konsideras la fakton, ke poŝtelefono kaj tekokomputilo havas mezuman vivodaŭron de 3-4 jaroj, la problemo ĉiujare iĝos pli kaj pli giganta.</p> <div style="text-align:right">Paŭl P<span class="mm">EERAERTS</span></div> 
