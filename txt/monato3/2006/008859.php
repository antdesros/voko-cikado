      <h2>Federacia kancelierino</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kun multe da intereso mi legis la enkondukon de la ĉefredaktoro Stefan Maul <i>Kancelierino</i> (M<span class="mm">ONATO</span> 2006/1 p. 5).</p> <p>Mi estas franco kaj ne parolas nek skribas Esperanton tre bone kaj flue (iu eterna komencanto), sed mi povis tuj prononci la vorton „kancelierino” sen ĝeno; mi tute ne trovis la vorton langorompa kaj prononcis ĝin sen ajna problemo. Eĉ se la unuan fojon kelkaj personoj devus diri „kan-ce-li-er-ino”, la duan fojon ili certe prononcos ĝin ĝuste sen iu ajn penso pri langorompo.</p> <p>Uzi la vorton „kancelierino” estas tre bone, kaj mi subtenas ankaŭ titolojn „d-rino” kaj „fizikistino”, eĉ sciante, ke esperantistoj longe batalis por kaj kontraŭ la uzo de la ina sufikso en la titoloj. Eĉ en la malnova Francio de la malnova kontinento oni diras nun „<i>la procureure</i>” [la prokurér] (la prokurorino) kaj en Kebekio oni diras ekzemple: <i>Julie Carrier, professeure-chercheuse adjointe au Département de psychologie</i> (Julie Carrier, helpa profesorino-esploristino en la fakultato pri psikologio).</p> <p>La karesnomon de s-ino Merkel francaj ĵurnalistoj prononcas „anĝi” aŭ „ajnĝi”, sed ne „ejnĝi”.</p> <p>Stefan Maul skribas: „Cetere, anĝeloj laŭdifine ne havas sekson, do tutcerte ni ne devas diri Anĝelino Merkel.” Kial ne? Tute ne temas pri sekso, sed pri traduko de nomo kun itallingva deveno.</p> <div style="text-align:right">Guy W<span class="mm">ERLINGS</span></div> <div style="text-align:right"><i>Francio</i></div> 
