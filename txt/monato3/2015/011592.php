      <h3>Medio</h3>
      <h3 class="tem">SUD-ORIENTA AZIO</h3>
      <h2>Kiam finiĝos la fumego?</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <b>Antaŭ nelonge aperis tre densa fumo en la ĉielo de la insulo Sumatro. Mi nomas ĝin
      „fumego”. Ĝi ja ne estas normala fumo. Ĝi kovras la tutan areon de kelkaj partoj de okcidenta
      Indonezio, speciale en la regiono Ĝambio kaj Riaŭo, Sumatro. Ĝi eĉ jam atingis Malajzion
      kaj Singapuron, kaj kelkajn partojn de Brunejo, Tajlando kaj Filipinoj.
   </b>
</p> 
<p>Tiu ĉi internacia katastrofo ekestis meze de aŭgusto kaj malpliboniĝas. La ĉefa kaŭzo
   estas arbarbrulego sur la insuloj Sumatro kaj Borneo. Tiu ĉi brulego okazis ne pro
   naturaj kialoj. Oni kredas, ke fiuloj faris tion, celante praktiki la metodon „hakado kaj bruligado” por krei pliajn kampojn. Malfeliĉe, pro la seka sezono ĉe la ekvatoro, oni ne povas
   regi la fajron kaj ĝi pligrandiĝis, kaŭzante brulegon. Cetere la fenomeno El-Ninjo
   kaŭzis, ke ne pluvis dum tiu ĉi periodo.
</p> 
<p>La fumego jam estigis sanproblemojn por la loĝantaro en Ĝambio kaj Riaŭo. Laŭ la indonezia
   sistemo de poluada indico, la nombroj de 0 ĝis 50 signifas bonan, sanan aeron kaj
   super 300 signifas danĝeran aeron venenan. En la regiono Riaŭo, la indico atingis
   984 kaj en centra Kalimantano 1986. Pro pluraj spiraj kaj okulaj malsanoj suferas
   la loĝantaro. Krome oni ne povas labori aŭ studi, ĉar la aero estas tiel danĝera,
   ke neniu povas iri eksteren.
</p> 
<h3>Ekonomio</h3> 
<p>La malavantaĝoj ne nur rilatas al la sano. Ankaŭ la ekonomio en Sumatro kaj Kalimantano
   ne funkcias. Multaj flugoj estas nuligitaj pro limigita vida distanco. Turistoj mankas,
   ĉar ili ne povas atingi turismejon, kaj eĉ se tio eblus, ili povus nenion vidi tie.
   Resume, la du plej gravaj malutiloj koncernas la sanon kaj la ekonomion.
</p> 
<h3>Internacia katastrofo</h3> 
<p>Krom Indonezio ankaŭ la najbaraj landoj spertas la malavantaĝojn. La poluada indico
   en Brunejo atingis 68 kaj iom influas la sanon de la popolo. En Malajzio la polua
   indico eĉ atingis 252. Tial tie la lernejoj ne funkcias por gardi la sanon de la lernantoj,
   kaj kelkaj flugoj estis nuligitaj. Ankaŭ en Singapuro, kies polua indico atingis 341,
   lernejoj estis fermitaj kaj kelkaj eventoj en la monda ĉampionado pri naĝado estis
   nuligitaj pro la malbonaj vetero kaj aero.
</p> 
<p>Simile la ĉielo de Tajlando estas kovrita de fumo pro tiu arbarbrulego en Sumatro
   kaj Borneo. Cetere, la filipina registaro supozas, ke la fumnebulo, kiu atingis Ĉebuon,
   devenas de Borneo, ĉar ĝi ne malaperas dum kelkaj tagoj. En Filipinoj la fumego kaŭzas
   nuligon de kelkaj flugoj. Do tiu ĉi katastrofo suferigis ses landojn: tre granda problemo
   en sud-orienta Azio.
</p> 
<h3>Kion faru la registaroj?</h3> 
<p>Multaj homoj kritikas la indonezian registaron, ĉar ĝi ne sukcesas venigi la suspektatojn
   pri la bruligoj antaŭ la tribunalon. La kritiko aperis, ĉar similaj problemoj ripetiĝas
   ĉiujare. Oni opinias, ke neniu plu bruligos arbarojn, se la registaro povas juĝi kaj
   puni la suspektatojn. Dume la indonezia armeo faras ĉion eblan por ĉesigi la brulegon,
   kaj prezidanto Joko Widodo promesas, ke en tri jaroj la registaro povos ĉion solvi.
   Cetere Malajzio kaj Singapuro jam pretas helpi Indonezion.
</p> 
<p>Kvankam la registaroj provas ĉion, la popolo jam multe suferas, speciale infanoj kaj
   maljunuloj, kaj multaj el ili ne plu povas atendi la freŝan aeron. Indonezio estas
   fama pro la freŝa aero en kelkaj regionoj, pro la ekzisto de multaj arbaroj. Sed kion
   fari, kiam la arbaroj malrapide malaperadas pro brulego? La popolo suferas, la registaro
   ĉion provas. Sed kiam la fumego finiĝos? Ŝajnas, ke ni povas nur atendi.
</p> 
