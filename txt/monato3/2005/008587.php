      <h3>Ekonomio</h3>
      <h3 class="tem">AVIADO</h3>
      <h2>Ĉinaj aviadaj kompanioj grandskale aĉetas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Komence de 2005 en Vaŝingtono pluraj ĉinaj kompanioj pri civila aviado mendis ĉe la usona kompanio <i>Boeing</i> 60 grandegajn pasaĝeraviadilojn de la tipo <i>Boeing 787</i>, je valoro de 7,2 miliardoj da usonaj dolaroj. En la historio de la ĉina civila aviado tio estas la plej granda aĉeto laŭ kvanto kaj prezo. Samtempe la Suda Aviadkompanio de Ĉinio subskribis kontrakton kun la eŭropa kompanio <i>Airbus</i> por mendo de kvin supergrandaj <i>Airbus A380</i>.</b></p> <h3><i>Boeing 787</i>: liverota ekde 2008</h3> <p><i>Boeing</i> ĵus komencis fabriki tiatipajn pasaĝeraviadilojn, kaj Ĉinio iĝis unu el la plej gravaj aĉetantoj. Ĉe la subskriba ceremonio la estro de la kompanio ĝoje diris: „Certe ni vidos, ke en 2008 <i>Boeing 787</i> kun la ĉina standardo, portos gastojn el la tuta mondo, por en Pekino spekti la Olimpiajn Ludojn. Ĉinio postulis, ke en 2008 la unua mendita aviadilo nepre alflugu Ĉinion, kaj tiun postulon ni certe povos plenumi.” La reprezentanto de la ĉinaj aviadaj kompanioj aldonis: „Indas multon elspezi, ĉar ni malhavas avangardajn aviadilojn. Importado bone koincidas kun nia urĝa bezono. Krome, ni esperas pligrandigi internaciajn interŝanĝojn, utiligante pli oportunajn aerajn trafikilojn, kiel <i>Boeing 787</i>.”</p> <h3>Vasta perspektivo en aviada kunlaboro</h3> <p>Kvankam dum la lastaj du-tri jaroj la ĉina civila aviado ĉiam havas progresantan tendencon, en 2004 okazis almenaŭ tri gravaj aviadilaj akcidentoj. Aĉeti pasaĝeraviadilojn de evoluintaj landoj stimulos kunlaboron. La Internacia Organizaĵo pri Civila Aviado (<a href="http://www.icao.org">www.icao.org</a>) prognozis, ke en la venontaj 20 jaroj Ĉinio iĝos la dua merkato de pasaĝeraviadiloj, post Usono. La pogranda aĉeto de <i>Boeing 787</i> spegulas vastan perspektivon en aviada kunlaboro inter Ĉinio kaj Usono. Specialistoj asertas, ke se la du landoj faros kunan strebon, ili multe profitos el la aviada komercado.</p> <h3>Olimpiaj Ludoj 2008</h3> <p>En Parizo la Suda Aviadkompanio de Ĉinio mendis ĉe la eŭropa <i>Airbus</i>-kompanio kvin pasaĝeraviadilojn de la tipo A380. Tiatipa aviadilo povas transporti 555 pasaĝerojn kaj kapablas atingi flugdistancon de 15 000 kilometroj <i>(vidu ankaŭ la artikolon en M<span class="mm">ONATO</span> 2005/4, paĝo 14)</i>. Pro la ĉina mendo la nombro de aĉetantoj de A380 leviĝis ĝis 15 kaj la suma vendokvanto ĝis 154. <i>Airbus</i>-respondeculo deklaris al la gazetara rondo, ke por lia kompanio Ĉinio estas grava strategia merkato. La unuafoja vendo de <i>A380</i> estas granda sukceso por lia kompanio. La ĉina kompanio klarigis sian aĉetcelon: tiuj unuaklasaj aviadiloj liveru al diverslandaj pasaĝeroj plej bonan servadon, dum okazos en Pekino la Olimpiaj Ludoj de 2008 kaj en Ŝanhajo la Monda Ekspozicio de 2010.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div>   
