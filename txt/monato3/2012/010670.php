      <h3>Arto</h3>
      <h3 class="tem">EKSPOZICIOJ</h3>
      <h2>Arto kreas arton</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La „Internacia foiro por klasika moderneco kaj nuntempa arto”, mallonge nomata „art Karlsruhe” jam la naŭan fojon altiris dum kvar tagoj de marto pli ol 220 galeriistojn kaj preskaŭ 50 000 vizitantojn al ekspoziciejo proksime al la urbo. Epitetoj kiel „Paradizo por artaĉetantoj” aŭ „Bonfarta foiro” ne troigas.</b></p> <p>Kompare kun la elitemaj artfoiroj en Kolonjo, Bazelo, Venecio, la „art Karlsruhe” ja celas altiri kapablajn artinvestantojn, sed samtempe kvalifikas sin mem „familieca”, do kompleza flanke de la ekspoziciantoj kaj interesa ankaŭ por artemaj klientoj ne aparte bonhavaj.</p> <h3>Aktualaj kreaĵoj</h3> <p>Ankaŭ ĉi-jare la kvar haloj ne estis tro ampleksaj por unutaga vizito. Ĉio estis alirebla kaj ne postlasis la impreson de troa hastado kaj neviditaj verkaroj. Prezentiĝis precipe aktualaj kreaĵoj de la juna kaj mezaĝa generacioj el Germanio kaj la najbaraj Francio kaj Svislando, sed ankaŭ artistoj orient-aziaj, inter ili multaj virinoj.</p> <p>Aparte bonvenan oni konstatis la ĉeeston de regionaj palatinataj, badenaj kaj alzacaj artistoj, el inter kiuj intertempe kelkaj akiris sufiĉe da transregiona renomo. Memkompreneble la propono estis plene malkohera. Ĉia stilo, ĉia tekniko estis videblaj. Unukoloraj farbŝildoj, detalecaj klasikecaj pentraĵoj, simplaj serioj de brikoj el ruinoj, komplikaj mekanikaj aparatoj, fotaĵoj, skulptaĵoj ĉiamaterialaj, pormomentaj kreaĵoj kaj al la eterneco strebantaj ŝtonaj kaj metalaj instalaĵoj vekis atenton, admiron, sed ankaŭ kapskuadon kaj fojfoje silentan indigniĝon.</p> <h3>Kontribui</h3> <p>Aparta plezuro estis la kelkfoja propono al la vizitantoj, kontribui poze, penike, fote aŭ krajone al kreata artaĵo, kion la M<span class="mm">ONATO</span>-raportistoj volonte akceptis (vidu la apudajn fotojn).</p> <p>En la pli klasikaj salonoj la kapoj moviĝis direkte al la prezoŝildoj. Se prezoj ne estis indikitaj kaj la ŝildoj invitis al la konsultado de la salonestro, la konkludo pri preza surprizo ne estis senkiala. Tamen, astronomiaj sumoj kiel antaŭjare por bildoj de Gerhard Richter mankis. Kompense oni regalis abunde per postrestintaj stokoj de Fritz Winter kaj Serge Poliakoff. Ankaŭ por bildoj de la amatora pentranto Hermann Hesse, multe pli fama kiel lingva aŭtoro, oni postulis ĝis 50 000 eŭrojn. Sed ĉiu, kiu volis aĉeti impresan objekton, trovis ion konvenan por la propra monujo. Kaj tio certe same estos en la venontjara jubilea ekspozicio „art Karlsruhe 2013” inter la 7a kaj 10a de marto.</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div> 
