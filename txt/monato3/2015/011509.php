      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Esperantistoj  ĉu kat-enitaj?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kiam vi legos ĉi tiujn vortojn – se entute vi legas miajn vidpunktojn – vi ĝuos, almenaŭ se en la norda duonsfero, tagojn agrable varmajn, somere ripozigajn.
   Jen la sezono por sidi en la ĝardeno ĝis la naŭa, deka horo de la vespero kaj trinki
   vinon, babili, diskuti ... kaj eventuale cerbumi pri aferoj pli intelektaj, pli filozofiaj,
   ol kutimaj laborejaj taskoj kaj devoj.
</p> 
<p>Do jen demando por vin okupi dum longaj, lumaj vesperaj horoj. Ĉu enestas en la esperantista
   karaktero trajtoj pli katecaj, katemaj, ol aliflanke hundecaj, hundemaj? Alivorte:
   ĉu esperantistoj pli inklinas, eĉ pli similas, al katoj ol al hundoj?
</p> 
<p>Leviĝis la demando antaŭ kelkaj semajnoj post hazarda interŝanĝo de kat-fotoj kun
   britaj esperantistoj. „Ĉu”, ni demandis nin, „ekzistas pli da esperantistoj en nia lando kun katoj ol kun hundoj? Se jes, kial?”
</p> 
<p>Malfacilas tuj respondi la demandojn. Pro tio mi nun ilin prezentas, esperante, ke
   legantoj de M<span class="mm">ONATO</span> dum la somero cerbumos kaj, post konvena tempo por pripensi, dum aŭtuno leteros al
   la redakcio kun mature analizitaj kaj argumentitaj opinioj.
</p> 
<p>Permesu, tamen, ke mi enkonduku la debaton per kelkaj komencaj komentoj. Unue nepras
   diri, ke mi mem estas katsklavo, posedata de tri miaŭantaj mastroj (verdire unu mastro
   kaj du mastrinoj). Do nek neŭtrale, nek objektive mi proksimiĝas al la afero.
</p> 
<p>Tamen restas forta impreso, ke en mia lando pli multnombras katemaj ol hundemaj adeptoj
   de la verda lingvo. Efektive ensaltas mian menson nur unu brita esperanta hundo-hejmo.
   Tamen eble mi min trompas: povas esti, ke pro mia katamo mi nature asociiĝas kun aliaj
   esperantaj katemuloj.
</p> 
<p>Aliflanke ne trompas faktoj, kiuj, pravigante miajn impresojn, parolas pri si mem.
   Ekzemple, unu el la kongrese plej videblaj fakgrupoj estas tiu de 
   <i>Rondo Kato</i>, kies anoj kunvenas por diskuti ... nu, evidente, ne rondojn, sed katojn. Ĉu ekzistas
   ekvivalenta 
   <i>Rondo Hundo</i>? Mi kredas, ke ne.
</p> 
<p>Ankaŭ literature ne eblas refuti la faktojn. Ĉu hundoj havas sian teatraĵeton, sian
   biografion? Ankoraŭfoje, mi kredas, ke ne. Sed katoj, jes. En 1959 aperis en la kolekto
   
   <i>Virino ĉe la landlimo</i> „fantazia drameto por infanoj” titolita „La fremduloj”. En ĝi rolas abisena, siama, persa, manksa kaj aliaj katoj.
</p> 
<p>Sekvis en 1964 volumeto 
   <i>Dekdu piedetoj</i>, kiu komenciĝas tiel: „Min havas kato. Tiu ĉi ne estas gramatika eraro. Mia kato estas aŭtokato kaj aristokato.
   Tiuj ĉi ne estas eraroj de ortografio”.
</p> 
<p>Verkis kaj la teatraĵeton, kaj la biografion Marjorie Boulton, parenteze prezidanto
   de 
   <i>Rondo Kato</i>. Certe neniu alia esperanta verkisto tiom katumis, kiom Boulton – eĉ ne Raymond Schwartz, kiu kabaredis ĉe la 
   <i>Verda Kato</i> (notu: ne la 
   <i>Verda Hundo</i>) kaj kiu postlasis sian 
   <i>Verdkatan Testamenton</i>.
</p> 
<p>Kontribuis al nia literaturo almenaŭ unu alia „kato”, la tradukistino Kate Hall, konata kiel Katelina Halo. Ĉu Hunde Hall? Malgraŭ bela
   aliteracio, ne.
</p> 
<p>Sed kial? Kial disponas katoj pri siaj esperantaj ĉampionoj, dum ŝajne hundoj ne?
   Ĉu enestas io en la psikologio de esperantistoj, kio kongruas kun tiu de katoj? Katoj
   impresas sendependaj, krom dum nutrohoro, sed samtempe tre lojalas, tre fidelas. Katoj
   sekretemas, studemas, sed ankaŭ komunikas, agemas. Ĉu tiaj kombinoj karakterizas ankaŭ
   esperantistojn?
</p> 
<p>Ne volas mi respondi kaj tiel forpreni de vi, kara leganto, la plezuron rondirigi
   en via menso gravajn ekzistencialismajn demandojn tiajn, dum vi bierumas, vinumas
   en via ĝardeno.
</p> 
<p>Do agrablan cerbumadon pri nia kata enketo, kaj entute agrablan someron, deziras al
   vi M<span class="mm">ONATO</span> ... kaj ne forgesu dividi kun ni viajn proprajn vidpunktojn pri la kateco de esperantistoj.
</p> 
