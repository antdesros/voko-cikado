      <h3>Moderna vivo</h3>
      <h3 class="tem">BALKANO</h3>
      <h2>Inter najbaroj malpli da baroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pliboniĝas rilatoj inter Serbio kaj ĝia najbaro Albanio. Ekzemple, en albana universitato – en Skadar – malfermiĝos fako pri la serba lingvo. Fiksita estas tago en februaro por honori en Albanio la serban lingvon.</p> <p>Antaŭe mankis al serboj kaj montenegranoj en Albanio rajtoj, ekzemple pri lingvo, kredo, lernejoj kaj nomoj. Nova registaro kaj alproksimiĝo de Albanio al Eŭropa Unio nun redonas al serboj kaj montenegranoj tiajn rajtojn. Ekzemple, multaj serboj kaj montenegranoj petas siajn antaŭajn nomojn, malpermesatajn en Albanio ekde 1975. Memorindas, cetere, ke en 1967 Albanio estis deklarita – unue kaj unike en la mondo – ateista lando. Tiam estis detruitaj multaj preĝejoj.</p> <p>Malgraŭ pli bonaj rilatoj, tamen, restas malsamaj opinioj en la najbaraj landoj pri la sendependeco de Kosovo kaj Metohio.</p> <div style="text-align:right">Dimitrije J<span class="mm">ANIČIĆ</span>/<span class="mm">PG</span></div> 
