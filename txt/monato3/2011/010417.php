      <h3>Politiko</h3>
      <h3 class="tem">NIĜERIO</h3>
      <h2>Dividoj venkitaj  almenaŭ parte</h2>
      <!-- komenco de la enhavo -->

       

       <p>Fine de majo Niĝerio inaŭguris novajn prezidanton, federacian parlamenton kaj guberniestrojn en 26 el la 36 ŝtatoj, kie okazis balotoj.</p> <p>La balotoj, en aprilo, estis interesaj. Laŭ la konstitucio, la povo devas alterni inter sudo (99 % kristana) kaj nordo (99 % islama). La lasta prezidanto, Shehu Muea YarAdua, estis nordulo, kiu laŭplane restus prezidanto eble ĝis 2015. Tiam la prezidanteco revenus al la sudo.</p> <p>Tamen mortis YarAdua en majo 2010 kaj la povon transprenis d-ro Goodluck Jonathan. Tiu devenas de la niĝerdelta regiono de Bayelsa en la sudo de Niĝerio. Lia ŝtato estas unu el dek, de kie venas la nafto, kiu kontribuas 90 % al la ekonomia bonfarto de Niĝerio.</p> <h3>Malkonsentis</h3> <p>Do nordaj politikistoj, kaj ankaŭ nepolitikistoj, postulis, ke nordulo aŭ islamano sekvu la mortintan YarAdua. Aliaj niĝerianoj malkonsentis. Ili argumentis, ke Niĝerio jam sufiĉe maturas, por ke iu ajn, sendepende de etno aŭ religio, aspiru al federaciaj postenoj, ekzemple prezidanteco. Cetere liberaluloj subtenis Jonathan, bone edukitan homon, kiu jam iniciatis pozitivajn reformojn kaj enpostenigis je federacia nivelo intelektulojn kaj aliajn kapablulojn.</p> <p>Tamen sin prezentis diversaj kandidatoj el la nordo, inkluzive de eksaj armeaj ŝtatestroj. Fine elektis la norduloj, kiel kompromisan kandidaton, Generalon Muhammadu Buhari, ŝtatestron inter 1983-85. Sed en aprilo, malgraŭ bomboj en diversaj ŝtatoj, kiuj mortigis centojn da niĝerianoj, la popolo turnis la dorson al etnaj kaj religiaj dividoj kaj kvereloj kaj elektis kiel prezidanton Jonathan.</p> <h3>Atakis</h3> <p>Islamaj fundamentistoj apenaŭ kredis la novaĵon. Jonathan akiris 24 milionojn da voĉoj, Buhari nur 12 milionojn. Ĝihadistoj do atakis kristanojn, loĝantaj en norda Niĝerio. Mortis miloj, kiam domoj kaj preĝejoj de kristanaj subtenantoj de Jonathan estis bruligitaj.</p> <p>Aparte ŝokis la mortigo de dek t.n. junularservantoj. En Niĝerio, post universitata eduko, oni devas servi la ŝtaton dum unu jaro en alia regiono ol tiu, de kiu oni devenas. Sudanoj servas en la nordo, norduloj en la sudo. Sed unuafoje en la historio de la servo estas mortigitaj junuloj, rigardataj kiel posedaĵoj de la ŝtato kaj do iel sanktaj.</p> <h3>Fuĝis</h3> <p>La junulojn, kiuj travivis la unuan ĝihadistan atakon, kaj kiuj fuĝis al policejo, oni tie mortigis, kune kun policanoj. La internacia komunumo kondamnis la atakon. Tamen, malgraŭ sangaj makuloj tiaj, oni taksas la baloton sukcesa kaj senfraŭda.</p> <p>Venis gratuloj el ĉirkaŭ la mondo, interalie el Usono, Eŭropa Unio kaj aliaj afrikaj landoj. Ili bonvenigis stabilan kaj demokratian Niĝerion, la plej homnombran landon en Afriko, kun 130 milionoj da loĝantoj, kaj ties plej gravan nafto-produktanton.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> 
