      <h3>Moderna vivo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Ŝanĝiĝanta feliĉo-festo</h2>
      <!-- komenco de la enhavo -->

       

       <p>La printempa festo estas la plej grava kaj movoplena festo de la ĉina popolo. Ĝi estas novjara festo laŭ la luna kalendaro kaj okazas la unuan tagon de la unua monato de tiu ĉi kalendaro. En 2007 la printempa festo okazis la 18an de februaro en la jaro de la porko, laŭ la ĉina kulturo.</p> <p>La printempa festo fontas el la kultado de dioj kaj prapatroj antaŭ ĉirkaŭ 3000 jaroj. Ĝi anoncas la alvenon de printempo, la refreŝiga periodo, kiam komenciĝas nova sezono de semado kaj rikoltado. De la 23a ĝis la 30a de la lasta monato de la luna kalendaro okazas „tagoj por bonvenigi printempon” aŭ „tagoj por purigado”. Kutime ĉinoj purigas siajn hejmojn antaŭ la printempa festo.</p> <p>Dum 10 tagoj antaŭ la festo familioj aĉetadas, ekzemple kortobirdon, fiŝaĵon, viandon, teon, drinkaĵojn, spicaĵojn, nuksojn, sukeraĵojn, fruktojn ktp. Krome ili preparas donacojn: novajn vestaĵojn por la infanoj. Cetere, dum tiu ĉi periodo ili gluas novjaran versparon sur la pordon de la loĝejo por esprimi bondezirojn. La versparo estas skribita sur ruĝa papero. Apudporde pendas granda ruĝa lanterno, kaj sur la pordklapo legeblas papero kun ideogramo signifanta „feliĉo”.</p> <h3>Besto</h3> <p>Laŭmite aperas jarfine minacanta besto. Ĝi estu forpelita per petardoj: eksplodigi fajraĵojn fariĝis do kutima printempfesta amuzaĵo. Sed temas pri tempo ne nur por petardoj, sed ankaŭ por familiaj renkontiĝoj. Dum la tiel nomata jarfina vespero familianoj kuniĝas por ĝui bongustaĵojn: tiuj, kiuj laboras for de la hejmo, revenu antaŭ la festo.</p> <p>Ĉe la unua kokeriko kaj la sonado de sonorilo, familianoj eksplodigas siajn petardojn. Ĉiu surmetas novajn vestaĵojn, salutas la maljunulojn kaj donacas monon al la infanoj. La unuan novjaran tagon oni kunmanĝas; la duan vizitas amikojn kaj parencojn. Surstrate oni dancas kaj paradas.</p> <h3>„Riĉiĝon!”</h3> <p>Tamen la festo ŝanĝiĝas. Dum maljunuloj kaj mezaĝuloj ankoraŭ flegas tradician feston, junuloj volas kartludi, interretumi aŭ simple malstreĉiĝi kaj eskapi la premojn de la ĉiutaga vivo. Kelkaj junuloj eĉ ne hejmeniras por festi kun siaj gepatroj. Anstataŭ persone saluti per tradicia „Riĉiĝon!” aŭ „Gratulon!”, ili sendas telefonajn aŭ elektronikajn mesaĝojn. Pli da familioj ferias dum la festperiodo, elektante hotelojn anstataŭ la propra hejmo.</p> <p>Laŭ sondaĵo, 81 % de ĉinoj opinias, ke la tradicia printempa festo estu transdonita de generacio al generacio. Preskaŭ samnombre ili kredas, ke kelktaga printempa feriado utilas al familieco kaj socia harmonio. Tamen jam velkas la tradicioj. Malpli oni atentas manĝaĵojn kaj novajn vestaĵojn. Pli oni atentas homan kontakton. Kaj estas aliaj, kiuj vidas en la printempa festo manieron akceli konsumadon kaj stimuli ekonomion.</p> <div style="text-align:right">C<span class="mm">HEN</span> Lina/pg</div> 
