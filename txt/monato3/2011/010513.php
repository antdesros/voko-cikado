      <h2>Alloga finalo</h2>
      <!-- komenco de la enhavo -->

       

        <p>Serbio estas la duan fojon (post 2001) eŭropa ĉampiono pri retpilko por viroj. Ĝi venkis Italion en la finalo je 3:1 ludoj (17:25, 25:20, 25:23, 26:24) en Vieno. En la ĉampionado, kiu okazis en Aŭstrio kaj Ĉeĥio, partoprenis teamoj de 16 landoj.</p> <p>En la unua ludo de la finalo la italaj retpilkistoj dominis. Per bonega teama koncentriĝo ili ne permesis al la serboj distingiĝi. Ĉi-lastaj atakantoj realigis nur naŭ poentojn el 23 atakoj.</p> <p>En la konvinke gajnita dua ludo fare de serbaj retpilkistoj estis multe pli da premo en ilia ludado. Pro tio malaperis ĉe la italoj la komence demonstrita precizeco. Ivan Miljković, la kapitano de la serba teamo, traboris nun multe pli bone la italan blokadon.</p> <h3>Dubinda decido</h3> <p>La antaŭdecido okazis en la tria ludo. Post kiam la italoj egaligis 23-23 pro la elstara gvido de ilia kapitano Cristian Savani, dubinda decido de la ĉefjuĝisto donis eblon al la serboj gajni la ludon.</p> <p>Tion ili faris helpe de Miloš Terzić, kiu rekte per serviro markis la lastan poenton. En la kvara ludo la itala teamo estis kelkan tempon fronte laŭ poentoj, sed fine denove Terzić per serviro certigis la venkon por Serbio.</p> <h3>Mondpokalo</h3> <p>Pro tiu granda sukceso la serboj fariĝis ĉi-momente la plej bona landa teamo kaj reprezentos kune kun la italoj Eŭropon ĉe la mondpokalo-turniro en Japanio fine de la jaro.</p> <p>Igor Kolaković, la trejnisto de Serbio, estis tre feliĉa post la matĉo: „Neniu kredis je nia sukceso, ankaŭ en nia patrujo. Ankaŭ ni ne traktis nin kiel favoritojn, ni volis okupi almenaŭ la sesan lokon kaj tiel certigi nian partoprenon en la sekva eŭropa ĉampionado. Ni brave ludis ĉiun matĉon, aparte dum la duonfinalo kontraŭ Rusio. En ĝi ni estis perfektaj en la defendo. Tio estis nia plej malfacila ludo.”</p> <h3>Seniluziigita</h3> <p>Mauro Berruto, la ĉeftrejnisto de Italio, estis seniluziigita: „Iomete tristaj ni estos kelkajn horojn, tamen la partopreno en la finalo estas por ni granda sukceso. Ni partoprenos en la mondpokalo en Japanio.”</p> <p>En Vieno antaŭ la finalo okazis la matĉo por tria loko. Pollando, eŭropa ĉampiono ekde 2009, venkis Rusion je 3:1 (25:23, 18:21, 25:21, 25:19). La rusaj retpilkistoj okupis la kvaran lokon same kiel en 2009 en Turkio.</p> <p>„Ni batalis kuraĝe kaj digne”, diris Andrea Anastasi, la itala ĉeftrejnisto de Pollando. Vladimir Alekno, la ĉeftrejnisto de Rusio, parolis pri meritita malvenko: „En la duonfinalo ni ne havis bonŝancon, sed nun ni malgajnis kiel teamo.”</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div> 
