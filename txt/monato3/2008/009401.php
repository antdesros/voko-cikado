      <h2>Moderna klasikaĵo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Ĉu vere moderna klasikaĵo? Nu, pripensu: la ekapero de ĉi tiu libro, en sia anglalingva originalo, okazis en 1954. Dum la sekvaj jardekoj, pluraj dekoj da verkegoj, de tre diversaj aŭtoroj, estas eldonitaj, imite al la karaktero de ĉi tiu. Jam de 54 jaroj, ĉi tiu verko estas eldonita, kaj reeldonita, kaj rereeldonita, ne nur en sia anglalingva teksto, sed en preskaŭ kvindek aliaj lingvoj. Tri filmoj (po unu pri la tri volumoj) furioze sukcesis.</p> <p>Tradukis ĝin en Esperanton William Auld; la unuan volumon eldonis Sezonoj en 1995, la duan en 1996, kaj la trian en 1997. La aktuala, dua eldono estas zorge korektita. (Pro sia tre granda amplekso, la origina traduko fare de Auld enhavis plurajn nekonsekvencaĵojn, grandparte rilate al la varianta ortografio de diversaj nomoj.)</p> <p>Por legantoj de ĉi tiu recenzo, kiuj ne jam legis la verkon, mi povas diri, ke temas pri aventurromano, verkata kaj prilaborata dum kelkaj jardekoj. En aparta romano, <i>La hobito</i>, Tolkien verkis pri mistera sorĉoringo, kiu regas areton de aliaj sorĉoringoj. Kiu posedas ĉiujn el la ringoj kaj scipovas ilin uzi, povas mastri la tutan teron, iĝi senmorta kvazaŭ-dio, kaj senscie esti tiom, kaj tiel, koruptita, ke neniu fiaĵo maleblas, por tiu mastro. Ĉar la senlima potenco, kiun donas la ringoj kune, plene koruptas ilian mastron. Nu, la ĉefringon akiras hobito (speco de hometoj, en la fantazia mondo de Tolkien) nomita Frodo, kiu supozas ĝin nur kuriozaĵo, utila, ĉar kiam oni surfingrigas ĝin, oni iĝas nevidebla.</p> <p>En <i>La mastro de l' ringoj</i>, potenca sorĉisto, nomita Gandalfo, informas Frodon pri la danĝera karaktero de la ringo, kaj la neceso pereigi ĝin, kio fareblas nur per ĵeto en tre malfacile atingeblan vulkanecan monton.</p> <p>Tamen nekredeble potenca fiulo, kiu jam posedas preskaŭ ĉiujn el la ringoj, strebas akiri, iel ajn, la ringon nun havatan de Frodo. La veturo al la monto por pereigi la ringon eblas nur per danĝeroplena penado, dum kiu abundas militoj, kaj vere strangaj popoloj diversnaturaj, amikaj kaj malamikaj. Kaj konstanta minaco kaj atencado fare de la potenca fiulo, kiu strebas konkeri kaj regi la mondon.</p> <p>La tri volumoj de la romano ampleksas grandan, detalan priskribon de la multaj bataloj kaj de la kvazaŭ senfinaj misspertoj de Frodo kaj liaj akompanantoj.</p> <p>La traduko farita de Auld estas neriproĉebla. La preso estas vere bona. Do al tolkienismuloj mi multe rekomendas ĝin.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">J. R. R. Tolkien: La mastro de l' ringoj, tr. William Auld. Eld. Sezonoj, Kaliningrado, 2007. Tri volumoj kudre binditaj: 461 + 367 + 326 paĝoj. Neniu ISBN indikita.</h6>  
