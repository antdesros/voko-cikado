      <h3>Spirita vivo</h3>
      <h3 class="tem">RELIGIOJ</h3>
      <h2>Bektaŝismo plu vivas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Bektaŝanoj konsistigas unu el la kvar ĉefaj religioj de Albanio: katolikoj, ortodoksuloj, islamanoj kaj bektaŝanoj. La monda centro de tiuj ĉi troviĝas en la albana ĉefurbo Tirano.</b></p> <p>La fondinto de tiu islama sekto, Haĝi Bektaŝ, en la 13a jarcento sendis sian disĉiplon Sari Saltik al sud-orienta Eŭropo kaj Albanio. Helpe de li estis konstruitaj la plej praaj tekeoj, la bektaŝanaj preĝejoj.</p> <h3>Sen patrujo</h3> <p>En la fundamento de bektaŝismo estas la ideoj pri justeco kaj egaleco, toleremo, harmonio kaj kunlaboremo kun ĉiuj religiaj institucioj. Ĝia devizo estas „Sen patrujo ne ekzistas religio”. Historie bektaŝanoj estis notinde kontribuintaj al la albana renesanco kaj kulturo, same en ĝia sendependiĝo.</p> <p>Dum la jaroj 30 de la 20a jarcento kvarono de la tuta albana loĝantaro estis bektaŝana.</p> <h3>De Ankaro al Tirano</h3> <p>En la jaro 1913 en la monda centro de la bektaŝana komunumo, tiutempe en Turkio, kiel ĉefavo nomumiĝis Sali Niazi Dede, albandevena. Li gvidis la komunumon ĝis la 25a de novembro 1925, kiam la nacia asembleo turka decidis pri la fermo de la tekeoj kaj la monda centro en Ankaro. Tiam, rezulte de intertraktadoj de Sali Niazi Dede kun la reĝo Zog la 1a de Albanio dum la jaroj 1926-28, oni decidis establi la mondan centron en Albanio, aŭguste de 1930.</p> <p>La antaŭa sidejo en Turkio, en Haĝi Bektaŝ, restis kiel muzeo kaj pilgrimejo, kie ĉiun aŭguston ariĝas multaj religiuloj.</p> <p>La bektaŝanoj agadas kiel organizita religia komunumo, nepolitika kaj neŝtata.</p> <h3>Persekuto</h3> <p>La ĉefavejo bektaŝana funkciis ankaŭ dum la dua mondmilito. La ĉefavo Dede Ahmeti, postmilite, partoprenis en kelkaj packonferencoj riĉigante sian institucion per plia dimensio. De la jaro 1967 ĝis 1990, kiel ĉiuj religiaj institucioj, ankaŭ bektaŝanoj estis malpermesitaj kaj persekutataj de la komunisma reĝimo, kiu siaflanke, per konstitucio, deklaris sin ateisma.</p> <p>Dume, bektaŝismo daŭre funkciis en Usono, kie la albana ĉefavo sindonema kaj erudiciulo sankta, patro Redĝep, ĝuis ankaŭ institucian helpon de la registaro usona, kiu aprezis la kulturajn valorojn. Bektaŝanoj agadis senĉese en Kosovo, Makedonio kaj aliloke, ĝis la 22a de marto 1990, kiam ili estis finfine reviviĝintaj en Albanio, okaze de la Tago de Nevruzo. Unuafoje en la solenaĵo partoprenis ankaŭ Patrino Tereza, notinde kontribuinta al la restariĝo de la monda ĉefavejo en Tirano.</p> <p>Nuntempe la monda bektaŝana ĉefavejo estas jura persono surbaze de la internacia juro kaj de la konstitucia leĝo de la albana ŝtato.</p> <p>Laŭ kelkaj donitaĵoj, oni taksas, ke ekzistas ĉirkaŭ 7 milionoj da bektaŝanoj enmonde (proksima kaj fora oriento, norda Afriko kaj Balkanio). En Albanio oni taksas ĝiajn adeptojn je ĉirkaŭ 100 000.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div>  
