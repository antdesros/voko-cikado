      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Tri jaroj da stagnado</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ tri jaroj ŝanĝiĝis la japana politika scenejo. En la tutlandaj elektoj la Demokratia Partio (DP) venkis la Liberalan Demokratian Partion (LDP). Tiel perdis LDP la povon, kiun ĝi antaŭe tenadis dum ĉirkaŭ 50 jaroj.</p> <p>LDP sukcese gvidis Japanion, kiu reviviĝis el la ruinoj de la dua mondmilito. Danke al la diligento kaj laboremo de la popolo, alta teknologio, malarmado sub la protekto de Usono, kaj distanca engaĝiĝo en regionaj militoj, ekzemple en Koreio kaj Vjetnamio, Japanio ĝuis pacon.</p> <h3>Klaso</h3> <p>Rezulte LDP konstruis dum la 1960aj jaroj la due plej grandan ekonomian potencon en la mondo. Pli ol 90 % de la popolo sentas sin apartenanta socie al t.n. meza klaso. Eblas argumenti, ke tiel Japanio atingis kaj altan ekonomian kreskadon kaj relative egalan distribuon de enspezo.</p> <p>Tamen en la lastaj jaroj kreskis popola kritiko kontraŭ LDP, kiu ne povis realigi strategion por alfronti novajn socian kaj politikan situaciojn: maljuniĝanta popolo, manko de mendo kontraŭ oferto, malalta interezo, kiu forprenis monon de ordinaraj homoj kaj ĝin transdonis al bankoj, konkurenco de aliaj aziaj landoj, ktp.</p> <h3>Kolekto</h3> <p>Dum la elektoj en 2009 DP lanĉis tri sloganojn: 1. socialdemokrate protekti la popolan vivon; 2. transdoni la povon de burokratoj al politikistoj; 3. diplomatie distanciĝi de Usono. Tiel oni esperis, ke freŝa politiko de DP frakasos la malnovan komunuman strukturon de ekzistantaj interesoj kaj koncesioj.</p> <p>Efektive, tamen, la trijara DP-mastrumado fiaskas. Tri ĉefministroj plenumis preskaŭ neniom, kiom ili celis. Kulpas la fakto, ke DP konsistas el improvizita kolekto de homoj kun diversaj tendencoj: eks-anoj de LDP, socialistoj, kaj aliaj.</p> <h3>Imposto</h3> <p>La nuna ĉefministro Noda Yosihiko rompis elekto-promeson kaj subite enkondukis leĝon por plialtigi la spez-imposton laŭgrade de la nuna 5 % ĝis 10 %. Proteste forlasis DP-on pli ol 70 parlamentanoj.</p> <p>Estas evidente, ke DP malvenkos en la baldaŭaj tutlandaj balotoj. Multaj homoj ĝin subtenas, sed ankaŭ nur 25 % LDP-on. Preskaŭ duono de la popolo subtenas neniun partion.</p> <p>Eĉ se LDP venkos kaj reprenos la povon, ĝi ne povos sola formi registaron. Neeviteble sekvos koalicio. La japanoj frontas malstabilecon kaj necertecon, dum stagnas same la ekonomio kaj politiko.</p> <div style="text-align:right">I<span class="mm">SIKAWA</span> Takasi</div> 
