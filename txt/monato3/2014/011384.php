      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Aĉetunto, atentu!</h2>
      <!-- komenco de la enhavo -->

       

      <p>La vendisto, tre ĉarma, disradiis fidon kaj optimismon. „Jen, gesinjoroj, la nova hejmo, kiun mi planas por vi. Tie vi vivos kiel kukoj en
   butero, riĉe kaj sekure kaj libere. Ne plu servutuloj vi estos, servutuloj de pli
   potenca najbaro, sed fine mastroj, suverenoj, en via propra domo. Do aĉetu, nepre
   aĉetu!”
</p> 
<p>Ekjubilis la homoj. „Jes, ja,” ili kriis. „Do montru al ni, por ke ni mem vidu!”</p> 
<p>„Nu,” agnoskis la vendisto. „La domo ankoraŭ ne estas konstruita. Efektive, pro la najbaroj, ruzaj, ĉikanemaj,
   kun riĉaj amikoj, damnu la poltronojn, ne tute certas, kiom da ĉambroj ĝi havos, kaj
   kiom granda estos la ĝardeno, se entute ĝi havos. Tamen mi certigas al vi ... oj!
   Gesinjoroj ... ne forkuru ...!”
</p> 
<p>Tamen jes forkuris, referendume, aĉetuntoj de la nova, sendependa skota hejmo proponata
   de la vendisto Alex Salmond [aleks samond], estro de la Skota Nacia Partio kaj la
   unua ministro en la skota parlamento. Memevidente ili forkuris: kiu aĉetus domon surbaze
   de planoj neniel firme fiksitaj kaj, ĉu ĝuste, ĉu malĝuste, diversflanke kritikataj?
</p> 
<p>Kompatinda Salmond, kiu sincere kredis je sendependa Skotlando – la tria plej riĉa regiono de la Unuiĝinta Regno kaj laŭ ekonomikistoj plene kapabla
   ĝui prosperan memekziston ekster Anglio, Kimrio kaj Norda Irlando. Kompatinda, ĉar
   anstataŭ atendi, kaj trovi definitivajn respondojn al la multaj demandoj kirliĝantaj
   ĉirkaŭ sendependigo, li „vendis” fine pli da promesoj ol da faktoj.
</p> 
<p>Ekzemple: Salmond asertis, ke sendependa Skotlando daŭre uzos la britan pundon. La
   londona registaro diris, ke ne. Salmond asertis, ke sendependa Skotlando facile membriĝos
   en Eŭropa Unio, ĉar la union ĝi neniam estos forlasinta. EU diris, ke nepros novnacie
   peti aliĝon. Salmond asertis, ke sendependa Skotlando finance stabilos pro ekvivalento
   de 24 miliardoj da bareloj da nafto sub la Norda Maro. Edinburga energio-konsilista
   firmao diris, ke temas nur pri 15 miliardoj. Kaj tiel konfuzante, kontraŭdire, plu.
</p> 
<p>Tiel bombardis jesuloj kaj neuloj unu la alian per veroj, duonveroj, kredoj, esperoj,
   mensogoj kaj aliaj propagandiloj. Memorigis la referendumo-kampanjo pri la tranĉeoj
   de la unua mondomilito. Kun eta diferenco: avancis iom post iom la fortoj de sendependigo.
</p> 
<p>Ĝis, tamen, opinio-sondado du semajnojn antaŭ la referendumo sugestis venkon por la
   sendependiga movado. Tuj la londona politika establo, konsternite, mobilizis siajn
   grandajn kanonojn. Dubojn kaj necertecon ili pafis, samtempe promesante eĉ pli da
   memdeterminado al regiono jam dorlotata (kompare kun anglaj regionoj ekster Londono).
   Ne forgesendas, ke skotoj disponas pri propra parlamento (angloj ne) kaj, danke ĉefe
   al subvencioj el Londono, pri nacia prokapa enspezo ĉ. 2000 eŭrojn pli alta ol en
   Anglio. Cetere skotaj deputitoj en la brita parlamento rajtas voĉdoni pri anglaj aferoj;
   anglaj deputitoj ne pri skotaj.
</p> 
<p>Diskuteblas, kiel, eĉ ĉu, la antaŭreferendumaj promesoj al skotoj (ekzemple la povo
   mem fiksi impostojn) realiĝos. Jam tagojn post la rezulto ekkverelis la politikistoj
   pri la programo kaj horaro. Tamen ne ĉio estas perdita. La 45 % de la skotoj, kiuj
   diris „jes” al Skotlando, implicite „ne” al la hegemonio de Londono, parolis por ĉiuj britoj en neglektataj regionoj ekster
   la ĉefurbo. Ili klare mesaĝis, ke britoj ne plu toleros regadon de riĉa, aroganta,
   indiferenta politika kliko en Londono.
</p> 
<p>Jam, do, leviĝas voĉoj favore al pli da ekvilibro en la mastrumado de la diversaj
   partoj de la Unuiĝinta Regno. Menciitaj estas angla asembleo aŭ eĉ regionaj parlamentoj
   kun skotsimilaj povoj. Se, rezulte, eĉ en la konservativa Britio, elfluos povoj el
   centrisma Londono al la regionoj, la ŝajna malvenko de la skotaj jesuloj fariĝos efektive
   venko. Tamen estu la popolo, kiu kampanje, manifestacie, balote, prie insistu. Politikistoj
   per si mem ne faros.
</p> 
<p>Do eble ne vane kampanjis Alex Salmond. Klare, erarojn li faris. Anstataŭ intertrakti
   kun EU, la brita registaro, industriistoj kaj aliaj, por ekscii, interkonsente, kio
   eblos, kio ne, kiel precize aspektos lia nova sendependa hejmo, Salmond hastis al
   referendumo, apelaciante al koroj, ne kapoj. Ne surprize, ke reagis skotoj laŭ maksimo
   
   <i>caveat emptor</i>: aĉetunto, atentu!
</p> 
<p>Sendependistoj en Katalunio kaj aliloke, notu. Ne gravas fina, referenduma demando,
   sed respondoj malfermaj kaj neambiguaj al la ja pli gravaj, kaj antaŭreferendumaj,
   demandoj.
</p> 
