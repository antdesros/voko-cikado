      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>La Eŭropo-eksperimento: ĉu fiaskonta?</h2>
      <!-- komenco de la enhavo -->

       

       <p>En balotoj fine de majo por elekti membrojn de la Eŭropa Parlamento venkis naciismaj
   aŭ ksenofobiaj partioj en tri landoj – Danio, Francio kaj la Unuiĝinta Reĝlando. En kvin aliaj landoj partioj samkoloraj
   gajnis pli ol 10 % de la voĉoj. Ĉu maltrankviliĝi?
</p> 
<p>Verdire ne, aŭ almenaŭ ne tro, malgraŭ la komentoj de reprezentantoj de grandaj kaj
   establitaj politikaj partioj draŝitaj ĉe la urnoj. Nepras memori, ke tra Eŭropo voĉdonis
   nur 43,09 % de la balot-rajtigitoj (kontraste al 61,99 % en 1979 aŭ 49,51 % en 1999)
   por elekti parlamenton kun minimumaj povoj kaj perceptatan, ĉu vere, ĉu malvere, seninflua,
   senutila kaj senvalora.
</p> 
<p>Krome, malgraŭ supraĵa sukceso, ne eblas paroli pri civitana brakumado de naciismaj
   partioj. En Danio, ekzemple, subtenis la Popolan Partion nur 15 % de la balot-rajtigitoj;
   en Francio la Nacia Fronto kaptis nur 10,6 %; kaj en Britio la Sendependiga Partio
   de la Unuiĝinta Reĝlando nur 9 %. Do ne indas histerii pri Eŭropo en la manoj de faŝistoj,
   rasistoj kaj aliaj ekstremdekstremuloj.
</p> 
<p>Aliflanke oni ne indiferentu. Malgraŭ tio, ke diversgrade, diverslande, eblas interpreti
   la rezultojn kiel vangofrapon kontraŭ naciaj registaroj kaj iliaj estroj, la ĵusaj
   balotoj tamen indikas profundan skeptikecon pri la eŭropunia eksperimento.
</p> 
<p>Jes ja, ankoraŭ eksperimento, ĉar malgraŭ tio, ke la unio fontas el postmilitaj projektoj
   pri industria kunlaboro, la 63 jaroj post la Traktato de Parizo apenaŭ sufiĉas ne
   nur por forigi jarcentojn da politikaj, militaj, lingvaj diferencoj sed ankaŭ, pli
   defie, unuigi plej diversajn landojn el la norda, okcidenta, suda kaj centra Eŭropo
   kaj ties pli ol 500 milionoj da loĝantoj.
</p> 
<p>Jen, verdire, la kerna problemo: la nuna unio – legantoj en laste aliĝintaj EU-landoj verŝajne malkonsentos – tro rapide kreskis. Spronite parte de Usono, kiu celis relative stabilan bufron en
   la centra Eŭropo kontraŭ Rusio, parte de subfosantoj de la eksperimento (ekzemple
   la iama ĉefministro brita John Major, kiu klare komprenis, ke pli vasta Eŭropo pli
   malfacile regeblos), la unio plivastiĝis sed samtempe ne profundiĝis.
</p> 
<p>Argumenteblas, ke estintus pli saĝe resti ĉe 15 membroj kaj koncentriĝi pri politika,
   financa kaj socia integriĝo inter ŝtatoj jam ekonomie kunlaborantaj, ol enunuigi aliajn,
   postkomunismajn ŝtatojn, kun apartaj kaj monon glutantaj bezonoj. Unue, eble, sufiĉus
   traktatoj kiel inter Svisio kaj Eŭropa Unio: kunlaboro laŭ difinitaj kaj ambaŭflanke
   akceptitaj kriterioj.
</p> 
<p>Tamen jen la pasinteco. Nepras nun, ke la novaj parlamentanoj, sed ĉefe la Eŭropa
   Komisiono, agnosku la malkontenton. Urĝas redifini la celojn kaj la valorojn de la
   unio kaj alfronti la demokratian deficiton inter la regantoj kaj regatoj. Necesas
   parlamento, kiu parolos ne pri sed por la popoloj de Eŭropo, kiu alproprigos al si
   povojn por efike reprezenti ĉiujn eŭropajn civitanojn.
</p> 
<p>Rezigni pri la superflua kaj multekosta strasburga parlamentejo, for de la eŭropa
   „ĉefurbo” Bruselo, mesaĝus al la cinika publiko, ke parlamentanoj kuraĝas ekagi por krei fiske
   pli respondan, finance pli konscian organizaĵon. Ago nur simbola, eble, sed simboloj
   fojfoje gravas.
</p> 
<p>Certas, tamen, ke se la mastroj en Bruselo preteratentas kaj la malkontenton kaj – eventuale pli danĝere – la indiferenton pri Eŭropo, ambaŭ klare esprimatajn dum la ĵusaj balotoj, riskas
   fiaski la EU-eksperimento. Estos venkinta naciismo: la 21a jarcento fariĝos denove
   la 19a.
</p> 
<p>Granda defio, evidente, por la parlamentanoj. Pro tio, gekaraj, 43,09 % da eŭropanoj
   vin elektis.
</p> 
