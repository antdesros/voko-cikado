      <h3>Moderna vivo</h3>
      <h3 class="tem">BELGIO</h3>
      <h2>Eŭtanazio por neplenkreskuloj</h2>
      <!-- komenco de la enhavo -->

       

       <p>En belga radiodissendo estas atesto de patrino. Ŝia infano havas nekuraceblan kanceron,
   kaj daŭre doloron. Kiun ajn kontraŭdolorilon oni donas, nenio efikas, kaj la infano
   daŭre suferas. La morto estas certa sed ne tuja. Estas neelteneble. La solan aferon,
   kiun la patrino ankoraŭ povas fari, ŝi faras. Ŝi petas kuraciston mallongigi la suferegon,
   fini la vivon de ŝia infano. Kaj li faras.
</p> 
<p>Tio okazis antaŭ 10 jaroj, kaj la patrino ankoraŭ opinias, ke ŝi faris, kion ŝi devis
   fari, pro amo. Sed estis kontraŭleĝe, kaj ŝi kaj la kuracisto riskis tre gravajn punojn.
   La infano ne povis adiaŭi ĉiun, kiun li volis, ĉar estus riske. Oni povas supozi,
   ke vere ne estis maniero por mildigi la suferon, ke la infano ankaŭ petis, ke tiu
   konsciis, kion li volis, sed oni ne povas esti certa. Oni havis nur sian konsciencon
   por juĝi.
</p> 
<p>Tiaj aferoj okazas ĉie en la mondo, sed kun multaj eblaj misuzoj aŭ miskomprenoj.
   Plej ofte la kuracisto mem decidas, ĉar nur tiel li ankoraŭ povas helpi, ĉar li supozas,
   ke tion oni volas. La aferon priparoli estus tro riske, aŭ povus plifortigi la doloron
   (kio estas dubinda).
</p> 
<h3>Medikamentoj</h3> 
<p>Rekte fini la vivon same kunportas riskojn. Plej ofte temas pri ĉesigo de necesa medikamentado,
   traktado aŭ manĝo (kun danĝero je agonio), aŭ troigo de kontraŭdoloriloj. Ofte sen
   la scio de la infano. Aŭ la infano povas mortigi sin, kio estas la plej malbona. Foje
   gepatroj akiras mortigajn medikamentojn, sed restas danĝero pri malsukceso aŭ plia
   sufero.
</p> 
<p>Klare, malpermesi eŭtanazion ne signifas, ke tio ne okazas, sed ja ke oni kaŝe faras
   kun multaj diversaj danĝeroj.
</p> 
<p>Nova leĝo en Belgio nun ebligas eŭtanazion por neplenkreskuloj (jam eblas ekde 2002
   por plenkreskuloj), sed kun striktaj kondiĉoj. La neplenkreskulo mem plurfoje petu,
   buŝe kaj skribe; psikologo deklaru, ke la infano estas sufiĉe matura (ne estas aĝlimo
   de 12 jaroj, kiel en Nederlando); la gepatroj konsentu; kaj du kuracistoj atestu,
   ke la fizika doloro neelteneblas kaj senperspektivas (do ne malaperos). La vivfinado
   estas farata de kuracisto.
</p> 
<h3>Konscienco</h3> 
<p>Okazis tre serena debato pri tio en Belgio kaj la parlamento. Ĉe diversaj politikaj
   partioj la membroj rajtis mem decidi laŭ propra konscienco. Malplimulto (ĉefe religiuloj)
   akceptas la volon de la plimulto (sed supozeble mem ne praktikus eŭtanazion), kaj
   la leĝo estas apogata de granda plimulto de la belga popolo. Kontraŭargumentoj estas,
   krom evidentaj religiaj, ke estas malfacile taksi kaj ke psikologo povas mise taksi
   la maturecon de infano aŭ kuracisto mistaksi lian aŭ ŝian doloron kaj nekuraceblecon.
</p> 
<p>Nun oni povos senkaŝe praktiki eŭtanazion ĉe neplenkreskuloj. Ĉio okazos kun la scio
   kaj volo de ĉiuj koncernatoj. Suferanto povas adiaŭi ĉiun, diri kion li aŭ ŝi volas
   diri, kaj havi dignan forlason. Estas ankaŭ multe pli bone por la postrestantoj: ili
   povas pli bone organizi funebron, paroli pri tio, ne riski, ne reteni suspektojn aŭ
   konsciencriproĉojn.
</p> 
<p>Spertoj en Nederlando montras, ke eŭtanazio ĉe neplenkreskuloj multe pli ofte estas
   petata ol farata (nur kvin fojojn en 10 jaroj). Estas unue helpkrio, kiu ebligas malkaŝan
   priparoladon de aliaj ebloj. Donas animtrankvilecon havi iun „planon Z”. 
</p> 
