      <h3>Scienco</h3>
      <h3 class="tem">PARKINSONA MALSANO</h3>
      <h2>Ju pli toksaj legomoj, des pli da riskoj por homoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>La parkinsona malsano povas trafi homojn ankaŭ pere de agrikulturo – por tiuj, kiuj laboras en tiu fako – kaj, pli ĝenerale, per la fruktoj kaj la legomoj, kiujn ni ĉiutage metas surtablen:
      elmetiĝo al pesticidoj, herbicidoj kaj insekticidoj fakte estas science asociita kun
      rimarkinda risko pri ekapero de la menciita malsano.
   </b>
</p> 
<h3>Revizio</h3> 
<p>Tion konkludas itala esploro, kiu konsideris kaj reviziis la rezultojn de cento da
   antaŭaj esploroj. Temas pri revizio plenumita de sciencistoj el 
   <i>Fondazione IRCCS Policlinico San Matteo</i>, en la lombarda urbo Pavia, kunlabore kun tiel nomata 
   <i>Centro Parkinson – Istituti Clinici di Perfezionamento</i> (mallonge: ICP). Ĉi lasta institucio situas en Milano kaj estas kunordigata de doktoro
   Gianni Pezzoli.
</p> 
<p>Doktoro Emanuele Cereda (la ĉefa aŭtoro de la esploro) kaj liaj kolegoj analizis 104
   studojn, kiuj ekzamenis la ligitecon inter elmetiĝo al pesticidoj, herbicidoj, insekticidoj
   kaj risko ekhavi la parkinsonan malsanon, precipe por ĉiuj, kiuj laboras en la agrikultura
   fako. En la esploro, tamen, oni ankaŭ inkluzivis personojn, kiuj pro aliaj kialoj
   elmetiĝas al similaj riskoj: alivorte personojn, kiuj vivas proksime al la kamparo,
   laboras apud agrikulturaj kampoj aŭ – eĉ pli maltrankvilige – simple kutimas trinki en sia hejmo kran-akvon.
</p> 
<h3>Solviloj</h3> 
<p>La esploro, publikigita en 
   <i>Neurology</i>, medicina revuo de la Usona Akademio de Neŭrologio, montris, ke ofta elmetiĝo al
   tiuj nesanigaj substancoj povas pliigi la riskon, ke oni estos trafita de la parkinsona
   malsano, eĉ je 80 procentoj. En kelkaj pasintaj studoj reviziitaj kadre de la esploro,
   la elmetiĝo al certaj pesticidoj kaj solviloj cetere estis asociita kun duobla risko
   pri disvolviĝo de la neŭrologia malsano. 
</p> 
<p>Oni ne enketis la influon de la diversaj tipoj de elmetiĝo (ĉu la kombinaĵo estas
   ekzemple enspirita aŭ sorbita tra la haŭto). Tamen, la esploro ĝenerale sugestas,
   ke la risko kreskas proporcie kaj paralele kun la daŭro de elmetiĝo al danĝeraj kemiaĵoj.
</p> 
<h3>Kvanto aŭ kvalito</h3> 
<p>La bezono – ofte substrekata de „saĝuloj” en la pasintaj jaroj – de iu agrikulturo, kiu prefere donu prioritaton al la kvanto anstataŭ al la kvalito,
   havis la jenan rezulton: ke la uzo de kemiaĵoj en la kampoj atingis neelteneblajn
   dimensiojn. Sen konsideri, ke ĉio, kion homo enmetas en la medion, poste revenas al
   ni je malsamaj kaj kaŝitaj formoj: do en la nutraĵoj, kiujn ni alportos al la tablo;
   en la akvo, kiun ni trinkas; en la aero, kiun ni spiras. Se tiuj, kiuj laboras en
   agrikulturo, estas sendube pli elmetitaj al tiaj riskoj, ankaŭ ordinaraj uloj, kiuj
   simple manĝas, spiras, trinkas (!) ĉiutage, ne estas 100-procente protektitaj. Kiu
   havas orelojn por aŭdi, tiu aŭskultu: legantoj (kaj ĉefe regantoj!) tiun ĉi antikvan
   frazon pripensu.
</p> 
