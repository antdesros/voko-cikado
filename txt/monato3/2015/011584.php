      <h3>Medio</h3>
      <h3 class="tem">POLUADO</h3>
      <h2>Ruĝaj ŝlimoj en Mediteraneo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En suda Francio, apud Marsejlo, uzino de baŭksito elverŝas la restaĵojn (ruĝajn ŝlimojn)
   en Mediteraneon, nun en naturan rezervejon.
</p> 
<h3>Iom da historio</h3> 
<p>Baŭksito estas ekspluatata en Provenco ekde 1894. De pli ol 70 jaroj uzino 
   <i>Pechiney</i> [peŝiné], nun 
   <i>Alcan</i>, kun komplezo de la ŝtato enverŝas la restaĵojn de la pritraktado en grandan maran
   foson apud Cassis [kasí]. Tiu foso estas 300 metrojn profunda kaj 2000 metrojn longa.
</p> 
<p>Ĉar sciencistoj prave diris, ke maro mem resaniĝas, neniu havis moralan problemon
   daŭrigi la poluadon. De 1966 subtera kaj submara tubego, 47 kilometrojn longa, portas
   la poluaĵojn en la foson. Sed intertempe validiĝis en 1978 la Konvencio de Barcelono
   por protektado de Mediteraneo, kaj la ŝtato la 1an de julio 1996 devigis la ekspluatanton
   ĉesigi la enĵetadon la 31an de decembro 2015. Des pli, ke tiu foso enestas en la Nacia
   Rezervejo de la Fjordetoj (<i>Parc national des Calanques</i>), la sola en Francio apud granda urbo (Marsejlo) kaj la sola tera kaj mara.
</p> 
<h3>Kia poluado</h3> 
<p>Kune kun fero, kiu donas la belan ruĝan koloron, estas enmarigataj natro (NaOH), aluminio
   (Al), titano (Ti), kromo (Cr), kadmio (Cd), hidrargo (Hg), arseno (As) kaj aliaj pezaj
   metaloj, danĝeraj por la sano, kiuj havas grandan efikon sur la flaŭro kaj faŭno kaj
   jam senvivigis grandan zonon inter Ĝenovo en Italio kaj Montpeliero en Francio.
</p> 
<p>De 900 000 tunoj en la 1970aj jaroj reduktiĝis la enĵeto al 300 000 tunoj en 2004.
   Tamen restas ĉirkaŭ 20 ĝis 30 milionoj da tunoj da poluita materio. Verŝado de 270
   m
   <sup>3</sup> da elfluaĵo hore signifas 180 000 tunojn jare. Sekve, en 2007 estis instalitaj filtriloj-premiloj
   por redukti la poluon al 35 mg litre. Aperis ruĝa polvo, kiun oni stokas en la apudeco
   de la uzino.
</p> 
<p>Laŭ la industriisto, natro, kiu nature troviĝas en la mara salo, stokas inertan hidrokalciton,
   kiu kaptas la pezajn metalojn. Tio ne estas la opinio de la fiŝkaptistoj, kiuj konstatas,
   ke kiam blovas la norda vento, la ruĝaj ŝlimoj alsurfaciĝas.
</p> 
<h3>Nuna debato</h3> 
<p>Ĉar finiĝos la permeso ekspluati, iuj proponas simple fermi la uzinon, kaj tiam oni
   ĉantaĝas pri la perditaj laborpostenoj (750) en regiono, kie mankas laboro, dum neniu
   kalkulis, kiom da postenoj de fiŝkaptistoj malaperis pro la poluo de la maro. Alia
   timo estas, ke, ĉar la baŭksito venas el Gvineo, eblos instali tian uzinon aliloke
   en la mondo, kie la normoj ne estas tiom rigoraj kaj la popolo ne estas tiom sentema.
</p> 
<p>Iuj proponas teknike kaj ekonomie plu esplori, ĉar granda parto de la poluo koncentriĝas
   en malgranda volumo. La ministerio pri ekologio petis kromajn esplorojn; sciencistoj
   bedaŭras, ke ilia voĉo ne estas aŭskultata kaj ke la kontroloj estas farataj de la
   ekspluatanto mem kaj ne de sendependaj fakuloj. La loĝantaro de la 13 koncernaj urboj
   devas doni sian opinion, sed apenaŭ disponas pri taŭgaj dokumentoj aŭ informoj.
</p> 
<p>Tiu debato estas vere nuntempa: kiel pluekspluati, reduktante maksimume la konsekvencojn
   por la mara medio? Kiel savi nian planedon, tamen daŭrigante nian konsumapetiton?
</p> 
