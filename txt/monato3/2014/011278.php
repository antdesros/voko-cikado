      <h3>Eseoj</h3>
      <h3 class="tem">SIGNOLINGVOJ</h3>
      <h2>Vortaro intuicia, horizonto internacia</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>En pluraj landoj oni uzas saman vorton (itale „<i>lingua</i>”, france „<i>langue</i>”, greke „<i>γλώσσα</i>” ktp) por indiki kaj muskolon situantan ene de la buŝo (esperante: „lango”) kaj idiomon (esperante: „lingvo”). Tiu kutimo baziĝas verŝajne sur la malaktualiĝinta konvinko, ke lingvo estas nepre
      sinonimo de parolado, de agado efektivigata pere de la voĉo.
   </b>
</p> 
<p>Ni tamen jam bone konscias, kiom da alternativaj sistemoj ekzistas en la nuna epoko
   por komunikiĝi kun aliaj homoj sen uzi la voĉon. Unu rigardo aŭ rapida gesto de la
   manoj diras fojfoje pli multe ol severa patra skoldo (ĉu mem malaktualiĝinta?) aŭ
   senfina sacerdota prediko (ankoraŭ aktuala). Estas nun klare, kiel al fakuloj, tiel
   al nefakuloj, ke la koncepto de lingvo estas multe pli vasta ol antaŭe kredite. Kiel
   „lingvaj agoj” povas esti determinitaj plej diversaj manieroj komunikiĝi, inkluzive laŭdire de muziko,
   fotografio aŭ pentrarto.
</p> 
<p>Lingvo ne estas nura aro da voĉe esprimeblaj aŭ plume skribeblaj vortoj. Kun plezuro
   oni do aplaŭdu la novaĵon pri la apero en Interreto de vortaro noveca kaj el pluraj
   vidpunktoj bezonata. Temas pri vortaro pri signolingvo, aŭ pli ĝuste pri signolingvoj:
   alivorte pri la gestaj lingvoj uzataj de surduloj, unuafoje prezentitaj ĉe kompar-vortaro
   senpage ĝuebla ĉe la adreso 
   <a href="http://www.spreadthesign.com">www.spreadthesign.com</a>.
</p> 
<h3>Nesukcesaj klopodoj</h3> 
<p>Ankaŭ homoj kun malsufiĉa aŭ nula aŭdkapablo havas, kiel konate, propran (tre efikan)
   manieron komunikiĝi. En la lastaj jaroj multiĝis aliflanke en la tuta mondo kursoj
   kun la celo instrui signolingvon ne nur al surduloj mem (kiuj kutime ĝin konas denaske,
   escepte de la okazoj de malfrua surdiĝo), sed ankaŭ al uloj, kiuj volas akiri novajn
   profesiajn kompetentojn, volante fojfoje fariĝi veraj interpretistoj. Ĉiu nacilingvo
   havas bedaŭrinde sian apartan surdulan version. Laŭ 
   <i>Vikipedio</i>, „oni parolas pri signolingvoj kaj ne nur signolingvo, ĉar ĉiu lando, fojfoje ankaŭ
   regionoj, havas propran signolingvon. Entute estas nuntempe 114 signolingvoj en la
   mondo. Signolingvoj en apudaj landoj estas ofte tute malsimilaj, eĉ kiam tiuj landoj
   havas komunan parolan lingvon; la signolingvoj en Britio kaj Irlando estas unu ekzemplo”. Surda leganto de nia revuo, Vilhelmo Rowe, plendis per letero sendita en 2007, ke
   ne ekzistas, apud malmultaj nesukcesaj klopodoj (Gestuno, Signuno), ununura pontolingvo
   por surduloj, responda al tio, kio estas ekzemple por nia legantaro Esperanto. Ĉu
   internacia komunikado por surduloj estas do ankoraŭ utopio?
</p> 
<h3>Virtuala renkontopunkto</h3> 
<p>La temo de ĉi tiu artikolo ne estas la invento de surdula komuna lingvo. Sep jarojn
   post tiu letero do nemulto ŝanĝiĝis. Kiam surduloj vizitas alian landon por feriumado,
   ili daŭrigas alfronti pli-malpli la samajn malfacilaĵojn, kiujn aŭdkapablaj vojaĝantoj
   spertadas: ili plejparte ne komprenas la lingvon (ĉi-okaze la signolingvon) de la
   gastiganta lando. Kelkaj signoj ja estas universalaj, ĉar ili mimas agon, kiun ĉiuj
   priskribas sammaniere, aŭ ĉar temas pri gestoj pruntitaj de unu signolingvo al la
   alia (oni serĉu tiurilate en la menciita retejo „biciklo”-n, „amo”-n, kaj oni vidos grandan similecon inter la signoj utiligataj en malproksimaj landoj).
   Tamen pluraj aliaj gestoj estas vere malsamaj de lando al lando kaj ne facile rekoneblaj
   fare de alilandaj surduloj (oni serĉu „ŝtelisto” por trovi bonan ekzemplon). Sed io finfine moviĝas kaj la iama utopio fariĝas iom
   post iom espero. Internaciaj institucioj ekkomprenis la neceson, la urĝon disponigi
   al la homoj de la tuta mondo, pere de la potencialoj de Interreto, iun virtualan renkontopunkton,
   nome kompletan multlingvan vortaron.
</p> 
<h3>Malproksime de la arbo</h3> 
<p>La iniciato nomiĝas 
   <i>Spread the sign</i> (tio estas, en la angla, „Disvastigu la signon”), kaj la multlingva vortaro publikigita estas la rezulto de internacia kunlabor-projekto.
   Ĝi kunmetas fakulojn, kiuj reprezentas 24 lingvojn kaj samnombrajn landojn. Inter
   ili estas grandaj kaj malgrandaj ŝtatoj de la mondo (ĉefe tamen el Eŭropo), kiel Italio,
   Aŭstrio, Britio, Brazilo, Francio, Grekio, Hispanio, Islando, Japanio, Latvio, Litovio,
   Pollando, Turkio, Ugando, Usono. La plurlingva tradukilo ebligas spekti dekmilojn
   (sen troigo!) da filmitaj signoj, rapide tradukeblaj de unu signolingvo al alia.
</p> 
<p>Temas kompreneble pri instrumento utila ne nur por la surduloj mem, kiuj ekzemple
   vojaĝonte eksterlanden deziras lerni kelke da utilaj vortoj aŭ esprimoj (kiel pli
   multo el la turistoj faradas), nelaste por povi mendi kafon en trinkejo aŭ peti informojn
   sur la vojo. La paĝaro estos fakte ŝatata ankaŭ de fakuloj pri lingvoscienco, psikologio
   aŭ komunikado antaŭvide al akademiaj esploroj. Aŭ – kial ne? – ĉion ĉi rigardos interesa ni mem, „ordinaraj” personoj, se ni iam havos la deziron ekkoni de proksime la surdulan realon kaj ĝiajn
   komunik-sistemojn.
</p> 
<p>Por citi la titolon de eksterordinara libro verkita de Andrew Solomon, la surduloj
   (kune kun aliaj „malsamulaj” minoritatoj) ege similas al pomoj senvole falintaj malproksime de sia arbo, nek danĝeraj
   pro tio, nek malpli bongustaj ol la ceteraj. Ni ĉiuj povas fari kelkajn paŝojn por
   alproksimiĝi al ilia mondo, tiom proksima al la „normaleca” arbeto, sub kiu ni (same senvole) naskiĝis. Ni tiumaniere aprecos la provon de la
   surdula komunumo signolingve informi pri si kaj informiĝi pri la mondo: temas pri
   realo paralela al la nia, kvankam ne ofte videbla. La surduloj estas homoj plej ofte
   aparte sentemaj, kiuj vivas normalan vivon kaj estas kapablaj komunikiĝi per gestoj
   esprimantaj ajnan faldon de la homa penso kaj ilian kulturon mem. Ĝis nun eble mankis
   al ni la ilo por plenumi tiun alproksimiĝon. Ni limiĝis je la lernado de multaj nacilingvoj
   aŭ de unu „nesurdula” pontolingvo. Eble venis la momento ekscii, almenaŭ parte, la riĉecon de diferencaj
   lingvoj, tiuj de tutmondaj surduloj, tute malsamaj (ĉu ni vetu?) ol la jam al vi konataj.
   Nun tiu malfermiĝo estas pli facila kaj, se ni volas, eĉ amuza.
</p> 
<h3>Ĉiutagaj frazoj</h3> 
<p>La projekto ricevis la moralan kaj financan subtenon de la Eŭropa Komisiono, pere
   de la oficejo por t.n. Internacia Sveda Programo pri Instruado kaj Klerigado. La 24an
   de februaro antaŭ delegacioj de 15 landoj estis lanĉita ĉe la universitato Ca' Foscari
   de Venecio la vortara platformo ankaŭ en la itallingva versio, kiu enhavas ĉirkaŭ
   dek mil erojn. Senpaga, rapida, facile alirebla kaj intuicia en la uzado, la internacia
   retejo entenas entute 200 000 filmetojn: ĉiu el ili respondas al unuopa signo, malantaŭ
   kiu kaŝiĝas vortoj, ĉiutagaj frazoj kaj nombroj. Penvaloras viziti almenaŭ unufoje
   ĉi tiun originalan retejon, tiel ke la loga surdula mondo, eble ĝis nun nekonata al
   la plimulto, siluetiĝos ĉe la ekrano de via komputilo: cetere ĉiu havas nuntempe aliron
   al Interreto, ni ne volas „aŭdi” pretekstojn!
</p> 
