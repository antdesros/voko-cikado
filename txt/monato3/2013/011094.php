      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Palacaj perturboj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉiutage diligente plenumas siajn taskojn la japana imperiestro Akihito, nun 79-jaraĝa, kaj lia edzino Mitiko, 78-jaraĝa. Ili estas amataj kaj respektataj ... sed ili suferas kordoloron. Ĝin kaŭzas la mensa malsano de la kronprinc-edzino kaj la ĉi-rilata timo pri la estonteco de la imperiestra familio.</p> <p>La kronprinco Naruhito, 53-jaraĝa, kaj lia edzino Masako, 49-jaraĝa, geedziĝis antaŭ 20 jaroj kaj havas unu filinon, Aiko, 11-jaraĝan. Masako estas filino de eksdiplomato, nun juĝisto de la Internacia Kortumo en Hago, Nederlando.</p> <h3>Diplomato</h3> <p>Ŝi pasigis sian junaĝon en Sovetio, Svisio kaj poste Usono, kie ŝi studis ekonomikon ĉe la universitato Harvard. Poste ŝi komencis diplomato-karieron kaj tiel ŝi renkontis la kronprincon. Tamen malfacilis al ŝi alkutimiĝi al la tradicia ceremonia vivmaniero en la imperia palaco.</p> <p>La imperiestra sistemo de Japanio staras sur du piedoj. La unua estas moderna konstitucia monarkio: la imperiestro ne estas ŝtatestro sed simbolo de Japanio kaj de la unueco de la popolo.</p> <p>La dua estas antikva kaj mistera kulto bazita sur mito kaj la ŝintoisma religio. Por multaj japanoj la imperiestra sistemo kun tradiciaj, misteraj ceremonioj estas unu el la fontoj de japaneska kulturo. Tamen multaj ritoj en la imperiestra palaco ne estas publikigitaj kaj eĉ palacaj virinoj estas ekskluditaj.</p> <h3>Kodo</h3> <p>En tia imperiestra palaco la unua tasko de la edzino de la princo estas naski filon kaj subiĝi al ŝia edzo. En 2001 naskiĝis Aiko, sed laŭ la nuna kodo por la imperiestra familio nur viroj per la vira deveno rajtas heredi la kronon.</p> <p>Oni ekdiskutis ŝanĝi la kodon por ebligi al virinoj imperiestrinan kronon. Sed en 2006 naskiĝis filo al princo Humihito, la dua filo de la nuna imperiestro kaj frato de Naruhito. La kodo diktas, ke tiu ĉi filo fariĝu imperiestro, kio provizore nuligas embarasan diskuton, ĉu ŝanĝi la kodon.</p> <p>Turmentadis la kronprincedzinon Masako ne nur la debatoj ĉirkaŭ la eblo, ke iam Aiko fariĝu imperiestrino, sed ankaŭ la adaptiĝo al la palaca vivmaniero. Rezulte, ĉirkaŭ la jaro 2001, ŝi mense malsaniĝis. Ŝi deprimiĝis, malgraŭ la subteno de sia edzo.</p> <h3>Erudiciuloj</h3> <p>Jen ŝi akompanas sian edzon dum oficialaj eksterlandaj vizitoj, jen ne, kaj tio malfaciligas la taskon de estonta imperiestrino. Pro tio kelkaj erudiciuloj eĉ diras, ke Naruhito eksiĝos kiel kronprinco.</p> <p>Jen tragedio de klera virino, kiu ne povas travivi en palaco katenita al rutino kaj tradicio. Ankaŭ ŝia bopatrino Mitiko trairis malfacilaĵojn por adaptiĝi. Nun la japana publiko maltrankvile rigardas la princedzinon kaj la estontecon de la imperiestra familio.</p> <div style="text-align:right">I<span class="mm">SIKAWA</span> Takasi</div> 
