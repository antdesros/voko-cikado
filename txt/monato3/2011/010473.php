      <h3>Ekonomio</h3>
      <h3 class="tem">INTERNACIAJ FINANCOJ</h3>
      <h2>Magraj jaroj</h2>
      <!-- komenco de la enhavo -->

       

       <h3>Ŝtataj ŝuldoj estas altegaj</h3> <p>Ne nur malgrandaj landoj, sed ankaŭ la plej grandaj ekonomiaj potencoj suferas pro grandegaj ŝtataj ŝuldoj. En la eŭrozono la ŝtataj ŝuldoj mezume atingas 90 % de ĉio, kion la ekonomio produktas en unu jaro (la malneta enlanda produkto – MEP). La usona federala ŝuldo jam atingas 100 %, sed ĝi estas 120 %, se oni aldonas la ŝuldojn de la ŝtatoj. La ŝuldo de Ĉinio oficiale estas nur 20 %, sed fakuloj ne fidas la ĉinajn ciferojn kaj taksas ĝin je 80 % de la MEP.</p> <p>Krome la plej granda ŝuldo de okcidentaj ŝtatoj estas nevidebla, ĉar ĝi konsistas el financotaj elspezoj por pensiotoj kaj por sanzorgaj servoj, kiuj ege altiĝos pro la maljuniĝo de la popolo. Se oni enkalkulas tiujn, oni atingas pluroblon. Belgio tial havas virtualan ŝuldon de 375 % de la MEP. Tiuj ciferoj donas impreson pri la monto superenda, se la ŝtatoj provos rimedi kontraŭ tio.</p> <p>Eŭropo krome baraktas por solvi la eŭrokrizon. Por la subteno al Grekio, Irlando kaj Portugalio jam necesis 370 miliardoj da eŭroj kaj la problemoj tute ne finiĝis. Aldoniĝas la privataj ŝuldoj, precipe en landoj, kie pro troa optimismo troigitaj prezoj de nemoveblaĵoj subite falis. Usono tiom pli suferas pro multjara manio de kreditaĉetoj. La privataj ŝuldoj tie egalas al 260 % de la MEP.</p> <h3>Grandaj diferencoj</h3> <p>Se la ekonomio en Ĉinio daŭre kreskos, tio povos malaltigi ĝiajn ŝuldojn. Eŭropo kaj Usono ne aŭ ne plu havas la dinamismon por tiamaniere solvi sian problemon.</p> <p>Feliĉe ekzistas landoj, kie la ŝtata kaso estas malriĉa, sed la civitanoj riĉaj. Tiel estas en pluraj eŭropaj landoj. Ekzemple en Belgio la havaĵo de ĉiuj civitanoj – eĉ sen la nemoveblaĵoj – estas 730 miliardoj da eŭroj, do pli ol la duoblo de la ŝtataj ŝuldoj.</p> <p>Gravas ankaŭ, kiu financas la ŝuldojn: ju pli da mono oni prunteprenas eksterlande (ekzemple Grekio), des pli vundebla la ŝtato restas pro reagoj de la financaj merkatoj. Ŝtato, kiu havas ŝuldojn esprimitajn en ĝia propra valuto, kiel Usono, povas krompresigi monbiletojn. Tio identas al devaluto, sed la civitanoj ne tuj sentas la efikon.</p> <h3>Reveno al realo</h3> <p>La tutmonda financa krizo, en Eŭropo sekvata de la eŭrokrizo, akre montris la vundeblecon de la registaroj. La bonfarto kaj prospero en la plimulto de la ŝtatoj baziĝas sur iluzioj kaj troelspezado je kostoj de venontaj generacioj. Nur se la nuna krizo kondukos al pli realisma longdaŭra ekonomia politiko, la postaj generacioj povos esperi je pli stabila estonteco.</p> <div style="text-align:right">Ivo D<span class="mm">URWAEL</span></div> 
