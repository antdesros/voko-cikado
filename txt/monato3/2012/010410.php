      <h2>Ĥaose de A ĝis Z</h2>
      <!-- komenco de la enhavo -->

       

     <p>Jam en la antaŭparolo de la franclingva libro <i>L'Espéranto de A à Z</i> la aŭtoro André Cherpillod tre emfaze anoncas, por kiuj lia libro estu kaj por kiuj ĝi ne estu. Jen:</p> 
<p><i>La libro ne estas por la neniosciantoj pri Esperanto, kiuj simple regurdas iujn stultaĵojn.</i><br/> <i>La libro ne estas por elitanoj, ne volantaj neŭtralan internacian lingvon, ĉar tiu endanĝerigus la lingvodiktaturon de la angla, la lingvo Wall-Street-a, etendanta siajn tentaklojn ĉien, grandparte subtene al ia povo de la elitanoj.</i><br/> <i>La libro nur eble estas por homoj, kiuj kredas je internacia lingvo donanta egalajn komunikadajn kapablojn al ĉiuj. Sed tiuj prefere aliĝu al loka kurso.</i><br/> <i>La libro estas por homoj – aŭ sen la tempo, aŭ sen la inklino lerni Esperanton, kiuj tamen volas esti honeste informataj pri la fenomeno Esperanto.</i></p>
<p>Bedaŭrinde, la aŭtoro skribas ĉi ĉion france – do por multaj legeble. La recenzanto ne povas imagi pli mallertan agadon plene kontraŭ ĉiuj reguloj de „publikaj rilatoj”. La unuan grupon oni nerekte nomas stulta, la duan – adeptoj de diktaturo, la tria prefere vizitu kurson, la kvaran li kvalifikas kvazaŭ la nelernema. Jam pro tio, la recenzanto mem, certe, ne plu aĉetus ĝin.</p> 
<h3>Honestaj informoj</h3> <p>Evidente la aŭtoro celas al ne-esperantistoj ne-esperantistiĝontaj. Li promesas pri honestaj informoj. Tamen la temoj de la libro estas parte propagandaĵoj, taŭgaj nur por la plej kredemaj, plej sintrompemaj ĝisostuloj inter ni. Kaj fakte nur tiuj povus serĉi laŭ la alfabete ordigitaj kapvortoj, ĉar nur tiuj konus la kapvortojn serĉotajn. Oni tute ne povas serĉi „Lapenna”, se oni ne jam scias, ke iam vivis specifa profesoro Ivo Lapenna. Do jen sistemo plene maltaŭga por ne-esperantisto. Kaj fakte neniu ajn povus serĉi la kapvorton <i>Réactions psychologiques</i> (reagoj psiĥologiaj), ne jam antaŭe sciante, ke tia rubriko troviĝas.</p> 
<p>Kelkaj asertoj estas tre unuflankaj, propagandaĉaj, kaj kiel kutime, plene troigitaj. Sub la kapvorto „<i>Nombre des espérantophones dans le monde</i>” (Kvanto de Esperanto-parolantoj en la mondo) la plej modesta takso estas 2 milionoj, kaj la plej freneza fieras jam pri 15 milionoj.</p> <h3>Netrovebla</h3> 
<p>La aŭtoro traktas multajn „famulojn” kaj „famaĵojn” de Esperantujo – alfabete. Inter tiuj, kaj inter sufiĉe da historiaj tedaĵoj, troviĝas interesaĵoj, ekzemple pri la strukturo de la lingvo Esperanto. Sed tiujn neniu povos trovi – laŭalfabete. Ekzemple, kiel iu ajn legonto trovu la rubrikon „<i>Uniformisation des finales</i>” (Unuformigo de la finaĵoj), se li ne jam antaŭe scias, ke tia rubriko kun specife tiu ĉi kapvorto ekzistas? En la rubriko temas pri la ebla kritiko kontraŭ tio, ke internacia vorto kun fina vokalo – ekzemple „<i>panorama</i>” – ĉe ni por Esperanto, anstataŭ reteni sian internacie bone konatan finan vokalon „a”, nepre ricevu finan substantivigan „o”. Jen rubriko interesa, sed tute ne trovebla.</p> 
<h3>Propagando</h3> <p>Se, foliumante tra la 87-paĝa broŝurita verketo en formato A5, oni hazarde trovas rubrikon lingvan pri Esperanto, la enhavo estas certe leginda. Menciiĝas eĉ kelkaj aliaj planlingvoj. Sed preskaŭ ĉio alia estas pli propagando ol io alispeca. Ankaŭ la rubriko „<i>Sottises débitées par les partisans de l'espéranto</i>” (Stultaĵoj disvastigitaj fare de la adeptoj de Esperanto), en kiu oni mencias kelkajn plene nekonatajn – kaj pro tio plene sensignifajn – stultaĵojn, ne vere ŝanĝas tion.</p> 
<p>Aliflanke, kelkaj gravaj temoj mankas. Ekzemple, pri Esperanto en Interreto estas malpli ol 6 linioj nur kun gugla statistiko kaj mencio de <a href="http://www.lernu.net">www.lernu.net</a>. Sed neniu mencio de PMEG aŭ Revo aŭ Vikipedio. Same pri Esperanto en radiofonio staras absolute nenio. Historie oni menciu eĉ la fifaman „soc.culture.esperanto”, sed ankaŭ tiu ĉi malĉeestas. Ĉu la aŭtoro tute ne havas interretan aliron kaj same ne radion?</p> <h3>Eltondaĵoj</h3> 
<p>Aldone, la alfabeta ordigo faras la libron de kovrilo ĝis kovrilo plene nelegebla. Post ĉiu tria alineo embuskas abrupta ŝokŝanĝo de temo. La tuta libro estas plene malkohera. Estas tiel, se iu kolektus eltondaĵojn el gazetoj dum dek jaroj kaj tiam gluus tiujn en kajeron, ordigante ĉion laŭ la unua litero de la gazeta fraplinio. Kio bone funkcias en alia verko de tiu ĉi aŭtoro – tre laŭdinda verko – (<i>Les outils grammaticaux</i>, vidu la <a href="http://www.esperanto.be/fel/2011/010411.php">recenzon</a>) plene fiaskas tie ĉi. Sed finfine la netrovebleco malmulte ĝenis la recenzanton, ĉar ne troviĝas tiom multe trovinda.</p> <p>Se fakte iu ne-esperantistiĝonta ne-esperantisto vere aĉetus tiun ĉi libron, li ĵetus ĝin post nur kvin minutoj en la ujon, kiun oni en Francio nomas honore al specifa Monsieur Eugène Poubelle (1831-1907).</p> <div style="text-align:right">P<span class="mm">EJNO</span> Simono</div> <h6 class="bib">André Cherpillod: L'Espéranto de A à Z. Franclingva. Eld. Éditions de la Blanchetière, Courgenard, Francio, 2009. 185 paĝoj, glue broŝurita. ISBN: 2-906134-85-6.</h6> 
