      <h3>Ekonomio</h3>
      <h3 class="tem">D. R. KONGO</h3>
      <h2>Kongaj bankoj ekhavas malbonan reputacion</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En 2004 preskaŭ 500 000 usonaj dolaroj (USD) malaperis el la monkelo de <i>Banque Centrale du Congo</i> (BCC), la centra banko de la Demokratia Respubliko Kongo. En la sama jaro, post interna kontrolo, evidentiĝis, ke ĉe <i>Banque Commerciale du Congo</i> (BCDC) mankis 4,5 milionoj da USD.</b></p> <p><i>Western Union</i> estas internacia organizaĵo pri ĝirado de mono. En landoj sen bone funkcianta banka sistemo ĝi estas la sola ebleco por fidinde sendi monon de unu loko al alia. En Kongo ĝi kunlaboras kun BCDC. En multaj kongaj urboj la agentejoj de BCDC ricevas monon kaj plenumas pagordonon por <i>Western Union</i>. Kiam aperis bruoj pri neregulaĵoj, BCDC fermis la giĉetojn de <i>Western Union</i> kaj lanĉis enketon. La enketo montris, ke per komplikaj procedoj dungitoj de la banko malaperigis 4,5 milionojn da USD.</p> <p>Ĉe la BCC la 14an de oktobro 2004 oni konstatis, ke monĉambro, en kiu devis esti diversaj valutoj kun egalvaloro de preskaŭ 500 000 USD, estis malplena. La pordo de la ĉambro estis blokita, kaj, trarompinte ĝian muron – laboro, kiu postulis plurajn tagojn – oni konstatis, ke en ĝi estis nenio. Kvar dungitoj, respondeculoj pri tiu monĉambro, estis maldungitaj kaj transdonitaj al la tribunalo. Laŭ krim-specialistoj ili solaj ne povis fari tion kaj devas esti retoj kaj protektantoj.</p> <p>Depost kelka tempo la faskoj de monbiletoj el la centra banko estis neplenaj: fojfoje ses ĝis dek biletoj mankis en ĉiu ligaĵo de dudek kvin biletoj. Tamen la banko havas kontrolilojn. La ŝtatoficistoj pagitaj tra la centra banko ĝemas pro tiu nesufiĉo, ĉar iliaj salajroj jam ne permesas al ili bone vivi. Ankaŭ iuj faskoj enhavas multajn monbiletojn ŝiritajn aŭ gluitajn kun malsamaj numeroj. Nur la transportistoj kaj la petrolŝipoj facile akceptas tiajn biletojn. Feliĉe, la valoro de la konga franko nun estas stabila.</p> <div style="text-align:right">Serge-Benjamin Z<span class="mm">ANDANDU</span></div> 
