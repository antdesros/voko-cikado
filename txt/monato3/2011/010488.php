      <h3>Moderna vivo</h3>
      <h3 class="tem">ARKEOLOGIO</h3>
      <h2>Rubo de la romianoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Multon da rubo la romianoj postlasis en Vindobona (nun la aŭstra ĉefurbo Vieno) dum la unua ĝis kvina jarcentoj.</p> <p>Antaŭ la romia kastelo, parto de la limeso kontraŭ ĝermanoj loĝantaj norde de Danubo, troviĝis civilula urbo. Tie arkeologoj malkovris rubodeponejon el la tria jarcento. Tiaj restaĵoj spegulas la ĉiutagan vivon antaŭ 1700 jaroj.</p> <p>Metalobjektojn kaj vitraĵajn splitojn oni malofte trovis en la rubamaso. Pro la alta valoro de la materialo oni ilin fandis kaj reuzis.</p> <h3>Afriko</h3> <p>Ostoj de bovoj kaj ŝafoj fojfoje estas rompitaj por ekhavi la medolon. Figoj kaj daktiloj pruvas, ke oni importis nutraĵojn el foraj regionoj, eĉ el la norda Afriko.</p> <p>Sed kvante la plej granda ruboparto estas diversaj ceramikaĵoj. Ankaŭ ili venas parte de malproksime, ekzemple de meza Francio kaj Italio. Temas pri teleroj, pelvoj kaj amforoj, kelkaj el <i>Terra Sigillata</i>, la plej luksa varo. Nur hazarde moneroj falis en la rubon.</p> <p>Nun eblas vidi la romiajn restaĵojn ĉe ekspozicio, kiu daŭros ĝis la 15a de aprilo 2012. Detaloj ĉe la retpaĝo de Vieno-Muzeo: <a href="http://www.wienmuseum.at/de/standorte.html">www.wienmuseum.at/de/standorte.html</a>.</p> <div style="text-align:right">Walter K<span class="mm">LAG</span></div>  
