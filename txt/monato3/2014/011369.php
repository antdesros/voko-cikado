      <h3>Ekonomio</h3>
      <h3 class="tem">EŬROPA UNIO</h3>
      <h2>Lido foriras, eŭro alvenas</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Litovio estos la 19a membro-ŝtato de EU, en kiu ekfunkcios la komuna EU-valuto 
      <i>eŭro</i>. La konsilio de ministroj pri ĝeneralaj aferoj de EU la 23an de julio 2014 firme
      decidis: Litovio plenumas ĉiujn mastriĥtajn kriteriojn kaj ĝi rajtas aliĝi al la eŭrolandoj
      ekde la 1a de januaro 2015. Samtempe estis konfirmita la oficiala kurzo de eŭro kaj
      nacia valuto lido (LTL - 
      <i>litas</i>), kiu ne ŝanĝiĝis ekde la jaro 2004: 1 eŭro daŭre valoros 3,4528 lidojn.
   </b>
</p> 
<p>Litovio jam ekde la jaro 2007 celis funkciigi eŭron, tamen tiam malhelpis duboj pri
   traktado de la inflacio. Tiel Litovio fariĝas la lasta el la tri baltaj landoj, kiu
   ekuzos eŭron. Estonio kun ĝi vivas jam ekde 2011, Latvio ekde 2014.
</p> 
<h3>Du valutoj</h3> 
<p>La 1an de januaro 2015 en Litovio komenciĝos transira periodo, kiam en la lando oni
   povos pagi per du valutoj, tamen restmono estos redonata nur en eŭroj. Malkontanta
   mono la 1an de januaro estos aŭtomate ŝanĝata al eŭroj. Kontantajn lidojn eblos ŝanĝi
   al eŭroj dum ses monatoj ekde la oficiala dato de ĝia funkciigo en ĉiuj bankoj kaj
   poŝtejoj; en la centra banko eblos sen limdato. Ĝis la mezo de 2015 ĉiuj prezoj en
   la lando devos esti indikataj en ambaux valutoj.
</p> 
<p>Okaze de la oficiala aprobo super la enirpordo de la litova centra banko (<i>Lietuvos bankas</i>) aperis speciala horloĝo, kiu montros la tempon ĝis la dato historia – la 1a de januaro 2015. Alterne ankaŭ videblas la firma valuta kurzo.
</p> 
<h3>Plorantaj lidoj</h3> 
<p>Malgraŭ ke la registaro ĝoje asertas, ke la veno de eŭro forte helpos al negoco kaj
   investado, kaj plifaciligos monpruntadon, la litova popolo akceptas la historian decidon
   sen troa optimismo. Laŭ enketado, ĝin aprobas malpli ol 50 % de la loĝantaro. La kialo
   de la maloptimismo estas la timo de kreskantaj prezoj, kiuj povas aperi dum valutoŝanĝo.
   Tamen la centra banko trankviligas la popolon dirante, ke eŭro altigos la prezojn
   je nur ĉirkaŭ 0,2 ĝis 0,3 procentoj.
</p> 
<p>La departemento pri statistiko jam deklaris cent varojn kaj servojn, kies prezoj estos
   atenteme observataj. Se prezoj estos ŝanĝitaj malĝuste povos esti aplikataj punoj
   kontraŭ entreprenistoj.
</p> 
<p>Dum la lastaj monatoj de la cirkulado de la nacia valuto aperis monbiletoj, sur kiuj
   videblas plorantaj naciaj herooj. En la socia reto 
   <i>Facebook</i> (<a href="http://www.facebook.com/pages/Verkiantys-Litai/1440695379530774">www.facebook.com/pages/Verkiantys-Litai/1440695379530774</a>) formiĝis grupo „Plorantaj lidoj”, kiu jam kunigas pli ol 12 000 personojn. Tiuj personoj kontribuas al la adiaŭo de
   la nacia valuto tiel signante monbiletojn. Iniciatintoj de la agado asertas, ke ili
   ne kontraŭas eŭron, tamen ili asertas, ke priplori nacian valuton estas simbola ago.
</p> 
