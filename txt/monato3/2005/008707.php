      <h2>Kiu estis Hillel, kio estis hilelismo?</h2>
      <!-- komenco de la enhavo -->

       

     <p>André Cherpillod jam estas bone konata, pro siaj erudiciaj studoj lingvaj kaj historiaj. En ĉi tiu libreto li entreprenas konigi al ni la judan saĝulon Hilelon, diskuti la hilelismon de Zamenhof, kaj ensume konigi al ni la faktojn – ĉio en tre konciza, sed tre klara formo.</p> <p>Malmulto historia estas sciata pri Hilelo mem, ĉar pri li plu ekzistas nur legendoj kaj diversaj citaĵoj, laŭdire reprezentantaj liajn instruojn. Kion diras la legendoj, tion prezentas, koncize, Cherpillod, kaj grandparte malakceptas, kiel fikciaĵojn. Sed pri la instruoj, ja la plej grava hilela afero, estas malsimile.</p> <p>Rilate al la konservitaj instruoj: ĉar la vivo de Hilelo parte koincidis kun tiu de Jesuo, pluraj el liaj instruoj komparindas kun tiuj de Jesuo. La komparo indikas pli-malpli plenan kongruon. Ĉu Jesuo konis Hilelon? Verŝajne ne, ĉar neniu mencio pri Hilelo troviĝas en la Biblio. Sed Palestino estis malgranda teritorio, kaj estas neverŝajne, ke Jesuo ne estus aŭdinta, se nur nerekte, la instruojn de Hilelo. Ja ambaŭ estis judoj, farizeoj. Laŭ la bibliaj tekstoj, Jesuo tute ne intencis fondi novan religion, nur purigi judismon, tutsame kiel Hilelo, kaj ... sammaniere.</p> <p>Esperantistoj plejparte scias, ke Zamenhof iel rilatis al religia projekto nomita hilelismo, sed pri ĝia karaktero ili kutime ne scias; nur ke ĝi renomiĝis homaranismo, kaj tio siavice mildiĝis al ia „interna ideo” (kiu iom svage signifas pacamon kaj internacian amikecon). Ke hilelismo/homaranismo kaj Esperanto intime interrilatis en la menso de Zamenhof, ni kelkfoje legas, sed malofte pripensas.</p> <p>Zamenhof, antaŭ la aperigo de Esperanto, estis cionisto. Li ankaŭ unue kredis, ke la lingva aparteco de grupoj kaŭzas malamon kaj diskriminacion. Sed li konceptis, ke kredaroj estas eĉ pli gravaj kaŭzoj. Li konceptis, ke judismo mem bezonas reformon, ke oni devas distingi inter la kompleksa regularo de judismo kaj la baza koncepto, kiun Hilelo resumis jene: „Kio estas malagrabla al ci, tion ne faru al cia proksimulo. Ĉi tio estas la tuta Leĝo, la cetero estas nur komento. Iru kaj lernu ĝin.”</p> <p>Rapide Zamenhof ekkonsciis, ke ĉi tiu principo estas ne specife juda, sed aplikebla al la tuta homaro. Li do retitolis sian projekton „Homaranismo”, kio estas universalismo por ĉiuj homoj.</p> <p>Ĉion ĉi Cherpillod prezentas per citado de la originalaj tekstoj. Kiel preskaŭ ĉiam, ĉi tiu lia studo estas zorgege farita. Al tiu, kiu volas scii pri la vera spirita komplekso, el kiu Esperanto estas nur unu parto, ĉi tiu libreto estas rekomendenda.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">André Cherpillod: Hillel la saĝulo, lia vivo, lia instruo. Eld. La Blanchetière, Courgenard, 2005. 36 paĝoj vinktitaj. ISBN 2-906134-67-8.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=hillel">la Retbutiko</a>.
        </h6>
