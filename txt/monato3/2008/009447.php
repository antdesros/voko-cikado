      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Eminenta amiko</h2>
      <!-- komenco de la enhavo -->

       

      <p>Ekde numero 1/81 en nia revuo enketadis detektivo Jano Karál kaj – kompreneble – brile solvis la problemojn. La policano en fikcia lando okupiĝis pri ofte eksterordinaraj krimoj kun nekutimaj intrigoj. Kreis la figuron Johán Valano, kaŝnomo de Claude Piron. Kiam mi proponis al li verki krimnovelojn, li tuj entuziasme akceptis kaj tiel dum kompleta jarkolekto aperis streĉaj kaj krome amuzaj rakontoj de tiu eminenta aŭtoro. Estis plezuro legi la tekstojn de brila stilisto, kiu ĉiam klopodis pri „la bona lingvo”. Poste li daŭre subtenis nian magazinon, sed bedaŭrinde ne plu multe verkis por ĝi. Liaj preferoj kaj interesoj fokusiĝis precipe pri la lingvo mem kaj aparte pri ties psikologiaj aspektoj. Unu el liaj ekkonoj estis, ke Esperanto estas miraklo kaj tial havas problemon, ĉar ne multaj homoj kredas je mirakloj.</p>
<p>Mi ne scias, kiom multaj el vi estas abonantoj de la unua horo kaj do memoras pri la tiamaj krimnoveloj. Sed certe tre multaj el vi memoras pri la sagaca analizo, kiun Claude Piron verkis okaze de la 25jara jubileo de M<span class="mm">ONATO</span> (<a href="http://www.esperanto.be/fel/2004/008264.html"><i>Funkcio kreas organon</i></a>, M<span class="mm">ONATO</span> 2004/4, p. 8). Li skribis pri la emocio, kiu kaptis lin, kiam li ekvidis la magazinon kaj – laŭ mi, iom troige – komparis tiun eventon kun la apero de Esperanto mem. Sed lia eseo, kiel kutime, estis tre sagaca kaj facile komprenebla. Lia precipa tezo estis, ke simple estas tempo por apero de tia magazino kaj ke ĝustatempe jen estis homoj por realigi ĝin. Kaj la lingvisto Piron ne forgesas emfazi la gravan rolon de nia revuo por la evoluo de Esperanto. Li finas sian eseon per danko al ĝiaj produktantoj: „Vi povas fieri.”</p> <p>Nun Claude Piron mortis, 77jara. Esperanto perdis korifeon, kiu estis brila stilisto kaj sagaca psikologo, krome eksterordinare talenta oratoro. Ni perdis eminentan amikon.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
