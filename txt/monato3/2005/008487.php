      <h2>Fina fakturo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Konata ejo en la irlanda ĉefurbo Dublino estis la tiel nomata Orienta Kafejo <i>Bewley</i> [bjule], amata de pluraj generacioj de dublinanoj kaj turistoj. Oni rigardis ĝin kiel unu el la grandaj tradiciaj kafejoj de Eŭropo kaj kiel veran institucion en la irlanda socio. Tamen en aŭtuno la proprietuloj anoncis, ke la kafejo <i>Bewley</i> kaj ĝia filio fermos siajn pordojn antaŭ la fino de novembro.</p> <p>La entrepreno perdis lastatempe pli ol milionon da eŭroj en urbo, kie elspezoj (lokaj impostoj, terena lupago ktp) kvinoble superas tiujn en aliaj eŭropaj ĉefurboj. Sekve 234 dungitoj (parte gejunuloj el malriĉaj landoj, parte veteranaj stabanoj) perdis siajn postenojn. Verŝajne unu el la du kafejoj fariĝos magazeno de laŭmodaj vestaĵoj (magazenoj tiaj jam abundas en la kvartalo): tiel Dublino, jam perdinta tro da siaj tradiciaj entreprenoj, konstruaĵoj kaj vidindaĵoj, iĝos kulture kaj socie eĉ pli malriĉa.</p> <p>La kompanio <i>Bewley</i> estis fondita de Charles [ĉarlz] Bewley, te-importisto kaj membro de respektata angla-irlanda kvakera familio. En 1894 la familio starigis diversloke kafejojn, kiuj dum la 20a jarcento iĝis gravaj elementoj en la urba kulturo. La plej prestiĝa – la Orienta Kafejo <i>Bewley</i> – estis fondita en 1924 en la strato Grafton. Pro la malkovro en 1922 de la tombo de Tutanĥamono la fasado de la kafejo estis ornamita laŭ „egipta” stilo, kaj la dublina artisto Harry Clark [hari klark] desegnis kolorajn vitralajn fenestrojn.</p> <h3>Teatro</h3> <p>La ejo baldaŭ populariĝis. Ne nur la dublina publiko fariĝis klientoj sed ankaŭ eminentaj verkistoj kaj poetoj, ekzemple James Joyce [ĝejmz ĝojs] kaj Mary [mejri] Lavin, kiuj menciis la kafejon en siaj verkoj. Por ordinaraj dublinanoj kaj turistoj la Orienta Kafejo estis preferata renkontejo kaj ripozejo dum laborpaŭzoj aŭ post butikumado. En la domo ĉe strato Grafton troviĝis ne nur la pluretaĝa kafejo, sed ankaŭ eta muzeo-kafejeto kaj malgranda teatro.</p> <p>En la lastaj jardekoj de la 20a jarcento la familio Bewley tamen decidis ne plu daŭrigi la entreprenon. Ne dezirante rompi la tradicion kaj maldungi fidelajn stabanojn, la familio transposedigis la kafejojn al la dungitoj, kiuj provis funkciigi ilin kiel laboristan kooperativon. La eksperimento malsukcesis.</p> <p>Por ke la kafejoj ne malaperu, la tiama ĉefministro Charles Haughey [ĉarlz hoĥe] provizore „naciigis” la entreprenon <i>Bewley</i>, sed nur por tempo sufiĉe longa por permesi, ke alia firmao aĉetu kaj konservu ĝin. Tion faris loka firmao, kiu jam posedis plurajn nutraĵ-entreprenojn en Irlando kaj Usono: la kafejoj estis modernigitaj, sed restis la problemoj. Nun dublinanoj esperas pri lastminuta savanto, sed, se neniu sin prezentos, malaperos definitive plej amata renkontejo. Multaj sentos ĝian mankon; multaj pro ĝi funebros.</p> <div style="text-align:right">Garbhan M<span class="mm">AC</span>A<span class="mm">OIDH</span></div>  
