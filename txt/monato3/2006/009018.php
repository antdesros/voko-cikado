      <h3>Ekonomio</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Litovio sen eŭro</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Evidentiĝis, ke Litovio, male al alia novulo de Eŭropa Unio (EU) Slovenio, ankaŭ en la jaro 2007 restos sen eŭro, al kiu ĝi strebas de longa tempo. Eŭropa Komisiono analizis la situacion kaj konkludis, ke la lando ankoraŭ ne estas preta por funkciigi eŭron en sia teritorio.</b></p> <p>Fakte ĝi plenumis ĉiujn mastriĥtajn kriteriojn, krom unu: ĝi ne sukcesis regi la inflacion. Laŭ kalkulo de la Komisiono, la averaĝa jara inflacio ĝis marto de 2006 en Litovio estis 2,7 % kaj iomete superis la fundamentan indicon 2,6 %.</p> <p>La litovaj aŭtoritatuloj tamen akiris laŭdojn pro siaj penadoj de la EU-komisionano pri ekonomiaj kaj financaj aferoj Joaquín Almunia. Li kuraĝigis la litovojn pro „bonaj rezultoj de ekonomia agado” kaj esprimis la esperon, ke baldaŭ Litovio plenumos ĉiujn kondiĉojn.</p> <p>La iama ministro pri financoj Zigmantas Balčytis al tiu konkludo reagis, dirante, ke EU ankoraŭ ne estas preta al vigla disvolvigo de la eŭro-zono. „Kiam tiu ĉi afero estos pritraktita kaj kiam ni interkonsentos pri kondiĉoj kun la Eŭropa Komisiono, tiam ĉio devus solviĝi”, rimarkigis li. Balčytis aldonis, ke ĝis la sekva etapo de la eŭro-zona disvolviĝo la litova registaro penos havi klarigon pri la kvanto de anoj, laŭ kies donitaĵoj estas kalkulata la inflacia indico. Ja ĉi-foje oni enkalkulis inflacion de landoj, kiuj ne havas eŭron, ekzemple de Pollando.</p> <p>Valdas Adamkus, la prezidanto de Litovio, instigis la registaron aktive agi por strikta buĝeta politiko, ne akcepti popularajn decidojn kaj vigligi reformojn, celitajn al longdaŭra kresko de la ekonomio kaj popola bonstato, por ke en la estonteco ne aperu duboj pri enkonduko de la eŭro. Li rimarkigis, ke eraroj pri la eŭro devas servi kiel leciono por estonte eviti similajn rezultojn en alia integriĝa flanko – aniĝo al la ŝengena areo<sup>1</sup>.</p> <p>Intertempe la kvanto de malsimpatiantoj de la eŭro en Litovio senĉese kreskas. Dum la lastaj ses monatoj ĝi altiĝis je 3,7 % kaj atingis 41,8 %. Samtempe kreskis kvanto de tiuj, kiuj estas por pli rapida apero de eŭro en Litovio: ĝi pliiĝis je 2,1 % al 30,5 %.</p> <div style="text-align:right">Laimius S<span class="mm">TRAŽNICKAS</span></div> <h6 class="pied">1. Schengen [ŝengen] estas la nomo de urbo en Luksemburgo. En Junio 1985 kvin landoj de Eŭropa Unio subskribis traktaton, celante forigon de limkontroloj. Pliaj landoj aliĝis al la traktato dum la pasintaj jaroj. La 15 Schengen-landoj estas Aŭstrio, Belgio, Danio, Finnlando, Francio, Germanio, Grekio, Hispanio, Islando, Italio, Luksemburgo, Nederlando, Norvegio, Portugalio kaj Svedio. Ĉiuj tiuj landoj membras en Eŭropa Unio, escepte de Norvegio kaj Islando. (<a href="http://eo.wikipedia.org/wiki/Schengen">eo.wikipedia.org/wiki/Schengen</a>)</h6>  
