      <h3>Noveloj</h3>
      <h3 class="tem"></h3>
      <h2>Taŭro???  Festo!!!</h2>
      <!-- komenco de la enhavo -->

       

      <p>Ŝprucas varmegaj sunradioj sur la senĉese hompleniĝantan cirkon. Somerĝojiga heleco
   bonvenigas moviĝeman spektantaron, ĉiu serĉe de sia sidloko por de tie ĝui posttagmezan
   toreadon. Fortlaŭtaj la babiladoj garnitaj per grandaj ridoj de la plinombriĝanta
   publiko. Joviala etoso regas en la medio kaj rigardoj jam celas la scenejon por Taŭro???-Festo!!!.
</p> 
<p>Trumpeto elsonoras la komencon de l' spektaklo, aplaŭdoj pliampleksigas la popolan
   voĉbruadon. Ie, meze de la ruĝfarbita ligna trabaĵo ĉirkaŭanta la arenon, oni malfermas
   larĝan pordon por tralasi toreadajn taĉmentanojn: banderilistojn, incitistojn kaj
   ankaŭ taŭroprenantojn; ĉiuj surhavas strikte sidantajn kostumojn bunte orbroditajn,
   kio allogas la okulojn de la spektantoj. Sur bele ornamitaj ĉevaloj sekvas rajdistoj
   tre elegantaj ene de siaj aparte distingindaj jakoj fasonitaj laŭmode de antikvaj
   aristokratoj, kaj fajne ellaboritaj volutaj desegnaĵoj; ĉekape trikornaj ĉapeloj,
   okulfrape plumplenaj.
</p> 
<p>Je la sono de muziko pursangaj rajdbestoj, per artaj volteoj, dancas kaj riverencas
   al la senhalta manklakado. Tradiciajn ritojn ĉiuj tiuj aktoroj solene plenumas en
   la areno, ĝis eliro laŭtakta kun la orkestro.
</p> 
<p>Sonora ordono revenigas unu rajdiston, kiu per ceremoniaj gestoj respekteme akceptas
   de kunulo longan banderilon. Plia trumpetado kaj jen la vico de la ĉefa rolulo de
   tiu festo: 
   <i>la taŭro</i>! Ĝi impetas en la cirkon, kaj per siaj minaceme levitaj kornoj krakege frapas kontraŭ
   la ruĝlinian trabaĵon. Incitistoj klopodas trompi ĝin per tukmovaj fintoj. Renkonte
   al la bestio vigle sin ĵetas ĉevalo, de sur kies selo la rajdanto, per vigla manpuŝo,
   enirigas en la bovdorson ĉepinte hokprovizitan stangon; ĉi tiu krakfendiĝas kaj lasas
   flageton tuj fiere elmontratan al la aplaŭdanta spektantaro. Unu ... du ... tri ...
   kvar ... eĉ kvin kaj pliaj pikiloj vundas la malraciulon, kaj elfluado de sango ekruĝigas
   ĝian dorsfelon. Ĉe ĉiu sukcesa trapiko la spektantaro eksplodigas vivuojn – Taŭro??? – Festo!!!
</p> 
<p>Tradicie, scenoj tiaj ripetiĝas ĉe toreado de ĉiu plia besto enirigita en tiun arenon,
   kaj de alia ..., kaj de alia ... trafe trapikitaj de la rajdistoj; ĉi-tiuj estas kriade
   tiucele instigitaj de la entuziasmoplenaj spektantoj, ĉiam postulantaj de ili elmontradon
   de kiel eble plej riskemaj krudbravegaĵoj. Unu plia, unu plia ... kulmine jen la simultana
   banderilpara enpikado, sekve ankaŭ uzo de spanpikilo, aŭ ankoraŭ la akrobatarta violonstila
   klingenŝovo. Tion oni malavare rekompencas per multoblaj pli viglaj aplaŭdoj kaj vivuoj.
   Post ĉiu batalspektakla sceno la taŭro vagas tien kaj reen kun sia kapo turniĝanta
   ĉirkaŭen serĉe de eliro el tia por ĝi timiga sieĝloko; dume la sur ĝi alkroĉitaĵoj
   supre svingiĝas groteske, kaj interne plu diskarnigas ĝian dorson ... Taŭro??? – Festo!!!
</p> 
<p>Ĉi prezentadojn akompanas etosgajigaj muzikaranĝoj belsonore premiantaj kontentigajn
   plenumadojn de la arenprotagonistoj. Sur sia sidloko ĉiuj amuziĝas, dum la sangoverŝado
   videbliĝas ne nur el la bovodorsoj kaj eĉ surventre de la ĉevaloj pro akra spronado
   de ties mastroj – Taŭro??? – Festo!!!
</p> 
<p>Ree laŭ ordona trumpetado, jen envenas grupo da taŭroprenantoj, kiuj sen ia ajn ruzilo
   ĉemane, maltime oferdonas sian korpon al la furiozo de la bestio. Ĉi tiu muĝas kaj
   hufoskrapas ĝuste antaŭ ol ataki tiujn provoke defiantaj ĝin. La interfrapiĝo estas
   senbrida: la unua viro en la vico alfrontanta la taŭron enbrakigas ĝian kapon, kaj
   forte-firme eltenas sin inter ties kornoj, kie li skuiĝe vojaĝas rapidajn sed longajn
   momentojn. Liaj kompanoj kiel eble plej tuje sin ĵetas sur la virbovon, sed iuj estas
   de ĝi abrupte forpelitaj aeren, flanken, surteren, eĉ sub ĝiajn hufojn. Per violentaj
   kornofrapoj la taŭro karamboligas kaj kaprioligas ilin; unu el ili eĉ falas kun la
   kapo malsupren. Sed neniu rezignas. Ĉiuj obstine insistas, persistas, en kolektiva
   strebado senmovigi sian predon. Danĝere miksiĝas kapoj kun la tie enpikitaj banderiloj,
   kaj la sango verŝiĝanta el la bova dorso makulas jaketojn, ĉemizojn kaj precipe iliajn
   vizaĝojn. Trafita de violenta ekpuŝego, unu taŭroprenanto svenas antaŭ la minace reatakonta
   bruto, tamen, senhezite kaj sentime, iu kamarado ofereme tuj sin ĵetas protektocele
   sur la faligitan korpon sendefendan, tiele ricevante mem la kornopikojn. – Taŭro??? – Festo!!!
</p> 
<p>Dum tiu baraktado, sed de fore kaj kontente sur siaj sekuraj sidlokoj, entuziasme-gaje
   partoprenas ankaŭ spektantoj per kuraĝigaj incitadoj – eĉ histerivirinaj – kaj ridegas, se ĉe pudendo de iu el tiuj kuraĝuloj ŝiriĝas pantalono – Taŭro??? – Festo!!!
</p> 
<p>Poste plia sangceremonio efektiviĝas de sur ĉevalo kun flankoprotektiloj, kontraŭ
   kiuj la taŭro trompige impetas, samtempe ricevante de la rajdisto reajn stangpikadojn.
</p> 
<p>Festokresĉende, alia kuraĝulo – ĉi-foje tutsola, sed kun banderiloj en siaj manoj – montriĝas nun riskeme en kvazaŭa dancado fronte al plia taŭro laste enirigita en
   la arenon. Ekvidante la provokanton, la besto tuj sin ĵetas kontraŭ li, sed maltrafas
   lin, trompite de ties fintoj; tiele lerte sukcesinte enŝovi la du pikilojn sur ĝian
   dorson, la banderilisto forfuĝas rekte al la ruĝa trabaĵo por sin savi de la furioza
   bovo persekutanta lin. Tamen li ne sufiĉe rapidas por eskapi la kornobaton, kiu sur
   lian postaĵon transirigas lin ĝuste kien li celis, kvankam kun kapo malsupren kaj
   kruroj aeren. Laŭta ridegado eksplodas el la spektantaro, kune kun fortaj aplaŭdoj.
</p> 
<p>Laste envenas la matadoro, kiu per stara toreado instigas la koncernan virbovon – ĉi tiu ne kun kornoj globigitaj kiel la antaŭaj – al difinitaj alkuroj tien kaj reen per trompfintoj de sia muletao; je ĉiu specifa
   tukmovo sur la besta dorso ĉiam senĉese akompanas „holeoj”. Subite lia kruro estas kaptita de unu korno, kio igas lin transkapiĝi kaj fali.
   „Ho!!! ... Hororo!!! ...” Tuj tuj ekstaras spektantoj unuanime afliktiĝintaj, incitistoj alkuras por deviigi
   la kornhavulon; sed la toreadoro reekstaras, feliĉe nenio grava! Eksplodas aplaŭdoj.
   – Taŭro??? – Festo!!!
</p> 
<p>La spektaklo pluiris kun pli intensa klakado kaj apogaj krioj. Kvankam de la homspecio
   bredata cele propran amuziĝon tiom riskeman, la bruto ne povas superi tian mastron.
   Oni permesas nur mezuri ĝian neracian animalan povon kompare kun la homa ruzinteligento,
   ne plu! Jen alvenonta la superkulmina momento: ĝia mortigo! – Taŭro??? – Festo!!!
</p> 
<p>Kun glavo ĉemane rekte antaŭen direktata, la toreadoro alfrontas sian predon, logtrompe
   incitas ĝin, kiu naive impetas kontraŭ la defianto, kaj ĉi tiu enŝovas la klingon
   en ĝian dorson, funde ĝiskore. La bestio, tiele mortvundita, oscilas, ĝia peza korpo
   balanciĝas, stumble genuiĝas, la muzelo mergiĝas en la arenpolvon ... Ĝi agonias.
   – Taŭro??? – Festo!!!...
</p> 
<p>Eksplodas vivuoj, bruegas klakaro, muziko pli laŭte sonas. La venkinto, kun la sanga
   spado ĉemane levita, orgojle dankas al tia amaso aklamanta triumfe la homan krudlertecon
   super la animala malracio.
   <i> – Taŭro??? – Festo!!!</i>
</p> 
