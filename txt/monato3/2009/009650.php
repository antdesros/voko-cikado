      <h2>Ĉu vera katoliko?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Laŭ oficiala statistiko 79 % de la litovoj estas romkatolikoj. Malgraŭ tio, ke katoliko estas konsiderata tiu, kiu estas oficiale baptita, pli granda parto de litovaj katolikoj ne estas veraj – ili malofte vizitadas preĝejon, ilia ĉiutaga vivo estas iom for de katolikaj doktrino kaj reguloj. Pri sia katolikeco ili rememoras nur ĉe geedziĝo aŭ funebra ceremonio.</p> <p>Ideala katoliko estas tiu, kiu festas la „Sinjoran tagon”, dimanĉon. Ĝenerale la litovoj amasvizitas siajn preĝejojn nur dum la tago de la mortintoj, Kristnasko kaj Pasko. La litova eklezio tiurilate havas modestan postulon: necesas almenaŭ unufoje dum la jaro, Paske, fari konfeson kaj akcepti komunian sakramenton.</p> <p>Litovaj sociologoj rimarkigas, ke dum multaj jaroj 79 % de la litovoj identigas sin kiel katolikoj. Tio ne signifas, ke ĉiuj estas praktikantaj katolikoj. Pri sia katolikeco la popolo senkaŝe deklaris nur de post la ŝtata sendependiĝo en la jaro 1990. Samtempe aldonindas, ke la litovaj katolikoj estas dispartigindaj en du kampoj: ne veraj kaj veraj, kies ĉiutaga vivo strikte baziĝas sur katolikaj valoroj. Al la lastaj apartenas ĉirkaŭ kvinono de la litova socio.</p> <p>Krome, spertuloj parolas pri siaspeca socia premo – devigo esti katoliko. Ekzemple, estas normo geedziĝi en la preĝejo, malgraŭ ke Litovio estas unu el la progresintaj EU-landoj laŭ statistiko de divorcoj. Dum la unuaj jaroj post la resendependiĝo rimarkiĝis pli granda kvanto de tiuj, kiuj neis ajnan konfesion, malgraŭ ke nun la plej granda parto deklaras sin katolikoj. Dum la sovetia periodo la kredantoj estis en malfavora situacio. Post la resendependiĝo, kontraŭe: ne estas bonmore esti laiko, kio estas konsiderata kiel postrestaĵo, relikvo de la sovetiaj tempoj.</p> <p>Tamen la litova eklezio ne maltrankviliĝas pri la religiemo de siaj samlandanoj. Se oni komparas ĝin kun la situacio en aliaj landoj de Eŭropo, kie preĝejon vizitadas nur 4-5 % de la katolikoj, Litovio aspektas tute normale. La kvanto de edziĝoj en la preĝejo daŭre kreskas. Pli bedaŭrinde estas, ke pri sia katolikeco multaj litovoj memoras nur, kiam ili intencas geedziĝi aŭ devas entombigi proksimulojn.</p> <p>En pasintjara sociologia enketo evidentiĝis, ke 80,2 % de la respondintoj atribuas sin al romkatolikoj, 2,7 % al la ortodoksa kristanismo, 0,7 % al malnovritanoj, 0,6 % al evangeliaj luteranoj, 0,4 % al reformitoj. Ĉiu deka indikis pri nenia aparteno al religio. 33 % de la respondintoj indikis, ke preĝejon ili vizitas nur dum religiaj festoj, Kristnasko kaj Pasko, 17 % tion faras nur unufoje en la jaro aŭ eĉ pli malofte, 16 % almenaŭ unufoje en monato, 9 % kelkfoje dum monato, 8 % almenaŭ unufoje en semajno. 13 % ne vizitas preĝejon kaj ne partoprenas religiajn ceremoniojn. Malgraŭ ĉio 78,6 % sin kvalifikas kiel kredantojn kaj 16,4 % kiel indiferentajn al religio.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
