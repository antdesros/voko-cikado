      <h3>Moderna vivo</h3>
      <h3 class="tem">VITO-KULTIVADO</h3>
      <h2>Je via sano!</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Hungario vino havas tradicion kaj feston. Aparte gravas la rikolto-festo, kiu indikas finon ne nur de la rikolto, sed ankaŭ de la peza agrikultura laboro. Kamparanoj estas liberigitaj de devoj kaj zorgoj; samtempe ili gaje anticipas la konsumotan vinon, kiu devas esti fortrinkita por liberigi ankaŭ la barelojn.</p> <p>La festo okazas kutime fine de septembro, komence de oktobro. Tamen iuloke, ekzemple okcidente de la tokaja regiono, aperas t.n. neĝvino: la beroj estas rikoltataj, nur kiam la unua frosto aŭ la unuaj neĝeroj ilin blankigas.</p> <p>Festoj okazas preskaŭ tra la tuta lando. Ili komenciĝas per parado de ĉaroj kaj ĉevaloj. La ĉaroj estas ornamitaj per floroj kaj per grapoloj. Festantoj surmetas naciajn vestaĵojn (se ili estas haveblaj); ornamitaj estas ankaŭ la ĉevaloj. Akompanas la ĉarojn muziko kaj kantado; de sur la ĉaroj oni proponas vinon, el bareloj aŭ kruĉoj. Fojfoje la veturantoj trinkas rekte el boteloj.</p> <h3>Vestaĵoj</h3> <p>Portas la virinoj iuloke longan, buntan jupon kun unukolora, sed brodita kitelo; aliloke iom malpli longan, ĝisgenuan jupon kun almenaŭ deko da subjupoj. La kapornamaĵoj montras la familian staton de la virinoj: ĉu fraŭlino, ĉu edzino. La viroj vestas sin pli diskrete per blanka ĉemizo kun blanka aŭ jam pli ofte nigra pantalono kaj botoj.</p> <p>La rikolta balo pli frue estis grava evento: ne nur la komenco de pli trankvila, malpli labordona periodo, sed ankaŭ okazo por danci, interkonatiĝi, amindumi kaj plifirmigi amrilatojn. Nuntempe oni dancas longe, eble ĝis mateniĝo.</p> <h3>Renomaj</h3> <p>Hungaraj vinoj estas konataj ekster sia propra lando. Renomaj estas ekzemple la vinoj de Badacsony [badaĉonj] pro <i>Szürkebarát</i> (Griza Monaĥo) kaj <i>Kéknyelü</i> (Blu-tiga), tiuj de Tokaj pro <i>Aszu</i>, Villány pro <i>Kékoportó</i> kaj Eger pro <i>Leányka</i> (Fraŭlineto).</p> <p>Ĝis antaŭ du jarcentoj ankaŭ en la regiono de Buda estis kultivataj vinberujoj: epidemio, la radik-malsano filoksero, ilin detruis. Sed ankoraŭ troviĝas resovaĝiĝintaj vitoj sur la sunaj deklivoj de la ĉirkaŭaj montetoj kaj ankaŭ enĝardene zorgeme rastritaj, alligitaj vito-linioj.</p> <p>Apenaŭ eblas imagi ĝardenon sen vinber-laŭbo: agrablas la vito grimpanta sur la tegmenton, donanta delikatan ombron en la verando, ankaŭ rubenkoloraj, acidetaj beroj, kiujn oni manĝas konkurante kun la merloj.</p> <div style="text-align:right">Judit F<span class="mm">ELSZEGHY</span></div>   
