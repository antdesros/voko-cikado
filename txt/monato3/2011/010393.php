      <h3>Politiko</h3>
      <h3 class="tem">EBUR-BORDO</h3>
      <h2>Dividita lando, komunaj esperoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Post monatoj da interna milito estas fine inaŭgurita kiel prezidanto de Ebur-Bordo Alassane Ouatarra. Li estis elektita en novembro 2010, sed la tiama prezidanto, Laurent Gbago, rifuzis akcepti la rezulton.</p> <p>Pro la sekva politika krizo mortis pli ol 3000 ebur-bordanoj, kaj fuĝis plia miliono al najbaraj landoj, kiel Burkino, Liberio kaj Ganao. Fine, en aprilo, soldatoj el la norda parto de Ebur-Bordo bombardis la prezidantan palacon en Abidjano, la ekonomia ĉefurbo, kaj kaptis Gbago-n.</p> <p>Jam suferis ebur-bordanoj pro tri jaroj da interna milito inter 2002 kaj 2005, kiam la lando estis dividita inter kristana sudo kaj islamana nordo. Intervenis Unuiĝintaj Nacioj (UN), kiuj ellaboris kondiĉojn por reunuiĝo kaj fine elektoj, kiujn dum longa tempo Gbago rifuzis permesi malgraŭ internacia premado.</p> <h3>Palaco</h3> <p>Fine cedis Gbago, kiu okazigis elektojn, sed malgraŭ ĉeurna venko de Ouatarra, kun 54 % de la voĉoj, Gbago deklaris, ke venkis li per 51 % kaj insistis resti en la prezidanta palaco.</p> <p>Proponis al Gbago azilon ŝtatoj kiel Usono, Francio kaj Niĝerio, sed en decembro li inaŭguris sin mem kiel prezidanton. Sekvis milito, mortoj kaj mizero tra la lando.</p> <p>La inaŭguron de Ouatarra ĉeestis la prezidantoj franca Sarkozy kaj niĝeria Jonathan, kune kun la ĝenerala sekretario de UN Ban Ki Moon. Nun restas la demando, ĉu la nova prezidanto povos alporti pacon al Ebur-Bordo kaj unuigi landon etne kaj religie dividitan.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> 
