      <h3>Scienco</h3>
      <h3 class="tem">REAGO</h3>
      <h2>Laste (ĉu?) pri scienco kaj religio</h2>
      <!-- komenco de la enhavo -->

       

       <p>Neniam venis al mi en la kapon la ideo „kritiki” sciencon. Dum 25 jaroj mi estis instruisto, unue pri kemio kaj poste pri komputscienco.
   Mi do havas la esperon iomete kompreni pri scienco (kompatindaj estus miaj gelernantoj,
   se ne ...).
</p> 
<h3>Dentisto</h3> 
<p>Mi ja sufiĉe fidas je scienco: se ne mi ne kuraĝus sidi ĉe la dentisto, mi devas konfesi.
   Se oni atente legas la artikolon, oni vidas, ke mi plendas nur pri tio, ke de religio
   oni petas certecon tian, kian neniu homo mense sana kuraĝus peti de scienco. Kompreneble
   scienco neniam atingos la staton de plena pruvo, kaj tio tute ne estas malforto (kaj,
   laŭ mia gusto, estas pli bone tiel, ĉar ĉiam staros antaŭ nia menso defioj). Ne hazarde
   la ĉarma Pastro Brown (dankon, s-ro Chesterton) atentigis pri tio, ke kritiki racion
   estas karakterizaĵo de malbona teologio.
</p> 
<h3>Fido</h3> 
<p>Mi ripetas: Dio ne estas ŝtopilo nepre uzenda ĉie, kie scienco ankoraŭ havas nenion
   por diri, nek scienco turniĝu en religion (afero eĉ pli malbona, miaopinie). Ili ne
   uzurpu funkciojn, ne kunpuŝiĝu, ne subpremu unu la alian. Kurioze, scienco kaj religio
   similas unu la alian pro tio, ke ambaŭ ripozas sur ia fido. Scienco postulas de ni
   du grandajn elmontrojn de fido: unue kredi, ke la universo estas kohera, kaj due,
   ke ĝi estas por ni komprenebla. Neniu supera instanco certigis nin pri tio: tion ni
   supozas kaj akceptas; ĝis nun ĉio bone funkcias, kaj kompreneble ni same agos estonte.
</p> 
<h3>Aksiomoj</h3> 
<p>Laŭ vi, „Religiaj asertoj siaflanke baziĝas ĉefe sur subjektiva kredo, ne sur faktoj kaj logikaj
   konkludoj”. Eble vi iomete miskomprenas religion. Religioj estas same logikaj, kiel kiu ajn
   scienco, kondiĉe ke oni akceptu iliajn aksiomojn (laŭ PIV: aserto, apartenanta al
   certa aro da asertoj, kiun oni ne pruvas, sed el kiu sekvas per matematikaj pruvoj
   ia teorio). Pri la rolo de subjektiveco en sciencoj, ajna lerta historiisto de sciencoj
   havus multon direndan, tutcerte.
</p> 
<h3>Feinoj</h3> 
<p>Krome: ne pensu, ke nur tio, kion oni kapablas mezuri, estas afero vera, „ne subjektiva”. Amo, bono, malbono estas ege subjektivaj, sed mi povas aserti al vi, ke ili estas
   sufiĉe realaj. Kiom pezas penso? Ĉu oni sukcesis esprimi matematike simfonion de Betoveno?
   Tikla vorto, „subjektiva”. Ne ĉio ĉiam pruveblas. „Ne eblas pruvi, ke akiritaj trajtoj ne heredeblas. Samkiale, oni ne povas pruvi, ke
   ne ekzistas feinoj” (Dawkins, 
   <i>La blinda horloĝisto</i>).
</p> 
<h3>Entrudulo</h3> 
<p>„Do, se scienco okupiĝas pri la esplorado de la tuta universo, pri kiu afero okupiĝas
   religio?”. Interese. Neniel la sinteno de scienco aŭ de religio estu: „Mi okupiĝas pri tio ĉi, do prefere iru for, entrudulo”. Ili ambaŭ faras demandojn, nur el diversaj vidpunktoj. En la pasinteco religio provis
   regi unusola. Scienco bonvolu ne ripeti la saman eraron. Nu, la diskuto evidentiĝis
   multe pli interesa ol mia artikolo! Ĉu ni petu de la kelnero alporti trian trinkaĵon?
</p> 
