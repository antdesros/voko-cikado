      <h3>Moderna vivo</h3>
      <h3 class="tem">KOMUNIKILOJ</h3>
      <h2>Ĉine, ĉu ne?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Propran poŝtelefonon posedas pli ol triono de la malagasoj. En ĉiuj urboj en Madagaskaro vendiĝas poŝtelefonoj, kaj tri kompanioj batalas por domini la merkaton.</p> <p>La plej sukcesaj kompanioj estas ĉinaj, kies telefonoj estas plej malmultekostaj. Akiri telefonon eblas kontraŭ 10 eŭroj, sufiĉe alta sumo en Madagaskaro.</p> <p>Multaj komercistoj preferas butikumi rekte en Ĉinio, ĉefe en Guangzhou, por akiri bonan prezon. La ĉina konsulo liveras senprobleme vizon al malagasoj.</p> <h3>Bankoj</h3> <p>Kvankam bankoj limigas la kvanton de devizoj kunporteblaj, komercistoj trovas solvon kaj ŝanĝas valutojn ĉe la nigra merkato. Se vojaĝanto sukcesas trapasi la flughavenan doganejon, tiu povas aĉeti multe da varoj en Ĉinio.</p> <p>Poŝtelefono estas unu el la plej serĉataj varoj. Tiu fabrikita en Ĉinio tuj rekoneblas pro sia duobla SIM-karteto. Intertempe pli kaj pli aplikeblas telefonoj: 24-horojn tage rapidaj mesaĝoj, kun avantaĝa prezo por gejunuloj; sendi/ricevi monon, eĉ de eksterlando; retumi.</p> <p>Tiu lasta punkto entenas multe da problemoj. Ofte nur malforte riceveblas telefon-signalo pro teknikaj fuŝoj ĉe la ĉina poŝtelefono. Cetere, la ene muntita TTT-legilo ne ĉiam taŭgas por malfermi retpaĝon. Fojfoje ne eblas elŝuti dokumentojn, aŭ la telefono simple malŝaltiĝas.</p> <h3>Retkafejo</h3> <p>Ĝenerale duoblas la minuta retkosto de la poŝtelefono kompare kun tiu en publika retkafejo. Tial retumi restas lukso por la malagasoj.</p> <p>Tamen furoras ĉinaj poŝtelefonoj pro kulturaj kialoj. Gejunuloj ŝatas ilin por ĉiumomente aŭskulti muzikon kaj spekti filmojn. Oni pagas malmultekoste por enigi en la memorkarton kantojn kaj filmetojn.</p> <p>Malgraŭ ĉinaj markoj, poŝtelefonoj fabrikitaj en okcidento kaj en Japanio popularas. Ili notindas pro siaj kvalito kaj vivdaŭro. Tamen eĉ la plej simplaj tiaj aparatoj kostas duoblon de la baza monata salajro. Do nur riĉuloj kaj altranguloj ilin posedas.</p> <div style="text-align:right">Henriel F<span class="mm">IDILA