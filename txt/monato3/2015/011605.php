      <h3>Medio</h3>
      <h3 class="tem">AKTUALE</h3>
      <h2>Interrilato de la oceano kaj klimatŝanĝiĝo</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Komence de decembro de ĉi tiu jaro okazos kunsido de la Konferenco de la Partioj (COP21),
      en kiu ĉiuj landoj de la mondo devus akcepti pakton pri klimatŝanĝiĝo. La celo estas
      limigi la tutmondan varmiĝon de la tero ĝis 2<sup>o</sup>C kompare kun la nivelo de la antaŭindustria epoko.
   </b>
</p> 
<h3>Forcejaj gasoj</h3> 
<p>Ekde ĉirkaŭ 1750 p.K. homoj signife influas la klimaton tutmondan. El la bruligado
   de fosiliaj brulaĵoj rezultas altiĝintaj koncentriĝoj de forcejaj gasoj, inter ili
   karbondioksido (CO<sub>2</sub>). Ĉi tiuj forcejaj gasoj povas sorbi infraruĝan radiadon, kiu estas elsendata de
   la tero. Konsekvence, parto de la radiado estas sendata ree al la surfaco, kio signifas,
   ke parto de la varmo estas konservata. Pro tio la averaĝa temperaturo de la atmosfero
   kaj de la supraj 700 m de la oceano rimarkeble leviĝis. Laŭ libro aperigita en 2014,
   la oceano ensorbis ĉirkaŭ 93 % de la plia varmo kaŭzita de la plimultiĝo de forcejaj
   gasoj.
</p> 
<p>La rolo de la oceano kiel parto de la klimato estas preskaŭ ĉiam subtaksata. Pro la
   vasteco de la oceano kaj ties povo sorbi CO<sub>2</sub> malaperas triono ĝis duono de la homfarita CO<sub>2</sub>. Akvo havas grandan specifan varmon, kio signifas, ke la oceano havas grandegan varmokapaciton.
   Pro tio, krom la homfarita CO<sub>2</sub>, ankaŭ multe da homfarita varmo malaperas en la oceanon. La oceano acidiĝas. Fakte,
   la averaĝa pH-valoro malpliiĝis de 8,1 ĝis 8,0 ekde la antaŭindustria epoko.
</p> 
<h3>Acidiĝo</h3> 
<p>La CO<sub>2</sub> en la aero emas al ekvilibro kun la CO<sub>2</sub> en la supra stratumo de la oceano. Pro tio la oceano sorbas CO<sub>2</sub>. Tiu gaso estas acido en akvo, do la oceano acidiĝas. Fakte, 40-50 % de la konservado
   de la homkaŭzita karbono okazas en la oceano, dum la resto konserviĝas en la ter-biosfero.
</p> 
<p>Pro la varmiĝo kaj la acidiĝo de la oceanoj ekosistemoj ŝanĝiĝas. Multaj specioj malaperas,
   sed aliaj specioj prosperas. La rezulto estas malkresko de la biodiverseco. Ankaŭ
   fiŝkaptado el la oceano povas kontribui al tio. Tre konata definitiva efiko de homfarita
   CO<sub>2</sub> estas la fakto, ke en la daŭro de kelkaj jarcentoj ni bruligis fosilian brulaĵon,
   kiu bezonis milionojn ĝis centmilionoj da jaroj por formiĝi. Ni devos atendi milionojn
   da jaroj, antaŭ ol signifaj kvantoj da karbono estos amasigitaj (temposkalon de pli
   ol 100 000 jaroj ni povas nomi „definitivaĵo”).
</p> 
<p>Sur pli mallongaj (sed ja longaj) temposkaloj kalcia karbonato estas konservata en
   la sedimentoj de la oceano. Kiam organismoj kun kalcikarbonataj skeletoj (kiel kokolituloj,
   kiuj fiksas CO<sub>2</sub> per fotosintezo) mortas, ili subakviĝas al la profundo de la oceano. Kvankam parto
   solviĝas en la marakvo, alia parto iras en la sedimenton. Ĝis kiu grado kalcia karbonato
   solviĝas en la profunda oceano, dependas de la premo (do profundo) kaj de la pH-valoro.
   Ju pli da CO<sub>2</sub> troviĝas, des pli da kalcia karbonato solviĝas.
</p> 
<h3>Bufro</h3> 
<p>En partoj de Pacifiko la skeletoj tute solviĝas je granda profundo. Pro tio neniu
   kalcia karbonato enfosiĝas. En Atlantiko tio ne okazas: tie la kalcia karbonato atingas
   la profundon de la oceano. Rezulto de la enfosado de kalcia karbonato estas, ke ĝi
   funkcias kiel bufro kontraŭ oceanacidiĝo: pro solvado de kalcia karbonato el la sedimento
   malpliiĝas la CO<sub>2</sub>-koncentriĝo en la marakvo, pro kio kokolitoj solviĝos malpli rapide. Pro la acidiĝo
   de la oceano eblas, ke Atlantiko iĝas kiel Pacifiko: la tuta kalcia karbonato solviĝus
   antaŭ ol atingi la sedimenton. Rezulte malpli da karbono enfosiĝus kaj pli da CO<sub>2</sub> pli longe restus en nia klimatsistemo. Mallonge, la aldonatan CO<sub>2</sub> kaj la varmon sorbas la oceano, kio havas signifajn efikojn sur kaj mallongaj kaj
   longaj periodoj. Kelkaj efikoj estas neinversigeblaj (aŭ, pli precize, restas dum
   tre longega tempo en nia klimatsistemo). Plej bone estas ekstreme malpliigi la CO<sub>2</sub>-eligon, aŭ eĉ eligi negative, tiel ke la damaĝo estos limigita.
</p> 
<p>La ideo vidi la klimatsistemon kiel ne pli ol la atmosferon, kaj eventuale la teron,
   do malĝustas. Ĉi-jara publikaĵo montras koncize la rilaton inter la oceano kaj la
   klimato kaj donas konsilojn por la COP21.
</p> 
<h3>Komputilsimuladoj</h3> 
<p>La rakonto ĉi tie ne estas nova. Jam en raporto el la jaro 1979 oni konsideris la
   efikojn de klimatŝanĝiĝo. Komputilaj simuladoj enhavis la oceanon, kompareble kun
   tio, kion faras klimatsciencistoj hodiaŭ, kvankam nun pli detale. La rezulto de la
   esploro, komisiita de la usona registaro, estis, ke pli realismaj modeloj prognozis
   plivarmiĝon de la tersurfaco de inter 2<sup>o</sup>C kaj 3,5<sup>o</sup>C. Tio estas pli-malpli la sama konkludo, kiel tiu de la lasta raporto de la Interregistara
   Grupo pri Klimatŝanĝiĝo (angle: Intergovernmental Panel on Climate Change [IPCC]).
   Tio signifas, ke la klimatesplorado de ĉi tiu jarcento havas la saman prognozon kiel
   antaŭ tridek jaroj (ĉar la IPCC-raporto nur estas taksado de la koncerna scienca literaturo).
</p> 
<p>La koincido de la necertecoj rilatas al la fakto, ke ni nun havas pli da scio pri
   la klimatsistemo kaj ankaŭ pri ĝiaj necertecoj, sed evidentas, ke ni jam sciis la
   problemojn kaj jam havis la sciencan bazon. Oni eĉ povas retroiri al la fino de la
   19a jarcento, kiam jam klaris, ke CO<sub>2</sub> estas forceja gaso kaj ke nia eligado iĝos pli kaj pli amasa.
</p> 
