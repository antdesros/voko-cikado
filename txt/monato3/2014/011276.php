      <h3>Lingvo</h3>
      <h3 class="tem">LINGVAROJ</h3>
      <h2>Ĉu la angla ne devenas de la praangla?</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Povas esti erara la disvastiĝinta opinio, ke la britoj prunteprenis vortojn kaj konceptojn
      de la norvegaj kaj danaj vikingoj kaj iliaj idoj. La realo estas, laŭ la norvego Jan
      Terje Faarlund el la Universitato de Oslo, alia: la lingvo nuntempe konata kiel „angla” kaj en ĉiuj kontinentoj disvastiĝinta estas mem skandinav-devena.
   </b>
</p> 
<p>Eble oni ne scias, kiom facile estas por la norvegoj lerni la anglan. La kialo estas
   ne nur la ĝenerala simileco de la anglaj vortoj kun la respektivaj norvegaj. Estas
   io plia: la baza strukturo mem de la angla frape similas al tiu de la norvega, kaj
   tio estas fenomeno ekstreme malofta. Tial aron da eraroj, kiujn alilingvaj homoj regule
   faras, la norvegoj nature evitas pro la simileco, sendepende de la vorttrezoro, de
   la du gramatikoj mem. Do la angla, laŭ aserto de profesoro Faarlund, ja estas kroma
   skandinava lingvo, apud la norvega, la dana, la sveda, la islanda kaj la feroa: temas
   pri lingvo alivorte apartenanta al la nord-ĝermana lingvobranĉo.
</p> 
<p>Ĉi tiu ideo, unuafoje aperinta en norvega ĵurnalo antaŭ jarduo, estas tute nova
   kaj kontrastas kun tio, kion opinias aliaj esploristoj pri lingvistiko kaj la ĝenerala
   publiko, laŭ kiuj la angla estas rekta devenaĵo de la praangla. Ĉi-lasta, nomata ankaŭ
   anglosaksa, estas okcident-ĝermana lingvo, kiun la angloj kaj la saksoj kunportis
   el norda Germanio kaj suda Jutlando tiam, kiam ili ekloĝis sur la britaj insuloj,
   en la 5a jarcento.
</p> 
<h3>Politika suvereneco</h3> 
<p>Laŭ Faarlund, la angla, kian ni konas hodiaŭ, estas tamen rekta ido ne de la praangla,
   sed de la lingvo parolata de la skandinavoj, kiuj ekloĝis sur la britaj insuloj multajn
   jarcentojn antaŭ la konkero de la lando fare de franc-parolantaj normanoj, kiu okazis
   en 1066. La norvega sciencisto atentigas, ke la praangla kaj la moderna angla estas
   verŝajne du tre disigitaj lingvoj. Kiel povas okazi tio? „Niaopinie, ĉar iam la praangla simple mortis. Anstataŭe transvivis la tiel nomata
   nordia, kvankam ĝi estis ia nordia forte influita de la praangla.”
</p> 
<p>La kunvivado inter angloj kaj skandinavoj grandparte estis malpacema. Ambaŭ batalis
   por gajni politikan suverenecon. La skandinav-devenaj idoj de la vikingoj ekhavis
   la superregon en la orientaj kaj nordaj partoj de la lando. La Dan-leĝujo (angle 
   <i>Dane-law</i> [dejnlo], skandinave 
   <i>Danelag</i> [dánelag]) troviĝis sub nordiaj estroj dum duona jarcento. Samkiel plej multaj koloniantoj,
   ankaŭ la nordi-parolantaj loĝantoj trovis neniun kialon por transpreni la lingvon
   de la lando, en kiun ili alvenis. „Aparte grava geografia argumento en nia esplorado estas, ke la areo East Midlands,
   kie la parolata lingvo estis evoluonta al la moderna angla, preskaŭ tute koincidas
   ekzakte kun la dense loĝata suda parto de Dan-leĝujo”, diras la norvega profesoro.
</p> 
<p>Dum la periodo, kiu sekvis la venon de la normanoj, la lingvo multe ŝanĝiĝis. La mizeraj
   kondiĉoj, en kiuj tiam vivis la popolo, kondukis al kompleta kunfandiĝo de la du grupoj
   ĝis tiam disigitaj, t.e. la parolantoj de la praangla kaj la parolantoj de la nordia
   lingvo. Kaj tio estigis la antaŭstadion de la moderna angla (konatan kiel mez-angla).
</p> 
<h3>Ĉiutagaj objektoj</h3> 
<p>De la norvegidaj kaj danidaj loĝantoj de Dan-leĝujo la lingvo asimilis multajn vortojn.
   En la jena frazo, ekzemple, ĉiuj vortoj estas nordi-devenaj: „<i>He took the knife and cut the steak</i>” (= „Li prenis la tranĉilon kaj tranĉis la stekon”). En iu vario de la norvega oni havas „<i>Han tok kniven og kutta steika</i>”. Nur „<i>he</i>”, „<i>the</i>” kaj „<i>and</i>” originas el la praa angla. „Aparte interese estas, ke oni asimilis vortojn por ĉiutagaj objektoj, por kiuj la
   lingvo jam posedis vortojn. La normalo ja estas prunti vortojn pro la bezono nomi
   aĵojn kaj konceptojn novajn. En la angla tamen okazis laŭ iuj preskaŭ inverse: la
   ĉiutagaj vortoj estas ĉiuj nordiaj, kaj ili estas frape multaj”, sciigas la lingvosciencisto. Ekzemple: „<i>bag</i>”, „<i>band</i>”, „<i>big</i>”, „<i>birth</i>”, „<i>both</i>”, „<i>bull</i>”, „<i>cake</i>”, „<i>call</i>”, „<i>cast</i>”, „<i>cross</i>”, „<i>die</i>”, „<i>dirt</i>”, „<i>dream</i>”, „<i>egg</i>”, „<i>flat</i>”, „<i>gain</i>”, „<i>gift</i>”, „<i>guest</i>”, „<i>hug</i>”, „<i>husband</i>”, „<i>kid</i>”, „<i>law</i>”, „<i>lift</i>”, „<i>loan</i>”, „<i>mistake</i>”, „<i>race</i>”, „<i>root</i>”, „<i>seat</i>”, „<i>sister</i>”, „<i>skill</i>”, „<i>skin</i>”, „<i>skirt</i>”, „<i>sky</i>”, „<i>steak</i>”, „<i>Thursday</i>”, „<i>trust</i>”, „<i>ugly</i>”, „<i>want</i>”, „<i>weak</i>”, „<i>window</i>”, „<i>wing</i>”, „<i>wrong</i>” kaj multaj aliaj. La esploristoj opinias, ke la praangla jam havis 90 % el ĉi tiuj
   konceptoj en sia propra vort-stoko. La skandinava elemento tamen ne limiĝis al la
   vortprovizo, kio estas kutima, kiam lingvoj interkontaktiĝas: kvankam lingvo malfermas
   la pordon al amaso da novaj vortoj, ĝi tamen retenas sian propran gramatikon. Ĉi tio
   havas la valoron de kvazaŭ-universala leĝo. Aldone en Anglio estis transprenitaj elskandinave
   ankaŭ gramatikaj vortoj kaj morfemoj, t.e. la minimumaj abstraktaj lingveroj kun signifohava
   funkcio, kaj ili pluvivas en la hodiaŭa angla.
</p> 
<h3>Sintakso kaj strukturo</h3> 
<p>Sed tio ne sufiĉas: profesoro Faarlund montras, ke la fraza konstruo en la mez-angla
   – kaj per tio ankaŭ en la moderna angla – estas skandinava kaj ne okcident-ĝermana. „Sintakson kaj strukturon oni ne facile pruntas de unu lingvo al la alia. Nun ofte
   okazas, ke en nacilingvoj oni utiligas unuopajn vortojn anglajn, kaj tio multajn afliktas.
   Tamen la vort-strukturo de la norvega neniel estas influita de la angla. Ĝi estas
   firma kaj neŝancelebla. La samo okazas pri la strukturo de la angla: ĝi estas praktike
   ne influita de la praa angla. En preskaŭ ĉiuj punktoj, en kiuj la angla sintakse diferencas
   de la aliaj okcident-ĝermanaj lingvoj (precipe la germana, nederlanda kaj frisa),
   ĝi havas la saman strukturon, kiel la skandinavaj lingvoj”. Jen kelkaj ekzemploj.
</p> 
<p>1) La angla kaj la skandinavaj lingvoj havas la rektan objekton post la verbo:</p> 
<p>„Mi legis la libron”. Angle: „<i>I have read the book</i>”. Norvege: „<i>Eg har lese boka</i>”. La germana kaj la nederlanda (samkiel la praangla) havas male la verbon fine. Germane:
   „<i>Ich habe das Buch gelesen</i>”.
</p> 
<p>2) En la angla kaj la nord-ĝermanaj lingvoj prepozicio povas esti fine de frazo:</p> 
<p>„Pri ĉi tio ni parolis” (kun la prepozicio en la fino ni havus „Ĉi tio ni parolis pri”). Norvege: „<i>Dette har vi snakka om</i>”. Angle: „<i>This we have talked about</i>”. Germane, nederlande kaj praangle ĉi tio ne eblas.
</p> 
<p>3) En la skandinavaj kaj en la angla estas ebla grupa genitivo.</p> 
<p>„La ĉapo de la reĝino de Anglio”. Norvege: „<i>Dronninga av Englands hatt</i>”. Angle: „<i>The Queen of England's hat</i>”. En la okcident-ĝermanaj lingvoj tio ne direblas.
</p> 
<p>4) La angla kaj skandinavaj lingvoj permesas disigitan infinitivon, tio estas enŝoveblon
   de vorto inter la infinitiva rekonilo kaj la verbo.
</p> 
<p>„Mi promesas neniam refari tion”. Norvege: „<i>Eg lovar å aldri gjera det igjen</i>”. Angle: „<i>I promise to never do it again</i>”. En la germana aŭ la nederlanda tio ankoraŭfoje ne estas ebla. Kaj la listo povas
   esti plilongigita.
</p> 
<h3>Racia klarigo</h3> 
<p>La sola racia klarigo estas, laŭ la norvega fakulo, ke la angla estas nordia (do skandinav-devena)
   lingvo, kaj ke ĉi tiu lingvo, kia ĝi estas nuntempe konata, estas daŭrigo de la norvega-dana
   lingvo uzata en Anglio ekde la mezepoko, ne rekta ido de la praangla.
</p> 
