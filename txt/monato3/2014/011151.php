      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Kiu ĉi mi?</h2>
      <!-- komenco de la enhavo -->

       

      <p>La bibliografio nur pri liaj libroforme aperintaj verkoj ampleksas kvindek sep titolojn;
   ne indas mencii kromajn artikolojn dise aperintajn, kiuj estus centoj. Cetere, multo
   el la unua kvarono de tiu bibliografio estas kolektita en 
   <i>Mia pado</i> (1997). Tio sufiĉas por meti la nederlandan akademianon Gerrit Berveling (1944, esperantisto
   de 1972) inter la plej produktivajn verkistojn de nia literaturo. Inter tiom da libroj,
   granda plimulto estas tradukaĵoj el la helena, latina, malnovhebrea, nederlanda. Kolosa
   iniciato estas lia 
   <i>Antologio latina</i>, planita en 12 volumoj, el kiuj ĉi-jare aperis la kvina. Bedaŭrinde, la UEA-katalogo
   registras nur dek ok titolojn, kaj ne nepre la plej reprezentajn.
</p> 
<h3>Persona kroniko</h3> 
<p>La ĝenro de la nun recenzata verko respondas al aliaj jam verkitaj: la persona kroniko.
   Berveling komencis per 
   <i>La unuaj 25 jaroj en mia memoro</i>, rakontanta pri lia vivo de 1944 ĝis 1969; sekvis 
   <i>De duopo al kvaropo</i> pri la okazaĵoj inter 1969 kaj 1975; kaj fine la epizodoj inter 1975 kaj 1980 estas
   la esenco de 
   <i>Streĉitaj koroj</i>.
</p> 
<h3>Haste</h3> 
<p>Kritiko ne estis ĉiam indulga al li; pluraj liaj poemoj estis forte draŝitaj de Camacho
   en 
   <i>Beletra Almanako</i>; liaj notoj pri vojaĝo al Jokohamo tra Rusio estas konsiderataj plejparte senutilaj
   kaj nespecifaj de Melnikov; alia kritikisto riproĉas al Berveling, ke li iom tro haste
   enlibrigas siajn impresojn, kiuj povas aspekti tute ordinaraj, do ne specife rakontindaj.
   Entute ni diru, ke la aŭtoro preskaŭ timas, ke io el li perdiĝas, do li rapidas ĝin
   fiksi sur papero: ĉion, senkribre.
</p> 
<h3>Malkaŝe</h3> 
<p>Por tiuj, kiuj jam legis liajn biografiajn verkojn, pluro estas konata. La aliaj trovas,
   verŝajne, ĉion, kio estas spertebla dum plena vivo.
</p> 
<p>Berveling tute malkaŝe rakontas sin de la adoleska dubo, ĉu katolikismo, en kiu li
   estas denaske edukita, estas kontentiga vojo al la kontakto kun Dio, ĝis la bruo en
   la klasoj, kie li instruas klasikajn lingvojn kaj historion; de la bicikla veturado
   ekde la propra hejmo jen al tiu, jen al alia laborloko, ĝis la elekto de la lernejo
   por la unua filino; de la rilatoj kun la pluraj gefratoj ĝis la jen-kaj-jena simpatio
   por iama amikino de la knaba tempo; de la vojaĝoj al Italio ĝis la ekkontakto kun
   Esperanto tra la bopatrino (eble unika okazo en la historio); de la sincera deziro
   helpi la proksimulon, iĝante remonstranta pastoro, ĝis la problemoj de edukado de
   infanoj al dulingveco; de la pasio por vojaĝado ĝis la laboro en la estraro de la
   landa Esperanto-asocio aŭ de la Akademio de Esperanto, en kiu li membras de 1989.
</p> 
<h3>Plezuriga</h3> 
<p>La legado estas vere plezuriga, ne tial, ke ĝi diras ion sensacian, sed ĝuste ĉar
   ĝi rakontas tutan aron da etaj okazaĵoj, en kiuj ĉiu povas retrovi sin; ekzemple,
   la provizora malvarmiĝo de la rilatoj inter la geedzoj, kiu paro ne spertis ĝin? La
   fadeno estas facile interrompebla je ĉiu tria paĝo, ĉar ne temas pri fadeno, sed pri
   sinsekvo de eventoj, impresoj, prenitaj decidoj preskaŭ senkoneksaj. Se ne estus la
   deklaro de la aĝo aŭ de la jaroj, oni povus inversigi la ĉapitretojn. Jen enhospitaligo
   de la edzino, jen infekto ĉe la tonsiloj, jen re- kaj refoja malsukceso ĉe la ekzameno
   por stirrajtigo, jen la glito sur frostiĝinta trotuaro, jen la kunvenoj pri diversaj
   kulturaj temoj en la lernejo, jen la ekzameno kiel pastoro kaj la unua publika prediko,
   jen la remonstranta aliro al la amo gea (sen geedziĝo) kaj geja, kio en la katolika
   mondo estis ankoraŭ tabua temo dum longa tempo.
</p> 
<p>Jen tute normala, sed kontraŭa al la miljara tradicio, la decido, ke al Dio oni atribuas
   ne necese viran pronomon. La aŭtoro neniam timas antaŭ nova situacio: lin helpas la
   religia fido, ke Dio protektas homojn kaj ilin gvidas al ilia bono. Ekzemple, estas
   prezentate kiel nature, ke oni alprenas novan filinon por permesi junulinan vivon
   senĝenan al amikino de amikino, forlasita de la patro de la infano. La novulino tuj
   integriĝas en la familion, kie estas aliaj du knabinetoj, kaj estos nur bedaŭro, ke
   la nova situacio de la patrino ebligos al ŝi repreni sian ideton, kaj la familio Berveling
   restos denove nur kvaropo.
</p> 
<h3>Mi</h3> 
<p>Kion plian oni legas en ĉi tiu verko? Al la aŭtoro plaĉas ĉi tiuj demandoj: Kion mi
   trovos? Kiu mi estas? Kaj la titolo de la verko ĝuste spegulas dubojn, kvankam en
   la vivo li, laŭ sia rakontado, aspektas tre decidema persono; lia predikado estas
   edifa; lia rakontado estas instrua. Ĉar la protagonisto estas la aŭtoro kaj nur la
   aŭtoro, samfamilianoj aŭ geamikoj aperas preskaŭ nur kiel figurantoj; la leganto ne
   miru, ke averaĝe en ĉiu dua linio troviĝas la pronomo „mi” aŭ „ni”. Tiun saman stilon ni trovas ankaŭ en aliaj verkoj de Berveling, el kiuj mi konsilus
   al tiuj, kiuj ne konas ilin, almenaŭ la impulso-ritman 
   <i>Ajnasemajne</i>; se iu sentas, ke por io li ne havas tempon, li lernu, kiom da aferoj Berveling kapablas
   fari kaj kompletigi dum vere mallonga tempospaco.
</p> 
<h3>Konataj nomoj</h3> 
<p>La aŭtoro rakontas, kompreneble, ankaŭ pri la evoluo de la internacia politika situacio,
   pri NATO, pri la Irana Respubliko de Ĥomejni, nu, kiel ordinara nederlandano vidas
   la aferojn kaj estas, kelkfoje kontraŭvole, implikita. Esperantisto trovos tutan aron
   da konataj nomoj, kiujn Berveling kontaktis, vizitis, ĉe kiuj li gastis, do ankaŭ
   la leganto estas envolvita, kvankam iom tro rapide sin sekvas la rakontataj epizodoj,
   ne estas momenta halto por mediti. Al la aŭtoro nun sepdekjara oni nur povas bondeziri
   ankoraŭ multajn jardekojn da tiom intensa vivo.
</p> 
