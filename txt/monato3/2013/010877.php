      <h3>Moderna vivo</h3>
      <h3 class="tem">INSTRUADO</h3>
      <h2>Komerce en la klasĉambro</h2>
      <!-- komenco de la enhavo -->

       

       <p>Senĉese kreskas en Madagaskaro la nombro de privataj institucioj kaj universitatoj, kiuj situas ĉefe en Antananarivo. Videblas ĉie reklamoj por allogi kaj gepatrojn kaj gejunulojn. La ĉefa allogilo estas la monata „plej avantaĝa studkosto”, kiu tamen egalas al la baza monata salajro de ordinara laboristo.</p> <p>La plimulto de tiuj institucioj luas lokon ĝenerale apenaŭ taŭgan. Kiam kreskas la studentaro, la respondeculoj improvizas, starigante kursejon eĉ en antaŭĉambro. Kelkaj institucioj troviĝas en la urbocentro, kie regas bruaĉoj kaj noca atmosfero.</p> <p>Reklamante, la institucioj substrekas la enhavon de siaj studprogramoj, sed ofte silentas pri la instruistaro, aŭ simple citas la nomon de tiu aŭ alia konata docento de la ŝtata universitato, kiun ili dungis.</p> <h3>Permesilo</h3> <p>Studfakoj – popularas juro, firmao-mastrumado, komputila programado, domkonstruado, gazetara komunikado – ripetiĝas ĉe diversaj institucioj. Tamen el cento da tiaj organizaĵoj, nur dudeko estas agnoskita de la ŝtato. La ceteraj kontentiĝas pri permesilo simple malfermi studejon.</p> <p>Tial viktimiĝas multaj gejunuloj ekzemple el kamparaj regionoj: la instruo-komercistoj vendas al ili kursojn kaj disdonas diplomojn, pri kiuj firmaoj apenaŭ interesiĝas.</p> <p>Aliflanke ne ĉiu institucio celas trompi siajn studentojn. Kelkaj insistas pri altaj kvalifikoj ĉe sia docentaro, publikigas la nomojn de instruistoj, kaj kunlaboras kun prestiĝaj eksterlandaj universitatoj.</p> <h3>Diplomitoj</h3> <p>La institucio INSCAE en Antananarivo, kiu fakas pri kursoj en librotenado kaj volas protekti sian reputacion, interrilatas kun kanadaj universitatanoj. Lokaj firmaoj donas prioritaton al la diplomitoj de INSCAE. Tamen la tiea studkosto tri- aŭ kvar-oble pli altas ol ĉe aliaj studejoj.</p> <p>Antaŭ 50 jaroj Madagaskaro povis fieri pri sia sola, sed avangarda universitato Ankatso [ankacu]. Poste la registaro malaltigis la instruado-buĝeton kaj neglektis la universitaton. En la 80aj jaroj la Monda Banko antaŭenigis la privatigon de ŝtataj universitatoj. Tiam ili trejnis studentojn laŭ la bezonoj de lokaj firmaoj kaj apenaŭ okupiĝis pri scienca esplorado.</p> <p>En 2012 strikis docentoj ĉe ŝtataj universitatoj, postulante pli da mono. Ilin subtenis studentoj, kiuj mem neregule ricevis siajn stipendiojn.</p> <h3>Bredado</h3> <p>La plimulto de la malagasoj estas kamparanoj. Ĉiujare kvardeko da abiturientoj estas akceptita ĉe la supera studejo pri agrikulturo kaj bredado, el kiuj nur deko studas besto-kuracadon. Post kvin jaroj, Madagaskaro produktos nafton, sed la registaro ne antaŭvidas trejnadon de koncernaj teknikistoj kaj fakuloj.</p> <p>Ŝajnas, do, ke la registaro volas konservi la nunan situacion, kiu sen rigoraj normoj, sen planado, permesas al kelkaj redukti instruadon al nura komerca afero.</p> <div style="text-align:right">Henriel F<span class="mm">IDILALAO</span></div> 
