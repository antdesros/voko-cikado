      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Forfalas la landlimoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ 19 jaroj civitanoj de mia patrujo ekdormis en unu regno, sed vekiĝis en diversaj novaj landoj. Tiel ni vivis, momente fariĝinte fremduloj al sampatrujanoj el frataj respublikoj.</p> <p>Nun Sovetio ne plu ekzistas jam preskaŭ 20 jarojn. Dum ĉi tiu tempo ni rigardas trans landlimojn kaj vidas homojn similajn al ni, edukitajn per lernolibroj komunaj; homojn, kiuj posedas la internacian lingvon de nia patrujo. Nek kazaĥoj nek belorusoj forgesas la rusan lingvon. Certe, la multnombra rusa loĝantaro de Kazaĥio kaj Belorusio savas sian kulturon, estimante la morojn de aliaj nacioj en siaj hejmlandoj.</p> <h3>Abismoj</h3> <p>Ni malgaje rigardis al abismoj, kiuj kreskis dum la jaroj inter niaj novaj landoj. Eŭropaj ŝtatoj male proksimiĝis unu al la alia, sentante la neceson esti solidaraj, malfermi landlimojn kaj krei unuecan ekonomian spacon. Mi vidis, ke en Finnlando limgardistoj ridetas al civitanoj de Nederlando kiel al samlandanoj.</p> <p>Esti gasto kaj senti sin hejme estas ĉiam agrable. Popoloj ne kulpas pri „grandaj” politikaj malakordoj. Homoj volas vojaĝi, lerni en universitatoj kaj interŝanĝi spertojn. Ili deziras liberiĝi de senbezonaj formalaĵoj.</p> <p>Antaŭ nelonge ni kun ĝojo eksciis, ke finfine niaj landoj ekiris renkonte unu al la alia. Tri ŝtatoj – Rusio, Kazaĥio kaj Belorusio – komencis krei komunan ekonomian spacon. Ekde julio 2011 la doganunuiĝo ekfunkcios, kaj la landlimoj neniiĝos.</p> <h3>Spertuloj</h3> <p>Opinioj de spertuloj malkongruas pri la ĉefaj demandoj, ekzemple pri la utilo por la ekonomioj de la koncernaj ŝtatoj, kiel tio influos al komerco, kaj kia fariĝos la konkurenco ekonomia en la novaj kondiĉoj. Respondi tiujn demandojn estas zorgo de specialistoj, sed nun estas interese, kiamaniere la popoloj de la postsovetiaj respublikoj rigardas la baldaŭan reunuigon.</p> <p>Multaj homoj, certe, optimisme reagas. La abismoj stringiĝas, kaj foriĝas la historia maljusteco. La fratecaj popoloj devas vivi ŝultr’ al ŝultro kaj komune lukti kontraŭ recesioj kaj, ĉefe, kreskigi estontajn generaciojn. Tre multe da studentoj el ekssovetiaj respublikoj lernas en universitatoj de Rusio.</p> <h3>Kapabloj</h3> <p>Sed baldaŭ la junularo de tri ŝtatoj rajtos senbare trovi por si laboron en la vasta teritorio de la nova ekonomia unio, agante en tre variaj kampoj. Pordoj malfermiĝos, kaj la junaj gefakuloj povos realigi sin tie, kie estos bezonataj iliaj kapabloj: ĉu en Rusio, Kazaĥio aŭ Belorusio.</p> <p>Kaj tio ĉi estas la plej grava. La mondo estas tre granda, kaj ju malpli da ŝtatlimoj estos, des pli ĉiu trovos sian profesian niĉon. Tiom da obstakloj estas en la vivo; almenaŭ la artefaritaj malaperu.</p> <div style="text-align:right">Dmitriy V<span class="mm">LASSOV</span></div> <div style="text-align:right"><i>Rusio</i></div> 
