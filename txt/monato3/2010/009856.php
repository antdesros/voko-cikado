      <h2>Respektinda virino. Granda animo  ĉu iom naiva?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Se fotojn oni vidas pri Lidia Zamenhof, ĝene povas malhelpi nin bone rigardi la tiama modo: virinon ni vidas en ege eksmodaj vestaĵoj – kiuj ja supozeble estis tre laŭmodaj tiutempe. Ĉar se io certas, tiam ja, ke Lidia Zamenhof staris firme en sia propra epoko; ŝi ne estis „eksmoda”. Ŝi vidis la minacojn, sed ŝi ankaŭ vidis multajn eblojn por strebi al bono. Ŝi vivis en fido al Dio, kies signojn kaj mesaĝojn ŝi kredis trovi en la revelacioj de Bahaismo.</p> <p>La volumo estas kompilaĵo de ĉiuj ĉi-momente konataj originalaj tekstoj de la plej juna filino de L. L. Zamenhof: eseoj, artikoloj, paroladoj en kunvenoj kaj antaŭ radiomikrofonoj, kaj rakontoj. En la dua parto troviĝas la retrovitaj leteroj.</p> 
<h3>Leteroj kaj recenzoj</h3> <p>Jam en 1980 – same en Antverpeno, sed ĉe TK/Stafeto – aperis de Isaj Dratwer la kompilaĵo <i>Lidja Zamenhof; vivo kaj agado</i>. En ĝi krom mallonga enkonduko kaj kelkaj fotoj la ĉefan rolon havas pluraj leteroj – fotokopie kaj preslitere. Siatempe mi recenzis ĝin. Laŭ mia memoro: iom strange estas, ke la erara redono <i>Contona</i> (p. 32 ĉe Dratwer) de la manskribita <i>bontona</i> (p. 33 samlibre – oni do mislegis ŝajne la minusklan <i>b</i> kiel majusklan <i>C</i>), pri kiu siatempe mi atentigis, ne estas ĝustigita. Oni bonvolu do en p. 302 de la presita verko ĝustigi tion en la antaŭlasta linio.</p> 
<p>La leteroj, kiuj restas de Lidia, ĉefe temas pri renkontiĝoj kun konatoj, pri Ĉe-kursoj, kiujn ŝi donis, pri prelegoj en bahaaj kunvenoj ktp. Estas bone konservi tiujn atestaĵojn de ŝia vivo – eĉ se enhave fojfoje ili estas sufiĉe simplaj.</p> 
<p>En la nuna libro kelkfoje okazis bedaŭrinda komputila ŝovo de linioj al sekva paĝo. La unuaj du linioj de p. 323 troviĝas tute sube je p. 322; la 3 lastaj linioj de p. 325 troveblas je p. 326 supre.</p> 
<p>Pli interesa supozeble por la ĝenerala publiko estas la unua parto de la libro – paĝoj 29-297. Jen ŝi donas tute modelan recenzon – p. 84-92 – pri <i>Sinjoro Tadeo</i>, p. 174-183 pri <i>Plena Gramatiko</i> de Kalocsay-Waringhien; jen kortuŝas nin tute senpretenda memoraĵo pri rompita paperpremilo el la gepatra domo – p. 105-106; jen trafas la priskriboj pri kreskantaj zorgoj pri la malproksima patrujo Pollando – p. 121-123: „kapti la eĥojn el fora Polujo”; jen estas klara pledo kontraŭ tro da neologismoj, pledo por multaj tre rekonebla ankoraŭ – p. 124-125. En pluraj tekstoj oni kvazaŭ travidas la animon mem, kiun nutras la bahaa fido: sur p. 161, ekzemple, ŝi provas klarigi, ke laŭ ŝia kompreno vere konkrete ekzistanta malbono tutsimple ne ekzistas: ekzistas nur „manko de bono” (la saman doktrinon oni konas ĉe Aŭgusteno). Kelkfoje ŝi regalas nin per ŝajne simpla fabelo – pri Hom’ kaj lia ombro – p. 242-246.</p> 
<h3>Viva junulino</h3> <p>Kiel ajn oni aliras ĉi tiun libron, ĉu kiel historiisto, kiu deziras plireliefigi la historion de la leginda libro de Wendy Heller – <i>Lidia, la vivo de la Lidia Zamenhof; filino de Esperanto</i>, ĉu kiel simpla esperantisto scivola pri tiu filino de Zamenhof, ĉu kiel interesito pri la frua bahaa historio – kiel ajn: ĉi libro montras ege vivan junulinon, kiu vidis la kreskantajn minacojn de naziismo kaj milita perforto, kiu samtempe plu fidis je pozitivaj fortoj – fortoj de Dio kaj fortoj de homa fidelo – kaj kiu ĝis sia morto plu strebis al la bono. Tamen, kiam fine ŝi reiras hejmen meze de la minacoj, oni tamen emas demandi sin, ĉu iel ankaŭ ŝi eble estis iom naiva, ne povante kredi je tioma malbono de naziismo. Sed mi kredas, ke tiun subtaksadon de la malbonaj fortoj povus fari preskaŭ ĉiu.</p> 
<p>Lidia poluris sian lingvuzadon ĝis alta nivelo. Estas bone kompreneble, ke dumlonge ŝi estis kvazaŭ unika reprezentantino de Esperanto, kiel vojaĝanta instruistino, kiu logis amasojn al siaj kursoj.</p> <p>Dankon al la kompilintoj por la konservado de ĉi memoroj!</p> 
