      <h3>Moderna vivo</h3>
      <h3 class="tem">HOMAJ RILATOJ</h3>
      <h2>Ami, estimi kaj kokri</h2>
      <!-- komenco de la enhavo -->

       

       <p>La amo kaj la sindonemo de italoj al la familio estas proverbaj. Tamen estas alia itala proverbo, ripetata de ĉiuj instruistoj pri algebro, laŭ kiu „matematiko ne estas opinio”. Kaj la ciferoj, neniam pli ol ĉi-foje, estas elokventaj.</p> <p>Laŭ freŝa statistiko, 55 % de la edzoj kaj 45 % de la edzinoj trompis almenaŭ unufoje sian vivkunulon aŭ, pli precize, tiujn, al kiuj la plejparto solene promesis „ami, estimi kaj honori, dum ĉiuj tagoj de mia vivo, en bonaj kaj malbonaj tagoj, en sano kaj malsano, ĝis la morto nin disigos”.</p> <h3>Konscienco</h3> <p>Aperinte fine de oktobro 2010, tiu ĉi novaĵo vaste eĥiĝis tra Italio. Oni subite ekkonsciis, ke, el kvar geedzaj paroj renkontataj en superbazaro, kamparo aŭ eĉ preĝejo, nur unu havas tute puran konsciencon. Ĉe la ceteraj tri paroj preskaŭ certe almenaŭ unu el la geedzoj kokris, se ne ambaŭ.</p> <p>Senkompata kaj iel miriga estas analizo publikigita de la asocio de italaj fakuloj pri geedzeco-juro. Ili rimarkis, ke la nombro de perfiduloj senĉese kreskas. Ĉefe kulpas la daŭra disvastiĝo de la tiel nomataj sociaj retejoj (kiel <i>Facebook</i>) kaj aliaj perretaj servoj. Ili kapablas provizi facile kaj senpage amikecojn tre apartajn.</p> <h3>Sperto</h3> <p>Certe ne temas pri fenomeno nur itala. Ekzistas ekzemple tutmonde sukcesa internacia retpaĝo rezervata al geedzoj serĉantaj eksteredzecan sperton. Rimarkindas, ke jam 90 000 italoj aliĝis al tiu ĉi servo. Cetere la malfidelula ĉefurbo ne estas Romo (kiu klasifikiĝis dua), sed ties „rivalo” Milano.</p> <p>El la studaĵo evidentiĝas, ke 60 perfidoj el 100 okazas tage, ĉe la laborejo aŭ dum nesuspektinda tagmanĝa paŭzo. Plej kutime temas pri simplaj eskapetoj, pli kaj pli ofte tolerataj (verŝajne, ĉar reciprokaj). Kelkfoje tamen naskiĝas ankaŭ stabilaj amrilatoj.</p> <h3>Aventuroj</h3> <p>Negravan rolon ludas ago. La mezuma trompanto estas 44-jara homo, kun kreskanta nombro de virinoj inklinaj al aventuroj aŭ veraj rilatoj kun partneroj pli junaj, kio ne plu estas tabua temo.</p> <p>Ne mankas – sed ne abundas – geedzoj emaj al samseksaj rilatoj (7 % inter viroj kaj 4 % inter virinoj). Plejparte la averaĝa italo ankoraŭ preferas virinojn, laŭeble elektitajn el la edzinaj konatinoj kaj amikinoj. Virinoj rigardas interesate siajn kolegojn, sian personan trejniston aŭ instruiston pri skiado aŭ dancado.</p> <p>Ke trompantoj sin kredas pli ruzaj ol siaj kunuloj, facile konstateblas. Sed tion neas la japana psikologo Satoshi Kanazawa. Liaj esploroj montras, ke kutime malfideluloj ne fidindas eĉ en la laborejo. Ili estas ankaŭ malpli inteligentaj.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
