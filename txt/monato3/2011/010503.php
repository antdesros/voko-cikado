      <h3>Politiko</h3>
      <h3 class="tem">NIĜERIO</h3>
      <h2>Prezidanto ne plu bonŝanca</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kiam Goodluck (esperante: „bonŝanco”) Jonathan estas elektita prezidanto en majo 2011, la tuta lando esperplenis. Jonathan estas la unua prezidanto el la niĝerdelta regiono, subpremita de islamaj regantoj dum 40 el la 50 jaroj da niĝeria sendependeco.</p> <p>El la regiono fluas la riĉaĵojn de la lando, en la formo de nafto, kiu konsistigas 99 % de la tutlanda enspezo. Tamen etnaj kaj religiaj konfliktoj, kune kun korupto, signifas, ke homoj, kiuj ĝis la perforta unuigo flanke de Britio en 1914 pace kunvivis, nun ankoraŭ interbatalas – ĉefe pro nafto.</p> <p>En 1999 revenis al Niĝerio demokratio. Niĝerianoj esperis, ke sekvos paco kaj prospero, sed tio ne okazis. Eble oni tro esperis, same kiam estis elektita Jonathan – universitata doktoro, tamen el modesta deveno.</p> <h3>Homo malsperta</h3> <p>Nun, tamen, post la unuaj 100 tagoj de la prezidanteco de Jonathan, niĝerianoj opinias, ke eble ili eraris, elektante homon politike malspertan.</p> <p>Ĉie en Niĝerio okazas sensencaj mortigoj. Ekzemple, en la niĝerdelta regiono, militistoj atakas kaj detruas, protestante kontraŭ la tiea neglekto. Rezulte ĉesis nafto-esplorado kaj suferas ŝtataj financoj.</p> <p>Reagis perforte la registaro, kio malsukcesis. Poste la militistoj ricevis amnestion, kune kun laboro kaj edukado, kaj la situacio en niĝerdelta regiono stabiliĝis. Eĉ revenis al la regiono nafto-kompanioj.</p> <h3>Religia purigado</h3> <p>Tamen daŭras religiaj kaj etnaj purigadoj en aliaj ŝtatoj, ekzemple Plateau, Kaduna kaj Kano. Tie islamaj fundamentistoj mortigas senkulpajn niĝerianojn el aliaj gentoj aŭ triboj, kiel Igbo aŭ Jorubo.</p> <p>Lastatempe estis mortigitaj 40 homoj, viroj, virinoj kaj infanoj, en Jos, la ĉefurbo de Plateau. La ŝtato estas dividita inter kristana sudo kaj islama nordo. Tie en la lastaj kvin jaroj mortis miloj da homoj.</p> <h3>Problemo danĝera</h3> <p>La plej danĝeran problemon prezentas la radikala islama grupo Boko Haram. La grupo kontraŭas ĉion okcidentan: edukon, civilizacion, morojn ktp. Ekde majo pereis 5000 homoj pro atakoj de tiu ĉi grupo.</p> <p>Tiel mesaĝas Boko Haram, ke nenie en Niĝerio sekuras. Sekurecaj servoj timas la islamistojn kaj forkuras, kiam ili atakas. La prezidanto restas senkonsila.</p> <p>Nun indas montri al Goodluck Jonathan, ke elĉerpiĝis lia bonŝanco – kaj tiu de la niĝerianoj. Nepras, ke li agu, ĉar, ĝis tiam, daŭre fluos ĉie sango.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> 
