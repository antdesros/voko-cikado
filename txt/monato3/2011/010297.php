      <h2>Senmita biografio de Zamenhof</h2>
      <!-- komenco de la enhavo -->

       

       <p>Estimataj, se, leginte la <i>Biografian Skizon</i> de Al. Korĵenkov pri Zamenhof, Donald Broadribb komprenis interalie (M<span class="mm">ONATO</span> 2011/2, p. 24) ke „<i>Ludoviko</i> kaj <i>Lazaro</i> estas respektive la germana kaj la greka formoj de unusama nomo”, ke Zamenhof ne estis polo tial, ke „la moderna Pollando estis parto de Rusio, do oficiale li estis ruso”, kaj ke „en la frua vortuzo de Zamenhof mem, lia nacio estis ... judismo”, mi povas konkludi nur, ke la ideo-fluo inter aŭtoro kaj recenzinto grave paneis. Ĉar nek Korĵenkov, se juĝi laŭ lia interesega kaj samtema, sed kvinfoje pli ampleksa <i>Homarano</i>, povis diri tiajn stultaĵojn, nek Broadribb, se juĝi laŭ liaj aliaj kontribuoj al M<span class="mm">ONATO</span>, estas homo stulta aŭ freneza, mi devas konjekti, ke la recenzata <i>Skizo</i> (kiun mi mem ne jam vidis) estas iom tro skiza kaj tial eble erariga. Tamen, ĉar leganto tute senscia pri tiuj aferoj eble povus konfuziĝi, ne estas senutile indiki ĉi tie, ke:</p> <p>1. La antaŭnomo de Zamenhof estis Lazaro. Pli ĝuste, laŭ lia naskiĝ-atesto ĝi estis Lejzer (transskribo de jida formo) kaj laŭ aliaj oficialaj dokumentoj ruslingvaj Lazarj. <i>Ludoviko</i> estas <b>alia</b> antaŭnomo, kiun li uzis (en la pola formo Ludwik), same kiel multaj Moŝeoj kaj Abrahamoj fariĝis Maurice-oj kaj Albertoj „en la mondo”, sed ĝi ne estis oficiala. La sola rilato inter la du nomoj estas, ke ili komenciĝas per la sama litero (por ke oni povu subskribi „L. Zamenhof”, ne bezonante memori, kiun nomon oni uzas en la koncerna medio).</p> <p>2. Oficiale, Zamenhof estis <b>ruslandano</b>, t.e. ŝtatano de Ruslando kaj subulo de la caro. Tio ne signifas, ke samtempe li ne estis polo, litovo, ĉeĉeno aŭ kartvelo. Ke li ne konsideris sin polo, tion oni povas apogi per aliaj argumentoj, kaj plej konvinke per lia propra kortuŝa deklaro antaŭ la krakova UK –, sed ne per argumentoj pri ŝtataj limoj!</p> <p>3. Zamenhof ĉiam sin nomis „ruslanda hebreo” kaj ĉiam sin alkalkulis laŭ deveno al „la hebrea popolo/gento/nacio”. La formon „jud-” li preskaŭ neniam uzis, ĉar en la tiama rusa ĝi havis malŝatan nuancon; kaj li certe neniam nomis sian genton „judismo”, kio povis signifi nur judan religion aŭ doktrinon, do ĝuste tion, kion li <b>ne</b> kundividis kun siaj samgentanoj!</p> <div style="text-align:right">Angelos T<span class="mm">SIRIMOKOS</span></div> 
