      <h3>Scienco</h3>
      <h3 class="tem">AKTUALECO</h3>
      <h2>Virinoj abortigas, nombroj pensigas</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Dek kvin jarojn post la komenciĝo de la 21a jarcento, milionoj da virinoj tra la tuta
      mondo daŭre submetiĝas al abortigo kaj al ĉiuspecaj riskoj, kiujn tio ĉi entenas.
      Sufiĉas rapida analizo de la tutmondaj nombroj pri la jaro 2012 por konstati, ke en
      tiu jaro pli ol 7 milionoj da virinoj estis oficiale akceptitaj en malsanulejoj, klinikoj
      aŭ similaj institucioj kun la celo ĉesigi la propran gravedecon.
   </b>
</p> 
<p>Ĉi tio signifas, alivorte, ke mezume 6,9 el ĉiu milo da virinoj (inter 15 kaj 44 jaroj)
   ricevis en la koncerna jaro medicinan traktadon cele al abortigo. Estas rimarkinde,
   ke tio signifas 40-elcentan kreskon kompare kun la jaro 2005. La plej ofte uzata rimedo
   por abortigi? Englutado de medikamento nomata 
   <i>misoprostol</i> kaj konsiderata ege malpli riska ol aliaj: ĝi kaŭzas kuntiriĝon de la utero, sed
   verdire ne ĉiam sukcesas eligi la tutan embrion, tiel ke la eventuala restaĵo ja povas
   kaŭzi gravan enan infekton.
</p> 
<h3>Rekordaj nombroj</h3> 
<p>Oni notas neneglekteblajn malsamojn de lando al lando: fakte la mezuma kvanto de abortigoj
   estis plej malalta en Etiopio, Maŭricio kaj ĉefe Brazilo (lando de la verkanto), kaj
   plej alta en Pakistano, Kenjo, Malavio kaj Ugando. En pluraj afrikaj – kaj ankaŭ en iuj latinamerikaj – landoj, la gravedec-ĉesigoj proksimiĝis al rekordaj nombroj. Oni eltiris ĉiujn suprajn
   nombrojn el tutmonda esplorado efektivigita de faka medicina revuo (nome 
   <i>BJOG</i>) kaj disvastigita de la medicina retpaĝaro 
   <a href="http://medscape.com">medscape.com</a> en aŭgusto 2015. La nombroj rilatas al 26 landoj situantaj en diversaj kontinentoj.
</p> 
<h3>Punebla krimo</h3> 
<p>Se oni konsideras, ke nur 60 elcentoj de la virinoj, kiuj spertas komplikaĵojn kaŭze
   de riskaj abortigoj, sin turnas al hospitalo por medicina traktado, tiam eblas konkludi,
   ke la tuta kvanto de abortigoj verŝajne estas multe pli granda. Krome la registrado
   de abortigantaj pacientoj en landoj, en kiuj la memvola gravedecĉesigo estas punebla
   krimo, povas esti kelkfoje kaŝita, por eviti ekzemple problemojn kun la polico.
</p> 
