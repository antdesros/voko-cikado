      <h3>Politiko</h3>
      <h3 class="tem">AFGANIO</h3>
      <h2>Kreskas narkotaĵo-komerco</h2>
      <!-- komenco de la enhavo -->

       

        <p>En Vieno okazis la tria ministra konferenco de la Pariza Pakto pri la kontaŭbatalo de narkotaĵo-kontrabando el Afganio.</p> <p>Ĝin partoprenis ministroj kaj politikistoj el pli ol 60 ŝtatoj, inkluzive la ĝeneralan sekretarion de Unuiĝintaj Nacioj (UN) Ban Ki-moon, la direktoron de la UN-Oficejo pri Narkotaĵoj kaj Krimado Juri Fedotov, la kunprezidantajn ministrojn pri eksteraj aferoj Sergej Lavrov (Rusio) kaj Alain Juppé [alé jipé] (Francio).</p> <p>Ban Ki-moon urĝis la mondan komunumon plifortigi la klopodojn por elradikigi narkotaĵojn el Afganio. Li menciis, ke kompare kun la antaŭa jaro la kultivado de papavoj pliiĝis je 7 % kaj la produktado de opio kreskis je 61 %.</p> <h3>Krimado</h3> <p>Li avertis, ke la problemo plivastiĝas sendepende de tiuj, kiuj misuzas narkotaĵojn, kaj minacas Afganion mem. La narkotaĵo-komerco kaj la internacie organizata krimado subfosas la sanon en malstabilaj landoj kaj malfortigas la jurŝtatecon.</p> <p>Antaŭ ĉio, laŭ la ĝenerala sekretario de UN, la afgana registaro devas doni prioritaton al la problemo de narkotaĵoj, ĉar la jaraj enspezoj el afganaj opiaĵoj atingas 2,4 miliardojn da usonaj dolaroj. Oni ne povas esperi je stabileco, se tiu sumo prezentas 15 % de la afgana malneta produkto.</p> <p>Responde la afgana ministro pri la kontraŭbatalo de narkotaĵoj, Zarar Osmani, diris, ke la narkotaĵoj povus esti elradikigitaj, se la sekureco en la lando pliboniĝus. Okazas 95 % de la kultivado de papavoj en naŭ malsekuraj provincoj de Afganio.</p> <h3>Kemiaĵoj</h3> <p>Kadre de la Pariza Pakto, kreita en 2003 kaj ampleksanta 56 landojn kaj 14 internaciajn organizaĵojn, oni celas redukti la produktadon kaj komercon je narkotaĵoj el Afganio, bloki la financajn fluojn de la kontraŭleĝa narkotaĵo-komerco, ĉesigi la fluon de kemiaĵoj por produkti heroinon kaj fortigi lokajn iniciatojn por kontraŭbatali narkotaĵo-misuzon fare de afganoj.</p> <p>Tamen la konferenco ne havas devigan povon, kaj pro tio la internaciaj provoj solvi la afganan narkotaĵo-problemon havis ĝis nun malgrandan sukceson.</p> <p>Laŭ januara raporto de la UN-Oficejo pri Narkotaĵoj kaj Krimado, la enspezoj de opia produktado pliiĝis en 2011 je 133 % al proksimume 1,4 miliardoj da usonaj dolaroj, aŭ ĉirkaŭ dekono de la afgana malneta enlanda produkto.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>    
