      <h3>Moderna vivo</h3>
      <h3 class="tem">NOMADOJ</h3>
      <h2>Marĝene  sed sametne</h2>
      <!-- komenco de la enhavo -->

       

       <p>La nomada komunumo en Irlando konsistigas aron da familioj (efektive kvazaŭ triboj), kiuj sekvas cigansimilan vivmanieron, sed ne estas etne cigandevenaj. Oni kalkulas, ke ili nombras proksimume 25 000 en la Irlanda Respubliko, kaj ĉirkaŭ 1500 en Nord-Irlando. Plie, estas samoriginaj komunumoj en Britio kaj en Nord-Ameriko. En la angla lingvo ili nomas sin simple <i>travellers</i> (vojaĝantoj), aŭ <i>pavee</i> [pavi] en sia propra lingvaĵo, nomita <i>cant</i> [kant]. Ilia antaŭa nomo, <i>tinkers</i> (laŭvorte: stanistoj), ne plu estas akceptata de la nomadoj mem, kiuj rigardas ĝin insulta, kvankam en pasintaj jarcentoj ilia pli frua stanista metio ne estis malestimata.</p> <p>Tiuj nomadoj precipe vivas marĝene de la socio, kaj aparte de la domloĝanta popolo, kun kiu ili malofte interedziĝas. La nomadoj ofte plendas, ke ili spertas diskriminacion. Dum sezonoj de bona vetero, ili migras ene de la irlanda insulo, kaj inter Irlando kaj Britio, veturante en aŭto-ruldomoj aŭ (hodiaŭ malpli ofte) en tradiciaj ciganaj „barel”-ruldomoj (<i>vardo</i> aŭ <i>wardo</i> en la romaa lingvo). Ne estas ĝenerale akceptita konsento pri la origino de la nomadoj, sed nenio indikas, ke ili ne havas la saman devenon de la aliaj irlandanoj.</p> <h3>Identeco</h3> <p>Lastatempe estiĝis vigla disputo en la nomada komunumo pri ilia propra identeco. Unu frakcio postulas, ke ili estu agnoskitaj kiel aparta etno, kun unikaj lingvo kaj kulturo, kaj eĉ deziras havi sian propran „nacian” flagon. Ŝajnas, tamen, ke la plimulto de nomadoj rigardas sin kiel integra parto de la irlanda popolo, kaj akceptas kiel sian la trikoloran flagon de la Irlanda Respubliko.</p> <p>Tiuj fiere rememorigas siajn samkomunumanojn, ke estis membro de la nomada komunumo, kiu ricevis la honoron porti la irlandan flagon antaŭ la irlanda teamo ĉe la Olimpiaj Ludoj en Atlanta en la jaro 1996.</p> <div style="text-align:right">Garbhan M<span class="mm">AC</span>A<span class="mm">OIDH</span></div>  
