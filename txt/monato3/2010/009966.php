      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Retaksante la buĝeton</h2>
      <!-- komenco de la enhavo -->

       

       <p>Finiĝis retaksado de projektoj proponitaj de japanaj ministrejoj. La retaksado sekvas promeson de la nova registaro malkaŝi al japanoj la misuzon de la ŝtata buĝeto sub la antaŭa administrado.</p> <p>Laŭ la retaksado de entute 447 projektoj, 71 estos nuligitaj kaj 19 provizore haltigitaj. Tiel la registaro ŝparos 1600 miliardojn da enoj.</p> <p>Publike observis la retaksado-kunvenon dum naŭ tagoj 20 000 homoj, interrete 340 000. Opiniis la respondeca ministro Sengoku: „En familiaj konversacioj kaj eĉ en la rondoj de senhejmuloj tiu kunveno estis paroltemo. Tiu granda intereso inter popolanoj signifas, ke ili jam sentis malsanon en politiko kaj administrado.”</p> <h3>Kontenta</h3> <p>Komentis la nova ĉefministro, Hatojama Jukio de la Demokrata Partio: „La buĝeto iĝis videbla al la popolo. Mi estas kontenta pri la kunveno.”</p> <p>Ĝis nun japanoj simple akceptis la buĝeton proponitan de la registaro. Aprobis ĝin la parlamento sen multe da diskutoj. Nun japanoj pli interesiĝas kaj komprenas, kiel estas uzataj impostoj. Tamen restas problemoj.</p> <p>Ekzemple, la registaro celis ŝpari 3000 miliardojn da enoj, sed la ŝparota sumo ne atingis la celon. Pro tio, kiam la registaro preparos la venontjaran buĝeton, kiu enhavos novajn projektojn laŭ la manifesto de la reganta partio, mankos financoj. Ankaŭ kelkaj decidoj estas tro rapide trafitaj. Malgrandigota estas la buĝeto por sciencaj esploroj: plendis sciencistoj, dirante, ke por la malgranda Japanio gravas esploroj tiaj.</p> <h3>Sanktaĵoj</h3> <p>Krome restas sanktaĵoj. Ankoraŭ permesataj estas konstruado de aviadilŝipo, misila defendo-projekto kaj „afabla buĝeto por usonaj garnizonoj en Japanio”. Tiuj projektoj estas kontraŭ la japana konstitucio kaj laŭ la volo de Usono.</p> <p>Alia sanktaĵo koncernas publikan monon por apogi politikajn partiojn (escepte de la komunista). Tio signifas, ke japanoj per siaj impostoj helpas partiojn, kiujn ili ne subtenas. Tio atencas la politikan rajton de la popolo.</p> <p>La ĵurnalistaro estas kritikema pri ĉio, kion faras Hatojama. Tiu certe bezonas tempon por „perfekte purigi Japanion”, kiel li promesis, ĉar restas multo putra kaj polva post longa regado de la Liberala Demokratia Partio.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
