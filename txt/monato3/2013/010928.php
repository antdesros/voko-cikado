      <h3>Moderna vivo</h3>
      <h3 class="tem">KRISTNASKO</h3>
      <h2>Adori, odori</h2>
      <!-- komenco de la enhavo -->

       

        <p>Post la paska sezono komenciĝas jam la kristnaska, kiu eksplodas en la semajnoj novembraj kaj frudecembraj. Ne temas pri religiaj preparoj, sed pri la komercaj.</p> <p>Eble sezonan dolĉaĵon oni jam antaŭfestene frandis, aŭ spicitan bolantan vinon jam antaŭvintre ekzamenis. Sed ne nur en trink- kaj dolĉaĵ-anguloj de la magazenoj troveblas kristnaskaj ofertoj: ankaŭ, tute neatendite, en tiuj pri necesejaj paperoj.</p> <p>Estas unu el la gloraj kulturaj atingoj de la homaro, ke oni ne plu bezonas magrajn herbotigojn, agacajn pajlofaskojn aŭ rompiĝemajn florfoliojn por fini sian digestadon, sed ke oni nun disponas pri paperrulaĵoj normigitaj kaj fidindaj.</p> <h3>Motivoj</h3> <p>Aktuale, inter kutimaj varoj tiabezonaj, oni povas trovi specialan kristnaskan oferton. Firmao, kiu dumjare prezentas siajn necesejajn paperproduktojn sub la nomo <i>Happyend</i>, do „Finaĵo feliĉa”, nun proponas necesejan paperon kun kristnaskaj motivoj.</p> <p>Tamen, post skrupula ekzamenado, la papero evidentigas sin ankaŭ por islamaj kaj esperantistaj animoj, respektive postaĵoj, nedanĝera. La stelo estis jam antaŭ 1887 internacia signo de la espero, kaj la prauloj de la du ĉiufolie aperantaj rangiferoj certe ne staris apud bovo kaj azeno ĉe la betleĥema kripo.</p> <p>Do, simboloj laikaj, neniel minacantaj ies religian kredon aŭ karnon. Eble nur tiun de kristanoj, sed tiuj ja estu nepre toleremaj, aparte se temas pri simboloj kaj la lastaj aĵoj.</p> <p>Sed tio ne estas ĉio en la surpriza pakaĵo. Malatenta aĉetanto, hejmenveturante, subite flaras ion neatenditan el la varokesto en la aŭtomobila fono. Ne, tute ne jam uzitan paperon. Hejme li gaje rimarkas, ke oni regalis lin eĉ duoble.</p> <h3>Bakaĵo</h3> <p>Plia surskribo perfidas, ke la pura neceseja papero odoras je kristnaske spicita bakaĵo (<i>Spekulatius</i>). Ĝoje li konstatas: Kristnaskaj manĝaĵoj, trinkaĵoj antaŭ mi, kristnaska papero post mi: jen la festo savita!</p> <p><i>Pecunia non olet</i> / „(Mono) ne fiodoras”: tiun klarigon donis jam la romianoj, kiam temis pri gajnoj el la publikaj latrinoj. Nun ankaŭ ne plu fiodoros hejme tradome, danke al la kristnaskbakaĵumita kemiaĵo enpapera.</p> <p>Kaj ankaŭ la mono ne fiodoras, konsidere, ke la ĝojiga rulaĵo kostas preskaŭ la duoblan prezon de la ordinara. Sed tiu konstato estas forgesinda antaŭ la fina rezulto de la kristnaskaj plezuroj.</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div>  
