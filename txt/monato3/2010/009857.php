      <h2>Ĉarma, originaltema kaj pensiga</h2>
      <!-- komenco de la enhavo -->

       

      <p>Ĉi tiuj tri noveloj, kun belaj, samstilaj kovriloj, formas iun etan serion. Kvankam ne temas pri la sama ĉefrolulo, la temo estas simila en la tri rakontoj: la interagoj inter afrikanoj kaj blankuloj. Tamen la rakontoj sufiĉe diferencas, interalie, ĉar ili okazas en aliaj historiaj tempoj.</p> <p>En la unua duono de <i>La paŝo senelirejen</i> Biova kreskas kaj fine, post longa atendado kaj multe da espero – pere de ceremonio – iĝas viro. Sed la feliĉo ne longe daŭras ... alvenas „flosanta domo” kun „bruligitaj homoj”, kiuj mortigas kelkajn vilaĝanojn. Necesas debatoj pri tio, kiel reagi al tiuj ŝajne nevenkeblaj homoj. Oni decidas, ke Biova iru por lerni ilian lingvon kaj tiel iĝi same inteligenta. Pasis generacioj, kaj kelkaj homoj tre bone adaptis sin al la ĉeesto de la blankuloj, kaj bone lernis, kiel utiligi la bonajn rilatojn kun tiuj riĉuloj. Regas speco de demokratio, sed nur por pruvi al la mondonacantoj, ke indas donaci monon al tiu vilaĝo; venas tiom da mono, ke la ricevantoj preferas aĉeti kastelojn en la blanka mondo ol konstruigi pliajn en la vilaĝo. Tamen la simplaj homoj, kiuj ne havas tiajn rilatojn kun la blankuloj, perdis sian kulturon kaj siajn loĝlokojn, ŝajne, sen elirvojo. Tiel ĉi tiu koloni-etosa rakonto finiĝas sufiĉe negative.</p> 
<p>La dua novelo traktas ĝuste tion, kion sugestas la titolo: <i>Dialogo inter surduloj</i>. Kiel infano Dege kreskis ĉe la blankuloj, kaj nun estas „nigra-blankulo”. Post jaroj li kiel plenkreska „sukcesinto” revenas de tempo al tempo al la vilaĝo kaj rakontas pri siaj spertoj en Jovotome, la blanka lando. Sed la homoj ne povas kompreni lin, kvankam li ankoraŭ parolas la lokan lingvon, eĉ se miksitan kun iom da francaj vortoj. Ĉe la fino de la novelo la vilaĝanoj komencas uzi la teknikaĵojn de la blankuloj, sed tio ne nepre feliĉigas ilin.</p> 
<p>En ĉi tiuj unuaj du rakontoj estas iom da magio, kio starigas demandojn, kiuj pensigas la leganton. Unuflanke, tio donas iom da spico al la rakontoj, sed aliflanke, se la respondoj ankaŭ ĉe la fino ne venas ... tio donas iun ne tre agrablan senton. Tamen kelkaj detaloj, kiuj povus starigi demandojn, fakte eble estas nur mistajpoj ... ekzemple en la unua novelo, la voduo Dan estas unue „blanka virino” kaj du liniojn poste „li”.</p> <p><i>Aventuro de ŝajnigado</i> ludiĝas en nia epoko de migrado de multaj afrikanoj al okcidentaj landoj. Jeko, la ĉefrolulo, kiu multe similas al la aŭtoro, migras de Afriko al Germanio kaj poste al Kanado. Li volas lerni pri la miraklo de la sukceso de la blankuloj, sed malesperiĝas pro la multaj kontraŭdiraj konstatoj. En Kanado li edziĝas kaj eksedziĝas, kaj ankoraŭ priskribas la malfacilaĵojn kun du aliaj partnerinoj. Ĉu la malfacilaĵoj estiĝis nur pro la kulturaj diferencoj? Unu afero certas por Jeko: estas multe da ŝajnigado en la blankula mondo. Kaj li komencas listigi kaj priskribi plurajn ekzemplojn de tiu ŝajnigado: la mizero en Jovotome kompare al la emo helpi al afrikaj landoj, la hipokrita toleremo, la iluziplena libereco ... Sed li konkludas, ke, tamen, estas pli da similecoj inter la homoj ol da malsimilecoj. Sed estas malfacile tion klarigi al la homoj en lia naskiĝvilaĝo, kiuj konsideras Jovotome daŭre kiel la paradizon sur la tero. Ĉi tiu rakonto estas la plej realisma, malplej magia el la tri, sed starigas aliajn, eble ne por ĉiuj tre agrablajn, demandojn pri kulturo, toleremo ktp.</p> 
<p>La lingvaĵo de la rakontoj estas sufiĉe flua, sed kio vere ĝenas dum la legado, estas multaj mistajpoj kaj gramatikaj kaj leksikaj eraroj. Ili aperas precipe en la unua novelo, sed ne nur, kaj eĉ en la dorsflanka teksto. Tamen la unuaj du jam unue aperis en la reto en respektive 2001 kaj 2002. Ĉu ne sufiĉe da tempo por provlegigi antaŭ la eldonado?</p> 
<p>Konklude, estas ĉarmaj, originaltemaj kaj pensigaj noveloj, kiuj montras iun nekutiman rigardon al la rilato inter blankuloj kaj afrikanoj en diversaj epokoj, sed kiuj, bedaŭrinde, ne ricevis la necesan redaktan atenton.</p> <div style="text-align:right">Heidi G<span class="mm">OES</span></div> 
