      <h3>Eseoj</h3>
      <h3 class="tem">LERNEJAJ JAROJ ... KAJ POSTE</h3>
      <h2>Tridek jarojn poste</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <b>Lernejaj jaroj ege gravas. Kelkaj ilin rememoras ĝoje, aliaj amare. Tamen eĉ jardekojn
      poste ili povas daŭre influi nian vivon. Jen du flankoj de la sama, postlerneja medalo:
      unu teatraĵeta, alia esea. Ĝuu!
   </b>
</p>   
<p>
   <i>La scenejo indikas parkon. Oni aŭdas mallaŭtajn paŝojn. Evidente iu marŝas.</i>
   <br></br> 
   <b>Glenda</b>: Oj, estas vi, Oliver! Mi ne atendis iun ĉi tie. 
   <br></br> 
   <b>Oliver</b>: Kaj mi supozis, ke nur mi sidos ĉi tie je la 6-a matene. Sed bonvolu eksidi, Glenda.
   Estas sufiĉe da loko sur la benko.
   <br></br> 
   <i>Glenda eksidas.</i>
   <br></br> 
   <b>Glenda</b>: Hm jes, bela vidaĵo de ĉi tie supre. Jen la vilaĝo, kiu iam estis nia sola hejmo.
   <br></br> 
   <b>Oliver</b>: Mi estis ĉi tie nur unu fojon dum la lastaj tridek jaroj. Kaj vi?
   <br></br> 
   <b>Glenda</b>: Dum miaj gepatroj vivis, mi vizitis de tempo al tempo. Sed ili mortis antaŭ dudek
   jaroj.
   <br></br> 
   <b>Oliver</b>: Estis bona iniciato de Edward, ke li organizis tiun ĉi klasan revidiĝon.
   <br></br> 
   <b>Glenda</b>: Jes, surprizis min. Memoru, kiam ni estis dudek du en la klaso, kaj Edward estis
   kutime la lasta ĉe niaj monataj ekzamenoj ...
   <br></br> 
   <b>Oliver</b>: Kaj nun li estas la lasta klasano, kiu vivas ĉi tie. Li diris, ke li heredis la
   laktobienon de sia patro, sed devis vendi ĝin al multnacia korporacio, kiu dungis
   lin kiel manaĝeron.
   <br></br> 
   <b>Glenda</b>: Al mi li diris, ke neniu el la malgrandaj familiaj bienoj restas. Usona kompanio
   aĉetis preskaŭ ĉiujn. Domaĝe, ĉar tiuj familioj estis la solida kolono de nia vilaĝa
   komunumo.
   <br></br> 
   <b>Oliver</b>: Jen nia mondo, tutplaneda komerco, ktp. La malgranduloj perdas, la trogranduloj
   profitas.
   <br></br> 
   <i>Refoje oni aŭdas paŝojn.</i>
   <br></br> 
   <b>Glenda</b>: Vidu, venas iu alia ĉi supren. Estas nia granda kapitalisto, Charlie!
   <br></br> 
   <b>Charlie</b>: Ha ha! Kion vi du faras ĉi tie? Estas pli facile ludi tunelon kaj trajnon en via
   hotelĉambro!
   <br></br> 
   <b>Glenda</b>: 
   <i>(Pli amuzite ol ofendite)</i> Charlie, via senso por humuro ne pliboniĝis. Se vi vere volas scii, Oliver kaj mi
   venis aparte por ĝui la freŝan aeron ĉi tie. Kaj kial vi venis?
   <br></br> 
   <b>Charlie</b>: Mia edzino ronkas kiel furzanta bovo, ha ha! Movu vian pugon, Oliver, estas loko
   por tri sur la benko.
   <br></br> 
   <i>Charlie brue eksidas.</i>
   <br></br> 
   <b>Charlie</b>: Nekredeble, ĉu ne, kiel la homoj ŝanĝiĝas en tridek jaroj. Mi apenaŭ rekonis kelkajn.
   Tiu eta mumio – mi ne divenus, ke estas Jimmy Lock. Kaj la balono en la robo – tio estas Jeanie Thomas, kiu estis iam maldikulineto!
   <br></br> 
   <b>Glenda</b>: El la 22 en nia klaso ĉeestas 17. Mi scias, ke Sammy kaj Roberta mortis, sed ankoraŭ
   mankas tri.
   <br></br> 
   <b>Oliver</b>: Ni devas gratuli Edward, ke li sukcesis kontakti 17. Mi kredas, ke Kristina edziniĝis
   al kanadano kaj vivas en Toronto.
   <br></br> 
   <b>Charlie</b>: Mi kalkulis, ke dek el ni estas ĉi tie kun edzo aŭ edzino. Vi du estas inter la
   esceptoj. Kompreneble mi kunportis mian dormosakon, ha ha!
   <br></br> 
   <b>Oliver</b>: Charlie, iu flustris al mi, ke Charmaine ...
   <br></br> 
   <b>Charlie</b>: Charlene. Ŝia nomo estas Charlene.
   <br></br> 
   <b>Oliver</b>: Pardonu, Charlene. La onidiro estis, ke Charlene estas la ŝlosilo de via sukceso
   en la komerca mondo.
   <br></br> 
   <b>Charlie</b>: En la sola mondo, kiu gravas. Sed jes, ne estas sekreto – mi faris tion, pri kio aliaj junaj dungitoj nur revas – edzinigis la filinon de la kompaniestro, ha ha!
   <br></br> 
   <b>Glenda</b>: Kaj poste vi transprenis la kompanion, sendube.
   <br></br> 
   <b>Charlie</b>: Trafite! Sed tio estis nur la unua paŝo. Ĉu vi scias – jen, prenu mian vizitkarton – mi nun havas tutan aron da kompanioj. Mi estas sur la listo de Clarkson.
   <br></br> 
   <b>Glenda</b>: La listo de la dek plej riĉaj homoj de la lando? Hm, gratulon.
   <br></br> 
   <b>Oliver</b>: Pardonu mian senscion, Charlie, sed mi ne aŭdis pri tiu listo.
   <br></br> 
   <b>Charlie:</b> Eĉ ne aŭdis! Sed tio diras ĉion pri ni, ĉu ne, Oliver? Vi, la grandcerbulo de nia
   klaso – kaj kion vi havas nun? Mi bone memoras, ke vi ĉiam komprenis tuj, kion la instruistino
   diras, kaj dum ni aliaj stultuloj penis la trian fojon kompreni ŝin, vi amuziĝis,
   desegnante kaj montrante karikaturojn de ni aliaj!
   <br></br> 
   <b>Glenda</b>: 
   <i>(Ridas)</i> Ankaŭ mi memoras tion, Oliver! Kaj viaj desegnaĵoj estis lertaj, kaj ŝajne vi ŝatis
   tro emfazi la dikecon de nia grandulo Charlie. Jes, certe ni ridis, eĉ vi, Charlie!
   <br></br> 
   <b>Charlie</b>: Jes, mi ridis, sed mi malamis vin, Oliver – kaj vidu nun, mi estas sur la listo de Clarkson kaj vi – ha, vi diris, ke de tempo al tempo vi sukcesas vendi pentraĵon! Ha, kia vivo estas
   tio? Kiu rajtas ridi nun, Oliver?
   <br></br> 
   <b>Oliver</b>: 
   <i>(Ironie)</i> Mi povus aldoni al via triumfo, Charlie. Vi havas ŝikan, riĉan edzinon, kaj viaj
   gefiloj lernis en la plej prestiĝaj gimnazioj en la ĉefurbo. Mi ne havas gefilojn,
   kaj mi estas triobla eksedzo. Vi venis ĉi tien per Rolls-Royce kaj mi per trajno.
   
   <br></br> 
   <b>Charlie</b>: 
   <i>(Kolere)</i> Mi kredas, ke vi mokas min, Oliver! Ne per desegnaĵo sed per vortoj. Mi aŭdis sufiĉon.
   Ĝis revido!
   <br></br> 
   <i>Charlie brue foriras.</i>
   <br></br> 
   <b>Glenda</b>: Ĉu vi intencis moki lin?
   <br></br> 
   <b>Oliver</b>: Komence ne, sed ŝajne mi ne povis regi min. Malantaŭ la fasado de la fanfaronulo
   Charlie estas eble nesekura homo, tre akreaŭda pri ironiaĵoj. Hm, eble estis mia ironiemo,
   kiu perdigis al mi tri edzinojn …
   <br></br> 
   <b>Glenda</b>: Vi ofendis lin per tio, ke vi nomis Charmaine ... 
   <br></br> 
   <b>Oliver</b>: Charlene.
   <br></br> 
   <b>Glenda</b>: Jes, 
   <i>ŝi</i>. Vi nomis ŝin 
   <i>ŝika</i>. Oni tuj vidas, ke la sola altiraĵo de la edzino de Charlie estas, ke ŝi estis la
   filino de la ĉefo. Charlie sendube trovas sian erotikan plezuron aliloke – tiukampe li estis ia ĉampiono – sed evidente estas grave al li, ke li publike ŝajnu respektinda edzo. Sed li sciis,
   ke la vorto 
   <i>ŝika</i> neniel aplikeblas al lia edzino. 
   <br></br> 
   <b>Oliver</b>: Vi parolas kvazaŭ psiĥiatro.
   <br></br> 
   <b>Glenda</b>: Mi estas bone trejnita de psiĥologo. Sed pri Charlie kaj lia edzino: li rajtas diri
   ĉian krudaĵon pri ŝi – ke ŝi ronkas kiel furzanta bovo, ke ŝi estas lia dormosako – sed neniu alia ŝercu pri ŝi, ĉar ŝi estas lia propraĵo.
   <br></br> 
   <b>Oliver</b>: Nu, vi pravas, mi supozas. Mia damna emo ironii …
   <br></br> 
   <b>Glenda</b>: Ankaŭ mia eksedzo emis paroli ironie. Mi kredas, ke li volis per tio protekti sin,
   sed … . Strange, li estis psiĥologo kaj povis brile konsili al aliaj, sed lia sarkasmemo
   estis finfine tro por mi. Cetere, ĉu vi vere eksedziĝis tri fojojn?
   <br></br> 
   <b>Oliver</b>: Unu fojon. Mia dua edzino mortis pro kancero antaŭ kelkaj jaroj. Mi vere ne scias,
   kial mi parolis tiel al Charlie.
   <br></br> 
   <b>Glenda</b>: Sed vi ja venis al la vilaĝo per trajno, ĉu ne?
   <br></br> 
   <b>Oliver</b>: Ho jes, kaj pravas, ke mi enspezas mizere per miaj pentraĵoj. Mi havas dekojn, eble
   centojn da pentraĵoj – nevenditajn.
   <br></br> 
   <b>Glenda</b>: Ne forgesu, ke Van Gogh neniam sukcesis vendi dum sia tuta vivo!
   <br></br> 
   <b>Oliver</b>: Prave, sed estas eta diferenco inter mi kaj Vincent – li estis geniulo.
   <br></br> 
   <b>Glenda</b>: Sed vi estas tre inteligenta kaj sendube talenta.
   <br></br> 
   <i>Iom longa silento.</i>
   <br></br> 
   <b>Glenda</b>: Oliver, ni estas sufiĉe aĝaj, ke mi diru al vi ion, kion mi ne kuraĝis diri antaŭ
   tridek jaroj.
   <br></br> 
   <b>Oliver</b>: Interesa prologo!
   <br></br> 
   <b>Glenda</b>: En la lernejo vi estis enamiĝinta al Kristina, ĉu ne?
   <br></br> 
   <b>Oliver</b>: Ĉiuj knaboj enamiĝis al Kristina!
   <br></br> 
   <b>Glenda</b>: Kaj vi neniam eĉ rigardis min.
   <br></br> 
   <b>Oliver</b>: Ĉu vi volis, ke mi rigardu vin?
   <br></br> 
   <b>Glenda</b>: Kompreneble, vi aĉulo!
   <br></br> 
   <i>Ambaŭ ridas</i>
   <br></br> 
   <b>Oliver</b>: Do nun mi rigardas, kaj mi vidas ankoraŭ plaĉaspektan virinon, respektatan kuracistinon,
   kies kompanio estas agrabla al mi. Ĉu mi aldonu ion?
   <br></br> 
   <b>Glenda</b>: Mi vidas antaŭ mi senmonan pentriston, preskaŭ kalvan, viron kiu nebone regas sian
   sarkasman langon, iom tro maldikan, sed ...
   <br></br> 
   <b>Oliver</b>: Sed kio?
   <br></br> 
   <b>Glenda</b>: Sed mi certe ne kontraŭus, se li pentus pro siaj pekoj de antaŭ tridek jaroj kaj
   rigardus min.
   <br></br> 
   <b>Oliver</b>: Aĥ, nun mi komprenas, kial mi venis ĉi tien je la sesa matene!
   <br></br> 
   <i>Ambaŭ ridas</i>
   <br></br> 
   <b>Glenda</b>: Ankaŭ mi malofte promenas antaŭ la matenmanĝo. Cetere, mi aŭdas ian sonorilon. Ĉu
   vi supozas, ke tio vokas nin al matenmanĝo?
   <br></br> 
   <b>Oliver</b>: Aŭ la matenmanĝo estas preta, aŭ la hotelo forbrulas. Sed mi ne vidas flamojn.
   <br></br> 
   <b>Glenda</b>: Do ni iru manĝi.
   <br></br> 
   <b>Oliver</b>: Ni iru. Sed dum la promeno tra la vilaĝo poste mi invitas vin, sinjorino, marŝi
   apud mi. Eble ni trovos kelkajn babiltemojn.
   <br></br> 
   <b>Glenda</b>: Post tridek jaroj ... jes, tio eblas.
</p> 
