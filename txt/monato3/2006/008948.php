      <h3>Moderna vivo</h3>
      <h3 class="tem">TOGOLANDO</h3>
      <h2>Konfuzo kaj kontraŭdiroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 29an de marto 2006 okazis en Togolando eklipso de la suno. La registaro deklaris ferio la matenon de la eklipso-tago kaj togolandanoj restis hejme. Tamen antaŭe kaj dume regis konfuzo kaj kontraŭdiroj.</p> <p>Ĉirkaŭ du semajnojn antaŭ la eklipso amaskomunikiloj atentigis, ke oni dume ne rigardu la sunon pro blindiĝo-danĝero. Rezulte homoj aĉetis ĉe apotekoj apartajn okulvitrojn. Tamen tri tagojn antaŭ la eklipso oni aŭdis, ke la okulvitroj, kiujn disponigis al la popolo la registaro, ne protektas kontraŭ blindiĝo.</p> <p>Anstataŭe estis dirite, ke oni restu hejme, enfermita en la propra ĉambro, kaj ne rigardu la eklipson. Krome rondiris diversaj onidiroj: oni malŝaltu elektronikajn aparatojn, kiuj dum la eklipso difektiĝos; oni translokiĝu, se oni loĝas en domo el pajlo aŭ konsistanta el feraĵoj, ktp. Panikiĝis multaj homoj, aĉetante antaŭ la eklipso-tago manĝaĵojn kaj alian provianton.</p> <h3>Rabistoj</h3> <p>Venis la tago mem kaj la ordonoj de la registaro estis klaraj: „Ĉiu restu hejme, en la ĉambro kun fenestroj kaj pordoj fermitaj.” La stratoj de la ĉef- kaj aliaj urboj restis senhomaj kaj la popolo sekvis la progreson de la eklipso per televido- kaj radio-raportoj. Tamen ne ĉiu restis hejme: rabistoj diversloke profitis la okazon por priŝteli bienojn kaj butikojn aŭ forpreni bredatajn dombestojn.</p> <p>Ĝuis la fenomenon ankaŭ turistoj, kiuj spektis la eklipson per siaj okulvitroj. Rezulte multaj togolandanoj malkontentis, dirante, ke ilin trompis la registaro, ĉar la eklipso ne tiom timigis, kiom ili atendis. Ekzemple, la prognozita nigro similis al tiu de pluva tago kaj ne al plena nokto.</p> <p>Tiuj, kiuj memoris la lastan eklipson en Togolando en 1947, diris, ke tiam estis pli mallume. Nun togolandanoj devos atendi ĝis 2081, ĝis okazos la venonta eklipso en ilia lando.</p> <div style="text-align:right">G<span class="mm">BEGLO</span> Koffi/pg</div>  
