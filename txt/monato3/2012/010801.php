      <h3>Moderna vivo</h3>
      <h3 class="tem">GERMANIO</h3>
      <h2>Murdoj, mankoj kaj misoj: demisias la ĉefo</h2>
      <!-- komenco de la enhavo -->

       

        <p>Komence de julio la prezidanto de la konstituci-protekta oficejo en Germanio (<i>Bundesamt für Verfassungsschutz </i> aŭ sekreta servo) petis antaŭtempan liberigon el sia posteno. Kelkajn tagojn poste eksiĝis ankaŭ du regionaj ĉefoj de la sama servo.</p> <p>Heinz Fromm prezidis la oficejon ekde 2000. Malgraŭ tio, ke ekde tiam la batalo kontraŭ ekstremdekstrismo havis prioritaton, en 2000 okazis la unua el naŭ murdoj farita de la rasisma terorgrupo NSU (<i>Nationalsozialistischer Untergrund</i> aŭ nacisocialisma subtera movado).</p> <p>En 2007 okazis eksplodo en Kolonjo kaj oni kulpigis NSU-on. Publikaj instancoj, inkluzive de la konstituci-protekta oficejo, estus devintaj rekoni la ekstremdekstran fonon de la atenco. Sed ne estis tiel.</p> <h3>Etkomercistoj</h3> <p>Inter 2000 kaj 2006 okazis murdoj en diversaj urboj. La viktimoj estis ĉefe eksterlandanoj, ofte etkomercistoj, vendistoj de legomoj, floroj kaj de la turka specialaĵo <i>doner</i>-kebabo, turniĝanta rostata viando. Tial oni parolis pri la <i>doner</i>-murdoj.</p> <p>La atencoj ĉiuj similis. La krimuloj alproksimiĝis de malantaŭe kaj pafis el mallonga distanco en la kapon. Okazis en 2007 ankaŭ atenco kontraŭ policanoj en la urbo Heilbronn [hajlbron]: mortis policistino. Memore al tiu oni instalis tabulon sen mencio pri ajna nazia terorismo.</p> <p>Enketistoj supozis kverelojn inter prostituitinoj aŭ narkotaĵo-komercistoj, ĉantaĝon de mafiecaj bandoj, aŭ atencojn fare de la kurda laborista partio PKK. Politikistoj neis rasismon, dum atestintoj supozis dekstreman terorismon.</p> <h3>Banko</h3> <p>En novembro 2011 du ekstremdekstraj krimuloj atakis bankon en Eisenach [ájzenaĥ]. Kiam la polico volis aresti ilin, ili mortpafis sin. Poste oni trovis armilojn ŝtelitajn de atakitaj policanoj.</p> <p>Pliajn armilojn oni trovis en bruligita domo, kie loĝis la du naziteroristoj. Tie oni trovis diskojn surhavantajn fotojn de la murditoj kaj ankaŭ konfesojn faritajn de la krimuloj. Tiel oni kunligis la rasismajn murdojn kun tiu de la policistino. Oni do starigis novan tiurilatan memortabulon.</p> <h3>Paneo</h3> <p>Evidentis, ke la konstitucio-protekta sekreta servo ne rikoltis informojn, kiuj estus malhelpintaj la murdojn. La publiko opiniis, ke temas pri paneo, ne pri simpla hazardo.</p> <p>Okazis plia afero. Kiam la NSU-bando estis malkovrita, iu en la konstituci-protekta oficejo neniigis la koncernajn dokumentojn. Pro tio, ankaŭ pro la murdoj, frue eksiĝis la prezidanto Fromm.</p> <p>Kulpas pri la afero iamezure la federaciismo en Germanio. Federacia oficejo ne konas la agentojn en la diversaj regionoj, la t.n. landoj. Siaflanke la landaj oficejoj ne interŝanĝas informojn. Kunlaboro tia estas leĝe malpermesata.</p> <div style="text-align:right">Jomo I<span class="mm">PFELKOFER</span></div>   
