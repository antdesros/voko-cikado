      <h2>Kie kalumnioj skuas la animon</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Proteste kontraŭ bridado de libera esprimado en Kroatio, Zlatko Tišljar rifuzas plu artikoli pri tiu ĉi lando. Siajn motivojn li sube klarigas.</b></p> <p>Pasintjare en novembro zagreba tribunalo kondamnis kroatan intelektulon kaj multjaran prezidanton de la kroata PEN-klubo, Predrag Matvejević, je kvinmonata malliberiĝo pro kalumnio. Profesoron Matvejević akuzis bosnia verkisto, kiu en la kroata gazetaro dissemis malverojn pri bosniaj intelektuloj. Matvejević menciis lin en artikolo titolita „Niaj taliboj”.</p> <p>La kroata leĝo pri informiloj permesas akuzojn pro kalumnio. Plej ofte la akuzantoj estas politikistoj, advokatoj kaj subtenantoj de la nuna politiko. Rezulte, en la nova kroata ŝtato, gazetoj kaj ĵurnalistoj ĝis nun estas punitaj je pli ol 1,5 milionoj da eŭroj. Ekzemple, al unusola homo la semajnrevuo <i>Feral Tribune</i> devis pagi pro unu artikolo 30 000 eŭrojn. Pro aliaj tiaj kondamnoj kaj la sekva financa ŝarĝo la revuo nun aperas laŭ relative malmultekosta nigra-blanka formato.</p> <h3>Pli granda malbono</h3> <p>Sed ŝajne venos eĉ pli granda malbono. Nova leĝo ebligos al tiuj, kiuj per artikolo sentas siajn honoron aŭ reputacion atakitaj, postuli tribunalan repagon de domaĝo sen prezenti pruvojn pri t.n. animskuo. Krome, la interezoj pri la sumo pagota de la „kalumniinto” komenciĝas, kiam aperis la ofenda teksto kaj ne kiam verdiktis la tribunalo.</p> <p>Tial gazetoj aŭ unuopaj verkistoj povos esti materie detruitaj pro iu ajn kritiko. Tio kaŭzos verŝajne memcenzuron kaj praktike neniigos liberan pensfluon kaj la eblon kritiki politikistojn, koruptulojn kaj krimulojn.</p> <p>Kiom politikistoj, ligitaj al juristoj kaj juĝistaro, agas por silentigi kritikon, montras la plej nova kazo. Kroato akuzis la kroatan filozofon Milan Kangrga pri animskuo, ĉar la filozofo en sia libro skribis, ke „kroatoj estas pli malsaĝaj ol bestoj”. Kangrga diris, ke kolombo konas siajn interesojn, sed male kroatoj, kiuj voĉdonis por partio, kiu dum 10 jaroj detruis la ekonomion de la lando.</p> <p>La akuzinto ofendiĝis kaj ĉefoj ĉe la tribunalo rapidigis la proceson kontraŭ la filozofo malgraŭ tio, ke la akuzo estas kontraŭleĝa. Oni ne povas akuzi iun kiel kroaton (apartenanton al nacio) sed nur kiel civitanon.</p> <div style="text-align:right">Zlatko T<span class="mm">IŠLJAR</span></div> 
