      <h3>Scienco</h3>
      <h3 class="tem">ISLANDO</h3>
      <h2>Rekorda konkulo</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Ke estaĵoj povas atingi altan aĝon – foje ege pli altan ol tiu de homoj – estas bone konate. Kelkaj arboj povas fakte vivi milojn da jaroj, dum gigantaj testudoj
      de la Galapagoj povas iĝi eĉ 170-jaraj. La rekordulo inter maljunaj bestoj, tamen,
      pezas nur 52 gramojn kaj malpli grandas ol testudoj: temas pri konkulo kaptita en
      2006 apud Islando kadre de esplora agado!
   </b>
</p> 
<p>Freŝa studo montris, ke tiu konkulo naskiĝis en la jaro 1499. Tiam multaj eŭropanoj
   ankoraŭ kredis, ke la suno rivoluas ĉirkaŭ la tero! La konkulo ekvivis dum la tiel
   nomata Minga dinastio de Ĉinio (1368-1644), kaj jen la kialo, kial oni ĝuste elektis
   la nomon 
   <i>Ming</i>. Tiu konkulo, kiu apartenas al la speco 
   <i>Arctica islandica</i>, fakte aĝis, en la momento, en kiu la esploristoj ĝin kaptis, 507 jarojn, do pli
   ol duonjarmilon.
</p> 
<h3>Ĉarniro</h3> 
<p>
   <i>Ming</i> estis unu el la ducento da konkuloj kolektitaj de sciencistoj de la brita 
   <i>Bangor University</i> en 2006, kadre de priklimata esplorado. Kiel estas kutime, la sciencistoj fridigis
   la konkulojn por ebligi ilian transportadon. Tiu traktado, memkompreneble, provokis
   la morton de la moluskoj, sed tiutempe ili ankoraŭ ne sciis la aĝon de la eroj. En
   laboratorio, la sciencistoj malfermis la konkojn por nombri la jarringojn, videblajn
   per mikroskopo ĉe la ĉarniro, kiu unuigas la du konkoduonojn. Same kiel la arbaj,
   la konkaj jarringoj mem ne nur indikas la aĝon de la ena besto, sed ja enhavas multe
   da informoj pri klimato en la pasinteco. La diko de la ringoj varias cetere laŭ la
   akva temperaturo. Somere konkuloj kreskas pli rapide, tiel formante pli dikan ringon.
   Teamano James Scourse asertis, ke tiuj jarringoj estas la unikaj atestoj de la nordatlantika
   klimata ŝanĝiĝo.
</p> 
<h3>Taksado</h3> 
<p>Jam en 2007 unua taksado pri la efektiva aĝo de la konkulo (405 jaroj) estis publikigita.
   Tiam la libro de rekordoj 
   <i>Guinness</i> registris 
   <i>Ming</i>-on kiel la plej maljunan beston. Nova studo, plenumita en 2013, montris, ke tiu kalkulo
   estis erara, kaj ke la koncerna molusko fakte havis pliajn 102 jarojn. 
   <i>Ming</i>, do, frakasis sian propran rekordon.
</p> 
<h3>Radiokarbona metodo</h3> 
<p>Pro la rimarkinda aĝo de la konkulo, la internaj jarringoj estis tre densaj kaj malfacile
   videblaj. Pro tio la sciencistoj provis nombri la kresk-ringojn videblajn sur ekstera
   faco de la konko. Pli alta aĝo estas konfirmita nelaste per apliko de la tiel nomata
   radiokarbona (C-14) metodo. James Scourse, estro de la teamo, ne havas dubojn: la
   aĝo de 507 jaroj estas tute certa.
</p> 
<h3>Kritiko</h3> 
<p>Post la publikigo de la rezultoj, la scienca teamo ricevis senfundamentan publikan
   kritikon. Ili devis dementi akuzon, laŭ kiu oni konscie detruis la konkon, kvankam
   sciante ekde la komenco pri ties alta aĝo. Sed la kaptita konko estis, kiel ili certigas,
   parto de pli granda scienca projekto, kaj nur hazarde oni poste trovis, ke ĝi havis
   tiom altan aĝon. La sciencistoj alivorte malkovris tiun ĉi karakterizon de la konko
   nur post ĝia morto. Maljunaj konkoj kreskas tre malrapide, kaj tial ne eblas diri
   unuavide, ĉu granda konko aĝas je 100, 200 aŭ 300 jarojn. „Miloj da konkoj de tiu speco estas aliflanke kaptataj kaj manĝataj ĉiujare pro komercaj
   kialoj, do estas tre probable, ke fiŝistoj jam kaptis konkojn, kiuj havis la saman
   aŭ eĉ pli altan aĝon”, rimarkis Paul Butler, ano de la teamo.
</p> 
