      <h3>Turismo</h3>
      <h3 class="tem">USONO</h3>
      <h2>Nekutima feriejo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Ĉe la sud-okcidenta ekstremo de la usona lago Erio – akva landlimo inter Usono kaj Kanado – antaŭe estis Granda Nigra Marĉego. Somere sur areo de 600 km<sup>2</sup> tie vivis nur serpentoj kaj insektoj, ĉefe moskitoj. Tiusezone la vespertoj, kiuj nokte ĉasis super ĝi, preferis iri al siaj kavernoj. Vintre la marĉego estis tre danĝera, ĉar la taga akvonivelo de la apuda lago Erio ofte ŝanĝiĝis. La marĉega glacio moviĝis kaj rompiĝis.</p> <p>Sed aŭtune kaj printempe, dum la grandaj migradoj de bestoj kaj birdoj, la marĉego transformiĝis en grandan manĝejon. Bestoj kaj birdoj, kiuj sezone migris, haltis tie por manĝi. Eĉ la migrantaj papilioj el specio „Monarko”, kiuj somerumas en Kanado kaj vintrumas en Meksiko, uzis kiel haltlokojn la insuletojn trans la okcidenta bordo de Erio. Centmiloj da kolomboj, anasoj, anseroj, agloj kaj aliaj birdoj haltis tie por manĝi. Poste ili migris norden aŭ suden.</p> <p>Nun la marĉego estas plejparte sekigita. Tamen birdoj, papilioj kaj insektoj plu venas. Centoj da hektaroj de ties areo troviĝas sub naturprotekto. La sekigo de la restintaj marĉoj estas leĝe malpermesita.</p> <h3>Parko ĉe la lago Erio</h3> <p>Antaŭ ĉirkaŭ deko da jaroj la ŝtato Ohio decidis estigi parkon apud la bestprotektejoj. Ĝi ekestis tie, kie la rivero Maumee [maŭmi] transiras en golfon de la lago Erio.</p> <p>Kvankam tiuloke la marĉetoj kaj neuzataj terenoj okupas multe da spaco, oni ne ŝanĝis la planojn por la nova parko <i>Maumee Bay State Park</i>. En ĝi oni konstruis luksajn kabanojn sur betonfostoj super la marĉetoj. Ekestis ankaŭ golfludejo kun pli da sablo ol gazono, kun multaj herboj plaĝaj apude.</p> <p>Ĉe la lago oni konstruis bonkvalitan hotelon, kaj apud ĝi ŝiphaveneton. Estis aranĝitaj ankaŭ du plaĝoj. Unu el ili situas krute ĉe Erio, kaj la alia troviĝas tuj malantaŭe kaj estas ĉe konstruita naĝejo subĉiela.</p> <p>La akvo de la rivero Maumee kutime malpliiĝas ĉiusomere pro manko de pluvo. Kaj bakterioj, aparte de specio <i>Escherichia coli</i>, plimultiĝas. Tiu lasta grandakvante malsanigas naĝantojn. Kiam la bakterioj multas kaj malsanigas homojn, oni fermas la plaĝon. En la rivero ja estas bakterioj, ĉar oni ne sufiĉe bone purigas la poluakvon, venantan de urboj, situantaj ĉe la rivero.</p> <p>La naĝejo ja uzas purigitan akvon kaj oni opiniis, ke ĝi ne prezentas danĝeron por ties uzantoj. Tamen evidentiĝis, ke ankaŭ en ĝi estas tro multe da bakterioj de la jam menciita specio. Fine oni konstatis, ke la poluakvo devenas ne de homoj, sed de sovaĝaj bestoj kaj birdoj, kiuj naĝas ene aŭ superflugas ĝin.</p> <p>Kompreneble, ne ĉiujare la nivelo de malpureco sufiĉas, por ke oni fermu la naĝejon. Tamen se vi deziras naĝi, preferinde venu komence de somero.</p> <div style="text-align:right">John G<span class="mm">OODHAND</span></div>     
