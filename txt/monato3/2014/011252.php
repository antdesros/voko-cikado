      <h3>Scienco</h3>
      <h3 class="tem">REAGO</h3>
      <h2>Scienco kaj religio kunfluu, ne kverelu</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>La eseo de Garbhan MacAoidh (M<span class="mm">ONATO</span> 2013/12, p. 12-13), kies prilingvajn artikolojn mi ĉiam frandas, pensigis min. Jen
      la rezulto: kelkaj taŭzitaj konsideroj.
   </b>
</p> 
<p>La verkinto sincere bedaŭras, ke li dediĉis tiom da tempo al teologio. Nu, mi sincere
   bedaŭras, ke li bedaŭras. Eble ni iam sidos ĉetable kaj amike konversacios. La unua
   afero, kiun religio instruas, aŭ instruu, al ni estas humilo. Humilo, sed ne honto.
   Ni ricevis kaj cerbon kaj manojn, kaj la kapablon ami kaj la kapablon kompreni. Jes,
   ni estas polvo, sed polvo, kiu revas pri la steloj, kiel elvoke asertis Carl Sagan.
   Tute ne veras, ke ni estas ia manpleno da malfeliĉaj estaĵoj gvatataj de ĵaluza kaj
   kolera dio, ĉiam preta nin puni, kiel fojfoje oni pensas. Mi ne scias, kies Dio estas
   tia: tutcerte ne la mia.
</p> 
<h3>Injekto</h3> 
<p>Religioj estas aferoj porhomaj: neniu Dio aliflanke alpreĝas sin mem. Religioj provas
   ordigi kaj koherigi tion, kion oni scias aŭ kredas scii pri Dio (Dioj), aŭ kio estas
   al la homo rivelita, montrante la vojon por ke ni kondutu konsekvence. Kaj jes, nia
   stumbla irado antaŭen ofte okazis kaj ankoraŭ okazas sur tereno pavimita je eraroj
   kaj eĉ krimoj plenumitaj je la nomo de la religio. En la okazo de la kristanaj eklezioj,
   se ni akceptas, ke Dio mem fariĝis karno por nia savo, tio estas des pli priplorinda.
   Kristo konceptis sian eklezion kiel societon kontrastan, alternativan. Ni ne ĉiam
   estis aŭ estas kapablaj fari tion, kion la Prediko sur la Monto petas de ni fari.
   Ne por nenio la katolika eklezio pardonpetis, sojle de la tria jarmilo. Pri aliaj
   eklezioj mi ne scias. Mi supozas, ke ili same agis.
</p> 
<p>Krome, ĉar ni ekzistas en tiu ĉi reala mondo kaj ne en iu abstraktaĵo filozofia, ni
   renkontas en la vivo korpremajn aferojn, kies kialon ni eble neniam komprenos. Se
   ni prenas serioze en konsideron la ideon, ke tiun ĉi universon kreis Dio, ni devas
   esti pretaj akcepti, ke ni ne sukcesos kompreni ĉion, kion li faras. Estas tro granda
   la diferenco: nia limigita menso ne povas kompreni. Kelkmonata bebo krianta pro la
   doloro de injekto simile ne povas kompreni, ke temas pri vakcino, aŭ pri grava kuracilo,
   kaj nure premas sin kontraŭ la patrino ... Eble ni agadas sammaniere. Sed kiel konvinki
   suferanton, aŭ denaskan kriplulon, ke malantaŭ nia vivo estas dia amo, Dio ama? Jen
   nia defio.
</p> 
<h3>Atombombo</h3> 
<p>Nekredantoj ofte postulas, flanke de ni kredantoj, ne nur neriproĉeblan konduton (tutprave)
   sed ankaŭ, ke religio ĉion ekspliku. Se religio tion ne scipovas, aŭ la klarigon oni
   trovas nekonvinka (ĉar la aferoj restas trans nia kompreno aŭ postulas tiom da fido,
   kiom kelkfoje scienco petas de siaj adeptoj, aŭ ĉar la klarigon oni rigardas nescienca),
   do religio estas nura opio. Sed al neniu normala homo venus en la kapon la ideo postuli,
   ke scienco faru la samon.
</p> 
<p>Nu, ne ĉio opiniata de religiuloj estas kredinda (krom dogmoj); same ne ĉio farebla
   de sciencistoj estas farinda (la unua, kio venas en mian kapon, estas atombombo).
   Scienco ne havas respondojn pri ĉio ajn. Same ni di-kredantoj devas konscii, kaj konsciigi,
   ke kelkajn aferojn ni akceptas pro nura fido. Kaj bedaŭrinde ne ĉiuj kredantoj kapablas
   konvinke ekspliki, kio subtenas ilian fidon (demandu tiurilate la germanan teologon
   Karl Rahner). Kaj de tempo al tempo serioza sciencisto agnoskas eraron aŭ petas de
   ni revizii la mondkoncepton (demandu tiun intelektan giganton, Stephen Hawking, aŭ
   Albert Einstein). Do ankaŭ scienco ne posedas kiun ajn respondon.
</p> 
<p>Por multaj personoj, scienco kaj religio estas du apartaj mondoj. Scienco apartenas
   al lumo, religio al ombro, al mallumo. Tiu, kiu apartenas al ajna religio, tute ne
   rajtas nomi sin klerulo aŭ humanisto. Ĉio devas esti aŭ nigra aŭ blanka. Griza koloro
   ne rajtas ekzisti.
</p> 
<h3>Alimondanoj</h3> 
<p>La horizonto de scienco estas la donitaĵoj. Do la kutima sinteno en bona scienco estas
   pli-malpli la jena: se pri io ne haveblas donitaĵoj, pri ĉi tio ni ne povas juĝi.
   Punkto fina. Sed kelkaj pluiras: se pri io ne estas donitaĵoj, ĉi tio tute ne ekzistas.
   Laŭ tia rezonado, en la mezepoko tute ne ekzistis radioaktiveco (neniu kapablis koncepti
   aŭ mezuri ĝin). Nek ekzistis nanoplanedoj, hercaj ondoj, kosma radiado. Oni povus
   longe kotopoi. Kaj bedaŭrinde ne ekzistas scienca instrumento por „detekti” Dion aŭ por foti la animon.
</p> 
<p>Mi multe ŝatas la sciencpopularigajn verkojn de Sagan. Se de tempo al tempo agacas
   min liaj kontraŭreligiaj atakoj, mi konscias, ke li estis sincera kredanto je scienco.
   Mi komprenas, pardonas kaj plulegas. Jen ĉio. Kiam finfine eblis spekti en mia lando
   lian televidserion 
   <i>Kosmo</i>, mia tuta familio kvazaŭreligie sidis antaŭ la malnova televidilo, tagon post tago.
   Ni dividas lian revon trovi eksterteran vivon. Se gravas daŭrigi la esploradon cele
   al la malkovro de eventualaj alimondanoj, kiel faras la sciencistoj per la programo
   
   <i>SETI</i>, kvankam ĝi ŝajnas al multaj uloj tute senutila afero, same gravas la esplorado pri
   Dio, por multaj mem sensenca kaj senutila. Ambaŭokaze temas pri aferoj pensigaj: eĉ
   la plej malgranda malkovro gravas. Kompreneble (almenaŭ por ni romkatolikoj) neniel
   ekestos teologiaj problemoj, se iam oni trovos eksterterajn estaĵojn; aperos nur interesegaj
   demandoj.
</p> 
<h3>Gravito</h3> 
<p>Aliflanke, kelkaj el niaj plej ŝatataj teorioj restas ankoraŭ teorioj, ĉar ne plene
   pruvitaj. Ĉiu nova teorio iel kolizias kun la antaŭaj. Tiom da fido ni bezonas por
   ilin akcepti (ĉar ni ne kapablas sekvi la matematikan rezonadon, aŭ ĉar la ekapero
   de novaj specioj postulas milionojn da jaroj, do ne sufiĉas sidiĝi kaj atendi por
   esti tute certaj, aŭ ĉar la modelo de nia universo ne estas finpolurita, aŭ ĉar kelkaj
   obstinaj faktoj ne akceptas esti enkadrigitaj en teorio), kiom por akcepti religion.
   Fortan fidon mi ja bezonas por sendiskute akcepti (kaj tute ne kompreni!), ke gravito
   deformas la spacon (kiel tio povas okazi aŭ, eĉ pli grave, kial?) aŭ imagi, ke iam,
   en la iamo longe for (sed en tiu momento ne ekzistis tempo, do tute ne havas sencon
   paroli pri „momento” aŭ pri „iam”!), la tuta materio (ege disputveka koncepto, tiu de materio) en la universo estis
   koncentrinta sin en sendimensian punkton kaj eksplodis (denove: kial?) laŭ leĝoj fizikaj
   (sed nenio ekzistis, do ankaŭ ne leĝoj!), kaj komencis aperi kaj partikloj kaj atomoj.
   Neeviteble krablas en mia menso la penso: kaj antaŭe?
</p> 
<p>Eĉ nur se temus pri la argumento de Tomaso el Akvino, ke ne eblas imagi senfinan ĉenon
   da kaŭzoj (iam oni provis konvinki min pri tio, ke neracionalaj numeroj estas ekzemplo;
   sed temas pri argumento piroteknika, ne pri la samo), mi dubus. Oni metas antaŭ nin
   iun universon, kie ĉio, ne nur vivo, ŝuldiĝus al nura hazardo: universon supozeble
   (aŭ supozende, laŭ kelkaj) pulsantan de sendimensia punkto al eksplodo kaj reen, aŭ
   dancantan laŭ la takto de la teorio de la kordoj (se nur temus pri kaloĉajaj streĉitaj
   kordoj!). Kaj ĉio ĉi regule laŭ leĝoj fizikaj, kiuj tute hazarde estus ĉiam la samaj.
   Nu, tro da hazardo por mi.
</p> 
<h3>Ŝtopilo</h3> 
<p>Kio gravas estas tio, ke nek teologio nek scienco ŝovu la nazon en fremdan vazon.
   Dio ne estas ŝtopilo nepre uzenda ĉie, kie scienco ankoraŭ havas nenion por diri,
   nek scienco turniĝu en religion. Al ĉiu sanktulo apartan kandelon. Plej grave: homo
   ne estas batalkampo por scienco kaj religio. Ili kunfluu, ne kverelu. Kaj jen, sufiĉe:
   ni prefere petu al la kelnero alporti du pliajn trinkaĵojn kaj parolu pri aferoj iom
   malpli komplikaj.
</p> 
