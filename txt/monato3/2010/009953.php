      <h2>Aŭtoro je vere monda nivelo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Mi pensis, ke jam iom mi scias pri la historio de l' 20a jarcento; mi pensis, ke jam iom mi legis pri ambaŭ grandaj militoj, kiuj vundis nian kontinenton tiu-jarcente, pri la sensenceco de la interbuĉado de la Unua Mondmilito, pri estiĝanta Naziismo k.s.; eble mi eĉ pensis, ke mi jam legis pri tio tro, sed mi konstatis, ke denove mia korpo reagas harhirtige: Trevor verkas terure – terure realec-proksime, terure timtremige: oni kunvivas la riskan periodon, kiam Berlino ne nur estis kulturbredejo de Eŭropo plej riĉa, sed ankaŭ nesto de vipuroj plej hida. Oni aŭskultas la raportaĵojn el fora Aŭstralio, el tempo multe pli posta pri grupo ĉirkaŭ maldekstrema revueto <i>Novaj Folioj</i> (la „kajeroj”, kiel amplene oni nomas ilin): legante, oni kunvivas, kunvidas disvolviĝantan memkonscion de junuloj, ŝanĝiĝantajn sintenojn en la socio, ekeston de minacoj kaj danĝeroj ktp. Trevor denove malkovras por ni, kio estis la 20a jarcento.</p> 
<p>Kaj en tio li ne nur estas majstra manipulanto (li pardonu la esprimon) pri la montrado de diversaj bildoj, scenoj, kiuj kompletigas unu la alian: ili kuniĝas en impona klimakso. Kaj samtempe li estas stile tre zorgema, ne uzas superfluajn neologismojn, sed, aliflanke, ankaŭ ne nepre limigas sin je minimumo. Almenaŭ mi estas kontenta, ke foje eĉ kelkajn vortojn li uzas nacilingve por iom „kolorigi” la medion. Nur unu veran bedaŭrindaĵon mi trovis: sur paĝo 181 la faman pacifisman romanon <i>Im Westen nichts Neues</i> el 1929 de E.M. Remarque li nomas piednote <i>Nenio nova el okcidento</i>, sed efektive ĝi aperis en Esperanto jam samajare sub pli ĝusta titolo: <i>En okcidento nenio nova</i>. Parenteze, la malmultaj piednotoj ie tie tute ne bremsas la legadon, male: ili utilas. Estas bonvene eĉ en romano legi spicajn detalojn.</p> 
<h3>Granda ĵurnalisto</h3> <p>La rakontata tempo de la verko iras de „antaŭ la Granda Milito, nun nomata la Unua Mondmilito” (paĝo 15) ĝis en la nunon de la rakontanto, 1990 (paĝo 9). Tiu nuno daŭre rolas fone, kaj tial ne temas pri nur „denove historia romano pri tiuj militoj”. Sed la vera temo estas la <i>personeco</i> de vere granda ĵurnalisto, kia li estis – ĉi Kurt en la romano, la viro, kiu rifuzis eskapi, kiam naziismo ekregis, simple ĉar li kredis, ke „mi devas agi kvazaŭ nun ĉio dependus de mi”. La rakontisto, al kiu ni ŝuldas tiun ĉi verkon, intencis omaĝi al Carl von Ossietzky, unu el la fruaj viktimoj de la terora reĝimo de Hitler, kaj al lia brila kunbatalanto Kurt Tucholsky. Li gratulinde brile sukcesis. Foje oni kvazaŭ vidas la inteligentan grimac-rideton, kun kiu Trevor – negermano – skribas: „negermanaj legantoj eble bezonas iom da klarigo” (paĝo 91) por doni vere senchavan klarigon. Kelkfoje li eĉ eksorĉas ĉe mi – naskita dum la lasta periodo de l' milito – el obskuraj anguloj de la cerbo etajn memoraĵojn pri tiu fora tempo.</p> <p>Se Trevor ne jam delonge pruvis sian staturon – ne de simple unu inter la Esperanto-verkistoj, da kiuj nuntempe ni havas plurajn manplenojn, sed de aŭtoro je vere monda nivelo, tiam certe nun tion li montris.</p> 
