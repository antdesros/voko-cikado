      <h3>Spirita vivo</h3>
      <h3 class="tem">KONFESIOJ</h3>
      <h2>Ŝanco por interreligia dialogo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Nova centro pri interreligia dialogo ekfunkcios en 2012 en Vieno. Ĝi estos financata de Sauda Arabio. Temas pri persona iniciato de la saud-araba reĝo Abdullah. Jam ĉi-jare en la centro 60 kunlaborantoj komencos sian agadon. Ties sidejo estos en la palaco Sturany, en la viena urbokerno.</p> <p>Per la fondo de la centro pri interreligia dialogo „Reĝo Abdullah” oni celas dialogon inter reprezentantoj de malsamaj konfesioj. Per ĝi oni volas preventi konfliktojn kaj militojn – tiucele estos planataj konferencoj, seminarioj kaj perfektigaj aranĝoj.</p> <h3>Kontrolkonsilio</h3> <p>En la kontrolkonsilio de la nova organizaĵo estos dek du reprezentantoj de la grandaj mondreligioj. Oni antaŭvidas en ĝi almenaŭ tri islamanojn (du sunaistojn, unu ŝijaiston), tri kristanojn (po unu reprezentanto de katolikoj, anglikanoj kaj ortodoksuloj), unu budhiston, unu hinduiston kaj unu judon. Kiel plian organon de la dialog-centro oni antaŭvidas konsilion. En la ĉi-lasta estos 100 personoj de la mondreligioj kaj aliaj kredkomunumoj, kaj ankaŭ sciencistoj kaj reprezentantoj de la civila socio.</p> <h3>Kontrakto</h3> <p>La ministroj pri eksteraj aferoj Michael Spindelegger (Aŭstrio), Trinidad Jiménez (Hispanio) kaj princo Saud Al-Faisal (Sauda Arabio) subskribis en Vieno kontrakton pri la fondo de la centro. La tri landoj lanĉis per tio la konstrufazon de nova internacia organizaĵo, kiu devas proponi dialogon inter la mondreligioj. Ili emfazis la signifon de interreligia dialogo por prevento de konfliktoj kaj por firmigo de monda paco. La aŭstra ministro Spindelegger akcentis kadre de la subskriba ceremonio, ke per la antaŭvidita strukturo de la centro neniu el la reprezentataj religioj havos hegemonion. La celo estas, ke laŭeble multaj landoj eniru la organizaĵon. En ĝi la Sankta Seĝo havos statuson de observanto.</p> <p>La fondan solenaĵon en la viena muzeo Albertina partoprenis multaj religiaj reprezentantoj, politikistoj kaj diplomatoj, inter kiuj kardinalo Jean-Louis Tauran, reprezentanto de la Papa Konsilio por Interreligia Dialogo. Nerekte oni povas nomi la papon Benedikton la 16an unu el la „baptopatroj” de la nova forumo. En novembro 2007 reĝo Abdullah vizitis la papon en Romo. Tiu renkontiĝo poste stimulis lin lanĉi la iniciaton, pri kies realigo reprezentantoj de la menciitaj landoj decidis jam en julio 2009.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>        
