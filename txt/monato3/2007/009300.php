      <h2>Sukcesoplena, ŝercoplena kaj tre malkutima</h2>
      <!-- komenco de la enhavo -->

       

     <p>Unuavide onin trafas la rimarko, ke tute mankas tabelo de enhavo, aŭ iu alia indekso, por ĉi tiu libro. Kombininte tiun rimarkon kun medito pri la tre strangasona titolo „Nemave edifi”, oni konkludas, ke atendas legon tre nekutima libro. Kaj oni plene pravas.</p> <p>Krome tiu libro ne nur siamaniere ja edifas, kaj tute vere „nemave” (= ne malbone), la enhavo tiom ridigas la leganton, ke oni facile forgesas, ke temas pri instrulibro.</p> <p>Temas, plejparte, pri la spertoj de spiono 005, kies sekreta tasko estas protekti Esperanton kontraŭ atakemaj malamikoj kaj insidaj reformistoj. Sed kiaj malamikoj? Nu, ĉefe la loĝantoj de Volapukastano. Kaj kiaj reformistoj? Nu, oniaj antaŭsupozoj eraras, kaj mi lasos al vi legi pri ili en la libro.</p> <p>Forme, la plejparto de la libro konsistas el sinsekvo de satiraj kaj parodiaj dramtekstoj, en kiuj spiono 005 ĉefe aŭ parte rolas; ĉiun sekvas granda serio de enigmaj demandaroj, ekzercoj kaj taskoj, kies plenumo postulas detalan kaj tre kribran trastudadon de la koncerna dramo. Enestas ankaŭ poemo, prikomputila farso (titolita „La Diservanto kaj la tomato”), scienc-fikcia dramo, celanta kompari la nombron de esperantistoj sur la tero kaj sur Venuso, kaj kiom pli! Ĉiu kun propraj ekzercoj.</p> <p>Per la parodioj kaj spektaklaj satiroj vi multegon lernos per ĉi tiu libro. Ĉu vi volas scii, kial la Universala Kongreso en Adelajdo daŭris multajn monatojn (ĉar iu forŝtelis la oficialan martelon, per kiu oni fermas universalajn kongresojn)? Kiom da vortoj kapablas paroli kamelo? Kiel sonas la Fonetika Esperanto, laŭ kiu oni prononcas ankaŭ ĉiujn interpunkciajn signojn? (Se mi ĝuste memoras, la muzik-komediisto Victor Borge distradis siajn aŭskultantojn per simila sistemo, rilate la anglan lingvon, antaŭ multaj jaroj.) Kiel produkti konstantajn furzojn? Kiel kalkuli la suman nombron de esperantistoj? Kial fiaskis la Esperanto-movado sur Venuso? Kiu torturo estas la plej maltolerebla el ĉiuj torturoj? Veran enciklopedieton da absurdaj, komikaj pseŭdofaktoj prezentas al ni Stefan MacGill. Kaj, kiel oni scias, ĉe la bazo de ĉiu ŝerco, kutime kaŝite, ekzistas tre reala vero, kiun oni preferas ignori aŭ nur prieŭfemismi.</p> <p>Ĉu via klubo aŭ kursanaro deziras enscenigi rolludojn bazitajn, pli-malpli, sur la dramoj en la libro? Detalojn pri la materialoj, metodoj kaj temoj donas la antaŭlasta parto de la libro.</p> <p>Da preseraroj, lingvaj eraroj kaj aliaj ĝenaĵoj mi rimarkis neniom.</p> <p>Pli profundan rekomendon de libro mi ne kapablas fari.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Stefan MacGill: Nemave edifi. Eld. FEL, Antverpeno, 2007. 263 paĝoj kudritaj. ISBN 90-77066-25-X.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=nemave">la Retbutiko</a>.
        </h6>
