      <h3>Leteroj</h3>
      <h3 class="tem"></h3>
      <h2>La nuna financa krizo (4)</h2>
      <!-- komenco de la enhavo -->

       

      <p>Oni ne povas rigardi la aktualan krizon sen ligoj al la periodaj (ciklaj) krizoj de la kapitalismo. Ili estas ne invento de la komunistoj, sed necesa parto de la sistemo. Periodaj krizoj aperas post ĉiuj 7-10 jaroj. La lasta estis fine de la pasinta jarmilo, estis krevo de interretaj entreprenoj. La krizon komence de la 1990aj jaroj en Germanio la unuiĝo nuligis – sub certaj cirkonstancoj la periodaj krizoj povas malesti.</p> <p>Oni respondu la demandon: kio estas mono? Laŭ novaj teorioj, mono estas nur valuta unuo en komputilo. Oni povas laŭplaĉe ŝovi monon de unu konto al alia. Pli klasika (kaj laŭ mia kredo ĝusta) teorio diras, ke mono estas „realigita plusvaloro”, pli simple dirante, oni devas produkti varon kaj vendi ĝin. La diferenco inter la kostoj por la produktado de varo kaj ties prezo, do la plialtiĝo de la valoro, naskas monon. La ŝtato do devas kalkuli, kiom da valoro estas realigata en la ekonomio, kaj presigi tian kvanton de mono. Unu el la kaŭzoj por la ciklaj krizoj estas malĝusta rilato inter reale produktata valoro kaj presita mono.</p> <p>Kaj fine: kiel la aŭtoro skribas, la bankoj uzas la monon plurfoje. Mono, kiu ekzistas nur en komputilo, povas esti ŝovata de unu banko al alia, al tria, al kvara. Kun reala mono tio ne estas farebla: mi havas monon en la mano, aŭ ne. Aĉetante ion, mi donas monon kaj ne povas doni ĝin duan fojon. Evidente la bankoj reciproke donadis kreditojn, ĝis neniu sciis, kiom da reala mono kovrata de plusvaloro estas en la sistemo. La ŝtatoj ne estas senkulpaj: de jardekoj la monto de ŝuldoj kreskas. La ŝtatoj prenas kreditojn de la bankoj – sed de kie la bankoj prenas la monon? Ili ĝin interŝovas en komputilo.</p> <p>Iam tiu procezo kolapsas. Ĝi devas kolapsi, ĉar la merkata ekonomio ne permesas senliman misuzon de ekonomiaj reguloj. Do la aktuala krizo okazas ne ĉar la merkato malfunkcias; ĝi estas konsekvenco de merkataj reguloj. La merkato funkcias devige, ĉie kaj ĉie, kaj kiam la amikaro konsistanta el bankoj kaj ŝtatoj misuzas la eblojn de komputila mono, tiam la merkato kadre de la sekva cikla krizo restarigas la necesan ekvilibron inter produktado kaj vendado de varoj unuflanke kaj la kvanto de mono rezultanta el tio aliflanke.</p> <p>Do mono ne estas detruita dum la krizo. La ŝajne detruita mono neniam ekzistis.</p> <div style="text-align:right">Michael L<span class="mm">ENNARTZ</span></div> <div style="text-align:right"><i>Germanio</i></div> 
