      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Abrupta demisio  denove</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 1an de septembro 2008 la ĉefministro Fukuda Jasuo anoncis sian demision. Fukuda ĉefministriĝis la 26an de septembro 2007, post subita eksiĝo de la antaŭa ĉefministro Abe Ŝinzoo.</p> <p>Salutante sian ĉefministriĝon, Fukuda diris, ke lia estas „kabineto, kiu senelire batalas kun rivero malantaŭ si”, kio signifis, ke eĉ unu malgranda fuŝo dronigus la Liberalan Demokratan Partion (LDP). Verdire li ja dronigis sian partion pro sia abrupta, senrespondeca demisio.</p> <p>Li klarigis sian demision tiel: „La plej granda opozicia Demokrata Partio (DP) forte kontraŭis ĉiujn gravajn leĝprojektojn en la lasta sesio. Se mi plu estos la ĉefministro, la samo okazos en la venonta sesio, tiel ke mi volas lasi tiun taskon al alia”.</p> <h3>Kialoj</h3> <p>Ekzistas ĉefe tri kialoj por la demisio. Unue, rilate Usonon, la politiko de LDP tute ne konvenas al la deziroj de japanoj, kiuj suferas pro malfacila vivo. LDP ĉiam direktas sin al Usono, sekvante ĝin kaj akceptante ĝiajn postulojn kiel subtenon al la Iraka Milito.</p> <p>Due, LDP multe dependas de la partio Koomei, sen ties helpo ĝi ne povas venki en elektoj. Tamen Koomei hezitis subteni la ĉefministron rilate subtenadon de usona ŝiparo en la Hinda Oceano. Cetere ĝi postulis malaltigon de impostoj por malriĉuloj, de kiuj Koomei dependas. Fukuda ne volis akcepti malaltigon tian, sed devis, timante, ke Koomei ne plu helpos dum elektoj.</p> <p>Trie, temas pri la nekapablo de Fukuda mem. Li fariĝis parlamentano je la aĝo de 54. Antaŭ ol ĉefministriĝi, li laboris kiel ĝenerala sekretario en antaŭa kabineto, sed li ne havis sperton kiel ministro. Ankaŭ de tiu ĉi posteno li fine demisiis.</p> <h3>Honoro</h3> <p>Lastatempe subtenis lin nur ĝis 30 % de la popolo. Oni kritikis lin, dirante: „Li parolas ĉion kiel fremdaĵojn”, kaj „Ni ne povas vidi, kion li volas fari”. Sume al li mankis gvidkapablo kiel ĉefministro. Ŝajne li pensis nur pri sia honoro, ne pri Japanio.</p> <p>LDP devas elekti novan partiestron, sub kiu ĝi povos venki en la ĝenerala elekto. Tamen Abe kaj Fukuda demisiis same abrupte, ĝuste antaŭ parlamenta sesio. Tiel ili seniluziigis la popolon, kiu pridubas la regmanieron kaj la kapablon de la partio. Estos malfacile al la partio reakiri fidon inter la popolo. Samtempe perdis Japanio sian konfidon en la mondo.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
