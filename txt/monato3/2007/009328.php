      <h3>Ekonomio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Registaro asertas, ke manĝaĵoj estas sekuraj</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Lastatempe en la eŭropa kaj usona gazetaroj aperis artikoloj pri ĉinaj produktoj, kiuj povas endanĝerigi la sanon. Precipe temis pri ludiloj, kies farbo povas noci infanon, kiu metas la ludilon en la buŝon, sed estis plendoj ankaŭ pri manĝaĵoj.</b></p> <p>Meze de aŭgusto de 2007 la novaĵa oficejo de la Ŝtata Konsilantaro de Ĉinio publikigis „blankan libron”, kun titolo <i>La kvalito kaj sekureco de ĉinaj manĝaĵoj</i>. Per tia libro la registaro unuafoje provis senpartie konigi al la monda publiko la sekuran staton de ĉinaj manĝaĵoj.</p> <p>La libro montras, ke ĝenerale la manĝaĵa kvalito stabilas je alta nivelo. Dum la pasintaj jaroj ĉiam pli ol 99 % de la eksportitaj manĝaĵoj respektis la kvalitnormojn. Por eksportoj al Eŭropo tiu kvoto atingis eĉ 99,8 %. Okaze de enlandaj kontroloj la kvalito pliboniĝas. Specimenaj kontroloj en la tuta lando dum 2006 montris, ke 77,9 % de la manĝaĵoj estis bonkvalitaj; en la unua parto de 2007 la proporcio kreskis ĝis 85,1 %. De la totala eksportita agrikultura produktado, 90 % estas produktoj „verdaj” (produktitaj kun respekto por la natura medio).</p> <p>Kvankam rilate al la kvalito de ĉinaj manĝaĵoj ja ankoraŭ ekzistas kelkaj problemoj, la registaro streĉas la pensojn kaj agojn, por ke tiuj problemoj iom post iom estu solvataj. Ekzemple, ĝi jam faris la unuajn paŝojn por enkonduki arkivsistemon ĉe manĝaĵaj entreprenoj por plifaciligi kaj pliefikigi la kontroladon. Ĝi ankaŭ komencis aperigi nomlistojn ruĝan (por bone kondutantaj eksportantaj entreprenoj) kaj nigran (por kritikendaj entreprenoj). Precipe la etaj manĝaĵfarejoj (kun malpli ol dek laboristoj) estu severe kaj ofte kontrolataj, ĉar nur 70 % de tiuj funkcias laŭnorme.</p> <h3>Porka malsano altigas la prezojn</h3> <p>Pro terura infekta malsano kiu trafis kelkajn regionojn, multaj porkoj en ĉinaj bredejoj mortis. Tio ne influas la kvaliton de la vendata viando, sed pro la malpli granda oferto la prezoj de porkoviando leviĝis. En julio tiu viando vendiĝis kontraŭ 26 juanoj por unu kilogramo. En aŭgusto la prezo estis 30 ĝis 32 juanoj. Ankaŭ aliaj varprezoj draste supreniras.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div> <p>La teksto de la blanka libro (en angla lingvo) legeblas je <a href="http://www.chinadaily.com.cn/bizchina/2007-08/18/content 6032837.htm">www.chinadaily.com.cn/bizchina/2007-08/18/content 6032837.htm</a></p> 
