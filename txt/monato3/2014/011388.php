<p>La plej priparolata malgajno estas tiu de la Laborista Partio. Ĝia estro, David Cunliffe
   [kanlif], volis resti ĉefo, sed fine li rezignis. Kvar novaj kandidatoj sin prezentis.
</p> 
<p>Komence de oktobro policanoj serĉis la hejmon de Nicky Hager kaj forprenis komputilojn,
   kadre de enketo pri kodrompado de retmesaĝoj de unu el la fiblogistoj de la Nacia
   Partio. Hager rifuzis malkaŝi la fonton de siaj informoj, pravigante tion per ĵurnalisma
   konvencio.
</p> 
<p>En la nova registaro la ĉefministro, John Key, mem fariĝos ministro pri enlanda sekureco
   kaj informado. Judith Collins ne estas nomumita ministro kaj do la plej altranga virino
   en la kabineto estas Paula Bennett, kiu respondecas pri ŝtataj servoj, sociala loĝigado,
   lokaj registaroj, kaj rolas kiel vicministro pri financoj.
</p> 
