      <h3>Moderna vivo</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Celataj: malpli altaj luprezoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Kreskanta urbo – malabundaj loĝejoj: Jen la situacio en Vieno, la ĉefurbo de Aŭstrio. La loĝantara
   kresko estas la sekvo de daŭra enmigrado el aliaj landoj de Eŭropa Unio, Afriko kaj
   la Proksima Oriento.
</p> 
<p>Baldaŭ loĝos en Vieno du milionoj da homoj. Sekve la prezoj de loĝejoj kreskas. Nuntempe
   por luata loĝejo oni pagas ĉiumonate 10 ĝis 20 eŭrojn por ĉiu kvadrata metro, kaj
   por propra loĝejo 2000 ĝis 5000 eŭrojn por kvadrata metro – plej ofte sumon nepageblan por juna familio.
</p> 
<h3>Kooperativoj</h3> 
<p>Privataj firmaoj kaj kooperativoj konstante konstruas loĝejojn, sed ne sufiĉe. Tial
   la komunumo Vieno, post kelkjara paŭzo, baldaŭ konstruos loĝejojn kun la celo malaltigi
   la luprezojn. Ĝis la jaro 2018 estiĝos 2000 novaj loĝejoj, kies subvencio atingos
   25 milionojn da eŭroj.
</p> 
<p>La monata luprezo estos maksimume 7,50 eŭroj por ĉiu kvadrata metro. La politikistoj
   de la Verda Partio kritikis tiun planon de la socialdemokratoj. Ili volas 1000 loĝejojn
   pli.
</p> 
