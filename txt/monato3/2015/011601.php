      <h3>Politiko</h3>
      <h3 class="tem">UNUIĜINTAJ NACIOJ</h3>
      <h2>Novaj planoj pri daŭripova evoluo</h2>
      <!-- komenco de la enhavo -->

       

         <p>Malsaton kaj ekstreman malriĉon volas forigi la internacia komunumo laŭ ambicia programo
   akceptita de Unuiĝintaj Nacioj (UN) en Vieno. La programo celas la periodon 2015-2030,
   kiam ĉiuj en la mondo havu aliron al pura akvo, deca necesejo kaj senpaga elementlerneja
   klereco.
</p> 
<p>La listo de 17 evoluigaj celoj daŭrigas tiujn de la periodo 2000-2015, kiam laŭ UN
   okazis la plej sukcesa programo kontraŭ malriĉo en la historio de la mondo.
</p> 
<p>La t.n. jarmilaj evoluigaj celoj, laŭ UN, kunhelpis savi milionojn da homoj el malriĉo,
   rajtigi knabinojn kaj virinojn, kaj plibonigi sanon kaj bonfarton. Ekzemple, la nombro
   de homoj, kiuj vivas en ekstrema malriĉo, pli ol duoniĝis, de 1,9 miliardoj en 1990
   al 836 milionoj en 2015.
</p> 
<h3>Egaleco</h3> 
<p>La mondo spertis ekde 2000 draman plibonigon ĉe la seksa egaleco en lernejoj, aparte
   en bazaj lernejoj, kaj pli multaj knabinoj frekventas lernejojn. Krome, en la lastaj
   20 jaroj, virinoj en preskaŭ 90 % de la 174 landoj, kiuj liveris informojn, pli bone
   reprezentiĝas en parlamentoj.
</p> 
<p>Ekde 1990 pli ol duoniĝis la nombro de mortintaj infanoj sub kvin jaroj, de 90 al
   43 el 1000 novnaskitoj. La nombro de mortintaj patrinoj montras tutmonde malgrandiĝon
   de 45 %.
</p> 
<p>Pli ol 6,2 milionoj da malario-mortoj estis malhelpitaj inter 2000 kaj 2015. Krome
   per tuberkulozo-profilaktiko, diagnozado kaj kuracado, oni savis la vivon de 37 milionoj
   da homoj inter 2000 kaj 2013.
</p> 
<h3>Konfliktoj</h3> 
<p>Tamen progreso malegalas en diversaj regionoj kaj landoj, kie restas signifaj mankoj.
   Konfliktoj prezentas kiel antaŭe la plej gravan minacon al homa evoluo, ĉar malstabilaj
   kaj militantaj landoj havas la plej altan malriĉon.
</p> 
<p>La novaj 17 celoj estas direktataj al malegalecoj, ekonomia kresko, urboj kaj homaj
   vivospacoj, industriigo, energio, klimatŝanĝiĝo, daŭripovaj konsumado kaj produktado,
   samkiel al paco kaj justeco.
</p> 
<p>La programo devigas ĉiujn landojn aktive agadi ne nur por elimini la kaŭzojn de malriĉo,
   sed ankaŭ plialtigi ekonomian kreskon kaj plibonigi sanprotekton, klerigon kaj sociajn
   servojn, protektante samtempe la naturmedion.
</p> 
