      <h3>Moderna vivo</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Eklezia ludo kondamnita</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 1an de julio Litovio adiaŭis sian unuan demokratie elektitan prezidanton post la denova sendependiĝo. Mortis 77-jaraĝa Algirdas Brazauskas, kiu estis la prezidanto inter 1993 kaj 1998 kaj ĉefministris inter 2001 kaj 2006. Malgraŭ sia komunista pasinteco (en la tempo de Sovetio li estris la litovan komunistan partion), li tamen fariĝis unu el la plej ŝatataj politikistoj en Litovio.</p> <p>Neniu atendis, ke lia morto estigos tiom da kontraŭaj emocioj. Sed okazis ĝuste tio, kiam romkatolika kardinalo decidis, ke sankta meso por la forpasinto estos celebrata en la katedralo de Vilnius sen la kadavro de la mortinto.</p> <h3>Duboj kaj diskutoj</h3> <p>Litovio estas katolika lando, kie religia tradicio tre gravas, des pli dum funebra ceremonio de eminenta persono. Tial la decido de la eklezio semis dubojn kaj diskutojn.</p> <p>Leviĝis la demando, kial Brazauskas, kiu dum la liberiĝa periodo redonis la katedralon, tiaman bildgalerion, al la eklezio, ne bonvenas tie. Fakte, eĉ okaze de lia inaŭguro kiel prezidanto la eklezio malpermesis al li uzi la ĉefpordon de la katedralo, insistante pri flanka pordo.</p> <h3>Dua edzino</h3> <p>Eble, argumentis la popolo, la eklezio malkontentis pri lia komunista pasinteco. Aŭ eble kulpis la fakto, ke li vivis kun sia dua edzino sen religia geedziĝo. Ne respondis la pastraro. Intertempe sian koleron esprimis la nuna prezidanto de Litovio, Dalia Grybauskaitė.</p> <p>Tial okazis du funebraj mesoj – unu en Vilnius kaj alia en la urbo, kie Brazauskas plenkreskis. Pro tio rifuzis partopreni unu aŭ alian meson Grybauskaitė, dirante, ke ŝi ne volas partopreni ludon de eklezio kaj politikistoj.</p> <p>Post la funebra meso la ĉefepiskopo provis pacigi la popolon. Li klarigis, ke unu enteriga formo taŭgas por adiaŭi fervoran katolikon; dua iun, kiu famiĝis pro karitata aktivado; kaj tria, kiam oni konfidas nur al la mizerikordo de Dio.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
