      <h3>Moderna vivo</h3>
      <h3 class="tem">VIRINOJ</h3>
      <h2>Inter Oriento kaj Okcidento</h2>
      <!-- komenco de la enhavo -->

       

       <p>„La araba virino inter Oriento kaj Okcidento” – jen la temo de Unesko-simpozio, organizita en junio en Parizo. Efektive, kiel konstateblis dum la simpozio, malfacilas difini, kiu estas araba virino, ĉar „araba” ofte estas komprenata kiel „islama”, dum ekzistas araboj ne islamanaj kaj islamanoj ne arabaj; krome Nord-Afriko ne situas oriente de Francio.</p> <p>Ĝenerale, tamen, la bildo pri la araba virino simplas: ŝi sidas hejme, ŝi estas analfabeto kaj ŝi estas batata de la edzo. Sed la situacio de virinoj malsimilas de lando al lando. En Maroko, ekzemple, 10,6 % de la parlamentanoj estas virinoj; en Egiptio 2 % (averaĝe 14 % en la mondo). En kelkaj landoj nur la Korano (legata kaj interpretata evidente nur de viroj) decidas pri la statuso de virinoj, dum aliaj landoj havas konstitucion, kiu respektas homajn rajtojn.</p> <h3>Virino-patrino</h3> <p>Avantaĝo por la arabaj landoj estas komuna lingvo. Sed la klasika lingvo estas sentata kiel vireca; ĝi estas la lingvo de religio kaj kunligata kun ideo de regado. Ĝi restas nealirebla al virinoj tro ofte analfabetaj (tri kvaronoj de la 65 milionoj da analfabetoj en arabaj landoj estas virinoj). Male, dialektaj variaĵoj asociiĝas kun la virino-patrino, kun transdono de emocioj, sentoj kaj travivaĵoj.</p> <p>Malfacilas al la araba virino liberiĝi de la premo de la Korano kaj de la viroj. Tro ofte la tiel nomataj feministinoj luktas laŭ okcidenta modelo kaj perdas sian kulturon. La problemon kreas viroj, kiuj deziras konservi sian privilegian pozicion. Virinoj devas reakapari la religiajn tekstojn, legante ilin per virinaj okuloj. Modernaj islamaninoj preferas surmeti kap-tukon (kelkfoje ĝi iĝas beligaĵo) por ebligi al si kontraŭi al Okcidento kaj ties valoroj; samtempe ili volas ĝui la liberecon viziti universitaton aŭ iri al kafejo aŭ kinejo. Memorindas, ke antaŭ nur kelkdeko da jaroj ankaŭ okcidentaj virinoj neniam eliris surstraten sen kapkovrilo.</p> <h3>Kvalitaj programoj</h3> <p>Rilate kulturon, eĉ se jam en 1890 libananino, Zeinab Fawaz, batalis en Kairo kiel ĵurnalistino por liberigi virinojn, tro ofte mankas kvalitaj programoj por disvastigi kulturan diversecon. Ĉefe bremsas liberan esprimon cenzuro, regado fare de religiistoj, tabuoj kaj malpermeso de minoritataj lingvoj.</p> <p>Kiel virinoj povos kontribui al kultura politiko? Ĉeestantoj ĉe la simpozio estis tre malfermitaj, sed leviĝas la demando, ĉu la partoprenantaj ambasadorinoj, universitataninoj, ĵurnalistinoj kaj ministrinoj, kiuj ofte edukiĝis en okcidentaj universitatoj, reprezentas la orientan araban virinon.</p> <p>Tamen Unesko akceptis la tiel nomatan <i>Planon Arabian</i> por promocii la dialogon inter la kulturoj, konigi la kontribuon de la araba kulturo, kontribui al disvastigo de la nuna kulturo kaj helpi al libera interŝanĝo de homoj.</p> <div style="text-align:right">Renée T<span class="mm">RIOLLE</span></div>  
