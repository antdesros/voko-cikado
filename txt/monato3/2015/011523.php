      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Ĉu finiĝis la postmilita periodo?</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>Ĉi-jare, en 2015, okazas tutmonde ceremonioj por memorigi la 70an datrevenon de la
      fino de la dua mondmilito kaj de la liberigo el faŝismo kaj koloniismo. Kaj en Eŭropo
      kaj en Azio politikistoj kaj gvidantoj de la koncernaj landoj kuniĝas por ne forgesi
      la militon kaj por ĵuri kunlabori por monda paco.
   </b>
</p> 
<p>Kvankam veras, ke en la nuna mondo daŭras bataloj kaj konfliktoj, almenaŭ kelkloke
   ja videblas provoj limigi suverenajn rajtojn cele al komuna agado, ekzemple Eŭropa
   Unio. Membroj saĝe pritraktis la finon de la milito, ne plu luktante, sed diskutante.
</p> 
<p>Kia, tamen, estas la situacio en orienta Azio? Ĉu finiĝis la postmilita periodo? Tute
   ne. La cikatroj de la dua mondomilito ankoraŭ videblas, eĉ doloras kaj sangas. Ekzemple,
   la korea duoninsulo restas duonigita inter norda kaj suda partoj kaj daŭre minacas
   nuklea konflikto.
</p> 
<h3>Disputoj</h3> 
<p>Rilate Japanion, la ĉefrolulon en la invada milito en Azio, ankoraŭ ekzistas teritoriaj
   insulo-disputoj kun tri najbaraj landoj, Ĉinio, Koreio kaj Rusio. Tiaj landaj problemoj
   estis ĝenerale solvitaj kadre de la sanfranciska pactraktrato de 1951, kiam Japanio
   fordonis ĉiujn teritoriojn, kiujn ĝi forrabis aŭ alimaniere akiris.
</p> 
<p>Sed nek Ĉinio, nek Koreio, nek Rusio partoprenis la kunsidojn en Sanfrancisko kaj
   do ne subskribis la traktaton. Ili poste kaj aparte starigis rilatojn kun Japanio,
   tamen la teritoriaj disputoj ankoraŭ pendas. Kaj dume en tiuj ĉi landoj furiozas naciismo,
   kio ne ebligas seriozajn traktadojn por solvi la situacion.
</p> 
<p>Per la traktato Japanio ŝajne regajnis sian suverenecon kaj do sendependiĝis. Al Unuiĝintaj
   Nacioj (UN) ĝi aliĝis en 1956. Sed verdire la sendependiĝo estis nur supraĵa, ĉar
   daŭre okupis la landon Usono.
</p> 
<h3>Soldatoj</h3> 
<p>Subskribitaj estis la traktato pri sekureco inter Japanio kaj Usono kaj la akompana
   traktato pri la statuso de la usona armeo en Japanio. Ili pli pezas ol la japana konstitucio.
   Rezulte ekzistas 132 usonaj militbazoj kaj ĉ. 50 000 usonaj militpersonoj en Japanio.
   Ili ĝuas eksterteritoriajn rajtojn, tiel ke ekzemple la japana polico ne povas prienketi
   krimojn de usonaj soldatoj sen konsento de usonaj aŭtoritatoj.
</p> 
<p>En la gubernioj Tokio kaj najbara Kanagawa troviĝas grandegaj bazoj: la aerbazoj Yokota
   kaj Acugi kaj la haveno de Yokosuka, la hejma haveno de usona aviadilportanta ŝipo.
   Por tiuj aerbazoj Usono okupas vastan aerspacon etendiĝantan de la okcidenta parto
   de Tokio al la najbaraj ok gubernioj je alteco de 2400 m - 7000 m. Japanaj aviadiloj
   devas akiri permeson de la usona aerarmeo por flugi en tiu ĉi aerspaco, kio ne eblas
   al komercaj aviadilo-kompanioj.
</p> 
<p>Do ĉiuj flugoj el la flughaveno Haneda, Tokio, al la okcidento, inkluzive de Osaka,
   Kyushu, Koreio kaj Ĉinio, devas ekflugi orienten, kaj poste turni sin okcidenten.
   Pro tiu ĉirkaŭiro komercaj aviadilaj kompanioj perdas jare ekvivalenton de pli ol
   200 milionoj da eŭroj.
</p> 
<h3>Militaviadiloj</h3> 
<p>Usonaj militaviadiloj, kiuj de- kaj al-teriĝas, ne flugas super la domojn de usonaj
   militpersonoj, sed super tiuj de japanoj. Tio estas diskriminacio kaj grava atenco
   kontraŭ la homaj rajtoj. La damaĝo severas en Okinavo („Sendependiĝo – ĉu senbaza?” M<span class="mm">ONATO</span> 2015/4, p.8).
</p> 
<p>Okinavanoj kaj aliaj japanoj petis la Konsilion pri Homaj Rajtoj de UN, ke ĝi agu.
   Tamen tiuj petoj ne estas akceptitaj pro artikolo 107 de la UN-ĉarto kaj la laŭa statuso
   de Japanio kiel dummilita malamika ŝtato.
</p> 
<p>Japanio pagas 20 % de la tuta buĝeto de UN, plej multe post Usono, sed ankoraŭ estas
   traktata kiel triaranga ŝtato. En 1995 la Ĝenerala Asembleo de UN decidis per pli
   ol du-triona plimulto aboli la artikolojn pri malamikaj ŝtatoj (Germanio, ekzemple,
   ne plu estas tiel konsiderata).
</p> 
<p>Tamen por ratifi la decidon necesas la apogo de du trionoj de la membro-ŝtatoj, inkluzive
   de ĉiuj anoj de la Sekureca Konsilio, kio longe daŭros.
</p> 
