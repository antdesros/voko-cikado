      <h3>Politiko</h3>
      <h3 class="tem">BRITIO</h3>
      <h2>Malpli malbonaj inter aliaj malbonaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum semajnoj opinio-sondaĵoj prognozis la saman rezulton en la britaj tutlandaj elektoj
   komence de majo: neniu el la du grandaj partioj, la dekstrema konservativa partio
   kaj la teorie maldekstrema sed efektive dekstremeta laborista partio, atingos parlamentan
   plimulton.
</p> 
<p>Verŝajnis koalicio, eventuale daŭrigo de tiu inter la konservativa partio kaj la pli
   malgranda kaj centre-maldekstrema liberaldemokrata partio, kiu regis ekde 2010, aŭ
   eble freŝa koalicio inter la laborista partio kaj la pli maldekstrema skotnacia partio.
</p> 
<p>Kia surprizo, do, kiam tuja postbalota sondaĵo indikis, ke klare venkos la konservativa
   partio kaj ties estro, la ĉefministro David Cameron [kamron]. Kaj tiel estis. Britoj,
   ne tute ĝuste ankoraŭ kulpigante la tiaman laboristan registaron pro la ekonomiaj
   veoj de antaŭ kelkaj jaroj, ne volis kredi, ke la nuna laborista partio finance fidindas.
   La partio perdis 24 seĝojn, la konservativuloj gajnis 28, kio prezentis al Cameron
   malgrandan sed sufiĉan plimulton.
</p> 
<h3>Verduloj</h3> 
<p>Tamen rolis aliaj faktoroj. En Skotlando perdis la laborista partio ĉiujn deputitojn
   krom unu. Lavangis la skotnacia partio, kaptante 56 skotajn seĝojn el 59. Tiel ĝi
   fariĝis en la brita parlamento la tria plej granda partio, forkubutante la liberaldemokratojn,
   kiuj retenis nur ok el siaj 57 deputitoj. Verduloj kaj la sendependiga partio, kiu
   kampanjas por eksigi Brition el Eŭropa Unio, rikoltis po unu seĝon.
</p> 
<p>Tiel nun aspektas la politika mapo de Britio: en Skotlando, la skotnacia partio (flava);
   en norda Anglio kaj partoj de Londono, la laborista partio (ruĝa); en meza kaj suda
   Anglio, la konservativa partio (blua). Tiel malfacilas kredi, ke tiu aŭ alia partio
   reprezentas la tutan (ankoraŭ) Unuiĝintan Reĝlandon.
</p> 
<p>Malantaŭ la parlamentaj statistikoj kaŝas sin aliaj, pli insidaj ciferoj. Ekzemple,
   por rikolti siajn 56 deputitojn, la skotnacia partio akiris 4,7 % de la voĉoj. Verduloj
   ricevis 3,8 %, kiuj liveris nur unu deputiton. La sendependiga partio, la tria plej
   forta laŭ voĉoj, ricevis 12,6 % kaj, kiel la verduloj, nur unu deputiton.
</p> 
<h3>Krioj</h3> 
<p>Aŭdiĝis denove, kio ofte okazas post balotoj en Britio, krioj por reformi la evidente
   maljustan neproporcian voĉdonado-sistemon uzatan dum naciaj elektoj (sed, mallogike,
   ne dum eŭropaj aŭ, en Skotlando kaj Kimrio, parlamentaj aŭ asembleaj elektoj).
</p> 
<p>Malgraŭ peticio kun preskaŭ duonmiliono da subskriboj prezentita al la ĉefministro,
   por postuli reformojn, dubindas, ĉu io ŝanĝiĝos. Klare, la tradicia neproporcia sistemo
   riĉe servis al Cameron, kies partio regas per 36,9 % de la voĉoj. En Britio tio sufiĉas
   por determini la direkton de la lando dum la venontaj kvin jaroj.
</p> 
<h3>Planoj</h3> 
<p>Kiu direkto, tamen, nur parte klaras. Dum la elekto-kampanjo Cameron nur svage parolis
   pri siaj financaj planoj, dirante nur, ke necesos pli fortranĉi publikajn elspezojn.
   Jam avertas policestroj, ke ili frontas al pliaj ŝparrimedoj, dum urbaj kaj komunumaj
   ĉefoj timas, ke ili estos devigataj fermi eĉ pli da bibliotekoj kaj limigi aliajn
   sociajn servojn.
</p> 
<p>Kredeble tiuj, kiuj voĉdone apogis la konservativulojn, ricevas salajrojn, relative
   sekuras en siaj hejmoj, kaj ne dependas de la ŝtato pro senlaboro-, infano- aŭ loĝejo-subtenaj
   monoj. Ili ne estas devigataj viziti la kreskantan nombron de t.n. manĝaĵo-bankoj
   por sin aŭ siajn familiojn vivteni. Ili ne estas elmetataj al t.n. nulhoraj kontraktoj,
   kiuj permesas al dungantoj reteni la servojn de laboremulo sen promeso pri pagata
   posteno.
</p> 
<p>La tragedio de la ĵusaj elektoj en Britio, krom evidente la skandalo pri neproporcia
   voĉdonado, kuŝas en tio, ke britoj, ne tute fidante Cameron pro ties dumkampanja sekretemo,
   ankaŭ pro ties privilegiaj familiaj kaj edukaj cirkonstancoj, tamen malpli fidis la
   opozician laboristan partion.
</p> 
<h3>Nacio</h3> 
<p>Senentuziasme kaj kontraŭvole ili elektis la venkantojn, kiel la malpli malbonajn
   inter aliaj malbonaj. Nur en Skotlando senteblis, ke oni pozitive, ne negative, voĉdonis,
   specife por eĉ pli libera kaj sendependa, eĉ tute sendependa nacio kun propraj povoj
   kaj politikoj.
</p> 
<p>Super ĉio, tamen, ŝvebas Eŭropo aŭ, pli ĝuste, Eŭropa Unio. Cameron promesas referendumon
   pri brita membreco, se li ne elvringos koncedojn el Bruselo, ekzemple pri enmigrado
   kaj popol-movado tra Eŭropo (aŭ al Britio). Jam komenciĝis la luktado: Cameron volas
   nuligi en Britio la eŭropan konvencion pri homaj rajtoj kaj ĝin anstataŭigi per brita
   ekvivalento.
</p> 
<p>Sed intertempe la laborista partio serĉas novan estron, kaj klaran ideologion; la
   liberaldemokratoj serĉas novan partion, aŭ almenaŭ rekonstruotan; kaj la popolo, almenaŭ
   la politikaj tavoloj, kiuj konsideras tiajn aferojn, serĉas novan voĉdonado-sistemon.
   La unua atingeblos, eventuale sen klara ideologio; la dua atingeblos sed nur post
   du generacioj; kaj la tria ... en la tago de Sankta Neniamo.
</p> 
