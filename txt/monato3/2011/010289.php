      <h2>Ĉu malsano patologia?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Vi sidas en restoracio aŭ kafejo kun amiko, kiun vi jam de jaroj ne vidis. Vi babilas pri komunaj memoroj, duonforgesitaj momentoj, kiam ekjukas la poŝtelefono aŭ simila elektronika aparato de via kunulo. Tiu tuj reagas, pardonpetojn murmurante, kaj turnas sian atenton al mesaĝo ricevata. Vi dume tie sidadas, superflua, kroma, eble embarasita: sola en la kunesto de via amiko.</p> <p>Tial la titolo de nova libro, eldonota en Usono: angle <i>Alone Together</i> (<i>Kune solaj</i>). La verkinto, Sherry Turkle [ŝeri tekl], sociologo, per ĝi aliĝas al kreskanta rondo de fakuloj, kiuj taksas tujan mesaĝadon, blogadon, t.n. pepadon, kaj uzon de ekzemple la interreta socia forumo <i>Facebook</i>, modernaj frenezaĵoj.</p> <h3>Kontakto</h3> <p>Turkle kaj aliaj opinias, ke teknikaĵoj pli kaj pli superregas niajn vivojn kaj tiel malhumanigas nin. La interreto, do, kreas la impreson de pli multa homa kontakto, sed nur en virtuala, elektronika sfero: dume ni neglektas vid-al-vidan komunikadon, konversaciojn, babiladon kaj aliajn rektajn homajn interŝanĝojn.</p> <p>Ĉu pravas la sociologoj? Certagrade, jes. En mia propra familio mi konstatas, ke, vizitante genevojn en fora urbo, kiujn mi malofte vidas, ili preferas tekstumi kun amikoj ol paroli kun mi. Bone: teda grizbarbulo mi estas, kaj sendube la lerneja, geknaba rondo pli realas – kaj ja pli gravas – ol distanca, kristnaskkarte konata onklo.</p> <h3>Leteroj</h3> <p>Aliflanke ne eblas nei, ke la modernaj teknikaĵoj interligas homojn laŭ manieroj neimageblaj antaŭ nur unu generacio. En la fruaj 1970aj jaroj mi, eŭropano, pasigis plurajn tre agrablajn jarojn studante en Kanado. Eĉ ne unu fojon mi telefonis al miaj gepatroj: la kosto estis tro alta. Ĉiun semajnon, tamen, regule, ni interŝanĝis leterojn – kaj tio, en tiu fora ŝton-, almenaŭ poŝt-epoko, sufiĉis.</p> <p>Nuntempe, danke nur al interreto (mi nek vizaĝlibrumas, nek blogas, nek pepas: la vivo tro mallongas, dum surbretas libroj nelegitaj), mi rekaptis kontakton kun lernejaj, universitataj kaj aliaj amikoj, kaj interŝanĝas novaĵojn kun homoj en ĉiuj mondopartoj. Ĉu eblus sen interreto? Certe ne same multe.</p> <h3>Informoj</h3> <p>Kaj kio pri Esperanto? Ne plu oni avide atendas leteron de plumamiko el fora kontinento. Ĉiutage alvenas mesaĝoj de esperantistoj el la tuta mondo, kun salutoj kaj informoj, petoj kaj konsiloj. Internacie komuniki neniam tiel facilis: ĉu mi troigas, dirante, ke la interreto estas inventita kvazaŭ por Esperanto?</p> <p>Ĉu do konsenti kun la sociologo Turkle, kaj aliaj, ke tuja mesaĝado, tuja komunikado, fariĝas malsano patologia? Mi kredas, ke ne. Almenaŭ estus tre aŭdaca esperantisto, kiu tion asertus.</p> <div style="text-align:right">Paul G<span class="mm">UBBINS</span></div> 
