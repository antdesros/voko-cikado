      <h3>Turismo</h3>
      <h3 class="tem">IRLANDO</h3>
      <h2>Rondvojaĝi trajne</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ nelonge mi legis romanon de la germana verkisto Sten Nadolny. Ĝia titolo estas
   
   <i>Netzkarte</i>, kio signifas „tutreta bileto”. La romano temas pri rondvojaĝo, kiun faris juna viro tra la tuta fervoja sistemo
   de Germanio. Survoje li pli bone konatiĝas kun sia lando kaj renkontas diversajn personojn
   – precipe junajn virinojn, kiujn li celas.
</p> 
<p>Ĉar mi estas ne plu juna, sed feliĉe edziĝinta viro kun plenkreskinta familio, mi
   ne serĉas junajn, nek eĉ malpli junajn virinojn. Tamen post finlegado de la libro
   de Nadolny mi decidis, ke estus interese fari rondvojaĝon tra la fervoja reto de Irlando
   – la lando, en kiu mi loĝas. Kvankam mi sufiĉe vojaĝis dum mia vivo, mi konstatis,
   ke estas regionoj de la Verda Insulo, kiujn mi neniam vizitis, do ili estas al mi
   tute nekonataj. Mia edzino jam konas preskaŭ ĉiujn partojn de Irlando, ĉar ŝi plurfoje
   partoprenis arkeologiajn, geologiajn kaj turismajn ekskursojn en la plej foraj distriktoj.
   Mi ne partoprenis, ĉar miaj interesoj estas malpli sciencaj kaj mi malŝatas grupan
   turismon.
</p> 
<p>Tamen rondvojaĝo tra la tuta fervoja reto ŝajnis al mi alloga ideo. Do mi decidis
   imiti la ĉefrolulon de la menciita romano, aĉetante tutretan bileton por veturi ne
   tra Germanio, sed tra Irlando. Mi malkovris, ke tio estas malpli facila ol en la hejmlando
   de Nadolny. Tute simila bileto ne estas aĉetebla ĉe la irlanda fervoja instanco, sed
   iom malpli komplika afero estas la tiel nomata 
   <i>esplorbileto</i>, kiu ebligas, ke oni veturu al granda parto de la insulo, kvankam kelkaj regionoj
   estas apenaŭ atingeblaj sen aŭto.
</p> 
<h3>Unua etapo: Monastarevin – Dublino</h3> 
<p>La komenco de mia rondvojaĝo ne ŝajnis tre promesplena. Mi decidis eki en la urbo
   Monasterevin (gaele: 
   <i>Mainistir Eimhin</i>, „monaĥejo de la sanktulo Evin”) en la graflando Kildare (<i>Cill Dara</i>). Ĝi situas je 23 km de Dublino sur la ŝoseo R445 kaj estas ne speciale interesa
   sed agrabla urbeto sur la bordoj de la rivero Barrow kaj de la Granda Kanalo. Pro
   la nombro da pontoj sur ĝiaj akvovojoj, Monasterevin estas iom pretendeme konata kiel
   „Venecio de Irlando”. Ĝia stacidomo datumas de la jaro 1847, kiam la urbo estis fervoje ligita al la ĉefurbo.
   Sed en la 20a jarcento, kiam la tuta fervoja reto estis draste reduktita, ĝi fermiĝis.
   Dum la mallonga prospera periodo post la aliĝo de Irlando al Eŭropa Komunumo (nun
   EU), la stacidomo estis rekonstruita kaj remalfermita, ebligante pendolveturadon inter
   Monasterevin kaj Dublin al laborantoj en la ĉefurbo.
</p> 
<p>Bedaŭrinde malmultaj trajnoj haltas en Monasterevin. Mi intencis tie komenci mian
   vojaĝon iun someran dimanĉon, sed dimanĉe ne eblas. Pro tio mi devis veturi lunde
   al Dublino kaj eki tie.
</p> 
<p>La hodiaŭa Irlando ne estas ideala lando por fervoja turismo. En la 19a jarcento,
   kiam la lando estis regata de la brita reĝino Viktoria, estis konstruita reta sistemo,
   kiu ampleksis la tutan insulon. Nun estas nur magra skeleto de tiu fervoja reto, kaj
   grandaj partoj de la insulo ne estas fervoje atingeblaj. En la nord-okcidenta graflando
   Donegal tute mankas fervojo. Do sen aŭto oni apenaŭ povas turismi tien aŭ tien.
</p> 
<h3>Dua etapo: Baile Atha Cliath (Dublino) – Belfasto</h3> 
<p>La postan lundon mi veturis per la unua matena trajno al Heuston, la ĉefa dublina
   stacidomo por vojaĝi suden kaj okcidenten. Ĝi estas impona domo, konstruita en la
   jaro 1846. Por eliri al la kajo, pasaĝeroj nun devas enmeti sian bileton en maŝinon,
   kiu malfermas la eliran duonpordon. Tiuj maŝinoj bone funkcias en Connolly, la centra
   dublina stacidomo, sed plej ofte fervoja dungito devas malfermi la elirejon, kaj stari
   apud la maŝino, kontrolante la biletojn. Do la celo anstataŭi la stacidoman dungiton
   per aŭtomato ne povas esti tute plenumata.
</p> 
<p>Hodiaŭ ĉiuj entreprenoj, instancoj kaj aŭtoritatoj en Irlando (kiu estis longe postrestanta
   kompare kun aliaj eŭropaj landoj) decidis, kiam eble, maldungi siajn laborulojn kaj
   instali aŭtomatajn maŝinojn. Kompreneble tiu ŝanĝo eĉ pli kreskigas la nombron da
   senlaboruloj kaj komplikas la vivon de la publiko, kiu ofte ne kapablas funkciigi
   la maŝinojn kaj volas (kiel antaŭe) trakti persone vivantajn homojn.
</p> 
<p>Ĉi-foje la sistemo funkciis en Heuston, kaj mi povis eliri kaj regali min per kafo
   en unu el la stacidomaj kafejoj. Poste mi necesumis. Laŭ mia sperto la necesejoj en
   Heuston, kvankam ne speciale allogaj kaj certe ne luksaj, estas tolereblaj aŭ almenaŭ
   ne pli malbonaj ol en aliaj lokoj en Irlando. Kompreneble pri la inaj necesejoj mi
   scias nenion. Elirante el la necesejo, mi aŭdis konversacion inter du usonaninoj,
   kiuj evidente ankaŭ ĵus estis klientoj de la loko (ne nomata „necesejo” en la usonangla, sed 
   <i>powder room</i>, „pudroĉambro” aŭ 
   <i>rest room</i>, „ĉambro de ripozo”). Unu el la usonaninoj komentis: „Tio estas la lasta fojo, kiam mi intencas viziti triamondan landon!”
</p> 
<p>La reago de tiuj usonaninoj (supozeble turistinoj) memorigas min pri alia turisto
   el Usono, kiu volis viziti la necesejon, sed devis heziti pro la fakto, ke ofte tiuj
   lokoj estas indikitaj en la irlanda lingvo: 
   <i>Fir</i> (viroj), kaj 
   <i>Mna</i> (virinoj). La usonano estis inteligenta viro kaj rezonis: La logiko de irlandanoj
   estas malsama je tiu de aliaj popoloj. Do pro la fakto ke la vorto 
   <i>Mna</i> similas la anglan 
   <i>Men</i> (viroj), kaj 
   <i>Fir</i> komenciĝas per la litero F de 
   <i>Femala</i>, kaj ĉar la irlandano kutime inversigas ĉion, 
   <i>Fir</i> devas signifi „Viroj”, kaj 
   <i>Mna</i> certe signifas „Virinoj”. Tiel rezonante, la usonano sukcesis trovi sen embaraso la ĝustan necesejon.
</p> 
<h3>Tria etapo: Belfasto – Derry</h3> 
<p>De la stacidomo Connolly (kie la elirmaŝinoj funkcias), mi eniris la nord-irlandan
   trajnon. Iom norde de Dublino la fervojaj reloj transpasas mallarĝan digvojon, kiu
   ligas ambaŭ bordojn de mara golfo, preskaŭ donante la impreson, ke la trajno veturas
   tra la akvo. Ni atingis Belfaston sen problemo, ĉar la atencoj, kiuj ofte estis spertataj
   dum la antaŭa terorisma periodo, ne plu okazas (aŭ almenaŭ preskaŭ neniam). Kvankam
   ambaŭ nord-irlandaj komunumoj – respublikista kaj uniista aŭ katolika kaj protestanta – ne reciproke amas sin, la politikistoj de ambaŭ frakcioj sukcesis kunlabori kaj pli-malpli
   akordiĝi. Belfasto speciale ne plaĉas al mi, sed ties magazenoj kaj restoracioj estas
   bonaj kaj malpli kostaj ol en Dublino. Krome la ekspozicio pri la malbonŝanca vaporŝipo
   „Titanic” (kiu estis konstruita en Belfasto) estas interesa. Plie, la botanika ĝardeno estas
   bela, kvankam mi preferas la dublinan. Do mi restis dum du noktoj en la norda ĉefurbo.
</p> 
<h3>Kvara etapo: Derry – Donegal</h3> 
<p>La fervoja vojaĝo laŭ la nord-orienta marbordo de Norda Irlando estas tre bela, kaj
   valoras viziton. La urbo Derry (gaele Doire), nomata de la uniistoj Londonderry, estas
   histoririĉa urbo, ĉirkaŭata de mezepokaj muroj. Bedaŭrinde ŝajnas, ke ankoraŭ regas
   sekteca suspektemo kaj malamo inter ambaŭ komunumoj de loĝantoj. Espereble tiu situacio
   iom post iom pliboniĝos.
</p> 
<p>La vizito al Derry plaĉis al mi, sed poste mi refoje renkontis problemojn, kiam mi
   volis daŭrigi mian rondvojaĝon. De Derry en Norda Irlando al Donegal en la respubliko
   Irlando, kiun la nord-irlandanoj (fakte la plej norda parto de la insulo!) nomas „Suda Irlando”, ne estas fervoja konekto, ĉar Derry estas la lasta trajna haltejo de la nord-irlanda
   fervoja reto. Tre malvolonte, do, mi devis fari la veturadon inter Derry kaj Donegal
   per buso – komplika, teda kaj longa vojaĝo. Mi konstatis, ke Donegal (gaele: Dú na nGall) estas
   tipa irlanda provinca urbo, sekve ne speciale interesa aŭ unika, kvankam ne malagrabla.
   Tamen la kampara pejzaĝo de la graflando Donegal havas apartan sovaĝan belecon. Fakte
   ĝi estas unu el la plej belaj partoj de la insulo.
</p> 
<p>Miaj vojaĝaj problemoj ne ĉesis en Donegal. La nord-okcidenta angulo de Irlando estas
   la plej malriĉa, neglektata kaj fora regiono de la respubliko. Tio estas neklarigebla,
   ĉar oni pensus, ke tiom impona distrikto allogas multege da turistoj, enlandaj kaj
   eksterlandaj, kaj havas promesplenajn ekonomiajn perspektivojn. Ŝajne ekde la fondiĝo
   de respubliko Irlando, la alternantaj registaroj preskaŭ forgesis, ke ĝi ekzistas.
   Donegal, kaj la tuta okcidenta kaj sud-okcidenta regiono de Irlando, estas historie
   gaele parolanta zono. (La sola vere gaela distrikto en orienta Irlando estas la eta
   loko Rathcairn, nord-okcidente de Dublino.)
</p> 
<h3>Kvina etapo: Sligo (Sligeach) – Galivo – Limeriko</h3> 
<p>En Sligo rekomenciĝas la fervoja linio. Post mallonga restado en Sligo, alia tipe
   irlanda kampara urbo, mi entrajniĝis kaj vojaĝis ĝis Galivo (angle: Galway, gaela:
   Gaillimh). Galivo estas tre interesa urbo, oficiale la ĉefurbo de Gaelio. Pro la universitato
   kaj pro ĝiaj multaj allogaĵoj (inkluzive de la proksimaj Aran-insuloj) ĝi havas tre
   junan loĝantaron kaj estas vizitata de multaj turistoj. Ankaŭ Limeriko meritas viziton
   pro sia impona situo apud la granda rivero Shannon kaj ĝia kastelo.
</p> 
<h3>Fina etapo: Limeriko – Dublino</h3> 
<p>Bedaŭrinde en tiu okazo mi ne havis tempon por iri al kelkaj aliaj tre interesaj urboj,
   nome Cork (gaele: Corcaigh), Waterford (gaele: Port Láirge) kaj Wexford (gaele: Loch
   Garman). Eble en alia okazo mi povus daŭrigi mian rondvojaĝon. En ĉi tiu etapo mi
   renkontis plurajn fascinajn kunpasaĝerojn, kiuj inkluzivis simpatian kimralingvan
   paron (Kimrio estas princlando en okcidenta Britio), R.O. – fifaman, sed surprize afablan kaj inteligentan respublikistan aktivulon kaj ĉarmajn
   turistojn el diversaj landoj.
</p> 
<p>Venontsomere mi verŝajne ree aĉetos esplorbileton kaj veturos al urboj kaj regionoj,
   kiujn mi ĉi-foje ne sukcesis viziti.
</p> 
