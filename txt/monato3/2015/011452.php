      <h2>Alia kalendaro eblas</h2>
      <!-- komenco de la enhavo -->

       

       <p>Unu tago estas la tempo, kiu pasas inter unu kulmino de la suno (unu tagmezo) kaj
   la sekva. Unu jaro estas la tempo, kiu pasas inter unu vintra solstico (plej ofte
   okazanta la 21an de decembro) kaj la sekva. De unu vintra solstico ĝis la sekva pasas
   precize 365,24219 tagoj.
</p> 
<p>Kial februaro estas la plej mallonga monato? Ĉar, antaŭ la kalendara reformo fare
   de Julio Cezaro, la jaro komenciĝis la 1an de marto. Tio klarigas ankaŭ la hodiaŭajn,
   iom elvojigajn, nomojn 
   <i>septembro</i> („sepa monato” laŭ ĝia latina etimologio), 
   <i>oktobro</i> („oka monato”), 
   <i>novembro</i> („naŭa monato”), 
   <i>decembro</i> („deka monato”).
</p> 
<h3>Julio Cezaro</h3> 
<p>En la jaro -45 (45 antaŭ nia erao) Julio Cezaro reformis la kalendaron. Ekde tiam
   la jaro komenciĝas en januaro kaj, en ĉiu kvara jaro, februaro havas 29 tagojn. La
   aldona tago nomiĝas supertago. Laŭ tiu kalendaro la jaro daŭris averaĝe 365,25 tagojn,
   kaj la devio estis unu tago en 128 jaroj. Pri diversaj konfuziĝoj oni bonvole legu
   
   <a href="http://http://eo.wikipedia.org/wiki/Julia kalendaro">http://eo.wikipedia.org/wiki/Julia kalendaro</a>.
</p> 
<h3>Papo Gregorio</h3> 
<p>En 1582 Papo Gregorio la 13a reformis denove la kalendaron: post ĵaŭdo, la 4a de oktobro,
   sekvis rekte vendredo, la 15a de oktobro. Dek tagoj tiel elfalis. Laŭ tiu kalendaro
   la jaro daŭras averaĝe 365,2425 tagojn. La devio estas unu tago en 3000 jaroj.
</p> 
<h3>Reforma propono</h3> 
<p>La verkinto proponas novigan, precizan kalendaron. La jaro komenciĝus en la tago post
   la vintra solstico, kiu estus lundo, la 1a de januaro. La semajno daŭrus, kiel nun,
   sep tagojn. La tagoj de la semajno restus kiel nun, de lundo ĝis dimanĉo. Kiam inter
   du vintraj solsticoj pasas ne 365, sed 366 kalendaraj tagoj (unu fojon post ĉiu kvara
   jaro), enŝoviĝus unu supertago, kiu estus la 31a de junio. La 31a de junio kaj la
   31a de decembro havus neniun semajntagan nomon, sed nomiĝus simple festotagoj. Oni
   cetere povus nomi ilin respektive „tago de la kalendaro”, ekzemple, kaj „tago de la jarfino”, kaj ili povus esti feritagoj.
</p> 
<p>La privataj, ŝtataj kaj religiaj festotagoj restus kiel nun, sed surbaze de la nova
   kalendaro. Se iu nacia festotago estas ekzemple en la 31a de majo laŭ la nuna kalendaro,
   ĝi estus laŭ la nova kalendaro en la sekva tago, do en la 1a de junio. La reformo
   povus okazi en la jaro 2020: post la 21a de decembro 2020 rekte sekvus la 1a de januaro
   2021: do, alivorte, la 1a de januaro 1n (n = nova). La Kristnaska tago povus okazi
   en la jaro 2020 escept-okaze en la 21a de decembro, sed de poste en la 25a de decembro,
   kiel nun.
</p> 
<h3>Praktikaj avantaĝoj</h3> 
<p>Jen fine la avantaĝoj de tia kalendaro: ĉiu jaro kaj ĉiu sezono komenciĝus je lundo;
   la monatoj, sezonoj kaj duonjaroj estus preskaŭ same longaj; la nova kalendaro orientiĝus
   pli laŭ la astronomiaj faktoj kaj malpli laŭ arbitraj homaj reguloj; la nova kalendaro
   estus malpli eŭrope bazita ol la malnova; la 1a de majo, la tiel nomata „tago de la laboro”, estus neniam sabate aŭ dimanĉe (kio foje nun okazas), sed ĉiam ĵaŭde; kaj ĝi estus
   ĉiujare feritago; fine, la averaĝa daŭro de la jaro tiumaniere estus ĝuste 365,24219
   tagoj.
</p> 
