      <h3>Moderna vivo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Misiisto, marŝado kaj detenanta mano</h2>
      <!-- komenco de la enhavo -->

       

        <p>En oktobro 1937 la usona ĵurnalisto Edgar Snow publikigis sian libron 
   <i>Ruĝa Stelo super Ĉinio</i>. Snow rakontas pri la monatoj, kiujn li pasigis kun la Ĉina Laborista-Kamparana Ruĝa
   Armeo en 1936.
</p> 
<p>Li uzas siajn intervjuojn kun Maŭ Zedong kaj aliaj altranguloj por prezenti vivecajn
   priskribojn de la Longa Marŝado kaj biografiajn rakontojn pri gvidantoj de ambaŭ flankoj
   de la konfliktoj. Per tiu libro la okcidentaj landoj eksciis pri la ĉinaj komunistoj.
</p> 
<p>Sed tre malmultaj scias, ke antaŭ unu jaro jam aperis simila libro, 
   <i>La Detenanta Mano: Kaptiteco por Kristo en Ĉinio</i>, verkita de la svis-devena brita misiisto Rudolf Alfred Bosshardt.
</p> 
<h3>Sieĝo</h3> 
<p>La Longa Marŝado (oktobro 1934 - oktobro 1936) estis strategia moviĝo de la Ĉina Laborista-Kamparana
   Ruĝa Armeo (nun konata kiel la Ĉina Popola Liberiga Armeo) gvidata de la Komunista
   Partio de Ĉinio por eviti la sieĝon kaj persekuton de la armeo de Kuomintango (Naciista
   Partio, tiama reganta partio).
</p> 
<p>La Laborista-Kamparana Ruĝa Armeo marŝis 12 500 kilometrojn tra 11 provincoj de la
   sudo ĝis la nordokcidento. Ili sukcese eskapis el la danĝero kaj starigis sian novan
   bazlokon.
</p> 
<p>En oktobro 1934 en Jiuzhou de Guizhou-provinco, sudokcidenta Ĉinio, Rudolf Alfred
   Bosshardt renkontiĝis kun la sesa armegrupo de la Ruĝa Armeo. Komence la armeo kaptis
   lin kiel „imperiisman spionon”, kaj poste retenis lin kiel helpanton. Li marŝis kun la armeo 18 monatojn.
</p> 
<h3>Revo</h3> 
<p>Bosshardt naskiĝis en svisa familio en Manĉestro, Anglio, en 1897. Ekde la aĝo de
   dek jaroj li aktive dissemis kristanismon. En 1907 Bosshardt hazarde aŭskultis prelegon
   de misiisto pri vojaĝo en Ĉinio. La prelego vekis revon de Bosshardt pri Ĉinio.
</p> 
<p>Ŝanco venis, kiam li estis 20-jara. Li rekomendis sin al la Ĉina Enlanda Misio (ĈEM).
   Tiutempe Ĉinio estis en ĥaoso, kaj misiistoj estis sendataj al foraj regionoj en Ĉinio.
   Tial tre malmultaj kandidatis. ĈEM tuj aprobis lian peton, kaj trejnis lin tri monatojn
   en Londono. Dum la sekvaj du jaroj li lernis ankaŭ medicinon, kiun li poste multe
   praktikis en Ĉinio.
</p> 
<p>En oktobro 1922 Bosshardt atingis Ŝanhajon. Tie li aĉetis necesaĵojn kaj lernis la
   ĉinan lingvon. Lia instruisto donis al li gracian ĉinan nomon Bo Fuli. Bo estas mallongigo
   de ĉina transskribo de Bosshardt, kaj la propra nomo Fuli, kiu signifas „sin deteni kaj reveni al la deco”, estas citita en la libro 
   <i>La Analektoj de Konfuceo</i>. De tiam la ĉina nomo Bo Fuli akompanis lin en Ĉinio.
</p> 
<h3>Malsatego</h3> 
<p>Laŭ ordono de ĈEM Bosshardt iris al Zunyi, la due plej granda urbo en Guizhou-provinco,
   kie situis malnova kristana preĝejo. En pli ol dek jaroj Bo migris de loko al loko
   kaj spertis militojn kaj malsategon.
</p> 
<p>Li ankaŭ edziĝis al la svisa misiistino Rose Piaget. Fine li kaj lia edzino sin instalis
   en preĝejo en Zhenyuan, malgranda gubernio en Guizhou. Tie, hazarda renkontiĝo kun
   la ĉina komunista Ruĝa Armeo en aŭtuno 1934 ŝanĝis lian vivon.
</p> 
<p>En aŭgusto 1934 Bo kaj lia edzino gastis ĉe Amolis Heyman, alia fremdlanda misiisto.
   La geedzoj volis atingi sian hejmon sed subite trovis la vojon barita.
</p> 
<h3>Stelo</h3> 
<p>Bo priskribas en sia libro la okazaĵon jene: „Homoj aperis. Ili surhavis samajn grandajn ĉapojn kun ruĝa stelo. Komence venis al
   mi la ideo, ke ni estas kaptitaj de banditoj. Tamen vidinte la novajn sloganojn sur
   muroj, mi ekkomprenis, ke ili estas komunistoj.”
</p> 
<p>Pro propagando de Kuomintango, lokanoj ne multe sciis pri la Ruĝa Armeo. Ankaŭ Bo
   nenion sciis. La Ruĝa Armeo redonis al li liajn posedaĵojn, kaj kondukis lin al „juĝisto” kaj lia edzino. Bo klarigis siajn religiajn dogmojn, tamen la „juĝisto” ŝajne ne komprenis.
</p> 
<p>Lia edzino, afabla kaj bone edukita, kun mallonga hararo kaj en vira milituniformo,
   plie interpretis la „diablajn parolojn” de Bo. La „juĝisto” fakte estis Wu Defeng, estro de la sekureca buroo de la sesa armegrupo. Lia edzino
   Qi Yuande estis sekretario de la komunista partio en la buroo.
</p> 
<h3>Komandanto</h3> 
<p>Ankaŭ la misiisto Heyman kaj lia edzino estas kaptitaj. Generalo Xiao Ke, komandanto
   de la sesa armegrupo, mencias en sia libro: „Dum la streĉa marŝado, ni certe ne povis preterlasi neidentigitajn fremdlandanojn.
   Baldaŭ ni konstatis ilian statuson kaj liberigis iliajn familianojn. Tamen ni detenis
   Bo Fuli kaj Heyman.”
</p> 
<p>Klarigas Xiao, kial la armeo retenis la misiistojn: „Post pli ol 50 tagoj de milito kaj pro longa marŝado, ni havis multe da vunditoj kaj
   malsanuloj. Ni sciis, ke misiistoj kapablas akiri medikamentojn kaj monon; tial ni
   promesis liberigi ilin je la kondiĉo, ke ili liveru al ni medikamentojn kaj monon.”
</p> 
<p>Komence Bo timis la Ruĝan Armeon. Sed la timo iom post iom foriĝis en „kunlaboro” kun la komunistoj. Ekzemple, Bo helpis traduki mapon, publikigitan en la franca lingvo.
   Li memoris, ke tiel Xiao esperis „eviti aŭtovojon dum la moviĝo”.
</p> 
<h3>Korifeo</h3> 
<p>En sia libro Bo priskribas ŝanĝiĝon de sia sinteno al ĉinaj komunistoj. Li estis jam
   liberigita, kiam li verkis la libron, do li ne bezonis diri komplimenton al la Ruĝa
   Armeo. Tamen li priskribas Xiao kiel energioplenan korifeon, kiu esperas starigi komunisman
   reĝimon en orienta Guizhou. Bo demandis sin, ĉu „bandito” povas esti tia, kia Xiao. Li pli kaj pli komprenis verajn komunistojn.
</p> 
<p>Xiao konfesis en sia libro, ke antaŭ la kunlaboro kun Bo li ne havis bonan impreson
   pri misiistoj. Li opiniis, ke ili venis al Ĉinio nur pro „kultura agreso”. Tamen lia opinio ŝanĝiĝis: „Bo helpis nin traduki la mapon, por ke ni bone batalu kaj marŝu en Guizhou. Mi neniam
   forgesos tiun, kiu helpas nin en malfacila tempo.”
</p> 
<p>Irinte el Jiuzhou, la misiistoj komencis sian longan marŝadon kun la armeo. Ili spertis
   novan viv-manieron. Preskaŭ ĉiutage ili manĝis nur unu fojon, kaj neniun tagon ili
   ripozis.
</p> 
<h3>Flagoj</h3> 
<p>Bo detale priskribas sieĝojn kaj sturmojn alfrontitajn de la Ruĝa Armeo. Propraokule
   li vidis la renkontiĝon de la dua kaj la sesa armegrupoj en Muhuang: „Tiutage oni ludis muzikajn instrumentojn, kaj koloraj flagoj flirtis ĉie. Ni renkontiĝis
   kun la dua armegrupo sub generalo He Long. Liaj soldatoj estis en pli ĉifonaj vestoj,
   tamen la ruĝaj flagoj estis okulfrapaj.”
</p> 
<p>En Muhuang la gvidantoj de la Ruĝa Armeo decidis iri al la okcidenta Hunan-provinco.
   Por forskui la malamikon la armeo faris pli rapidan marŝadon, kaj tio ege doloris
   la fremdlandanojn.
</p> 
<p>Qi Yuande, ilia prizorganto, konsilis, ke ili rajdu sur ĉevaloj. Ili sciis, ke nur
   komandanto havas ĉevalon. Krome, dum marŝado trovi ĉevalon estis malfacile. Tamen
   tri tagojn poste oni trovis mulon. Bo kaj Heyman alterne rajdis por sekvi la soldatojn.
</p> 
<h3>Falintoj</h3> 
<p>La „vojo” por la armeo estis apenaŭ irebla. Padoj sur montoj estis kotaj kaj glitaj. Pro faloj,
   vestoj malsekiĝis, sekiĝis kaj denove malsekiĝis. Oni helpis falintojn restariĝi,
   sed neniu ilin mokis.
</p> 
<p>Bo skribas: „Mi vidis, ke krom plekti pajlajn sandalojn aŭ fliki vestojn la diligentaj soldatoj
   utiligas ĉiun ripoz-horon por lernado. Veninte ien por mallongtempa ripozo la soldatoj
   tuj konstruis t.n. Lenin-ĉambron – legejon provizore starigitan per plektitaj bambubranĉoj kaj pajlo kiel plafono.”
</p> 
<p>Bo ankaŭ miris, ke eĉ plej bazaj soldatoj scias la „kompletan teorio-sistemon”. Li demandis soldaton: „Kiel vi distingas inter kamparanoj kaj bienuloj?” Tiu respondis: „Per laboro. Oni devas vivteni sin per sia propra laboro. Se ies tero estas kultivata
   de aliaj, li estas subpremanto.”
</p> 
<h3>Hazardludoj</h3> 
<p>Bo rimarkis diversajn vesperajn amuzaĵojn – kantadon, luktludadon, teatraĵojn. Hazardludoj estis malpermesitaj, kaj opiofumantoj
   nepre dekutimiĝu. Sanitaristoj donis al tiuj medikamenton. Post ĉirkaŭ du semajnoj
   ili forskuis la opi-manion.
</p> 
<p>Bo kaj Heyman restis en la Ruĝa Armeo jam unu jaron. En novembro 1935 Heyman ricevis
   liberigo-atestilon pro sia malforta korpo. Bo restis plu en la armeo. Tiam li ne plu
   timis la homojn kun ruĝa stelo, tamen li estis deprimita pro sopiro al sia edzino
   kaj libero.
</p> 
<p>La sekvan jaron tamen generalo Xiao alportis al Bo bonan novaĵon: la armeo decidis
   liberigi lin. La liberiga tago ŝajne estis zorgeme elektita kaj preparita. Bo klare
   memoris, ke la dato estas dimanĉo kaj Pasko. La armeo faris escepton por bankede regali
   lin antaŭ la foriro.
</p> 
<h3>Manuskripto</h3> 
<p>Du tagojn poste en Kunming li revidis sian edzinon Rose Piaget. En la sekvaj tri monatoj
   li dikte plenumis sian libron 
   <i>La Retenanta Mano: Kaptiteco por Kristo en Ĉinio</i>, kaj sendis la manuskripton al Britio por eldono.
</p> 
<p>En oktobro li revenis al Britio kaj estis invitita prelegi pri siaj spertoj en Ĉinio.
   Lia priskribo pri la Komunista Partio de Ĉinio estas amika kaj objektiva. En 1939
   Bo estis denove sendita kiel misiisto al Panxian en la provinco Guizhou, kaj tie li
   restis ĝis 1951.
</p> 
<p>Pasis ankoraŭ tri jardekoj, kaj la maljuna generalo Xiao denove trovis sian amikon
   Bo. Xiao tiel priskribas ilian amikecon: „Ni havas malsamajn kredojn, tamen tio ne malhelpas nian komunikadon. Male, ni povas
   influi unu la alian kaj disvolvi nian amikecon.”
</p> 
<p>En 1993 Bo forpasis pro bronkito en Pembury, Britio, en la aĝo de 96 jaroj.</p> 
