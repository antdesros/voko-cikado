      <h3>Turismo</h3>
      <h3 class="tem">FINNLANDO</h3>
      <h2>Ĝui la silenton</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Finnlando estas kutimo somere vizitadi preĝejojn. Ili estas malfermitaj ĉiutage, kun deĵoranta volontulo preta rakonti senkoste pri la historio kaj arkitekturo de la loko. Vojaĝantoj tie ripozas en kvieto kaj trankvilo: unuj envenas pro scivolo aŭ kaprico, aliaj pro deziro revidi la sanktejon. Trafiksignoj kun la vorto <i>Tiekirkko</i> (vojkirko) invitas al tiuspecaj vidindaĵoj. La vizito povas esti ne nepre religia.</p> <p>Post dudek-jara laborado libervola oni finkonstruis vizitindan malgrandan kapelon ekumenan en la arbareca parto de la finna urbo Virrat. Ĉi-jare ĝin vizitis jam pli ol 100 000 personoj, inter kiuj estis eĉ turistoj el Azio kaj Ameriko. La kapelo estis konstruita el sveda ŝtalo, finnaj granito, pina kaj picea ligno. Ĝi situas sur privata tereno, apud bieno de familio, en kiu ekestis la ideo pri ĝia konstruado. Lokanoj, eksterlandanoj kaj EU finance helpis. La plej grava kontribuo tamen estis la senlaca fizika laboro kaj entuziasmo de loĝantoj de la ĉirkaŭaĵo.</p> <h3>Trankvilo kaj malstreĉiĝo</h3> <p>Kiam vizitantoj alvenas, ili tuj sentas la trankvilon kaj malstreĉiĝon, kaŭzitajn de la bela naturo. En la kapelo mem la altaron anstataŭas fenestrego, tra kiu oni vidas verdblankajn betulojn kaj bluan lagon. La koloroj ŝanĝiĝas laŭsezone. La spirhaltiga natura vidaĵo igas la preĝejon taŭga por ĉies meditado kaj memorigas pri tio, ke niaj praavoj adoris la naturon.</p> <p>La akustiko de la kapelo estas tre bona. Tial tie ŝatas prezenti sian arton muzikistoj kaj poemdeklamantoj. La benkoj sufiĉas por kvardeko da ĉeestantoj. Ĉi tie okazas familiaj festoj, baptoj, nuptoj, kunvenoj kaj kursetoj.</p> <p>Buspleno da turistoj povas inkludigi la kapelon en sian itineron. Per publikaj veturiloj oni ne povas atingi la sanktejon, ĝi foras dudekon da kilometroj de la urbocentro. Kaj ĝuste tial ĝi refreŝigas kaj ripozigas la animon de vizitantoj en preskaŭ sovaĝeja etoso. „Perceptu la silenton!” sonas la invito de la Matias-kapelo. Informoj en la finna, sveda, germana kaj angla ĉe <a href="http://www.matias-kappeli.net">www.matias-kappeli.net</a>.</p> <div style="text-align:right">Saliko</div> 
