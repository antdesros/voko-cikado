      <h2>Scenoj el la familia vivo</h2>
      <!-- komenco de la enhavo -->

       

       <p>El kvar homoj konsistas mia familio: mi kaj miaj patrino, edzino kaj filino, kiuj apartenas do al tri generacioj. Miaopinie, ĝi estas tre ordinara ĉina familio.</p> <p>Mia patrino estis kamparano, sed pro sia alta aĝo perdas laborkapablon. Ŝi nun loĝas en urbo kun ni. Ŝi estas laborema kaj purigema kaj laŭ sia kapablo ŝi faras ankoraŭ hejmajn laborojn, ekzemple kuiradon, purigadon ktp.</p> <p>Alitempe ŝi babilas kun najbaroj kaj, vespermanĝinte, promenadas surstrate kun siaj similaĝaj amikinoj. Kompreneble ŝi finance bezonas mian helpon, kvankam la ĉina registaro ĉiumonate donacas al ŝi 110 juanojn (ĉ. 13,6 eŭrojn). Tio ne sufiĉas por vivteni ŝin. Mi do kun granda ĝojo liveras al ŝi monon por montri min bona filo.</p> <h3>Salajro</h3> <p>Mia edzino laboras en hospitalo. Ŝia salajro ne estas granda, tamen jam sufiĉas por ŝia vivo. Dum liberaj tagoj ŝi aŭ dormas aŭ vizitas vestaĵo-vendejojn.</p> <p>Vespere ŝi ŝatas spekti televidfilmojn diverstemajn. Plaĉas al ŝi ankaŭ ĵurnaloj kaj gazetoj. Mi diras, ke ŝi estas legemulino kaj scioriĉulino, kiu havas vastan vidkampon danke al informoj de la amaskomunikiloj.</p> <p>Mia filino lernas en gimnazio. Kvankam per mia aŭto la lernejo situas nur kvin minutojn for de nia hejmo, ŝi devas laŭregule tie tranokti. Lecionojn ŝi havas ekde frumateno ĝis enlitiĝo.</p> <h3>Ekzamenoj</h3> <p>Preskaŭ ĉiuj ĉinaj gimnazianoj tiel energie lernas por sukcesi en ekzamenoj kaj eniri bonan universitaton. Nur sabate mia filino povas hejmeniri, kiam kune ni ĝuas familian etoson. Tamen ĉiuvespere ŝi telefonas al mi kaj mia edzino por saluti nin. Tio trankviligas nin.</p> <p>Mi laboras kiel policisto. En ĉiuj landoj mi havas kolegojn, ĉar miaopinie ĉiuj homoj volas vivi en situacio sekura kaj paca. Nur policistoj havas kaj devon kaj kapablon liveri tian vivkondiĉon.</p> <p>Do tage mi diligente laboras por vivteni mian familion kaj vespere mi disponas pri mia propra tempo. Vespermanĝinte, mi kutime tuj sidiĝas antaŭ la komputilo kaj tiel interparolas kun geamikoj diverslandaj, ĉu tajpe, ĉu voĉe. Ĝuinda tempo ĝi ja estas.</p> <h3>Interreto</h3> <p>Kvankam mi kaj miaj kunbabilantoj loĝas en diversaj lokoj tra la mondo, mi sentas, ke ili estus ĉe mi kvazaŭ en la sama ĉambro. Danke al Interreto nia mondo fariĝas pli malgranda.</p> <p>Menciindas ankaŭ mia alia hobio. Temas pri fumado – en la okuloj de nefumantoj „helpo de diablo”. Mi bone sciis, ke mia „hobio” ne nur malutilas al fumantoj sed ankaŭ al proksimuloj. Bedaŭrinde, kvankam mi kelkfoje decidis ĉesigi la fumadon, mi fiaskis.</p> <p>Kvankam ĉiu en mia familio havas propran vivmanieron, ni bone fartas en etoso harmonia kaj ĝojoplena. Ĉiuj komplete sentas sin kontentaj pri la vivo.</p> <div style="text-align:right">Xu J<span class="mm">INMING</span>/pg</div> 
