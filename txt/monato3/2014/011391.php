      <h2>Ne nepre malstabileco</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kredeble ne ĉiuj konsentos kun la titolo de la artikolo „Grava elemento de monda nestabileco” (M<span class="mm">ONATO</span> 2014/10, p. 25-26). La apartiĝo kaj sendependiĝo de nacioj ne ĉiam kondukas al nestabileco.
</p> 
<p>El la antaŭnelonga historio oni povas citi kelkajn favorajn ekzemplojn: Norvegio,
   kiu sendependiĝis de Svedio en 1905; Islando de Danio en 1944; Slovakio rezulte de
   la disfalo de Ĉeĥoslovakio en 1993. Kiam temas pri pacemaj civilizitaj popoloj, nestabileco
   estas evitebla. Lenio Marobin mencias la kazojn de Skotlando, kie ĉi-jare okazis referendumo
   pri ebla sendependiĝo, kaj de Katalunio, kie referendumo ĝis nun ne estis permesita.
</p> 
<p>La disvolvo post la skota referendumo ŝajnas indiki, ke se la rezulto estus pozitiva,
   sekvus malpli da nestabileco ol inverse. Britio certe estus akceptinta (kvankam malvolonte
   kaj malĝoje) la jesan rezulton, pro la fakto ke la angloj estas ĝenerale tolerema
   popolo. Kontraste, pro la negativa rezulto restas granda malkontento en Skotlando,
   kaj miloj da skotoj decidis daŭrigi la sendependigan kampanjon. En la semajno post
   la referendumo forte kreskis la membraro de la politikaj partioj, kiuj apogis la jesan
   kampanjon, kaj tiu kresko daŭras. Estas vere, ke la deklarita rezulto de la referendumo
   estis 53,3% (por „ne”) kontraŭ 44,7 % (por „jes”), sed tio tute ne signifas, ke la plimulto de la skotoj ne deziras sendependecon.
   En la referendumo ĉiuj registritaj loĝantoj en Skotlando, ĉu skotoj, ĉu angloj, ĉu
   de eksterlanda deveno, rajtis voĉdoni, sed 
   <i>neniu</i> skoto loĝanta ekster Skotlando.
</p> 
<p>La loĝantaro de Skotlando inkludas proksimume 400 000 anglojn kaj milojn da homoj
   de eksterlanda deveno, kompare kun la 800 000 skotoj, kiuj loĝas aliloke en Britio.
   Se oni komparas la 2 001 926, kiuj nee voĉdonis, kaj la 1 617 989, kiuj jese decidis,
   kaj aldonas la aliajn faktorojn, ŝajnas evidente, ke la majoritato de la skotoj ja
   volas esti sendependaj kaj estos nekontentaj, ĝis ili atingos sian celon.
</p> 
<p>La escepta kaj tragika sperto de la baltaj landoj kaj de Ukrainio ne implicas, ke
   la sendependiĝo de la malgrandaj nacioj de Eŭropo nepre estos kaŭzo de nestabileco
   aŭ malpaco.
</p> 
