      <h3>Moderna vivo</h3>
      <h3 class="tem">VULKANOJ</h3>
      <h2>Kiam tremetoj fariĝas tremegoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Pasintjare, en septembro, pli ol 200 homoj ĝuis aŭtunan veteron ĉe la pinto de Monto
   Ontake, vulkano 3067 metrojn alta, en la centro de Japanio kaj 200 km okcidente de
   Tokio.
</p> 
<p>Subite erupciis la vulkano, tiel ke falegis rokfragmentoj kaj cindroj sur la kapon
   kaj dorson de la terurataj homoj. Pereis 63.
</p> 
<p>Komence de la monato la meteologia buroo registris malgrandajn tertremojn ĉirkaŭ la
   monto, sed ne taksis ilin danĝeraj. Do neniu avertis la monto-grimpantojn pri eventuala
   risko.
</p> 
<h3>Senhoma</h3> 
<p>Malfacile estas prognozi vulkanan aktivecon. Ekzemple, en 2013 erupciis submara vulkano
   proksime al la senhoma insulo Niŝinoŝima (Okcidenta Insulo), 1000 km sude de Tokio.
   Niŝinoŝima estas la pinto de vulkano, kies bazo staras sur la marfundo 4000 metrojn
   sube.
</p> 
<p>Jam erupciis Niŝinoŝima en 1973, sed 40 jarojn poste la lafo grandigis kaj altigis
   la insulon. Tiel kontribuas la naturo al Japanio, kiu gajnis senmilite kaj sendispute
   novan teritorion.
</p> 
<p>La japana insularo, alinome vulkana arkipelago, posedas 110 aktivajn vulkanojn. Vulkano
   estas taksata aktiva, se ĝi erupciis en la lastaj 10 000 jaroj. Pri la vulkanoj, laŭ
   fakuloj, kulpas la kvar platoj (pacifika, filipina, eŭrazia kaj nord-amerika) troveblaj
   sub la insularo.
</p> 
<h3>Cunamo</h3> 
<p>Japanoj memoras, ke antaŭ 20 jaroj okazis tertremo apud Kobe, kiu viktimigis 6500
   homojn. Ili memoras ankaŭ la orient-japanan grandan katastrofon, aŭ cunamon, kiu okazis
   antaŭ kvar jaroj kaj mortigis aŭ malaperigis ĉirkaŭ 18 500 homojn.
</p> 
<p>Tamen vulkanoj kunportas ne nur damaĝon sed ankaŭ favoron. Danke al subtera magmo
   troviĝas pli ol 27 600 varmo- kaj energio-fontoj (bedaŭrinde ne ekspluatataj) por
   tervarmaj centraloj. Ankaŭ imponaj pejzaĝeroj kiel la monto Fuĵi kaj kaldronaj lagoj
   estas kreitaj de vulkanoj.
</p> 
<h3>Eksplodis</h3> 
<p>Fakte japanoj nun sin demandas, kiam erupcios Fuĵi, la plej granda kaj relative juna
   vulkano en la lando, kiu eksplodis laste en la jaro 1707. Tokio, 90 km malproksime,
   estas konstruita sur cindroj iam elpafitaj el la vulkano. En oktobro 2014 tri gubernioj okazigis
   komunan trejnadon kontraŭ erupcio de Fuĵi, kontrolante precipe kontraŭcindrajn rimedojn.
</p> 
<p>Spertuloj diras: „Se Fuĵi morgaŭ erupcius, tio ne surprizus. Se ĝi erupcius samskale kiel en 1707, damaĝoj
   en Tokio estus gravaj. Centraloj ne funkcius, aviadiloj ne flugus, aŭtoŝoseoj neuzeblus
   kaj malsanoj de spirorganoj multiĝus.”
</p> 
<p>Krome malaperus la nuna eleganta formo de Monto Fuĵi. Aŭ eble, reformita, ĝi fariĝus
   eĉ pli eleganta.
</p> 
