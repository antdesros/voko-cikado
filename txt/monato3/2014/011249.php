      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Kolekto da lingvaj artikoloj, aperintaj en Heroldo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Kiu ne konas André Cherpillod [ŝerpijó], almenaŭ en la franca Esperanto-medio? Li
   verkis multajn librojn en la franca kaj en Esperanto. Fojfoje pri la samaj temoj,
   ekzemple, pri la historia tiel nomata „Johana d' Arko”, kiu, 
   <i>verŝajne</i>, ne estis bruligita en Rouen en 1430 – spite al tio, kion la oficialaj katolikaj historiistoj delonge gurdas al ni.
</p> 
<p>Cherpillod verkis multajn broŝurojn en la franca por konatigi nian lingvon internacian
   kaj ties meritojn, ekzemple, en la konstruado de vere popola Eŭropa Unio. Sed kiel
   aŭtoro, verkanta rekte en Esperanto, li rolas de multaj jaroj kiel 
   <i>kleriganto</i> pri bazaj gramatikaj konoj de nia lingvo. Tiuteme mi citos nur kelkajn, per kiuj
   mi povis pliprofundigi miajn konojn: 
   <i>La fundaj tekstoj de Esperanto</i>; 
   <i>La gramatika karaktero de la esperantaj radikoj</i>; 
   <i>Vortkonsisto, vortfarado, vortanalizo, vortordo</i>; 
   <i>La tradukarto kaj ties problemoj</i>. Mi varme konsilas al verkemuloj la legadon de tiuj du lastaj (plu disponeblaj).
   La stilo estas klara, la argumentoj estas bazitaj sur racia logiko kaj – ĉefe – sur multaj ekzemploj kaj ties fontoj.
</p> 
<h3>Al la bazoj</h3> 
<p>Nu, en sia lasta libro, Cherpillod revenas al la bazoj de nia lingvo, nepre konindaj,
   se ni volas, ke Esperanto post 126 jaroj da disvolviĝo plu ekzistu kiel internacia
   komunikilo. Jam la titolo estas ia vort-ludo : La „fundaj komentoj” pensigas pri la „Fundamento”, tiu netuŝebla mejloŝtono!
</p> 
<p>La leganto povas ŝvebi de ĉapitro al ĉapitro, laŭguste, ĉar temas pri kolekto da lingvaj
   artikoloj, aperintaj en 
   <i>Heroldo de Esperanto</i>. Jen kelkaj: 
   <i>Internacieco de geografiaĵoj</i>, 
   <i>„Aŭtobuso” vera universala vorto, kvankam artefarita!</i>, 
   <i>Metaforoj: ĉu internaciaj?</i>, 
   <i>Kial duspecaj landnomoj?</i>, 
   <i>Ĉu „li grandas” estas sinonimo de „li estas granda”?</i>, 
   <i>JES, NE: ne-internaciaj kutimoj</i>, 
   <i>La tri specoj de adverboj</i>. Kaj tiel plu.
</p> 
<p>Kio estas rimarkinda en la metodo de Cherpillod, tio estas lia konstanta reveno al
   la bazoj de Esperanto, kiujn li ĉerpas (jes ja!) el la Fundamento kaj la plej klasikaj
   verkoj de Zamenhof mem. Kelkfoje li havas dubojn pri iu temo, fojfoje li rimarkas
   eraron de Zamenhof, kiun tiu ĉi korektis poste. Esperanto ne estas perfekta lingvo
   (tiaĵo ne ekzistas), sed ĝi havas multajn bonajn kvalitojn. La aŭtoro substrekas precipe
   la 
   <i>internaciecon</i> de nia lingvo (ne nur en la vortprovizo, sed ankaŭ en la sintakso). Li bedaŭras,
   ke tiu internacieco estas kelkfoje malrespektata pro neutilaj neologismoj. Li ankaŭ
   batalas kontraŭ la nuna kutimo anstataŭigi la ĉapelitajn literojn per iksoj. Kompreneble,
   la leganto rajtas havi malsaman opinion, sed estas malfacile rebati la argumentojn
   de la aŭtoro, ĉiam bazitaj sur racia pensado kaj ekzemploj kun precizaj referencoj.
</p> 
<h3>1800 paĝoj</h3> 
<p>Ne eblas nei la kompetentecon de la aŭtoro (kiu jam verkis pli ol 1800 paĝoj pri Esperanto).
   Bedaŭrinde, okazis multaj polemikoj pri liaj artikoloj, kun kelkfoje malhonestaj sintenoj
   (ekzemple, misprezento de tio, kion li skribis). Ĉar la aŭtoro estas memdefendo-kapabla,
   li ne hezitas aldoni al siaj artikoloj kromajn rimarkojn, titolitajn 
   <i>Vage tra la deliraj recenzoj</i>, kiujn li enpaĝigis en kadro fine de paĝo, por klare dispartigi ilin de la koncerna
   artikolo. Mi konfesas, ke mi ĝuis la legadon de tiuj aldonoj, jes, malgraŭ ilia polemika
   aspekto, pro la ironia tono, kiu, verŝajne, ne plaĉos al ĉiuj, sed amuzis min!
</p> 
