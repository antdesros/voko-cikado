      <h3>Moderna vivo</h3>
      <h3 class="tem">FRANCIO</h3>
      <h2>Publika mono por privata koncerto</h2>
      <!-- komenco de la enhavo -->

       

       <p>Marsejlo, en Provenco, suda Francio, estas elektita kiel eŭropa kultura ĉefurbo por la jaro 2013. Tiu titolo estas laŭvice disponigata al eŭropa lando, kiu poste decidas, kiu urbo iĝu la tiujara kultura ĉefurbo.</p> <p>Marsejlon oni decidis elekti en septembro 2008. La decidantoj opiniis, ke Marsejlo bezonas tiun helpon, kiun povas liveri ĉefurba statuso.</p>   <p>En la decido partoprenis ĉiu registara tavolo. La kulturan programon subvencias la franca registaro, la regiona konsilio, la departementa konsilio, la marsejla urba konsilio, aliaj lokaj administracioj kaj privataj subtenantoj.</p> <h3>Spektakloj</h3> <p>Konstruado de novaj muzeoj, renovigo de malnovaj muzeoj, ekspozicioj, spektakloj (danco, cirko, teatro), koncertoj ktp aperis en la programo. Samtempe multaj partoj de la urbo estas renovigataj.</p>   <p>Multaj lokaj artistoj proponis partopreni, sed ili renkontis du obstaklojn. La decidantoj preferis decidi per si mem, ne engaĝante lokajn artistojn (aŭ tre malmultajn). Cetere la organizantoj deklaris, ke ili ne havas sufiĉan buĝeton. Multaj interesaj kaj novecigaj projektoj ne sukcesis pro manko de publika subteno.</p>   <p>Tamen antaŭ kelkaj monatoj la marsejla urba konsilio opiniis, ke mankas granda somera koncerto por allogi pli da homoj. Do ĝi decidis subteni koncerton de David Guetta, diskludisto konata en Francio kaj muzik-produktanto, kontraŭ la sumo de 400 000 eŭroj.</p> <h3>Biletoj</h3> <p>Krome ĝi decidis senpage disponigi la parkon Borely (la plej granda parko de la urbo) al tiu koncerto. Guetta planas koncerti la 23an de junio, kaj la biletoj kostas inter 44 kaj 60 eŭrojn.</p> <p>Do la urbo finance helpas privatan eventon de komerca artisto (se oni konsideras Guetta kiel artiston), permesas senpagan uzon de la plej granda parko, dum la koncerto restas multekosta por la publiko kaj granda fonto de profito por la „artisto”.</p>   <p>Cetere neniu scias, kiel la parko estos purigita kaj reordigita post la koncerto. Antaŭ kelkaj monatoj, la urba konsilio rifuzis disponigi la parkon al organizantoj de loka elektromuzika festivalo (<i>Aires Libres</i> – Liberaj aeroj), dirante, ke tio difektus la parkon.</p> <h3>Impostpagantoj</h3> <p>Sed al tiuj dekmiloj da homoj, kiuj venos al la koncerto de Guetta, ne gravas: la urbo (per mono de la impostopagantoj) reordigos la parkon.</p>   <p>Kompreneble multaj lokuloj protestas kontraŭ tiu ĉi misuzo de publika mono. Dum nur unu semajno, multaj reagis. Petskribo estas lanĉita: post unu semajno, ĝi ricevis jam pli ol 15 000 subskribojn, dum pli ol 28 000 membroj aliĝis al Facebook-grupo (<a href="http://www.facebook.com/groups/368202943287361/">www.facebook.com/groups/368202943287361/</a>) kontraŭ tiu subvencio.</p>   <div style="text-align:right">Thierry S<span class="mm">PANJAARD</span> kaj Esperanto-Marseille</div> 
