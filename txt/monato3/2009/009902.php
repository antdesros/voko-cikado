      <h2>Sango kaj honoro</h2>
      <!-- komenco de la enhavo -->

       

       <p><i>La artikolo klarigas, kial M<span class="mm">ONATO</span> rajtas, ja nevole, sed sentime aperigi nazian sloganon.</i></p> <p>Paragrafo 86a de la germana krimjura kodekso malpermesas, minacante per puno, publikan uzon de faŝistaj simboloj, paroloj, motoj ktp, devenantaj el la tempo de la „tria regno”. Ĝi validas ankaŭ por aktualaj faŝismaj simboloj, do por tiaj, kiuj „similas konfuzeble” al la naziaj.</p> <p>Laŭ la leĝo, akrigita en 2005: „Tri jarojn da karcero aŭ monpunon ricevas tiu, kiu publike aŭ en kunveno lezas la publikan pacon per konsento al, glorado de kaj justigo de la nacisocialista regado.”</p> <p>Klara decido, sed sekvoj en la praktiko montriĝas komplikaj. Jam plumpaj imitoj ne estas facile trakteblaj de la juĝistoj kaj, inverse, kontraŭfaŝista bonintence trastrekita svastiko antaŭ du jaroj apenaŭ povis eskapi punan verdikton.</p> <h3>Delikata</h3> <p>Aktuala kazo estas aparte delikata. La ekstremdekstra organizaĵo „Blood and Honour”, fondita en la 80aj jaroj en Britio, estas malpermesita en 2000 en Germanio. Laŭ la ministrejo pri internaj aferoj, ĝi agas kontraŭ la konstitucia ordo kaj internacia kompreniĝo.</p> <p>La organizaĵo uzas anglalingve la sloganon de la hitlera junularo „Blut und Ehre”, do „Sango kaj honoro”. Sed nun, vendisto de t-ĉemizoj kun la anglalingva slogano, evidente kontraŭleĝa, kontraŭis advokate akuzon antaŭ suba juĝejo – kaj gajnis la proceson ĉe la federacia kortumo en Karlsruhe.</p> <p>Laŭ la decido de la tieaj juĝistoj, naziaj sloganoj en fremdaj lingvoj ne rekte peras ties enhavon kaj ne plu estas rekoneblaj kiel identigiloj de kontraŭkonstituciaj organizaĵoj. Sekve, germanlingva uzo de naziaj motoj estas punata, dum celkonscia tradukaĵo en aliaj lingvoj restas nepersekutata.</p> <h3>Stultaj</h3> <p>Ĉu do <i>Karlsruhe locuta, causa finita</i> – Karlsruhe parolis, do finiĝis la afero? Almenaŭ mirigas, ke germanaj juĝistoj en 2009 agnoskas la anglan lingvon, post pli ol 60 jaroj da pena instruado en sia lando, „fremda”. Ĉu ili vere taksas membrojn, klientojn, simpatiantojn, do la „bruneman” parton de la germana popolo, tiom stultaj, ke ili ne rekonas la tiamajn kaj ankoraŭ aktualajn naziajn kaj naziemajn kaptilojn?</p> <p>Ne, ili bone konas la trukojn de la ekstremaj dekstruloj, ludantaj nun ankaŭ per lingvoj. Sed ili preferas reĵeti la kazon al la politiko, la socio mem. Laŭ ili, la punleĝo sola ne kapablas forigi la nazian idearon el la socio. Certe. Sed en aliaj kazoj tiaj superaj kortumoj ne hezitas memkonscie kaj decideme interveni per verdikto.</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div> <div style="text-align:right"><i>Germanio</i></div> 
