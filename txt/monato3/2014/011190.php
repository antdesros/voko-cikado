      <h3>Eseoj</h3>
      <h3 class="tem">MODERNA VIVO!</h3>
      <h2>La lasta kilometro aŭ ... nia nova poŝto</h2>
      <!-- komenco de la enhavo -->

       

        <p>Antaŭ la epoko de Eŭropa Unio ne estis problemo ricevi paketon. Se oni ne estis hejme, oni trovis informon en la poŝtkesto kaj ricevis la paketon ĉe proksima poŝtejo. Sed hodiaŭ ekzistas ne nur „la” ŝtata poŝto, sed ankaŭ pliaj privataj poŝtservoj, kaj la afero pli komplikiĝas.</p> <p>Ekzemple en la vilaĝo Grinzing [grincing], kiun amase vizitas drinkemaj turistoj, jam delonge ne ekzistas poŝtejo. Ties rolon ludas nun apoteko ĉe la centra placo. Aliaj kaŝ-poŝtejoj ne estas tiel facile troveblaj, kiel indikas la sekva, kaj ja vera, rakonto. Nur la nomoj estas ŝanĝitaj.</p> <h3>Paketo</h3> <p>Meze de oktobro 2013 mi trovas papereton gluitan sur la pordon de mia loĝejo: „Malgranda paketo atendas en Sanktloka Strato 202”. Mi iras al bushaltejo, atendas dek minutojn, veturas kvar haltejojn kaj staras antaŭ la domo Sanktloka Strato 8.</p> <p>Post dekminuta piedirado mi venas al numero 202 kaj staras antaŭ magazeneto de lano kaj kudriloj. Tiu magazeno estas fermita pro ferioj, sed laŭ afiŝo paketoj preneblas en apuda magazeno de fruktoj kaj legomoj.</p> <p>Tie simpatia vendistino kun kaptuko alparolas min unue turke, sed tuj poste germane. En malantaŭa parto de la magazeno mi ekvidas amason da paketoj. La vendistino longe serĉas, sed ne trovas tiun, kiu min koncernas.</p> <p>Fine ŝi parolas telefone kun sia ĉefo. Dume krias infanoj, kaj venas pliaj. Post 20 minutoj ŝi diras, ke mia paketo troveblas en magazeno en Gencostrato 84.</p> <h3>Plua serĉo</h3> <p>Ne estas problemo veturi en la najbaran distrikton: unue per buso, poste per metroo, fine per tramo. Mi serĉas numeron 84. Ekpluvas. Virino helpema diras al mi, ke paketoj atendas en magazeno de armiloj, iom proksime. Sed tie oni nenion scias.</p> <p>Mi eniras apudan frizistan salonon, kie hundo min alsaltas. Tie kliento diras, ke paketoj haveblas en magazeno de poŝtelefonoj aŭ en magazeno de gazetoj. En la telefonvendejo staras kelkaj grandaj viroj arabe parolantaj. Ĉu klientoj aŭ babilemuloj?</p> <p>Mi atendas ekstere en la pluvo. Post kelkaj minutoj la grupo malaperas kaj vendisto trovas mian paketon. Mi devas montri mian pasporton kaj tiel fariĝas la fiera posedanto de mia kara paketo. La domo havas la numeron 48, ne 84. Nur 40 minutojn poste mi estas hejme ... kaj feliĉa.</p> <div style="text-align:right">Walter K<span class="mm">LAG</span></div>  
