      <h3>Politiko</h3>
      <h3 class="tem">EŬROPA UNIO</h3>
      <h2>Historia momento</h2>
      <!-- komenco de la enhavo -->

       

      <p>En decembro pasintjare granda parto de Eŭropo ricevis historian donacon: al la tiel nomataj Ŝengen-ŝtatoj de Eŭropa Unio (EU) aliĝis naŭ novaj landoj (plejparte iamaj socialismaj landoj). Sekve la kontinenta parto de EU kune kun Islando kaj Norvegio iĝis unueca landego konsistanta el 24 ŝtatoj. Post unu jaro ankaŭ Svislando aliĝos, baldaŭ ankaŭ Rumanio kaj Bulgario.</p> <p>La ceremonio komenciĝis en Tallinn, ruliĝis suden kaj finiĝis ĉe la itala-slovena ŝtatlimo. Partoprenis kremo da politikistoj, inter aliaj la prezidanto de la Eŭropa Komisiono José Manuel Barroso, la prezidanto de EU José Sócrates, kaj la prezidonto de EU Janez Janza. Malfermis la solenaĵon Barroso, kiu diris: „Vere estas historia momento”.</p> <h3>Limkontrolado</h3> <p>En la nordo restas problemo pri Kaliningrad (iama Königsberg), „fremda korpo” en la Unio, havanta marbordon, sed bezonanta ankaŭ surteran kontakton kun Rusio. Tie necesas severa limkontrolado. Tamen la historie signifa limo Odro-Niso ne plu ekzistas: la germana kanceliero Angela Merkel ĝin solene malaperigis. Aliloke, la slovaka ministrestro Robert Fico kaj la aŭstra kanceliero Alfred Gusenbauer kune dissegis limobarilon ĉe la aŭstra-slovaka limo.</p> <p>Tuj poste hungaroj, aŭstroj kaj slovakoj faris ceremonion en la trapasejo Hegyeshalom [hedjeŝhalom]. La hungara ministrestro Ferenc Gyurcsány [djurĉanj] diris: „La neniesejo iĝis ĉiesejo”. En la hungara oni nomumis neniesejo mallarĝan zonon ĉe la limo. Preter neniesejo laŭ la hungara flanko troviĝis tiel nomata fera kurteno kun palisaro, pikdrato, pafistoj kaj minoj. Ĝuste en la iama neniesejo Gyurcsány, Fico, Barroso, Sócrates kaj la aŭstra ministro Günther Platter metis memorpalison, sur ĝi vojmontriloj.</p> <h3>Laborlokoj</h3> <p>La festoj ne okazis senprobleme. En Germanio kaj Aŭstrio oni timas pri laborlokoj kaj pri teroristoj. En Aŭstrio proksime de Hungario ofte aperis hungarlingvaj surskriboj: „Hungaro! Ne ŝtelu!” Ĉe la slovaka-hungara limo troviĝas urbo Sátoraljaújhely [ŝatoraljaujhej], kiun la nova ŝtatlimo en 1920 disigis al hungara kaj slovaka partoj, ĉar Ĉeĥoslovakio pretendis la kompletan fervojan linion el Rumanio. La pli malgranda urboparto kun stacidomo nomiĝas Slovenské Nové Mesto. Inter ili estis trapasejo. La slovakoj timis la supozatan grandan trafikon, tial ili baris la vojon per betonaj florvazoj.</p> <p>La ceremonio finiĝis en Skofije, ĉe la slovena-itala limo. Sócrates diris: „Mi ne povus pli bone fini mian mandaton pri prezidanteco” kaj simbole transdonis al Jansa la prezidantecon. Ili konstatis: „Antaŭ 20 jaroj ĉi tie oni pafis la homojn”. Nun 400 milionoj da ŝtatanoj de EU libere trafikadas en granda parto de kontinenta Eŭropo.</p> <div style="text-align:right">Karlo J<span class="mm">UHÁSZ</span></div> 
