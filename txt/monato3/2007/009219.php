      <h3>Ekonomio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Eksterlandaj investantoj rapide aĉetas nemoveblaĵojn</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Antaŭ nelonge eksterlanda investfonduso aĉetis parcelon de ĉ. 24 000 kvadrataj metroj en Ŝanhajo. Ĝi planas investi 1,3 miliardojn da ĉinaj juanoj por tie konstrui multetaĝan oficejan domegon kaj uzi la restantan parton por alia komerca celo.</b></p> <p>Jen ekzemplo kiel en pluraj provincoj kaj municipoj de Ĉinio alilandaj entreprenoj specialigitaj en negoco de nemoveblaĵoj aĉetas terpecojn kaj memstare konstruas kaj vendas novajn domojn. Ankaŭ almenaŭ 100 famaj ĉinaj entreprenoj el la sama sektoro kunlaboras kun alilandanoj, kondiĉe ke tiuj grandkvante kuninvestas.</p> <p>La ciferoj de la Ŝtata Statistika Buroo de Ĉinio montras, ke en 2006 la eksterlanda kapitalo, kiu eniris la ĉinan merkaton de terenoj kaj domoj, atingis 8,23 miliardojn da usonaj dolaroj. Tio estas pliiĝo je 51,9 % kompare kun la antaŭa jaro. La Buroo antaŭvidas ke la kreska tendenco daŭros en 2007.</p> <h3>Alilandaj entreprenoj stimulas konkurencon</h3> <p>Ĉar en la pasintaj jaroj en la ĉina landinterno la domprezoj senĉese leviĝis, domkomercistoj facile gajnis profitegojn. La blinda ekspansio kaj la senorda konkurenco malbone influis la tutan socion kaj kaŭzis grandan atenton ĉe la registaro. Ekde la dua parto de 2006, la registaro praktikas severan reguligan politikon. Unuflanke la ŝtato limigas grandajn investojn kaj eliminas nebonajn entreprenojn. Aliflanke, por rompi la monopolon de la enlandaj entreprenoj, eksterlandaj entreprenoj pli facile ricevas investpermesojn. Do estas evidente ke tiuj ludos pli kaj pli gravan rolon en unu el la plej viglaj industrioj de la lando.</p> <h3>Transdono de projektoj</h3> <p>La ŝtato multe plialtigis la grund-impostojn. Pro tio, mezaj kaj malgrandaj enlandaj entreprenoj unu post alia forlasas la origine planitajn projektojn. Por alilandaj entreprenoj estas ŝanco por rapide akiri ion. Alia faktoro estas la alta kurzo de la ĉina mono. Tio stimulas la eksterlandajn investantojn, kiuj jam delonge rezervis ĉinan monon, por tuj aĉeti projekton kaj gajni altan profiton. En januaro, entrepreno el Hanzhou, orienta Ĉinio, ofertis du parcelojn en pitoreska loko kontraŭ kontanta mono. Du aŭ tri alilandaj entreprenoj tuj ligis kontaktojn.</p> <h3>Altaj profitmarĝenoj</h3> <p>La decidiga motivo por eksterlandaj entreprenoj estas la altaj profitoj, kiuj nuntempe eblas. Laŭ raporto, antaŭ nelonge publikigita de RREEF, investfilio de la germana <i>Deutsche Bank</i> (<a href="http://www.rreef.com">www.rreef.com</a>), la malneta profitmarĝeno de loĝdoma projekto en Ĉinio jam atingas 20 ĝis 30 % kaj eble baldaŭ superos tiun de Usono. Pekino kaj Ŝanhajo okupis la unuan lokon, antaŭ usonaj urbegoj. En Parizo, Sidnejo kaj Honkongo la profitmarĝeno estas nur 4 ĝis 5 %.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div> 
