      <h3>Politiko</h3>
      <h3 class="tem">BALKANIO</h3>
      <h2>Per into al onto</h2>
      <!-- komenco de la enhavo -->

       

       <p>En aŭtuno malfermiĝis en Tirano (Albanio) ekspozicio <i>Ni rememoru por la estonteco</i>. La vojaĝanta ekspozicio, vizitebla ankaŭ en Skopjo (Makedonio), Sarajevo (Bosnio-Hercegovino), Beogrado (Serbio-Montenegro), Cluj-Napoca (Rumanio) kaj en Sofio (Bulgario), celas per historio malkovrigi komunajn radikojn kaj, samtempe, komprenigi diferencige diversajn historiajn eventojn.</p> <p>Tiel organizantoj de la Instituto por Internacia Kunlaboro de la Asocio de Altlernejoj de Germanio volis certigi dialogon interetnan kaj internacian por paca komuna estonteco en suda Eŭropo sen emociaj ŝarĝoj kaj antaŭjuĝoj. La projekto komenciĝis en oktobro 2002 per seminario pri tiel nomata buŝa historio. La ideo pri ekspozicio naskiĝis en Sofio en 2003 dum renkontiĝo kun la temo <i>Premo kaj liberiĝo</i>.</p> <h3>Teruraj kondiĉoj</h3> <p>Albanio prezentiĝis en la ekspozicio per vidaĵoj pri la fifama politika prizono Spaĉ, kie suferis dum 17 jaroj 1100 politikaj malliberuloj. Pro teruraj kondiĉoj okazis tie en 1973 ribelo, kiun la tiama reĝimo sovaĝe subpremis. Severe rekondamnitaj estis 175 malliberuloj, kelkaj al morto. Bosnio-Hercegovino prezentiĝis per scenoj de suferaĵoj de la ĉefurbo inter 1992 kaj 1995.</p> <p>Bulgario prezentiĝis per vidaĵoj, spegulantaj la vivon de la socialisma junularo antaŭ 1990; Makedonio per 200 fotoj pri sia historio kaj etnografio (okulfrapis, ke oni prezentis Aleksandron la Grandan kiel sametnulon de la nuntempaj makedonoj, ankaŭ patrinon Tereza kiel makedoninon kaj ne albaninon).</p> <p>Rumanio prezentiĝis per la temo <i>La armita rezistado kontraŭkomunisma dum la jaroj 1944-1962</i>; fine Serbio-Montenegro per fotoj el marto 1991, kiam okazis studentaj manifestacioj en la ĉefurbo, sovaĝe subpremataj de la reĝimo de Milošević.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
