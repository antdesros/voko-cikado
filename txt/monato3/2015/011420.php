      <h3>Hobio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Majstro de la tradicia ceramiko</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Gan Zhiyou loĝas en la vilaĝo Fu Li, la provinco Zaozhuang (orienta Ĉinio). Ordinara
      kamparano, li famiĝis pro sia hobio. Kaj Unesko donis al li honoran titolon „Popola Metiartisto”.
   </b>
</p> 
<p>Gan Zhiyou estas 63-jara kaj denaskiĝe loĝas en sia vilaĝo Fu Li. Tie ĉio estas ordinara:
   domoj, kampoj, montetoj, arboj ... Kaj la vilaĝanoj trankvile okupiĝas pri agrikulturo
   jaron post jaro. Nur la nomo de la vilaĝo Fu Li estas eksterordinara. Ĝi estas mallongigita
   el Fu Xi Gu Li. Laŭlegende Fu Xi estis unu el la kvin pratempaj imperiestroj de Ĉinio.
   Li iam loĝis ĉi tie kaj instruis ceramikfaradon al la popolanoj. La nomo de la vilaĝo
   pruvas tion.
</p> 
<h3>Libera tempo</h3> 
<p>La kamparanoj de tiu vilaĝo faras tradicie ceramikaĵojn dum la libera tempo. Neniu
   el ili scias, kiam ekestis la farado de ceramikaĵoj. Ankoraŭ en sia infanaĝo oni rimarkas,
   ke ĉiu familio en la vilaĝo posedas fornon por ceramikaĵoj – grandan aŭ etan. En la granda oni bakas ceramikaĵojn por la ĉiutaga vivo, ekzemple
   kruĉojn, flor- kaj ter-potojn. Kaj en la eta oni bakas ceramikaĵojn por sia hobio.
   Ĉi-lastajn oni kreas en la formo de birdoj, bestoj, homoj k.a.
</p> 
<p>La familio de Gan Zhiyou havis du ceramikaĵ-fornojn – grandan kaj etan. Ekde la infanaĝo Gan jam havis grandan emon fari la ceramikaĵojn
   kaj baki ilin en la eta forno. Li ofte modlis el argilo diversajn objektojn. Li diras,
   ke tiutempe la noktoj estis tre belaj pro la ruĝigita ĉielo sekve de la fornaj rebriloj.
   Tamen poste, en la sesdekaj jaroj de la pasinta jarcento, dum la katastrofa tempo
   de la „Kultura Revolucio”
   <sup>1</sup>, oni ruinigis la fornojn por ceramikaĵoj kaj detruis la tradiciajn muldilojn. La
   respondo de la regpotenco estis tre simpla: „Ili ĉiuj estas restaĵoj de la feŭdismo!”
</p> 
<p>Kun granda indigno Gan diras: „Tio ja estis la ‚donaco’ de la ‚Kultura Revolucio’!” Poste li serĉis kaj trovis muldilojn, kiuj estis kaŝitaj de iuj vilaĝanoj dum la
   katastrofa tempo. Li restarigis fornon per la mono, ŝparita por la geedziĝo de lia
   frato. Tiu bakujo denove ekfunkciis, sed estis sola en la vilaĝo.
</p> 
<h3>Trovaĵoj el la neolitiko</h3> 
<p>En 1993 en la vilaĝo oni malkovris spurojn de la pratempa neolitika kulturo 
   <i>Da Fen-kou</i>, trovis restaĵojn de fornoj por ceramikaĵoj kaj pecojn de muldiloj. Science oni konstatis,
   ke ili ekestis antaŭ 5600 jaroj. Tio famigis Gan kaj lian vilaĝon. Estis daŭrigita
   la tradicia kulturo de antaŭ 5600 jaroj! Tiam Gan subite ekkomprenis, ke la afero
   jam ne estas tiel simpla kiel hobio. Ĝi estas ligita kun la kultura tradicio de la
   ĉina nacio! Li sentis, ke li devas savi la heredaĵon de la nacia kulturo. Kun granda
   deziro li serĉis kaj trovis tradiciajn muldilojn kaj studis la metodojn de la pratempa
   ceramiko. Finfine li posedis 39 muldilojn, el kiuj la plej malnova aĝis 1425 jarojn
   kaj estis el la epoko de Sudaj kaj Nordaj Dinastioj en Ĉinio. Per la muldiloj kaj
   helpe de tradiciaj metodoj li faris pli ol 600 ceramikaĵojn!
</p> 
<p>Nun liaj ceramikaĵoj troviĝas jam en 21 landoj kaj regionoj de la mondo kaj iuj estas
   konservataj de muzeoj (ekzemple, de la Belarta Muzeo de Ĉinio, Arta Instituto de Ĉinio,
   Muzeo de Ludiloj en Japanio ktp). Fakuloj el Ĉinio kaj eksterlando opinias, ke la
   ceramikaĵoj el Fu Li estas vivantaj historiaj objektoj de pratempa ceramiko kaj esprimas
   la homan naivecon per absurdaj figuroj.
</p> 
<p>Ceramikaĵo 
   <i>Staranta maturaĝa leono</i> gajnis la Kupran Pokalon de la ministrejo pri kulturo de Ĉinio en la Unua Ekspozicio
   pri Originala Tekniko de la Ĉina Popolarto. Oni diras, ke Gan Zhiyou per sia hobio
   faris sensacion, ĉar li savis la pratempan ceramikan tradicion de la Ĉinio! Pro tio
   Unesko donis al li la honoran titolon „Popola Metiartisto”. Do estas vero, ke oni povas famiĝi, praktikante ankaŭ ordinaran hobion!
</p> 
