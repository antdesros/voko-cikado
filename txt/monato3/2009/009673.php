      <h3>Spirita vivo</h3>
      <h3 class="tem">JUDISMO</h3>
      <h2>Nova sinagogo en Speyer</h2>
      <!-- komenco de la enhavo -->

       

      <p>La 9an de novembro 2008 en Speyer [ŝpajer], suda Germanio, la urbo kaj la juda komunumo kune fondis novan sinagogon. En la lastaj jardekoj multaj judoj el Rusio translokiĝis en tiun apudrejnan urbon. Tial la antaŭe eta judaro nun povis kuraĝe aliri la fondon de juda centro kun sinagogo kaj kromaj salonoj kaj instalaĵoj.</p> <p>La dato de la fondado estas simbola. Akurate sepdek jarojn post la detruado de multege da sinagogoj en la hitlera Germanio dum pogroma nokto, tiam moke nomata „imperia kristalnokto”, oni fondis la trian sinagogon en Speyer post la mezepoka kaj post la dua el la dek-naŭa jarcento, en 1938 detruita de la la nazioj. Speyer apartenis al la SCHUM-komunumoj, la plej renomaj judaj komunumoj en la mezepoko, kie elformiĝis la aŝkenazaj rito, teologio kaj vivmaniero. La akronimo SCHUM ligas la urbojn Speyer (SCH – Ŝ), Worms (W – Ŭ) kaj Mainz (M).</p> <p>Ke nuntempe en Germanio fondiĝas sinagogoj, ne plu estas malofta mirindaĵo. Sed en Speyer la loko de la konstruaĵo estas aparte menciinda. La nova sinagogo ne estas konstruebla sur la tereno de la siatempe detruita sinagogo. Tie, urbocentre, instaliĝis jam antaŭ multaj jaroj granda vendejo. Anstataŭe oni trovis konvenan lokon en la tereno de la iama katolika monaĥejo Sankta Guido, antaŭ nelonge profanita kaj akirita de la urba komunumo. La sinagogo mem havos sian lokon precize tie, kie oni dum jarcentoj kultis kristane.</p> <p>Dum la solena metado de la baza ŝtono parolis inter aliaj Kurt Beck, ministro-prezidanto de la federacia lando Rejnlando-Palatinato, Werner Schineller, la urbestro kaj precipa apoganto de la projekto, Peter Waldmann, la prezidanto de la Landa ligo de judaj komunumoj en Rejnlando-Palatinato, Karl-Heinz Wiesemann, la episkopo de la diocezo Speyer, Eberhard Cherdron, prezidanto de la evangelia eklezio de Palatinato kaj Alfred Jacoby, la arkitekto. Ili ĉiuj emfazis la simbolan karakteron de la fondado, de la dato, de la loko. Samtempe ili memorigis la dolorigajn eventojn en 1938 kaj aktualajn kontraŭjudajn agadojn kun la resumo: Neniam ree! Entute ili taksis la refondadon de la juda komunumo kaj la konstruadon de nova sinagogo esperiga momento en la urba kaj regiona historio, signifa religie, politike kaj kulture.</p> <p>La ceremonion akompanis specife judaj kantoj. Kantoro kantis psalmon antaŭ la baza ŝtono, kiu trovis sian lokon en la niĉo, kie iam tabernakle manifestiĝis la dia ĉeesto. Granda aro da judoj kaj simpatiantoj estis konsciaj pri la historia signifo de la fondado. Per ĝi mirakle reekfloros la juda vivo en Speyer, antaŭe preskaŭ plene estingita. Laŭ la planoj la nova centro ekfunkcios en 2010, pliriĉigonte per judaj kontribuoj la kulturan vivon de la urbo, aktuale pli konata pro la katolika katedralo, tombejo de pluraj germanaj imperiestroj, kaj la protestanta protestado.</p>     <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div> 
