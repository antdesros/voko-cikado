      <h3>Moderna vivo</h3>
      <h3 class="tem">IRLANDO</h3>
      <h2>Lingvoj, komunumoj  kaj politiko</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kiel gaelparolanta regiono (<i>Gaeltacht</i> [gŭejltaĥt]) estas agnoskita en septembro kvartalo en okcidenta Belfasto. Neniu alia nordirlanda instanco krom la ĉefurba magistrato ĝis nun nomumis regionon tia.</p> <p>La termino <i>Gaeltacht</i> estas uzata en la Irlanda Respubliko por regionoj, kie la gaela (irlanda) estas parolata de la plimulto aŭ granda parto de la popolo. Laŭkonstitucie la gaela estas la unua oficiala lingvo de la Respubliko, sed ne de Nord-Irlando.</p> <p>Sekve membroj de la majoritata Demokrata Uniista Partio de Nord-Irlando protestis kontraŭ gaelparolanta kvartalo en Belfasto. Laŭ ili, iniciatis la projekton Sinn Féin [ŝin fejn], la respublikista partio, kiu celas unuigi la irlandan insulon kaj ĉesigi britan regadon en Nord-Irlando.</p> <p>Defendantoj de la gaelparolanta regiono atentigis, ke en aliaj urboj troveblas simile agnoskitaj kvartaloj, ekzemple la tiel nomataj „ĉinaj urboj” (angle: <i>China Town</i>).</p> <h3>Nesektaj lernejoj</h3> <p>En Nord-Irlando oni neniam povas neglekti politikon. Multaj nordirlandanoj reagas pli politike ol kulture al lingvo-demandoj. La politiko decidas interalie pri muziko, danco, sporto, folkloro kaj precipe religio. Tamen videblas signoj, ke tiu ĉi sinteno ŝanĝiĝas, ekzemple en nesektaj lernejoj kaj en universitatoj.</p> <p>Aliflanke estas raportite lastatempe pri gaelparolanta skota interŝanĝ-studento, kiu vizitis Nord-Irlandon. Surpriziĝis la skotino, ke la gaela estas rigardata kiel lingvo parolata nur de katolikoj. Simile, nordirlandanoj miris, ke nekatolikaj skotoj parolas la skotan varianton de la gaela. En Skotlando la gaela neniam estas rigardata kiel politika aŭ sektisma afero.</p> <p>Kaj konata estas gaela poeto de nordirlanda deveno, kiu intence verkas ne en la irlandgaela sed en la skota, ĉar li ne volas esti rigardata kiel respublikisto.</p> <p>Tamen vizitantoj al multaj urboj en Nord-Irlando rimarkos, ke la gaela ofte estas pli aŭdebla kaj videbla (ekzemple en vendejoj kaj negocoj) en oficiale anglalingvaj distriktoj, ol en Dublino kaj aliaj urboj en la Respubliko. Oni povas havi la impreson, ke Belfasto iom post iom iĝas pli irlandlingva ol la suda ĉefurbo.</p> <div style="text-align:right">Garbhan M<span class="mm">AC</span>A<span class="mm">OIDH</span></div> 
