      <h2>Edikto el oriento?</h2>
      <!-- komenco de la enhavo -->

       

     <p>Jen libro – ho, jam tro respektema estas la vorto, „broŝureto” estas pli taŭga –, kiu ne plenumas sian promeson. La titolo „Ĉu kultura integriĝo de Eŭropo?” starigas demandon. La subtitolo „Jes, sed nur per demokratia simbiozo de la kulturoj” donas la respondon, kvazaŭ aserto. Kaj jen la fino de la afero. Vi, leganto, akceptu tiun edikton de la aŭtoro.</p> <p>En la broŝuro mem Jiři Laube sondas la fundamenton de la eŭropa kulturo, kiu, laŭ li, situas precipe en la lingvoj. Li distingas du specojn de lingvoj. Je unu flanko troviĝas la (nov-)hebrea/araba. Ili, laŭ lia takso, estas precipe intuiciaj, interalie pro manko de facila unusignifeco. Pro tio ke ŝvebas, ekzemple, vokaloj, interpreto de la lingvaĵo bezonas grandan imagopovon. Aliflanke, estas la eŭropaj, latinparencaj lingvoj, kiuj pro tro granda influo de la klasika latina gramatiko estas tro raciemaj. Por ponti la abismon inter la du, aŭ pli bone por anstataŭigi la racian latinan lingvon per intuicia lingvo, la aŭtoro proponas Esperanton.</p> <p>En la dua parto li klarigas, kial Esperanto estas la solvo por eŭropa integriĝo. Bedaŭrinde li tie ne alportas novajn elementojn, sed nur papagas la jam tradiciajn kaj konatajn argumentojn (facile lernebla, neŭtrala, egalrajtiga ktp).</p> <p>Kvankam lia komenca analizo de la bazo de la eŭropa kulturo estas promesplena, la aŭtoro ne sukcesis profundigi sian diskurson, nek alportis iun ajn fundamentan, renovigan aŭ freŝigan koncepton.</p> <div style="text-align:right">Guido V<span class="mm">AN</span> D<span class="mm">AMME</span></div> <h6 class="bib">Jiři Laube: Ĉu kultura integriĝo de Eŭropo? Jes, sed nur per demokratia simbiozo de la kulturoj. Eld. KAVA-PECH, Dobřichovice, 2007. 31 paĝoj broŝuritaj. ISBN 978 808585 396 4.</h6>  
