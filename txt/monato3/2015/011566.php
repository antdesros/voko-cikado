      <h3>Moderna vivo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Judaj rifuĝintoj en Ŝanhajo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Quan Furong, 92-jara, elkore ridetas, montrante la tri-etaĝan domon sur la strato
   Zhoushan, kie loĝis Michael Blumenthal, eksa ministro pri financo de Usono, dum la
   plej malbonaj tagoj de la dua mondmilito.
</p> 
<p>La domo troviĝas en la centro de la loĝkvartalo en la distrikto Hongkou, nordorienta
   Ŝanhajo, kiu fariĝis „regiono difinita por senŝtataj rifuĝintoj”, aŭ, pli neoficiale, la ŝanhaja geto, en 1943. Dum la milito kaj posta tempo, tie
   estis rifuĝejo de ĉirkaŭ 23 000 judoj, inkluzive de membroj de la familio de Blumenthal,
   kiuj fuĝis el Germanio kaj aliaj eŭropaj landoj dum la holokaŭsto.
</p> 
<p>Ĉe la vojkruciĝo de la vojoj Zhoushan kaj Huoshan, Quan montras alian imponan tri-etaĝan
   domon kun abrupte oblikva tegmento. „Tio estas mia hejmo,” li diras.
</p> 
<h3>Gastigis</h3> 
<p>La familio de Quan ekloĝis en la domo en fine de 1942, kiam li estis 20-jara, nelonge
   antaŭ ol la judkvartalo akiris oficialan statuson kiel rifuĝejo el la japanaj invadantoj.
   En tiu ĉi domo ili gastigis judajn familiojn dum la milito. Nun, tiel multajn jarojn
   poste, ĝi restas tie, kie aliaj homoj, kiuj deziras pli bone kompreni la historion
   de tiu loko, kontaktiĝas kun Quan por lin intervjui aŭ nur babili.
</p> 
<p>Post la malvenko de Japanio en 1945 la judaj rifuĝintoj komencis iri el Ĉinio – multaj al Ameriko aŭ Aŭstralio, dum aliaj revenis al Eŭropo. Hodiaŭ, la malnova juda
   geto estas okupata de ĉinaj loĝantoj, kies familioj tien transloĝiĝis post la milito.
</p> 
<p>Longa tempo estas 70 jaroj. „Plejmulto da homoj, kiuj tiam tie loĝis, jam forpasis, kaj tiuj, kiuj estis ĉi tie,
   estis tre junaj siatempe, kaj ne povas vere memori ion,” diris Kitty Han, direktoro pri publikaj rilatoj en la Ŝanhaja Muzeo de Judaj Rifuĝintoj.
   „Sed Quan estas malsama. Li estis pli-ol-20-jara tiam, kaj povas klare memori ĉion,
   kio okazis.”
</p> 
<h3>Luis</h3> 
<p>„Ses judaj familioj loĝis en nia domo. Ni havis tre grandan domon, kaj ni luigis la
   ĉambrojn aparte al la judoj,” Quan diras. En la militaj tagoj, lia familio, kiu posedis tajloran negocon, kaj okupis
   tutan tri-etaĝan konstruaĵon, havis sufiĉe grandan spacon por loĝigi multajn judajn
   rifuĝintojn.
</p> 
<p>„Tiutempe ni ne konsciis la koncepton de geto,” Quan diras. „Ni sciis, en kiu regiono estas multaj judoj, kaj en kiu regiono estas malpli.” Li aldonis, ke el la pli grandaj avantaĝoj dum la milito en Ŝanhajo estis, ke la
   urbo grandparte subtenis sian entrepreneman spiriton malgraŭ la okupado de Japanio.
</p> 
<p>„En Ŝanhajo la judaj rifuĝintoj rajtis labori kaj prosperi, kaj multaj el ili havis
   laborojn,” Quan diras. „Ekzemple, iu juda virino pli-ol-40-jara loĝis en mia domo. Ŝi laboris por Jardine
   Matheson, parto de la brita Jardines (unu el la plej grandaj fremdlandaj komercaj
   firmaoj tiam en Azio), sur la Pekina Vojo.”
</p> 
<h3>Pendigis</h3> 
<p>La luantoj de Quan estis tre bonŝancaj. La judaj rifuĝintoj kutime pendigis ŝildojn
   sur si. La ŝildoj, ofte unu antaŭe, alia malantaŭe, estis reklamoj de la okupo de
   la posedanto kun la vorto kiel ekzemple „tajloro” aŭ „barbiro”. „Tio estis ilia maniero de memreklamado,” Quan diras.
</p> 
<p>La reklamoj efikis. Multaj judoj ja trovis tiumaniere laborojn. Eĉ tiuj, kiuj ne povis
   esti regule dungitaj, ofte akiris sufiĉe da enspezoj por subteni la ĉiutagan vivon.
   La familio de Quan iam dungis judan farbiston pli-ol-50-jaran, kiu portis tiajn ŝildojn.
   „Li laboris senĉese de mateno ĝis tagmezo, haltis por tagmanĝo, kaj daŭrigis la laboron
   ĝis vespero,” Quan memoras. „Li laboris tre bone.”
</p> 
<h3>Traktis</h3> 
<p>Quan ripetas komplimentojn, kiam li parolas pri la diligenteco kaj konfidindeco de
   la judoj. „Ili estis tre laboremaj kaj fidindaj, kaj traktis sian laboron serioze kaj sincere.
   Judoj ofte diris al mi, ke tiuj, kiuj havas siajn proprajn kapablojn, povas trovi
   laboron ie ajn en la mondo. Kaj mi tute konsentas,” Quan diras.
</p> 
<p>La rememoroj pri la geto estis neniam pli fortaj ol nun de post la milito. Registara
   fonduso savis diversajn malnovajn konstruaĵojn en la regiono kaj renovigis la malnovan
   sinagogon kiel muzeon de geto.
</p> 
<p>„Kiam Ĉinio malfermis siajn pordojn al la ekstera mondo, multaj judoj revenis ĉi tien
   ekde la 1990aj jaroj, kaj demandis, kie estas la sinagogo,” li diras.
</p> 
<h3>Rekonstruis</h3> 
<p>„Do ni trovis la originalan planon de la sinagogo en la Ŝanhaja Arkivo, kaj rekonstruis
   la sinagogon laŭ la dizajno,” Kitty Han diras. „Ni pensas, ke tio estas tre grava scenejo por internaciaj rilatoj, kaj ni havas la
   devon rakonti la historion.”
</p> 
<p>Tiusence, Quan ankoraŭ havas gravan mision en sia vivo. Kiel unu el la tre malmultaj
   homoj, kiuj povas plastike priskribi la historion de la ŝanhaja geto, li rakontas,
   ke dekmiloj da homoj sukcese eskapis el masakro, kiu mortigis milionojn da homoj.
</p> 
<p>Ĉirkaŭ la distrikto Hongkou estas pli kaj pli multaj modernaj ĉielskrapantoj. Tial
   la domo de Quan kaj la aliaj konstruaĵoj restantaj en la iama geto fariĝis ŝirmiloj
   kontraŭ la homa tendenco forgesi la pasintecon kaj antaŭeniri.
</p> 
