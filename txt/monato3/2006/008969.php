      <h3>Moderna vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Pensante pensie ...</h2>
      <!-- komenco de la enhavo -->

       

       <p>La postmilita generacio, nun pensiuliĝanta, estas tre egoisma – tiel opinias Osku Pajamäki, la aŭtoro de <i>La avida generacio</i>. Tiu ĉi generacio elspezis la havaĵon de la ŝtato por malpezigi la impostadon de la plej riĉaj popolanoj. Ĝi kreis socion, kie tiuj ĵus eklaborintaj pasigu sian tutan vivon kiel servistoj de la pli aĝaj homoj, pagante iliajn pensiojn kaj ankaŭ la ŝtatan ŝuldon amasigitan dum la regotempo de la postmilitnaskitoj.</p> <p>Nunaj junaj laboruloj povos ĝui nek sekuran dungitecon nek tradician familian vivon. Aldone, la idealo de vivlonga lernado, kiu por iliaj antaŭuloj estis agrabla nova defio kaj ŝanco, garantias al la nova generacio nur disrompitajn karierojn kaj homajn rilatojn. Kvankam la juna generacio pagas pli por pensioj, ili mem ne havos pensiojn egalajn al tiuj de la antaŭuloj, kiuj povas senzorge amuzi sin sur sunaj plaĝoj de sudaj landoj, sciante, ke Finnlando disponigos al ili bazan sekurecon pagatan de nunaj laboruloj.</p> <h3>Rekompenco</h3> <p>Aliflanke eblas diri, ke plejparte tiuj naskitaj post la milito multe penadis por altigi la vivnivelon de la tuta lando. Al siaj infanoj ili volis doni pli bonan, pacan kaj ne tro penigan estontecon. Tion ĝenerale ja ĉiuj gepatroj ĉie ĉiam alstrebas. Ĉu la rekompenco estas nur akuzoj, amareco kaj la nevolemo dece zorgi pri la kadukuloj kaj maljuniĝintoj?</p> <p>Konstante aperas en la gazetaro artikoloj pri la bedaŭrinda stato de maljunulejoj kaj pri la nesufiĉa nombro de iliaj vartistoj kaj flegistoj; simile pri pensiuloj, kiuj vivas solaj kaj izolitaj en siaj apartamentoj kaj domoj. Sed antaŭe finnoj kreskis en kuniga ni-animeco gvidate de tradicio luterana. Ili alproprigis al si valorojn, kiaj estas religiemo, solidareco, deco kaj interhelpado.</p> <h3>Postmortaĵoj</h3> <p>Tiam la edukado estis unueciga, sed hodiaŭaj adoleskantoj sentas sin ne aranoj, sed individuoj. Ilin ne plu interesas predikoj pri postmortaĵoj: ili pli maltrankvilas pro la stato de la nuna medio kaj la naturo kaj pro la fakto, ke multajn gravaĵojn oni decidas ne en Finnlando, sed ie ekstere.</p> <p>Ili ne scias, ĉe kies pordo indus proteste manifestacii. De la eklezio ili atendas pli socian mesaĝon, ankaŭ konsideron pri la medio, ne disputojn pri la rajtoj de virinoj pastoriĝi aŭ de samseksemuloj kuniĝi. Pli kaj pli da homoj eksiĝas de la eklezio, opiniante, ke fido kaj savo ne dependas de tradiciaj institucioj – tial ili esploras aliajn kredojn.</p> <p>Tamen la merkatekonomio ne lasos ilin liberiĝi de siaj katenoj forĝitaj el reklamado, konsumado, dungado kaj timo de senlaboreco.</p> <div style="text-align:right">Raita P<span class="mm">YHÄLÄ</span></div> <div style="text-align:right"><i>Finnlando</i></div> 
