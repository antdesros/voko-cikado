      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Rifuĝintoj pro domperdo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Hieraŭ mi promenis en mia najbaraĵo. Tie, kie estis antaŭe rizkampoj, staras nova loĝkvartalo kun multaj domoj. En unu el la domoj mi trovis en la fenestro anoncon pri ties vendo. Tiu ĉi domo estis konstruita antaŭ eble du jaroj kaj estis loĝata de juna familio. Tiu, tamen, devis forlasi sian hejmon, supozeble ne plu povante repagi al la banko pruntitan monon.</p> <p>En tia okazo unue la banko provas vendi ĝin. Kiam ĝi ne vendiĝas, la banko almetas la domon al aŭkcio tra tribunalo. La nombro de domoj ĉe aŭkcioj ege multiĝis ekde la lasta somero. En marto 2008 la nombro estis 3773, sed en julio 2009, 7229. Pro la malbona ekonomio multaj ne plu povas repagi la monon pruntitan por aĉeti domon.</p> <h3>Fabriko</h3> <p>La ĵurnalo <i>Mainiĉi</i> raportis en aŭgusto pri diversaj tiaj okazoj. Ekzemple Ŝimada Kooĵi (42-jara, falsa nomo por kaŝi la identecon de la familio), kiu laboris en fabriko pri elektraj aparatoj, aĉetis domon antaŭ 16 jaroj kontraŭ 48 milionoj da enoj (400 000 eŭroj).</p> <p>Li planis repagi monate 160 000 enojn kaj somere kaj vintre, pro specialaj kromsalajroj, 450 000 enojn. Ĝis nun li ĉiam repagis, sed en la lasta aŭtuno la situacio ĉe la fabriko malboniĝis, kaj lia salajro estas tranĉita je 30 %. Cetere li multe ŝuldis pro la edukado de siaj gefiloj. Nun li timas, ke, se li ne ricevos la vintran krompagon, li devos forlasi sian hejmon.</p> <h3>Apartamento</h3> <p>Laŭ la Asocio de Nemovebla Propraĵo, multiĝas rifuĝintoj pro domperdo. Eĉ se homoj volas vendi sian domon, tio malfacilas en la nuna ekonomia situacio. Tial, ekzemple, Kizuki Tooru (50-jara, denove falsa nomo) perdis sian apartamenton, aĉetitan 18 jarojn antaŭe. Tamen restas ŝuldo, kaj li deklaris sin bankrota. Liaj familianoj forlasis lin, kaj li loĝas sola en malgranda apartamento, vane serĉante laboron.</p> <p>Ĝis la 1990aj jaroj multaj homoj laboris en la sama kompanio ĝis emeritiĝo. Enspezitaj monoj daŭre multiĝis laŭ tiel nomata sistemo de laŭlaborjara salajro. Multaj, tamen, ne antaŭvidis la ŝanĝojn en la socio kaj en la ekonomio. Savi homojn tiajn estas la tasko de la politiko. Helpo tamen ne venas, aŭ tre malrapide, kaj pli kaj pli videblas rifuĝintoj pro domperdo en parkoj de la grandaj urboj.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> <div style="text-align:right"><i>Japanio</i></div>  
