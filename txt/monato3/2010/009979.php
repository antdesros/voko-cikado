      <h2>La ekologiaj nutraĵoj de AMAP</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum la sesdekaj jaroj en Japanio patrinoj timis pri la efikoj de altrendimenta kultivado kaj malsanigaj manĝaĵoj por la infanoj. Ili kontaktis unu agrokulturiston kaj aĉetis lian sanan produktaĵon, antaŭ ol li produktis ĝin. Tiel ekestis <i>Teïkeï</i> (meti la vizaĝon de kamparano sur manĝaĵon); ĝi estis la praulo de la franca AMAP (<i>Aide au Maintien d'une Agriculture Paysanne</i>, „Asocio por helpo al konservado de kamparana agrikulturo”). De 2001 ekzistas AMAP-asocio en suda Francio, apud Tulono (danke al la familio Vuillon); nur en Provenco estas nun 120 „AMAP'oj” kun pli ol 20 000 profitantaj familioj.</p> <h3>Kiel funkcias?</h3> <p>Unu AMAP naskiĝas pro la renkontiĝo inter grupo de konsumantoj kaj unu agrokulturisto. La partoprenantoj serĉas sanan manĝaĵon, tiun, kiu kreskas respektante homon kaj naturon. Pro tio ili anticipe aĉetas la produkton de agrokulturisto. Povas esti fruktoj, legomoj, ovoj, viandaĵo. Konsumanto kaj produktanto kontraktas por unu aŭ du sezonoj (printempo/somero, aŭ aŭtuno/vintro). Interŝanĝe la produktisto sin devigas kreskigi siajn legomojn kaj fruktojn sen kemiaĵo, ekologie, kaj prepari semajne (unufoje aŭ pli, dependas de la grandeco de la kampoj) unu korbon por ĉiuj AMAP-anoj. La enhavo de la korbo dependas de la rikolto, sed ĝi devas nutri kvar personojn dum unu semajno. Principe la tuta produktaĵo estas konsumata, senkonsidere de laŭnorma aŭ nelaŭnorma aspekto.</p> <p>La distribuado okazas ĉe la agrokulturisto mem aŭ en proksima loko. La prezo de la korbo similas la prezon en superbazaro kaj monproblemo ne malebligu partoprenon (ekzistas diversaj ebloj por pagi).</p> <h3>Kiuj estas la bonaĵoj?</h3> <p>Ĉar agrokulturistoj havas garantiatan enspezon kaj plibonigitan statuson, ili ne plu ŝatas volonte vendi siajn kampojn al kultivejoj aŭ konstruigistoj. Pro tio AMAP malrapidigas la malaperon de kultivkampoj kaj produktistoj.</p> <p>Ĉar produktanto kaj konsumanto dividas inter si la riskon, ambaŭ pli bone konservas nocion pri sezonoj.</p> <p>Ĉar la produktokampo estas proksima, la manĝaĵoj estas freŝaj kaj plej sanaj; ne necesas multe da transporto, do okazas malpli da poluo.</p> <p>Ĉar la agrokulturisto ne uzas kemiaĵojn, kultivejoj iĝas pli fekundaj, manĝaĵoj estas bonkvalitaj.</p> <p>Ĉar AMAP estas renkontiĝejo, ĝi rekreas homajn ligojn, ĝi rekonektas urbanojn kaj kamparanojn.</p> <p>Nun estas AMAP-asocioj tra la mondo, en Japanio <i>Teïkeï</i>, en Usono <i>CSA (Community Supported Agriculture)</i>, en Kanado „Farmisto de la Familio”, Svisio <i>ACP (Agriculture Contractuelle de Proximité)</i>. Eble jam ekzistas unu proksime de vi, se ne, nenio malebligas krei ĝin.</p> <div style="text-align:right">Marie-Ho</div> 
