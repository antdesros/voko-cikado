      <h3>Ekonomio</h3>
      <h3 class="tem">FERVOJOJ</h3>
      <h2>Litovio relas al Eŭropo</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Meze de oktobro 2015 oficiale estis inaŭgurita parto de la transeŭropa fervoja linio
      
      <i>Rail Baltica</i> inter la pola ĉelandlima urbo Suwałki kaj la litova Kaunas. La konstrulaboroj komenciĝis
      en 2013 kaj kostis entute 364,5 milionojn da eŭroj, 8,6 milionojn malpli ol estis
      planitaj.
   </b>
</p> 
<p>Tra 120 km estis konstruita nova eŭropnorma trako kaj apud ĝi estis renovigita la
   ekzistanta rusnorma trako. En la tuta linio estis uzitaj 233 km da novaj reloj. La
   eŭropnorma fervojlinio 
   <i>Rail Baltica</i> kun distanco de 1065 km kunligos la baltajn landojn kaj Finnlandon kun la aliaj landoj
   de Eŭropa Unio (EU). Preskaŭ ĉiuj EU-landoj uzas 1435 mm larĝan ŝpuron, dum en Litovio
   ĝis nun oni uzis rusdimensian (1520 mm) ŝpuron. Tiu diferenco malpermesis al baltaj
   landoj aliĝi al la fervoja reto de EU.
</p> 
<p>Dum la solena inaŭguro, kiu okazis en la fervoja stacidomo de Kaunas, altpostenuloj
   de la Eŭropa Komisiono kaj de Litovio rimarkis, ke 
   <i>Rail Baltica</i> estas vere grava projekto, kiu ebligos al ĉiuj baltaj landoj integriĝi al la komuna
   merkato de EU.
</p> 
<p>Malgraŭ tio, ke la laboroj en Litovio estas finitaj, gravaj obstakloj restas trans
   la limo en Pollando. Ĉi-momente ne estas klare, kiom longe povos daŭri vojaĝo inter
   Kaunas kaj la pola urbo Białystok, nek kiam komenciĝos regula pasaĝera ligo. La konstrulaboroj
   en Pollando ankoraŭ ne estas finitaj. Trajnoj inter Suwałki kaj Białystok en iuj lokoj
   povas veturi nur je rapideco de 30 km/h anstataŭ la meza rekomendata de 160 km/h por
   pasaĝeraj trajnoj. Tio signifas, ke glata fervoja kunligo inter la du najbaraj landoj
   ankoraŭ estas problema.
</p> 
<p>Algirdas Butkevičius, la ĉefministro de Litovio, esprimis la esperon, ke ankaŭ la
   polaj partneroj celos al moderna fervojlinio inter Varsovio kaj la estona ĉefurbo
   Talino. „Por ni estas tre grave, kune kun la partneroj en la regiono krei novan loĝistikan
   ĉenon, direktitan al kvalitaj servoj de pasaĝera kunligo kaj vartransportado inter
   la baltaj landoj, Pollando kaj ceteraj EU-anoj. Ĉiu kilometro de 
   <i>Rail Baltica</i> proksimigos la baltan regionon al Eŭropo kaj EU-civitanoj ĉi tie estos pli kaj pli
   oftaj gastoj, kaj ankaŭ kreskos la amplekso de la vartransportado”, diris Butkevičius.
</p>  
<p>En la sekva etapo la konstrulaboroj ekde Kaunas devus moviĝi al Latvio, kie tra la
   latva ĉefurbo Rigo ili celos al Talino. La longeco de 
   <i>Rail Baltica</i> en Litovio estas 366 km: ekde la pola ŝtatlimo ĝis Kaunas 120 km kaj de Kaunas ĝis
   la latva ŝtatlimo 246 km.
</p> 
<p>
   <i>Rail Baltica</i> estas parto de la internacia transporta koridoro 
   <i>North Sea-Baltic</i>, kiu kunligos la landojn de Benelukso, Germanion, Pollandon kaj Litovion, kaj sekve
   Latvion, Estonion kaj Finnlandon.
</p> 
