      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Frandaĵo por kleruloj</h2>
      <!-- komenco de la enhavo -->

       

     <p>Antaŭ deko da jaroj, en 1998, aperis tre promesplena poemaro, <i>Sur Parnaso</i>, kaj la antaŭparolo de William Auld klasis ĝian aŭtoron kiel poeton ĝisostan kaj vere dediĉitan al sia arto. Temis tiam pri ses poemcikloj, plejparte el sonetoj, tamen kun ne malgrava parto el rondeloj; la temoj estis pri socio, kontraŭ la milito, pri la esperantista medio, pri amatino. Surprizis tiam la kapablo ĵongli per la vortoj, la ne kunigo, tamen apudigo de bildoj kaj vortradikoj, la peno trudata al la leganto kompreni la delikatecon de iuj bildoj, la sinsekvon de emocioj, la aluditajn eventojn, spegulantajn ofte nur epizodojn de kroniko, ne ja mondskuajn faktojn. Ne temis tamen tiam pri absoluta debuto; la aŭtoro, Timothy Brian Carr, angla bibliotekisto, ĉeestis jam, eĉ se lastapaĝe, en la dua eldono de <i>Esperanta antologio</i>, kiu registras poemojn ĝis 1981: li do aperis tie kiel lasta alveninto (fakte nur en 1981 li aperas en la poezia kampo), per unu sola kontribuo. Kiu tamen atentis tiun kontribuon, povis facile prognozi, ke tiu poeto havas multon dirindan. Liaj tiamaj versoj spegulis samreflekte kaj la denson de Miĥalski kaj la muzikon de Hohlov.</p> 
<p>La kariero de Carr pluis diversrevue: premiita en Belartaj Konkursoj kaj en la Floraj Ludoj ekde 1985, konstanta kunlaboranto de <i>Fonto</i> kaj de <i>Literatura foiro</i>, al li estis dediĉitaj pluraj recenzoj, kaj li estis menciita en eseoj pri la lastatempaj atingoj de la esperanta poezio. La rigoro de lia versado igis lin gajni la nomon de <i>novparnasisto</i> kune kun la poetino Krys Ungar, dek jarojn pli juna ol Carr naskiĝdate kaj duonon de tiom poezidebute.</p> <h3>237 sonetoj</h3>
 <p>Kiuj estas do la karakterizoj de ĉi tiu nova versvolumo, venanta, kiam la poeto jam preterpasis la sesdek-kvinan jaron? Nu, tutunue temas nur pri sonetoj: ducent tridek sep (!) sonetoj. Eĉ en naciaj literaturoj malfacile troveblas tia kvanto da fiksformaj poeziaĵoj ĉe unu sola poeto. Due: dum la unua kolekto estis dividita laŭ cikloj, la nuna dividojn ne havas, kaj la poeziaĵoj estas apudigitaj preskaŭ sen videbla kriterio: nek de temo nek de tempo. Surprizas ankaŭ, ke pluraj sonetoj venas de tempo antaŭ la apero de la unua poemaro.</p> 
<p>La perfekteco de la ritmo, de la versado, de la rimoj, de la akcentoj iĝis preskaŭ obsede rigora: neniu elizio, la vortoj fluas facile kiel pura akvo; elizioj ja mankas, sed kunmetitaj vortoj eĉ tute nekutimaj abundas. Sed por ĝui tiom da estetikaj kvalitoj la leganto devas sidi komforte, havi ĉe si jarkolektojn de sportaj gazetoj, vortarojn anglan, francan, latinan kaj italan kaj de la lingvoj (iam) parolataj en regionoj de Britio kaj Francio, koni la latinan lingvon aŭ almenaŭ ofte ripetitajn latinajn frazojn, koni Iliadon, Odiseadon, Eneidon, Danton, Ŝekspiron, la anglan literaturon, francajn aŭtorojn ktp. Nu, eble tion oni povus postuli de tre klera okcidentano, kiu studis en klasika liceo; kaj tiel kompreni, ke <i>timeo Danaos et dona ferentes</i> estas „mi timas helenojn, eĉ kiam ili alportas donacojn” (tradukoj de tiuj „dediĉaj” frazoj apenaŭ estas).</p> <h3>Dolĉversaj puzloj</h3> 
<p>Alia defio estas la esperantigo de la propraj nomoj, kio ne estas tute kutima en nia lingvo: do leganto devas diveni, ke Joĉjo Halideo estas Johnny Hallyday, mondfama rokmuzikisto; ke Ĵandarka estas Jeanne d'Arc, Kopoidoj estas la idoj de Kopa, franca futbalisto fama antaŭ kelkdek jaroj; kaj ke Ĉeniero estas Andreo Chenier ktp. Kaj krome nepras scii pri filmoj kaj aktoroj, pri muzikistaj bandoj, pri sportaj, precipe futbalaj matĉoj, gajnitaj aŭ malgajnitaj de la francaj aŭ britaj naciaj teamoj dum sia historio; nepras koni la tiamajn ludantojn, kaj je kiuj respektvaj poentoj finiĝis la matĉoj ktp. Se leganto tion ne konas, li troviĝas antaŭ tre dolĉversaj puzloj, tre belsonaj vortoj, tre graciaj kaj tute malbanalaj rimoj, sed laŭsence ofte nedeĉifreblaj poemoj, ĉar la aludoj estas obskuraj. Li trovos puran formon, plej altan estetikon, komplezan al si mem: Certe Carr perceptas tian poezipurecon kaj fieras (prave) pri ĝi, sed oni havas la impreson, ke li verkas nur por si kaj por elita aro da altkulturaj konsumantoj. Do prefere oni lasu sin luli per la muziko de la versoj ol klopodi kompreni la politikan aŭ sportan aŭ teatran eventon, kiun tiu versmelodio aludas. Paĝon post paĝo la leganto renkontiĝos kun la itala opero kaj kun la filmoj de Laurence Olivier, kaj kun multo alia. Yvonne estas virino, kiu aperas, sub la nomo Ivona, en la dua duono de la libro. La amo estas pli intuiciata ol rakontata, sed tiu estas la plej bela parto de la sonetaro.</p> 
<p>Konscie pri la fakto, ke multegaj aludoj estas nekompreneblaj al la ĝenerala publiko, Carr enmetis kelkajn klarigajn notojn, sed vere ili apenaŭas. Jen unu lia soneto, titolita <i>Elizabetano</i>, kiu havas franclingvan unulinian enkondukon el Niderst pri Racine:<br/> <br/> Ombrante Ilirion per komploto <br/> li persektas sobran Malvolion. <br/> Ŝajlokon reduktinte al bankroto, <br/> li trudas al li sian religion. <br/> (Cirkestro de pajaco sur golgoto <br/> se bistro ŝanĝas viron en bestion.) <br/> Per peĉo de malica patrioto <br/> li skribas pri Ĵandarka kalumnion. <br/> „Ree, amikoj! Ree en la breĉon!” <br/> Ĉu li komandus futbalhuliganojn? <br/> Anglio plu eksportas sian feĉon, <br/> li apartigas por la loka bando <br/> francojn, nigrulon, judon, puritanojn. <br/> Venkinta duko mankas ĉe normando.</p> 
<p>En aliaj sonetoj la ritmo vin trenas impulse: preskaŭ sen spirpermesa interpunkcio, tiel ke vi ensorbas tro rapide la versojn, kaj devas relegi. Ĝuu la ritmon kaj la fluon malrapide; ĝi indas vian halton kaj atenton.</p> <div style="text-align:right">Carlo M<span class="mm">INNAJA</span></div> <h6 class="bib">Timothy Brian Carr: Yvonne. Rotterdam, Bero, 2009, 252 paĝoj broŝuritaj.</h6> 

