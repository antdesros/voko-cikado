      <h3>Hobio</h3>
      <h3 class="tem">LUDKARTOJ</h3>
      <h2>Trufludo</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Ŝatantoj de kartludoj tutmonde ne mankas. Forestas eble iom da varieco. La plimulto
      de ili fakte emas dediĉi sian libertempon al 4 aŭ 5 ludoj, ĉiam la samaj. Jen do,
      ekskluzive por 
      <i>Monato</i>, originala kartludo: trufo aŭ, se vi preferas, trufludo. Ĝi tute ne postulas aritmetikajn
      konojn kaj pro tio vere taŭgas por la tuta familio, inkluzive junegajn idojn.
   </b>
</p> 
<p>La origino de la trufludo estas simple klarigebla: la kreinto (verkinto de tiu ĉi
   artikolo) estis ludanta kartojn kun sia juna filo (ĝenerale amanto de tablaj kaj kolektivaj
   ludoj) dum varmega – kaj iom teda – somera posttagmezo. La etulo, kiu ankoraŭ ne komencis la bazlernejon, malfacile komprenis
   la subtilajn regulojn de kutimaj kartludoj (precipe rilate la eblon adicii) kaj krom
   tio iom enuis pro la ripetemo de proponitaj ludoj, kiuj escepte de kelkaj detaloj
   tre similas. Eble ĉerpinte (sed senvole!) inspiron el la intuicia kartludo 
   <i>Uno</i>, kiun, tutmonde disvastiĝintan, ĉiuj infanoj pli-malpli ŝatas kaj komprenas, ni kune
   naskis la jenan ideon.
</p> 
<h3>Komenco</h3> 
<p>Oni uzu francan (52) aŭ italan (40) kartaron. Kun iom da fantazio, ja adapteblas al
   trufludo ankaŭ eventualaj aliaj naciaj kartaroj. Rajtas ludi 2 personoj ĉiufoje kaj
   oni povas facile organizi etan turniron: Ekzemple, patro ludos kun filino, patrino
   kun filo kaj la du gajnontoj poste ludos la finalon. Komence de la partio, zorge miksinte
   la kartaron, unu el la ludantoj disdonu po 3 kartojn al la kunludanto kaj aldone metu
   malkaŝe 4 kartojn sur la tablon.
</p> 
<h3>Emblemo</h3> 
<p>
   <i>Regulo 1.</i> En tiu ludo neniom valoras nombroj (2, 5, 8, 10...), do oni rajtas per propra karto
   preni alian de sur la tablo, nur se ambaŭ havas la saman emblemon, do sendepende de
   la presita numero. Pro la sama kialo, kompreneble malpermesita estas ankaŭ adicio:
   do ĉiufoje, kiam ludanto gajnas karton, li neeviteble aldonos al sia propra gajnkartaro
   du kartojn, nenial tri aŭ kvar kiel en aliaj ludoj. Ekzemple, se ni havas ason de
   pikoj, ni ne rajtas preni ason aliembleman (ekzemple de keroj), sed ja ajnan karton
   (3, 6, 9, eventuale reĝon, fanton...) de pikoj. Se ni havas reĝon de karooj, ni rajtas
   preni ajnan karton de karooj (ason, 2, 4, 8...), sed neniam aliembleman reĝon. Se
   neniu karto estas surtable samemblema, ni simple ĵetos nian karton surtablen, apud
   la ceterajn, gajnante nenion. Ĉiu gajnita karto nomiĝas „trufo”, kaj oni konservu ĝin en sia kaŝata „trufkesto” (t.e. la aro de ĝis tiam gajnitaj kartoj). Kiam la ludantoj estos ĵetintaj siajn
   tri kartojn, la miksinto ree disdonos po tri kartojn kaj tio okazos ĝis la fino de
   la partio.
</p> 
<h3>Trufego</h3> 
<p>
   <i>Regulo 2.</i> Kiam ludanto sukcesas per samemblema karto (ne laŭ la kartnumero!) gajni la lastan
   karton, troviĝantan sur la tablo, tiam la gajnita karto nomiĝas ne plu „trufo”, sed „trufego”.
</p> 
<p>Ekkriante tiun vorton por krei pli amuzan etoson, certe plaĉontan al infanoj, oni
   aldonas la trufegon (do la gajnitan karton) al sia gajnkartaro malkaŝe. Tiel oni memoros,
   fine de la partio, ke tiu karto havas duoblan valoron. Ekzemple, se fine de la partio
   ni havas 22 kartojn (trufojn) kaj 3 trufegojn, ni kalkulas 22 + 6, do 28 kartojn.
   Tiel simple!
</p> 
<h3>Taktiko</h3> 
<p>Tio signifas, ke la ceteraj ludantoj devas havi la taktikon, se tio eblas, estigi
   ĉiam pli ol unu karton sur la tablo, ĉar kelkfoje gajni antaŭlastan karton estas riske
   kaj kontraŭefike. Tio povus gajnigi al la kunludanto altvaloran trufegon. Spite al
   tiu defenda taktiko, kelkfoje tamen estos neeviteble lasi ununuran karton surtable,
   kaj oni devas nur esperi, ke la kunludanto ne havos karton samembleman.
</p> 
<h3>Inversiĝo</h3> 
<p>
   <i>Regulo 3.</i> Tiu, kiu gajnas trufegon, sendepende de la komenca vico, ĵetos ja surtablen novan
   karton kaj la luda vico nepre inversiĝas (tiel ke ankaŭ la ceteraj ludantoj havos
   la eblon gajni trufegojn: aliokaze laŭ mia sperto tiu, kiu gajnos la unuan trufegon,
   emos gajni ankaŭ la sekvajn).
</p> 
<h3>Nombrado</h3> 
<p>Fine de la partio, la ludantoj nombras la gajnitajn kartojn (trufojn), kalkulante
   ĉiun trufegon kiel duoblan karton. La reguloj ŝajnas unuavide iom komplikaj, sed la
   ludo estas simplega kaj oni vidos, ke ĉio estas tre intuicia kaj ke post unu aŭ du
   partioj la regularo estos ĉiam pli klara. Mi provis la trufludon kun mia fileto kaj
   li tuj komprenis la mekanismojn.
</p> 
<p>Li opinias la ludon treege facila kaj amuza. La trufludo eĉ fariĝis lia preferata
   kartludo. Estas tre facile por infanoj rekoni la saman emblemon. Kaj ĉefe la momento,
   kiam oni ekkrias la vorton „trufego”, estas tre gajiga.
</p> 
<p>Ĉiuj ludantoj gajnas kutime ne malpli ol unu trufegon, kaj tio minimumigas la eventualan
   frustracion pri perdo. Mi ĉiam diras al miaj filo kaj nevo (fojfoje ploranta pro iu
   malgajno), ke post unu jaro aŭ du oni ne memoros tiun, kiu gajnis la pasintjaran partion,
   sed nur la fakton, ke oni iam kunludis kaj sin amuzis familie! Provu proponi ĉi tiun
   kartludon al viaj parencoj kaj amikoj. Vin atendas drolaj partioj, kiujn povus partopreni
   ankaŭ viaj gebubetoj!
</p> 
<p>La lasta kuriozaĵo, eble utila por estonta luda enciklopedio aŭ muzeo de Esperanto-strangaĵoj:
   La nomo de la kartludo (trufo aŭ trufludo) dependas de tio, ke la edzino de la kreinto
   en la tago, kiam li elpensis la ludon, tute hazarde preparis gustoplenajn trufĉokoladetojn.
   Do la verkinto kun sia filo provis unuafoje la ludon, gustumante tiujn dolĉaĵojn!
</p> 
