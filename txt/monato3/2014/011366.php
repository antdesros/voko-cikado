      <h3>Turismo</h3>
      <h3 class="tem">OCEANIO</h3>
      <h2>Ĉarma insulo en Pacifiko</h2>
      <!-- komenco de la enhavo -->

       

       <p>Nov-Kaledonio estas insulo, kiu situas en la suda Pacifiko 1700 km for de Sidnejo
   (Aŭstralio) kaj Aŭklando (Nov-Zelando) kaj 17 000 km for de Parizo. Ĝi estas franca
   teritorio kun vasta aŭtonomio, nomata „Transmara lando”. La landon indas viziti pro la granda bela rifo kaj ĉarma naturo. Aviadilkompanioj
   plurfoje semajne alflugas ĝin.
</p> 
<p>La insulo, en la formo de cigaro kvarcent kilometrojn longa kaj kvindek-sesdek kilometrojn
   larĝa, estas ĉirkaŭita de grandega laguno kaj bela koralrifo (unu el la plej grandaj
   kaj belaj en la mondo). Al Nov-Kaledonio apartenas pliaj kvar insuloj pli malgrandaj
   (unu 50 km longa sude kaj tri aliaj situantaj 100 km oriente de la ĉefa insulo), kaj
   ankaŭ pluraj insuletoj.
</p> 
<p>La koralrifo laŭ grandeco estas la dua en la mondo post tiu de Aŭstralio. Diversaj
   ekskursoj kondukas al la rifo, dum kiuj eblas enakviĝi por sperti la riĉecon kaj belecon
   de la subakva pejzaĝo (diversformaj koloraj fiŝoj kaj belaj koraloj). En la ĉefurbo
   Numeo oni nepre vizitu la akvarion, kiu unua en la mondo prezentas fosforeskajn koralojn.
</p> 
<p>La naturo de Nov-Kaledonio proponas ravajn kamparajn pejzaĝojn, vidindajn praarbarojn,
   akvofalojn (ĉefe sur la orienta marbordo, kie la montaro preskaŭ enfalas la lagunon);
   verdajn valojn kun riĉa vegetaĵaro; grandajn ebenaĵojn sur la okcidenta flanko, kie
   oni bredas neintense bovojn; koralajn insulojn Lifou, Mare, Ouvea, en kiuj nur kanakoj
   estas bienuloj.
</p> 
<h3>Mozaiko de etnoj</h3> 
<p>La loĝantaro de Nov-Kaledonio estas multetna: lokaj kanakoj kaj alidevenaj homoj:
   eŭropanoj, vjetnamoj, ĉinoj, japanoj, indonezianoj, polinezianoj, antilanoj, reunianoj
   kaj mestizoj.
</p> 
<p>Tiun „mikspoton” oni rimarkas, promenante tra la ĉefurbo Numeo mem kaj vizitante muzeojn, el kiuj
   elstaras la granda muzeo pri kanaka kulturo, la belega Centro „Jean Marie Tjibaou”. Tra la lando oni povas vojaĝi per aŭto aŭ per aviadilo de la landa kompanio 
   <i>Aircal</i>. Turistoj restadas en hoteloj aŭ en tribaj loĝejoj, kie eblas ekkoni la kuirarton
   de la diversaj etnoj, gustumante iliajn manĝaĵojn.
</p> 
<p>La malkovro de pluraj metaloj, ĉefe de nikelo (Nov-Kaledonio estas la trie plej granda
   produktanto de la mondo), alvenigis plurajn homojn el Eŭropo sed ankaŭ el Azio, ĉefe
   el Vjetnamio, Indonezio, Japanio, kaj krome el Polinezio.
</p> 
<p>La produktado de nikelo estas la ĉefa branĉo de la novkaledona ekonomio, kun tri uzinoj:
   en la ĉefurbo, en la sudo kaj en la norda provinco. Alia branĉo de la ekonomio estas
   agrikulturo – ĉefe neintensa bovbredado en grandaj areoj sur la okcidenta flanko de la insulo.
   Tie la surĉevale laborantaj vakeroj adoptis la nomon 
   <i>stockmen</i>, uzatan en la najbara Aŭstralio. De tempo al tempo ili kunpelas la brutaron por traktado
   kontraŭ iksodoj – per banado aŭ aspergado. Ankaŭ fiŝkaptado kaj turismo estas branĉoj de la novkaledona
   ekonomio.
</p> 
<p>Laŭ la lasta popolnombrado la lando havas 260 000 loĝantojn (el kiuj proksimume 100
   000 estas en la ĉefurbo). La plimulto de ili estas francaj civitanoj. 40,34 % de la
   loĝantaro deklaris sin kanakoj, 29,20 % eŭropanoj, 8,30 % mestizoj, 4,96 % novkaledonanoj,
   3,34 % posteuloj de azianoj, 10,69 % polinezianoj, kaj la restantaj preskaŭ 7 % ne
   indikis naciecon. Laŭaĝe plimultas la grupo de la 15-64-jaraj. La naskokvanto estas
   16,05/1000 loĝantoj, kaj la mortokvanto – 5,34/1000 loĝantoj.
</p> 
<h3>Historio</h3> 
<p>Fine de la 18a jarcento pluraj eŭropaj marveturantoj ekvidis la insulon kaj iris sur
   ĝian bordon. La brita kapitano James Cook nomis ĝin Nov-Kaledonio pro ĝia simileco
   al lia patrolando Skotlando. Tiu insulo estis loĝata de kanakoj, parolantaj proksimume
   30 lingvojn, vivantaj en homgrupoj ofte batalantaj inter si; lertaj agrikulturistoj,
   tre korligitaj al sia tero.
</p> 
<p>Nov-Kaledonion oni oficiale deklaris franca kolonio en 1853. Fine de la 19a jarcento
   ĝi estis punlaborejo por kondamnitoj pro politikaj kialoj (ĉefe post la Pariza Komunumo
   en 1871), por la afrikanoj (kiuj ribelis dum la konkero de iliaj landoj en norda Afriko
   fare de francoj), kaj ankaŭ por krimuloj.
</p> 
<p>Pluraj kanakaj ribeloj okazis ekde la unuaj jaroj de koloniado ĝis 1917 – ĉefe pro terforpreno fare de koloniantoj kaj sekve de devigaj transloĝiĝoj.</p> 
<p>Nun Nov-Kaledonio estas dividita je tri provincoj, kiuj estas vaste aŭtonomiaj. La
   registaro kompetentas pri pluraj temoj, aparte pri financoj (helpate de Francio) kaj
   la fiska leĝaro. En la venontaj jaroj okazos voĉdonoj pri la estonta politika statuso
   de la lando.
</p> 
