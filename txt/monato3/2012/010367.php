      <h3>Spirita vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Organdonanto jes, organdonanto ne</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la lastaj monatoj oni multe pridiskutis la eblon, ke la nuna papo (Benedikto la 16a, sekulare Joseph Ratzinger) je sia morto donacos siajn organojn. La debato ekiĝis pro kelkaj deklaroj liaj, tre favoraj al tia agado, kiam en 1999 li estis ankoraŭ simpla kardinalo de la romkatolika eklezio. Estas sufiĉe konate aliflanke, ke li anas delonge en asocio de organdonantoj.</p> <h3>Relikvo</h3> <p>Pro tio tamen, ke Ratzinger estis elektita ĉefpontifiko en 2005, lia deziro ne povos realiĝi. Lia sekretario, Georg Gaenswein [gensvajn], klarigis februare al la itala ĵurnalo <i>La Repubblica</i>, ke papo neniuokaze rajtas donaci organojn: la korpo de pontifiko, sendepende de ties sankteco (kiu ne estas aŭtomata, kiel montras tio, ke la eklezio kanonizis nur 86 el la ĝisnunaj 265 papoj), estu konsiderata kaj adorata kiel relikvo: ĝi apartenas al la tuta kristanaro kaj tial ĝi devas resti integra.</p> <h3>Eterneco</h3> <p>Kontraŭ unu perdiĝinta organdonanto, ekzistas tamen milionoj da potencialaj organdonantoj kiuj, kvankam povante libere konsenti la elplanton de siaj organoj, ankoraŭ ne estas certaj pri la boneco kaj neceso de tia konduto. Disponigi la proprajn organojn por savi aliulajn vivojn estas nobla, sendolora kaj senpaga donaco, tute akordigebla – kiel asertis la papo mem – kun la kristana doktrino. Ĝi ebligas tanĝi, aŭ almenaŭ alproksimiĝi iomete, la eternecon kaj la senmortecon, al kiuj la homaro ĉiam sopiris.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
