      <h3>Lingvo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Ĉu dulingvismo (en Esperantujo) eblas?</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la esperantista medio, al kiu apartenas multaj legantoj de tiu ĉi revuo, oni ofte
   aŭdas, ke onia celo estas, ke ĉiu homo estu kapabla paroli sian gepatran lingvon kaj
   Esperanton, do ke ĉiu homo estu fakte dulingva. Observante la ekzistantajn dulingvismojn
   (kaj la popolajn kaj la profesiajn), ni tamen konstatas, ke dulingvismo postulas regulan
   uzon de ambaŭ lingvoj, por ke ne perdiĝu la scipovo; ŝajnas, ke mankas io, en la okazo
   de Esperanto, por ke amaso da homoj povu regule uzi tiun internacian lingvon, alivorte
   por ke ĝi estu uzata ne nur de pioniroj, sed ankaŭ de konsumantoj.
</p> 
<h3>Popola dulingvismo</h3> 
<p>Ĝi funkcias ĝenerale kiel transiĝo inter malaperanta lingvo kaj konkeranta lingvo.
   Mi skribas el Francio, kaj ekzemple en mia regiono, kie la okcitanaj dialektoj estas
   malaperantaj, oni ja povas observi dulingvismon. Miaj geavoj parolis preskaŭ nur sian
   okcitanan dialekton, sed kapablis komuniki franclingve kun la pastro, la kuracisto,
   la instruisto ktp; ili povis ankaŭ legi franclingvan gazeton. Miaj gepatroj kaj mi
   regas egalnivele la okcitanan kaj la francan. Miaj infanoj parolas nur la francan.
   La enmigrintaj familioj spertas ion similan. Tiu dulingvismo daŭris, ĉar la homoj
   regule uzis ambaŭ lingvojn: en la familio kaj kun la najbaroj la kampuloj parolis
   nur okcitane, sed ili regule renkontis la francan oficialan lingvon. Dimanĉe la pastro
   predikis franclingve, kaj en la franca estis redaktita ankaŭ la semajna regiona gazeto,
   kiu informis pri la mondo, pri Francio kaj pri la najbaraj vilaĝoj. Tio daŭrigis la
   scipovon de la franca lingvo lernita en la lernejo dum la infana aĝo. Tiel, kiam la
   kampuloj bezonis uzi la francan lingvon por paroli kun la kuracisto, kun la instruisto,
   kun iu ŝtatfunkciulo, ekzemple la impostokolektanto, ili ja kapablis tion fari. Dulingvismo
   funkciis dum du aŭ tri generacioj, do dum malpli ol unu jarcento, inter la ĝenerala
   uzo de la okcitana dialekto kaj la ĝenerala uzo de la oficiala franca lingvo. Tiu
   transiĝa periodo estis pli longa, kaj daŭris proksimume tri jarcentojn, por kelkaj
   profesioj, ekzemple notarioj kaj pastroj. La arkivoj montras, ke ili komencis skribi
   aktojn en la franca ĉirkaŭ la jaro 1600, tamen ili plu kapablis paroli okcitane kun
   siaj klientoj ĝis la komenco de la 20a jarcento. Sed tio estis speco de profesia dulingvismo.
</p> 
<h3>Profesia dulingvismo</h3> 
<p>En kelkaj profesioj oni daŭre bezonas paroli plurajn lingvojn, ĉar oni rilatas kun
   homoj el diversaj landoj; tio inkluzivas la sciencistojn, la internaciajn komercistojn,
   esprimo kiu siavice inkluzivas la profesiulojn de turismo, la diplomatojn ktp. Tiuj
   homoj ofte bezonas paroli kun fremduloj; ili parolas plurajn lingvojn, minimume sian
   gepatran kaj la anglan, kiu rolas fakte kiel internacia lingvo. Tiu dulingvismo koncernanta
   la nacian kaj la anglan lingvojn daŭras, ĉar tiuj profesiuloj ofte bezonas la anglan.
   Mi spertis tion, estante sciencisto en franca filio de usona kompanio. Preskaŭ ĉiutage
   mi iel bezonis utiligi la anglan lingvon, ĉu por legi raporton, ĉu por redakti ion
   en tiu lingvo, ĉu por paroli kun usona kolego ktp.
</p> 
<h3>Regula praktiko</h3> 
<p>En ambaŭ ĉi-supre priskribitaj dulingvismoj la homoj regule praktikas ambaŭ lingvojn.
   Sed ne ĉiuj homoj povas regule fari tion. La esperantista movado konsideras Esperanton
   kiel lingvon por la tuta mondo, por ke iu ajn povu komuniki kun iu ajn fremdulo, kvankam
   la origina ideo de la kreinto de Esperanto ne celis tion. Konstatante, ke en Bjalistoko
   pluraj gentoj vivantaj kune ne kapablas bone komuniki, Zamenhof volis krei lingvon
   por tiaj situacioj, ne por ke iu ajn japano povu paroli kun iu ajn polo.
</p> 
<p>Kaj, eĉ se ĉiuj japanoj kaj ĉiuj poloj lernus Esperanton en la elementa lernejo, ili
   praktikus ĝin tre malofte, en la nuna stato de la mondo, do ili forgesus ĝin. Preskaŭ
   ĉiuj junaj francoj lernis la anglan lingvon en la mezgrada lernejo; tiuj, kiuj ne
   regule praktikas ĝin, tute ne kapablas paroli angle post kelkaj jaroj. En Hungario
   multaj studentoj lernas Esperanton por ricevi unu plian diplomon pri fremda lingvo,
   sed la plimulto el ili ne plu praktikas ĝin poste, do Esperanto ne utilas por ilia
   internacia komunikado. En 1921 ĉe la Ligo de Nacioj, oni proponis instrui Esperanton
   en ĉiuj elementaj lernejoj; la franca registaro fiaskigis tion, sed, eĉ en okazo de
   efektivigo, tio ne ebligus, ke la popoloj komuniku per Esperanto, ĉar post la lerneja
   tempo la granda plimulto ne plu uzus tiun lingvon, do oni forgesus ĝin. Praktikado
   de Esperanto eblas en grandaj urboj danke al Esperantaj kluboj, kies membroj povas
   ofte renkontiĝi kaj tiel plibonigi kaj konservi la kapablon paroli tiun lingvon. Sed
   tio – regula vizitado de klubo – povas koncerni nur pionirojn, ne konsumantojn.
</p> 
<h3>Pioniroj kaj konsumantoj</h3> 
<p>Nun Esperanto koncernas nur pionirojn, kiuj deziras jen plibonigi la interfratiĝon
   de homoj, jen amuziĝi per originala lingvo. Pioniroj estas ĉiam malmultaj. Esperanto
   disvastiĝos nur tiam, kiam ĝi koncernos konsumantojn, kiuj estas la granda plimulto
   de la homaro. Eĉ se Esperanto anstataŭus la anglan lingvon por la internacia komunikado,
   kio tute ne antaŭvideblas, tio ne respondus al la interna ideo de Esperanto laŭ Zamenhof.
   Nur komercistoj kaj financistoj komunikus per Esperanto, sed la popoloj plu restus
   izolitaj, ĉiu en sia propra lingvo. Necesas trovi procezon, por ke Esperanta dulingvismo
   povu daŭri, do procezo, laŭ kiu la plimulto de la homoj praktikos tiun lingvon sufiĉe
   ofte. Nur tiel ili plu kapablos paroli ĝin por rilati kun fremduloj, kiam ili okaze
   renkontos iun. Ne estas facile difini, kiel kaj kiom ofte necesas praktiki duan lingvon
   por daŭre scipovi ĝin. Tio varias laŭ la personoj kaj laŭ la lingvoj; la reguleco
   de Esperanto certe helpas. Mi supozas, ke sufiĉus uzi Esperanton ĉiusemajne dum ĉirkaŭ
   unu horo, aŭdante, legante aŭ prefere parolante.
</p> 
<p>En la 1920aj jaroj ekzistis semajna gazeto en Esperanto, kiu do ebligis oftan uzon
   de tiu lingvo; temis pri 
   <i>Sennaciulo</i>, la organo de la asocio SAT (Sennacieca Asocio Tutmonda), destinita al la laboristoj,
   kun la slogano „Laboristoj ĉiulandaj unuiĝu”. Sed tiu iniciato estis kontraŭbatita de Stalino kaj Hitlero, kiuj detruis la esperantistajn
   movadojn de Rusio kaj Germanio. Poste la laborista movado ŝajne forgesis unuiĝi tutmonde.
   La tiel nomata 
   <i>Pasporta Servo</i> estas alia tipa iniciato por uzi Esperanton kadre de utila servo, sed ne amaso da
   homoj havas la emon kaj la rimedojn por vojaĝi internacie. Tio estas efika, sed ne
   sufiĉa por amasa praktikado de la internacia lingvo. La semajna gazeto, kiun Stefan
   Maul fondis antaŭ la fondo de la magazino 
   <i>Monato</i>, ne povis funkcii, ĉar la movado ne estas sufiĉe forta por vivigi (tio estas redakti
   kaj aboni) semajnan gazeton. Eble tra Interreto io estas farebla. Multaj homoj havas
   aliron al ĝi, sed tiuj homoj jam ricevas multajn informojn en multaj lingvoj. Ekzemple
   
   <i>Vikipedio</i> ja ekzistas ankaŭ en Esperanto, sed mi vetas, ke preskaŭ nur la redaktantoj de tiu
   versio malfermas ĝin, kaj ke la konsumantoj de 
   <i>Vikipedio</i> uzas aliajn lingvojn.
</p> 
<p>Ekzistas en Interreto televidaj elsendoj, filmoj kaj gazetoj, ekzemple 
   <i>Le Monde Diplomatique en Esperanto</i> kaj 
   <i>Monato</i>; tio nun ne altiras amason da spektantoj aŭ legantoj, sed ĝi eble estas interesa
   vojo por interesi la homojn pri Esperanto. Oni povas imagi, ke lerta alpaŝo estus
   interesi unue la infanojn, kiuj ankoraŭ ne havas antaŭjuĝojn kontraŭ Esperanto.
</p> 
<h3>Io mankas</h3> 
<p>Zamenhof inventis genian lingvon internacian, sed ankoraŭ mankas ia invento, por ke
   tiu lingvo povu funkcii kiel komunikilo inter la popoloj. Necesas inventi ion, por
   ke amaso da homoj deziru praktiki Esperanton sufiĉe ofte por konservi la kapablon
   paroli ĝin, ion pli utilan, pli interesan, pli dezirindan ol tio, kio jam troveblas
   en gazetoj, en televidoj kaj en Interreto en multaj lingvoj.
</p> 
