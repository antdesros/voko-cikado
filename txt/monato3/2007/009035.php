      <h3>Arto</h3>
      <h3 class="tem"></h3>
      <h2>Knabinoj, cignoj, burĝonoj</h2>
      <!-- komenco de la enhavo -->

       

     <p>Jen kompakta disko, kiu prezentas 16 kantojn, ĉiuj krom unu novaj. Krom la titolaĵoj: amantinoj, printempo, Parizo – jen komencaj kaj trakuraj temoj de la kolekto. Kiel kutime, Bronŝtejn mem aŭtoris la tekstojn kaj melodiojn. Enkonduke li pledas, ke oni atentu la tekstojn kaj iliajn aludojn. Feliĉe tio facilas, ĉar la kantado estas klara kun bonkvalita registrado, en prizorgo de Lev Rumjancev. Krome estas aldonitaj ĉiuj tekstoj en alloga kolore presita 16-paĝa libreto. Por aldoni varion al la aŭskultado, kunlaboris profesiaj kantistinoj Tatjana Belova, Vera Vlasova kaj Evelina Sokolova, kies fotojn vi vidas fine de la libreto. Por la kantoj gitaro estas la baza instrumento, sed suplemente sonas pluraj pliaj.</p> <p>Eroj 8-14 formas kompleton omaĝe al la 200-jariĝo de la eminenta poeto Aleksandr Puŝkin. En la unua kanto el tiuj la aŭtoro mem omaĝas; sekvas iom ŝercaj kontribuoj de delegitoj diverslandaj: brita, pola, franca, sveda kaj ĉina. Ili aludas iujn okazaĵojn aŭ sintenojn de siaj landanoj al la omaĝato. La brito komparas lin kun Byron, la polo kun Mickiewicz; la franco montriĝas la pranepo de tiu, kiu duele mortpafis la poeton en 1837. Fremdlingvaj esprimoj kaj historiaj aludoj ricevas klarigon en la libreto.</p> <p>Enhave okulfrapas, almenaŭ al mi, versparo de <i>Adiaŭ Paris!</i>:</p> <p><i>... en la koro restas nun<br/> cikatro inter sen kaj kun ...</i></p> <p>La lasta kanto <i>Por Ĵomart</i> reprenas la sezonan fadenon de la unua kaj la kor-centrecon de la ĵusa cito. Du eltiraĵoj:</p> <p><i>... en koroj apenaŭ aŭtunas<br/> la fajre junula amem' ...</i><br/> <br/> <i>... retrilos printempo ekverde<br/> en nia amika ĝarden' ...</i></p> <p>Por tiu, kiu konas kaj aprezas la kantojn de Mikaelo, jen inda aldono al via disko-kolekto.</p> <div style="text-align:right">Stefan M<span class="mm">AC</span>G<span class="mm">ILL</span></div> <h6 class="bib">Mikaelo Bronŝtejn: Printempo bluĵinsa. Kompakta disko kun 16 kantoj. Eld. la aŭtoro, Tiĥvin (Rusio), 2006. Ludodaŭro: 50 minutoj.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=insa">la Retbutiko</a>.
