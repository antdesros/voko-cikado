      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Mi vin invitas ...</h2>
      <!-- komenco de la enhavo -->

       

      <p>En februaro la eventoj en Ukrainio ne restis tro for de la ĉefpaĝoj de niaj gazetoj
   kaj de la ekranoj de niaj televidiloj. Pro tio la redakcio de M<span class="mm">ONATO</span> mesaĝis al abonantoj kaj abonintoj, legantoj kaj legintoj en la regiono, petante
   ilin artikoli pri la tiea situacio.
</p> 
<p>Sincere mi dankas al ĉiuj, kiuj rapide reagis, ĉu per artikoloj, ĉu per proponoj.
   En la nuna numero vi trovos du kontribuaĵojn, kiuj prezentas diversajn aspektojn de
   la konflikto, kaj tuŝas aferojn – ekzemple el la ĉiutaga vivo – ofte neglektatajn de amaskomunikiloj.
</p> 
<p>Mi ricevis, iom surprize, ankaŭ mallongan poemon, kiun ĝenerale mi ne aperigus, ĉar
   M<span class="mm">ONATO</span> prezentas proze, ne poezie. Tamen la versoj de Tatjana Auderskaja iel kaptas la momenton:
</p> 
<p>
   <b>Kanto de neeŭropa homo de la 21a jarcento</b>
</p> 
<p>Ho, Eŭropo!
   <br></br> En la 15a jc vi bruligis viajn sorĉistinojn en la fajroj surplace. Mezepoko.
   <br></br> En la 20a jc vi bruligis viajn civitanojn de ajna sekso kaj aĝo en la fornegoj. Progreso.
   <br></br> En la 21a jc vi bruligas pneŭojn en la fajroj surplace. Degenero.
   <br></br> Ho ve, Eŭropo, Eŭropo!
   <br></br> Kien vi ruliĝas tiel senbride, kiel brulantaj pneŭoj? Kio atendas vin fine de la
   deklivo?
   <br></br> Neniu respondas.
</p> 
<p>Kiel dirite, kaj kontraste al la lasta verso de la poemo, pluraj respondis al la redakcia
   peto rilate kontribuaĵojn pri Ukrainio. Sed tio memorigas, ke ne necesas atendi alvokon
   por kontribui al M<span class="mm">ONATO</span>. Tute male. La redakcio bonvenigas raportojn el ĉiuj partoj de la mondo: vi, abonantoj
   kaj legantoj, estu niaj okuloj kaj oreloj.
</p> 
<p>Tio, evidente, unikigas nian revuon. Ne dependas M<span class="mm">ONATO</span> de specialaj korespondantoj, metafore paraŝutitaj en fremdajn regionojn por priraporti
   aferojn, kiujn ili nur supraĵe komprenas (ofte pro mankantaj lingvo-scioj), kaj kie
   ili restas nur dum kelkaj tagoj, dum daŭras konflikto, krizo aŭ katastrofo.
</p> 
<p>Nia revuo, tamen, disponas almenaŭ teorie pri aro da pli spertaj, pli kvalifikitaj
   korespondantoj ol iu ajn nacia gazeto aŭ radio-stacio. Mi aludas vin, legantojn, kiuj
   ĉiuj posedas apartajn kaj fundajn sciojn pri via lando, pri via regiono, kaj kiuj
   kapablas unike prilumi eĉ konatajn situaciojn – militojn, ribelojn, tumultojn, jam priraportitajn en mondaj amaskomunikiloj – de nekonataj perspektivoj. Jen la forto de M<span class="mm">ONATO</span>, kiu ĝin elstarigas de ĉiuj aliaj similaj revuoj.
</p> 
<p>Alivorte vi povas iri malantaŭ kulisojn, pri kies ekzisto la eksterlanda raportisto
   eĉ ne scias. Vi povas esplori kaj analizi de enlanda pozicio privilegiita, tiel prezentante
   la politikon, ekonomion, socion, ja vivon kaj spiriton de via lando, por ke eksterlandanoj
   kuninformiĝu, kunsentu, kunploru aŭ kunridu tra la paĝoj de M<span class="mm">ONATO</span>.
</p> 
<p>Mi menciis supre, ke ni disponas almenaŭ teorie pri aro da korespondantoj. Nenio pli
   plaĉus, se niaj teoriaj kontribuuntoj – do eble vi – fariĝus praktikaj kontribuantoj, sendante raportojn (kaj originalajn, pro kopirajto
   ne guglitajn fotojn) el mondopartoj malofte aŭ neniam menciataj en la kolumnoj de
   M<span class="mm">ONATO</span>.
</p> 
<p>Verdire temis pri la alvoko al amikoj en Ukrainio, kiuj inspiris min tiel inviti ankaŭ
   vin kontribui al nia revuo. Ne necesas atendi ĝis krizo aŭ katastrofo en via lando
   por ŝalti la komputilon kaj ekverki. Jam ĉirkaŭas vin sennombraj aferoj, kiuj sendube
   interesos. La sola problemo ... kiun elekti.
</p> 
<p>Kaj do nun, vidpunktuminte, mi antaŭĝuas ricevi viajn raportojn. Do ek al la klavaroj,
   amikoj!
</p> 
