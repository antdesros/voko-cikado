      <h3>Scienco</h3>
      <h3 class="tem">SUKERA DIABETO</h3>
      <h2>Noviga apliko je dispono de kuracistoj kaj pacientoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Ekzistas pluraj aplikoj por tabulkomputiloj kaj porteblaj telefonoj koncerne sukeran
      diabeton (latine: 
      <i>diabetes mellitus</i>). Temas, kiel konate, pri disvastiĝinta kronika malsano, en kiu la sangosukera nivelo
      estas ekscese alta. Haveblas dekoj da tiaj ciferecaj programoj: iuj utilas por krei
      virtualan taglibron pri la propra nivelo de glukozo, aliaj helpas la pacienton nombri
      karbonhidratojn ene de diversaj nutraĵoj aŭ preskribas la necesan kvanton de insulino
      por injekti. Ne mankas aplikoj, kiuj siavice helpas mastrumi la etajn porciigilojn
      de insulino. Nu, la apliko, kiun ni nun prezentas en ĉi tiu numero de M<span class="mm">ONATO</span>, faras absolute nenion tian. Sed 
      <i>Sybilia</i>, kreita de la itala doktoro Andrea Kleiner (vidu intervjuon kun li en M<span class="mm">ONATO</span> 2013/12), faras aferojn, kiujn neniu el la ceteraj aplikoj ankoraŭ faras. Ni vidu
      ĉi-sube, pri kio temas precize.
   </b>
</p> 
<h3>Apliko kompleta</h3> 
<p>
   <i>Sybilia</i>, simple kaj el nulo, kvazaŭ moderna „orakolo” (se analizi etimologie ties nomon), kreas insulin-terapion por tiuj, kiuj neniam
   antaŭe utiligis tiun substancon. Ne estas troigo aserti, ke ĝi reprezentas kompletan,
   kvankam migrantan kaj miniaturan, diabetologian centron. Ĝi ebligas kalkuli la korvaskulan
   riskon de pacientoj, ilian korpovolumenan indicon (angle: 
   <i>Body Volume Index</i>), la funkciadon de iliaj renoj, rekomendante kontroladon (se necese) de sangpremo
   kaj lipidoj. Depende de ies klinika profilo, la apliko proponas la plej taŭgan dieton,
   la plej taŭgan tipon de gimnastiko, la plej taŭgajn kuracilojn (ne nur rekte ligitajn
   al diabeto, sed ankaŭ statinojn aŭ medikamentojn, kiuj preventas kungluiĝon de trombocitoj).
</p> 
<h3>Apliko skrupula</h3> 
<p>La apliko malkovras eĉ eventualajn erarojn rilate al antaŭe preskribitaj medikamentoj:
   ekzemple ĝi rimarkigas la okazojn, kiam t.n. metformino estas kontraŭindikita. Ĝi
   ankaŭ proponas, kiam haveble, terapiojn pli korektajn por la paciento (ekzemple kiam
   statino ne estas sufiĉe efika por atingigi al oni la bezonatan nivelon de kolesterolemio
   kaj povas esti anstataŭigata de alia).
</p> 
<h3>Apliko fidinda</h3> 
<p>La apliko utiligas algoritmojn, kiuj sekvas la gvidliniojn de la prestiĝa 
   <i>American Diabetes Association</i>. La uzataj algoritmoj estas derivitaj plejparte de medicinaj tekstoj, kiel 
   <i>Medical Management of Type 1 Diabetes Mellitus</i>, unu el la tutmondaj fundamentoj rilate al la kuracado de sukera diabeto unuatipa.
</p> 
<h3>Apliko universala</h3> 
<p>La „aŭguroj” de 
   <i>Sybilia</i> direktiĝas al ĉiuj: tiu ĉi apliko utilas al la kuracisto, al la fakulo kaj (kial
   ne?) al la paciento mem. Kvankam ĝi enhavas kompleksajn funkciojn, ĝi sume estas je
   dispono de kiu ajn (ĉiam rekomendinda estas, tamen, la superrigardo de medicinisto).
</p> 
<h3>Apliko simpla</h3> 
<p>La apliko estas ekipita per interfaco tiom simpla, ke ĝi kutime ne postulas apartan
   trejniĝon por povi esti utiligata. La uzo de la apliko antaŭvidas malmultegajn simplegajn
   paŝojn, al kiuj cetere respondas samnombraj helpaj prembutonoj, kun la celo akompani
   (paŝon post paŝo) la pacienton. Se tio ankoraŭ ne sufiĉas, ene de la apliko troviĝas
   ligilo al tre detala filmeto (havebla anglalingve en interreto ĉe la adreso http://www.youtube.com/watch?v=fO89EV7Eu0w).
</p> 
<h3>Apliko senriska</h3> 
<p>
   <i>Sybilia</i> estas sekura kaj tute sena je riskoj, ne nur ĉar ĝi baziĝas sur internaciaj sciencaj
   gvidlinioj, sed ankaŭ ĉar ĝi paradas per longa testado.
</p> 
<h3>Apliko fulmrapida</h3> 
<p>Mirinda rapideco karakterizas tiun ĉi aplikon. Nova baza insulin-terapio povas esti
   kreita ene de nur kvin sekundoj. Por krei propran klinikan profilon, oni bezonos malpli
   ol duonan minuton.
</p> 
<h3>Apliko internacia</h3> 
<p>
   <i>Sybilia</i> aperis en Italio, sed jam disvastiĝas tra la tuta mondo. Ĉi tiu apliko haveblas,
   por la momento, en du lingvoj: la itala kaj la angla. Kun la helpo de iu volontula
   leganto, ne estus utopie pripensi estonte ankaŭ esperantlingvan version!
</p> 
<h3>Apliko malmultekosta</h3> 
<p>La aplikon, nun elŝuteblan al ajna 
   <i>Apple</i>-aparato (<i>iPhone</i>, 
   <i>iPad</i>...) post plurjara prilaborado kaj testado, oni povas aĉeti en la cifereca vendejo
   de 
   <i>Apple</i> kontraŭ la kosto de apenaŭ 2,69 eŭroj. Neniu aldona kosto (ekzemple post kelkaj monatoj
   aŭ jaroj) estos postulata post ĝia aĉeto. 
   <i>Sybilia</i> estas utila programo, kiu niaopinie ne povas manki en la poŝtelefono aŭ en la komputilo
   de kuracistoj aŭ de homoj trafitaj de sukera diabeto. Ĉi-lastaj, danke al teknologio,
   havas jam la eblon simple, sekure kaj malmultekoste teni sub kontrolo sian sanproblemon.
</p> 
