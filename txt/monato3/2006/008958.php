      <h3>Politiko</h3>
      <h3 class="tem">ALBANIO</h3>
      <h2>Post bendo  fendo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Jam de 1997 Spartak Ngjela [nĝela] aktivas en la albana reganta Demokratia Partio, dum kelkaj monatoj precipe kiel ministro pri justico en la tiel nomata registaro de nacia saviĝo, ĝis venkis la socialistoj. Li estas filo de ministro pri komerco en la reĝimo de la albana diktatoro Enver Hoxha [hodĵa] kaj laŭprofesie advokato.</p> <p>En julio 2005 li estis elektita kiel deputito, sed la nova ĉefministro ne metis lin en la registaron. Anstataŭe Ngjela ricevis partiajn kaj parlamentajn postenojn, fariĝante komisionestro pri leĝaro. Tamen li kontraŭstaris registarajn klopodojn elradikigi korupton (unu el la kialoj pro kiuj Albanio ankoraŭ ne estas akceptita de internaciaj instancoj kiel Eŭropa Unio aŭ Nord-Atlantika Traktat-Organizaĵo).</p> <h3>Opozicio</h3> <p>Ngjela argumentis, ke ekzemple ŝanĝi la metodojn por elekti membrojn de sendependaj ŝtataj institucioj aŭ enkonduki elektronikan voĉdonadon en la parlamento kontraŭas la konstitucion. Lia konduto vekis admiron ĉe la opozicio socialista, sed seniluziigis liajn estrojn en la Demokratia Partio.</p> <p>Rezulte Ngjela aperis antaŭ partiestroj por klarigi sian starpunkton. Li akuzis la ĉefministron Sali Berisha pri blokado de ŝtata kaj ekonomia projektoj surbaze de kontraŭkonstitucia agado. Li aldonis, ke Berisha kaj du aliaj ministroj estas eks-komunistoj, kiuj daŭre agas laŭ komunisma pensmaniero.</p> <p>Ngjela kaŝe registris la debaton kaj la sekvan tagon donis la bendon al televida kompanio. Jen la unua fojo ekde 1990, ke neformalaj diskutoj estas kaŝe registritaj. Tiel rompiĝis la pacienco de la Demokratia Partio, kiu eksigis sian disidenton.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
