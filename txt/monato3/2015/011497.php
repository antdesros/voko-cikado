      <h3>Scienco</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Ekstremaj veter-fenomenoj obliĝas</h2>
      <!-- komenco de la enhavo -->

       

       <p>Lastatempe televidaj kaj radiaj novaĵoj tre ofte komenciĝas per avertoj pri neordinara
   vetero: printempe ciklono kun ŝtormo kaj fulmoj, somere varmego aŭ tajfuno, aŭtune
   tajfuno, vintre neĝfalego aŭ frosto. Sekvas avertoj pri rezultontaj inundo, terfalo,
   trafika interrompiĝo kaj damaĝoj por homoj kaj domoj. Fakte, en ĉiu sezono atakas
   senprecedenta pluvego aŭ neĝfalego iun lokon nepretan. Tian pluvegon oni nomas gerila
   pluvego. Tiun ekstreman veteron kaj tiun klimatoŝanĝon dum la lasta jardeko multaj
   japanaj meteologoj atribuas al la tiel nomata tutmonda varmiĝo. Precipe ili koncentriĝas
   sur la altiĝon de la temperaturo en la akvoj de la maroj Filipina kaj Japana.
</p> 
<h3>Anticiklono</h3> 
<p>Estas fakte malfacile science pruvi ligitecon inter unuopaj ekstremaj veteraj okazaĵoj
   kaj plivarmiĝo. Sed la japana subministerio pri meteologio anoncis, ke dum la lastaj
   100 jaroj la temperaturo de la marakvo ĉirkaŭ Japanio ja plialtiĝis je 1,07 gradoj
   celsiaj. En februaro 2014 la temperaturo meze de la Filipina maro estis pli varma
   je 0,5 gradoj celsiaj ol kutime. La alta temperaturo daŭris ankoraŭ en la somero,
   kiam altiĝis la mara temperaturo ĝis ĉirkaŭ 30 gradoj celsiaj, varmigante kaj kreante
   fortan aerfluon suprenirantan. Male sur la proksima maro de Japanio aerofluo malsupreniris,
   fariĝante altaerpremo (anticiklono) kaj kovrante la tutan japanan insularon: tie estis
   sekve tropika varmego tutlande.
</p> 
<h3>Varmofrapo</h3> 
<p>Tia – varmega – somero jam estas normala fenomeno en Japanio. Rezulte, ĉiujare oni spertas varmofrapon,
   kaj multaj mortas. Kvankam la preciza nombro de mortoj pro varmofrapo en 2014 ne haveblas,
   antaŭ 1993 la averaĝa nombro estis 67 jare, sed post 1994 ĝi estis 492 jare. En 2010
   la mortintoj atingis 1 745 unuojn. En 2013, cetere, pli ol 408 000 personoj ricevis
   medicinan kuracadon pro varmoatako.
</p> 
<h3>Marsurfaco</h3> 
<p>Laŭ la 5a taksa raporto de la Interregistara Spertularo pri Klimata Ŝanĝiĝo aŭ ISKŜ,
   plivarmiĝo povos malmultigi la ekeston de tropikaj ciklonoj, sed, se ĉi-lastaj iam
   re-ekaperos, ili povos kreski ege fortaj kaj furiozaj. La kialo? Kiam la supra aero
   varmiĝas kaj la varmdiferenco kun tiu de la marsurfaco malmultiĝas kaj la aeramaso
   stabiliĝas, tiam maloftiĝas la ekestiĝoj. Dume, varma vaporo pligrandigas la energion
   en la aeramaso kaj gigantigas la ciklonojn. Tiu hipotezo eble klarigas la ekeston
   kaj la fortecon de kelkaj gigantaj tropikaj ciklonoj, kiuj lastatempe frapis Vanuatuon,
   Filipinojn kaj Japanion.
</p> 
<h3>Retropaŝado</h3> 
<p>Do, ĉu la varma aeramaso sur la maro alportos varman vintron al la japana insularo?
   Parte jes, ĉar la averaĝa temperaturo dum vintro iom plialtiĝas. Sed problemo pli
   serioza estas, ke aeramaso kun multe da vaporo facile faligas neĝon: ofte ciklonoj
   alportas neĝegon kaj neĝoŝtormojn.
</p> 
<p>Oni vaste konscias, ke Japanio pro la tutmonda varmiĝo alfrontas apartan riskon, inkluzive
   de inundoj, alta tajdo, tornadoj, senpluveco, malmultiĝo en la fakoj de agrikulturo
   kaj fiŝkaptado pro la detruo de la ekologia sistemo. Tial ankaŭ la japana registaro,
   en la kadro de Konferenco de UN pri klimatoŝanĝo, agadas kaj provas redukti la enaerigon
   de karbona dioksido.
</p> 
<p>Tamen, kiam la japana registaro ĉe la konferenco okazinta en Pollando en 2013 (COP19)
   publikigis sian mezvojan celon ĝis 2020, nome redukton je 3,8 procentoj kompare kun
   tiu de 2005 (fakte plimultiĝo je 3,1 procentoj kompare kun 1990), multaj registaroj
   kaj neregistaraj organizaĵoj iritiĝis kaj kritikis Japanion. Ĉar tiu celo estis klara
   retropaŝado. La nerezoluta konduto de la japana registaro baziĝas sur la fakto, ke
   ĝi daŭre intencas dependi de nuklea energio, sed ankoraŭ ne povas decidi la proporcion
   inter la tuta energio kaj la nuklea.
</p> 
