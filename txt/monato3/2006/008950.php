      <h3>Leteroj</h3>
      <h3 class="tem"></h3>
      <h2>Ne irlandgaela, sed skotgaela</h2>
      <!-- komenco de la enhavo -->

       

    <p>Kvankam mi estis aŭtoro de <i>Lingva Davido kontraŭ Goljato</i> (M<span class="mm">ONATO</span> 2006/4, p. 19-20), ne estis mi, kiu elektis la bildojn. La dua ilustraĵo sur paĝo 20 ne taŭgas por eseo pri la gaela lingvo en Irlando, kvankam ĝi estas tre interesa kaj unika. Temas pri la titolpaĝo de ege rara kaj antikva biblio en la gaela lingvo, tradukita el la hebrea en la gaelan de William Bedel kaj el la greka de William O Domhnuill, kiu, por komuna profito de la skotaj gaeloj, estas transskribita el la irlanda tiparo al la legeblaj romiaj minuskloj.</p> <p>La historio de tiu versio estas interesa. Ekde la alveno de la kristana religio en la 5a jarcento, la irlandanoj konis Biblion en la latina lingvo. Neniu gaela traduko ekzistis ĝis preskaŭ la fino de la 16a jarcento, kiam la aŭtoritatoj de la anglikana eklezio proponis tradukigi la Novan Testamenton. En la 17a jarcento aperis irlanda eldono de la tuta Biblio, tradukita de O Domhnuill kaj Bedel, populare konata kiel „Bedel-biblio”. Ĉar en tiu epoko apenaŭ estis diferenco inter la gaela uzata en Irlando kaj tiu en Skotlando, aparta eldono por uzo en Skotlando estis presita en Londono, post antaŭpreparo en Dublino. Tamen oni baldaŭ konstatis, ke la skotgaeloj ne povis kompreni la originalan Bedel-tekston, ĉar tiam – kaj eĉ ĝis la lasta duono de la 20a jarcento – la tiparo, per kiu oni en Irlando presis gaelajn tekstojn (inkluzive tiun de la Bedel-biblio), estis la bela mezepoka manuskripta alfabeto. La ilustraĵo en M<span class="mm">ONATO</span> do rilatas al represo publikigita en la jaro 1690 kaj taŭgus ne por artikolo pri la irlandgaela, sed por bildigi specimenon de la skotgaela, kiu dum la jarcentoj post la 17a multe ŝanĝiĝis kaj evoluis ĝis aparta lingvo hodiaŭ ankoraŭ parolata en Skotlando.</p> <div style="text-align:right">Garbhan M<span class="mm">AC</span>A<span class="mm">OIDH</span></div> <div style="text-align:right"><i>Irlando</i></div> 
