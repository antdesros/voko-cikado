      <h3>Ekonomio</h3>
      <h3 class="tem">DEMOKRATIA RESPUBLIKO KONGO</h3>
      <h2>Privatiĝo de la publika transporto</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Antaŭ dek jaroj la Demokratia Respubliko Kongo (DRK) havis tri entreprenojn de publika transporto en grandaj urboj. Ĉiuj bankrotis. Pro la politika krizo la registaro ne estis kapabla krei alian entreprenon. Tiu situacio kaŭzas ekonomiajn malfacilaĵojn.</b></p> <p>En la grandaj urboj la transporto estas efektivigita de veturiloj de privatuloj. Tiuj ricevas ilin de siaj parencoj elmigrintaj, precipe el Eŭropo. Estas veturiloj, kiujn la okcidentanoj deklaras neuzeblaj aŭ kiujn ili ne plu ŝatas uzi. Ili estas aĉetitaj malmultekostaj. En 1997, kiam Laurent Kabila iĝis la prezidanto de DRK, li liberigis la importon de tiaj veturiloj kaj la dogano je ili estas malalta.</p> <p>La ĉefurbo Kinŝaso havas preskaŭ sep milionojn da loĝantoj, kaj la transporto estas malfacila, precipe matene kaj vespere. Matene la plej multaj laboristoj konverĝas al la urbocentro, kaj vespere ili revenas. Laŭ la belgaj planintoj de la urbo la grandaj entreprenoj kaj vendejoj estas en la centro, kaj de post la sendependiĝo nenio ŝanĝiĝis. La laboristoj, kies entreprenoj estas rigoraj pri akurateco, eliras siajn domojn je la kvara matene. En Kinŝaso, por uzi taksion aŭ alian transportilon, necesas batali, ĉar multaj atendas.</p> <p>La kondukistoj de tiaj veturiloj renkontas multajn obstaklojn: la malbonan staton de vojoj, la policistojn, kiuj postulas servomonon anstataŭ respektigi la leĝojn, la armeanojn kaj la policistojn, kiuj senpage veturas kaj kelkfoje amase eniras, la trafikobstrukcojn, la petrolan krizon.</p> <p>Por redukti la problemojn de la transportistoj, la registaro decidis, ke unu litro da benzino kostas malpli ol unu dolaro. Ankaŭ veturbileto malmulte kostu. Malgraŭ tio, la transporta situacio estas tre malfacila: anstataŭ kvin pasaĝeroj troviĝas sep en unu veturilo; la sidejoj en grandaj busoj estis forigitaj por meti benkojn por kvin aŭ ses pasaĝeroj. La registaro promesis la fondon de nova entrepreno.</p> <div style="text-align:right">Serge Z<span class="mm">ANDANDU<