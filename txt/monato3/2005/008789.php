      <h2>Laboro kaj la bero</h2>
      <!-- komenco de la enhavo -->

       

        <p>En aŭtuno 2005 finna entreprenisto flugigis 92 tajlandanojn al Savukoski, Laponio, por rikolti sovaĝajn berojn, kiuj ĉi-jare escepte abundis. La flugo kun pasporto, vizo, dumvojaĝaj manĝoj kaj veturado en Finnlando kostis al ĉiu tajlandano ĉirkaŭ 1000 eŭrojn. Kelkaj el la flugigitoj prunteprenis monon por pagi, kaj dum du monatoj la tajlandanoj plukadis kamemorojn en la marĉoj kaj mirtelojn kaj vakciniojn en la arbaro.</p> <p>La beroj, aŭ la produktaĵoj, faritaj el ili, estas eksportataj, ekzemple mirtelekstrakto al Japanio, kie ĝi estas uzata por plibonigi vidkapablon. Por produkti 30 000 kilogramojn da ekstrakto estas bezonataj 3 milionoj da kilogramoj da mirteloj. Kaj ĉi-jare plukiĝis tiom da vakcinioj, ke pro manko de transportkestoj ne eblis finfine ilin profitodone rikolti, kvankam plia eksportado eblus.</p> <h3>Riproĉe kaj kondamne</h3> <p>Sed en Savukoski okazis konflikto kun lokanoj, kiuj malŝatis vidi „siajn” berojn rikoltataj de fremduloj, kvankam en Finnlando principe kiu ajn kie ajn rajtas rikolti nekultivitajn berojn. La entreprenisto solvis la konflikton, veturigante la dungitojn 60-80 km for de la vilaĝo en arbaron, tiel ke la vilaĝanoj ne plu sentis „sian” rikolton minacata. Krome la lokanoj argumentis: Kial finnaj senlaboruloj, vivantaj koste de la socio, ne rikoltis la berojn?</p> <p>La tajlandaj dungitoj estis pagataj por ĉiu kilogramo da beroj. Averaĝe tajlandano kolektis po 70-80 kilogramojn da mirteloj tage, 15 kilogramojn pli ol li pezas, sed la rekordon atingis geedza paro, kiu kolektis tage po 140-159 kilogramojn. La paron la entreprenisto regalis en loka restoracio. Kutime la berorikoltistoj kuiris por si rizon per vaporumilo en ne plu uzata lernejo, kie oni loĝigis ilin. Pli kaj pli da lernejoj en la finnlanda kamparo estas fermataj, ĉar la lernantaro malpliiĝas pro transloĝiĝo de la familioj, kiuj serĉas laboron en la suda triono de la lando.</p> <h3>Unujara enspezo</h3> <p>La rikoltistoj diris, ke en Tajlando ili enspezas 4000 bahtojn (ĉirkaŭ 80 eŭrojn) monate, dum en Finnlando ili povis perlabori 950-1150 eŭrojn en du monatoj, eĉ post pago de loĝado kaj flugoj. Ili diris, ke tio egalvaloras unujaran enspezon. Finno per tia salajro eĉ unu monaton ne travivus: la averaĝa salajro egalas pli ol 2000 eŭrojn monate.</p> <p>Berorikoltado estas peza laboro, kiu ofte dolorigas la dorson. Pro la lingva barilo la dungitoj preferis la socion de siaj kolegoj kaj ne provis konatiĝi kun finnoj kaj la finna medio. Ili tamen lernis, ke boacoj ne estas sendanĝeraj kaj ke memoraĵojn ili ne aĉetu, por ne tuj elspezi ĉion, kion ili perlaboris.</p> <p>Oni povas demandi sin, kial indas flugigi homojn el Azio al finna Laponio (ankaŭ rusoj kaj estonoj alvenas Finnlandon por rikolti fragojn en julio). Responde oni povus paroli pri aventuremo, ekzotismo kaj la deziro pliriĉiĝi: ankaŭ, kaj eble pli funde, pri tordiĝintaj monda ekonomio kaj sociaj sistemoj.</p> <div style="text-align:right">Saliko</div>   
