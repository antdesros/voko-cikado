      <h2>Serioze revizii la historion</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kun intereso mi legis la artikolon pri religioj en Bulgario (M<span class="mm">ONATO</span> 2006/12, p. 24-25), tamen mi malkonsentas pri ĝiaj vidpunktoj. La aŭtoro sekvas la tradician opinion, ke en Bulgario estas unu tradicia religio (ortodoksa kristanismo). Li klarigas, ke islamo alvenis en la landon ĉefe pro perforto kaj ankaŭ „pro ekonomiaj konsideroj”. Kvazaŭ tio ne estus okazinta pri kristanismo. Cetere, la grado de perforto en la alpreno de islamo estas diskutinda, kaj certe la grado de perforto en la kristanigo de la lando post la sendependiĝo estas pli ol evidenta: la kvanto de islamanoj de 1878 ĝis hodiaŭ falis de proksimume duono de la loĝantaro al nunaj 10 %. Lasta epizodo de tiu perfortado estis la amas-forpeloj de junio kaj julio 1989, kiam 300 000 islamanoj estis puŝitaj en Turkion.</p> <p>Aliflanke, la konsidero, ke unu religio estas pli tradicia en la lando, ĉar ĝi estis oficialigita antaŭ 1100 jaroj dum alia „nur” antaŭ 600, estas pli ol diskutinda. Sub ĝi kaŝiĝas la koncepto de unuetna ŝtato, kiu fajrigis Balkanion en la lastaj 200 jaroj. Pri tio mi permesas al mi diri, ke la otomana imperio, dum la plej granda parto de sia ekzisto, estis multe pli permesema, multege pli, ol samepokaj okcident-eŭropaj kristanaj ŝtatoj (pri tio spertas ekzemple preskaŭ ĉiuj bulgaraj judoj, kies prapatroj tie trovis rifuĝon post sia forpelo el Iberio). Estus dezirinde, ke bulgaroj – kiel aliaj balkanaj popoloj – tre serioze reviziu sian historion kaj elsarku el ĝi naciismajn mitojn kovantajn novajn konfliktojn. Interalie, la pritrakto de la 500-jara otomana regado en la bulgaraj lernolibroj estas ege unuflanka, manka de ĉia nuanco kaj ofte ofenda por islamaj bulgarianoj (kaj aparte por la etne turkaj).</p> <p>Laste, sed ne balaste, trois al mi pluraj adjektivoj. La aŭtoro parolas pri „moderaj ŝijaistoj”, dum li ne distingas inter „moderaj” kaj „fundamentismaj” kristanoj – kaj tamen ili ja ekzistas. Li timas pri tio, ke kelkaj islamanoj vizitas privatajn nelaikajn lernejojn, sed tion ja faras ankaŭ kristanoj. Li timas, ke piaj islamanoj iras studi eksterlanden, sed ankaŭ ja tion faras kristanoj. Mi ne subtaksas la danĝeron de eventuala enŝteliĝo de perfortema islama fundamentismo en Bulgarion, sed mi certas, ke plena akceptado de la samlegitimeco de ĉiaj kredoj en la lando kaj aparta rehonorigo de islamanoj, mistraktataj kaj diskriminaciataj dum pli ol unu jarcento, estas la plej solida sekurigilo por Bulgario.</p> <div style="text-align:right">Hektor A<span class="mm">LOS</span> <span class="mm">I</span> F<span class="mm">ONT</span></div> <div style="text-align:right"><i>Katalunio</i></div> 
