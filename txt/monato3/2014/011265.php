      <h3>Turismo</h3>
      <h3 class="tem">MEDITERANEO</h3>
      <h2>En Palermo subtere</h2>
      <!-- komenco de la enhavo -->

       

       <p>Vi ŝatas someran vojaĝadon al Mediteraneo, sed – ne tute malprave – iom enuis pri la kutimaj suno, maro kaj precipe varmego, neniam tiel sufoka kiel
   en la lastaj jaroj ... Ne estas problemo. Jen originala, ombra, vere friska loko por
   viziti somersezone en suda Italio. Oni surpriziĝos pro la beleco de la subteraĵoj
   de Palermo. La ĉefurbo de la regiono Sicilio, jam konata danke al siaj naturaj, historiaj,
   artaj kaj gastronomiaj allogaĵoj, fakte staras super la realaĵo – same rava, sed ial ofte neglektata de la gvidlibroj pri Italio.
</p> 
<h3>Inĝenierio</h3> 
<p>Unue, alveninte al Palermo, oni havigu al si – ĉe unu el la pluraj lokaj vendejoj – gumajn botojn, kaskon speleologan, torĉon, gantojn. Tiel oni povas komenci esplori
   la t.n. Qanat-ojn: temas pri akvokondukiloj, konstruitaj inter la jaroj 800 kaj 1200
   post Kristo, inter la araba kaj normana okupadoj. Nur du el ili estas nun alireblaj,
   sed vere valoras vizito, inkluzive de promeno tra la akvo, al tiu verko de progresinta
   hidraŭlika inĝenierio. Qanat-oj estas vasta reto de galerioj, kiuj havas la apartan
   karakterizon ekspluati la deklivecon de la grundo kaj de la rokoj, kaj trairi ankaŭ
   la akvoriĉajn stratumojn. Tiumaniere la akvo sukcesas alveni al urbaj fontanoj, publikaj
   banejoj, fiŝbasenoj kaj ĝardenoj. Pasintece, kiam ajn siroko blovis (kutima fakto
   en Palermo), la Qanat-oj estis uzataj kiel ventoliloj, kiuj nature kaj efike malvarmigis
   la aeron.
</p> 
<h3>Mumiigo</h3> 
<p>Alia interesaĵo en la lokaj subteraĵoj estas la rifuĝejoj, utiligataj dum la dua mondmilito.
   Ili troviĝas ĉefe sub monaĥejoj, el kiuj la plej famaj nomiĝas itallingve 
   <i>della Misericordia</i> kaj 
   <i>dell'Assunta</i>. Ŝatataj de turistoj estas ankaŭ la katakomboj, precipe tiuj de la kapucenoj el la
   16a jarcento, situantaj en la kvartalo Cuba. Ili konservas pli ol 2 000 mumiojn, en
   diversaj pozicioj kaj en bona stato de konserviĝo. Ĝuste pro tio ĉi tiun lokon vizitas
   sciencistoj, kiuj intencas malkovri la sekretojn de mumiigo.
</p> 
<p>Jen kelkaj itineroj por alternative esplori tiun italan kaj eŭropan urbon, ĉiam pli
   moderniĝantan.
</p> 
