      <h2>Kiel aspektas miraklo</h2>
      <!-- komenco de la enhavo -->

       

     <p>La libro estas stranga. La strangaĵoj en ĝi estas kelkaj. Sed la unua, kaj ĉefa, estas la ĝenro. Fakte, preskaŭ ĉiuj „noveloj” el la prezentitaj 18 estas laŭ la unua vido fabeloj (la du lastaj – primovadaj satiraĵoj). Nu, fabelo estas ja ĝenro estiminda: sufiĉas rememori, ke laŭorda konatiĝo kun Esperanto komenciĝas de la fabelo („Unu vidvino havis du filinojn...”) por kompreni ĝian gravecon por nia literaturo. La fabeloj estas popularaj ĉe la infanoj, sed ne nur: ni nur menciu la furoron de „La mastro de l' ringoj” libro- kaj kino-forme, kaj vi komprenos, ke la ĝenro estas ankaŭ serioza. Samtempe, originale verkitaj fabeloj en Esperanta literaturo ne tro multas, kaj tial necesas tuj saluti la novan fabelkolekton.</p> 
<p>Sed ne ĉio tiom simplas. Ja la fabeloj ne estas tre kutimaj, eĉ se iuspeca influo de la klasikaj verkoj, kiel de Andersen, evidente senteblas. Parto de la fabeloj (se ne ĉiuj) aspektas iom strange, kaj ne ĉiam klaras, kion la aŭtoro volis diri. Tamen, post medito pri la strangaĵoj, mi faris la jenan konkludon: la ĉefa celo de la aŭtoro estis esplori la fenomenon de miraklo. Hm ... Sed ja ĉiuj fabeloj uzas miraklon, kaj en tiu senco esploras ĝin, ĉu ne? Kompreneble – por instruaj, distraj, alegoriaj kaj pluraj aliaj celoj. Sed Westover faras alion: li rigardas la imagitajn situaciojn de la ekzisto de la mirakloj apud ni kaj sekvas la homajn reagojn al ili.</p> 
<p>Tiaj estas la fabeloj <i>Marvirinstrato</i>, <i>Pigopago</i>, <i>Fiŝhepatoleo</i>, <i>Meduzet'</i> kaj kelkaj aliaj. La esplorataj en ili demandoj estas, ekzemple, tiaj: „Ĉu ino, simila al marvirino, vere estas tio, kaj ĉu marvirinoj vere vivas apud ni?”; „Ĉu la patrino ĝojos, se la filino ricevos donacon de feino?”; „Kio pli bonas – la fidela pajloĉapelo aŭ mirakla rimedo, permesanta paŝi sur akvosurfaco?”; „Ĉu oni akceptas trankvile la ekziston de racia meduzeto apud knabino?” kaj simile. La respondoj estas diversaj kaj paradoksaj – kaj ĝuste tiuj fabeloj konsistigas la plej bonan parton de la libro. Unu fabelo – <i>Nia bestoĝardeno</i> – iom tro rekte provas respondi la ĉefan demandon: „Kio ĝenerale estas miraklo?” La respondo, kiun donas la aŭtoro, estas iom banala, sed prezentita elegante. Proksimas al ĝi laŭ la tasko kaj solvo <i>El la taglibro pri la okcidenta vojaĝo</i>.</p> <p>Kelkaj aliaj fabeloj estas iom pli tradiciaj (kvankam ankaŭ ili enhavas paradoksojn) – ili traktas neantaŭvideblajn homajn dezirojn. Tio estas, ekzemple, <i>Orfiŝeto kaj glacia monto</i> kaj <i>Labirintoj</i>. Min ili impresis iom malpli – eble ĝuste pro la troa tradicieco, sed tio estas afero de la gusto.</p> <p>Iom aparte staras tri noveloj-fabeloj, inspiritaj de la esperanta kulturo. Unu el ili estas stranga knaba historio, tre usoneca stile, inspirita de „enigma” zamenhofa frazo pri „Tri ruĝaj knabinoj”. La afero devenas, fakte, de certa mistraduko, sed ne tio gravas ĉi-okaze: ni vidas, kiel tuja imago pri la knabinoj naskis ĉe la aŭtoro grandan knabpsikologian historion ... La historio estas strangeta (jes ja, strangeco ĝenerale estas trajto de la stilo de Westover!), sed atentokapta, kaj ĝuste ĝi pruvis al mi tion, ke la aŭtoro, kvankam komencanta, ja havas estimindan verkistan talenton.</p> <p>La dua esperant-tema novelo – <i>Estimata Vi</i> – „verdece” traktas la ĉefan temon pri mirakloj: „Ĉu esperantisto ĝojos, se ricevos donace tutan Esperanto-urbon, sed senhoman?” La solvo estas proksimume sama, kiel en la fabelo <i>Fiŝhepatoleo</i>. Kaj la fina novelo <i>Antaŭparolo al la plena verkaro de Yvette Swithmoor</i> estas sprita parodio pri la Esperanto-kulturo ĝenerale, montranta profundan konatecon de la aŭtoro kun nia literaturo, kritiko, moroj ktp. Min la gaje-galaj rikanoj de Westover pacigis kun ĉiuj ne tro gravaj mankoj de la kolekto!</p> 
<p>Kaj jen la lasta strangaĵo de la libro, absolute nekutima (dume) en la mondo: la aŭtoro kaj la eldonejo rajtigas ĉiun homon senpage legi la libron en la reto (kie ĝi troviĝas plene), elŝuti ĝin kaj uzi laŭplaĉe! Tiaspeca donaco estas io tute nova ... Malgraŭ tio, ke la homoj, kutimaj legi de l' ekrano, ne havas stimulon aĉeti la paperan eldonon, espereble, sufiĉos en la mondo tiuj, kiuj preferas foliumi la paperajn librojn ... Do, ili aĉetu – kaj la avaruloj legu senpage!</p> 
