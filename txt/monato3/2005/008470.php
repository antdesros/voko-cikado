      <h3 class="tem"></h3>
      <h2>Poezia trezoro</h2>
      <!-- komenco de la enhavo -->

       

     <p>Malofte aperas libro da poemoj, kiun mi povas legi ne nur kun plezuro, sed ankaŭ kun admiro. Rimarkinde, ĉi tiu poemaro estas verkita laŭ la klasika modo, laŭ metro, rimoskemoj, per konataj vortoj, tamen ĝi impresas kiel profundanima, animtuŝa, plurfoje dum la jaroj releginda verko. Ĝuste nun mi ne povas rememori simile rimarkindan poezilibron lastatempan. Krome, temas pri originalaj poemoj, ne tradukitaj.</p> <p>Mikaelo Giŝpling estas eks-rusa judo, kiu migris al Israelo, kie li trovis ne la sopirindan paradizon (kian mispromesis siamaniere la sovetia reĝimo kaj alimaniere la cionismo), sed egale milit-timan landon, en kiu loĝas, nu, ordinaraj homoj, spertantaj la normalajn problemojn de la homa vivo, kvankam en etoso iom alispeca.</p> <p>Naturan pesimismon ni trovas en la poemoj, konforme al la mondsituacio aktuala. Sed pli fortas la reakcio de la animo al tiu pesimismo. Jes, grandparte la poemoj estas malkaŝe bazitaj sur personaj spertoj. Sed mankas la egoismo, kiu plejofte plenigas tiajn poemarojn. La aŭtoro sukcese universaligas la senton de tiuj personaj spertoj, kaj tio devas roli en nia aprezo.</p> <p>Mi diris, ke la verkomaniero estas klasika. Kiu legis mian kritikon pri la verkoj de Dante, eble memoras mian propran malkontenton pri tiu verkomaniero. Tute alie ĝin taksas nia aŭtoro:</p> <p>„Liberaj versoj estas kuriozo / Aŭ eble speco de anomali' ... / Al mi nevole venas la supozo, / Ke ili estas ordinara prozo / versforme situanta sur foli'.”</p> <p>„Do fuĝu mi de l' poezi-renverso. / Al la muziko strebas la anim', / Al la kutima melodia verso – / Ĝin regas ritmo kaj beligas rim'”, diras Giŝpling sur paĝo 99.</p> <p>Nu, ankaŭ aliaj poeziaj formoj kaj manieroj ekzistas, kaj verŝajne ankoraŭ aliaj povus ekzisti, aparte de la klasika poezio kaj de la proza pseŭdopoezio. Sed mi devas konfesi, ke la poemoj de Giŝpling konvinkas min, kontraŭ mia volo, ke ankoraŭ povas la klasika maniero estigi majstropoemojn.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Mikaelo Giŝpling: Tie ĉi tie. Eld. FEL, Antverpeno, 2003. 104 paĝoj broŝuritaj. ISBN: 90 77066 10 1.</h6>  
