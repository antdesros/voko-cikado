      <h3>Moderna vivo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Per patrina amo</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Ŝi riskis sian vivon por esti patrino. Ŝi postvivis grandan katastrofon: antaŭ kvar jaroj ŝi estis 25-jara, en bela aĝo por junulino. Sed malfeliĉe ŝi velkis kiel floro en vintro, tiel ke ŝi havis flavan vizaĝon kaj marasman korpon. Kio okazis?</b></p> <p>Komence ŝi ne atentis pri sia sano. Sed poste pli kaj pli grandiĝis ŝia ventro kaj ŝi vizitis kuraciston. Ekzameno montris, ke ŝia hepato terure malsaniĝis. La kuracisto diris, ke ŝi ne travivos unu monaton sen hepata grefto. Feliĉe, ŝi baldaŭ estis operaciita kaj venkis la malsanon. Travivinte la danĝeron, ŝi fariĝis ordinara virino: ŝi edziniĝis kaj, unu jaron poste, ŝi trovis, ke ŝi estas graveda.</p> <p>Gravediĝo kaj nasko estas danĝeraj por virinoj. Kaj ŝi, suferanta de hepata malfunkcio, estis altirata de la morto. Ŝi bone sciis la aferon, sed ŝi aspiris esti patrino kaj, spite riskon, ŝi elektis patriniĝon. En marto 2004 kuracisto trovis, ke malfortiĝis la moviĝo de la feto. Tiam la virino suferis ankaŭ de kolestazo, kiu povas facile kaŭzi subitan morton de la feto. Kuracistoj faris al ŝi cezaran operacion: naskiĝis knabo tre debila, pezanta du kilogramojn kaj 42 centimetrojn longa.</p> <h3>Spirigilo</h3> <p>La knabeto ne estis malnormala, sed ne havis instinktan spiradon, kio povas kaŭzi cefalan damaĝon kaj pulman sangadon. Oni donis al li spirigilon. La virino sciis nenion pri la sanstato de la fileto, ĉar ankaŭ ŝi troviĝis en danĝero. Ŝi volis vidi la bebon, sed la kuracistoj kaj la edzo diris, ke pro tro frua naskiĝo la bebo devas esti sub speciala flegado en alia ĉambro.</p> <p>Ŝi do urĝis la edzon rigardi la fileton kaj demandadis pri la infano: Al kiu li similas? Kiel li fartas? Foje ŝi diris, ke ŝi vidis lin en sonĝo, sed la filo ne reagis. Pasis sep tagoj. Ŝia sanstato iom post iom boniĝis kaj ŝi insistis rigardi la filon. Tiu ankoraŭ estis en danĝero sed la kuracistoj kaj la edzo ne povis rifuzi.</p> <p>La okan tagon la virino venis al la speciala ĉambro. Ŝi vidis en oksigena kesto la ŝrumpan violan bebon kun multaj tuboj sur la korpo. Ŝi larmis senbrue. Ŝtona silento regis en la ĉambro. Oni ne sciis, kiel konsoli kaj klarigi al la virino suferanta de korŝira doloro. Ŝi malfermis la keston, enŝovis la manon kaj ekkaresis la malgrandan korpon kaj premis la piedetojn. Ŝi tenere karesis la bebon kiel valoregan delikatan trezoron.</p> <h3>Miraklo</h3> <p>En tiu momento la aero en la ĉambro kvazaŭ solidiĝis. Okazis miraklo: la bebo unuafoje malfermis la okulojn. La kuracistoj kaj flegistoj ekĝojegis kaj singulte ekploris la edzo. Jam la naŭan tagon la bebo ne bezonis spirigilon: tiam restariĝis la funkcioj de vivo.</p> <p>La 12an tagon la virino forlasis la hospitalon kun la fileto, akirita kontraŭ vivo-risko kaj revivigita per patrineco. Multaj ĵurnaloj de Ĉinio raportis pri la virino, kiu nomiĝas Luo Jiwei kaj loĝas en Yanjin de Yunnan-provinco en sud-okcidenta Ĉinio.</p> <div style="text-align:right">Z<span class="mm">IYU</span></div> 
