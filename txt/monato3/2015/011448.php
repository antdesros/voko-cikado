      <h3>Lingvo</h3>
      <h3 class="tem">IRLANDO</h3>
      <h2>Aperigi (librojn) por ne malaperigi (lingvojn)</h2>
      <!-- komenco de la enhavo -->

       

       <p>Unu el la plej promesplenaj aspektoj de la tuttera teksaĵo estas tio, ke unuafoje
   en la lastatempa historio de la mondo ni nun havas efikan ilon por kontraŭstari la
   malaperon de pli kaj pli da idiomoj. La aktuala fenomeno, kiu koncernas amasan morton
   de lingvoj, minacas la buntan mozaikon de kulturoj, kiu riĉigas la spiritan vivon
   de la homaro. Tamen la fakto, ke hodiaŭ iu ajn povas libere krei retajn paĝojn kaj
   uzi la interreton kun la celo komuniki kaj aperigi informojn pri – kaj en – sia propra nacia aŭ etna lingvo, eble donas iom da espero, ke endanĝerigitaj lingvoj
   ne nepre estingiĝos.
</p> 
<h3>Irlanda eldonejo</h3> 
<p>Nu, en malgranda urbeto de okcidenta Irlando situas unika eldonejo, kiu proponas librojn
   por plenkreskuloj kaj infanoj, presitajn en mirinde granda gamo de lingvoj, dialektoj
   kaj subdialektoj. Temas pri tiel nomata 
   <i>Eldonejo Evertype</i>, en la eta urbo Cathair na Mart [kaher na mart], anglalingve Westport. Ĝi presas
   siajn eldonaĵojn en allogaj, ofte ilustritaj formoj kaj en nekredeble vasta nombro
   de idiomoj.
</p> 
<p>Tio estas aparte notinda en la fako de porinfana literaturo; ekzemple la monde konata
   libro 
   <i>Alice in Wonderland</i> (<i>La aventuroj de Alico en Mirlando</i>), de la brita aŭtoro Lewis Carroll, estas havebla ĉe 
   <i>Evertype</i> ne nur en la originala angla lingvo, sed ankaŭ en la franca, la germana, la kornvala,
   la irlanda, la skotgaela, la itala, la dana, la latina, la manksa, la skota, la sveda,
   la kimra, la malaltgermana, la belarusa, la hungara, la ladina, la valona, la tonga,
   la zulua, la havaja, inter aliaj.
</p> 
<p>Plie, ĝi presas en pluraj obskuraj kaj multe malpli konataj lingvoj kaj dialektoj,
   kiel ekzemple la dialekto de la apalaĉa regiono de Usono, la menonita germana (mem
   usona), la pikarda, la ŝetlanda, la normand-franca dialekto de Ĵersejo (manik-markola
   insulo), la ulster-skota, la diversaj lokaj subdialektoj de Skotlando kaj de aliaj
   eŭropaj popoloj ktp. Ĝia listo de publikaĵoj inkluzivas ankaŭ planlingvojn kaj imagajn
   projektojn, kiel Volapuko, Esperanto kaj la tiel nomata „Lingwa de Planeta”. Eble unu el la plej nekutimaj eldonaĵoj estas versio de 
   <i>Alico en Mirlando</i> presita en la alfabeto Deseret, inventita en la 19a jarcento de mormonoj en Utaho.
</p> 
<h3>Endanĝerigitaj lingvoj</h3> 
<p>Tiu eldonejo konstante aperigas literaturon (fikcian kaj nefikcian) por plenkreskuloj
   en simile granda gamo de lingvoj pli-malpli gravaj, pli-malpli disvastigitaj kaj konataj.
   Entute ĝi prezentas tre riĉan legan fonton por ĉiuj, kiuj deziras posedi librojn en
   sia propra idiomo, kiuj deziras lerni aliajn lingvojn aŭ simple ŝatas kompari tekstojn
   en diversaj lingvoj.
</p> 
<p>Precipe, ĉi tiu eldona aktiveco estigas valoran, pozitivan kaj praktikan kontribuon
   al la konservado de malaperantaj kaj endanĝerigitaj lingvoj. Fine kaj parenteze: ĉi
   tio ne celas esti komerca reklamo, kaj la aŭtoro havas absolute nenian rilaton (krom
   admiron!) al la menciita eldonejo. Li simple aĉetis plurajn publikaĵojn el ĝi kaj
   estis tre kontenta pro la kvalito de la tradukoj, de la presado kaj de la bindado
   (ĝenerale per paperaj kovriloj).
</p> 
