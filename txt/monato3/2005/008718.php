      <h2>La gaela romanarto</h2>
      <!-- komenco de la enhavo -->

       

       <p>Mirinda estas la fakto, ke Esperanto, lingvo, kiu havas ne multe pli ol jarcenton de ekzisto kaj estas parolata de malgranda frakcio de la monda loĝantaro, estigis tiom multe da bona literaturo. Certe, tia riĉeco parte ŝuldiĝas al tio, ke al ĝi kontribuis kaj plu kontribuas verkistoj el multaj landoj en ĉiuj kontinentoj. Ĝiaj poezio kaj novelarto estas aparte abundaj kaj admirindaj; malpli abundaj estas la originalaj romanoj en la lingvo.</p> 
<p>Eĉ pli unika fenomeno estus situacio, en kiu laŭdire mortanta lingvo de eta kaj konstante ŝrumpanta minoritato subite komencus estigi romanojn de eksterordinare alta kvalito. Ĝuste tio estas okazanta nuntempe en la malgranda gaela komunumo en Skotlando.</p> 
<p>La gaela lingvo aktuale ekzistas en du variantoj, nome la irlanda, laŭkonstitucie la unua oficiala lingvo (kvankam ne la plej parolata) de la Irlanda Respubliko, kaj la skota, kies denaskaj parolantoj estas nun eta malplimulto, vivante precipe en nordokcidenta Skotlando. (Antaŭ jaroj mortis la lastaj denaskaj parolantoj de la tria gaela varianto, nome la manksa, indiĝena lingvo de la Manks-insulo, kiu troviĝas inter Irlando kaj Anglio.) Malgraŭ la malkuraĝiga situacio rilate al la nombro de denaskaj gaelparolantoj, la nombro de lernantoj de la gaela, kaj en Skotlando kaj en Irlando, lastatempe kreskis en preskaŭ ĉiuj regionoj.</p>
<p>Irlando estas tutmonde konata kiel la naskiĝlando de kelkaj el la plej gravaj kaj influhavaj verkistoj en la angla lingvo: Oscar Wilde, James Joyce, Bernard Shaw ktp, sed ĝiaj gaelaj verkistoj estas malpli konataj eksterlande. Plie, la kvanto de originalaj irlandgaelaj romanoj estas relative malgranda, kaj ilin legas malmultaj homoj, eĉ en Irlando mem. Aliflanke, la klasika literaturo en la gaela lingvo estas antikvega, precipe se oni agnoskas, ke multe da antikvaj irlandaj tekstoj ekzistis en formo de buŝaj tradicioj (eposoj, legendoj, rakontoj ktp), transdonitaj dum multaj jarcentoj, de generacio al generacio. Materialo el tiaj tradiciaj fontoj estis konservita – almenaŭ ĝis la unuaj jaroj de la dudeka jarcento – en la memoro de tre maljunaj homoj en ambaŭ gaelaj landoj.</p> 
<p>De tempo al tempo novaj romanoj aperis en la du variantoj de la lingvo, sed ĝia hodiaŭa florado en Skotlando estas aparte surpriza. Malgraŭ la longa historio de la gaela literaturo, romanoj, en la moderna senco de la vorto, aperis tre malfrue. Kontraste, simile al la okazo de literaturo en Esperanto, sufiĉe multe disvolviĝis la novelarto en la gaelaj landoj. La unua skotgaela romano <i>Dùn Àluinn</i> (Belkastelo) de Iain Mac Cormaic [mahk kormihk] aperis en la jaro 1912. En la periodo inter la du mondmilitoj estis eldonitaj malmultaj romanoj en la skotgaela, kaj dum la dua mondmilito gaelaj verkistoj estis tro okupitaj pri aliaj urĝaj aferoj.</p> 
<p>En la jaro 2003 fulme aperis sur la literatura horizonto la unua romano, <i>An Oidhche mus do Sheòl Sinn</i> (La nokto antaŭ nia forvojaĝo) de Aonghas Pàdraig Caimbeul, ĝis tiam konata kiel ĵurnalisto. Ĉi tiu multdimensia verko ampleksas, inter aliaj aferoj, la historion de diversaj anoj de la sama insula familio, la mondmilitojn, epizodon dum la hispana enlanda milito, tentojn kaj dubojn pri religio de juna katolika pastro (membro de la familio kaj protagonisto de la romano), gaelan tradicion kaj hispanan poezion, citaĵojn el aliaj lingvoj, personajn pensojn de la aŭtoro ktp. Ĝi estis bonvenigita kiel eble la plej elstara romano en la lingvo. Eĉ pli laŭdata, tamen, estas la dua gaela romano, verkita de Caimbeul kaj eldonita en 2005, kies titolo estas <i>Là a' Dèanamh Sgeil do Là</i> (Tago al tago transdonas diron). Per tiuj du verkoj la moderna gaela romanarto unuafoje atingis nivelon kompareblan kun tiu de aliaj gravaj verkoj de la monda literaturo. Recenzanto en la ĵurnalo <i>An Gàidheal Ùr</i> skribis: „Ĉu ne estas strange, ke aperas kelkaj el la plej bonaj tekstoj, precize kiam la gaela lingvo mortas sur la lipoj de la popolo?”</p> <p>Ĉu fina glora novaa brilego de mortanta stelo aŭ revigliĝo de eta kaj preskaŭ forgesita kulturo? Estus interese esplori, ĉu simila fenomeno okazas en aliaj regionoj, kie estas endanĝerigitaj minoritataj lingvoj.</p> <div style="text-align:right">Garbhan M<span class="mm">AC</span>A<span class="mm">OIDH</span></div>   
