      <h3>Spirita vivo</h3>
      <h3 class="tem">MADAGASKARO</h3>
      <h2>Ŝtatlaikeco minacata</h2>
      <!-- komenco de la enhavo -->

       

     <p>Kiuj ajn estas la regantoj en Madagaskaro, la Monda Banko (MB) kaj la Internacia Mona Fonduso ĉiam havas bonegajn rilatojn kun ili, ĉar ili sekvas laŭvorte la drastajn kondiĉojn, postulatajn de ambaŭ institucioj. Ĉio aspektas normale ĝis tiu ĉi punkto, sed kiel sin teni, kiam la loka reprezentanto de MB ekzemple publike deklaras personan apogon al la nuna prezidento? Kio des pli skuis la mondon politikan, tio estas la rekta engaĝiĝo de MB en la helpo al eklezioj.</p> <p>Antaŭ du monatoj nia prezidento vokis ĉiujn pastorojn en la ĉefurbon por trejni ilin pri la ŝtata afero. Novaj taskoj estis konfiditaj al tiuj pastoroj: ili nun iĝas la okuloj kaj oreloj de la ŝtato, ili okupiĝu ankaŭ pri ekonomia agado en la loko, kie ili deĵoras, por plenumi la devizon de la prezidento: „rapida kaj daŭra progreso”. Fakuloj instruis al ili serĉi eblojn pri mikrokredito por starigi etajn entreprenojn. Ili devas ankaŭ helpi la registaron batali kontraŭ la koruptado. Naŭcent milionojn da malagasaj frankoj provizis al la registaro MB por realigi la dutagan kunvenon en Antananarivo. Tio estas nur unu ekzemplo el pluraj pri tio, kiel la ŝtato enmiksiĝas en la religiajn aferojn kaj inverse.</p> <p>Ŝtatlaikeco, t.e. ke la ŝtato sin retenu de religiaj aferoj kaj inverse, iĝas batalĉeftemo en la senĉesa polemiko fare de la opozicio al la nuna gvidantaro. Ili kritikadas la malrespekton de la konstitucio flanke de la prezidento, kiu principe ne plu rajtas okupiĝi pri iu alia afero krom estri la nacion, – aludo al lia vicestreco ene de la protestanta eklezio FJKM. Menciindas, ke kristanaj eklezioj disetendiĝas sur la malagasa teritorio, ĝis la plej profunda nealirebla regiono. Sur ili apogis sin la prezidento dum la lasta baloto en 2001. Probable jen estas lia maniero danki la subtenintojn. Sed aliaj observantoj opinias, ke helpo al kristanaj eklezioj signifas ankaŭ starigado de ŝildo kontraŭ la kresko de la islama influo precipe dum la antaŭa reĝimo. Tamen ne ĉiuj kristanaj eklezioj profitas el la prezidenta malavareco, nur tiuj, kiuj membras en FFKM, la unuiĝo de protestanta, romkatolika, luterana kaj anglikana eklezioj, kiu delonge provas marĝenigi aliajn „sektojn”.</p> <p>El historia vidpunkto, la malagasaj religiuloj ja kutimas esprimi politikan pozicion, sed la nuna „kunlaboro” de la registaro kun eklezioj ne plu havas limon. La naciestro memdecide donacis plurajn milionojn al iuj lokaj paroĥoj, li konstruis preĝejon, la kardinalo deklaris, ke li subtenas la nunan registaron, ĉar ĝi alportas palpeblan progreson al la popolo ... El 17 milionoj da malagasoj nur pli-malpli 4 milionoj apartenas al la kristana komunumo, islamo duarangas kaj la plimulto alkroĉiĝas al la tradicia religio.</p> <div style="text-align:right">F<span class="mm">IDILALAO</span> Henriel</div> 
