      <h3>Ŝerco kaj satiro</h3>
      <h3 class="tem">BIBLIO</h3>
      <h2>Kiom varmega estas la infero?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Du sciencistoj eldonis ŝajne seriozan raporton kaj konkludis, ke infero brulas je 445 gradoj celsiaj. Jorge Mira Perez kaj José Vina, fizikistoj en la Universitato de Santiago de Compostela en Hispanio, uzis la Biblion por determini la varmecon de ĉielo kaj infero. Ili eltrovis, ke ankaŭ ĉielo estas diable varmega, 232 gradoj.</p> <p>Por eltrovi la inferan temperaturon la sciencistoj uzis la libron Apokalipso de sankta Johano: la nekredantoj estas „ĵetitaj en la fajran lagon ... la lagon fajran kaj sulfuran”. Do, la temperaturo de infero estas je la bolgradpunkto de sulfuro, nome je 445 celsiaj gradoj.</p> <p>La sciencistoj eĉ konsultis la episkopon de Madrido por konfirmi, ke iliaj interpretadoj de la bibliaj vortoj estis ĝustaj. Pri la temperaturo de la ĉielo ili legis la vortojn de Jesaja, kiu priskribas la brilegon de la suno kaj la luno tiel: „La lumo de la luno estos tiel brila kiel la suno, kaj la lumo de la suno estos sepobla”, do 232 gradoj.</p> <h3>Demando en universitata ekzameno</h3> <p>En la Universitato de Oklahomo (Usono), jen la fina demando en ekzameno por studentoj de kemia inĝenier-arto: „Ĉu la infero estas ekzotermika aŭ endotermika? Subtenu vian respondon per pruvo.”</p> <p>Nur unu studento serioze provis la demandon. Li skribis: „Unue ni devas postuli, ke, se ekzistas animoj, tiuj devas havi mason. Poste, ni devas demandi pri la nombro de animoj, kiuj eniras inferon, kaj la nombro de tiuj, kiuj foriras el infero. Ni certe povas supozi, ke, se animo eniras la inferon, tiu ne foriras; do, neniu animo foriras. Pri kiom da animoj eniras la inferon, ni devas ekzameni la diversajn religiojn.”</p> <p>„Iuj el la religioj asertas, ke, se vi ne estas membro de ilia religio, vi ja eniras inferon. Ĉar troviĝas pli ol unu religio, tial ni povas supozi, ke ĉiuj homoj, kaj tial ĉiuj animoj, iras al infero. Tial, la nombro de animoj konstante pligrandiĝas. Do, ni konsideru la rapidecon de la ŝanĝo de volumeno de infero. La regulo de Boyle asertas, ke, por ke la temperaturo kaj premo en infero restu samaj, necesas, ke, la proporcio de la maso de animoj kaj volumeno de infero restu konstanta”, daŭrigas la studento.</p> <p>„Do: a) Se infero ekspansias pli rapide ol la rapido de animoj enirantaj la inferon, tiam la temperaturo kaj premo falos ĝis la infero frostiĝos aŭ b) se la infero ekspansias pli malrapide ol la rapido de animoj enirantaj inferon, tiam la temperaturo kaj premo pligrandiĝos ĝis la tuta afero liberiĝas,” atentigas la studento.</p> <p>Li ne povis decidi pri la demando, sed supozis, ke infero estas ekzotermika pro lia sperto kun junulino. Dum lia unua universitata jaro ŝi rifuzis sekskuniĝi kun li kaj li neniam poste sukcesis. Ŝi estis dirinta: „Estos malvarma tago en infero antaŭ ol mi dormos kun vi!”</p> <p><i>QED!</i><sup>1</sup></p> <div style="text-align:right">Vilĉjo S<span class="mm">IMCOCK</span></div> <h6 class="pied">1. <i>Quod erat demonstrandum!</i> (latina: Kio estis demonstrenda!)</h6> 
