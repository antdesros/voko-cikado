      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Poŝkomputiloj kaj klasĉambroj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Dum la somero mi ĝuis la privilegion instrui Esperanton (jam la kvaran fojon) ĉe la
   Nord-Amerika Somera Kursaro (NASK) ĉi jaron okazigita en Nord-Karolino. Se vi ankoraŭ
   ne spertis ĉi tiun intensan, ok-tagan kurson (kun aŭ sen mi!), mi ĝin rekomendas.
   Alia pluso: ĝi kunligiĝas kun Kino-Teatro-Festivalo (KTF) – stimula kaj krea evento.
</p> 
<p>Sed, momenton. Jam aŭdiĝas plendoj. Ĉu M<span class="mm">ONATO</span> ne fanfaronas esti peresperanta, ne priesperanta revuo? Kial, diable, aboni (aŭ pruntepreni),
   se mi povas legi la samon en primovada revuo?
</p> 
<p>Paciencon, mi petas. Evidente la ĉefredaktoro de M<span class="mm">ONATO</span> tre bone scias, kian revuon li ĉefredaktas. Ĉu ne ĉiun matenon, antaŭ ol matenmanĝi,
   mi ripetadas la mantron „peresperanta, ne priesperanta”?
</p> 
<p>Tamen gravas la esperanta, nord-amerika fono. Ĉar, instruante en Nord-Karolino, mi
   spertis unuafoje la ... kion? ... povon? utilon? konkurencon? tiranecon? de la enklasĉambra
   poŝkomputilo.
</p> 
<p>„Poŝkomputilo”. Oni parolas pri „saĝtelefono”, pri „poŝtelefono”, sed ĝenerale ĝi uzatas ne telefone sed komputile. Krome „saĝa” ĝi ne estas: nur homoj ĝin saĝigas aŭ, miaokaze, pro nelerto, ĝin malsaĝigas.</p> 
<p>Evidente, ĉe la universitato, kie mi instruas en Britio, la studentoj enĉambrigas
   diversspecajn komputilojn. Malgraŭ malfortaj, malsukcesaj provoj, ne eblas malpermesi
   elektronikajn aparatojn. Registritaj estas 15 % de la studentoj (la nombro jaron post
   jaro kreskas) kiel psike aŭ korpe handikapitaj. Oftas disleksio, sed ne nur: aŭtismo
   aŭ sindromo Asperger notindas. Do pro medicinaj kialoj multaj nepre uzu komputilojn.
</p> 
<p>Krome raportas studentoj, ke ili preferas fari dumprelegajn notojn komputile, ne mane.
   Malofte ili manskribas, sed ofte komputilas. Apenaŭ imageblas la vekrioj pri dolorantaj
   manoj, kiam studentoj dum du-hora ekzameno devis skribi mane. Nur bierskatolojn ili
   tenas, ne skribilojn.
</p> 
<p>Sed, kaj jen la diferenco inter mia kutima brita sperto kaj mia nekutima nord-amerika,
   miaj universitataj studentoj ankoraŭ ne uzas siajn komputilojn por kontroli, kion
   mi diras. Jes, ili skribas siajn notojn, jes ili kaŝe, subtable, tekstas, pepas, fejsbukas
   ... tiel ke, ilin vidante, mi fojfoje demandas min, kiel fakte ili subtable okupas
   siajn fingrojn.
</p> 
<p>Tamen ĝis nun ili neniam, mi ripetas, neniam, levis la manon por interveni per freŝaj
   faktoj, korektitaj konoj, tuj ĉerpitaj el Interreto.
</p> 
<p>Aliflanke la spertaj, engaĝitaj esperantistoj, kun kiuj mi plezure kunlaboris ĉe NASK,
   pli avangarde, pli saĝe, aplikis siajn poŝkomputilojn. Kiam vorto diskutendis, jen
   tuja PIV-difino; kiam romano-titolo, dato serĉendis, jen tuja informo; eĉ, kiam mi
   fuŝis statistikojn, tuj ekvibris la komputileto en mia poŝo – iu, kiel mi poste konstatis, tre diplomatie kaj ne publike, sendis la ciferojn, kiujn
   mi vane provis eltiri el mia kompatinda, neelektronika menso.
</p> 
<p>Jen, por mi, nova kaj, sincere, iom timiga spertaĵo. Inter amikoj, inter esperantistoj,
   mi povas pli facile akcepti tion, kaj danki pro la riĉigo de la klasĉambra interŝanĝo
   de ideoj. Ekster la esperanta klasĉambro, tamen, ekzemple kun miaj kutimaj studentoj,
   mi ne tiom certas.
</p> 
<p>Dum 40 jaroj en diversaj instruejoj mi kutimis – ĉu bone, ĉu malbone – suvereni. Nepras, aparte en lernejoj, eventuale malpli en universitatoj, ke instruatoj
   fidu instruantojn. Se malaperas tiu ĉi fido, se lernantoj ekopinias, ke danke al poŝkomputilo
   ili subite pli scias ol la pedagogo, aŭ – terure! – malice plagas tiun per kromaj kaj aldonaj scioj, riskas kolapsi la klasĉambra disciplino.
</p> 
<p>Mi vidis la klasĉambran estontecon, kaj mi ne scias, ĉu ĝi plaĉas. Intelekte mi scias,
   ke necesas brakumi progreson, ke enestas en dumleciona poŝkomputilo-uzo multo aprobinda.
   Sed emocie ... jen io alia. Cedi lokon al poŝkomputilo, almenaŭ kun ĝi konkurenci
   ... prefere, ne.
</p> 
<p>Jen defio por nova instruisto-generacio: kiel primajstri ĉi tiun klasĉambran koboldon.
   Defio verŝajne ne por mi. Pli kaj pli mi sentas min pelata al la pordo de la emeritiĝo.
   Ne tuj, kvazaŭ sendata poŝkomputila mesaĝo, sed baldaŭ.
</p> 
