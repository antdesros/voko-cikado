      <h3>Fokuse</h3>
      <h3 class="tem">SVISLANDO</h3>
      <h2>Ĉarnira urbo</h2>
      <!-- komenco de la enhavo -->

       

         <p>
   <b>Jen la lasta el tri artikoloj, kiuj fokusiĝas pri Svislando.</b>
</p> 
<h3>Kantono Neuchâtel</h3> 
<p>La Chaux-de-Fonds troviĝas en la ĵuras-montara parto de la kantono Neuchâtel (176 000
   loĝantoj en 2014), kiu etendiĝas de la svisa-franca landlimo ĝis la lago de Neuchâtel.
</p> 
<p>Estante en franclingva Svislando (Romandio), La Chaux-de Fonds kun siaj 40 000 loĝantoj
   troviĝas laŭnombre en la tria loko post Ĝenevo kaj Lausanne. Sian pasintecon ĝi ne
   ŝuldas al potencaj regantoj aŭ militistoj, sed al pacemuloj, idealistoj, kuraĝaj pioniroj
   kaj al kelkaj geniuloj.
</p> 
<p>La Chaux-de-Fonds estas samtempe nova kaj historia urbo. La arkitektura kerno de la
   urbo estas raraĵo el la 19a jarcento. En 1984 la svisa registaro decidis protekti
   ĝin, ĉar tie videblas diversaj arkitekturaj tendencoj, kiuj karakterizas la 19an kaj
   la komencon de la 20a jarcento. La Chaux-de-Fonds estas la sola urbo en Svislando
   konstruita laŭ la stilo 
   <i>Art Nouveau</i> (Arto Nova).
</p> 
<p>Temas pri ĉarnira urbo okupanta pozicion inter Svislando kaj Francio. Aŭtoŝoseoj,
   flughaveneto kaj ligo kun la franca rapidtrajna reto TGV ebligas kontaktojn kun najbaraj
   centroj. La Chaux-de-Fonds estas la ekonomia, teknika, komerca, kultura, lerneja,
   sporta, socia kaj medicina ĉefurbo de interkantona kaj interŝtata grandregiono kun
   pli ol 100 000 homoj.
</p> 
<h3>Industrio kaj kulturo</h3> 
<p>Miloj da laboristoj el najbaraj urbetoj kaj vilaĝoj svisaj kaj francaj ĉiutage venas
   por perlabori sian vivon en La Chaux-de-Fonds. La urbo notindas pro siaj eksportaĵoj,
   ĉefe pro la horloĝmarkoj Ebel, Corum, Tag Heuer, Ulysse Nardin, Cartier, Movado aŭ
   Girard-Perregaux. La urbo estas ankaŭ centro pri mikrotekniko kaj informadiko.
</p> 
<p>Notindas la urbaj muzeoj (ĉefe la internacia muzeo pri horloĝoj, kiu montras la historion
   de tempomezurado per 3000 objektoj). La teatro estas belaĵo italeca el la 19a jarcento.
   La industriaj kaj kulturaj instalaĵoj povas facile konkurenci kun tiuj de pli grandaj
   urboj en Svislando.
</p> 
<p>Ĉirkaŭe troviĝas altebenaĵo kun vastaj paŝtejoj kaj multaj abioj. La ĵurasa montaro
   ĉiujare allogas milojn da migrantoj, biciklantoj, ĉevalrajdantoj kaj promenadskiantoj.
   Ĉi tie grandparte mankas bruo, malpuraĵo kaj agreso kutimaj en grandaj urboj. Malgraŭ
   tio, tamen, la regiono ankoraŭ ne fariĝis centro de amasturismo.
</p> 
<h3>Interlingvistika arkivo</h3> 
<p>La Chaux-de-Fonds estas ankaŭ unu el la mondaj centroj por Esperanto. La urba biblioteko
   protektas la interlingvistikan arkivon CDELI (Centro de Dokumentado kaj Esploro pri
   la Lingvo Internacia).
</p> 
<p>Krome eblas diri, ke La Chaux-de-Fonds estas la plej alte situanta granda urbo de
   Eŭropo (ĉirkaŭ 1000 m super la maro); ke laŭstatistike ĝi ĝuas plej oftan sunradiadon
   en Svislando; ke ĝia akvo fluas al la rejna baseno kaj plue al la Norda Maro; kaj
   ke ĝi vivtenas la solan profesian teatron en Romandio (Romanda Popola Teatro).
</p> 
<p>Tie naskiĝis la horloĝisto kaj mekanikisto Pierre Jaquet-Droz (1721-1790), la romantika
   pentristo Léopold Robert (1794-1835), la piloto kaj aŭtokonstruisto Louis Chevrolet
   (1878-1941), la poeto, verkisto kaj vojaĝanto Blaise Cendrars (1887-1961), kaj la
   arkitekto, urboplanisto kaj humanisto Le Corbusier (1887-1965). Cetere, ekde 2009,
   la urbo estas mondheredaĵo de Unesko.
</p> 
