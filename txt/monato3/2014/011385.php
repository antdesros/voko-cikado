      <h3>Spirita vivo</h3>
      <h3 class="tem">KATOLIKISMO</h3>
      <h2>Kial la papo vizitis Albanion?</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 21an de septembro la papo Francisko vizitis Albanion. Tio estis la dua vizito de
   papo, se ni menciu la viziton de Johano Paŭlo la 2a en 1993, tiam akompanata de Patrino
   Tereza. Demandite de ĵurnalistoj pri la kialo de ĉi tiu vizito al ĉi tiu malgranda
   balkana lando, la papo Francisko respondis: „Albanio kaj albanoj estas bona modelo por la cetera mondo pri la religia harmonio,
   solidareco kaj kunlaboro”. Kaj tio estas vera.
</p> 
<h3>Paŭlo</h3> 
<p>Dum pluraj jarcentoj la albanoj ne konas interreligiajn kverelojn aŭ konfliktojn.
   Ili akceptis la kristanismon jam en la unua jarcento, kiam la apostolo Paŭlo mem vojaĝis
   tra Ilirio (vasta teritorio, kie loĝis niaj antaŭuloj). Do, la kristaniĝo ĉe ni estas
   iel apostoleca. La iliridevena Konstanteno la Granda estis la unua romia imperiestro,
   kiu komprenis la gravecon de ununura eklezio kaj toleris la kristanojn. Post la granda
   skismo interkristana, la albanoj apartenis parte al katolikismo kaj parte al ortodoksismo.
   Oni eĉ diras, ke en la Bizanca Imperio la solaj katolikoj estis ja albanoj (norda
   Albanio, Kosovo kaj Montenegro, se oni konsideru Kroation ekster la Bizanca Imperio).
</p> 
<h3>La rajto porti armilojn</h3> 
<p>Islamo eniris ĉi tien jam en la 14a jarcento el Hispanio kaj poste el la Otomana Imperio,
   la bektaŝismo dum la 19a jarcento. Oni akceptis la islamon amase, precipe la lastajn
   du jarcentojn de la kvincentjara otomana jugo, precipe pro kelkaj kialoj: por rajti
   porti armilon, kio estis tre grava por la albanoj, por ne esti devigata liveri po
   unu knabon ĉiudome por la janiĉaraj taĉmentoj, por ne pagi la imposton de kristanoj
   (ĝizo).
</p> 
<p>Ankaŭ la hebreoj ĝuadis dum la reĝlanda tempo (1928-1939) plenrajtojn religiajn. Post
   1990 la landon eniris bahaanoj kaj pluraj novaj kristanaj sektoj kiel protestantoj,
   atestantoj de Jehovo kaj evangeliismanoj.
</p> 
<h3>Islamana plimulto</h3> 
<p>La plimulton niaregione konsistigas islamanoj (en Albanio: 60 %, en Kosovo: 90 %,
   en la okcidenta Makedonio: pli ol 90 %); poste venas ortodoksuloj (en Albanio: ĉirkaŭ
   18 %, katolikoj 15 % kaj la ceteraj 7 %). Ortodoksuloj en Kosovo konsistigas 6 %,
   dum katolikoj 4 %; en Makedonio la ortodoksuloj estas ĉiuj slavmakedonoj.
</p> 
<h3>Komunismo</h3> 
<p>Dum la komunisma reĝimo en Albanio, precipe la katolika eklezio suferis eksterordinare,
   ĉar ĝi estis pli intime ligita al Vatikano kaj Okcidento. Dekoj da eminentaj katolikaj
   pastroj estis pafmortigitaj, enprizonigitaj aŭ internigitaj longajn jarojn. Unu el
   ili, pafmortigita en 1945, estis Dom Lazer Ŝantoja, esperantisto ekde la jaro 1907!
   Sed ankaŭ aliaj eklezioj multe suferis. Albanio komunista prezentas la unikan kazon
   enmonde, kie dum 25 jaroj (1967-1990) estis laŭleĝe malpermesataj la religiaj praktikoj
   kaj propagando, ĉiuj preĝejoj estis fermitaj aŭ detruitaj.
</p> 
<p>Pro tio, la granda erao de demokratiiĝo notiĝis unuavice per la revigliĝo de la religia
   agado. Oni komencis denove la mesojn kaj preĝojn en la malmultaj kultaj institucioj
   restintaj kiel kulturaj monumentoj kaj la konstruadon de novaj centoj da moskeoj kaj
   preĝejoj. Tio ebliĝis ankaŭ per la mono donita de eksterlando.
</p> 
<h3>Interreligia harmonio</h3> 
<p>La interreligia harmonio, solidareco kaj kunlaboro manifestiĝas en la interrilatoj
   de la homoj (reciprokaj geedziĝoj, vizitoj okaze de religiaj festotagoj, komunaj konferencoj,
   manko de reciprokaj ofendoj aŭ kalumnioj, protektado de la objektoj de kultodomoj
   de aliaj ktp). Ekzemple, dum la sieĝo de la urbo Shkoder, norde, fare de serbaj kaj
   montenegraj armeoj (1912-1913) islamanoj trovis rifuĝejon en la katolikaj preĝejoj;
   aŭ dum la maloportuna jaro 1997, kiam la ŝtato ruiniĝis dum kelkaj monatoj, islamanoj
   kaj kristanoj savis la objektojn de anoj de aliaj religioj, aŭ okazis, ke en iu kristana
   vilaĝo loĝis kelkaj islamanoj, kiuj ne povis iri al sia preĝejo, aliloke ili petis
   la servojn de la vilaĝa pastro, okaze de mortoj aŭ aliaj eventoj familiaj, kaj inverse.
</p> 
<p>La vizito de la papo sekvas la viziton de la Patriarĥo de Konstantinopolo kaj pluraj
   aliaj ortodoksaj orienteŭropaj estroj junie 2014 okaze de la sanktigo de la nova granda
   katedralo ortodoksa en Tirano. Tia paca kunekzistado inter diversreligiaj albanoj
   prezentas grandan popolan tradician trezoron, kiun nia malgranda lando alportos estonte
   en EU.
</p> 
