      <h2>Mond' eterne militanta</h2>
      <!-- komenco de la enhavo -->

       

       <p>En sia verko <i>La infana raso</i>, la skota poeto William Auld [ŭiljam old] pesimisme asertas: „Ni rigardu iom la faktojn: tria milito / dum nia jarcento estas neevitebla”.</p> <p>Se li antaŭvidis trian mondmiliton antaŭ la fino de la dudeka jarcento, li feliĉe malpravis. Sed ni scias, ke bedaŭrinde milito en la mondo neniam ĉesis, sed daŭras en longa senfina ĉeno de detruo kaj masakro.</p> <p>Post la korea milito sekvis la vjetnama (kiu siavice estis daŭrigo de la franca-hindoĉina). Post la vjetnama sekvis militoj en alia parto de la mondo: la golfa, la iraka (kie sekvis la iran-iraka), la nuna usona-afgana (kiu daŭrigis la sovetian-afganan).</p> <h3>Besto</h3> <p>Ni povus mencii aliajn lokajn militojn en Afriko, Azio, Sovetio, Balkanio kaj tiel plu, sed tio pli ol sufiĉas. La homo estas militema besto.</p> <p>La granda tragedio de la homaro estas tio, ke ĝi konstante ripetas la saman krimon. En sia versaĵo <i>milita kanto</i> angla poeto, John Davidson [ĵon dejvidsn], ĝuste asertis: „La milito rebredas la militon ”.</p> <p>Eĉ pli trafe notis la germana filozofo Hegel: „Kion la sperto kaj la historio instruas, tio estas, ke la nacioj kaj la registaroj neniam lernis ion ajn de la historio, nek agis laŭ ajnaj lecionoj, kiujn ili estus povintaj dedukti el ĝi.”</p> <h3>Romanoj</h3> <p>Imponas la fakto, ke kelkaj el la plej renomaj kontraŭmilitaj romanoj, kiuj estiĝis el la teruraĵoj de la unua mondmilito, devenas de aŭtoroj, kiuj mem spertis tiujn tempojn, kaj kiuj sendube estis edukataj laŭ la tradiciaj koncepto de patriotismo kaj ŝajna gloro de la milito.</p> <p>Impresas ankaŭ, ke tiuj verkistoj apartenis al diversaj landoj, ne nur la ĉefaj protagonistoj, sed inkluzivante ilin. Inter tiuj romanoj ni rememoru la verkojn <i>Nenio nova en la okcidento</i> de la germana aŭtoro Erich Maria Remarque [remárk]; <i>Mare nostrum</i> kaj <i>La kvar rajdantoj de la Apokalipso</i> de la hispana aŭtoro Blasco Ibáñez [ibánjes]; kaj du gravajn romanojn en Esperanto, nome: <i>Kiel akvo de l' rivero</i> de la franco Raymond Schwartz [ŝvarts], kaj <i>La granda kaldrono</i> de la skoto John Francis [ĝon fransis].</p> <h3>Kreinto</h3> <p>Oni ne povas nei, ke la idealoj de la kreinto de la lingvo, en kiu aperas ĉi tiu magazino, estas noblaj kaj imitindaj, sed la teksto de lia poemo <i>La espero</i> hodiaŭ ŝajnas tro naiva: „Al la mond' eterne militanta / Ĝi promesas sanktan harmonion”.</p> <p>Neniam la homaro atingos sanktan harmonion (eĉ ne per Esperanto), ĝis ni konvinkos internaciajn instancojn kaj regantojn de la nacioj, kiuj pretendas esti gardantoj de la paco, ke milito kaj armita interveno nenion solvas.</p> <p>Bedaŭrinde, la tragedio daŭras, kaj daŭros.</p> <div style="text-align:right">Garvan M<span class="mm">AKAJ</span></div> 
