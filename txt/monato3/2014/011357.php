      <h3>Lingvo</h3>
      <h3 class="tem">SKRIBSISTEMOJ</h3>
      <h2>Kio estas la kipra silabaro?</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>En majo kaj junio 2014 okazis en la koro de Limasolo (la dua urbo, laŭ grandeco, de
      la Kipra Respubliko) la interesa ekspozicio 
      <i>I majia tis grafis'</i> (La magio de la skribado). La redaktoro de la lingva rubriko, laŭdeve ekipita per
      fotilo, vizitis ĝin la 1an de junio ekskluzive por la legantaro de M<span class="mm">ONATO</span>, al kiu estos jene prezentitaj historio kaj kuriozaĵoj pri antikva kaj fascina skribsistemo,
      la tiel nomata „kipra silabaro”.
   </b>
</p> 
<h3>Komuna silabaro</h3> 
<p>Tio, kion oni konas en epigrafio kaj lingvistiko kiel kipran silabaron, estas silaba
   skribsistemo utiligita en Kipro inter la dua duono de la 11a jarcento a.K. (stango
   de Ofelto, ICS
   <sup>1</sup> 18g) kaj la fino de la 2a jarcento a.K. Ĝi konsistas el sesdeko da signoj (56, por
   la ŝatantoj de precizeco), ĉiu el kiuj respondas al aparta silabo, kun iom da lokaj
   variantoj laŭ la formo de la signoj mem, sed ankaŭ laŭ la kronologio kaj la strukturo
   de la diversaj trovaĵoj; preskaŭ ĉiuj signoj, escepte de tiu por „ye” (ekzistanta nur en la pli malfrua pafosa varianto), estas atestitaj en la plej disvastiĝinta
   formo de la kipra silabaro, pro tio cetere menciata en fakaj publikaĵoj kiel „komuna silabaro”.
</p> 
<h3>Sud-okcidenta regiono</h3> 
<p>La tekstoj skribitaj en ĉi-lasta, ordinara, speco de silabaro estas kutime maldekstrumaj
   kaj venas de areo tre ampleksa: ili estis malkovritaj praktike sur la tuta insulo,
   escepte de la sud-okcidenta regiono – tiu de Pafoso – siaflanke karakterizata de propra, iom malsama, signaro, konata kiel „la pafosa silabaro”. Ĉi tiu loka versio de la silabaro, havanta kelkajn apartaĵojn, estis ofte uzata
   por la skribado de tekstoj dekstrumaj kaj estas karakterizata de aparta kronologio:
   oni fakte parolas pri iu „frua pafosa silabaro”, esence dokumentita ĉe tekstoj el la 6a jarcento a.K., kaj pri iu „malfrua pafosa silabaro”, per kiu oni skribis dokumentojn, kiuj datumas ĉefe de la 4a jarcento a.K.
</p> 
<h3>Funebraj surskriboj</h3> 
<p>La konserviĝintaj tekstoj estas plurspecaj, kun klara hegemonio de funebraj kaj religiaj
   surskriboj; cetere, inter la (relative malmultaj) publikaj tekstoj oni havas kelkajn
   el la plej gravaj kaj konataj kipraj surskriboj, kiel ekzemple iun dulingvan tekston
   el Amathunta (ICS 196; ĉirkaŭ 310 a.K.) aŭ la tabuleton de Idalion (ICS 217; ĉirkaŭ
   478-470 a.K.). La plejmulto de la tekstoj ĝis nun konataj estis gravurita sur ŝtono;
   oftaj estas ankaŭ skrap-skribaĵoj kaj tekstoj pentritaj sur ceramikaĵoj, dum estas
   malpli multenombraj la tekstoj gravuritaj sur metalo (kia tamen estas la ĵus menciita
   tabuleto de Idalion).
</p> 
<h3>Komplika deĉifrado</h3> 
<p>Oni ne malofte legas, ke la kipra silabaro estis deĉifrita en 1871 de la angla epigrafisto
   George Smith, kiu ja helpis sin per la dulingva kaj duskribsistema surskribo trovita
   en Idalion (kiu respondas al nuna Dali, urbeto apud la ĉefurbo Nikozio). La komplika
   deĉifrado de la kipra silabaro tamen ne povas esti atribuita al unuopa persono, ĉar,
   se esti honestaj, sekvis pluraj klopodoj, kelkaj el kiuj vere brilaj, komence fare
   de kleruloj el diversaj fakoj kaj poste fare de filologoj kaj fakuloj pri la greka
   lingvo. Danke al la dulingva (duone semita kaj duone greklingva) surskribo el Idalion
   (dediĉita al Reshef Mikal – Apolono Amyklos: ICS 220; komenco de la 4a jarcento a.K.) la brita fakulo George
   Smith ja donis en 1871 unuan gravan interpreton; ĝi tamen estis poste evoluigita kaj
   perfektigita fare de la egiptologo Samuel Birch (1872), de la numismato J. Brandis
   (1873), de la filologoj Moritz Schmidt, Wilhelm Deeke kaj Justus Siegismund (1874)
   kaj fine de la dialektologo H. L. Ahrens (1876).
</p> 
<h3>Egea origino</h3> 
<p>La egea origino de la kipra silabaro estis ĝenerale rekonita ekde la fino de la 19a
   jarcento p.K., danke al la esploroj de Arthur Evans (plenumitaj ekde 1894), kiuj identigis
   la minoan originon de la silabaro uzata en Kipro en la bronzepoko. La kipra silabaro
   atestita en la ferepoko do devenas de la kipro-minoa silabaro. Iel ajn, post esplorado
   pli ol 100-jara la preciza rilato, kiu kunligas tiel diversajn skribsistemojn (linearan
   A, linearan B, kipro-minoan silabaron kaj kipran silabaron) ankoraŭ ne estas difinita
   detale pro persistaj mankoj en niaj scioj, nelaste pro tio, ke oni sukcesis deĉifri
   nek la linearan A nek la kipro-minoan.
</p> 
<h3>Ekonomia krizo</h3> 
<p>Post la falo de la micenaj centroj fine de la 13a jarcento a.K. pro la ekonomia krizo,
   kiu en tiu epoko frapis la orientan regionon de Mediteraneo, multaj gentoj transloĝiĝis
   de ĉe Egea Maro al apudaj regionoj, kiuj ankoraŭ floris el ekonomia vidpunkto. Multaj
   el ĉi tiuj alvenis kiel migrantoj al Kipro, kie ili enloĝiĝis en la urboj-havenoj
   de la insulo. Tiuj gentoj, venantaj de ĉe Egea Maro, portis kompreneble kun si la
   propran lingvon: la grekan.
</p> 
<p>Tiam en Kipro oni uzis lokan antikvan skribsistemon nomatan „kipro-minoa”, per kiu verŝajne oni skribe esprimis la indiĝenan lingvon parolatan en Kipro en
   la malfrua bronzepoko. Tiu antikva skribsistemo (la kipro-minoa) estis laŭgrade adaptita,
   tiel ke ĝi kapablu transskribi ankaŭ la grekan lingvon de la enmigrintoj. Estas fakte
   rimarkinde, ke la egeodevenaj gentoj, kiuj alvenis al Kipro, kunportis kun si la propran
   lingvon, sed ne la propran skribsistemon (linearan B). La rezulto de la adaptado de
   la antikva kipro-minoa skribsistemo al la bezonoj de la greka lingvo estas tio, kion
   ni nun nomas „kipra silabaro”.
</p> 
<h3>La arkad-kipra dialekto</h3> 
<p>Tiumaniere komence de la unua jarmilo a.K. oni observis en Kipro du paralelajn interesajn
   fenomenojn: de unu flanko, la kipra silabaro fariĝas laŭgrade la ununura, ekskluziva
   skribsistemo uzata sur la insulo; de alia flanko, la greka (en la formo de la tiel
   nomata arkad-kipra dialekto) fariĝas la lingvo, kiun la plimulto de la loĝantaro komprenas
   kaj utiligas.
</p> 
<h3>Librotenaj celoj</h3> 
<p>Malsame ol la lineara A kaj la lineara B, kies utiligo laŭdire havis precipe librotenajn
   celojn, la kipra silabaro inter la 7a kaj la 4a jarcento a.K. estis uzata de ĉiuj
   klasoj de la loĝantaro pro plej diversaj kialoj. Krom la komuna silabaro, ekzistis
   aliaj variantoj, kiel tiu jam supre menciita de Pafoso kaj tiu, malpli konata eĉ inter
   la fakuloj, de Kourion (inter la nunaj Limasolo kaj Pafoso). Iel ajn, la plimulto
   de la tekstoj (70 %) estis skribata de maldekstro al dekstro.
</p> 
<h3>Indiĝena lingvo</h3> 
<p>Ekzistas malgranda nombro da surskriboj, kiuj ja estas legeblaj (ĉar skribitaj per
   la ordinara kipra silabaro), sed kies signifon tute ne eblas al ni kompreni. La ĉefa
   hipotezo estas, ke ili esprimas iun lokan, indiĝenan lingvon, kiun fakuloj konvencie
   nomas „eteo-kipra”, laŭ analogio kun la „eteo-kreta”, nekonata lingvo iam atestita sur la insulo Kreto. Tamen indas memorigi, ke ĝis nun
   ne ekzistas absolute konvinkaj pruvoj pri tio. La korpuso de eventualaj eteo-kipraj
   (aŭ ial ajn al ni nekompreneblaj) surskriboj estas ĉiuokaze limigita, kaj nombre (temas
   pri ne pli ol 15 tekstoj), kaj geografie (preskaŭ ĉiuj tekstoj venas de la regiono
   de Amathunta, apud la nuna Limasolo), kaj kronologie (la tekstoj estis skribitaj preskaŭ
   ĉiuj en la 4a jarcento a.K.). La plejmulto de la surskriboj en la kipra silabaro esprimas,
   cetere, iun grekan (arkad-kipran) dialekton. 
</p> 
<h3>Helenisma periodo</h3> 
<p>Ekde la 6a jarcento a.K. (almenaŭ laŭ nia nuna dokumentaro) ekaperis en Kipro la greka
   alfabeta skribo, per kiu oni esprimis nur la grekan lingvon (neniam eteo-kipran aŭ
   alian lokan idiomon); ĝi tamen estis malofte uzata ĝis la plena klasika epoko. La
   uzo de la kipra silabaro malfortiĝis precipe post la ĉesigo de la kipraj regnoj fare
   de Ptolemeo, en la dua duono de la 4a jarcento a.K., kaj tute malaperis ĉe la fino
   de la 3a jarcento a.K. En la helenisma periodo la komuna greka lingvo (kojneo), bazita
   sur la atika dialekto, estis ekuzata en la tuta greklingva mondo inkluzive Kipron,
   kaj ĝi estis regule skribata pere de la greka alfabeto, ne de alternativaj skribsistemoj.
</p> 
<h3>Duobla skribsistemo</h3> 
<p>Ekzistas kelkaj surskriboj skribitaj per duobla skribsistemo (ekzemple en la greka
   alfabeto kaj paralele en la kipra silabaro); aldone ekzistas tre grava dulingva surskribo
   (ICS 196), verkita unuflanke en nekonata lingvo – ĉu la eteo-kipra? – pere de la kipra silabaro kaj aliflanke en la greka pere de la greka alfabeto.
</p> 
<h3>Kalkŝtona pokalo</h3> 
<p>En la foto 1 oni vidas fragmenton de kalkŝtona pokalo, trovita en la antikva urbo
   Chytri. Ĝi rilatas al la nemalproksima templo de Afrodito. Ĝi datumas de la 7a aŭ
   la 6a jarcento a.K. Oni rimarkas du malsamajn surskribojn, en kipra silabaro, ambaŭ
   maldekstrumaj. La unua, pli klara kaj skribita per pli grandaj signoj, situas en la
   supra parto. Ĝia enhavo estas tradukebla jene: „Mi apartenas al la Pafosa (Afrodito)”. La dua, ĉe du linioj, ne estas kompleta kaj tial estas malfacile komprenita: oni
   tamen rekonas la esprimon „por la imposto”.
</p> 
<h3>Senvoĉaj konsonantoj</h3> 
<p>En la foto 2 oni trovas tabelon, ĉeestan en la muzeo, koncernan al la signoj, kiuj
   konsistigis la kipran silabaron: estas rimarkinde, ke voĉaj kaj aspiraciaj konsonantoj,
   kiel „b”, „d”, „g”, „ph”, „th” kaj „kh”, estis pritraktataj kaj skribataj kiel la respondaj senvoĉaj (do „p”, „t”, „k”); finaj konsonantoj estis ĉiam sekvataj de la vokalo „e”; du apudaj konsonantoj estis ĉiam disigataj de la vokalo „e”. Do, ekzemple, la nomo Jakobo (en la pragreka „<i>Iakobos</i>”) estu skribata jene: „<i>I-a-ko-po-se</i>”. La nomo Nikolao (en la pragreka „<i>Nikólaos</i>”) estu „<i>Ni-ko-la-o-se</i>”.
</p> 
<h3>Laŭmetia distordo</h3> 
<p>Per iom da fantazio ĉiuj legantoj povos amuzi sin skribante sian nomon laŭ la kipra
   silabaro. En la muzeo estis bela nigra tabulo, kie ludemaj infanoj povis klopodi skribi
   sian nomon aŭ aliajn ŝatatajn vortojn. Se mi volus dum mia vizito klopodi skribi mian
   nomon, ĝi devus esti „<i>Ro-pe-re-to Pi-ke-ro</i>”. Sed mi konfesas, ke, eble pro t.n. laŭmetia distordo, ene de la muzeo sur la nigra
   tabulo mi preferis skribi la nomon de nia ŝatata revuo: „<i>Mo-na-to</i>” (vidu foton 3)!
</p> 
