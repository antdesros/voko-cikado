      <h3>Politiko</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Maldekstre antaŭen</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la oktobraj elektoj en Litovio voĉdonis ĉirkaŭ 35 % de la balotrajtigitoj. Venkis la socialdemokratoj kun 38 mandatoj en la 141-persona parlamento. La konservativuloj, kiuj regis dum la lastaj kvar jaroj, fariĝis do la dua plej granda parlamenta frakcio kun 33 mandatoj.</p> <p>La konservativa, kristandemokrata Unio de la Patrujo, kies ruiniĝon multaj prognozis pro ĝia dura ŝpar-reĝimo, nun restos en opozicio. Regas nun koalicio konsistanta el tri maldekstraj partioj: socialdemokratoj, kun ties estro Algirdas Butkevičius; la Labora Partio, kiun gvidas la rusdevena milionulo Viktor Uspaskich [uspaskiĥ]; kaj la partio <i>Ordo kaj justeco</i>, estrata de eksa prezidanto de Litovio, Rolandas Paksas.</p> <h3>Disponas</h3> <p>En la nova parlamento la koalicio disponas pri 78 lokoj. La koalicianoj kandidatigis la estron de la socialdemokratoj, Butkevičius, al la posteno de ĉefministro.</p> <p>En sia programo la koalicio indikis revivigadon de la ŝtata buĝeto (ankaŭ de la imposta politiko) kaj de la planoj por integriĝo de la ŝtato en la eŭro-zono, prokrastante ĝin ĝis 2015, kaj plialtigon de la minimuma salajro.</p> <p>Tuj post la publikigo de la elekto-rezultoj la prezidanto de Litovio, Dalia Grybauskaite, turnis sin al la konstitucia kortumo, por ke tiu decidu, ĉu la elektoj laŭleĝis. Tio sekvis indikojn pri subaĉeto de balotoj en kelkaj regionoj profite al kandidatoj de la Labora Partio.</p> <h3>Deklaris</h3> <p>Malgraŭ decido de la kortumo, ke kelkaj anoj de la Labora Partio estas elektitaj kontraŭleĝe, Butkevičius tamen deklaris, ke li ne intencas nuligi la koalician interkonsenton. Inter la laboruloj estas ankaŭ honestaj personoj kun principoj, li diris.</p> <p>En la nova parlamento sidas 105 viroj kaj 34 virinoj (du kromaj lokoj ne estas konfirmitaj). Kiel parlamentestro estas elektita ano de la Labora Partio, Vydas Gedvilas.</p> <p>Kun la parlamentaj elektoj okazis referendumo pri la konstruo de nova nuklea centralo en Litovio. Nur triono de la elektintoj jesis al la centralo. La referendumo ne devigas, sed la registaro, laŭ Grybauskaite, „atentos la sintenon de la pli granda parto de la loĝantaro.”</p>. <div style="text-align:right"><span class="mm">LAST</span></div>    
