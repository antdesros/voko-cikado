      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Stilo rekta, senafekta, pri reala mondo malperfekta</h2>
      <!-- komenco de la enhavo -->

       

     <p>La unua novelaro de Lena Karpunina, <i>La bato</i> (2000), tuj prezentis talenton jam esence maturan, seriozan, kviete potencan kaj foje potence malkvietigan. Ŝia dua novelaro, <i>Neokazinta amo</i>, konfirmos ŝian reputacion.</p> <p>Temas pri 15 prozaĵoj: noveloj, foje eble sugestitaj per personaj spertoj; ses temas pri la sovetiaj koncentrejoj, la gulago, en la epoko de Stalin. Ĉiuj havas tiun facilan legeblecon, kiu ofte kostas al aŭtoro malfacilan verkadon.</p> <p>Kvankam ironio aperas ofte, kaj humuro foje, mi sentas, ke la ĝenerala etoso estas sombra, malgraŭ pluraj ekzemploj de homa kuraĝo, eltenivo aŭ bonkoreco. Nu, koncentrejoj estas ja suferegejoj; sed alimaniere karakterizaj estas rakontoj pri atendita brila libertempo en Egiptio – makulita per cinikaj trompoj; aŭ pri avino, kiu urĝe petis viziton, sed tuj forsendas parencojn, alvenintajn post sinofere malfacila vojaĝo; aŭ pri dediĉita „bestamiko”, frue forgesita de supozitaj kolegoj sendankaj – el kiuj unu „modele personigas nesatigeblan kaj egoisman naturon de la homa speco”.</p> <p>Lena Karpunina ne mem spertis la gulagon; ŝi naskiĝis jarojn post la morto de Stalin; sed ŝi sendube aŭdis rememorojn, kaj ŝia frua vivo en Rusio kaj Taĝikio havis siajn rigorojn. Al tiuj, kiuj, kiel mi, fortune vivis en landoj relative liberaj kaj prosperaj, la nuna libro enhavas pensigajn lecionojn pri fremdaj suferoj kaj diversaj reagoj. Ni ricevas impreson de serioza realismo, plena kredebleco, animprema scipovo pri la mondo.</p> <p>La libro lasas ankaŭ impreson pri vera scipovo de nia lingvo: jen vera klareco, stilo tute internacia, modeste zamenhofeca, rekta, kaj komprenebla, tiel ke mi povas rekomendi ĉi tiun literature meritan verkon ankaŭ kiel legolibron por progresemaj lernantoj, simile al la romanoj de Julio Baghy. La verkisto celas komuniki pli ol uniki!</p> <p>Post la aĉa recenzista devo, serĉi erarojn, mi mencios nur unu trovitan: „haro” anstataŭ „hararo”, tre eble, en sia kunteksto, preseraro. Tre eble „doloroj” por „dolaroj” (p. 92) estas intenca ŝerco pri lingva eraro de (malestiminda) agento, kiu ja donis dolorojn kontraŭ dolaroj!</p> <p>Pliriĉigas la libron valora, perceptema enkonduko de Sten Johansson. La aranĝo kaj presado estas tute bonaj. Se mi devas montri muŝojn en la mielo, mi menciu, ke du el la piednotoj troviĝas sur malĝustaj paĝoj.</p> <p>Ne estas surprize, ke 6 el la 15 noveloj ricevis premiojn aŭ menciojn en Belartaj Konkursoj de Kongresoj. Ni esperu pri pluaj volumoj.</p> <div style="text-align:right">Marjorie B<span class="mm">OULTON</span></div> <h6 class="bib">Lena Karpunina: Neokazinta amo. Eld. Flandra Esperanto-Ligo, Antverpeno 2007, 149 paĝoj, ISBN 90 7706628 7.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=neokazinta">la Retbutiko</a>.
        </h6>
