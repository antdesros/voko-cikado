      <h3>Noveloj</h3>
      <h3 class="tem"></h3>
      <h2>Nesuspektindaj flutistoj</h2>
      <!-- komenco de la enhavo -->

       

     <p>La afero ekis tute neatendite. En la komenco, ĉio pensigis pri rutina esploro. Sed
   poste ĝi montriĝis granda sensacio. Kaj la fino estas ankoraŭ nekonata.
</p> 
<p>La universitato „San Martino” ricevis ordinaran esplortaskon. Ĝi estis petita precizigi la aĝon de iu fluto, kiu
   estis taksita je ĉirkaŭ 12 000 jaroj. Laŭ la kalkuloj plenumitaj, ĝi estis do fabrikita
   en la sama epoko, en kiu la 
   <i>homo sapiens</i> komencis montri sian talenton per la pentraĵoj en la hispanaj kavoj de Altamira kaj
   en tiuj de 
   <i>Les Eyzies</i>, male situantaj en Francio.
</p> 
<p>La materialo de la fluto estis iuspeca ceramiko. Rilate ĉi-lastan, la malkovro de
   la aĝo estas malfacila. La disvastigita metodo C14 ne funkcias: tiu helpas nur ĉe
   organikaj materialoj. La aĝon de ceramikaĵoj oni povas malkovri, nur se la loko, kie
   la objekto estis trovita, bone konatas, ankaŭ koncerne la aĝon.
</p> 
<p>En ĉi tiu okazo, bonŝance la ejo estis bone konata. La raporto de la trovintoj estis
   preciza. La esploristoj do iris al Afriko kaj tre zorge ekzamenis la lokon kaj ĝian
   ĉirkaŭaĵon. Unua rezulto estis tre entuziasmiga: dua fluto estis fakte elterigita.
   Eĉ pli precize ol ĉe la unua malkovro, ĉiuj detaloj estis dokumentitaj. La sensacio
   kreskis, kiam la aĝo de la ter-ebeno de la trovaĵoj estis rivelita: ĉirkaŭ ducent
   milionoj da jaroj! Do ili estis ege pli aĝaj ol imagite. Sed tiu estis la epoko de
   la dinosaŭroj!
</p> 
<p>Spertuloj cerbumis pri la estaĵoj, kiuj estis produktintaj kaj utiligintaj la flutojn.
   La distancoj inter iliaj truoj estis preskaŭ egalaj al tiuj de nuntempaj flutoj. Do
   la fingroj de la ludantaj estaĵoj havis la saman interspacon, kiel la niaj.
</p> 
<p>Kia dinosaŭro povus esti la uzanto? El la multaj diversaj specoj suspektindaj oni
   devus konsideri nur tiujn, kiuj havis la antaŭajn ekstremaĵojn liberaj por povi manipuli
   fluton.
</p> 
<p>Nia nekonata saŭro tuj ricevis labornomon en la latina lingvo: 
   <i>dino sapiens</i>.
</p> 
<p>Ampleksa programo por pliaj esploroj estis starigita. Ĉu 
   <i>dino sapiens</i> produktis pentraĵojn en kavoj? Ĉu ĝi uzis fajron? Ĉu metalojn? Antikvaĵoj el la tuta
   mondo estis inkluzivitaj en la esplor-programo. Je ĉiu objekto, kies aĝo estis necerta,
   ĝi estis ree evoluigita.
</p> 
<p>La ĝis nun plej esperiga rezulto estis du tekstoj skribitaj en speco de kojnoskribo,
   ankoraŭ ne deĉifritaj. Ili vekis la intereson de la esploristoj. Unu estas skribita
   sur kupro, la alia sur ŝtono. Ĉu hazarde tiuj devenas el la epoko de 
   <i>dino sapiens</i>? Se jes, kaj se deĉifrado sukcesus, ni povus legi, kion pensis nia antaŭulo 
   <i>dino sapiens</i>.
</p> 
<p>Nun esploristoj traserĉas centojn da antikvaj tekstoj ĝis nun nedeĉifritaj. Niaj plej
   kapablaj komputiloj havas treege interesan taskon por plenumi.
</p> 
<p>La dinosaŭroj vivis dum 170 milionoj da jaroj, do dum ege longa tempo. La 
   <i>homo sapiens</i> evoluis ene de malpli ol miliono da jaroj. Modernan teknikon li disvolvis ene de
   tre malmultaj jaroj. Vapormaŝinoj, fervojoj, radioaparatoj estis inventitaj ene de
   ducento da jaroj, do dum mikroskopa parto de unu miliono da jaroj. Kiom multe evoluis
   
   <i>dino sapiens</i> dum sia ege pli longa vivtempo? Kaj kiom da tio ni eltrovos?
</p> 
