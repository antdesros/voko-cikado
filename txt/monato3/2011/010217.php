      <h3>Spirita vivo</h3>
      <h3 class="tem">HISPANIO</h3>
      <h2>Santiago Florbuĉanto</h2>
      <!-- komenco de la enhavo -->

       

       <p>En 2010 la urbo Santiago de Compostela, la celo de la Vojo Jakoba en la galega parto de Hispanio, atendis eksterordinare multajn pilgrimantojn pro Sankta jaro. Ĉiuloke estis videblaj la emblemo de la festojaro kaj bildoj de la sanktulo. Pro lia legenda venka interveno en la batalo de Clavijo, post kiam meze de la 9a jarcento la kristana reĝo Ramiro rifuzis liveri la jaran tributon de cent virgulinoj al la muzulmanaj malamikoj, ekestis lia kromnomo „Matamoros”, kio signifas „Buĉanto de la maŭroj”. Memkompreneble ankaŭ la artistoj ne evitis la prezentadon de la sanktulo kiel kavaliro rajdanta surĉevale, kun glavo enmane, kaj la dispecigitaj viktimoj sub la hufoj de lia ĉevalo. Tiel li oftege aperas surbilde kaj kiel statuo, nerare vivogrande, en preĝejoj.</p> <p>Ankaŭ en la katedralo de Santiago ekzistas tia impona memorigilo, kaj longe ĝi povis elstarigi la sanktulon kiel savanton de la kristana Hispanio. Neniun ŝokis la kruelaj detaloj, ĝis pli delikata kaj sentema socio, do la aktuala, ne plu povis elteni la religiajn imagojn pri mezepoka batalo. Pli precize, ĝi ne plu volis konfronti la tiam malvenkintan kulturon kun tiaj incitaj, nuntempe „politike maloportunaj” restaĵoj.</p> <h3>Aŭtenta skulptaĵo</h3> <p>La respondeculoj de la katedralo ne kuraĝis ne reagi al la evidenta opiniŝanĝiĝo en la sekulara socio, aparte en jaro kun alfluo de pilgrimaj kaj turistaj amasoj. Ilian elturniĝon povis konstati tiujaraj pilgrimantoj. Ĉe la koncerna loko, senintuaj homoj vidis nur impetan rajdanton, kavaliron, ne nepre sanktulon, kaj trankvile preterpasis tiun ekzotikaĵon sen ekscitiĝo aŭ konsciencriproĉoj. Sed tiuj, kiuj konis la aŭtentan statuon, mire provis serĉi ĝian skandalan bazon. Normalstaturaj homoj ne plu povis vidi la subsanktulan okazintaĵon. Ĝi simple kaj senkulpe kaŝiĝis – malantaŭ la ondado de impona flormaro.</p> <h3>Festjara kamuflado</h3> <p>Oni ja povus ridi pri la inventemo de la kanonikoj, eĉ fari ŝercon, se la hispana lingvo tion plene permesus: el „Santiago Matamoros” estiĝus „Santiago Matafloros” – la Maŭrbuĉanto transformiĝus al Florbuĉanto, se la hispana pluralo de „floroj” („<i>flores</i>”, sed ne „<i>floros</i>”) permesus pli striktan rimadon. Sed la afero estas pli serioza. Ĉu iu vere volas kredigi, ke kamufla festjara florornamaĵo kapablas mildigi historiajn eventojn? Ĝi nur taŭgas por kontentigi homojn, kiuj kredas sin pli moralaj ol la aliaj. Ili ne volas lerni el la kruda historio, nur forŝovas ĝin. Kaj hejme ili tamen avidas renkonti kadavrojn, nome en la televido, pli ofte ol iam ajn viditajn en religiaj cirkonstancoj. La kaŝado de la pasinteco ne aŭtomate kondukas al pli paca socio, ne purigas religiojn kaj ne evitigas estontajn kruelaĵojn.</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div>  
