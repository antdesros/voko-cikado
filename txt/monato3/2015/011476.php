      <h3>Politiko</h3>
      <h3 class="tem">ĜIHADISMO</h3>
      <h2>Kune kontraŭ ekstremistoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Kunlabori kontraŭ ĝihadismo interkonsentis kvar EU-ŝtatoj kaj ses landoj el okcidenta
   Balkanio. Ĉeestis kontraŭterorisman konferencon en Vieno la EU-komisionano pri internaj
   aferoj, Dimitris Avramopoulos, kaj ministroj de Italio, Slovenio, Kroatio, Aŭstrio
   kaj de Albanio, Bosnio-Hercegovino, Kosovo, Makedonio, Montenegro kaj Serbio.
</p> 
<p>Laŭ la aŭstra ministro pri eksteraj aferoj, Sebastian Kurz, identigeblas tri kialoj
   por okazigi la konferencon „Batali kune kontraŭ ĝihadismo”. Unue, ĝihadismo minacas la internan kaj eksteran sekurecon. Due, ekzistas ligoj
   inter ekstremistoj en okcidenta Balkanio kaj EU-landoj. Trie, troveblas en la partoprenantaj
   ŝtatoj problemoj, kiuj favoras krimecon kaj ekstremismon, ekzemple mankantaj perspektivoj
   por junaj homoj.
</p> 
<h3>Najbaraj</h3> 
<p>Nepras, aldonis Kurz, ke Aŭstrio subtenu najbarajn landojn en okcidenta Balkanio en
   ilia batalo kontraŭ ĝihadismo.
</p> 
<p>Krome, laŭ Johanna Mikl-Leitner, ministrino pri eksteraj aferoj de Aŭstrio, necesas
   plilarĝigi la EU-registrejon pri kontraŭleĝaj interretaj enhavoj ankaŭ al ŝtatoj de
   okcidenta Balkanio. Aŭstrio jam proponas al aliaj ŝtatoj informojn de la konsultejo
   pri ekstremismo, establita en la pasinta jaro, al kiu povas sin turni gepatroj de
   radikaligitaj gejunuloj.
</p> 
<p>Aldonis Mikl-Leitner, ke oni pli bone trejnu limgardistojn por malebligi el- kaj en-migradon
   de perfortopretaj ekstremistoj kaj plifortigi la rilatojn kun la limgarda agentejo
   Frontex. Endas ankaŭ intensigi kunlaboron kun la eŭropa polica oficejo Europol kaj
   kun la kontraŭterorisma kunordiganto de EU.
</p> 
<h3>Eksterlandaj</h3> 
<p>Substrekis Avramopoulos, ke ĝihadismo povas esti solvita nur pere de pli intima kunlaboro
   de la eŭropaj landoj. Li indikis, ke la nombro de eksterlandaj batalantoj en Sirio
   kaj Irako kreskas „de tago al tago”.
</p> 
<p>Ivica Dačić [daĉiĉ], ministro pri eksteraj aferoj de Serbio kaj nuna prezidanto de
   la Organizaĵo pri sekureco kaj kunlaboro en Eŭropo (OSCE), riproĉis, ke la ĝihadisma
   minaco longe estis subtaksata. La t.n. islama ŝtato (IS) ekestis en 2006, atentigis
   Dačić, tamen kontraŭterorisma agado nur en 2014.
</p> 
<p>Li diris: „Kion ni faris dum tiuj ok jaroj? Serbio avertis, ke Aŭstrio estas centro de vahabitaj
   ekstremistoj. Hodiaŭ estas en ordo ĉio, kion ni faras. Tamen dum multaj jaroj ni ebligis
   al ili fortikiĝi.”
</p> 
