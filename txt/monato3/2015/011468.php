      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Unu regulo por riĉuloj, dua regulo por malriĉuloj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kiom da mono jare ricevu deputito en nacia parlamento? En Britio, deputito ricevas
   ĉirkaŭ 2,75-oble la mezan salajron. Tamen por kelkaj parlamentanoj tio ne sufiĉas.
</p> 
<p>Lastatempe iama ministro pri defendado, poste eksterlandaj aferoj, Kavaliro Malcolm
   Rifkind [malkom rifkend], anoncis, ke li ne kandidatiĝos en ĉi-printempaj naciaj elektoj
   kaj forlasos la parlamenton.
</p> 
<p>Kial? Rifkind estis kaŝe filmata, promesante kontraŭ pago malfermigi parlamentajn
   pordojn al privataj kompanioj, por ke tiuj profitu de rekta aliro al ministroj kaj
   aliaj altrangaj funkciuloj. Defendis sin Rifkind, dirante, ke ne eblas vivteni sin
   per jara salajro de 67 000 britaj pundoj (ĉ. 92 000 eŭroj), kaj ke necesas alimaniere
   enspezi.
</p> 
<p>Nu, du jarojn antaŭe, en 2013, kolego de Rifkind, la ministro pri socia sekureco,
   Ian Duncan Smith [ien dankan smif], taskata i.a. malaltigi publikajn elspezojn, asertis,
   ke li (do ajna civitano) povus semajne vivteni sin per 53 britaj pundoj (ĉ. 73 eŭroj),
   do jare per 2750 pundoj (ĉ. 3800 eŭroj).
</p> 
<p>Kia kontrasto. Unu, nun eksa ministro, kredas jare 92 000 eŭrojn neniel sufiĉaj; dua,
   ankoraŭ ministro, opinias 3800 eŭrojn plene adekvataj por ordinarulo. Apenaŭ ekzistas
   ekzemplo pli okulfrapa por reliefigi sociajn diferencojn ne nur en Britio sed ankaŭ,
   verŝajne, en ĉiu t.n. evoluinta lando, por ne mencii la oscedantan abismon inter havuloj
   kaj senhavuloj.
</p> 
<p>Tamen la afero surtabligas aliajn demandojn. Rifkind ne estas la sola deputito, kiu
   suplementas apenaŭ mizeran salajron per kroma laboro. Multaj, fakte, profitas de plusaj
   kaj alte rekompencataj postenoj, ofte kiel direktoroj de bankoj aŭ aliaj grandaj entreprenoj.
   Por kelkaj, sendube, parlamenta salajro signifas nuran poŝmonon.
</p> 
<p>Parlamentanoj sin defendas, dirante, ke necesas akiri spertojn (pri salajroj kaj honorarioj
   ili silentas) pri la eksterpolitika mondo. Vidpunkto certe pravigebla, tamen malmultas
   la deputitoj, kiuj en liberaj horoj malpurigas la manojn en fabrikoj, prizonoj, lernejoj
   aŭ hospitaloj. Eblas argumenti, ke direktorumi bezonas verŝajne nur unu posttagmezon
   monate, kio nur minimume konfliktos kun parlamenta horaro. Aliflanke, instruistoj,
   flegistoj kaj multaj, multaj aliaj kun „ordinaraj” postenoj ne povas tiel facile sin forpermesi por obligi siajn apenaŭ malavarajn salajrojn.
</p> 
<p>Leviĝas aparta problemo, kiam deputito okupas duan plentempan postenon. La londona
   urbestro, Boris Johnson [boris ĝonsen], kandidatiĝas en venontaj naciaj elektoj por
   fariĝi parlamenta deputito. Verŝajne li estos elektita, sed li anoncis, ke li ne rezignos
   pri sia urbestra mandato, kiu validos ĝis unu jaron post la elektoj.
</p> 
<p>Tiel li insultos kaj tiujn, kiuj elektis lin urbestro, kaj tiujn, kiuj elektos lin
   deputito. Johnson mesaĝas, ke ambaŭ postenoj estas efektive nur duontempaj, ke eblas
   plenumi samtempe du rolojn. Do ĉu efektive deputiti aŭ urbestri estas ja nur duontempa
   tasko? Se jes, Johnson ricevu nur duontempajn salajrojn. Simile tiuj, kiuj nur partatempe
   deputitas, kiuj dum parlamentaj horoj direktoras, ricevu nur partatempajn parlamentajn
   enspezojn.
</p> 
<p>Klare, ne ĉiuj deputitoj tiel arogante traktas la popolon kiel Rifkind, Johnson, kaj
   aliaj. Multaj honeste, diligente kaj pli ol plentempe laboras. Tamen la publiko, pli
   kaj pli konscia pri la abismo inter regantoj kaj regatoj, ekpostulas, ke deputitoj
   havu unusolan laboron, nome parlamentan, kaj ke ili servu ekskluzive homojn en sia
   elektodistrikto.
</p> 
<p>Almenaŭ unu partio promesis limigi eksterparlamentan laboron. Ĉu la promeso realiĝos,
   estas alia demando. Intertempe ju pli la publiko ekscias pri partatempa, kroma laboro,
   des pli ĝi koleras. Lastatempe la BBC-ĉefo, Rona Fairhead [rona féahed], agnoskis,
   ke ŝi ricevas jare duonmilionon da pundoj (aldone al BBC-salajro) kiel direktoro de
   la banko HSBC.
</p> 
<p>Kiam oni demandis Fairhead, ĉu ŝi scias, ke la banko per sekretaj svisaj kontoj permesis,
   ke riĉuloj evitu impostopagadon, ŝi nee skuis la kapon. Kulpas, ŝi diris, subuloj.
</p> 
<p>Imagu ... duonmiliono da pundoj, direktoreco, kaj nenion scii. Nu, jen enviinda laboro.</p> 
