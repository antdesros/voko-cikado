      <h3>Eseoj</h3>
      <h3 class="tem">NOMBROJ</h3>
      <h2>Dana abako dolorigas la kapon</h2>
      <!-- komenco de la enhavo -->

       

       <p>Unu el la plej junaj anoj de nia familio – malgranda knabo, kiu iĝis bazlernejano antaŭ nelonge – fieras pri la fakto, ke li kapablas kalkuli ĝis dek du en kvar lingvoj: la skotgaela,
   la angla, la germana kaj la itala. Li cetere scipovas kalkuli ĝis tre altaj nombroj
   en almenaŭ du el la menciitaj lingvoj.
</p> 
<h3>Triboj</h3> 
<p>Lastatempe mi legis, ke, laŭ antropologoj kaj lingvistoj, en kelkaj partoj de la mondo
   estas eĉ plenkreskuloj, kiuj ne scipovas kalkuli preter kvar aŭ kvin, ĉar iliaj idiomoj
   ne posedas nomon por pli altaj numeraloj. Laŭ iu raporto ekzistas triboj en foraj
   regionoj de kelkaj landoj, kiel Aŭstralio, Amazonio kaj Nov-Gvineo, kiuj kalkulas
   jene: „Unu, du, tri ... multo!”. Preter tiu modesta nombra sistemo ili ne kapablas progresi. Tio tute ne signifas,
   ke tiuj tribanoj estas stultaj aŭ mense malsuperaj; oni fakte konstatis, ke ili havas
   vastan konon pri sia medio, inkluzive arbojn, plantojn, bestojn, klimaton kaj geografion.
   Ili scias, ekzemple, kiuj kreskaĵoj efikas kiel kuraciloj, kiuj estas utilaj kaj kiuj
   estas venenaj. Entute ili estas perfekte adaptiĝintaj al sia propra ĉirkaŭaĵo kaj
   al la kondiĉoj de la loka vivo. Al ili ne mankas inteligento, sed en la praarbaro
   aŭ en la aŭstralia dezerto oni tute ne bezonas pli komplikan matematikon. Iliaj tribaj
   aŭ familiaj grupoj estas malgrandaj kaj ili havas limigitan aŭ nenian kontakton kun
   homaj amasoj kaj kun ilia tiel nomata civilizacio.
</p> 
<h3>Civilizuloj</h3> 
<p>Mi legis monografion pri la aŭstraliaj aborigenoj, verkitan de profesoro A.P. Elkin
   el la Universitato de Sidneo. Li detale priskribas la ege komplikan sistemon de parenceca
   klasifikado ekzistantan inter la aborigenoj. Temas pri vasta reto de rilatoj kaj interrilatoj,
   kiun ili povas bone difini kaj renombri, kvankam ili ne uzas matematikon por tion
   fari pro la simpla fakto, ke ili ne kalkulas preter la unuaj ciferoj. Ni, sinnomantaj
   civilizuloj, ne kapablus efektivigi ion tian sen la uzado de elektronikaj komputiloj.
</p> 
<h3>Fingroj</h3> 
<p>Tre primitiva sistemo de kalkulado konsistas el nombrado de la fingroj de la manoj
   (certe tio estas la origino de nia dekuma sistemo). Kiam la manoj ne sufiĉas, oni
   povas aldoni la piedfingrojn. Kelkaj homoj, ekzemple (laŭraporte) la tiel nomata jupna
   popolo de Papuo, asignas nombrojn al ĉiuj partoj de sia korpo – naztruoj, okuloj, cicoj ktp. En Mez-Ameriko la majaa popolo, kies landon eŭropanoj
   invadis detruante ilian riĉan kulturon, posedis unu el la plej altgradaj matematikaj
   sistemoj ĝis tiam konataj. Ilia lingvo antaŭvidis nomojn por ĉiuj numeraloj, kaj ili
   povis nombradi almenaŭ ĝis pluraj milionoj. Pro tio ili sukcesis fari mirinde ekzaktajn
   astronomiajn kalkulojn.
</p> 
<h3>Prononco</h3> 
<p>La plimulto de la eŭropaj popoloj uzas la dekuman sistemon, kvankam ne tute kaj ne
   ĉiam konsekvence. En la plimulto de la hindeŭropaj idiomoj la kalkulado similas tiun
   de la klasikaj lingvoj, kies numeraloj havas rilaton kun tiuj de la sanskrita. Ekzemple,
   la unuaj dek numeraloj en la hindustana (hinda-urdua) estas la jenaj: 
   <i>ek</i>, 
   <i>do</i>, 
   <i>tin</i>, 
   <i>char</i>, 
   <i>panch</i>, 
   <i>chhah</i>, 
   <i>sat</i>, 
   <i>ath</i>, 
   <i>nau</i>, 
   <i>das</i>. Ili evidente havas la saman originon, kiel la plimulto de la idiomoj ankoraŭ parolataj
   en Eŭropo (la ĉefaj esceptoj estas la finna, la estona, la hungara, kvankam ekzistas
   ankaŭ aliaj). Cetere rimarkindas, ke la pragrekaj numeraloj estas nemulte ŝanĝitaj
   (malgraŭ iom da simpligoj kaj modifoj rilataj al la prononco) en la novgreka.
</p> 
<h3>Subtraho</h3> 
<p>La latina nombra sistemo (<i>unus</i>, 
   <i>duo</i>, 
   <i>tres</i> ktp) similas la grekan, sed estas malpli perfekta. Post la numeralo 
   <i>decem</i> sekvas 
   <i>undecim</i>, 
   <i>duodecim</i>, 
   <i>tredecim</i> ktp, sed tiu logika nombrovico estas interrompata de 
   <i>duodeviginti</i>, 
   <i>undeviginti</i> („dudek minus du”, „dudek minus unu”), kiuj iomete komplikas la ordon por junaj lernantoj kaj devigas ilin fari subtrahon
   (kio, aliflanke, ja ne estas malbona afero!).
</p> 
<h3>Poezio</h3> 
<p>La Esperantaj numeraloj estas tute logikaj, kiel la grekaj, dum la ĝermanaj idiomoj
   iomete inversigas la ordon; ekzemple, anstataŭ la Esperantajn dek tri, dek kvar ktp,
   la germanoj diras 
   <i>dreizehn</i>, 
   <i>vierzehn</i>, kaj la angloj 
   <i>thirteen</i>, 
   <i>fourteen</i> („tri kaj dek”, „kvar kaj dek”). Cetere, nesimile al la Esperantaj dudek unu, dudek du, la germanoj diras 
   <i>einundzwanzig</i>, 
   <i>zweiundzwanzig</i> (komparu la fruajn anglajn 
   <i>one and twenty</i>, 
   <i>two and twenty</i>, ankoraŭ troveblajn en poezio).
</p> 
<h3>Kompatindaj infanoj</h3> 
<p>La francoj saĝe sekvas la decimalan sistemon (do bazitan sur 10) ĝis la numeralo 
   <i>soixante</i> (sesdek), sed poste ili ial devigas la kompatindajn infanojn ŝanĝi al sistemo dudekuma,
   alivorte bazitan sur 20, tiel ke oni havas: 
   <i>soixante-dix</i> („sesdek dek”), 
   <i>quatre-vingts</i> („kvar dudekoj”), 
   <i>quatre-vingts-dix</i> („kvar dudekoj plus dek”). La franclingvanoj en Belgio kaj Svislando ja pli racie uzas la decimalajn numeralojn
   
   <i>septante</i>, 
   <i>octante</i>, 
   <i>nonante</i>.
</p> 
<h3>Konfeso</h3> 
<p>Mi devas konfesi, ke ni mem, kiuj en nia familio parolas la skotgaelan lingvon, ne
   rajtas plendi pri la dudekuma sistemo, ĉar ankaŭ ni uzas ĝin. Ĝis la numero dek estas
   nenia malfacilaĵo. Ni kalkulas: 
   <i>a h-aon</i>, 
   <i>a dha</i>, 
   <i>a tri</i>, 
   <i>a ceithir</i>, 
   <i>a coig</i>, 
   <i>a sia</i>, 
   <i>a seachd</i>, 
   <i>a h-ochd</i>, 
   <i>a naoi</i>, 
   <i>a deich</i> (mi ellasis la akcentojn – ankaŭ ĉe la hindustana antaŭe –, ĉar ili ne gravas). Tamen, post dudek (<i>fichead</i> en la gaela), la afero iom komplikiĝas. Ĝi iĝas: „dudek dek”, „duoble dudek”, „duoble dudek plus dek” (aŭ ankaŭ „duoncent”), „trioble dudek”, „trioble dudek plus dek”, „cent”. Tamen, antaŭ nelonge, la edukadaj instancoj en Skotlando enkondukis pli facilan
   decimalan sistemon de la gaela nombrado similan al tiu en la moderna irlanda lingvo,
   kaj nur ĉi-lasta estas nun instruata al niaj infanoj.
</p> 
<h3>Mallongigoj</h3> 
<p>En la islanda kaj en ĉiuj skandinavaj lingvoj, krom la dana, oni uzas decimalan sistemon
   de kalkulado. Eĉ en la norvega, kiu en la tempo de Ibsen estis apenaŭ distingebla
   de la dana kaj estis konata kiel „dananorvega”, la numeraloj estas nun decimalaj. Sed – pro iu (por mi) nekomprenebla kialo – ekde la numero dudek la danoj nuntempe kalkulas tute malsame ol la ceteraj eŭropanoj,
   jene: 
   <i>tyve</i>, 
   <i>tredive</i>, 
   <i>fyrre</i>, 
   <i>halvtreds</i>, 
   <i>tres</i>, 
   <i>halfjerds</i>, 
   <i>firs</i>, 
   <i>halvfems</i>, 
   <i>hundrede</i>. Se mi bone komprenas, la tieaj numeraloj laŭvorte signifas aferojn kiel: mallongigon
   de duoble dek, trioble dek, mallongigon de kvaroble dek, dudek kaj duono de trioble
   dudek, mallongigon de trioble dudek, trioble dudek, trikajduon-oble dudek, kvaroble
   dudek, kvarkajduon-oble dudek kaj cent ... Ne surprize do la dana ne iĝis mondolingvo.
</p> 
<h3>Proverbo</h3> 
<p>La 807a proverbo en la zamenhofa 
   <i>Proverbaro</i> estas 
   <i>Kalkuli muŝojn</i>. Kalkulu muŝojn, se vi volas, sed ne en la dana!
</p> 
