      <h3>Ekonomio</h3>
      <h3 class="tem">EKS-JUGOSLAVIO</h3>
      <h2>Disdivido de la oro</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Dum aprilo kaj majo 2005 en diversaj urboj okazis intertraktado pri la divido de la rezervoj en oro kaj valutoj de la centra banko de iama Jugoslavio kaj pri la reguligo de ties restantaj havaĵoj kaj ŝuldoj.</b></p> <p>En Skopjo oni decidis pri la disdivido de parto de la oraj rezervoj, deponita en diversaj eŭropaj bankoj, de la eksa Jugoslavio. Tiel el 8,5 tunoj da oro, je valoro de 110 milionoj da usonaj dolaroj, Serbio-Montenegro ricevos 38 %, Kroatio 23 %, Slovenio 16 % kaj Bosnio-Hercegovino 15,5 %. La ceteron ricevos Makedonio. Antaŭe jam 46 tunoj estis dividitaj. La novaj respublikoj per tio i.a. kompensu kompaniojn el aliaj respublikoj, kiuj perdis siajn havaĵojn.</p> <p>En Zagrebo komisiono ricevis instrukcion esplori pri duona miliardo da dolaroj, malaperintaj el kontoj de la iama centra banko dum la plurjaraj militoj kaŭzitaj de reĝimo de Milošević, kaj supozeble uzitaj por la bezonoj de ties armeo.</p> <h3>Kosovo forgesita</h3> <p>Cetere nenio restis por la kosovanoj, kies tuta mono, deponita en la bankoj, estis forrabita de la militista reĝimo serba antaŭ kaj dum la milito de 1998-1999.</p> <p>Kosovo estis grave detruita (rememoru nur la 40 000 domojn bruligitajn kaj dezertigitajn) kaj travivis seriozajn malfacilaĵojn pro manko de mono. Ĝis antaŭ nelonge ĉiuj pensiuloj ne povis ricevi la pension, je kiu ili rajtis pro sia multjara laborado en iamaj ŝtataj entreprenoj, dum centoj da familioj seniĝis je sia tuta ŝparmono. La ekskludo de Kosovo de la disdivido de la oro pruvas ankoraŭfoje la intencon de la internacia komunumo ne agnoski la memstarecon de tiu granda regiono de la eksa Jugoslavio, kiu okupis la trian lokon laŭ la nombro de loĝantoj, tuj post serboj kaj kroatoj.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
