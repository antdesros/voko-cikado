      <h3 class="tem">JAPANIO</h3>
      <h2>Baraktas fiŝistoj ekster la akvo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Strikis meze de julio miloj da japanaj fiŝistoj. Ariĝis en Tokio 3000 reprezentantoj de la fiŝistoj, postulante por sia industrio registaran subvencion. De sur podio deklaris sinjorino Hatakejama Ecuko el la gubernio Mijagi: „La prezo de nafto pli kaj pli altiĝas, sed fiŝprezoj neniam. Kotizoj por pensio kaj sanasekuro, impostoj, ĉio altiĝas. Se tiu situacio daŭros, ni ne povos vivteni nin. Helpu nin!”</p> <p>Fiŝkaptado multe dependas de nafto. Ĉirkaŭ 50 % de la elspezoj de la fiŝistaro rilatas al nafto, ĉefe pro ŝipetoj. Lastatempe la kosto de nafto kvarobliĝis (de ekvivalento de 200 eŭroj ĝis pli ol 800 eŭroj por mil litroj). Tamen fiŝistoj ne povas aldoni tiun kroman elspezon al la prezoj de kaptitaj fiŝoj. La pograndistaro, havante pli da forto ol la fiŝistaro, premas prezojn por afabli al konsumantoj.</p> <h3>Ostetoj</h3> <p>Gustoj de konsumantoj pri fiŝoj ŝanĝiĝis. Ili pli ŝatas tiajn fiŝojn kiel salmo, tinuso kaj seriolo (<i>Seriola quinqueradiata</i>), kiujn oni povas facile kuiri kaj manĝi same kiel viandon. Nuntempe homoj estas tiom okupataj, ke ili ne volas malrapide kuiri kaj manĝi malgrandajn fiŝojn kun multaj ostetoj. Tamen por kapti tinusojn, fiŝistoj devas vojaĝi al malproksima maro apud Sud-Afriko.</p> <p>Tial al la kunveno en Tokio venis mesaĝo de unu el tiuj fiŝistoj: „Estimata ministro, ni nun kaptas tinusojn 650 mejlojn sude de Kaburbo, Sud-Afriko, en malvarmegaj ventoj. Ni aĉetis nafton en Kaburbo en junio kontraŭ 125 000 enoj (830 eŭroj) por mil litroj, 50 000 enojn (300 eŭrojn) pli multekoste ol en januaro. Ni ŝparis kaj ŝparis, sed ni ne povas tion plu fari.”</p> <h3>Aviadilo</h3> <p>La tinusojn oni transportas per aviadilo al Japanio. Do oni moke diras, ke tinusoj, kiuj venas al Japanio, ne nur naĝas en la maro, sed ankaŭ flugas en la aero. Ankaŭ por tio oni bezonas nafton.</p> <p>Ekzistas alia problemo pri japana fiŝado: maljuniĝintaj fiŝistoj. En 1984 estis 440 000 fiŝkaptistoj en Japanio, sed nun estas nur 210 000. Dum la pasintaj 20 jaroj la nombro duoniĝis, kaj 40 % de la fiŝistoj estas pli ol 65-jaraĝaj. Do pro nafto-kostoj, ŝanĝiĝantaj gustoj de konsumantoj kaj maljuniĝanta fiŝistaro la japana fiŝindustrio travivas malfacilajn tempojn.</p> <p>Tamen konsumantoj ŝajne ne multe interesiĝas pri tiu problemo. En la vespera televido multas programeroj pri frandaĵoj kaj ekzemple pri „rotaci-suŝi-restoracioj”, kie oni surtabligas malmultekostajn, sed bongustajn suŝiojn (suŝio: kruda fiŝaĵo sur rizbulo). Tie infanoj kaj gepatroj manĝas kun feliĉa mieno, tute ne sciante, kiamaniere tiuj fiŝaĵoj venas al ilia tablo.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
