      <h3>Eseoj</h3>
      <h3 class="tem">MEDIO</h3>
      <h2>Klimata ŝanĝiĝo kaj konfliktoj: kia interrilato?</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>La tergloba klimata aliiĝo superregos la tutan 21an jarcenton. Depost la antaŭindustriiĝa
      tempo la temperaturo ja altiĝis kaj eble jam estas tro malfrue por eviti la aldonon
      de pliaj 2 gradoj celsiaj, kio portus la mondon al nivelo rigardata kiel „danĝera”.
   </b>
</p> 
<h3>Varmiĝo</h3> 
<p>Kvankam la preciza evoluo de varmiĝo dependos de politikaj, ekonomiaj kaj sociaj faktoroj,
   estas evidente, ke la rezultonta aliiĝo de la klimato prezentos kaj rektajn kaj nerektajn
   konsekvencojn, kreante novan medion. La efikoj de la klimata ŝanĝiĝo inkluzivas leviĝon
   de la mar-niveloj kaj delokiĝojn de loĝantaroj el unu regiono al alia. Al tio oni
   povas aldoni plioftiĝon de ekstremaj veteraj fenomenoj, kiel senpluveco kaj inundoj,
   perturbadon de akvaj rimedoj kaj ekologiaj sistemoj, spontanajn furiozajn brulegojn
   en sovaĝaj areoj, ekaperon de gravaj malsanoj, paralele al malpliiĝo de kultivaĵoj
   kaj malabundiĝo de nutraĵoj.
</p> 
<p>Pluraj el tiuj problemoj estas interdependaj: ekzemple kaj troo kaj maltroo da akvo
   damaĝas agrikulturon, nesufiĉa nutriĝo pliprobabligas epidemiojn kaj kronikajn malsanojn,
   ktp. La mondo jam komencis sperti la unuajn sekvojn de aliiĝanta klimato.
</p> 
<p>En 1992, en konferenco pri klimat-ŝanĝiĝo organizita de Unuiĝintaj Nacioj (UN) en
   la brazila urbo Rio de Janeiro, estis prezentita unuafoje al landaj reprezentantoj
   la ideo pri necesa ekvilibrigo de la ellaso de gasoj en la atmosferon, kun speciala
   averto precipe pri la kreskantaj niveloj de karbona dioksido rezulte de homa aktiveco
   kaj ĝia influo sur la planeda klimato.
</p> 
<h3>Minacoj</h3> 
<p>La minacoj estigataj de la klimata ŝanĝiĝo inkluzivas disputojn pri teritoriaj landlimoj
   kaj nelaste konfliktojn pri la uzado de naturaj rimedoj. Ĉar la marnivelo altiĝas,
   la marbordo mem modifiĝas, tiel ke insuloj situantaj je malaltaj niveloj malaperas,
   ofte kaŭzante disputojn rilate al iaj teritoriaj postuloj. La maldensiĝo kaj la ŝrumpiĝo
   de la somera glacitavolo en la arkta regiono malkaŝas grandegajn ekspluateblojn, kiuj
   koncernas nafton kaj gason antaŭe ne ekspluateblajn en tiu regiono. La sama fenomeno
   ebligos la malfermon de novaj ŝipvojoj. Tio kondukos al ŝanĝoj en la geostrategia
   kaj geoekonomia ekvilibro.
</p> 
<p>La haveblo de dolĉa akvo kaj de nutraĵoj, la distribuo de portantoj de patogenaj malsanoj,
   la stabileco de tutaj komunumoj: ĉio ĉi kuŝas sub la potenciala influo de klimat-ŝanĝo.
   Ĝi povas ankaŭ kontribui al la pliseverigo de riskoj por la homa sekureco kaj rekte
   aŭ malrekte instigi al politika aŭ perforta konflikto en malfortaj aŭ misfunkciantaj
   ŝtatoj. Tio signifas, ke tiaj minacoj al la internacia stabileco venantaj de tiuj
   ŝtatoj riskos pliintensigi transnaciajn organizitan krimon, terorismon, migradon,
   kaj ke la ŝtatoj devos rekonsideri sian internacian politikon kaj la sekurecajn koncernojn
   kaŭzatajn de la tutmonda varmiĝo.
</p> 
<p>Klimata transformiĝo funkcios kiel „multiplikilo” aŭ „akcelilo” de minacoj koncerne al konfliktoj kaj nestabileco. La klimata aliiĝo jam havas rektan
   influon sur la produktadon de nutraĵoj kaj sur la prez-kontrolon, kaj nun, kiel vidite,
   povas influi ankaŭ la homan sekurecon en diversaj manieroj.
</p> 
<p>Inter 2000 kaj 2004 preskaŭ 5 % de la loĝantaro en evoluantaj landoj estis tuŝitaj
   de klimataj katastrofoj kontraŭ malpli ol 1 % en la evoluintaj, sekve de geografia
   fokuso de la minaco kaj de pli granda varieco aŭ fleksebleco de rimedoj, kompreneble
   ligita al riĉeco.
</p> 
<h3>Konfliktoj</h3> 
<p>La ligiteco inter klimata aliiĝo kaj konflikto estas pridiskutata. Kelkaj empiriaj
   esploroj montras fortan ligon, dum aliaj montras kompletan malrilaton aŭ pure sugestas,
   ke historie la problemo koncernas malvarmiĝon, ne varmiĝon. Pro tiuj necertecoj estas
   malfacile prognozi klimatan evoluon en tia maniero, ke la rezultoj estos utilaj por
   politikaj programoj.
</p> 
<p>La risko pri interŝtataj konfliktoj rezultantaj de ĉiam pli malabundaj akvaj rimedoj
   estas eta aŭ neekzistanta, kaj fakte la administrado de translimaj rimedoj estas tereno,
   kie antagonismaj landoj emas al kunlaborado. Konfliktoj surbaze de naturmediaj faktoroj
   okazas precipe ene de la ŝtatoj mem, kaj, kiam ili transiras la ŝtatajn landlimojn,
   ili emas fariĝi subnaciaj anstataŭ klasikaj interŝtataj konfliktoj.
</p> 
<p>Konklude, ŝanĝiĝanta klimato povas havi rimarkindan geopolitikan signifon ĉirkaŭ la
   mondo, kondukante al malriĉeco, naturmedia degenerado kaj kroma malfortiĝo de jam
   politike malfortiĝantaj registaroj. Klimata ŝanĝiĝo kontribuos al malabundo de nutraĵoj
   kaj al manko de dolĉa akvo, pliintensigante la disvastigon de malsanoj kaj instigante
   al amas-migrado. Dum aliiĝanta klimato per si mem kaŭzas neniun konflikton, ĝi tamen
   povos agi kiel multobliga faktoro de malekvilibroj aŭ konfliktoj, lasante la respondecon
   de reago al civilaj kaj militaj institucioj okaze de gravaj veter-fenomenoj postulantaj
   homajn defendon kaj asistadon.
</p> 
