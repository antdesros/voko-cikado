      <h3>Medio</h3>
      <h3 class="tem"></h3>
      <h2>Strangulo en la naturo</h2>
      <!-- komenco de la enhavo -->

       

      <p>La 62-jaraĝa litovo Algimantas Arcimavičius jam ekde kelkdekoj da jaroj vivas ne en sia komforta loĝejo en la urbo Kaunas, sed en primitiva, ligna, fragmita, arbofolia dometo, kiun li mem konstruis en arbara profundo. Lia dometo dum malvarma jarsezono estas simpla subtera bunkro, kaj somere io simila al grandega birda nesto inter pinarboj. Por atingi ĝin necesas grimpi ĉirkaŭ 20 aŭ 30 metrojn inter arbobranĉoj.</p> <p>Kiu legis libron pri Tarzano, povus konstati, ke la vivo de Algimantas multe similas al ĝi. Antaŭ multaj jaroj la viro vivis kaj laboris kiel kudristo en la urbo Kaunas. Sed la urba vivo ne plaĉis al li. Ankoraŭ vivante tie li ofte emis kunpreni dorsosakon kaj vagadi en la naturo. Unufoje partopreninte piedmarŝon en la nacia parko apud Ignalina li estis kvazaŭ kaptita en la sinon de la naturo kaj finfine decidis forlasi la urbon.</p> <p>La apero de strangulo en la arbaro aspektis ege suspektinda por la apudvilaĝaj loĝantoj. Komence ili opiniis, ke li estas aŭ senhejmulo aŭ drogulo, lokaj drinkuloj eĉ provis serĉi kompanion ĉe li. Se io malbona okazis en la apudaj vilaĝoj, ĉiuj tuj suspektis pri tio la novveninton. La litova Tarzano eĉ memoras, ke plurfoje li estis kaptita de la polico kiel suspektato pri ŝteloj aŭ murdoj.</p> <p>Tamen la viro de la naturo eĉ ne intencis forlasi la arbaron. Li plene ekŝatis sian vivmanieron. Ankaŭ la bestoj ekŝatis lin. Iam sciuro kaj mustelo eĉ nestis en lia domo surpinte de la pinoj. Alko tiel amikiĝis, ke sufiĉis nur iom ekkrii kaj ĝi tuj estis ĉe li. Algimantas havis eĉ selon kaj povis rajdi sur la alko kiel sur ĉevalo. Poste iu ĉasisto mortigis la beston. Nun la amikecon de la viro kaj de la besto memorigas nur ostoj, kiujn Algimantas lasis por la memoro.</p> <p>Tarzano estas ankaŭ fotisto kaj vivante en arbaro li povis plene sin dediĉi enkadrigi la naturon. En sia natura ĉirkaŭaĵo li eĉ havis fotogalerion kaj en sia bunkro – fotolaboratorion, kies elektrokurenton liveris akumulatoro.</p> <p>Vivante en la naturo Algimantas lernis vivi vegetare. Liaj ĉefaj manĝaĵoj fariĝis nuksoj, folioj de diversaj arboj, plantoj kaj lia plej ŝatata trinkaĵo estis teo el pomarbaj petaloj. „Mi ne bezonas viandon, ĉar ĉiujn necesajn mineralojn kaj vitaminojn mi ĉerpas el la naturo”, asertas li.</p> <p>Ofte apud Tarzano estis virinoj. Tamen ili ne povis longe elteni en tiaj kondiĉoj kaj forkuris.</p> <p>En la apuda arbaro je dek metroj alte inter la arboj staras „diskutsalono” kaj ĵus finita dormejo pendas je 20 metroj. Kiam venas gastoj Algimantas vestiĝas per sia tarzana kostumo.</p> <p>Kiel 45-jarulo Algimantas sentis dorsdolorojn. De kiam li ekvivis en la naturo, tiam li forgesis siajn malsanojn. Nun la viro aspektas dekkelk-jarojn pli juna. La litova 62-jarulo asertas, ke li entute malkutimiĝis vivi en brua urbo kaj precipe valorigas liberecon, kiun li trovis en la arbaro.</p> <div style="text-align:right">Laimius S<span class="mm">TRAŽNICKAS</span></div>  
