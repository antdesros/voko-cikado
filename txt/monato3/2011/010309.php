      <h2>Ludilmuzeo de Helsinko</h2>
      <!-- komenco de la enhavo -->

       

       <p>La ludilmuzeo de la finna ĉefurbo troviĝas sur unu el la insuloj Suomenlinna, kiuj situas antaŭ la haveno. La muzeo estas en romantika lignodomo, konstruita en la cara epoko komence de la 20a jarcento. Ĝi havas multege da eksponaĵoj de la 19a jarcento ĝis la 60aj jaroj de la 20a jarcento, inter ili 100 pluŝursojn.</p> <p>La ludiloj estas ordigitaj laŭ historiaj temoj kaj epokoj. Tiel speguliĝas ŝanĝoj en la socio kaj homa konduto. Pri milittempa malriĉo, mankoj kaj inventemo atestas kartonaj veturiloj, strobilaj bovinoj, paperaj pupoj, tabulludoj kun nomoj kiel „Batalo por Karelio” kaj „Lukto por nutrovaroj”. En familioj oni faris pupdomojn el fadenbobenoj, alumetskatoloj, ŝtofpecoj kaj aluminia folio (por speguloj, fenestroj). Fine oni bruligis ilin en la korto, samkiel brulis la domoj post bombardado.</p> <h3>Edukilo</h3> <p>En la 19a jarcento la celo de la plenkresk-aspekta bela pupo estis eduki honestajn virinojn kaj prepari knabinetojn por la estonta virina rolo de edzino, patrino, dommastrino. Oni edukis ankaŭ knabetojn per ludilarmiloj kiel soldatojn.</p> <p>Nur en la 20a jarcento la infano mem komencis speguliĝi en la pupoj. Per la valoraj pupoj de la antaŭa jarcento oni eĉ ne rajtis ludi; ili estis faritaj por admiro kaj ornamo. En la 1950aj jaroj aperis tute alispecaj pupoj, kiujn oni unue malaprobis, ĉar ili aspektis kiel objektoj de seksa admiro. Oni nomis ilin Lilli en Germanio.</p> <h3>Barbie</h3> <p>La Barbie-pupo unue estis talismano de vira klubo kaj nur poste ricevis sian nomon. Iom post iom ŝia vestaro plimultiĝis, kaj ĉiuspecaj akcesoraĵoj estis kreitaj por pliigi la vendadon. La Barbie-pupoj specialiĝis pri metioj kaj ekesprimis la idealojn de moderna virino.</p> <p>La ludilmuzeo povas starigi pontojn inter la generacioj. Irante en ĝi de ejo al ejo kaj sekvante temon post temo, oni povas rakonti kaj demandi pri la pasintaj kaj nunaj travivaĵoj, deziroj kaj sentoj.</p> <p>Dek mil turistoj jare vizitas la ludilmuzeon de Helsinko. Somere ĝia korto estas disponigata por subĉielaj infanaj teatraĵetoj. Interreto: <a href="http://www.lelumuseo.fi">www.lelumuseo.fi</a> (finne, svede, angle, ruse). </p> <div style="text-align:right">Saliko</div>         
