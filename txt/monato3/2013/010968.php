      <h3>Politiko</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Soldatservi: ankoraŭ devige</h2>
      <!-- komenco de la enhavo -->

       

       <p>En januaro la aŭstroj voĉdonis por reteni la sesmonatan devigan soldatservon por ĉiuj viroj. Restos ankaŭ la eblo anstataŭe civilservi naŭ monatojn.</p> <p>Partoprenis en la „popola pridemandado” 50 % de la rajtigitoj. El tiuj 40 % voĉdonis por profesia armeo, sed 60 % preferis daŭrigi la nunan sistemon.</p> <p>Ĝis 1955 Aŭstrio ne estis libera lando. La orienta parto estis okupata de la sovetia armeo, la okcidentaj federaciaj landoj de la armeoj de Francio, Britio kaj Usono.</p> <h3>Taskoj</h3> <p>Laŭ la traktato de 1955 la fremdaj soldatoj devis forlasi Aŭstrion. Kreita estis aŭstra armeo kun tri taskoj: defendi la limojn, protekti la instituciojn de la ŝtato kaj garantii la internan sekurecon kaj helpi okaze de katastrofoj.</p> <p>Kiam disfalis Jugoslavio en 1991, defendocele amasiĝis ĉe la suda landlimo 7700 aŭstraj soldatoj, 150 tankoj kaj 60 pliaj armeveturiloj.</p> <p>En 1965 estis starigita bataliono por subteni eksterlande Unuiĝintajn Naciojn (UN). Ĝis nun en 90 000 okazoj aŭstra soldato aŭ soldatino servis kadre de UN, de Eŭropa Unio, de la Organizaĵo pri Sekureco kaj Kunlaboro en Eŭropo aŭ de <i>Partnereco por Paco</i>.</p> <p>Hodiaŭ aŭstraj soldatoj servas ekzemple en la Golanaj Altaĵoj (Israelo/Sirio), Kosovo (Serbio/Albanio) kaj Bosnio.</p> <h3>Partioj</h3> <p>En Aŭstrio regas koalicio el la Socialdemokrata Partio (SP) kaj la Popola Partio (PP). La socialdemokratoj pledis por profesia armeo, malgraŭ tio ke ĝis antaŭ du jaroj ili preferis ĝeneralan soldatservon.</p> <p>Male PP argumentis por la ĝenerala soldatservo: ĝis antaŭ du jaroj ili preferis profesian armeon. Fakte en Aŭstrio ekzistas ambaŭ. Estas 16 000 profesiaj soldatoj kaj nur 10 000 rekrutoj, kiuj servas dum sesmonata baza trejnado.</p> <p>Nun ĉiuj partioj promesas reformi la armeon. Ili jam ofte promesis.</p> <div style="text-align:right">Walter K<span class="mm">LAG</span></div>   
