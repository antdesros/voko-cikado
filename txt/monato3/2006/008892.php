      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Diktatoroj dorlotataj</h2>
      <!-- komenco de la enhavo -->

       

     <p>La diktatoron de Irako, per sanga milito, detronigis Okcidento kun Usono ĉepinte. Sed saman sorton multaj aliaj diktatoroj ne timu – male: oni dorlotas ilin. Ni prenu nur kvar ekzemplojn el Afriko: Etiopio, Ugando, Eritreo kaj Ruando. En ĉiu el ili regas despotoj, tamen Okcidento dorlotas ilin per respekto kaj, precipe, mono.</p> <h3>Enigmo</h3> <p>La ĉefministro de Etiopio Meles Zenavi estas bone akceptata gasto en Eŭropo. Lia brita kolego vokis lin en sian Afriko-komisionon, antaŭ nelonge li gastis ĉe la germana prezidanto kiel „esper-portanto de la kontinento”, kaj dum la G8-pinto en Skotlando oni ne nur laŭdis lin pro bona regado, sed ankaŭ nuligis ĉiujn eksterajn ŝuldojn de la ŝtato. Estas enigmo, kial Okcidento tiom dorlotas tiun despoton. Sole post la elektoj en majo 2005 la armeo mortigis pli ol cent homojn (inter ili infanojn) dum manifestacioj kontraŭ la reĝimo, pli ol dek mil homoj vegetas en karceroj, inter ili la tuta gvidantaro de la opozicio; ili estas akuzitaj pro ŝtatperfido kaj – plej absurde – genocido. La kialo estas simpla kaj klara: ĉe la elektoj la opozicio venkus, se oni ne manipulus la nombradon de voĉoj.</p> <p>Ankaŭ la prezidanto de Ugando Joveri Museveni estas dorlotata de Okcidento, li ricevas ekzemple grandan parton de germana evoluhelpo, lian landon same la G8-pinto senŝuldigis. Sed Museveni, kiu diktatoras jam dum 19 jaroj, same brutale, kiel Zenavi, persekutas kontraŭulojn. Ankaŭ la uganda opoziciestro estas akuzita, pro ŝtatperfido kaj seksperforto (?). Ke ne temas pri politika proceso, oni ne kredas eĉ en la lando mem.</p> <h3>Promesoj</h3> <p>Diktatoro Isaias Afeverki en Eritreo estas evidente paranoja; lian landon oni plej trafe karakterizas kiel gigantan militistan karceron. Ankaŭ Paul Kagame en Ruando subpremas sian popolon per fera pugno. Tamen ĉiujn tiujn despotojn okcidentaj politikistoj antaŭ nelonge gloris kiel „novajn gvidantojn de Afriko”, ĉar ili promesis demokration, homajn rajtojn, ekonomi-reformojn kaj pli bonan estontecon por siaj popoloj. Oni abunde donacas al la diktatoroj monon. Etiopio ricevas 40 % de la ŝtat-buĝeto rekte el Okcidento, Ugando kaj Ruando eĉ pli ol 60 %. Sed kial? Demokration, bonan regadon kaj homajn rajtojn oni vidas nenie en tiuj ŝtatoj.</p> <p>Kritikantoj ofte parolas pri Afriko kiel „forgesita kontinento”, tamen ŝajnas, ke ĝuste despotoj tie ne estas forgesitaj, sed dorlotataj per milionoj da eŭroj kaj dolaroj. Kial? Certe, la <i>popoloj</i> bezonas helpon kaj monon. Sed la okcidenta evoluhelpo devus ne cementi despotajn reĝimojn, ĝi devus akceli demokration kaj homajn rajtojn. Aŭ ĉu mi miskomprenas ion?</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
