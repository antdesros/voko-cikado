      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Nur ses el dek</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kiom kostas, morale kaj finance, militadi en foraj, fremdaj landoj? Tikla demando,
   aparte en ŝtato kiel Britio, ĵaluze fiera pri sia milita historio kaj nuna memelektita
   rolo kiel vic-ŝerifo en la servo de la usona tutmonda polico.
</p> 
<p>Verdire, ĝis antaŭ nelonge, kritiki la intervenojn de britaj militistoj en Irako aŭ
   Afganio estis preskaŭ hereze. Asertis la registaroj, ke nepras defendi la demokration,
   faligi diktatorojn, kontraŭi terorismon (sed prefere nur tie, kie estis minacata nafto-produktado).
</p> 
<p>Subtenis la registarojn la armilo-industrio: sen militoj, evidente, ne vendiĝas tiom
   da tankoj, aviadiloj kaj aliaj mortigiloj. Jubilis la popolo (kaj la bulvarda gazetaro)
   pro la daŭra „heroeco” de „niaj buboj” batalantaj en dezertoj kaj sovaĝejoj for de la patrujo.
</p> 
<p>Jubilis, ĝis revenis kaj revenadis en ĉerkoj la kadavroj de la junuloj, serĉintaj
   relative bone salajratajn, kamaradecajn postenojn, eventuale iom da gloro, sed trovintaj
   morton.
</p> 
<p>Tiam ekleviĝis la demandoj: kial ni bataladas en landoj, kiujn ni ne povus eĉ surmape
   identigi? Kial estas ŝajne senelirejaj tiuj ĉi militoj, kiuj kostas neimageblajn sumojn,
   kiujn ni povus pli saĝe elspezi hejme, konstruante pli modernajn transportilojn, lernejojn,
   malsanulejojn, eĉ ordinarajn, modestajn loĝejojn? Kaj ĉu, entute, niaj kampanjoj sukcesas,
   alportas al viktimoj kaj suferantoj en la tiel nomataj milito-teatroj (aĉa koncepto!)
   benojn kaj bonojn?
</p> 
<p>Kiel dirite, malfacilis klare respondi al tiaj demandoj – ĝis antaŭ kelkaj semajnoj, kiam aperis funda kaj kredeble fidinda studaĵo, kiu finfine
   pravigis la dubojn kaj la kritikojn de la oponantoj de la enlanda militismo.
</p> 
<p>La raporton, titolitan 
   <i>Militoj en paco</i>, publikigis la Reĝa Instituto pri Unuiĝintaj Servoj, kaj ĝi liveras kondamnan verdikton
   pri lastatempaj britaj intervenoj militistaj.
</p> 
<p>La aŭtoroj kalkulis, ke la kosto de militaj intervenoj post la fino de la tiel nomata
   malvarma milito sumis 34,7 miliardojn da britaj pundoj (42,6 miliardojn da eŭroj).
   Pliaj 30 miliardoj da pundoj estu elspezataj por prizorgi eksajn militistojn.
</p> 
<p>Tamen, laŭ la raporto, ĉi tiuj miliardoj kondukis nur al „strategiaj malsukcesoj” en Irako (ekde 2003) kaj en Afganio (ekde 2005). Post 1990 la „malsukcesoj” solaj konsumis 84 % de la tuta milita interveno-buĝeto.</p> 
<p>Krome, argumentis la aŭtoroj, la tieaj kampanjoj – malgraŭ kontraŭaj asertoj de ĉefministroj kaj aliaj – kontribuis al kreskanta radikalismo kaj fundamentismo inter junaj islamanoj en Britio.</p> 
<p>Ili skribis: „Anstataŭ limigi la internacian terorismon, la invado de Irako en 2003 efektive ĝin
   antaŭenigis. Radikalaj ĝihadistaj grupoj, kiuj etendiĝas trans la iraka-sirian landlimon,
   nun terorisme minacas la Unuiĝintan Reĝlandon kaj ĝiajn aliancanojn, kio eble ne okazus,
   se Sadam Husajn estus reteninta sian povon.”
</p> 
<p>Krome la raporto notis, ke pereis pro la milito verŝajne 100 000 irakanoj, dum pliaj
   du milionoj fuĝis al najbaraj landoj.
</p> 
<p>El entute dek intervenoj la aŭtoroj taksas sukcesaj nur ses: ili estas la malpli multekostaj
   (kompare kun Irako ekde 2003 kaj Afganio) operacoj dum la unua Irak-milito (1991),
   sekvaj flugmalpermesoj super Irako, en Bosnio kaj en Kosovo (1999), Sieraleono (2000),
   kaj unuaj misioj en Afganio ekde 2001, kiuj fuĝigis Talibo-estrojn.
</p> 
<p>Malsukcesaj estis la unuaj packlopodoj en Bosnio en la fruaj 1990aj jaroj, en Irako
   ekde 2003, en Afganio, kaj aer-misioj super Libio en 2011.
</p> 
<p>Eble seso el dek indikas ian sukceson. Certe, en pedagogiaj rondoj, 60 %-sukceso ĝojigus
   multajn lernejanojn kaj gepatrojn. Tamen milito ne estas lerneja aŭ universitata ekzameno.
   Ĝi estas pli grava kaj tuŝas, rekte aŭ nerekte, la vivon de milionoj da senkulpaj,
   viktimiĝintaj homoj.
</p> 
<p>La raporto certe ne kontentigos tiujn, kiuj post 1990 izole kaj promokate levis sian
   feblan voĉon kontraŭ militado. Espereble la konkludoj, kiuj ilin tamen pravigas, iom
   konsolos.
</p> 
