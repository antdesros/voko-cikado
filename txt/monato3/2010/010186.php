      <h3>Medio</h3>
      <h3 class="tem">TOGOLANDO</h3>
      <h2>Lernejoj en akvo</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 13an de septembro 2010 malfermiĝis la lernejaj pordoj en Togolando. Dum iuj gelernantoj senprobleme ekiris al la lernejo, aliaj ĝis nun restas hejme. Ĉu pro la nepovo de la gepatroj pagi la lernokotizon?</p> <p>Oni povas memori, ke en aŭgusto 2008 inundo okazis en Togolando. Tiam homoj iĝis senhejmaj kaj devis ekloĝi en lernejoj. Iujn el tiuj atakis la akvo. Tial la registaro estis devigita prokrasti je du semajnoj la komencon de la nova lerneja jaro. Pasis tiu tempo, sed ne ĉiuj lernejoj seniĝis je akvo. Tamen komenciĝis tiam la lerneja jaro. Ĉiuj tiam pensis, ke nenio tia plu okazos en Togolando. Bedaŭrinde la lerneja jaro 2010-2011 rezervis alian surprizon al la gelernantoj kaj iliaj gepatroj.</p> <p>La 13an de septembro 2010 en pluraj lernejoj en Lomeo (ĉefurbo de Togolando) ankoraŭ estis pluvakvo. La tuta lernejo dronis en akvo, kio malhelpis la gelernantojn komenci la novan jaron. Tamen jen iuj gelernantoj kaj geinstruistoj spitis la akvon kaj komencis la laboron, malgraŭ flakoj en la klasĉambroj. Certe ne ĝojis la akvo, ke homoj loĝas kun ĝi en la klasĉambro. Jen la 18an de septembro 2010 pluvis la tutan tagon, kaj nun tutajn klasojn plenigis la akvo, tiel ke neniu povas eniri. En iuj lernejoj ne estas akvo en la ĉambroj, sed tiom da akvo ĉirkaŭ ili, ke ne eblas aliri ilin. Ĉu la gelernantoj restu hejme? Pripensiga afero.</p> <h3>Uzitaj pneŭoj</h3> <p>La geinstruistoj kaj gepatroj interkonsiliĝis, kaj jen ili fabrikis ion pontecan el uzitaj pneŭoj de aŭtegoj kaj el brikoj, ke la gelernantoj, unu post la alia, aliru la restintajn ne okupitajn klasĉambrojn. Necesas esti tie por vidi, kiel dancas geinstruistoj sur pneŭoj irante al la klasĉambroj. Estas tute aparta spektaklo. La kurshoroj reduktiĝis, ĉar estas malfacile uzi kelkajn klasĉambrojn por pli ol dek klasoj. Tamen pli bone havi iom ol neniom. Jen la gelernantoj nun kontentas pri la iomo nun instruata al ili. Oni povas demandi kiam kaj kiel reagos la registaro.</p> <p>La akvo tiom multas en lernejoj, ke ne pli frue ol en januaro 2011 ĝi povos tute malaperi. Aŭtoritatuloj vizitas la lernejojn, fotas la aferojn kaj reiras. Jam pasis pli ol tri semajnoj post la eko de la nova lerneja jaro, sed nenia reago de la registaro aperis. Dume la geinstruistoj kaj gelernantoj, unu post la alia, uzas la pontetojn por atingi la klasĉambrojn. La lernejo CEG Anfame estas unu el la lernejoj inunditaj. Jen fotoj pri ĝi!</p>           <div style="text-align:right">G<span class="mm">BEGLO</span> Koffi</div> 
