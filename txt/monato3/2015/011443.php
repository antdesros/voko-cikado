      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Ni estas Charlie</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ne necesas ĉi tie skizi la hororojn, kiuj trafis dum merkreda mateno en Parizo redakcianojn
   de la franca satira magazino 
   <i>Charlie Hebdo</i>. Raportoj kaj filmeroj ekplenigis amaskomunikilojn kaj interretajn forumojn nur minutojn
   post la atenco, kiu lasis 12 homojn mortaj, inkluzive de policanoj, kaj plurajn aliajn
   vunditaj.
</p> 
<p>Reagis ŝokite kaj naŭzite ne nur politikistoj kaj ĵurnalistoj, pro atako kontraŭ libera
   esprimado kaj do kontraŭ fundamento de la demokratia socio, sed ankaŭ ordinaraj civitanoj
   en urboj kaj en kaj ekster Francio. Ilia mesaĝo, diversmaniere esprimata, sed esence
   unuvoĉa: Armiloj ne malakrigu niajn krajonojn.
</p> 
<p>Kio trairis la mensojn de la teroristoj, malfacile diveneblas, eĉ malpli facile kompreneblas,
   kaj neniel pardoneblas (krom, eventuale, en la ĉielo, misagordita, tordita, je kiu
   verŝajne kredas la murdistoj). Laŭraporte, unu el la atencantoj, Cherif Kouachi, 32,
   jam kondamnita en la 2000aj jaroj al 18-monata enkarcerigo, ĉar li helpis sendi militistojn
   al Alkaido en Irako, diris dum la proceso, ke laŭ islamaj sanktaj tekstoj estas devigaj
   kaj ĝihado kaj memmortigo.
</p> 
<p>Jen, ŝajne, homo fie influata de religiaj tekstoj interpreteblaj ajnmaniere, ne nur
   de ekstremaj kaj fanatikaj imamoj kaj aliaj predikantoj. Do, jes, en la pariza atenco
   eblas aŭdi venĝon (kiel anoncis mem la teroristoj) kontraŭ karikaturistoj, kiuj primokis
   profeton neprimokendan; eblas vidi enmiksiĝon de okcidentaj landoj en mez-orientaj
   aferoj, kiuj – malgraŭ nafto – ilin ne koncernas; eblas flari frustriĝon kontraŭ okcidenta mondo pli kaj pli preta
   kulpigi fremdulojn, enmigrintojn, pro ekonomiaj kaj aliaj krizoj.
</p> 
<p>Tamen ne eblas eviti la respondecon de religio. Kiel supre dirite, religiaj tekstoj
   diversmaniere interpreteblas. Do senkulpigoj de islamanoj (kaj nepras memori, ke nur
   eteta elcentaĵo da samkredanoj atencas, murdas, ĝihadas), ke la teroristoj misinterpretas
   t.n. noblajn tekstojn, ke ili ne estas veraj islamanoj, nur ĝis difinita grado kontentigas.
</p> 
<p>Islamanoj mem elradikigu ekstremismon el siaj moskeoj kaj lernejoj. Nepras, ke ordinaraj
   islamanoj agu ne nur surstrate solidare kun aliaj „charlie-oj” sed ankaŭ en siaj preĝejoj, komunumoj, eĉ familioj.
</p> 
<p>Kiel pepis la verkisto Salman Rushdie, mem celo en 1989 de islama murd-minaco pro
   sia libro 
   <i>Satanic Verses</i>, „respekti religiojn nun signifas timi religiojn”. Alivorte, necesas pli sisteme, honeste kaj kuraĝe alfronti religiajn kredojn kaj,
   se necese, atentigi pri mankoj kaj misoj.
</p> 
<p>Efektive tion jam faris la teroristoj. Kriante post la sangoverŝado „<i>Allahu Akbar</i>” (pli malpli „Gloras dio”), ili pleje malglorigis sian dion kaj, verdire, senhonorigis sian profeton oble pli
   efike ol faris la karikaturistoj de 
   <i>Charlie Hebdo.</i>
</p> 
<p>Samtempe, tamen, ili nutris la antaŭjuĝojn de rasistoj kaj dekstremuloj, kiuj timas
   malfortigon de tradiciaj kulturoj kaj moroj. Jam antaŭ la atenco en Parizo surstrate
   manifestaciis en Dresdeno, Germanio, t.n. patriotaj eŭropanoj kontraŭ la islamigo
   de la okcidento. Post la atenco laŭtiĝis la voĉoj de naciistaj politikistoj kiel Marine
   Le Pen en Francio kaj Nigel Farage en Britio.
</p> 
<p>Jen la danĝero. Ne fanatikuloj, frenezuloj, malstabiligos nian socion, sed ni mem,
   aŭ niaj politikistoj. Ili nianome postulos pli da sekureco, pli da sekreteco, pli
   da kontroloj, por nin „defendi”, samtempe forigante aŭ nuligante ĝuste la valorojn, kiujn volas protekti la t.n.
   patriotoj. Eble tion scias la teroristoj kaj iliaj predikantaj, imamaj mastroj. Eble
   ĝuste tion ili celas.
</p> 
<p>La eventoj en Parizo denove emfazas la bezonon pri libera, malferma gazetaro. Evidente,
   la murdoj ne silentigos ĵurnalistojn – plejparte obstine kuraĝaj, fiere sendependaj.
</p> 
<p>Ankaŭ M<span class="mm">ONATO</span>, feliĉe ne alfrontita al la danĝeroj, al kiuj estas elmetitaj la redakcianoj de 
   <i>Charlie Hebdo</i>, aŭdigos sian voĉon por krei pli liberan, pli justan, pli internacian mondon. Tiel
   ni daŭre montros, ke ankaŭ ni estas Charlie.
</p> 
