      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Porkonkursa reflekto</h2>
      <!-- komenco de la enhavo -->

       

     <p>Spirita kulturo ĝenerale estas reflekto de la koncerna socio. Kiam ĝi sufiĉe maturiĝas, ĝi mem komencas reflekti, naskante diversajn formojn de la kulturologiaj riveliĝoj, inkluzive eseojn pri literaturo. Ekzistas diversaj opinioj pri la nivelo de seriozeco de priliteraturaj esploroj en Esperantio, sed, ĉiuokaze, en 1976 en la Belartaj Konkursoj de UEA aperis speciala branĉo, „Eseo”, kun premio je la nomo de Luigi Minnaja, al kiu oni kontribuas, interalie, per literaturologiaj esploroj, kiuj foj-foje en la konkurso gajnas. Michela Lipari kaj Humphrey Tonkin kolektis 11 eseojn de 9 aŭtoroj, kiuj temas pri Esperanto-literaturo kaj kiuj gajnis la premion, kaj UEA eldonis ilin en aparta libro.</p> <h3>Stranga logiko</h3> <p>La libro estas vere tre interesa kaj enhavoriĉa; mi legis ĝin kun streĉita intereso de la komenco ĝis la fino. La sola afero, kiun mi ne komprenis, estas la maniero de kompilo: la eseoj estas aranĝitaj nek kronologie, nek alfabetorde laŭ la aŭtoroj, nek laŭteme ... Eble iu logiko ĉe la kompilintoj ekzistis, sed mi ĝin ne sukcesis kapti. Nu, tio tute ne malhelpas al la legado ...</p> <p>Inter la aŭtoroj de la libro ni trovas nomojn de kelkaj konataj prozistoj kaj poetoj, kiuj kompetentas pri la literaturo almenaŭ pro tio, ke ili mem verkas beletron: Sten Johansson, Lena Karpunina, Krys Ungar, Gonçalo Neves kaj aliaj. Aliaj estas seriozaj esploristoj de nialingva kulturo, kiel, ekzemple, Geoffrey Sutton, Eva Tófalvy kaj Jukka Pietiläinen. Do la nivelon de kontribuoj karakterizas ne nur la fakto de ricevo de la premio.</p> <h3>Tre malsama</h3> <p>Karakteroj kaj stiloj de la eseoj estas tre malsamaj: de pure publicaj pecoj, kiel tiu de Karpunina (pri eldonpolitiko) kaj Tófalvy (pri la vivmaniero kaj motivoj por verkado de niaj verkistoj), ĝis profundaj sciencecaj esploroj de Pietiläinen kaj Sutton pri periodigo de la esperantlingva literatur-historio. Estas skizoj pri apartaj verkoj kaj aŭtoroj – esploro pri la zamenhofa „La vojo” far Carmel Mallia, pri Maria Hankel far Ungar, pri Stellan Engholm, Karolo Piĉ kaj Eli Urbanová far Johansson. (Cetere, se paroli pri la pure literaturstilaj karakterizoj, do la du eseoj de Johansson estas la plej elstaraj pecoj en la tuta kolekto.)</p> <h3>Matureco</h3> <p>Krom ĝenerala ĝuo legi la libron, kiu povas stimuli la leganton, ekscii ion novan, enpensiĝi pri problemoj de nia kulturo kaj (re)legi certajn konsideratajn verkojn, ĝi brile pruvas la maturecon de nia kulturo, eĉ kiam temas pri verkoj porkonkursaj. Aparteni al tia kulturo, eĉ kiel ĝia nura konsumanto, estas malhonte!</p> <div style="text-align:right">Nikolao G<span class="mm">UDSKOV</span></div> <h6 class="bib">Pri homoj kaj verkoj. Eseoj pri la Esperanto-kulturo . Red. Michela Lipari kaj Humphrey Tonkin. Eld. UEA, Rotterdam, 2012. 143 paĝoj broŝuritaj. ISBN 978-92-9017-116-4.</h6>  
