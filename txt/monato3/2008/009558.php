      <h3>Moderna vivo</h3>
      <h3 class="tem">SINMORTIGOJ</h3>
      <h2>Mankas mono, mankas sano</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Japanio mortigas sin ĉiujare pli ol 30 000 homoj. En junio 2008 raportis la ŝtata policagentejo, ke en 2007 mortigis sin 938 homoj pli ol en la antaŭa jaro – entute 33093 homoj. Statistike propramane pereas plej ofte 60-69-jaruloj kaj 30-39-jaruloj.</p> <p>En la pli aĝa grupo, konsistanta el 8451 personoj, ĉefaj kaŭzoj estas korpa malsano (3644 kazoj), mensa malsano (2070) kaj malriĉeco (371). En la pli juna grupo, konsistanta el 3365 personoj, temas pri mensa malsano (996), korpa malsano (346) kaj ŝuldoj (341). Aliaj kialoj rilatas al lacego pri prizorgado de malsanaj parencoj (pli aĝa grupo) aŭ malbonaj rilatoj kun kolegoj (pli juna grupo).</p> <p>Ŝajnas, do, ke multaj maljunuloj, ofte malbone kaj nesufiĉe pensiitaj, malfacile vivas. Ne eblas trovi laborlokojn; familianoj ilin ne povas prizorgi; vivi en malsanulejoj tro multe kostas. Malriĉeco ofte kontribuas al la deprimo, kiu ilin pelas al sinmortigo.</p> <h3>Deprimo</h3> <p>Pli junaj homoj, kontraste, multe suferas ĉe siaj laborejoj. Kvar el kvin sinmortigoj en tiu ĉi grupo rilatas al laboro: mensa malsano (deprimo), korpa malsano, lacego pro laboro kaj malbona rilato kun kolegoj. Japanaj kompanioj anstataŭigas plentempajn laboristojn per partatempaj. La plentempuloj devas labori du- aŭ tri-oble pli ol antaŭe por ŝtopi truojn kaŭzitajn de malpliigo de laboristoj. Partatempaj laboristoj sklave laboras laŭ postuloj de kompanioj, kiuj abrupte ĉesigas laborkontraktojn. Tiaj malkontento kaj maltrankvilo foje erupcias kiel sinmortigo, kaj ne malofte kiel krimo.</p> <p>En ambaŭ grupoj gravas ŝuldoj. Fojfoje ne eblas al maljunuloj prunte preni monon. Ili do turnas sin al kontraŭleĝaj uzuristoj, kaj tiel amasiĝas ŝuldoj.</p> <h3>Aproba sinteno</h3> <p>En Japanio ekzistas aproba sinteno pri sinmortigo. Laŭ esploro en urbo en la gubernio Aomori, 30 % de la loĝantoj inter 40 kaj 69 jaroj kredis, ke permeseblas sinmortigo, se ekzistas gravaj kialoj por ĉesigi la vivon. En situacioj tiaj, 20 % de la enketitoj de la registara esploro konfesis, ke ili iam serioze pripensis sinmortigon. Multaj japanoj opinias, ke problemoj estas personaj, kaj do per sinmortigo solveblaj.</p> <p>Lastatempa furoro estas kunaj sinmortigoj de gejunuloj, kiuj arigis kunmortontojn per Interreto. Antaŭe ili mortis per karbona monooksido eligita el bruliganta karbo. Lastatempe ili mortas per venena gaso el kunmetitaj banejaj purigaj medikamentoj. Multaj junuloj ne povas trovi vivcelon en la socio, kaj danke al hejmpaĝoj pri sinmortigo ili trovas pli facilajn rimedojn por tion efektivigi.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
