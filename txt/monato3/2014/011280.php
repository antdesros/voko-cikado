      <h3>Scienco</h3>
      <h3 class="tem">FOSILIOJ</h3>
      <h2>De Gabono al Aŭstrio por unika ekspozicio</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Ĝis la fino de junio en Vieno admireblas unuafoje originalaj fosilioj gabon-devenaj.
      Ili pruvas, laŭ artikolo aperinta en la revuo 
      <i>Nature</i> en 2010 kaj kontraŭe al tio, kion homoj ĝis tiam kredis, ke multĉelaj vivaĵoj ekzistis
      jam antaŭ 2100 milionoj da jaroj.
   </b>
</p> 
<h3>Geologo</h3> 
<p>Nia tero estiĝis antaŭ 4500 milionoj da jaroj, sed la vivo aperis antaŭ 3800 milionoj
   da jaroj. La unuaj organismoj estis nuraj unuĉelaj bakterioj kaj arkeoj. Rilate al
   multĉelaj vivaĵoj, fakuloj antaŭe opiniis, ke ili ekestis antaŭ 580 milionoj da jaroj:
   oni aludis al la tiel nomata kambria eksplodo, dum kiu la vivformoj multobliĝis danke
   al la plialtiĝo de la oksigen-niveloj kaj el kiu evoluis la nuntempa vivo mem. Tamen
   en 2008 la franc-maroka geologo Abderrazak El Albani, el la franca universitato de
   Poitiers, malkovris tre bone konservatajn fosiliojn de multĉelaj organismoj, ĝis 17
   centimetrojn grandaj.
</p> 
<h3>Sedimentoj</h3> 
<p>La altvalora malkovro okazis en Gabono, afrika lando situanta proksime al la ekvatoro.
   La loko, kie oni trovis la fosiliojn, kuŝas apud la urbo Franceville, en la samnoma
   baseno. Temas pri iama maro, kie amasiĝis sedimentoj, kiuj restis tie senŝanĝaj dum
   pli ol du miliardoj da jaroj ĝis hodiaŭ. Estas rimarkinde, ke oni tie ekspluatas uranion,
   bezonatan por francaj atom-centraloj.
</p> 
<h3>Mikrotomografio</h3> 
<p>La menciitaj fosilioj troviĝas en ardezo kaj aĝas 2100 milionojn da jaroj. Helpe de
   mikrotomografio eblis tridimensie rekonstrui ilian kompleksan internan strukturon.
   La trovitaj 450 eroj havas diversajn formojn: cirklan aŭ longiĝintan, kian ekzemple
   havas vermoj, sed ĉiam kun globforma centra korpo. El la formo de la rando oni rajtas
   konkludi, ke la ĉeloj estis specialigitaj kaj iel interkomunikis. Ne ekzistas paralelaĵoj
   en la nuna mondo. Eble la gabonaj fosilioj estis speco de viv-eksperimento, kiu tute
   ne postlasis spurojn. Krome ne estas konate, ĉu tiaj vivaĵoj ekzistis sur la tuta
   tero, ĉar preskaŭ ĉiuloke la fosilioj malaperis pro geologiaj procezoj. La esploristoj
   trovis, ke tiuj organismoj vivis en mara medio relative proksima al la marbordo (oni
   parolas pri profundeco de ne pli ol 30 metroj) kaj trankvila, kvankam fojfoje trafebla
   de tajdoj, ondoj kaj tempestoj.
</p> 
<h3>Oksigeno</h3> 
<p>Dum la gabon-fosilia periodo, nomata Proterozoiko, la kvanto de oksigeno libera en
   la atmosfero ege kreskis, eble pro la ĉeesto de cianobakterioj. La multiĝo de oksigeno
   ebligis la rapidan kreskon de multĉelaj vivaĵoj. Cent milionojn da jaroj poste, la
   oksigena elcento malaltiĝis kaj la organismoj, kies fosiliojn oni trovis ĉe la ekvatora
   Afriko, forsvenis.
</p> 
<h3>Muzeo</h3> 
<p>La ekspozicio estas ĝuebla dum kelkaj monatoj en la Viena Naturhistoria Muzeo. Ĉi-lasta,
   malfermita en 1889, paradas per 30 milionoj da objektoj kaj estas unu el la plej gravaj
   kaj vizitindaj natursciencaj muzeoj de la mondo.
</p> 
