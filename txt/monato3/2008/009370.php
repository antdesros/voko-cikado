      <h2>Beletraj prezidantoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>En marto 2008 rusianoj elektos la prezidanton de la Rusia Federacio. Dume aŭskultantoj de la radiostacio <i>Eĥo Moskvi</i> (Eĥo de Moskvo) povas ĉiusemajne elekti „prezidanton” inter herooj de rusaj literaturaĵoj.</p> <p>En ĉiu radioelsendo oni konsideras kvar kandidatojn el unu aŭ kelkaj verkoj. Unu el la kandidatoj venkas per interreta voĉdonado antaŭ la radioelsendo mem, kaj la tri aliaj estas pridiskutataj dum la elsendo. Oni povas telefoni kaj agiti por aŭ kontraŭ kandidatoj. La aŭskultantoj ankaŭ estas informataj pri opinioj de famaj verkistoj, ĵurnalistoj kaj politologoj. Poste okazas telefonaj voĉdonadoj, kaj finfine aŭskultantoj elektas inter la interreta kaj telefona venkintoj.</p> <p>Por la „prezidanta posteno” oni proponas neordinarajn figurojn, ne ĉiam pozitivajn, ne ĉiam protagonistojn. Ekzemple, el la romano de M. A. Bulgakov <i>La majstro kaj Margarita</i>, en elektoj partoprenis Poncio Pilato, Azazello, la kato Behemoto kaj Kerubjev (venkis Behemoto). Dum konkurado de fabelaj herooj Ivan la Malsaĝulo superis la fortulon Aloŝa Popoviĉ (Aleĉjo, filo de popo), Koŝĉejon la Senmortan kaj la arbaran monstron Solovej-razbojnik (Najtingalo-rabisto).</p> <p>Estis du konkuradoj ekskluzive por virinoj. En la unua sukcesis Tatjana Larina el <i>Eŭgeno Onegin</i> de A. S. Puŝkin. Ŝi ŝajnis al aŭskultantoj pli taŭga por prezidanteco, ol Koroboĉka el <i>Malvivaj animoj</i> de N. V. Gogol, Nataŝa el <i>La majstro kaj Margarita</i> kaj Ranevskaja el <i>Ĉeriza ĝardeno</i> de A. P. Ĉeĥov. En la dua elsendo venkis Sonja Marmeladova el <i>Krimo kaj puno</i> de F. M. Dostojevskij (kun 52 % kontraŭ 48 % por Nataŝa Rostova el <i>Milito kaj paco</i> de L. N. Tolstoj).</p> <p>En aliaj elsendoj konkuradis kvaropoj el verkoj de A. S. Gribojedov, N. V. Gogol, L. N. Tolstoj, A. P. Ĉeĥov, I. Ilf kaj Je. Petrov, F. M. Dostojevskij, M. A. Bulgakov, A. S. Puŝkin, M. Ju. Lermontov kaj I. S. Turgenev.</p> <p>La elsendo, kiu nomiĝas „Superpotenco”, unuafoje aperis somere, kiam feriis la kutima politika programo <i>Potenco</i>. En <i>Superpotenco</i> estas iom da politiko, tamen ĝi ĉefe celas revenigi aŭskultantojn al bonega rusa literaturo.</p> <div style="text-align:right">Anna kaj Mati P<span class="mm">ENTUS</span></div> 
