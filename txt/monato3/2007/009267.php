      <h2>Flickr malbone flikas</h2>
      <!-- komenco de la enhavo -->

       

       <p><i>Flickr</i> (<a href="http://www.flickr.com">www.flickr.com</a>), unu el la multaj servoj de la giganta firmao <i>Yahoo!</i>, estas la plej granda reta foto-arkivo de la mondo. Centmiloj da fotoamatoroj kaj profesiuloj montras tie siajn verkojn. La 13an de junio la germanlingvaj vizitantoj de <i>Flickr</i> subite rimarkis, ke multaj bildoj, kiujn ili en la antaŭaj tagoj povis vidi, nun subite ne plu estas alireblaj. Anstataŭ la foto venis mesaĝo: „Tiu ĉi foto ne estas disponebla al vi”.</p> <p>Komence oni emis kredi, ke temas pri cimo en la programaro, sed post kelkaj horoj la realo montriĝis alia. Iu rimarkis malgrandan ŝanĝon – tute ne anoncitan – en la regularo: „Se via identiga numero havas kiel bazon Singapuro, Germanio, Honkongo aŭ Koreio vi povos vidi nur bildojn kun sekura enhavo”.</p> <p>Tiu ĉi ekscio ene de kelkaj horoj kaŭzis lavangon da protestoj. Miloj da fotografoj kaj fotografaj grupoj el la tuta mondo afiŝis protest-tekston aŭ -foton sur sia ĉefpaĝo: „Ne al la cenzuro!” Tute strange, <i>Flickr</i> dum preskaŭ 24 horoj ne reagis. Nur la postan tagon venis mallonga komunikaĵo: „La nuna situacio estas por ni [<i>Flickr</i>] tre malagrabla, sed – kredu nin – ni ne havas alternativon.”</p> <h3>Lokaj leĝoj</h3> <p>Intertempe la protestoj daŭris kaj multaj minacis venontjare ne rekotizi. Kelkaj protestafiŝoj iĝis eĉ tiel popularaj, ke ili aperis en la ĉefpaĝo inter la plej vidataj fotoj. Post plenaj du tagoj da protestado venis nova oficiala komunikaĵo: „La decido ŝanĝi la <i>Flickr</i>-sperton en Germanio neniam rilatis cenzuron. Ĝi estas farita en provo certigi, ke <i>Yahoo!</i>-Germanio estu konforma al la lokaj leĝoj.”</p> <p>Pri kiuj leĝoj temas, ne estis dirite. Germanaj juristoj diskutis, sed ne trovis precizajn leĝojn. Eble la cenzurado baziĝas sur <i>Jugendschutzgesetz</i> (leĝaro por protekti gejunulojn), plistriktigita, post kiam en 2002 19-jaraĝulo en Erfurt mortigis instruistojn kaj lernantojn.<sup>1</sup></p> <p>Laŭ tiu ĉi leĝo montri „danĝerajn fotojn” al junuloj estas krimo. Sed <i>Flickr</i> baris aliron ne nur al junuloj, sed al ĉiuj germanlingvaj abonantoj. Cetere ĝi baris milionojn da fotoj, kies „danĝero” estas ridinda. Tiel <i>Flickr</i> provis fliki sian paĝaron laŭ malbone pripensita maniero.</p> <h6 class="pied">1. Du semajnojn poste oni informis, ke la koncerna leĝo estas <i>Staatsvertrag über den Schutz der Menschenwürde und den Jugendschutz in Rundfunk und Telemedien</i> (Federacia traktato pri la protektado de la homa digno kaj de la junularo en radio, televido kaj Interreto).</h6> <div style="text-align:right">pp</div>   
