      <h2>Kiom da elektroniko?</h2>
      <!-- komenco de la enhavo -->

       

      <p>Antaŭ nelonge ni aĉetis poŝtelefonon por nia nepino, ĉar ĉiuj ŝiaj amikinoj jam delonge posedas tian aparateton. Do ankaŭ ŝi devas havi, ĉar sen ĝi evidente la vivo de 12-jarulo estus mizera. Ŝi havis klaran imagon pri la minimuma ekipo: oni devas ne nur telefoni, sed ankaŭ foti, aŭskulti muzikon, produkti filmetojn, skribi mesaĝetojn per poŝtelefono. Kiam komence (dum pluraj semajnoj) ne bone funkciis telefonado, tio ne multe ĝenis ŝin; gravis, ke ŝi povas foti kaj filmi, instali muzikon – kaj ĉion ĉi konstante ŝi fiere prezentis al ni. Tiam mi komprenis, ke poŝtelefono estas precipe ludilo. Efektive estas nun tre malfacile akiri poŝtelefonon, per kiu oni nur telefonas. Tamen kelkaj produktistoj intertempe proponas denove tiajn simplajn telefonilojn por malmodernaj homoj. Sed ordinare poŝtelefonoj estas nun plenŝtopitaj per akcesoraj programoj, tiel ke oni devus trovi tute alian nomon por ili.</p> <p>Fakte ja abunda elektroniko troviĝas nun en preskaŭ ĉiuj aparatoj kaj maŝinoj. Ekzemple, niaj lavmaŝino kaj manĝilarlavilo proponas dekojn da programoj. Por ni ja sufiĉus kelkaj programoj: por blanka tolaĵo, por bunta, je 30 kaj 60 kaj 90 celsiaj gradoj. Sed estas 23 diversaj programoj, kvankam ni uzas maksimume 5 diversajn. Tamen ni, aĉetante la maŝinon, devis ja pagi ankaŭ la ceterajn 18. Kaj kiam ekestis eta difekto, la mekanikisto elprenis la kompletan komputileton kaj metis novan en la maŝinon. Li ne kapablis ripari nur la difekteton. Ankaŭ nia aŭtomobilo havas tiom da elektroniko, ke ni devus dum horoj studi la instrukciojn – kaj tamen ĝis hodiaŭ mi ne kapablas simple manipuli la radion en ĝi. La rapidometro sur la biciklo de mia edzino kostis nur ĉirkaŭ dek eŭrojn, sed havas pli ol 20 funkciojn, el kiuj ŝi bezonas nur 3. Sed instali ĝin mem ni ne kapablis, estis tro komplike.</p> <p>Mi demandas min, ĉu ne ĝustas, ke en modernaj aparatoj malpli da elektroniko estus pli bone. Por resti ĉe la poŝtelefono: ĝiaj fotoj estas miniaturaj kaj mizeraj kompare kun tiuj de normala fotoaparato, ĝia muziko estas mizera kompare kun tiu de kompakta disko ktp. Do kial ne produkti aparatojn kun nur tiuj funkcioj, kiujn ni vere bezonas kaj volas havi? Nia nepo havas rapidometron kun nur kvin funkcioj, kiu estas facile instalebla; li estas tre kontenta. Sed ĝi kostis multe pli ... Ho kia renversita konsumomondo! Kaj atendas nin novaj superelektronikaĵoj, ekzemple nova televidilo kun eĉ pli akraj bildoj. Por tia eta kvalitosalto mi devus pagi plurajn centojn da eŭroj – ne, dankon!</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
