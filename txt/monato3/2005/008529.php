      <h3>Moderna vivo</h3>
      <h3 class="tem">INFANOJ</h3>
      <h2>Edukado  ne ekspluatado</h2>
      <!-- komenco de la enhavo -->

       

       <p>La fruaj jaroj devus prepari la infanon por respondeca maturula vivo. Infano ne devus labori kaj eĉ malpli devus esti elmetata al milito, ĉu kiel viktimo, ĉu kiel soldato. En Afriko tamen, en la jaro 1990, 120 000 infanoj en la aĝo de 7 ĝis 18 jaroj iel implikiĝis en militoj. Dum la 90aj jaroj tiu ĉi cifero sendube kreskis. Sole en la militoj en la Demokratia Respubliko Kongo en 1996 kaj 1997 partoprenis 20 000 infan-soldatoj.</p> <p>Samtempe en DR Kongo kreskis infan-laboro. Multaj gejunuloj estis submetataj al la plej severaj formoj de laboro, malpermesitaj en la konvencio 182 kaj la rekomendo 190 de la Internacia Labor-Organizaĵo. Laŭ raporto de Unuiĝintaj Nacioj devas labori en DR Kongo ĉirkaŭ kvar milionoj da infanoj por vivteni sin aŭ iel ekonomie subteni siajn familiojn.</p> <p>Infan-laboro estas konsiderata kiel rimedo kontraŭ senlaboreco de la gepatroj. Tamen ĝi bremsas aŭ tute malebligas la lernadon de la infanoj. En DR Kongo kreskis en la lastaj dek jaroj la nombro de infan-laborantoj de 49 % al 70 %.</p> <h3>Falsaj malsanuloj</h3> <p>Estas rimarkite, ke ekde 1990 en DR Kongo infanoj laboras i.a. kiel servistoj, soldatoj, portistoj de handikapuloj, muzikistoj kaj dancistoj, prostituatoj, ŝtonfrakasistoj ĉe konstruejoj, kaj ministoj. Krome infanoj estas trejnitaj de la gepatroj kiel almozpetantoj, falsaj malsanuloj (por trompi helpemulojn) aŭ kiel vendistoj de narkotaĵoj.</p> <p>Aparte en kamparaj regionoj la gepatroj ekspluatas siajn infanojn, kiuj laboras en la kampoj, portas pezajn ŝarĝojn aŭ perlaboras iom da mono ĉe potfabrikoj. Ĉe plantejoj la infanoj estas tre dezirataj por porti nuksojn, funkciigi muelilojn aŭ premilojn kaj por vendi palmoleon.</p> <h3>Or-minejaj regionoj</h3> <p>Gravas la situacio en diamant- aŭ or-minejaj regionoj (ekzemple Mbuji-Mayi [mbuĝi maji], Tshikapa [ĉikapa], Kisangani). Ĉi tie multaj infanoj forlasas la lernejojn por fariĝi ministoj. Tiam ili alfrontas diversajn danĝerojn, kiuj ne malofte kondukas al morto.</p> <p>Nepras informi la kongan popolon pri tio, kio okazas al la infanoj. Ĉe la bazaj komunumoj en la lando oni ekluktu kontraŭ infan-laboro: la komunumoj grupiĝu por pli fortike rezisti al ekspluatado tia. Samtempe necesas kunlabori kun la diversaj instancoj por enkonduki kaj por respekti leĝojn pri infan-laboro. Ankaŭ gepatroj rekonsideru sian sintenon rilate infanojn: ili ne elmetu gejunulojn al malfacila, iufoje kruela laboro.</p> <div style="text-align:right">Emile M<span class="mm">ALANDA</span> N<span class="mm">IANGA</span>/pg</div> 
