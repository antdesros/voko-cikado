      <h2>Fosilioj en suda Germanio</h2>
      <!-- komenco de la enhavo -->

       

       <p>Fosilio de la mondvaste konata „pra-birdo” arkeopterigo troviĝas en la Londona Naturhistoria Muzeo (fondita el parto de la Brita Muzeo), kiu ĝin aĉetis tuj post ĝia malkovro en 1861. Kaj en la Berlina Muzeo por Naturscienco de la Humboldt-Universitato estas la probable plej bela kaj kompleta ekzemplero.</p> <p>Ambaŭ kaj pliaj aliaj troviĝis en supra ĵurasio kaj devenas el ŝtonrompejoj proksime al Solnhofen kaj najbaraj urboj en suda Germanio. La lasta estis trovita en 1992 (<i>Brockhaus multimedial</i>) aŭ en 2005 (<i>Vikipedio</i>).</p> <p>Krom tiuj vaste konataj fosilioj la molaso en suda Germanio, antaŭ jarmilionoj ŝutiĝinta de la alpoj, konservis multajn interesajn bestojn kaj plantojn. Inter ili troviĝas fiŝoj, akvolilioj, skeleto de mezosaŭro, flugsaŭro, kraboj, terciara hipokampo, skorpio el la kretaceo, fosilia rano kun konserviĝinta haŭto el la mioceno. Videndaj estas fosiliaj piedsignoj kaj spuroj kaj birdovoj el la kratero Ries [ris], fosiliaj ginko-folioj, insektoj en sukceno kaj impresa trovo de makzelo de antropomorfa prasimio el la ĉirkaŭaĵo de Aŭgsburgo.</p> <p>Troveblas ĉiaj fosiliaj bestospecioj, de stomatolitoj el antaŭkambrio ĝis restoj de vertebruloj el la glaci-epoko. Inter la trovitaj senvertebruloj estas precipe amonitoj kaj ĵurasiaj spongoj.</p> <p>Kompare al la teksasaj flugsaŭroj, la ĵurasiaj el Solnhofen kaj Eichstätt [ajĥŝtet] estis nanoj: de pasera grandeco ĝis maksimume la grandeco de aglo.</p> <p>En decembro 2004 ĉe eta vilaĝo apud Aŭgsburgo en gruzejo oni malkovris ion ege sensacian: skeleton de pra-elefanto. Fakuloj identigis ĝin kiel Arkeobelodonon. Ĝi estas unika en Germanio, en Eŭropo nur unusola plia ekzemplero estas en Parizo, mondvaste ankoraŭ unu en Kairo.</p> <p>Paleontologo miris, malkovrinte du brunajn striojn en argilo de la gruzmuro: eburo de elefantecaj pikdentoj. Tuj li sciis, ke li malkovris ion nekutiman.</p> <p>Kiam mallongtempe poste komenciĝis elfosado, la fakuloj spertis pluan surprizon: ne nur pikdentoj konserviĝis, sed preskaŭ tuta skeleto de praulo de elefanto aĝa ĉirkaŭ 14 milionojn da jaroj. Pro la eluzo de dentoj oni supozas, ke la besto mortis pro aĝokadukeco, kaj la formo de la pelvo indikas, ke ĝi estis vira besto.</p> <p>Ses semajnojn sciencistoj laboris por elfosi kaj savi la skeleton. Oni ne volis perdi tempon, por ke la elfositaĵoj ne disfalu en la aero. Tage kaj nokte la sciencistoj restis tie por gardi la valoran trovitaĵon kontraŭ vandalaj scivolemuloj kaj stultuloj. Helpema buĉisto ĉiutage provizis la teamon per nutraĵoj. Fakuloj moknomis la elefanton <i>backleg</i> – „malantaŭgambulo”, ĉar mankis la antaŭaj gamboj de la skeleto.</p> <p>La posedanto de la gruzejo donacis la skeleton al la naturmuzeo de Aŭgsburgo, kie oni baldaŭ ekspozicios la unikan trovitaĵon.</p> <div style="text-align:right">Jomo I<span class="mm">PFELKOFER</span></div> 
