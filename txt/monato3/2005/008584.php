      <h2>Kampoj de Soria</h2>
      <!-- komenco de la enhavo -->

       

      <p>Dezirus mi aldoni kelkajn alĝustigojn al la bonega eseo de Garbhan MacAoidh <i>Kampoj de Soria</i>, aperinta en M<span class="mm">ONATO</span> (2004/12, p. 22 24).</p> <p>Unue, „Castilla-León” ne estas ĝusta nomo. Oficiale ĝi estas „Castilla y León” (Kastilio kaj Leonio), ĉar temas pri kunigo en unu aŭtonoma komunumo de du malnovaj landoj: Castilla la Vieja (Malnova Kastilio) sen du provincoj Santander kaj Logroño, kaj León. Leonio estas nomata <i>Llión</i> en la asturleona lingvo. Tiu ĉi lando havas proprajn historion, lingvon kaj kulturon malsamajn ol tiuj de Kastilio.</p> <p>Due, la keltiberoj estis aro da popoloj: arevakoj, beloj, lusonoj kaj titoj (kvankam laŭ kelkaj aŭtoroj aliaj, jen palendonoj, jen vaceoj). Tiuj grupiĝoj havis karakteron pli etnan kaj geografian, ol politikan. La klasikuloj nomis ilin keltiberoj, eble konstatante la fortan kulturan influon de la ibera mondo, sed eraris, konsiderinte ilin kiel miksaĵon de keltoj kaj iberoj. Hodiaŭ oni konsideras tiujn grupiĝojn keltaj.</p> <p>Tie. Rilate la sieĝon de Numancio la klasikaj fontoj mencias, ke la romiaj legioj, konkerinte la urbon, trovis neniun vivanton. La lastaj defendintoj ekbruligis la urbon kaj mortigis sin por ne fali en sklavecon post tiom heroa rezistado.</p> <div style="text-align:right">Nikolas Xil C<span class="mm">ARVALHO</span></div> <div style="text-align:right"><i>Eŭskio</i></div> 
