      <h3>Moderna vivo</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>La plej granda tragedio</h2>
      <!-- komenco de la enhavo -->

       

       <p>Falis la nombro de litovianoj. Hodiaŭ en Litovio loĝas 3 053 800 personoj, antaŭ dek jaroj 430 000 pli. Tio signifas, laŭ spertuloj kaj aliaj sociaj analizistoj, ke la lando pereas kaj ke la registaro ne kapablas ĝin savi.</p> <p>La vicprezidanto de la Litova Banko Raimondas Kuodis diris: „Tiuj tri milionoj ne povas kaŝi la ĉefan direkton kaj tendencon de nia lando: homoj strebas forlasi Litovion, forkuras, ne volas ĉi tie loĝi. Tio estas la plej granda tragedio de Litovio.”</p> <h3>Korupto</h3> <p>„Elmigradon”, aldonis Kuodis, „instigas ne nur la ekonomio, sed ankaŭ la morala kaj politika klimato, ekzemple socia maljusteco, korupto kaj duoblaj kriterioj por elektitoj kaj elektintoj.”</p> <p>Ankaŭ la direktoro de la Instituto de Laboraj kaj Sociaj Esploroj Boguslavas Gruževskis ne kaŝis sian disreviĝon. „Estas neimagebla situacio”, li diris. „Milito forestas, sed la loĝantaro malaperas. Verŝajne la radikoj de la problemo nestas en nia socia kaj ekonomia ĉirkaŭaĵo. La registaro nepre esploru, kial homoj ne volas loĝi en Litovio.”</p> <h3>Strategio</h3> <p>La ĉefministro de Litovio Andrius Kubilius diris: „Por ni ege gravas, ke tiuj, kiuj nun estas for de Litovio, ne perdu ligon kun sia gepatra lando kaj vidu la eblojn por reveni. Ekzistas aprobita tiurilata strategio.”</p> <p>Li aldonis: „Kiel ekzemplon Litovio povus havi Irlandon, kiu en la 1990aj jaroj modernigis sian ekonomion. Tio helpis regajni elmigrintojn. Tamen la loĝantaro de Litovio malkreskas ne nur pro elmigrado, sed ankaŭ pro malgranda nasko.”</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
