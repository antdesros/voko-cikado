      <h3>Noveloj</h3>
      <h3 class="tem"></h3>
      <h2>Cent fabeloj</h2>
      <!-- komenco de la enhavo -->

       

      <p>La entombigo estis longa kaj pro tio iomete laciga. La pastro parolis longe, konstante
   li ripetis sin mem. Kvazaŭ li ne povus trovi la finon de sia prediko. Ĉiuj ekspiris,
   aŭdante la delonge atendatan „amen”. Eĉ la vidvino jam ne plu havis larmojn, nur konstante ŝi viŝis siajn okulojn per
   blanka naztuko kun nigra rando.
</p> 
<p>La poŝtista ĥoro kantis la kutiman kanton, kaj fine ekiris la granda homgrupo supren,
   al la tombo de la maljuna poŝtisto, kiu troviĝis ĝuste sur la pinto de la monta tombejo.
</p> 
<p>La suprenirado estis ege malrapida, ĉar multaj maljunuloj alvenis por akompani la
   lastan fojon la maljunan poŝtiston. Pro tiu malrapideco ĉiuj havis sufiĉan tempon
   alrigardi la tombojn preterpasatajn kaj kontroli, ĉu estas iu neprizorgata tombo sen
   floroj. Tuj ekis klaĉo, kiam oni trovis similan tombon! Ĉiuj havis malbonan opinion
   pri vidvino neprizorganta la edzan tombon: tio signifas, aŭ ke ŝi forgesis la mortintan
   edzon, aŭ havas novan amanton, aŭ tro pigras. Same malbone oni taksis ankaŭ vidvinojn,
   kiuj tro pompe ornamas aŭ per tro multaj floroj plenŝutas la tombojn. Severe juĝas
   la vilaĝo!
</p> 
<p>Ekpluvis. Komence nur pluvetis, sed ĝis la funebra vico atingis la montopinton, eĉ
   tiuj malsekiĝis, kiuj hazarde havis ombrelon. Kun la pluvo ankaŭ ventaĉo alblovis,
   kaj la ombreloj iĝis senutilaj, la ventego elŝiris ilin de la manoj.
</p> 
<p>La homoj ĉirkaŭstaris la tombon. Nun ankaŭ la ĝishaŭte malsekiĝinta pastro ekkomprenis,
   ke ne oportunas longa parolado. Surprize, je ĉies ĝojo, li galoprapide finis la preĝon.
</p> 
<p>Ĉiuj forrapidis, ja la tempesto iĝis pli kaj pli neeltenebla. La vidvino diris al
   la tombofosistoj, ke ankaŭ ili iru hejmen; nur post la ŝtormo ili finu la entombigon.
</p> 
<p>La homoj ege malfacile descendis; la koto faris la krutan deklivon glitiga. Ili apogiĝis
   unu al la alia, provante sin helpi reciproke. Kiam unua homo glitfalis teren, tiam
   komenciĝis la katastrofo. Tio iel similis la situacion, kiam sur glacia aŭtoŝoseo
   unu aŭtomobilo bremsate ekglitas kaj kruce haltas meze de la vojo.
</p> 
<p>Sub batanta pluvego kaj furioza ventego la homoj, unu post la alia, falsterniĝis sur
   la ŝlimon. Ne nur malsekaj ili estis, sed jam tute enkotigitaj. Kaj ne nur iliaj vestaĵoj!
   Kotaj estis ĉies haroj, piedoj, vizaĝoj, kaj la manoj perdis ĉian utilon: glitante
   sur akvo kaj ŝlimo ili ne plu kapablis alkroĉiĝi al io ajn. Vane la homoj provis liberiĝi
   de tiu situacio. Senpove ili baraktis kunpremiĝante. Dume la aero malvarmiĝis kaj
   la homgrupo, kiel bestoj en frosto, eksimilis malfeliĉan brutamason.
</p> 
<p>Ankaŭ du infanoj, parencoj de la maljuna poŝtisto, ĉeestis la entombigon. Kutime la
   infanoj ne partoprenas entombigojn, sed tiun tagon ili nepre volis veni. La maljunulo
   ĉiam donis al ili bombonon, kiam ili renkontis lin surstrate, pro tio ili tre amis
   lin.
</p> 
<p>Nun, inter la kota homamaso, ankaŭ ili kuŝis, kaj kun timo ili provis resti ĉe la
   patrino, kiu mem provis gardi ilin kontraŭ la furiozantaj naturelementoj. Vanaj provoj.
   Tamen la fakto, ke ili kuŝas apude, iomete faciligis la preskaŭ neelteneblan situacion.
   La pastro, alta, maldika viro, kuŝis inter siaj eklezianoj kaj sentis, ke la cirkonstancoj
   estas vere nedecaj kaj humiligaj. Do li provis leviĝi. Ege malfacile li sukcesis momenton
   ekstari, sed tuj poste li denove ekglitis kaj kun granda bruo replonĝis en la koton.
   La afero iĝis tragikomika. Ĝuste tial ĝi havigis al unu el la infanoj nerezisteblan
   motivon por ekridi. Lia patrino provis admoni lin, sed jam la alia infano siavice
   ekridis, kaj la patrino mem, rimarkinte la humuran flankon de la afero, ne povis reteni
   sin, kaj siavice laŭte ekridis. Fine ankaŭ la pastro, kaj eĉ la trista vidvino ridis
   kaj ridis, kaj la tuta homamaso ne plu estis skuata nur de pluvego kaj ventaĉo, sed
   ankaŭ de nebremsebla komuna ridegado, kiu per si mem havigus al ĉiuj agrablan varmosenton.
</p> 
<p>La rido ja similas al ploro. Fakte post nelonge la vidvino jam ne ridis, sed ploris.
   Tamen, neniu tion rimarkis.
</p> 
<p>La evento ĉiziĝis en ĉies menso. Ekde tiam la vilaĝanoj ĉiam memoris pri la entombigo
   de la maljuna poŝtisto, kvazaŭ tiu tago signus por ili la komencon de nova erao. Menciante
   ion ajn, ili diris: „antaŭ la entombigo”, aŭ „post la entombigo”. Neklarigeble la
   interrilatoj en tiu homgrupo tute ŝanĝiĝis. Ili iĝis kvazaŭ parencoj. Ne plu okazis
   kvereloj inter ili. Nevidebla fadeno interligis ilin. Pli volonte ili alparolis kaj
   helpis unu la alian ol aliulojn, kiuj ne ĉeestis la faman entombigon. La pastro mem
   sentis, ke tiuj homoj de la komuna enkotiĝo kaj de la korusa ridegado iĝis iomete
   pli karaj por li.
</p> 
<p>La maljuna vidvino ne miris, sed feliĉe konstatis, ke ŝi iĝis kvazaŭ ĉies avino. Ŝi
   kaj la maljuna poŝtisto ne havis infanojn, sed ambaŭ tre ŝatis la geknabojn de la
   najbaroj. Nun ne nur la najbaraj infanoj vizitis ŝin, sed ankaŭ aliaj. Kaj ne nur
   infanoj venis. Ne pasis tago sen vizitanto. Ĉiuj diskutis kun ŝi, petis konsilojn,
   kore helpis ŝin. La infanoj feliĉe aŭskultis ŝiajn fabelojn. Unue ŝi rakontis la jam
   konatajn fabelojn, sed poste naskiĝis novaj fabeloj, de ŝi mem elpensitaj. Somere
   en ŝia ĝardeneto ariĝis infangrupo por aŭskulti la novajn, kuriozajn fabelojn. Vintre
   ne estis sufiĉe granda ŝia eta loĝejo. Tiom pli, ĉar jam ne nur infanoj venis aŭskulti,
   sed pli kaj pli multaj plenkreskuloj.
</p> 
<p>Foje vizitis ŝin la pastro. Li aŭskultis, kaj tuj poste li konsilis al la maljunulino,
   ke ŝi surpaperigu la belajn rakontojn. La vidvino diris, ke ŝi ne kapablas tion fari.
   Tiam la pastro petis la infanojn, ke ili transskribu la aŭskultitajn fabelojn. Tiel
   okazis. La avino fabeladis, la infanoj skribadis, la pastro zorge kolektis la plej
   belajn rakontojn. Je la fino li sekrete presigis la tutan kolekton.
</p> 
<p>Grandega estis ĉies surprizo, kiam publike aperis la bela fabellibro. La titolo estis
   simpla: Cent novaj fabeloj.
</p> 
<p>La avino ne kredis al siaj okuloj, kiam la pastro alportis al ŝi la bele binditan
   volumon. La tuta vilaĝo feliĉis kaj fieris pro tio, ne forgesante, ke tion oni ŝuldas
   al la fama entombigo de la maljuna poŝtisto.
</p> 
<p>La fabellibro rapide elĉerpiĝis, oni devis ĝin reeldoni. Poste ĝi estis tradukita
   en kelkdek lingvojn, kaj en la Internacian, kaj atingis mondfamon.
</p> 
<p>La maljuna poŝtisto ridetis inter la anĝeloj kaj modeste dankis iliajn gratulvortojn.</p> 
