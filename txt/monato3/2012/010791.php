      <h3>Turismo</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Muzeo en trunko</h2>
      <!-- komenco de la enhavo -->

       

       <p>2012 estis anoncita kiel la Jaro de la muzeoj en Litovio. Antaŭ 200 jaroj en la vilaĝo Bijotai, proksimume 220 km nord-okcidente de la ĉefurbo Vilnius, aperis stranga ekspozicio de antikvaĵoj, aranĝita en la trunko de miljara kverko. Tio donis la komencon al posta muzea movado en la tuta lando.</p> <p>Bijotai, antaŭe preskaŭ nekonata loko, famiĝis en 1812. Tiama litova verkisto, Dionizas Poška [poŝka], decidis segi grandegan kverkon, kiu kreskis sur apuda monteto. La arbo estis ege malnova kaj jam preskaŭ mortinta. Sub ĝiaj radikoj vulpoj havis siajn truojn. Juna ĉasisto, dezirante forigi tiujn bestojn, ekbruligis fojnan bulon kaj enŝovis ĝin sub la radikojn. La arbo restis preskaŭ senviva. Por preventi eventualajn postajn damaĝojn la verkisto decidis segi ĝin kaj transporti apud sian farmodomon.</p> <p>Poste Poška tranĉis sufiĉe grandan pecon de la trunko, forigis ĝian internaĵon, metis sur ĝin fojnan tegmenton, ellaboris fenestretojn, pordon kaj ene aranĝis etan ejon. Li nomis ĝin Baublys, kies nomo plej verŝajne rilatas al pagana dio de abeloj Bubilas. En sia kverka ejo li ripozis kaj verkis. Sur la lignaj muroj li pendigis bildojn, estigis breton, sur kiu li lokis sian libraron. Estis loko ankaŭ por diversaj antikvaĵoj, ĉar serĉado kaj kolektado estis lia granda pasio.</p> <p>Post dek du jaroj Poška decidis sammaniere ellabori la duan kverkan ejon nur iom pli etan por ene loki sian kolekton. Finfine ambaŭ kverkejoj fariĝis muzeoj kaj la famo pri tio ege disvastiĝis. Tiun unikan fenomenon priskribis la gazetaro de Vilnius kaj Varsovio. Homoj komencis amase venadi por propraokule percepti la neviditan.</p> <p>Post la morto de Poška en 1830 la pasie kolektitaj antikvaĵoj dissemiĝis tra Litovio, multaj eksponaĵoj malaperis dum la unua mondmilito. Tamen tiu kverka muzeeto, hodiaŭ en vitraj ejoj, daŭre ekzistas. Ĉiujare ĝin vizitas ĉirkaŭ 20 miloj da vizitantoj kaj ĉiuj miras pri tiu unika miraklo de la naturo.</p> <h3>Kverka mirindaĵo en preĝejo</h3> <p>Interalie la „kverkomanio” disvastiĝis ankaŭ en apudaj lokoj. Kvar kilometrojn for de Bijotai situas vilaĝo Girdiške [girdiŝke], en kies preĝejo de la Sankta Virgulino Maria de Neĝo oni povas vidi alian kverkan miraklon – du flankajn altarojn.</p> <p>Onidire unikaj kaj originalaj altaroj aperis ĉi tie en 1928 danke al loka pastro Kazimieras Andriukaitis, kiu pasie ŝatis kaj mem iom studis arkitekturon. Li mem veturis al apudaj arbaroj serĉi taŭgajn kverkojn, senŝeligis trunkojn kaj ellaboris eksterordinarajn altarojn. En la niĉoj de tordiĝintaj branĉoj li lokis bildojn kaj statuojn de sanktuloj. Sendube la pastro pruntis la „kverkan” ideon, en kiu pagana mitologio harmonias kun kristana kredo, el la proksime situanta kverka muzeo de la verkisto Poška.</p> <div style="text-align:right"><span class="mm">LAST</span></div>       
