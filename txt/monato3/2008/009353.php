      <h3>Spirita vivo</h3>
      <h3 class="tem"></h3>
      <h2>Diservoj en diversaj lingvoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Eksterlandaj kredantoj ne malofte komparas la litovan ĉefurbon Vilnius al Jerusalemo. Ja en Vilnius ĉie videblas preĝejaj turoj. Alilingvanoj preskaŭ ĉiutage en preĝejo povas preĝi eĉ sciante ne unu vorton en la litova. Poloj, rusoj, belorusoj, ukrainoj – ne nur tiuj minoritatoj ĝuas eblecon partopreni la diservon en sia gepatra lingvo.</p> <p>En aliaj preĝejoj dimanĉe matene oni povas aŭdi preĝojn kaj preĝkantojn ankaŭ en la angla kaj la germana. La pastroj diras, ke en tiuj diservoj partoprenas ne nur eksterlandanoj, sed ankaŭ litovoj, kiuj volas perfektigi siajn konojn pri tiuj lingvoj. Somere en tiuj diservoj oftas turistoj.</p> <p>Fremdlingvajn diservojn kutime partoprenas oficistoj de alilandaj ambasadejoj. Oficistino de la germana ambasadejo, Irja Berg, rakontis, ke en sia gepatra lando ŝi preskaŭ ĉiudimanĉe iradis al la preĝejo. Translokiĝinte al Vilnius, ŝi nur komencas lerni la litovan lingvon. „Mi tre ĝojis eksciinte, ke ankaŭ ĉi tie okazas diservo en lingvo, kiun mi komprenas”, diris la virino, kiu nun estas ofta partoprenanto de la diservo, en la evangelia luterana preĝejo. Ŝiaopinie, la fremdlingva diservo ankaŭ kontribuas sociaspekte – en la preĝejo eblas konatiĝi kun aliaj, trovi novajn amikojn. Tio estas tre grava, kiam oni estas novulo en fremda urbo.</p> <p>La brita ambasadoro Colin Roberts laŭdis la fremdlingvan ideon de la pastroj, dirante, ke por kredantaj britoj tio estas unu el la pluraj aferoj, kiuj kreas bonan vivĉirkaŭon.</p> <p>Dimanĉe je la naŭa kaj duono en la evangelia luterana preĝejo de Vilnius komenciĝas diservo en la angla. La plimulto de la preĝantoj estas eksterlandaj diplomatoj. Unufoje monate ĉi tie oni ankaŭ preĝas en la germana. La helpantino de la pastro, Irena, diris, ke la germanoj kutime partoprenas la anglalingvan diservon, en kiu ankaŭ multas britoj, usonanoj, skandinavoj, danoj. La virino rimarkis, ke la kvanto de la preĝantoj ofte dependas de la religiemo de la ambasadoro mem. Se li estas fervora kredanto, sekve ankaŭ aliaj oficistoj de la ambasadejo pli aktive vizitas la preĝejon. Ŝi menciis pri la edzino de eksa norvega ambasadoro. Ŝi estis aktiva partoprenanto de la diservo kaj sekve ankaŭ pliaj oficistoj de la tiulanda diplomata servo vizitadis la diservon.</p> <p>Irena rakontis, ke inter eksterlandaj paroj ne maloftas, ke unu apartenas al unu religio, alia – al alia. Ili kompromisas dimanĉe vizitante protestantan kaj ankaŭ katolikan preĝejon. La diservon servas pastro el Usono, Robert Jones.</p> <p>Anglalingva diservo okazas ankaŭ en la preĝejo de la Bernardanoj. Tio estas iniciato de franciskanaj monaĥoj. En la preĝejo de la Sankta Triunuo la diservo okazas en la ukraina lingvo. La pastro rimarkigis, ke lastatempe en Vilnius plimultiĝis ukrainoj, kiuj venas por ĉi tie labori. Ili dimanĉe volonte venas por preĝi.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
