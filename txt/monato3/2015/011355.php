      <h2>Komunikaĵo de Ikaro</h2>
      <!-- komenco de la enhavo -->

       

     <p>
   <i>Eltondaĵo el la lasta raporto pri la planedo Ikaro</i> 
</p> 
<p>En mia antaŭa raporto mi provis priskribi la aspekton kaj fizikajn karakterizojn de
   la ikaranoj (t.e. la plej inteligentaj loĝantoj de ĉi tiu planedo). Bedaŭrinde mi
   timas, ke mi tiam malsukcesis prezenti koheran imagon pri tiuj estaĵoj, ĉar ili estas
   tiom malsimilaj al iu ajn, kiun ni konas sur nia tero. Ekzemple, ni ne povis konstati,
   kie troviĝas iliaj vidorganoj aŭ aŭdorganoj, nek la cerbo, kvankam estas evidente,
   ke ili ja vidas, aŭdas kaj pensas. La ununura komparo, kiun ni kapablas fari, estas
   eble kun la diafanaj estaĵoj observataj en la lumfasko de la reflektoro de la submara
   miniveturilo de Jacques Cousteau kaj aliaj, kiuj esploras la plej profundajn abismojn
   de niaj oceanoj.
</p> 
<p>Tamen, la ikaranoj estas nek marloĝantoj, nek amfibiaj. Plie, ili aspektas iom pli
   fortikaj ol tiuj loĝantoj de la maraj profundejoj, malgraŭ la fakto, ke malkiel la
   homa fiziko, iliaj korpoj aspektas tiom gracilaj, ke oni havis la impreson, ke la
   plej malforta vento forblovus ilin. Ili memorigas nin pri teraj plantoj aŭ delikataj
   arboj, kiel nia tremolo aŭ la floranta sambuko.
</p> 
<p>Kelkaj el nia teamo trovas ilin belaj, sed por kelkaj ili ŝajnas strange timigaj pro
   sia alimonda aspekto. Tamen, ni havas neniun motivon por supozi, ke li estas malamikaj
   aŭ danĝeraj. Tute kontraŭe, ĝis nun ili montris neniun signon de malamikeco, kvankam
   ili ne faris al ni bonvenigajn gestojn kaj proponis al ni neniun helpon.
</p> 
<p>Nia plej granda problemo estis la malfacilaĵo iel komuniki kun ili. Feliĉe, nia kunulo,
   la filologo Neuwirth, kiu jam pasigis pli ol jaron inter la ikaranoj, trovis provizoran
   solvon. Li decidis, ke ilia lingvo estas tute nelernebla por teranoj. (Poste, ni klarigos,
   kial.) Do li havis la aŭdacan ideon instrui al unu aparte kapabla ikara individuo
   Ilon, la internacian lingvon, kiun ni uzas inter nia plurlingva ŝipanaro. La planedano,
   kiun li elektis por tio, estas (laŭ nia tera terminaro) iu fakulo pri lingvistiko,
   kiun ni nomu U, pro la fakto ke lia vera nomo estas neprononcebla.
</p> 
<p>La tasko de Neuwirth ŝajnas neefektivigebla, ĉar la parolorganoj de la ikaranoj ne
   permesas, ke ili eligu la sonojn de iu tera lingvo. Pro tio, U kaj Norwirth interkonsentis,
   ke ĉiu fonemo de Ilo estu anstataŭigita per tute alia ikara fonemo, por krei specon
   de kodo, kiun nur ili ambaŭ povis kompreni. Plie, ĉar la gramatiko, sintakso kaj vortordo
   de la ikara lingvo malsimilas niajn eŭropajn konvenciojn, la originala Ilo devis esti
   komplete transformita, por efektive krei novan lingvon, sed kun du variantoj – unu prononcebla por U, kaj la alia por Neuwirth.
</p> 
<p>Eĉ tiuj ŝanĝoj, tamen, ne tute venkis la malfacilaĵon de interkomunikado. La ikara
   lingvo ne klare distingas inter nominativo kaj akuzativo, subjekto kaj predikato,
   verboj transitivaj kaj netransitivaj. Ĉiuj el la lastaj (kvankam „verbo” ne estas taŭga termino en ĉi tiu kunteksto) estas 
   <i>ergativaj</i>. Ikaranoj ankaŭ ne rekonas distingon inter substantivo, verbo aŭ adjektivo. Ili pensas
   laŭ 
   <i>Gestalt-konceptoj </i>, t.e. laŭ tutaĵoj, ne detaloj, pri kiuj ili apenaŭ interesiĝas.
</p> 
<p>Ni menciis, ke la ikara lingvo estas nelernebla. La kialo fontas el la historio de
   la planedo. Antaŭ longe (<i>kiom</i> longe ni ne konstatis), ĉiu grupo kaj nacio parolis sian propran, kaj tute apartan
   lingvon. Entute temis pri pli ol dek tri idiomoj, ĉiuj apartenantaj al malsamaj lingvaj
   familioj. Por unuigi la planedanojn oni decidis krei ununuran lingvon konsistantan
   el la tuta gamo da idiomoj, inkluzivigante la gramatikon, vortaron kaj sintakson de
   ĉiuj el la originalaj etnaj lingvoj. Ni teranoj neniam povus enkapigi tiel kompleksan
   lingvan sistemon.
</p> 
<p>Tamen, se la ikara lingvo ŝajnas bizara, eĉ pli stranga estas la fiziologio de la
   ikaranoj. Ili ne posedas sekson, do estas nek viraj nek inaj. Konsekvence, ili reproduktiĝas
   ne per seksumado sed per klonado. Do, estas neniu persona rilato, korinklino aŭ sento
   de aparteno inter la generacioj. Neniu havas gepatrojn, do ne ekzistas „patrina” aŭ „patra” sentimento. La vorto „amiko” ne ekzistas en la ikara lingvo. Plej proksimaj tradukoj estas „tiu, kiu loĝas apud mi”, kaj „tiu, kun kiu mi foje manĝas”. Pro tio, konceptoj kiel „amo”, „kompato”, „sentimento” estas neimageblaj inter la ikaranoj, kaj sekve ili ne kreas literaturon, poezion,
   aŭ muzikon. Ili ŝajnas esti tute neemociaj, kaj en sia abunda „libertempo” (ĉar ili ne laboras en nia senco de la vorto) ili okupas sin per matematiko – ne nur pri novaj problemoj kaj teoremoj, sed eĉ per invento de novaj matematikaj
   sistemoj.
</p> 
<p>Pro tio, ke ili ne estas seksaj estaĵoj, ili kompreneble spertas nek la plezurojn
   nek la problemojn de amo, seksumado, partnereco aŭ intimo. Ili ne ĵaluzas, nek envias.
   La miskomprenoj, kiuj ofte aperas en teraj familioj kaj inter geedzoj aŭ amantoj,
   ne tuŝas ilin. Iliaj korpoj ne konas la efikojn de vira testosterono aŭ de adrenalino,
   do ili ne militas, kverelas, aŭ ekscitiĝas. Ŝajne neniam okazis milito sur la planedo.
   Ilia socio ne povus esti pli simpla, kaj estas, en pozitiva senco, anarkia. La ikaranoj
   konas nek registaron, nek policon. Ili ne bezonas ion ajn, kio similas monon, bankojn
   aŭ ekonomion. Neniu el ili posedas proprietaĵon, do ne okazas ŝtelado, uzuro aŭ krimo.
   Tamen, tio neniel signifas, ke Ikaro estas Paradizo, almenaŭ laŭ la kriterioj de ni
   teranoj. Pri tio, nia nekredanta ŝipestro (kiu, tamen, devenas de hinda familio) ŝercis:
   „Eble ne Paradizo, sed ĝi tro similas la tradician imagon de la kristana Ĉielo – neniu peko, sed neniu plezuro!”.
</p> 
<p>Ikaranoj neniam praktikis agrikulturon, kaj kiam Neuwirth provis planti kaj kultivi
   kelkajn legomojn, la planedanoj forte kontraŭis kaj malhelpis liajn klopodojn. U asertis,
   ke lia popolo nenion faras, kio povus difekti, vundi aŭ transformi la terenon aŭ la
   naturon per plugado, fosado aŭ tondado, agoj kiuj „cikatrigus” la korpon de la tero. Evidente ili paŝas malpeze sur la supraĵo de la tero, perturbante
   nenion, kiel la praloĝantoj de la amerika kontinento antaŭ la alveno de la blankuloj.
   Ili eĉ ne konstruas domojn, simple ŝirmante sin, kiam la vetero malmildas, en la kavernoj,
   kiuj abundas sur Ikaro. Tiuj kavernoj estas varmaj kaj komfortaj, nature garnitaj
   per musksimila kreskaĵo sur la vandoj kaj la planko, do bone izolitaj.
</p> 
<p>La ikaranoj nutras sin per fruktoj kaj nuksoj, kiuj falas konstante, en ĉiu sezono,
   de la arboj kaj arbustoj. Ili ne plukas, deŝiras aŭ tondas ilin. Simile, ili ne mortigas
   aŭ manĝas la faŭnon de la planedo. Nenio, kio similas niajn terajn fiŝojn, troviĝas
   sur Ikaro, kaj mankas birdoj, mamuloj, rampuloj kaj marsupiuloj (ventropoŝuloj). Fakte,
   la faŭno estas malabunda kaj malvaria. Pro tio, ke la ikaranoj estas ne agrikulturistoj
   sed kolektantoj, ilia planedo ne povas subteni grandan loĝantaron, do nur kiam mortas
   iu el ili, estas klonata unu nova individuo, tiel konservante stabila la nombron de
   planedanoj. Tiu, kiu estas la antaŭulo – ne temas pri patro aŭ generinto – de klono poste ne zorgas aŭ interesiĝas pri ĝi, kaj sentas neniun „familian” rilaton kun ĝi. Tiuj ikaraj klonoj ekestas jam kun la memoro kaj konoj (krom la plej
   lastaj) de la tuja antaŭulo. Eĉ la komplikan ikaran lingvon ili heredas. 
</p> 
<p>Ni ne sukcesis malkovri, ĉu la loĝantoj de ĉi tiu planedo devas dormi, aŭ kapablas
   sonĝi, kvankam ili kelkfoje ripozas en siaj kavernoj. La plimulton de la tempo ili
   pasigas kolektante la falintajn fruktojn kaj la kreskaĵojn faligitajn de ventoj kaj
   ŝtormoj.
</p> 
<p>La ikaranoj ne havas religion, kaj tiu koncepto estas al ili tute fremda. Malkiel
   kelkaj teranaj religiuloj, ili ne timas la morton, kaj kiam iu el ili forpasas, neniu
   funebras. Laŭ nia pensmaniero, ilia vivo ŝajnas paca kaj trankvila, sed monotona kaj
   teda. Ili neniam spertas ekstazon, eksciton aŭ stimulon (eble parte pro tio, ke ili
   estas senseksaj). Tamen, povas esti, ke la malesto de perforto, agreso aŭ intenca
   detruo de la medio iom kompensas tiun mankon.
</p> 
<p>Kiam venis la tempo de nia reveno al nia hejma planedo, ni kuniĝis kun grupo da ikaranoj
   apud nia kosmoŝipo. Nome de nia teamo kaj pere de la interpretisto U, Neuwirth adiaŭis
   la planedanojn, dezirante al ili – kaj precipe al U, ĉe kiu li tiom longe vivis kaj
   kunlaboris - „Ĉion bonan!” La reago de U surprizis nin. Li simple diris „Iru!”, kaj tuj forlasis la lanĉejon. Ni poste rimarkigis al Neuwirth, ke la malvarma sinteno
   de lia ikara amiko ŝajnas stranga. „Tute ne”, respondis Neuwirth. „La ikaranoj ne havas amikojn. Ili rekonas nek amikojn, nek malamikojn.”
</p> 
<p>Do, jen niaj konkludoj: Kvankam la ikaranoj estas liberaj de multaj el la problemoj,
   malĝojoj kaj suferoj, kiujn ni, pro nia homa kondiĉo, devas sperti, mi persone, kaj
   la anoj de nia teamo, preferus loĝi sur nia kruela kaj tragika planedo kaj suferi
   ĝiajn malbonaĵojn ol pasigi la senemocian kaj apatian vivon de la ikaranoj. Ŝajnas,
   ke precize 
   <i>pro</i> la ekzisto de malĝojo, pezo kaj penigo ni teranoj povas ankaŭ koni ĝojon, ekstazon
   kaj feliĉon. Bedaŭrinde, sur la tero, estas precize pro la ekzisto de la malriĉeco
   de multaj mizeruloj, ke ekzistas la vasta riĉeco de malmultaj privilegiuloj; kaj estas
   ĝuste la ĉeesto de krueleco, kiu ebligas kaj elvokas la kompaton – kiu, laŭ la poeto
   Kálmán Kalocsay, estas la plej sankta kaj plej nobla el la homaj sentoj.
</p> 
