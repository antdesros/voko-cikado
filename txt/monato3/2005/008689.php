      <h2>Formortis pluraj lingvoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kun la morto de Reĝo Jirake [ĝirake] en aprilo, mortis la lasta tribano, kiu sciis ĉiujn dek variantojn de la granda-andamana lingvo. Tial, krom la ceteraj 49 anoj de lia tribo, funebris lian morton ankaŭ la esploristoj de la universitato Jawaharlal [ĝavaharlal] Nehru en Nov-Delhio, kies laboro kompili trilingvan vortaron (en la granda-andamana, angla kaj hindia) ricevis gravan baton.</p> <p>Laŭ raporto en la anglalingva tagĵurnalo <i>The New Indian Express</i>, ne pli ol 18 tribanoj scipovas la granda-andamanan, kaj post la morto de Jirake, nur kvin parolas ĝin flue. Laŭ Anvita Abbi, profesorino de la universitato, „Jirake havis grandan scion ne nur pri sia popolo sed ankaŭ pri aliaj triboj. Li estis plurlingvano, lia patro estis de la tribo Bo kaj la patrino de la tribo Cari. Tiuj triboj jam formortis, sed Jirake scipovis ambaŭ lingvojn krom pluraj aliaj, ekzemple, <i>jeru, khora</i> kaj <i>pucikwar</i>.” Laŭ nekrologo verkita de sociolingvisto Alok Das, Jirake ankaŭ scipovis bone la birman kaj ankaŭ <i>sadari</i>, kiu estas parolata ankaŭ en la okcident-barata subŝtato de Biharo.</p> <p>La raporto mencias ankaŭ la tragikan trouzon de la alkoholo en la tribo. Diris Abbi, „la alkoholismo estas io, kion ni enkondukis en la tribon, kaj nur plirapidigas ĝian formorton. Eĉ mortante, Jirake ripete petis alkoholon.”</p> <p>Post kvazaŭ mirakla saviĝo post la cunamo en decembro 2004, Jirake kaj liaj tribanoj loĝis en gastejo en la ĉefurbo Port Blair [blejr], kie vizitadis ilin d-ro Das. La lingvisto raportas, ke atingante la gastejon, li surpriziĝis, ke ĉiuj volis premi lian manon, agon la granda-andamananoj neniam antaŭe faris. Poste oni diris al li, oni premas la manojn post morto en la komunumo.</p> <div style="text-align:right">A Giridhar R<span class="mm">AO</span></div> 
