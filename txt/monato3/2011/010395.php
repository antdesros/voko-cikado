      <h3>Moderna vivo</h3>
      <h3 class="tem">BURUNDO</h3>
      <h2>Pri elefantoj kaj herbejoj</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Mireille Grosjean vizitis Burundon en aprilo kaj sendis jenan kunverkitan raporton:</b></p> <p>Burundo travivis dum jardekoj militon, kiu finiĝis en 2005. Tio signifas, ke vundoj ankoraŭ doloras en ĉies koro, en ĉies memoro, en ĉies kapo, eble en kelkaj korpoj. Gravas nun paco; gravas, ke la popolo unuiĝu, lernu kune kaj harmonie vivi.</p> <p>Tion celante, la registaro dungis pacperanton, s-ron Rukara, kiu alfrontas grandan kaj gravan taskon. Por tiun plenumi, li ellaboris por la popolo diversajn kaj komunajn aktivaĵojn kaj taskojn.</p> <h3>Manoj</h3> <p>Tiuj laboroj okazas sabat-matene tra la lando. Popolanoj komune ion konstruas, purigas, kaj tiel kune ŝvitas sed interkonatiĝas. Admirinde estas, ke same kunlaboras registaranoj. Tiuj aperas simple vestitaj kaj man-en-mane laboras kun popolanoj. Jen fenomeno malofta en la mondo: ĝenerale politikistoj malaperas en luksaj veturiloj kaj ne malpurigas siajn manojn. Male en Burundo.</p> <p>Mi partoprenis tiujn komunajn laborojn. Dum portado de tero en sakoj miaj kunlaborantoj estis la pacperanto mem, s-ro Rukara, kaj la parlamenta prezidanto, s-ro Ntavyohanyuma. Kun tiu mi povis diskuti la lingvan situacion: la franca estis, la angla estas. Afrika proverbo diras: „Kiam elefantoj luktas inter si, herbejo suferas”. Denove afrikanoj estas viktimoj. „Eble via lingvo estas la solvo”, diris la parlamenta prezidanto.</p> <h3>Teamoj</h3> <p>Alia strategio de la pacperanto estas piedpilka ĉampionado. Piedpilkado popularas en Afriko. Kiam s-ro Rukara notis, ke inter motociklistoj kaj policistoj ekzistas streĉoj, li starigis piedpilkajn teamojn. La du teamoj ludis en stadiono: fine venkis la motociklistoj.</p> <p>Ne facilas repacigi landon post longdaŭra milito, kiam oni kutimas pensi etne, ne home. La pacperanto havas pezan respondecon. Sed ankaŭ Esperanto helpas: parolantoj de la lingvo, eĉ tiuj de diversaj etnoj, jam kutimiĝas al kunvivado, kunlaboro, interparolado kaj interhelpo.</p>  <div style="text-align:right">