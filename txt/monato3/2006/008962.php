      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Reveno al la hejmlando</h2>
      <!-- komenco de la enhavo -->

       

       <p>Post longa malĉeesto mi revizitis pasintjare mian iaman patrujon, Libanon. Kiam mi estis knabo en mia vilaĝo At-Tajba (La Bonklimata), en la sudo, apenaŭ rimarkeblis la 20a jarcento. Nun ĝi estas relative prospera urbeto. En la centro mi trovis mian iaman domon kun ĝia frukt-ĝardeno (malgraŭ postsigno de israela bombo sur la muro) bone prizorgata de la komunumo omaĝe al mia patro, la mulao en la regiono. Mi promenadis tien kaj reen kaj manĝis de la samaj ŝatataj fig-, granat- kaj oliv-arboj. Malmultaj samaĝuloj restas, sed la gejunuloj varme akceptis min.</p> 
<p>Orienten el At-Tajba oni vidas la pinton de Hermon-monto okupata de Israelo. Tuj sude de At-Tajba estas bone videbla juda vilaĝo. Kontraŭ la okupo de Israelo batalas Hizbul-Laah. Tamen miaopinie ne Hizbul-Laah forpelis Israelon de Libano sed okcidentaj potencoj, kiuj unue instigis Israelon ekokupi la landon kaj ĝin terurigi dum pli ol du jardekoj.</p> 
<h3>Masakroj</h3> <p>Nun la ĉefa rolo de Hizbul-Laah estas helpi la popolon de la sudo de Libano reakiri sian egalecon kun la nordo. Alia rolo estas protekti la popolon kontraŭ eblaj masakroj, kiel tiuj de Sabra kaj Ŝatella en 1982. Samtempe la okcidentaj potencoj bezonas Hizbul-Laah-on, por ke Israelo restu ĉiam veka, armita kaj preta ataki.</p> 
<p>Pasintjare estas mortigita la iama ĉefministro de Libano, Al-Hariri. Tiu ĉi fariĝis milionulo, sed liaj riĉoj ne restis en Libano kaj la lando apenaŭ profitis de liaj projektoj. Li konstruigis plej modernan flughavenon en Bejruto kaj ŝoseojn por turismo. Lokaj komercistoj kolektis la profitojn kaj resendis ilin al okcidentaj bankoj.</p> 
<p>Plue, Al-Hariri starigis fonduson por finance subteni libanajn studentojn en okcidento kaj ilin poste revenigi. En 1996 mi renkontis lin en Londono kaj demandis, kiom da studentoj fakte revenas hejmen. Li promesis esplori kaj faksigi respondon, sed neniun mi ricevis.</p> <h3>Strategio</h3> <p>Ankaŭ Sirio estas parto de la okcidenta strategio pri Libano kaj la regiono. En 1976 okcidentaj potencoj petis, ke Sirio eniru Libanon por „protekti kristanojn” dum la interna milito (1976-1991). La armea ĉefo ricevis 50 milionojn da dolaroj, nun investitaj en Londono. Tiuj samaj potencoj postulis pasintjare la reirigon de la siria armeo al Sirio, verŝajne por ke la armiloj troviĝu en pli malgranda tereno kaj, se necese, estu pli facile detrueblaj.</p> <p>La kristana elito estas la plej lerta tavolo de libana socio. Tamen oni trompe kredigas eĉ al ĝi, ke Okcidento estas iel ĝia „patrino”. Se vere la okcidenta Francio kompatus Libanon, ĝi estus helpocele interveninta por eviti la buĉadon dum la interna milito. Kaj nun la kristanoj kondamnas Sirion kiel okupanton kaj subpremanton de Libano.</p> <p>Apenaŭ mirige estas, ke tiom da ambiciaj junuloj forlasas Libanon, lasante hejme junajn virinojn, kiuj mem atendas eksterlandajn svatantojn.</p> <div style="text-align:right">Husejn A<span class="mm">L</span>-A<span class="mm">MILY</span>/pg</div>  
