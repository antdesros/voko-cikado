      <h2>Islama ekonomio: ĉu modelo por hodiaŭ?</h2>
      <!-- komenco de la enhavo -->

       

        <p>Se Muhamado, la islama profeto, estus vivanta en nia epoko, ĉu ne eblus, ekzemple,
   ke lia koncepto pri ekonomio malhelpus la nuntempan globan krizon, kiu malriĉigas
   kaj endanĝerigas la mondon?
</p> 
<p>Konataj estas de li pluraj reguloj, kiujn oni povus legi kiel simbolajn konsilojn
   pri la temo: Evitu luksan vivon – tio forigus la benadon de abundo (Hadiso).
</p> 
<h3>Malpermesoj</h3> 
<p>Metia produktado aŭ alia laboro aŭ servado havas en si mem valoron. Oni rajtas komerci,
   sed ludi per mono estas en si mem danĝere, ĉar, tiel agante, oni efektive ricevas
   ion gajn-intence kontraŭ neniu valoraĵo. Tion Muhamado do rekte malpermesis.
</p> 
<p>En lia epoko en Mekko oni pruntedonis monon kontraŭ vere rabema interezo, same kiel
   nuntempe la interezo de la Internacia Mona Fonduso (IMF) por multaj malriĉaj landoj
   rabeme efikas. Oni devas pagi rentumon super rentumo. Muhamado tion malpermesis.
</p> 
<p>Por li la vera celo de komerco ne estis gajno, sed interŝanĝo de varoj kaj servoj.
   Male funkcias la nunaj borsoj, de kiuj devenas financaj krizoj, ĉar precipe gajnon
   ili celas.
</p> 
<p>Laŭ esprimoj de sia epoko, la profeto fojfoje malpermesis „forvendi nutraĵ-karavanon antaŭ ol ĝi jam alvenis en la bazaron”, kie kutime atendis tiuj, kiuj bezonis ties varojn. Same li malpermesis ankaŭ ekvendi
   varon, tuj post kiam oni aĉetis ĝin. Tio nur povus altigi la finan prezon por tiuj,
   kiuj bezonis la varon.
</p> 
<p>La profeto malpermesis la monopoligadon kaj la kaŝ-amasigadon de varoj. Laŭ li en
   la juĝotago oni bruligos tiajn krimulojn per iliaj akiritaj riĉaĵoj (Hadiso).
</p> 
<h3>Naturo</h3> 
<p>La bazaj fontoj de energio kaj nutraĵoj estu, laŭ li, ŝtate regataj: arbaroj kaj verdo-kampoj
   (ni diru: la naturo ĝenerale) estu protektataj, ĉar tiuj apartenas ne al homoj, sed
   al Alaho (Dio).
</p> 
<p>En la nuna epoko oni povas tiel kompreni, ke naturaj riĉaĵoj, kiel arbaroj, akvofontoj,
   nafto k.s., estu sub ŝtata regado. Eble oni plivastigu la nocion ankaŭ al la poŝto-
   kaj elektro-servo ktp.
</p> 
<h3>Ordono</h3> 
<p>Tre baza ordono de Muhamado estis, ke en ĉio oni konsideru sin dependa de Alaho, kun
   respondeco ankaŭ pri kunhomoj malpli feliĉaj: senhavuloj, orfoj kaj vidvinoj. Por
   tion atingi oni ofte devas lukti kontraŭ si mem.
</p> 
<p>„Ĝihado” estas la esprimo de tia lukto: „La pli granda ĝihado estas luktado por bonfarto de viaj familianoj” (Hadiso).</p> 
<p>Ĉu tio simbole ne estas la esenco de sana homara ekonomio?</p> 
