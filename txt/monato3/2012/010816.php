      <h3>Politiko</h3>
      <h3 class="tem">MADAGASKARO</h3>
      <h2>Restas nur espero</h2>
      <!-- komenco de la enhavo -->

       

       <p>En 2009 okazis plurmonata manifestacio kontraŭ la madagaskara prezidanto Ravalomanana Marc [ravalumanana mark]. La protestojn organizis Andry Rajoelina [andri radzuelina], apogata de diversaj politikaj partioj.</p> <p>Ankaŭ Francio kaŝe subtenis Rajoelina-n por forpeli de la povo homon taksatan tro anglosaksema. Rajoelina promesis pli decan vivon, dum la popolo ne plu eltenis la diktatorecon kaj arogantecon de la prezidanto.</p> <p>Sub premo ĉefe de la armeo Ravalomanana devis demisii. Tamen kontraŭkonstitucie li transdonis povon al grupo de altrangaj oficiroj, antaŭ ol forfuĝi al Sud-Afriko. La nuna transira registaro estas nur parte agnoskata de la internacia komunumo, kio signifas tamen ĉesigon de ĉiuj eksterlandaj helpoj.</p> <h3>Kontrakto</h3> <p>Ĉar malagasoj ne kapablas mem solvi la disputon, enmiksiĝas la internacia komunumo reprezentata de SADEC (kunlabora asocio de pluraj landoj el orienta Afriko). Lastjare ĝi devigis dekon da politikaj partioj kune subskribi kontrakton por trovi solvon.</p> <p>Menciita estas starigado de komuna registaro por prepari laŭ internacia normo balotojn. Tamen partianoj de Rajoelina kaj Ravalomanana malemis kunlabori dum transira periodo.</p> <p>Reciprokis akuzoj, tiel ke ordinaraj civitanoj svenas antaŭ la kaŝitaj ludaĉoj de la politikistoj. Regas senlaboreco kaj sensekureco kaj galopas inflacio.</p> <h3>Kutimoj</h3> <p>Pro la politika krizo naskiĝis diversaj malbonaj kutimoj. Suferegas kaj la ekonomio kaj la medio, ĉar senskrupululoj profitas por senhonte ekspluati la palisandro-industrion.</p> <p>Cetere ŝajnas, ke ĉinaj importistoj kunlaboras kun malagasaj altranguloj por subteni kontrabandistojn. Kilogramo da palisandro kostas milojn da eŭroj. Sed ĝis nun, malgraŭ diversaj policaj enketoj, neniu nomo estas publikigita.</p> <p>Kelkaj fipolitikistoj ekimitas ankaŭ terorismajn metodojn kaj eksplodigas bombetojn. Sur naciaj vojoj, banditoj atakas aŭtojn. Tamen plej suferas kamparanoj pro atakoj faritaj de bovŝtelistoj, dum ĝendarmoj kaj policistoj postulas ŝmirmonon de homoj kontrolataj.</p> <h3>Stagnado</h3> <p>Laŭ ĵurnalistoj, rivaleco inter Usono (kiu subtenas Ravalomanana-n) kaj Francio (kiu subtenas Rajoelina-n) estas la ĉefa kialo de la stagnado. Post kelkaj jaroj Madagaskaro ekproduktos nafton kaj potencaj landoj jam konkurencas por akiri la plej grandajn rajtojn.</p> <p>Se persistos la krizo, sekvos eble triba milito. Sendube la sola eliro estas balotoj por la prezidanto kaj parlamento. Laŭ Unuiĝintaj Nacioj, elektoj fareblos nur en junio 2013.</p> <p>La nuna registaro sekvu la decidon de la internacia komunumo por organizi balotojn kredindajn kaj akceptitajn de ĉiuj partioj. Dume, la popolo atendas kaj esperas, ke baldaŭ finiĝos ĝia suferado.</p> <div style="text-align:right">F<span class="mm">IDILALAO</span> Henriel</div> 
