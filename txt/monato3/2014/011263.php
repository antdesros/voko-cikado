      <h3>Politiko</h3>
      <h3 class="tem">GERMANIO</h3>
      <h2>Ne kulpas la kulpanto</h2>
      <!-- komenco de la enhavo -->

       

       <p>Plene rehabilitita estas la antaŭa germana federacia prezidanto, Christian Wulff,
   kiun prokuroroj akuzis pro korupto kaj malkonvena akcepto de avantaĝoj.
</p> 
<p>Dum dujara priesplorado de plentempa stabo de ĉ. 24 personoj, kio devigis la prezidanton
   demisii, la afero evidentiĝis fontanta el arbitraj denuncadoj, senbazaj asertoj kaj
   orgojla konduto de la prokurora instanco.
</p> 
<p>Kune kun politikaj malamikoj kaj sensaci-produktema ĵurnalistaro, ŝtataj akuzistoj
   ne sobre esploris duban supozon, sed kreis detruan persekutadon.
</p> 
<h3>Venkis</h3> 
<p>Wulff, kiun ne nur profesie sed ankaŭ private trafis dolorige la afero, ne akceptis
   elirigan oferton. Li insistis daŭrigi la proceson. Kaj li venkis. La koncerna juĝejo
   verdiktis, ke li estas plene senkulpa pri korupto.
</p> 
<p>Kontraŭuloj malgraŭe hurlis, ke juran absolvon li ja povis akiri, tamen restas „morale kulpa”. Ĉiam estas homoj, kiuj ne nur komence ĵetas ŝtonojn, sed eĉ post la afero ne ĉesas
   ĵeti. Kaj tiuj ofte taksas sin la plej bonaj homoj en la socio.
</p> 
<p>Aldoniĝis nova skandalo. La koncerna prokurorejo la parlamentanon Sebastian Edathy
   (SPD / socialdemokrata) akuzis pro „mola suspekto pri ebla neleĝa posedo de pedofiliaj varoj”. Edathy antaŭ nelonge prezentis sin mem en parlamenta esplorkomisiono kiel akran
   akuziston kontraŭ ŝtataj instancoj, strebante al pli alta posteno. Antaŭ la publika
   diskonigo fuĝis la suspektato. La afero ankoraŭ ne finiĝis.
</p> 
<h3>Avertis</h3> 
<p>Tamen jam finiĝis la kariero de la ministro Hans-Peter Friedrich (CDU / kristandemokrata),
   kiu konfidence avertis la superulojn de la konkurenca partio SDP, ke troviĝas „putra ovo” en ties nesto.
</p> 
<p>Tiun geston tuj la publiko interpretis „perfido de sekreto en la ofico”. La ministro la sekvan tagon demisiis sub la forta premo. Jam longe estas konate:
   Ne kulpas la kulpanto, sed la informinto.
</p> 
<p>Nuntempe daŭre en Germanio aperas tiaj kazoj. Ne plu la faktoj gravas, sed opinioj
   kaj babiladoj. Kaj tiujn oni lerte scias produkti kaj manipuli.
</p> 
<p>Kompreneble maladekvata aŭ krima agado en ŝtataj funkcioj ne estas tolerebla. Sed
   ankaŭ la senindulgaj akuzantoj demandu sin, kiaj homoj ili mem estas.
</p> 
