      <h3>Spirita vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Dio kaj religioj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pri dio mi havas neklaran ideon. Niaj prapatroj nomas ĝin Maŭ (io, kion nenio superas). Tiun Maŭ niaj prapatroj ne adoris, sed ja siajn voduojn. Mi vizitis katolikan lernejon kaj tie oni instruis nin pri adoro al la dio de la biblio. Islamanoj adoras sian Alahon, kaj tial konsideras nekredantoj la kristanojn. Tiuj ĉi havas diversajn grupojn batalantajn inter si. Mi demandas min, ĉu dio vere aprobas tiajn kondutojn. Mi pripensadas. Subite io atingis mian kapon. Jen!</p> <p>Dio montros sin al la homoj tre klare. Jes, ja la homoj malkovros dion, ne en la ĉielo, sed nur en la koroj de la homoj. Tiam vizitado de preĝejoj ne plu necesos. Kredantoj ne plu bezonos iri al preĝejo, kaj eĉ okazos, ke kredantoj ribelos kontraŭ la religiaj ĉefoj. Pastroj, pastoroj, imamoj ktp estos forpelitaj de la preĝejoj, kiujn oni bruligos. La homoj do vidos, ke dio donas al ili tutan liberecon, kiun la religiaj ĉefoj rabas de ili. Ne nur la liberon ili rabas, sed ankaŭ monon, devigante la fidulojn ĉiel ajn kotizi.</p> <h3>Kion mi komprenigu al mi mem?</h3> <p>Kio estas en la homo, ke ĝi vivas – tio estas dieco. Dio tute ne estas iu <i>estulo</i> ie, kiu punas aŭ rekompencas. Ĝi estas la forto en la naturo, kiu igas, ke plantoj kresku, homoj naskiĝu kaj mortu, ĉio okazu/moviĝu. Tiun dion kun miaj prauloj mi nomas Maŭ – aŭ FUKRo (Forto la Universon Kreanta kaj Reganta). Tiu dio, laŭ mi, tute ne bezonas adorkliniĝon de la homoj, nek de ĉio ajn en la naturo. Ĝi disponigas ĉion al la tuta mondo egale. Tial, ĉiu rajtas esti ĉio, kio la aliaj estas. Nia stato dependas de nia vivmaniero. Nenio de ekstere trafos nin, se ni mem de interne ne altiras tion. La mondo estas tia, ke kion vi semas, tion vi rikoltas. Maŭ estas tre justa, kaj lasas ĉiun libera agadi, kun la sciigo, ke la celo de la agado de ĉiu homo estas tiu ĉi mem. Tial, kiu volas, ke bono trafu lin, tiu faru bonon, kio validas ankaŭ por la malo. Kaj kio poste ...?</p> <p>Jes, pliaj ideoj alvenas, kaj ilin mi baldaŭ disponigos al ĉiuj.</p> <p>Ni iru kune sur la vojo liberiganta la homojn de ĉio katenanta ilin.</p> <div style="text-align:right">G<span class="mm">BEGLO</span> Koffi</div> 
