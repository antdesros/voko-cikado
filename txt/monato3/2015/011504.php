      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Tre bonvena verko, sed ...</h2>
      <!-- komenco de la enhavo -->

       

      <p>Pri la unua mondmilito en Nederlando relative malofte ion oni aŭdas aŭ legas. Nia
   tiuepoke eble plej fama aŭtoro, Louis Couperus, el la urbo, kie en Germanio li esperis
   fari brilajn raportaĵojn pri la heroa militado por sia haga taggazeto, verkis pri
   ĝi iom nostalgie kaj kun seniluziiĝo. Post nelonge li reiris hejmen, al Hago.
</p> 
<p>Tamen la Granda Milito, kiel prave oni titolis ĝin plurlingve, meritas pli da atento.
   Tial do estas tre bonvene, ke aperis 
   <i>In Flanders Fields</i> de Herwig Verleyen el Bruĝo, kiu en Toronto fariĝis doktoro 
   <i>honoris causa</i> pro sia multjara dediĉo konigi la verkon kaj vivon de John McCrae en Flandrio. Prave
   oni donis pro ĉi tiu 
   <i>In Flanders Fields</i> al li la premion de la Asocio de Flandra-Naciaj aŭtoroj.
</p> 
<p>En la libro troveblas, laŭ mi, ĉio, kion en tia libro oni povas serĉi: la familio
   de John McCrae, ambaŭ gepatroj (la patrino eĉ legis latine kaj greke!), liaj gefratoj,
   la familia heredaĵo: estis tradicia tiufamilie la devo servi la patrolandon, sin dediĉi
   al la apuduloj kaj okupiĝi pri poezio. Ĉiuj tri fadenoj en John kuniĝis: li fariĝis
   militisto – kun ĉiam pli altaj rangoj, kuracisto – ankaŭ ĉiam pli altrange, kaj poeto, finfine internacie fama, eĉ se nur pro unu poemo.
</p> 
<h3>Militaj spertoj</h3> 
<p>Liaj unuaj militistaj travivaĵoj, 1899-1901, estis en la milito inter la brita kolonia
   imperio kaj la sendependiĝemaj buroj. Pro la granda impreso, kiun multloke vekis la
   prezidanto de la buroj, Paul Krüger – en sia bonvolveka migrado laŭ eŭropaj kortegoj kaj parlamentoj –, kaj pro la relative multaj gazetaj raportaĵoj pri la britaj unuaj koncentrejoj de
   la mondo, multaj homoj en Eŭropo certe spertis tiun militon multe malpli favore ol
   John McCrae, kiu vivis la britan flankon. Poste en Montrealo John McCrae rapide avancis
   kiel kuracisto, laboris pri aŭtopsioj, pri patologio, pri kontaĝaj malsanoj – instruis, verkis studlibrojn ktp. Ankaŭ poemojn fojfoje li aperigis.
</p> 
<p>En 1914 sekvis Flandrio. La germanaj trupoj, kontraŭe al ĉiu militjuro, ekuzis venengason:
   la linioj ambaŭflanke apenaŭ milimetrojn moviĝis. Mortis milionoj da homoj. Terura
   buĉado – John McCrae prizorgis vunditojn, akompanis mortantojn. Tiutempe li verkis sian plej
   faman poemon 
   <i>In Flanders Fields</i>. Manskribaĵon en la libro oni vidas.
</p> 
<p>Verleyen detaleme provas montri la ekzaktajn datojn kaj ankaŭ tion, kio plejfunde
   instigis lin al la verkado ĝuste tiumomente. Post tio li rezonas, kiuj ecoj de la
   poemo kaŭzis, ke ĝuste ĉi tiu fariĝis la blazona poemo pri la unua mondmilito.
</p> 
<h3>La poemo mem</h3> 
<p>Kelkaj etaj ĉapitroj montras la postan historion: malsaniĝo de John McCrae kaj morto.
   Tiam venas pluraj reagaj poemoj al ĉi 
   <i>In Flanders Fields</i> en la angla, plus kelkaj tradukoj en Esperanton.
</p> 
<p>La dua parto de la libro enhavas plurajn „Rememorojn” pri la aŭtoro kaj la poemo. La tuton sufiĉe trafe ilustras diversaj fotoj. Entute
   tre inda libro pri fame konata poemo, kiu pripensigas pezajn momentojn el nia homa
   historio.
</p> 
<h3>Tamena bedaŭro</h3> 
<p>Pri la traduko kelkfoje mi ne povas fieri: eble plej ĝene estas, ke por la angla 
   <i>larks</i> oni prenas, eble pro sonimito, la vorton „lariksoj”, sed, kiel sciate, tiaj arboj ne flugas. Ankaŭ enestas kelkaj aliaj lingvaj eraretoj,
   „preĝejo” (konstruaĵo) por „eklezio” (institucio), „presbitera” („presbitero” estas homo; vidu PIV) por „presbiteriana”, fojfoje „lia”, kie konvenus „sia”, sed, kutime, la eraretoj ne donas kialon miskompreni.
</p> 
<p>Tamen, la tradukinton mi dankas, ĉar li havigis al nia publiko valoran atestaĵon pri
   inda poemo pri tiu terura unua mondmilito.
</p> 
