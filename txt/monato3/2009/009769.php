      <h3>Sporto</h3>
      <h3 class="tem">BAZOPILKO</h3>
      <h2>Iom da historio kaj iom da fiziko</h2>
      <!-- komenco de la enhavo -->

       

       <p>Bazopilko estas tre populara en Usono, sed ĝi estas ludata en multaj landoj. „<i>Besuboru</i>” jam prosperis dum ĉirkaŭ jarcento en Japanio kaj la „<i>homu ran</i>” estas tiom parto de la japana kulturo, kiom sumo-luktado, kabuko kaj suŝio. Bazopilko estas ankaŭ tre populara en la pacifik-randaj orient-aziaj landoj, en Kanado kaj en Kubo, Domingo kaj la kariba regiono.</p> 
<h3>Sendependa institucio</h3> <p>Bazopilko, kiel Usono mem, evoluis el brita antaŭaĵo en unikan kaj sendependan institucion. La origino de bazopilko venis de neformala formo de la angla sporto kriketo nomata „rondkuro”, kiu estis ludata en la nord-amerikaj kolonioj jam en la mezo de la 18a jarcento. La ludo jam estis nomata „bazopilko” en libro por infanoj en 1744.</p> <p>Usonanoj ekludis bazopilkon per neformalaj ludantaroj, uzante lokajn regulojn, en la frua 19a jarcento. Dum la 1860aj jaroj la sporto, senrivala je populareco, jam estis vortpentrita kiel la „nacia distraĵo” de Usono.</p> 
<p>Alexander Joy Cartwright [Aleksander Ĝoj Kartrajt] (1820–1892) el Nov-Jorko inventis la modernan bazopilkan kampon en 1845. Li kaj la anoj de lia Novjorka Knikerboka Bazopilka Klubo elpensis la unuajn regularojn por la moderna ludo de bazopilko.</p> <p>La unua registrita bazopilka ludo okazis en 1846, kiam ĉi tiu ludantaro malgajnis kontraŭ la Novjorka Naŭopa Bazopilka Klubo. La ludo okazis ĉe Elsian Fields en Hoboken, Nov-Ĵerzejo. En 1858 la Nacia Asocio de Ludantoj de Bazopilko formiĝis. En 1869 la Cincinatiaj Ruĝ-Ŝtrumpoj fariĝis la unua malkaŝe salajrata bazopilka ludantaro. En 1871 la unua profesia bazopilka ligo, la Nacia Asocio de Profesiaj Bazopilkaj Ludantoj, establiĝis. En 1876 la unua ĉefa ligo, la Nacia Ligo, formiĝis. En 1878 Frederick Winthrop Thayer [Fredrik Ŭintrop Tejer] de Masaĉuseco, kapitano de la bazopilka klubo de Universitato Harvard, ricevis patenton por bazopilka kaptanto-masko. En 1884 la unua Bazopilka Mond-Serio estis ludata.</p> <h3>Iom da fiziko</h3> <p>La ĵetanto staras malpli ol 20 metrojn for de la batanto kaj ĵetas la pilkon je rapido de proksimume 40 metroj en sekundo. Dum sia flugo la pilko falas el sia „rekt-linia” vojo je proksimume 1,2 metroj. Aldone, pro la kudraĵoj sur la pilko estas konsiderinda aer-tumultemo, kiu tuŝas ĝin kaj ŝanĝas ĝian vojon kaj vertikale kaj horizontale. Por pligrandigi ĉi tiun efikon la ĵetanto ofte aldonas ekstran turniĝon al la pilko. La pilko do kutime faras proksimume 20 rotaciojn dum sia vojo. Pro tio la pilko povas moviĝi ĝis 44 centimetrojn horizontale el sia „rekt-linia” vojo. Kaj la pilko faras la plimulton de sia horizontala moviĝo dum la lasta kvarono de la vojo! Oni ankaŭ konsideru, ke la pilko bezonas malpli da tempo por trairi tiun kvaronan vojon (proksimume 1/6 da sekundo) ol la batanto bezonas por antaŭen-svingi la batilon (proksimume 1/5 da sekundo). La batanto do devas komenci sian antaŭen-svingon antaŭ ol la pilko komencas montri multan horizontalan moviĝon.</p> <p>Se la batanto mistaksas la rapidon de la pilko, lia antaŭen-svingo estos aŭ tro frua aŭ tro malfrua. Kaj se li misjuĝas la alton de la pilko, lia antaŭen-svingo estos aŭ tro alta aŭ tro malalta. Pro tio la batado de bazopilka pilko per la batilo fare de la batanto estas tiel malfacilega!</p> <p>Unu el la plej bonaj batantoj iam estis Ted Williams. Pri la batado de bazopilka pilko li diris: „La batado estas 50-elcente super la ŝultroj.” Kaj Yogi Berra, unu el la plej amuzaj ludantoj iam diris, „90 % de la bazopilka ludo estas mensaj kaj la alia duono estas fizika!” Nepre ambaŭ pravis!</p> <div style="text-align:right">Vus-Tit Z<span class="mm">IK</span></div> 
