      <h2>Historiromano duoble tradukita</h2>
      <!-- komenco de la enhavo -->

       

     <p>Hella S. Haasse (1918-2011) estis unu el la plej sukcesaj modernaj verkistoj nederlandaj, kaj ŝia romaneto <i>Urug</i> estis ŝia unua romano, unue eldonita en 1948.</p> <p>Inter proksimume 1800 kaj 1949 Indonezio estis kolonio de Nederlando (kvankam dum la dua mondmilito Japanio suverenis ĝin ĝis 1945). La jaroj tuj post tiam estis periodo de ribelo, dum la „indiĝenoj” strebis sendependigi sian arkipelagon. Haasse dum la antaŭmilita periodo estis lernejano en Nederlanda Hindio, kiu fine renomiĝis Indonezio.</p> <h3>Ĉarma</h3> <p>La romaneto <i>Urug</i> havas nur fone la politikan/kulturan baraktadon inter la nederlandaj mastroj kaj la indiĝena loĝantaro. Multe pli, ĝi estas tre ĉarma, bele verkita rakonto pri du najbaraj infanoj. Unu estas knabo, kiu senescepte koniĝas simple kiel „mi”, do kvazaŭ pseŭdoaŭtoro de la verko. Li estas la juna filo de regiona estro el Nederlando. La alia infano estas indiĝena knabo samaĝa, nomita Urug.</p> <p>Kvankam laŭteorie devus esti granda abismo inter la ĉiutaga vivo de la nederlandaj mastroj kaj tiu de la indiĝenoj, en ĉi tiu rakonto tio preskaŭ tute mankas. La patrino de la nederlandfamilia knabo estas intima amiko de la patrino de Urug, kaj ambaŭ patrinoj urĝas la du knabojn kunludi kaj viziti unu la alian. Granda parto de la romaneto temas pri tiu kunludado kaj la infana amo inter ili.</p> <h3>Indoneziaj vortoj</h3> <p>Sed kial du samtempe aperantaj kaj pseŭdokonkuraj tradukoj kaj eldonoj de la rakonto? Mi zorge ekzamenis kaj komparis ilin, unu kun la alia. Kaj mia konkludo: ambaŭ egale bonas. Ambaŭ tradukoj estas bone faritaj, la lingvaĵo estas bona en ambaŭ. Komposteraroj proksimume same oftas en ambaŭ. Iomete ĝena karakterizaĵo de ambaŭ estas la relative abunda uzo de indoneziaj vortoj, kvankam miaopinie por multaj el ili ordinaraj nialingvaj esprimoj kaj terminoj ekzistas. Ambaŭ eldonoj klarigas tiujn indoneziajn vortojn per apendico.</p> <p>Aspekte, la eldono de VoKo estas pli alloga, ĉefe ĉar ĝi uzas iometete pli facile legeblan literaron. Tamen la presliteroj en la eldono de Internacia Esperanto-Instituto estas pli grandaj, do pli favoras personojn, kies vidkapablo estas iom neakra. Ĝi ankaŭ havas nelongan enkondukon, dum la VoKo-a eldono donas nur la rakonton kaj vorteksplikan apendicon.</p> <p>Mia takso pri la rakonto: tre leginda literaturaĵo, interesa, facile legebla verko, de talenta romanisto.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Hella S. Haasse: Urug. El la nederlanda tradukis Gerrit Berveling. Eld. VoKo, Zwolle, 2012. 79 paĝoj gluitaj. Neniu ISBN indikita.</h6> <h6 class="bib">Hella S. Haasse: Urug. El la nederlanda tradukis Piet Buijnsters. Eld. Internacia Esperanto-Instituto, Hago. 114 paĝoj gluitaj. ISBN 978-90-805651-0-4.</h6>  
