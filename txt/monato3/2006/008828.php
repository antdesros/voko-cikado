      <h3>Politiko</h3>
      <h3 class="tem">GENOCIDO</h3>
      <h2>Krimo forgesita</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En aprilo 2005 okazis la 90a datreveno de la genocido de la armena popolo. La evento estas mondskale agnoskita, ĉu per sciencaj kongresoj, ĉu per eldonado de libroj kaj vidbendoj. Ĉi tie estas prezentataj fono kaj faktoj pri la unua genocido de la 20a jarcento.</b></p> <p>Komenciĝis la ekstermado en la 19a jarcento. Inter 1894 kaj 1896 pereis 300 000 armenoj kaj pliaj 300 000 fuĝis al aziaj, eŭropaj kaj afrikaj landoj. Sed la tiama masakro apenaŭ kompareblis kun la genocido okazinta en 1915.</p> <h3>Sekreta instrukcio</h3> <p>En 1911, en la nun greka urbo Tesaloniko, kaŝe kunvenis Junaj Turkoj, kiuj decidis turkigi islamanojn en la turka imperio kaj ekstermi kristanojn. Ili atendis oportunan momenton. Kiam komenciĝis la unua mondomilito, en aŭgusto 1914, la turka registaro komprenis, ke dum milita ĥaoso ne rimarkeblos genocido. En aprilo 1915 estas sendita sekreta instrukcio, laŭ kiu la armena loĝantaro estu forpelita al arabaj dezertoj kaj tiel eliminita.</p> <p>La programo efektiviĝis tripaŝe. Komence la turkoj rekrutigis ĉiujn armenajn virojn, kapablajn uzi armilojn, kaj ilin sekrete buĉis. Duapaŝe arestiĝis partiaj, intelektaj kaj religiaj armenoj: ekzemple iun nokton en aprilo 1915 en Istanbulo la polico arestis 800 intelektulojn. Triapaŝe oni sendis ekzilejen virinojn, infanojn kaj maljunulojn.</p> <h3>Masakritaj</h3> <p>El 2,5 milionoj da armenoj estas masakritaj aŭ mortis pro malsato kaj malsano 1,5 milionoj. Aliaj fuĝis eksterlanden kaj 200 000 perforte turkiĝis. Samtempe okazis kultura genocido: estis detruitaj 66 urboj, 2500 vilaĝoj kaj 1500 lernejoj. Krome la turka registaro alproprigis al si posedaĵojn kaj bank-kontojn de armenoj.</p> <p>La krimo restis senpuna. Nur post la dua mondomilito oni ekkonsideris genocidon punebla. Nun 15 registaroj de la mondo agnoskas la genocidon de armenoj en Turkio. En 1967, en Parizo, dum dua internacia kongreso pri krimoj, oni akceptis la nocion „armenocido”. Tamen la turka registaro ankoraŭ ne agnoskis la genocidon, kiel analoge la germana agnoskas la holokaŭston.</p> <div style="text-align:right">Susanna G<span class="mm">EVORGJAN</span></div> 
