      <h3>Ekonomio</h3>
      <h3 class="tem">Ĉinio</h3>
      <h2>Geedziĝoj stimulas konsumon</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En septembro 2007 la ĉina ministrejo pri komerco aperigis enketan raporton, kiu montras, ke dum la jaro 2006 en la lando la totala konsumo pro geedziĝoj atingis gigantan sumon. Averaĝa paro da novaj geedzoj elspezis por sia geedziĝo 0,3 milionojn da juanoj. Tio egalas al la sumo de iliaj enspezoj dum kvar jaroj.</b></p> <p>La influo de la geedziĝaj konsumoj sur la malnetan enlandan produkton (MEP) vere estas mirinda: en 2006 ili atingis 13 % de MEP! En tiu jaro en la tuta lando oni pro geedziĝoj sume konsumis varojn kaj servojn super 1500 miliardoj da juanoj (150 miliardoj da eŭroj). Ankaŭ la agrikulturo kaj la industrio nerekte je ĉirkaŭ 1200 miliardoj profitis el tio.</p> <p>La ministrejo enketis 60 000 parojn en urboj kaj vilaĝoj kaj trovis, ke dum 2006-2007 ĉiu paro meznombre elspezis 4000 juanojn por vestaĵoj, 6000 por perloj kaj juveloj, 10 000 por nupto, 60 000 por renovigo de la loĝejo, 10 000 por turismo en la miela tempo kaj 40 000 por aĉeti modajn meblojn kaj modernajn elektrajn aparatojn.</p> <p>Laŭ ĉina tradicia moro la novgeedzoj devas okazigi ceremonion, sin montri antaŭ siaj parencoj kaj amikoj kaj regali ilin per abunda festeno, alie ili, eĉ registritaj, ne povas esti rigardataj kiel „laŭleĝaj” geedzoj. Tial ili kaj iliaj gepatroj volas fari bonan ceremonion. Ĝenerale oni preparas 20 tablojn por la festeno. Kun 1000 juanoj por ĉiu tablo kiel kalkula normo, nova paro por tio elspezas 20 000 juanojn. Bonhavaj paroj por paradi ankaŭ aĉetas novan aŭton.</p> <h3>Salajro de kvar jaroj</h3> <p>Inter la geedziĝaj konsumoj la elspezoj por nova loĝejo okupas relative grandan porcion. En urboj la novgeedzoj meznombre pagis 163 000 juanojn por aĉeti domon. Tio estas pli multe ol la rektaj elspezoj de la geedziĝo. La ministrejo el la enketo konkludis, ke unu paro havas averaĝan monatan salajron de nur 6250 juanoj. Domo do kostas 26-oblon de la monata enspezo. La rekta konsumo pro geedziĝo alproksimiĝas al 127 000 juanojn kaj estas 20-oblo. Sume oni do nepre atingas elspezon, kiu egalas al la sumo de la salajroj de ambaŭ personoj dum kvar jaroj.</p> <p>Tiel alta estas la geedziĝa konsumo, ke per sia propra ŝparmono la novgeedzoj ne povas ĝin kovri. Ĉar geedziĝo estas grava afero en la homa vivo, la gepatroj do volonte donas monhelpon. Laŭ la raporto, 81,6 % de la gefianĉoj akiris diversgrade financan subtenon de la gepatroj. Ĉirkaŭ duono ricevis de la gepatroj kaj pliaĝaj gefratoj subvencion por kovri 20 ĝis 60 % de la tuta konsumo. Por 15 % la gepatroj malavare pagis eĉ 80 ĝis 100 % de la tutaj elspezoj.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div> 
