</p> 
<h3>Signifoj</h3> 
<p>La signifoj de la vortoj en ĉi tiu listo estas respektive: „koni”, „preni”, „havi”, „piedfingro”, „fari”, „kredi”, „devi” (en la skota) kaj „-os” (estonta tempo, en la islanda), „generi”, „konstrui”, „bruli”, „aĉeti”, „tero”, „ludi”, „elekti”, „postulo”, „difekti”, „transloki”, „infekti”, „toleri”, „resti”, „demandi”, „veki”, „profeti”, „trovi”, „inviti”, „observi”, „prunti”, „perdi”, „malpleni”, „skribi”, „infano”, „granda”, „piediri”.</p> 
<p>Preskaŭ neniu el tiuj vortoj ekzistas en la moderna angla. Oni notu, ke ĉi tiu listo
   konsistas preskaŭ sole el verboj. Oni ankoraŭ povus citi centojn da pliaj skandinavdevenaj
   vortoj per aldono de substantivoj, adjektivoj ktp.
</p> 
<h3>Konkludo</h3> 
<p>La fakto, ke la angla kaj la skota estas samdevenaj lingvoj, plifortigas – aŭ almenaŭ ne senvalidigas – la interesan teorion de Jan Terje Faarlund. Samtempe ni eble tiel demonstras, ke
   la skota – se ne la angla – povas esti rigardata kiel precipe skandinavdevena idiomo.
</p> 
