      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Eternaj demandoj el rubkamiono</h2>
      <!-- komenco de la enhavo -->

       

      <p>Kelkajn tagojn antaŭ kristnasko pereis en Glasgovo, Skotlando, ses butikumantoj, kiam
   rubkamiono, kies ŝoforo kredeble kolapsis ĉe la stirilo, suriris trotuaron, renversis
   kiel keglojn siajn viktimojn kaj, veturinte kelkcent metrojn, fine haltis kontraŭ
   muro de hotelo.
</p> 
<p>Tiel, ene de sekundoj, mortis geavoj kaj ilia nepino, plus 29-jaraĝa instruistino
   kaj du virinoj iom pli ol 50-jaraĝaj. Vundiĝis ankaŭ dekopo da aliaj homoj.
</p> 
<p>Sendube tragedio, kiu ekster Britio meritis maksimume du-tri frazojn en interna paĝo
   de gazeto aŭ, eble, ĉar kristnasko kaj do „sennovaĵa” periodo, duonminuton kiel piednoton en televida bulteno. Tutcerte, eĉ se vi ja flanke
   informiĝis pri la akcidento, vi tuj ĝin forgesis.
</p> 
<p>Evidente, ĉar la viktimoj estis skotoj. Viaj samnacianoj tiam ne butikumis en Glasgovo,
   do ne tie viktimiĝis. Tial la amaskomunikiloj en via lando nur flanke, se entute,
   priraportis.
</p> 
<p>Jen bizara, sed ĝis difinita grado komprenebla afero, ke ies tragedio fariĝas via
   kuriozaĵo. Komprenebla, ĉar la homa menso ne povas kapti, la homa koro ne povas enteni,
   la tragediojn, kiuj nin ĉirkaŭas. Eĉ en t.n. internacia epoko, ni emas ankoraŭ nacie
   pensi.
</p> 
<p>Esceptojn prezentas mondskuaj eventoj, kiel la buĉado en Parizo de ĵurnalistoj de
   la satira magazino 
   <i>Charlie Hebdo</i>. La murdo de redakcianoj, ĉar – almenaŭ en Eŭropo – vere escepta, altiris mondan simpation kaj tutmonde surstratigis arojn da ŝokitaj
   simpatiantoj.
</p> 
<p>Aliflanke, la murdado de centoj da palestinanoj kaj la vundado de miloj da aliaj dum
   pasintsomera konflikto kun Israelo, ne estigis paralelan reagon. Konfliktoj tiaj,
   kun oble pli katastrofaj homaj rezultoj, ne sufiĉe esceptas – malfeliĉe – por veki amasan, publikan simpation. Do, malgraŭ esceptoj, ja validas la tezo: paroĥas
   novaĵoj laŭ la supra principo, ke ies tragedio fariĝas via kuriozaĵo.
</p> 
<p>Do ĉu britoj ne interesiĝus pri la novjara enmarigo de aviadilo de 
   <i>Air Asia</i>, kiam pereis 162 homoj, se la gazetoj ne anoncus, ke inter la viktimoj troviĝas brito?
   Kio pro samsemajna incendio sur greka pramŝipo, inter kies pasaĝeroj estis kvar britoj?
   Tia naciismo, de ĵurnalista vidpunkto, liveras hokon, sur kiun pendigi internaciajn
   (kaj do, cinike, malpli interesajn, ĉar forajn) novaĵojn.
</p> 
<p>Evidente ni esperantistoj – sennaciaj, transnaciaj, supernaciaj, internaciaj – ne tiel infektiĝas. Aŭ ĉu? Dum universalaj kongresoj oni kaptas foje la impreson,
   ke la vera internacia lingvo estas naciaj popoldancado, folkloraj kostumoj ktp, kaj
   tro ofte dum kongresa teatro-vespero oni pavas per tradukita, nacia dramo anstataŭ
   per indiĝena, vere internacia kaj esperanta originalaĵo.
</p> 
<p>Klare, skotoj reagis al la tragedio alimaniere ol en aliaj britaj kaj eksterbritaj
   urboj. Dum kelkaj horoj, memore al la viktimoj, oni malŝaltis kristnaskajn lumojn
   en la urbocentro de Glasgovo, dum Fejsbuk-kampanjistoj petis, ke privatuloj same faru
   dum du minutoj ankaŭ en privataj domoj.
</p> 
<p>Krome – kaj tion, mi, humanisto, vere ne komprenas – pastroj kaj sacerdotoj okazigis diservojn por memori la viktimojn. Raportite estas,
   ke kreskis la nombro de preĝejo-vizitantoj. Kial? Kial serĉi konsolon ĉe dio, kiu,
   kiam la rubkamiono kuglis laŭ la trotuaro, dormetis aŭ alimaniere okupiĝis? Kial fidi
   „ĉiupovan” dion tiel senpovan?
</p> 
<p>Ne kontentigas la sinteno de unu el la skotaj pastroj, priraportita en nacia gazeto.
   „Oni demandas min,” diris Kelvin Holdsworth, „ĉu malfacilas paroli pri dia amo, kiam okazas malbono.”
</p> 
<p>Li daŭrigis: „Fojfoje tre malfacilas trovi la ĝustajn vortojn, tamen la vero jenas, ke la amo de
   Dio prilumas la plej mallumajn horojn. Tion ni festas je kristnasko.”
</p> 
<p>Espereble la familianoj de la viktimoj de la glasgova kamiono-tragedio konsolon ĉerpos
   el la preĝoj de la pastroj kaj el la kristnaska „klarigo”, ke meze de mallumo surtrotuare tragedia brilas ja dia lumo. Mi tamen dubas.
</p> 
