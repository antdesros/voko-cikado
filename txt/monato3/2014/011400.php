      <h3>Ekonomio</h3>
      <h3 class="tem">Baltio</h3>
      <h2>Nova vojo por natura gaso</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Litovio jam ne plu estos dependa de la gasodukte importata rusa gaso, ĝis nun la unusola
      vojo por kontentigi la proprajn gasbezonojn. En la havenurbo Klaipėda komencis funkcii
      nova terminalo de likvigita natura gaso (LNG). La projekto konsistas el LNG-ŝipo kaj
      la strukturo por ĝia priservado: kajo kaj gasokondukilo.
   </b>
</p> 
<p>La terminalon gaso atingos danke al speciale konstruita ŝipo. La LNG-ŝipo 
   <i>Independence</i> lastan lundon de oktobro 2014 alvenis en la havenon de Klaipėda. La ŝipo estas 294
   m longa, 46 m larĝa kaj 47 m alta. Ĝin konstruis la kompanio 
   <i>Hyundai Heavy Industries</i> en Suda Koreio. La ŝipon Litovio luos dum dek jaroj de la norvega entrepreno 
   <i>Hoegh LNG</i>. La luo kostos 189 000 usonajn dolarojn tage aŭ 689 milionojn dum dek jaroj. Poste
   litovoj akiros rajton aĉeti la ŝipon.
</p> 
<p>Minimuman gaskvanton de 0,54 miliardoj da kubmetroj (m
   <sup>3</sup>) la nova terminalo ricevos de la norvega kompanio 
   <i>Statoil</i>. Ĝia prezo dependos de la valoro de naturgasa indico en la brita borso 
   <i>National Balancing Point</i>.
</p> 
<p>La projekton de la terminalo, kies valoro egalas al preskaŭ 145 milionoj da eŭroj,
   Litovio efektivigis memstare, sen financa helpo de Eŭropa Unio.
</p> 
<p>Onidire en la fino de 2013 por la rusa gaso Litovio pagis la plej altan prezon en
   Eŭropo – 465 usonajn dolarojn por mil m
   <sup>3</sup>. Pro tio Litovio ekprocesis kontraŭ la rusa kompanio 
   <i>Gazprom</i> en stokholma arbitracia kortumo. Litovio postulis redonon de 1,5 miliardoj da usonaj
   dolaroj por konsumantoj, kiuj tropagis en la periodo de 2004-2012. Finfine 
   <i>Gazprom</i> malaltigis la prezon je 20 % kaj rezulte ĝi preskaŭ egaliĝis al la averaĝa gasprezo,
   kiun pagis germanaj konsumantoj.
</p> 
<h3>Solena alveno</h3> 
<p>La alveno de la LNG-ŝipo 
   <i>Independence</i> kaŭzis solenan manifestadon sur la havena kajo en Klaipėda. Ĝin partoprenis la prezidanto
   de Litovio Dalia Grybauskaite, ĉefministroj de Litovio, Latvio kaj Estonio, ambasadoroj
   de pluraj landoj de Eŭropo kaj Usono, kaj delegitoj de Eŭropa Komisiono.
</p> 
<p>La prezidanto de Litovio diris, ke Litovio en la 25a jaro de sia sendependeco povas
   fieri pri siaj kuraĝo, aŭdaco kaj politika volo, ĉar ĝi kiel unua en Baltio konstruis
   LNG-terminalon, kiu forte influos la estontecon de la tuta regiono. La prezidanto
   ĝojis, ke Litovio finfine iĝis energie sendependa lando, kiu ĉiam helpos najbarojn,
   se tio estos necesa, kaj kontentigos 90 % de la naturgasaj bezonoj de ĉiuj baltaj
   landoj. Ŝi aldonis, ke nun Litovio ne plu maltrankviliĝas pro eventuala perturbo en
   la gasliverado el Rusio, ja la nuna LNG-terminalo permesas agi en relative fleksebla
   kaj libera maniero.
</p> 
<p>Ano de la Eŭropa Komisiono respondeca por energio, Günther Oettinger, nomis la inaŭguron
   de LNG-projekto vere sukcesa okazaĵo por Litovio. Laŭ lia aserto, tio estas deca ekzemplo,
   kiu pruvas, kiel eblas solvi problemojn de gaslivero en Eŭropo.
</p> 
<div style="text-align:right"><span class="mm">LAST</span>
</div>   
<p>En Klaipėda estas uzata nova teknologio por pritrakti LNG: FSRU, mallongigo de la
   anglalingva 
   <i>Floating Storage Regasification Unit</i>, flosanta stokad- kaj regasig-unuo. Tio estas ŝipo, kiu funkcias kiel kombinitaj
   transportilo, stokejo kaj instalaĵo por transformi LNG de likva (malvarmigita je -161&#x9d;ĝis
   -164
   <sup>o</sup>C) al gasforma stato.
</p> 
<p>FSRU-instalaĵo konstrueblas pli rapide kaj malpli koste ol la tradicia surtera regasigilo
   kaj havas kroman avantaĝon, ke ĝi estas transportebla al la loko, kie ĝi estas plej
   bezonata. Surtere nur necesas konstrui la konekton al la enlanda gasreto. (rr)
</p> 
