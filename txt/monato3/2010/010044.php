      <h2>Kaptu la feliĉon!</h2>
      <!-- komenco de la enhavo -->

       

     <p>La libro <i>Kiam mi estis la plej feliĉa en la vivo?</i> naskiĝis el la samnoma rubriko de la revuo <i>Bazaro</i>, eldonata en Rumanio. La redaktorino de <i>Bazaro</i> (kaj kompilinto de la libro) Júlia Sigmond petis la legantojn de la gazeto respondi al ĉi tiu demando, kaj en la libro vi trovos 104 respondojn. Kelkaj el ili estas anonimaj aŭ subskribitaj nur per la persona nomo, sed ĝenerale videblas ankaŭ la familinomo kaj lando. Inter la aŭtoroj estas kelkaj famaj esperantistoj, sed la plimulton vi verŝajne ne konas. La lingvaĵo estas tiu de ĉiutaga vivo, facila kaj bone komprenebla.</p> 
<p>La respondintoj jam profitis, ke ili povis rememori la plej feliĉajn momentojn de sia vivo, iom filozofi pri la feliĉo mem, pri valoroj de la homa vivo. Kaj kion ni povas promesi al la legontoj de la libro?</p> <p>La legantoj ricevos aron da malgrandaj rakontoj, de kelkaj linioj ĝis kelkaj paĝoj. Kelkaj el la rakontoj estas veraj beletraĵoj, kaj oni povas legi tiujn historiojn kiel romanon kaj aldone esti certa, ke ĉio finiĝos feliĉe! Iuj aŭtoroj priskribas sian tutan vivon, aliaj rakontas pri emociplena aŭ streĉa momento, ekzemple, sendifekta savo el akcidento, neatendita helpo en ŝajne senelira situacio, partopreno en konkurso, kortuŝa festo.</p> 
<p>Aliajn respondojn oni povas trakti kiel etajn filozofiajn eseojn. Homoj meditas pri fido, pacienco, kompreno, libero, ĝojo, bono, naturo, paco, interna kaj ekstera harmonio. Multaj konfesas, ke por vera feliĉo „necesas” problemoj kaj malfeliĉo; nur post suferoj oni povas senti sin vere feliĉa. Por multaj respondintoj feliĉo estas ligita kun religio kaj Dio. Multaj deziras feliĉon ne por si, sed por proksimuloj.</p> 
<p>La legontoj ankaŭ ricevos liston de „ordinaraj” feliĉaj eventoj de la homa vivo: sukcesaj ekzamenoj, enamiĝo, la unua amletero, promenoj kun la amato, geedziĝo, naskiĝo de gefiloj kaj genepoj, profesiaj atingoj. Uzu tiun aron, ne preteratentu tian eventon en via propra vivo! Aŭ eble vi klopodu mem efektivigi ion? Eble vi povas fieri, ke via feliĉa momento estas pli originala kaj neordinara? Ĉu realigo de iu revo?</p> 
<p>Multaj respondantoj parolis pri Esperanto en sia vivo. Feliĉon donas la studado de la lingvo, korespond-amikoj (kiuj ofte poste iĝas realaj amikoj), instruo de Esperanto, diligentaj lernantoj, sukcesaj tradukoj, verkoj, prelegoj, Esperanto-aranĝoj.</p> <p>Interesa estas ankaŭ la formo de la respondo. Necesas konfesi, ke la demando mem ne estas tre originala. Oni ofte demandas pri feliĉo politikistojn, artistojn kaj aliajn famulojn, ĉu ne? Kion elpensi, por ke la respondo estu neordinara, interesa kaj ... eventuale demonstru elokventon?</p> 
<p>Kelkloke en la libro troviĝas grafikaĵoj, sed, ĝenerale, ili ne estas ligitaj kun la teksto.</p> <p>Do, tiu libro provizos vin per agrabla filozofia-priesperanta-beletra legaĵo. Feliĉan tempopasigon!</p> 
