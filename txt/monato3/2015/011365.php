      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Rigardo al la interna Rusio deekstere</h2>
      <!-- komenco de la enhavo -->

       

     <p>Kalle Kniivilä estas finndevena sveda ĵurnalisto, brile posedanta la rusan lingvon
   kaj multe verkinta pri kaj el Rusio dum la lastaj 25 jaroj. Aŭtune de 2013 li faris
   novan vojaĝon tra Rusio, vizitante Moskvon kaj ankaŭ grandajn kaj malgrandajn rusajn
   urbojn por trovi la respondon al la demando: kial plimulto de la homoj partoprenantaj
   balotojn (do, ĉirkaŭ triono de la tuta loĝantaro) ĉiam denove subtenas per siaj voĉoj
   sinjoron Putin, kiu de la unua vido (mi diru, ankaŭ de la dua kaj la tria!) ne aspektas
   ideala prezidanto kaj faras sufiĉe multe da misoj. Kiu do estas la reala homa bazo,
   danke al kiu tiu persono, kiu ofte neglektas homajn rajtojn kaj objektive reduktas
   civitanajn liberojn, jam 15 jarojn okupas en la lando la plej altajn postenojn?
</p> 
<h3>Ĵurnalisto-observanto</h3> 
<p>Kniivilä partoprenis, kiel ĵurnalisto-observanto, la balotojn de la ĉefurbestro en
   Moskvo, li parolis kun pluraj homoj de diversaj profesioj: eksaj inĝenieroj, kiuj
   devas okupiĝi pri io alia, fabrikaj laboristoj, kiuj ĉu perdis, ĉu konservis siajn
   postenojn, busŝoforoj, flegistinoj – bunta vico de simplaj homoj, kiuj travivis malfacilan periodon de la vivo en la 1990aj,
   spertis falojn kaj sukcesojn kaj nun plejparte atingis certan stabilecon de la vivo.
   Estas ne nur rusoj, sed ankaŭ reprezentantoj de aliaj etnoj – tataroj, ĉuvaŝoj. Ili ofte bone scias, ke ne ĉio glatas en la lando – polico kruelas, koruptado grandas, ĉefaj televidkanaloj plenas je ŝtata propagando
   – sed tiujn homojn tiaj aferoj ne multe tuŝas, ĉar politikon ili strebas eviti, en
   televido interesiĝas pri filmoj kaj seriaroj, kaj volas konservi almenaŭ tion, kion
   ili ja havas. Por unu generacio unu tertremo sufiĉas. Tial ĝuste el la vicoj de la
   provincanoj, finfine akirintaj stabilecon, venas plejmulte da voĉoj por Putin, kies
   epoko signifas stabilecon. Pri la nova generacio, kiu ne spertis la revoluciajn kaj
   ekonomiajn ŝtormojn de la 1990-aj, ankoraŭ ne klaras – ĝi ne rapidas riveli siajn strebojn.
</p> 
<h3>Klarigoj trafaj, sed ...</h3> 
<p>Kiel ĵurnalista verko, la libro de Kalle Kniivilä estus senriproĉa, se li ne provus
   fari kelkajn proprajn klarigojn al la priskribataj aferoj. Ofte tiuj klarigoj pri
   antaŭnelongaj historiaj eventoj estas tre trafaj (kiel, ekzemple, emfazo de tiu observo,
   ke la politiko de Putin estas daŭrigo de la politiko de Jelcin k.s.), sed samtempe
   li faras tri bazajn metodologiajn erarojn, kiuj iom senvalorigas la tuton. Tio estas,
   unue, blinda sekvo al la libro de Jegor Gajdar („Falo de Imperio ...”) en klarigo de la kaŭzoj de la ruiniĝo de Sovetio. Gajdar, kiel fanatika novliberalulo
   (kvankam ekskomunisto!) ĉefe emfazas certajn internajn ekonomiajn kaŭzojn, kaj lia
   celo estis sin indulgi pri absolute malsukcesaj ekonomiaj reformoj, kiuj ĉion detruis,
   sed nenion kreis – anstataŭ „ŝoka terapio” okazis ŝoko sen terapio (ke povis esti male, pruvas la ekzemplo de pluraj landoj,
   de Ĉinio ĝis Slovenio).
</p> 
<p>Due, sekvante Gajdar-on, nia aŭtoro faras la duan principan eraron, sen kiu nenio
   en la nuna Rusio bone kompreneblas. Ja ne estas, kiel li asertas, iu „konspirteorio” pri tio, ke Sovetio ruiniĝis pro usona influo: ekzistis reala milito (bone, ke nur
   malvarma!), kiun Sovetio tute klare malgajnis. Kaj en la postsovetia Rusio en la komuna
   konscio gravegan rolon ludas la socia psikologio de malvenkinta popolo. Tion nepre
   necesis konsideri ankaŭ analizante la relative grandan subtenon al Putin.
</p> 
<p>Kaj, trie, parolante pri la opozociuloj (eĉ „radikalaj”), la aŭtoro apenaŭ montras, kiel iliaj 
   <i>pozitivaj</i> programoj diferencas de tiu de Putin. Eble pro tio, ke grandaj diferencoj tie ne
   ekzistas – kaj Proĥorov, kaj Navalnij, kaj plejparto de la ceteraj estas laŭ siaj ekonomiaj
   ideoj eĉ pli konsekvencaj novliberaluloj ol Putin mem (kiu, pro desubaj protestoj,
   devis lasi iujn socialajn garantiojn kaj programojn, kion la aŭtoro ja honeste mencias)
   – se iu el ili ricevus potencon, la ekonomia situacio de la malfortaj tavoloj de la
   popolo povus nur malboniĝi. Mi kaj multaj miaj amikoj, kiuj dum jaroj kontraŭstaras
   al Putin, dum neniu vetero voĉdonos por Navalnij aŭ Proĥorov. Do la popolo (tiu parto,
   kiu dume voĉdonas) ofte preferas la kutiman malbonon al la minacanta de Navalnij kaj
   la ceteraj malpliboniĝo!
</p> 
<h3>Rekomendinda</h3> 
<p>Ĉiuokaze, la libro estas ege interesa, kaj unuavice rekomendindas por legado al esperantistoj
   el Rusio – por ili plej grave estas, kiel ilia kutima vivo aspektas deekstere. Almenaŭ la frazo
   „Moskvo estas urbo de kontrastoj”, per kiu Kalle Kniivilä komencas la ĉapitron pri Moskvo, valoras multon ... 
   <i>(Noto: famega frazo el populara sovetia komedio, kiu parodiis la komencon de ĵurnalismaj
      kliŝaj raportoj pri kiu ajn ekstersovetia urbo.)
   </i> Ankaŭ alilandaj homoj, kiuj interesiĝas pri la lando, ekscios multajn vivajn detalojn,
   kiujn oni kutime neglektas, sed kiuj konsistigas realecan bildon de normala ekzistado
   de simplaj homoj.
</p> 
