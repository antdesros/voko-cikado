      <h3>Politiko</h3>
      <h3 class="tem">BALKANIO</h3>
      <h2>Denove elmigrado</h2>
      <!-- komenco de la enhavo -->

       

         <p>Ekde septembro pasintjara ĉirkaŭ 40 000 homoj elmigris el Kosovo. La elmigrado sekvas
   tiun de la jaroj 1998-1999 rezulte de la milito kaj serba terorismo. Tamen tiam, kiam
   aperis en Kosovo trupoj de Nord-Atlantika Traktat-Organizaĵo (NATO), revenis ĉirkaŭ
   95 % de la elmigrintoj. Ili volis krei prosperan, pacan vivon en sia patrujo.
</p> 
<h3>Seniluziiĝo</h3> 
<p>Nun, tamen, 16 jarojn poste, kosovanoj seniluziiĝas. Kulpas parte ekonomiaj kialoj.
   Privatigo de iamaj ŝtataj entreprenoj riĉigis manplenon da homoj kaj senlaborigis
   dekmilojn da aliaj.
</p> 
<p>Alia faktoro estas la daŭra izoleco de la lando. Kosovanoj rajtas libere moviĝi nur
   al kvar najbaraj landoj. Pro tio opinisondoj montras, ke la optimismo pri eŭropa estonteco
   draste falis (de 95 % antaŭ kelkaj jaroj ĝis 45 % ĉe la novjaro 2015 kaj ĝis 5 % du
   monatojn poste).
</p> 
<p>Rolon ludas ankaŭ la konduto senskrupula de politikaj partioj. Ekzemple, en 2014 la
   lando restis ses monatojn sen parlamento, ĉar la partioj volis ŝtatpotencon nur por
   si mem. Homoj senpacienciĝis, atendante pli bonan vivon en sia lando malgranda.
</p> 
<h3>Mafianoj</h3> 
<p>Enplektita en la elmigrado estas kredeble la serba registaro. Por malstabiligi Kosovon
   kaj por montri, ke la tieaj albanoj ne kompetentas pri memregado, serboj kunlaboras
   kun mafianoj. Ili veturigas kosovanojn ĝis Subotica, apud la hungara landlimo, kaj
   ilin kontraŭleĝe transirigas al Hungario.
</p> 
<p>De tie la elmigrintoj provas atingi aliajn eŭropajn landojn. Laŭdire ili elspezis
   ĉirkaŭ 45 milionojn da eŭroj por bus- kaj trajn-biletoj kaj por ŝmiri la manojn de
   mafianoj. Tamen multaj restas tendare en Hungario, ankaŭ Aŭstrio, en malfavoraj kondiĉoj,
   atendante azilon, kiun ili verŝajne ne akiros.
</p> 
<p>Plejparte ili estos hejmen revenigitaj. Intertempe la parlamento en Kosovo provas
   enkonduki leĝojn por haltigi tian elmigradon.
</p> 
