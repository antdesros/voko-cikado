      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Rumanio post 25 jaroj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Pasis kvaronjarcento post la t.n. revolucio (reĝimŝanĝo) en Rumanio, kaj oni povas
   pripensi la bonajn kaj la malbonajn ŝanĝojn en la vivo de la plimulto.
</p> 
<p>Eble pli multnombras la bonaj ŝanĝoj. Oni povas aĉeti ĉion en plenplenaj vendejoj
   (se oni havas monon). Oni povas rigardi multajn televidajn kanalojn (filmojn plenajn
   de agresemo kaj seksumado). Oni povas legi ĉiuspecajn gazetojn kun ĉiuspecaj opinioj,
   oni havas Interreton; en sociaj retejoj oni povas esprimi opiniojn (ankaŭ trompi aliajn
   homojn, ŝteli monon helpe de Interreto).
</p> 
<p>Krome oni rajtas manifestacii eĉ kontraŭ la registaro (rezulto malcerta). Gejunuloj
   povas lerni eksterlande, se ili havas riĉajn gepatrojn. Sed ankaŭ la gefiloj de malriĉuloj
   povas lerni en itala, franca aŭ brita lernejo, se ili estas kun gepatroj, kiuj laboras
   en la koncernaj landoj.
</p> 
<p>En Rumanio post la reĝimŝanĝo la plej multaj fabrikoj estis privatigitaj aŭ fermitaj:
   tiel miloj da laboristoj perdis siajn laborlokojn. Gejunuloj iras eksterlanden por
   labori (aŭ almozpeti, ĉefe ne bone edukitaj romaoj). Homoj pli ol 40-jaraĝaj, kiuj
   ne trovas laboron, vivtenas sin mizere per ŝtata monhelpo kaj per okazaj taglaboroj,
   se ili ne trovas eksterlande sezonan laboron, ekzemple en agrikulturo.
</p> 
<p>Ne pasas monato sen tragika novaĵo en la televido. En Italio, kie laboras miloj da
   rumanoj, estas murdita virino, kiu havas infanojn en Rumanio. Sed en preskaŭ ĉiuj
   landoj, ne nur Italio, kie vivas rumanoj, okazas murdoj. La viktimo ofte estas virino,
   patrino.
</p> 
<p>Laŭ statistiko ekzistas en Rumanio nuntempe ĉirkaŭ 350 000 infanoj „postlasitaj” pro eksterlande laborantaj gepatroj. Aliaj 400 000 infanoj spertis aŭ spertas dum
   pli-malpli longa tempo la foreston de eksterlandaj gepatroj. Al tiuj infanoj ne mankas
   materiaj bonoj: la gepatroj sendas monon, donacojn, parolas kun la idoj per Skajpo,
   sed mankas la ĉeesto gepatra.
</p> 
<p>Multaj postrestantaj infanoj estas prizorgataj de avino aŭ alia parenco. Iuj estas
   solaj, aliaj en institucioj. Laŭ opinio-esploroj, ĉirkaŭ duono de la gelernantoj post
   la fino de la studjaroj deziras eksterlande labori.
</p> 
<p>Kion do faris la novaj politikistoj, kiuj ekregis post la ekzekuto de la geedzoj Ceaușescu?
   Ekaperis post la reĝimŝanĝo klaso de riĉeguloj, el kiuj kelkaj nun estas arestitaj
   pro korupto, dum pligrandiĝis la nombro de malriĉuloj.
</p> 
<p>Antaŭ 25 jaroj ĉiu havis sekuran laborlokon kaj iamaniere vivis, eĉ se malriĉe, en
   malbone hejtataj, malbone lumigataj domblokoj. Oni vojaĝis per malpuraj, plenplenaj
   aŭtobusoj, spektis televidon tage nur du horojn, ne rajtis esprimi malkontenton pro
   vivkondiĉoj, pro manko de libereco. Estis malfacile akiri pasporton por vojaĝi eksterlanden,
   kaj eĉ tiam precipe nur en t.n. „socialismajn” landojn.
</p> 
<p>Ne estas dezirinde reveni al tia epoko, sed tamen ... Kaj nun, post la prezidantiĝo
   de Klaus Iohannis (M<span class="mm">ONATO</span> 2015/1, p. 6), la popolo de Rumanio komencis denove esperi pri bonaj ŝanĝoj (eĉ se
   la lastan fojon mortis la espero).
</p> 
