      <h2>Mortis Izabel Santiago</h2>
      <!-- komenco de la enhavo -->

       


<img src="http://www.esperanto.be/fel/gif/lyra-s.jpg" alt="Iza"/>
      <p>En Roterdamo forpasis la 13an de majo Izabel Cristina Oliveira Santiago [izabéŭ kristina olivejra sanĉiagu], multjara redaktoro, revizianto kaj redakcia sekretario de M<span class="mm">ONATO</span>.</p> <p>Izabel Santiago naskiĝis en Rio-de-Ĵanejro en 1959, kiel filino de portugalo el Avejro. Ŝi estis magistrino pri antropologio kaj psikiatro, sed tiun profesion ŝi nur mallonge praktikis pro prietikaj malkonsentoj kun siaj kolegoj medicinistoj. Ŝi instruis Esperanton en sia hejmurbo kaj gvidis muzikan programeron en radio <i>Imprensa-FM</i>. Ŝian Esperanto-kurson <i>O Que é Esperanto</i> (Kio estas Esperanto) publikigis la ne-esperantista eldonejo <i>Editora Brasiliense</i>.</p> 
<p>En 1989 ŝi venis al Eŭropo por volontuli ĉe Universala Esperanto-Asocio (UEA). Tuj poste ŝi estis varbita de Paul Peeraerts por kunlabori en la internacia skipo de reviziantoj de M<span class="mm">ONATO</span>. Kvankam ŝi laŭ instruiteco ne estis lingvisto, ŝi montris tre grandan kaj profundan konon de la lingvo. Ŝi posedis escepte delikatan lingvan senton pri Esperanto. Ŝia lingvoregado montriĝis, kompreneble, en la diskutoj pri detaloj en la lingvaĵo de M<span class="mm">ONATO</span>-tekstoj, sed ankaŭ estis ĝuinda tio, ke ŝi kapablis pri subtile humura sinesprimado per rimedoj de la lingvo.</p> 
<p>En 1994 Izabel Santiago transprenis la redaktadon de la rubriko <i>Kuirado</i> de M<span class="mm">ONATO</span>, en 1995 ŝi iniciatis la novan rubrikon <i>Mozaiko</i> (por legantoj, kiuj preferis mallongajn artikolojn) kaj en 2000 ŝi transprenis la redaktadon de la rubriko <i>Scienco</i>. En aŭgusto de 2001 ŝi devis „provizore” demisii de siaj funkcioj pro familiaj kialoj, sed ŝi bedaŭrinde neniam povis repreni siajn taskojn.</p> 
<p>Ŝiaj amikoj memoros ŝin kiel teneran kaj precipe tre sentivan homon. En sia privata vivo Izabel Santiago estis ĉiam serĉanta la veron: la veron en la interhomaj rilatoj, sed ankaŭ la veron en la naturo, la veron pri morto kaj postmorta vivo. Tiu scio pri postmorta vivo verŝajne igis ŝin decide rifuzi kuracistan helpon kaj medikamentojn en la lastaj tagoj de sia vivo.</p> 
