      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Sigloj</h2>
      <!-- komenco de la enhavo -->

       

     <p>En niaj mondoj kaj lingvoj abundas mallongigoj kaj sigloj, kaj daŭre ni devas alkutimiĝi al novaj. Hodiaŭ mi lernis, ke la viruso, kiu kaŭzas birdogripon (vidu ankaŭ ĉi-numere „El mia vidpunkto”, p. 7), nomiĝas H5N1 – ho-kvin-no-unu. Tute ne estas interesa, kion tio signifas kaj kiel eventuale la viruso nomas sin mem. Dum la pasintaj monatoj preskaŭ ĉiutage ni legis ankaŭ alian siglon: CIA. Estas tiu usona sekreta servo, al kiu oni riproĉis forkapti kaj torturi homojn, kiuj laŭ ĝi estas teroristoj. En Esperanto prononci ĝin memorigas pri intima, karesa alparolo „ci”, sed estas ja iom paradokse, paroli pri „cia” servo, se vere ĝi turmentas homojn. En Germanio ligita al tiu skandalo ludas rolon alia sekreta servo kun la siglo BND – bo-no-do. Sed ankaŭ pri ĝi tiuokaze ne eblas paroli pri bono do, ĉar al ĝi oni riproĉas, ke iliaj agentoj dum la atakoj de la usona armeo kontraŭ Bagdado komence de la Irak-milito helpis al usonaj sekretaj kolegoj trovi bombardocelojn, kvankam la tiama germana registaro strikte kontraŭis tiun agresan militon. Ne estis bono, do.</p> <p>Ankaŭ en Esperanto abundas sigloj, kiuj ofte erargvidas homojn, precipe komencantojn. Ekzemple ili devas lerni, ke SAT-ano ne estas diablo sed membro de SAT, kaj ke krome temas ne pri organizaĵo por satigi homojn. Asocio de vegetaranoj nomiĝas VEK, eble ĉar oni volas veki viandomanĝantojn? Kaj ĉu skoltoj, kies ligo nomiĝas SEL, volas signali, ke ili ankaŭ rajdas? Aliflanke FEL ne estas entrepreno por senhaŭtigi bestojn, sed organizaĵo, kiu inter aliaj eldonas tiun ĉi revuon. Interesa estas GEA; ŝajnas, ke la germana organizaĵo volas emfazi, ke oni akceptas membrojn de ambaŭ seksoj. Kontraste asocio de verkistoj nomiĝas EVA, kvazaŭ ĝi akceptus nur virinojn (Eva laŭ la biblio estis la unua ino). Eble vi konas ankaŭ la siglon CO de UEA. Se tiu Centra Oficejo en Roterdamo interesas vin, vi povas veni al la malferma tago la 13an de majo 2006. En ties kadro mi parolos pri mia nova libro <i>El verva vivo ĵurnalista</i> kaj pri nia revuo.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
