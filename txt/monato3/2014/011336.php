      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Temoj por pluraj romanoj</h2>
      <!-- komenco de la enhavo -->

       

     <p>Iam Anton Ĉeĥov diris, ke la vivo de ĉiu homo estas temo por malgranda rakonto. Eble,
   kutime estas tiel, kaj la vivoj de ordinaraj homoj donas ne pli ol tiom. Sed tio veras
   por la ordinaruloj! La nova libro de Trevor Steele, kiu estas lia aŭtobiografio, inkludas
   tiom da temoj, taŭgaj por pluraj romanoj (iuj ja estis uzitaj tiel de la aŭtoro),
   ke oni povas nur miri pri tio, kiom da gravaj eventoj povas enhavi unusola homa vivo.
</p> 
<h3>Bildoj</h3> 
<p>Antaŭ ni aperas bildoj de profunde provinca vilaĝa vivo en kvinslandaj arbaroj; edukiĝo
   en katolika etoso, studado en porpastra seminario ... (Mi devas noti, ke, leginte
   la koncernajn ĉapitrojn, mi tuj rememoris la etoson de la fama romano 
   <i>La dornarbustaj birdoj</i> de Colleen McCullough). Poste sekvas rifuzo de la pastra kariero, ekinteresiĝo pri
   lingvoj (komenciĝintaj de la sukcesoj en la latina), unua instruista sperto en fora
   dezerta vilaĝeto kaj la unua granda vojaĝo al Eŭropo por ricevi la lingvoperfektiĝon
   en la germana. Tri jaroj da travivaĵoj en Germanio, inkluzive varbadon de enmigrontoj
   al Aŭstralio, laboron en kolonio por eksterlandaj kondut-deviuloj, kaj aliaj ... Veno
   al Anglio – kaj tie, krom aliaj travivaĵoj kaj spertoj, konatiĝo kun Esperanto kaj la unua engaĝiĝo
   en la movadon. Poste – laboro en Hispanio (ankoraŭ regata de Franco), denove en Germanio, en Bona Espero
   en Brazilo ...
</p> 
<h3>Epokfaraj ŝanĝoj</h3> 
<p>Eble, sensencas listigi sinsekve ĉiujn lokojn, kien trafis kaj kiujn priskribas la
   aŭtoro – oni legu mem. Inter la plej admirindaj aventuroj estis lia perpieda marŝo tra la
   tuta Aŭstralia kontinento kaj laboro inter la aborigenaj triboj. Necesas diri nur,
   ke li ial sukcesis trafi en la regionojn de la mondo, kie okazadis gravaj epokfaraj
   ŝanĝoj, ekzemple en Pragon antaŭ ĝia fama printempo, en Sovetion en la momento de
   ĝia disfalo – kaj prezentas vere interesajn skizojn pri siaj impresoj tie. Kompreneble, ĉio tio
   estas 
   <i>nur fragmentoj</i> de tio, kion li povus rakonti!
</p> 
<h3>Sincereco</h3> 
<p>La leganton kortuŝas sincereco, konfeseco de la rakonto. Tio koncernas kaj lian internan
   spiritan evoluon, kaj liajn rilatojn kun virinoj. Se temas pri la spirita evoluo,
   li bone montras la evoluon de la senrezerva katolikeco al plena senreviĝo pri la eklezio
   pro ĝia konduto kontraŭa al la sana morala sento, kaj veno al preskaŭa ateismo – sed de ĝi, sub influo de spertitaj permeditaj resaniĝoj (kaj filozofiaj rezonoj pri
   vivo-senco), al iom hezita agnosko de reenkarniĝo. Same li montras, kiel sub premo
   de eksteraj impresoj kaj interna honesto, li evoluas de opinioj konservative dekstraj
   al liberale maldekstraj. Kio koncernas la rilatojn kun virinoj (en la junaĝo tre influitaj
   de la rigora katolika fido), li tute sincere prezentas la historion de siaj enamiĝoj
   kaj profundaj senreviĝoj, kiuj, tamen, ne kondukis al ĝenerala senreviĝo pri la virinoj,
   sed venigis al la lasta vera amo.
</p> 
<h3>Vivaj spertoj</h3> 
<p>Trevor Steele estas unu el la plej elstaraj, fekundaj kaj bonstilaj modernaj esperantlingvaj
   aŭtoroj. Kaj ankaŭ pri tio, kiel li ekverkis, li iom rakontas – mi opinias, ke tiu temo okupas maltro da loko en la libro, ĉar ĉiam estas interese
   por la leganto konatiĝi kun la metiaj sekretoj de verkisto. Sed evidentas, ke la temoj
   de liaj libroj venis el la vivaj spertoj, el la realaj travivaĵoj, kaj nur malgranda
   parto el ili estis ĝis nun uzita en liaj verkoj. Do ni povas esperi pri apero de pluraj
   liaj novaj romanoj kaj noveloj. Tamen la aŭtobiografio mem, kiel preskaŭ ĉiu nova
   libro de la aŭtoro, devas fariĝi nova granda evento en nia literaturo, kiun absolute
   ĉiu legos kun granda ĝuo.
</p> 
<p>Kaj ni disdividu kun la aŭtoro lian esperon, ke post jardekoj lia libro servos kiel
   utilega fonto por ontaj historiistoj de nia literaturo!
</p> 
