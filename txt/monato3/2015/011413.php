      <h3>Scienco</h3>
      <h3 class="tem">EKSPERIMENTO</h3>
      <h2>Ulo dormanta salajron ja kaptas</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Kelkaj homoj prirevas simplan laboron kun bona salajro kaj kiel eble plej malmultaj
      respondecoj. Postenon ŝajnis proponi lastatempe la usona Nacia Aeronaŭtiko kaj Spaco-Administrado
      (NASA) kadre de eksperimento. Oni ricevis 3500 eŭrojn monate por „simple” resti en lito dum 70 tagoj. Kaj interesiĝis pri tio multaj homoj en ĉi tiu stranga
      laborbezona mondo!
   </b>
</p> 
<h3>Proverbo</h3> 
<p>Asertas proverbo, ke „lupo dormanta ŝafon ne kaptas”. La neo en la frazo eble tamen jam estas forigebla, ĉar lupo (ĉi-okaze ulo) ja povas
   atingi sian celon labori kaj riĉiĝi nenion farante: la ununura farenda afero estas
   longe dormi aŭ, kiam tio ne eblas, kuŝi en la sama loko.
</p> 
<p>En Usono okazis en la lastaj monatoj ĝuste tio: oni dungis homojn, por ke ili pasigu
   tutajn tagojn en lito. Kiu konsideras alloga la ideon pri tiu (ĉu ne troe?) sida laboro,
   tiu nepre kontaktu NASA-n aŭ serĉu enrete la ĉi-rilatan oficialan konkurs-anoncon.
   Eble restis liberaj postenoj, aŭ oni antaŭmendu kelkajn por la venonta testado.
</p> 
<p>Oni tute ne pensu, tamen, ke temas pri facila laboro. Eĉ plej konvinkita mallaboremulo
   malfacile akceptus resti samloke kuŝanta dum pli ol du monatoj. Ĉu penvaloras ĉion
   ĉi fari por havigi al si ja bonan, sed ne esceptan, salajron de 3 500 eŭroj monate?
</p> 
<h3>Ekzercoj</h3> 
<p>La eksperimento okazis en la esplor-centro FARU, ĉe la universitato de Teksaso, en
   Galveston. La celo estis observi, kia estas la reago de homa korpo ĉe longdaŭraj kosmaj
   misioj.
</p> 
<p>Esti sternita dum longa tempo, kun la kapo iomete sube kaj la piedoj iom supre kreas
   – laŭ la fakuloj – pozicion similan al tiu, kiu karakterizas astronaŭtojn en la kosma spaco, kiam gravito
   mankas. Oni do volis imiti la samajn kondiĉojn sur la tero.
</p> 
<p>La volontuloj estis priesplorataj kaj antaŭ, kaj dum, kaj post la plenumo de sia kun-
   (kaj kuŝ-)laboro. Tiel oni rimarkis la ŝanĝojn, kiujn ege longa nenifarado kaŭzos
   al homoj el kor-vaskula vidpunkto, sed ankaŭ koncerne la muskolojn kaj skeleton.
</p> 
<p>La rezultoj, jam venantaj, helpos kompreni la kondiĉojn, kiujn povus sperti astronaŭtoj
   en la kosma spaco dum longa interplaneda vojaĝado, kaj trovi rimedojn, kiujn oni povus
   utiligi por mildigi la problemojn. Ekzemple oni provas kompreni, kiuj estas la plej
   taŭgaj ekzercoj fareblaj en lito por teni en sana stato la propran muskolan mason.
</p> 
