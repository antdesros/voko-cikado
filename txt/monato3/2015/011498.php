      <h3>Lingvo</h3>
      <h3 class="tem">KELTAJ LINGVOJ</h3>
      <h2>Engaĝiĝo ne mankas por savi la manksan</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ multaj jaroj ni ricevis en nia hejmo viziton de rimarkinda homo. Temis pri Brian
   Stowell [brajan stoŭel], manks-insulano kaj tiam unu el la ege malmultaj parolantoj
   de la loka lingvo. Ĝi iam estis la ĉiutaga idiomo de la tuta loĝantaro de la insulo,
   sed, ekde ĉirkaŭ la lastaj jardekoj de la 20a jarcento, ĝi estis rigardata kiel formortinta
   lingvo. Ni konversaciis uzante miksaĵon de niaj respektivaj keltaj lingvoj, li en
   la manksa, ni en la parenca skot-gaela, kaj ni pli-malpli interkompreniĝis.
</p> 
<h3>Kruroj</h3> 
<p>La manksa insulo, kies areo estas 572 kvadratkilometroj kaj kies loĝantaro estas proksimume
   76 000, situas meze de la Irlanda Maro, inter norda Anglio kaj la irlanda respubliko.
   Ĝi neniam iĝis parto de la Unuiĝinta Reĝolando de Britio, nek estas regata de la brita
   parlamento, kies dekretoj ĝenerale ne validas sur la insulo. Ĝia statuso estas priskribita
   en la angla lingvo kiel 
   <i>Crown Dependency</i>, tio estas teritorio sub la aŭtoritato de la brita monarkio. Tial ĝi posedas siajn
   proprajn leĝojn, parlamenton, monon, flagon (ruĝan, kun bizara simbolo estigita de
   tri kunigitaj homaj kruroj) kaj nacian himnon (<i>Arrane Ashoonagh dy Vannin</i>, kiu tradukeblas kiel „Manksa Insulo nia Naskiĝa Lando”). La oficialaj lingvoj tie estas la angla kaj la manksa. La insulo ne estas membro
   de EU. Ĝia nomo en la manksa estas 
   <i>Mannin</i> aŭ 
   <i>Ellan Vannin</i>.
</p> 
<h3>Asocio</h3> 
<p>La manksa lingvo (sinnomanta 
   <i>Gaelg Vanninagh</i>) formortadis dum la 20a jarcento kiel unua lingvo, sed poste revivigis ĝin lingvoaktivistoj.
   Ĝia plej proksima parenco estas la skot-gaela lingvo. La manksa, samkiel la skot-gaela,
   estis iom influita de la antikva skandinava, sed ĝia deveno estas tute kelta. Ankoraŭ
   en la 18a jarcento ĝi estis lingvo vivanta, kun ĉirkaŭ 20 000 parolantoj – preskaŭ la tuta tiama loĝantaro de la manks-insulo. Tio ŝanĝiĝis ekde 1765, kiam
   la Brita Reĝolando ĝin aneksis kaj dum pli ol 100 jaroj subpremis la lingvon. En 1899
   estis fondita, por la konservado de la lingvo, 
   <i>Yn Cheshaght Ghailckagh</i> (la manks-gaela asocio). La lingvon parolis kiel gepatran lingvon nur kelkaj homoj
   en la 1930aj jaroj. Esplorantoj klopodis trovi ankoraŭ homojn parolantajn ĝin, sed
   ilia nombro estis en 1946 ne pli ol 20 personoj. En la 1950aj jaroj la asocio komencis
   surbendigi la originan prononcon. Laŭraporte, la lasta denaska parolanto de la lingvo,
   nome Ned Maddrel, mortis en 1974.
</p> 
<h3>Gasto</h3> 
<p>La plej elstara korifeo de la revigligo estas sendube Brian Stowell, la sama viro,
   kiu estis nia gasto antaŭ longe, kiam ni estis freŝe geedziĝintaj. Dum jaroj ni ne
   plu aŭdis ion pri li, sed iutage mi legis gazetaran raporton pri grava evento, okazinta
   sur la manksa insulo, kiu rilatis al Brian. Ĝi informis, ke en 2010 la manksa parlamento
   aljuĝis al li la plej altan honoron pro liaj rimarkindaj kontribuaĵoj al la manksa
   kulturo. Kiam li estis juna, li iĝis flua parolanto de la insula lingvo kaj surbendigis
   la voĉojn de maljunaj ankoraŭ ekzistantaj denaskaj parolantoj. Plie, li regis la parencan
   irlandan lingvon kaj tradukis irlandajn lingvokursojn por instrui la manksan. En 1990,
   Stowell publikigis manksan tradukon de la infana libro 
   <i>Alico en Mirlando</i> kaj, en 2006, estis eldonita lia libro 
   <i>Dunveryssyn yn Tooder-Folley</i> („La vampiraj murdoj”), la unua plenlonga romano en la manksa. Li plurfoje partoprenas elsendojn de la
   manksa radio. Li havas la statuson de 
   <i>Yn Lhaihdir</i> („Legisto” aŭ „Leganto”) de la manksa parlamento.
</p> 
<h3>Lernejo</h3> 
<p>Dumtempe la nombro de la parolantoj kreskis ĝis kelkaj centoj. La personoj, kiuj parolis
   la lingvon kiel duan lingvon, estis en 1991 jam 634 el ĉirkaŭ 80 000 loĝantoj. Ekde
   tiu jaro estas rimedoj por ke ĉiu lernanto studu la manksan lingvon kiel duan lingvon.
   Jam en la unua jaro vizitis la kursojn 1 400 lernantoj. Nun troviĝas eĉ personoj,
   kiuj parolas la manksan (kune kun la angla) denaske. La plej aĝa el ili en 2005 havis
   14 jarojn. La baza lernejo 
   <i>Bunscoill Ghaelgagh</i> en la urbo Saint John’s estas la sola lernejo en la mondo, kie la lecionoj okazas
   sole en la manksa lingvo kaj kie la infanoj lernas flue paroli ĝin. De tie la lernantoj
   povas progresi al mezlernejo (angle: 
   <i>High School</i>), kie ili povas ekde la aĝo de 12 jaroj plustudi la manksan, por akiri la ĝeneralan
   duagradan atestilon pri edukado en la insula lingvo. Oni konstatis, ke tiuj, kiuj
   estas dulingvaj, pli rapide kaj facile lernas fremdajn lingvojn.
</p> 
<h3>Literumado</h3> 
<p>Bedaŭrinde, kiam la Biblio (bazo de la literatura lingvo por multaj lingvoj) estis
   tradukita manksen (la verko estis plenumita en la jaro 1772), la klerika komitato,
   kiu entreprenis la taskon, elektis literumadon, kiu imitis la nekonsekvencajn kaj
   netaŭgajn konvenciojn de la skribita angla lingvo, kun iom da influo el la skribita
   kimra (ekzemple koncerne la uzadon de la litero 
   <i>y</i>) kaj kun la adopto de la unika kombinaĵo 
   <i>çh</i>, kiu respondas la sonon „ĉ”. Ĝi tute malsimilas la keltan ortografion de la skot-gaela aŭ de la irlanda, do,
   kvankam parolantoj de la aliaj variantoj de la gaela lingvo kapablas iom kompreni
   la parolatan lingvon, ili tute ne sukcesas legi tekstojn en la manksa, krom post longa
   alkutimiĝo al ĝia kurioza literumado. Mi iam menciis tion al Brian, kaj li konsentis,
   ke la irlanda ortografio, aŭ tiu de la simila skot-gaela, estus multe pli konvena.
   Unuavide la ortografio de la aliaj gaelaj idiomoj povas ŝajni stranga kaj nefonetika,
   sed ĝi posedas internan logikon, kiu tute mankas en la manksa skribsistemo. Por esplorantoj
   de la keltaj lingvoj la manksa literumado estas grava obstaklo, ĉar – kiel ĉe nekonataj vortoj kaj nomoj en la angla lingvo – oni neniam povas esti certa pri la efektiva prononco de manksa vorto. Por plie malfaciligi
   la legadon de la manksa, en tiu lingvo – same kiel en la ceteraj membroj de la gaela familio de keltaj idiomoj, nome la irlanda
   kaj la skot-gaela – preskaŭ neniu sono respondas aŭ konformas al sonoj de la angla lingvo, kiu apartenas
   al tute alia lingva familio: la ĝermana.
</p> 
<h3>Testamento</h3> 
<p>Mi supre asertis, ke neniu, kiu ne studis la manksan, kapablus legi ĝin, sed en unu
   sola okazo mi sukcesis kompreni mallongan frazon en tiu lingvo. Iam, kiam mi profesie
   laboris kiel tradukisto, advokato petis, ke mi traduku kelkajn manksajn vortojn, kiujn
   li trovis en la teksto de la testamento de forpasinta kliento. Feliĉe mi povis, sed
   tio estis escepto. Longaj tekstoj en la manksa restas por mi volapukaĵo. Tamen mi
   tute ne deziras, ke miaj asertoj malinstigu iun ajn lerni kaj uzi la interesan lingvon
   de la bela manksa insulo!
</p> 
