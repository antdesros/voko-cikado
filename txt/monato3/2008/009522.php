      <h3>Ekonomio</h3>
      <h3 class="tem">ASOCIOJ</h3>
      <h2>Asocia kaj volontula laboroj ekonomie gravas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En nia socio multe da taskoj – ekzemple la eldono de ĉi tiu revuo – estas plenumataj de asocioj kaj volontuloj. Kvankam tia laboro ja havas iun valoron, ĝis nun tute mankis fidindaj ciferoj. Je la 9a de majo 2008 en Bruselo okazis studotago, dum kiu la unuaj rezultoj de nova registradmetodo estis prezentitaj.</b></p> <p>La „Fonduso Reĝo Baldueno” (FRB) estis fondita en 1976, okaze de la 25-jara jubileo de Baldueno la 1-a, la tiama reĝo de Belgio. Li petis ke oni ne havigu al li donacojn, sed ke anstataŭe oni kreu fonduson, kiu kontribuu al pli harmonia socio. Ekde tiam, FRB ludas daŭre pli grandan rolon en la stimulo de diversgrandaj asocioj en Belgio. Iom post iom ĝi ankaŭ internacie aktivas kaj ĝuas prestiĝon, interalie pro ĝia scienca laboro pri tiu sektoro. Unu tia scienca projekto estas la kalkulo de la ekonomia valoro de la agado de asocioj kaj volontuloj. Por tio ĝi kunlaboris kun profesoro Lester M. Salamon de la Centro pri Civitanaj Studoj de la usona universitato <i>Johns Hopkins University</i> (JHU).</p> <p>En ĉiu lando, la centra banko aŭ simila instanco okupiĝas pri la „naciaj kontoj”, kies fina rezulto estas la „malneta enlanda produkto”, la tuto de varoj kaj servoj kiujn lando produktis dum difinita periodo. La kalkulado okazas laŭ metodo preskribita de Unuiĝintaj Nacioj, sed la ciferoj de asocioj ĝis nun estas nur parte registritaj kaj la volontula laboro – esence nepagata – tute ne. JHU verkis aldonan manlibron por unuece fari tion. Kelkaj landoj jam uzis ĝin farante siajn kontojn kaj nun sufiĉe da ciferoj estas disponeblaj por unuafoje prezenti ilin al la publiko.</p> <p>Kunmeti kaj interpreti la ciferojn ne estas facila afero, ĉar en ĉiu lando la situacio malsamas. Ekzemple en Belgio pri instruado kaj sanzorgo grandparte okupiĝas asocioj, dum en aliaj landoj tiuj taskoj povas esti komplete prizorgataj de la ŝtato, aŭ kontraŭe grandparte lasitaj al la komerca sektoro. Oni do estu singarda komparante la ciferojn. Tamen, estas klare ke la asocia sektoro, inkluzive de la volontula laboro, ĉie estas proksimume same granda kiel la sektoroj de konstruado aŭ de financaj servoj: po proksimume 5 % de la malneta enlanda produkto.</p> <p><a href="http://www.kbs-frb.be">www.kbs-frb.be</a>, <a href="http://www.jhu.edu/ccss">www.jhu.edu/ccss</a></p> <div style="text-align:right">Roland R<span class="mm">OTSAERT</span></div> 
