      <h3>Ekonomio</h3>
      <h3 class="tem">ENERGIO</h3>
      <h2>Nuklea energio restu en Litovio</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>El la du Ĉernobil-tipaj reaktoroj de la nuklea centralo en Ignalina la unua jam estis fermita la 31an de decembro 2004 kaj la dua estas fermota fine de 2009. Tio estis la kondiĉo por aliĝo de Litovio al Eŭropa Unio. Tamen, Litovio por sia elektroprovizo ege dependas de nuklea energio.</b></p> <p>Estroj de energiaj kompanioj de la tri baltaj landoj – <i>Lietuvos energija</i> el Litovio, <i>Eesti Energia</i> el Estonio kaj <i>Latvenergo</i> el Latvio – interkonsentis pri konstruo de nova nuklea centralo en Ignalina kaj financado de konstrulaboroj. Ĝi ekfunkcios ĉirkaŭ la jaro 2015 kaj produktos malpli multekostan energion por baltaj landoj, tiel malpliigante la energian dependecon de Rusio. La planojn ankoraŭ devas aprobi registaroj kaj parlamentoj de la tri landoj.</p> <p>Oni planas, ke la nova centralo estos konstruata sur la loko de la tria, planita sed neniam finkonstruita, reaktoro. Ĝia projektado daŭrus kvar jarojn. La kosto estos ĉirkaŭ 10,36 miliardoj da eŭroj. Sian intencon pri partopreno en la konstruprojekto jam esprimis la germana energia konzerno <i>E.ON</i>, kaj siajn nukleajn teknologiojn proponas la japanaj kompanioj <i>Mitsubishi</i> kaj <i>Mitsubishi Heavy Industries</i>, la franca konzerno <i>Areva</i> kaj la kanada <i>AECL</i>. El 26 tipoj de nukleaj reaktoroj estis elektitaj 11, kiuj observas ĉiujn regulojn. Inter ili estas ankaŭ rusaj reaktoroj.</p> <p>Konsilistoj asertas, ke la kompanioj de la tri baltaj landoj estas kapablaj financi la konstruon de reaktoro kun potenco de 1600 megavatoj. Ne estas klare ankoraŭ, kiom da reaktoroj necesas. Ĉi-momente en Ignalina funkcias unu reaktoro kun potenco de 1350 megavatoj. Kiam en la centralo funkciis du reaktoroj, ilia potenco atingis 3000 megavatojn.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
