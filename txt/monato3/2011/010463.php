      <h3>Politiko</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Forpasas granda eŭropano</h2>
      <!-- komenco de la enhavo -->

       

        <p>En Vieno estas enterigita Otto von Habsburg, la plej aĝa filo de la lasta imperiestro de Aŭstrio-Hungario Karl la 1a. Li forpasis la 4an de julio en Pöcking (Germanio) en aĝo de 98 jaroj.</p> <p>Post pompa ceremonio li estis enterigita en la familia kripto, en kiu kuŝas liaj antaŭuloj, kiuj regadis la monarkion Aŭstrio-Hungario dum preskaŭ 650 jaroj. Apud li oni enterigis lian edzinon Regina, kiu forpasis la antaŭan jaron.</p> <p>La enterigan ceremonion ĉeestis reprezentantoj de eŭropaj reĝofamilioj, kaj ankaŭ politikaj gvidantoj de diversaj landoj, multaj el kiuj antaŭe apartenis al la monarkio Aŭstrio-Hungario.</p> <h3>Abatejo</h3> <p>Miloj da homoj venis adiaŭi la forpasinton, kies ĉerko estis elmetata en la viena preĝejo <i>Kapuzinerkirche</i>. La koron de la forpasinto oni entombigis en Hungario, en la abatejo Pannonhalma.</p> <p>Iuj aŭstroj plendis pri la pompo kaj honorsalutoj, akompanantaj la enterigon de tiu ĉi lasta kronprinco de la dinastio Habsburg, argumentante, ke tio ne konvenas en respubliko kiel Aŭstrio.</p> <p>Otto von Habsburg dediĉis sian vivon al la batalo por unueca Eŭropo kaj estis dum 20 jaroj (1979-1999) deputito en la Eŭropa Parlamento, el la germana Kristan-Sociala Unuiĝo.</p> <h3>Pikniko</h3> <p>En 1989 li kunorganizis la piknikon de Tuteŭropa Unio, okaze de kiu oni malfermis la limon inter Hungario kaj Aŭstrio. Danke al tio ĉirkaŭ 600 ŝtatanoj de Germana Demokratia Respubliko (GDR) sukcesis fuĝi okcidenten.</p> <p>Tiu ĉi evento helpis levi la tiel nomatan feran kurtenon. Post kelkaj monatoj falis la berlina muro kaj okazis demokratiaj revolucioj en orienta Eŭropo.</p> <p>Poste li obstine aspiris, por ke orient-eŭropaj landoj aliĝu al Eŭropa Unio. La prezidanto de la Eŭropa Komisiono, José Manuel Barroso, nomis lin „granda eŭropano”.</p> <h3>Ekzilo</h3> <p>Otto von Habsburg naskiĝis en 1912, ses jarojn antaŭ la likvido de la monarkio Aŭstrio-Hungario fine de la unua mondmilito. En 1919 lia familio foriris en ekzilon, kaj multajn jarojn li loĝis for de Aŭstrio.</p> <p>Li rezignis pri la trono en 1961 kaj post kvin jaroj oni permesis al li reveni al Aŭstrio – la unuan fojon post 1919. Li kritikis la nazian reĝimon en Germanio kaj kontraŭis en 1938 la aligon de Aŭstrio al la Tria Regno.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>   
