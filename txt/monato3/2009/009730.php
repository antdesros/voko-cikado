      <h2>Litovio sen flugiloj</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En la vespero de la 16a de januaro 2009 la flugo de la litova flugkompanio <i>flyLAL – Lithuanian Airlines</i> el Amsterdamo al Vilniuso, prokrastita je unu horo, estis la lasta en la historio de la kompanio. Poste la agado de la kompanio, fondita en la jaro 1938, estis haltigita. Ne plu estas rektaj flugoj el Vilniuso al Budapeŝto, Madrido, Londono, Amsterdamo, Frankfurto, Milano, Kievo kaj Moskvo. Oni informas, ke la ŝuldoj de la kompanio superas 26 milionojn da eŭroj.</b></p> <p>Kial <i>flyLAL</i>, privatigita en la jaro 2005, alfrontis pezan financan situacion, kiu kondukis al ĝia morto? Vytautas Kaikaris, la ĝenerala direktoro de la kompanio, klarigis: „La litova flugkompanio longan tempon konkuradis en la merkato je malsamaj kondiĉoj. La falon kaŭzis plikostiĝo de aviada benzino kaj la monda financa krizo. Krome notindas, ke la posedataj aviadiloj ne respondis al la novaj merkataj kondiĉoj. Ili fariĝis tro grandaj, kaj tial flugoj kaŭzis troajn elspezojn. Ni projektis diversajn solvojn por eskapi el la malfacila situacio. Tamen ni ne sukcesis trovi investantojn por plua aktivado kaj, ne dezirante pligrandigi la ŝuldojn, ni devas ĉesigi la agadon.”</p> <p>En decembro la posedantoj de <i>flyLAL</i> turnis sin al la ministrejo pri transporto kun la propono, ke la ŝtato akiru 51 % de la akcioj de la kompanio kontraŭ nur unu lido. Tamen la registaro rifuzis. Tuj post la malsukcesa propono aperis ebla investanto, la svisa kompanio <i>SCH – Swiss Capital Holdings</i>. Ĝi deklaris sin preta aĉeti la flugkompanion. La kontrakto valoris unu milionon da usonaj dolaroj kaj samgrandan kvanton da eŭroj por pagi la ŝuldojn de <i>flyLAL</i>. Tamen la aĉetonto, kiu nur en 2008 registriĝis en Svislando kaj antaŭe agadis en aliaj landoj, montriĝis neserioza. Ĝi ne plenumis sian promeson ĝiri la interkonsentitan sumon antaŭ la fiksita dato.</p> <p>La decido pri la ĉesigo de la agado de la ununura litova flugkompanio konfuzis milojn da vojaĝantoj, kiuj jam havis biletojn. Post la kolapso de la flugkompanio ankaŭ la litova prezidanto estis devigita serĉi novan transportanton. Krome la decido lasis la ĉi-jaran eŭropan ĉefurbon de kulturo sen rekta ligo kun pluraj aliaj ĉefurboj de la kontinento. Ĉi-momente ne klaras, kiel la situacio evoluos. Eligijus Masiulis, la ministro pri transporto de Litovio, menciis, ke juraj instancoj probable ekanalizos, ĉu la bankroto de la kompanio sen konkreta posedaĵo ne estis intenca. Ja antaŭ tiu funebra dato ĉiuj dekkelkaj aviadiloj de <i>flyLAL</i> flugis sub la administrado de la najbara kompanio <i>flyLAL Charters</i>, kiu zorgas pri mendoflugoj.</p> <div style="text-align:right">LAST</div>  
