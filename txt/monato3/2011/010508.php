      <h3>Moderna vivo</h3>
      <h3 class="tem">RUMANIO</h3>
      <h2>Sovaĝa sorto</h2>
      <!-- komenco de la enhavo -->

       

       <p>Post la falo de la komunisma reĝimo en Rumanio ĉesis funkcii ŝtataj agrikulturaj bienoj. Kultivistoj rericevis la proprajn grundojn kaj terenojn kaj komencis mem prizorgi la teron.</p> <p>Ĝenerale la homoj estis malriĉaj kaj uzis ĉevalojn aŭ bovojn en la terkultivado. Poste tiuj, kiuj fariĝis bonhavaj, aĉetis traktoron. Tamen en multaj vilaĝoj oni uzas eĉ nun ĉevalojn, ne havante monon por aĉeti maŝinojn.</p> <h3>Vagantaj</h3> <p>En pli prosperaj lokoj videblas forlasitaj, vagantaj ĉevaloj, ankaŭ senmastraj hundoj. De tempo al tempo amaskomunikiloj raportas pri la kruela sorto de tiuj bestoj forpelitaj de siaj mastroj.</p> <p>Ĉi tiun someron oni raportis pri ĉevaloj „sovaĝaj” en la Danubo-delto. La bestojn oni volis fortransporti en mizeraj kondiĉoj al buĉejo. Temis pri 52 ĉevaloj, el kiuj 11 mortis, malgraŭ la helpo de volontuloj, kiuj provis ilin savi.</p> <h3>Protektindaj</h3> <p>Biologoj opinias, ke la ĉevaloj ne estas „sovaĝaj”, nur sovaĝiĝintaj. Ili do ne apartenas al loka ekologia sistemo kaj detruas rarajn, protektindajn plantojn kaj arbojn. Ekzemple vintre, malsategaj, ili ronĝas la arboŝelojn.</p> <p>Forlasi dombeston estas leĝe malpermesite. Se la iama mastro estos konata, tiu estas punita. Tamen ofte malfacilas identigi la posedinton.</p> <p>Entreprenemaj homoj vidas ŝancon allogi turistojn en regionon, kie vivas ĉ.1500-3000 „sovaĝaj ĉevaloj”. Fakuloj asertas, ke jam de jarcentoj ne ekzistas en Rumanio sovaĝaj ĉevaloj ... kaj la sovaĝiĝintaj bestoj ne estu rezerveje protektataj.</p> <div style="text-align:right">Lenke S<span class="mm">ZÁSZ</span></div> 
