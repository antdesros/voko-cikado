      <h3>Ekonomio</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Aperis hejtoleo-ŝtelantoj</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En Hokkajdo, la plej norda insulo en la japana insularo, estas tre malvarme dum vintro. Oni konsumas multe da hejtoleo por hejtado kaj akvovarmigo. En februaro la hokkajda gubernia polico dissendis la jenan averto-retmesaĝon:</b></p> <p><i>En la lasta januaro disvastiĝis ŝtelado de hejtoleo tra la tuta Hokkajdo. Okaze de ĉirkaŭ 70 % de la damaĝkazoj ŝtelantoj elprenis hejtoleon el la eksterdoma hejtoleujo-konservujo. Por eviti tian ŝteladon vi uzu ŝtopilon kun ŝlosilo, surmetu kovrilon sur tubon de la ujo kontraŭ tranĉado k.a. Se vi tenas hejtoleon en porteblaj plastaj ujoj, vi enŝlosu ilin sekure.</i></p> <h3>Sekvo de dudekjara krizo</h3> <p>Jam de ĉirkaŭ 20 jaroj Japanio daŭre suferas pro depresio deveninta de la altiĝo de la eno. La alta kurzo de la eno malfavoras eksportajn industriojn. Samtempe, en la enlanda merkato deflacio kreskas pli kaj pli. Ĉar la salajroj de la laboristoj malaltiĝis, popolanoj emis malmulte aĉeti.</p> <p>La nova registaro, kiu naskiĝis en decembro de 2012, komencis novan financan politikon, kiu celas inflacion kaj malaltiĝon de la eno. Danke al tiu ĉi politiko, la kurzo de la eno efektive malaltiĝis<sup>1</sup> kaj eksportaj industrioj regajnis spiron. Precipe la aŭtomobila industrio senklopode profitas.</p> <p>Tamen, por industrioj, kiuj dependas de importo, tiu ĉi situacio estas neeltenebla. La prezoj de petrolo, ercoj, raraj metaloj ktp. altiĝis multe. Sekve, tiu ĉi politiko serioze influis la vivon de la popolanoj. La prezoj de benzino kaj hejtoleo altiĝis kaj tio rekte batis la hejm-mastruman buĝeton.</p> <h3>De 60 ĝis 100 enoj por litro da oleo</h3> <p>En la vintra Hokkajdo oni ĉiam unue zorgas pro la prezo de hejtoleo, nur due pro tiu de manĝaĵo. Ekzemple, en mia hejmo almenaŭ 200 litroj da hejtoleo necesas por unu monato. Antaŭe la hejtoleo kostis 60-70 enojn por unu litro. Tiutempe mi pagis 14 000 enojn (tiam 140 eŭrojn) monate. Tamen, en la lastaj tagoj la prezo superas 100 enojn. Nun mi devas pagi pli ol 20 000 enojn (ĉirkaŭ 160 eŭrojn). Tiu ĉi pago nepre necesa superas 10 % de la tuta monata elspezo de mia hejmo.</p> <p>Verŝajne pro tio aperis multaj hejtoleo-ŝtelantoj. Eble aĉaj homoj grupe ŝtelas kaj transvendas hejtoleon por sia profito.</p> <h3>Senelira registara politiko</h3> <p>La japana registaro havas memfidon pri sia financa politiko: celi inflacion kaj malaltiĝon de la eno. Tamen, se altiĝos nur la prezoj sed ne la salajroj de la laboristoj, homoj ne volos aĉeti, do ŝrumpos la ekonomio, kontraŭe al la antaŭsupozo de la registaro. Kiel eblas altigi la salajron de la laboristoj, ne estas klare. Oni timas plie suferi pro tiu nova politiko. Tial kelkaj ekonomiistoj avertas, ke nun unue necesas politiko por altigi la salajrojn.</p> <div style="text-align:right">K<span class="mm">AWAI</span> Yuka</div> <h6 class="pied">1. En novembro 2012 1 eŭro = 100 enoj, en februaro 2013 1 eŭro = 126 enoj.</h6>  
