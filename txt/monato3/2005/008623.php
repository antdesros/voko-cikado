      <h2>Sciencfikcia romano tre intereskapta</h2>
      <!-- komenco de la enhavo -->

       

     <p>Ĉi tiu sciencfikcia detektiveca romano preskaŭ tuj kaptas la leganton. Ni komencas per la ekalveno de Maksim. Ni sekvas lin sur 304 paĝoj, kaj iom post iom ni sciiĝas (kun li), ke nenio estas tia, kia ĝi ŝajnas. Iom post iom ni lernas ion novan, ke niaj antaŭaj supozoj estis eraraj, sed ke nun ni scias la veron. Sed kiam tio estas okazinta dekon da fojoj, ni komencas dubeti, kaj ŝajne ankaŭ Maksim dubetas. Kaj la klimakso de la rakonto, kiun ni trovas nur en la kelkaj lastaj paĝoj, denove renversas niajn supozatajn sciojn. Tre lerte.</p> <p>Nu, Maksim, kiu kredas esti veninta el alia mondo, trovas sin en socio, kiu estas malfacile komprenebla. Ke ĝi estas diktatore regata, ŝajnas certe; ke neniu kredas, ke Maksim venis el alia mondo, estas certe. Ke ĉiuj personoj en la socio kondutas preskaŭ idente, estas certe. Ke antaŭ nelonge okazis atombomba militado, estas certe. Ke restas multa radiado, estas certe. Ke tiun specifan socion minacas pluraj tre potencaj ŝtatoj najbaraj, estas certe. Ke nur sistemo de tre evidentaj defendoturoj, ĉirkaŭanta la landon, detenas la malamikojn, estas certe. Ke Maksim estas eksterordinare forta, povas certagrade rezisti kuglojn kaj radioaktivecon kaj rapidege lernas, estas certe. Ke en la lando mem ekzistas spionoj kaj perfiduloj, estas certe. Ke li devas kontraŭbatali ilin, estas certe. Ke poste necesas partopreni en perfidula grupo, estas certe. Aŭ ĉu? Ne ĉio certa efektive estas certa.</p> <p>Estas grave ne tro detalumi la enhavon, ĉar tio povus malkaŝi la koron de la intrigo, kiun Maksim mem nur lastamomente ekkonas. Nu, interdepartementaj burokratismaj konfuzoj ja okazas, negrave kie en la universo oni estas ... Ĉu sufiĉe por veki vian scivolon? Espereble, jes.</p> <p>La rakonto fluas bone. Iom amuzas, ke la vorto „la” plejparte mankas, kaj kiam ĝi ja aperas, plej ofte ĝi estas misuzata. Pli bonus, se la tradukinto plene rezignus pri tiu vorto, laŭ la iama konsilo de Zamenhof. Escepte de tio, mi ne povas riproĉi la tradukon. La eldonaĵo, kvankam la paĝoj estas gluitaj, estas tre fortika. Oni bezonas bonajn okulojn por legi la literojn, sed mi supozas, ke por eviti tro grandan volumon, necesis iom dense presi.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Arkadij kaj Boris Strugackij: La loĝata insulo. Eld. Impeto, Moskvo, 2005. 304 paĝoj gluitaj. ISBN 5-7161-0122-4.</h6>   
