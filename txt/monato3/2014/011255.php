      <h2>Pikas la ludoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kiam en septembro 2013 oni anoncis, ke la Olimpikoj en 2020 okazos en Tokio, la japanaj
   registaro kaj popolo aplaŭdis la decidon. Sed poste aŭdiĝis kontraŭaj kaj kritikaj
   voĉoj.
</p> 
<p>Ekzemple multaj japanoj surpriziĝis, kiam la ĉefministro Abe Shinzo, prezentante sian
   landon al la organizantoj de la ludoj, asertis, ke ne plu estas radioaktiva la akvo
   en la haveno de la fukuŝima atomcentralo. Fakte ankoraŭ estas poluitaj la akvoj en
   kaj ekster la haveno.
</p> 
<p>Aliaj kritikantoj opinias, ke la japana registaro kaj la metropolo Tokio turnu sin
   al pli urĝaj taskoj antaŭ ol elspezi monon por la ludoj.
</p> 
<h3>Konstruado</h3> 
<p>La taskoj inkluzivas la rekonstruon de la nord-orientaj regionoj damaĝitaj de la tertremo
   kaj cunamo kaj la malkonstruon de la paneinta atomcentralo. Kvankam abunda buĝeto
   estas asignita por rekonstruo, la laboro ne glate progresas pro manko de lerta laborforto
   kaj taŭgaj materialoj.
</p> 
<p>Se la konstruado de sportaj kaj rilataj bazaj instalaĵoj komenciĝos, tio certe akcelos
   la mankojn. Tio rezulte malhelpos la rekonstruadon kaj revivigon de la regionoj, kie
   troviĝas ankoraŭ 278 000 rifuĝintoj.
</p> 
<h3>Tertremo</h3> 
<p>Krome nepras, ke Tokio preparu sin por eventuala granda tertremo. Fine de 2013 registara
   esplor-komisiono publikigis raporton, ke ene de 30 jaroj okazos sub la metropolo je
   probableco de 70 % tertremo de separanga magnitudo.
</p> 
<p>Tiam en Tokio kaj en la tri najbaraj gubernioj pereos ĉirkaŭ 23 000 homoj kaj estos
   detruitaj ĉirkaŭ 610 000 hejmoj. Tokio aparte vundeblas pro siaj dense starantaj lignaj
   domoj kaj tro koncentritaj institucioj politikaj, financaj kaj kulturaj. Jam delonge
   spertuloj insistas, ke tiuj institucioj estu dislokitaj.
</p> 
<p>Ankaŭ homoj kun sennacieca koro sentas malkomforton pro troe ripetataj raportoj pri
   rezultoj de samlandaj sportistoj kaj altrudo de naciismo. Tian malagrablon ili jam
   sentis en la raportoj de la vintraj Olimpikoj en Soĉi, Rusio, en televido kaj ĵurnaloj.
</p> 
