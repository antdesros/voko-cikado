      <h3>Politiko</h3>
      <h3 class="tem">Nov-Zelando</h3>
      <h2>Momento de vero en naciaj elektoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Malriĉeco, malegaleco; medio kaj poluado el laktobienoj; ekonomio, alta kosto de domoj
   kaj problemoj rilate asekuron post tertremoj en kaj apud la urbo Christchurch: jen
   temoj de la tutlandaj elektoj en Nov-Zelando en septembro 2014. Almenaŭ ĝis devojigis
   la elektokampanjon aferoj nekutimaj.
</p> 
<p>En aŭgusto Nicky Hager publikigis libron titolitan 
   <i>Dirty Politics</i>, do 
   <i>Fipolitikoj</i>. Ĝia subtitolo: „Kiel atakpolitiko venenas Nov-Zelandon”. Hager montris, kiel la reganta Nacia Partio celas esti reelektita surbaze de metodoj
   prunteprenitaj el la iama usona prezidanto Richard Nixon en la 1960-aj jaroj, tamen
   uzante Interreton.
</p> 
<p>Laŭ Hager, fiblogistoj pagataj de diversaj nacipartiaj politikistoj kaj komercistoj
   uzas dubindajn metodojn por misinformi la publikon. Ofte tradiciaj ĵurnalistoj prenas
   tiajn blogaĵojn kaj, ne kontrolinte la ŝajnfaktojn, ilin diskonigas.
</p> 
<h3>Akuzis</h3> 
<p>Hager asertis, ke ministro, Judith Collins [kolinz], likigis konfidencan informon
   pri ŝtatoficisto. Antaŭe oni akuzis Collins pri intereskonflikto: ŝi kunsidis kun
   ĉefoj de kompanio, kiu donacis milojn da dolaroj al la Nacia Partio.
</p> 
<p>Collins eksiĝis fine de aŭgusto pro skandalo pri siaj ligoj al la fiblogistoj. Poste
   la ĉefministro, John Key [ĝon ki], ricevis kopion de retmesaĝo, en kiu Collins ŝajne
   subfosis la ĉefon de la buroo pri gravaj fraŭdoj. Hager diris, ke ŝi truke eksiĝis
   por kaŝi, ke ŝi kulpas pri subfosado. Collins restis tamen kandidato en la parlamenta
   baloto.
</p> 
<h3>Spekteblis</h3> 
<p>Kvin tagojn antaŭ la baloto, la 15an de septembro, okazis kunveno de la nova Interreta
   Partio, titolita „Momento de vero”. La kunveno spekteblis Interrete. Parolis la usona ĵurnalisto Glenn Greenwald [grinvald],
   kiu diris, ke spionoj de la nov-zelanda registaro amase rikoltas informojn el telefonaj
   konversacioj, retpoŝto kaj tekst-mesaĝoj. La akuzon jam neis la ĉefministro, dirante
   tamen, ke tion oni iam konsideris.
</p> 
<p>La usona eksa spiono Edward Snowden [edŭard snoŭdn], parolante per Interreto el Rusio,
   diris, ke li mem legis mesaĝojn de nov-zelandanoj, kiam li laboris ĉe Nacia Sekureco-Agentejo
   (NSA) en Havajo. Li diris, ke NSA havas laborejon en Auckland kaj alian en la nordo
   de Nov-Zelando.
</p> 
<h3>Kritikis</h3> 
<p>Parolis ankaŭ Julian Asange, el Londono, kiu atentigis, ke gvatado estas en ordo nur
   kun la permeso de la koncerna popolo, kaj la kanada juristo Robert Amsterdam, kiu
   kritikis la proponitan Transpacifikan Partnerecan Akordon (TPPA), kiun Usono provas
   trudi al landoj ĉirkaŭ Pacifiko.
</p> 
<p>Tiu similas la akordon TTIP, proponatan inter Usono kaj Eŭropo (M<span class="mm">ONATO</span>, 2014/08-09, p. 19). Amsterdam avertis, ke grandaj kompanioj puŝas la usonan registaron
   por devigi aliajn ŝtatojn ŝanĝi siajn leĝojn favore al la kompanioj.
</p> 
<h3>Gajnis</h3> 
<p>Kiam estis anoncitaj la rezultoj de la balotoj, evidentiĝis, ke la Nacia Partio gajnis
   unu plian seĝon en la parlamento kaj povas regi kun subteno de tri etaj partioj. La
   efikoj de 
   <i>Fipolitiko</i> kaj „Momento de vero” ne estas konataj, sed ŝajnas, ke la elektantoj emis akcepti la argumentojn de tiuj,
   kiuj taksas gvatadon necesa. Pro tio la opoziciaj partioj estis malpli subtenataj
   ol dum la lastaj elektoj en 2011.
</p> 
<p>La nesukcesa Interreta Partio celis interesi nevoĉdonemulojn kaj kuraĝigi ilin partopreni
   la baloton. Fakte partoprenis 78 % de la voĉdonrajtuloj, pli ol en 2011, do eble tiurilate
   ĝi iom sukcesis. Tamen, laŭ Hager, unu el la celoj de atakpolitiko estas seniluziigi
   homojn, por ke ili ne voĉdonu.
</p> 
