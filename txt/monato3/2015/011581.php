      <h3>Politiko</h3>
      <h3 class="tem">NORVEGIO</h3>
      <h2>Floras veraj verduloj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Dum jaroj la norvega Verda Partio (<i>Miljøpartiet de Grønne</i>) ricevis nur malmultajn elcentojn de la voĉoj. Sed en la ĉi-septembra komunuma baloto
   ĝi finfine klare videblas sur la politika scenejo kun 4,2 %. En la ĉefurbo, Oslo,
   ĝi eĉ iĝis kun 8,1 % la trie plej granda partio.
</p> 
<p>Tio donas pli da potenco ol supozigas la nudaj ciferoj: En Oslo, kaj en 12 aliaj komunumoj,
   nek la socialisma nek la konservativa alianco ricevis absolutan plimulton en la komunuma
   estraro. Tial ambaŭ partioj devos intertrakti kun la verduloj por provi starigi urboregistaron.
</p> 
<h3>Kunfandi</h3> 
<p>Tutlande la Verda Partio, fondita en 1988, havos ekde la novjaro pli ol 200 reprezentantojn
   en la 428 komunumoj (kiujn oni cetere proponas kunfandi al ĉ. 100).
</p> 
<p>Krom kutimaj lokaj temoj – publika transporto, lernejoj, malsanulejoj – aparte aktuala dum la antaŭbalotaj semajnoj estis debato pri la kapablo de la komunumoj
   loĝigi la multajn rifuĝintojn el interalie Sirio.
</p> 
<h3>Esperi</h3> 
<p>La Verda Partio havas nun (ekde la parlamenta baloto en 2013) unu reprezentanton en
   la parlamento. Sed partioj, kiuj tutlande ricevis pli ol 4 % de la voĉoj, ricevas
   ekstrajn parlamentajn seĝojn kompense distribuotajn al partioj alie subreprezentotaj.
   Tiel la verduloj povas esperi ok parlamentanojn (el 169) en 2017.
</p> 
<p>Diference de Svedio kaj Germanio, la norvega Verda Partio ricevis en la unuaj jaroj
   malmultan subtenon pro konkurenco kun aliaj partioj, kiuj jam prezentis sin „verdaj”.
</p> 
<p>Sed en la hodiaŭa mondo ilia verdo ne sufiĉas. Nun floras „vera” verda partio ankaŭ en Norvegio.</p> 
