      <h2>De triopa monopolo al mikrosofta monopolo</h2>
      <!-- komenco de la enhavo -->

       

      <p>En la pratempo de la personaj komputiloj (tio estis antaŭ malpli ol 15 jaroj!) tri programoj superregis la oficejojn: WordPerfect por tekstprilaborado, Lotus por tabelkalkuloj kaj dBase por datumbazoj. Ili funkciis per la tiam plej ofte uzata mastruma sistemo, DOS, la antaŭulo de Vindozo. Ekzistis aliaj programoj, sed ilia parto de la merkato estis sensignifa. Komuna eco de tiuj programoj estis ilia malkapablo efike kunlabori. Ne eblis simple al letero farata per WordPerfect aldoni adreson el dBase-datumbazo kaj konteltiron faritan per Lotus. La uzantoj de tiuj programoj plejparte laboris individue kaj ne vere sentis la bezonon de facilaj interŝanĝoj. Ankaŭ la funkciado de tiuj tri ĉefaj programoj tute malsamis, kaj kono de unu programo nur malmulte helpis en lernado de alia programo. La monopolistoj kontentis pri la preskaŭa monopolo en sia specialaĵo kaj ne interesiĝis pri kunlaboro kun siaj kolegoj.</p> <p>Kontraŭe, konstruante Vindozon, Mikrosofto tre atentis pri la ebloj de interŝanĝo kaj pri unueca aspekto kaj uzado de la programoj. Estis logike, ke ĝi mem kreis oficejajn programojn, kiuj plene ekspluatis la eblojn de Vindozo. Verŝajne tute ne estis la intenco de Mikrosofto akiri la monopolon de oficeja programaro, kiun ĝi nun havas. Ĝia nova monopolo simple alvenis pro la kombino de neadaptiĝemaj ekzistantaj monopolistoj kaj usonstila, ofte agresema merkatado de Mikrosofto.</p> <p>WordPerfect estis plurfoje vendita, kaj la sinsekvaj posedantoj ŝajne ne sciis kion fari per ĝi. Ĝi ankoraŭ nun ofertas oficejan programaron, sed nur de la tekstprilabora ero restas malgranda grupo de fervoraj uzantoj. Lotus estis aĉetita de IBM. dBase ankoraŭ ekzistas (sub alia posedanto) kaj priservas uzantojn, kiuj ne povis aŭ volis transiri al Microsoft Access aŭ alia datumbaza programo.</p> <p>Nuntempe nur Mikrosofto povas liveri kompletan pakaĵon por oficeja uzado. Malgraŭ la alta prezo kaj la daŭre ŝanĝiĝantaj versioj, la entreprenoj daŭre aĉetadas ĝin. Ĝi ja estas tutmonde uzata kaj en multaj lernejoj instruata. La restantaj konkurantoj laŭeble adaptis sin al Mikrosofto. Ili atentas pri la kapablo legi kaj skribi la kutimajn mikrosoftajn dosierojn kaj ekuzis la samajn dosiernomajn sufiksojn (.doc por teksto, .xls por tabelkalkulilo, .ppt por prezentado), tiel faciligante eblan transiron.</p> <table> <tr> <td/><td>praprogramo</td><td>Microsoft Office</td><td>OpenOffice</td></tr> <tr> <td>Tekstprilaboro</td><td>WordPerfect</td><td>Word (.doc)</td><td>jes</td></tr> <tr> <td>Tabelkalkulilo</td><td>Lotus</td><td>Excel (.xls)</td><td>jes</td></tr> <tr> <td>Datumbazo</td><td>Dbase</td><td>Access (.mdb)</td><td>(MySQL)</td></tr> <tr> <td>Prezentado</td><td>-</td><td>PowerPoint (.ppt)</td><td>jes</td></tr> </table> <div style="text-align:right">Roland R<span class="mm">OTSAERT</span></div> 
