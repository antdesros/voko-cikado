      <h3>Ŝerco kaj satiro</h3>
      <h3 class="tem">KIPRO</h3>
      <h2>Terasa apartamento</h2>
      <!-- komenco de la enhavo -->

       

      <p>Estas landoj en Eŭropo kaj en la cetera mondo, en kiuj oni rajtas aldoni neniun modifon al sia domo sen permeso. Oni riskas pli-malpli salitan monpunon. Tute ne en Kipro, vera paradizo el tiu ĉi vidpunkto.</p> <p>Observu la belan dometon en la foto: ne, ne temas pri fotomuntaĵo, kaj – estu trankvilaj! – vi ne suferas je halucinoj. Iu kiprano, prave aŭ malprave ekkonsciiĝinte, ke li bezonas pli spacohavan loĝejon, iris iun belan tagon al vendejo de antaŭpretigitaj konstruaĵoj, akiris unu el ili kaj ĵetegis ĝin sur la ebenan tegmenton de sia domo. Bonvolu ne demandi min kiel: mi supozas, ke per helikoptero aŭ levitacio – malpli probable per katapulto, kiun oni ne ofte vidas sur la insulo. Eble iutage la mistero de la egiptaj piramidoj malkovriĝos, kaj tiam ni komprenos.</p> <p>Ĉiuokaze la viro solvis la problemon simple, rapide, efike kaj malmultekoste. Nun li vivigas sian familion treege pli komforte kaj finfine povis dungi servistinon el ... Ŝri-lanko <sup>1</sup> (en Kipro tiu ĉi estas rangosimbolo: ĉiu respektinda hejmo devas havi, almenaŭ sur la tegmento, tian servistinon). Ŝi neniam kaj nenial plendu, ĉar oni disponigis al ŝi terasan apartamenton kun spirhaltiga panoramo, kaj, se ŝi sendanke ne ŝatas tion, ŝi retroiru al sia lando!</p> <p>Tiaj dometoj ekaperas pli kaj pli ofte kiel fungoj en ĉiu kvartalo. La najbaroj? Ili ne priatentas kaj trankvile dormas. „Konstrua senrajteco” estas aliflanke esprimo, kiu troveblas tradukita en neniu vortaro de la kipra dialekto. Nenio cetere konstruiĝis de iu. Oni nur akiris kaj poste apogis domon sur alian domon. Tute aliaj estas la problemoj de Kipro, oni scias: juraj konfliktoj kun Turkio, senlaboreco, prezaltiĝo. La unusolaj perpleksuloj estas mia edzino kaj mi. Ankaŭ ni – kiel pluraj servistinoj el Ŝri-lanko – estas vere sendankuloj: matene ni ĝuas senpage tiel agrablan panoramon vidalvide de nia balkono, kaj ni rigardas ĝin abomenaĵo ... Antaŭ ol ekdormi, ĉiuvespere ni kune preĝas, por ke almenaŭ neniu el la kunposedantoj enkapigu al si malfermi diskotekon, noktan klubon aŭ lignometiejon – Dio savu kaj protektu nin almenaŭ de tio! – super nia dormoĉambro.</p> <h6 class="pied">1. Kun ŝ, laŭ la loka prononco</h6> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
