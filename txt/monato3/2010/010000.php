      <h2>Belgio eksportas siajn krimulojn</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Belgio tradicie estas ŝtato, kiu vivas de sia eksporto. Ke ĝi tamen eksportas ankaŭ siajn krimulojn, jen tute nova fenomeno ...</b></p> <p>En februaro la malliberejo de Tilburgo, suda Nederlando, estis oficiale transdonita al la belga ŝtato por „hejmigo” de belgaj krimuloj. Tio ĉi okazis en la ĉeesto de la belga ministro pri justico Stefaan de Clerck [stefán de klerk] kaj de nederlandaj ministro Ernst Hirsch Ballin [hirŝ balin] kaj ŝtatsekretario Nebahat Albayrak.</p> <h3>Ĉeloj</h3> <p>Dum la ceremonio oni ne transdonis malplenan konstruaĵon, ĉar jam en la semajnoj antaŭe 98 enkarcerigitoj estis transportitaj al Tilburgo. Laŭplane baldaŭ la ĉeloj devas esti plenaj. Ili kapablos „gastigi” 500 kulpigitojn.</p> <p>La iniciato celas rompi diablan cirklon, kiu jam de jaroj regas en la belga justico: la polico faras sian plejeblon por kapti krimulojn, kaj post la aresto kaj juĝado la koncernaj instancoj diras: „Bedaŭrinde mankas lokoj en la malliberejoj. Do vi estas libera. Bonvolu konduti bone.” En la praktiko ĉiuj, kiuj ricevis punon malpli grandan ol trijaran, povis iri hejmen pro manko de ĉeloj.</p> <h3>Protesto</h3> <p>La franclingva Ligo por la Homaj Rajtoj jam anoncis, ke ĝi apelacios ĉe la Kasacia Kortumo kaj provos nuligi la elŝtatigon de krimuloj. Laŭ la ligo la belgaj kaj nederlandaj krimuloj certe estos traktataj per malsamaj mezuroj, kio estas komplete malakceptebla. Krome la ligo timas, ke la enkarcerigitoj perdos la necesan kontakton kun sia familio, kiam ili estos eksterlande. La urbo Tilburgo troviĝas proksimume dek kilometrojn for de la belga landlimo. La eksperimento – se malsukcesos la ligo – daŭros tri jarojn.</p> <div style="text-align:right">Paŭl P<span class="mm">EERAERTS</span></div> 
