      <h3>Hobio</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Estiĝi la plej alta</h2>
      <!-- komenco de la enhavo -->

       

      <p>34-jara junulo Vaidotas Steponavičius el la litova banloko Druskininkai ne povis kontentiĝi pri sia alteco. Tial li havigis al si unikajn ŝuojn.</p> <p>Siajn novajn ŝuojn li ne povas meti en unu saketon. La ŝuparo ja pezas 7,72 kg. Li ĉiam portas ilin aparte. En tiuj ŝuoj estas malfacile marŝi. Ĉiu ŝtoneto aŭ iu malglataĵo tuj povas kaŭzi lian falon. Tial li kunportas ilin en du saketoj al ĉiu vizitata aranĝo, ekzemple koncerto, kaj serĉas tie trankvilan lokon por surmeti ilin. Sekve de tio li sukcesas iĝi la plej alta inter la homamaso.</p> <p>La junulo asertas, ke li ekdeziris havigi al si la ŝuojn, post kiam li tralegis artikolon pri litova korbopilkisto Arvydas Sabonis. La sportisto jam estis 220 cm alta, kaj la alteco de Steponavičius estis nur 187 cm. La junulo volis esti tiel alta kiel la populara sportisto. Li multe sportis, ĉiam maksimume etendiĝis. Tamen tiel li sukcesis kreski nur je unu centimetro. Tiam venis la ideo, ke li akiru almenaŭ ŝuojn, en kiuj li iĝus la plej alta homo en la lando.</p> <p>Komenciĝis klopodoj tiucele. Unue la fraŭlo aĉetis ŝuojn. Poste li serĉis ŝuiston, kiu povus altigi la plandumojn. Pene li trovis unu, kiu komence penis konsili la junulon rezigni pri tiu ideo, sed poste konsentis. Kondiĉe ke tiuj ŝuoj estos la unuaj kaj lastaj en lia ŝuista praktiko.</p> <p>La laboro daŭris ses monatojn. Por pretigi la ŝuojn, la majstro bezonis duonsitelon da gluo, kiun li uzis por alfiksi 1,5 kaj 2 cm dikajn plandumojn. La ŝuisto aldonis dek ses tavolojn. Ĉiutage Steponavičius venis al la ŝufarejo por praktiki marŝadon. Ju pli altaj la plandumoj estiĝis, des pli malviglis la paŝoj de la junulo.</p> <p>Kiam la ŝuoj fine estis pretaj, Vaidotas Steponavičius multe ĝojis pri tio, en la ŝuoj li ja „elkreskis” ĝis 221 cm, je unu centimetro superis la faman sportiston kaj iĝis la plej altkreska homo en Litovio.</p> <div style="text-align:right">Laimius S<span class="mm">TRAŽNICKAS</span></div>  
