      <h3>Ekonomio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Rafinejo jubileas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La petrola rafinejo <i>Fushun Numero 1</i> estis fondita en la jaro 1928. Ĝi estis la unua rafinejo de artefarita oleo kaj unu el la plej fruaj petrolaj rafinejoj en Ĉinio. De 1928 ĝis nun ĝi trapasis neordinarajn vojojn de evoluo: konstruado de la rafinejo en la 1930aj jaroj, restarigo en la 1950aj jaroj, reformado en la 1970aj jaroj, grandiĝo en la 1990aj jaroj kaj plua renovigo en la nuna jarcento. Ĝi grave kontribuis al la evoluo de la ŝtata ekonomio.</b></p> 
<p>La rafinejon konstruis la japana armeo invadinte Ĉinion. Tiam ĝi prilaboris skiston por produkti skistan oleon, nomatan ankaŭ artefarita oleo. La kvanto de prilaborita skisto atingis 4000 tunojn tage. La produktopaŝoj ĉefe konsistis el dispecigo de la skisto, kribrado, seka distilado, eltiro de gudro, distilado de kruda oleo kaj distilado de kruda vakso. La rafinejo estis grave damaĝita dum la regado de Kuomintango kaj povis esti restarigita nur post la liberigo de Fushun novembro de 1948.</p> 
<p>Ekde 1961, post kiam en 1960 oni trovis petrolkampon Daqing [daĉing], la rafinejo komencis prilabori ĝian petrolon kun alta enhavo de vakso (25 %). De tiu tempo ĝis la jaro 1987 ĝi traktis kaj skistan oleon kaj Daqing-petrolon. Pro kostaltiĝo kaj malnoviĝo de la ekipaĵoj oni en 1987 ĉesigis la produktadon de skista oleo. Dume estis sukcese konstruitaj la unua aparato de Ĉinio por vaksa splitiĝo por sinteza lubrika oleo en 1970 kaj kataliza splitiĝa aparato en 1987. Tiam la prilabora kapablo de petrolo estis 2 milionoj da tunoj jare. Ankoraŭ en 1987 la rafinejo ekprilaboris, krom Daqing-petrolon, petrolon el Shenyang [ŝenjang] kun pli alta enhavo de vakso (42 %). Oni konstruis novan produktan areon en 1997. La prilabora kapablo siatempe atingis 4 milionojn da tunoj jare. Estis instalitaj novaj aparatoj, ekzemple por ketono-toluena sen-vaksigilo kaj sen-oleigilo, hidrogenado sub alta premo, grajnigo de parafina vakso, ktp. Tiuj aparatoj estas la bazo por transformi la rafinaĵon el brulaĵo al parafina vakso kaj lubrika oleo. Nun ĝi jam estas la plej granda produkta kaj eksporta unuo de parafina vakso en Ĉinio.</p> <h3>Lastatempa evoluo</h3> 
<p><a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2293435"><img src="http://u1.ipernity.com/6/34/35/2293435.22a8a8a3.500.jpg" width="500" height="375" alt="La produktareo de Fushun 1" border="0"/></a></p><p><i>La produktareo de Fushun Numero 1 konstruita ekde 1997.</i></p><p>La petrola rafinejo <i>Fushun Numero 1</i> dum sia 80-jara ekzisto trapasis sortoŝanĝojn neordinarajn. Ĝia historio estas ankaŭ la historio de la nuna Fushun Petrolkemia Kompanio. La nuna projekto „kunproduktado de parafina vakso kaj lubrika oleo”, celas dum la dek-unua kvinjara plano (2006-2010) atingi produktajn kvantojn de parafina vakso kaj lubrika oleo de respektive 0,6 milionoj kaj 0,5 milionoj da tunoj jare. Ĝi iĝos grava je monda skalo, interalie pro la 50 000 tunoj da specialaj vaksaĵoj jare.</p> <div style="text-align:right">W<span class="mm">U</span> Guojiang</div>  
