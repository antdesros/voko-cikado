      <h3>Politiko</h3>
      <h3 class="tem">BRITIO</h3>
      <h2>Jes ni povas ... kaj ili povis.</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum pli ol tri jardekoj li restis ordinara, ekster parlamentaj rondoj apenaŭ konata
   membro de la brita ĉambro de deputitoj, ribelema (jam pli ol 500 fojojn li defiis
   siajn partiestrojn), politike maldekstra, ĉiam ŝajne senzorge vestita: nun tamen Jeremy
   Corbyn [ĝéremi korbin] trovas sin, en la aĝo de 66 jaroj, je sia granda surprizo (kaj
   tiu de siaj samlandanoj), ĉepinte de la brita laborista partio.
</p> 
<p>Pro skua malvenko dum naciaj elektoj en majo (M<span class="mm">ONATO</span>, 2015/07, p.6-7) tuj eksiĝis la tiama partiestro. Nepris elekti alian. Prezentis
   sin tri kandidatoj de la dekstro/centro de la teorie maldekstra, socialisma partio:
   nur lastminute estis nomumita kiel kandidato Corbyn, ĉar manpleno da deputitoj deziris,
   ke dum la debato pri nova partiestro estu aŭdata alternativa, maldekstra voĉo.
</p> 
<p>Ekde la unuaj debatoj, kiam la kvar kandidatoj sin televide aŭ kunvene prezentis,
   evidentis, ke Corbyn ne parolas la lingvon de la aliaj. Ili elgurdis la samajn melodiojn,
   kiuj tiom misagordiĝis en la oreloj de elektuntoj en majo.
</p> 
<h3>Bankistoj</h3> 
<p>Provante loki la partion en la centron de la politika spektro, ili ripetis esence
   la ideojn de la reganta, dekstra konservativa partio: bankistoj kaj entreprenistoj
   riĉigas la landon, do nepras ilin finance apogi; la rolo de la ŝtato estu minimumigata
   favore al privataj kompanioj; retenata estu nuklea defendo-(atako-)kapablo.
</p> 
<p>Pro tio, almenaŭ parte, la laborista partio suferis ĉe la urnoj en majo. Kial voĉdoni
   por pala centrisma/dekstreta imitaĵo, kiam eblas dekstra realaĵo?
</p> 
<p>Tamen Corbyn, kvazaŭ memorante, ke la t.n. laborista partio reprezentu unuavice laboristojn,
   substrekis la ligojn inter la partio kaj la jam de jardekoj flankenpuŝataj sindikatoj.
   Li promesis aserti la rolon de la ŝtato, ĉesigi la iom-post-ioman privatigon de la
   nacia sanservo kaj aliaj tradicie ŝtataj funkcioj, kaj reŝtatigi la privatigitajn
   fervojojn (kiuj, ironie, apartenas kelkokaze al ŝtate posedataj nebritaj fervojoj).
</p> 
<p>Samtempe li montris sin kontraŭulo de la registara ŝpar-politiko. Laŭ Corbyn, dum
   ekonomia krizo la ŝtato investu, ne tranĉu. Pro tio aplaŭdis la elekton de Corbyn
   la estro de la spirite parenca hispana partio 
   <i>Podemos</i>, Pablo Iglesias. „Nia rolo estas reprezenti la socian plimulton, la homamasojn pistitajn de registara
   modelo favora al la financaj elitoj kaj iliaj klientoj,” skribis Iglesias en brita gazeto.
</p> 
<h3>Platformo</h3> 
<p>Sampartianoj, inkluzive de iamaj estroj kaj ĉefministroj kiel Tony Blair kaj Gordon
   Brown, atakis la platformon de Corbyn. Ili taksis lin neelektebla kiel estontan ĉefministran
   kandidaton kaj kredis, ke li detruos la laboristan partion – almenaŭ la dekstretan, kiun ili konstruis.
</p> 
<p>Malkonsentis la popolo. Rekorde oni aliĝis al la laborista partio simple por povi
   partopreni en la partiestraj elektoj. Aparte junuloj, ĝenerale indiferentaj rilate
   politikon (ĉio samas) kaj politikistojn (ĉiuj mensogas), entuziasmiĝis. Ili vidis
   en Corbyn (malgraŭ lia aĝo, malgraŭ tio, ke li prezentas ideojn ofte de la 1980aj
   kaj pli fruaj jaroj) ion freŝan, honestan kaj fidindan. Krome – granda pluso – li nek sonas nek aspektas kiel profesia politikisto.
</p> 
<p>Videblis T-ĉemizoj kun devizo aludanta al la antaŭnomo de la kandidato: „Jez we can” („Jez [jes] ni povas”). Jen 
   <i>Podemos</i> denove: ankaŭ tio signifas „Ni povas”. Kaj fine la Corbyn-adeptoj ja povis. Venkis ilia heroo en la unua baloto kun preskaŭ
   60 % de la voĉoj.
</p> 
<h3>Gazetaro</h3> 
<p>Tuj, tamen, videblis fendoj en la laborista partio. La potenca dekstra alo, kun tradiciaj
   blair-ecaj granduloj, rifuzas kunlabori kun Corbyn. Oni flustras jam pri puĉo, por
   ke Corbyn ne estru la partion okaze de venontaj naciaj elektoj en 2020.
</p> 
<p>Ankaŭ la konservativa kaj dekstrega nacia gazetaro en Britio atakas lin. Jam legeblas,
   ke ĉefministro Corbyn lasos la landon sendefenda, senprestiĝa, se tiu ĉi „komunisto” rezignas, kiel li volas, pri la brita nuklea arsenalo.
</p> 
<p>Ĉio ĉi restas en la estonteco. Nun, tamen, ne nur maldekstremuloj sed ankaŭ tiuj,
   kiuj opinias, ke politikistoj ilin prifajfas, bonvenigas la alvenon de Corbyn. Fine,
   ili kredas, aŭdiĝos denove voĉo, kiu honeste, nehipokrite, parolos kun kaj por la
   marĝenigitaj etulo-tavoloj en Britio.
</p> 
