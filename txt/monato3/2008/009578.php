      <h3>Arto</h3>
      <h3 class="tem">KINO</h3>
      <h2>Silentiga kaj spirhaltiga spektaklo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĵus vidinte la filmon <i>Ben X</i>, oni iĝas silenta. Oni silentas, ĉar oni pripensas la temaron de la rakonto kaj oni silentas, ĉar oni ankoraŭ devas digesti ĝin: kio nun vere okazis? La kulmino de la filmo aperas nur en la lastaj minutoj, antaŭkonstruata ade kaj ade tra la tuta historio. La rakonto, bazita sur vera historio, temas pri la junulo Ben, kiu estas modere aŭtisma. Por povi eskapi el la realeco kaj precipe por povi toleri la daŭran ĉikanadon kaj turmentadon de siaj kunlernantoj en teknika lernejo, li kaŝas sin en sia persona mondo de komputilaj ludoj, en kiu li iĝas vera majstro. Li atingas eĉ nivelon 80! Sed kiam amikino lia, kiun li „renkontis” en Interreto, havas kontakton kun li en la reala mondo, Ben devas iom ŝanĝi sian vivon. Pli pri la enhavo ne eblas diri, ĉar tio ruinigus ĉion. Oni devas „submetiĝi” al la filmo.</p> 
<p><a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2430001"><img src="http://u1.ipernity.com/7/00/01/2430001.a3286682.500.jpg" width="500" height="400" alt="Ben X" border="0"/></a></p>
<p><i>Ben X</i> estas la unua longluda filmo de Nic Balthazar, iama filmkritikisto kaj multjara prezentisto kaj produktanto de VRT, <i>Vlaamse Radio en Televisieomroep</i>, la flandra radio kaj televido. La temo de <i>Ben X</i> iasence ne estas nova. La filmo estas bazita sur furorlibro de Balthazar <i>Niets is alles wat hij zei</i> (Li diris nenion krom nenio), kiu poste estis siavice uzata kiel bazo de la teatraĵo <i>Niets</i> (Nenio). Nenio nova do? Tamen!</p> <p>Por povi igi viva al la spektantoj la mondon de Ben, la produktanto ne hezitis miksi en la ĉefan rakonton scenojn el komputila ludo, ĉi-okaze la ludo <i>Archlord</i> de <i>Codemasters</i>. „Ni venigis ses ludantojn de la ludo al mia oficejo, petis ilin ludi laŭ miaj instrukcioj kaj uzi mian scenaron, kaj poste ni petis al la teknikistoj de <i>Codemasters</i> registri la ludadon sur disko”, klarigas Nic Balthazar. Ĉio tio estis miksita kun realaj scenoj, kaj liveras spirhaltigan spektaklon.</p> <p><i>Ben X</i> pli-malpli nun aperas en ĉiuj kinejoj. Tamen ĝi jam aĝas unu jaron. En la filmfestivalo de Montrealo de 2007 Nic Balthazar sukcesis arigi tri premiojn, nome la trofeon <i>Granda Premio de la Amerikoj</i>, la premion pri la plej populara filmo kaj la premion de la eŭkumena ĵurio. En la <i>Sedona International Film Festival</i> de 2008 <i>Ben X</i> ricevis la mencion „plej bona eksterlanda filmo”.</p> <p>Estas nekredeble, kiel ies unua filmo tiel proksimiĝas al perfekteco. Kaptu la okazon por spekti ĝin, se ĝi aperas en via regiono. La filmo ankaŭ jam aperis kiel aĉetebla viddisko.</p> <h6 class="pied">Vidu ankaŭ la retejon de la filmo: <a href="http://www.benx.be">www.benx.be</a></h6>  <div style="text-align:right">Paŭl P<span class="mm">EERAERTS</span></div> 
