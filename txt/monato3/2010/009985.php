      <h2>Fluge tra la flughaveno</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum la lastaj jaroj la Internacia Asocio pri Aertransporto (IATA) laboras por plirapidigi la administradon de flugvojaĝoj. Tion ebligas la fulma disvolviĝo de elektronikaj rimedoj, necesaj por kontraŭpezi la atendotempon antaŭ la sekurecokontrolo en la flughavenoj.</p> <p>Tial oni disvolvis t.n. Rapidvojaĝan Programon, kiu antaŭvidas ne nur tempoŝparon en kvar aspektoj de flughavenaj formalaĵoj, sed ankaŭ jaran monŝparon de 1,6 miliardoj da usonaj dolaroj favore al la flugindustrio. Samtempe la nova iniciato povos maldensigi la homamasojn, kiuj koncentriĝas en la akceptejaj haloj de multaj flughavenoj.</p> <h3>Elementoj</h3> <p>La kvar elementoj de la Rapidvojaĝa Programo jenas:</p> <p>1. Rapida transdono de bagaĝo. La pasaĝero uzas memservan budon por printi la bagaĝetikedojn kaj poste transdonas la bagaĝon al fluglinia akceptisto. Tio funkcias, eĉ kiam la pasaĝero jam enlistiĝis interrete aŭ poŝtelefone, kaj rapidigas la enlistigon de pasaĝeroj, kiuj vojaĝas kun bagaĝo.</p> <p>2. Skanado de vojaĝdokumentoj. Eblas ĉe la sama memserva budo skani pasportojn kaj aliajn identigilojn. Tiel la pasaĝeroj povas iri rekte al sekurecokontrolo, evitante sinprezenton antaŭ akceptejo. En 2009 jam en 75 lokoj aperis tiu ĉi eblo.</p> <h3>Pordo</h3> <p>3. Mem-enaviadiliĝo. Ne necesas viciĝi por eniri la aviadilon. Aŭtomata enaviadiliĝa pordo ebligas al pasaĝero skani sian enirpermesilon. Tio rajtigas eniri la aviadilon, laŭ la sama sistemo trovebla ĉe trajno- kaj metro-stacioj.</p> <p>4. Retrovo de bagaĝo. Pasaĝero informiĝas ĉe memserva budo pri mankanta bagaĝo kaj ne viciĝas ĉe bagaĝejo. Detalojn pri perditaj valizoj la pasaĝero entajpas ĉe la budo.</p> <p>En 2009 ĉi tiujn eblojn proponis nur kelkaj fluglinioj en kelkaj flughavenoj. Oni antaŭvidas ĝeneralan enkondukon ĝis 2012.</p> <div style="text-align:right">Ivo S<span class="mm">CHENKEL</span></div> 
