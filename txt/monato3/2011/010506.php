      <h3>Moderna vivo</h3>
      <h3 class="tem">KOMUNISMO</h3>
      <h2>Murdoj memorigitaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 9an de septembro oni memorigis per solenaĵo en Tirano, Albanio, la 65an datrevenon de la kontraŭkomunista ribelo de Postriba, verŝajne la unua tia en orienta Eŭropo post la dua mondmilito.</p> <p>Pro tio, ke la sekurecaj servoj anticipis la ribelon, kaj pro la interveno de la jugoslavia armeo (tiam Albanio kaj Jugoslavio estis intimaj amikoj), la ribelo kolapsis. Mortpafitaj estis dekoj da homoj, arestitaj kaj poste kondamnitaj al longdaŭra enkarcerigo 1200 aliaj.</p> <p>Kelkaj iamaj arestitoj kaj iliaj familianoj partoprenis la solenaĵon, kune kun politikistoj kaj aliaj eminentuloj.</p> <h3>Terorismo</h3> <p>Per videokonferenco parolis el Usono profesoro Sami Repishti, eksa ribelulo. Li substrekis la sangvenĝan instaliĝon de komunismo en Albanio, ties ŝtatan terorismon kaj la sekvan rezistadon flanke de patriotoj, kontraŭkomunistoj kaj intelektuloj.</p> <p>La politikaj ŝanĝoj en 1990-1991 estiĝis sensange kaj senvenĝe, sed, aldonis Repishti, la ŝtatpotencon ankoraŭ posedas eksaj komunistoj kaj ties posteuloj. Ĉi-lastaj pliriĉiĝas ekonomie kaj politike plifortiĝas ĉirkaŭ maldekstraj partioj.</p> <p>Estas ĝuste tiuj partioj, laŭ Repishti, kiuj jam de pluraj jaroj blokas en la albana parlamento reformojn necesajn por aligi la landon al EU.</p> <h3>Reĝimo</h3> <p>Eksaj persekutitoj kaj ties gefiloj ŝokiĝas – tiel Repishti – kiam ili renkontas en ŝtataj oficejoj, en la parlamento, siajn iamajn persekutintojn. Ekzemple la ĉefo de la socialistaj deputitoj estas eksa ministro pri internaj aferoj. Aliaj deputitoj aŭ eĉ ministroj estas gefiloj de eksaj altranguloj de la komunisma reĝimo.</p> <p>Cetere la eksaj ĉefkomunistoj (ekzemple la lasta diktatoro, Ramiz Alia, kaj la vidvino de la diktatoro dum 46 jaroj, Enver Hoxha, se ne mencii centojn da aliaj) ankoraŭ ne pardonpetis pro la krimoj de la tiama reĝimo.</p> <p>En Albanio, atentigis Repishti, oni ankoraŭ ne hontas pro la nomo „komunisto”, kontraste al Italio aŭ Germanio rilate „faŝisto” aŭ „nazio”.</p> <p>En la Nacia Historia Muzeo, kie okazis la solenaĵo, ekzistas ekde pluraj jaroj pavilono pri la genocido komunista. Ĝi estas ege vizitata ankaŭ de alilandaj turistoj.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div>  
