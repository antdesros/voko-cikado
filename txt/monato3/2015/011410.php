      <h2>Malmoliĝas la mola vojo</h2>
      <!-- komenco de la enhavo -->

       

        <p>La esprimo „fenomeno ŭimbeldona” estas konata almenaŭ en Japanio por indiki enlandajn sportistojn venkitajn de eksterlandaj.
   Ĝi rilatas al la teniso-konkurso ĉiujare okazanta en Wimbledon [ŭimbeldn], Britio,
   unu el la plej prestiĝaj tenisejoj en la mondo.
</p> 
<p>Tamen ĝis venkis ĉe Wimbledon en 2013 Andy Murray [andi m'ri] neniu brito tie sukcesis
   post la jaro 1936. Do la „fenomeno ŭimbeldona” rilatas al nacilandanoj nekapablaj venki en la propra patrujo aŭ, pli ĝenerale, al
   indiĝena sporto pli prospera ekster- ol en-lande. Du ekzemploj sin prezentas en Japanio:
   ĵudo kaj sumoo.
</p> 
<h3>Prezidanto</h3> 
<p>Ĵudo estas internacia luktosporto kaj olimpika ludo. Ĝi popularas en Eŭropo, Rusio
   kaj Brazilo. En Francio estas registritaj ĉirkaŭ duonmiliono da adeptoj, duoble pli
   ol en Japanio. Ankaŭ la rusa prezidanto, Vladimir Putin, estas ĵudo-amanto.
</p> 
<p>Ekde 2012 batalartoj estas devigaj studobjektoj en Japanio. Ĉiuj lernantoj inter la
   aĝoj de 12 kaj 15 jaroj praktikas almenaŭ unu jaron aŭ ĵudon, kendon (japana skermado)
   aŭ sumoon. Pro relative malalta kosto ĉirkaŭ 70 % elektas ĵudon, kiu tamen ne popularas.
</p> 
<h3>Trejnado</h3> 
<p>Evidentas du kialoj. Origine ĵudo signifas „mola vojo”: tiel eĉ malgranda ludanto povas venki pli grandan rivalon. Tamen nun regas forto,
   anstataŭ lerto, en ĵudo, kiu farigis ja malmola vojo. Inter 1983 kaj 2010 mortis 114
   ĵudo-ludantoj en lernejoj aŭ kluboj: mankas taŭgaj gvidantoj por certigi sekurecon
   dum trejnado. Por eviti atakojn luktantoj nur ŝanceliĝas kiel dancantaj ursoj. Matĉoj
   interesas nek ludantojn nek spektantojn.
</p> 
<p>Dua kialo rilatas al korupto en la japana ĵuda mondo. Ofte okazis skandaloj rilate
   al perforto, seks-atakoj, misuzo de subvencioj ktp. Apenaŭ komenciĝis reformoj en
   la mondo de japana ĵudo. Pro tio la „fenomeno ŭimbeldona”: ĵudo pli furoras ekster ol en Japanio.
</p> 
<h3>Luktejo</h3> 
<p>Same sumoo, tradicia luktoludo kaj nacia sporto. Simplas ĝia regulo: perdas tiu, kiu
   estas eligita el rondforma luktejo, aŭ kies korpoparto, escepte de la plando, tuŝas
   la grundon.
</p> 
<p>Ekzistas ĉirkaŭ 700 profesiaj sumoistoj, de trejnatoj ĝis tri grandaj ĉampionoj, el
   kiuj ĉirkaŭ 40 estas nejapanoj. Sed el ĉirkaŭ 42 altrangaj sumoistoj 16 estas alilandanoj,
   inkluzive de 10 mongoloj. Ŝajnas, ke mongoloj konkeras la japanan sumoon.
</p> 
<p>En Mongolio troviĝas luktoludo simila al sumoo, kaj mongolaj luktantoj, precipe nomad-devenaj,
   estas tre fortaj. Do la tri plej altrangaj grandaj ĉampionoj estas mongoloj. Tiel,
   rilate sumoon, eblas paroli malpli pri ŭimbeldoniĝo de la sporto sed pri mongoliĝo.
</p> 
