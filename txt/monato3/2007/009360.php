      <h3>Politiko</h3>
      <h3 class="tem">PORTUGALIO</h3>
      <h2>La saĝeco de Sócrates</h2>
      <!-- komenco de la enhavo -->

       

       <p>Maltrankviliĝis portugaloj pro polica agado kontraŭ manifestaciintoj kaj sindikatanoj. Komence de oktobro policanoj vizitis manifestaciintojn en la sud-portugala urbeto Montemor-o-Novo [montemór-u-nóvu] kaj, samtage, membrojn de instruista sindikato en la mezlanda urbo Covilhã [kuvilján].</p> <p>La agado pensigis ne nur pri la nuna stato de demokratio en Portugalio, sed ankaŭ pri la situacio en 1974, en la tempo de faŝismo. Sed kontraste al la spirito de la t.n. aprilistoj (t.e. la revoluciuloj de aprilo 1974), ordonemaj ideoj rapide disvastiĝas sub la registaro de José Sócrates, ĉefministro ekde 2005.</p> <p>Kontraŭ Sócrates protestis la 9an de oktobro pli ol cent homoj, kiuj koncentriĝis ĉe lernejo en Covilhã. Tial la polico vizitis la sidejon de la tiea instruista sindikato kaj forprenis dokumentojn kaj flugfoliojn. Politikistoj de ĉiuj partioj petis klarigon, kaj la registaro diris, ke la vizito rutinas kaj ne plu okazos. Tamen la popolo miris, ke polica vizito al sindikato fariĝas „rutina” en demokratia ŝtato.</p> <h3>Ekspluatado</h3> <p>Persekutataj estas instruistoj kaj ilia sindikato, ĉar ili apartenas al unu el la politike plej konsciaj sociaj klasoj. Konsciaj, ĉar kelkaj instruistoj, iufoje grave malsanaj, devas labori ĝis la morto: ne permesas ilin emeriti la socia asekuro. Temas ne nur pri ekspluatado de la instruistoj, sed ankaŭ pri vera abomeno. Tiun abomenon la ministrino pri klerigado, Maria de Lurdes Rodrigues [rudriges], apenaŭ kapablis kaŝi antaŭ televidaj kameraoj, kiam ŝi enposteniĝis.</p> <p>Antaŭ nelonge Sócrates estis prifajfita en la insulo Madejro, samkiel en la urboj Évora [évura] kaj Coimbra [kuimbra]. Malkontento ĝeneralas, ĉefe pro politikoj pri sano, klerigo kaj maldungo. La estraro akuzas la Komunistan Partion, dirante, ke kulpas komunistoj pro la manifestacioj. Antaŭ la revolucio la Komunista Partio estis akuzita pro ĉio malbona en Portugalio. Eble ripetas sin la historio.</p> <div style="text-align:right">João José S<span 