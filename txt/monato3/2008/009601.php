      <h3>Turismo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Evoluanta filmejo Hengdian</h2>
      <!-- komenco de la enhavo -->

       

       <p>Danke al la politiko de reformado kaj pordo-malfermo iama malgranda vilaĝo Hengdian (provinco Zhejiang, orienta Ĉinio) grave evoluis. Unue ĝi prosperis pro sukceso de silka entrepreno, lige kun disponebla magneta materialo kaj kiel loko de elektronika kaj medikamenta industrioj, kaj nun ankaŭ kiel evoluanta filmejo kaj turisma loko.</p> <p>Lastatempe Hengdian altiris la atenton en Ĉinio lige kun la intenco konstruigi modelon de palaco Yuanming-Yuan – malnova Somera palaco en Pekino, detruita de anglaj kaj francaj trupoj en 1860 – cele al prezento de ĝia pompo.</p> <p>En 1995 fama ĉina reĝisoro Xie Jin ekplanis produkti filmon „La Opia Milito”. Loĝantoj de Hengdian donis grandan helpon al la reĝisoro, ĉar li havis grandajn malfacilaĵojn en la preparo. Rezulte de la laboroj de hengdiananoj dum malpli ol kvin monatoj ĉi tie aperis modeloj de malnovaj Kantono kaj Honkongo. La filmo havis sukceson kaj iĝis la plej grava en la kariero de reĝisoro Xie.</p> <p>Kun granda ĝojo Xie Jin sciigis siajn amikojn: „Estis saĝa decido filmi en Hengdian.” Post li ĉi tie laboris famaj reĝisoroj kaj geaktoroj ĉinaj kaj alilandaj: Zhang Yimou, Chen Kaige, John McTiernan, Kim Heeseon. Tie oni produktis ankaŭ aliajn filmojn: „Heroo” de Zhang Yimou, „Promeso” de Chen Kaige, kaj aliaj.</p> <h3>Vidinda loko</h3> <p>Se vi vizitus Hengdian, vi eble trovus tie ion familiaran. Kompreneble ĝin vi jam vidis en iu filmo, aŭ vi konas ĝin de historiaj libroj. En tiu montara loko en Orienta Ĉinio vi povas vidi „palacojn” de dinastioj Qin- kaj Ming-Qing, iri laŭ strato de dinastio Song, sperti ankaŭ la Longan marŝadon de Ruĝa Armeo en 1934-1935. Do, se oni intencas prezenti aferojn historiajn, certe troveblas ĉi tie bonaj lokoj por tio.</p> <p>En Hengdian nun estas sep grandaj pejzaĝaj zonoj, en kiuj troviĝas 13 filmstudioj. La unua plej granda moderna filmstudio havas areon de 1944 m<sup>2</sup> kun alteco de 23 m, kaj la dua areon de 1080 m<sup>2</sup>. En 2003 la ĉina registaro decidis konstruigi ŝtatan filmindustrian eksperimentan parkon en Hengdian. Tiu speciala parko estas tre grava por la filmindustrio, ĉar ĝi estas la sola filmejo en Ĉinio kaj la plej granda en Azio. Ĉiam filmproduktaj grupoj laboras ĉi tie pro la belaj pejzaĝoj, modernaj servoj, bonaj kondiĉoj kaj aliaj.</p> <p>Hengdian estas unu el la turismaj lokoj, kiuj akiris turisman ateston AAAA, kiu estas la plej alta turisma kategorio en Ĉinio. Ĉi tie inter la naturaj belaĵoj menciindas ankaŭ Ping Yan, la unua groto en la suda parto de rivero Yangzi.</p> <div style="text-align:right">C<span class="mm">HEN</span> Yinhua</div>      
