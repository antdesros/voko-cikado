      <h3>Politiko</h3>
      <h3 class="tem">KATALUNIO</h3>
      <h2>Ponto por dialogo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Balotis 41 % de la voĉdonrajta loĝantaro de la kataluna urbeto Arenys de Munt [arenŝ' de mun] responde al la demando: „Ĉu vi konsentas, ke Katalunio fariĝu plenrajta ŝtato sendependa, demokratia kaj socia ene de Eŭropa Unio?” El tiuj, kiuj voĉdonis, respondis 96,18 % favore al sendependeco de Katalunio disde Hispanio.</p> <p>Entute estis kunvokitaj la 13an de septembro 6515 civitanoj por voĉdoni. Partoprenis la baloton pli da homoj ol en la referendumo pri la eŭropa konstitucio (40,43 %) kaj en la lastaj eŭropaj elektoj (35,14 %).</p> <p>Al la balotejo iris pli da urbanoj, ol esperis la organizantoj. Antaŭ la baloto oni ricevis leterojn de la ekstremdekstra frankisma partio <i>Falange</i> [falanĥe] kaj de la socialisma partio nuntempe reganta en Hispanio. Ambaŭ leteroj invitis la loĝantaron ne partopreni. Cetere okazis samtage aliaj balotoj, sed eblis respondi al la demando pri sendependiĝo nur en unusola balotejo.</p> <h3>Kaseroloj</h3> <p>Kontraŭ la baloto protestis en la centra placo de Arenys de Munt 60 anoj de la frankisma partio. La falangistoj montris hispanajn flagojn kaj frankismajn simbolojn. Ili faŝisme salutis, asertante, ke Katalunio estas hispana. Policanoj izolis la faŝismajn protestantojn disde katalunaj sendependistoj. De apudaj balkonoj, najbaroj batis kaserolojn por brue protesti kontraŭ la ĉeesto de faŝistoj.</p> <p>Oni balotis en la sidejo de kultura asocio apud la urbodomo pro malpermeso de ŝtata tribunalo kunvoki la referendumon. Tial la magistrato de la urbeto transdonis la iniciaton al la asocio. Laŭ Carles Móra [karles mora], urbestro de Arenys de Munt, la celo estis obei la juĝan decidon, kiu malpermesis ĉian kunlaboron de la magistrato. La urbodoma salono, kiu devus gastigi la urnojn, akceptis sangodonacadon.</p> <h3>Amaskomunikiloj</h3> <p>Malgraŭ tio, ke la baloto malhavas leĝan valoron, la fakto, ke urbeto tamen organizis baloton kontraŭ la volo de la ŝtato, kaj kun faŝisma protesto, estis vaste priraportata en internaciaj amaskomunikiloj. La urbestro difinis la tagon kiel feston de demokratio, dirante: „Ni kontraŭas neniun, sed klopodas starigi pontojn por dialogo.”</p> <p>Laŭ la magistrato, Arenys de Munt estas la unua kataluna municipo, kiu libere kaj demokratie voĉdonis por sendependa Katalunio. Nun pli ol 60 aliaj municipoj planas same referendumi. Ili kunlaboras kaj kreis retejon: <a href="http://www.referendumindependencia.cat">www.referendumindependencia.cat</a>.</p> <p>La ĵurnalo <i>La Vanguardia</i> raportis, ke lastatempe duobliĝis la apogantoj de sendependiĝo de Katalunio. Same, laŭ la ĵurnalo, kreskas sendependismo ekzemple en Kebekio.</p> <div style="text-align:right">Ferriol Macip i Bonet</div> 
