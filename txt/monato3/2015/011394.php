      <h3>Fokuse</h3>
      <h3 class="tem">SVISLANDO</h3>
      <h2>Kultura mozaiko</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>Jen la unua el tri artikoloj, kiu fokusiĝas pri Svislando.</b>
</p> 
<h3>La unua pakto</h3> 
<p>En 1291 ekzistis nenia intenco fondi ŝtaton, sed nur promeso okaze de atako pri reciproka
   helpo inter tri arbaraj regionoj ĉirkaŭ la alpa pasejo Gotardo. Poste tiu grupo kreskis
   al ok, 13, 22, kaj finfine 23 ŝtatetoj, aŭ kantonoj.
</p> 
<p>Do Svislando kreskis iom en la sama maniero kiel Eŭropa Unio en la lastaj 60 jaroj.
   La lasta modifo de la ŝtataj limoj okazis en 1815. La lasta enlanda milito okazis
   en 1848.
</p> 
<h3>Limo</h3> 
<p>Tra Eŭropo, de Belgio ĝis Triesto, kuras limo inter latindevenaj kaj ĝermandevenaj
   kulturoj. Ambaŭflanke de tiu „kurteno” homoj alimaniere reagas al politikaj kaj sociaj problemoj.
</p> 
<p>La diferencoj estas ne nur lingvaj sed ankaŭ kulturaj – kaj profundaj. Svislando kuŝas sur tiu limo kaj devas ĉiutage maĉi, gluti kaj digesti
   tiujn kulturajn diferencojn. Okazas, ke ĉe voĉdonoj la diversaj landopartoj alimaniere
   decidas.
</p> 
<h3>Regionoj</h3> 
<p>Norde de Svislando de Ĝenevo ĝis la zurika flughaveno etendiĝas montaro kun la nomo
   Ĵuraso. Ĝi konsistas el paralelaj harmoniaj faldoj ornamitaj de multnombraj abioj.
</p> 
<p>De la mezo de la lando ĝis la sudo laŭ horizontala linio ŝprucas la Alpoj, pintaj
   rokaj montoj, kiuj kovras 60 % de la surfaco de la lando. La plej alta svisa punkto
   situas en la Alpoj apud Zermatt kaj atingas altecon de pli ol 4600 m.
</p> 
<p>Inter tiuj du bariloj etendiĝas la Meza Ebenaĵo, kiu fakte ne multe ebenas. Tie situas
   la grandaj urboj Lausanne [lozán], Berno, Zuriko. Svislando devas akcepti gigantan
   trafikon de nordo al sudo kaj inverse, de Germanio al Italio, ĉu aŭte aŭ kamione,
   ĉu vagonare. Sub la pasejo Gotardo estas finkonstruata nova fervoja tunelo, kiu ekfunkcios
   en 2016. Ĝi estas la plej longa en la mondo kun 58 km.
</p> 
<h3>Politiko</h3> 
<p>Urboj kaj vilaĝoj formas bazan nivelon. Sekvas kantona nivelo: kantonoj estas ŝtatetoj.
   La konfederacia nivelo kun la federa urbo Berno iom similas al Bruselo por Eŭropa
   Unio.
</p> 
<p>Tiu ĉi strukturo datiĝas de 1848. Estas tendencoj doni pli da povo al Berno, dum aliaj
   tendencoj volas konservi povon ĉe la kantonoj por protekti la ecojn de ĉiuj regionoj.
   Edukado, ekzemple, restas grandparte en la manoj de la kantonoj. Tio kaŭzas problemojn.
   Kiam familio translokiĝas al alia kantono, la infanoj trovas, ke la studprogramo ne
   samas.
</p> 
<p>Svisoj kutimas voĉdoni kvar ĝis kvin fojojn jare. En kelkaj kantonoj ne-svisoj rajtas
   voĉdoni. En kelkaj kantonoj ne-svisoj rajtas esti alelektitaj al urbaj parlamentoj.
</p> 
<h3>Kriterioj</h3> 
<p>Svislando estas mozaiko laŭ tri malsamaj kriterioj. La plej konata kriterio estas
   tiu pri lingvoj. Ekzistas tri lingvaj regionoj: 70 % de la loĝantoj, en Alemanio,
   parolas unu el la germandevenaj dialektoj kaj skribe uzas la germanan; 20 %, en Romandio,
   parolas la francan; 10 %, en Tiĉino, parolas la italan.
</p> 
<p>En la plej granda kantono svisa, nomata Grizono, aŭdiĝas diversaj idiomoj nomataj
   „romanĉa lingvo”. Estas malfacile savi tiujn minoritatajn lingvojn kaj do la konfederacio kaj la kantono
   Grizono dungis lingviston kun la tasko krei unuecigitan planlingvon, kies nomo estas
   „Rumantsch Grischun”.
</p> 
<p>Tiu novbakita lingvo estas nur skriba lingvo. Plie, pro la abundo de ne-svisaj loĝantoj
   (23%), aŭdiĝas en Svislando dekoj da aliaj lingvoj alvenintaj pro enmigrado.
</p> 
<p>Dua kriterio estas religio. Bazelo, Berno, Zuriko, Ĝenevo estas protestantaj kantonoj.
   Sankt Gallen, Friburgo, Soloturno estas katolikaj.
</p> 
<p>Tria kriterio estas la eco de ĉiu kantono: ĉu urba, ĉu kamparana. Oni do povas kompreni,
   ke Svislando estas aro da interplektitaj regionoj. Ekzemple franclingva kantono povas
   senti ligon al alemana kantono, se ambaŭ havas la saman religion.
</p> 
<h3>Industrio</h3> 
<p>Gravas maŝinindustria produktaĵo, mikrotekniko, kemiaĵoj kaj kuraciloj, horloĝoj,
   nutraĵoj kaj ĉokolado. Mankas nafto kaj diamantoj: oni do instruas al infanoj, ke
   la ĉefa produkto estas bone organizitaj kaj funkciantaj cerboj. Renomaj universitatoj
   kaj politeknikaj altlernejoj ekzistas en Lausanne kaj Zuriko.
</p> 
<p>Ne la homoj estas plurlingvaj, sed la lando estas plurlingva. Same pri financoj: ne
   la homoj estas riĉaj, sed la lando estas riĉa, pli precize la svisaj bankoj. Grava
   faktoro en la bonfarto de la svisa ekonomio estas, ke Svislando travivis la 20an jarcenton
   sen militoj. Do en 1945 ĝi povis tuj eklabori kaj produkti, kiam aliaj eŭropaj landoj
   devis unue rekonstrui.
</p> 
<p>Helpas ankaŭ trankvila socia etoso kun interparolado inter dungantoj kaj dungatoj
   pere de sindikatoj. Pro tio maloftas strikoj kaj strataj manifestacioj. Regas bona
   laboretoso: svisoj laboras semajne 42 horojn. Pliaj t.n. translimlaboristoj venas
   el la najbaraj Francio, Germanio, Aŭstrio kaj Italio.
</p> 
<h3>Unuiĝintaj Nacioj</h3> 
<p>En Ĝenevo, en domaro konstruita por la Ligo de Nacioj inter 1929 kaj 1933, situas
   nun UN kun duoble pli da kunsidoj ol en Novjorko. Filioj de UN en Ĝenevo: homrajta
   konsilio, rifuĝintoj, sano, laboro, meteologio, telekomunikado, intelekta proprieto
   kaj multaj neregistaraj organizaĵoj.
</p> 
<h3>Kulturo</h3> 
<p>Reprezentataj estas diversaj kulturoj: ekzemple klasika dancado (Premio de Lausanne),
   kaj muziko (Internacia Konkurso pri Muzikludado de Ĝenevo). Notindas muzeoj kiaj fondaĵo
   Beyeler en Riehen, Olimpia Muzeo en Lausanne, kaj la Centro Paul Klee en Berno.
</p> 
<p>Intere la svisaj eminentuloj menciindas Leonhard Euler (1707-1783), sciencisto kaj
   matematikisto; Johann Heinrich Pestalozzi (1746-1827), pedagogo kaj verkisto; Henry
   Dunant (1828-1910), la fondinto de Ruĝa Kruco; Alberto Giacometti (1901-1966), artisto,
   skulptisto; Le Corbusier (1887-1965), artisto, arkitekto; Hector Hodler (1887-1920),
   la fondinto de UEA; Edmond Privat (1889-1962), sekretario de Gandhi, esperantista
   aktivulo; Jean Ziegler (1934), sociologo, politikisto, verkisto, filozofo; Roger Federer
   (1981), tenisoludanto.
</p> 
