      <h3>Eseoj</h3>
      <h3 class="tem">SEPARISMO</h3>
      <h2>Grava elemento de monda nestabileco</h2>
      <!-- komenco de la enhavo -->

       

        <p>Antaŭ 100 jaroj separistoj batalantaj por la sendependeco de Bosnio kaj Serbio el
   la aŭstra-hungara imperio kaŭzis la morton de Ĉefduko Frederiko Ferdinando. Tiel ili
   ĵetis Eŭropon kaj la mondon en periodon de nestabileco, krizoj kaj militoj neniam
   antaŭe viditaj en la historio de la homaro.
</p> 
<p>Nun, dum la centjariĝo de la komenco de la unua mondomilito, nova movado serĉanta
   aŭtonomion igas Eŭropon kaj la mondon rememori, kiel separismo povas esti faktoro
   por malstabiligi aferojn geopolitikajn kaj ekonomiajn.
</p> 
<p>La decido de Krimeo apartiĝi de Ukrainio kaj aliĝi al Rusio obeas la forton kaj la
   distingeblajn motivojn de klasikaj separismaj movadoj. Ĉi-okaze, tamen, la loĝantoj
   de la duoninsulo ne volas sendependecon. Sed la rezultoj povas esti tiel katastrofaj
   kiel iu ajn alia tradicia separismo.
</p> 
<p>La deziro de la duoninsulanoj elekti propravole la sorton de la regiono, kie ili vivas,
   influos ne nur la mapon de Orienta Eŭropo. Ilia sukceso revigligos aliajn okcidentajn
   grupojn kun la intenco redesegni la mondan mapon.
</p> 
<h3>Tamburas</h3> 
<p>En Eŭropo tri regnoj nuntempe frontas la kreskantan premon de siaj loĝantoj favore
   al sendependiĝo. Skotlando decidos en septembro, ĉu resti membro de la Unuiĝinta Reĝolando
   de Granda Britio kaj Nord-Irlando; Katalunio pliintensigas la premon por apartiĝi
   de Hispanio; kaj eĉ la romantika Venecio tamburas apartiĝon disde Italio.
</p> 
<p>Aliflanke de Atlantiko, la franclingva kanada provinco Kebekio provas plian fojon
   fariĝi la plej nova nord-amerika regno. Pli ol antaŭe, separismo montriĝas afero ne
   nur afrika aŭ azia.
</p> 
<p>La longdaŭra intereso de Eŭropa Unio estas konservi la postmilitan ordon, kie landlimoj
   estas bone difinitaj, kaj kie teritoriaj postuloj ne estu perforte decidataj.
</p> 
<p>Krimeo ne voĉdonis por fariĝi pli libera, kiel en klasikaj separismaj movadoj, sed
   anstataŭe por esti argumenteble malpli libera sub la regado de Rusio. Rusoj, pafinte
   eĉ ne unu solan kuglon, tiel aneksis al si regionon, kie situas historia haveno de
   ilia mararmeo.
</p> 
<h3>Postulas</h3> 
<p>Ene de la Rusa Federacio, en la Norda Kaŭkazio, ribelaj movadoj en Ĉeĉenio kaj Dagestano
   postulas sian propran sendependecon. Separismo supozigas la timon perdi la potencon,
   naturajn mediojn, impostojn kaj loĝantaron. Ne estas regno en la mondo, kiu akceptus
   perdi teritorion.
</p> 
<p>Antaŭ Krimeo, la lasta okazo de separismo en la eŭropa kontinento estis turmenta.
   Kun preskaŭ du milionoj da loĝantoj, plejparte de albana etna deveno, Kosovo ricevis
   defendan apogon de Nord-Atlantika Traktakt-Organizaĵo (NATO) en milito por apartiĝi
   de Serbio, en kiu pereis pli ol dek mil homoj.
</p> 
<p>En 2008 Kosovo deklaris sin sendependa, sed Rusio kaj Ĉinio ne agnoskis ĝian novan
   statuson. Okcidentaj landoj pravigis sian akcepton de la sendependiĝo per la fakto,
   ke la albandevena loĝantaro en Serbio vivis sub premo de la serbaj aŭtoritatoj, suferis
   gravajn atencojn kontraŭ homaj rajtoj, kaj estis konstantaj viktimoj de etna purigado.
</p> 
<p>Kvankam ekonomiaj argumentoj havas centran gravecon en la debato, tamen kerne de separismo
   rolas kulturaj, etnaj kaj historiaj kaŭzoj krom la identeca sento.
</p> 
<h3>Komenciĝas</h3> 
<p>En Venecio, ĉefurbo de la norda regiono Venetio en Italio, komenciĝas iniciatoj cele
   al sendependiĝo kaj restarigo de la Tre Serena Respubliko de Venecio. Ties suvereneco
   finiĝis nur post la konkero fare de la trupoj de la franca imperiestro Napoleono fine
   de la 18a jarcento.
</p> 
<p>Loĝantoj de Venetio argumentas, ke la centra registaro en Romo kolektas ĉiujare de
   la preskaŭ kvin milionoj da loĝantoj de la regiono impostojn en sumo de 70 miliardoj
   da eŭroj. Ili ricevas interŝanĝe publikajn servojn egalvalorajn al 50 miliardoj da
   eŭroj.
</p> 
<p>Ju pli pravigebla ŝajnas la rajto de la plimulto decidi pri politikaj aliancoj laŭ
   sia sento de identeco, kiel okazis en Krimeo, des pli la prerogativo de memdetermino
   estas limigita en internacia juro. Interkonsentite estas, ke ĉio okazu ene de demokratia
   decidpovo, klare travidebla kaj akceptata de la centra registaro.
</p> 
<p>Aktuala ekzemplo okazas en la Unuiĝinta Reĝlando de Britio. Laŭ akordo farita antaŭ
   du jaroj, post longa traktado inter la brita kaj skota registaroj, en septembro okazos
   referendumo en Skotlando. Responde al unu sola demando la skotoj devos decidi jese
   aŭ nee pri la apartigo de sia lando disde unio, kiu daŭras jam 300 jarojn.
</p> 
<h3>Voĉdonos</h3> 
<p>La rezulto de enketo farita en marto 2014 montris, ke 57 % de la skotoj voĉdonos nee
   al la apartigo, certe influate de la ekonomiaj sekvoj de la rompo kun Londono.
</p> 
<p>Oficiale britaj regantoj diras, ke Skotlando estas pli forta ene de la unio, kaj ke
   la Unuiĝinta Reĝlando estas pli forta kun Skotlando. La konservativa ĉefministro de
   Britio avertis, ke sendependa Skotlando ne plu povos uzi la britan pundon kaj certe
   frontos malfacilaĵojn por eniri Eŭropan Union.
</p> 
<p>Hispanio havas specialan intereson bari la aliĝon de novaj sendependaj ŝtatoj al la
   eŭropunia bloko. De sudo ĝis nordo diversaj separismaj movadoj aktivas en ĝia teritorio.
   La registaro en Madrido baris ĉiujn provojn de la aŭtonoma regiono Katalunio lanĉi
   sian propran referendumon.
</p> 
<p>Pro la pligraviĝo de la ekonomia krizo en Hispanio, kie la meza nivelo de senlaboreco
   estas duoble pli alta ol tiu de la eŭrozono, sendependiĝo estas pli kaj pli apogata.
</p> 
<p>Katalunio estas tre industriigita regiono. Oni kalkulas, ke 43 cendoj de ĉiu eŭro
   pagita al la centra registaro ne revenas al la regiono. En novembro, katalunoj voĉdonos
   en referendumo eĉ sen la konsento de Madrido, kiu konsideras la agon nekonstitucia.
</p> 
<h3>Furoras</h3> 
<p>Ankaŭ ekster Eŭropo furoras separismo. En Kanado, la gvidantoj de la politika 
   <i>Parti Québécois</i> volas igi la provincon Kebekio nova lando. Kun ok milionoj da loĝantoj, plejparte
   francparolantaj, Kebekio estas la dua plej loĝata provinco de la lando.
</p> 
<p>Laŭ la separistoj, sendependa Kebekio garantios la servojn de la nuna federacia registaro.
   Tamen ĝi reinvestos la kolektitajn impostojn laŭ sia bontrovo, modernigante sian politikan
   sistemon, stimulante produktadon de elektra energio, kaj tiel sin liberigante de la
   importado de nafto.
</p> 
<p>En 1980 kaj 1995 okazis referendumoj. Ambaŭ rifuzis la proponon de la separistoj,
   sed la lasta prezentis diferencon de iomete pli ol 1 % favore al la kontraŭuloj. Tiu
   ĉi statistiko ankoraŭ nutras la esperon de favora rezulto estonteca.
</p> 
<p>Ankoraŭ fruas por prognozi, ĉu la nova ondo de separismoj fakte determinos novajn
   landlimojn sur la postmilita mapo. Sed io ŝajnas esti certa: la eterna deziro pri
   popola sendependeco restas grava elemento de monda nestabileco.
</p> 
