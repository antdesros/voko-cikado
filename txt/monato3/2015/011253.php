      <h2>Alpa kabano Albert Heim</h2>
      <!-- komenco de la enhavo -->

       

       <p>La kabano Albert Heim troviĝas je alteco de 2543 m ĉe la piedo de la montopinto Galenstock
   (3586 m) en la svisaj Alpoj. Ĉirkaŭita de la montaro kaj droninta en la silento de
   naturo, ĝi disponigas loĝeblecon por 80 personoj, hejmecan manĝoĉambron, helajn kaj
   amoplene aranĝitajn dormejojn. La alpa kabano estas bone provizata kaj taŭgas por
   familioj kun infanoj. Ĝi bele situas sur rokblokego kaj bone videblas de malproksime.
</p> 
<p>La situo konvenas al familioj ĉefe pro la fakto, ke la vojo de Tiefenbach ĝis la alpa
   kabano estas tre facila kaj piedirebla en ĉirkaŭ 1 horo kaj duono. Post la alveno
   tien eblas aŭ restadi en la ĉirkaŭaĵo, aŭ fari mallongan piedekskurson al montopinto
   Schafberg (proksimuma daŭro: 30 minutoj). Schafberg proponas belegan panoramon al
   la Urseren-valo kaj al la skiterenoj ĉe Nätschen-Gütsch, Gemsstock kaj Winterhorn.
</p> 
<p>La apuda ĉirkaŭaĵo inklinigas al restado. Oni bezonas piede proksimume 30 minutojn
   de la kabano ĝis la glaĉerolango de Tiefengletscher. Malsupre de la kabano la infanoj
   kune kun la gepatroj povas senprobleme restadi kaj ludi.
</p> 
<h3>Piedmarŝado</h3> 
<p>La mirinde bela ĉirkaŭaĵo de la alpa kabano Albert Heim ebligas piedekskursojn ankaŭ
   al aliaj montopintoj. El ili sendube elstaras la supreniro al montopinto Galenstock.
   Sur ĝi la migrantoj ĝuas neforgeseblan travivaĵon meze de spir-retena pejzaĝo.
</p> 
<p>Aparte belega promenado kun vido al la Urseren-valo eblas laŭ la altmontara vojo de
   Urseren, kiu kondukas de la alpa kabano al Andermatt. La marŝado tien daŭras proksimume
   6 horojn.
</p> 
<p>Aliaj, pli mallongaj promenadoj kondukas de la alpa kabano tra Saasegg kaj Tätsch
   al Tiefenbach (en ĉirkaŭ 2 horoj kaj duono) kaj preter Saasegg kaj Realp-arbaro malsupren
   al Realp (en 2 horoj).
</p> 
<p>La ĉirkaŭaĵo de la alpa kabano disponigas multnombrajn bonegajn eblojn por grimpado
   en ĉiuj gradoj de malfacileco. La grimpad-itineroj ĉe Gletschorn, Graue Wand kaj Winterstock
   atingeblas piede de la kabano en proksimume unu horo.
</p> 
<h3>Situo</h3> 
<p>Al la alpa kabano Albert Heim eblas alveturi aŭ per poŝtaŭtobuso el Andermatt en direkto
   al la Furka-montpasejo ĝis Tiefenbach, aŭ per person-aŭto ĝis la nova parkejo iom
   super la hotelo Tiefenbach (2160 m) 
  (<a href="http://www.hotel-tiefenbach.ch">www.hotel-tiefenbach.ch</a>). De tie oni atingas la alpan kabanon per agrabla marŝado laŭ bone markita promenvojo
   dum ĉirkaŭ 90 minutoj.
</p> 
