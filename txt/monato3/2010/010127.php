      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Malplenaj kapoj, plenaj korpoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Skuiĝis lastatempe pro diversaj skandaloj la mondo de sumoo, la japana tradicia lukto rigardata kiel nacia sporto. En 2007 mortis juna luktisto pro tiel nomata favora traktado, nome perfortado; en 2009 estis maldungitaj du rusaj luktistoj suspektataj pro haŝiŝo-fumado; kaj en 2010 la plej forta <i>Jokozuma</i>-luktisto Asaŝoorjuu estis eksigita pro perforto al konato dum drinkado.</p> <p>Cetere restas la problemo pri basbala vetado, kiam luktistoj kaj iliaj gvidantoj vetas je matĉoj inter basbalteamoj. Por ekvilibrigi la teamojn, spertulo donas handikapajn poentojn al unu el la teamoj. Tio estas profesia afero, sed administrata de mafianoj. Tiuj ricevas 10 % da makleraĵo. Tial veti por basbal-matĉo signifas helpi kontraŭleĝajn mafianojn.</p> <h3>Turniro</h3> <p>La Sumoo-Asocio malkaŝis, ke 65 luktistoj vetis je basbal-matĉoj. La asocio maldungis unu luktiston kaj lian gvidantan majstron pro multjara vetado. Ĝi malpermesis al pliaj 12 partopreni en turniro. Cetere la asocio-prezidanto estas provizore anstataŭigita. La ministrejo pri edukado kaj scienco, kiu prizorgas la asocion, ne taksas ĝin kapabla.</p> <p>Kun la kondiĉo, ke la asocio severe punu la koncernajn luktistojn kaj reformu sin, la ministrejo permesis, ke okazu grava turniro en Nagajo. Tamen malgraŭ tio la duonŝtata elsend-stacio NHK decidis ne priraporti la matĉojn. Krome la pokalon por la ĉampiono nome de la imperiestro oni ne donos. Kompanioj, kiuj donacis premiojn, retiriĝis, timante, ke sumoo-reklamado prezentos malbonan impreson.</p> <h3>Sklaviĝi</h3> <p>Verdire sumoo ne plu allogas junulojn. Basbalo, piedpilko kaj golfo pli altiras: profesiuloj en sportoj tiaj perlaboras pli da mono. Krome, por fariĝi sumoo-luktisto, oni eniras tiel nomatan ĉambron kaj trejniĝas sub la severa gvido de la majstro. Tio signifas sklaviĝi por pli altaj ranguloj.</p> <p>Sukcesaj luktistoj malmultas. Nur 68 luktistoj en la rangoj de <i>Makuuĉi</i> kaj <i>Ĵuurjoo</i> el 721 ricevas salajron. La aliaj luktadas sen salajro kaj, kiam ili eksiĝas en la aĝo de ĉirkaŭ 35 jaroj, al ili restas nur malplena kapo kaj plena korpo, kune kun kapabloj senutilaj en la socio.</p> <p>Povas esti, ke sumoo fariĝos pli internacia, allogante pli da eksterlandaj junuloj, ĉefe el malriĉaj landoj. Verŝajne ĝi perdos sian „sanktecon” kiel tradicia sporto. Same kiel ĵudo internaciiĝis kaj fariĝis ero en la Olimpikoj, sekvos ankaŭ sumoo. Tio estos neevitebla por la estonteco de la sporto.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
