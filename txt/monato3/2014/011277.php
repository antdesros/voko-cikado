      <h2>La parlamentano kaj la pornografiaĵoj</h2>
      <!-- komenco de la enhavo -->

       

         <p>Mi ekkonis la parlamentanon Sebastian Edathy en 2013. Li impresis kiel homo serioza
   kaj sincera. Tiam li prelegis pri parlamenta enketo, kiun li estris, rilate murdojn
   faritajn de novnazioj kontraŭ eksterlandanoj turk- kaj grek-devenaj kaj policistino.
</p> 
<p>En septembro 2013 okazis balotoj por la germana federacia parlamento (<i>Bundestag</i>). Edathy estis reelektita. Tamen en februaro 2014 polico kaj prokuroraro traserĉis
   liajn loĝejon kaj oficejon. Ili suspektis, ke tie ili trovos kontraŭleĝajn fotojn
   pri nudaj knaboj inter 9- kaj 14-jaraĝaj.
</p> 
<h3>Asertas</h3> 
<p>En listo de tiuj, kiuj aĉetis la fotojn de kanada kompanio, troviĝis la nomo de Edathy.
   Tiu asertas, ke li neniam posedis pornografiaĵojn. Efektive la prokuroro kaj polico
   konstatis, ke ĉe Edathy ne troveblas krima materialo.
</p> 
<p>Baldaŭ aperis pliaj novaĵoj skurilaj kaj kuriozaj. La tiama germana ministro pri internaj
   aferoj, Hans-Peter Friedrich [fridriĥ], nun ministro pri agrikulturo, evidente informis
   la prezidanton de la Socialdemokrata Partio (SPD), Sigmar Gabriel, pri esploroj kontraŭ
   Edathy, kiu povus esti avertita.
</p> 
<h3>Demisiis</h3> 
<p>Edathy subite rezignis pri sia parlamenta mandato „pro sanecaj kialoj” kaj malaperis, laŭdire en eksterlando. Denove li neis aĉeti kaj posedi pornografian
   materialon kaj diris, ke lia komputilo estis ŝtelita. Ankaŭ ministro Friedrich demisiis
   (vidu M<span class="mm">ONATO</span>, 2014/3).
</p> 
<p>Al ĵurnalistoj Edathy diris, ke li plurfoje ricevis murdminacojn kaj tial ne intencas
   reveni Germanion. Kaj en la Fejsbuk-paĝo de Edathy aperis mistera noto: „Al koncernatoj. La dokumentoj pri la tuta afero estas deponitaj en multaj lokoj kaj
   estos publikigataj en tiu momento, kiam io okazos al mi”. Ĉu Edathy mem skribis? Intertempe li anoncis sian intencon verki libron pri la afero.
</p> 
<p>Koincide, konfesinte, ke li posedas infano-pornografiaĵojn, demisiis la prezidanto
   de la Katolika Laboristo-Movado (<i>Katholische Arbeitnehmer-Bewegung</i>), Georg Hupfauer. 
</p> 
