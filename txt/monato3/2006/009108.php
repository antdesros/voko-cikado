      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Lern-fiasko</h2>
      <!-- komenco de la enhavo -->

       

     <p>En la junia numero mi raportis pri problemoj de germanoj pri la propra lingvo kaj ligite al tio pri la angla („Germana batalo”, M<span class="mm">ONATO</span>, 2006/6, p. 7). Mi citis sciencistojn, kiuj kritikis la troan aŭ preskaŭ ekskluzivan uzadon de la angla en scienco kaj diris, ke 30 % de la germanoj tute ne scias la anglan, pliaj 30 % nur rudimente (tio estas, ili lernis, sed ne scias ĝin). El tio oni povus konkludi, ke unu triono de la germanoj scias la anglan. Tion tamen kontestas nova esploro, kiu enketis 3700 studentojn pri angla lingvo. 34 % asertis, ke ili tre bone scias ĝin, 38 % diris bone. Sed postaj lingvotestoj montris: nur 1 (unu!) % scias la anglan tre bone, nur 4 % bone kaj 76 % kontentige (do nebone). Atentu: temas nur pri studentoj (do la elito), ne pri germanoj ĝenerale. Tiuj studentoj dum 8 ĝis 10 jaroj intense lernas la anglan. Oni do povas paroli nur pri grandioza fiasko de instruado kaj lernado de la angla, se oni vidas tiujn rezultojn. Tio ne povas mirigi, ĉar kontraste al ofte esprimata kontraŭa aserto, la angla por germanoj estas tre malfacila lingvo.</p> <p>Spite al tio tamen precipe politikistoj en Germanio plu kaj pli kaj pli favoras lernadon de la angla. Günther Oettinger [ginter étinger], kristandemokrata ministroprezidanto de la federacia lando Baden-Virtembergo, pledante por instruado de la angla, argumentis jene: „La angla fariĝas la laborlingvo. La germana restas la lingvo de familio kaj libertempo, la lingvo, per kiu oni legas private.” Laŭ li do la germana reduktiĝu al ludodialekto. Privilegion kaj avantaĝon aŭtomate havas per uson-angla lingvo tiuj homoj, kies gepatra lingvo ĝi estas, dum ĉiuj aliaj pene devas lerni ĝin, elspezante gigantajn energion, tempon kaj monon. Britoj kaj usonanoj devas lerni neniun fremdan lingvon kaj povas investi energion, tempon kaj monon por aliaj aferoj kaj tiel facile superi ĉiujn aliajn ekonomie kaj kulture. Ne malprave tial oni diras, ke tiel nomata tutmondiĝo fakte estas nenio alia ol usonigado de la cetera mondo. Estas por mi ĉiama enigmo, ke la homoj kaj precipe politikistoj ne ekkonas kaj ne perceptas tiun ĉi evidentan maljustaĵon.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
