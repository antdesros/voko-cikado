      <h3>Scienco</h3>
      <h3 class="tem">MATEMATIKO</h3>
      <h2>3-14-15: la jarcenta pi-tago estis celebrita</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Kio okazis sabaton la 14an de marto 2015, je la horo 09:26:53? Kaj kion vi estis faranta
      tiumomente? Se oni hazarde starigos al vi tian demandon, bonvolu respondi sincere,
      tute ne timante, ke temas pri triagrada enketado. Verŝajne via interparolanto treege
      ŝatas matematikon kaj volas nur ekscii, ĉu vi estas informita pri la ekzisto de originala
      festo: tiu de pi (la konstanto π).
   </b>
</p> 
<p>Jam de pluraj jaroj fakuloj rigardas la 14an de marto kiel oficialan pi-tagon. Kaj
   ne povas manki, en la nuna teknologia epoko, aparta retejo dediĉita al la okazaĵo:
   
   <a href="http://http://www.piday.org/">http://www.piday.org/</a>. Estas cetere rimarkinde, ke la ĉi-jara 14a de marto estis la plej grava pi-tago
   de la jarcento: fakte unuafoje ene de cent jaroj (la venonta fojo estos en la jaro
   2115) oni povis envicigi la unuajn dek ciferojn, kiuj estigas la konstanton π: 3,141592653.
   Oni pensu pri la jeno: 3 (tria monato, marto), 14 (tago), 15 (jaro), 9 (horoj), 26
   (minutoj), 53 (sekundoj). Estas facile imagi, ke plejaj fanatikuloj zorge atendis
   eĉ la sekund-milonon 589 (3,141592653589) kaj klopodis malkorki botelon de ŝaŭmvino
   ĝuste en tiu momento, sed grandaj matematikistoj mem kiel Arkimedo el Sirakuzo (kiu
   pritraktis la temon en sia verko 
   <i>Pri la mezurado de cirklo</i>, latine: 
   <i>Dimensio circuli</i>) aŭ Pitagoro tian fanatikecan nivelon verŝajne ne atingus.
</p> 
<h3>Relativeco</h3> 
<p>La pi-tago estas festo komenciĝinta en 1988 en la usona urbo San-Francisko laŭ iniciato
   de la fizikisto Larry Shaw: li volis trovi apartan daton dediĉendan al la celebrado
   de matematiko (scienco, kiu ne ofte trovas kialojn por esti solenata). La 14a de marto
   estas cetere ankaŭ la naskiĝdatreveno de Albert Einstein (la aŭtoro de la teorio de
   la relativeco): sendube tiu koincido mem ludis gravan rolon por la elekto de la koncerna
   dato. Iom post iom la okazaĵo fariĝis populara en kaj ekster Usono. Rezulte de tio
   abundas nuntempe en la tuta mondo la aranĝoj organizataj por celebri la pi-tagon.
</p> 
<h3>Konkurso</h3> 
<p>En Usono, kie la festo estas plej atendata kaj solenata, oni en pluraj urboj organizas
   kuradon, kie oni devas laŭiri 3,14-mejlan (5503-metran) distancon, dum kelke da krakmanĝejoj
   kaj restoracioj proponas menuojn je la simbola kosto de 3,14 dolaroj. Tiaj iniciatoj
   jam multobliĝas ankaŭ en Eŭropo: ekzemple en la itala urbo Udine estis aranĝita ĉi-jare
   konkurso, en kiu gajnis premion tiu, kiu kapablis enmemorigi kiel eble plej multe
   da decimaloj post la komo. La okazaĵo ŝajnas cetere esti inspirita de la historio
   rakontita en la libro 
   <i>Born on a blue day</i> (Naskiĝinta en blua tago) fare de Daniel Tammet: temas pri la historio de juna handikapulo,
   kiu lernas parkere dekmilojn da ciferoj, kiuj estigas la konstanton π.
</p> 
<h3>Tortoj</h3> 
<p>Oni fine priatentu tion, ke en Usono „pi” estas prononcata „paj”, ekzakte same kiel la angla vorto „<i>pie</i>” („kuko, torto”). Jen la motivo, pro kiu en Usono oni trovas pli kaj pli ofte la 14an de marto tortojn
   pi-dekoritajn. La festo mem jam estas neeskapeble ligita tie kun la koncepto de torto,
   kiu aliflanke estas mem cirkla, kio siavice referencas kaj honoras la konstanton π.
</p> 
