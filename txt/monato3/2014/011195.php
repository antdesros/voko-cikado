      <h3>Ekonomio</h3>
      <h3 class="tem">AŬSTRIO-ĈEĤIO</h3>
      <h2>Gasodukto Mozart</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Estas planata gasodukto <i>Mozart</i>, kiu konektu la aŭstran kaj ĉeĥan gasretojn. Ĝi havus tubojn kun diametro de 50 cm, longecon de preskaŭ 100 km, kostus inter 80 kaj 100 milionojn da eŭroj kaj ĝia konstruado daŭrus 2 ĝis 2,5 jarojn. La konstruado komenciĝus post la subskribo de la koncernaj traktatoj inter Aŭstrio kaj Ĉeĥio. Ambaŭ landoj estas interesataj, ĉar la kondiĉoj en iliaj naciaj gasmerkatoj, samkiel en Eŭropo, dramece ŝanĝiĝis. Krome la gasodukto povus kontribui al la malpliigo de gasprezoj.</b></p> <p>Aŭstrio kaj Ĉeĥio ambaŭ disponas pri gasoduktaj konektoj en la direkto orienta-okcidenta. Estas konate, ke la aŭstra konekto orienta-okcidenta estas troŝarĝita, dum la ĉeĥa ĉi-momente ne estas. Do ekestis la ideo konekti ambaŭ gasoduktojn per konekto norda-suda.</p> <p>Por realigi tiun ideon estas kelkaj variantoj, el kiuj <i>Mozart</i> estas unu. Laŭ ĝi ambaŭ gasoduktoj devas konektiĝi en la mezo de suda Bohemio kaj supra Aŭstrio. Estas du pliaj konekto-ebloj: ĉe la gasodukto en Baumgarten, aŭ ĉe alia en Oberkappel en Aŭstrio.</p> <p>Fakuloj el Aŭstrio kaj Ĉeĥio nun okupiĝas pri la elekto de la plej favora projekto. La decido ricevos en ambaŭ ĉefurboj politikan, kaj en Bruselo ankaŭ eŭropan subtenon. Se povus esti certigita EU-partopreno, tiuokaze parto de la elektita projekto certe ricevos financadon de Eŭropa Unio.</p> <p>La konsumantoj en ambaŭ landoj esperas, ke gaso rezulte de la pli multaj ebloj de liverado estonte iĝos malpli kosta.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div> 
