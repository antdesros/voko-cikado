      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Lingvofamilio, kiu ne ekzistas</h2>
      <!-- komenco de la enhavo -->

       

      <p>La titolo de la libreto estas misnomo, implicanta, ke ja pravigeblas filologia kungrupigo de la keltaj lingvoj kune kun la semidaj lingvoj. Tamen, ajna ideo, ke tiuj du grupoj havas komunan genealogian fonton, estas defendata de neniu serioza nuntempa lingvisto.</p> <p>Tion nek celas, nek opinias Cherpillod. En la ĉefa parto de sia verko li elmontras liston de 40 punktoj pri simileco inter la du lingvogrupoj. Laŭ la verkinto, pri tiaj konverĝoj „estas nesupozeble, ke ili ĉiuj estus kaŭzataj de simpla hazardo” (p. 124). Sed poste, ekzameninte la leksikon de la du grupoj, li konkludas, ke pro relativa manko de tieaj komunaj elementoj ne povas temi pri genealogia rilato.</p> <p>Li starigas, do, konkludan demandon pri tio, kiuj historiaj okazaĵoj povus klarigi tian situacion. Kiel solvon li subtenas la teorion de la munkena profesoro Theo Vennemann, ke influo de antikvaj kaj nun malaperintaj parolantoj de semidaj lingvoj en Eŭropo okazigis trajtojn en la iam najbaraj aŭ anstataŭontaj keltaj lingvoj.</p> <p>Sed ĉu la atestaĵoj vere sufiĉas, por fari tian konkludon? Kelkaj el la 40 punktoj de Cherpillod ŝajnas al mi malfortaj, ekzemple, lia punkto 10: familinomoj havas la formon „filo de ... ”. Cherpillod asertas, ke tiu kutimo ekzistas en malmultaj aliaj lingvoj (p. 47), menciante kiel solan alian ekzemplon la skandinavajn lingvojn. Tio simple ne estas vera, kaj cetere, temas ne pri lingva eco, sed kultura. Tra la mondo oni povas trovi tiun kutimon: hungaraj nomoj kiel Pálfi (<i>fi</i>, <i>fiu</i> = „filo”) kaj azerbajĝanaj kaj turkaj nomoj, finiĝantaj per <i>-oglu</i> (= „filo”), tuj venas en la kapon. En la kartvela nomo Ioseb Besarionis Dze Ĝugaŝvili (la naskonomo de Stalin) estas eĉ du tiaj patronimaj komponantoj (<i>dze</i> = „filo”, <i>ŝvili</i> = „ido”). Do, ĉu unika konverĝo, kie ne povas roli hazardo? Ne.</p> <p>Simile dubindaj estas iuj aliaj pretendataj konverĝoj (ekzemple, pri la malkutimeco de ingenra finaĵo „-a” (punkto 8) kaj plurala finaĵo „-n” (punkto 12)), kaj almenaŭ unu punkto (20: adjektivo post la substantivo) estas atendebla, se ne universala, sekvo de alia trajto jam menciita (18: posta determinado, t.e. kapvorto en la komenco, male al Esperanto).</p> <p>Tamen, eĉ se oni povas forharfendi proporcion de la punktoj, ja restas ankoraŭ konsiderinda serio da koincidoj inter la du lingvogrupoj. Tute fascina temo. Ĉi tiu verko estas bona komencopunkto por iu, kiu volas esplori ĝin, eĉ se ĝia aŭtoro jam decidis sian opinion.</p> <div style="text-align:right">Ed R<span class="mm">OBERTSON</span></div> <h6 class="bib">André Cherpillod: La kelt-semidaj lingvoj. Eld. La Blanchetière, Courgenard, 2008. 143 paĝoj. ISBN 2906134791.</h6>  
