      <h3>Spirita vivo</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>100 jarojn leĝo pri islamo</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La 15an de julio 1912 ekvalidis leĝo pri islamo en Aŭstrio. La leĝo agnoskis islamon laŭ la hanifita skolo kaj tiumaniere certigis al la islamanoj memdeterminon. Ekde tiam ili disponas pri koncernaj rajtoj, kiel ekzemple religia instruado. Ĝis hodiaŭ ĝi estas unika leĝo en Eŭropo.</b></p> <h3>Habsburgo</h3> <p>La leĝo pri islamo en Aŭstrio havas precipe historiajn kaŭzojn: en 1908 Aŭstrio-Hungario okupis la grandparte islaman Bosnion. Kun pli ol 600 000 tie loĝantaj islamanoj la habsburga regno unuafoje en sia historio staris antaŭ la defio akcepti islaman loĝantaron en sian regadon.</p> <p>Post la okupado la islamanoj aktivis en la aŭstra-hungara monarkio: bosnianoj servis en la imperiestra gvardio, estis imamoj en la armeo, kaj en Vieno oni planis la konstruon de moskeo – plano, kiun malebligis la eksplodo de la unua mondmilito. Do, helpe de leĝo la islamanoj ricevis la saman juran protekton kiel la aliaj leĝe agnoskitaj kredkomunumoj. Sub la patronado de la ŝtato ekde tiam estas realigataj iliaj aranĝoj kaj kutimoj en Aŭstrio. La islamanoj rajtas estigi fondaĵojn, ili havas la rajton pri aŭtonomio, se ili ne eniras konflikton kun la valida juro.</p> <h3>Gastlaboristoj</h3> <p>Komence la leĝo preskaŭ ne estis aplikata, ĉar baldaŭ venis la fino de la habsburga monarkio en 1918. Post la unua mondmilito apenaŭ loĝis islamanoj en Aŭstrio. Tamen en longtempa perspektivo la leĝo funkciis. En la dua duono de la 20a jarcento al Aŭstrio venis ne nur pli kaj pli da gastlaboristoj el Turkio kaj el la tiama Jugoslavio, sed ankaŭ studentoj, diplomatoj kaj fuĝintoj el islamaj landoj. La islamleĝo fariĝis la bazo de la kunvivado. Surbaze de ĝi la 2an de majo 1979 estis fondita „Islama Kredkomunumo en Aŭstrio” kiel korporacio de publika juro.</p> <p>Ekde la lernojaro 1982/1983 funkcias instruado de islamo en aŭstraj lernejoj. Kiel instruistojn oni dungis unue pedagogie neinstruitajn islamanojn aŭ teologojn el eksterlando. Iompostiome oni komencis tamen eduki instruistojn en Aŭstrio.</p> <h3>Speciala pozicio</h3> <p>Okaze de la leĝjubileo la islama kredkomunumo kaj aŭstraj politikistoj regule emfazas la specialan pozicion de Aŭstrio rilate al la agnosko de islamo. Pere de la publika-jura pozicio laŭ la prezidanto Heinz Fischer [hajnc fiŝa] la islamanoj devas respekti la devojn legitajn kun la rekono de islamo kaj la validajn leĝojn en la lando.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>   
