      <h3>Leteroj</h3>
      <h3 class="tem"></h3>
      <h2>La tri turoj de MKC (6)</h2>
      <!-- komenco de la enhavo -->

       

      <p>Husejn Al-Amily (M<span class="mm">ONATO</span> 2007/6, p. 6) listigas okazojn, en kiuj ŝtato komplote planas atakojn kontraŭ propraj instalaĵoj por akuzi aliajn pretekste por ataki ilin. Mi aldonas unu plian, kiu estas la dua tiaspeca faro de Hitler. Temas pri la bruligo de la Reichstag (parlamento) en Berlino la 27an de februaro 1933 fare de nazioj, pro kiu estis akuzitaj la tri bulgaraj ŝtatanoj Georgi Dimitrov, Vasil Tanev kaj Blagoj Popov kaj la germano Ernst Torgler, kiuj estis arestitaj kaj juĝitaj de la imperia tribunalo en Lejpcigo de la 21a de septembro ĝis la 23a de decembro 1933. Kun ili estis akuzita la nederlandano Marinus van der Lubbe, kiu estis poste ekzekutita de nazioj.</p> <p>Dum kontraŭproceso en Londono en septembro 1933 estis pruvite, ke la bruligo de la <i>Reichstag</i> estis faro de nazioj. Estas tamen aliaj pruvoj, kiuj fariĝis konataj poste. En sia 48-paĝa raporto la germana eksperto prof. Emil Josse, al kiu la juĝesploristo komisiis esplori la teknikan flankon de la evoluo de la incendio, konkludis, ke la konstruaĵo, kiu havas amplekson de pli ol 10 000 m<sup>3</sup>, ne povas esti bruligita de nur unu homo per torĉo, ke por tio estis uzitaj likvaj brulaĵoj.</p> <p>En siaj atestoj la supera direktoro de la berlina fajrobrigado Walter Gempp ekskludis, ke Van der Lubbe povis sola kaŭzi la incendion, ke la preparo de la incendio postulis longan tempon kaj estis farita de kelkaj personoj. Necesis minimume 20 kg da likva brulaĵo. Gempp estis arestita kaj strangolita en sia ĉelo, por ke li ne denuncu la efektivajn bruligintojn.</p> <p>Estas konservita letero de Karl Ernst, komandanto de sturmtaĉmento, murdita la 30an de junio 1934 laŭ ordono de Hitler. Jen citaĵo el la letero:„Ni decidis, ke van der Lubbe devas eniri tra fenestro de la restoracio. Mi kaj miaj kamaradoj estis vestitaj en ŝuoj kun gumaj plandumoj. Ni ŝmiris la seĝojn kaj la tablojn per fosforo kaj surverŝis la kurtenojn kaj la kanapojn per petrolo. Poste ni ekbruligis en kelkaj lokoj en la kunsida salono kaj en la salono de imperiestro Vilhelmo. Je kvarono post la 9a ĉio estis finita kaj ni eliris. Van der Lubbe estis certa ĝis la lasta momento, ke li agas sola ...”.</p> <div style="text-align:right">Dimitar H<span class="mm">AĜIEV</span></div> <div style="text-align:right"><i>Bulgario</i></div> 
