      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Premioj  kaj premoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pleniĝis pasintjare japanaj ĵurnaloj per novaĵoj pri kvar Nobel-premiitoj: pri fiziko Nanbu Jooiĉiroo (87-jara), Kobajaŝi Makoto (68-jara) kaj Masukaŭa Toŝihide (68-jara); kaj pri kemio Ŝimomura Osamu (80-jara). Ĝis nun neniam venis samtempe kvar premiitoj el Japanio. Tamen malantaŭ la novaĵoj kuŝas premoj por japana scienco.</p> <p>Grava problemo konsistas en tio, ke esploristoj elmigras el Japanio. En la lando mankas bona kondiĉaro por studi kaj tial multaj esploristoj elmigras al Usono, kaj neniam revenas. El la kvar premiitoj, du nun loĝas en Usono kaj unu el ili akiris usonan ŝtatanecon (do, li jam ne estas japano).</p> <p>Cetere velkas intereso en Japanio pri scienco. En 2003 japanaj lernantoj akiris bonan rezulton en internacia ekzameno pri scienco, sed al la demando „Ĉu lernado de scienco donas al vi ĝojon?” nur 19 % de la 13-jaraj lernantoj respondis jese – 25 % malpli ol la internacia mezumo de 44 %. Krome al multaj lernantoj mankas sperto pri naturo. Komputado, televidado kaj legado en la ĉambro ne malfermas la naturan mondon al lernantoj kaj sekve ne kondukas tiujn al scienco.</p> <h3>Multjaraj esploroj</h3> <p>Kulpas ankaŭ la japana registaro, kiu postulas nur tujajn rezultojn. La ministerio pri edukado kaj scienco distribuas monon al universitatoj laŭ klare videblaj rezultoj, kaj sekve tiuj postulas similajn de profesoroj. En tia situacio, multjaraj esploroj ne estas bonvenaj. Masukaŭa Toŝihide, unu el la kvar premiitoj, kritikas la nunan politikon: „Kiam fonto (baza esploro) mortos, la rivero mortos. Por scienco necesas 50 jaroj, 100 jaroj. Oni ne atendu tujan rezulton.”</p> <p>Eble pro la supraj kialoj malmultiĝas la nombro de japanaj studaĵoj pri scienco. En 2007 aperis 90 185 japanaj studaĵoj, tiel ke Japanio okupas la kvinan lokon en la mondo. En 2006, tamen, ĝi okupis la duan lokon. Dum la pasintaj 10 jaroj kreskis la nombro de studaĵoj je nur 2 %.</p> <p>La kvin pintaj landoj laŭ la nombroj de studaĵoj (kaj, inter krampoj, la kresko) jenas: 1. Usono 344 375 (10 %); 2. Ĉinio 180 970 (50 %); 3. Britio 109 342 (35 %); 4. Germanio 93 033 (25 %); 5. Japanio 90 185 (5 %). En ambaŭ kampoj (nombro de studaĵoj kaj kresko) perdas Japanio sian antaŭan gloron.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
