      <h3>Lingvo</h3>
      <h3 class="tem">SKOTLANDO</h3>
      <h2>Antikva lingvo por moderna lando</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la momento, kiam mi ekverkas ĉi tiun artikolon, la popolo de Skotlando (de kiu
   venis miaj gepatroj) anticipas gravan okazon, nome referendumon por decidi, ĉu la
   norda regno forlasu la britan regnaron (Unuiĝintan Reĝlandon) kaj refoje estu sendependa
   ŝtato. Jam en la jaro 1998 la antikva parlamento de Skotlando, abolita en 1707, estis
   restaŭrita, kvankam nur kun loka aŭtonomia povo.
</p> 
<p>Laŭ opinioj aperintaj en la populara gazetaro, la ŝanco obteni jesan decidon per la
   referendumo ne estas granda pro la supozeble granda nombro de la homoj, kiuj volas
   reteni Brition unuiĝinta. Tamen la fakto, ke la plej forta politika partio kun majoritato
   en la edinburga parlamento estas la Skota Nacia Partio, donas esperon al tiuj, kiuj
   deziras sendependecon.
</p> 
<p>Se la rezulto de la referendumo estos jesa, estiĝos aliaj gravaj demandoj, ekzemple
   pri la valuto, ekonomio, defendo, proprietaĵo – kaj nelaste pri la statuso de la lingvoj parolataj en Skotlando. (Pri juro, klerigsistemo
   kaj monbiletoj oni ne bezonos diskuti, ĉar Skotlando delonge havas siajn proprajn,
   tre malsamajn ol la anglaj.)
</p> 
<h3>Lingvoj</h3> 
<p>La ekzistanta skota parlamento iugrade oficialigis la gaelan lingvon. Oni rajtas paroli
   ĝin en la parlamento (nuntempe nomata „asembleo”) kaj klerigi la infanojn per ĝi. Multe da oficialaj ŝildoj, lok- kaj strat-nomoj,
   samkiel anoncoj, estas dulingvaj (la du lingvoj estas la gaela kaj la angla). Aliflanke,
   la tria lingvo parolata en Skotlando, t.e. la malalteja skota (pli bone konata kiel
   la lingvaĵo de la nacia poeto Robert Burns), ne estas oficiale agnoskita, kvankam
   temas pri idiomo de la plimulto de la skotoj, kiuj loĝas en la malaltejo.
</p> 
<p>Ĉi tio iom ĉagrenas la skote parolantojn, sed oficialigo de la skota estigus pli da
   problemoj, ol ĝi solvus. La skota ne estas normigita idiomo: ekzistas diversaj dialektoj.
   Simile al la svisgermana, ĝi estas buŝa lingvo kaj neofte estas skribata, krom por
   poezio.
</p> 
<p>Plie, preskaŭ neniu, escepte de anoj de la Skota Lingva Asocio kaj kelkaj verkistoj
   kaj poetoj, uzas iun puran skotan lingvaĵon. Ordinaraj skotoj tiom intermiksas anglajn
   kaj skotajn esprimojn dum sia parolado, ke iu germana kritikanto (H. Kloss) ne tute
   malprave priskribis la skotan kiel 
   <i>Halbsprache</i>, do „duonlingvon”. Tamen, en multaj dulingvaj kaj plurlingvaj landoj, intermeto en la paroladon de
   vortoj el la majoritata lingvo estas sufiĉe ofta kutimo.
</p> 
<h3>La gaela</h3> 
<p>En la pasinteco, oni ofte asertis, ke la gaela, kiu estas tre antikva lingvo, ne taŭgas
   kiel komunikilo en moderna mondo. Mi iam korespondis kun angla esperantisto loĝanta
   en Irlando, kiu respondis al invito partopreni lingvan konferencon eksterlande per
   kontribuo pri la irlandgaela lingvo. Mi protestis per letero, kiun mi sendis al li,
   ĉar li skribis al la organizanto de la konferenco, asertante, ke „la gaela estas primitiva lingvo, en kiu oni eĉ ne kapablas paroli pri komputiko”. Tio ŝajnis al mi tre stranga interpreto de la koncepto „primitiva”. Unue, tio ne veras, kiel ajna persono povas konstati per vizito al universitato
   en Irlando; kaj due, multe da tute „neprimitivaj lingvoj”, ekzemple la germana, uzas anglan terminaron (ekzemple: 
   <i>„computer”</i>), kiam temas pri komputiloj. Do la aserto – eĉ se ĝi estus vera – pruvas absolute nenion.
</p> 
<p>En nia familio ni kutime parolas ne la irlandgaelan, sed la parencan kaj samdevenan
   skotgaelan, kaj mi antaŭ nelonge decidis entrepreni la taskon demonstri, ke la gaela
   – kaj precipe ĝia skota varianto – kapablas plenigi ĉiujn postulojn de la moderna vivo. La vorttrezoro de la gaela estas
   riĉega, ege esprimpova kaj abunda en ĉiu fako de la homa aktiveco. La plej granda
   kaj respektata vortaro de la lingvo, tiu fare de Edward Dwelly, enhavas terminaron
   kaj esprimojn por ĉiu aspekto de la vivo, la naturo, la tekniko, la scienco, la kulturo
   ktp.
</p> 
<p>Simile, la centjara antologio 
   <i>An Tuil</i> [an tujl] (Tajdo), eldonita en 1999 kaj ampleksanta la tutan gamon de la gaela verkaro
   ekde la komenco de la 20a jarcento, montras, kiumaniere kaj kiom la lingvo disvolviĝis
   kaj moderniĝis dum tiu periodo. Uzante la vastan nombron da radikoj, afiksoj kaj partikuloj,
   oni facile povas formi nenombreblan kvanton da vortoj, iom simile al la eblo de la
   islanda lingvo, kiu apenaŭ uzas fremdajn pruntvortojn kaj esprimas ĉion per siaj indiĝenaj
   radikoj kaj vortaj fontoj.
</p> 
<p>Kontribuintoj al la plej grava kaj prestiĝa literatura revuo en la gaela, nome 
   <i>Gairm</i> [géram] (Alvoko), kiu estis eldonata seninterrompe dum duonjarcento, montris, ke
   oni povas pritrakti iun ajn temon: kulturan, historian, teknikan, sciencan, filozofian,
   politikan, artan, muzikan, ĉion ĉi en la propra mirinda lingvo. La gaela posedas siajn
   proprajn terminojn por la elementoj, la kemiaĵoj, la mineraloj – kaj kompreneble por la flaŭro kaj la faŭno, la geografiaj kaj geologiaj nocioj, la
   botaniko kaj la astronomio – eĉ por nuklea fiziko.
</p> 
<h3>Komunikilo</h3> 
<p>Mi esperas demonstri en libra formo mian tezon, ke la gaela estas tute taŭga esprimilo
   kaj komunikilo por nia jarcento. Dum longa tempo ĝi estis rigardata – eĉ de skotaj klerigistoj – kiel kruda lingvaĵo de needukitaj kamparanoj, kvankam iam parolantoj de la gaela
   lingvo – keltaj klerikoj kaj kleruloj – portis la kristanan kaj sekularan civilizacion al granda parto de Eŭropo, kiam ĝi
   estis pagana kaj brutala.
</p> 
<h3>Dukoj</h3> 
<p>Bonŝance ne ĉiuj inter la regantaj
   <sup>1</sup> kaj influhavaj tavoloj de la skota socio, el kiuj la plimulto forlasis sian kulturon
   favore al la angla, sammaniere neglektis sian nacian heredaĵon. Elstara ekzemplo el
   la 19a jarcento estis la sepa duko de Atholl [atol] (gaele: Athall [ahal]) kaj lia
   filino, lordino Evelyn Stewart Murray (1868-1940), kiuj ne nur parolis kaj amis la
   gaelan, sed instigis ĉiujn loĝantojn en sia vasta kampara bieno fieri pri sia lingvo.
</p> 
<p>Tiu duka bieno en la bela regiono Perthshire (gaele: Siorramachd Pheairt [ŝíramaĥk
   fjarŝtj]) pli similis etan ŝtaton ol nuran familian proprietaĵon. Ĝi eĉ posedis, kaj
   ankoraŭ posedas, sian propran soldatan regimenton, la ununuran privatan armeon en
   la britaj insuloj, kvankam hodiaŭ ĝia funkcio estas nur ceremonia, kaj ne plu milita.
   La duko kutimis regali per premioj la infanojn, kiuj bone parolis la gaelan. Lordino
   Evelyn diligente kolektis gaelan folkloron, kiu antaŭ nelonge estis eldonita de la
   tiel nomata Gaela Teksta Societo. Tiun familian tradicion daŭrigis la 11a duko de
   Atholl, John Murray, kiu estis gael-parolanto kaj kontribuanto al la gaela revuo 
   <i>Gairm</i>, kvankam li loĝis en Afriko. Bedaŭrinde li forpasis en la jaro 2012. Mi dubas, ĉu
   iu alia el la tre angligita nobelaro interesiĝas pri la gaela.
</p> 
<p>Se mi iam publikigos la frukton de mia kompilaĵo pri la moderna gaela, mi eble dediĉos
   ĝin al eventuala sendependa Skotlando.
</p> 
<h6 class="pied">1. En siaj memoraĵoj – 
   <i>Paĝoj el mia alteja taglibro</i> – reĝino Victoria menciis, ke ŝia edzo, la princo Alberto, kiu estis germano, provis
   lerni la gaelan. Iom kurioze, kvankam en Britio la gaela lingvo estis longe malestimata
   kaj eĉ malpermesita, lertuloj kaj aliaj lingvoŝatantoj en Germanio kaj en Skandinavio
   daŭre interesiĝas pri ĝi. Unu el la plej bonaj kaj lastatempaj verkoj por lerni la
   modernan gaelan estas la libro (kun kompakta disko) 
   <i>Lehrbuch der schottisch-gälischen Sprache</i>, fare de Michael Klevenhaus (Hamburg, Buske, 2009).
</h6> 
