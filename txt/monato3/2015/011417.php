      <h3>Turismo</h3>
      <h3 class="tem">MONDMILITO</h3>
      <h2>Du novaj muzeoj en Belgio</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Antaŭ cent jaroj eksplodis la unua mondmilito. Ĉi-okaze la belga provinco Okcidenta
      Flandrio – la sola provinco, kiun la germanoj okupis nur parte – lanĉis iniciatojn por rememori la militon kaj por krei aŭ renovigi memorlokojn kaj
      muzeojn. Du tiaj lokoj antaŭ nelonge estis remalfermitaj kaj meritas viziton. Ili
      tre malsamas: unu estas oficiala kaj luksa, la alia estas modesta projekto de volontuloj
      kaj montras la germanan flankon.
   </b>
</p> 
<h3>Kluzoj „Ganzepoot”</h3> 
<p>Oktobre 1914 la belga, franca kaj brita armeoj retiriĝis sud-okcidenten kaj regrupigis
   sin malantaŭ la rivero IJzer. Nieuwpoort estis la lasta neokupita belga haveno kaj
   oni nepre devis malebligi, ke la germanoj okupu la nord-francajn havenojn. Por tio
   ne estis uzataj armiloj, sed la geografio de la regiono.
</p> 
<p>La marborda regiono de Belgio estas poldero. Ĝi bezonas sistemon de digoj, kanaloj,
   kluzoj kaj pumpiloj por resti seka. En Nieuwpoort kuniĝas ses kanaloj, kelkaj uzataj
   por ŝiptrafiko kaj kelkaj por reguligi la akvonivelon de la poldero. Pro sia formo
   la tuto de kluzoj ricevis la nomon „Ganzepoot” (ansera piedo).
</p> 
<p>Jam delonge militistoj subakvigas regionojn por haltigi la malamikon. Ankaŭ en Nieuwpoort
   oni intencis agi tiel, sed bezonis iom da tempo por fari, laŭ konsiloj de lokuloj,
   la preparlaborojn. La 21an de oktobro 1914 la kluzisto Karel Cogge kaj la ŝipisto
   Hendrik Geeraert, kiuj perfekte konis la funkciadon de la kluzoj kaj la situon de
   la kanaloj, enlasis la marakvon en la polderon. La germanoj ja rimarkis, ke la akvonivelo
   altiĝis, sed ili komence pensis, ke tion kaŭzis la pluvo. Kiam ili konstatis, kio
   reale okazas, estis tro malfrue por interveni. Tiel la frontlinio fiksiĝis dum kvar
   jaroj.
</p> 
<p>En 1938 apud la kluzoj estis konstruita monumento honore al la reĝo Alberto la 1-a.
   Ĝi havas formon de cirklo kun diametro de 30 m. Dek kolonoj subtenas ringforman trabon
   en betono, sur kiu eblas rigardi la ĉirkaŭaĵon. Meze estas la statuo de la reĝo.
</p> 
<h3>Monumento „Westfront” </h3> 
<p>Dum la jaro 2014 la monumento estis modernigita kaj pligrandigita. Pro tiu kaŭzo ĝi
   ricevis la nomon „Westfront” (okcidenta fronto). Per la plej modernaj aŭdvidaj iloj ĝi rakontas la historion de
   la inundigo. En la kela etaĝo estas montrata (per projekciiloj) la panoramo de la
   frontregiono, kiun pentris Alfred Bastien post la milito. La dimensioj de la originala
   panoramo estas 115 oble 14 m kaj ĝi bezonus grandegan konstruaĵon por montriĝi. Krome
   restas spaco por ekspozicioj kaj konferencoj.
</p> 
<p>Lokaj, provincaj kaj flandraj gravuloj oficiale inaŭguris ĝin la 19an de oktobro 2014.
   La 28an ĝin vizitis la belga reĝo Filipo, en- kaj ekster-landaj eminentuloj, interalie
   la germana kancelierino Angela Merkel.
</p> 
<p>„Westfront” bone atingeblas aŭte. Ĝi troviĝas apud la marborda tramlinio, per kiu atingeblas
   la fervoja stacidomo de Oostende.
</p> 
<h3>Kanonego „Longa Max”</h3> 
<p>Post milito oni multe paroladas kaj skribadas pri la venkintoj, malpli pri la venkitoj.
   Samtiel pri la unua mondmilito. Ja estas la germanaj tombejoj, sed malfacile troveblas
   muzeo, kiu prezentas la germanan vidpunkton. Escepto estas malgranda muzeo en Koekelare,
   kiu ekestis ĉe la restaĵoj de germana kanonego el la unua mondmilito. La muzeon funkciigas
   skipo de volontuloj, helpata de diversfontaj subvencioj. Oni malfermis ĝin pasintsomere.
</p> 
<p>La kanonego, oficiale nomita „38-cm-SK-L/45”, estas produkto de la firmao Krupp. Origine oni intencis munti ĝin sur ŝipo, sed
   poste ĝi ankaŭ estis uzata sur trajno kaj sur fiksa fundamento. Ĝi ricevis la kromnomon
   „Longa Max” kaj devenas de vic-admiralo Maximilian Rogge, kiu respondecis pri la adapto de la
   origine surŝipa kanono al surtera uzo. Ne konsideru ĝin „Dika Berta”, alia produkto de Krupp.
</p> 
<p>„Longa Max” estis instalita en Koekelare post la detruo de simila kanono en 1915, kaj celis la
   nord-francan havenurbon Dunkerque, je distanco de 44 kilometroj. La unuaj obusoj al
   Dunkerque estis pafitaj la 27an de junio 1917. Fine de la milito la kanonego eluziĝis
   kaj iĝis malpreciza. Post la milito ĝi restis surloke kaj iĝis turisma allogaĵo, ankaŭ
   por eksterlandanoj. En 1941, en la dua mondmilito, la germanoj dispecigis ĝin kaj
   transportis la restaĵojn al Germanio por reuzi la metalon. Restis nur la fundamentoj.
</p> 
<h3>La vivo de germanaj soldatoj</h3> 
<p>En 1978 estis metita memorŝtono, en 2000 laborgrupo malfermis pacmuzeon, kaj en 2014
   ekfunkciis tute nova muzeo. Ĝi montras la vivon de la soldatoj ĉe la alia flanko de
   la fronto. Koekelare estas taŭga loko por tio, ĉar ĝi estis grava loko por la provizado
   de la germanaj soldatoj.
</p> 
<p>La muzeo dependas de volontuloj. Pro tio la vizithoroj estas limigitaj. Vizitonto
   kontrolu la retpaĝon. La muzeo situas je ĉirkaŭ 20 km oriente de Nieuwpoort kaj atingeblas
   nur aŭte.
</p> 
<p>En la centro de Koekelare estas muzeo parte dediĉita al la germana grafikistino Käthe
   Kollwitz (1867-1945), kies filo Peter mortis komence de la unua milito apud Diksmuide.
   Ŝia skulptaĵo memore al li nun troviĝas en la germana militista tombejo de Vladslo.
   La iniciatinto de la muzeo estis la loka instruisto Raf Seys (1928-2012). Eta libro
   de li pri la skulptaĵo estas tradukita en Esperanton.
   <sup>1</sup>
</p> 
