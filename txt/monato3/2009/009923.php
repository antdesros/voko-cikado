      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Ŝanĝoj okulfrapaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Post la venko de la Demokratia Partio (DP) en la ĝenerala elekto, Hatojama Jukio estis elektita meze de septembro la 93a ĉefministro de Japanio. La nova ministraro konsentis pri du bazaj principoj: „realigo de vera popola suvereneco” kaj „realigo de enhavoriĉa regiona suvereneco”. Kunligitaj estas tri sloganoj: „memstaro kaj kunvivado”, „rekreo de sociaj ligiloj” kaj „frateca socio”.</p> <p>Registaro gvidata de la Demokratia Partio estas historia evento. Dum la pasintaj 50 jaroj la Liberala Demokratia Partio (LDP) preskaŭ ĉiam regis Japanion. Tiu regado ŝajnis eterne daŭronta kaj ĝis la 90aj jaroj japanoj ne aparte interesiĝis pri politiko. Ili kontentis, vivante en paco kaj en ekonomia progreso.</p> <h3>Enketoj</h3> <p>Tamen iom post iom malaperis la optimismo kaj ĉi-jare japanoj elektis novan registaron, esperante pri pli homeca socio. Malgraŭ tio, ke laŭ enketoj preskaŭ duono de la japana popolo kredis, ke ne multo draste ŝanĝiĝos, tamen jam okazis kvazaŭ „revoluciaj” paŝoj politikaj.</p> <p>La plej okulfrapa koncernas la ĉesigon de la konstruado de la digo Janba en la gubernio Gunma. La digo estis planita en 1952 kaj estus preta en 2015. Ĝi estas jam ĝis 80 % kompleta, sed la nova registaro taksas la konstruadon misuzo de la ŝtata buĝeto. Ĝi konsumis jam la ekvivalenton de 3,5 miliardoj da eŭroj.</p> <h3>Maljunuloj</h3> <p>Ankaŭ leĝoj, kiuj mizerigis la vivon de japanoj dum la periodo de LDP, estos nuligitaj. Ili inkluzivas la leĝon „Helpo al handikapitoj”, kiu fakte ne helpis, sed trudis pli da elspezoj rilate prizorgadon. Ankaŭ la tiel nomata nova kuracada sistemo estos nuligita: tiu ĉi sistemo malaltigis la buĝeton por flegi maljunulojn kaj fariĝis unu el la kaŭzoj de la malvenko de LDP.</p> <p>Refarota estas la leĝo „Helpo al solpatrinoj”, kies aldona subvencio estas nuligita. Populara estas ankaŭ promeso malaltigi la ellasojn de karbona monooksido. LDP promesis 8-procentan malpliigon; la nuna registaro de Hatojama Jukio 25-procentan.</p> <p>Promesitaj estas ankaŭ aliaj reformoj kaj novaj leĝoj. Kun espero do japanoj nun spektas televidon kaj legas ĵurnalojn.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
