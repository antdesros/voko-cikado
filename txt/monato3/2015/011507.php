      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>La lastaj tagoj de Lillo</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <i>La minuto preskribita por la sturmo</i> estas sciencfikcia romano. Ĝin verkis la franco Jérôme Leroy ja en la franca lingvo.
   La originalo 
   <i>La Minute prescrite pour l'assaut</i> publikiĝis en 2008, kaj la elfrancigo, de Armela LeQuint kaj Ĵak Le Puil, aperis
   en 2014 per la Eldona Fako Kooperativa de SAT, la eldona fako de la sennaciisma organizaĵo.
   La sama paro, per la sama eldonejo, publikigis en 2010 alian elfrancigon, same sciencfikcian,
   de la sama aŭtoro: 
   <i>Sonĝoj de kristalo</i>. Tiu de 2010 estas, tamen, nur 90-paĝa novelo, 15,5 centimetrojn alta kaj 11,5 centimetrojn
   larĝa, dum tiu ĉi de 2014 estas 328-paĝa romano, 20 centimetrojn alta kaj 13 centimetrojn
   larĝa.
</p> 
<p>Okazis dum tepida lundmateno. Mi estis en laborejo, kiam la telefono sonoris. 
   <i>Alvenis korespondaĵo al vi</i>, informis mia laborkolegino. Mi iris al la malsupra etaĝo kaj prenis la korespondaĵon.
   Mi tuj malpakis ĝin kaj jen! Kiom bela ruĝa-griza kovrilo kun titolo en nigraj literoj,
   bone emfazitaj sur blanka fono! Mi foliumis la libron, kaj mi ŝatis ankaŭ la internon.
   Mi eĉ montris ĝin al mia kolegino kaj per spontana alparolo, sen celo treni ŝin al
   la verda sekto, klarigis, responde al ankaŭ spontane estiĝinta demando, kio estas
   Esperanto.
</p> 
<h3>Ĉiutagaĵoj</h3> 
<p>Tia eldonaĵo, kiun tiumatene mi ricevis, apartenas al tiaj, kiujn ni portu enmane,
   se ni volas porti pruvon pri la vivanteco de la lingvo, ĉar en la romano troveblas
   ĉiutagaĵoj bone konataj de nuntempuloj, kiaj estas, ekzemple, la retpoŝtaĵoj, kies
   bitajn kestojn, pro longa malĉeesto de la uzantoj, fojfoje plenigas la spamoj proponantaj,
   inter aliaj gravaj aferoj, la interesan servon igi „plilongigi sian penison” (paĝo 155). La libro estas aktualega romano laŭ la publikigo- kaj pres-datoj kaj
   temaro. Bonimpresas la kovrilo, la bindo, la folioj kaj la sobra ena redakto de la
   teksto. Preseraroj malabundas.
</p> 
<h3>Mislego</h3> 
<p>Mi konfesas, ke, je la unua fojo, kiam miaj okuloj rapide tralegis la priskribon de
   la libro, kiu troviĝas sur ĝia dorso, kaj kiu ne estas alio ol la komencaj alineoj
   de la romano, mi, misgvidite de mislego, komprenis, ke „Kleber renkontis Saharon iom antaŭ la fino de la mondo”, kiam vere temis pri „... renkonti Sarahon ...‚. Miaj okuloj vidis dezerton, kie devus esti virino. Kaj al la prepozicio ’antaŭ” mia cerbo donis lokan signifon anstataŭ tempan. Pro tiu unua romanlinio, mi ekimagis
   viron, kiu ekkonis la afrikan dezerton tie, en iu fora, mondfina loko, en loko malproksima
   de ĉio en la mondo, eble viron starantan sur sahara duno kun mallarĝaj okulkomisuroj,
   kiuj klopodas vidi fore.
</p> 
<p>Krome, la librokovrilo (kvankam bela – mi nepre ne stumblu sur kontraŭdiro) ne multe helpis: foto de fortikaĵo meze de nenio,
   krom seka tero kun malmultaj, malaltaj kaj dise naskiĝintaj arbustoj (aŭ herbaĉoj).
   Kaj kion miaj neŭronoradoj faris por kunmeti ĉi tiun unuan miskomprenitan linion kun
   la kunteksto de la sekvaj en senchavan aferon?
</p> 
<h3>Dediĉo</h3> 
<p>Mi malfermas la libron por serioza, nun sen mislego, plonĝo en la rakonton. La romano
   estas dediĉita „Al Hugo Cháves” kaj „Al Amy Winehouse”. Kongrua paro, ĉu ne? Ŝajnas, ke sur podio, ĉirkaŭita de ruĝaj standardoj, mi vidas
   la bolivaran revoluciulon krii akuzon kontraŭ la jankioj. Amy Winehouse, kun cigaredo
   en la buŝo, ŝvebante en alia realdimensio kaj ĉi tial sen konscii (ĉu konsciante?!),
   ke ŝia devenlando historie ligiĝas al la kritikatoj, spronas la patoson de la kunulo:
   jes, patrinfikulo! Fek' al tiuj fiaj jankioj! La homamas-vivuoj apogeas. Amy Winehouse
   profitas la amasan frenezon por pugno- kaj pied-bati ajnulon en la publiko. Hugo Cháves
   plu instigas la tumulton. Kaj ĉio ĉi okazas en la ĉielo, ĉar la omaĝatoj jam mortis
   (aŭ en la infero, depende de viaj politikaj vidpunktoj kaj via rilato al psikoaktivaj
   substancoj).
</p> 
<h3>Mondfino</h3> 
<p>La rakonto komenciĝas (kaj finiĝas) per la mondfino, la tempo kiam „la nomo de la aĵoj daŭris, dum la aĵoj mem de longe jam malaperis” (paĝo 13), la nesalubra tempo, kiam „perfekta mensa sano [...] estas maltrankviliga signo de frenezeco” (paĝo 122). Tiu fino ne estas la rezulto de natura kataklismo, ŝanĝo de la rivoluakso
   de la tero, abruptaj movoj en la tektonaj platoj, meteorkolizio, invado de aliplaneduloj
   aŭ io alia neregebla por la homoj.
</p> 
<p>La finon de la mondo, en la romano de Jérôme Leroy, kaŭzis la homoj mem dum periodo
   ne multe post la nuntempo, eble dum la 2020aj jaroj (paĝo 291). Tamen fino ekvenis
   eĉ al ĉi tiu imagita romana mondfino, ĉar la aŭtoro kreis okazontan rakonton kun veraj
   personoj: en ĝi ankoraŭ vivus Amy Winehouse kaj Hugo Cháves, kiuj mortis respektive
   en 2011 kaj 2013, ambaŭ post la publikigo de la franclingva originalo. Tie ĉi, la
   romano iĝis kontraŭvole pli fikcia ol scienca.
</p> 
<h3>Protagonistoj</h3> 
<p>La protagonistoj estas Kleber, mezaĝa profesoro kaj romanverkisto (kiu volis esti
   futbalisto, sed iĝis nur artŝatanto, kiu „pli prancas antaŭ pentraĵo ol antaŭ porna TTT-paĝaro” – paĝo 115) kaj Sarah, blonda, granda kaj ankoraŭ ne tridekjara juna virino, kiu estas
   leŭtenanto. Jérôme Leroy intence inversiis la, de la tradicia socio, atenditajn rolojn,
   donante al la viro pli kvietan okupon, en „apartamento plenplena da libroj odorantaj katpison” (paĝo 16) kaj al la virino la povon ordoni kaj esti obeata.
</p> 
<p>„En kiu lernejo vi instruas?” (paĝo 19), demandas Kleber al Sarah. Per tiu ĉi demando, Jérôme volas montri, ke
   oni kutimas ne atendi, ke virino kapablu fariĝi ano de soldata hierarkio, isto de
   profesio historie destinita al viroj.
</p> 
<p>Alia grava rolulo, mi nomu ankaŭ ŝin protagonisto, estas eldonistino en Parizo. Ŝi
   havas pri Kleber, de kiu ŝi eldonis libron, specialajn sentojn kaj atendis lin kiel
   savon el la katastrofo, veninta al Parizo. Kleber, aliflanke, kaj kvankam sen forgesi,
   ke li ŝuldas promesplenumon al la parizanino, travivas la lastajn mallumajn momentojn
   kun la leŭtenanto Sarah, en la urbo Lillo (kiun, probable la lastan fojon antaŭ la
   mondfino, vizitis, dum la 100a UK, la esperantistaro). „La kolĥozanino je nudaj mamoj”, nomo de la blogo de la eldonistino, „vagadis sola en la dezerto de apokalipso” (paĝo 75), dum ŝia dezirato estas kun alia virino.
</p> 
<h3>Seksumado kaj drogoj</h3> 
<p>En la unua parto de la romano, kiu konsistigas la unuan romanduonon, alternas tiuj
   du scenejoj: la unua en la lilla regiono, kun la paro; kaj la alia en Parizo, kun
   la eldonistino/blogistino. La intrigo estas superŝutita per cigaredfumoj, alkoholo,
   seksumado kaj seksrilataj aludoj, drogoj (kaj preskaŭ adoro al la drogdiino Amy Winehouse),
   altaj dozoj de antianksiiloj kaj pliaj tiaj aferoj, kiujn oni trovus ĉe homoj vivantaj
   en mondo, kiu alpaŝas al la fino, sendemande, ĉu temas pri tiu fino pli-malpli reala
   kaj pli-malpli fikcia de la romano, ĉu pri tute reala vivofino (kaj iasence ankaŭ
   mondofino, ĉar fino de unuopulaj mondoj), alveninta jam al iuj nuntempaj homoj (ĉar
   por aliaj mondanguloj la fikciaj ladvilaĝoj de romano estas reala fakto).
</p> 
<p>Ĉu mi estu naiva por distrumpeti hazardan rilaton inter la kongresurbo de la 100a
   UK kaj la tradukelekto de la romano? La urbo Lillo ne aperas okaze. Ĝi trapasas la
   tutan romanon. La urbo kaj la ĉirkaŭa regiono, ankaŭ kun mencioj pri Bulonjo-ĉe-maro,
   donas fonon al granda parto de la rakonto. En Lillo la protagonistoj ekkonas sin la
   unuan fojon (paĝo 13). En Lillo situas la apartamento de Kleber (paĝo 171), kaj same
   en Lillo ili por Sarah alian apartamenton serĉas por lui.
</p> 
<p>Al loko iom post Bulonjo-ĉe-maro, en fuorteto ĉe la marbordo, Kleber kaj Sarah iras
   por trinki kaj amori (paĝo 65). Diras la romano, ke en la ĉirkaŭaĵo de la lilla metropolo
   mizeraj ladvilaĝoj, kie troviĝas „ratoj dikaj kiel hundoj” (paĝo 45), multiĝas kiel rezulto de „ĉiudirekta elmigro, furioza, kun etnaj alfrontoj” (paĝo 48). La romano informas, ke Bulonjo-ĉe-maro „iĝis vere sinistra” (paĝo 57). Ke en la stacidomo Lillo-Eŭropo junulo eksplodigis sin dum atenco (paĝo
   165). Ke Lillo, malgraŭ la mondfino (kaj almenaŭ por mildigi ĉi fian kadron), ankoraŭ
   „odoras la historion” kaj la „poezion de urbaj devojiĝoj, trinkejoj najbaraj al noktokluboj kaj noktokluboj najbaraj
   al trinkejoj” (paĝoj 162/163). Kaj fine ke, je la alproksimiĝo de la fino de la roman-mondfino,
   Lillo, en kelkaj horoj, senhomiĝis pro amasa formigrado, forfuĝo for de la teruro
   (paĝo 269). Mi esperas, esperantistoj, ke vi bone ĝuis la lastajn tagojn de Lillo.
</p> 
<p>Kaj kia homa kataklismo! Estas la fino de civilizo, kiu ne kapablis alveni al konkordo.
   La rakonto prognozas kaj konturas la ekeston de kripligitaj homoj (pro mutacioj, kiuj
   estas rezulto de tropleno de radiado en ilia korpo); kompletan civitanan malordon
   en Francio (de Lillo vi vidis la kaosan situacion); kriman ekzaltiĝon de frenezaj
   knabetoj, kiuj pasigis horojn antaŭ komputilekrano, enmergiĝintaj en virtualan violentan
   ludon: intercivitanan militon en Belgio (paĝo 114); reciprokan sinekstermon inter
   Hindio kaj Pakistano per nukleaj bomboj (paĝo 155), probable pro la limkonflikto en
   Kaŝmiro (paĝo 91); kaj malintegriĝon de Usono, de kiu estas aparte interesa la politika
   mondfina konjunkturo.
</p> 
<p>Usono, kun escepto de Kalifornio, havus en la mondfino eduk- kaj san-indicojn apenaŭ
   superajn al tiuj de Malio. La lando timus la invadon de Venezuelo, kiu ankoraŭ estus
   komandata de Cháves (kiu siavice estus amiko de Schwarzenegger – ankoraŭ guberniestro de Kalifornio – ĝis la intimeco donaci kubajn cigarojn). Kaj plu en Kalifornio, en San-Francisko,
   okazus bombatenco, kiu igus „neloĝendaj” multajn el ĝiaj kvartaloj „dum la cent venontaj jaroj”, devigante la guberniestron formigri elde Usono (paĝoj 174 kaj 177).
</p> 
<p>Kaj, se ne sufiĉus tio ĉi por nin gapigi, restus io por legi, kio al mi aspektus neimagebla
   ĝis absurdo: ke ultrareligiaj subŝtatoj de Usono anstataŭigus la konstitucion per
   Levitoj (paĝoj 93, 174 kaj 175). Mi supozas, ke iuj cirkonstancoj, lokitaj en la romanestonteco,
   estas aludo al faktoj aktualaj en la tempo, kiam Jerôme verkis la romanon. Ekzemple,
   la prauloj de la usona prezidanto devenis de Mozambiko (paĝo 218), kio ŝajnas aludi
   al Barack Obama, kies prauloj venis de Kenjo.
</p> 
<h3>Leginda</h3> 
<p>Riĉa kaj leginda romano. Temas pri tri centoj da paĝoj, plenaj de citoj pri kantistoj
   kaj kantoj, skulptistoj kaj skulptaĵoj, verkistoj kaj libroj, aktoroj kaj filmoj,
   pentristoj kaj pentraĵoj, filozofoj kaj ideoj, politikistoj kaj insidoj. Mi feliĉas,
   ke mi alproprigis ĉi tiun romanon al mi. Mi sentas, ke, post la finlego, miaj kulturaj
   bicepsoj larĝiĝis je du centimetroj. Dankon, Armela LeQuint kaj Ĵak Le Puil.
</p> 
