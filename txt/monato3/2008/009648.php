      <h3>Arto</h3>
      <h3 class="tem">KONKURSOJ</h3>
      <h2>Arta futuro</h2>
      <!-- komenco de la enhavo -->

       

       <p>Tri mondfamaj arkitektoj partoprenis konkurson por la projekto de nova multflanka kultura centro en la litova ĉefurbo Vilnius. La centro fariĝus filio de novjorka modern-arta muzeo Guggenheim kaj sankt-peterburga Ermitaĝo. En ĝi lokiĝus ne nur ekspozicioj de tiuj muzeoj, sed ankaŭ bildarta centro de Jonas Mekas, litovdevena usona filmkreanto, artisto ofte nomata „baptopatro de usona avangarda kino”, kulturcentro de litovdevenaj judoj, junulara edukcentro, kaj du kinejoj de nekomercaj filmoj.</p> <p>Ĉiuj tri konkursantoj famiĝis per diversaj projektoj. Daniel Libeskind konceptis multajn dekojn da arkitekturaj muzeoj, kulturaj centroj, komercaj kaj loĝaj konstruaĵoj en norda Ameriko, Azio kaj okcidenta Eŭropo. Unu el liaj lastaj plej famaj verkoj estas la turo de libereco en Novjorko, konstruata sur la loko de la detruitaj nubskrapuloj. Kun Libeskind konkursis unu el la plej modaj en la mondo irakdevena brita arkitekto Zaha Hadid kaj la fama italo Massimiliano Fuksas.</p> <p>Tiu ĉi konkurso de arkitektaj projektoj estis organizita de fonduso Solomon R. Guggenheim kaj ŝtata muzeo Ermitaĝo. Ilia celo estis pritaksi arkitekturajn, ekonomiajn kaj kulturajn eblojn por fondo de la nova muzeo en Vilnius. La plej ĉefaj postuloj – la kosto de la projekto ne devus superi 65-70 milionojn da usonaj dolaroj kaj la areo – ne pli ol 15 000 kvadratajn metrojn. La reprezentantoj de la centro de bildarto de J. Mekas, kiu iniciatis la konkurson, ne dubas, ke por Vilnius, kiu sekvontjare fariĝos ĉefurbo de eŭropa kulturo, nova muzeo povus esti serioza instigo por allogi spertulojn pri moderna arto, kino kaj arkitekturo.</p> <p>Internacia komisiono decidis, ke la plej bona projekto estas tiu de la brita arkitekto Z. Hadid, premiito de la plej prestiĝa arkitekta premio Pritzker. Ŝiaj plej famaj verkoj estas la transporta muzeo en Glasgovo (Britio), la centro <i>Aquatics</i> por la Olimpiaj Ludoj de 2012 en Londono, la muzeo de artoj de la 21a jarcento en Romo, la muzeo Guggenheim en Tajvano.</p> <p>Anticipaj kalkuloj montris, ke realigo de la projekto kostus ĉirkaŭ 118 milionojn da usonaj dolaroj. Tamen ĉefministro de litova registaro Gediminas Kirkilas diris, ke la ŝtato povus trovi nur ĉirkaŭ 10-15 procentojn de la bezonata sumo. Alia parto devus veni de la urba municipo, privataj mecenatoj, kaj helpo de EU-fondusoj. La muzeo devus malfermiĝi en la jaro 2013.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
