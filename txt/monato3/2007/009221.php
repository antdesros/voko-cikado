      <h3>Moderna vivo</h3>
      <h3 class="tem">VIRINOJ</h3>
      <h2>Neniu konsolo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En 1993 la japana registaro agnoskis la ekziston de tiel nomataj konsol-virinoj, kiuj servis japanajn militistojn dum la dua mondomilito kiel seks-sklavinoj. Tamen lastatempe la nova japana ĉefministro, Shinzo Abe, neis ilian ekziston.</p> <p>Forte reagis Usono kaj Nederlando. En la usona senato oni apelaciis al Japanio oficiale agnoski la konsol-virinojn kaj ilin finance kompensi. Sed japanaj konservativuloj koleris, dirante, ke la virinoj estis nuraj prostituitinoj.</p> <p>Virino, kiu dum 16 jaroj batalas por la 200 000 virinoj, ĉefe el Koreio, estas amsterdamanino Berthy Korvinus. En 1944, kiel sesjarulino en tendumejo Muntilan sur Meza Javo, ŝi vidis, kiel junaj virinoj estis perforte pelvarbitaj de la japana armea polico al bordelo en Magelang.</p> <h3>Proceso</h3> <p>En 1991 Korvinus ĉeestis konferencon en Seulo, Suda Koreio, kiam unuafoje prezentis sin publike la tiel nomataj jungshindeoj, alivorte koreinoj perforte tiritaj el siaj domoj por fariĝi seks-sklavinoj. Tiam tri el la virinoj iniciatis en Tokio proceson kontraŭ la japana ŝtato. Unu jaron poste, la japana registaro agnoskis la ekziston de konsol-virinoj kaj pardonpetis per letero al la koreinoj, „rekrutitaj por prostituado kaj suferintaj nepriskribeblajn fiagojn”.</p> <p>En 1994 sinjorino Korvinus renkontis profesoron Yoshimi en Washeda-universitato en Tokio. Li posedis dokumentojn el armeaj arkivoj, kiuj nedubeble pruvas, ke ekzistis sistemeca plano pri deviga prostituado dum la dua mondmilito.</p> <h3>Milit-tribunalo</h3> <p>Cetere notis la Internacia Virina Tribunalo pri Militkrimoj de Japan-armea Seksuma Sklavigo, okazinta en 2000 en Tokio, ke la aliancanoj ne procesis kontraŭ japanaj respondeculoj dum la internacia milit-tribunalo en Tokio inter 1946 kaj 1948, „malgraŭ la fakto, ke ili posedis pruvojn pri ĉi tiu seksuma sklavigo”.</p> <p>La nuna japana ĉefministro, Shinzo Abe, admiras sian avon, kiu estas registrita kiel A-kategoria militkrimulo. Ne mirinde, ke la ĉefministro reprenas la iaman neadon pri konsol-virinoj. Subtenas lin oficiala japana raporto, publikigita en marto pasintjare, kiu asertas, ke mankas fizika pruvo pri la seksa ekspluatado de eksterlandaj virinoj.</p> <div style="text-align:right">Johan D<span class="mm">ERKS</span></div> 
