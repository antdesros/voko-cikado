      <h3>Scienco</h3>
      <h3 class="tem">TRIAGRADA FUMADO</h3>
      <h2>Kie fumo leviĝas, tie fajro (kaj malsanoj) troviĝas</h2>
      <!-- komenco de la enhavo -->

       

       <p>Bedaŭrinde por plej obstinaj cigared-ŝatantoj, esploroj pri la efikoj de fumado sinsekvas
   kun rezultoj, kiuj ĉiam substrekas multflankan danĝerecon kaj neniom da utilo. Post
   tiuj pri aktiva kaj pasiva fumado, studo publikigita en junio 2013 atentigis unuafoje
   pri la riskoj ligitaj al t.n. triagrada fumado: temas pri la fum-reziduo, kiu neflareble
   restas sur muroj, meblaro kaj ĉiaj objektoj de la hejmo, kaj al kiu nefumantoj senvole
   sin elmetas.
</p>
<p>Tiu ĉi maniero sin polui estas kaŝtrompa kaj, laŭŝajne, same aŭ eĉ pli danĝera ol
   rekta kaj nerekta fumado. En 2014 nova esploro efektivigita de la universitato de
   Kalifornio ne nur konfirmas la antaŭan esploron, sed ankaŭ pruvas, ke triagrada fumado
   provokas hiperaktivecon, gravajn domaĝojn en pulmoj kaj hepato, kaj, nelaste, maltrankviligan
   malfruon en la resaniĝo post iaj vundoj. Fumantoj do, eĉ post longa tempo, estas danĝeraj
   ne nur por si mem, sed ankaŭ por la personoj, kun kiuj ili vivas aŭ laboras. La kompletaj
   rezultoj de la esploro estas publikigitaj de la revuo 
   <i>Plos ONE</i>.
</p> 
<h3>Mortokvanto</h3> 
<p>La usonaj esploristoj verdiktas, ke aktiva kaj triagrada fumado kaŭzas minimume egalan
   mortokvanton (eventuale iom pli altan en la dua okazo). Oni tiurilate pristudis ĉe
   musoj la efikojn de triagrada fumado sur pluraj aparatoj iliaj, je kondiĉoj, kiuj
   celis ŝajnigi la homan eksponon al triagrada fumado. Manuela Martins-Green, profesorino
   pri ĉela biologio kaj respondeculo pri la koncerna esplorado, asertis, ke la plej
   trafitaj organoj estas pulmoj kaj hepato; ke ronĝulaj vundoj bezonis pli multe da
   tempo por cikatriĝi; kaj, fine, ke la bestetoj montris plurajn signojn de hiperaktiveco.
</p> 
<h3>Oficejoj</h3> 
<p>La ejoj, en kiuj oni fumadas, longe retenas kancerigajn substancojn ege riskajn por
   la proksimuloj, ĉefe por infanoj: oni trovis ĉe musoj elmetitaj al triagrada fumado
   nivelojn de tabako similajn al tiuj de infanoj eksponitaj al duagrada (do pasiva)
   fumado. Kompreneble tiuj ejoj ne nepre estas partoj de la hejmo, sed ankaŭ oficejoj,
   vendejoj kaj aŭtomobiloj, kie oni ankoraŭ havas la kutimon fumi, kvankam tio en pluraj
   landoj estas jam malpermesita.
</p> 
<h3>Cirozo</h3> 
<p>En la hepato de musoj oni malkovris nivelojn de graso pli altajn ol la ordinaraj kaj
   t.n. „dikan hepaton”, antaŭsignon de hepata cirozo, kancero kaj kor-vaskulaj malsanoj. En la pulmoj, la
   triagrada fumado ŝajnas respondeci pri ekscesa produktado de kolageno kaj pri altaj
   niveloj de inflamaj substancoj. Ĉio ĉi povas aperigi astmon kaj kronikan obstrukcan
   malsanon de la pulmoj.
</p> 
<h3>Lernejo</h3> 
<p>La hiperaktiveco montrata de infanoj eksponataj al triagrada fumado pensigas, ke tiu
   fakto povas kaŭzi neŭrologiajn ĝenojn. Aldona rimarkinda risko estas, ke oni trafos
   duatipan diabeton, kvankam ne estante dikventra. Multaj problemoj ligitaj al triagrada
   fumado ne tuj aperas, sed montriĝas kun plej gravaj konsekvencoj en maljuna aĝo. Tamen
   kelkaj el la efikoj estas frue konstateblaj: ekzemple la gefiloj de fumantoj malsaniĝas
   kaj tial perdas lecionajn tagojn en sia lernejo je 40 % pli ofte ol la infanoj de
   nefumantoj.
</p> 
