      <h3>Spirita vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Ankaŭ per armiloj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Aŭtune de 2014, pro la murdoj, perfortoj, atakoj kaj gajnoj de la tiel nomata Islama
   Ŝtato, la gazeto de la evangelia landa eklezio per ĉefartikolo opiniis: „Paco defendeblas ankaŭ per armiloj”.
</p> 
<p>Antaŭ nur malmultaj jaroj eldiro tia perdigus al redaktoro la postenon. Tiam la Germana
   Evangelia Eklezio kredis, per slogano „Krei pacon, ne minacon”, pacigi la tutan mondon. En la nova Germanio la eklezio transprenis la bibliodevenan
   gvidlinion „plugiloj el armiloj”.
</p> 
<p>La verkinto de la koncerna ĉefartikolo klarigis, ke la evangelia eklezio komencis
   ŝanĝi sian pacpolitikon kaj ke okazos ankoraŭ pli decidaj paŝoj. Memkompreneble ordinaraj
   kredantoj ne tiom rapide volas ŝanĝi sian nekontestatan bonan kurson. Protestoj pli-malpli
   akraj sekvis.
</p> 
<p>Sed okazis en novembro 2014 sinodo de la evangelia eklezio. Elektita estis kiel prezidanto
   la bavara landa episkopo Heinrich Bedford-Strohm. Li junaĝe evidentiĝis pacaktivulo
   kaj lastatempe ne malofte montris sin simpatianto de islamo, tiel konfuzante multajn
   eklezianojn.
</p> 
<p>Tamen, post propraj spertoj en la norda Irako, li forlasis striktan pacismon. Li ekkonis,
   ke „povas ekzisti religioj, kiuj, laŭvide de kristanismo, ne meritas estimon kaj, lezante
   homajn rajtojn, eĉ ne respekton”.
</p> 
<p>Krome li aprobis liveradon de armiloj al kontraŭuloj de la Islama Ŝtato, en bona kompanio
   kun la reprezentanto de la eklezio ĉe la germana federacia registaro kaj Eŭropa Unio,
   Martin Dutzmann. Kelkaj el la evangelia komunumo flaras perfidon, ke nun ankaŭ gvidantoj
   de germanaj protestantoj almenaŭ ideologie subtenas armilojn „en difinitaj aktualaj situacioj”, kiel tiuj religiaj funkciuloj prudente kaj memprotekte pravigas sian konvertiĝon.
</p> 
<p>La moralaj instruoj de la biblio kapablas montri idealan homan konduton. Sed la vangoj
   jesuaj ne povas esti ĉiuokaze la modelo. Frapon de du vangoj homo facile eltenas.
   Sed post la murdo de korpo kaj animo, ĉu de unuopa homo, ĉu de tutaj socioj, ne plu
   restas dua vango por pure pacama konduto.
</p> 
