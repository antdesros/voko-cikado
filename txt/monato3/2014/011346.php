      <h2>Kuraci - konscience aŭ krede?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Laŭ la pola konstitucio, „rilatoj inter ŝtato, eklezioj kaj aliaj religiaj organizaĵoj estas konstruataj surbaze
   de respekto de ilia aŭtonomio kaj reciproka sendependeco”.
</p> 
<p>Tamen pli kaj pli la limoj inter la pola eklezio kaj ŝtataj servoj malklare videblas.
   Lastatempe, okaze de la sanktuligo de la „pola papo” Johano Paŭlo la 2a, lia longtempa amikino kaj kuracistino Wanda Półtawska proponis,
   ke kuracistoj subskribu 
   <i>Deklaracion de kredo de kuracistoj</i>.
</p> 
<h3>Dekalogo</h3> 
<p>Subskribante la deklaracion, kuracistoj kaj studentoj de medicino agnoskas ses kredojn.
   Tiuj inkluzivas, ke la homa korpo kaj vivo estas sankta kaj netuŝebla donaco de Dio;
   ke la korpo dependas de la natura leĝo, kaj naturo estis kreita de Kreanto; ke la
   momento de gravediĝo kaj la momento de foriro el tiu ĉi mondo dependas 
   <i>nur</i> de la Dia decido; kaj ke se homo decidas fari abortigon, artefaritan koncipon, eŭtanazion
   aŭ uzi kontraŭkoncipilon, tio rompas la bazajn regulojn de la dekalogo.
</p> 
<p>Krome, „la bazo por digno kaj libereco de katolika kuracisto estas nur konscienco iluminita
   de la Dia Spirito kaj eklezia scienco, kaj kuracisto havas rajton agi laŭ sia konscienco
   kaj laŭ kuracista etiko, kiu enhavas reziston al agado kontraŭ la konscienco”. Plie, la subskribinto „rekonas prioritaton de la Dia leĝo super homa leĝo”.
</p> 
<h3>Hospitaloj</h3> 
<p>Ŝajnis komence, ke la deklaracio ne popularos. Tamen, surprize, pli ol 3000 kuracistoj,
   direktoroj de hospitaloj, ĉefoj de hospitalaj sekcioj k.s. subskribis. Se konsideri,
   ke ili estis klerigitaj per ŝtata mono kaj plejparte kuracas en ŝtataj hospitaloj
   kaj en la publika sanservo, tio kreas interesajn problemojn, ne nur teoriajn.
</p> 
<p>Ĉu paciento demandu, ĉu kuracisto estas katoliko, kiu subskribis la deklaracion kaj
   do malrekomendas ekzemple testojn dum gravedo pro medicinaj kialoj aŭ pro religia
   kredo?
</p> 
<h3>Kredantoj</h3> 
<p>Multaj kuracistoj kondamnis la katolikan proponon, dirante, ke ili estas unue kuracistoj
   kaj nur poste kredantoj (se ili entute estas kredantoj). Por ili plej gravas la bono
   de la paciento. Laŭ kelkaj la subskribintoj de la deklaracio eble redonu la monon,
   kiun la ŝtato pagis por ilia klerigado. Ili kreu privatajn kuracejojn kun klara indiko
   pri sia religibaza servo.
</p> 
<p>Jam en Pollando kuracistoj povas uzi t.n. leĝon de konscienco kaj rifuzi fari iujn
   operaciojn, ekzemple abortigon. Tamen ili estas devigitaj sendi pacienton al kuracisto,
   kiu faros ĝin. Sed, kiel diras multaj homoj, ekzemple reprezentantoj de la Pola Scienca
   Akademio en sia lasta raporto, tiu ĉi leĝo estas tro ofte uzata – kaj misuzata.
</p> 
<p>Kio sekvos? Ĉu venos deklaracio de katolikaj advokatoj, kiuj ne helpos dum divorcoj,
   kaj katolikaj juĝistoj, kiuj ne oficialigos divorcojn pro religiaj kredoj?
</p> 
