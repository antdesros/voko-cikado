      <h3>Lingvo</h3>
      <h3 class="tem">HINDIO</h3>
      <h2>Notoj indaj pri lingvaroj hindaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Oni ofte aŭdas aŭ legas pri 
   <i>la</i> hinda civilizo kaj pri 
   <i>la</i> hinda etno (aŭ eĉ raso). Temas pri akceptebla simpligo; penvaloras tamen memorigi,
   ke tiu de la hindoj ne estas difinebla kiel unuopa grupo, kaj ke, el la vidpunkto
   de la genetikaj karakterizoj listigitaj kiel markiloj de raso, ilia identeco estas
   nek homogena (fakte ekzistas rimarkindaj diferencoj inter la diversaj grupoj de loĝantoj),
   nek iumaniere rekonebla.
</p> 
<p>Pri la genezo de tiea civilizo, ekzistas reganta (sed pruvite erara) teorio, kiun
   ni povus nomi „rasa teorio pri la hinda civilizo”, laŭ kiu ĉi-lasta originis de la kunpuŝiĝo kaj posta miksiĝo de unu civilizita blanka
   raso kun alia „barbara” nigra raso. La rasa teorio estis konvinke dementita danke al la malkovro de la civilizo
   de la indusa valo, kiu malproksimigas al iu fora pasinteco la komencojn de hinda civilizo.
   Oni ja kredas, ke la hinda civilizo naskiĝis de la kombinaĵo inter almenaŭ tri malsamaj
   grupoj, kio estas konfirmita de la fakto, ke en Barato ekzistas tri grandaj lingvaroj
   sen parenceco inter si. En la ordo de malpliiĝo de grandeco temas pri la lingvofamilioj
   hindarja, dravida kaj munda. La celo de ĉi tiu artikolo estas mallonge prezenti al
   la legantaro ĉi tiujn tri gravajn lingvarojn.
</p> 
<h3>Baseno</h3> 
<p>La hindarja familio, kiu inkluzivas la hindian kaj la urduan, estas parolata en la
   supra baseno de la rivero Gango. La hindia estas la oficiala lingvo de la respubliko
   Barato. Fakte, la hindia kaj la urdua estas du dialektoj de ununura lingvo, kvankam
   ili diferencas kaj laŭ la skribmaniero (la hindia uzas la tiel nomatan nagarian skribsistemon,
   derivitan el la antikva brahmia, dum la urdua uzas varianton de la araba-persa skribsistemo)
   kaj laŭ la leksiko (la urdua prunteprenis multe da persaj kaj arabaj vortoj). Tendence,
   eblas aserti, ke la hindia kaj la urdua estas parolataj respektive de hinduoj kaj
   islamanoj.
</p> 
<h3>Mapo</h3> 
<p>La nomoj de aliaj hindarjaj lingvoj estas klare ligitaj al la respektivaj geografiaj
   deven-areoj: observante la mapon de norda Hindio de okcidento al oriento, ni trovos
   la marathan en Maharaŝtro, la guĝaratan en Guĝarato, la sindan en Sindo; en Penĝabo
   la penĝaban, en la valo de la rivero Gango la hindian, en Nepalo la nepalan; poste
   la bengalan en Bengalo kaj Bangladeŝo, kaj fine la orijan en Oriso (por nomi nur la
   plej gravajn). Al la sama familio apartenas ankaŭ la singala, kiun oni parolas multe
   pli sude, nome en Sri-Lanko.
</p> 
<h3>Hinduismo</h3> 
<p>Ĉiuj ĉi lingvoj estas idoj de la sanskrita, la lingvo de la sanktaj libroj de hinduismo,
   kiu estis enkondukita en Hindion ĉirkaŭ la jaro 1400 a.K. fare de homoj, kiuj nomis
   sin arjoj. Indas mencii, ke la sanskrita estas mem parto de la granda hindeŭropa lingva
   familio, kiu inkluzivas, krom la sanskritan kaj ĝiajn derivitajn lingvojn, ankaŭ la
   malnovan persan kaj la idojn de ĉi-lasta, disvastiĝintajn en Irano, Afganio kaj Pakistano,
   kaj, kiel certe konate, plej multajn el la eŭropaj lingvoj.
</p> 
<h3>Triboj</h3> 
<p>La dravida kaj la munda lingvaroj estis atestitaj en Hindio longe antaŭ la sanskrita.
   Hodiaŭ la grandaj dravidaj lingvoj estas parolataj en suda Barato: jen ekzemple la
   tamila (parolata en Tamilnado), la teluga (en Andra-Pradeŝo), la kanara (en Karnatako)
   kaj la malajala (en Keralao). Temas entute pri 26 dravidaj lingvoj, iuj el kiuj estas
   parolataj de triboj en centra Barato (kiel la gonda, parolata de pli ol 5 milionoj
   da homoj). Du dravidaj lingvaj „insuloj” situas cetere en la valo de Gango kaj en tiu de Induso.
</p> 
<h3>Fakuloj</h3> 
<p>Antaŭ la alveno de la sanskrita, la dravidaj lingvoj estis disvastiĝintaj tra la tuta
   Hindio, kvankam en la nordo ili laŭgrade cedis al la hindarjaj lingvoj. Al tio oni
   ŝuldas la fakton, ke hindarjaj lingvoj ja enkorpigis lingvajn trajtojn kaj prunteprenis
   multajn vortojn de dravidaj. Fakuloj ne sukcesis identigi la devenon de la lingvoj
   dravidaj: kelkaj kredas, ke ili iel rilatas al la lingvaro finn-ugra (entenanta lingvojn
   parolatajn en Finnlando, Hungario, Estonio, la ĉefaj ne hind-eŭropaj lingvoj ĉeestaj
   nun en Eŭropo), aliaj preferas pripensi ligitecon kun la elamita (lingvo iam parolata
   en sud-okcidenta Irano). Ĝenerale, tamen, ĉiuj opinias, ke la dravidaj lingvoj alvenis
   Hindion de okcidento aŭ de nordokcidento, ja antaŭ ol la sanskrita.
</p> 
<h3>Kamboĝo</h3> 
<p>La munda lingvaro, aliflanke, havas rilaton kun aŭstral-aziaj lingvoj de Asamo kaj
   Hindoĉinio (inkluzive de la ĥmera parolata en Kamboĝo), kaj estas preskaŭ certe, ke
   ĝi alvenis al Hindio el oriento antaŭ la sanskrita, kiu importis diversajn vortojn
   el lingvoj dravidaj kaj mundaj. Ĉi tiuj lingvoj, la mundaj, estas parolataj de tribaj
   gentoj en centra kaj centr-orienta Barato.
</p> 
<h3>Fandiĝo</h3> 
<p>De jarcento al jarcento, la diversaj popoloj kaj iliaj lingvoj forte miksiĝis, kaj
   tio, kion ni neprecize nomas hinda civilizacio, kvazaŭ temus pri homogena afero, estas
   la rezulto de ĉi tiu komplika fandiĝo.
</p> 
<h3>Aspiracio</h3> 
<p>La nomo 
   <i>Hindio</i> deriviĝas de la vorto 
   <i>sindhu</i>, kiu en la sanskrita havas la signifon de „rivero” kaj estis ne hazarde la nomo donita al Induso: ĉi-lasta estis antonomazie nomita
   tiel („<i>la</i> rivero”), pro sia grandeco. En la transpono de la vorto en la antikvan persan, la komenca
   
   <i>s</i> de sanskritaj vortoj fariĝis 
   <i>h</i>; la persoj nomis do 
   <i>Hindush</i> kaj la riveron kaj la teron, kiun ĝi transiris: tiun nomon la grekoj igis poste 
   <i>Indos</i>, forigante kiel kutime en la prononco la komencan 
   <i>h</i> (rekoneblan tamen en la tiel nomata kruda spirito, kiu akompanas ĉi-okaze la komencan
   vokalon 
   <i>i</i>). En la greka, oni parolas pri 
   <i>Indía</i>, 
   <i>Indós</i>, 
   <i>Induismós</i>, ĉiam sen aspiracio. Ankaŭ en latino ĉiuj formoj estas senaj je komenca 
   <i>h</i>.
</p> 
<p>En Esperanto ŝajnas, male, ke oni kurioze utiligis la grekan etimologion (kun la verŝajna
   perado de la responda latina vorto) por indiki la riveron (Induso), sed la sanskritan-persan
   formon por indiki geografiajn (<i>Hindio</i>, 
   <i>Hindustano</i>, 
   <i>Hindoĉinio</i>), lingvajn (<i>hindia</i>, 
   <i>hindarja lingvaro</i>) kaj religiajn terminojn (<i>hinduismo</i>, 
   <i>hinduo</i>).
</p> 
<h3>Letero</h3> 
<p>Simila duobligo ekzistas en la ĉina lingvo, kie Hindion oni nomas en du manieroj:
   
   <i>Sin-tu</i> kaj 
   <i>Yin-tu</i>: la unua nomo estas klare pruntita el la sanskrita, la dua el la persa aŭ el la greka.
   La aŭtoro ne scias, tamen, ĉu ekzistas diferenco en la uzado de la du vortoj: legantoj
   el Ĉinio povus klarigi per letero al 
   <i>Monato</i> tiun aspekton. La araboj, sekvante la ekzemplon de la persoj, parolis siaflanke pri
   
   <i>Al-hindi</i>, ĉiam konservante do la aspiracion.
</p> 
