      <h3>Eseoj</h3>
      <h3 class="tem">CENTJARIĜO</h3>
      <h2>La unua mondomilito: kiu ĝin kaŭzis?</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la dimanĉa mateno de la 28a de junio 1914 aŭtokavalkado rapidegis tra la stratoj
   de Sarajevo. En la ĉefa aŭto veturis unu el la plej potencaj homoj en Eŭropo, ĉefduko
   Francisko Ferdinando, rekta heredulo de la aŭstra-hungara trono. Sen lia scio sep
   bosnaserbaj murdontoj firme decidintaj eksigi Bosnion el la aŭstra-hungara imperio
   embuske lin atendis.
</p> 
<p>Kiam la aŭtokavalkado haltis ĉe trafikvigla strato, unu el la murdontoj, la 19-jara
   Gavrilo Princip, elpaŝis el la homamaso spektanta la karavanon de nobelaj vizitantoj
   kaj pafis dufoje je nula distanco celdirekte al la ĉefduko kaj lia edzino Sofia. Spite
   la tujan reagon de la korpogardistoj de la ĉefduko por revivigi lin, sango elfluis
   tra lia uniformo. Post kelkaj minutoj la sonoriloj de preĝejoj tra Sarajevo sonorigis
   la morton de la paro.
</p> 
<p>Kiel la morto de unu homo povis konduki al milionoj da mortintoj en milito tiel grandampleksa
   kaj kruela? Por trovi la respondon al tiu ĉi demando cerbe ekzercas sin historiistoj
   dum 100 jaroj.
</p> 
<h3>Agresantoj</h3> 
<p>En julio 1914 ĉiuj en Eŭropo estis konvinkitaj, ke oni batalas en sindefenda milito.
   Registaroj laboris pene por montri, ke ili ne estis la agresantoj en la komencaj momentoj
   de la milito, precipe dum la monatoj julio kaj aŭgusto. Tio estis la kerna supozo
   de la ideo, ke vastaj armeoj da soldatoj, bezonataj por la milito, ne povus esti rekrutataj
   por agresmilito.
</p> 
<p>Socialistoj, kiuj nombris plurajn milionojn en 1914, ne estus apogintaj agresan eksterlandan
   politikon. Ili emus aliĝi nur al sindefenda milito. Francoj, belgoj, rusoj, serboj
   kaj britoj estis konvinkitaj, ke ili vere estas implikitaj en sindefenda batalo por
   justaj celoj. Aŭstroj kaj hungaroj estis batalantaj por venĝi la morton de Francisko
   Ferdinando.
</p> 
<p>Germanoj estis konvinkitaj, ke najbaraj landoj „trudis la glavon” en la germanajn manojn, kaj certis, ke ili ne komencis la militon. Tamen, se ne ili
   (kiuj malgraŭ ĉio invadis Belgion kaj Francion en la komencaj semajnoj de la milito),
   kiu, do, kaŭzis la militon?
</p> 
<h3>Kompensoj</h3> 
<p>Por la venkintoj, la demando facile respondeblas. Ili interkonsentis ĉe la packonferenco
   de Versajlo en 1919, ke Germanio kaj ĝiaj alianciĝintoj respondecis kaj kaŭzis la
   militon. Surbaze de tiu decido oni postulis vastajn kompensojn pro la perdoj rezultintaj
   de la milito. La t.n. „militkulpa deklaro” direktis la longan debaton pri la kaŭzoj de la milito.
</p> 
<p>Ekde 1919 registaroj kaj historiistoj, okupiĝantaj kiel reviziistoj pri tiu demando
   (ili volis revizii la verdikton de Versajlo), koliziis kun kontraŭreviziistoj konsentintaj
   kun la trovoj de la venkintoj. Sennombraj publikaĵoj kaj dokumentoj fariĝis haveblaj
   por pruvi la senkulpon de Germanio kaj la kulpon de aliaj.
</p> 
<h3>Interpretado</h3> 
<p>Dum la intermilita periodo similaj opinioj influis novan interpretadon, plu emfazante
   la senkulpon de Germanio kiel kaŭzinto de la milito, kaj anstataŭe identigante la
   malsukceson de la alianciĝa sistemo antaŭ 1914. Tiu interkonsentita opinio daŭris
   tra la dua mondomilito kaj poste.
</p> 
<p>La plej granda kontesto al tiu interpretado estis jam vaste konata en Germanio ekde
   la 1960aj jaroj. La historiisto Fritz Fischer publikigis tute surprize novan tezon,
   kiu minacis renversi la interkonsenton pri la veraj kaŭzoj de la milito.
</p> 
<h3>Naciismo</h3> 
<p>Germanio, Fischer argumentis, ja havis la plej grandan parton de la respondecoj pri
   la eksplodo de la milito. Aldone, la gvidantoj de Germanio intence komencis la militon
   kun la celo adopti viglan internacian politikon mirege similan al la naciismo de Hitlero
   en 1939.
</p> 
<p>En la lastaj jaroj la opinio de Fischer siavice ankaŭ estis reviziata. Historiistoj
   revenis al la argumentoj de la intermilitaj jaroj. Nun la fokuso estis la rolo de
   Rusio kaj Francio en la komenco de la milito kaj la demando, ĉu la brita registaro
   vere estis faranta ĉion eblan por eviti la militon en 1914. Oni ankaŭ malemfazis la
   rolojn de Germanio kaj Aŭstrio-Hungario.
</p> 
<p>Post 100 jaroj da debatoj ĉiuj imageblaj interpretoj de la kaŭzoj de la milito ŝajne
   estas pluesploritaj, reviziitaj kaj forigitaj. Ĉi tiu polemiko daŭros almenaŭ pliajn
   100 jarojn.
</p> 
