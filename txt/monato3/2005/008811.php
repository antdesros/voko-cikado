      <h2>Katalunio, Okcitanio  Kaj Tiel Plu</h2>
      <!-- komenco de la enhavo -->

       

       <p>La dua disko de la grupo <i>Kaj Tiel Plu</i> nomiĝas <i>Plaĉas al mi</i>. Ni povus simple doni al tiu ĉi recenzo la saman nomon, ĉar la disko ekstreme plaĉas al ni kaj plaĉos, ni ne dubas, al ĉiuj, kiuj jam konas <i>Kaj Tiel Plu</i>. La dua KD laŭ la stilo, enhavo, organizo, kantmaniero kaj ĝenerala mirinda impreso similas al la unua, <i>Sojle de la klara temp'</i>, do, kiam parkeriĝos unu disko, nepre aĉetu la duan! Interalie, la dua disko estas je 16 minutoj pli longa.</p> <p><i>Plaĉas al mi</i> estas diversa: ĝi enhavas kantojn kaj instrumentaĵojn, tion, kio estis kantata mezepoke kaj dum la hispana civila milito, tradiciajn, precipe katalunajn kaj okcitanajn, kaj aŭtorajn muzikaĵojn. Aliflanke, ĉio konsistigas unuecan tuton, aŭskultiĝas per unu spiro, verŝajne pro ega pasieco de la plenumantoj.</p> <p>Por deziranto konatiĝi kun muzikaj tradicioj de Katalunio kaj Okcitanio tra la tempoj tiu ĉi disko estas bonega rimedo. La grupo prezentas verkon de la trobadoro de la 12a jarcento Berenguer de Palou; la faman mezepokan kanton <i>Cuncti simus concanentes</i> el la <i>Ruĝa libro</i> el la monaĥejo de Montserrat (la 14a jarcento); popolkanton el la hispana milito, kiu originas el tradicia andaluzia amkanto <i>Romanco de kontrabandistoj de Ronda</i> de la 19a jarcento, kaj multajn tradiciajn dancojn kaj kantojn, kiuj sonas tute nature, brile, arde. Tio tre malsimilas al iuj kompaktaj diskoj kun popolaj kantoj, vendataj por „gastoj de la lando”, tute ne transdonantaj la spiriton de la regiono. Kiom ofte ni aŭdas sur ili akademiajn korusojn, kiuj, kvankam ili kantas tradiciaĵojn, sonas tro profesie kaj seke, aŭ, male, lokajn maljunulojn, kiuj kantas vere „popole”, tamen ĝenerale sen akompano kaj iom tede.</p> <p>La kantojn <i>Kaj Tiel Plu</i> prezentas ĥore (jen unisone, jen plurvoĉe) aŭ sole (tre emocie kaj tuŝante la animon) kun bela instrumenta akompano. Tuj ĉe aŭskultado aperas la deziro kunkanti, kaj tio eblas, ĉar en enmetita kajereto troviĝas tekstoj de ĉiuj kantoj. Oni povas kanti kune kun la artistoj en Esperanto aŭ en la lingvo de la originalo. Vere, tiuj kantoj kaj melodioj loĝiĝas en la memoro, kaj vi kaptadas vin je tio, ke vi ĉiuminute kantas ilin!</p> <p>Tiun ĉi diskon oni povas aŭskulti denove kaj denove, iĝante en tiu tempo vera kataluno, aŭ okcitano, aŭ sefardo, aŭ kastiliano, aŭ ekleziulo, aŭ trobadoro.</p> <div style="text-align:right">Anna kaj Mati P<span class="mm">ENTUS</span></div> <h6 class="bib">Kaj Tiel Plu: Plaĉas al mi. Eld. Vinilkosmo, Donneville, 2004. Kompakta disko, 45 minutoj.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=ktp">la Retbutiko</a>.
