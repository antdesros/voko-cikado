      <h2>Plia sukceso de Jacques Yvart</h2>
      <!-- komenco de la enhavo -->

       

     <p>La antaŭan diskon <i>Jacques Yvart kantas Georges Brassens</i> (Jacques diris: „Georges Brassens, mia artista baptopatro”) aperintan en 1998 mi tiel ŝatis (ne nur pro tio, ke Georges Brassens [ĵorĵ braséns] estas unu el miaj plej ŝatataj francaj kanzonistoj, sed ankaŭ pro la kantado de Jacques Yvart [ĵak ivár]), ke mi entuziasme kaptas la ŝancon recenzi la novan diskon de Yvart. Evidentiĝis, ke ankaŭ tiu ĉi, lia dua disko en Esperanto, havas kanton laŭ la muziko de Georges Brassens (<i>La ĉevaleto</i>) kune kun 14 aliaj kanzonoj de diversaj aŭtoroj kaj en variaj poeziaj stiloj kaj muzikaj ritmoj. „De longa tempo mi estas vojaĝanto, kaj tiu disko estas la okazo por pluvojaĝi tra la mondo. Kaj Esperanto tre taŭgas por tio.”, diras Jacques.</p> <p>Kelkajn el la muzikaĵoj sur la disko Jacques Yvart ne nur plenumis (kante kaj gitar-akompane), sed ankaŭ aŭtoris (muzikon, tekston aŭ esperantigon). Mi aparte ekŝatis la sentimentalan diskotitolan baladon de Charles Baudelaire [ŝarl bodlér] (adapto de Marcelo Redoulez [redulé] laŭ traduko de Kalocsay), la plaĉe kortuŝan <i>La am' por la am'</i> kaj ankaŭ la ironian <i>La korpo Eŭropa</i> kun fajraj ritmo kaj melodio (ĉiuj tri komponitaj de Yvart).</p> 
<p>La varieco de uzitaj muzikinstrumentoj (violono, akordiono, pian-klavaro, drumo, kontrabaso, bas-gitaro, mandolino, buzuko, citro) tute ne kaŝas la agrablan, amikecan basan voĉon de Yvart. Lia prononcado estas klara kaj internacieca. La melodioj kaj muzika aranĝo estas vere ĝuindaj kaj la tekstoj belsonaj kaj pensigaj. Mi rimarkis nur tre malmultajn lingvajn malglataĵojn: notindaj estas la ripeta uzo de <i>kurento</i> en la senco de (evidente neelektra) fluo en <i>Homo kaj Maro</i> („porte sur la kurent' al la Maro Sargasa”) kaj la misuzon de <i>bruli</i> kvazaŭ ĝi estus transitiva en „la vero ilin ekbrulas” en <i>Magelanoj de l' Universo</i> (tion mi sufiĉe ofte rimarkis ĉe franclingvanoj). La instrumento-nomo <i>cistro</i> sur la dorsa kovrilpaĝo verŝajne devus esti <i>citro</i> (la fundamenta vorto, kiun – laŭ Reta Vortaro – pluraj vortaroj uzas ankaŭ por la instrumento, kiu france nomiĝas <i>cistre</i>).</p> 
<p>Kelkloke la akcentaj silaboj de vortoj trafas la neakcentajn poziciojn de la verso – konjekteble la aŭtoroj/tradukintoj ne zorgis eviti tion pro la influo de la silaba franca metriko, kiu esence ne konas vortajn akcentojn. Tio, kvankam fremda al la tradicia Esperanta metriko, plejparte ne impresis min agace kaj eĉ spicis la kantadon per aldona recitativeca karaktero.</p> <p>Krom la kantoj, sur la disko troviĝas tre plaĉa „videosesio” (plejparte intervjuo kun Jacques Yvart, interalie rivelanta lian homecan sintenon kaj okulfrapan karismon), kaj ankaŭ aparta dosierujo kun karaokaj variaĵoj de ĉiuj kantoj. La diskon akompanas eleganta broŝureto kun tekstoj de ĉiuj kantoj, karikaturaj portretoj de la kunlaborantoj kaj bazaj informoj pri la disko kaj la roloj de la kontribuintoj.</p> 
<p>Ĉiuj kantoj sur la disko sonas freŝe, sincere kaj elkore kaj estas memoreblaj, pensigaj kaj ĉarmaj. Mi gratulas Jacques Yvart kaj ĉiujn kunlaborintojn pri la nova sukceso! Resume: aŭskultinte tiun ĉi diskon plurdekon da fojoj – kaj ĉiam ĝue! – mi varme rekomendas ĝin ankaŭ al vi!</p> <div style="text-align:right">Alexander S<span class="mm">HLAFER</span></div> <h6 class="bib">Jacques Yvart: Invito por vojaĝo. Eld. Vinilkosmo, Donneville, 2008.</h6>

