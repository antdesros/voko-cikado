      <h3>Leteroj</h3>
      <h3 class="tem"></h3>
      <h2>Ĝisostaj eksperimentoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Estas malfacile ne reagi al la saĝaj konsideroj de nia redaktoro-ĉefo enkondukantaj la monatajn M<span class="mm">ONATO</span>jn, kaj estas eĉ pli nekontentige ne komenti liajn emociajn memprezentojn samloke. Sed apud ne-Pigraj kaj pliaj konkurenculoj rilate seriozan artikolspacon, mi ne artikolumante, sed nur legantoletere volas komenti la ĉefulan enkondukon al M<span class="mm">ONATO</span> 2012/08-09.</p> <p>Sufiĉas citi la konfesojn „Mi, entuziasmulo pri la granda eksperimento Eŭropa Unio” kaj „Mi, ĝisosta socialdemokrato”. Kara klera kungeneraciulo, ĉu vi ne lernis, ke ne nur la aĉaj kapitalistoj desupre, sed ankaŭ bonkoraj socialigemuloj desube kapablas detrui la mondon? Ĉu ni en Eŭropo estu solidaraj kun ŝtataj krimuloj, kiuj fraŭdas, forrabas, elpremas komunan monon per trompoj, mensogoj, trukoj, faroj, misfaroj, malfaroj? Ĉu vi vere volus miksi vian privatan monujon kun tiu de tiaj uloj? Kaj vi proklamas tiun postulon nome de socialdemokrataj partioj, kiuj arogas al si esti „La partioj de la sociala justeco”? Ĉu estas juste kaj ĝuste, ke iuj uloj servadas sin el la poŝoj ne nur, eble prave, de senmerite riĉiĝintoj, sed ankaŭ de suferantaj samklasanoj kun malalta vivnivelo, ekzemple mem ĝuante pensiiĝon jam de 55-jariĝo kaj lasante pene labori samajn Eŭropanojn ĝis 67? Kiuj ricevas senpage sanajn servojn por la familiego, kiujn vi pagas trioble aparte; kiuj enkasigas subvenciojn senkontrole nur por surpaperaj, fikciaj aranĝoj? Kaj tion vi nomas „Eŭropa eksperimento”? Nia tuta vivo estas eksperimento. Sed honestaj homoj eksperimentas per si mem, kaj espereble ne per la vivo de aliuloj. Ni bezonas organizi klare, travideble, serioze, sentrompe la ielan kunfandiĝon de nia malnova kontinento. Jes, ja, eble eĉ eksperimente, sed sen tia aktuala rezulto de financa kaoso, politika mensogado kaj publica hipokriteco plus la detruo de multege da vivokonceptoj.</p> <p>Ĉu tian sobran taksadon de ĝisfunda skeptikulo, neniam al io ĝisosta, oni rajtas peti ankaŭ de socialdemokratia idealisto?</p> <div style="text-align:right">Franz-Georg R<span class="mm">ÖSSLER</span></div> <div style="text-align:right"><i>Germanio</i></div> 
