      <h3>Ekonomio</h3>
      <h3 class="tem">MONO</h3>
      <h2>Donaco-ekonomio: alia mondo eblas!</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Pro la ekonomia krizo de la pasintaj jaroj, la grandskala trompado, fraŭdo kaj ekologia
      konsciiĝo, pli kaj pli da homoj serĉas alternativajn vivmanieron kaj ekonomion. Aŭ,
      pli ĝuste, „nekonomion”: vivi sen elspezi monon.
   </b>
</p> 
<h3>Unuaj iniciatoj</h3> 
<p>En la jaro 1982 la kanadano Michael Linton elpensis LETS: 
   <i>Local Exchange Trading System</i> (Loka Interŝanĝa Sistemo).
   <sup>1</sup> La sistemo ne uzas monon, sed iuspecan krediton. Membroj akiras krediton donacante
   ion aŭ farante komplezon al alia membro, ekzemple tondi harojn, varti infanojn, instrui,
   ripari ion. Per la kredito ili povas peti komplezon de alia membro. La kredito ne
   estas mono, sed fakte funkcias same, kaj depende de la loko, la „valuto” havas alian nomon, kutime ligitan al la loko.
   <sup>2</sup> En Nederlando la unua grupo fondiĝis en 1992; Flandrio sekvis du jarojn poste kaj
   nun havas preskaŭ 40 grupojn.
</p> 
<p>Malavantaĝoj de la sistemo estas la fojfoje peniga aliĝoproceduro, la fakto, ke postulado
   kaj proponado ne ĉiam kongruas, kaj ke ne ĉiuj havas la samajn eblojn kontribui. La
   sistemo postulas aliĝkotizon kaj striktan librotenadon pri ĉiuj unuopaj komplezoj.
   Tiuj ĉi malavantaĝoj malhelpas la ĝeneraligon de la sistemo.
</p> 
<p>En Germanio, antaŭ jardeko, mi trovis libroŝrankon en iu strato, kie homoj rajtis
   libere preni kaj meti librojn. Ie-tie, homoj ankaŭ metis ankoraŭ uzeblajn aferojn
   (seĝojn, manĝilojn ktp) en interkonsentitaj lokoj, por ke aliaj prenu kaj uzu.
</p> 
<p>Antaŭ ses jaroj en Utrecht, Nederlando, estis fondita „donaco-vendejo”. Homoj rajtis libere veni kaj preni tion, kion ili bezonas, sen ajna valuto, kredito,
   librotenado kaj tute sen kontrolo. La iniciatintoj mem surpriziĝis pro la granda amaso
   da aferoj (vestaĵoj, libroj, manĝiloj, ...), kiujn homoj pretis donaci por tiu celo.
</p> 
<h3>Donaco-placoj kaj -vendejoj</h3> 
<p>Estas malfacile ekscii, ĝuste kiam kaj en kiu urbo komenciĝis la donaco-placoj kaj
   -vendejoj. Eble tiu nova fenomeno aperis samtempe diversloke. Jen superrigardo pri
   la plej menciindaj iniciatoj.
</p> 
<p>Antaŭ kvin jaroj, komence de decembro, unuafoje okazis en Antverpeno endoma donacobazaro
   antaŭ Kristnasko, kiel alternativo kontraŭ la grandega, troigita komerco kaj la aĉetado
   de multe da donacoj, el kiuj multaj ofte ne estas vere utilaj kaj kelkfoje eĉ neplaĉaj.
   Ĉe enveno oni rajtis donaci uzeblajn aferojn, kaj oni ricevis du kuponojn por elekti
   aĵon el la bazaro por kunporti hejmen.
</p> 
<p>Ekde 2010 okazas en Bonaero la unua donaco-bazaro en Argentino, kiu intertempe okazadas
   ankaŭ en aliaj argentinaj urboj. La ideo estis la jena: plej multaj homoj havas aferojn
   en la domo, kiujn ili ne uzas. En la kelo aŭ subtegmentejo, sed ankaŭ en tirkestoj,
   sur ŝrankoj, sur librobretoj ktp kuŝas la plej diversaj objektoj. Iujn librojn oni
   eble legas plurfoje kaj nepre volas konservi, sed multajn aliajn librojn oni povos
   donaci, por ke alia persono legu ilin. Io, kio ne plu utilas al unu, povas ja feliĉigi
   alian personon. Tiumaniere la troaj aferoj ne plu kolektas polvon, sed servas al sia
   celo.
</p> 
<p>La 6an de februaro 2013 grekaj bienistoj senpage disdonis tunojn da fruktoj kaj legomoj
   sur la placo ĉe la ministrejo de agrikulturo en Ateno al 5000 homoj por protesti kontraŭ
   la altaj karburaĵoprezoj, kiuj troaltigas la transportkostojn kaj sekve la prezojn
   de la fruktoj kaj legomoj.
</p> 
<p>Oni mencias tion kiel inspiron por la publikaj donacoplacoj, kiuj nun pli kaj pli
   ofte okazas en Flandrio kaj sendube ankaŭ en aliaj landoj. En kaj ekster Antverpeno
   la donacobazaroj fariĝis vere furoraj. Plurfoje monate jam homoj venas eksteren sur
   diversajn placojn kaj en diversajn (kultur)centrojn por donaci siajn aferojn kaj por
   serĉi inter la proponataj varoj, eĉ meze de la vintro.
</p> 
<p>En Antverpeno malfermiĝis antaŭ nelonge ankaŭ du donacovendejoj, el kiuj unu estas
   por la publiko kaj la alia por homoj en finance malfacila situacio. Plie troviĝas
   ankoraŭ unu eksterdoma „donacoŝranko” – fakte pluraj ŝrankoj – sub tegmento ĉe ponto norde de la urbo. Tiuj ĉi iniciatoj estas politike sendependaj,
   sed ja devenas de homoj kun pli maldekstraj ideoj.
</p> 
<h3>Interreto 2.0</h3> 
<p>Danke al la lastaj evoluoj en Interreto fariĝis pli facile trovi homojn kun la samaj
   ideoj. Dum antaŭe sendube ekzistis diskutgrupoj, en kiuj homoj povis peti kaj proponi
   librojn kaj aliajn aferojn, la ebloj ege multobliĝis pere de la nun furora retejo
   
   <i>Facebook</i>. En aprilo 2012 fondiĝis la grupo 
   <i>Freecycle Antwerp</i>, kunfandiĝo de 
   <i>free</i> (senpaga) kaj 
   <i>recycle</i> (recikligado), kiu nun, eĉ ne du jarojn poste, havas jam 11 000 membrojn. Por kompari,
   la provinco Antverpeno havas 1 780 000 loĝantojn. Ni povus do konkludi, ke aliĝis
   0,6 % de la loĝantoj de la provinco. Vere, la denseco de la membraro ĉirkaŭ la urbo
   Antverpeno estas la plej granda, tiel ke eblas aserti, ke jam ĝis 1 % de la familioj,
   kiuj loĝas en kaj apud Antverpeno, membras en la grupo.
</p> 
<p>La grupo fakte estas virtuala donaco-placo. Homoj proponas aferojn, kiujn ili ne plu
   volas, kaj anstataŭe ricevas pli da spaco en la domo kaj dankan vizaĝon de la ricevinto,
   foje ankaŭ surprizdonaceton. Aliaj membroj, kiuj serĉas iun specifan kolektaĵon, kiuj
   bezonas ekzemple televidilon, ĉar difektiĝis la malnova, kiuj ne havas la rimedojn
   aĉeti novajn vestaĵojn aŭ ludilojn por la infanoj, povas simple peti tion en la grupo.
   Kutime, por ne tro maloftaj demandoj, venas respondo ene de unu, maksimume kelkaj
   horoj, kaj ene de kelkaj tagoj la transdono okazas. Ĉio ĉi sen mono, sen aliĝproceduro,
   simple sufiĉas sendi aliĝpeton, la kutima maniero, laŭ kiu eblas aliĝi al ajna grupo
   de 
   <i>Facebook.</i>
</p> 
<p>En pluraj aliaj regionoj en Flandrio fondiĝis loka 
   <i>Freecycle</i>-grupo, kaj krome ekzistas similaj grupoj kun aliaj nomoj, kiel ekzemple „senpaga” aŭ „donacogrupo”. Per tiaj ŝlosilvortoj eblas serĉi similajn grupojn en ĉiu ajn regiono.
</p> 
<p>Intertempe fondiĝis ankaŭ la grupo 
   <i>Freecycle Foods Antwerp</i>, por donaci (troajn) manĝaĵojn, alia grupo 
   <i>DoeDoe Antwerpen</i> por trovi homojn, kiuj petas helpon pri iu praktika afero, kiel ripari biciklon,
   farbadon, solvi elektran problemon ktp.
</p> 
<h3>Vivi tute sen mono</h3> 
<p>Laste menciindas ankoraŭ kelkaj personoj, kiuj fariĝis konataj, ĉar ili ekvivis tute
   sen mono. La germanino Heidemarie Schwermer volis vivi tutan jaron sen mono. Intertempe
   ŝi jam faras tion dum dek ses jaroj, ĉar ŝi ne volis reiri al sia antaŭa vivo. Manĝaĵojn
   ŝi ricevas interŝanĝe por taskoj kiel varti infanojn kaj purigi, vestaĵojn ŝi ricevas
   de geamikoj, kaj ĉion, kion ŝi ne povas kunporti en sia valizo, ŝi pludonacas.
</p> 
<p>En Britio la irlandano Mark Boyle vivas sen mono kaj fondis la retan 
   <i>Freeconomy</i>-komunumon (<i>Freeconomy</i> = senpaga ekonomio). Li ĉefe vivas manĝante tion, kion li trovas en la naturo. Daniel
   Suelo en Usono ĉesis uzi monon en la jaro 2000 kaj vivas en kaverno. Aperis aŭtobiografio
   de li, sed li rifuzis ĉiun pagon kaj insistis, ke anstataŭe certa kvanto de ekzempleroj
   de lia libro estu donacataj senpage.
</p> 
<p>Vivi tute sen mono postulas tutan transformadon de la vivmaniero, sed pludoni nebezonatajn
   aferojn estas agado, kiun ĉiu ajn povus fari senprobleme. Doni kaj foje ankaŭ mem
   ricevi donacojn estas aventuro, kiu denove proksimigas la homojn, kiuj ofte vivas
   tre anonime en grandaj urboj. La kompenso: dankemo kaj gajaj vizaĝoj de niaj apuduloj.
</p> 
