      <h2>Lernantoj senesperaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Japanio la ekonomia regreso gravas, kaj nun malbone efikas sur lernejanojn. Ekzemple, en elementaj kaj mezaj lernejoj en gubernio Tottori multiĝas lernantoj nekapablaj pagi siajn tagmanĝojn. Postulante pagon, instruistoj vizitas familiojn, sed trovas, ke tiuj ne povas pagi pro maldungiĝo kaj/aŭ eksedziĝo. Nuntempe multaj lernejestroj kaj instruistoj pagas por siaj lernantoj.</p> <p>Mankas al „malfacilaj” familioj subvencioj, laŭ s-ino Abe Aja, aŭtoro de la libro <i>Malriĉeco de geknaboj</i>. El ĉiuj 17 geknaboj unu kreskas en senpatra familio: tial, laŭ la libro, tiaj geknaboj riskas esti nesufiĉe edukitaj. Naskiĝas ankaŭ timo, ke la nuna ekonomia situacio tiel severas, precipe por solaj patrinoj, ke multiĝos senhejmaj patrinoj kun gefiloj.</p> <h3>Samklasanoj</h3> <p>Laŭokazaj laboristoj ofte ŝanĝas sian laborlokon, sekve sian loĝlokon. Se ili havas gefilojn, ankaŭ tiuj devas ŝanĝi sian lernejon sen sufiĉe da tempo por edukiĝi kaj amikiĝi kun samklasanoj.</p> <p>Pli ol 97 % da 15-jaraĝaj lernantoj eniras superan mezlernejon. Se ili tie ne finstudas, malfacilas trovi laborlokon. Ili ne povos akiri diplomojn por akiri pli bonajn postenojn. En 2008 72 854 lernantoj forlasis superajn mezlernejojn meze de sia lernado.</p> <h3>Diplomoj</h3> <p>La ĵurnalo <i>Asahi</i> raportis pri lernantoj en gubernia supera mezlernejo. Unua-jara klaso pretis akcepti 40 lernantojn. Fakte nur 24 eniris kaj nun restas nur 15. En februaro okazis kursfina ceremonio, kiun ĉeestis 121 el 210 akceptitoj. Kien iris la forlasintoj?</p> <p>Oni ĝenerale opinias, ke la japana socio garantias egalecon al ĉiuj. Ekzistas nek kastoj, nek sociaj katenoj, nek klasoj. Ĉiuj superaj mezlernejoj kaj universitatoj estas malfermitaj al ĉiuj kandidatoj. Se tiuj sukcesas en la enirekzameno, havante sufiĉe da mono kaj kapablo, ili rajtas lerni kaj studi en iu ajn loko. Kiam Japanio estis ekonomie prospera kaj ĉiuj relative riĉaj, tio veris.</p> <h3>Malklereco</h3> <p>Nun atakas multajn familiojn malriĉeco, tiel ke iuj lernantoj ne povas lerni eĉ en supera mezlernejo. Por tiuj, iri al universitato estas ekster la supozo. Nun malriĉeco de la familioj kaŭzas al iliaj gefiloj malklerecon, kaj sekve perdon de espero, kiu kaŭzas pli da malriĉeco, kaj pli kaj pli da aliaj malbonaĵoj.</p> <p>Gravas, se multaj junuloj devos malriĉe, malklere kaj senespere vivi. Tio damaĝas ne nur tiujn junulojn, sed ankaŭ Japanion. Malgraŭ malbona ekonomio, Japanio ankoraŭ havas grandan potencon. Savi gejunulojn estas la plej granda devo de la registaro kaj de la socio.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
