      <h3>Moderna vivo</h3>
      <h3 class="tem">KATALUNIO</h3>
      <h2>Pri kato, rato, ŝtato  kaj azeno</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la 1960aj jaroj stariĝis apud hispanaj ŝoseoj taŭro-figuroj ĝis 14 metrojn altaj, kiuj reklamis brando-markon. En 1988, kiam leĝo malpermesis reklamojn ĉe la ŝoseoj, ilia sorto ŝajnis decidita. Sed aŭdiĝis voĉoj, ke oni „amnestiu” la taŭrojn, levitajn je la rango de hispana nacia simbolo, kion la kasacia kortumo faris en 1997. De tiam la taŭro plifirmiĝis kiel fetiĉo ŝovinisma kaj masklisma (pro la tre elstaraj generiloj), uzata eĉ kvazaŭ blazono centre de la hispana flago fare de soldatoj kaj sportaj zelotoj.</p> <p>Reage al tio la sola staranta taŭrego en Katalunio ricevis iom ŝercajn atakojn dum 2002, kiam ĝi aperis pentrita laŭ la koloroj de la kataluna flago aŭ, post ĝia nigra refarbado, oni travestis ĝin al bovino per aldono de blankaj makulegoj. Fine, nekonatoj disfaligis ĝin. Ankaŭ la sola taŭro en Balearoj – pli malgranda figuro nur kvin metrojn alta kaj du tunojn peza – suferis la saman sorton post diversaj okazaĵoj, inter kiuj estis rozkolora farbado.</p> <h3>Sendependismo</h3> <p>Samtempe ekdisvastiĝis en Katalunio opoziciaj simboloj. Kato estis elektita de grupo, sin nomanta <i>Kontraŭkolonia Ligo</i>, ĉar „<i>cat</i>”, anglalingve kato, estas ankaŭ mallongigo por „Katalunio”. Oni desegnis ĝin per iom masivaj formoj, kiuj klare memorigas la taŭron; eĉ la oreloj similas la bovokornojn. La simbolo estas tre ligita kun difinita sendependisma movado, kio ne helpis ĝian populariĝon ekster ĝia naskiĝloko, en la okcidentaj regionoj. Tamen ĝi inspiris la valencian „<i>rat</i>”, la vesperton de la tieaj sendependistoj, surbaze de la tradicia simbolbesto de Valencilando.</p> <p>Sed meze de 2003 aperis nova simbolo, la azeno, inspirita en aŭtoktona vario de tiu specio. Ne estas hazardaj la faktoj, ke ĝi troviĝas en danĝero de malapero kaj ke en la kataluna azeno simbolas obstinon. Sed ĉefe tio, ke stultulojn oni nomas azenoj, igis ĝin ŝerca kontraŭheroo de la taŭro, kaj por gaje spiti la taŭron nuntempe azenaj glubildoj kovras multajn aŭtojn en la konsiderinde pli loĝata oriento de la lando.</p> <h3>Naciismo</h3> <p>Al tiu disvastiĝo helpis alia okazaĵo: la dekstra naciisma registaro enkondukis en septembro 2000 egalajn licenc-platojn de aŭtoj en la tuta ŝtato. En ili nur la eŭropa flago kaj la ŝtata internacia kodo „E” sub ĝi, sur blua fono, identigas la devenon de la aŭto, sen ia indiko de la aŭtonoma komunumo, kio kaŭzis fortan malkontenton ĉe katalunoj, eŭskoj kaj galegoj.</p> <p>Kampanjoj por surglui eŭropan flagon kun la mallongigo „CAT” por Katalunio ricevis certan subtenon, sed ne estis amase sekvataj. Sed la algluo de simbolo, kiu amuze signas devenon, ŝajne kovras tiun bezonon, sentatan de multaj, distingi sian nacian apartecon disde la oficiale distingenda ŝtataneco.</p> <div style="text-align:right">Hektor A<span class="mm">LOS</span> <span class="mm">I</span> F<span class="mm">ONT</span></div>   
