      <h3>Moderna vivo</h3>
      <h3 class="tem">KONGO</h3>
      <h2>Semante esperon</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pro la prirabado de ekonomiaĵoj en Demokratia Respubliko Kongo dum la 90aj jaroj, kaj la militoj, kiuj mizerigis la popolon, kreskis senhejmeco, senlaboreco, senlernejeco, malsato kaj malsano. Samtempe naskiĝis fenomenoj, kiel stratbubaĉoj kaj infana prostituado.</p> <p>En julio 2001 grupo da terkulturistoj decidis kontraŭbatali la sociajn problemojn de la lando. Ili lanĉis t.n. organizaĵon por integra evoluo, socioprotektan strukturon agnoskitan de la ministrejo pri sociaj aferoj. Celo de la organizaĵo estas instruado pri modernaj agrikulturaj metodoj, por ke homo vivtenu sin kaj tiel integriĝu en la socion.</p> <p>La organizaĵo posedas agrikulture apenaŭ ekspluatitajn terenojn en la provinco Malalta Kongo, kie ĝi instruas kaj subtenas novajn bienistojn. Al tiuj la organizaĵo pruntedonas ilojn kaj semojn laŭ la principo: „Pli bone doni fiŝkaptilojn ol fiŝon.”</p> <h3>Trejnado</h3> <p>Aliaj celoj koncernas helpon por virinoj, familio-estrinoj, vidvinoj kaj nematuraj forlasitaj patrinoj. Starigitaj estas et-entreprenoj, kiuj provizas bazajn bezonataĵojn al tiuj homoj. Gravas ankaŭ lukti kontraŭ analfabetismo kaj senlernejeco: tial, por tiuj, kiuj ne terkultive laboros, ekzistas trejnado en propra kudrejo, garaĝo, konstruejo, lignaĵejo aŭ komputilejo.</p> <p>Tamen la ĉefa agado de la organizaĵo temas pri agrikulturo (ekz. manioko, maizo, sojo, bulboj, legomoj, ktp) kaj bredado (porkoj, kaproj, ŝafoj, kunikloj, testudoj, kokoj, anasoj, anseroj, meleagroj, cignoj, ktp). La organizaĵo prizorgas ankaŭ prisanajn aferojn kaj respondecas pri farmacio kaj konstruas klinikon.</p> <p>Sed la organizaĵo, kiu enkondukis kiel unu el siaj laborlingvoj Esperanton, daŭre bezonas monon. Promesita financa subteno de la ŝtato ankoraŭ ne venis: pro tio oni serĉas partnerojn – aŭ simple helpon – en eksterlando. Interesatoj kontaktu la organizaĵon ĉe Avenue Selembao 65, Quartier Nkulu, Commune de Selembao, Kinshasa/RDC, BP47 Kinshasa 24/RDC aŭ rete ĉe <a href="mailto:odijvmonde@yahoo.fr">odijvmonde@yahoo.fr</a>.</p> <div style="text-align:right">Jean-Vital M<span class="mm">ONDENGI</span>/pg</div>   
