      <h3>Scienco</h3>
      <h3 class="tem">KURIOZAĴOJ</h3>
      <h2>Pluveraj veroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Kiam la unuaj pluveroj falas sur sekan teron, en la aero oni rekonas nekonfuzeblan
      odoron: tiun de la pluvo. Nun du sciencistoj el Masaĉuseca Instituto de Teknologio
      (MIT) malkovris kaj priskribis la manieron, kiel ĉi tiu odoro eliĝas en la aeron.
   </b>
</p> 
<p>Oni lastatempe malkovris iujn karakterizojn (ĝis nun nekonatajn) de tiu odoro, al
   kiu fakuloj donis la nomon 
   <i>petriĥoro</i>. Sed pri kio temas precize? Laŭ Isabel Joy Bear kaj Roderick G. Thomas, kiuj priskribis
   ĝin per gravega artikolo aperinta en la revuo 
   <i>Nature</i> antaŭ 51 jaroj kaj kiuj utiligis unuafoje la grekdevenan neologismon „petriĥoro” (el la grekaj vortoj „<i>petra</i>”, „ŝtono”, kaj „<i>ichor</i>”, „limfo”), la tipa pluv-odoro rezultas el kombinaĵo de vegetalaj oleoj kaj geosmino.
</p> 
<p>La unuaj (la vegetalaj oleoj) estas produktataj de la plantoj dum senpluvaj periodoj
   por malplirapidigi la seman ĝermadon. La plantoj eksudas tiujn oleojn, kaj ĉi-lastaj
   estas poste sorbataj de rokoj kaj argilaj terenoj: la utileco de la oleoj estas „averti” – se tiel diri – la semojn, ke ne estas sufiĉe da akvo por kreski.
</p> 
<p>La dua (geosmino) estas miksaĵo produktata de pluraj klasoj da mikroboj: ĝi estiĝas
   je la morto de kelke da bakterioj kaj havas sian propran tipan odoron de tero, fungo
   kaj ŝimo. Geosmino – pli simple, por nefaka legantaro, tiu miksaĵo, kiu donas al ruĝaj betoj ilian teran
   guston kaj al karpo kaj katfiŝo ilian odoron de koto – estas identigata de la homa olfakto eĉ je infinitezimaj niveloj. Petriĥoro estas
   kombinaĵo de vegetalaj oleoj kaj geosmino.
</p> 
<h3>Aerbobeloj</h3> 
<p>Pli ol 600 fojojn du sciencistoj el MIT, Young Soo Joung kaj Cullen R. Buie, imitis
   lastatempe la falon de la pluvo sur 28 malsamajn tipojn de surfaco. Ĉiu kolizio estis
   filmita per altrapida videokamerao (kiu ebligis lantanalizi unuopajn falojn) kaj poste
   zorge studita. Rezultis, ke la kolizio de pluvoguto kun pora materialo (kia estas
   tero) aperigas ĉe la kontakta surfaco tre malgrandajn aerbobelojn.
</p> 
<p>Kiam la bobeloj eksplodas, ili ellasas en la aeron iun aerosolon, kiu estas priskribebla
   kiel miksaĵo de akvomolekuloj kaj aliaj substancoj surface ĉeestaj. Alivorte, la unua
   pluvo aktivigas la miksaĵon de vegetalaj oleoj kaj geosmino, kiu estis antaŭe staplita
   en la tero. La malkovro de tio, ke la pluvo frapante la teron kreas aerosolon, estas
   konsiderita de la du sciencistoj tre grava aspekto. Kompreni tian sistemon, iliadire,
   povus helpi klarigi la manieron, kiel iuj infektoj (ekzemple tiu kaŭzata de 
   <i>Escherichia coli</i>) disvastiĝas peraere. La aerosoloj kaŭzataj de la pluvo povas efektive transporti,
   krom la pluvan odoron, ankaŭ kelkajn malutilajn substancojn.
</p> 
