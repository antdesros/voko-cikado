      <h3>Ekonomio</h3>
      <h3 class="tem">FERVOJOJ</h3>
      <h2>Trajne al Baltio</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En majo 2010 en la eta litova-pola limpunkto Mockava ekis internacia projekto nomata „<i>Rail Baltica</i>”, kies celo estas per eŭropdimensia fervojo kunligi la baltan regionon – Litovion, Latvion, Estonion – kun okcidenta Eŭropo. Tiu ideo jam delonge flirtis en la kapoj de la regionaj aŭtoritatuloj kaj instancoj de Eŭropa Unio, sed fina decido venis nur nun.</b></p> <p>Por trajne kunligi Baltion kun la cetero de Eŭropo oni finfine decidis ne konstrui novan fervojon, sed rekonstrui la malnovan, konstruitan dum la sovetia periodo. Pavel Telička, la kunordiganto de la projekto, klarigis, ke la unua etapo de la projekto devas finiĝi en la jaro 2014. La nova eŭropa fervojlinio iros kune kun la sovetia larĝtraka. La unua etapo kunligos la limurbeton Mockava kun Kaunas, la dua urbo de Litovio. Nur en la lokoj, kie tio montriĝos neebla, estos konstruata nova parto. Tiel la kostoj de la projekto falis de la antaŭe planitaj 580 milionoj da eŭroj ĝis 232 milionoj. 27 % de la necesaj monrimedoj liveras Eŭropa Unio.</p> <p>La projekton kune realigos poloj kaj estonoj. Estonte la fervojo, parto de la tuteŭropa transport-reto TEN-T, kunligos la tri baltajn landojn kaj kondukos ĝis Helsinko, la ĉefurbo de Finnlando. <i>Rail Baltica</i> unue kontribuos al la ekonomia kresko de la regiono kaj nur poste utilos al pasaĝera trafiko. Komence la rapido de la ŝarĝtrajnoj estos nur ĉirkaŭ 90 km/h.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
