      <h3>Moderna vivo</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>La mistero de la mankanta medicinisto</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉu kuracisto povas senspure malaperi meze de granda urbo kiel Vieno, kie loĝas pli ol unu miliono da homoj? Tio okazis la 28an de decembro 2012. Jen la kronologio:</p> <p>7h ĝis 11h30: La kuracisto Matthäus Wegmann [mateus vegman] laboras en la hospitalo Rudolfinerhaus en la 19a distrikto de Vieno.</p> <p>12h ĝis 14h: Tagmanĝo kun la edzino en la 2a distrikto.</p> <p>14h30: Li telefonas en la unua distrikto. Poste mallonga vizito en Rudolfinerhaus. Li adiaŭas kaj komencas 14-tagan ferion.</p> <p>16h42: Li telefonas apud la metrohaltejo Heiligenstadt [hajligenŝtat].</p> <p>18h: Li serĉas iun sur kajo de tiu haltejo.</p> <p>19h30: En la operejo, loko rezervita por li restas neokupita.</p> <p>La edzino ne maltrankviliĝas, ĉar la 41-jaraĝa kuracisto ofte devas nokte deĵori en la malsanulejo. Tamen la sekvan tagon Wegmann aperas nek en la hejmo, nek en la hospitalo. Oni ne sukcesas kontakti lin telefone.</p> <p>Li planis flugi al Novjorko por festi la jarfinon, sed tie li ne alvenas. Ankaŭ en liaj du kuracistaj praktikejoj en Vieno kaj Hainburg [hajnburg] neniu vidas lin. Li senspure malaperis.</p> <p>Restas nur demandoj. Ĉu li tro laboris kaj volis ĉion forĵeti? Tion li foje aludis. Ĉu li havis akcidenton, kiun neniu rimarkis? Preskaŭ neeble en dense loĝata regiono. Ĉu iu paciento lia, kiu ne ricevis ĝustan kuracadon, atakis lin?</p> <p>Ĉu nun li vivas kaŝe en alia lando? Kial li ne havis kreditkarton kaj ofte portis multe da mono ĉe si? Eble li mortigis sin. Daŭras policaj enketoj kaj esploroj.</p> <div style="text-align:right">Walter K<span class="mm">LAG</span></div>  
