      <h3>Hobio</h3>
      <h3 class="tem">FINNLANDO</h3>
      <h2>Vintra banado</h2>
      <!-- komenco de la enhavo -->

       

       <p>Bani sin vintre en malvarmega maro, rivero aŭ lago estas ekstrema ŝatokupo de ĉirkaŭ 120 000 finnoj. Se la akvo jam frostiĝis, oni faras truon en la glacion kaj sin banas. Se la truo fermiĝas pro la frosta vetero, estas uzata elektra kirlilo por teni ĝin malfermita. La ŝatantoj de vintra banado venas al sia glacitruo ĉiutage, aŭ malpli ofte, sed tamen ripete. En bankostumo, kun ĉapo surkape kaj ofte gantoj surmane, ili trempas sin en la akvon kaj rondnaĝas dum kelkaj minutoj. Poste ili elakviĝas fieraj kaj feliĉaj.</p> <h3>Kial?</h3> <p>La malvarmega akvo (temperaturo nula aŭ kelkgrade plusa) akcelas la sekreciadon de streĉhormonoj, malaltigas la sangopremon, igas la haŭton pli elasta, bonhumorigas, donas psikan forton kaj feliĉigan senton de bonfarto, kuraĝo kaj eltenemo. Poste oni eltenas pli facile la ŝarĝojn kaj animan streĉon en la vivo. Iuj diras ankaŭ, ke la glaciakva banado helpas kontraŭ migreno kaj reŭmatismo.</p> <h3>Kie?</h3> <p>La truo por la banado povas esti memfarita: ĉe propra strando aŭ ĉe publika banejo, prizorgata municipe aŭ de klubo de hobiuloj. La finna ĉefurbo Helsinko disponigas pli ol 20 vintrajn subĉielajn banejojn al ŝatantoj de vintra banado. Ĉe la duono el ili apude staras hejtata saŭno, ĉe la aliaj varmigata vestoŝanĝejo. Tamen prefereblas, ke oni varmigu sin en la saŭno nur post la malvarma bano. Unue oni eniru trankvile kaj malrapide la malvarmegan akvon, kies temperaturo ofte estas multgrade pli alta ol tiu de la aero (minusaj gradoj ĝis 10-20). La uzantoj de la servoj pagas 10-150 eŭrojn.</p> <h3>Kia glacitruo?</h3> <p>La truo mezuras almenaŭ 3 m x 3 m, kaj la akvo devus profundi almenaŭ 1,5 metrojn ĉe la enira ŝtupareto. Se la akvo fluas, estas grave ĉirkaŭi la glacitruon per reto subakva tiel, ke neniu sinbananto subglaciiĝu (danĝero de senelirejo, sufokiĝo, hipotermio). La pado al la truo ne estu glitiga: oni surŝutu ĝin per sablo aŭ salo kaj ĝi havu varmigeblan maton ĉe la fino. La enakviga ŝtupareto havas gardrelojn ambaŭflanke. Oni povas uzi elakvigan ŝnuregon kun nodoj por helpi la eliradon el la glacitruo. Ĉiu banejo devas nepre disponi pri savringo kaj ĵetebla savŝnurego.</p> <p>Ĉu hobio aŭ sporto? Tion vi mem decidu. Verdire okazas en Finnlando ankaŭ vintraj naĝokonkursoj en subĉielaj basenoj, sed tio estas jam alia afero.</p> <div style="text-align:right">Saliko</div> 
