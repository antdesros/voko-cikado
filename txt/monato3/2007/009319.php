      <h3>Politiko</h3>
      <h3 class="tem">HONKONGO</h3>
      <h2>Migranta filo post 10 jaroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉi-jare okazas la 10-jariĝo de la reveno de Honkongo al ĝia patrolando Ĉinio. Antaŭ la reveno multaj dubis pri la politiko de „unu lando kun du sistemoj”. Okcidentaj komunikiloj profetis, ke Honkongo „mortos”, perdante siajn proprajn socian sistemon kaj vivmanieron.</p> <p>Tamen post 10 jaroj evidentas, ke la ekonomio pli prosperas, la socio pli harmonias, kaj la leĝfarado pli kompletas. Ekzemple, en la 1970aj jaroj, Honkongo elstaris kiel la unua internacia financa centro en Azio. Hodiaŭ Honkongo, Londono kaj Novjorko konsistigas la tri grandajn financajn centrojn de la mondo. Laŭ indicoj publikigitaj en marto en Londono, Honkongo enviciĝas kiel la tria plej forta, tamen antaŭvideblas plia kresko.</p> <h3>Eksportado</h3> <p>Honkongo fariĝis ankaŭ internacia komerca centro. La sumo de la vara kaj serva komercoj proksimiĝas al kvaroblo de la tuta loka produktokvanto. Eksportado de konteneraj varoj kreskas je ducifera elcento en sinsekvaj kvar jaroj, tiel ke Honkongo estas naviga centro en la azi-pacifika regiono. Cetere, krom ŝtatdefendo kaj eksteraj aferoj regataj de la ĉina centra registaro, Honkongo ĝuas altan memstarecon en administracio, leĝfarado kaj en juĝaferoj.</p> <p>Antaŭ la reveno al Ĉinio, Honkongo estis „migranta filo”. Post la reveno Honkongo konscias pri la subteno de la ĉina ĉeftero. Tio probable estas la plej granda ŝanĝo, kiu efikis al la disvolviĝo de Honkongo.</p> <p>Laŭ enketo farita de la honkonga registaro aprile 2006, 75 % de honkonganoj fieras esti ĉinoj; 62 % fieras esti civitanoj de Ĉinio; 65 % opinias, ke pro la ĉina statuso en la internacia komunumo, ĉinoj ne plu suferas humiliĝon; kaj 76 % konsentas, ke honkonganoj bezonas patriotisman edukon.</p> <div style="text-align:right">Alice L<span class="mm">IU</span></div>   
