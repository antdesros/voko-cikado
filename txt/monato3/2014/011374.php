      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Raporto el la estonteco</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <b>Apud Tel-Avivo. 4an de aŭgusto 2029.</b> Agonias Israelo. Sendube la ŝtato eniras siajn lastajn tagojn. Laŭraporte jam fuĝis
   al Svislando la ĉefaj politikistoj kaj armeanoj, dum la popolo kaŭras en keloj kaj
   atendas la mortigajn manojn de la invadintoj, posteuloj de la iama terorisma organizaĵo
   Hamas.
</p> 
<p>Neeviteblis la baldaŭa sangobano, post kiam Usono, dum jardekoj la plej fidela amiko
   kaj armilo-liveranto al Israelo, iom post iom retiriĝis por koncentriĝi pri siaj propraj
   teritoriaj problemoj – kaj ankaŭ ekonomiaj – laŭ la suda landlimo kun Meksiko.
</p> 
<p>Ankaŭ la 10-membra Eŭropa Unio, konsistanta el la finance plej fortaj kaj stabilaj
   nordaj ŝtatoj, taksas la mez-orientan konflikton periferia. Laŭ porparolanto en Berlino:
   „Ni bedaŭras la sangoverŝadon, sed ni lernis pro intervenoj komence de la jarcento,
   ekzemple en Irako kaj Afganio, ke ludi la rolon de internacia policisto, eĉ el humanismaj
   principoj, estas senutile. Se transmaraj mediteraneaj najbaroj, do Italio aŭ Grekio,
   volas iel asisti, do jen afero por la koncernaj registaroj.”
</p> 
<p>Eĉ UN, montrante denove sian malfortiĝon, ŝajnas nekapabla akiri sufiĉan mandaton
   por kunordigi en la regiono blukaskulan pac-trupon.
</p> 
<p>Kompreni la kialojn de la nuna buĉado ne estas malfacile. Okazis turniĝo en tiu ĉi
   jam turbula mondoparto en la somero de 2014, kiam denove eksplodis milito en la tiel
   nomata Gaza-strio. Tiam la mondo, aŭdinte tagon post tago pri la mortoj de senkulpaj
   infanoj kaj civiluloj pro israelaj aer-atakoj, kaj vidinte fotojn kaj filmerojn pri
   detruitaj loĝkvartaloj en Gaza-urbo, perdis paciencon, kulpigante Israelon pro ekscesa
   perforto kontraŭ popolo plejparte senprotekta.
</p> 
<p>Personan perspektivon donas la 22-jaraĝa palestinano Moussa (ne la ĝusta nomo), jam
   batalo-hardita kaj portanta cikatrojn de militista ekzisto, dum paf-paŭzeto en unu
   el la antaŭurboj de Tel-Avivo. „Kial mi strebas por justeco por mia popolo ... tion vi volas scii, ĉu? Do mi diros.”
</p> 
<p>„Sep jarojn mi havis, kiam mi ludis kun la gekuzoj apud la maro ĉe Gaza-urbo. Dum momento
   mi ilin forlasis, por trovi trinkaĵon, kiam pum! eksplodo orelŝira. Mi turniĝis ...
   kaj jen, kie ni ludis, polvo, sablo ... kaj granda truo. Familianoj ... neniigitaj.”
</p> 
<p>„Oni diris, ke israela militŝipo kredis nin soldatoj ... sed, diable, ĉu eblas? Konfuzi
   infanojn kaj soldatojn? Pa! Kaj jen pliaj teruraĵoj. Pro senĉesa bombado estas detruita
   nia hejmo ... finance frakasita mia patro, kiu unu jaron antaŭe alkonstruigis plian
   etaĝon ... ĉio enaerigita. Sekvis ankaŭ la apartamento de miaj geavoj ... kaj la butiko
   de la plej aĝa frato. Senhejmaj, senlaboraj ... kaj vi scivolas, kial ni malamegas
   israelanojn?”
</p> 
<p>Moussa kraĉas sur la grundon, kaj daŭrigas: „Mi ne pretendas, ke la tiamaj Hamas-uloj estis senkulpaj. Jes, armilojn ili kaŝis
   en la urbo ... sed ne en lernejoj, malsanulejoj, kiujn aparte celis la israelanoj,
   kaj kie provis ŝirmi sin niaj civitanoj. Nu, mi bone komprenas, ke ĉiu rajtas sin
   defendi ... sed, se pugnobatate, vi ne rezistu tranĉile, pafile. Tio ne justas.”
</p> 
<p>„Kaj jen la eraro de la tiamaj israelanoj. La granda eraro. Preter la propraj nazoj
   ili ne vidis, ne en la estontecon, por kompreni, ke tiel, rakete, bombe, ili bredas
   malamegon, abomenon, tiel turnante kontraŭ si tutan venontan generacion, nun, kiel
   vi vidas, venĝavidan. Ili semis, ni rikoltas. Jen, nuksoŝele, ĉio.”
</p> 
<p>„Ĉu nun dialogi? Tro malfrue. Eble antaŭ la murdoj, la masakroj, de 2014, sed la israelanoj
   ĉiam rifuzis. Almenaŭ tiel oni instruis. Sed mi vere ne scias. Kiel mi diris, tiam
   mi estis nur knabeto. Nur knabeto.”
</p> 
<p>Moussa eksilentas, larmeton en la okulo, kaj rigardetas en la distancon, de kie trafas
   nin obtuza, sporada tamburado de mitraloj. Li sin skuetas, kaj rektigas la grenado-lanĉilon
   sur la ŝultro.
</p> 
<p>„Nur 48 horoj,” li diras. „Nur 48 horoj, kaj la longe atendataj venko, venĝo, estos niaj.”</p> 
