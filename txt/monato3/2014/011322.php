      <h3>Moderna vivo</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Dekmil trompoj kaj centmil trukoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>En majo kvindeko da ĵurnalistoj ĉeestis la prezentadon de ekspozicio kun stranga titolo:
   Dek mil trompoj kaj cent mil trukoj. Temas pri citaĵo el libro de la filozofo Frantz
   Fanon kun la titolo „La damnitoj de tiu ĉi tero”, en kiu la aŭtoro priskribis la alĝerian revolucion.
</p> 
<p>La ekspozicio okazis unue en Zagrebo. Poste ĝi migris al Antverpeno, Kairo, Honkongo,
   Bejruto kaj Vieno. En Moskvo estos la fina haltejo.
</p> 
<p>La ekspozicio aktuale okazas en Vieno, en 
   <i>21er Haus</i> (21a domo), muzeo por la arto el la 21a jarcento. Tiu pavilono havas longan historion:
</p> 
<p>1958: Karl Schwanzer [ŝvanca] (1918-1975) konstruis la aŭstran pavilonon por la Monda
   Ekspozicio en Bruselo. Kostoj 2,5 milionoj da eŭroj. Pro la nestabila grundo la supra
   etaĝo staras sur kvar kolonoj.
</p> 
<p>1962: Post alikonstruado la pavilono trovis novan lokon proksime al la ĉefa stacidomo
   de Vieno. Ĝi estis nun ejo por la arto de la 20a jarcento kaj nomiĝis 
   <i>20er Haus</i>.
</p> 
<p>2001: La arta kolekto translokiĝis al la nova Muzea Kvartalo de Vieno.</p> 
<p>2010 ĝis 2011: La pavilono oni denove alikonstruas. La kostoj estis 31,9 milionoj
   da eŭroj.
</p> 
<p>2011: Malfermo kiel 
   <i>21er Haus</i>.
</p> 
<p>Nun, ĝis la 31a de aŭgusto, en la supra etaĝo, viziteblas la menciita ekspozicio.
   La temo estas ege ambicia: koloniismo, subpremo, revolucioj, ĉefe en la arablingvaj
   kaj orienteŭropaj landoj.
</p> 
<h3>Katalogo</h3> 
<p>44 artistoj el tiu regiono montras tute diversajn objektojn: pentraĵojn, skulptaĵojn,
   fotojn, objektojn el diversaj materialoj kiel uzitaj libroj aŭ plastaj skatoloj por
   transporti legomojn. Tion kompreni malfacilas, kiel indikas la katalogo.
</p> 
<p>Tie legeblas: „La ekspozicio malfaldiĝas kiel daŭra dialogo, kies nelinearaj ĉapitroj devojiĝas,
   sin reciproke interrompas, plenumas kaj kontraŭdiras, sondante la eblojn kaj limojn
   de tio, kion ekspozicio povas efektivigi rilate al pridemandado de politikaj historiaj
   evoluoj en najbaraj kaj diverĝaj spacoj.”
</p> 
<p>Se vi ne scias kiel reagi, nepras mem viziti la artaĵojn. Jam la lumplena pavilono
   estas interesa konstruaĵo, kun kafejo en la suba etaĝo.
</p> 
