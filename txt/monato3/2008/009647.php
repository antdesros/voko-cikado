      <h3>Spirita vivo</h3>
      <h3 class="tem">PILGRIMEJOJ</h3>
      <h2>Dia Filo estu glorata dum jarcentoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kun tiu slogano en septembro en la litova urbeto Šiluva estis vaste celebrata la 400a datreveno de la apero de la Sankta Virgulino Maria, oficiale konsiderata kiel ŝia unua apero en Eŭropo kaj unu el la kvin aperoj agnoskataj de la romkatolika eklezio.</p> <p>La eta litova urbeto Šiluva, en la centro de la lando, ĉiuseptembre festas la tradician naskiĝtagon de Maria. La nombro de apenaŭ 800 loĝantoj tiam multobliĝas. Tamen tiom da gastoj, pilgrimantoj, kiom ĝi vidis dum la ĉi-jara jubilea festado, la urbeto ne spertis antaŭe. En la inaŭgura jubilea patronfestado en Šiluva dum kvar tagoj partoprenis ĉirkaŭ 50 000 kredantoj. Dum la naŭ festotagoj la lokon vizitis ĉirkaŭ miliono da homoj. Perpiede tiun sanktan urbeton atingis pli ol du mil pilgrimantoj el diversaj lokoj de Litovio. Al Šiluva tra tuta Eŭropo estis perpiede portata kruco el Lourdes. Speciale por la ĉi-jara patronfesto estis dediĉita skulptaĵo de la Sankta Virgulino Maria muldita en la pola urbo Krakovo.</p> <p>Ĉiu solena tago inter la 6a kaj la 15a de septembro estis riĉa je diversaj aranĝoj. La jubilea festado finiĝis per sankta meso sur la apudbazilika placo. La solenaĵon ankaŭ partoprenis eksterordinara nuncio de papo Benedikto la 16a, kardinalo Joachim Meisner. La kardinalo reprezentis la papon, kiu tiutempe gastis en la franca urbeto Lourdes, en kiu samspeca apero okazis antaŭ 150 jaroj.</p> <p>La eksterordinara nuncio de la papo gvidis la solenan eŭkaristion sur la placo inter baziliko kaj kapelo de la apero.</p> <p>La Sankta Virgulino Maria en Šiluva aperis en 1608. Knaboj de najbara vilaĝo paŝtis gregon sur la herbejo, kie en la mezo de la 15a jarcento staris malnova katolika preĝejo. Subite sur granda ŝtono ili ekvidis plorantan virinon kun bebo en la manoj. La paŝtistoj tuj kuris al kalvina kateĥisto de Šiluva. Li vokis la instruiston Salamono. Kune ili proksimiĝis al la ŝtono kaj ekvidinte la plorantan virinon, ili ekparolis al ŝi: „Knabineto, kial vi ploras?” Ŝi respondis: „Mi ploras pro tio, ke antaŭe sur tiu ĉi loko estis adorata mia Filo kaj nun ĉi tie oni plugas kaj semas”. La rakonto pri la apero de Maria tuj disvastiĝis kaj la urbeto baldaŭ famiĝis per miraklaj resaniĝoj de malsanuloj.</p> <p>La 7an de septembro 1993 en Šiluva gastis kaj ĉi tie preĝis papo Johano Paŭlo la 2a. Por omaĝi la 15an datrevenon de tiu ĉi vizito estis inaŭgurita monumento pri papo Johano Paŭlo la 2a. La inaŭguran leteron subskribis ĉiuj episkopoj, kiuj partoprenis la solenaĵon kaj la aŭtoro de la monumento. La ĉeestantoj esprimis la spertitan ĝojon pri la valora donaco por Šiluva.</p> <p>La solenaĵo en Šiluva finiĝis per dankfesto.</p> <div style="text-align:right"><span class="mm">LAST</span></div>       
