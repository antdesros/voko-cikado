      <h3>Ekonomio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Tri milionoj da aŭtoj en Pekino</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La Pekina Komitato pri Trafiko anoncis, ke en majo 2007 en la ĉina ĉefurbo estis almenaŭ tri milionoj da privataj aŭtomobiloj. Tio montras, ke la urbo eniris la aŭtan socion.</b></p> <p>Ekde la pasinta jaro la nombro de privataj aŭtoj en la urbo kreskis kun rapideco de po 1000 en unu tago. Kiam en 1950 la nova Ĉinio fondiĝis, en Pekino troviĝis nur 2300 civilaj aŭtoj. En februaro 1997 la nombro atingis unu milionon; por tio estis bezonataj pli ol 40 jaroj. Pasis nur iom pli ol ses jaroj, ĝis en aŭgusto 2003 la nombro superis du milionojn. Por plimultiĝi ĝis tri milionoj estis bezonataj nur tri jaroj kaj naŭ monatoj. Tiu cifero estis atingita multe pli frue ol la specialistoj pri urba disvolviĝo antaŭkalkulis.</p> <h3>Ĉu estas ekonomia sukceso?</h3> <p>Oni povus konkludi, ke la ekonomia konstruado de la urbo brile sukcesis, sed tio estas unuflanka vidpunkto. Dum intervjuo kun la internacie fama profesoro Lu Huapu, estro de la Trafika Instituto ĉe Quinghua-Universitato, ĵurnalisto trovis, ke kompare kun privatuloj en evoluintaj landoj, kiuj plejparte posedas aŭtojn por per ĝi pasigi libertempon aŭ malstreĉi sin, la aĉetcelo de plimulto de la pekinanoj estas nur por eviti la embarason, kaŭzatan de malbona publika trafiko.</p> <p>Lu Huapu substrekis: „Pekino nun alfrontas pezan trafikan ŝarĝon. Preskaŭ ĉiutage estas terura trafikŝtopiĝo. La plej ŝlosila problemo estas, ke al la urbo Pekino, dense loĝata, mankas kapabla sistemo de publika trafiko. Laŭ statistiko nuntempe en Pekino la publika trafiko havas porcion de nur 20 % de la tuta urba trafiko, dum en Novjorko, Tokio kaj aliaj urboj, la publika trafiko ĝenerale havas 70 %.”</p> <h3>Atentiga signalo</h3> <p>Unu el la zorgigaj problemoj estas, ke specialistoj kaj koncernaj instancoj de la ĉina registaro nepre bezonas fari pli sciencan prognozon pri la kresko de aŭtokvanto en Pekino kaj aliaj urbegoj. Estonte, la tempoperiodo, en kiu la civilaj aŭtoj de Pekino atingos kvar milionojn, certe pli mallongos ol la bezonita tempo de du milionoj ĝis tri milionoj.</p> <p>La trafikŝtopiĝo iĝis por pekinanoj tre doloriga problemo. La pligrandigo de la aŭtokvanto certe ne povos ĝin solvi. Necesas senprokraste plani rimedojn por adaptiĝi al rapidega intensiĝo de trafiko. Sen apliko de sciencaj metodoj kaj efika administrado, la kombino de efikoj, kaŭzitaj de la aŭtomobila socio kiel, ekzemple, kresko de energikonsumo kaj grandkvanta eliĝo de nocaj gasoj, povas konduki al katastrofo. Do, tri milionoj da aŭtoj kiel turnopunkto levis por la ĉina ĉefurbo atentigan signalon.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div> 
