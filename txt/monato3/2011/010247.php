      <h2>Kipro eksportas, Ĉinio importas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Kion eksportas la malgranda Kipro al la mondo? Jam delonge forpasis la tempo, en kiu la lando fosis kaj eksportis kupron, metalon kies nomo signifas ĝuste „kiprodevena”. Pro la sama kialo cetere la insulo aperas kuprokolora centre de la propra flago. Tamen, ankaŭ la nuntempa Kipro havas kelkajn produktojn por eksporti.</b></p> <h3>Ne nur citrusoj</h3> <p>Ties pampelmusojn kaj aliajn citrusojn oni trovas jam en tuteŭropaj fruktovendejoj, sed ne estas konate, ke en tiu mez-orienta landeto aktivas ankaŭ rimarkinda farmacia industrio. Nur en la urbo Limasolo ekzistas tri kompanioj, kies instalaĵoj donas laboron al centoj da homoj, sciencistoj kaj simplaj laboristoj. La medikamentojn oni vendas tra la tuta insulo, sed ankaŭ eksterlande: kiom ajn nekredebla tio aspektas, unu el la ĉefaj importantoj de kipraj kuraciloj estas Ĉinio, la lando en kiu, laŭ ofta supozo, hodiaŭ estas fabrikata ĉio ajn.</p> <h3>Pro serĉado de laboro</h3> <p>Tiu komerco tre utilas al la ekonomio de Kipro, kiu ne dependu sole de turismo. La plej malnova kompanio estas <i>Medochemie</i>. Ĝi komencis agadi en 1976, por helpi la gekipranojn kiuj, perdinte sian tutan havaĵon pro la tragikaj faktoj de 1974, senespere serĉis laboron kaj elmigris al ne-okupitaj urboj de la sudo. Nuntempe <i>Medochemie</i> disponas pri naŭ grandaj instalaĵoj, el kiuj unu troviĝas en Nederlando. En ili laboras 1200 personoj, pliparte sciencistoj. La kompanio havas 19 eksterlandajn oficejojn, promociantajn ties 3800 ekvivalentajn aŭ ĝeneralajn medikamentojn (t.e. medikamentoj kies patentprotekto forfalis kaj kiuj povas esti kopiataj de iu ajn), divideblajn en dek kuracilajn kategoriojn. Krom la kipra registaro, eĉ EU trifoje premiis tiun kompanion.</p> <p>Tuj poste indas mencii la firmaon <i>Remedica</i>. Malfermita en 1980, ĝi estas malpli granda ol <i>Medochemie</i> kaj kun siaj 480 produktaĵoj, inter kiuj analgeziloj kaj vitaminoj, ĉefe strebas al la ekstera merkato. Danke al nebridebla kresko, ĝi ĉeestas en pli ol cent landoj ĉiukontinentaj, kaj pretas jam malfermi sian duan instalaĵon. Rimarkindas, ke ĝi provizas ankaŭ neprofitcelajn organizaĵojn, kiel Monda Organizaĵo pri Sano, Unicef kaj Kuracistoj sen landlimoj.</p> <h3>Perbuŝaj kuraciloj</h3> <p>La tria farmacia fabriko de Limasolo nomiĝas <i>Vogen Laboratories Ltd</i>. Fondita en 1985, ĝi limigas sin al la produktado de perbuŝaj kuraciloj, kiel antibiotikoj, antisepsaĵoj kaj kontraŭinflamaj medikamentoj. Kvankam malpli konata ol la aliaj du kompanioj, ĝi paradas per volumeno, kiu varias inter 1 kaj 2,5 milionoj da usonaj dolaroj jare.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
