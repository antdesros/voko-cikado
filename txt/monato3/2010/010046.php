      <h2>Tre sukcesa komika dramo</h2>
      <!-- komenco de la enhavo -->

       

     <p>La franca aŭtoro Jean-Baptiste Poquelin (1622 – 1673; pseŭdonomo: Molière) estas la plej bone konata kaj, verŝajne, la plej talenta dramisto en la historio de la franca teatro. Krom grandskalajn dramojn, li verkis ankaŭ komedietojn, el kiuj ĉi tiu estas inter la plej famaj.</p> <p>Por ĉi tiu eldono, André Cherpillod prezentas sur alternaj paĝoj la francan tekston kaj la esperantan tradukon.</p> <p>Temas pri drameto, kian povus facile surscenejigi loka klubo, por siaj membroj kaj gastoj, aŭ por kongreso, aŭ alia publika prezento. Cherpillod tradukis per tre flua, konversacieca lingvaĵo.</p> <p>La intrigo estas simpla: maljunulo riĉa, nomata Sganarelo, enamiĝis al junulino nomata Dorimena. Sed li ne certas, ĉu tia geedziĝo estus bona afero. Li demandas diversajn personojn, por aŭdi ilian opinion pri tio.</p> <p>Li demandas al du „filozofoj”, unu post la alia. Molière forte mokas ilin, pro ilia sinpriokupo, kiu malebligas al ili, ke ili konsideru realan situacion, anstataŭ teoriumadon. La unua, Pankraco, pseŭdo-aristotelumadas, uzante amason da latinaj esprimoj, tiom, ke la aŭskultanto tute perdiĝas en la sensenca vortogalimatio. La dua, Marfurio, rifuzas rekte esprimi opinion, tiel ke ne eblas konkludi, kia estas lia konsilo, nur ke li ne kapablas konsili.</p> <p>Aliaj personoj ŝajnas diri, ke, ĉar la demando estas tiom subjektiva, nenia konsilo povus esti dirita.</p> <p>Diskuto inter Sganarelo kaj Dorimena evidentigas, ke li kaj ŝi havas en sia menso nur la ideon, ke post la geedziĝo, li/ŝi povos pluvivi sian apartan vivon, sen atento al sia partnero.</p> <p>Sganarelo ankaŭ subaŭskultas konversacion de Dorimena kun alia viro, per kio evidentas, ke ŝi ne amas Sganarelon, sed volas edziniĝi al li nur pro lia riĉo; ŝi ankaŭ supozas, ke Sganarelo ne longe vivos.</p> <p>Sganarelo decidas nuligi sian proponon edzinigi Dorimenan. Sed ŝia patro rifuzas permesi tion, kaj per truko la frato de Dorimena perforte devigas Sganarelon okazigi la geedziĝon.</p> <p>Mia sola dubo pri nuntempa surscenejigo de la drameto estas, ke la spektantaro tiom konfuziĝus pro la arego da latinlingvaj frazoj de la filozofo Pankraco, ke oni ne volus resti por spekti la ceteron de la drameto.</p> <p>Tamen, por la leganto, estas alie. Cherpillod aldonas multajn notojn, por klarigi la tekston, tiel ke la leganto povas plene ĝui la dramon kaj la tradukaĵon. Kaj tiu tradukaĵo estas vere bona.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Molière: Le mariage forcé/La trudita edziĝo. Tr. André Cherpillod. Eld. La Blanchetiere, Courgenard, 2010. 64 paĝoj gluitaj. ISBN 2-906134-83-X.</h6>  <h6 class="bib">Por mendi, vi iru al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=mariage">la Retbutiko</a>.
        </h6>
