      <h3>Politiko</h3>
      <h3 class="tem">EŬROPA UNIO</h3>
      <h2>Ankaŭ paco konas siajn (land)limojn</h2>
      <!-- komenco de la enhavo -->

       

       <p>Eŭropa Unio konsistas el 28 ŝtatoj. Aliaj ŝtatoj deziras aliĝi, sed eble ne ĉiuj estos akceptitaj. Pro tiom da anoj, la internaj organizaĵoj de la unio aspektas plumpaj kaj malfacile administreblaj.</p> <p>Ne ĉiuj membroj kontentas pri la unio. Konservativuloj en Britio, ekzemple, volas eliĝi el ĝi. En Irlando, kie dum mallonga tempo kelkaj (tamen ne ĉiuj) profitis de la unio, tie malpli bone pagataj laboristoj, inkluzive fiŝkaptistojn, senlaboruloj kaj viktimoj de la banka krizo nun dubas, ĉu la klopodoj por fondi unuiĝintan Eŭropon estis tiom bona ideo. Certe la ĉiam kreskanta EU-burokratio tre ĝenas.</p> <p>Tamen, apogantoj de la unio fanfaronas pri tio, ke ŝtatoj, en la pasinteco malamikaj, ekzemple Germanio kaj Francio, nun amike kunmembras. Ne imageblas, ili asertas, ke tiuj ŝtatoj denove inter si ekmilitos. Pro tio sekura estos la paco en Eŭropo.</p> <h3>Disputoj</h3> <p>Aliflanke ne perfekte regas harmonio en Eŭropo. Kritikantoj povas prave atentigi pri ankoraŭ ekzistantoj disputoj pri landlimoj kaj pri supozata koloniismo ene de la unio. Hispanoj rigardas la britan posedon de Ĝibraltaro neakceptebla okupo de parto de sia teritorio.</p> <p>Defendanto de la hispana sinteno pri „la roko” demandis, ĉu angloj akceptus, ke hispanoj okupu parton de la insulo Wight [ŭajt] en suda Britio. Ĝibraltaranoj povas kontraŭi, ke la lingvo de la tiea plimulto estas la angla, kaj la kulturo brita, ne hispana. Do kio decidigas: Ĉu lingvo? Ĉu teritorio?</p> <p>En Irlando, kaj en la suda respubliko kaj en la norda, brita provinco Ulstero, ekzistas tiuj, kiuj konsideras la ekziston de teritorio apartigita de la respubliko kiel fremdan okupadon. Tamen en Ĝibraltaro, oni pli-malpli akceptas la dividon de la t.n. verda insulo, kaj ne konsideras ĝin krude koloniisma.</p> <h3>Infanoj</h3> <p>Ĉar konstante kreskas la nombro da infanoj de nord-irlandaj familioj, kiuj elektas irlandan naciecon, demografiaj ŝanĝoj povus iun tagon estigi respublikistan plimulton kaj eventuale unuigon de la insulo.</p> <p>Ĉiaokaze restas fakto, ke kelkaj ŝtatoj, aŭ partoj de tiuj ŝtatoj, ne povas akcepti, ke en pretendata unio de amikaj partneroj ekzistas io, kio similas al imperiismo de la pasinteco. Disputoj pri landlimoj aspektas kiel stranga anomalio en tiel nomata unio de „egaluloj”.</p> <p>George Orwell skribis en <i>Bestofarmo</i>: „Ĉiuj bestoj estas egalaj, sed kelkaj bestoj estas pli egalaj ol aliaj”. Do prisilentendas ĉi tie la pretendoj de Aŭstrio pri suda Tirolo, la plendoj de hungarparolantoj en najbaraj landoj de Hungario aŭ de la slovenoj en Italio, la postuloj de skotaj sendependemuloj. Mencioj tiaj nur plilongigus la artikolon.</p> <div style="text-align:right">Garvan M<span class="mm">AKAJ</span></div> 
