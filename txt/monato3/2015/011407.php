      <h3>Ekonomio</h3>
      <h3 class="tem">AŬSTRIO - GERMANIO</h3>
      <h2>Multobla banka skandalo: BayernLB kaj HGAA</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Dek naŭ miliardoj da eŭroj – malfacile imagebla sumo. Tiom kostas al la aŭstra ŝtato
      kaj la impostpagantoj multobla banka skandalo. Per tiu sumo oni povus konstrui kompletan
      urbon por pli ol 100 000 loĝantoj. La 
      <i>Hypo</i>-afero estas la plej granda kriminal-afero en Aŭstrio.
   </b>
</p> 
<p>Kun negocado finiĝis la proceso kontraŭ Werner Schmidt, la eksa estro la de germana
   banko 
   <i>BayernLB</i>. La 71-jara eks-bankisto estis kondamnita al kondiĉa malliberigo de dek ok monatoj
   pro subaĉeto de oficialulo. Krome, li pagu monpunon je 100 000 eŭroj.
</p> 
<p>Tian punon la juĝisto Joachim Eckert antaŭvidigis al Schmidt, kondiĉe ke li konfesas
   la koruptadon de Jörg Haider, la iama landestro de Karintio (la aŭstra federacia lando
   
   <i>Kärnten</i>). Schmidt tiam konfesis la subaĉeton de Haider lige kun la aĉeto de 
   <i>Hypo Group Alpe Adria</i> (HGAA). La tiama politikisto postulis 2,5 milionojn da eŭroj por la karintia futbalo,
   por konsenti kun la vendo de la HGAA al 
   <i>BayernLB</i> 
</p> 
<p>Jen la kroniko de la skandalo:
   <br></br> 1989 D-ro Jörg Haider [jerg hajda] estas landestro de Karintio ĝis 1991.
   <br></br> 1991 La karintia publika banko 
   <i>Kärntner Landes-Hypothekenbank</i> iĝas akcia kompanio kun la nomo 
   <i>Hypo Alpe Adria Bank</i>. Ĝi ege ekspansias en la balkanaj ŝtatoj. La ĉefo de la banko estas Wolfgang Kulterer.
   <br></br> 1999 Haider iĝas la duan fojon landestro de Karintio.
   <br></br> 2006 La nomo iĝas 
   <i>Hypo Group Alpe Adria</i>; financaj perdoj pro derivaĵaj spekuladoj.
   <br></br> 2007 50 % de la banko vendiĝas al la germana bavara landa banko 
   <i>BayernLB</i>.
   <br></br> 2008 Kulterer konfesas, ke li falsis la bilancon de la banko. La aŭstra ŝtato devas
   plialtigi la kapitalon de la banko je 800 milionoj da eŭroj, en 2009 sekvas 1,5 miliardoj.
   <br></br> 2008-10-11 La landestro Haider mortas pro aŭtoakcidento. Li veturas kontraŭ betona
   paliso. La rapideco de la aŭto estas 142 km hore, 72 km hore pli ol permesite sur
   tiu strato. En la sango de Haider estas 1,8 ‰ da alkoholo. Antaŭ la akcidento Haider
   kverele disputis kun sia samseksema amiko.
   <br></br> 2009 Por eviti nesolventecon de la banko la aŭstra ŝtato transprenas ĝin entute kontraŭ
   simbola prezo de unu eŭro.
   <br></br> 2011 Unuaj procesoj kontraŭ la bankaj manaĝeroj.
   <br></br> 2014 La banko iĝas 
   <i>bad bank</i> („ruba banko”) kaj estos iom post iom likvidata. La financa perdo estos sume 19 miliardoj da eŭroj.
   <br></br> 2014-10-27 Verdikto en la proceso kontraŭ la iama BayernLB-estro Werner Schmidt.
   La monpuno por Schmidt estas 100 000 eŭroj plus 18-monata karcero kun kondiĉa liberigo.
</p> 
