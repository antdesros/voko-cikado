      <h3>Turismo</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Favore al turismo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Zhongdian [ĝongdjan] estas malgranda gubernio en la nord-okcidenta parto de la ĉina provinco Yunnan [junan], kiu situas najbare de Tibeto. Antaŭe tre malmultaj konis ĝin, sed nun la gubernio ĝuas grandan famon. Ĉiujare ĝi altiras milionojn da turistoj. Tiu granda interesiĝo ekestis rezulte de la deklaro de la loka registaro en 1997, ke la fikcia loko Ŝangrilao (angle <i>Shangri-La</i>), unuafoje menciita en la romano <i>Perdita horizonto</i> de la brita aŭtoro James Hilton, troviĝas en la gubernio.</p> <p>En 1933 Hilton verkis la libron, en kiu li priskribas belan lokon, en kiu estas longa kaj mallarĝa valo Blua Luno kaj lamaejo Ŝangrilao, situanta sur krutaĵo ĉe la piedo de la neĝkovrita monto Karakal. Laŭ la romano iam en la 30aj jaroj de la pasinta jarcento okazis tumulto en la urbo Baskul en iu lando najbare de Barato. Brita diplomato, lia sekretario, usonano kaj misiistino decidis forlasi la urbon. Ili flugis per malgranda aviadilo al Peshawar [peŝavar], en la nuna Pakistano (tiam parto de la brita kolonio Hindio). Tamen la aviadilo estis kaptita kaj surterigita en dezerta loko. Gvidate de ĉina maljunulo, scipovanta la anglan, ili venis al la lamaejo Ŝangrilao. Ĝi regis la valon Blua Luno, kie loĝis miloj da homoj de pluraj gentoj, apartenantaj al malsamaj religioj. Ili harmonie vivis laŭ la principo de modereco kaj ĝenerale estis longvivuloj. En la lamaejo oni kolektis multajn trezorojn, librojn kaj instrumentojn eksterordinarajn el la orienta kaj okcidenta civilizacioj. Ĉiuj lamaoj estis bone klerigitaj ...</p> <p>Post dekoj da jaroj en iuj landoj homoj deklaris, ke Ŝangrilao troviĝas sur ilia teritorio: Ladakh de Kaŝmiro, Nepalo, Butano, valo Hunza en Pakistano kaj en sud-okcidenta Ĉinio. En la lasta regiono, pli precize en la gubernio Zhongdian, troveblas neĝmonto, profunda valo kaj lamaejo, menciitaj en la romano <i>Perdita horizonto</i>. Tie estas 470 montoj, kiuj altas je pli ol 4000 metroj super la marnivelo. Inter ili kuŝas valoj, stepoj, lagoj, virgaj arbaroj kaj glaciejoj. Pro specifaj situo kaj klimato tie formiĝis mirindaj vidindaĵoj, konsistantaj el komplika geomorfologia strukturo. Tie estas ankaŭ raraj bestoj kaj plantoj. En la antikveco la gubernio servis kiel grava transŝarĝa stacio ĉe la sud-okcidenta Silka Vojo. Nun ĝi havas ĉirkaŭ 130 000 loĝantojn el 13 etnoj kaj el malsamaj religioj. Ĉiu etno trankvile vivas laŭ siaj propraj moroj kaj kutimoj. En 2002 la gubernio Zhongdian transprenis la nomon Ŝangrilao.</p> <div style="text-align:right">Alice L<span class="mm">IU</span></div>        
