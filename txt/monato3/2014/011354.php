      <h3>Scienco</h3>
      <h3 class="tem">NUTRADO</h3>
      <h2>Ĉokolado, nia plej kora amiko (sed sen troigo!)</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Lastatempe multobliĝis sciencaj artikoloj pri la favora efiko de ĉokolado sur la homa
      sano. La esploroj venis el pluraj landoj: Francio, Aŭstralio, Italio, Britio. Oni
      studis la efikon de ĉokolado precipe ĉe la sangocirkula sistemo de homoj kaj konkludis,
      ke tiu nutraĵo, ŝatata de etuloj kaj ne nur, efektive protektas la angiojn, la koron,
      la cerbon.
   </b>
</p> 
<h3>Polifenoloj</h3> 
<p>Ĉokolado favoras la komplikan kaj delikatan metabolismon de histoj ene de la arterioj
   kaj samtempe malhelpas la estiĝon de arteriosklerozaj platoj. La sanigaj substancoj
   ĉeestaj en ĉokolado estas nomataj „polifenoloj”. Kompreneble, ju pli altas la proporcio de nigra ĉokolado en la englutata manĝaĵo,
   des pli saniga ĉi lasta estas. Laktoĉokolado male perdigas parton de tiu efiko.
</p> 
<h3>Marŝado</h3> 
<p>Antaŭ nelonge nekutima esploro plenumata en Italio mezuris la marŝokapablon de maljunaj
   homoj kun malbona sangocirkulado en la gamboj. Montriĝis, ke tiuj pacientoj, kiuj
   manĝis amaran, puran ĉokoladon du horojn antaŭ la testo, sukcesas marŝi pli longe
   ol tiuj, kiuj tion ne faris. Englutado de laktoĉokolado ne montris saman rezulton.
   Ĉi tiu studaĵo ankoraŭ estas provizora, sed medicinaj revuoj jam sciigas pri ĝi.
</p> 
<h3>Proverbo</h3> 
<p>Rilate al konsumado de ĉokolado, tamen, ĉiuj sciencaj publikaĵoj atentigas, spite
   al ĝia bongusteco kaj sanigeco, ke oni ne devas troigi: apud polifenoloj ne forgesendas
   la kvanto de kalorioj kaj sukero, kiujn ĉokolad-tabuletoj kaj similaj frandaĵoj enhavas.
   Aliflanke, ĉu ne estas jam konata la proverbo, laŭ kiu „ĉio supermezura estas terura”?
</p> 
