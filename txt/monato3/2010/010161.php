      <h2>Rajto je minimuma digno</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kun maloftaj esceptoj, italaj malliberejoj prezentas impreson de granda plimalboniĝo. Malobservataj estas eĉ minimumaj kriterioj difinitaj de artikolo 3 de la Eŭropa Konvencio pri Homaj Rajtoj, laŭ kiu karcerulo disponu pri almenaŭ 3,5  m<sup>2</sup> kaj povu esti ekster la propra ĉelo ne malpli ol ses horojn tage.</p> <p>En Italio, tamen, en ĉeloj de 6 m<sup>2</sup> kutime nepras enŝlosi almenaŭ tri personojn, dum 12 m<sup>2</sup> fojfoje devas sufiĉi por deko da prizonuloj. La tiel nomataj horoj da aero, kiuj laŭ Strasburgo estu tage almenaŭ ses, malaltiĝas en Italio al du.</p> <p>Alarmon sonigas la itala gazetaro post raporto komisiita de la konstanta observejo pri mortoj interne de malliberejoj. La raporto klare indikas, ke en la 11 malliberejoj, kiujn kontrolis la observejo, prizonuloj ricevas traktadon, kiun la Eŭropa Kortumo pri Homaj Rajtoj difinas „nehoma kaj humiliga”.</p> <h3>Konsekvencoj</h3> <p>La malliberejoj estas plenplenaj, asertas italoj, disetendante la brakojn, sed la konsekvencoj postulus malpli fatalisman reagon. Ekzemple, granda problemo restas enprizonaj memmortigoj. La neniel honorindan rekordon tenas la malliberejo de Kaljaro, en Sardinio, en kiu ekde 2005 prenis sian vivon 11 el la 50 malliberuloj. Sekvas la milana prizono San Vittore kun 1127 malliberuloj kaj en la sama periodo 13 memmortigoj.</p> <p>Rimarkindas, ke en la plimulto de la okazoj la memmortigintoj estas junuloj, enkarcerigitaj pro krimoj ne gravaj. Kelkaj el ili estas antaŭ nelonge arestitaj aŭ eĉ proksimaj al elkarcerigo.</p> <p>Pri Kaljaro oni legas en la raporto: „La konstruaĵo estas kaduka, la bontenado okaza. Ekstere de la ĉelo oni rajtas esti ne ses, sed nur kvar horojn. Instruaj kaj distraj aktivaĵoj estas tre limigitaj.” Plue, en septembro, oni eksciis, ke en la historia roma malliberejo Regina Coeli (konstruita en 1654) mankas jam dum 40 tagoj varma akvo. Tie 1000 kondamnitoj preparas sin al dua sinsekva vintro sen hejtado.</p> <h3>Higieno</h3> <p>Ĉu akcepteblas, ke en progresinta eŭropa lando, kia devus esti Italio, la malliberejoj entenas 67 580 prizonulojn en spaco, kiu apenaŭ sufiĉas por 44 218, kaj ke fine oni rajtas eĉ ne al minimuma persona higieno aŭ eĉ hejtilo? Tial ĉiujare la Eŭropa Kortumo pri Homaj Rajtoj ricevas milojn da priaj apelacioj.</p> <p>En Eŭropo, kun ĝiaj homaj rajtoj, mortopuno estas malpermesita. Eŭropanoj indigne kondamnas malproksimajn landojn, kiuj ankoraŭ ĝin aplikas. Ĉu estas tamen pli konsole por eŭropana burĝa kaj filistra konscienco kondamni homon al memmortigo aŭ fari nenion por ĝin eviti?</p> <p>Ankaŭ tio estas mortopuno, eble nerekta, sed same grava. Estu konsentite, ke ĉiu, kiu eraris, elpagu sian kulpon ĝis la lasta guto, sed en prizono estas homoj, ne bestoj, kiuj ankoraŭ rajtas je vivo kaj minimuma digno.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
