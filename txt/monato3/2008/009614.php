      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Historia histerio</h2>
      <!-- komenco de la enhavo -->

       

     <p>Ĉu vi ankoraŭ memoras pri danĝeroj, kiuj onidire grave minacis nin, jen en iuj partoj de la mondo, jen tutmonde. Estis ekzemple malsanoj aŭ venenoj. Kiam vi la lastan fojon aŭdis aŭ legis pri Ĉernobilo? Frapis min mencio pri ĝi okaze de akcidentoj en francaj atomcentraloj ĉi-somere. Sed ĝenerale la giganta atomkatastrofo, kiu minacis grandajn partojn de Eŭropo kaj Azio, estas forgesita. Do mi trakribris mian memoron pri aliaj danĝeroj, kiuj timigis nin en pasinteco. Ĉu vi ankoraŭ scias, kio estis „bovofrenezo” (laŭ la populara nomo)? Malsano de ŝafoj kaj bovoj, kiu estis riska ankaŭ por homoj, kiel oni distrumpetis dum multaj jaroj. Kiel malaperis (kun la miloj da mortigitaj malsanaj bestoj) ŝajne komplete tiu malsano, sen ke vere multaj homoj infektiĝis? Sekvis tiel nomata „birdogripo”, pro kiu oni avertis infanojn, kolekti plumojn. Jes, mortis ne malmultaj anasoj, anseroj kaj aliaj sovaĝaj kaj kortaj birdoj, sed ankaŭ en tiu okazo mi nek legis nek aŭdis pri gravaj infektiĝoj en Eŭropo (jes en Azio, de kie venis tiu malsano). Intertempe neniu plu parolas pri birdogripo.</p> <p>Pli danĝera evidente estis malsano nomata „sarso”, pro kiu (denove en Azio) mortis relative multaj homoj, sed ankaŭ ĉi-foje avertoj por Eŭropo estis troigitaj. Nur ege malmultaj eŭropanoj infektiĝis (kutime dum vojaĝo en Azio). Ĉi-jare en Germanio oni ĝojigis nin per la informo, ke tiu ĉi lando definitive estas senrabia. Fiere la fakuloj informis, ke tiun sukceson oni dankas al ampleksa programo por vakcinadi vulpojn kontraŭ rabio.</p> <p>Kiel ajn, la rilataj histerioj, kiujn estigis nelaste miaj ĵurnalistaj kolegoj en amaskomunikiloj, montriĝis troaj kaj superfluaj. Tiuj histerioj nun estas historiaj. Ĉi-jare en Germanio oni emfaze avertis pri kreskinta danĝero pere de iksodoj, kiuj pli abundas pro klimatoŝanĝo. Sed jam estas aŭtuno, kaj mi legis kaj aŭdis pri eĉ ne unu grava infektiĝo ... Do denove histerio, kiu nur iritas homojn. Restas tamen unu grava malsano mortdanĝera: aidoso. Sed ankaŭ pri ĝi oni preskaŭ ne plu parolas en evoluintaj landoj, neglektante hejmajn malsanulojn. Jes, en Afriko daŭre amase mortas homoj pro aidoso. Sed Afriko estas ekster la interesosfero de la norda hemisfero.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
