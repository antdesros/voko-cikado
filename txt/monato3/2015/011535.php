      <h3>Ekonomio</h3>
      <h3 class="tem">Litovio</h3>
      <h2>Malsukcesa aviado</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Du okazaĵoj en la litovia aviado de la pasinta printempo pruvas, ke tiu sektoro en
      Litovio ne prosperas: aviadilo falis, kompanio bankrotis ...
   </b>
</p> 
<h3>Malaperis aviadilo</h3> 
<p>La 16an de majo 2015 en Litovio estis tragika informo – super Balta Maro dum favoraj veterkondiĉoj malaperis aviadilo. Ĝi apartenis al eta
   kompanio 
   <i>Klaipėdos avialinijos</i> (Fluglinioj de Klaipėda), kiu aktivas precipe sur agrikultura kampo (sterkoŝutado),
   sed ankaŭ ofertas servojn de aera fotado kaj filmado kaj transportado de paraŝutistoj.
   Tiu ĉi en la jaro 1981 produktita unumotora rusa aviadilo 
   <i>An-2</i> flugis de la sveda urbo Gotenburgo al Klaipėda. La litova kompanio ĝin aĉetis en
   Svedio, kaj du pilotoj devis transporti ĝin al Litovio.
</p> 
<p>La taskon plenumis la du plej kvalitaj litovaj pilotoj Adolfas Mačiulis [maĉjulis]
   kaj Alvydas Selmistraitis. Ili jam estis en la aerspaco de Litovio, je preskaŭ 97
   marmejloj de la ĉemara urbo Palanga, kiam la kontakto kun la aviadilo subite malaperis.
   Des pli strange estis, ke la aviadilo estis teknike en ordo kaj ke la pilotoj havis
   kune 40-jaran sperton.
</p> 
<p>Tuja serĉado per aviadilo ne estis fruktodona. Ĉu la pilotoj saviĝis aŭ ne, longe
   ne estis klare. Kial la aviadilo malaperis, nur ili povus respondi. La aviadilon serĉis
   litovoj kaj latvoj en siaj respektivaj akvoteritorioj. Tri tagnoktojn oni bezonis,
   ĝis finfine la aviadilon oni trovis je pli ol 100 km for de la marbordo, je profundo
   de 124 metroj. Oni petis la nederlandan kompanion 
   <i>Mammoet</i> organizi observlaborojn de la falinta aviadilo. Nederlandaj spertuloj levis la flugmaŝinon
   al profundo de 5-10 m kaj observis ĝin. Oni konstatis, ke ene estas la korpo de nur
   unu piloto.
</p> 
<p>Kio estis la kialo de la katastrofo, ĝis nun ne estas klare. Medicinspertuloj post
   la observo de la trovita korpo de Adolfas Mačiulis konstatis, ke en lia sango estas
   0,77 promiloj da alkoholo. Onidire, ne li pilotis la aviadilon, sed lia kolego. Kie
   estas la dua piloto, ĝis nun ne estas klare, kaj probable tiu demando ankoraŭ longe
   restos enigmo en la litova aviadhistorio.
</p> 
<h3>Bankrotis flugkompanio</h3> 
<p>Tagmeze de la 22a de majo 2015 la litova flugkompanio 
   <i>Air Lituanica</i> informis, ke pro financaj problemoj ĉiuj flugoj ne plu estos efektivigataj. Onidire
   pasintjare la kompanio spertis malprofiton de ĉirkaŭ 10 milionoj da eŭroj. Tamen oni
   sukcesis trovi solvon por la pasaĝeroj: ilin transportis la latva flugkompanio 
   <i>Air Baltic</i>.
</p> 
<p>
   <i>Air Lituanica</i> estis la kompanio, kiun en 2013 kreis la municipo Vilnius. De tiam ĝi neniam estis
   en prospera situacio kaj senĉese bezonis financajn injektojn. La problemoj akriĝis,
   kiam ŝanĝiĝis la urbestro de Vilnius. La nova urbestro Remigijus Šimašius [ŝimaŝjus]
   rimarkis, ke la sindevigoj de la kompanio pli ol trioble superas la posedaĵon. En
   tia situacio neniu privata investanto volas subteni ĝin, kaj la municipo ne havas
   sufiĉe da financaj rimedoj por nutri la malprosperantan kompanion.
</p> 
<p>Dume la eksa urbestro kaj kreinto de 
   <i>Air Lituanica</i> Artūras Zuokas asertis, ke la decido fermi la kompanion estas pure politika. Post
   la bankroto ŝtataj organizaĵoj komencis pli aktive diskuti pri tio, kiel plibonigi
   la atingeblojn de Litovio. Oni decidis krei specialan fonduson por altiri novajn flugojn
   al Vilnius. La nova urbestro diris, ke la registaro tiucele povus investi ĉirkaŭ unu
   milionon da eŭroj kaj similan sumon povus investi ankaŭ la urbo.
</p> 
<p>Post la malapero de 
   <i>Air Lituanica</i> ekis negocado kun la latva flugkompanio 
   <i>Air Baltic</i> pri eventuala komuna flugkompanio de ĉiuj tri baltaj landoj: Latvio, Litovio kaj
   Estonio. Tuj post la malapero de la litova kompanio la latva anoncis ses novajn flugdirektojn
   el Vilnius.
</p> 
<h3>Kial la litovoj malprosperas?</h3> 
<p>Litovaj aviadspertuloj daŭre penas respondi al la demando, kial spite al multjaraj
   malprofitoj la estona flugkompanio 
   <i>Estonian Air</i> kaj la latva 
   <i>Air Baltic</i> plu funkcias, sed litovaj kompanioj unu post la alia fermiĝas. Iam la litovoj fieris
   pri sia aviado.
</p> 
<p>La unuaj aviadiloj el Kaunas komencis flugi en 1919, kaj la unua pasaĝera linio inter
   Königsberg (la nuna Kaliningrado) kaj Kaunas kun germanaj aviadiloj estis inaŭgurita
   la 26an de julio 1921. En 1938 estis fondita la kompanio 
   <i>Lietuvos oro linijos</i> (Litovaj fluglinioj) kun du seslokaj aviadiloj 
   <i>Percival Q.6</i>.
</p> 
<p>Tuj post la ŝtata resendependiĝo sur la bazo de la loka branĉo de la sovetia flugkompanio
   
   <i>Aeroflot</i> kreiĝis 
   <i>Lithuanian Airlines</i>. Antaŭ 15 jaroj ĝi havis dekon da itineroj al diversaj urboj de Eŭropo, sed la reto
   konstante disŝiriĝis. En 2005 la flugkompanio estis privatigita kaj ŝanĝis la nomon
   al 
   <i>FlyLAL – Lithuanian Airlines</i>. Ĝi funkciis ĝis sia bankroto en 2009.
</p> 
<p>Intertempe kreiĝis ankaŭ kelkaj aliaj etaj kompanioj, kiuj funkciis mallonge, estis
   malprofitaj kaj finfine bankrotis.
</p> 
<p>Ekonomikistoj kredas, ke litova komerca modelo ne estas praktika kaj agadorientado
   nur al Litovio aŭ Vilnius estas tro malgranda, por ke ĉi tie povu prosperi kompanioj.
   La plej bona solvo estus havi unu komunan kompanion en ĉiuj tri baltaj landoj aŭ subvencii
   ceterajn itinerojn laŭ aŭkcia modelo. Oni rimarkas ankaŭ, ke litovoj ĉion emas rapide
   ĉesigi. Dume la latva kompanio prosperis kaj daŭre plilarĝigas sian agadon.
</p> 
<p>Ankaŭ la estona 
   <i>Estonian Air</i> spertis malprosperajn momentojn, sed poste ĝi reviziis sian agadon kaj efike korektis
   ĝin. Ankoraŭ unu grava aspekto: en Latvio kaj Estonio rimarkeblas vere forta ŝtata
   subteno de la naciaj fluglinioj.
</p> 
