      <h3>Politiko</h3>
      <h3 class="tem">TOGOLANDO</h3>
      <h2>Vivo nevivebla</h2>
      <!-- komenco de la enhavo -->

       

       <p>Togolando kun 56 600 km<sup>2</sup> havas ĉirkaŭ kvin milionojn da loĝantojn. De sudo ĝis nordo la vivo ŝajnas pli kaj pli nevivebla por multaj togolandanoj, kies vivkondiĉoj malboniĝas. Ne ĝojas la popolo pri la situacio socia, kie nur malplimulto profitas.</p> <p>La problemo minacanta la popolon estas precipe ekonomia, kaŭzita de la politika. Notindas, ke antaŭ 1990 la loĝantoj vivis iom pli bone ol nun. Je la alveno de demokratio ĉiuj esperis, ke liberiĝos la popolo, kiu suferis dum multaj jaroj sub reĝimo diktatoreca. Unue ĉio evoluis glate: organizita estis i.a. nova registaro, en kiu reprezentiĝis diversaj politikaj partioj, kaj eĉ diplomitoj ektrovis laboron.</p> <h3>Danĝeroj</h3> <p>Malfeliĉe en novembro 1992 komenciĝis ĝenerala striko. Ĉesis ĉiuj aktivecoj publikaj kaj privataj, escepte de minimumaj kuracistaj servoj. Multaj homoj estis devigataj forlasi la ĉefurbon Lomeo kaj reiri al siaj foraj urbetoj aŭ vilaĝoj. Kelkaj eĉ fuĝis al najbaraj landoj, ekzemple Benino aŭ Ganao, timante eĉ pli grandajn danĝerojn. Mankis laboro kaj mankis salajroj: tiel estiĝis la ekonomia krizo.</p> <p>Tio daŭris naŭ monatojn, kiam aperis nova problemo: la uzata valuto FCFA (<i>Franc de la Communauté Financière Africaine</i>, franko de la afrika financa komunumo) perdis duonon de sia valoro. Rapide kreskis la kosto de varoj kaj servoj kaj restis nepagitaj salajroj de antaŭaj jaroj. Rezulte pli ol 90 % de la togolanda popolo ne taŭge manĝas: eĉ ne ekvivalenton de unu eŭro tage homoj kapablas elspezi. Oni nutras sin unu aŭ dufoje en la tago anstataŭ tri, sed al la disponeblaj manĝoj mankas kvalito kaj kvanto.</p> <h3>Kuracistoj</h3> <p>La socio tiel streĉe vivas, ke ties bezonoj eĉ urĝaj estas neglektataj. Kreskas malsatoj, sekvas malsanoj: mankas mono por viziti kuracistojn kaj hospitalojn. Ankaŭ edukado suferas: studado eĉ ĉe bazaj lernejoj devas esti pagata, kaj tiuj, kies gepatroj ne povas pagi, ne vizitas lernejojn.</p> <p>Krome, la krizo estigas ŝteladon, kiu vere maltrankviligas homojn. Rabistoj, agresantaj kaj minacantaj, forprenas posedaĵojn, tiel ke la togolandanoj senĉese sin demandas, ĉu la estonteco iam prosperos al ili.</p> <div style="text-align:right">S<span class="mm">AGBADJELOU</span> Kokou/pg</div>  
