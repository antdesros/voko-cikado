      <h3>Politiko</h3>
      <h3 class="tem">BALKANIO</h3>
      <h2>Ŝtato ankoraŭ ne stabila</h2>
      <!-- komenco de la enhavo -->

       

         <p>Post 1991, kiam Makedonio sendependiĝis, la tieaj albanoj esperis, ke ankaŭ ilia politika
   kaj ekonomia stato pliboniĝos. Tio ne okazis. Sekve albanoj ribelis, aparte post la
   liberigo de Kosovo en 1999, okazinta danke al trupoj de Nord-Atlantika Traktat-Organizaĵo.
</p> 
<p>En 2001 eksplodis milito en la okcidenta parto de Makedonio, precipe ĉirkaŭ Tetovo
   kaj Kumanovo. La ŝtato, dominata de slavomakedonoj, uzis la armeon por subpremi la
   ribelon kaj por ataki loĝlokojn de albanoj. Denove intervenis la internacia komunumo.
   Rezulte estis subskribita en 2001 la Interkonsento de Ohrid, kiu donis al albanoj
   diversajn rajtojn.
</p> 
<p>La albanoj ekokupis pli da postenoj en ŝtataj institucioj. La albana lingvo fariĝis
   oficiala en lokoj, kie albanoj konsistigis plimulton, kaj la albanan flagon oni rajtis
   vidigi okaze de festotagoj. Sed malgraŭ senĉesaj postuloj de albanaj civitanoj, la
   ĉefaj ekonomiaj instancoj plu restis en la manoj de slavomakedonoj.
</p> 
<h3>Verdiktoj</h3> 
<p>La ŝtato ne investis tie, kie vivas albanoj kaj limigis laborpostenojn. Severis juĝaj
   verdiktoj kontraŭ albanoj, kaj centmilo da albanoj ne estis registritaj kaj do ne
   povis akiri pasporton. Albanojn la ŝtato daŭre diskriminaciis rilate edukadon kaj
   kulturon.
</p> 
<p>En 2014 malkontento trafis ankaŭ la slavomakedonojn, aparte opoziciajn socialdemokratojn
   sub Zoran Zaev. Ne plu tolereblis la volo de la ĉefministro Nikola Gruevski avide
   arogi al si, simile al Putin en Rusio, ĉiujn povojn.
</p> 
<p>Krome estis ŝokita la publiko pro tio, ke la ministrejo pri internaj aferoj elspezis
   20 milionojn da eŭroj por havigi al si subaŭskultilojn. Kiam malkaŝiĝis la skandalo,
   albanoj eksciis, ke la ministrino pri internaj aferoj, Gordana Jankulovska, taksas
   ilin „sovaĝuloj, pro tio oni devas forpeli ilin translimen”.
</p> 
<h3>Tankoj</h3> 
<p>Tiel, kaj ne hazarde, la ministrejo pri internaj aferoj ĉi-jare komence de majo informiĝis
   pri neleĝa kunveno de armitaj albanoj en Kumanovo. Tien ĝi sendis dumnokte per 100
   busoj 4000 militistojn kun tankoj kaj kanonoj, kiuj pafis kontraŭ civilulaj domoj.
   Dum 16-hora batalo mortis 8 policanoj kaj 14 ribeluloj kaj la kvartalo estis almenaŭ
   parte detruita. Multaj civiluloj provis eskapi al suda Serbio, kie loĝas albanoj,
   aŭ Kosovo.
</p> 
<p>La registaro deklaris la ribelon subpremita kaj konsentis tute aŭ parte kompensi al
   tiuj, kies hejmoj estis damaĝitaj. Tamen ne eskapeblas demandoj, kial oni atakis tiel
   forte kvartalon loĝatan de ĉirkaŭ 20 000 albanoj kaj kial antaŭe oni ne evakuis civilulojn.
</p> 
<p>Kelkaj argumentis, ke la atako estis ruzo por direkti la atenton for de gravaj politikaj
   problemoj, inkluzive de subaŭskultado. Ĉiaokaze pro la afero eksiĝis du ministroj:
   Jankulovska kaj la ministro pri transportado. Tamen plu demandas sin albanoj, kiam
   vere kaj entute estos aplikata la Interkonsento de Ohrid.
</p> 
