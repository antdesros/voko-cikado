      <h3>Medio</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Klimatŝanĝaj fabeloj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉu vi legis aŭ aŭdis, ke la glacio en la mondo rapide degelas kaj baldaŭ sekvos giganta inundo en la landoj proksimaj al la maro? La faktoj montras tute alian bildon.</p> <p>Ĉirkaŭ la norda poluso ne estas firma tero. Naĝas sur la maro bankizo, kies dikeco estas inter kelke da centimetroj kaj 2 metroj. Vintre la bankiza areo estas ĉiujare pli-malpli 14 milionoj da km<sup>2</sup>, somere nur 4 milionoj da km<sup>2</sup>. Dum la lastaj jaroj la areo iom ŝrumpis. La bildo 1 montras la evoluon dum la lastaj 30 jaroj.</p> <p>Pro la malgranda dikeco la tuta volumeno de la glacio estas nur 10 000 km<sup>3</sup>.</p> <p>Sude de la norda poluso troviĝas Gronlando, kovrita de dikaj glaĉeroj. La glacia volumeno estas 3 000 000 km<sup>3</sup> kaj stabila. Tiu ĉi glacio aĝas ĝis 200 000 jarojn.</p> <h3>Sesa kontinento</h3> <p>Ĉirkaŭ la suda poluso kuŝas la sesa kontinento, Antarkto, kovrita de same dikaj, malrapide kreskantaj glaĉer-tavoloj. Ili aĝas pli ol 20 milionojn da jaroj. La volumeno estas giganta: 29 000 000 km<sup>3</sup>, do 90 % de la tuta monda glacio.</p> <p>Ĉirkaŭ Antarkto naĝas bankizo kaj ŝelfo, tio estas glaĉero glitinta ĝis sur la maro. Ankaŭ tiu glacio alternas laŭsezone inter 2 kaj 16 milionoj da km<sup>2</sup>. Averaĝe la volumeno estas 10 000 km<sup>3</sup>. Jen la evoluo en la lastaj 30 jaroj (Bildo 2).</p> <h3>Alpoj</h3> <p>Krome ekzistas aliaj glaĉeroj en diversaj mondopartoj. Ilia volumeno estas entute nur 200 000 km<sup>3</sup>. La volumeno de ĉiuj alpaj glaĉeroj estas nur 100 km<sup>3</sup>. Tiuj glaciamasoj dum la lastaj jaroj ĝenerale ŝrumpis, kelkaj kreskis. Mondskale tio signifas nenion. Katastrofo ne okazos.</p> <p>Krome ekde antaŭ 15 jaroj la tutmonda temperaturo ne plu kreskas.</p>       <div style="text-align:right">Walter K<span class="mm">LAG</span></div>  <p>En la apuda artikolo vi povas legi personan opinion pri kontestata temo. La redaktoro de la rubriko <i>Medio</i> havas funde alian opinion pri la temo. Se vi serĉas en la reto laŭ la serĉvortoj <i>global warming</i> aŭ <i>Greenland glacier loss</i> vi trovos amason da sciencaj artikoloj kun multegaj faktoj, kiuj kontraŭos la opiniojn de la artikolo. Jen resuma frazo: La scienca konsento pri klimatŝanĝo estas, ke „la klimato estas ŝanĝiĝanta kaj tiuj ĉi ŝanĝoj estas grandparte kaŭzitaj de homaj agado” [<i>America's Climate Choices: Panel on Advancing the Science of Climate Change</i>; National Research Council (2010). Washington, D.C.: The National Academies Press. ISBN 0309145880.] Ni bonvenigas viajn leterojn pri tiu ĉi temo. (Rimarkoj de la redaktoro)</p> 
