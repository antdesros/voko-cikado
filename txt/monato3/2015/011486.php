      <h3>Moderna vivo</h3>
      <h3 class="tem">TEĤNOLOGIO</h3>
      <h2>Neleda LEDo</h2>
      <!-- komenco de la enhavo -->

       

        <p>Inkandeskaj ampoloj donas agrablan lumon, sed bezonas multe da elektro kaj produktas
   nenecesan varmon. La tiel nomataj energi-ŝpar-lampoj bezonas malpli da elektro, sed
   enhavas venenan hidrargon. La medi-protekta alternativo estas LED-ampoloj.
</p> 
<p>La akronimo signifas Lum-Elsenda-Diodo. Lumas ne metalo, kiel en inkandeska lampo,
   sed gaso. LED malpli varmiĝas ol inkandeska lampo, sed bezonas elektronikan sistemon,
   kiu limigas la intenson de la elektra kurento. Tial ĝi kostas pli.
</p> 
<p>Tamen kompareblas kostoj. Inkandeska lampo, lumflukso 1350 lumenoj, povumo 100 vatoj,
   prezo 2 eŭroj, funkcia daŭro 1000 horoj. Unu kilovathoro da elektro kostas ekzemple
   0,20 eŭrojn. Tial 1000 horoj da lumo kostas 22 eŭrojn.
</p> 
<h3>Amortizita</h3> 
<p>Kontraste same hela LED, lumflukso 1350 lumenoj, povumo nur 15 vatoj, prezo 10 eŭroj,
   funkcia daŭro 20 000 horoj. Tial 1000 horoj da lumo kostas nur 3,50 eŭrojn. Se oni
   uzas LEDon tri horojn tage, la investo estos amortizita post kelke da monatoj.
</p> 
<p>Aktuale jam vendiĝas pli kaj pli da LEDoj. Laŭ prognozo de la esplor-instituto 
   <i>Statista</i> la vendosumo en Eŭropo estos 9 miliardoj da eŭroj en 2016, en 2020 jam 14 miliardoj.
</p> 
