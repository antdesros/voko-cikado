      <h3>Moderna vivo</h3>
      <h3 class="tem">TELEKOMUNIKADO</h3>
      <h2>Pioniro de sendrata teĥnologio</h2>
      <!-- komenco de la enhavo -->

       

         <p>La komenco de ricevado kaj elsendado de telegrafaj mesaĝoj estas ligita al la nomoj
   de la italo Guglielmo Marconi [guljelmo markoni] kaj de la ruso Aleksander Popov.
   Ne multaj scias, ke al tiuj nomoj apartenas ankaŭ tiu de la slovaka inventisto Jozef
   Murgaš [murgaŝ].
</p> 
<p>Murgaš kiel la unua en la mondo praktikis sendratan elsendon de homa voĉo jam en 1905.
   Lia klopodado pri elektrotekniko alportis al li sukceson kaj internacian agnoskon.
</p> 
<p>Murgaš, pastro, pentristo kaj amatora elektroteknikisto, naskiĝis antaŭ 150 jaroj,
   la 17an de februaro 1864, en la slovaka montvilaĝeto Tajov, ĉirkaŭ 150 km norde de
   la ĉefurbo Bratislavo.
</p> 
<h3>Kalumnioj</h3> 
<p>Kiel pastro li studis pentrarton en Budapeŝto kaj poste en Munkeno. Pro kalumnioj
   kaj insultoj deflanke de superaj hungaraj ekleziaj organoj li decidis elmigri al Usono.
</p> 
<p>Kiel 32-jara romkatolika pastro li alvenis al la minista vilaĝo Wilkes Barre, kie
   vivis proksimume 65 slovakaj familioj. Li zorgis pri slovakaj enmigrintoj, establis
   lernejon, bibliotekon, gimnastikejon, konstruis lud- kaj ban-ejojn, kaj komencis pli
   profunde interesi sin pri elektrotekniko en malgranda teknika laboratorio.
</p> 
<h3>Patentoj</h3> 
<p>En 1904 la federacia patentoficejo en Vaŝingtono agnoskis al Murgaš du patentojn.
   La unua estas konata kiel „aparato por sendrata telegrafo” kaj la dua kiel „metodo de transmisio de informaĵoj per sendrata telegrafo”.
</p> 
<p>La sukceso de liaj inventaĵoj estis grandega. La 26a prezidanto de Usono, Theodore
   Roosevelt, vizitis lian laboratorion en Wilkes Barre en 1905 kaj promesis al li financan
   registaran subtenon.
</p> 
<p>Post la vizito de la prezidanto sekvis aliaj patentoj: i.a. ondometro (1907), elektra
   transformatoro (1907), aparato por produkti elektromagnetajn ondojn (1908), sendrata
   telegrafo (1909) kaj aparato por produkti elektrajn oscilojn (1911).
</p> 
<h3>Mondomilito</h3> 
<p>Dum la unua mondomilito estis malpermesataj en Usono privataj radiotelegrafaj stacioj.
   Pro tio Murgaš decidis dediĉi sin denove al pentrarto. Li estis konata ankaŭ kiel
   pasia kolektanto de mineraloj, fungoj kaj kreskaĵoj. Krome li havis unikan kolekton
   de insektoj, ĉirkaŭ 9000 ekzempleroj.
</p> 
<p>Kiel prezidanto de la Slovaka Nacia Konsilio en Usono li organizis subskribojn por
   subteni la kreadon de la Ĉeĥoslovaka Respubliko. En 1918 li kunsubskribas la Pitsburgan
   Konsenton, laŭ kiu reprezentantoj de ĉeĥoj kaj slovakoj konsentis fondi komunan ĉeĥoslovakan
   ŝtaton post la disfalo de Aŭstrio-Hungario.
</p> 
<h3>Diplomo</h3> 
<p>En 1920 Murgaš, jam konata kiel usona inventisto, decidis reveni hejmen kaj instrui
   elektroteknikon. La tiamaj ĉeĥoslovakaj lernejaj instancoj rifuzis lian proponon,
   ĉar li ne posedis oficialan diplomon de profesoro de elektrotekniko. Vundite, li amare
   revenis al Usono, kie li mortis en 1929.
</p> 
<p>En lia slovaka naskiĝa vilaĝeto estas kreita memorĉambro, kie vizitantoj povas trovi
   originalajn bildojn, maketojn pri la inventaĵoj kaj diversajn dokumentojn kaj skribaĵojn.
</p> 
