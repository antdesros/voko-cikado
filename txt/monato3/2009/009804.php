      <h3>Politiko</h3>
      <h3 class="tem">BALKANIO</h3>
      <h2>Naskiĝas du pliaj NATO-membroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ekde frua aprilo 2009 Albanio kaj Kroatio membras oficiale en Nord-Atlantika Traktat-Organizaĵo (NATO). Finfine eblis membriĝo post interna reformado de la ekonomia kaj jura sistemoj de la du ŝtatoj, same plifortigo de sekureco, libereco kaj aliaj aspektoj de la socia vivo.</p> <p>Albanoj estas sufiĉe entuziasmaj pro membriĝo. Tion spegulas lastatempa enketo, kiu indikas, ke 98 % de la albanoj subtenas membriĝon. Tiel ili atendas pli da fremdaj investaĵoj, kiuj probable altiĝos pro enlanda sekureco. Cetere membriĝo en NATO signifas alpaŝon al membriĝo en la Eŭropa Unio (EU). Fine, NATO-membriĝo firmigas landlimojn kaj ties agnoskon fare de najbaraj ŝtatoj, ekzemple Grekio.</p> <p>Ne hazarde, antaŭ la NATO-membriĝo de Albanio, Grekio subskribis traktaton pri suda marlimo, malgraŭ protestoj de naciismaj organizaĵoj en Grekio, kiuj delonge volas teritorion en Norda Epiro aŭ suda Albanio.</p> <h3>Blokinta</h3> <p>Bonŝance, Kroatio jam solvis problemojn kun Slovenio, kiu portempe estis blokinta ties membriĝon kaj kiu same atendas baldaŭan membriĝon ankaŭ en EU.</p> <p>Aliflanke Makedonio ankoraŭ stagnas pro blokiĝo de najbara Grekio, kiu postulas ŝanĝon de la nomo de tiu ĉi ŝtato. Albanaj partioj en Makedonio volas, ke ĝi nomiĝu Norda Makedonio, kio kontentigus grekojn. Poste oni esplorus sciencmaniere la etnogenezajn radikojn de la popolo de Makedonio. Se dekstrulaj regantoj slavmakedonaj ne tiom longe obstinus, Makedonio estus jam la 29a NATO-membro.</p> <p>Oni atendas, ke baldaŭ membriĝos al NATO ankaŭ du aliaj okcident-balkanaj landoj: Bosnio-Hercegovino kaj Montenegro. Tiucelan iniciaton jam entreprenis Albanio kaj Kroatio kunlabore kun Usono. Antaŭ nelonge estis subskribita interkonsento pri libera senviza komunikado inter Albanio kaj Bosnio-Hercegovino.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
