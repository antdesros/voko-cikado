      <h3>Politiko</h3>
      <h3 class="tem">DEFENDADO</h3>
      <h2>Ĉu Eŭropa Unio fariĝas militema?</h2>
      <!-- komenco de la enhavo -->

       

       <p>La membroŝtatoj de Eŭropa Unio (EU) decidis en novembro transformi sian sekurec- kaj defendo-politikon. Jam venontjare la unua el 13 t.n. batalgrupoj estu aktivigebla. Tiuj grupoj estu la bazo de eŭropa intervenarmeo.</p> <p>Precipe la grandaj EU-ŝtatoj favoris koncepton de rapidege moveblaj trupoj. Dum du jaroj 15 000 soldatoj estu rekrutigitaj por tiu truparo, kies unua celo estas pacigi konfliktregionojn. „Tio montras la signifon, kiun oni atribuas al la defendopolitiko”, ĝojas EU-komisiito por eksteraj aferoj Javier [ĥabjér] Solana. Ankaŭ reprezentanto de la nederlanda EU-prezideco kontentas: „Tiel ni pli bone povos reagi al la tutmondaj defioj.” Kun tiu decido la eŭropa sekurec- kaj defendo-politiko ricevas unuafoje konkretan formon.</p> <h3>Forte armitaj soldatoj</h3> <p>Batalgrupo konsistas el 1500 forte armitaj soldatoj. En maksimume du semajnoj tiuj grupoj kadre de UN-operacioj estu sendeblaj al regionoj ĉie en la mondo, kiujn trafis subita krizo, kaj ili povu tie resti dum maksimume kvar monatoj. Eblaj engaĝiĝoj estas difinitaj per la „Petersburgaj taskoj”, nome konfliktprevento, devigo al paco aŭ humanecaj help-engaĝiĝoj. Kvar tiaj unuoj estu ĉiam aktivigeblaj, por ke du operacioj povu esti realigataj samtempe.</p> <p>Francio, Italio, Britio kaj Hispanio ekspedos propran batalunuon. La aliaj batalgrupoj estos plenigitaj per soldatoj el aliaj EU-ŝtatoj. Nome de la nederlanda EU-prezideco la nederlanda ministro Henk Kamp deklaris, ke la EU-batalunuoj estos komplementaj al la reagunuoj de NATO por eviti kreon de duoblaj strukturoj. Ankaŭ la neŭtralaj EU-ŝtatoj Aŭstrio kaj Finnlando partoprenas la koncepton, same kiel la NATO-ŝtato Norvegio, kiu ne membras en EU.</p> <h3>Krizoregionoj</h3> <p>Solana jam pasintjare atentigis kaj petis la membroŝtatojn, ke ili atentu ne nur pri la starigo de militaj reagtrupoj, sed ankaŭ pri sufiĉe da policaj kaj administraj fortoj por eventuala sendo al krizoregionoj. Krome bone progresas starigo de komuna EU-agentejo por armadaj aferoj.</p> <p>Solana, kiu fariĝos la unua EU-ministro pri eksterlandaj aferoj, kiam la EU-konstitucio estos ratifita, jam planas por tiu tempo. Jam decidita estas starigo de diplomata servo propra al la Unio, kiu povos ampleksi ĝis 7000 homojn. Konfliktoj jam antaŭvideblas inter la Eŭropa Komisiono kaj la Konsilio de EU, ĉar Solana reprezentos ambaŭ organojn kaj estos tre potenca sub la nova konstitucio, ĉar li respondecas krom pri la eksterlanda politiko ankaŭ pri komerco kaj evoluigo.</p> <p>La servo konsistos el oficistoj de la Komisiono kaj el la membroŝtatoj kaj estu starigota antaŭ la fino de 2006. Ĉi-momente la EU-membroŝtatoj havas 600 ambasadojn, dum la Komisiono havas pli ol 100 delegaciojn en la tuta mondo.</p> <div style="text-align:right">Marko Naoki L<span class="mm">INS</span></div>  
