      <h3>Moderna vivo</h3>
      <h3 class="tem">KANTADO</h3>
      <h2>Konkurso videble notinda</h2>
      <!-- komenco de la enhavo -->

       

       <p>Paco, frateco. Du bazaj ingrediencoj por estonta socio, kiu estos – oni esperas – pli justa ol la nuna. Du idealoj, kiuj tre bone karakterizas dum pli ol duonjarcento internacian festivalon pri kanzonoj por infanoj, kies itallingva nomo estas <i>Zecchino d'oro</i> [dzekino], ora zekino.</p> <p>Organizata ĉiun novembron en la itala urbo Bolonjo de la bonfara institucio Antoniano, fondita en 1953 de kvar katolikaj monaĥoj, tiu kermeso estas dissendata eŭrovizie de la ŝtata televidkanalo <i>Rai Uno</i>.</p> <h3>Kulturheredaĵo</h3> <p>Ĉu kanto-festivalo kiel multaj aliaj? Absolute ne. Fakte ĝi estas la sola televida elsendo rekonata de Unesko – apud centoj da monumentoj kaj urboj – kiel monda kulturheredaĵo. Atendas ĝin jaron post jaro milionoj da infanoj kaj gepatroj, kaj dum kelkaj tagoj haltas la kutimaj posttagmezaj elsendoj.</p> <p>Partoprenas tiun muzikan eventon infanaj kanzonoj devenantaj kaj el Italio kaj el eksterlando. Nur ok el la proksimume 300 kantoj, kiuj alvenis en marto al la sidejo de Antoniano, sukcesis trapasi la ĉi-jaran selekton.</p> <h3>Spektantaro</h3> <p>Konkursaĵoj el ekster Italio – en 2010 el Japanio, Pollando, Filipinoj kaj Azerbajĝano – estas nur parte kantataj en la devenlanda lingvo. Plejparte ili estas tradukitaj italen, por ke la spektantaro povu kompreni.</p> <p>Kutime la kanzonoj estas amuzaj kaj malseriozaj, kun gaja melodio kaj sprita teksto. Tamen neniam mankas kantoj, kiuj alfrontas problemojn pli profundajn: minoritatanan laboron, rasismon, perforton.</p> <p>La kermeso daŭras preskaŭ semajnon. Ĉiun posttagmezon la kantistoj, elektitaj dum alia konkurso tra la tuta Italio kaj eksterlande, prezentas sian pecon, akompanate de la ĥoro de Antoniano. Voĉdonojn ili ricevas ne nur de la enstudia publiko, sed ankaŭ de la televidaj spektantoj, pere de telefona voĉdonado.</p> <h3>Muso parolanta</h3> <p>La elsendo estis dum longa periodo ligita al sinjorino Mariele Ventre, kiu dum jardekoj estris la koruson. Aliaj simboloj de la festivalo estis ĝia fondinto, Cino Tortorella, alinome Mago Zurlì, pro siaj iamaj magiaĵoj, kaj la amegata pupo Topo Gigio (muso parolanta, konata ankaŭ en hispanlingvaj landoj). Ilin tamen, en la lastaj jaroj, pro miskomprenoj kaj, ŝajne, ekonomiaj kialoj, oni sendanke forigis el la festivalo.</p> <p>La konkurso en 2010 finiĝis per nekutima duobla triumfo: du kanzonoj, dediĉitaj respektive al laborema kamparano kaj al lerta ĉiofara patro, atingis precize la saman poentaron.</p> <p>Indas mencii, ke ĉiujn enspezojn (ekzemple de la vendado de diskoj) ricevas bonfaraj organizaĵoj, kaj ke la finan premion – oran zekinon – oni aljuĝas ne al kantisto sed al la aŭtoro kaj komponisto de la venkinta kanzono.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
