      <h2>Kvin jaroj en Eŭropa Unio</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Profesoro Jonas Čičinskas de la Instituto pri internaciaj rilatoj kaj politiko de la Universitato de Vilnius opinias, ke Litovio dum la kvin jaroj de sia aneco en Eŭropa Unio (EU) gajnis multon. Tamen por atingi averaĝan vivnivelon de EU la litovoj ankoraŭ bezonos 15 jarojn.</b></p> <p>Kvinjara aniĝo de Litovio en EU por la poŝo de statistika litovo alportis duoblan salajron, sed ankaŭ duoblan prezon por multaj ĉiutage uzataj produktoj. Se ni komparas la prezojn de antaŭ la aniĝo en EU je la 1a de majo 2004 kun la hodiaŭaj ni rimarkas, ke i.a. altiĝis prezoj de terpomoj (de 0,17 EUR ĝis 0,38 EUR) kaj de kilogramo de sekala pano (de 0,62 EUR ĝis 1,31 EUR). Inter la ĉefaj produktoj nur sukero nun kostas iom malpli ol antaŭ kvin jaroj.</p> <p>EU manifestiĝas ne nur per kresko de prezoj kaj vigla ekonomio sed ankaŭ per altiĝo de enspezoj. La meza pensio kreskis de 107,74 EUR ĝis 234,59 EUR, la minimuma monata salajro de 144,81 EUR ĝis 231,70 EUR kaj la averaĝa neta monata salajro de 241,83 EUR ĝis 513,50 EUR. Hodiaŭ litovoj atingas ĉirkaŭ 60 % de la averaĝa EU-vivnivelo, kompare kun nur ĉirkaŭ 40 % antaŭ kvin jaroj.</p> <p>Falis la kvanto de loĝantaro. La grandega elmigrado estas konsiderata kiel la plej serioza perdo de la lando. Antaŭ kvin jaroj Litovio havis 3,425 milionojn da loĝantoj kaj nun 3,346 milionojn.</p> <p>Kiel ni vivus se ni estus ekster EU? „Probable ni estus kvazaŭ blanka korvo inter la postkomunismaj landoj, ĉar la aliaj estus en EU. Eble ni havus pli da propraj bankoj kaj asekuraj kompanioj, sed tiuj estus malmodernaj kaj malfortaj. Supozeble nia ekonomio estus direktita al Ruslando aŭ Ukrainio, dum komercado kun Okcidento estus pli malfacila. Ni produktus pli por Oriento ol por Okcidento”, opinias profesoro Čičinskas.</p> <p>Liaopinie, Litovio eluzis siajn eblojn je 75 %; por atingi la averaĝan EU-vivnivelon ĝi bezonos ankoraŭ 15 jarojn. La plej granda malsukceso de Litovio estas la nesufiĉa modernigo de ekonomio; la plej sukcesa estas la komerco orientita al Okcidento. Li aldonas, ke gravan instigon por la ekonomio donus alveno de grandaj investantoj.</p> <p>La prognozoj por la estonteco ne estas optimismaj. Onidire la ekonomio de la lando ĉi-jare falos je 11 %, dum en la eŭro-landoj oni antaŭvidas falon de nur 4 %. La senlaboreco en Litovio ĉi-jare atingos 13,8 % kaj sekvontjare 15,9 %. La sola fajrero de espero estas la malkresko de la inflacio, kiu ĉi-jare devus esti 3,6 %. Sekvontjare ni atendu eĉ deflacion je 0,4 %.</p>. <div style="text-align:right"><span class="mm">LAST</span></div> 
