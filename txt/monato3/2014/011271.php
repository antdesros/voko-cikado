      <h2>Marŝi kun mortontoj</h2>
      <!-- komenco de la enhavo -->

       

        <p>Antaŭ cent jaroj eksplodis la unua mondomilito (1914-1918). Tiam unu el la militaj
   pentristoj estis la aŭstro-tirolano Albin Egger-Lienz. En marto malfermiĝis en Vieno
   ekspozicio de liaj pentraĵoj.
</p> 
<p>Jam en 1909 Egger-Lienz pentris 
   <i>Der Totentanz von Anno Neun</i> (La marŝo de la mortontoj de 1809) okaze de la centjariĝo de la liberiga milito de
   la tirolanoj kontraŭ Bavario.
</p> 
<p>Laŭ Agnes Husslein-Arco, la direktorino de la muzeo Belvedero (<a href="http://www.belvedere.at/de">www.belvedere.at/de</a>), kie okazas la ekspozicio, tiu bildo en ses versioj iĝis poste unu el liaj plej
   gravaj verkoj kaj ikono de la unua mondomilito.
</p> 
<p>Por Egger-Lienz mem tiu pentraĵo estis ne kontraŭmilita, sed esprimo de homoj, kiuj
   rezignacie transprenas sian sorton kaj pereon.
</p> 
<p>La ekspozicio „Egger-Lienz kaj la Milito” estas vizitebla ĝis la 9a de junio 2014.</p> 
<p>
   <b>Biografio de Albin Egger-Lienz:</b>
   <br></br> 1868-01-29: Naskiĝis en Stribach apud Lienz en orienta Tirolo kiel eksteredzeca filo
   de Maria Trojer kaj Georg Egger.
   <br></br> 1881: Subtenis lin la pentristo Hugo Engl, amiko de la patro.
   <br></br> 1884-1893: Studis ĉe la Munkena Akademio por Figura Arto. Konatiĝis kun Franz Defregger,
   kiu influegis lin.
   <br></br> 1891: Unua publika ekspozicio de liaj verkoj en Munkena Vitra Palaco. Ekde nun Albin
   uzis la nomon Egger-Lienz.
   <br></br> 1893 ĝis 1899: Laboris kiel libera pentristo en Munkeno kaj orienta Tirolo. Edziĝis
   kaj translokiĝis al Vieno.
   <br></br> 1900: Ricevis bronzan medalon ĉe la Monda Ekspozicio en Parizo pro sia pentraĵo 
   <i>Feldsieger</i> (Venkinto sur la batalkampo).
   <br></br> 1901: Ricevis la Grandan Oran Ŝtatan Medalon pro la pentraĵo 
   <i>Das Kreuz</i> (La kruco).
   <br></br> 1902 ĝis 1905: Instruis private en sia ateliero kaj fondis privatan pentrado-lernejon
   por virinoj.
   <br></br> 1906: Ricevis ŝtatan komision por la unua versio de 
   <i>Der Totentanz von Anno Neun</i> (La marŝo de la mortontoj de 1809).
   <br></br> 1910: Malsukcesis pro interveno de la tronheredonto Franz Ferdinand fariĝi profesoro
   en la Akademio por Figura Arto en Vieno.
   <br></br> 1911: Forlasis Vienon kaj ekloĝis en Hall en Tirolo.
   <br></br> 1912: Iĝis profesoro en la Akademio por Figura Arto en Weimar, Germanio. En Dresden,
   Germanio, oni montris 12 el liaj verkoj en la ekspozicio Monumenta-Dekoracia Pentroarto.
   Rilate al tiu Egger-Lienz publikigis polemikan traktaĵon, kies sekvo estis la Hodler-disputo.
   La pentristo Ferdinand Hodler estis la patro de Hector Hodler, fondinto de Universala
   Esperanto-Asocio. En siaj Marignano-freskoj Hodler montris ne la heroecon de la milito,
   sed ties realismon kaj detruemon.
   <br></br> 1913: Rezignis pri la profesora posteno en Weimar. Translokiĝis al Sankt Justina
   ĉe Bozen en suda Tirolo. Tie li gvidis en Klausen privatan artan lernejon, kie kunlaboris
   12 el liaj lernantoj el Weimar.
   <br></br> 1914: Komenciĝis la unua mondomilito. Tria versio de 
   <i>Totentanz</i>.
   <br></br> 1915: Iĝis soldato, poste milita pentristo. Kvara versio de 
   <i>Totentanz</i>.
   <br></br> 1920 ĝis 1921: Kvina kaj sesa versioj de 
   <i>Totentanz</i>.
   <br></br> 1923-1925: Pentris freskojn en la Memoriga Kapelo por Soldatoj en Lienz. Pro la grafika
   prezento de la releviĝinta Jesuo la eklezio malpermesis mesojn en la kapelo.
   <br></br> 1926-11-04: Mortis 58-jara en Sankt Justina.
</p> 
