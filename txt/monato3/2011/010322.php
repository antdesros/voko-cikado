      <h3>Moderna vivo</h3>
      <h3 class="tem">JUSTECO</h3>
      <h2>Kial la kolero?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Murdita en januaro en Pornic, Francio, estis junulino nomita Laëticia. Pri la krimo respondecis recidivulo. Laŭ la franca prezidanto, Nicolas Sarkozy, „homoj” estu punataj: la „homoj” estas la juĝistoj kaj tiuj, kiuj prizorgas krimulojn liberigitajn. Malakcepteblas, diris la prezidanto, ke viro konata al la justico denove murdas.</p> <p>Sarkozy forgesis, ke la situacio ne estas nova. Tiuj „homoj” helpas eksajn malliberulojn trovi laboron aŭ loĝejon; ili helpas al aliaj, kondamnitaj al puno kun prokrasto, alkonformiĝi al la postuloj de la tribunalo, ĉar, okaze de malobeo, la kondamnito reiros malliberejen.</p> <p>La problemo kuŝas en tio, ke la specialaj juĝistoj kaj la aliaj fakuloj, kiuj prizorgas malliberulojn, respondecas ĉiuj pri po inter 140 kaj 180 dosieroj. Ili devus havi maksimume 80.</p> <h3>Striki</h3> <p>La justic-oficistoj do ekkoleris, ne povante akcepti la vortojn de la prezidanto. Ili decidis striki, sed ili ne rajtas (kvankam en Francio rajtas striki soldatoj aŭ policistoj). Verdire la situacio ekzemplas gravajn problemojn en la franca justico. Mankas sufiĉe da justic-oficistoj, ŝtat-oficistoj kaj ankaŭ mono.</p> <p>La sindikatoj avertis la parlamenton pri la mankoj, kiuj signifas, ke rilate justico-buĝeton Francio sidas en Eŭropo en la 37a loko el 40. Kulpas Sarkozy, kiu antaŭ kvar jaroj forigis financajn kaj stabanajn rimedojn.</p> <p>Certe nenio povis eviti la murdon de Laëticia. La juĝistoj simple aplikas la francan leĝon. Krimulo kondamnita al 10-jara enkarcerigo ĝenerale estas elprizonigata post ses jaroj. La problemo estas, ke necesas adapti la punan respondon laŭ la personeco de la deliktinto.</p> <h3>Kaŝi</h3> <p>La murdo de la junulino estas hontinde kaj malinde ekspluatata de la prezidanto kaj liaj ministroj por kaŝi la malsukceson de ilia sekureca politiko. Envere kulpas la prezidanto, kiu kreis pli da dosieroj kaj malpli da homoj por ilin pritrakti, plus multe da novaj leĝoj.</p> <p>Notindas, ke unuafoje viktimo-asocioj, advokatoj kaj ankaŭ 65 % de la franca popolo subtenas la koleran reagon de la justic-oficistoj.</p> <div style="text-align:right">Astrid M<span class="mm">ILLET</span></div> 
