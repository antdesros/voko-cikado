      <h3>Moderna vivo</h3>
      <h3 class="tem">GERMANIO</h3>
      <h2>Tapiŝon ne deziris, traŭmaton akiris</h2>
      <!-- komenco de la enhavo -->

       

       <p>Persa tapiŝo dum multaj jaroj kuŝis neatentate en magazeno. La posedanto, tapiŝ-komercisto en Munkeno, donacis ĝin al sia iama dommastrumistino. Tiu heredigis ĝin al konatino.</p> <p>Necerta pri la valoro de tiu tapiŝo, la heredintino en oktobro 2009 komisiis aŭkciiston, kiu taksis la tapiŝon je valoro de 900 eŭroj.</p> <p>Fakte la tapiŝo estis aŭkciita je la konsiderinda sumo de 19 700 eŭroj al tapiŝ-komercisto el Hamburgo. Ŝajne lukra vendo. Ĝojiga profito por ambaŭ, la posedintino kaj la aŭkciisto.</p> <h3>Aŭkciejo</h3> <p>Hazarde la iama heredintino eksciis dum televidsendo, ke duonjaron poste la sama tapiŝo atingis prezon de 7 200 000 eŭroj ĉe aŭkciejo <i>Christie's</i> en Londono.</p> <p>Rekordo: la plej valora tapiŝo en la mondo. Spertuloj ĉe <i>Christie's</i> eltrovis, ke la tapiŝo originis el la irana provinco Kerman kaj estis produktita en la 17a jarcento.</p> <h3>Nevo</h3> <p>Iam ĝi apartenis al la franca art-mecenatino grafino de Béhague [behág] (1870-1939). Post ties morto ŝia nevo, markizo de Garnay [garné], heredis la valoran pecon, kaj ĝis aŭkcio en 1987 en Monako la tapiŝo restis en la posedo de tiu aristokrata familio. Poste ĝi venis al Germanio en la posedon de munkena tapiŝ-komercisto.</p> <p>Nun la heredintino akuzas la komencan aŭkciiston, ke tiu ne rekonis la veran valoron de la malnova tapiŝo kaj grave subtaksis ĝin. Ŝi postulas 350 000 eŭrojn: la prezo taksita de <i>Christie's</i> estis 346 000 eŭroj.</p> <div style="text-align:right">Jomo I<span class="mm">PFELKOFER</span></div> 
