      <h2>Aludaj ludoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Sociaj retejoj, kiel <i>Facebook</i>, <i>Twitter</i> kaj <i>Ipernity</i>, ludas ĉiam pli gravan rolon en la ĉiutaga vivo (minimume en tiu de okcidentanoj).</p> <p>Tamen ŝajnas, ke pluraj famuloj ankoraŭ ne komprenas la resonon, kiun povas estigi iliaj interretaj deklaroj. Frazeto povas transformiĝi en vitriolan komenton, kiu facile rondiras la mondon.</p> <p>Nuntempe „pepadas” ne nur birdoj, sed ankaŭ iom naivaj kantistoj, futbalistoj kaj politikistoj. Ili emas kredi, ke estas ĉies rajto havi kaj dissendi propran opinion pri ajna temo (ekonomio, politiko, religio).</p> <h3>Atletino</h3> <p>Eble tamen renomuloj bridu siajn vortojn, ĉar ili elmetas sin al ekspluatado. Tio okazis lastatempe en Grekio, kie atletino, Paraskevi Papachristou [paraskjeví papaĥrístu], pli fama en sia patrolando laŭ la karesforma nomo Voula [vula], estas ekskludita el la ĉi-jaraj Olimpiaj Ludoj.</p> <p>Ŝi deklaris ĉe <i>Twitter</i>: „Grekio estas tiel plena de afrikanoj, ke moskitoj, kiuj devenas de Afriko, ne plu devas ŝanĝi siajn nutrajn kutimojn”.</p> <p>La koncepto, malbone esprimita kaj des pli maloportune lokita, aludis la fakton, ke ĉi-jare „invadis” Grekion tiom da moskitoj kiom da afrikanoj – ĉi-lastaj spite al landa ekonomia krizo kaj kreskanta senlaboreco.</p> <h3>Rasismo</h3> <p>La greka olimpia komitato, kiu lastatempe malpermesis al siaj atletoj uzi sociajn retejojn por disvastigi personajn kaj nesportajn komentojn, akuzis la junulinon pro rasismo kaj do rezignis pri ŝia talento.</p> <p>La reago de la komitato ŝajnis al pluraj iom troa, nelaste ĉar la atletino tuj petis pardonon pro mispaŝa, laŭdire humura (post sumigo de ĉio), komento.</p> <p>Ne temas cetere pri izolita kazo. Tuj post la komenco de la ludoj la svisa futbalisto Michel Morganella post nerva matĉo kontraŭ Sud-Koreio „pepis”, ke sud-koreanoj estas senescepte „mensaj handikapitoj”.</p> <p>Evidente sensencaĵo. Argumenteblas, ke la mense handikapitoj estas tiuj, kiuj naive uzas interreton, ne rezonante kaj ne komprenante la forton de siaj vortoj kaj ties eventualan efikon.</p> <div style="text-align:right">Roberto P<span class="mm">IGRO</span></div> 
