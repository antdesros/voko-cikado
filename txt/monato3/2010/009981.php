      <h3>Politiko</h3>
      <h3 class="tem">TERORISMO</h3>
      <h2>Kial la diskriminacio?</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 25an de decembro 2009 niĝeriano provis eksplodigi usonan aviadilon kun preskaŭ 300 pasaĝeroj. Unue, en Niĝerio oni taksis la raporton erara kaj ridinda. Sed kiam montriĝis, ke temas ja pri 23-jaraĝa niĝeriano, ne nur ŝokiĝis, sed ankaŭ ekkoleris la tuta lando. Se oni estus kaptinta la junulon, li estus dispecigita.</p> <p>Eble malfacilas al neniĝerianoj kompreni reagon tian. Tamen Umar Farouk Abdul Mutallab devenis el unu el la plej riĉaj familioj en Niĝerio. Lia patro, Alhaji Mutallab, estris la plej grandan kaj plej sukcesan bankon en Niĝerio. Kiam li emeritis, oni elektis lin estro de nove fondita islama banko, la unua en Niĝerio.</p> <h3>Lernejo</h3> <p>Dum aliaj junuloj vivas en malriĉeco, kaj apenaŭ povas trovi panon, Abdul Mutallab edukiĝis eksterlande, ĉe brita lernejo en Togolando kaj poste ĉe la Universitato de Londono. Cetere li facile vojaĝis ĉien en la mondo.</p> <p>Kiom el la 150 milionoj da niĝerianoj havas tiajn eblojn? Kiom el ili povas akiri vizojn por studi aŭ vojaĝi? Tio ne estas la sorto de ordinara niĝeriano, kiu ĉiutage alfrontas malriĉecon kaj senlaboron, kaj kiu manĝas rubaĵojn. Do estas fakto, ke Abdul Mutallab ne reprezentas niĝerianojn.</p> <h3>Konfliktoj</h3> <p>Cetere, malgraŭ konfliktoj en Niĝerio, ĉefe inter kristanoj kaj islamanoj, dum kiuj mortas centoj, miloj da homoj, ĝis nun neniu niĝeriano estas enplektita en terorismo. Krome nekredeblas, ke niĝeriano tiel agus kontraŭ Usono, ĉar multaj niĝerianoj havas usonan ŝtatanecon kaj tie loĝas.</p> <p>Notindas ankaŭ, ke la junulo estas trejnita de fundamentistoj ekster Niĝerio, do en Britio kaj en Jemeno, kie li akiris la rimedojn por detrui la aviadilon.</p> <h3>Ŝtato de intereso</h3> <p>Komence de januaro Usono listigis Niĝerion kiel tiel nomatan ŝtaton de intereso, do ŝtaton, kiu subtenas terorismon. Tio signifas, ke Usono aparte kontrolos pasaĝerojn, kiuj alvenas aviadile el Niĝerio (ankaŭ tiujn el Kubo, Sirio, Sudano kaj Norda Koreio).</p> <p>Tamen kial tiamaniere puni landon pro unu homo, kiu neniel reprezentas niĝerianojn? Niĝerio estas ŝtato, kiu kunlaboras kun diversaj mondaj instancoj por haltigi terorismon. Do oni juĝu la aferon laŭ ĝia propra kunteksto kaj ne humiligu niĝerianojn, kaj ilin ne diskriminaciu, pro unusola korupta kaj dorlotita knabaĉo.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> <div style="text-align:right"><i>Niĝerio</i></div> 
