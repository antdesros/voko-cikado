      <h2>La verde farbita birdo</h2>
      <!-- komenco de la enhavo -->

       

     <p>Kiam mi legis – estis tasko en la kadro de universitata kurso pri monda literaturo – la verkon <i>La farbita birdo</i> de Jerzy Kosinski, mi estis tre kortuŝita sed plena de duboj. Temas ja pri romano. Neniu infano povus havi tian kruelan danĝeran vivon kaj tamen atingi virecon kaj iĝi intelektulo. Nun mia samgrupano instruis al mi, ke realo povas esti pli kruela, pli nekredebla ol iu ajn sonĝita romano. Post legado de „La longa vojaĝo” la farbita birdo eĉ iom paliĝis, almenaŭ perdis sian okulfrapan ruĝan koloron!</p> 
<h3>La pala najbaro</h3> <p>Mi estas membro de la Verda Stelo, la antverpena Esperanto-grupo. De tempo al tempo mi tie renkontas simplan ĉiutagan homon, kies vivon mi imagis ĉiutaga. Iam, post prelego en Hago, en la domo de Internacia Esperanto-Instituto, li invitis min al vojaĝo hejmen, en sia aŭto. Denove li aspektis al mi ĉiutaga homo, iom nigra – mi ne parolas pri lia haŭto, sed pri lia interno ... ĉar li neniam montris rideton – sed tre inteligenta kaj tre afabla. Hazarde mi sciiĝis, ke li publikigis aŭtobiografion, kaj ĉar ĝi kuŝis antaŭ mi en stando Esperanta dum kulturbazaro, mi iom foliumis la libron kaj iom eklegis ĝin. Kion sepdekjarulo ja povas memori kaj trovi rakontinda pri la dek komencaj jaroj de sia vivo? Almenaŭ mi preskaŭ nenion memoras pri tia foreco! Sed ... ŝajnas, ke tio nepre dependas de la tiamaj travivaĵoj. Eĉ en juneco spertoj povas esti tiel penetraj, ke ili gluiĝas dum la tuta vivo, kaj verŝajne daŭre elradias influon! Samtempe mi konsciiĝis pri la fakto, ke ni scias tiel malmulton pri tiuj, kiuj ofte apudas nin!</p> <h3>Kaŝita historio</h3> 
<p>Imagu: vi estas kvarjara, milito minacas, la patro soldatiĝas, sed tuj estas resendita hejmen, ĉar oni konstatis ĉe li tuberkulozon ... la patro decidas iri al Svislando por resaniĝi post la morto de la plej juna infano, ĉar li konscias, ke li infektis, senscie, la bebon kaj ne deziras infekti la edzinon kaj la aliajn kvin infanojn (la kvina sin anoncis sed ankoraŭ ne naskiĝis) ... oni konstatas plie ... ke vi, kvarjarulo, havas minacan makuleton sur unu el la pulmoj ... kaj panjo, malgraŭ tio, ke jam estas milito, sukcesas sendi vin per trajno al paĉjo ... sed sola, sub (neplanita) bombardado kaj aliaj minacoj ... Mi neglektis interpunkcion, ĉar la rakonto tiel supersaltis min, ke mi ne sukcesas kaj ne volas spiri ... Tiel koŝmara la vojaĝo estis, ke Eddy neniam sukcesis forgesi la eĉ por kvarjarulo hontindan scenon, ke dum la vojaĝo germanaj soldatoj en la trajno kontrolis, ĉu li hazarde ne portas judan pasporteton en sia pantaloneto ...</p> <h3>Bona ideo</h3> <p>Ne, mi ne rakontos la enhavon pli ... vi mem malkovru ĝin! Mi nur malkaŝu, ke estis ankaŭ periodo de plena silento, de muteco ... kiel en la farbita birdo ... kaj ankaŭ aliloke en la monda literaturo! Interese kaj pripensende! Entute la libro ampleksas malpli ol dek jarojn, sed kiaj jaroj! Ie mi legis reagon de mia amiko Ivo Durwael, kaj li sugestas, ke oni uzu la libron – pro la ĉiutaga lingvo, kiu igas ĝin tre facile digestebla – en daŭrigaj kursoj. Ankaŭ mi pensas, ke tio estus bonega ideo, ĉar ĝi povus esti fonto de treege riĉigaj diskutoj kaj pripensoj. La kelkaj tajperaroj kaj aliaj eraretoj kaj eble kelkaj gramatikaj pridiskutindaĵoj plie bone povas akrigi la atenton de la legantoj kaj de la instruistoj. Pri Esperanto tute ne estas io ajn en la libreto, sed la lingva problemo tamen grave rolas. Dum la jaroj en Svislando Eddy perdis sian denaskan lingvon, kaj do ... ne plu komprenas la patrinon. Sed ne nur tion li perdis dum tiuj jaroj. Fakte daŭre temas pri perdado, sed ... denove ... nepre malkovru tion mem.</p> <h3>Ĝi gustas je pli</h3> 
<p>Ŝajnas, ke en la nuna momento Eddy verkas daŭrigon de sia aŭtobiografio, kaj al iu li diris, ke en la unua parto li tute ne atingis la kulminon de sia suferado. Bona afero estas, ke mi nun jam estas certa, ke li transvivos siajn suferadojn ... ĉar mi scias, ke li daŭre estas inter ni, kaj silente kaj nigre kaj senrideta vagas kaj ofte apudas nin en la kunvenejo de <i>La Verda Stelo</i>.</p> <div style="text-align:right">Petro <span class="mm">DE</span> S<span class="mm">MEDT</span></div> <h6 class="bib">Eddy Raats: La longa vojaĝo. Travivaĵoj de etulo. Eld. Monda Asembleo Socia, Embres-et-Castelmaure, 2009. 106 paĝoj gluitaj. ISBN 978 2 918300 05 2.</h6> 

