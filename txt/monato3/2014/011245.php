      <h3>Lingvo</h3>
      <h3 class="tem">MEZOPOTAMIO</h3>
      <h2>Emesal: ĉu la plej antikva planlingvo?</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Artefaritaj lingvoj estas multenombraj. La lingvo internacia Esperanto estis ĝis nun
      laŭdire la plej uzata kaj sukcesa. Ĝenerale la plej konataj estas ĉefe tiuj, kiuj
      venas de la eŭropaj kulturoj: ĝuste ĉi tio estas la temo de la libro 
      <i>La ricerca della lingua perfetta nella cultura europea</i> (La serĉado de la perfekta lingvo en la eŭropa kulturo), verkita de la italo Umberto
      Eco. Sed ekzistas iu lingvo, kiu ŝajne estis mem artefarita kaj kiu, se tio estus
      fine konfirmita, estus difinita la plej malnova inter ĉiuj planlingvoj. Ĝia nomo?
      Emesal.
   </b>
</p> 
<p>Temas pri dialekto inventita en la lando Sumero, en la nuna suda Irako, verŝajne en
   la tria jarmilo antaŭ nia erao. En ĉi tiu artikolo oni proponos, unue, resumon pri
   la sumeranoj kaj ilia lingvo kaj, due, prezenton pri la sumera dialekto Emesal.
</p> 
<h3>La sumeranoj</h3> 
<p>La sumera historio kaj la homara ekas samtempe, ĉe la apero de la skribo, elpensita
   en Sumero ĉirkaŭ 3400 jarojn a.K. Antaŭ tiu tempo oni parolas ne pri historio, sed
   pri prahistorio. Tiu skriba sistemo, kiu estas tre kompleksa, iĝis (proksimume en
   2800 a.K.) la kojnoforma skribo, kiun oni skribis sur argilaj tabeloj kaj kiun uzis
   ĝis la fino de la unua jarcento de nia erao la sumeranoj, la akadanoj, la asiranoj
   kaj la babilonanoj.
</p> 
<p>La sumera civilizacio kreskis kaj disvolviĝis dum la unua parto de la tria jarmilo
   a.K. Tiam estris reĝoj de urbŝtatoj, sendependaj unu de alia, ekzemple Uruk, Ur, Ŝuruppak,
   Lagaŝ ktp, ĝis proksimume 2400 a.K. Tiam semidaj popoloj, kiujn estris Sargono de
   Akado, kaptis la potencon, unuigis la tutan sumeran landon, invadis la najbarajn landojn
   ĝis Mediteraneo kaj kreis la novan ĉefurbon de la unua reala imperio, Akadon. Tial
   oni nomas tiun periodon „akada”. La akada lingvo apartenas al la familio de la semidaj lingvoj, samkiel la hebrea,
   la aramea, la araba ktp. Poste tiu akada lingvo evoluis al la babilona kaj al la asiria.
</p> 
<p>Post la falo de la akada imperio okazis ekde la 2100aj jaroj a.K. la „sumera renaskiĝo”. Tiam estris grandaj reĝoj, ekzemple Gudea (la dua dinastio de Lagaŝ) aŭ Ur-Nammu
   (la tria dinastio de Ur, la konstruinto de la zigurato de Ur). Pri tiu periodo, oni
   malkovris multajn tekstojn: arĥivojn, reĝajn skribaĵojn (interalie destinitajn al
   la fundamentoj de monumentoj), beletraĵojn ktp.
</p> 
<p>Dum la 19a jarcento a.K. la sumeranoj iom post iom malaperis. Semidaj reĝoj estris
   la regionon. Inter ili estis la reĝo de Babilono Hamurabo (1792-1751 a.K.). La sumera
   lingvo ne plu estis vivanta, ne plu estis ies gepatra lingvo. Oni tamen ĝin konservis
   kiel klasikan lingvon, kiel lingvon de skribistoj, de kleruloj kaj, eĉ pli rimarkinde,
   kiel lingvon ritaran. Tiu 18a jarcento estis la granda epoko de la aranĝo kaj de la
   finfina eldono de la literaturaj sumerlingvaj tekstoj. Oni fine diru, ke, verŝajne,
   la sumera lingvo jam estis tre malnova tiam, kiam oni ĝin ekskribis en 3400 a.K. La
   sumera estas do prahistoria lingvo, kies parolantoj ĝin trenis al la historio, elpensante
   la skribon. Kiel klasikan lingvon, oni ĝin uzadis en Mezopotamio ĝis la fino de la
   unua jarcento p.K. La sumera do estis legata kaj komprenata dum almenaŭ tri jarmiloj
   kaj duono.
</p> 
<h3>La sumera lingvo</h3> 
<p>La sumera lingvo, kiel multaj aziaj lingvoj (sed ankaŭ interalie Esperanto), havas
   aglutinan strukturon: oni ligas al iu neŝanĝebla radiko (substantivo aŭ verbo) aliajn
   elementojn semantikajn aŭ gramatikajn. Estas rimarkinda ankaŭ la fakto, ke multaj
   elementoj aŭ radikoj estas unusilabaj. Plie, la sumera lingvo ne estas akuzativema
   (kiel Esperanto, kiu indikas la objekton per akuzativo), sed ergativema (kiel la eŭska;
   ĝi substrekas la ergativon, do la subjekton agantan). Ekzemple, en la frazo „la homo konstruas la domon” en Esperanto oni montras la akuzativon per la finaĵo 
   <i>-n</i>, dum la sumera montrus la ergativon, tio estas, laŭ la sama ekzemplo, „la homo”. Ni diru nenion alian pri la sumera lingvo, kiu, samkiel ajna „natura” lingvo, estas tre malsimpla, havas kompleksajn gramatikon kaj skribsistemon ktp.
   Oni nur diru, fine, ke la sumera apartenas al neniu pli vasta lingva familio. Ĝi estas
   unu el la malmultaj lingvoj „sen familio”, do tute izolitaj.
</p> 
<h3>Historio de la lingvo Emesal</h3> 
<p>Emesal estas sumera dialekto, plej verŝajne artefarita. Cirkulas almenaŭ tri ĉefaj
   diversaj hipotezoj pri ĝia deveno:
</p> 
<p>1) ĝi povas esti idiomo havanta lokajn propraĵojn: Emesal estus do „natura” lingvo, sed oni ne bone komprenas, se tio verus, kial oni ĝin elektis kiel „klasikan” lingvon (legu sube);</p> 
<p>2) ĝi povas esti lingvo de virinoj kaj de virin-emaj uloj. Ekzemple, oni legas iujn
   versojn emesal-lingvajn en kelkaj beletraĵoj, aparte tie, kie parolas virinoj (diinoj
   aŭ pastrinoj);
</p> 
<p>3) ĝi povas esti literatura aŭ poezia dialekto, aranĝita konforme al la recitativoj
   de sanktaj himnoj.
</p> 
<p>Aldone oni opinias, ke Emesal estas plej verŝajne artefarita lingvo, ĉe la lumo de
   internaj karakterizoj, pro kies teknikeco mi nun nenion diros prie. Fakte, Emesal
   montras neniujn kvalitojn de iu „natura” dialekto rilate al la sumera, kaj aludoj el antikvaj tekstoj ebligas al ni pensi,
   ke Emesal vere estas artefarita lingvo. Eble ĝin uzis virinoj, verŝajne pastrinoj,
   dum la unuaj tempoj kaj poste ĝin uzis (legu sube) la religiaj kantistoj.
</p> 
<p>Oni ne scias precize, kiam Emesal aperis. Fakte, oni konas neniun tekston emesal-lingvan
   skribitan dum la tria jarmilo a.K., do dum la epoko, en kiu la sumera estis vivanta
   lingvo. La plej malnovaj tekstoj emesal-lingvaj estis skribitaj dum la frubabilona
   periodo (ĉirkaŭ la 18a jarcento a.K.). Temas ĉefe pri preĝoj, religiaj kantoj, beletraj
   lamentoj.
</p> 
<p>Oni ankaŭ ofte renkontas Emesalon legante tekstojn de la unua jarmilo a.K. (la periodo
   de la grandaj mezorientaj imperioj, nome laŭ tempa ordo: la asira, la babilona, la
   persa-aĥemenida kaj la helena-seleŭkida). Dum tiu epoko estis kvin grandaj klerulaj
   profesioj:
</p> 
<p>1) la skribarto (
   <i>ţupšarūtu</i>, laŭ la babilona lingvo);
</p> 
<p>2) la astrarto, alivorte la profesio de astrologoj kaj astronomoj;</p> 
<p>3) la medicino (
   <i>asûtu</i>);
</p> 
<p>4) la „ekzorcarto” (
   <i>āšipūtu</i>), do, fakte, la profesio de la ritaristo;
</p> 
<p>5) la religia kantarto (
   <i>kalûtu</i>), nome la profesio de la 
   <i>Kalû</i> (religia kantisto).
</p> 
<p>Tiuj kantistoj konis – probable parkere – la multenombregajn kompleksajn tekstojn, kiuj enhavis specialajn kantojn kantendajn
   al ge-diaĵoj en iliaj temploj aŭ en iuj sakralaj lokoj. Komence de la unua jarmilo
   a.K. la sumera lingvo estis mortinta jam de mil jaroj. Depost tiu epoko la sumera
   estis klasika kaj religia lingvo. Tiuj sanktaj kantoj estis kaj sumerlingvaj kaj emesal-lingvaj.
   Emesal do estis klasika lingvo, aparte religia, kaj iom laŭdire „sekreta”. Emesalon do parolis praktike nur unu el la plej gravaj profesiuloj en la socio de
   Asirio kaj Babilonio.
</p> 
<h3>Lingvaj aspektoj de Emesal</h3> 
<p>La lingvo mem ŝajnas pli-malpli sama kiel la sumera. La malsamoj estas nur sonaj kaj
   vortaj. La gramatiko nepre estas la sama en la sumera kaj en Emesal. Iuj ŝanĝoj estas
   kutimaj: [g] sumerlingva iĝas [b] en Emesal; [g] iĝas fojfoje siavice [d] aŭ [z];
   [ng] ŝanĝiĝas al [m]; kaj [u] malfermiĝas je [e]. Oni renkontas ankaŭ tipajn nomajn
   ŝanĝojn: ekzemple [lu] iĝas [mulu] („homo”, „viro”), kaj [nin] ŝanĝiĝas al [gaŝan] („virino”, „sinjorino”). La cetero estas neatendebla por ni. Oni proponas ĉi-sube kelkajn ekzemplojn, kun
   kojnoformaj skribsignoj (laŭ novasira skribsistemo, tio estas unu el la skriboj de
   la unua jarmilo a.K.).
</p> 
<p>Oni komprenas Emesalon danke al la ekzistantaj dulingvaj aŭ trilingvaj tekstoj (do
   emesala-sumera-babilona): ĉefe temas pri la religiaj kantoj, pri kiuj mi traktis ĉi-supre.
   Alia fonto estas leksikaj listoj, kiuj estis similaj al vortaroj: nur sumerlingvaj
   aŭ dulingvaj (sumera-babilona), organizitaj laŭ temoj, laŭ signoj ktp. Tiuj listoj
   estis konsiderataj klasikaj tekstoj en Mezopotamio. Inter ili estis la Emesal-listoj,
   kiuj proponis ekvivalentojn inter Emesal, la sumera kaj la babilona, iomete kiel en
   nia ekzempla kompara tabelo.
</p> 
<h3>Konkludo</h3> 
<p>Emesal verŝajne estas la unua artefarita lingvo en la homara historio, laŭ niaj nunaj
   konoj. Ĝi estas forte ligita kun sia origina lingvo, la sumera, kaj havas la saman
   gramatikon: malsamas nur kelke da sonoj kaj vortoj. Oni ne bone konas ĝiajn originojn.
   Tamen, en pli malfruaj epokoj ol la sumera (ĉefe unua jarmilo a.K.) Emesal estis,
   kune kun la sumera, la lingvo, per kiu oni kantis al la diaĵoj. Neniom temas pri helpa
   lingvo (kiel Volapuko aŭ Esperanto), nek pri ia serĉo pri perfekta lingvo (kiel iuj
   lingvoj kreitaj en Eŭropo; vidu la libron de Umberto Eco): Emesal estis lingvo kun
   magia kaj religia efiko.
</p> 
