      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Aŭskultu la voĉon, ne la sonon!</h2>
      <!-- komenco de la enhavo -->

       

        <p>Post la katastrofo ĉe la atomcentralo <i>Fukuŝima</i> en marto 2011 progresis la kontraŭatomenergia movado en Japanio.</p> <p>En majo kaj junio 2012 pro paneo aŭ ekzameno funkciis neniuj nukleaj reaktoroj en Japanio. Tamen komence de julio, malgraŭ opozicio, refunkciis la atomcentralo <i>Ooi</i>. La popolo ne silentis.</p> <p>Meze de julio en Tokio okazis grandskala kunveno kontraŭ la atomenergia politiko, nomata „Kunveno centmilhoma por adiaŭi atomcentralojn”. Partoprenis 170 000 homoj, inkluzive de konataj japanaj ĵurnalistoj, muzikistoj kaj verkistoj.</p> <h3>Memvole</h3> <p>Ĉeestis ankaŭ anoj de politikaj partioj kaj sindikatoj, sed plejparte kolektiĝis homoj ne organizite, sed memvole. Multe helpis Interreto, precipe <i>Twitter</i> kaj <i>Facebook</i>.</p> <p>Jam delonge en Japanio ne okazis tia grandskala manifestacio por postuli ion de la registaro. Kontraste, en la 1960aj kaj 1970aj jaroj ofte okazis kunvenoj kontraŭ la Pakto pri Sekureco inter Japanio kaj Usono.</p> <p>Tiam asertis multaj, ke la pakto estas neegala kaj danĝera. Japanio povus fariĝi ŝtato, kiu militus kun Usono. Tamen la pakto estis aprobita kaj la japana popolo sentis rezignacion, ke politikon oni ne povas ŝanĝi. Tial la tokia manifestacio fariĝis io memorinda en la japana popola historio.</p> <h3>Ĉefministro</h3> <p>Komenciĝis alia nova movado. Fine de junio, kiam la registaro de ĉefministro Noda Joŝihiko decidis refunkciigi la nuklean centralon <i>Ooi</i>, protestantoj manifestaciis antaŭ la oficiala ĉefministra sidejo.</p> <p>„For la atomenergio!” kaj „Haltigu <i>Ooi</i>!” ili kriis, denove homoj, kiuj sporade kaj neorganizite amasiĝis – laboristoj, junuloj, maljunuloj, rifuĝintoj el Fukuŝimo, politikistoj de diversaj partioj, gepatroj kun infanetoj k.a.</p> <p>Komence manifestaciis nur 300 homoj. Tamen semajnon post semajno, ĉiuvendrede, ariĝis ĉiam pli da homoj. Reagis la ĉefministro, dirante: „Aŭdiĝas granda sono.”</p> <h3>Afiŝego</h3> <p>Tio kolerigis la popolon. La sekvan semajnon aperis afiŝego „Aŭskultu voĉon, ne sonon!”. En vespero fine de julio manifestaciantoj finfine sukcesis ĉirkaŭi la parlamentejon.</p> <p>Fine de aŭgusto la ĉefministro ne povis plu neglekti la protestantojn, kies reprezentantojn li akceptis. Li diris: „Kvankam la principa politiko de la registaro estas ne dependi de nuklea energio, tio okazu ne tuj, sed en longa perspektivo. Ni ne povas nun ĉesigi la atomcentralon <i>Ooi</i>.” Ne kontentis la protestantoj.</p> <p>Laŭ lastatempaj enketoj, multaj homoj akceptos pliigon de la elektra tarifo por nuligi la atomcentralojn. Ili preferas eventualajn elektromankojn kaj pli altajn elektrofakturojn ol atomenergion.</p>  <div style="text-align:right">K<span class="mm">AWAI</span> Yuka</div> 
