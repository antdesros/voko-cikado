      <h3>Enkonduko</h3>
      <h3 class="tem"></h3>
      <h2>Retproblemoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Ke Interreto alportis multajn utilajn uzeblojn, ne estas dubinde. Sed ĝi ankaŭ alportis problemojn kaj malavantaĝojn. Ekzemple, novaj esploroj montras, ke legantoj de tekstoj en la reto kutime ĉesas legi post nur kvinono de artikolo. Por komparo: legantoj de presitaj tekstoj interrompas plej frue post la duono. Evidente do la reto akcelas malemon legadi. Verkantoj tial reagas al tiu fenomeno, skribante tekstojn pli kaj pli mallongajn. Eble iam ili reduktos la enhavon al nur unu frazo. Modelo por tio ja estas la mallongaj mesaĝoj, kiujn oni povas skribi per poŝtelefonoj.</p> <p>Akompanas tiun fenomenon uzado de telegramstila aŭ eĉ kripla lingvaĵo. Ankaŭ multaj aŭtoroj de nia revuo evidente opinias, ke oni tiel devas reagi al kreskanta mallegemo: ili liveras pli kaj pli mallongajn artikolojn (bonŝance ankoraŭ ne en kripla lingvo).</p> <p>Alia malavantaĝo de la reto estas perdo de privata sfero. Pli kaj pli multaj homoj ne hezitas prezenti siajn intimajn aferojn publike en la reto. Tion tamen multaj homoj ankaŭ timas. Aktuala ekzemplo estas la servo <i>Street View</i> de <i>Google</i>. Neŭtrale oni povus traduki tion per „stratorigardo”, sed kritikantoj preferas la formon „stratogvato” kaj protestas, se en tiu servo ili trovas sian propran straton kun sia hejmo.</p> <p>Aparte ampleksaj estas protestoj en Germanio, kie <i>Google</i> planis komenci tiun servon en septembro. Jam en aŭgusto oni kalkulis pli ol 200 000 intervenojn de civitanoj. Laŭ enketo 52 elcentoj de la germanoj malakceptas prezentadon de siaj hejmoj per stratogvato. Tio ne mirigas, ĉar germanoj pro historiaj spertoj estas aparte sentemaj koncerne sian privatsferon. Dufoje dum la pasinta jarcento totalismaj sistemoj rigore spionadis siajn civitanojn. Komence la nazioj per pluraj sekretaj servoj kaj policoj ekstermis ĉiun privatsferon kaj persekutis amase homojn, kiuj ne konformis al la nazia ideologio, kaj murdis milionojn. Poste orientgermanoj dum 40 jaroj suferis spionadon de komunistaj sekretaj servoj, precipe la fifama „<i>Stasi</i>” (<i>Staatssicherheitsdienst</i>, servo por ŝtatsekureco). La problemo ĉe <i>Google</i> estas, ke temas pri privata entrepreno, kies manipulado de datenoj estas netravidebla. Tial multaj homoj ne fidas al ties promesoj ne prezenti hejmojn de protestintoj. Nun la germana registaro planas apartan leĝon pri la afero. Ĉu tio solvos la problemojn, ne estas certe.</p> <p>Sincere via</p> <div style="text-align:right">Stefan M<span class="mm">AUL</span></div> 
