      <h3>Moderna vivo</h3>
      <h3 class="tem">KLERIGADO</h3>
      <h2>Intervenu senbuĝete!</h2>
      <!-- komenco de la enhavo -->

       

        <p>Konservativaj politikistoj japanaj ŝatas lerni el alilandaj spertoj. Ekzemple, la
   nuna vic-ĉefministro Aso Taro diris, ke oni lernu la hitleran manieron pri la konstitucia
   revizio (M<span class="mm">ONATO</span>, 2013/10, p. 19).
</p> 
<p>La ĉefministro Abe Sinzo volas rigore reformi instruadon parte laŭ la modelo de Margaret
   Thatcher, eksa brita ĉefministro. En 2004 „reformema” grupo en la Liberal-Demokratia Partio (LDP), inkluzive de Abe, sendis esploran komisionon
   al Britio por studi la tieajn klerigajn reformojn.
</p> 
<p>Ĝi trovis, ke la brita popolo, venkinte en la milito pri la insularo Falkland, regajnis
   dignon kaj subtenis korekton de devojigitaj lernolibroj.
</p> 
<p>Dum la unua Abe-reĝimo (2006-2007) oni reviziis la bazan instruadan leĝon, sed la
   reformo mezvoje ĉesis. La nuna kaj dua Abe-reĝimo eknavigis cele al kompleta instrureformo.
</p> 
<h3>Ministro</h3> 
<p>Abe nomumis membron de la esplora komisiono, Simomura Hakubun, ministro pri instruado.
   Laŭ ilia kompreno, post la dua mondmilito instruado malfortigis la japanan potencon
   precipe pro maldekstraj instruistaj sindikatoj.
</p> 
<p>La taskaro de Simomura jenas: 1. kontroli kaj revizii lernolibrojn, precipe historiajn
   kun „masoĥisma” vidpunkto (do eviti priskribon de agreso en aziaj landoj ktp); 2. submeti ĝisnunajn
   relative neŭtralajn klerigajn komitatojn en ĉiuj municipoj sub aŭtoritaton de la municipa
   ĉefo; 3. instigi konkuradon inter lernantoj, lernejoj kaj municipoj; 4. enkonduki
   moralan lernobjekton por krei pli obeemajn civitanojn.
</p> 
<p>Do, kia estas la situacio en klerigejoj? Fakte, dum la longa regado de LDP post la
   mondmilito, la klerigsistemo jam formiĝis laŭ la volo de konservativaj politikistoj.
   La influo de la sindikatoj daŭre malfortiĝis.
</p> 
<h3>Morto</h3> 
<p>Lastatempa enketo farita de instruista sindikato montris, ke la averaĝa kromlaboro
   de la instruistoj en ĉiaj lernejoj sumas monate 95,5 horojn (inkluzive de laboro farita
   en la propra hejmo). Tiom da horoj superas la „danĝeran” limon rilate morton pro kromlaboro (monate 80 horoj) difinitan de la ministrejo pri
   publika bonstato kaj laboro.
</p> 
<p>Instruistoj dum longa tempo devas okupiĝi pri devigaj raportoj kaj statistikoj, kiuj
   ne rekte rilatas al instruado. Rezulte mankas tempo por pasigi kun lernantoj.
</p> 
<p>Plie, kunsidoj de instruistoj nun ebligas al lernejestroj kaj fakestroj aparte, kaj
   de supre, ordoni kaj administri. Liberaj diskutoj estas subpremataj. Etoso deprima
   kovras lernejojn, kaj instruistoj estas lacaj. Ne malmultaj fariĝas korpe aŭ mense
   malsanaj.
</p> 
<h3>Plendas</h3> 
<p>Instruistoj plendas ankaŭ pri tro grandaj klasoj kun multaj lernantoj. Laŭ raporto
   de Organizaĵo por Ekonomiaj Kunlaboro kaj Evoluigo (OEKE), la averaĝa nombro de lernantoj
   en klaso (unua parto de meza instruado) en 2006 sumis 23,9, tamen en Japanio 30,7.
</p> 
<p>La publika elspezo en Japanio por instruado estas malgranda. En 2010 tiu elspezo estis
   3,6 % de la malneta enlanda produkto, la plej malgranda en 30 kompareblaj OEKE-landoj.
   Tie la averaĝa elspezo atingis 5,4 %.
</p> 
<p>Do la politiko por kleriga reformo en Japanio tiel resumiĝas: „Intervenu senbuĝete!”.</p> 
