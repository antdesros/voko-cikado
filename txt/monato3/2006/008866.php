      <h2>Stelegoj inter libroj</h2>
      <!-- komenco de la enhavo -->

       

       <p>El la 12 plej popularaj libroj en Hungario 7 estas verkitaj de eksterlandaj aŭtoroj. Jen la rezulto de enketo en hungaraj bibliotekoj, kunlabore kun la ŝtata televido, por trovi la plej ŝatatan libron.</p> <p>La iniciato, kiu havis la titolon „La granda libro”, daŭris duonjaron. En la televido estis nomataj la plej popularaj titoloj, kaj konataj artistoj pledis por la elektitaj romanoj. La plej sukcesaj verkoj ricevis advokatojn, kiuj elokvente laŭdis la koncernajn librojn. Tiuj televidaj elsendoj anstataŭis ĉen-telefilmojn, kaj fine oni faris filmetojn pri la plej sukcesaj romanoj.</p> <p>La sep eksterlandaj aŭtoroj, kiuj reprezentiĝis en la finalo, estis Orwell (<i>1984</i>), Milne (<i>Winnie-la-Pu</i>), de Saint-Exupéry (<i>La eta princo</i>), Tolkien (<i>La mastro de l' ringoj</i>), Rowling (<i>Harry Potter kaj la ŝtono de la saĝuloj</i>), Bulgakov (<i>La majstro kaj Margarita</i>) kaj Márquez (<i>Cent jaroj da soleco</i>).</p> <h3>Porinfanaj romanoj</h3> <p>El la kvin hungaraĵoj du estas porinfanaj romanoj, el kiuj la plej konata estas <i>La knaboj de Paŭlo-strato</i> de Molnár, en kelkaj landoj deviga lerneja legaĵo. La aliaj estas <i>La orhomo</i> de Jókai, romantika romano pri komercisto ĉe Danubo; <i>Abigela</i> de Szabó, pri fraŭlino dum la dua mondmilito; kaj en la unua loko <i>Steloj de Eger</i> de Gárdonyi, patriota romano, kiu portas la leganton en la fortikaĵon Eger, kiun turkoj vane sieĝas.</p> <p>Laŭ la enketo hungaroj tre ŝatas porinfanajn kaj porjunularajn verkojn. En bibliotekoj kreskas la nombro de legantoj kaj prunteprenitaj libroj.</p> <div style="text-align:right">J<span class="mm">UHÁSZ</span> Károly</div> <h6 class="pied">La plimulto de tiuj libroj aperis ankaŭ en Esperanto, kaj pluraj el ili estas ankoraŭ aĉeteblaj. Vidu <a href="http://www.retbutiko.net">www.retbutiko.net</a>.</h6> 
