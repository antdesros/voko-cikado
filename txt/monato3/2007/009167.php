      <h3>Moderna vivo</h3>
      <h3 class="tem">NATURO</h3>
      <h2>Birdo de la jaro</h2>
      <!-- komenco de la enhavo -->

       

       <p>Estis serena, trankvila malfrusomera antaŭtagmezo. Subite la elektra lumo flagris. Tra la fenestro ni vidis leviĝantan fumnubon proksime de nia domo. Poste ni vidis rostitan turfalkon en masto de alttensia konduktlineo. Eble ĝi postflugis paseron kaj tuŝis elektrajn dratojn. Tuj la birdo mortis.</p> <p>Pli da bonŝanco havis turfalkino, kiu rompetis al si flugilon. Ni trovis ŝin en heĝo de nia ĝardeno. Najbaro transdonis ŝin al ĉasisto, kiu zorgis pri ŝi. Unu monaton poste la birdo povis denove flugi.</p> <p>La turfalko estas la plej ofta rabobirdo en Aŭstrio. En Eŭropo vivas 350 000 paroj. Tiu ĉi adaptiĝema birdo ne timas homojn, tamen la nombro malkreskas pro reduktiĝantaj vivmedio kaj ebloj nutri sin. Tial la organizaĵo <i>Birdovivo Aŭstrio</i> kaj la Germana Asocio pri Naturprotekto deklaris la turfalkon Birdo de la Jaro 2007.</p> <h3>Vivmaniero</h3> <p>La turfalko vivas en altaj arboj, turoj, mastoj kaj simile altaj konstruaĵoj. Falko ne mem konstruas neston, sed reuzas tiun de korvo. Ĉar falko estas tro malforta por forpuŝi korvon, ĝi uzas forlasitan neston de la pasinta jaro. La ino alvokas la viron al pariĝo. Meze de aprilo ŝi demetas tri ĝis ses ovojn, kiuj longas 4 cm. La idoj eloviĝas post 28 tagoj. Dum la unuaj tagoj ŝi protektas la birdidojn sub siaj plumoj. Poste la gepatroj serĉas nutraĵon por la idoj. Dum la tria vivsemajno ili ekhavas jam la mason de plenkreskuloj. Turfalkoj povas vivi maksimume 16 jarojn.</p> <p>Turfalko volonte manĝas musojn, talpojn, lacertojn kaj insektojn. Por ilin ĉasi ĝi ŝvebas alte en la aero. Poste ĝi fale alpuŝiĝas al sia viktimo. Por kapti muson ĝi foje devas flugi 3 km. Alia ĉasmaniero estas la kaptado de flugantaj malgrandaj birdoj.</p> <h3>Aspekto</h3> <p>La latina nomo <i>Falco tinnunculus</i> venas de la tintanta kriado de la birdo: ti! ti! ti! Ekzistas pli ol dek tiaj „vortoj”. Starante, birdoj de ambaŭ seksoj altas 35 cm; la etenditaj flugiloj estas kune 75 cm larĝaj; la maso estas 200 gramoj. Rimarkindaj estas la diversaj aspektoj laŭ koloro: la kapo de la vira birdo estas helgriza, de la ina ruĝ-bruna. Ĉar la kolspino konsistas el 15 vertebroj, la birdo – tipe por falkoj – povas turni la kapon 180 gradojn.</p> <p>Ĉar oni markis la birdojn per ringoj, oni scias la migradvojojn. Kelkaj travintras en Eŭropo, se kapteblas sufiĉe da musoj. Aliaj ŝanĝas sian loĝregionon nur iomete. Aliaj forflugas aŭtune pli suden kaj restas en la tropika pluvarbaro, kiam en Eŭropo malvarmas.</p> <div style="text-align:right">Walter, Renate, Irene, Stefan K<span class="mm">LAG</span></div>    
