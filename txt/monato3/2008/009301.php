      <h2>Konciza historio de la Esperanto-movado en Afriko</h2>
      <!-- komenco de la enhavo -->

       

     <p>Kvankam esperantistoj frue troviĝis en Afriko, jam en la 1890aj jaroj kaj la komenco de la dudeka jarcento, preskaŭ senescepte ili estis eŭropanoj, deĵorantaj serve al la koloniemaj ŝtatoj eŭropaj. Ili nelonge restis, kutime nur kelkajn jarojn, tiel ke la nombro de esperantistoj ne pliiĝis, kaj preskaŭ neniuj indiĝenoj ekkonis Esperanton.</p> <p>La politika mapo de Afriko ŝanĝiĝis plurfoje dum la sekvantaj cent jaroj, kaj socia stabileco malofte, kaj kelkfoje nur efemere, okazis en la plejparto de la kontinento. Granda parto de tiu senstabileco ŝuldiĝas al la agado de la koloniantoj/koloniintoj, kiuj ne ĉiam opiniis la farton de la afrikaj landoj ĉefcelenda.</p> <p>Apud granda nombro da etnaj lingvoj estis uzataj pluraj eŭropaj lingvoj: ĉefe la franca, angla kaj germana. Provoj interkomunikigi la landojn estis malmultaj kaj apenaŭ tuŝis la indiĝenajn kulturojn.</p> <p>Ŝajnas, ke la malmultaj eŭropaj esperantistoj en la afrikaj landoj ne penis, aŭ almenaŭ ne sukcesis, interesi indiĝenojn pri Esperanto. Ili plurfoje provis fondi societojn esperantajn kaj eldoni gazetojn, sed preskaŭ ĉiam ne daŭrajn.</p> <p>Kiel Esperanto povus utili en Afriko? Laŭteorie, simile kiel en Eŭropo. Sed eŭropanoj, eŭropece pensante, ĝis nun apenaŭ sukcesis aplikigi tiujn utilojn en Afriko, inter la indiĝenoj. Se indiĝena esperanta movado estus komenciĝinta en Afriko, eble tiuj afrikanoj mem trovus proprajn utilojn por Esperanto.</p> <p>La libro recenzata baziĝas sur la dua parto de disertacio, finpreparita en 1999. Ĝi prezentas informojn, laŭ afrikaj landoj, kronologie aranĝitajn, pri la esperanta movado en ĉiu afrika lando, por kiu priesperantaj detaloj troveblas. Ĝi ne estas, do, facila legaĵo. Efektive, ĝi estas tre klera informilo pri nomoj, datoj kaj ĉefaj eventoj. Granda nombro da fotoj montras al ni multajn el la koncernatoj. Tamen ĝi ne estas nur amaso da datoj kaj nomoj. Ankaŭ kritikoj pri la afrikcela agado de eŭropaj esperantistoj helpas kompreni la eventojn, same kiel ankaŭ la fiaskojn. Mi resumis la esencon de tiuj kritikoj en la kvin komencaj alineoj de ĉi tiu recenzo.</p> <p>La sento ricevita de la leginto, kiam tiu fermas la libron, estas pesimisma. Mi ne kredas, ke la verkinto celis tion, nur ke tiel efikas la ekzameno de la ĝis-1999a afrika poresperanta strebado.</p> <p>Por historiistoj, kaj por personoj, dezirantaj scii pri la multaj, siatempe tre reklamataj, provoj disvastigi Esperanton en Afriko, la libro estas valora. Mankas indekso, tiel ke difinitajn nomojn kaj eventojn oni malfacile trovas, sen zorge trakribri la enhavon de la verko.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Heidi Goes: Afero de espero. Eld. UEA, Rotterdam, 2007. 86 paĝoj gluitaj. ISBN 978 92917 096 9.</h6>   <h6 class="bib">Por mendi, vi krozu al <a href="http://www.esperanto.be/cgi-bin/mal/Web_store/web_storeie.cgi?keywords=Afero de espero">la Retbutiko</a>.
