      <h3>Spirita vivo</h3>
      <h3 class="tem">KUBO</h3>
      <h2>Frato José Olallo Valdés beata</h2>
      <!-- komenco de la enhavo -->

       

        <p>La 29an de novembro 2008, per amasa, solena ceremonio, oni proklamis beata fraton José Olallo Valdés [ĥosé olajo valdés] de la ordeno Hospitalana de Sankta Johano de Dio. Kvankam ja temis pri la unua tia ceremonio en Kubo, frato Olallo (pro sia bonkoreco de ĉiuj nomata „Patro”, kvankam li neniam pastriĝis) ne estis la unua kuba beatulo, ĉar la 28an de oktobro 2007 oni jam proklamis, sed en Romo, beata fraton José López Piteira [ĥosé lopes pitejra], unu el la 498 martiroj murditaj dum la civila milito en Hispanio. La eraro estas certe pardoninda, des pli ke nur lastatempe oni komencis pli ofte pritrakti tiajn aferojn ĵurnale.</p> <p>Frato Olallo estis ricevita la 15an de marto 1820 en la orfejo Sankta Jozefo en Havano. Nenion oni scias pri la gepatroj: paperslipo trovita apud la bebo nur tekstis: „Li naskiĝis la lastan 12an de februaro kaj ne estas baptita.” La familinomon Valdés li ricevis memore al la episkopo Jerónimo [ĥerónimo] Valdés, fondinto de la orfejo.</p> <p>Dum preskaŭ 54 jaroj, ekde 1835, kiam furiozis ĥolera epidemio, frato Olallo dediĉis sin al flegado de malsanuloj kaj malriĉuloj en la hospitalo de la komunumo hospitalana en la nuna urbo Camagüey [kamagŭéj]. Dum la malsukcesa dekjara sendependiga milito (1868-1878) la frato, kiun ĉiuj opiniis sanktulo, prizorgis vunditojn el ambaŭ armeoj, kaj iufoje evitis amasan murdadon de civiluloj. Krome, li riskis sian vivon, kiam li zorgis kaj lavis la kadavron de la fama ribelulo Ignacio Agramonte, forĵetita surstrate de la koloniaj militistoj. Je lia morto la 17an de marto 1889 la tuta urbo marŝis procesie post la ĉerko, kaj per publika monkolektado oni starigis memorigan monumenton. Ankaŭ lian nomon portas kaj placo kaj strato.</p> <p>Fraton Olallo oni agnoskas beatulo pro la mirakla resanigo de la infano Daniela Cabrera Ramos, suferinta pri kancero kaj renaj problemoj.</p> <p>La solenan ceremonion, tiunokte elsenditan de la tutlanda televidkanalo 15, ĉeestis, krom la kuba episkoparo kaj helpepiskopoj amaso da fiduloj.</p> <div style="text-align:right">Alberto G<span class="mm">ARCÍA</span> F<span class="mm">UMERO</span></div> 
