      <h3>Scienco</h3>
      <h3 class="tem">NUTRADO</h3>
      <h2>Juglando (kaj aliaj nuksoj): survoje al longviveco</h2>
      <!-- komenco de la enhavo -->

       

       <p>Juglando estas nutraĵo alte taksata pro ĝia proteina, vitamina, minerala kaj fibra enhavo. Tio ebligas al ĝi protekti la korpon kontraŭ la toksaj substancoj, kiuj ordinare formiĝas. Juglandoj, samkiel aliaj nuksoj, ne efikas nur por kontraŭbatali kor-angiajn malsanojn, kanceron kaj inflamojn, sed iumaniere eĉ preventas diabeton. Ĉi-rilate, lastatempe, la prestiĝa usona scienca revuo <i>New England Journal of Medicine</i> publikigis grandan esploron, konkludante, ke la mortokvanto inter la homoj regule manĝantaj juglandojn, estas malplia ol inter la homoj, kiuj ne havas la saman kutimon.</p> <h3>Tempodaŭro</h3> <p>La esploristoj analizis, kadre de sia studo, dekmilojn da viroj kaj virinoj dum la jaroj 1980-2010. Senkonsidere de aliaj faktoroj, ili kalkulis, ke inter jugland-manĝantoj okazis kvanto de mortoj malpli alta je 7 procentoj kompare kun la ceteraj. Pere de matematika forigo de aliaj intervenantaj faktoroj, ŝajnas, ke tiu ĉi nombro povas atingi 20 procentojn. Rimarkindas, ke ĉe la „sciuroj” – se tiel diri – okazis ĝenerale malpli da okazoj de morto pro kancero, kormalsanoj kaj pulmomalsanoj.</p> <p>La valoro kaj la ununureco de ĉi tiu esploro konsistas ĉefe en la fakto, ke oni neniam antaŭe observis enkadre de tiuspeca esplorado tiom da homoj dum tiom longa tempodaŭro. Tiu plurjardeka atento igas malprobabla tion, ke oni eltiris miskonkludojn, kaj ke aliaj faktoroj povus determini tian rezulton. La antaŭaj esploroj, kiuj jam atentigis pri la helpemo de nuksoj (ligita interalie al ilia alta vegetal-sterola enhavo) por la homa sano, ĉiam tamen koncernis relative malgrandajn kvantojn de pacientoj. Ne hazarde, cetere, ankaŭ en la nuna esploro la manĝado de arakidaj nuksoj montris sanigajn ecojn similajn al tiuj de juglandoj.</p>  <div style="text-align:right">Paulo Sérgio V<span class="mm">IANA</span></div> 
