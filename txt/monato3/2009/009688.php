      <h3>Moderna vivo</h3>
      <h3 class="tem">BRITIO</h3>
      <h2>Profitoj forportitaj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Vintre en Weston-super-Mare [ŭestan-supa-mea], marborda feriejo en sud-okcidenta Britio, okazas historia karnavalo. Ĝi originis en la jaro 1605 por celebri la malsukceson de romkatolika komploto detrui la protestantan parlamenton de Anglio.</p> <p>Komence la karnavalo konsistis el simpla procesio kun homoj kantantaj kaj svingantaj fajrilojn. Nuntempe traktoroj tiras grandajn ekspociziaĵojn brile iluminatajn kaj akompanatajn de laŭta, sonbendigita muziko. Ne plu la karnavalo festas la fiaskigon de romkatolika komploto, sed instigas la publikon mondonaci al almozaj fondaĵoj. Registritaj monkolektantoj akompanas la procesion, proponante sitelojn, en kiujn oni ĵetas monerojn.</p> <h3>Sorĉvergoj</h3> <p>En 2008, tamen, organizantoj de la karnavalo plendis, ke kolportistoj forrabis de la almozaj fondaĵoj proksimume 25 000 britajn pundojn (27 500 eŭrojn), vendante lumajn sorĉvergojn, ekbrilajn torĉojn kaj ruĝbrilajn kolĉenojn. La 2008-a enspezo por la diversaj fondaĵoj falis de 22 000 pundoj en 2007 al nur 11 000 pundoj.</p> <p>La prezidanto de la karnavalo-komitato, Brian Coombes [brajan kumz], diris, ke proksimume 200 kolportistoj grave bremsis la sukceson de la evento, kiu devis toleri ankaŭ pluvegon. „Ili pagas malmulte por siaj permesiloj kaj povas leĝe komerci ie ajn, dum oficialuloj ne povas haltigi ilin. Ili venis al nia karnavalo el ĉiuj partoj de la lando kaj foriris, redoninte neniom.”</p> <h3>Akto</h3> <p>La loka konsilantaro povus mem regi strat-komercadon pere de loka parlamenta akto, kiel oni faris en aliaj urboj. Tio, tamen, kostus al la konsilantaro 45 000 pundojn (50 000 eŭrojn), kio ne eblas pro la nuna ekonomia situacio. Povas nur koleri karnavalo-organizanto Steve Townsend [stiv taŭnzend]: „La celo estas donaci al almozaj fondaĵoj, sed kolportistoj venas pro propra profito.”</p> <p>Ne ĉiu, tamen, kulpigis la kolportistojn. Karnavalo-ĉeestinto Jo Payne [ĝo pejn] opiniis: „Spektantoj de la karnavalo havas elekton, kaj se ili volas aĉeti sorĉvergojn kaj torĉojn, ili rajtas. Se la karnavalo-enspezo estis vere malpli ol en 2007, kulpis probable la aĉa vetero. Gepatroj saĝe ne permesis al siaj infanoj ĉeesti en tia vetero. Cetere stratvendistoj plibonigas la etoson kaj ĝuigas, kvankam multekoste, multajn infanojn kaj vizitantojn.”</p> <p>Aliaj spektintoj demandis, ĉu vere temis pli 200 kolportistoj, kiel diris organizantoj. Multaj kredis, ke la malsukceson kaŭzis, se ne la kolportistoj, aŭ la vetero, do la financa krizo.</p> <div style="text-align:right">David C<span class="mm">URTIS</span></div>  
