      <h3>Turismo</h3>
      <h3 class="tem">BULGARIO</h3>
      <h2>Festo de ĝemeloj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Interesa kaj ĝis nun unika tradicio estiĝis en la bulgara ripozejo <i>Zlatni pjasaci</i> (Oraj sabloj) ĉe la Nigra Maro. Ekde 1991 tie ĉiujare en septembro okazas landa renkontiĝo de ĝemeloj.</p> <p>La pasintjara renkontiĝo estis jubilea. Ĝin partoprenis 300 ĝemeloj. La plej junaj partoprenantoj estis paro de la sesmonataj beboj Ivan kaj Georgi el Varno, venigitaj de sia patro Ivajlo. La plej maljuna paro estis 83-jaraj Zlatka kaj Vasilka Trifonov el Sofio. Partoprenis ankaŭ 2 triĝemelinoj – la 17-jaraj belulinoj el urbo Ruse kun nomoj de la filinoj de Sankta Sofia – Vera (Kredo), Nadeĵda (Espero) kaj Ljubov (Amo) kaj Iva, Nedelina kaj Diljana el la urbo Tvardica. La dujaraj knabinoj Viktoria kaj Joana, vestitaj en egalaj koketaj robetoj, estis venigitaj de siaj du avinoj – la 63-jaraj ĝemelinoj Slavka kaj Stefka Krumovski el Varno.</p> <p>Du paroj – la 21-jaraj studentoj Milen kaj Veselin kaj ĝemelinoj Mimi Marinova kaj Pepi Patronova, naskitaj la 2an de septembro 1963, festis sian naskiĝtagon. Mimi Marinova siaflanke estas patrino de geĝemeloj Ĵanet kaj Pavel, kiuj venis en la vivon la 2an de oktobro 1999. Ĉiuj ĝemeloj, kiuj festis sian naskiĝtagon, ricevis donacon de la gastigantoj de la festo – torton kaj botelon da ruĝa vino de la marko <i>Zlatni pjasaci</i>.</p> <p>Ĉiuj paroj kaj triopoj surhavis samajn vestojn. Kaj tio ege bone efikis. Tial la ĉiutaga ĵurnalo <i>Standart</i>, detale raportante pri la evento, informas pri paniko, obsedinta eksterlandajn turistojn. Germanoj kaj angloj miris ĉu ili ne trafis filmadon. Apude ja promenis egale vestitaj paroj kun samaj vizaĝoj kaj figuroj. Fakte la eksterlandanoj hazarde trafis la kulminon de la amuzfesto de ĝemeloj de Bulgario.</p> <p>La programo de la festo estis tre varia. La feliĉaj paroj kaj triopoj dancis la tradician bulgaran rondodancon. Dum la 15-jara ekzisto de tiu iniciato pli ol 10 mil ĝemeloj renkontiĝis. Kvin paroj, kiuj partoprenis ĉiujn ĝisnunajn renkontiĝojn, ricevis inviton de la ripozejo. Ili estos gastoj de la ripozejo <i>Zlatni pjasaci</i> okaze de ties duonjarcenta jubileo, festota ĉi-jare.</p> <div style="text-align:right">Dimitar H<span class="mm">AĜIEV</span></div> 
