      <h3>Diskoj</h3>
      <h3 class="tem"></h3>
      <h2>Kajto flugas plu</h2>
      <!-- komenco de la enhavo -->

       

      <p>
   <i>Kajto</i> debutis en Esperantujo ĉirkaŭ 1989, kiam aperis ĝia unua disko; mi unuafoje aŭdis
   la grupon en koncerto en decembro 1990. Sekvis ankoraŭ kvar KD-oj de la kvaropo, ĝis
   
   <i>Lokomotivo, rulu nun!</i> en 2004. Jen do la sesa disko de 
   <i>Kajto</i>, sed, kiel oni eble divenus per la titolo „Duope”, temas ne plu pri kvaropo, sed duopo. Havante kaj ŝatante ĉiujn antaŭajn diskojn,
   mi scivolis, ĉu multo ŝanĝiĝis pro la duoniĝo.
</p> 
<p>La nova disko malfermiĝas per 
   <i>La fiŝkaptisto</i> kaj 
   <i>Estrino Trin</i>, kiuj plej pensigis min pri la disko 
   <i>Masko</i> el 1999. Sekvas alistila 
   <i>Ne permesas Panjo</i>, melodie ne tre interesa, nek originala, sed tre vigla kaj danciga; mi povas imagi
   ĝin kiel sukcesan kanton en koncertoj, speciale se oni povus iel envicigi kelkajn
   el la muzikiloj nomitaj en la kanto kaj laŭvice prezentitaj en la registraĵo: banĝo,
   zimbalono, gitaro, violono, mandolino ktp.
</p> 
<p>
   <i>Estu vi mem</i> estas kanono, kiel antaŭaj kanonoj de 
   <i>Kajto</i>, sed kun la diferenco, ke ĝi estas plurlingva, ankaŭ nederlanda kaj frisa. 
   <i>Mi estas Rozo</i> estas tradukaĵo, kaj 
   <i>Rave kun vi</i> estas originalaĵo, laŭ la tekstobroŝuro, sed ambaŭ belaj kantoj, en kiuj la vira
   kaj virina voĉoj bonege kunludas duope. Sekvas 
   <i>Kara Amato de mi</i>, laŭsone pli lul- ol am-kanto, 
   <i>Surbordigitaj</i>, kun eble la plej interesa teksto el la kantoj en ĉi tiu kolekto, kaj 
   <i>La Lageto de mia Vivo</i>, kun eble la plej interesa melodio.
</p> 
<p>Iom lulkante sonas ankaŭ 
   <i>La Blanka Lumturo</i>, kiun sekvas ĝia senvorta varianto, 
   <i>La Valseto de la blanka Lumturo</i>, kaj 
   <i>Lonicero</i>. 
   <i>Finfine diru mi</i>, kun sia 12-vorta teksto, funkcias kiel enkonduko al la lasta kaj plej longa ero,
   
   <i>La „Mi amas vin”-Kanto</i>, kies teksto ampleksas nur la tri vortojn cititajn en la titolo, sed en dek ok lingvoj
   kaj bele aranĝitajn por la du voĉoj de la 
   <i>Kajto</i>-duopo.
</p> 
<p>Ĉu multo ŝanĝiĝis pro la duoniĝo? Fakte ne: ĝi estas facile rekonebla kiel la sama
   
   <i>Kajto</i>, kun la samaj fortoj kaj malfortoj. Inter la fortoj mi certe kalkulus la muzikajn
   aranĝojn kaj interpretojn, kaj inter la malfortoj la tekstojn, kiuj, laŭ mi, ne atingas
   la saman nivelon – sed eble mi estas iom tro postulema en tiu rilato; eble mi konfesu, ke lastatempe
   mi malofte aŭskultas la muzikajn registraĵojn, kiujn mi posedas, preferante ŝalti
   la radion por malkovri ion, kion mi ne jam aŭdis, aŭ, en maloftaj okazoj, sidiĝi ĉe
   la piano por pli funde esplori la detalojn de iu klasikaĵo. Bona kanto por mi estas
   kanto, kiu restas en mia memoro, tiel ke mi povas reaŭdigi ĝin en mia kapo, kaj tial
   la 
   <i>Kajto</i>-kantoj, kiujn mi plej ŝatas, estas la muzikigitaj poemoj 
   <i>Orangutango</i>, 
   <i>La soldato kiu marŝas</i> kaj 
   <i>Rabista dormo</i>.
</p> 
<p>Tamen, tamen, kiam mi aŭdigas al mi diskon de 
   <i>Kajto</i>, kaj ĉi tiu nova disko estas nenia escepto, mi ĉiam surpriziĝas, kiel pli bone ĝi
   sonas reale ol laŭ mia memoro. Verŝajne oni kulpigu pri tio la interpretan magion
   de Nanne kaj Ankie, kiuj kapablas fari eĉ el teksto ne tre ekscita kaj melodio simpla
   kaj tradicia muzikaĵon vere atentokaptan. Ŝajne mi emas forgesi, ĝis la reaŭskulto,
   kiel trafaj estas la akompanoj kaj la instrumentaj partoj inter la strofoj.
</p> 
<p>Mi esperas, do, ke ĉi tiu disko bone vendiĝos, kaj ke venos eĉ pliaj diskoj de 
   <i>Kajto</i>. Mi esperas ankaŭ, ke iam mi refoje spertos 
   <i>Kajton</i> en koncerto.
</p> 
