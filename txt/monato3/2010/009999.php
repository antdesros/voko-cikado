      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Interlingvo: komparo inter du solvoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Tre ofte okazis al mi, kun plezuro, laŭdi senrezerve verkojn de mia kunakademiano kaj kolego André Cherpillod. Des pli multe mi bedaŭras, ke ĉi-foje kelkajn rezervojn mi havas. La titolo „Ido, unu jarcenton poste” supozigus detalan portreton pri la nuna situacio. Male, la leganto trovas iom tro sintezan frazon: „Nuntempe, la Interretaj paĝaroj pri Ido asertas, ke idistojn oni nombras inter 2000 kaj 5000. Tre dubinde ...”. Nenio alia. Ke ekzistas pli ol 14000 titoloj en <i>Vikipedio</i> en Ido, ke la revuo <i>Progreso</i> daŭre aperas ekde 1908, do kun sama vivdaŭro, kiel la UEA-revuo, ke ĉiujare plu okazas kongresoj diverslande, kvankam kun nur kelkdeko da partoprenantoj, ke el la plurcentoj da artefaritaj planlingvoj nur kvin havas duliteran kodon en la internacia ISO-normaro, kaj unu estas Ido – nu, tiujn sciigojn mi estus ŝatinta trovi emfazitaj.</p> <h3>Estu aŭdata la alia flanko</h3> 
<p>Kelkio estas dediĉita al Beaufront; tamen Cherpillod rigardas al Beaufront kvazaŭ per ia pra-esperantista mieno, kaj li parolas pri la „oktobra perfido”, kiam en oktobro 1907 la Delegacio por la Alpreno de Internacia Helplingvo decidis alpreni Esperanton kun kelkaj reformoj. Sed la reformoj estis tiom multaj, ke ne plu estis Esperanto, sed alia lingvo. „Trompa faro de Couturat”, „hipokriteco” de Beaufront, „mitomaniulo”, „malhonoriga rolo”, „pajlohomo” ...</p>
<p>Ŝajne la aŭtoro ne legis la nun abundan diverslingvan verkaron pri tiu evento kaj pri la homo: raporto pri la laboroj de tiu Delegacio aperis en la franca en 1908 fare de Couturat kaj, tute senpartia kaj ekvilibra, fare de Jespersen, en Ido, en 1912 (esperantigo de Perla Martinelli en <i>Literatura Foiro</i>, decembro 2007, p. 294-298); en 2005 aperis la tuta leteraro inter Peano kaj Couturat (prezento mia en <i>Language Problems and Language Planning</i>, aŭtuno 2007, p. 281-289); ekzegezo de Claude Gacond pri la personeco de Beaufront kaj lia graveco por la esperanta didaktiko aperis en sep sinsekvaj numeroj de <i>Literatura Foiro</i>, de la februara 2007 ĝis la februara 2008. Nu, la aŭtoro el ĉio ĉi ŝajne nenion legis, kaj en la bibliografio nenio ĉi-koncerne troviĝas, kvankam lia verko estas deklarita eldonita en la kvara trimestro de 2007. Preter tiuj ĉi rimarkoj, tamen science nepraj, li ja meritplene sintezas la historion de Ido ekde ĝia naskiĝo, ĉerpante el esperantaj kaj francaj verkoj, ne el la idaj (<i>audiatur et altera pars!</i> = estu aŭdata ankaŭ la alia flanko!).</p> 
<p>Esperanto estas la venkinto inter la planlingvoj, do al la plenvenkitoj, kiel Ido, oni agnoskas nenian honoron; la Esperanto-historio ostracismis ĉi tiun misnaskitan idon <i>Ido</i>, kvazaŭ hontante, sed certe malbenante, ke ĝi naskiĝis. Pro tiu spurado en la historio Cherpillod estas do ege dankinda; ni trovas en lia broŝuro detalojn pri la morto de Beaufront, forbruligo de liaj paperoj, tombo kaj, en la lastaj paĝoj, pri la (magra) literaturo en Ido.</p> 
<h3>Polemika regurdo</h3> <p>Tre interesa estas atenta komparo inter la strukturo de Ido kaj de Esperanto, baziĝanta sur Ido-gramatikoj kaj Ido-vortaroj. Tamen tre bedaŭrinde ĝi komenciĝas per regurdo de ridinda polemiko pri intern-uza maniero kodigi la ĉapelitajn esperantajn literojn. Cherpillod ankoraŭ opinias, ke Esperanto devas esti grafike bela (por kies okuloj?) por allogi adeptojn per sia ortografio, kaj pri la x-sistemo li verdiktas: „Tiu Esperanto-karikaturaĵo, kiun kelkaj penas entrudi en la zamenhofan lingvon, forte ridigas niajn kontraŭulojn kaj decidigas ilin neniam lerni lingvon tiagrade barbaran, tre tre tre malsuperan al Ido.” Same barbaraj al nekutimanto povas aspekti la ideogramoj aŭ la latinaj literoj kun diakritoj, kaj tamen ĉiu verkanto en lingvo kun diakritoj eldonas eksteren per diakritoj kaj solvis interne senprobleme la transkodigojn de siaj literoj al skribmaniero klakebla per ĉia klavaro kaj rete transsendebla kaj legebla ĉiuekrane. Se Cherpillod scius, ke M<span class="mm">ONATO</span> uzas la x-sistemon por transsendo komforta cele al posta transkodigo ...</p> 
<p>Pri la distrumpetita „internacieco” de la tute sendiakrita 26-signa latinlitera sistemo oni ege rajtas dubi; ĝin uzas preskaŭ nur anglalingvanoj, do ege malpli ol kvarono de la terglobo. Zamenhof en la <i>Fundamento</i> konsilis surogaton per postmeto de „h”, en la <i>Lingvaj Respondoj</i> eksplicite allasis surogaton per postmeto de apostrofo; interretuloj trovas pli komforta kaj uzas surogaton per postmeto de „x”, jen ĉio. Ĉar temas eksplicitadire pri surogato, kaj ĉar la <i>Fundamento</i> tute ne devigas al la h-sistemo (ĝi diras: presejoj „povas anstataŭigi per”, ne ja „povas anstataŭigi <i>nur</i> per”), la afero estus finita; sed aperis anatemuloj kontraŭ sistemoj, malsamaj de tiu proponita de la <i>Fundamento</i>. Ĝuste por silentigi tiujn mi mem iniciatis en la Akademio de Esperanto la rezolucion, ke la diversaj teknikaj elturniĝoj por la transkodigo de la ĉapelitaj literoj tute ne estas kontraŭ-Fundamentaj. Post du jaroj la Akademio fine decidis aprobi mian rezolucion, kaj tiel ĉiu retulo rajtas transkodigi, kiel li volas, sen la artefarita (pli ĝuste: artetrudita) konsciencriproĉo, ke li uzas ion nelojalan al la Fundamenta lingvo. Sed mi pardonpetas, ke, pro mia persona engaĝiteco en la akademia decido, mi lasis min treni de polemiko, kiu bezonus tomboslabon anstataŭ resurekto.</p> <h3>Komparo</h3> 
<p>La prezento de Ido okupas pli ol la duan duonon de la libreto. Tre zorge atenta, kun ekzemplofrazoj, estas la komparo inter Esperanto kaj Ido; kaj ekvilibre Cherpillod honeste diras siajn preferojn, kiam Idon li opinias pli taŭga ol Esperanto, kaj tre klare li prezentas la esencan malsimilecon de la strukturoj de la du lingvoj, konfirmante, ke Ido neniel povas sin konsideri ia Esperanto reformita, sed ĝi ja estas alistruktura lingvo, kaj pri la derivmaniero, kaj pri la verboj, kaj pri la tabelvortoj. Estas bedaŭrinde, ke Esperanto ne naskiĝis kun kelkaj el la sendube superaj Ido-karakterizoj, sed oni ne pretendu, ke ĉiu ideo de Zamenhof estas la plej genia. Iuj solvoj, proponitaj en Ido, estus sendube preferindaj: tri genroj (vira, ina kaj sendistinga, aŭ, se uzi pli fakajn terminojn, maskla, femala kaj nedifinita); verboj havas tri infinitivojn (prezencan, preteritan kaj futuran) unuvorte esprimeblajn; la pasiva verboformo povas esti kaj analiza kaj sinteza, kaj tiurilate rimarkinda estas la iompostioma moderna evoluo de Esperanto al la uzado de pasivaj formoj, kiel „amatas”, „amitas”, paŭsantaj formojn, normalajn en Ido.</p> 
<p>Same iuj sufiksoj, malfrue venintaj en Esperanton, kiel „iv”, „oz”, „iz”, havas inspiron el Ido. Cherpillod apogas tiun analizon de la esperantaj vortoj, kie finaĵoj, prefiksoj kaj sufiksoj estu memstaraj vortoj; se tiel, oni konsekvence antaŭenpaŝu kaj agnosku, ke ankaŭ radikoj estas memstaraj vortoj. Ja, se en „labor-is” la finaĵo „is” estas memstara vorto, do ankaŭ „labor” estas memstara vorto, kaj „laboris” aspektas tute ordinara kunmetaĵo. Al la ekzemplo de Cherpillod, tute ĝusta, kvankam ne kutima, „Ĉu vi lavis la teleraron?” „Ne is, sed os” oni povus analoge respondigi: „Ĉu vi lavis la plankon?” „Ne lav, sed bala”; esprimo tute logika kaj komprenebla, eĉ se ankoraŭ (tute) ne uzata. Kaj tiu analizo ĝustigus ankaŭ alian ekzemplon de Cherpillod, kiu diras, ke „laborema” ne estas derivaĵo de „labori”; tute prave, sed ankaŭ ne kunmetaĵo de du vortoj „labori” kaj „ema”, kiel li, bedaŭrinde, diras; en tia analizo la vortoj estas tri: „labor”, „em” kaj „a”.</p> 
<p>Ĉe la fino de la lego estas demando: kial Ido fariĝis konkuranto tiel febla de Esperanto? Finfine, ĝi havis kelkajn tute bonajn atutojn, kiujn ĉi tiu libreto evidentigas. Kial idistoj ne kapablis altiri pli multe da adeptoj, precipe post la dua mondmilito? Finfine, tiam estis granda spaco por malfermo de komunikado kaj ankoraŭ granda nekono de fremdaj lingvoj. Malfacilas la respondo, kiel ĉiam, se temas pri sociaj fenomenoj. Prave Cherpillod rimarkas, ke la evolupovo de Esperanto estas senlima, do alia lingvo ne necesis al la esperantistoj, kiuj nur etparte transiris al Ido. Ido do ne povis ĉerpi el jam preta puto de la esperantistaj grupoj, kiel male faris Esperanto kun la volapukistoj. Al tio aldoniĝas la fakto, ke, se en kelkaj eĉ gravaj punktoj Ido superas Esperanton kaj alidetale ili povas egalmerite rivali, en la tuto Esperanto aspektas pli fleksebla kaj pli facile lernebla, precipe por neokcidentanoj, kiuj en Ido ne trovus tiujn leksikajn avantaĝojn, troveblajn de latinidlingvanoj. Krome, tute paradokse, la tuj aperinta Ido-Akademio unuflanke decidis ŝanĝojn ĝis 1913, tiel malstabiligante la lingvon, poste pro insisto de Couturat ĝin gipsumis.</p> <h3>Valora kontribuo</h3> 
<p>Ido naskiĝis kiel lingvo desupra, teoria, kiu evoluas per akademiaj decidoj. Esperanto ĝuis (feliĉe!) dek ok jarojn <i>sen</i> Lingva Komitato, do la lingvon stabiligis la popolo per sia uzado kaj povis senti ĝin sia. La ombro de Zamenhof estis ĉiam malantaŭe, sed sen troa pezo. Tute adekvata do estas la grafikaĵo, komparanta la du evoluprocezojn. Lerni tra ĉi tiu broŝuro pri Ido, fakte ne ido, sed nur kuzo de Esperanto, estas tre utile, ĉar oni komprenas ankaŭ multon de Esperanto mem, pri tio, kiel funkcias planlingvo, pri la ĉefaj principoj, kiuj igas lingvan strukturon kohera, pri reciprokaj influoj inter planlingvoj. Precipe kursgvidantoj trovos tie argumentojn por konvinkiĝi pri la boneco de la solvo, kiun la esperantistoj favoras. Entute tre valora kontribuo al la studado de interlingvistiko ĝenerale, de Ido kaj de Esperanto.</p> <div style="text-align:right">Carlo M<span class="mm">INNAJA</span></div>
