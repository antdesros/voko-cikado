      <h3>Scienco</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Iamaj vulkanoj, iama maro, ĉe nuna montaro</h2>
      <!-- komenco de la enhavo -->

       

       <p>Antaŭ 20 milionoj da jaroj, en la orienta parto de Stirio (aŭstra federacia lando)
   estis maro kun vulkanaj insuloj. Dum la maro malaperis, sala akvo profunde sub la
   tero ja restas. Nun kelkaj vulkanoj, malaktivaj kaj ege eroziitaj, superregas la pejzaĝon.
   Sur unu el tiuj vulkan-ruinoj staras la kastelo Riegersburg, imprese videbla ankaŭ
   al tiu, kiu venas de malproksime.
</p> 
<h3>Lafo</h3> 
<p>Multaj vulkanoj en la mondo ne havas same belan kegloformon kiel Fuĵi en Japanio.
   Ofte la lafo malrapide elfluas kaj kovras vastajn areojn. La lafo, ĉi-okaze, solidiĝis
   kaj estas nun uzata en ŝtonrompejo situanta ĉe Kloech: tie oni povas aĉeti bazaltajn
   ŝtonojn de diversa grandeco. Borante la grundon, oni malofte trovas nafton, sed fojfoje
   ja varman mineralan akvon. Tio klarigas la ĉeeston de tuta vico da varm-banejoj, de
   Vieno ĝis Murska Sobota en Slovenio.
</p> 
<h3>Legomoj</h3> 
<p>Jam nun la varma akvo hejtas multajn vilaĝojn kaj urbojn en tiu regiono. En Blumau
   la loka firmao 
   <i>Frutura</i> volas nun uzi la varm-energion de la grundo por forcejoj, kie rikolteblos legomoj
   dum la tuta jaro. Oni bezonas du truojn: el unu oni pumpos la varmegan akvon, en la
   alian oni reĵetos la akvon malvarmigitan. La afero estas profitiga, se konsideri,
   ke oni akiras en ĉiu sekundo minimume 60 litrojn da akvo, kies temperaturo estas minimume
   110-grada. La projekton kontraŭas kelkaj loĝantoj de Blumau, ĉar ili timas la eventualan
   malpurigon de la grunda akvo.
</p> 
