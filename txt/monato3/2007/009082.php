      <h2>Majstra verko de aŭtoro preskaŭ nekonata</h2>
      <!-- komenco de la enhavo -->

       

     <p>Ĉi tiu kolekto de rakontoj, el kiuj nur manpleneto aperis antaŭ nun en kelkaj revuoj, ŝajnas esti la sola literaturo, kiun iam ni havos, de preskaŭ neniam renkontita aŭtoro nialingva. Pri Sen Rodin ni scias preskaŭ neniom: nur ke la nomo estas pseŭdonimo de Filippo Franceschi, itala esperantisto, konata pro siaj lingva kaj traduka talentoj.<sup>1</sup></p> 
<p>Mi tamen aŭdacas aserti, ke ĉi tiu kolekto de 51 rakontoj rangas inter la plej majstraj verkoj de la aktuala esperanta literaturo. Certe ĝi rangas apud la noveloj de Trevor Steele; kaj el la ĉefaj novelmajstroj de nia pasinteco, apud tiuj de Stellan Engholm kaj Ferenc Szilágyi.</p> 
<p>Kio karakterizas la verkojn de tiuj eminentaj literaturuloj, kiel egale ĉi tiun rakontaron? Ili rigardas la vivon, bazante siajn verkojn pli-malpli sur propraj spertoj, kaj ĉerpante el tiuj epizodoj la eternan fundamente homan karakteron, same validan antaŭ kelkmil jaroj, nuntempe, kaj post kelkmil jaroj. Ĉiu rakonto konvinkas la leganton pri sia realo; ĉio efemera menciiĝas nur por reliefigi la esencajn animojn de la rolantoj.</p> 
<p>Eble kelkaj ekzemploj helpos komprenigi mian konstaton. La unuaj 32 rakontoj parolas al ni pri la impresoj, faritaj al itala junulo, enamiĝinta kun sveda junulino, kiam li vizitas la hejmlandon kaj hejmvilaĝon de tiu junulino. Ĉiumonate, en taglibra stilo, li konigas al ni iun sperton, kiu markas permanente en lia memoro la etoson de tiu lando tia, kia li renkontas ĝin, kaj la homoj, kiuj loĝigas ĝin. La naturo de la spertoj multe varias; povas esti simpla karikaturo unu-du-paĝa de evento amuza, distra, malplezuriga. Povas esti multepaĝa priskribo de spertoj kun lia dunginto, kies artista talento diversmaniere interpreteblas, laŭ onia vidpunkto. Neniam teda, ofte surpriza, troviĝas la vivo en tiu norda lando, kiel ĝin renkontas la sudlandano.</p> 
<p>Post tiuj 32 rakontoj, kiuj ŝajne konsistigis la originalon de la rakontokolekto (en 1960 ĝi estis rekomendata de Gaston Waringhien al Juan Régulo, por eldono en la tiama serio Stafeto, sed pro diversaj kaŭzoj la eldono estis prokrastita, kaj poste ĝi ne povis realiĝi pro la abrupta morto de Régulo), sekvas 19 rakontoj el diversaj periodoj, ĉu el la tempo de la dua mondmilito, ĉu el jardekoj post la 1960a.</p> <p>Inter miaj plej amataj rakontoj rangas la sinsekvaj rakontoj <i>Belfagor</i> kaj <i>Belfagor standardo</i>, en kiuj rakontiĝas sukcesa projekto, fare de kelkaj junuloj, flirtigi ruĝan standardon ĉe la plejsupro de alta, mallarĝega, kamentubo de iama fabriko. Ordonas unue la urbestraro, poste la nacia registaro mem, malmunti tiun politikan manifestaĵon. Sed ĉiuj streboj de la polico, de la fajrobrigado, de ĉiaj istoj, fiaskas. La rakonto estas sobra, tre simpatia al la junuloj kaj ilia defio al la aŭtoritatuloj, kaj pensiga.</p> <p>Tute alispeca estas la rakonto <i>Konflikto inter viando kaj spinacoj</i>, kiu prezentas la spertojn de juna knabo, vidanta la buĉadon de bestoj por la festomanĝoj, kaj naŭzata de la fiodoro de la kadavroj. Li konsekvence rifuzas manĝi viandon, ne pro filozofiaj konsideroj, sed pro la ribelo de lia korpo, kontraŭ la fetoro de la kadavraĵoj, eĉ post ilia plena kuiriĝo. Ĉiu alia en la familio amas manĝi viandon; la patrino ĉefe opinias la infanon mense malsana. Solvas la situacion nur la interveno de parenco, honorata psikologo, kiu sukcesas konvinki la familianojn, ke vegetarismo ne estas mensmalsano.</p> <p>Selekti la plej elstarajn el ĉi tiu rakontaro estas neebla tasko. Preskaŭ ĉiu rakonto estas malgranda gemo, kelkafoje granda gemo, kiu por la literaturamanto estas vere plezuriga legaĵo.</p> <p>Klara preso; interna marĝeno iomete tro mallarĝa, por facile legi la tekston, sen damaĝi la gluon kuntenantan la paĝojn; belaspekta kovrilbildo.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 clas