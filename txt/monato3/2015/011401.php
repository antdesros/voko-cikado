      <h2>Ĝemelaj desegnaĵoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>El pluraj desegnaĵoj, kiujn la instruistino estis ĵus taksanta, du atentigas ŝin pri
   io malĝusta. Ambaŭ estas tute similaj, kvazaŭ ĝemelaj, kvankam subskribitaj de malsamaj
   lernantinoj.
</p> 
<p>„<i>Aĥ, la friponinetoj! Ili kopiis unu de la alia, sendube ... Trafe! Jen Paŭla kaj Helena,
      nedisigeblaj amikinoj. Naivulinetoj ... Mi devas tuj malkovri, kiu kopiis de kiu.
      Ke ili venu tuj ĉi tien!
   </i>”
</p> 
<p>Starante antaŭ la instruistina surpodia skribotablo la du etulinoj, kapkliniĝantaj,
   atendas skoldon. Ambaŭ jam divenis la kialon de la alvoko, ĉar ili bone sciis, kion
   ili estis farintaj.
</p> 
<p>– Vi du atentu, ke mi povus esti via avino, do ne facile trompebla de bubinaĉoj, kiel
   vi. Nun mi volas, ke vi diru al mi, kiu kopiis de kiu? – ŝian rigardon de riproĉo ili ne vidas, ĉar neniu kuraĝas levi la okulojn, nek doni
   respondon. Kaj tio daŭradas ... longe ...
</p> 
<p>– Nu! ... Mi atendas! ...</p> 
<p>Sed la silenton ili ne rompas.</p> 
<p>– Ne timu, ke mi punbatos vin. Tion mi lasos al viaj gepatroj, kiam ili ricevos mian
   informon pri ĉi tiu malbela ago via: el... ko... pi... aĵo!!! – ŝi forte silabumas. – Mi volas koni la veron por poentumi laŭmerite: pri la originalo, ni vidos; pri la
   kopiado, nulon! Ĉu ne?!! La „ĉu ne?!!”, ŝi jam kriegas.
</p> 
<p>La du lernantinoj ektremas, sed la silento persistas.</p> 
<p>– Mi perdas la paciencon. Mi devas poentumi la du ekzercojn, nenion plu!</p> 
<p>– Amikino ne denuncas amikinon ... – subite eldiras Paŭla.</p> 
<p>– Finfine! Laŭdinda respondo, ja! Kaj vi, Helena, kion vi diras?</p> 
<p>– Amikino ne perfidas amikinon ...– ŝi respondas, kuraĝigita de la aŭdaco de la alia.</p> 
<p>– Bravo! Fidelaj amikinoj! Sed ... kiu kopiis de kiu?</p> 
<p>Pro la obstina manko de respondo, la instruistino avertas:</p> 
<p>– Bone! Vi devigas min informi viajn gepatrojn pri ĉi tiu mistero, kaj peti, ke ili
   venu paroli kun mi. Jen! – kaj ŝi etendas al ili du paperfoliojn, kiujn ŝi estis dume pleniginta. – Morgaŭ vi redonu al mi ĉi tiujn notojn, subskribitaj de viaj gepatroj.
</p> 
<p>Ambaŭ eliras, kun la notoj enmane, kaj jam kun larmoj ĉe la okuloj.</p> 
<p>– Kion ni faru nun? – jam malapud la instruistino, demandas Helena.</p> 
<p>– Mi havas ideon! Mi solvos la problemon! – konvinke asertas Paŭla. – Ni reiru al la instruistino.</p> 
<p>– Por fari kion?</p> 
<p>– Vi faros nenion, eĉ ne malfermu la buŝon. Se demandata de ŝi, vi konfirmu nur tion,
   kion vi aŭdos de mi.
</p> 
<p>– Kio nun? – surpriziĝas la instruistino. .../...</p> 
<p>Paŭla decideme alpaŝas, kaj kuraĝe konfesas:</p> 
<p>– Estis mi, kiu kopiis de ŝi.</p> 
<p>– Ha!!! Finfine! ... Jen bona konduto via. Afero klarigita, do redonu al mi tiujn paperojn,
   ĉar mi ne volas ĉi-foje ĝeni viajn gepatrojn. Sed ne ripetu ... Se ne! ... Samtempe
   ŝi pensas:
</p> 
<p>„Severeco ankoraŭ funkcias. La vero rapide malkovrita”.</p> 
<p>Ekster la lernejo, jam kvietigita pro la favora solvo de la situacio, Helena demandas:</p> 
<p>– Kial vi faris tion, Paŭla? Vi ne diris la veron.</p> 
<p>– Ne! Sed tiel mi evitis punon de niaj gepatroj. Se mi estus dirinta la veron, ĉu ne
   estus ankoraŭ malpli bone por mi? ...
</p> 
<p> </p> 
<p>Rehejmiĝante, ĝuste ĉe la doma enirejo, Helena renkontas sian patron.</p> 
<p>– Nebona mieno la via ... kio okazis?</p> 
<p>– Nenio, patro.</p> 
<p>– Rakontu ... Ne trompu min! Io bildiĝas ĉe viaj okuloj.</p> 
<p>Ŝi hezitas, kaj ne kapablas kaŝi la okazintaĵon:</p> 
<p>– Nur problemo de ĝemelaj desegnaĵoj.</p> 
<p>– Kio?! ... Ĝemelaj desegnaĵoj? Kio estas tio? Klarigu pli bone!</p> 
<p>– Estis nur taksado fare de la instruistino pri du desegnaĵoj. Ŝi akuzis Paŭla kaj
   min pri elkopiaĵo.
</p> 
<p>– Ho vi du! ... </p> 
<p>– Paŭla kulpigis sin antaŭ la instruistino, sed ŝi mensogis.</p> 
<p>– Ĉu estis vi, kiu kopiis de ŝi?</p> 
<p>– Ne, patro, mi ne kopiis de ŝi, nek ŝi de mi.</p> 
<p>– Mi ne komprenas. Finfine, kiu kopiis de kiu?</p> 
<p>– Neniu kopiis de neniu.</p> 
<p>– Kiel do?</p> 
<p>– Eĉ la pretendema instruistino estis sufiĉe malsprita por ne sukcesi eltrovi, ke la
   du desegnaĵoj estas ĝemelaj, ĉar ambaŭ estis faritaj de nur unu el ni ... de mi mem!
   ...
</p> 
