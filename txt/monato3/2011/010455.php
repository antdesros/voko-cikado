      <h3>Scienco</h3>
      <h3 class="tem">ESPLORADO</h3>
      <h2>Memsanigaj materialoj?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kiam oni rompas oston aŭ vundiĝas, molekuloj en la korpo ekreagas, provante sanigi la lezon. Sciencistoj nuntempe serĉas eblojn por enkonstrui similan kapablon de memriparo en ne-organikajn materialojn.</p> <h3>Ne tiom simpla</h3> <p>La organoj de la korpo estas tre fleksiĝemaj. Kontraste, kiam temas pri artefaritaj materialoj, ĝis nun oni strebis krei materialojn kiel eble plej fortajn kaj solidajn. La granda defio estas enkonstrui flekseblecon en materialon ne perdigante al ĝi la aliajn ecojn. Krome necesas eviti nocajn efikojn. Ekzemple, por malhelpi metalkorodon, oni uzas kromato-pigmenton. Ŝajnas bona ideo kombini tion kun memriparaj ecoj de polimeroj; tamen la pigmento estas kanceriga. Do necesas trovi alian eblon.</p> <h3>Esploroj</h3> <p>Facile kompreneblas ke la usona NASA (<i>National Aeronautics and Space Administration</i>/Tutlanda Aeronaŭtika kaj Spaca Administrado) havas tiurilatan esplorcentron, ĉar riparoj en la kosmo estas ekstreme malfacilaj. Ankaŭ aviadilkompanioj povus ege profiti de memripareblecoj de fendetoj dum flugado.</p> <p>Sed laŭ profesoro Herman Terryn de la Libera Universitato en Bruselo, Flandrio kaj Nederlando havas pioniran rolon en la esploroj kaj certe ne postrestas kompare al NASA.</p> <h3>Realigoj</h3> <p>La esploroj estas ankoraŭ en eksperimenta fazo, sed oni povas atendi praktikajn aplikojn en la venontaj jaroj.</p> <p>Jam ekzistas okulvitroj por skiantoj sur kiuj etaj skrapaĵoj ripariĝas sub influo de suna varmo. La aŭtokonstruisto Nissan eksperimentas pri simila tekniko por memriparo de skrapaĵoj en lako de aŭtomobiloj. Ĝis nun tio eblis nur por nigra lako pro ĝia granda ensorbo de varmo kaj ne sukcesas kiam temas pri ripetaj difektetoj aŭ karoserio plena de skrapaĵoj.</p> <p>En Nederlando, oni kovris en decembro 2010 kvarcent-metran strion de aŭtovojo per memripara asfalto. Oni aldonis ŝtaldratetaron konektitan al elektra bobeno. Ĉi tiu povas sendi kurenton en la dratetaron kaj tiel varmigi ĝin ĝis 60 gradoj celsiaj. Tiel la asfalto iom fandiĝas kaj la fendetoj fermiĝas. La unuaj rezultoj estas kontentigaj. Nun oni pluesploros en laboratorio kiom ofte necesas apliki tiun fandoprocedon: ĉu ĉiuprintempe aŭ eble ne ĉiujare.</p> <p>En Belgio oni eksperimentas per aldono de neaktivaj bakterioj en betonon. Kiam estiĝas fendetoj, la enfluantaj akvo kaj aero restimulas la bakteriojn kaj tiuj ekproduktas kalkoŝtonon kaj tiel la fendetoj refermiĝas.</p> <p>En alia eksperimento oni aldonas polimerojn. Kiam la betono fendiĝas, la polimeroj ekkontaktiĝas kaj refermas la fendojn.</p> <p>La ĉiujaraj postvintraj riparoj de aŭtovojoj estas granda zorgo en moderna lando kaj sendube oni kiel eble plej baldaŭ provos apliki la menciitajn teknikojn en la praktiko.</p> <p>La grandaj industrioj investas pli kaj pli en la esploradojn de sinriparantaj materialoj kaj oni povas do atendi multajn aplikojn en la venontaj jaroj.</p> <div style="text-align:right">Ivo D<span class="mm">URWAEL</span></div> 
