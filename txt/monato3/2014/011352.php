      <h3>Ekonomio</h3>
      <h3 class="tem">INTERNACIA KOMERCO</h3>
      <h2>Ĉu ni devas timi TTIP?</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Nova kaj preskaŭ sekreta projekto por krei grandan liberan interŝanĝ-zonon inter Usono
      kaj Eŭropo minacas ŝanĝi la civitanan vivon en la du regionoj. Estos kolizio inter
      du idearoj: unuflanke la uson-brita, kiu kredas ke senbrida liberalismo plej utilas
      al la loĝantaro, aliflanke la kontinenteŭropa, kiu emas reguligi la merkaton por eviti
      ekscesojn.
   </b>
</p> 
<p>TTIP estas mallongigo de anglalingva esprimo 
   <i>Transatlantic Trade and Investment Partnership</i> (Transatlantika partnereco pri komerco kaj investo). Ĝia antaŭa, sed ankoraŭ ofte
   uzata nomo estas TAFTA (<i>Transatlantic Free Trade Area</i>, Transatlantika Liberkomerca Zono).
   <sup>1</sup> Estas projekto de Usono kaj de Eŭropa Unio por konstrui grandan liberan interŝanĝ-zonon.
   Ĝi estas apenaŭ konata de la civitanoj, sed koncernas ĉiujn aspektojn de la vivo:
   sociaj rajtoj, ekologio, nutraĵo, sano, energio, akvo, kulturo, edukado ktp.
</p> 
<p>TTIP celas nuligi la restantajn barilojn kiuj malfaciligas komercon inter Usono kaj
   Eŭropa Unio. Ĉar la dogantarifoj jam estas malaltaj, la vera celo estas forigi aliajn
   obstaklojn, kiuj ĝis nun protektas la civitanojn. La batalkampo kuŝas inter translandaj
   firmaoj kaj demokratie elektitaj registaroj. Se la nuna TTIP-projekto realiĝus, la
   transnaciaj firmaoj povus devigi ĉiujn ŝtatojn ŝanĝi siajn regulojn laŭ la interesoj
   de la entreprenoj.
</p> 
<h3>Kiel TTIP funkcius?</h3> 
<p>La principo estas krei liberan kaj ne maljustan konkurencon inter entreprenoj de tiu
   grandega zono. Teorie, tiu principo estus garantiata per mekanismo de finaranĝo de
   malkonsentoj inter privataj investantoj kaj ŝtatoj (angle: ISDS, 
   <i>Investor-state dispute settlement</i>). Laŭ tiu mekanismo, entrepreno kiu opinias ke ĝiaj rajtoj estas limigitaj pro decido
   de ŝtato povas persekuti tiun ŝtaton. Krome kaj ĉefe, tia konflikto ne estus solvata
   per procesa proceduro en kortumo, sed pere de internacia arbitracio.
</p> 
<p>Arbitracio estas rimedo solvi juĝaferojn sen turni sin al la ordinara juĝinstanco.
   La koncernaj partioj, interkonsente, decidas elekti trian sendependulon, nomatan arbitracianto,
   kiu respondecos solvi la konflikton. Arbitracio jam estas ofte uzata en internaciaj
   komercaj konfliktoj, sed sperto de NAFTA (Nord-Amerika Liberkomerca Interkonsento)
   montras ke, kiam usonaj firmaoj persekutas Kanadon, la usonaj firmaoj pli ofte gajnas;
   kiam kanadaj firmaoj persekutas Usonon, Usono pli ofte gajnas. Estas malmultaj arbitraciantoj
   kaj iuj dubas pri ilia sendependeco.
</p> 
<p>Grava subtenanto de TTIP estas OEKE (Organizaĵo pri Ekonomiaj Kunlaboro kaj Evoluigo).
   Laŭ OEKE-studo, TTIP pligrandigus la malnetan enlandan produkton (MEP) je 3 ĝis 3,5
   % en Usono kaj EU, sed aliaj studoj taksas la favoraĵon venontan el TTIP malpli granda,
   eĉ neglektinda.
</p> 
<h3>Kelkaj eblaj sekvoj</h3> 
<p>Per TTIP, transnaciaj firmaoj povus decidi pri siaj reguloj pri varoj, servoj kaj
   investoj. Tio favorus kunfandiĝojn kaj akirojn, ĉefe de malgrandaj entreprenoj. Pri
   dungiteco kaj laborkondiĉoj, la translandaj firmaoj povus atingi, ke fundamentaj rajtoj
   de laborantoj estu forigitaj (reguloj pri dungo kaj maldungo, labortempo, malsano,
   gravedferioj, ktp...).
</p> 
<p>Povus okazi, ke eŭropaj normoj estas konsiderataj kiel bariloj al libera komerco.
   Tiel, reguloj pri pesticidoj kaj herbicidoj, genetike modifitaj organismoj (GMO),
   atesto pri origino protektata (AOP), normigitaj etikedoj kun indiko de origino, viandaĵo
   kun hormonoj ktp, povus esti atakataj de usonaj firmaoj. Nafto-entreprenoj povus persekuti
   registarojn por akiri rajton ekspluati skistogasojn en ĉiuj partoj de la TTIP-zono.
</p> 
<p>Nun akvo kaj energio povas esti liverataj de publikaj aŭ privataj entreprenoj. Se
   TTIP ekfunkcius, akvaj kaj energiaj firmaoj povus persekuti urbojn kaj ŝtatojn kiuj
   decidas liveri tiujn servojn nur per publika sistemo, ĉar tio estas barilo al libera
   konkurenco.
</p> 
<h3>La publiko nepre estu informata</h3> 
<p>La civitanoj de tempo al tempo legas en sia gazetaro iom pri multlandaj kompanioj
   kiuj misuzas patentojn aŭ opinias ke eŭropa regularo pri manĝaĵoj estas tro strikta,
   kaj pri protestoj de manifestaciantoj kontraŭ tio, sed ili ĝenerale ne konscias, ke
   tio estas eroj de jam delonge okazanta traktado inter Usono kaj EU. Kunago de bone
   informitaj civitanoj povas influi la finajn decidojn, atendatajn antaŭ la fino de
   2015. Multfoje jam en la historio civitanaj agadoj sukcesis haltigi projektojn de
   nekontrolataj politikistoj kun internaciaj firmaoj.
</p> 
<p>La novaj Eŭropa Komisiono kaj Eŭropa Parlamento ĉi-rilate havas grandan respondecon,
   sed eble malgrandan povon. Dum la pasintaj jardekoj, ĉiuj gravaj komercaj novaĵoj,
   de 
   <i>Amazon</i> ĝis 
   <i>Google</i>, ekestis en Usono kaj surprizatakis Eŭropan Union.
</p> 
