      <h2>Klimatŝanĝaj fabeloj, ĉu?</h2>
      <!-- komenco de la enhavo -->

       

       <p>La titolo <i>Klimatŝanĝaj fabeloj</i> super la letero de Martin Purdy (M<span class="mm">ONATO</span> 2012/05, p.6) estis verŝajne pli trafa ol oni celis, ĉar la komentoj de sinjoro Purdy efektive prezentas serion da fabelaĵoj ne bazitaj sur la realo.</p> <p>Neniu serioza homo disputus pri tio, ke „La klimato ŝanĝiĝas jam dum milionoj da jaroj, kaj daŭre ŝanĝiĝos.” Tio estas eĉ baza premiso de ĉia klimatoscienco; la celo de sciencaj esploroj estas malkovri, kiel kaj pro kio klimato ŝanĝiĝis antaŭe, por pli bone kompreni, kiel ĝi verŝajne ŝanĝiĝos dum venontaj jaroj kaj jardekoj. Sen tia kompreno nek ekonomie, nek politike eblas serioze plani.</p> <h3>Ĥimero</h3> <p>Precipe la aserto, ke „Antaŭ mil jaroj, la planedo estis pli varma, ol nun” estas senfunda fantaziaĵo. Povas esti, ke okazis periodoj antaŭ milo da jaroj, kiam iuj partoj de Eŭropo aŭ aliaj mondopartoj estis pli varmaj ol lastatempe, sed nenio pruvas, ke tio estis fenomeno tutmonda. Kaj multaj studoj montras, ke varmiĝoj dum la lastaj jarmiloj neniam estis tiom fortaj kiel la lastatempa.</p> <p>S-ro Purdy simile starigas ĥimeron pri la „malgranda glaciepoko” inter la 14a kaj 18a jarcentoj (fakte, oni pli ofte parolas pri malvarmiĝo inter la 17a jarcento kaj komenco de la 19a, sed ankaŭ pri tio ne estas klare, ĉu temis pri tutmonda afero): kiel ajn oni analizas la ciferojn, temis pri pli moderaj temperaturvarioj ol la ŝanĝoj dum la lastaj jardekoj, do ekzistas nenia bazo por atribui lastatempan varmiĝon al „la natura (re-)varmiĝo, kiu sekvas la finon de tiu malgranda glaciepoko.” Ĉu kreskanta varmiĝo 200 jarojn post la fino de iu periodo vere povas esti nur natura reekvilibriĝo?</p> <h3>Studoj</h3> <p>Sed plej grave, estas absurde imagi, ke la nuna ĝenerala konsento en klimatoscienco baziĝas nur sur la laboroj de „eble kvindeko da sciencistoj”. Ŝajne mankas plej baza kompreno pri tio, kiel funkcias scienco: sciencistoj ne „verkas raportojn” sed faras studojn kaj prezentas la rezultojn de tiuj studoj en sciencaj revuoj, kie aliaj sciencistoj povas siavice kritiki tiujn rezultojn kaj konkludojn kaj prezenti pliajn studojn. En seriozaj sciencaj revuoj ne eblas „silentigi la malkonsentantojn” – se la malkonsentantoj havas almenaŭ kelkajn solidajn rezultojn de siaj studoj. En la reala mondo, la konstato pri scienca konsento rilate al ŝanĝiĝo de la klimato pro homkaŭzata pliiĝo de karbondioksido en la atmosfero baziĝas sur la konstatoj de vastega plimulto el la studoj aperintaj en la scienca literaturo.</p> <p>La tuta temo de ŝanĝiĝanta klimato kaj kompreno pri ĝiaj kaŭzoj estas tro grava por esti tereno de misinformaj disputoj.</p> <div style="text-align:right">Brian M<span class="mm">OON</span></div> 
