      <h3>Politiko</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Monumente tra la pasinteco</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Komentas la aŭtoro: „Mi estis ŝokita, kiam mi ekvidis la barbaran rilaton de kelkaj personoj al plurjare
      starantaj monumentoj. Antaŭ nelonge ankaŭ en mia urbo – Ĥarkivo, Ukrainio – oni faligis monumenton al Lenin, kiu jardekojn staris en la urbocentro, sur unu el
      la plej grandaj placoj en Eŭropo.” Jen reago ...
   </b>
</p> 
<h3>Komence de la 20a jarcento</h3> 
<p>Mi naskiĝis en 1870, poste, kiel preskaŭ ĉiuj, mi lernis en lernejo, universitato.
   Mi ekinteresiĝis pri politiko kaj decidis laŭpove helpi al homoj inde vivi, ne ekzisti.
   Tiel mi venis al revoluciaj personoj. Tiuj al mi plaĉis. Kaj poiomete mi ilin ekgvidis.
</p> 
<p>Poste sekvis vojaĝoj, ĉu en Siberion, ĉu en eksterlandon, kie mi loĝis, verkante kaj
   gvidante landan revolucian movadon. Verkado ... Mi senĉese verkis. Verkis mi miajn
   pensojn – kiel mi vidas la estontan vivon, kio necesas por tion realigi. Evidentiĝis, ke mia
   verkaro fariĝis plurvoluma, kaj ke laŭ decido de landaj partiaj oficialuloj ĉiu studu
   miajn verkojn.
</p> 
<p>Venis la revolucio. Eble mi mem ne estis certa pri la rezulto, tamen ni, la popolo,
   simplaj laboristoj kaj kamparanoj, venkis. Venis tempo por konstrui novan ŝtaton.
   Ne ĉiuj komprenis miajn dezirojn, ne ĉiuj ilin subtenis. Tiel okazis atencoj, mi eĉ
   estis vundita. Tiel ĝis 1924, kiam mi mortis.
</p> 
<h3>Meze de la 20a jarcento</h3> 
<p>Kredeble mi faris multon por la lando. Posteuloj konstruigis por mi maŭzoleon kaj
   metis min tien. Senĉese oni komencis konstrui honore al mi monumentojn. Rigardante
   desupre, mi rimarkis, ke en diversaj anguloj de la lando mi (pli ĝuste la monumentoj)
   malsame aspektas. En Kaŭkazio mi havas trajtojn de kaŭkazdevena homo, en Mez-Azio
   mi aspektas kiel aziano.
</p> 
<p>Mi kredis, ke oni ne vane honorigis min, kaj ke tio longe daŭros. Mi vidis ankaŭ,
   ke oni honorigis aliajn: ĉu vere ili tion meritas? En 1953 apud mi en la maŭzoleon
   en Moskvo oni metis iun, kiun mi, mole dirante, ne ŝatis, ne estimis: Josif Stalin.
   Mi estis devigita kuŝi apud li longajn ok jarojn. Fine oni korektis ĉi tiun „eraron”, kaj mi restis denove sola en mia „doko”.
</p> 
<h3>Fine de la 20a jarcento</h3> 
<p>Pli ol 70 jarojn daŭris socialisma reĝimo. Mi pensis, ke tiel eterne estos. Sed jen
   al la povo venis relative juna kaj ambicia persono, kiu komencis „renovigi” la socion, anonci liberan esprimon de propraj pensoj kaj opinioj, do 
   <i>glasnost</i>.
</p> 
<p>Sed ankaŭ tio ne al multaj plaĉis. En 1991, danke al la provo de supre estantaj apuduloj
   faligi la reĝimon, la lando, kiun mi komencis unuigi, vastigi, plene disfalis. Anstataŭe
   aperis sendependaj ŝtatoj, kiuj ekzistis kiel respublikoj.
</p> 
<p>Sed ne tio min teruris. Kiu venis al la povo? Evidentiĝis, ke gvidantajn rolojn prenas
   tiuj, kiuj laŭ mi tute ne kapablas.
</p> 
<p>Rezulte ĉie tra la lando (kompreneble nun temas pri la pasinta periodo, pri la granda
   lando, granda imperio) oni detruas monumentojn, kiujn oni konstruis honore al mi,
   same al aliaj miaj kunlaborintoj.
</p> 
<p>Kaj mi konstante volas demandi, ĉu la aŭtoroj de tiuj monumentoj kulpas, ke miaj „kolegoj” kaj mi ne plaĉas al tiuj barbaroj. Alimaniere mi ilin ne povas nomi.</p> 
