      <h3>Eseoj</h3>
      <h3 class="tem">TEKNOLOGIO</h3>
      <h2>Nuntempo sciencfikcia</h2>
      <!-- komenco de la enhavo -->

       

       <p>Neniu alia afero evoluis en la lastaj 30 jaroj tiom imprese, kiom teknologio. Tion
   bone konscias homoj, kiuj estis junaj en la 1980aj jaroj, al kiuj ŝajnis tiutempe
   „nekredebla” la eblo kapti kolorajn televid-elsendojn pere de plumpa televidilo aŭ fotadi per
   zoma 
   <i>Polaroid</i>-aparato rapidprinta. Notindan progreson konscias ankaŭ tiuj, kiuj adoleskis dum la
   1990aj jaroj: ĉi-lastaj, kun nemalhavebla kapradio ĉe la oreloj dum lernejaj ekskursoj,
   opiniis magio la mesaĝetojn ricevitajn aŭ senditajn de la propraj pezaj, long-antenaj
   poŝtelefonoj – afero rigardata de la hodiaŭa junularo kiel tute normala.
</p> 
<h3>Sekura pagmaniero</h3> 
<p>En la nuna epoko, cetere, granda parto de nia (profesia kaj precipe persona) vivo
   disvolviĝas laŭ la vojoj de komputiko kaj precipe de Interreto, vera labirinto de
   informoj kaj rimedoj – sed ankaŭ, ni estu honestaj, de ĉiuspecaj tentoj kaj danĝeroj. Sociaj retejoj, komunik-programoj
   kiel 
   <i>Skajpo</i>, nemultekostaj ĉionfaraj poŝtelefonoj, kiuj estas io ajn (poŝlampo, agendo, termometro,
   skanilo, eĉ sekura pagmaniero) krom simplaj telefonoj, ebligas facile al homoj esti
   en konstanta kontakto kun malproksimaj amikoj, parencoj, eĉ klientoj, neniigante ajnajn
   distancojn.
</p> 
<h3>Karmemora avino</h3> 
<p>Kiom ĝojus mia karmemora avino Nina – kiu komunikiĝis kun sia fratino en Usono nur du-tri fojojn jare, kaj ja ĉiam mallonge,
   pro ekonomiaj kaj praktikaj kialoj –, se ŝi estus hodiaŭ viva kaj povus havi je sia dispono tian diablan instrumenton
   por senpage babili kun sia fratino kaj esti informita en reala tempo pere de aplikaĵoj
   kiel 
   <i>Viber</i> kaj 
   <i>WhatsApp</i> pri kiu ajn novaĵo de trans la oceano. Kaj estas certe, ke ĉio ĉi, kion ni hodiaŭ
   spertas, estas ankoraŭ „prahistorio” kompare kun estonteco, kiu sendube kaŝas multajn kromajn surprizojn, tiel neimageblajn
   kiaj ŝajnus en la pasinta jarcento la nunaj normalaĵoj. Kion niaj okuloj vidos? Kion
   vidos tiuj de niaj idoj? Oni iam ekscios.
</p> 
<h3>Aŭtomobila radio</h3> 
<p>La teknologio evoluas kun ritmo kaj rapido, kiuj ne permesas distraĵojn – post unu-du jaroj komputiloj kaj poŝtelefonoj eksmodiĝos; post jardeko rido akompanos
   demandojn kiel „ĉu vi memoras tiun aparaton, kiun ni havis por ludi videokasedojn?”. Antaŭ 15 jaroj, en la plena teknologia epoko, onia revo estis aĉeti aŭtomobilan
   radion kun sistemo „mem-inversiga”: la stiranto ne bezonu eltiri kasedon por ekaŭskulti ĝian b-flankon. Ĉia komento
   tiurilate estas superflua!
</p> 
<h3>Samideanoj el 1980aj</h3> 
<p>Vekis en mi tiajn konsiderojn la trovo de teksto datumanta el 1982, en kiu samideanoj
   priskribas ja kiel „sciencfikcion” la eventualan inventon de sistemo sufiĉe simila al tio, kio estas nuntempe Interreto.
   La legado de la jena teksto ridetigos multajn junajn kaj nejunajn legantojn kaj eble
   komprenigos al ĉiuj, kiom da paŝoj antaŭen, kaj kiajn paŝojn antaŭen, oni faris rilate
   teknologion kaj komunikadon ene de la lasta jartrideko. Kiel okazas ĉe gepatroj, kiuj
   vidas siajn gefilojn kreski de tago al tago ene de la sama hejmo kaj ne konscias pri
   ilia kreskado, multaj el ni simple ne konstatas la teknologian revolucion, kiu karakterizis
   la unuajn jarojn de ĉi tiu jarmilo.
</p> 
<h3>Semajna bulteno</h3> 
<p>Temas pri la ĵurnalisma artikoleto 
   <i>Telekomunikoj</i>, esperantigita kaj publikigita de la itala esperantisto Antonio De Salvo: la artikoleto
   estis radie elsendita en februaro 1982 de la itala ŝtat-radio RAI, kiu tiam dissendadis
   al la tuta mondo semajnan bultenon, ne nur en Esperanto, sed ankaŭ en pluraj aliaj
   lingvoj, koncerne la plej notindajn novaĵojn el Italio. La teksto, vere emblema kaj
   profeta, estis lastatempe reakirita kaj skanita, kaj oni intencas baldaŭ ĝin publikigi,
   kune kun centoj da aliaj tiaj tekstoj, en papera kaj cifereca versioj, zorge de la
   eldonejo 
   <i>Edistudio</i>. Temas pri la tiel nomata projekto 
   <i>Radio Romo</i>.
</p> 
<h3>Televida terminalo</h3> 
<p>„Iom post iom telefono iĝas io pli ol simpla voĉkanalo, kaj ĝi ampleksas informajn
   fluojn kun senprecedenca graveco. ‚Sciencfikciaj’ perspektivoj malfermiĝas. La itala telefona kompanio SIP, ekzemple, nuntempe eksperimentas
   kun sistemo nomata ‚Videotel’, kiu kunigas telefonan draton kun televida terminalo (eventuale, ankaŭ kun ordinara
   televidaparato) por ‚teledissendi’ ĵurnalon. Oficeje aŭ hejme oni povos elekti per telekomando la deziratan fakon (ĝeneralajn
   informojn, aktualaĵojn, kulturon, ekonomion, turismon ktp) post telefona alvoko al
   la komputilo, kiu funkcias kiel donitaĵa banko.”
</p> 
<h3>Kompleksa aparataro</h3> 
<p>„Jes, efektive estas amuze vidi, kia plirapidigo okazis ene de malmultaj jaroj”, konfesas al M<span class="mm">ONATO</span> la tradukinto de tiu artikolo Antonio De Salvo. „Oni pensu, ke, kiam en 1984 mi parolis kun vendisto pri mia intenco krei ciferecan
   vortaron italan-esperantan kun milionoj da karaktroj, tiu respondis al mi, ke temas
   pri projekto realigebla nur en centro por prilaborado de donitaĵoj helpe de kompleksa
   aparataro, ja ne en hejma medio!”
</p> 
<h3>Serioza komputilo</h3> 
<p>„Mi bone memoras”, De Salvo rakontas, „la unuajn eksperimentojn mem pri 
   <i>Videotel</i>, kiuj en tiu epoko ŝajnis futuremaj. Mi mem gajnis la famon (plejparte malpravan)
   esti fakulo pri komputiko nur pro tio, ke mi akiris mian unuan fakson en 1982, mian
   unuan ‚seriozan’ komputilon en 1984 (temis pri iu 
   <i>Apple //e</i>, kiu hodiaŭ ŝajnus ridinda, sed tiam estis granda paŝo antaŭen kompare al 
   <i>VIC-20</i> kaj al 
   <i>Commodore 64</i>; kaj mi pagis por ĝi pli ol kvar milionojn da tiamaj italaj liroj, proksimume 6000
   eŭroj hodiaŭaj). Mia unua vicnutrilo (angle: UPS, 
   <i>uninterruptible power supply</i>) datumas el 1989, mia unua skanilo el 1991, mia unua laser-printilo el 1992. Kaj,
   kiam mi translokiĝis en 1993 en la nunan hejmon 3-etaĝan, mi kablokunligis ĉion, tiel
   ke mi povas aserti, ke mi havas veran elektronan centron.”
</p> 
<h3>Nuklea fizikisto</h3> 
<p>„Al ĉio ĉi”, daŭrigas De Salvo, „mi estis stimulita ne nur de la deziro prilabori grandan kvanton da donitaĵoj por
   Esperanto (ĉi-momente mi havas pli ol 70 000 dosierojn!), sed ankaŭ de la fakto, ke
   mia filo Alessandro, nun nuklea fizikisto ĉe la Nacia Instituto pri Nuklea Fiziko
   kaj ĉe CERN en Ĝenevo, montris ekde sia infana aĝo grandan intereson rilate komputosciencon:
   li cetere fariĝis internacia aŭtoritatulo en tiu fako kaj, interalie, kunordigas la
   mondan reton de la eksperimento ATLAS.”
</p> 
<h3>Mensa projekciilo</h3> 
<p>Kiu scias, post kroma jartrideko, kiom da novaj diablaĵoj estos en niaj hejmoj kaj
   super kiuj sciencaj projektoj laboros la genepoj de Antonio De Salvo. Eble ili povos
   babili kun malproksimaj parencoj, se ne kun homoj el la estinteco aŭ el la estonteco,
   renkontiĝante kun ili en ciferecaj trinkejoj por trinki tason da kafo pretigita per
   3-dimensia printilo. Iel ajn, se ĉi tiu teksto ankoraŭ iumaniere legeblos, ekzemple
   sur ĉielaj nuboj pere de mensa projekciilo, oni ne forgesu, ke ankaŭ en ĉi tiu prahistoria
   epoko eblis vivi bone, ĝuante la vivon kaj amuziĝante en pli simpla kaj tradicia maniero;
   kion certe povus aserti la homoj el la jaro 1982. Ĉiu estas filo de sia tempo: teknologio
   ja povas faciligi nian vivon, sed la vivo (kun aŭ sen teknologio) povas esti bela
   kaj spirhaltiga iel ajn!
</p> 
