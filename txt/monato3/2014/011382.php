      <h3>Politiko</h3>
      <h3 class="tem">SKOTLANDO</h3>
      <h2>Ne al sendependiĝo</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>La 18an de septembro okazis en Skotlando referendumo pri sendependiĝo. Rajtis voĉdoni
      ĉiuj en la lando – ĉu denaskaj skotoj, ĉu eksterlandanoj – ekde la aĝo de 16 jaroj. La rezulto de la referendumo estis anoncita la sekvan matenon:
      55 % kontraŭ, 45 % por.
   </b>
</p> 
<p>Dum la tagoj antaŭ la voĉdono la etoso similis al karnavalo. Amasoj da jesuloj, portantaj
   skotajn flagojn, kantis, dancis kaj ridis; surstratiĝis ankaŭ amasoj da neuloj, portantaj
   la britan flagon. Svarmis eksterlandaj ĵurnalistoj kune kun entuziasmuloj pri sendependeco,
   ekzemple el Flandrio kaj Katalunio, en la plej grandaj skotaj urboj. Ĝenerale mankis
   rankoro, malamikeco aŭ malbonhumoro inter la frakcioj, krom izolitaj okazoj en malmultaj
   lokoj.
</p> 
<p>Komence de la kampanjo ŝajnis preskaŭ certe, ke venkos la apogantoj de la sendependiga
   movado. Ĝi konsistis ĉefe el la Skota Nacia Partio (iniciatintoj de la kampanjo, kaj
   majoritato en la skota parlamento) sed ankaŭ el verduloj, socialistoj, komunistoj
   kaj anoj de la Kampanjo por Nuklea Malarmado. Postulis ĉi tiu heterogena alianco forigon
   de la brita-usona nuklea submarŝipa bazo en Skotlando.
</p> 
<h3>Impeto</h3> 
<p>La brita ĉefministro, David Cameron [dejvd kamron], permesis la referendumon, tiam
   ne kredante, ke skotoj vere forlasos la Unuiĝintan Regnon. Tamen baldaŭ post la lanĉo
   de la referenduma kampanjo, li konstatis, ke la sendependiĝa impeto vere fortas. Li
   panikiĝis – ne nur pro rezulto, kiun li ne volis, sed ankaŭ pro sia konservativa partio, malkontenta
   pro la permeso okazigi referendumon. Minacata estis lia ĉefministra pozicio.
</p> 
<p>Pro tio, Cameron invitis la ĉefojn de grandaj podetalaj entreprenoj al Londono, kiuj
   tuj anoncis, ke altiĝos varprezoj en la supervendejoj en Skotlando, okaze de sendependiĝo.
   Krome, ĉu pro instigo de Cameron aŭ ne, bankoj deklaris, ke iliaj skotaj ĉefsidejoj
   translokiĝos al Londono. Simile, ĉu instigite de Cameron aŭ ne, la usona prezidanto
   Obama, kun la ĉefministroj interalie de Aŭstralio, Kanado kaj Hispanio, publike kontraŭis
   sendependiĝon.
</p> 
<h3>Bankistoj</h3> 
<p>Asertite estis, ke sendependa Skotlando malfacile, aŭ tute ne, povus aniĝi al Eŭropa
   Unio, Nord-Atlantika Traktat-Organizaĵo, aŭ al la eŭro-valutozono. Bankistoj antaŭdiris,
   ke Skotlando ne rajtos uzi britan pundon kiel nacian valuton. Entute kapitalistoj, miliarduloj
   (inter tiuj konservativaj ministroj en la brita registaro), kaj posedantoj de vastaj
   bienoj en la norda Skotlando (anglaj kaj skotaj konservativuloj kaj nobeloj) unuiĝis
   por ataki, aŭ almenaŭ malinstigi, sendependiĝon.
</p> 
<p>En la lastaj tagoj de la kampanjo, ŝokite, ke opinio-sondado indikas venkon por la
   sendependistoj, la ĉefoj de la tri plej grandaj partioj en la brita parlamento, kun
   multaj partianoj, „invadis” Skotlandon por provi konvinki, ke „kune pli bonas”. Sukcesis la truko. Skotoj ekkredis, ke sendependiĝo negative efikos sur pensiojn,
   la sanservon, kaj ke la vivkostoj altiĝos. Do responde al la simpla referenduma demando
   „Ĉu Skotlando estu sendependa lando?” ili klare diris, ke ne.
</p> 
<h3>Partoprenado</h3> 
<p>Nur kvar el la 32 elektoregionoj voĉdonis por sendependa Skotlando. Nur en Glasgovo,
   la plej granda urbo, kaj en tri aliaj industriaj urboj, kie vivas la plej malriĉaj,
   la plej socie kaj labore marĝenigitaj homoj, oni subtenis sendependiĝon. Cetere rimarkindis
   nekutime granda partoprenado en la referendumo: partoprenis 85 % el la rajtigitaj
   voĉdonantoj, kompare kun iom pli ol 50 % dum la lastaj elektoj al la skota parlamento.
   Tiel la referendumo engaĝis ankaŭ skotojn kutime indiferentajn rilate politikon.
</p> 
<p>
   <i>Postskribe: Kvankam mi loĝas ekster Skotlando, en Norda Irlando, mi devenas de skotaj
      gepatroj, kaj ĉiuj miaj antaŭuloj estis skotaj. Dum mi tuta vivo mi arde deziris,
      ke mia lando rehavu la sendependecon, kiun ĝi perdis antaŭ 307 jaroj. Mi esperis,
      ke antaŭ mia morto mi estos civitano de libera lando. Tion mi ne vidos, ĉar mi ne
      plu estas juna. La 19a de septembro 2014 estis la plej amara tago de mia ekzisto.
      En nia familio ni funebras.
   </i>
</p> 
