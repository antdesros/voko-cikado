      <h3>Politiko</h3>
      <h3 class="tem">IRLANDO</h3>
      <h2>Nova ĉefo, nova aŭroro</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ekde majo 2008 la Irlanda Respubliko havas novan ĉefministron, aŭ <i>taoiseach</i> [tiŝaĥ], nome Brian Cowen [brajan koen]. Cowen, profesia advokato, kiu naskiĝis en 1960, jam okupis gravajn ministrajn oficojn en sinsekvaj registaroj, en kiu regis la respublikana partio Fianna Fáil (FF). Li estas rigardata, eĉ de rivaloj, kiel kompetenta kaj energia politikisto, kiu aparte sukcesis kiel ministro pri financoj.</p> <p>La nuna registaro de la respubliko konsistas el FF (la majoritata partio), la Verda Partio (ekologia), kaj la malgranda Progresiva Demokrata Partio (PD). Antaŭ sia elekto kiel ĉefministro, Cowen surprizis la nacion per prelego, en kiu li fortege esprimis sian intencon apogi la irlandan (gaelan) lingvon.</p> <h3>Patro de la ŝtato</h3> <p>Laŭ la konstitucio de la Irlanda Respubliko, la unua oficiala lingvo estas la irlanda. La angla estas dua oficiala lingvo. Post la morto de Eamon [ejmon] de Valera, la „patro” de la moderna irlanda ŝtato, kaj ĉefa aŭtoro de la konstitucio, preskaŭ neniu posta registaro faris seriozajn klopodojn por firmigi la statuson de la nacia lingvo kaj igi ĝin efektiva unua idiomo de la lando. En unu el la antaŭaj FF-registaroj, ministrino pri irlandlingvaj aferoj montris neniun intereson pri la lingvo kaj eĉ konfesis, ke ŝi ne parolas ĝin.</p> <p>Laŭŝajne Cowen intencas ŝanĝi tiun situacion. Kiam li anoncis sian novan kabineton, li unue nomis la novajn ministrojn per la gaelaj formoj de iliaj nomoj. La plimulto de la ministroj meze aĝas proksimume 40 jarojn, kaj ĉiuj flue parolas la irlandan lingvon. Verŝajne tio neniam okazis en la irlanda parlamento. Cowen mem majstras la lingvon, sed ne nur la FF-ministroj estas gaelparolantoj. Ankaŭ la verduloj fervore apogas la lingvon kaj uzas ĝin je ĉiu eblo.</p> <p>Ĉu nova aŭroro por la irlanda?</p> <div style="text-align:right">Garvan M<span class="mm">AKAJ</span></div>   
