      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Malsana sistemo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kredis japanoj, ke pro alta nivelo de japana medicino la popolo estas perfekte protektata kontraŭ malsanoj. Kredo tia, tamen, ŝanceliĝas. Tra Japanio, ne nur en provincaj urboj kaj vilaĝoj, sed ankaŭ en grandaj urboj kiel Tokio, okazas bankrotoj kaj fermoj de hospitaloj. Aperas novaĵoj pri mortoj pro manko de sufiĉa kuracado.</p> <p>Fermiĝis ekzemple la hospitalo de la urbo Ĉooŝi kun 72 000 loĝantoj en la gubernio Ĉiba. Ĝi estis polikliniko kun 16 fakoj kaj 393 litoj. La urbestro abrupte fermis la hospitalon, ĉar la urba financo estas tiel malbona, ke ĝi ne plu povas subteni ĝin. Kolektiĝis 50 000 subskriboj kontraŭ la fermo, sed vane: tiamaniere 166 enhospitaligitoj kaj vizitantaj pacientoj estas forĵetitaj de la urbo (kaj de la ŝtato) kiel medicinaj rifuĝintoj.</p> <h3>Bebo</h3> <p>En Tokio 36-jara gravedulino suferis pro vomado, lakso kaj severa kapdoloro. Oni petis, ke la Tokio-municipa hospitalo Bokutoo ŝin akceptu, sed tiu rifuzis pro manko de kuracistoj. Oni petis ĉe sep aliaj hospitaloj, sed ĉiuj rifuzis. Fine la unua hospitalo Bokutoo decidis akcepti ŝin kaj sukcesis naskigi la bebon. La patrino tamen mortis pro sangado en la cerbo. Poste evidentiĝis, ke al 11 municipaj hospitaloj en Tokio mankas 56 kuracistoj, el kiuj 12 estas akuŝologoj.</p> <p>En la 1970aj jaroj la registaro havis projekton „Unu gubernio, unu medicina universitato”, kaj la nombro de kuracistoj grandiĝis. En 1982 la registaro decidis subpremi la nombron de studentoj en medicinaj fakultatoj. En 1997 ĝi aprobis daŭrigon de tiu ĉi politiko. La rezulto estas la nuna situacio.</p> <h3>Statistiko</h3> <p>Tial, laŭ statistiko de la Organizaĵo por Ekonomiaj Kunlaboro kaj Evoluigo (OEKE), Japanio okupas la 26an lokon inter ĝiaj 30 membroŝtatoj koncerne la nombron de kuracistoj por 1000 loĝantoj. En 2008 la registaro decidis grandigi la nombron de studentoj en medicinaj fakultatoj, de 4000 al 4300, sed tio estas „akvoguto sur brula ŝtono”, kio efikos nur post 10 jaroj.</p> <p>Kontribuas al la krizo la maniero trejni kuracistojn. Ĉiu volas finstudi ĉe bone ekipitaj hospitaloj, kaj do ne ĉe provincaj universitataj hospitaloj. Al ili do mankas kuracistoj, kaj tial ili revenigas medicinistojn el lokaj hospitaloj. Tiam al tiuj siavice mankas fakuloj kun la rezulto, ke fermiĝas lokaj hospitaloj, kaj suferas la popolo.</p> <p>Dum la pasintaj 10 jaroj naskiĝis en Japanio granda malegaleco inter riĉuloj kaj malriĉuloj. Simile naskiĝas malegaleco pri kuracado.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
