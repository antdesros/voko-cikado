      <h3>Turismo</h3>
      <h3 class="tem">KOSOVO</h3>
      <h2>La marmora groto Gadime</h2>
      <!-- komenco de la enhavo -->

       

       <p>La marmora groto Gadime estas la sola ĉi-momente vizitebla el inter dekoj da kavernoj en Kosovo. Ĝi troviĝas je ĉirkaŭ 20 km sude de la ĉefurbo Priŝtino. La groto estas vere mirinda pro siaj belegaj stalagmitoj kaj stalaktitoj. Tuj post la liberiĝo de Kosovo, junie 1999, la aŭtoro plezure vizitis ĝin trifoje kaj de tiam rekomendas ĝin al vojaĝantoj.</p> <p>La groto estis malkovrita tute hazarde en la jaro 1969 de vilaĝano Ahmet Asllani, kiu serĉis ŝtonojn por sia nova domo. Li estis ŝokita de tio, kion li vidis ene. Poste la groton esploris fakuloj el la eksa Jugoslavio, aparte profesoro Jovan Petroviĉ kaj speleologiaj kluboj el Vojvodino. Oni taksas ĝian aĝon je 80 milionoj da jaroj.</p> <p>Ĝi kuŝas je 584 m super la marnivelo. La groto fariĝis vizitebla por turistoj en la jaro 1976. Ĝi longas 1500 m, sed eblas trarigardi nur 800 metrojn de unu el ĝiaj tri etaĝoj. Ene la temperaturo estas 9,6-16 <sup>o</sup>C. En ĝi estas lageto kun areo de 500 kvadrataj metroj, kaj je 6-10 m sube trafluas rivereto Klusure.</p> <p>En la groto estas kelkaj ejoj. Unu el ili estas la „ejo de geamantoj”. Oni nomas ĝin tiel pro stalagmito kaj stalaktito „Romeo kaj Julieto”, alproksimiĝintaj unu al la alia je distanco de nur 5 cm. Laŭ fakuloj ili povus tuŝi unu la alian nur post 1 500 000 jaroj, ĉar por 1 mm estas bezonataj 30 000 jaroj! Tre interesaj estas ankaŭ aliaj ejoj kaj koridoroj. Sur la plafono de unu el ili troviĝas marmoraj elstaraĵoj, kiuj malgraŭ la gravito turniĝas supren! Neniu ĝis nun povas ekspliki tiun evoluon. Laŭ legendo leporo enveninta la groton eliris post kelkaj tagoj en Janjevo (apud Graĉanica) – loĝloko, situanta aerlinie je 12 km.</p> <p>Antaŭ kelkaj jaroj oni komencis ĉerpi trinkeblan akvon el ĝi por la najbaraj vilaĝoj pere de pumpstacio. Karakteriza por la groto Gadime estas formacioj de marmoro kaj pluraj mineraloj, inter kiuj la rara mineralo aragonito.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
