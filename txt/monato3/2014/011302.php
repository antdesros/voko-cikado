      <h3>Moderna vivo</h3>
      <h3 class="tem">MURDADO</h3>
      <h2>La hejmo: por virinoj la plej malsekura loko</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum 2012 preskaŭ duonmiliono da homoj (437 000) tra la mondo perdis sian vivon rezulte
   de murdoj. Tion raportis en Vieno la Oficejo pri Narkotaĵoj kaj Krimado de Unuiĝintaj
   Nacioj (UNODC).
</p> 
<p>Ĉirkaŭ 80 % de la viktimoj de murdoj kaj 95 % de la krimuloj estas viroj. Preskaŭ
   15 % de ĉiuj murdoj okazas sekve de perforto en la familio (63 600). En la plimulto
   de tiaj kazoj, preskaŭ 70 %, la pereintoj estas virinoj.
</p> 
<p>Do por virinoj la hejmo povas esti la plej danĝera loko. Tio estas aparte kortuŝa,
   ĉar tiuj, kiuj devas defendi siajn proksimulojn, estas iliaj murdintoj. Pli ol duono
   de ĉiuj viktimoj estas malpli-ol-30-jaraj, el kiuj infanoj en aĝo ĝis 15 jaroj estas
   iom pli ol 8 % de ĉiuj murdoj (36 000).
</p> 
<h3>Regiona situacio</h3> 
<p>Preskaŭ 750 milionoj da homoj loĝas en landoj kun alta nombro de murdoj, nome en norda
   kaj suda Ameriko kaj en Afriko. Tio signifas, ke preskaŭ duono de ĉiuj krimoj okazas
   en landoj, en kiuj troviĝas 11 % de la monda loĝantaro. Aliaflanke estas tri miliardoj
   da homoj, ĉefe en Eŭropo, Azio kaj Oceanio, kiuj loĝas en landoj kun relative malalta
   nombro de murdoj.
</p> 
<p>Averaĝe tra la mondo estas murdataj 6,2 homoj je ĉiuj 100 000 loĝantoj. Tamen en Afriko
   kaj centra Ameriko la cifero estas pli ol kvaroble alta (respektive 30 kaj 26 pereintoj
   el ĉiuj 100 000 homoj).
</p> 
<p>Maltrankvilige estas, ke murdoj en norda kaj orienta Afriko kaj la suda parto de Azio
   kreskas pro socia kaj politika malstabileco. Tamen en suda Afriko duoniĝis inter 1995
   kaj 2012 la nombro de murdoj, de 64,5 ĝis 31 el 100 000 homoj.
</p> 
<p>La aŭtoroj de la esploro notas, ke en Ameriko 30 % de la murdoj faris bandoj kaj organizitaj
   grupoj. En Eŭropo kaj Azio tiu cifero ne superas 1 %. 
</p> 
<h3>Kaŭzoj de murdado</h3> 
<p>Konsumado de alkoholaĵo kaj/aŭ de malpermesitaj narkotaĵoj pligrandigas la riskon
   de murdo. En iuj landoj ĉe pli ol duono de la murdoj la krimuloj agis sub influo de
   alkoholo. Kvankam la efiko de malpermesitaj narkotaĵoj estas malpli bone dokumentita,
   kokaino kaj stimuliloj de amfetaminaj specoj estis ligitaj al perforto kaj murdo.
</p> 
<p>Pafarmiloj estas la plej vaste uzataj murdiloj, en kvar el dek murdoj en la mondo.
   Ĉirkaŭ kvarono de la viktimoj estas murditaj per klingoj kaj akraj objektoj kaj iom
   pli ol triono mortis rezulte de aliaj perfortaj manieroj (sufokado, venenado k.a.).
</p> 
<p>Notite estas, ke pafarmiloj estas disvastigitaj precipe en Usono, kie du trionojn
   de la murdoj oni faras per pafiloj. Akrajn objektojn oni uzas plej ofte en Eŭropo
   kaj Oceanio.
</p> 
<p>Tutmonde pro intenca murdo estas 43 kondamnitoj kontraŭ ĉiuj 100 murdoj. Tamen tiu
   nombro diferencas tra la mondo kaj konsistigas 24 % en Ameriko, 48 % en Azio kaj 81 %
   en Eŭropo.
</p> 
