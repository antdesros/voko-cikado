      <h3>Politiko</h3>
      <h3 class="tem">BRAZILO</h3>
      <h2>Parlamento korupton apogas</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pri mandatĉesigo de unu el siaj membroj decidis la federacia ĉambro de deputitoj – unu el la du ĉambroj de la parlamento: la alia estas la senato. Jaqueline Roriz [ĵakeline horís], 49-jaraĝa, estas akuzita pri kontraŭleĝa akcepto de mono por la kaso de sia elektokampanjo.</p> <p>Tamen en aŭgusto, en hontiga sesio, la parlamentanoj ignoris kaj la okulfrapan pruvon pri korupto kaj publikan opinion, tiel ke 265 el la 451 voĉdonintaj parlamentanoj rifuzis aprobi la ĉesigon de la mandato.</p> <p>Jaqueline Roriz estas filino de Joaquim Roriz [ĵoakín horís], kiu en 2007 rezignis pri sia posteno en la senato, kiam oni denuncis lin pri korupto. En filmo malkovrita de la polico antaŭ du jaroj, Jaqueline aperas kun sia edzo en kabineto de ŝtata funkciulo, kiu liveras al ŝi paketon da monbiletoj. Eblas spekti parton de tiu filmo en <a href="http://www.youtube.com/watch?v=cSAfEFf6Eqc&amp;feature=related">www.youtube.com/watch?v=cSAfEFf6Eqc&amp;feature=related</a>.</p> <h3>Prifraŭdi</h3> <p>La polico konkludis, post plurmonata enketo, ke tiu ago de la deputitino, okazinta en 2006, ligiĝas al vasta reto de korupto, per kiu la tiama registaro de la Federacia Distrikto subaĉetis distriktajn parlamentanojn, por pli facile prifraŭdi la ŝtatan kason.</p> <p>La deputitino ne neis sian krimon. Por eviti la forigon el ŝia posteno, ŝia ĉefa argumento estis, ke la afero okazis antaŭ multaj jaroj, kiam ŝi ankoraŭ ne estis membro de la federacia parlamento. Ŝajne tiu rezonado ne konvinkis la partiestrojn, kiuj rekomendis al kolegoj aprobi la mandatĉesigon.</p> <p>Tamen la voĉdonado estis sekreta, kaj la plimulto favoris ŝin. Oni diras, ke la rezulto estas simptomo de ĉeesto de multaj kaŝitaj kulpuloj en la ĉambro de deputitoj.</p> <h3>Kortumo</h3> <p>Ekde la pasinta jaro validas en Brazilo leĝo, kiu malpermesas, ke homo kondamnita pri krimo kandidatiĝu al politika posteno. Tiu leĝo estas kontestata de kelkaj politikistoj ĉe la plej supera brazila kortumo, kiu ankoraŭ ne decidis pri la afero.</p> <p>Nun oni timas, ke la sinteno de parlamentanoj povas influi la juĝistojn de la supera tribunalo malfavore al la leĝo, rigardata de la popolo kiel necesa por bari al friponoj la vojon al la politika povo.</p> <p>La 7an de septembro, nacian ferion en Brazilo, okazis en pluraj urboj publikaj protestoj kontraŭ korupto, kun granda partopreno de junuloj.</p> <p>Estas interese, ke la kunordigantoj de tiuj manifestacioj ne permesis dum la marŝoj flagojn de sindikatoj kaj politikaj partioj, ĝuste por montri, ke temas pri movado spontanee popola, senpartia.</p> <div style="text-align:right">José Mauro P<span class="mm">ROGIANTE</span></div>   
