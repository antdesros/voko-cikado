      <h3>Lingvo</h3>
      <h3 class="tem">REAGO</h3>
      <h2>La angla kiel ebla skandinava lingvo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En sia artikolo 
   <i>Ĉu la angla devenas ne de la praangla?</i>, citante la norvegon Jan Terje Faarlund, Otto Prytz bone argumentas pri la skandinava
   deveno de la angla. Kompreneble, li mem scias, ke la moderna angla estas, laŭ germana
   terminologio, 
   <i>Mischsprache</i> (miksita lingvo), konsistanta el anglosaksaj, skandinavaj, normanfrancaj kaj multe
   da pliaj elementoj.
</p> 
<h3>Poetoj</h3> 
<p>Mi volas proponi, ke estus pli fruktodone esplori ne, rekte, la rilaton inter la angla
   kaj la skandinavaj lingvoj, sed tiun inter la skandinava (precipe la islanda) kaj
   la „skota”. Eble mi unue devas iom priskribi la lingvan situacion en Skotlando. La tiel nomata
   skota – ne la skotgaela, kiu estas kelta lingvo, tute sen rilato al la skota aŭ al la angla
   – disvolviĝis el la norda „anglia” dialekto parolata en la regiono ĉirkaŭ la landlimo inter Anglio kaj Skotlando.
</p> 
<p>Dum la Mezepoko la skota estis la oficiala lingvo de la reĝa kortego, kvankam la gaela
   estis disvastiĝinta en preskaŭ la tuta lando, krom la sud-orienta regiono, kion oni
   povas konstati per la universala ĉeesto de gaelaj loknomoj tra Skotlando, de la nordo
   al la sudo. Nun la skota en „pura” formo ne estas ĉiutaga idiomo de la junaj generacioj, sed ĝi ankoraŭ estas uzata
   kaj ŝatata de poetoj, lingvaj entuziasmuloj kaj anoj de la tiel nomata Skota Lingva
   Asocio. Laŭ tiuj uzantoj, la skota estas aparta lingvo; laŭ malpli interesitoj, ĝi
   estas nura norda varianto de la angla.
</p> 
<h3>Listo</h3> 
<p>Dum lastatempa vizito al Islando, kiel skribite ankaŭ en la pasinta artikolo, mia
   edzino kaj mi estis frapitaj de la grandega simileco inter la vortaro de la islanda
   lingvo kaj tiu de la skota. Ĉi tiu simileco estas eĉ pli notinda, ol kiam oni faras
   komparon inter la skandinavaj lingvoj kaj la angla. Jen kelkaj ekzemploj el multaj
   eblaj. (Rimarko: unue aperas la skota vorto, poste, inter parentezoj, la islanda.
   Eta noto pri islandaj literoj: „ð” = „dh”; „þ” = „th”. Malgraŭ malsama ortografio, la prononco de la du idiomoj sufiĉe similas, se oni
   ne konsideras la finaĵon „-a” de la islanda infinitivo.) 
</p> 
