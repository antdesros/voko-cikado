      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Ŝrumpanta lando, kreskanta ŝanco</h2>
      <!-- komenco de la enhavo -->

       

       <p>Unuafoje post 1899 la nombro de japanaj mortintoj superis tiun de naskiĝintoj. Fine de la pasinta jaro la japana registaro anoncis, ke en 2005 la nombro de naskiĝoj estis 1 067 000 kaj la nombro de mortoj 1 077 000.</p> <p>Ke tiel ŝrumpas Japanio, rezultas de pluraj faktoroj. Unue temas pri altaj viv- kaj loĝ-kostoj. Salajroj ne altiĝas, male impostoj. Tio signifas, ke multaj gepatroj ne povas toleri la eduko-kostojn de siaj idoj: kotizoj ĉe infanvartejoj kaj ankaŭ ĉe universitatoj plialtiĝas.</p> <p>Alia kialo rilatas al laborhoroj. Preskaŭ du trionoj de la homoj inter la aĝoj de 30 kaj 39 revenas hejmen post la 20a horo kaj preskaŭ unu triono eĉ post la 22a. Virinoj inter tiuj ĉi aĝoj revenas meze iom antaŭ la 19a horo. Tial ili ne havas tempon por infanoj.</p> <h3>Kariero</h3> <p>Krome en Japanio virinoj preferas labori anstataŭ patriniĝi. Malfacile estas havi bebon kaj teni karieron. Alia kialo: pli ol duono de la laboristoj inter la aĝoj de 20 kaj 24 ne havas firmajn postenojn: ili malmulton perlaboras kaj estas en situacioj tre malstabilaj. Rezulte ili ne geedziĝas kaj tial, laŭ statistikoj, ne naskiĝas ĝis eble 28 000 beboj.</p> <p>Prognozite estas, ke en la jaro 2050 vivos 100 milionoj da japanoj (en 2005 estis 128 milionoj), el kiuj 35 % havos pli ol 64 jarojn (en 2005, 20 %). Tamen oni ne parolu pri krizo. Japanio estas malgranda lando kun malriĉaj naturfontoj kaj japanoj tro multe laboras por teni sian ekonomie prosperan vivnivelon. Do estas tempo ŝanĝi ĉi tiun principon kaj vivi harmonie kun aliaj landoj kaj kun la naturo. Anstataŭ paroli pri krizo, oni parolu pri granda ŝanco por modele vivi en la 21a jarcento.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo/pg</div> 
