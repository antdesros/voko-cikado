      <h3>Lingvo</h3>
      <h3 class="tem">IRANO</h3>
      <h2>Lingvo kiel sukero: notoj pri la nuntempa persa</h2>
      <!-- komenco de la enhavo -->

       

       <p>En la jaro 1921 la irana verkisto Mohammad-Ali Ĝamalzade eldonis en Berlino novelon
   kun la esprimoplena titolo 
   <i>La persa estas sukero</i>. Tie la rakontanto, revenante hejmen post kelkaj jaroj pasigitaj en Eŭropo, estas
   detenita ĉe la limo kaj enprizonigita en senfenestra kelo, kune kun tradiciista mulao,
   kun pseŭdo-modernisto kaj kun ordinara, humila persono, kiu komprenas nek la arablingve
   ŝarĝitan persan de la unua, nek la same fremdan lingvon de la lasta, plenplenan de
   francaj vortoj kaj esprimoj. La verkisto vivece bildigas la samĉelanojn kiel difinitajn
   sociajn tipojn: la mulaon retiriĝinta en la estinteco, kaj la pseŭdo-moderniston trovanta
   rifuĝon en surfaca ksenofilio. Tio igas la rakonton de Ĝamalzade kvazaŭ spegulo, en
   kiu la persa socio de lia epoko – kaptita inter la kunpuŝiĝo de heredita tradicio kaj eŭropa kulturo – estas reflektita.
</p> 
<h3>Moderneco kaj tradicio</h3> 
<p>Nu, se ni simple volas anstataŭigi la ekskutiman francan per la pli aktuala uson-angla
   lingvo, la novelo – post la forpaso de preskaŭ cent jaroj – povus certe esti skribita ankaŭ en la hodiaŭa Tehrano, eble kun scivolema alilandano
   en la loko de la humilulo. Oscilante inter diversaj antropologiaj kaj lingvaj modeloj
   ofte konfliktantaj, nuntempe Irano havas en sia interno la plej altan gradon – kiel kaj eĉ pli ol en la epoko de Ĝamalzade – de moderneco kaj tradicio, kaj tio nature reflektiĝas en la lingvaj decidoj de ĝiaj
   parolantoj. Tiel, eĉ la elekto de vorto aŭ radiko hodiaŭ povas havi altan simbolan
   signifon, kaj ankaŭ politikan.
</p> 
<h3>Hindeŭropa lingvo</h3> 
<p>Tamen tio ne estas nova fenomeno. Oni povas diri, ke la nova persa, kiel ni konas
   ĝin nun, skribata per arabaj skribsignoj, kvankam ĝi estas hindeŭropa lingvo, naskiĝis
   ĉirkaŭ la 9a jarcento de la komuna erao el konsiderinda araba grefto al la trunko de
   la mez-persa lingvo de la antaŭ-islama sasanida dinastio. Tiu grefto, anstataŭ morfologia
   aŭ sintaksa, estis unue leksika, kaj tiel, ekde la origino de tiu literaturo, la verkistoj
   povis elekti facile – utiligante pli aŭ malpli da arabaj pruntoj, depende de la okazo – kiom „arabigita” aŭ „persigita” devus esti ilia skribaĵo. Tio havis religian kaj, iamaniere, politikan reflektiĝon:
   la poeto Ferdoŭsi, ekzemple, en sia epopeo pri la gloroj de la antikva Irano, utiligis
   intence malmulte da arabaj terminoj, sed aliaj klasikuloj, precipe la religiuloj kaj
   la mistikuloj, faris tion ege abunde.
</p> 
<h3>Dekadenca paseo</h3> 
<p>En la moderna epoko, ekde la 19a jarcento, aperis nova leksika influo – precipe rilate al teknikaj vortoj – el la rusa, la angla, kaj ĉefe el la franca al la persa. Tiu estis la erao, en kiu
   Irano, post pli ol jarcenta postresto, remalkovris la modernecon, tiun tempon simbolatan
   de la ekzemplo de Eŭropo. Poste la angla lingvo, en la dua duono de la lasta jarcento,
   definitive trudanstataŭis la francan en la rolo de eduka kaj distingiteca lingvo por
   la altaj klasoj, kiuj pli kaj pli flankelasis la studon de la araba, en tiuj jaroj
   ofte rigardata kiel simpla supervivo de dekadenca paseo.
</p> 
<h3>Araba elemento</h3> 
<p>Alia firstolinio el lingva perspektivo estis la islama revolucio de 1979. Ekde tiu
   dato la araba elemento, kiu ĉiam ĉeestis vigla en la antaŭurboj kaj en la provincoj
   de la lando, komencis antaŭeniĝi ree ankaŭ en la politika diskurso kaj en la amaskomunikiloj.
   Tio okazis ne sen iuj troigoj, kaj kelkfoje ankoraŭ okazas, ke oni nuntempe vidas
   tekstojn plenplenajn de maloftaj arabaj vortoj – pura ekzerco de sinonimoj por la malutilo de la komprenebleco mem de la mesaĝo.
</p> 
<h3>Amata verkistino</h3> 
<p>Per tiaj osciloj, tamen, ne estu trompita la leganto: la persa lingvo hodiaŭ estas
   fakte tre vigla, kaj ĝi estas ega fierindaĵo kaj firma fundamento de la nuntempa irana
   identeco. Iranano eĉ ne pensus, kiel ekzemple faras multe da libananoj, eduki siajn
   gefilojn en lingvo alia ol la persa. Tia respekto por la propra lingvo estas tre disvastiĝinta
   ankaŭ en la novaj generacioj: estas kortuŝe vidi, kiom da junuloj ĉiun semajnon renkontiĝas
   ĉe la tombo de la poetino Forug Farroĥzad, en la nordo de Tehrano, por kanti kune
   la versojn de tiu amata verkistino. Eĉ pli miriga estas la tute aŭtentika pilgrimado,
   kiu kondukas al la urbo Ŝirazo junulojn kaj maljunulojn, riĉulojn kaj malriĉulojn
   el la tuta lando, por omaĝi la memoron de du el la plej grandaj poetoj de la klasika
   literaturo, Sadio kaj Hafizo. Ĉe la du maŭzoleoj kaj en la dolĉaj ĝardenoj, kiuj ĉirkaŭas
   ilin, en absorbita kaj serena atmosfero, oni povas malkovri alian manieron interpreti
   la paseon kaj la nuntempon, la morton kaj la vivon. Kun enmane la kanzonaro de Hafizo,
   kiun Goethe nomis „mia orienta ĝemelo”, oni voĉlegas kaj komentas la malfacilajn versojn de la mezepoka poeto, dum infanetoj
   vendas malgrandajn fermitajn kovertojn, kiuj entenas verson de tiu volumo, el kiu
   oni tradicie antaŭdiras la estonton. Kaj la futuro de Irano, nedubinde, pasos ankaŭ
   tra ĉi tie.
</p> 
