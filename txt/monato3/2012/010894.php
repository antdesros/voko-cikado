      <h3>Politiko</h3>
      <h3 class="tem">BRAZILO</h3>
      <h2>Meĉo kaj bombo</h2>
      <!-- komenco de la enhavo -->

       

       <p>La supera kortumo de Brazilo fine pritraktis 38 homojn akuzitajn pri subaĉetado en la nacia kongreso. Temas pri skandalo okazinta en la dua mandato de eksprezidanto Lula da Silva. Inter ili estis du ĉefroluloj de la Partio de la Laboristoj (PL), ankoraŭ la reganta partio.</p> <p>Por la skandalo ne mankis neologismo, kreita de la unua el la akuzitoj, eksdeputito Roberto Jefferson [robertu ĵéferson]. Li baptis la aferon <i>mensalão</i> [mensaláŭm], tradukebla el la portugala kiel „neformala monata subvencio”.</p> <h3>Venĝo</h3> <p>Se la konfeso de Jefferson, eksprezidanto de unu el la partioj de la koalicio estrita de Lula, funkciis kvazaŭ meĉo, tamen la bombo nur ĵuse eksplodis. Pro manko de pruvoj la afero atendis de 2005 ĝis 2012 por atingi la superan kortumon. Tie ĉio aliformiĝis en politikan venĝon, sed fakte ne venkon, kontraŭ la reganta koalicio.</p> <p>La origino de PL estas la vigla sindikata movado de la 1970aj jaroj. Ĝia plej grava fondinto estas Lula, rekonata batalanto kontraŭ la militista reĝimo (1964-1985). Kiam ĝi estis la plej grava opozicia partio, PL ĉiam atentigis pri la graveco de la etiko en la politika afero.</p> <h3>Risko</h3> <p>La kondamno de eminentaj figuroj de la partio, ĉefroluloj en la periodo de Lula, sufiĉe instruis pri la risko misuzi la samajn armilojn kiel la malamikoj. Rimarkindas, ke eksprezidanto Fernando Henrique Cardoso [fernandu enriki kardozu], ĝuis duan sinsekvan mandaton kiel respublikestro (1999-2002) pro subaĉetado.</p> <p>Tiel Cardoso sukcesis nuligi leĝan malpermeson pri dua sinsekva mandato por respublikestro. Pri tio silentis la supera kortumo.</p> <h3>Balotado</h3> <p>Praktike, la nuna skandalo, malgraŭ la bruega pafado el la opozicio kun eĥo de la konservativa flanko de la gazetaro, trafis nek eksprezidanton Lula, kiu siatempe plensukcese renovigis per balotado sian mandaton (2006-2009), nek prezidantinon Dilma Roussef [diŭma rusef], kiu heredis la modelon de Lula (M<span class="mm">ONATO</span>, 2011/01, p. 10).</p> <p>Kaj Lula kaj Roussef rikoltis laŭdojn en- kaj ekster-landajn por tio, ke ilia politiko solvis almenaŭ parte la ekonomiajn kaj socialajn problemojn postlasitajn tra la nacia historio de la sama elito, kiu nuntempe vestas sin en la majesta talaro de la supera kortumo por pozi teatre antaŭ la televidaj kameraoj.</p> <div style="text-align:right">Jozefo L<span class="mm">EJĈ</span></div> 
