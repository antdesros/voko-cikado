      <h3>Arto</h3>
      <h3 class="tem">SLOVAKIO</h3>
      <h2>Pentristo de la homo kaj de la naturo</h2>
      <!-- komenco de la enhavo -->

       

        <p>Inter la slovakaj pentristoj de la 20a jarcento okupas apartan lokon Martin Benka,
   grafikisto, ilustristo kaj desegnisto, kaj unu el la fondintoj de la slovaka pentroarto.
   Laŭ Benka, la naturo estas intime ligita kun homa agado. Pro tio gravan lokon en liaj
   pentraĵoj havas la pejzaĝo.
</p> 
<p>Benka naskiĝis en 1888 en la vilaĝeto Kostolište, ĉirkaŭ 40 km okcidente de la ĉefurbo
   Bratislavo, en ĉarpentista familio. Li ellernis la farbistan metion en la ĉeĥa urbo
   Hodonín kaj poste laboris kiel farbisto-kolorigisto en diversaj konstrufirmaoj en
   Vieno.
</p> 
<h3>Lernejo</h3> 
<p>Sed la kolorigista metio ne estis lia vivcelo. Pro tio li pasigis du jarojn en privata
   lernejo ĉe ĉeĥaj pentristoj. Li ellaboris propran artan stilon ligitan al la naturo
   kaj la homo. Jam lia unua pentraĵo 
   <i>Suna mateniĝo en arbaro</i>, ekspoziciita en la praga Domo de Artistoj (ĉeĥe 
   <i>Rudolfinum</i>) en 1913, rikoltis grandan sukceson.
</p> 
<p>Post 39 jaroj li forlasis Pragon kaj ekloĝis en la slovaka urbo Martin. Tie la urba
   estraro konstruigis al li domon kun ateliero kaj muzeo pri pentraĵoj. Reciproke Benka
   transdonis al la ŝtato sian bazan kolekton de pentraĵoj – pli ol 5200 proprajn verkojn, kiuj formis la artfonduson de la hodiaŭa muzeo de Martin
   Benka.
</p> 
<h3>Verkaro</h3> 
<p>Benka multe veturis kaj pentris en diversaj regionoj de Slovakio. Tamen sukceson kaj
   agnoskon li trovis en pluraj landoj. Ekspoziciataj estis liaj pentraĵoj en Aŭstrio,
   Belorusio, Ĉeĥio, Germanio, Hungario kaj Rusio.
</p> 
<p>Benka estis universala artisto. Li okupis sin ankaŭ pri arĥitekturo, grafiko, ilustrado
   de tekstoj, muzika komponado kaj desegnado de poŝtmarkoj. Inspirita de kubismo li
   eĉ mane fabrikis 13 violonojn. Popularaj restas liaj ilustraĵoj de naciaj popolaj
   rakontoj. Cetere li esperantistiĝis 59-jaraĝa kaj kontribuis i.a. al la revuo 
   <i>Esperantisto Slovaka</i>.
</p> 
<h3>Ordeno</h3> 
<p>Okaze de sia 65a naskiĝdatreveno Benka akiris la prestiĝan titolon „nacia artisto” – la unua slovaka pentristo tiel titolita. En 1963 li ricevis respublikan ordenon pro
   sia dumviva artista laboro. Li estis unu el nur kelkaj reprezentantoj de la moderna
   pentroarto, kiuj favore akceptis la komunistan reĝimon en la iama Ĉeĥoslovakio.
</p> 
<p>Benka mortis en 1971 kaj estis entombigita en la nacia tombejo en Martin. En lia slovaka
   naskiĝa vilaĝeto estas kreita memorĉambro pri liaj vivo kaj verkaro, kie vizitantoj
   povas trovi originalajn pentraĵojn.
</p> 
