      <h3>Ekonomio</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Post ni, la diluvo!</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Antaŭ nelonge la tuta mondo spirretene rigardis la evoluadon de la financa krizo de
      Grekio. Ankaŭ en Japanio oni ĵurnale kaj televide raportis la politikan negocadon
      inter Grekio kaj Eŭropa Unio kaj la konfuzon en la socio kaj la popolo.
   </b>
</p> 
<p>Ŝajnas, ke la japanoj cinike rigardis la aferojn en Eŭropo kaj priridis la grekan
   tragedion, sed ĉu la japanoj povas fajfi kaj rigardi la aferojn de Grekio kiel fajron
   trans la bordo? Ni rigardu la ciferojn.
</p> 
<p>En marto 2015 la ŝtata ŝuldo de Japanio atingis 1053 milionojn da miliardoj (1 053
   000 000 000 000) da enoj (JPY), tio estas 7750 miliardojn da eŭroj. Alivorte, ĉiu
   naciano ŝuldas po 8,3 milionojn da enoj (61 089 eŭroj), kaj la ŝuldo, kiel procentaĵo
   de ĝia malneta enlanda produkto (MEP), estas 246 %, dume tiuj por Grekio, Germanio,
   Francio kaj Italio estas 172 %, 75 %, 117 % kaj 133 % respektive. Ĉu la situacio por
   Japanio estas malpli bona ol tiu de Grekio? Jes kaj ne.
</p> 
<p>Por repagi la ŝuldon, ankaŭ la japana registaro devas praktiki severan ekonomian politikon
   inkluzive de altigo de impostoj kaj malaltigo de la nivelo de sociala bonfarto, kaj
   ekzistas risko, ke la prezo de la japanaj ŝtataj obligacioj subite plonĝos kaj ke
   la interezo altiĝos.
</p> 
<p>Tamen estas granda diferenco inter Grekio kaj Japanio: plejmulto de la kreditoroj
   de Grekio estas alilandanoj, nome eksterlandaj financaj institucioj, dum la japana
   registaro ŝuldas plejparte al sia popolo kaj al enlandaj bankoj. Tial la japana registaro
   ne estas premata de alilandanoj pri redukta ekonomia politiko kaj povas daŭre elspezi
   multe pli ol enspezi.
</p> 
<p>Alia diferenco estas, ke ekde la 1990aj jaroj Japanio ĉiujare akumuladis ŝtatan ŝuldon,
   dum Grekio komencis ŝuldegi nur post la tutmonda financa krizo en 2008. Nu, por kio
   la japana registaro tiom elspezis? Precipe por la sociala bonfarto kaj stimulado de
   la konjunkturo. La plimulton de la sociala bonfarto okupas pensioj, medicino kaj maljunulvartado,
   alivorte socialaj servoj al maljunuloj.
</p> 
<p>Denove vidu la internacian komparon. Inter du el naŭ kampoj de ŝtata elspezo, nome
   por maljunuloj (diversaj disdonoj por maljunuloj) kaj por familioj (infanvartado aŭ
   instigo de plimultigo de naskiĝoj) Japanio disdonis 46,5 % por maljunuloj kaj 5,7
   % por familioj, dume Britio 28,7 % kaj 16,9 %, Germanio 32,0 % kaj 8,4 %, Francio
   39,9 % kaj 9,4 % kaj Svedio 34,1 % kaj 13,2 % respektive laŭ la raporto de OEKE (Organizaĵo
   por Ekonomiaj Kunlaboro kaj Evoluigo) en 2011.
</p> 
<p>Evidentiĝas, ke en nuna Japanio maljunuloj havas sufiĉe agrablan socialan sekurecon
   koste de gejunuloj. Kelkaj kritikantoj diras, ke tiu politiko estas atenco aŭ krimo
   kontraŭ la nunaj gejunuloj aŭ la venonta generacio. Sed la politikistoj tute ne zorgas,
   ĉar ili konscias nur pri la nuna tempo kaj scias, ke neniu respondecas pri la eventuala
   krizo aŭ kaoso.
</p> 
