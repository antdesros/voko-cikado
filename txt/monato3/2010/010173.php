      <h3>Spirita vivo</h3>
      <h3 class="tem">ALBANIO</h3>
      <h2>Centjariĝo de Patrino Tereza</h2>
      <!-- komenco de la enhavo -->

       

       <p>La 26an de aŭgusto 1910 naskiĝis la verŝajne plej fama virino en la mondo, Patrino Tereza. Ŝia origina nomo estas Gonxhe Bojaxhi [gonĝe bojaĝi], albandevena.</p> <p>Fruaĝe ŝi jam komprenis, ke ŝi havas mision je la nomo de Dio por servi al la plej malriĉaj inter la malriĉuloj. De Prizeren (Kosovo) al Skopje (Makedonio), poste al Zagrebo (Kroatio) kaj Dublino (Irlando) ŝi maturiĝis kiel vera misiisto kristana por definitive instaliĝi en Kolkato en Barato. Dum 60 jaroj ŝia personeco radiis tra la tuta mondo, ĉie kie okazis malfeliĉoj, militoj, amasaj epidemioj, kruelaj murdoj. Je la nomo de Jesuo patrino Tereza certigis monon, panon, vestojn kaj rifuĝon al miloj da homoj, kiuj vivis marĝenigite, diskriminaciite, malsataj kaj malsanaj.</p> <p>Bedaŭrinde, la patrujo, la komunisma Albanio, ne akceptis ŝin ĝis la jaro 1989, kiam ŝi vizitis ĝin kaj la tombojn de la patrino kaj fratino en la urbo Shkoder [ŝkoder]. Estis tempo, kiam la komunisma reĝimo estis ŝanceliĝanta, kio kondukis en la jaro 1991 eĉ al ties falo. Ekde tiam patrino Tereza starigis ankaŭ en sia patrujo plurajn misiejojn, kie deĵoras kaj servas ankoraŭ centoj da fratulinoj katolikaj. Ŝi iniciatis ankaŭ la konstruon de nova granda katedralo de Sankta Paŭlo apud la centro de Tirano. Ŝi persone atendis mem papon Johanon Paŭlon la 2an dum lia vizito en Albanio, printempe 1993.</p> <p>Kvankam foririnte junaĝe el la patrolando, Tereza neniam forgesis ĝin nek ĝian popolon. Iufoje ŝi diris: „Laŭ la sango mi estas albana, laŭ civitaneco baratano, laŭ la kredo kristana, laŭ la koro misiisto de Jesuo”. Daŭre ŝi diris: „Ni preĝas kaj kredas, ni kredas kaj amas, ni amas kaj servas”.</p> <p>La albana registaro omaĝe al ŝi deklaris la jaron 2010 <i>Jaro de Patrino Tereza</i>. Tiuokaze oni aranĝis ekspoziciojn, stariginte novajn monumentojn, prezentante plurajn koncertojn. Lastatempe la urbodomo de Tirano decidis starigi apartan muzeon pri ŝi, rekonstrui la placon samnoman apud la Universitato, nomumi novan <i>Straton de Patrino Tereza</i>, kie oni metos ŝtonojn kun la surskribo de mondaj eminentuloj pri ŝi. La albana poŝtoserva kompanio eldonis novajn poŝtmarkojn honore al ŝi. La parlamenta prezidanto aranĝis apartan riĉan fotoekspozicion ĉe la fronto de la parlamentejo, laŭ granda bulvardo. La aktivecoj daŭros dum la resto de la jaro. Similaj aktivecoj okazis ankaŭ en Kosovo, Makedonio, Barato kaj en pluraj aliaj landoj, ĉie kie patrino Tereza postlasis siajn neforviŝeblajn spurojn.</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
