      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Revuon redakti ... kaj ranoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>Dum la somero la redakcio de M<span class="mm">ONATO</span> ricevis plurajn komentojn pri la nova aspekto de la magazino. Legantoj ĝenerale laŭdis
   la grafikajn ŝanĝojn, dirante, kiom plaĉas la kvalito de la papero, kaj aldonante,
   ke la artikoloj pli klare, pli facile legeblas.
</p> 
<p>„Mi devas gratuli vin pro la nova aspekto de M<span class="mm">ONATO</span>, kun belaj fotoj,” komentis unu leganto. Alia mesaĝis, por „gratuli pro la daŭra kvalito, kaj lastatempa pli bela aspekto, de M<span class="mm">ONATO</span>, kiun mi ĉiam legas kun granda plezuro”. Tria lakonis: „Belege!”
</p> 
<p>Tamen ne por fanfaroni, ne por distrumpeti la talenton de M<span class="mm">ONATO</span>-kunlaborantoj (kiuj aplaŭdojn ja meritas, aparte nia grafikisto Ferriol Macip), mi
   nin tiel laŭdas. Ĉar, kun la laŭroj, venis ankaŭ dornoj – ne multaj, tamen ili piketis.
</p> 
<p>Temas pri la fotoj. Laŭ unu leganto, kiu ja gratulis pri la aspekto: „Mi ne komprenas, kial aperas nun tiom grandaj fotoj, kaj, sekve, pli mallongaj tekstoj.
   Ĉu la magazino fariĝas pli rigardinda ol leginda, t.e. malpli aboninda por mi?”
</p> 
<p>Nepras tuj respondi al la demando: ne. Certe M<span class="mm">ONATO</span> ne celas pli rigardindigi ol legindigi la magazinon: kun televidaj novaĵ-elsendoj
   ne eblas, kaj certe ne deziratas, konkurenci.
</p> 
<p>Sed restas fakto, ke en kelkaj someraj numeroj de M<span class="mm">ONATO</span> troveblis paĝoj dominataj ne de vortoj, sed de fotoj. Kaj, jes ja, kontraste, mallongis
   pluraj artikoloj.
</p> 
<p>Parte kulpas mi. Fojfoje mi koncizigas kontribuaĵojn senditajn de korespondantoj,
   ĉar la temo ne meritas longan pritraktadon. Artikolo pri, ni diru, tertremo vaste
   kaj aktuale priraportita en naciaj gazetaro kaj televido, balastas, se ni regurdas
   en M<span class="mm">ONATO</span> tion, kion ni jam scias pri evento jam de longe pasinta. Ne tiom gravas la tiama
   „kio”, resumebla per unu memoriga frazo; gravas la nuna „kio”: kiel reagas la homoj suferantaj, kion faras la registaro ktp.
</p> 
<p>Simile, en raporto pri naciaj elektoj, internacia legantaro ne bezonas informojn pri
   ĉiu etpartia kandidato, kiu fine rikoltis nur du elcentojn de la voĉoj. Gravas enlande,
   jes, sed eksterlande nepras scii pri la politiko de la nova registaro. Do tiel eblas,
   ja nepras, koncizigi kaj legindigi raportojn.
</p> 
<p>Tamen – kaj jen konfeso – tiel mi jam de jaroj pritraktas artikolo-proponojn. Do la fenomeno de mallongaj,
   koncizaj raportoj certe ne novas. Flanke mi diru, ke vere malmultaj aŭtoroj plendis
   pro mia metafora ruĝa, redaktora krajono: ne malofte ili dankas, ke mi tiel poluris
   ilian prozon.
</p> 
<p>Kio novas, kaj pli gravas, estas malpli manko de kontribuaĵoj ĝenerale (proponoj alvenas
   kelkfoje lavange, kelkfoje ne: malfacilas prognozi kaj plani; eĉ ne ĉiam prosperas
   al ni ricevi komisiitajn artikolojn) sed pli manko de eseoj, komentarioj, do longaj,
   analizaj pecoj. Raporto pri tertremo, balotoj aŭ aliaj aktualaĵoj efektive ne bezonas
   multe da vortoj: male, zorge kaj funde elpensita esploro de difinita evento aŭ situacio.
   Kun kelkaj valoraj esceptoj, mankas ĝuste tiaj tekstoj.
</p> 
<p>Kial, mi ne scias. Ŝrumpas la libera tempo pro tio, ke almenaŭ en okcidentaj landoj
   malŝrumpas ebloj ĝin plenigi. Premas laboraj, familiaj, sociaj taskoj kaj devoj. Krome
   ni vivas en mondo jam novaĵo- kaj informo-riĉa, danke al amaskomunikiloj, Interreto
   ktp.
</p> 
<p>Mi iafoje demandas min, ĉu troe informi signifas fakte mal- aŭ ne-informi: niaj cerboj
   ne povas reteni tiom da informeroj en komplika, fojfoje ĥaosa, rapide ŝanĝiĝanta mondo.
   Ĉi-someron, varbante por M<span class="mm">ONATO</span>, mi petis, ke oni verku por la magazino. Venis respondo: „Mia lando tro grandas, por ke mi komprenu, kio okazas en ĝi. Kiel do prie verki?”
</p> 
<p>Tamen ŝtopeblas la komentario-, la analizo-breĉo en M<span class="mm">ONATO</span>. La solvo kuŝas en viaj manoj, do en tiuj de la legantoj. Admonas Julio Baghy en
   „Rankvarteto”:
</p> 
<p>Tio pruvas: se dum pluvo,
   <br></br> staras sub tegment' la kuvo,
   <br></br> Ranoj vane kvaki povas,
   <br></br> se je salto sin ne movas.
   <br></br> Do ne kvaku kiel ranoj,
   <br></br> karaj gesamideanoj!
</p> 
<p>Alivorte, karaj gelegantoj, ne plendu. Eksaltu ... kaj ekverku!</p> 
