      <h2>Fino de ramadano: ĉu festo de gejunuloj?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Miloj da islamanoj vestitaj per blankaj tunikoj dum festo fine de ramadano fluas pace kaj ĝoje tra ĉiuj ĉefaj urboj por komuna matena preĝo. En Burundo 90 % de la loĝantoj estas kristanoj kaj 10 % islamanoj, sed dum la festo oni pensus, ke la ĉefurbo iĝis islama.</p> <p>La celebrado de ĉi tiu festo iĝis tradicia en Burundo ekde la eniro de islamo pere de komercistoj el Zanzibaro, kiuj eniris Rumonge (trans la Tanganjika Lago) por komercaj celoj kaj islamigis tieajn loĝantojn, kiuj poste disvastigis sian religion en ĉiuj regionoj, precipe en la ĉefaj urboj.</p> <p>La festo fariĝis infana, ĉar por plenkreskuloj, post la matena preĝo, la festo finiĝas. Gepatroj (eĉ el malriĉaj familioj) devas aĉeti novajn vestaĵojn por siaj infanoj kaj multon elspezas por ili dum la festo, aĉetante tre bongustan manĝaĵon kaj donante al ili poŝmonon.</p> <p>Ĉiuj adeptoj de la islama religio renkontiĝas (frumatene) en elektita loko (en granda urbo) por komuna preĝo, en kiu virinoj, infanoj kaj viroj partoprenas. Post la preĝo okazas grandega festeno, kie ankaŭ kristanoj estas invitataj, kaj tio estas simbolo de paco kaj bona kunekzistado inter la du religioj. Posttagmeze, bonvestitaj kaj belaspektaj junuloj vidiĝas en ĉiuj stratoj kaj vagadas je ĉiuj distraj lokoj (marbordoj, kinejo ...). Estas bona festo, sed multekosta por la gepatroj, kiuj devas ŝpari monon por tio. Laŭ informoj de kelkaj gepatroj, en riĉaj familioj la elspezoj por unu infano povas atingi 150 usonajn dolarojn, kaj por malriĉaj kaj mezriĉaj familioj, la elspezo ĉirkaŭas 20-30 dolarojn. Komercistoj multon enspezas, vendante multekostajn vestaĵojn kaj manĝaĵojn dum la festo.</p> <p>La registaro ĵus ordonis, ke la festo estu konsiderata kiel feritago – ordono, kiun multaj personoj kritikis pro la fakto, ke en la nuna burunda kalendaro jam estas multaj ferioj pro mortigo de multaj gravaj personoj dum la milito; do necesas nuligi kelkajn festojn anstataŭ multobligi ilin, ĉar dum ĉi tiu festo nur 10 % de la loĝantoj festas, kaj la aliaj restas hejme, sen fari ion.</p> <div style="text-align:right">Jérémie S<span class="mm">ABIYUMVA</span></div> 
