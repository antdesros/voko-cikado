      <h3>Politiko</h3>
      <h3 class="tem">NOV-ZELANDO</h3>
      <h2>Tremas la tertremo-komisiono</h2>
      <!-- komenco de la enhavo -->

       

       <p>Post la tertremoj en Christchurch kaj la apuda regiono (2010-2013) domposedantoj postulis kompenson ĉe asekuraj kompanioj kaj ĉe la ŝtata t.n. Tertremo-komisiono (angle: <i>Earthquake Commission</i> EQC).</p> <p>Neniam EQC ricevis tiom da petoj. Eĉ dunginte novajn stabanojn, ĝi tamen nek konvene nek ĝustatempe pritraktis la viktimojn. Domposedantoj plendis, ke ili provis multfoje peti informojn, kiel progresas iliaj postuloj, sed EQC-stabanoj ne montras sin helpemaj kaj ne kunhavigas la informojn, kiujn ili posedas.</p> <p>En marto 2013 likis detaloj pri ĉirkaŭ 98 000 postuloj de 85 000 domposedantoj. La detalojn publikigis ĉe eksterlanda retejo iu kun la indiko EQC-veroj (angle: <i>EQC Truths</i>). Tiel oni ĉirkaŭiris kortuman ordonon, kiu baris la publikigon de la informoj.</p> <h3>Embarasis</h3> <p>La afero tre embarasis la tertremo-komisionon. Poste evidentiĝis, ke EQC-dungito publikigis la informojn, argumentante, ke domposedantoj rajtas scii, kio okazas. La EQC-ĉefo pardonpetis pro la malrapida servado de lia organizaĵo kaj ŝajnas nun, ke la komisiono pli bone laboras.</p> <p>Intertempe la registaro decidis pri grandiozaj novaj konstruaĵoj por Christchurch. Tieaj komercistoj, kiuj provis restarigi siajn firmaojn, subite malkovris, ke la registaro prenos iliajn terpecojn por plenumi siajn grandajn planojn.</p> <h3>Translokiĝis</h3> <p>Rezulte de la tertremoj la ministro pri edukado, Hekia Parata, anoncis en septembro 2012, ke fermiĝos aŭ kunfandiĝos 31 lernejoj en Christchurch. Multaj familioj translokiĝis post la tertremoj for de Christchurch kaj ne necesas tiom da lernejoj.</p> <p>Dudek du lernejoj diris, ke malpravas la statistiko, laŭ kiu oni faris la decidon. Sekvis protestoj kaj diskutoj. Fine, en februaro 2013, Parata anoncis, ke fermiĝos nur sep lernejoj, kaj kunfandiĝos 12.</p> <p>En julio kaj aŭgusto 2013 okazis novaj tertremoj en la centra parto de Nov-Zelando. Estiĝis novaj kompenso-petoj. Leviĝis la demando, ĉu la tertremo-komisiono povos ilin bonorde pritrakti.</p> <div style="text-align:right">D. E. R<span class="mm">OGERS</span></div> 
