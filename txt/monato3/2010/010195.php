      <h3>Politiko</h3>
      <h3 class="tem">SLOVENIO</h3>
      <h2>Demokratio en postdemokratia socio</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Slovenio okazis en oktobro lokaj balotoj por urbaj kaj komunumaj asembleoj kaj por urbestroj. La partopreno estis magra – iom sub 50 % de la rajtigitoj.</p> <p>En la du plej grandaj urboj Ljubljana kaj Maribor venkis la ĝisnunaj urbestroj Zoran Janković kaj Franc Kangler, ambaŭ subtenataj ne de partioj sed de propraj eksterpartiaj listoj. En Ljubljano la eksterpartia listo de Janković gajnis 47 % de la voĉoj kaj li povos facile regi praktike sola kun plimulto en la asembleo.</p> <p>Aliflanke en Maribor la dekstrisma listo de Kangler gajnis nur 10 el 45 asembleanoj kaj verŝajne ne sukcesos krei koalicion, ĉar la maldekstraj partioj havas plimulton. Do al Kangler pli malfacilos regi ol ĝis nun. Tio aparte gravas, ĉar Maribor estos en 2012 kultururbo de Eŭropo. Tiam oni unuece kaj senkverele eluzu la ŝancon por levi la urbon al pli alta nivelo.</p> <h3>Oligarĥioj</h3> <p>Argumenteblas, ke la balotoj pruvis la tezon de la brita sociologo Colin Crouch [kolen kraŭĉ], ke la nuna okcidenta politika sistemo estas ne plu demokratio, sed postdemokratio. Laŭ Crouch, ne plu venkas kvalitaj politikaj ideoj aŭ kontraŭulo de tiu, kiu malbone regis, sed tiu kun multe da mono, kiun subtenas financaj oligarĥioj.</p> <p>Tio montriĝis en Maribor. La ĝisnuna urbestro uzis sian potencon por personaj interesoj kaj tiuj de siaj proksimuloj. La urba buĝeto suferis pro grava deficito kaj la urbestro plurfoje agis kontraŭleĝe. Ekzemple, li prezentis la urban bilancon por la pasinta jaro nur en aŭtuno, anstataŭ laŭleĝe en printempo. Tial ne eblis scii, kia minuso estas en la kaso kaj kiel la mono estas uzita.</p> <h3>Propagando</h3> <p>Tamen li estas reelektita de pli ol 50 % de la balotantoj jam en la unua voĉdonado. Dum la kampanjo li uzis enorman sumon, prenitan verŝajne el urbaj fontoj por sin reklami. Krome li sendis personajn leterojn al balotontoj, proponante transporton al la balotejo kontraŭ voĉdono favora al li. Tion sciis mariboranoj, sed ili subtenis lin sub influo de forta kampanja propagando, pagita de ili mem.</p> <p>Demokratio tia ne estas justa maniero regi kaj korekti malbonajn procedurojn. Estu flanke notite, ke la Verda Partio de Maribor gajnis du lokojn en la urba asembleo: inter la elektitoj estas Melita Cimerman, prezidantino de la urba Esperanto-societo.</p> <div style="text-align:right">Zlatko T<span class="mm">IŠLJAR</span></div> 
