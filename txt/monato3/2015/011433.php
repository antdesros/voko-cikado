      <h3>Turismo</h3>
      <h3 class="tem">ŜIPVETURO</h3>
      <h2>Krozi laŭ la norvega marbordo</h2>
      <!-- komenco de la enhavo -->

       

      <p>La ŝipkompanio Hurtigruten ofertas krozadon laŭ la itinero Bergen – Kirkenes kiel „la plej belan en la mondo”. Multaj turistoj deziras sperti ĝin. La plimulto preferas printempan aŭ someran vojaĝon,
   dum kiu oni povas ĝui la neniam subirantan sunon. Tamen krozado eblas ankaŭ aŭtune
   kaj vintre, kaj tio estas avantaĝa kaj taŭga sezono por pensiuloj. Varmajn vestaĵojn
   en la valizon kaj ek! Surprizo: ne forgesu bankostumon, ĉar la varmaj bankuvoj (ĵakuzoj)
   subĉielaj sur la plej supra ferdeko certe plezurigos vin eĉ en nuba kaj aŭtuna vetero.
</p> 
<h3>Historio</h3> 
<p>
   <i>Hurtigruten</i> signifas „rapida itinero”. Ekde 1893 tiu kompanio servas al la norvegaj marbordanoj kaj insulanoj, transportante
   homojn, varojn kaj veturilojn. Komence la ŝipoj transportis ankaŭ poŝtaĵojn, kies
   surtera transporto ekzemple de Trondheim al Hammerfest en la 19a jarcento daŭris somere
   tri semajnojn kaj vintre kvin monatojn. Bonŝance la kapitano Richard With fidis siajn
   spertojn kaj maristaron por komenci la ŝipveturadon, kvankam aliaj dubis pro tio,
   ke ankoraŭ ne troviĝis sufiĉe da nordaj marmapoj kaj malmultis lumturoj sur la komplika
   marbordo. Al With komence sufiĉis kompaso kaj horloĝo por navigadi.
</p> 
<p>Nun la kompanio Hurtigruten uzas jam dek du ŝipegojn. El ili preskaŭ ĉiuj estas konstruitaj
   en Norvegio, escepte de du respektive en Germanio kaj Italio. Kiel ekzemplon ni prenu
   la ŝipon MS Midnatsol. Ĝi longas 135,75 m, larĝas 21 m kaj povas veturigi 1000 pasaĝerojn
   aldone al la kargo kaj 45 aŭtoj; krozada rapideco 15 nodoj. Ĝi veturas el Bergen al
   Kirkenes 6-7 tagojn, haltante en pli ol 30 havenoj.
</p> 
<h3>Vizito al vidindaĵoj</h3> 
<p>Kelkhavene turistoj elŝipiĝas por grupe aŭ individue trarigardi la proponatajn vidindaĵojn.
   Ĉarma estas la urbo Ålesund kun siaj multaj belaj domoj en la stilo Arto Nova. Interesa
   estas Trondheim, fondita en la jaro 997, kiu kun sia fama katedralo Nidaros estis
   la religia centro de Norvegio dum jarcentoj. En Tromsø, la plej granda urbo de norda
   Norvegio kun loĝantaro de 70 000, allogas la blanka katedralo de la Arkta oceano.
   Videblas ankaŭ pratempaj sanktejoj de sameoj. En granda vendejo de materialoj por
   manlaboro, la vizitanto komprenas, ke ĉi tie virinoj ŝatas krei ion per la manoj dum
   longaj hejmvesperoj en la mallumaj sezonoj. Dum monatoj la suno ne leviĝas. Estas
   tempo ne nur por spekti televidon, sed ankaŭ tempo por triki, teksi, kudri.
</p> 
<p>Kelkaj turistoj ne ekskursas, sed restas surŝipe, trankvile ĝuante la ŝanĝojn de la
   koloroj en la pejzaĝo, la montojn, nubojn, maron, deklivojn, vilaĝetojn kaj farmobienojn.
   De sur iu ponto gejunuloj flirtigas norvegan flagon aŭ kolorajn tukojn. Ankaŭ por
   ili la ŝipegoj gravas. Danke al la fidinda martrafiko ili ne sentas sin izolitaj.
   Per la ŝipoj venas novaj aĵoj, kutimoj kaj homoj; kelkaj venintoj ne foriros kaj restos.
</p> 
<h3>Sur la ŝipo</h3> 
<p>Multo interesa troveblas ankaŭ surŝipe: librobretaroj, komfortaj salonoj por legado,
   sporta trejniĝejo, saŭno, kafejoj, poŝtoficejo, vendejo, mapoj, sur kiuj oni vidas
   la ŝipon moviĝi kaj turniĝi en mallarĝaj markoloj kaj insulolabirintoj. Troveblas
   restoracioj, konferencejoj kaj kvietaj anguletoj kun komfortaj foteloj. Sur la ŝipaj
   vandoj videblas kreaĵoj de norvegaj artistoj, pri kiuj oni povas legi pli en broŝuro
   senpage disponigata. Eĉ ĉe rubujoj oni povas halti kaj informiĝi pri recikligo kaj
   la arkta flaŭro.
</p> 
<p>En la restoracio la kelneroj surtabligas abundajn kaj variajn bufedojn por matenmanĝo
   kaj lunĉo. Dum la vespermanĝo oni sidas ĉiam ĉe la sama tablo, kun la samaj kuntablanoj.
   La menuojn la kuiristoj faras, konsiderante lokajn tradiciojn kaj frandaĵojn. Pere
   de la menuo la manĝantoj lernas pri la marborda kuirarto. Fiŝaĵoj, salikokoj, mituloj,
   kraboj, ĉiaj maraĵoj, sed ankaŭ fruktoj, beroj, legomoj, radikaĵoj kaj viando, produktitaj
   en proksimaj marbordaj farmobienoj aŭ en paŝtejoj de boacoj super la polusa cirklo.
</p> 
<p>La kompanio Hurtigruten aranĝas ceremonion ĉe la polusa cirklo: post serioza ludado
   de naciaj himnoj aperas Reĝo Neptun por verŝi glaciokubetojn kaj akvon surkapen aŭ
   en la vestaĵojn de „baptiĝemuloj”, kiuj post tiu glacia duŝeto ricevas varmigan drinkaĵeton. Kelkaj aĉetas por si ĉampanon.
   La sekvan tagon ĉiuj ricevas atestilon pri la transiro de la polusa cirklo.
</p> 
<p>Jen tie ĉi, jen tie aŭdiĝas surŝipe anoncoj pri preterpasotaj vidindaĵoj, kiuj povas
   esti specialaj montoj, lumturoj, renkontotaj aliaj ŝipegoj, fiŝbredejoj kaj aliaj.
   Norvegoj produktas pli ol 700 000 tunojn da salmo, 70 000 tunojn da truto, malpli
   grandajn kvantojn da moruo, angilo, aliaj fiŝoj, konkoj. Oni bredas fiŝojn en flosantaj
   kaĝoj, kiujn oni povas movi de loko al loko por eviti fiŝmalsanojn. La reĝokrabo,
   importita el Siberio al la Arkta oceano pliiĝas, formanĝante ĉion sur la marfundo.
   Oni amare klopodas, ke ni defendu nin kontraŭ ĝi, manĝante ĝin en kiel eble plej grandaj
   kvantoj.
</p> 
<h3>Ekskursoj</h3> 
<p>Nordkapp, la plej norda loko, kiun oni povas atingi per aŭto en Eŭropo, estas unu
   el la celoj de la Hurtigruten-krozado. Ĝi verdire estas insulo, sed estas ligita al
   la kontinento per tuneloj kaj ponto. Tie la vizitantoj povas en ŝirmataj subteraj
   ejoj vidi belegajn filmojn pri la arktaj sezonoj kaj nordlumoj, konatiĝi kun la historio
   en muzeo. Konvene apudas restoracio, poŝtoficejo kaj memoraĵvendejo. Ekstere la vizitantoj
   rapide fotigas sin ĉe konata monumentglobo. Super la senarban rokplataĵon preskaŭ
   ĉiam blovas frosta vento, ŝaŭmiganta la maron kontraŭ la kruta deklivo 307 m alta.
   Surprizo: subtere troviĝas ankaŭ malgranda Taj-muzeo. Krome, arkeologoj trovis spurojn
   de homoj, loĝintaj antaŭ 9000 jaroj en tiu ĉi loko.
</p> 
<p>Kirkenes, kiu situas nur je 10 kilometroj de la norvega-rusa limo, estas la turniĝejo
   de la Hurtigruten-ŝipoj, revenontaj de ĉi tie suden al Bergen. Novaj krozantoj komencos
   siajn aventurojn, dum krozintoj aŭtobusos al la flughaveno, se ili ne deziras reveni
   kun la ŝipanaro. La vojaĝo ja estas la „plej bela en la mondo”.
</p> 
