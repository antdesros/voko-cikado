      <h3>Medio</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Ekspertoj pri la klimatŝanĝiĝo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Por la sekvaj jardekoj la prognozoj antaŭvidas sekecon en la sudo kaj inundojn en la nordo de la tero, ĉar la altaj averaĝaj temperaturoj kaŭzos degelon de glacioj ĉe la polusoj. Aŭstrio, kiu estas inter ambaŭ ekstremoj kaj kuŝas en meza Eŭropo, devas prepari sin por akcepti kreskantan nombron da klimatfuĝantoj el Afriko en la sekvaj dudek jaroj. „Ĉefe el la sudo de nia planedo plialtiĝos la enmigra premo al la vetere favoraj regionoj en la mondo”, diris Jacqueline McGlade [ĵaklin makglejd], administra direktorino de la Eŭropa Media Agentejo, dum podia diskutado de ekspertoj en Vieno.</p> <p>McGlade, kiu antaŭ nelonge revenis de vojaĝo al Arkto, estis maltrankviligita: „En la jaro 2025 glacio sur Barenca Maro estos malaperinta. Krom la novaj ŝipitineroj kaj fiŝteritorioj – la pozitivaj sekvoj – tio kaŭzos tamen ankaŭ tro altan nivelon de la Norda Maro.”</p> <h3>Pli da karbona dioksido en Azio</h3> <p>„La klimatŝanĝiĝo estos tiurilate la plej granda defio”, klarigis Sue Tierney [su tani], direktorino de la Vaŝingtona Analiza Grupo pri Tutmondaj Studoj. Laŭ ŝi, la tro rapida ekonomia evoluo ĉefe de Ĉinio kaj Barato estigos forte pliiĝantajn, klimatŝanĝajn ellasojn de karbona dioksido en la aeron: „Ĝis 2030 ĝi duobliĝos en Azio pro la kreskanta trafiko en tiu mondparto”, diris Tierney.</p> <p>La ministroj pri energio de Ĉinio kaj Barato „estas en grandaj zorgoj, kiel ili povus solvi tiujn problemojn”, raportis Hans Haider, la ĉefdirektoro de la Unuiĝo de Elektrosocietoj en Aŭstrio, pri la Monda Konferenco de Energio en 2004.</p> <p>Laŭ Tierney, la mondo postulas, ke la evoluon en Azio oni faru „laŭeble energiefika”. Tamen, laŭ ŝi, ankaŭ Usonon minacas 50-procenta pliiĝo de la eligo de karbona dioksido ĝis 2055, se ĝi plu uzos petrolon kaj karbon kiel energi-bazon.</p> <h3>Gvidrolo de Eŭropo</h3> <p>Tamen ja estas ankaŭ „signoj de espero”: unu triono de la usonaj ŝtatoj decidis akcepti klimatprotektajn programojn, akciuloj de firmaoj faris libervolajn interkonsentojn tiurilate. Per tio oni transprenis altan respondecon. „Eŭropo plu havas gvidrolon en la medipolitikaj aferoj mondskale”, aldonis McGlade.</p> <h3>Klimatprotekto duagrada</h3> <p>Konsidere al la labormerkata krizo tutmonde „oni tamen plu subtenu la konfidon je la bezono de klimatpolitiko”, emfazis McGlade.</p> <p>Laŭ la statistikulo Bjørn Lomborg kontraŭe, tiu neceso entute ne ekzistas. Laŭ li, la protekto de klimato laŭ la Kioto-protokolo (1997) estas multekosta recepto por plibonigo de la mondo kun neklaraj ŝancoj je sukceso.</p> <p>En la tiel nomata Kopenhago-interkonsento (2004) estis akceptita prioritatlisto de efikaj tutmondaj reformoj. Kiel unu el la plej grandaj problemoj de malriĉaj landoj montriĝis la poluado de aero. Batalante kontraŭ tiu granda danĝero por la homa sano, oni devus disponigi al la plej malriĉaj homoj monrimedojn, por ke ili aĉetu kerosenon por hejtado kaj kuirado.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>  
