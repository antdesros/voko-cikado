      <h3>Ekonomio</h3>
      <h3 class="tem">MADAGASKARO</h3>
      <h2>Rizo: senelira problemo</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Malagasoj kutimas sin nutri per tri rizpladoj ĉiutage. Tial je ĉiu kostaltiĝo de tiu varo la registaro timas popolan reagon.</b></p> <p>Kvankam 80 % de la loĝantaro konsistas el kamparanoj, kiuj mem estas rizproduktantoj, la rikolto tute ne sufiĉas por nutri 17 milionojn da homoj. Ĉiujare oni devas importi pli ol 200 000 tunojn da rizo. Kulpas la arkaika kultivmetodo, kies ĉefa grundplugilo estas ŝpato. Plie, la kultivado ekstreme dependas de la pluvabundeco, kiu ne ĉiam favoras la kamparanojn. Tial unu hektaro da rizkampo provizas al ili ne pli ol du tunojn. Aliaj problemoj, kiel malfacila transporto, manko de sekureco kampara kaj analfabetismo, pezegas sur la situacio ekonomia de la malagasa kamparo.</p> <p>Ekde novembro 2004 videblis senĉesa prezaltiĝo de la blanka rizo, kiu kulminis ĉe 8000 malagasaj frankoj (MGF, 1 eŭro = 12 000 MGF; averaĝa salajro estas 10 eŭroj monate) por kilogramo. Tio daŭris ĝis la nova rikolto en majo de la nuna jaro. La kialo estas, ke, unuflanke, rizkolektantoj spekulcele retenis siajn stokojn. Aliflanke, ne tre granda estis la produktaĵo pro inundoj, kaŭzitaj de du sinsekvaj ciklonoj. Malgraŭ averto de profesiuloj pri eventuala krizo, la aŭtoritatoj ne malhelpis ĝin. Ne troveblis rizo en la vendejoj, kaj ĉiuj aĉetis maniokon kaj maizon. Bedaŭrinde ankaŭ tiuj surogatoj malaperis post kelkaj tagoj.</p> <p>Plia malbonaĵo estis, ke, intertempe, la rizkurzo en la internacia merkato plialtiĝis, ĉar ne sufiĉis la propono, dum Ĉinio pligrandigis sian rizimporton. La malagasa registaro malfacile trovis ŝipon por transporti rizon al Madagaskaro. Malmultiĝis la provizado, kaj nur kelkaj urboj profitis el tiu malmultekosta rizo, vendata kontraŭ 3500 MGF por kvietigi, se ne la malsaton, almenaŭ la koleron de la popolo. Plimulto de tiu rizo venis el Barato. Ĝi estis la plej malbonkvalita, ĉar iuj konsumantoj, manĝinte ĝin, plendis pri jukado. Aliaj kvantoj venis el Pakistano kaj Ĉinio.</p> <p>Ne eblas al la „Granda Insulo” tuj solvi la menciitajn problemojn, kaj ripetiĝo de la sama situacio estas tute probabla. Nun kilogramo da loka rizo jam kostas 5000 MGF, tamen normala prezo dum la nuna rikolta periodo averaĝus 2500 ĝis 3500 MGF. La registaro ordonis al ĉiuj regionestroj regi la eliron de rizo el sia regiono, kio denove altigas la ŝancon de la koruptemaj spekulantoj.</p> <p>Kelkaj politikistoj, subtenantoj de la nuna registaro, instigas la popolon ŝanĝi la ĉefan manĝaĵon, propono ne tiom serioza, se konsideri la fakton, ke ĉi tie rizo estas ne nur nutraĵo, sed ankaŭ grava kulturelemento en la vivo de la malagasoj.</p> <div style="text-align:right">F<span class="mm">IDILALAO</span> Henriel</div> 
