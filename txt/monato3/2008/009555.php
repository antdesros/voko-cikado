      <h2>Gigantismo (3)</h2>
      <!-- komenco de la enhavo -->

       

      <p>En la porevolua propagando la interesatoj zorge prisilentas tiun timigan fakton, ke <i>ĉio</i>, kio aperis kiel grava progreso en industrio en la 20a jarcento kaj ankaŭ nun, apogas sin je tiu energio, nomita „fosilia energio”, kiu akumuliĝis en teraj tavoloj dum 600 milionoj da jaroj. Tiun kvanton da energio la homaro forkonsumas dum apenaŭ kelkaj generacioj. La tuta vivofilozofio de niaj elektitaj respondeculoj pri la homara estonto priskribeblas per la konata esprimo: <i>Carpe diem</i>. Se la homaro uzus nuklean energion por kovri sian tutan aktualan energibezonon, tiuokaze, laŭ la leĝo de grandaj nombroj, la nukleo-centralaj difektiĝoj estus tiom oftaj, ke tio grave danĝerigus la tutan biologian sistemon sur Tero.</p> <p>Tutmondiĝo kaj konsumsocieco eĉ pli pelas la homaron al senbrida malŝparado pri energio kaj bazmaterialoj, kaŭzas eĉ pli rapidan elĉerpiĝon de fosili-energiaj rezervoj.</p> <p>Pri la lastaj kvantoj de fosilia energio disponos nur potenculoj, ĉar ĝis tiam la procezo de sen-demokratiigo jam atingos tiun „sufiĉan” gradon, ke la plimulto de la loĝantaro jam ne plu havos eblon protesti, defendi siajn rajtojn. Ĉu vi miras pri tiu aserto? Nu, sufiĉas analizi la „miraklan efikon” de niaj amas-stultigiloj (televido, radio ktp) por ekkompreni tiun procezon. Malsinceraj reklamoj, propagandoj, kiujn kompilis <i>fakuloj</i> pri kiel influi plej efike subkonscie oniajn opiniojn, animojn, servas ĝuste por superruzi demokration.</p> <p>La „profesie” stultigitaj amasoj de homoj sekvas silente kaj blinde la ruze difinitajn „itinerojn” kaj submetas sin <i>kadre de demokratiaj reguloj</i> al la volo de siaj tiranoj.</p> <p>Tutmondiĝo signifas monopolon pri socio. Monopolo pri socio signifas tion, ke malaperas la necesa konkurado inter socioj kaj, kiel konsekvenco, loĝantoj ne plu havos la eblon elekti iun „pli bonan” ŝtaton, ĉar ili <i>ĉie</i> alfrontos la samajn kondiĉojn. Diversaj registaroj kuniĝas en socian „kartelon”.</p> <p>La pli vigle atentemaj civitanoj povis rimarki la malbonan efikon post la disfalo de tiu „alia” socio, markita per ĝia inta kerno Sovetio. Fakte en Eŭropo malaperis la konkurado inter la du plej signifaj socioj, kaj la vivnivelo por larĝaj amasoj de homoj perdis en kvalito ene de la tradiciaj kapitalistaj landoj. Neniam antaŭe spertiĝis tiom da malcerteco en vivsekureco.</p> <p>Pinte de tiu fenomeno staras tiu G8-„formacio”, al kiu baldaŭ aliĝos ankaŭ aliaj registaroj.</p> <div style="text-align:right">Laszlo Istvan T<span class="mm">OTH</span></div> <div style="text-align:right"><i>Hungario</i></div> 
