      <h3>Turismo</h3>
      <h3 class="tem">FINNLANDO</h3>
      <h2>Lerto admirenda</h2>
      <!-- komenco de la enhavo -->

       

       <p>La subĉiela metiista muzeo en Turku ekspozicias instrumentojn kaj prezentas la vivkondiĉojn de kvardeko da metiistoj el la 18a kaj 19a jarcentoj. Enkadre de ĝi sindona grupo de lertuloj strebas, ke oni daŭre posedu la sciojn kaj lerton de niaj antaŭuloj. Ili montras kiel oni faru permane gantojn, kuprajn kafopotojn, cigaredojn, ŝnurojn, presaĵojn, korbojn, ornamaĵojn el homaj haroj kaj aliajn objektojn, kiujn oni nun produktas en fabrikoj aŭ kiuj nun estas apenaŭ konataj. La muzeo estas unu el la urbaj fieraĵoj. La Provinca Muzeo de Turku jam eldonis pri ĝi kaj ties historio libreton kun koloraj bildoj. <a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2410217"><img src="http://u1.ipernity.com/6/02/17/2410217.53edc3b3.240.jpg" width="151" height="240" alt="Subĉiela metiista muzeo en Turku" border="0"/></a></p> 
<p>En la mezepoko la parcelo, kie nun metiista muzeo troviĝas, supozeble estis uzata kiel tombejo de la monaĥejo de Sankta Olavo. La loknomo <i>Luostarinmäki</i> signifas „monaĥeja altaĵeto”. Estas tamen mirakle, ke la lignaj dometoj de metiistoj 'transvivis' la teruran incendion, kiu forbruligis tri kvaronojn de la urbo Turku en 1827. Poste oni ne plu permesis, ke oni loĝu en ili pro danĝero de eventuala posta incendio. La dometoj tamen ne estis detruitaj. En la 20a jarcento ekestis la ideo estigi subĉielan muzeon de metiistoj. Ĝi estis realigita kaj oni inaŭguris ĝin en 1940. <a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2410216"><img src="http://u1.ipernity.com/6/02/16/2410216.fa193801.240.jpg" width="160" height="240" alt="Subĉiela metiista muzeo en Turku" border="0"/></a></p> 
<p>La metiista muzeo estas malfermita tutjare. En ĝi oni povas ekkoni la vivkondiĉojn, meblojn, lumigadon, hejtadon, kuiradon kaj laborilojn de la pasinteco. Multaj metiistoj en historie aŭtentikaj vestoj demonstras sian arton somere kaj dum specialaj tagoj aŭtune, kiam venas lertuloj ankaŭ el aliaj landoj por uzi la malnovajn instrumentojn, kiuj por la hodiaŭa homo enigmas. Kristnaske okazas ekspozicioj pri la tradicioj antaŭ 100-200 jaroj kaj kantado de malnovaj kristnaskaj kantoj. Al la muzeo oni aljuĝis la premion „Ora Pomo” pro ĝia kulturhistoria kaj turisma valoro.<a href="http://www.ipernity.com/doc/flandra_esperanto-ligo/2410215"><img src="http://u1.ipernity.com/6/02/15/2410215.e64d40b1.240.jpg" width="240" height="138" alt="Subĉiela metiista muzeo en Turku" border="0"/></a></p>    <div style="text-align:right">S<span class="mm">ALIKO</span></div> 
