      <h3>Ekonomio</h3>
      <h3 class="tem">ĈINIO</h3>
      <h2>Ekonomia kresko daŭros</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>En marto la Ŝtata Konsilantaro de Ĉinio en Pekino raportis al gazetara rondo pri sia laboro. Rilate al la estonta disvolviĝo de la ekonomio, ĝi prognozis, ke de 2006 ĝis 2010 la ĉina ekonomio plu rapide kreskos je ĉirkaŭ 8 % jare. Dum la jaroj 2010 ĝis 2020 la kresko iĝos malpli rapida kaj reduktiĝos ĝis sub 7 %.</b></p> <p>Laŭ la raporto kvar ĉefaj faktoroj subtenos la kreskon:<br/> 1) Ĉinio posedas kompare grandan kaj fortan bazon pri materialoj kaj teknologioj kaj fundamente venkis la kapitalmankon.<br/> 2) La ĉina industria strukturo jam trapasis grandan ŝanĝiĝon. En la internacia merkato ĝia konkurenca povo grandpaŝe plialtiĝas.<br/> 3) La enlanda merkato estas tre vasta kaj en ĝi estas kaŝita granda latenta forto por plu kreski.<br/> 4) La sistema reformiĝo de Ĉinio estas senĉese pliprofundiĝanta, kaj pro tio la medio por investado sendube plu pliboniĝos.</p> <h3>Investoj el eksterlando malgrandaj</h3> <p>La raporto ankaŭ menciis, ke, kvankam kompare kun 2003 en 2004 la investosumo el eksterlando iom plimultiĝis, ĝi tamen restis je malalta nivelo. En 2003 la rekta investo de Usono en Ĉinio estis nur 0,7 % de ĝia tuta investo en eksterlando. Dum la sama periodo 3,4 % de la usonaj eksterlandaj investoj iris al Meksiko kaj 1,2 % al Brazilo. El la investoj de la ĉefaj membroŝtatoj de Eŭropa Unio, kiel Britio, Germanio, Francio kaj Italio, nur 0,2 ĝis 1,5 % iris al Ĉinio.</p> <h3>Ĉina kresko utilas al ĉiuj</h3> <p>Ma Kai, ano de la Ŝtata Konsilantaro, deklaris ankaŭ, ke la disvolviĝo de la ĉina ekonomio kredeble vigligos la mondan ekonomion. Ekde la 1980aj jaroj la komerca importado de Ĉinio ĉiujare meznombre pliiĝis je pli ol 15 %. Ĉinio iĝis la tria plej grava importanto de la mondo.</p> <div style="text-align:right">M<span class="mm">U</span> Binghua</div>  
