      <h3>Eseoj</h3>
      <h3 class="tem">KULTURAJ INSTITUTOJ</h3>
      <h2>En rubujon, kulturo kaj lingvo de Italio</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Ke Italio ne estas la eŭropa korifeo de lingva kaj kultura ŝovinismo, estas fakto
      favora kaj sendube rekonata de la legantoj, kiuj almenaŭ unufoje vojaĝis tien. Ke
      la italoj tamen volas propravole, harakire, nuligi la aprezon de la italaj lingvo
      kaj kulturo en la mondo, estas emo probable senprecedenta en la malnova kontinento
      kaj – pro la malzorgema fatalismo, kiu ĝin akompanas – miaopinie maltrankviliga. Kial en Italio laŭregule la (ĉefe humanisma) kulturo pagu
      la koston de la registaraj eraroj kaj de la ekonomia krizo?
   </b>
</p> 
<p>Inteligenteco, ekvilibro, klarvideco: tiaj virtoj pli kaj pli malabundas tra la politika
   klaso de Italio, kie regas vera konfuzo. Iuj ekzemploj? La malrapida nekomprenebla
   usonangligo de universitatoj je la nomo de (supozata) internacieco, kun pluraj kursoj
   devige jam haveblaj nur en la angla lingvo, ankaŭ en iuj el la plej gravaj politeknikoj
   (Milano, Torino), kaj paralele la elfenestrigo, eĉ pli bedaŭrinda (nelaste pro la
   rapideco kaj manko de delikateco), de la humanismaj fakoj, tiuj samaj, sur kiuj baziĝis
   dum jarcentoj la italaj civilizo kaj vivmaniero. La krizo, se ĝi vere atingis la landon
   kaj ne estas nura onidiro (se pensi pri la nombro da venditaj iFonoj, oni prie dubetas),
   plirapidigis ian surfacan, ŝajnan, moderniĝon de la lando: oni ne plu povas permesi
   al si malŝparojn, laŭaserte, do oni rezignu pri ĉio, kio ne estas necesa al la kresko
   de la lando. Ekzemplo de malŝparo estas la disvastigo en kaj ekster Italio de la itala
   lingvo kaj, kompreneble, de la kulturo, kiun ajna lingvo inkluzive la italan nepre
   vehiklas.
</p> 
<h3>Elfenestrigo</h3> 
<p>Aliflanke kio estas kulturo? Nenio alia ol abstrakta koncepto, afero malproksima,
   nevidebla kaj verŝajne tial ne ekzistanta, ĉar en 2014 multaj kredas, ke ne ekzistas
   tio, kion ne eblas tuŝi, aŭdi, vidi, pesi, kvantigi, radiografi. Entute temas do pri
   porinfana fabelo, pli-malpli kiel religio, same rapide kaj maldelikate elfenestrigata
   de la iama popolo de (nun debaptiĝantaj, laŭ lastatempa modo) papistoj; kulturo estas
   brizo malforta kaj iom ĝena, pri kies ĉeso neniu ekkonscios aŭ plendos.
</p> 
<p>Impresas (kaj neniu povus imagi tion ĝis lastatempe), ke la ĝenerala laikigo de la
   lando koincidas kun samtempa sekularigo, kun paralela flankenmetado de la humanisma
   kulturo mem, kun plenkonscia rezigno de tio, kio pasintece estis la gloro de ia Italio
   de ĉiu ajn enviata: arto, beletro, pensado, beleco; alivorte de tio, kio igis homojn
   kiel Shakespeare, Goethe aŭ la anglan verkistinon Elizabeth Barrett Browning admiri,
   prirevi, alstrebi kaj fine sperti la t.n. koncepton de italeco (sinonimo de humanismo,
   klasikismo, remalkovro kaj daŭra renaskiĝo-renesanco de la antikveco).
</p> 
<p>Tiu evoluo konstateblas de jaro al jaro, de registaro al registaro (tiu malkutima
   esprimo ja taŭgas por priskribi la italan realon, en kiu ĉiun jaron sinsekvas almenaŭ
   du-tri registaroj, laŭ la kapricoj aŭ la personaj ambicioj de iuj partiestroj): eble
   estus pli ĝuste paroli pri ies „manovro” ol pri natura disvolviĝo, ĉar la tuja elfenestrigo de la humanisma kulturo ne povas
   esti hazarda projekto: oni volas krei novan estontecon forstrekante la pasintecon,
   kvazaŭ eblus konstrui la duan, la trian aŭ la lastan etaĝon de palaco sen antaŭe meti
   fundamenton aŭ almenaŭ la unuan etaĝon; kvazaŭ oni devus nepre elekti en sia vivo
   ununuran verbon inter „esti” kaj „havi”, kvazaŭ ekzistus leĝo, pro kiu ili ne povas kunekzisti en nia vorttrezoro je ekvilibraj
   procentoj.
</p> 
<h3>Tunelo</h3> 
<p>Nu, la situacio estas vere kompatinda: oni (nome la ĵus demisiinta aŭ eble demisiigita
   Enrico Letta) deklaras konvinkita en Ĉinio, ke la fino de la ekonomia krizo jam alvenis,
   ke la itala ŝtato estas preta por nova starto, ke finfine videblas lumo fine de la
   tunelo, sed la teorio estas tuj dementata de kromaj maldungoj, de novaj hakoj. La
   lumo je la fino de la tunelo estas nur, laŭ timo de multaj italoj, alproksimiĝanta
   vagonaro, kiu ĉesigos la agonion. La viktimoj? Refoje universitatoj (ĉefe la beletraj
   fakultatoj), lernejoj (kies plafonoj post ĉiu pluveto falegas), esploraj institutoj
   (ĉefe koncerne laŭdire senutilajn fakojn, kiaj lingvoscienco, arkeologio aŭ filologio),
   teatro: ĝenerale kio ajn rilatas kun la lingvo aŭ estas efektivigata pere de la lingvo,
   kio ajn proksimas al la humanisma kulturo aŭ al la disvastigo de tiu eksmodiĝinta
   kulturo, de kiu Italio estis iam la lulilo.
</p> 
<h3>Multlingveco</h3> 
<p>Apud la menciitaj, nova viktimo de la neceso limigi la elspezojn, raciigante la (ĝis
   nun jam ridindajn) financadojn, estas nun la Italaj Kulturaj Institutoj (<i>Istituti Italiani di Cultura</i>). Temas, por tiuj, kiuj ne scias tion, pri veraj ambasadejoj de la italaj lingvo
   kaj civilizo en la mondo, pri reto da instruaj kaj informaj centroj ĉeestaj en la
   plej grandaj urboj de la mondo, tutsimilaj al kio estas por Anglio 
   <i>British Council</i>, por Francio 
   <i>Institut Français</i>, por Hispanio 
   <i>Instituto Cervantes</i>: do pri institucioj, kiujn alilande neniu tuŝus, neniu fermus, ĉar ilia laŭdenda
   agado indas nur respekton, se ne veneradon.
</p> 
<p>En epoko, en kiu multaj ŝtatestroj faras signifoplenajn deklarojn pri multlingveco
   kaj multkultureco, pri la neceso alproksimigi la diversajn popolojn kaj laŭbezone
   refirmigi iliajn rilatojn kreante – ankaŭ aŭ precipe per la instruado de la propraj lingvo kaj historio, de la arto kaj
   de la literaturo – pontojn de amikeco kaj solidareco, nu en tiu ĉi epoko ŝajnas tiom pli anakronisma
   (kaj malsagaca) la ago de la eksa itala premiero Enrico Letta preskribi la fermiĝon
   de dek (el entute naŭdeko da) Italaj Kulturaj Institutoj en la mondo, de Islando al
   Danio, de Germanio al Francio kaj al Grekio. La plej rimarkinda kaj paradoksa okazo,
   al mi bone konata, ĉar mi instruis la italan tie de 2002 al 2004, estas eble ja tiu
   de Tesaloniko, la dua urbo de Grekio, simila lando (ĉu hazarde?) tre aŭ tro frapita
   de la ekonomia krizo.
</p> 
<h3>Proksimeco</h3> 
<p>Grekio kaj Romo? La radikoj de eŭropa civilizo? Plurjarmilaj rilatoj? Mediteranea
   komuna kulturo? Bagateloj forviŝeblaj per mallonga dekreto. En malriĉa lando, spite
   al ĝia pozicio kaj historio, ne rajtas kunekzisti du Italaj Kulturaj Institutoj. Estas
   malŝparo. La lernemuloj, se tute ne estas alternativoj, iru al Ateno. Fakte transvivos
   por la momento nur la instituto de la ĉefurbo, kvankam ĝia rolo estis ĉiam duaranga
   kompare kun tiu de Tesaloniko, ankoraŭ ege aktiva kaj frekventata interalie de dekoj
   da junuloj, kiuj pro gravaj kialoj interesiĝas pri itallingvaj ekzamenoj (kapablaj
   malfermi al ili la pordojn de la italaj universitatoj).
</p> 
<p>Ja logika decido: estas cetere normale postuli, ke kiu en la norda Grekio interesiĝas
   pri la itala lingvo, pri la eblo plenumi universitatajn studojn aŭ trovi laboron en
   Italio, trairu du-tri fojojn semajne 500 kilometrojn por atingi la Instituton de Ateno
   kaj poste reveni hejmen! Absolute neniu el la regantoj konsideris aŭ konsiderigis
   al la kolegoj, ke la rilatoj inter Grekio kaj Italio (pro geografia, kultura, historia,
   ĉiuvidpunkta proksimeco) eble indus escepton; ke tiuj rilatoj estas tiel intensaj
   kiel eble en neniu alia lando de la mondo; ke en Grekio estas tre malfacile eniri
   la ŝtatajn universitatojn, do oni bezonas alternativojn, kiuj aliflanke alportos al
   ni studentojn; ke grekaj studentoj en Italio estas rimedo, kaj kultura kaj ekonomia,
   gravega; ke ne estas hazarda la slogano kreita de la grekoj (<i>una faca, una rasa</i>: unu vizaĝo, unu raso) por substreki la similecon inter la du popoloj, tiu de Grekio
   kaj tiu de la tiom ŝatata kaj revata Italio.
</p> 
<h3>Sentokapablo</h3> 
<p>Sed kiel atendi tian sentokapablon el lando, kiu jam forigis kaj plue forigas la instruadon
   de geografio, art-historio kaj filozofio, en la nomo de la tri i-oj de la oka saĝulo
   Silvio Berlusconi (<i>inglese, informatica, impresa</i>: angla, komputiko, entrepreno)? Iu decidis, ke kulturo rimiĝu kun medicino, scienco,
   teknologio, ekonomio, enmerkatigo. Ne gravas scii la subjunktivon de verboj, la ĉefurbojn
   de fremdaj landoj (pri riveroj, montoj kaj lagoj oni perdis la esperon), la signifon
   de la platona mondo de la ideoj aŭ de lia mito pri la kaverno.
</p> 
<h3>Balaaĵo</h3> 
<p>La urbestro mem de Tesaloniko deklaris, ke fermi la Italan Kulturan Instituton en
   la koro de la urbo estos tre dolora vundo por ĉiu. Sed je la nomo de stabileco (?)
   estis decidite, ke tiom da profesiuloj kaj familipatroj kun multe da laboro (ĉar la
   instituto estas plena de lernantoj) restu malstabile sen laboro. Des pli paradokse:
   la ŝtata financado al la Itala Kultura Instituto de Tesaloniko reprezentis nur 4 %
   de ĝia tuta buĝeto, kaj pro tio oni petis povi daŭrigi la agadon tute rezignante pri
   ŝtataj financaj subtenoj, sed la respondo de la itala inteligencio estis nea. Ŝajne
   la itala lingvo, la itala kulturo, eksterlande devas esti elfenestrigitaj, kio okazis
   pasintece pri tiranoj, diktatoroj, ŝtopaj balaaĵoj. Kiel akcepti, ke balaaĵon nuntempe
   oni ial (kial?) kreu el tute funkcianta kaj taŭga institucio? Eĉ akceptante, ke povas
   ekzisti kelkaj (solveblaj) problemoj, ĉar neniu estas perfekta, oni pensu, ke aŭtomobilo
   sen radoj estas ja pli stabila ol aŭtomobilo kun etaj problemoj, sed la unua ne plu
   estas transportilo, dum la dua plenumas iumaniere sian rolon kaj povas per malgranda
   investo esti riparita.
</p> 
<h3>Herezeco</h3> 
<p>La instruado de lingvoj kaj kulturoj en la mondo estas aŭ devus esti armilo kontraŭ
   nesciado, kontraŭ nekompreno kaj antaŭjuĝoj, kiuj fojfoje riskas kaŭzi verajn konfliktojn,
   kiaj okazis pasintece en Eŭropo, kiam nehazarde mankis tiaj kulturaj centroj. Mi donu
   ekzemplon: en la tago de sankta Valenteno (la 14a de februaro), instruisto pri religio
   en grek-kipra ŝtata liceo skribis sur la nigra tabulo, ke tiutage la (ortodoksa) eklezio
   memoras pri sankta Afksentjo kaj sankta Marono, ne pri iu stulta kaj falsa sankta
   Valenteno: granda ekzemplo de interkultura alproksimiĝo, de la respekto por la aliulaj
   kredoj, kiu devus karakterizi nian epokon ...
</p> 
<p>Do urĝe bezonatas ankoraŭ en nia epoko tiuj, kiuj, instruante la lingvon kaj la kulturon
   de alia lando, propetu, klarigu, ekspliku, mediaciu inter mondoj nun proksimaj kaj
   amikaj, sed sen tiu mediacio eble pretaj ree disiĝi kaj malamikiĝi samkiel pasintece
   (Italio kaj Grekio faris militon antaŭ malpli ol jarcento). Krom se oni volas, ke
   baldaŭ Italio estu reasociita ne kun la artistoj, la verkistoj, la muzikistoj, la
   pentristoj, kiujn oni konigis en la lastaj jaroj al la loĝantoj de Tesaloniko, sed
   kun ĝiaj plej kliŝaj simboloj: pico, mafio, mandolino (la duan kaj la trian mi neniam
   vidis en Italio) kaj verŝajne nun ankaŭ herezeco, spite al la ekumenisma elano de
   la lastaj jardekoj.
</p> 
<h3>Platiĝo</h3> 
<p>Lingvo – skribas ĉi-rilate la poetino Bruna Milani en la ĵurnalo 
   <i>Libertà</i> – estas sinonimo de kulturo; ĝi utilas por ordigi la animon kaj la menson de la mondo
   kontraŭ la tutmondiĝa platiĝo, kiu igus malinteresa la renkontiĝon mem kun fremduloj.
   Ni ĉiutage jam konstatas, ke la teknologio kaj la ekonomio per si mem ne sufiĉas kaj
   igas neeltenebla ne nur la disvolviĝan modelon, sed ankaŭ tiun, kiu rigardas la vivon.
   Kaj se oni ja volas konsideri la gravecon de merkatoj, de financo, de tiaj nombreblaj
   tiom modaj aferoj, kiumaniere esperi je turismo nova, finfine konscia, vera, utila,
   kiu alportos ne nur monon, sed ankaŭ konkretan fruktodonan renkontiĝon inter personoj?
   Tio tute ne eblas, se oni malpotencigas aŭ neniigas la rolon de la kulturaj centroj,
   kiuj instruas la lingvon en la mondo. Ili utilas, laŭ Bruna Milani, por sciigi al
   la aliuloj, kiuj kaj kiaj ni estas; ili igas la homojn interesiĝi kaj enamiĝi pri
   nia lando, kreante la eventualan deziron ĝin viziti, ĝin atingi, ĝin vidi propraokule
   por profundigi la scion, por eble ekvivi apud ni, regalante nin per sia profesieco
   kaj tiel alportante materialan kaj spiritan riĉecon.
</p> 
<h3>Merkatado</h3> 
<p>Je la lumo de ĉio ĉi, oni lasu trankvila la kulturon tie, kie ĝi ankoraŭ funkcias;
   tie, kie ĝi estas ankoraŭ ne elfenestrigita (ĉar Italio daŭrigas iumaniere, danke
   al sia glora paseo, la eksporton de favora bildo pri si, spite al la nuntempa neniumado
   kreita de malbonaj kverelemaj politikistoj). Iuloke oni ne laciĝis rigardi nian lingvon,
   la italan lingvon iam uzitan de Dante, Boccaccio kaj Petrarca, kiel ilon por realigi
   la proprajn vivorevojn, por atingi iom de tiu ŝatata italeco. Oni parolas tiom ofte
   pri merkatado, sed kio produktas pli da merkatado ol iu favora bildo pri ni mem, pri
   lando kiu zorgas, fieras kaj pretas investi en sia lingvo, en sia kulturo, en sia
   arto, en siaj estinteco kaj estanteco (propedeŭtikaj al plej stabila estonteco), konservante
   kaj antaŭenigante sian animon?
</p> 
<p>La stulta decido de la itala registaro estas manfrapo al la landa historio kaj al
   tiom da homoj, ĉefe instruistoj, kiuj pacience laboris dum multaj jaroj en la kultura
   fako, konstruante de jarcento al jarcento, de jardeko al jardeko, en plej malfacilaj
   kondiĉoj, lingvajn kaj kulturajn pontojn, ĉar ili kredis, ke ĉio ĉi havas aŭ iutage
   havos iun sencon.
</p> 
<h3>Araneaĵoj</h3> 
<p>Tiujn belajn pontojn – kvazaŭ senutilajn araneaĵojn – hodiaŭ misedukita servistino (kun mia tuta respekto por tia laboro), vokita mallongege
   purigi iun longege neglektitan domon, abrupte elsuĉas kaj ĵetas senglore en la rubujon;
   krom se venos lastamomente iluminiĝo, repripenso, kiuj ebligos savi la ekziston de
   lokoj por lingvo kaj kulturo, kiuj por multe da homoj (kiel instruistoj eksterlande)
   plu havas grandan valoron: ne por stulte montri la propran superecon al la fremduloj
   (ĉar neniu kulturo estas supera al la ceteraj), sed por kontraŭbatali pere de vero,
   pere de humilo, pere de soci-kultura mediacio, la malamikecon, la malamon kaj la militemon,
   kiujn nekompreno malfeliĉe kaŭzas.
</p> 
