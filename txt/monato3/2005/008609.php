      <h3>Turismo</h3>
      <h3 class="tem">FINNLANDO</h3>
      <h2>Raŭmo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Raŭmo kaj raŭmismo estas konataj al esperantistoj, sed la vera famo de la ĉemara urbo Raŭmo baziĝas ĉefe sur la arkitekturo de la mezepoka urbocentro. Ĝi okupas 28 hektarojn kaj enhavas ĉirkaŭ 600 lignodometojn en malnova nordia stilo. Ĝis la komenco de la 19a jarcento la urbocentron ĉirkaŭis doganmuro.</p> <p>La stratoj estas mallarĝaj kaj nerektaj. La grandeco kaj formo de la privataj parceloj kun domoj varias, kaj multaj el ili plu havas apude puton, kelon kaj tenejon, kiu antaŭe servis kiel bovinejo.</p> <p>En unu el la kvartaloj antaŭe loĝis ĉefe maristoj, ĉar Raŭmo estas unu el la havenoj ĉe la Botnia Golfo de Balta Maro. Sur la fenestrobreto de la marista domo povas stari paro da blublankaj porcelanhundetoj, atendantaj la revenon de maristo. Per speguleto, fiksita al la muro, la mastrino observis, kio okazas sur la strato, sen esti videbla de ekstere. Altaj pordego kaj ligna barilo ŝirmas la korton, kie kreskas siringoj, pelargonioj kaj aliaj ornamplantoj inter roketoj. Komforta benko aŭ seĝoj kaj tablo sub arbo invitas trinki kafon kaj ripozi dum posttagmeza varmo somera.</p> <p>Unu el la preĝejoj en Raŭmo estas la preĝejo de la Sankta Kruco, konstruita de franciskanoj en la 15a jarcento. Nun ĝi ne plu estas katolika, malgraŭ tio statuo de franciskano plu staras en la parko. Pli malnova en la urbo estas preĝejo de la 14a jarcento dediĉe al la Sankta Triunuo. Ĝi ruiniĝis sekve de incendio kaj rezulte de tio nur kelkaj ŝtonoj restas.</p> <p>La lasta granda incendio en Raŭmo okazis en 1682, neniigante la plej malnovajn domojn. Tial la plimulto de la nunaj lignodomoj diverskoloraj estas konstruitaj en la novrenesanca stilo. Kvankam ili povas enhavi ene modernan komforton, la eksteron oni ne rajtas ŝanĝi. Kiam oni riparas kaj rekonstruas, oni ĉiam uzas tradiciajn farbojn kaj materialojn. Se oni devas malkonstrui tro kadukan lignodomon, oni konservas ĉiujn partojn kaj aĵojn por posta uzo en riparotaj konstruaĵoj.</p> <p>En 1991 Raŭmo estis enlistigita kiel monda kulturheredaĵo de Unesko kaj kiel modelo de malnova nordia lignokonstruado. Ĉiusomere raŭmanoj organizas puntosemajnon omaĝe al la elstara tradicio de puntofarado. En la pasinteco eĉ knaboj devis lerni tiun arton!</p> <p>Nun la tuta urbo havas 37 000 loĝantojn. Ekonomie en Raŭmo gravas industrio: produktado de papero, teksaĵoj, ŝipoj, konstrumaterialoj, elfaraĵoj el ligno, metalo kaj ledo.</p> <p>La dialekto de Raŭmo preskaŭ nekompreneblas al finnoj el aliaj regionoj. Ĝi famas pro anekdotoj, kanzonoj, samkiel pro noveloj kaj romanoj de loka verkisto Hjalmar Nortamo. La retpaĝo de la turisma oficejo de Raŭmo legeblas ankaŭ en la Esperanto: <a href="http://www.rauma.fi/esperanto">www.rauma.fi/esperanto</a>.</p>     <div style="text-align:right">S<span class="mm">ALIKO</span></div> 
