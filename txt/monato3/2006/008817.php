      <h3>Ekonomio</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Laborforto mankas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Litovio alfrontas malavantaĝan situacion. Dum 15 jaroj de ŝtata sendependeco la landon forlasis ĉirkaŭ 300 000 litovoj dum revenis nur 72 000. Post aniĝo en Eŭropa Unio la migradprocezo pligrandiĝis: en la jaro 2003 elmigris 11 000 kaj en 2004 15 200 personoj. La plej allogaj landoj estas Britio, Irlando kaj Skandinavio.</b></p> <p>Litovaj gazetoj kaj anonctabuloj plenas je diversaj laborofertoj. En la ĉefurbo mankas diversprofesiaj laboristoj: masonistoj, ŝoforoj, vendistoj ... Eĉ kompanioj, kiuj ne postulas altkvalitan laboristaron, ne havas elektoeblecon. Ekzemple la ĉefurba komunuma mastrumado plendas pro manko de kortistoj. Kvankam oficiale en Vilniuso estas 9000 senlaboruloj, en la labora borso la postulado de laborantoj centoble superas la realan proponadon.</p> <p>Spertuloj asertas, ke tia situacio baldaŭ negative influos la agadon de kompanioj kaj la evoluon de la ekonomio. Reala danĝero minacas transporton. Ĉefurbaj buskompanioj apenaŭ povas trovi po unu ŝoforon por ĉiu urba trafikitinero. Pliaj necesaj busoj jam kaŭzus grandegan problemon. Pluraj ŝoforoj konsentas labori ekster siaj oficialaj laborhoroj kontraŭ pli alta salajro.</p> <p>Aliaj kompanioj eĉ akceptas personojn sen sufiĉa kvalifiko por busa stirado kaj intencas mem instrui ilin. „Ni pagos la instruadon kaj ni strebos, ke la dungito laboru ĉe ni dum minimume du jaroj. Se li ekdezirus ŝanĝi laborejon, li devus redoni al la entrepreno tute aŭ almenaŭ parte la investitan monsumon”, diris Vilius Laužikas, la estro de la kompanio <i>Busturas</i> el la urbo Šiauliai. Tiu ĉi kompanio dum la dua duono de 2004 perdis 35 ŝoforojn, kiuj forlasis siajn postenojn por serĉi pli bone pagatajn lokojn. Pluraj forvojaĝis al Londono. Ŝarĝtransportaj kompanioj ne pli bone fartas. Al ili precipe mankas ŝoforoj por internaciaj itineroj.</p> <p>Direktoroj de diversaj litovaj entreprenoj riproĉas la registaron, ke ne venas decido pri libera laborforta enveno el aliaj landoj kaj ke ĝi eĉ ne havas ajnan strategion tiurilate. Iuj kompanioj jam konstatis, ke ili ne povas plivastigi sian agadon pro manko de laborforto. „En marto al ni mankos 500-600 personoj. Ĉi-momente ni bezonas 250 dungitojn, kiujn ni vane serĉas. Se la situacio daŭros, parton de produktado ni devos transloĝigi en aliajn landojn”, diris estro de la japana kompanio <i>Yazaki</i>, kiu jam dum dek jaroj funkcias en Litovio.</p> <p>La litova laborborso informis, ke dum la unua duono de la jaro 2005 permeson labori en Litovio ricevis 881 eksterlandanoj, samgranda kvanto, kiel dum la tuta antaŭa jaro (877). La plej granda kvanto da eksterlandanoj trovis laboron en Vilniuso.</p> <p>En 2004 en Litovio laboris 61,2 % de ĉiuj 15-64-jaraĝaj personoj, t.e. iom malpli ol la averaĝo en Eŭropa Unio (63,3 %).</p> <div style="text-align:right"><span class="mm">LAST</span></div> 
