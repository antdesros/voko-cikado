      <h2>Decida batalo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ĉi-jare okazis la 300a datreveno de la batalo de Almansa, kie la dinastioj de la Burbonoj kaj la Habsburgoj luktis por akiri la hispanajn reĝlandojn.</p> <p>La habsburga Karolo la 2a mortis senfila en 1700. La tiamaj eŭropaj potencoj disputis, kiu el ili akiros la hispanan monarkion, kiu posedos teritoriojn i.a. en Nederlando kaj Italio. Sekvis longa milito, kiun historiistoj taksas la unua eŭropa milito de la moderna epoko.</p> <p>En 1707 trupoj de la burbona kandidato por la hispana trono Filipo la 5a de Kastilio venkis ĉe Almansa kaj eniris la reĝlandon Valencio. Fine, en 1713, per la traktato de Utreĥto, la venkintoj ĉion inter si dividis. Filipo la 5a fariĝis la reĝo de Hispanio kaj de la hispanaj posedaĵoj en Ameriko. Aliaj eŭropaj teritorioj restis tamen por la habsburga kandidato.</p> <p>Anglio retenis Ĝibraltaron kaj Minorkon kaj ĉefe monopolon pri amerika sklavo-komercado. Pleje perdis tamen la reĝlandoj Aragono, Valencio, Majorko kaj la princlando Katalunio, kies leĝaroj estis nuligitaj kaj kiuj ekde tiam ĝis la nuno estas regataj de fremdaj leĝaroj. Do daŭre senteblas la postefikoj de batalo okazinta antaŭ 300 jaroj.</p> <div style="text-align:right">Xavier M<span class="mm">ARGAIS</span> <span class="mm">I</span> B<span class="mm">ASI</span></div>   <p>La 11an de septembro, en la nacia tago de Katalunio, oni memoras la malvenkon de Barcelono post kelkmonata sieĝo en la jaro 1714. Post la traktato de Utreĥto la princlando Katalunio decidis rezisti la burbonajn trupojn, sed vane. Do la kataluna popolo celebras sian nacian tagon por ne forgesi la perdon de sia suvereneco, la nuligon de siaj institucioj kaj civitanaj liberoj kaj la persekutadon de sia kataluna lingvo.</p> <p>Dum la nacia tago ĉefaj societoj kaj politikaj partioj alportas florojn al la monumento de Rafael de Casanova (1660-1743), la tiama ĉefministro de Barcelono; aliaj same ornamas la monumenton de Josep Moragues i Mas (1669-1715), tiama generalo defendinta Katalunion kaj poste ekzekutita.</p> <p>Ankaŭ organizaĵoj favoraj al re-sendependeco de Katalunio aranĝas omaĝojn kaj prelegojn ĉe la amastombejo de la defendintoj de la urbo. Okazas ankaŭ manifestacioj kaj koncertoj.</p> <div style="text-align:right">Ferriol M<span class="mm">ACIP</span> <span class="mm">I</span> B<span class="mm">ONET</span></div> 
