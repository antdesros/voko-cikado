      <h3>Turismo</h3>
      <h3 class="tem">FINNLANDO</h3>
      <h2>El fervoja pasinteco finnlanda</h2>
      <!-- komenco de la enhavo -->

       

       <p>La finna fervojmuzeo troviĝas en urbo Hyvinkää, je 50 km norde de Helsinko. La muzeo estas la trie plej malnova en la mondo. Ĝi situas en parko sur tereno de 3 hektaroj. Ĝiaj du modernaj ekspoziciaj haloj okupas areon de 5000 m<sup>2</sup>. La muzeo uzas malnovan fervojstacion, kies reloj kaj konstruaĵoj datiĝas de la 1870aj jaroj.</p> <p>En la muzeo la vizitantoj konatiĝas ne nur kun imponaj lokomotivoj, diverstipaj vagonoj, drezinoj kaj surrelaj aŭtoj de inspektoroj, sed ankaŭ kun uniformaro, ilaro kaj loĝejoj de fervojistoj, kun konstruado de la fervojreto, riparado de veturiloj kaj kun arkitekturo de la malnovaj stacidomoj, ĉefe lignaj. Kelkajn el la lokomotivoj kaj vagonoj oni povas plu uzi por nostalgiaj vojaĝoj. </p> <p>Ĉiujare en la muzeo okazas tago de la fervojoj kaj ekspozicio de miniaturaj trajnoj. Tiuokaze trajneto sur etŝpura trako veturigas infanojn kaj plenkreskulojn tra la parko. Krome tie okazas ankaŭ alternaj ekspozicioj. La ĉi-jara ekspozicio havas kiel temon „Vojaĝmanĝoj” kaj estas organizita kunlabore kun restoracia faklernejo.</p> <h3>Perlo de la muzeo</h3> <p>Finnlando estis parto de la Rusa Imperio ĝis 1917. La caroj posedis centon da privataj vagonoj por vojaĝi. Dum la oktobra revolucio tamen la furioziĝinta popolo detruis ilin. Restis nur ses vagonoj de la tiel nomata Rusa Imperiestra Trajno, kiuj konserviĝis en Finnlando. Per tiu trajno en Finnlando vojaĝis caroj Aleksandro la dua en 1876, Aleksandro la tria en 1885, 1888 kaj vidviniĝinta carino Maria Fjodorovna en 1914, fuĝanta dum la unua mondmilito tra Finnlando al Sankt-Peterburgo. Tri el la vagonoj – cara, carina kaj salona vagonoj – oni ekspoziciis en la muzeo kaj detale prezentis en la reto (<a href="http://www.rautatie.org/Keisari">www.rautatie.org/Keisari</a>, angle, nederlande kaj finne). La ceteraj tri vagonoj estis adaptitaj por alia uzo.</p> <p>La vizitantoj ne rajtas eniri la vagonojn de la Rusa Imperiestra Trajno, sed nur rigardi ilian internon tra la fenestroj. Elektra lumo anstataŭas la originalan gasan kaj kandelan lumigon. La eksteraj lignaj vagonmuroj estas bluaj, sur ili videblas la rusa blazono de dukapa aglo. La internon de la carina vagono karakterizas blua silko kaj lito pendanta de la plafono por malpliigi la skuadon dum multtagaj vojaĝoj. La koloro de la interno de la cara vagono estas ruĝa, kaj tiu de la salonvagono malhelverda. Pliaj detaloj de la vagoninternoj videblas sur retpaĝoj de la muzeo (<a href="http://www.rautatie.org">www.rautatie.org</a>).</p> <p>La finna fervojmuzeo havas librostokon el 6000 volumoj kaj historian fototekon el 200 000 eroj. La videblaj eksponaĵoj en la haloj kaj korto de la muzeo estas 20 000. Je dispono de la vizitantoj estas malgranda vendejo, kafejo kaj salono por grupoj.</p> <div style="text-align:right">S<span class="mm">ALIKO</span></div>         
