      <h3>Ekonomio</h3>
      <h3 class="tem">USONO</h3>
      <h2>Nombro de militistaj invalidoj kreskas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Pli ol 4000 usonaj soldatoj mortis en Irako, sed la nombro de vunditaj soldatoj estas dekoble pli alta. Internaj dokumentoj de la Administracio por Veteranaj Aferoj taksas, ke la kosto pro militistaj invalidoj post 25 jaroj kreskos de nunaj 29 miliardoj da dolaroj ĝis 59 miliardoj.</b></p> <p>Nun ankaŭ pli da maljunaj veteranoj suferas je malsanoj kaŭzitaj de militservo. Ju pli la veteranoj maljuniĝas, des pli ili petas monkompenson pro siaj militservaj malsanoj, kiuj pro la aĝo pligraviĝas. La nombro de invalidoj saltis je 25 % ekde 2001, ĝis 2,9 milionoj. Dum la ses jaroj antaŭ la militoj en Afganio kaj Irako, oni konstatis pli modestan kreskon je 4 %.</p> <p>Kvankam inflacio parte eksplikas la pli altan koston, ĝi ne estas la sola kialo. La ĉefa kaŭzo certe estas la plibonigo de la kuraca scienco. Antaŭ kelkaj jaroj la plimulto de la vunditoj mortintus, sed nuntempe ili postvivas multe pli gravajn vundojn, ekzemple membrojn amputitajn kaj traŭmatajn brulvundojn. Mensaj malsanoj ankaŭ estas kompensataj. Krome la hodiaŭaj veteranoj pli konscias pri siaj rajtoj ol la veteranoj de antaŭaj militoj, kiuj ofte honte hezitis peti registaran helpon. El 755 000 veteranoj de Afganio kaj Irako 181 000 nun ricevas iun kompenspagon.</p> <div style="text-align:right">Roland T<span class="mm">HIBAULT</span></div> 
