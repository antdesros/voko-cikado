      <h3>Moderna vivo</h3>
      <h3 class="tem">NUKLEA ENERGIO</h3>
      <h2>Ĉu sabotado ĉe nuklea centralo?</h2>
      <!-- komenco de la enhavo -->

       

        <p>
   <b>La firmao Electrabel en Belgio ekspluatas sep nukleajn reaktorojn, kvar en Doel en
      la nordo de Flandrio kaj tri en Tihange en la oriento de Valonio. Electrabel estas
      parto de la franca grupo 
      <i>GDF SUEZ</i>. La belga registaro decidis ne plu konstrui novajn nukleajn centralojn kaj laŭgrade
      ĝis 2025 ĉesigi la lastajn reaktorojn. Jam dum la venonta jaro la du plej malnovaj
      reaktoroj Doel1 kaj Doel2 (po 450 MW) devus esti fermitaj post 40 jaroj de funkciado.
   </b>
</p> 
<p>Komence de 2014 ĉiuj sep reaktoroj ankoraŭ funkciis. Montriĝis post inspektado per
   speciala roboto, ke la reaktoroj de Doel3 kaj Tihange2 havas multajn mikroskopajn
   vezikojn en la metalo de la reaktoro mem. Oni nun scias, ke tiuj vezikoj ekzistas
   ekde la konstruado de la grandaj ŝtalaj ringoj, el kiuj konsistas la reaktoro. Ili
   ambaŭ estas de la sama fabrikanto.
</p> 
<p>Pliaj eksperimentoj pri metalo sub influo de radiado estas bezonataj por decidi, ĉu
   la reaktoroj povos esti plu uzataj en plena sekureco. Ĝuste la reaktoro estas la sola
   parto ne elprenebla kaj anstataŭigebla, ĉar la kosto de tia operacio tro altus. Estas
   malklare, ĉu eblos surbaze de la venonta raporto de sciencistoj reaktivigi tiujn reaktorojn,
   kiuj jam dum 30 jaroj produktis energion.
</p> 
<h3>Ŝmiroleo</h3> 
<p>Sed komence de aŭgusto tute neatendite okazis alia problemo en la centralo de Doel4.
   Pro subita manko de ŝmiroleo por la vaporturbino ekestis alarmo. Pro tio la sekursistemo
   ekfunkciis kaj la reaktoro estis aŭtomate haltigita. La giganta turbino malrapidiĝis
   sed, kiam la oleonivelo tro malaltiĝis, ĝi abrupte estis bremsita, kio kaŭzis grandan
   damaĝon. La teknikistoj nun esploras kiel plej rapide ripari ĝin.
</p> 
<p>Klare necesas diri, ke tio ne estas nuklea akcidento, ĉar en klasika gascentralo estas
   tute similaj instalaĵoj. Post la incidento la direktoro de la centralo decidis tuj
   alvoki al polico, ĉar ekzistis evidentaj kialoj por suspekti sabotadon. Nun belgaj
   policaj esploristoj pritraktas la aferon, sed nenion komunikos, ĝis estos klara situacio.
</p> 
<h3>Kapacito</h3> 
<p>Inter majo kaj aŭgusto 2014 duoniĝis la produktado de elektro en belgaj nukleaj centraloj.
   La kapacito falis de 6 GW (gigavatoj) al 3 GW. La ekspluatantoj de la centraloj ankoraŭ
   ne scias, kiam eblos refunkciigi tri reaktorojn. En la gazetaro aperas nun pluraj
   komentoj pri la afero kaj precipe pri la eblaj sekvoj pro la provizo-malcerteco.
</p> 
<p>La averaĝa elektrokonsumado en Belgio egalas al 10 GW kaj dum malvarma vintra vespero
   (inter la 17a kaj 20a horoj) la kulmina konsumo egalas al 14 GW. Krom la nukleaj centraloj
   (6 GW) Belgio disponas pri karbocentraloj (1,1 GW), gascentraloj (6 GW) kaj renovigeblaj
   energifontoj kiel vento-energio (maksimume 1,8 GW), suna energio (maksimume 2,8 GW)
   kaj bio-energio (1 GW). Krome eblas enporti ĝis 4 GW el apudaj landoj Francio kaj
   Nederlando. Inter Germanio kaj Belgio ne estas alttensiaj transportlineoj. Teorie
   la sumo sufiĉas por la plej alta konsumopinto, sed en la praktiko tio ne veras.
</p> 
<h3>Kolapso</h3> 
<p>La elektra reto komence estis koncipita por transporti kaj distribui la elektron de
   kelkaj grandaj centraloj en Belgio al ĉiuj uzantoj. Nuntempe la renovigeblaj energifontoj
   estas konektitaj, sed ili povos liveri elektron, se vento blovas kaj suno brilas kaj
   se la elektra reto estas en ekvilibro. Dum kolapso de la reto ankaŭ tiuj fontoj estos
   forŝaltitaj. Ankaŭ la eksterlanda enportado de elektro estas limigita: maksimume 3
   GW el Francio, se la francaj centraloj havas troan kapaciton. Sed dum vintra frosto
   en la tuta Eŭropo tio ne estas certa. El Nederlando la maksimuma enportado estas 1,4
   GW.
</p> 
<p>Nepras mencii ankaŭ la ekonomiajn aspektojn. Multaj gascentraloj nuntempe ne produktas
   elektron, ĉar la gasprezo estas tro alta kaj samtempe la elektroprezo tro malalta.
   Do la ekspluatantoj ne emas funkciigi ilin pro financaj perdoj. Laŭ la ekonomia leĝo
   pri proponado kaj postulado la elektoprezo certe altiĝos, kiam okazos mankoj. Tiam
   la regulisto pri elektro ELIA postulos ekfunkciigi tiujn belgajn gascentralojn. Ekzistas
   urĝa sekurplano: la lando estas disdividita en multajn regionetojn kaj parto de la
   eksterurbaj regionetoj riskos post averto esti forŝaltita de la elektroreto por protekti
   la stabilecon de la cetera reto.
</p> 
<h3>Perturboj</h3> 
<p>Belgio estas forte industriigita lando kaj dum jardekoj oni profitas de bone sekurigita
   elektra reto, kiu apenaŭ konis perturbojn. La nuna situacio estas do tute nekutima.
   Fakte la provizo-malcerteco jam delonge estas afero diskutinda. Se oni volas antaŭ
   2025 anstataŭigi la nukleajn centralojn, la registaro kaj privatuloj nun decidu pri
   la grandaj investoj por konstrui precipe karbocentralojn kaj iom da gascentraloj kune
   kun amaso da ventoturbinoj kaj sunpaneloj. Krome necesas pli da kunlaboro inter la
   eŭropaj landoj.
</p> 
<p>Tiurilate ni referencas al intervjuo de Gérard Mestrallet (estro de 
   <i>GDF SUEZ</i>) en la gazeto 
   <i>Le Monde</i> de la 22a de majo 2013. Mestrallet atentigis, ke la eŭropa energiopolitiko havas
   trioblan celon: kontraŭi tervarmiĝon, plibonigi konkurenckapablon kaj plicertigi provizadon.
   Li aldonis, tamen, ke en ĉiuj tri terenoj noteblas malsukcesoj. Kulpas ne nur la Eŭropa
   Komisiono, sed ankaŭ landoj, kiuj decidis sen interparolado fermi nukleajn centralojn
   post la akcidento en Fukuŝimo en 2011. Germanio, ekzemple, fermis ok centralojn.
</p> 
<p>Diris Mestrallet: „Anstataŭ la celita unuiĝinta elektro-merkato, ni konstatis disdividiĝon en Eŭropo.
   Malgraŭ la protokolo de Kioto por redukti emisiojn de tervarmigaj gasoj, emisioj tamen
   plialtiĝis. Sed ĝuste en Usono, lando, kiu ne subskribis la Kioto-protokolon, malkreskis
   emisioj. Unu el la kialoj estas skistogaso en Nord-Ameriko. Pro la amasa gasekspluatado
   el skisto la prezo malaltiĝis kaj la lokaj elektrokompanioj transiris de karbo al
   skistogaso. Sekve de tio la karbominejoj vendas la troan karbon kontraŭ malalta prezo
   i.a. al Eŭropo. Averaĝe dum tri el kvar tagoj la gascentraloj en Eŭropo ne funkcias,
   dum la karbocentraloj, kiuj pli malpurigas la aeron, konstante produktas.”
</p> 
<h3>Nafto</h3> 
<p>Kiamaniere eblas solvi la problemojn? Pluraj landoj en la mondo, kiel Belgio, ne havante
   proprajn krudmaterialojn, bezonas diversajn sendependajn energifontojn por produkti
   elektron. Tro malmultas en Belgio la ebloj por stoki energion pere de akvopumpado
   al altaj lagoj. Industriigita lando ne povas funkcii sen certeco pri provizado. Nuklea
   energio daŭre por longa tempo estas konsiderinda solvo. Kaj el ekonomia vidpunkto
   ĝi estas tute sendependa de la prezoj de gaso kaj nafto. En pli novaj konceptoj de
   nuklea energio eblos produkti elektron kun multe malpli da rubaĵoj, tiel ke nuklea
   centralo ne emisios tervarmigajn gasojn.
</p> 
<p>Mi persone apogas ĉiujn esplorojn pri renovigeblaj energifontoj kaj ankaŭ pri nuklea
   fuzio, sed tiu ĉi teknologio estos por tre fora estonteco. Dum la venontaj jardekoj
   estas tre grave ankaŭ esplori pri pliperfektigo de la nuklea fisio, ĉar teorie eblas
   ankoraŭ multe pli efike ĉerpi la energion el uranio kaj ankaŭ el la izotopo torio-232,
   kiu multe pli abundas surtere. Certe ni klopodu pli ŝpare uzi niajn energifontojn
   kaj sekve malpli poluadi nian vivmedion.
</p> 
