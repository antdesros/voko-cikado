      <h2>Kiel protekti retadresojn?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Pli kaj pli multaj personoj havas donitaĵojn pri si en interretaj paĝoj. Al ili espereble estas konate, ke simple publikigi sian retadreson en Interreto ne estas tre konsilinde. Ja ne nur viaj amikoj kaj konatoj povas tie trovi vian adreson, sed ankaŭ la dissendantoj de amasreklamo, la tiel nomataj spamistoj, povas trovi ĝin tie, kaj baldaŭ superŝutos vin per reklamoj por Viagra, skribos, ke vi gajnis la ĉefan premion en loterio ktp.</p> <p>Ekzistas tamen „trukoj” por ebligi, ke viaj konatoj ja povu vidi vian retadreson, dum la spamistoj (kiuj uzas komputilajn programojn por serĉi ĝin) ne povu. Ĉar ne estas ĉiam tre klare, ĉu tiuj trukoj funkcias, M<span class="mm">ONATO</span> faris kelkajn testojn. Ĝi publikigis en la 1a de julio 2004 en la retpaĝo <a href="http://www.esperanto.be/fel/mon/testo.html">www.esperanto.be/fel/mon/testo.html</a> kelkajn novajn, ĝis tiam neniam uzitajn, retadresojn. Unu adreson M<span class="mm">ONATO</span> publikigis sen ajna ĉifrado. Ĝi do estas legebla por homa okulo same kiel por komputilo. En la dua adreso la heliko (@) estis anstataŭigita per #64; (la dekuma alternativo por @). En la tria adreso ĉiuj literoj estis anstataŭigitaj per la dekumaj alternativoj. En la kvara kaj kvina la adresoj estis tute forigitaj kaj anstataŭe iu javskripta programeto montras la retadreson por la uzanto. La interesatoj povas rigardi en la menciita adreso, kiel tio okazis.</p> <p>Kio estas la rezulto de nia testo? Nu, en la 23a de septembro, do post preskaŭ tri monatoj, alvenis la unuaj spamleteroj kaj virusoj al la unua adreso, al tiu, kiu tute ne estis protektita. Al ĉiuj aliaj adresoj ĝis nun, unu jaron poste, venis eĉ ne unu spamo. Kion do konkludi el tio? Ke la spamistoj estas tre mallaboremaj kaj ĉerpas el la reto nur „nudajn” adresojn, adresojn sen iu ajn formo de ĉifrado. Vi estas sufiĉe sekura, se vi penas nur iom ĉifri ilin. Ekzemple, mia.adreso#64;mia.servilo (anstataŭ mia.adreso@mia.servilo) estas jam bona protektilo.</p> <p>Por la „specialistoj” inter vi: La supra testo estis farita en HTML-paĝo. La sama testo estis farita ankaŭ en PHP-paĝo (<a href="http://www.esperanto.be/fel/mon/testo.php">www.esperanto.be/fel/mon/testo.php</a>). Ĝis nun eĉ la neprotektita adreso en la PHP-paĝo ne estis trovita. Ŝajne spamistoj skanas nur paĝojn kun la sufikso .html ...</p> <p>Pro scivolo M<span class="mm">ONATO</span> ankaŭ en la retgrupo soc.culture.esperanto publikigis mesaĝon kun nova adreso. Montriĝis, ke al tiu adreso la unuaj spamleteroj kaj virusoj alvenis post nur 12 tagoj.</p> <div style="text-align:right">Paul P<span class="mm">EERAERTS</span></div>  
