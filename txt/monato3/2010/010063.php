      <h2>Intervjue pri la grafo de Monte-Kristo</h2>
      <!-- komenco de la enhavo -->

       

     <p>La franca romanverkisto Alexandre Dumas (1802-1870) estas konata ne nur en Francio; liaj romanoj estas tradukitaj en ĉiujn eŭropajn kaj multajn aliajn lingvojn. Mi opinias, ke mi ne troigos, dirante, ke liaj ĉefaj verkoj – <i>La Tri Muskedistoj</i> (1844) kun du sekvoj : <i>Dudek jarojn poste</i> (1845) kaj <i>La Vicgrafo de Bragelonne</i> (1850), <i>Reĝino Margot</i>, <i>La Grafo de Monte-Kristo</i> (1844-1845) – estas inter la plej legataj libroj en la mondo. Kaj eĉ se vi ne legis ilin, vi certe vidis la filmojn, adaptitajn laŭ tiuj libroj – ne nur francajn, sed ankaŭ rusajn, italajn, usonajn, ...</p> 
<p>Mi tenas en la manoj la ĵus aperintan libron de Alexandre Dumas <i>La Grafo de Monte-Kristo</i>, kiun esperantigis la franca fakulo pri lingvoj Daniel Moirand. La traduko estas altnivela: la teksto de Dumas estas tre fidele tradukita – kaj samtempe vi tute ne havas la impreson, ke temas pri traduko; la romano estas facile legebla, ĉar verkita en bonega Esperanto.</p> 
<p>Daniel Moirand, nun emerito, profesia lingvisto, studis plurajn lingvojn: la anglan, hispanan, turkan, eĉ mongolan; diplomita pri la rusa, hungara, araba, ... Dum multaj jaroj li instruis la rusan lingvon. Li estas aŭtoro de originala esperantlingva krimromano, <i>Murdo en Esperantujo</i> (1988, FEL), kaj lernolibro, <i>Materialoj pri la nuna uzado de la Akuzativo en Esperanto</i> (1990, FEL). Li tradukis en Esperanton la grandegan verkon de Marko Polo <i>La libro de mirindaĵoj</i> (2001, UEA), kaj jen aperis lia traduko de <i>La Grafo de Monte-Kristo</i> (2010, FEL). Nun li finpoluras sian tradukon de <i>1001 noktoj</i> laŭ la traduko de Antoine Galland.</p> <p>Mi petis Danielon Moirand respondi kelkajn demandojn.</p> 
<p>– Kiam vi esperantistiĝis?</p> <p>– Kiam mi estis 40-jara. Mi volis scii, ĉu artefarita lingvo povas vere funkcii. Mi studis Esperanton en la lingva fakultato de Provenca Universitato, kie ĝin instruis Michel Duc Goninaz.</p> <p>– Kiom da tempo vi bezonis por vere bone ekposedi Esperanton?</p> <p>– Mi bezonis tri jarojn. Sed dum tiuj tri jaroj mi tre multe laboris pri la lingvo. Eblas diri, ke mi trapasis tri etapojn: la unuan jaron mi studis la bazojn de la lingvo; la duan jaron mi multe legis por pliriĉigi mian vortostokon; la trian jaron venis la parolkapablo, libera interkomunikado. La regula legado de esperantaj originalaj verkoj donis al mi grandan vortoprovizon. Certe, al mi multe helpis mia antaŭa lingvistika sperto.</p> <p>– Mi scias, ke vi multe okupiĝis pri la verkaro de Alexandre Dumas. Tamen en Francio oni ofte konsideras Dumas iomete kiel duagradan verkiston ...</p> <p>– Mi opinias, ke li estas vere grava verkisto. Oni ne sufiĉe agnoskas lian verkaron en Francio, verŝajne, pro tio, ke li ofte pli-malpli libere pritraktas historiajn eventojn kaj personojn. Sed se multaj eksterlandanoj konas Ludovikon la 13an, Ludovikon la 14an, la kardinalojn Richelieu kaj Mazarini, la reĝinon Margot kaj ŝiajn proksimulojn, la militon inter katolikoj kaj hugenotoj – tio okazas danke al la libroj de Dumas. Dumas neniam pretendis skribi historiajn verkojn: li verkis romanojn. Krome, Dumas verkas en belega franca lingvo. Li uzas altkvalitan literaturan lingvon. Se vi vere volas pasiigi vian infanon pri legado, donu al li librojn de Dumas!</p> 
<p>– Kial vi elektis <i>La Grafo de Monte-Kristo</i>?</p> <p>– Mi retrovis miajn junajn emociojn. Deksesjara, mi tralegis <i>La Grafo ...</i>, kiu forte impresis min, kaj post preskaŭ 50 jaroj mi decidis traduki la libron en Esperanton. Dumas verkis multajn belajn romanojn, sed <i>La Grafo ...</i> estas la plej elstara; neniu alia libro valoras ĝin. Ĉi tie oni trovas la plej interesajn karakterojn, la plej altajn filozofiajn ideojn ... Vi povas relegi ĝin plurfoje, kaj ĉiam vi malkovros ion novan.</p> <p>– Kiujn ĉefajn problemojn kaj malfacilaĵojn vi renkontis, tradukante <i>La Grafo ...</i> ?</p> 
<p>– La ĉefa malfacilaĵo – la epoko: la ĉefaj eventoj de la romano okazas en la jaro 1844. De tiam ĉio ŝanĝiĝis: Francio, la vivmaniero, la homoj, la lingvo ... Necesas vere mergi sin en la epokon por bone kompreni la situacion, la karakterojn, la agojn de personoj. Krom tio, dum 160 jaroj multaj aĵoj, nocioj, institucioj malaperis. Mi laboris per malnovaj vortaroj por trovi la ĝustan sencon de malaperintaj vortoj kaj nocioj.</p> 
<p>– En la fino de ĉiu ĉapitro vi klarigas francajn tiamajn realaĵojn – tiuj klarigoj estas rezulto de grandega laboro, eble, same granda, kiel la tradukado!</p> <p>– Certe, mi devis vere pliprofundigi kelkajn temojn. Sed necesas konsideri, ke la esperanta traduko estas ne nur por la francoj – ili ja povas legi la libron en la originala versio! – sed ankaŭ por la internacia publiko. Por ili multaj francaj realaĵoj signifas nenion. Mi esperas, ke tiuj klarigoj ebligos al esperantistoj de diversaj kontinentoj pli profunde kompreni kaj pli bone pritaksi la plej grandan verkon de Alexandre Dumas.</p> 
