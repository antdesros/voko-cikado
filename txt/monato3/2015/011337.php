      <h2>Malofta aktualaĵo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Jen libro pri Rusio, verkita de profesia (kaj premiita) ĵurnalisto, kiu iam raportadis
   el Moskvo por finna gazeto. Krom en Esperanto, 
   <i>Homoj de Putin</i> aperis ankaŭ en la finna kaj la sveda.
</p> 
<p>La aŭtoro, Kalle Kniivilä, famas en Esperantujo kiel redaktinto de 
   <i>TEJO Tutmonde</i> kaj unu el la fondintoj kaj redaktantoj de 
   <i>Libera Folio</i>. Li estis ankaŭ estrarano de UEA pri informado, kaj li uzis siajn esperantajn kontaktojn
   en la kreado de ĉi tiu libro, ĉar rusaj esperantistoj helpis lin dum liaj vojaĝoj
   en Rusio trovi taŭgajn homojn por intervjui: perfekta ekzemplo de reala aplikado de
   Esperanto, ĉar la produkto el tiu internacia kunlaboro utilas kaj por esperantistoj
   kaj por neesperantistoj.
</p> 
<p>La libro havas la trafan subtitolon „La silenta plimulto de Rusio”, ĉar ĝi temas pri ordinaraj rusoj, kiuj ne manifestacias surstrate, ne estas intervjuataj
   de eksterlandaj ĵurnalistoj (escepte kiam Kalle ilin vizitas), sed ripete voĉdonas
   por Vladimir Putin, konfidante al tiu la regadon de Rusio dum 15 jaroj. Kial ili tion
   faras?
</p> 
<p>Respondojn al tiu demando ni ricevas per la propraj vortoj de elektitaj membroj el
   tiu silenta plimulto, provizore malsilentigitaj de Kalle, ĉar la libro enhavas multajn
   citaĵojn el intervjuoj kun ili, sed ankaŭ per la klarigoj de Kalle, kiu ĉerpas el
   siaj propraj spertoj kaj el pluraj pezaj libroj pri la lastatempa historio de Rusio.
</p> 
<p>Temas, interalie, pri diveneblaj faktoroj: la homoj ĝuas relativan prosperon sub Putin,
   kaj ili ankoraŭ gardas malagrablajn memorojn pri la krima ĥaoso, kiu antaŭis lin.
   Tamen la ekonomio de Rusio preskaŭ tute dependas de la eksportado de gaso kaj nafto,
   kies internacian prezon Putin ne povas regi, do la estonteco estas malcerta, speciale
   ĉe la lastatempa ekspluatado de skistogaso. Relative nova evoluo estas, ke Putin de
   ĉirkaŭ 2012 pli-malpli turnis la dorson al la edukita elito de Moskvo, kompreninte,
   ke liaj plej fidelaj subtenantoj troviĝas en la ekonomie postrestintaj regionoj ekster
   la ĉefurbo.
</p> 
<p>Al mi estis interese legi, kiel la rusoj cinikiĝas pri politiko. Tiu evoluo ne surprizas
   post tiom da mensogoj kaj trompoj, sed ĝi ankaŭ paralelas al evoluo en multaj aliaj
   landoj. En Britio, ekzemple, pli kaj pli da inteligentaj kaj bone informitaj homoj
   ne plu malŝparas sian tempon por iri al balotejo, ĉar ili scias, aŭ opinias scii,
   ke la demokratio estas nur trompŝajnigo, ke ĉiuj politikistoj estas koruptitaj, kaj
   tiel plu. Bedaŭrinde, ili pravas, almenaŭ certagrade. Krome, la rusoj, simile al homoj
   en aliaj landoj, entuziasmiĝas pri konspiroteorioj. Bedaŭrinde, multaj el tiuj teorioj,
   eĉ kelkaj plej strangaj, enhavas kernon de vero.
</p> 
<p>Ne esperu trovi en la libro de Kalle Kniivilä ĝisfundan analizon, kaj certe ne atendu
   de li solvoproponojn. Li estas ĵurnalisto kaj verkas ĵurnaliste, citante homojn, perante
   siajn impresojn (interalie pri sia vizito al Moskvo en 1991, longe antaŭ ol Putin
   ekregis). Sed li faras tion tre bone, kaj la libro estas tre leginda, ankaŭ kiel ekzemplo
   de malofta ĝenro en Esperanto: nefikcia libro pri aktuala temo.
</p> 
<p>Mi ricevis la libron por recenzo kaj papere kaj kiel bitlibron. Mi konsultis ambaŭ,
   sed komencis per tralego de la bitlibro. Ĉar la libro havas nek bildojn nek grafikaĵojn,
   kaj ne estas komplikaj krucreferencoj, ĝi estas tre oportune konsumebla kiel bitlibro.
</p> 
