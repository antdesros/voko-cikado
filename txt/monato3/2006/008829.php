      <h2>La konkana skribdisputo</h2>
      <!-- komenco de la enhavo -->

       

       <p>La decido de la registaro de la sudbarata subŝtato Karnatako enkonduki la konkanan lingvon kiel studobjekton en registaraj lernejoj rebruligis la debaton pri la konkana skribo: ĉu uzi la devanagaran, kanaran aŭ la latinan? Tiu ĉi hindeŭropa lingvo ne posedas propran skribon. Laŭ la okcidenta marbordo de Barato la konkana estas skribata per la devanagara, kanara, latina, malajala kaj persoaraba skriboj depende de la ĉefa skribo de la regiono.</p> <p>La 3,7 milionoj da parolantoj de la konkana en Karnatako uzadas la kanaran skribon jam de pli ol jarcento. Viglas la literatura tradicio: en 2004 ĉirkaŭ 40 libroj aperis en la kanara-konkana, se diri nenion pri multaj gazetoj.</p> <p>Post sufiĉe ampleksa konsultado, kiu inkluzivis eminentulojn, verkistojn, instruistojn, gepatrojn de lernejanoj kaj la gelernejanojn mem, la subŝtata belarta akademio de la konkana decidis, ke la kanara estu la preferata skribo. Sed la politike potenca premgrupo devanagara kontraŭas tiun ĉi decidon, argumentante, interalie, ke la federa belarta akademio rekonas nur la devanagaran formon de la konkana.</p> <p>Kaj nun, kiam oni instruos la lingvon ankaŭ en registaraj lernejoj, kelkaj parolantoj de la konkana rekomendas, ke ekuzadi la devanagaran skribon plifortigus la etnajn ligojn kun parolantoj de la lingvo en la najbaraj subŝtatoj Maharaŝtro kaj Goao: en tiu lasta la oficialaj lingvo kaj skribo estas respektive la konkana kaj la devanagara. Kritikantoj tamen montras, ke laŭ statistikoj el la 979 registaraj elementaj lernejoj en Goao la konkana estas la instrulingvo en nur 81; kontraste, 930 lernejoj permesas la instruadon en la marata. Plue, en ĉiuj prestiĝaj privataj lernejoj la instrulingvo estas la angla.</p> <p>Tria premgrupo rekomendas la latinan alfabeton. La konkana en la latina skribo havas la plej malnovan literaturan tradicion (jam de 1622) kaj daŭre restas vigla (kontraste, la devanagara estis unue uzata por la konkana nur en 1893). Tiu ĉi grupo argumentas, ke oni devas adopti la latinan alfabeton, ne nur ĉar ĝi jam estas vaste uzata inter la konkanoj en Goao, sed ankaŭ ĉar ĝi facile funkcius ĉe la konsiderinda (kaj influa) konkana diasporo ekster Barato. Efektive, la apogantoj de la latina skribo disvastigas modifitan latinan skribon, kiu, laŭ ili, estas „ideala por la komputila klavaro”.</p> <div style="text-align:right">A. G<span class="mm">IRIDHAR</span> R<span class="mm">AO</span></div> 
