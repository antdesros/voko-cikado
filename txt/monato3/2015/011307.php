      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Rekomendinda romano en matura lingvaĵo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Mirinda romano. Kiel sciate, antaŭ longe Sten Johansson verkis mallongajn krimromanojn,
   kiujn siatempe mi laŭdis i.a. pro lia bone prizorgita lingvaĵo. Tion ĉe la nuna verko
   mi povus ripeti, sed estus banalaĵo: iasence nun temas pri misterromano, verko en
   kies daŭro malkovriĝas mistero. Sed ne tio estas la vera atentokapta punkto: 
   <i>Marina</i> unuavice temas pri la demando: kio estas, kion signifas „familio”?
</p> 
<h3>Datindikoj</h3> 
<p>Kiel nuntempe ofte okazas ĉe serioze maturaj aŭtoroj, la rakonto disvolviĝas ne simple
   nur unulinie. Male, la leganto bone atentu la datindikojn supre ĉe la diversaj ĉapitroj.
   Kiom mi sukcesis sekvi, la aŭtoro ne miskalkulas, sed la datoj kaj la aĝo de la roluloj
   bone akordiĝas (Fojfoje eĉ ĉe famaj alilingvaj aŭtoroj tio montriĝas problemo.) Tiujn
   diversajn datindikojn por ni klare konkretigas diversaj internacie konataj okazaĵoj,
   mi menciu, ekzemple, pri la surstrata murdo al Olaf Palme, kiun supozeble ĉiu svedo
   memortenas, same pri la disfamiĝo pri Gorbaĉov kaj lia perestrojko, pri la okazaĵoj
   sur la Placo de la Ĉiela Paco en Pekino kaj multaj aliaj.
</p> 
<p>Mi diris, ke funde temas pri preskaŭ filozofia nocio „kio estas familio”? Eble cito tion iom klarigos:</p> 
<p>Tomas diras en konversacio telefona kun Marina, ke li ne povas vortigi, kio vere mankas
   al li. (p. 105) Li daŭrigas:
</p> 
<p>„Eble temas pri ia sento de tuteco.”</p> 
<p>Marina pripensas liajn vortojn dum kelka tempo.</p> 
<p>„Ĉu tuteco? Tio sonas filozofie.”</p> 
<p>„Ne, tute ne. Sed ĉio ŝajnas iel splita, disa. Eble ĉar mi mem estas tia. Mia vivo
   konsistas el hermetaj ĉeloj. Nun mi estas kun vi, sed mi ne povos diri tion al Cecilia
   sen vundi ŝin. La laboro estas aparta, tie mi estas alia persono, la scienca Tomas.
   Mi ne havas familion, krom onklo Arne, sed ankaŭ miaj memoroj, mia pasinteco estas
   kvazaŭ aparta ĉelo sen kohero kun la nuno. Ĉu tio sonas freneze?”
</p> 
<p>„Ne, mi pensas ke mi komprenas. (...)”</p> 
<h3>Nostalgio</h3> 
<p>La homoj estas tiel vive priskribitaj, ke oni kvazaŭ kredas vere koni ilin tute persone.
   La infanoj – frue en la libro – kondutas kiel veraj infanoj, kiujn surstrate aŭ hejme tuj oni rekonus. La mezlernejanojn
   mi ankoraŭ ĵuse ĉiutage spektis en niaj klasoj. – Vekas certan nostalgion eĉ ...
</p> 
<h3>Instrusistemo</h3> 
<p>Sed unu aferon mi bedaŭras: la sveda instru-sistemo ne identas je la nederlanda, do
   kelkajn aferojn mi ne vere komprenas. Miaopinie estus tre bonvena iu klariga piednoto
   tiurilate. Kiel imagi, kio estas „socia branĉo de gimnazio”? kaj kio estas la 9a klaso? – de kie oni kalkulu tion, ĉu de la 4-jariĝo de la lernantoj aŭ ĉu ekde alia aĝo?
</p> 
<p>Sed nu, nur grumblulo tion menciu. Entute temas pri tre rekomendinda romano en bonega
   matura lingvaĵo, kiel de Sten Johansson oni kompreneble devus / povus atendi.
</p> 
