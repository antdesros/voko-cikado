      <h2>La saĝo de Salomono kaj salamo</h2>
      <!-- komenco de la enhavo -->

       

       <p>En decembro 2013 multaj homoj en Germanio (supozeble ĉefe viroj) ricevis leteron de
   advokatoj kun riproĉo, ke ili senpage, senpermese kaj kontraŭleĝe spektis en interreto
   filmojn pornografiajn. Ili pagu nemalgrandan sumon por eviti akuzon kaj malagrablan
   kaj embarasan juĝejan proceduron.
</p> 
<p>Ne konatas, kiom da homoj pagis. Baldaŭ poste esprimiĝis duboj, ĉu spekti tiajn filmojn
   fakte malpermesatas. Kelkaj asertis, ke ne temas pri punenda ago, se oni ne konservas
   la datenojn en komputilo.
</p> 
<p>Aliaj opiniis, ke la datenoj ja dum kelkaj sekundoj restas ie konservitaj. Tio povus
   esti konsiderata krimo kontraŭ kopirajto kaj priŝtelo de spirita posedaĵo.
</p> 
<p>Tio memorigas min pri vizito en juĝejo antaŭ jardekoj. Helplaboristo estis ŝtelinta
   pakaĵon da kafograjnoj kaj salamon. Li estis kondamnita al konsiderinda monpuno.
</p> 
<p>Ankaŭ lia edzino estis kondamnita al simila puno pro ricelado, ĉar ŝi, sciante, ke
   temas pri ŝtelitaj varoj, matene preparis kafon kaj sandviĉon, kiujn la edzo kunportis
   al la laborejo. Tio okazis monatojn antaŭe. Alia juĝisto en supera kortumo devis decidi
   en apelacia proceso pri la verdikto kontraŭ la edzino.
</p> 
<p>Mi tiam ne komprenis, sed bone memoras, kion la juĝisto celis per pridemandado, kiel
   ŝi preparis la kafon kaj la sandviĉon. – Per bolanta akvo ... – Pli detale! – Mi muelis la kafograjnojn ... – Ĉu tutan duonkilogramon? – Ne, nur du aŭ tri kulerojn ... – Kaj tion li trinkis? Sekan pulvoron? – Ne, mi preparis kafon el ĝi ... – Kiel? Do, kiel oni kutime faras tion: mi boligis akvon kaj verŝis en filtrilon ...
   – Kaj poste? – Mi verŝis la kafon en termobotelon ... – Nu, kion vi faris kun la muelita kafopulvoro? – Mi forĵetis en la rubujon ...
</p> 
<p>La juĝisto konstatis: „Via edzo ja manĝis ŝtelitan salamon, kiun vi preparis por li. Tio estis ricelado.
   Sed li fakte ne trinkis la ŝtelitajn kafogrenojn, sed nur akvon kun kafoaromo.
   <br></br> Ĉar mi supozas, ke la akvo ne estis ŝtelita, mi devas konstati, ke koncerne la kafon
   vi donis al via edzo nur aromon de ŝtelitaĵo, do ne ŝtelitaĵon mem. Aromo ne validas
   kiel objekto en jura senco.”
</p> 
<p>Tion dirinte, la juĝisto fiksis monpunon eble en kvarono aŭ eĉ malpli ol antaŭe. La
   geedzoj unuanime dankis kaj montris sian feliĉon. Ili ne komprenis, kial la juĝisto
   montris tian mildecon.
</p> 
<p>Nun gazetoj mencias, ke la advokatoficejo, leterante pri kontraŭleĝa rompo de kopirajto,
   mem ne agis laŭleĝe ...
</p> 
