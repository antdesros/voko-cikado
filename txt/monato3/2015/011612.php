      <h3>El mia vidpunkto</h3>
      <h3 class="tem"></h3>
      <h2>Vestoj kaj investoj</h2>
      <!-- komenco de la enhavo -->

       

      <p>En oktobro vizitis Brition la ĉina prezidanto Xi Jinping. Hazarde mi trovis min fine
   de la vizito en Londono. Ekster la palaco Buckingham, la rezidejo de la reĝino Elizabeta
   2a, purigistoj forbalais ĉifitajn ĉinajn kaj britajn flagojn kaj surtretitajn protest-banderolojn
   – tiujn ne konfiskitajn de la polico – kontraŭ lezado de homaj rajtoj en Ĉinio.
</p> 
<p>Sur la trotuaro mi rimarkis paperfolion. Helpemo, verŝajne, kaj solidareco kun la
   purigistoj, ĉiuj el mez-eŭropaj landoj, sen kiuj Britio ne plu funkcius, pelis min
   ĝin preni en la manon. Imagu mian surprizon, kiam mi konstatis, ke mi tenas eltiraĵon
   el la reĝina taglibro.
</p> 
<p>Kutime M<span class="mm">ONATO</span> ne aperigas tradukaĵojn. Tamen mi taksas la enhavon sufiĉe grava por rompi fundamentan
   principon de nia magazino. Jen, kion mi legis ... 
</p> 
<p>
   <i> ... plene elĉerpitaj. Ni faris nian devon kaj konsentis, laŭ la ordono de la ĉina
      imperiestro (tiel nomis lin nia edzo, princo Filipo), ke oni nin kune fotu. Ankaŭ
      komunistoj bezonas monarĥojn. Esti vidata kun ni, defendanto de la kredo kristana,
      helpos al la diktatoro plu persekuti ĉinajn kristanojn. Jes ja, diktatoro. Kiam estis
      prezentata nia ĉefo de la parlamenta opozicio, ni notis, ke stumblis la interpretisto.
      Kredeble en la ĉina ne ekzistas vorto por „opozicio”
   </i>.
</p> 
<p>
   <i>Tamen ni taksu la viziton sukcesa. Almenaŭ la kronprinco Karlo bridis sian langon.
      Neniu menciis, ke iam, vizitinte Ĉinion, nia filo priskribis la partian hierarĥion
      kiel maljunajn vaksfigurojn. Kiom ni ridis ... ĝis oni priraportis en la klaĉgazetaro.
   </i>
</p> 
<p>
   <i>Sed ĝenas ne la fotado, ridetado, manpremado. Tial ekzistas monarĥoj en la moderna
      mondo (kaj pro tio ni estas malavare kompensataj, ankaŭ niaj familianoj kaj iliaj
      familianoj, el la publika poŝo). Ne. Ĝenas, ke nia registaro ne kontentos, ĝis ĝi
      estos fordoninta nian estontecon al la orientaj koloniistoj. Kia ironio: en la epoko
      de nia pra-pra-avino, Viktoria, estis britoj la koloniistoj. Sed nun ... ne plu. Tiel
      malfavore turniĝas la mondo.
   </i>
</p> 
<p>
   <i>Karikaturo, skurila sed trafa, en nacia gazeto perfekte kaptis la viziton. Jen Xi
      kaj lia edzino, kies neprononceblan nomon mi jam forgesis, enirontaj sian aviadilon
      antaŭ ol hejmeniri. En sia mano portas Xi vestaĵojn ... sed jen antaŭ la aviadilo,
      adiaŭante, staras Filipo kaj ni, kaj tiu bubo Cameron, la ĉefministro, kaj lia kompano
      Osborne, la ministro pri financaj fiaskoj, pardonu, aferoj ... ĉiuj senvestaj. Nu,
      bone, videblas nur la dorsoj, la pugoj, do ne tiom privataj partoj ... tamen ... apenaŭ
      decas. Aliflanke ni ne povis ne rideti: la ĉinoj, modernaj vikingoj, nin nudigis,
      hejmenportante invitojn konstrui atomcentralojn, fervojojn kaj dio-scias-kion alian,
      kion ĝis antaŭ nelonge niaj subuloj perfekte kapablis fari kaj eĉ vendis kontraŭ grandaj
      profitoj al duonsovaĝa kaj dankema mondo.
   </i>
</p> 
<p>
   <i>Flanke: ni demandas nin, ĉu la ĉina gazetaro kuraĝus montri nuda – eĉ nur puge – la diktatoron. Ni povas imagi, ke redaktoroj estus tuj enkarcerigitaj. Nu, ideo ne
      malaprobinda. Ni proponos al Cameron. Ĉinio ne tiom kritikendas, kiom tiuj hom-rajtuloj
      – silentigitaj dum la vizito en nia insulo de libera esprimado – volas kredigi.
   </i>
</p> 
<p>
   <i>Ni perdas nian fadenon. Ho, jes. Cameron. Bubo ... ĉefministro. Tiu, kies partio plejparte
      abomenas ĉion eŭropunian, sed nun bonvenigas ĉinojn kaj ĵetas al ili promesojn kaj
      pundojn por investi en Brition. Kia humiligo. Ĉio ĉi en la sama semajno, kiam estas
      anoncita la kolapso de la brita ŝtal-industrio, kun dio-scias-kiom da perdotaj laborpostenoj,
      kaŭzita laŭdire de malmultekosta, malbonkvalita ŝtalo enportita el Ĉinio.
   </i>
</p> 
<p>
   <i>Ĉion ĉi aranĝas nia registaro en nia nomo. Nu, kiam ni fariĝis reĝino, antaŭ 63 jaroj,
      ni heredis tronon de prestiĝa, potenca lando, kun imperio, sed nun ... 
   </i>
</p> 
<p>Jen ĉio. Ŝokite, kaj kiel lojala subulo, mi donis la folion al palaca gvardiano, ke
   tiu tuj reportu al la reĝino. „Ĉu vi legis?” demandis la ulo, fingrumante sian pafilon. „Ho, ne,” mi mensogis. „Mi estas esperantisto. Kiu ĉiam diras la veron.”
</p> 
