      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Eksiĝo baziĝas sur bazo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Komence de junio la japana ĉefministro Hatojama Jukio eksiĝis post malpli ol unu jaro en sia ofico. Kulpis parte lia nehonesteco rilate privatajn financajn aferojn, parte problemo pri militbazo.</p> <p>Fariĝinte ĉefministro en septembro 2009, li unue tre popularis. Lian venkon en la deputitaj elektoj en aŭgusto bonvenigis la popolo, kiu suferis pro longdaŭra burokrata politiko de la Liberala Demokratia Partio.</p> <p>En novembro pasintjare evidentis, ke li ne raportis ĝuste pri siaj posedaĵoj al la ministrejo pri internaj aferoj. Li pretekstis, ke li naskiĝis en riĉa familio kaj ne atentis tiajn aferojn. Tamen jam en oktobro partia sekretario, Ozaŭa Iĉiroo, estis arestita pro kaŝado de mono. Klariĝis, ke la du plej gravaj homoj estas nepuraj pri mono.</p> <h3>Militbazo</h3> <p>Alia problemo koncernis la translokadon de usona militbazo en Okinaŭa. En aprilo Hatojama, dum 10-minuta renkontiĝo kun la usona prezidanto Obama, promesis en du monatoj transloki bazon en la centro de la homplena urbo Ginoŭan, kiu ĉiutage minacis la vivon de la loĝantoj.</p> <p>Tamen la ĉefministro ŝanĝis siajn ideojn, tiel ke la perfiditaj okinaŭa-anoj eksplodis kun kolerego kaj decidis batali kontraŭ Hatojama kaj lia nova plano. Tiamaniere la ĉefministro, komence tre populara, eksiĝis malpopulare post nur ok monatoj.</p> <h3>Aidoso-pacientoj</h3> <p>Du tagojn post la eksiĝo Kan Naoto estis elektita la 94a japana ĉefministro. La antaŭaj kvin ĉefministroj devenis de politikista familio, sed Kan ne. Naskiĝinte en ordinara familio, li okupiĝis en la civitana movado. En 1980 li elektiĝis deputito. Kiam ministro de bonfarto en 1996, li pardonpetis al aidoso-pacientoj pro la fuŝaĵoj de la registaro.</p> <p>Ŝajnas, ke li estas originala kaj unika politikisto, do multaj bonvenigas lian ĉefministriĝon. Tamen atendas Kan-on diversaj problemoj, inkluzive de la translokado de la usona bazo kaj de la financa krizo. Kutime popolanoj emas subteni novan kabineton, sed baldaŭ ili sentas sin malesperaj. Ankoraŭ vidiĝos, ĉu tio okazos al la nuna kabineto de Kan.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo</div> 
