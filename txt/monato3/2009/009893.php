      <h3>Moderna vivo</h3>
      <h3 class="tem">PRIZONOJ</h3>
      <h2>Pri lokoj kaj mokoj</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum jaroj en Belgio mankas karcerlokoj. Gardistoj plendegas pri streĉaj situacioj pro troloĝateco. Policistoj perdas motiviĝon, ĉar aro da krimuloj, kaptitaj en freŝa faro, tamen restas liberaj pro manko de lokoj. Diversaj ministroj pri justico faris promesojn, ekzamenis kaj daŭre ekzamenas eblajn solvojn, sed la situacio apenaŭ pliboniĝis.</p> <p>Sed enkarcerigitoj mem zorgas pri solvo. En julio tri krimuloj eskapis el prizono en Bruĝo per kaperita helikoptero. Kelkajn tagojn poste tri aliaj uzis eskalon, kiun laboristoj postlasis, por transrampi muron kaj fuĝi per la aŭto de la patrino de unu kaptito, kiu atendis antaŭ la enirejo (elirejo?).</p> <p>Denove, komence de aŭgusto, du gangsteroj forkuris, ĉi-foje el la juĝejo en Bruselo, post kiam tien eniris kaj minacis armitaj kaj maskitaj helpantoj. Tri eskapoj de ok personoj en nur du semajnoj. Ĝis la mezo de aŭgusto en la nuna jaro jam eskapis 39 kaptitoj.</p> <h3>Nepardoneblaj eraroj</h3> <p>Evidente la ministro pri justico, Stefaan De Clerck [stefán deklérk] dronas sub kritikoj de komunikiloj kaj de opoziciaj politikaj partioj. Oni efektive faris nepardoneblajn erarojn. Jam en 2007 okazis du fuĝoj per helikoptero. La parlamento tiam voĉdonis por sekurigi la ĉefajn prizonojn por malebligi tion kaj antaŭvidis buĝeton. Sed, en 2009, unu el la plej danĝeroj banditoj en Belgio troviĝis en malliberejo, kie mankis ĝuste la promesita sekureco.</p> <p>La krimuloj, kiujn oni volis prijuĝi en la brusela justicpalaco, jam faris plurajn perfortaĵojn kaj aperis antaŭ juĝisto pro provo prirabi, kun minacado al etulo kaj pafado al policistoj. Sed la servo, kiu zorgis pri ilia tiea alveno, taksis ilian ĉeeston en la juĝejo senriska, kaj do ĉeestis nur nearmitaj gardistoj.</p> <h3>Primokaj artikoloj</h3> <p>Ne nur eskapoj estigas primokajn artikolojn en la gazetoj. La kompatinda ministro pri justico devas digesti monton da plendoj kaj primokoj, sed lia povo estas tre limigita pro aŭtonomio de la justico.</p> <p>Ĉu la eskapoj efektive kontribuas al la solvo de la lokmankoj? Fakte la granda plimulto de la eskapintoj poste rekaptiĝas. Sed estus pli efike, se la belgaj polico kaj justico penadus gardi danĝerajn banditojn kaj solvi la lokmankon anstataŭ mobilizi armeon por retrovi eskapintojn. Aliflanke oni ne tro plendu. Antaŭ jaroj iam eskapis pli ol 250 homoj en unu jaro.</p> <div style="text-align:right">Ivo D<span class="mm">URWAEL</span></div> 
