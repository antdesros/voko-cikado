      <h3>Politiko</h3>
      <h3 class="tem">KOSOVO</h3>
      <h2>Nova Jerusalemo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Oni nomas la dividitan nordan urbon de Kosovo, Mitrovicon, la nova Jerusalemo. Tie restas serbaj loĝantoj de Kosovo, kiuj obeas ne la registaron en Prishtina sed nur la politikajn strukturojn subtenatajn de Serbio. En Mitrovico, dum la milito de 1998-1999, pereis multaj homoj kaj estis detruitaj domoj de albanoj.</p> <p>Pro la premo de lokaj serboj, centoj da albanaj familioj forlasis siajn hejmojn kaj fuĝis al la suda parto de la urbo aŭ aliloken en Kosovo. Post 1999 oni provis persvadi ilin hejmeniri kaj rekonstrui iliajn domojn, sed vane, ĉar lokaj serboj faris sian plejeblon por kontraŭstari. Dume la registaro finance helpis rekonstrui centojn da serbaj domoj.</p> <h3>Perforte</h3> <p>Lastatempe kvin albanaj familioj decidis rekonstrui siajn hejmojn en la kvartalo Berdjane, ĉe la loko <i>Kroi i Vitokut</i> aŭ Akvofonto de Vitok. Protektis ilin la polico, same anoj de la Kosova Taĉmento (KFOR) aŭspiciata de Nord-Atlantika Traktat-Organizaĵo (NATO). Tamen ĉiutage lokaj serboj perforte protestis, uzante ŝtonojn kaj eĉ kartoĉojn kaj grenadojn kontraŭ la polico. Ĉi-lasta intervenis per larmiga gaso.</p> <p>Antaŭe, serboj proponis al albanoj monon por aĉeti ties domojn kaj parcelojn proksime al la <i>Kroi i Vitakut</i>. Sed nun pro la rekonstruado velkas ofertoj tiaj. Helpas la vortoj de la kosova prezidanto Fatmir Sejdiu: „Ni strebas, ke Kosovo estu demokratia ŝtato por ĉiuj siaj civitanoj kun aparta atento pri malplimultoj.”</p> <div style="text-align:right">Bardhyl S<span class="mm">ELIMI</span></div> 
