      <h3>Ekonomio</h3>
      <h3 class="tem">SERBIO-MONTENEGRO</h3>
      <h2>Novi Beograd disvolviĝas</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>Unu el la komunumoj de la ĉefurbo Beogrado estas Novi Beograd (Nova Beogrado). Ŝajnas, ke ĝiaj estroj sufiĉe bone organiziĝis kaj sukcesas daŭre disvolvadi sian komunumon. Uzante la monon de eksterlandaj investantoj, ili kreas ankaŭ novajn laborlokojn.</b></p> <p>La investoj ne venis per si mem. La komunumestroj sukcesis altiri ilin per kvalitaj projektoj kaj per investado en la infrastrukturo, precipe ordigado de la komunuma teritorio. Ĉio komenciĝis, kiam la slovena entrepreno <i>Mercator</i> (<a href="http://www.mercator.si">www.mercator.si</a>) aĉetis de la komunumo, je prezo de ĉirkaŭ ok milionoj da eŭroj, unuhektaran terpecon por konstrui komercan centron. Tiu aĉeto estis la unua eksterlanda investo en Beogrado post la dua mondmilito.</p> <p>Anstataŭ por salajroj, kiel ofte estis la praktiko, la mono estis uzita por ordigi la terenon de la komunumo apud la planita komerca centro. Estis konstruitaj kvar kilometroj da bulvardoj ĉirkaŭ la centro, kloakaro kaj akvoreto. Ĉiuj tiuj investoj kaŭzis, ke ĝuste en tiu urboparto oni konstruis novajn konstruaĵojn por aliaj, ĉefe eksterlandaj, grandaj entreprenoj kaj komercaj centroj. Krome, oni ĵus faris akvo-parkon sur areo de kvin hektaroj apud la rivero Sava.</p> <p>Ankaŭ la loĝej-konstruado progresas. Dum la jaro 2003 oni konstruis nur 359 loĝejojn, sed en 2004 estis konstruitaj 1943 kaj en 2005 jam 3000 novaj loĝejoj.</p> <div style="text-align:right">Zoran Ć<span class="mm">IRIĆ</span></div>  
