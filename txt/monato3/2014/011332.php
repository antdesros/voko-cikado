      <h3>Scienco</h3>
      <h3 class="tem">KURIOZAĴOJ</h3>
      <h2>Dilatiĝo, kuntiriĝo ... kaj eksperimento por infana amuziĝo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Inĝenieroj bone konscias la gravecon de dilatiĝo kaj kuntiriĝo, du komplementaj, „parencaj”, fenomenoj, kiuj konstateblas pli-malpli ĉe ajna materialo pro variado de la temperaturo.
   Stratoj pavimitaj per betono, ekzemple, montras kelkajn fendojn aŭ enhavas kelke da
   interspacoj plenigitaj per gudro. La celo de tiaj fendoj? Ebligi al betono dilatiĝi
   dum plej varmaj tagoj, sen provoki la rompiĝon de la pavimo.
</p> 
<p>Ankaŭ ĉe fervojaj reloj oni antaŭvidas etajn breĉojn pro la sama kialo: se ili estus
   kuniĝintaj en ununuran grandan pecon, ili kurbiĝus pro la puŝo de dilatado, kaj trajnoj
   povus elreliĝi. La telegrafaj, telefonaj kaj elektraj kabloj, aliflanke, estas ĉiam
   malstreĉitaj pro la mala kialo: dum la vintro, pro malvarmo, iliaj materialoj kuntiriĝas,
   kaj, se la kabloj estus streĉitaj, ili povus dispeciĝi. Fine la ĉeesto de ruloj, sur
   kiuj baziĝas pontoj, estas pravigata de tio, ke la metalaj partoj de pontoj emas dilatiĝi
   aŭ kuntiriĝi laŭ la ekstera temperaturo, kaj tio povus kaŭzi damaĝojn al la cetera
   konstruaĵo.
</p> 
<h3>Ĉarfaristoj</h3> 
<p>Estas fojoj, tamen, kiam la efiko de dilatiĝo kaj kuntiriĝo povas esti utila. Kiam
   pasintece ĉarfaristoj konstruis aŭ riparis la lignajn radojn de ĉaroj, ili tre varmigis
   ferajn radringojn, kiujn ili poste aplikis (kompreneble per rimarkinda peno) al la
   radoj, pro tio, ke la radringoj estis iom pli malgrandaj ol la radoj mem. Dum la radringoj
   malvarmiĝis, la materialo, per kiu ili estis konstruitaj, kuntiriĝis: ili tiumaniere
   pli kaj pli adheris al la radoj.
</p> 
<h3>Termometro</h3> 
<p>Tre disvastiĝinta kaj konata ilo, kies funkciado ja baziĝas sur la dilatiĝo kaj kuntiriĝo
   de likvaĵoj pro variado de temperaturo, estas termometro. La hidrarga termometro,
   ĉiam pli malofta en niaj hejmoj (pro tio, ke en pluraj landoj de Eŭropo ĝi estis jam
   malpermesita pro la danĝero pri poluado kaj infektado okaze de ĝia eventuala rompiĝo),
   inkluzivas maldikan vitro-tubeton, kiu finiĝas per ŝvelo nomiĝanta „bulbo”. La hidrargo entenata en la bulbo kaj – parte – en la vitro-tubeto dilatiĝas pro la varmo kaj supreniras ene de la tubeto. Malvarmo,
   male, igas ĝin kuntiriĝi kaj sekve malsupreniri.
</p> 
<p>Gradigita skalo kompletigas termometron kaj ebligas kontroli temperaturon. Ekzistas
   plurspecaj termometroj, inter kiuj la tiel nomata klinika termometro, uzata por mezuri
   la enan temperaturon de onia korpo (kvankam, kiel dirite, nuntempe oni uzas ĉiam pli
   ofte ciferecajn modelojn), kaj aliaj termometroj, kiuj kapablas registri la plej altan
   kaj la plej malaltan temperaturojn ene de unuopa tago.
</p> 
<h3>Eksperimento</h3> 
<p>Simpla, sed interesa, eksperimento, kiun ĉiu povos fari en sia hejmo por klarigi tiun
   fenomenon al etuloj scivolaj (kiaj ĉi-lastaj ĉiam estas), estas la jena. Oni havigu
   al si iom da ferdrato, unu grandan ŝraŭbon kun cilindra (rondforma) „kapo”, unu gasfornelon aŭ alispecan fonton de varmo kaj pinĉileton. Oni unue ĉirkaŭvolvu
   per ferdrato la kapon de la ŝraŭbo, kiel eble plej streĉe, estigante feran ringon
   ĉirkaŭ ĝi. Due, oni eltiru la ŝraŭbon kaj varmigu ĝin pere de fornelo (oni kompreneble
   helpu sin per pinĉileto por ne brulvundiĝi). Post tri minutoj oni malproksimigu la
   ŝraŭbon el la fajro kaj klopodu igi ĝin pasi enen de la ringo. Fine, oni samtempe
   varmigu la ŝraŭbon kaj la ringon mem kaj poste reprovu enmeti unu en la alian. 
</p> 
<h3>Volumeno</h3> 
<p>Oni vidos, ke post la unua fazo de la eksperimento la ferdrata ringo estas tro malgranda,
   por ke pasu en ĝin la (varmigita) ŝraŭbo. Sed, se oni varmigas ambaŭ objektojn, la
   ringo refoje sukcesos volviĝi ĉirkaŭ la ŝraŭbon. La kialo estas, ke, kiam ni varmigas
   objekton, ĝi dilatiĝas, tio estas akiras plian volumenon. Komence la ŝraŭbo pligrandiĝis
   pro la varmo, dum la ringo, restante malvarma, konservis sian komencan diametron.
   Tamen, kiam oni varmigis ankaŭ la ringon, ĝi mem dilatiĝis kaj iĝis iom pli granda
   ol la ŝraŭbo, kiun ĝi ree envolvis.
</p> 
