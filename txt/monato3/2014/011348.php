      <h3>Eseoj</h3>
      <h3 class="tem">SCIENCO</h3>
      <h2>Novtipa gasminado: ĉu vere danĝera?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Por ebligi la ekstraktadon de nafto kaj gaso el la tero, la ŝtonaroj, kiuj enhavas
   tiujn materialojn, devas havi porojn. Bone taŭgas, ekzemple, sabloŝtono kaj – parte – ankaŭ kalkŝtono. Por ekstrakti pli multe da nafto kaj gaso (kemie: metano, CH
   <sub>4</sub>), oni de post 1947 disrompas ŝtonaron per akvo kaj granda premo; tra la fendoj, kiuj
   tiel ekestas, fluidoj ja povas eliĝi. Oni ĵetas kune kun la akvo sablon, kiu tenas
   la fendojn malfermaj. Por ke la sablo ne sedimentiĝu tro frue, oni dispersas ĝin per
   organikaj kemiaĵoj. Anglalingve tiuspeca disrompado estas nomata „<i>fracking</i>”, mallongiĝo por „<i>hydraulic fracturing</i>” (hidraŭlika disrompado aŭ fendado).
</p> 
<p>Ekde 1981 tion eksperimentis George P. Mitchell, kaj poste – kun ŝtata subteno – la firmao 
   <i>Halliburton</i> en Teksaso por apliki la teknikon ankaŭ al multe pli densa kaj malmola ŝtonaro, kiel
   (nafto-)ardezo kaj aliaj skistaj materialoj, inter kiuj oni povus mencii argiloŝtonon.
   En 1998 la tekniko sukcesis, kaj tial ekde 2008 okazas vera furoro rilate al la ekspluatado
   de tiu novtipa gas- kaj nafto-minado.
</p> 
<h3>Ukraina registaro</h3> 
<p>En Usono gaskostoj intertempe malleviĝis al triono de tiuj de Germanio. Usono tiumaniere
   plibonigas ne nur sian industrian konkurpovon, sed ankaŭ sian klimatobilancon, pro
   tio ke gaselektraj centraloj pli kaj pli anstataŭas karboelektrajn, kiuj eligas ĉirkaŭ
   duoble da karbondioksido por ĉiu energi-unuo. Montriĝis, ke mondvaste la tiel alirebla
   konata kaj supozata provizo de ter-gaso (kaj ankaŭ de nafto) etendiĝis tiel, ke ĝi
   ne elĉerpiĝos en la venontaj cent jaroj aŭ pli (rigardu la mapon). Pro la aktuala
   politika situacio estas rimarkinde, ke ankaŭ Ukrainio havas tiajn proprajn provizojn.
   Sed la registaro de Janukoviĉ ĉesigis ĉi-rilatajn esplorojn en la somero de 2013 pro
   kialoj ne publike konataj.
</p> 
<p>Alia rezulto estas, ke la gas- kaj nafto-kostoj mondskale malleviĝis (kiam estas pli
   granda disponeblo aŭ malpli granda bezono, ekzemple de iam granda kliento, kiel Usono,
   la prezo malaltiĝas), dum la tradiciaj liver-landoj, ĉefe en Proksima Oriento, vendas
   malpli multe.
</p> 
<h3>Eŭropa glaciaĵo</h3> 
<p>En Eŭropo oni ankoraŭ hezitas uzi ĉi tiun teknikon, kaj kelkaj ŝtatoj prokrastis ĝian
   aplikadon. Homoj timas pri la „kemia koktelo”, kiun tiu tekniko entrenas.
</p> 
<p>Do kiaj estas tiuj dispersiloj? Por malebligi tro fruan sedimentiĝon de la sablo,
   oni uzas kiel densigilon (dispersilon) la tiel nomatan guar-gumon. Temas pri karbonhidrata
   biopolimero ekstraktata de la hinda guar-arbo. Ĝi estas uzata ankaŭ por iom solidigi
   manĝaĵojn, kiel 
   <i>soft</i>-glaciaĵon, bone konatan en Eŭropo.
</p> 
<p>Polimero estas parenca al amelo, sed, kiel poli-manano, ne estas digestebla por homoj,
   kvankam ĝi estas biologie diserigata en la medio. Por igi ĝin pli efika, oni normale
   ĝin retigas (interligante la ĉenojn) per bor-acido aŭ borakso. Pro novaj maltrankviloj
   pri tiuj borkombinaĵoj (kiuj tamen estas uzataj en manĝaĵoj kaj laviloj, kaj ankaŭ
   en medicino) oni nun preferas titan-kompleksojn, kiuj ne estas venenaj laŭ la nuna
   scio. (Titan-oksido estas la vaste uzata blanka pigmento de domfarboj. Cetere ankaŭ
   en tiuj farboj estas uzataj dispersiloj, plej ofte hidroksietil-celulozo, sed fojfoje
   ankaŭ guar-gumo. Tiuj estas ellavataj de la pluvo al la grund-akvo je kvantoj de multaj
   tunoj jare.)
</p> 
<p>Por ke ne kresku algoj, oni ankaŭ aldonas biocidojn, simile al tio, kio okazas ĉe
   murfarboj. Por ke la akvo povu esti depumpata post la disrompado, oni aldonas aŭ oksidilon
   (natrian peroksodisulfaton, Na
   <sub>2</sub>S
   <sub>2</sub>O
   <sub>8</sub>, kiu estas konsumata de la reago) aŭ enzimon, kiu disrompas la polimeron. Do la aldonaĵoj
   al la akvo estas tute ne maltrankviligaj.
</p> 
<h3>Tergloba varmiĝo</h3> 
<p>Sed, post la suprenpumpado, la akvo ja enhavas naftogutetojn, krom kelkajn ellavitajn
   salojn. Nur tiuj estas la danĝero, se estas liko en la bortrua (betona) tubo. Sed
   tio estas problemo ĉe ĉia gas- kaj nafto-minado, ne nur ĉe la disrompada. Alia danĝero
   ekestas, se oni suprenpumpas akvon en malfermajn konservejojn; tiam la solvita metano
   eliĝas kaj grave kontribuas al la tergloba varmiĝo. La akvobezono estas grandega:
   oni kutime uzas, en ripetiĝantaj disrompadoj, entute 30 000 kubajn metrojn da akvo
   por ĉiu borado. Precipe pro tio jam de du jaroj oni reuzas la akvon en sinsekvaj procedoj.
   Apartaj firmaoj purigas ĝin por ebligi la reuzadon. 
</p> 
<h3>Usona filmo</h3> 
<p>Oni povas sin demandi, kio do elvokis tiajn maltrankvilojn inter la homoj. Verŝajne
   multe kontribuis la usona filmo 
   <i>Gaslando</i>, kiu montris, kiel metanhava trinkakvo el akvokrano estis ekflamigata. Poste la Usona
   Mediprotekta Ofico (EPA) eltrovis per analizo de izotopoj (karbono-13 kaj deŭterio),
   ke la metano en ĝi ne estas geologie aĝa, sed devenas de la putrado de marĉa akvo
   sur la tera supraĵo. (Tia metano estas nomata „marĉa gaso”.) Estas interese, ke simila filmo (<i>Promesita lando</i>) estis kunfinancita de Unuiĝintaj Arabaj Emirlandoj, unu el la grandaj nafto- kaj
   gas-eksportaj landoj en la Persa Golfo. 
</p> 
