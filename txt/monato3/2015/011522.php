      <h3>Scienco</h3>
      <h3 class="tem">RADIOAKTIVECO</h3>
      <h2>La akcidento de Ĉernobilo 30 jarojn poste</h2>
      <!-- komenco de la enhavo -->

       

       <p>
   <b>Pasis preskaŭ 30 jaroj, sed multaj legantoj certe memoras, kio okazis la 26an de aprilo
      1986: en Ĉernobilo, ukraina urbo ĝis tiam sena je aparta famo, eksplodis nuklea reaktoro
      (kiun oni kutimas indiki per la siglo RBMK) havanta altan koeficienton de potenco.
      Temas pri la plej serioza nuklea akcidento iam okazinta en la homa historio.
   </b>
</p> 
<p>
   <b>Estis tiam eligitaj 5,2 milionoj da terabekereloj. (Bekerelo estas la mezurunuo de
      radioaktiveco, el la nomo de la franca fizikisto Antoine Henri Becquerel, kiu en 1896
      hazarde malkovris radioaktivecon.) Por kompreni la gravecon de tiu akcidento, la radiado
      provokita de la atomaj bomboj ĵetitaj sur Japanion dum la dua mondmilito havis apenaŭ
      centonan intensecon.
   </b>
</p> 
<h3>Malvarmiga akvo</h3> 
<p>La kaŭzoj de la akcidento estis pluraj: unue kulpis erara projektado de la centralo,
   sed ludis rolon ankaŭ serio da gravaj homaj eraroj. Dum rutina kontrolo rilate al
   la kapablo de la sistemo funkcii je malalta povumo, la reaktoro fariĝis nestabila,
   ĉar ĝi ne ricevis sufiĉe da malvarmiga akvo. Oni estis malaktiviginta kelkajn gravajn
   funkciojn de la sistemo, kiuj rilatis interalie al la eblo de aŭtomata estingado.
   Kiam la situacio forglitis el la manoj de la laboristoj, ili estis devigitaj klopodi
   haltigi la reakcion per alternativaj rimedoj.
</p> 
<h3>Radioaktiva brulaĵo</h3> 
<p>Dum tiu fina kaj senespera provo ili tamen kaŭzis potenc-pinton: ĉi-lasta do deĉenigis
   eksplodon, kiu dissaltigis la supran (ŝtalan) protektoplaton kun proksimuma pezo de
   1000 tunoj. Sekve de tio liberiĝis radioaktivaj vaporoj kaj fisiaj produktoj en la
   atmosferon. Post kelkaj sekundoj sekvis kroma eksplodo, kiu elĵetis fragmentojn de
   brulaĵo el la kerno de la reaktoro.
</p> 
<p>La reaktoroj de la klaso RBMK estis projektitaj sen la sekureca volbo el ŝtalo kaj
   betono, kiu ekzistas sur la plimulto de nukleaj instalaĵoj: tial post la eksplodo
   absolute neniu mekanismo povus iom reteni la radiadon. La reaktoro brulis dum dek
   tagoj, eligante almenaŭ kvin elcentojn de la 192 tunoj da radioaktiva brulaĵo tie
   ĉeestaj.
</p> 
<p>Vastaj areoj de la ĉirkaŭanta teritorio estis frapitaj: pli precize, 150 000 kvadrataj
   kilometroj da teritorio en Ukrainio, Belorusio kaj Rusio estis preskaŭ tuj deklaritaj
   poluitaj (kun niveloj de radiado, kiuj atingis 38 000 bekerelojn por kvadrata metro),
   ĉefe fare de izotopoj de cezio, stroncio kaj jodo.
</p> 
<h3>30-kilometra radiuso</h3> 
<p>La deviga evakuado estis poste etendita al areo de 4300 kvadrataj kilometroj, kaj
   oni devis permanente reloĝigi 336 000 personojn aliloke. Tri jardekojn post la akcidento
   la registaro de Ukrainio plue malpermesas vivi ĉirkaŭ la centralo en 30-kilometra
   radiuso. La nombro de la homaj viktimoj de la katastrofo estas temo de debato. En
   la loko de la akcidento du laboristoj mortis tuj, kaj 28 pliaj mortis ene de tri monatoj
   post sia elmetiĝo al 20-siverta radiado.
</p> 
<p>Tamen estas klare, ke la realaj ciferoj pri la mortintoj estas ege pli altaj. Ne ĉiam
   facilas kvantigi la efikojn de ies elmetiĝo al radiado: ĉe individuoj, kiuj estis
   en altaj radiad-niveloj, ne nepre ekestis sanaj problemoj, kiel ekzemple kancero,
   sed, kiam kancero okazis, ne facilas kompreni, ĉu la kaŭzo de la malsaniĝo ja estis
   la elmetiĝo al radiado aŭ alia ago, kiel fumado.
</p> 
<h3>Imagaj simptomoj</h3> 
<p>La Monda Organizaĵo pri Sano siaflanke asertas, ke tiu katastrofo estas provokinta
   aŭ iel ajn provokonta la morton de 4000 homoj, kaj ke, sekve de ĝi, la ĝenerala ofteco
   de kancero jam pliiĝis je 3 procentoj; la sama organizaĵo argumentas, tamen, ke la
   ĉefaj problemoj por la publika sano estas la psikologia deprimiĝo por tiuj devigitaj
   forlasi la domon en la poluitaj areoj, samkiel la plimalboniĝo de ilia ekonomia kondiĉo,
   kaŭzita de tiu transloĝiĝo.
</p> 
<p>Oni ankaŭ atentigas pri la emo, komuna al multaj uloj, pritimi malsanojn surbaze de
   nur imagaj simptomoj. Aliflanke, fakuloj de tiel nomata 
   <i>Academy of Science</i> (Akademio de Scienco) en Novjorko rimarkas, ke la Internacia Atom-Energia Agentejo
   (IAEA) kaj la Monda Organizaĵo pri Sano, inter aliaj, grandparte subtaksis aŭ tute
   malagnoskis la rezultojn de multaj esploroj plenumitaj de sciencistoj el orienta Eŭropo.
   Ili aldone kalkulis, ke pli ol 985 000 homoj jam mortis kiel rezulto de la akcidento
   en Ĉernobilo.
</p> 
<h3>Maltrankviliga aspekto</h3> 
<p>La plej maltrankviliga aspekto estas, ke la plimulto de la radioaktiva materialo plu
   kuŝas ene de la reaktoro. Ĝi estas protektita de „sarkofago” ŝtala kaj betona, konstruita kiel portempa solvo tuj post la katastrofo. Tamen tiu
   ujo estas laŭgrade difektiĝanta kaj el la centralo ja elfluas kelke da radiado. Oni
   do planis instaladon de nova uja strukturo ĝis la fino de 2015, kio protektos la ĉirkaŭan
   medion kaj la homan sanon dum tuta jarcento.
</p> 
