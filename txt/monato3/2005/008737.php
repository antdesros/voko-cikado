      <h3>Politiko</h3>
      <h3 class="tem">LITOVIO</h3>
      <h2>Fiereco antaŭ la falo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Fiaskis la ministra kariero de la estro de la plej sukcesa politika partio en Litovio en la parlamentaj elektoj de 2004. Post duonjaro kiel ministro pri ekonomio Viktor Uspaskich, estro de la Labora Partio, rezignis pri sia posteno. Li restas tamen partiestro.</p> <p>Komisiono, kiu esploris la agojn de la ministro, konstatis, ke Uspaskich, sen aprobo de la registaro en Moskvo, komencis fondi komunan rus-litovan entreprenon. La ministrejo, gvidata de li, misuzis tiucele financan helpon de Eŭropa Unio. Evidentiĝis, ke du kompanioj, ligitaj al la ministro, prezentis du projektojn kaj ricevis monhelpon pli altan ol jure permesite.</p> <h3>Diplomoj</h3> <p>Kaj ankoraŭ pli. En sia biografio Uspaskich deklaris, ke li studis perkoresponde ĉe moskva naci-ekonomia instituto kaj en 1993 finis siajn bakalaŭrajn studojn. Poste la politikisto akiris magistran kaj doktoran diplomojn. Tamen malgraŭ tio, ke kopio de la moskva diplomo kuŝis en la universitato de Kaunas, la nomo Uspaskich ne staras en la listo de finintoj en la jaro 1993.</p> <p>La 46-jaraĝa eksministro, ŝajne la plej riĉa litova politikisto, naskiĝis en Rusio kaj en 1985 translokiĝis al Litovio. Post sukcesa komerca kariero li fondis la Laboran Partion, kiu baldaŭ fariĝis politika potenco. Ne klaras, ĉu la falo de la estro damaĝos la partion.</p> <div style="text-align:right"><span class="mm">LAST</span></div>  
