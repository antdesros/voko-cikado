      <h3>Eseoj</h3>
      <h3 class="tem">POEZIO</h3>
      <h2>Geraldo Mattos kaj Sutte Allá</h2>
      <!-- komenco de la enhavo -->

       

       <p>Ekde la jaro de mia esperantiĝo, mi havis la honoron kaj plezuron konatiĝi kun kelkaj
   el la plej elstaraj esperantaj poetoj kaj verkistoj, ekzemple: William Auld, Georges
   Lagrange, Claude Piron, Reto Rossetti, kaj tiel plu. Eble unu el la plej simpatiaj
   kaj interesaj el tiu rimarkinda grupo estis la brazila filologo kaj poeto Geraldo
   Mattos Gomes dos Santos, kiu naskiĝis en 1931, kaj bedaŭrinde mortis la 23an de marto
   2014. La lasta fojo, kiam mi vidis kaj parolis kun li, estis en Montpeliero. Li estis
   distinga maldika viro, kiu portis junaspektan harvoston.
</p> 
<p>Plurfoje Mattos kaj mi korespondis, precipe post la jaro 1998, kiam li estis redaktoro
   de la rubrikoj „Poezio” kaj „Noveloj” de ĉi tiu magazino, kaj mi de tempo al tempo kontribuis al la rubriko „Noveloj”. Li ĝentile akceptis miajn kontribuaĵojn, sed atentigis min, ke, laŭ li, kelkaj el
   la skribaĵoj, kiujn mi sendis al li, ne estas vere noveloj, sed io, kio en la brazilportugala
   lingvo nomiĝas 
   <i>crônicos</i>, t.e. „kronikoj”, nome, verbaj scenoj el la ĉiutaga vivo.
</p> 
<p>Verŝajne por klarigi kion li celis, li sendis al mi perpoŝte grandan pakaĵon de tre
   spritaj „kronikoj” en la portugala, supozeble verkitaj kaj publikigitaj de li (kvankam tion li ne klarigis).
   Mi legis ilin kun granda plezuro kaj amuzo, kaj eble senkonscie mi klopodis iom imiti
   lian stilon de verkado.
</p> 
<p>Tamen, ne nur pri miaj kontribuaĵo ni korespondis. Mi ĉiam admiris la poezion de Mattos.
   Aparte fascina mi trovis versaĵon lian en frua zamenhofa 
   <i>praesperanto</i>, kun la titolo „Sutte Allá”, kiu aperas sur paĝo 478 de la jubilea kolekto 
   <i>Esperanta Antologio – Poemoj 1887-1981</i> (UEA, Roterdamo, 1984), kaj estis dediĉita al la distinga esperanta eldonisto J.
   Régulo Pérez. Jen la poemo en praesperanto:
</p> 
<h3>Sutte Allá</h3> 
<p>Tre madja verb' esté i ĉiu kotto,
   <br></br> a maro e vivor' blessanta uroj 
   <br></br> sej manku fu ple pallo e ple otto,
   <br></br> odassa koj bal urboj e bal guroj ...
</p> 
<p>Kor no esté prerokoj i forvagoj
   <br></br> de ĉiaj tiskaj maro e abio,
   <br></br> e na moedo kreskigé mortagoj
   <br></br> e os mortagoj kreskigé na krio! ...
</p> 
<p>Ro kojpu palli fej al surda mondo
   <br></br> dum skribu ra pied; plenanta pondo,
   <br></br> kvul ŝemmo de un dio travuj beje.
</p> 
<p>Avionage ĉii, ro ne sidu
   <br></br> bal moa roko, sej ro pelle cidu:
   <br></br> faktassa es esté pu fakti preje!
</p> 
<p>En 
   <i>Lingvo kaj vivo</i>, skribante pri tiu praesperanta lingvo, Waringhien faras interesan komenton sub la
   titolo 
   <i>Mia penso</i>. Li skribas: 
   <i>Estas sendube fantazio mia – sed, se iam, por ia aparta efekto, tradukisto (aŭ eĉ originala verkisto) bezonus
      doni la impreson de arĥaismo, ĉu ne estus dezirinde, ke li helpu al si per tiu malnova
      dialekto de Esperanto ...?
   </i>.
</p> 
<p>Tio ŝajnas al mi tre bona sugesto por iu ajn, kiu verkas aŭ esperantigas historian
   romanon.
</p> 
<p>Plurfoje mi petis kaj perletere kaj persone al Mattos, ke li sendu al mi version en
   moderna Esperanto de tiu mistera poemo, sed mi neniam ricevis tradukon. Li mem diris
   ke li estas forgesema homo, do certe tio estis la kialo. Pro tio mi provis fari mian
   propran tradukon. Unue mi skribis al William (Vilhelmo) Auld, la redaktinto de la
   Antologio, petante klarigon de la praesperanta teksto. Li afable sendis al mi liston
   da kelkaj praesperantaj vortoj, kaj la ceterajn mi trovis en la ĉapitro „Pra-Esperanto” de la verko 
   <i>Lingvo kaj vivo</i> de Gaston Waringhien (La Laguno, 1959), kiu multe helpis min.
</p> 
<p>Ĉu mia versio estas pli-malpli ĝusta, mi ne povas certigi, sed jen ĝi:</p> 
<h3>SUTTE ALLÁ – ZORGE IRIS</h3> 
<p>(Prova traduko el Praesperanto 1881)</p> 
<p>Tre multa vort' estas en ĉiu flanko,
   <br></br> al maro kaj vivul' vundanta orelojn,
   <br></br> sed mankis tiu sen parolo kaj sen aŭdo,
   <br></br> aŭdita nur sur l' urboj kaj sur landoj ...
</p> 
<p>Ĉar estas ni rokegoj en forvagoj
   <br></br> ĉe ĉiaj plaŭdaj maro kaj arbaro,
   <br></br> kaj nia miec'* kreskigas mortintojn
   <br></br> kaj ankaŭ mortintoj kreskigas nian krion! ...
</p> 
<p>Li komencis paroli tial al la surda mondo,
   <br></br> dum skribis lia pied' plenanta respondon,
   <br></br> kiun sun' de unu tago sekigos poste.
</p> 
<p>Fluginte ĉie, li ne sidis
   <br></br> sur l' muta roko, sed li plue decidis:
   <br></br> farita ankoraŭ estis por fari troe!
</p> 
<p>Kvankam stranga, mi trovis tre bela ĉi tiun omaĝon en praesperanto al J. Régulo Pérez
   kaj mi esperas, ke pli da homoj legos kaj aprezos ĝin. (Mi demandas min, ĉu la „muta roko” estas la Kanariaj Insuloj, la hejmo tiu fama hispana eldonisto en Esperanto.)
</p> 
<h6 class="pied">* egoismo?</h6> 
