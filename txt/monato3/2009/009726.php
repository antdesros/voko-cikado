      <h3>Politiko</h3>
      <h3 class="tem">OKCIDENTA AFRIKO</h3>
      <h2>Solidaras soldatoj  sed ĉu por demokratio?</h2>
      <!-- komenco de la enhavo -->

       

       <p>Eksigita estas Gvineo el la Ekonomia Komunumo de Okcident-Afrikaj Ŝtatoj (EKOAŜ) post eksterordinara kunveno de la komunumo kunvokita de ties prezidanto, la niĝeria prezidanto Shehu Musa Yar'Adua.</p> <p>La komunumo reagis al la starigo de junto sub majoro Moussa Dadis Camara post la morto de la diktatora prezidanto de Gvineo, generalo Lansana Conte, kiu kaptis la potencon dum ŝtatrenverso en 1984. Ekde tiam Conte rezistis provojn demokratiigi la landon kaj, malgraŭ reformoj, kiuj devigis lin enpostenigi ĉefministron, daŭre retenis potencon.</p> <p>La subita morto de Conte, en decembro 2008, semis konfuzon en Gvineo kaj lasis truon en la lando, kiun malaltrangaj soldatoj sub Camara tuj plenigis. Ili avertis generalojn lojalajn al Conte, ke ili ne enmiksiĝu en la aferon, ĉar tiel ili povus malstabiligi la landon. La generaloj ŝajne aŭskultis kaj la Nacia Konsilantaro por Demokratio kaj Evoluigo, regata de Camara, promesis organizi elektojn ene de tri jaroj.</p> <p>Kontraŭis EKOAŜ, insistante, ke okazu elektoj (planitaj antaŭ la morto de Conte) ene de unu jaro. Pro tio la komunumo suspendis Gvineon.</p> <p>Simile reagis la komunumo, kiam soldatoj en Togo akaparis la registaron post la morto de generalo Eyadema. Tiam ĝi organizis sankciojn kaj blokadon. Tion riskas ankaŭ Gvineo, se la reganta soldataro ne tuj iniciatas demokratiigon de la lando.</p> <p>Partoprenis la eksterordinaran kunvenon en Abuĝo, ĉefurbo de Niĝerio, la prezidantoj de Niĝerio, Liberio, Ebur-Bordo, Togolando, Gambio, Burkino, Ganao kaj Siera-Leono. Ĉeestis ankaŭ la ĉefministro de Niĝero kune kun reprezentantoj de aliaj ŝtatoj.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> 
