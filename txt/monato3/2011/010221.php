      <h3>Spirita vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Einstein havis okulojn malfermitajn</h2>
      <!-- komenco de la enhavo -->

       

       <p><b>La 28an de oktobro mortis en malsanulejo frato G. F. De Ruyver [rejvr]. Lia ĉi-suba kontribuo atingis la eldonejon de M<span class="mm">ONATO</span> la 2an de novembro. Temas do pri la lasta aŭ unu el la lastaj verkoj de la forpasinto, kaj la fina frazo en la artikolo estas do kvazaŭ lia „lasta deziro” ...</b></p> <p>En la Biblio troviĝas multaj tekstoj, kiuj komprenigas al ni, ke la vido de la naturo, de la tuta kreaĵaro, povas konduki nin al Dio. Jen du ekzemploj.</p> <p>„Mi gloras Vin (=Dio), ĉar mi estas mirinde kreita; Mirindaj estas Viaj kreitaĵoj” (Psalmaro 139:14).</p> <p>„Benu la Sinjoron, vi, montoj kaj montetoj: laŭdu kaj gloru Lin por eterne. Benu la Sinjoron, vi, ĉiuj plantoj de la tero: laŭdu kaj gloru Lin por eterne” (<i>Laŭdkanto</i> en Danielo 3:75-76).</p> <p>La laŭdkanto de Danielo estis tre kara al Francisko de Asizo kaj lia fama <i>Sunkanto</i> sendube estis inspirita de ĝi. „Laŭdata Vi estas por nia fratino, nia patrino, la tero, kiu nin portas kaj nutras, kiu produktas ĉiaspecajn fruktojn, multkolorajn florojn kaj herbojn!” Malfermitaj estis la okuloj de Francisko, malfermita estis ankaŭ lia koro.</p> <p>Rilate la kredon al Dio oni ofte citas eldirojn de famaj personoj. Tamen en ĉi tiu temo tiuj famuloj tute ne estas pli kompetentaj ol iu ajn el ni. Sed estas probable, ke iliaj belaj atestoj iom partoprenas en ilia famo.</p> <p>Ni ĉiuj vidas ĉirkaŭ ni mirinde belan, nesondeblan mondon. En tio ni ĉiuj povas malkovri amoplenan agadon, klaran revelacion de ĉiopova Dio. Por tion fari oni ne bezonas erudicion, sed ja modestecon kaj sindonemon.</p> <p>Estas vere, ke en tiu mondo regas ankaŭ multe da suferoj kaj malbonaĵoj. Parte tio ligiĝas kun ĉi tiu materia, vundebla kreaĵaro. Sed – kiel bedaŭrinde – plejparte la homaro kaŭzas tion al si mem. Tamen tiuj multaj malbonaĵoj ne povas al ni forgesigi la neeldireblan multon da bonaĵoj, kiuj parolas al ni pri amoplena, ĉiopova Dio.</p> <p>Konsentite, ke la motivoj por kredi ne estas same klaraj kiel du plus du estas kvar. Sed ĝuste pro tio nia kredo estos tute persona, plene libera amoplena ekkono de la kreinto.</p> <p>Sekvas du atestoj, kiuj povas nin impresi, ne pro la famo de la persono, sed per si mem.</p> <p>Iam Edison, vizitinte la Eiffelturon, skribis en la gastlibro: „Edison, kiu alte estimas kaj honoras ĉiujn inĝenierojn, inkluzive la bonan Dion.”</p> <p>Kaj Einstein opiniis jene: „La plej bela sento, kiun homo povas sperti, estas la sento pri la mistero. Kiu neniam spertis tiun senton, kiu neniam prenis tempon por admire stari en raviĝo, plena de respekto, tiu homo estas kvazaŭ morta. Liaj okuloj estas fermitaj.” Ni funde pripensu ĉion ĉi.</p> <p>Vi, kiu legis ĉi tiun artikolon, bonvolu ĝin diskonigi en via gepatra lingvo. Varman dankon.</p> <div style="text-align:right">G. F. D<span class="mm">E</span> R<span class="mm">UYVER</span></div> 
