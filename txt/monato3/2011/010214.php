      <h3>Aktuale</h3>
      <h3 class="tem"></h3>
      <h2>Parolas posteulo</h2>
      <!-- komenco de la enhavo -->

       

      <p>Stefan Maul sinonimas kun M<span class="mm">ONATO</span>. Stefan kunfondis, formis kaj dum jardekoj ame flegas nian magazinon. Ĉefe legantoj, sed ankaŭ ĉiuj kunlaborantoj, ŝuldas al li multon. Sinceran dankon pro magazino unika en nia verda mondo: magazino, kiu raportas ne pri, sed per Esperanto, kiu intence turnas sin for de iufoje febra esperanta mondeto al iufoje same febra, sed multe pli vasta ekstera mondo. Stefan dediĉis sin multflanke al Esperanto, interalie kiel aŭtoro, poeto, akademiano, sed sendube M<span class="mm">ONATO</span> fariĝos lia monumento.</p> <p>Nun, pro bonaj kaj kompreneblaj kialoj, Stefan decidis rezigni pri la ĉefredaktoraj devoj. Ilin li transdonas al mi. Granda honoro, pri kiu mi ne tute certas, ĉu mi indas: la magazino perdas profesian, ĝisostan ĵurnaliston kaj akiras maksimume duonprofesian. Jes, mi instruas ĵurnalismon ĉe brita universitato kaj trejniĝis kiel ĵurnalisto antaŭ pli ol 30 jaroj; intertempe, tamen, kaj plejparte, mi perlaboris mian ĉiutagan panon, instruante universitatnivele la germanan.</p> <h3>Principoj</h3> <p>Sed, dum tiu ĉi periodo, mi kontribuis al M<span class="mm">ONATO</span>, artikolante kaj, iom poste, redaktante la rubrikojn <i>Politiko</i> kaj <i>Moderna vivo</i>. La magazino ebligis al mi, lingvo-instruisto, reteni kontakton kun la ĵurnalismo. Samtempe mi rekonis la valoron de M<span class="mm">ONATO</span>, pli kaj pli respektante ties principojn, kiujn nun kiel ĉefredaktoro mi defendos.</p> <p>Kio pri la estonteco? Ŝanĝi nian magazinon mi ne volas; laŭeble plifortigi, jes. Por tio mi bezonas interalie la helpon de legantoj: legantoj, ekzemple, el landoj, kiuj malofte reprezentiĝas inter la kovriloj de M<span class="mm">ONATO</span>. Legantoj pretaj verki pri protestoj en Francio kontraŭ altigo de la pensiaĝo; pri la dekstrema tiel nomata tefesta partio en Usono; pri la provoj de Putin denove prezidantiĝi; pri la burleskaĵoj de Berlusconi ktp ktp. Ne mankas temoj, sed ie-tie mankas verkantoj por kvante kaj kvalite ŝveligi la enhavon de niaj rubrikoj.</p> <p>Tiamaniere, kun via helpo, kun la subteno de Flandra Esperanto-Ligo, ni povos daŭrigi la M<span class="mm">ONATO</span>-tradicion grandparte establitan de Stefan Maul. Humile, iom nervoze, mi tamen antaŭĝuas sukcesan kaj fruktodonan kunlaboron.</p> <div style="text-align:right">Paul G<span class="mm">UBBINS</span></div>  
