      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Kio estas sklavo kaj kiu estis Kristo</h2>
      <!-- komenco de la enhavo -->

       

     <p><i>Arne la ĉefido</i> estas eta libro de iom pli ol cent paĝoj pri la dekjara knabo Arne. La rakonto okazas dum la periodo de la vikingoj. Arne estas la filo de la ĉefo Björn. Ili loĝas en malgranda vilaĝo en Svedio ĉe golfeto, kie vivas liberaj homoj kun siaj sklavoj.</p> <p>Arne lernis de sia patro, ke sklavoj devas labori kaj ke li ne rajtas ludi kun la sklavidoj. Li kutimis ludi kun Trumbe, sklavido, kiu estis samaĝa kiel li. Li obeas sian patron, sed ne bone komprenas, kial li ne rajtas amuziĝi kun Trumbe. Kiam li ekscias, ke ankaŭ liberaj homoj povas estiĝi sklavoj, li komencas pripensi, kiel estas ilia vivo. Lia amiko Gunnar, unu el la liberaj infanoj, tute ne okupiĝas pri tiaj aferoj.</p> <p>Ankaŭ Kristo estas io, pri kio Arne multe pensas kaj demandas. Li rimarkas, ke multaj sklavoj kaj la fremduloj faras krucsignon sur la frunto, kaj li volas scii kial. Ĉi tiuj gravaj kaj seriozaj temoj estas miksitaj kun pli streĉaj partoj – ĉasado de alkoj, alveno de fremduloj, fuĝo pro piratoj ktp – tiel ke la rakonto certe neniam enuigas dum la legado.</p> <p>La libreto taŭgas por sufiĉe junaj infanoj por laŭtlegi kaj por iom pli aĝaj, kiuj jam bone memstare legas. Kio estas sklavo kaj kiu estis Kristo estas bone klarigitaj, tiel ke ĉiu infano facile povas sekvi, pri kio temas. Ankaŭ la resto de la rakonto estas facile sekvebla kaj bone ellaborita, kaj la tuto estas bele verkita en facila lingvo.</p> <p>La libro estas presita en malgranda formato kaj enhavas kelkajn nigra-blankajn, simple desegnitajn bildojn de Pavel Rak. Mi rekomendus al ĉiuj gepatroj aĉeti ĉi tiun libron por siaj infanoj, ĉar ĝi vere indas kaj estas tre amuza por legi.</p> <div style="text-align:right">Karlijn V<span class="mm">AN</span> D<span class="mm">AMME</span></div> <h6 class="bib">Leif Nordenstorm: Arne, la ĉefido. Eld. Kava-Pech, Dobřichovice, 2006. Ilustris: Pavel Rak. 112 paĝoj glubinditaj. ISBN 80-85853-77-9.</h6>   
