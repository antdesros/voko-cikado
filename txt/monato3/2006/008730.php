      <h3>Moderna vivo</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Vendejen!</h2>
      <!-- komenco de la enhavo -->

       

       <p>Evoluas nuntempe t.n. 100-enaj vendejoj en Japanio. Valoras 100 enoj proksimume unu usonan dolaron; afranko de letero al aziaj landoj kostas 90 enojn, al Usono aŭ Eŭropo 110 enojn. Do por japanoj 100 enoj estas simbolo de „malmulta kosto”.</p> <p>Kiam aperis en la lasta duono de la 1980aj jaroj la unuaj 100-enaj vendejoj, kie ĉio aĉeteblas kontraŭ 100 enoj, multaj homoj pensis, ke la varoj estas malmultekostaj kaj do malbonaj. Tamen tiu ĉi impreso pli kaj pli malaperis, tiel ke nun regas tre favora opinio pri la vendejoj.</p> <p>La plej granda kompanio pri 100-enaj vendejoj estas Daisō, kiu posedas 2400 vendejojn enlande kaj 350 eksterlande. Ĝia vendosumo atingis 50 miliardojn da enoj en 1997, 200 miliardojn en 2000 kaj 320 miliardojn en 2005. Ĉe Daisō oni vendas varojn por ĉiutaga vivo – preskaŭ ĉion krom freŝajn manĝaĵojn kiel legomojn, fiŝojn aŭ viandojn.</p> <h3>Konkurenco</h3> <p>Sed, konkurence kontraŭ Daisō, en 2000 alia kompanio malfermis siajn t.n. vendejojn 99, kie aĉeteblas kontraŭ 100 enoj ankaŭ freŝaj manĝaĵoj. La kompanio nun havas 580 vendejojn en Tokio kaj ĉirkaŭe kaj, kompare kun marto 2003, ĝia vendosumo kvarobliĝis. Kontraŭ la novstilaj 100-enaj vendejoj, la t.n. oportunaj vendejoj komencis oferti freŝajn manĝaĵojn kontraŭ 100 enoj. Planataj ekzemple de la firmao Lawson estas 1000 tiaj vendejoj.</p> <p>Malantaŭ tiuj interbataloj kuŝas kelkaj kialoj, ekz. sociaj kaj familiaj ŝanĝiĝoj (la apero de maljunaj paroj, kies gefiloj sendependiĝis; junaj paroj sen gefiloj; senfamiliaj unuopuloj: por tiuj tre utilas la ebleco aĉeti malgrandkvante). Ankaŭ kreskanta malriĉeco en Japanio kontribuas al la florado de 100-enaj kaj similaj vendejoj: kiam Japanio ekonomie „bolis”, oni hontis eniri malaltprezajn butikojn.</p> <p>Analizisto diris, ke jam finiĝis la epoko, kiam apartaj specoj de vendejoj, ekz. magazenegoj, superbazaroj, oportunaj vendejoj kaj 100-enaj vendejoj, dependas de apartaj klientaj tavoloj. Venis la epoko, kiam ĉiuj vendejoj interbatalas por kapti pli da klientoj el ajnaj sociaj tavoloj.</p> <div style="text-align:right">H<span class="mm">ORI</span> Jasuo/pg</div> 
