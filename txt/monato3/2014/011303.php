       <h3>… pri laŭroj</h3> 
<p>Gratulojn al la gazetoj 
   <i>Washington Post</i> kaj 
   <i>Guardian</i>, kiuj en aprilo ambaŭ ricevis la plej prestiĝajn usonajn ĵurnalismajn laŭrojn, nome
   la premion Pulitzer.
</p> 
<p>La du gazetoj – unu usona, la alia brita – estis premiitaj pro siaj raportoj pri la agado de la usona Nacia Sekureco-Agentejo
   (NSA) surbaze de t.n. likigoj fare de Edward Snowden.
</p> 
<p>Snowden montris interalie, kiom la agentejo, subtenata de paralelaj organizaĵoj, ekzemple
   en Britio, spionadis telefonajn kaj aliajn komunikaĵojn ne nur de ordinaraj civitanoj
   sed ankaŭ de ĉefoj (ekzemple Angela Merkel) de laŭsupoze amikaj kaj samaliancaj ŝtatoj.
</p> 
<p>Hodiaŭ 
   <i>Washington Post</i> kaj 
   <i>Guardian</i>: ĉu morgaŭ – kia bela revo! – M<span class="mm">ONATO</span>? Dubinde ... sed kia propaganda pluso por Esperanto!
</p> 
<p>Bedaŭrinde, malgraŭ korespondantoj en multaj landoj (verŝajne pli ol disponeblas al
   multaj naciaj gazetoj), mankas al M<span class="mm">ONATO</span> la financaj rimedoj por subteni longajn kaj temporabajn esplorojn en la sekretaj,
   senlumaj keloj de registaroj kaj grandaj kompanioj, protektataj de densa leĝaro kaj
   alte salajrataj advokatoj.
</p> 
<p>Pli grave, ni ne estas profesiaj ĵurnalistoj aŭ esploristoj. Ni havas aliajn postenojn,
   pri kiuj ni dependas por surtabligi nian ĉiutagan panon; ni havas familiajn, sociajn
   kaj aliajn devojn kaj respondecojn. Tamen tutcerte, sub niaj nazoj, estas sekretoj,
   kies gardistoj ne volas, ke tiuj estu publike ekzamenataj.
</p> 
<p>Baza maksimo ĵurnalisma estas la jena: se iu, ie, deziras ion kaŝi, jen ĵurnalismo:
   ĉio alia estas reklamado. Pripensu: la registaro, regiona parlamento, anoncas vastan
   projekton, kiu kreos centojn da laborlokoj. Bone ... jen novaĵo, nepre raportinda
   en novaĵ-bulteno. Tamen samtempe temas pri reklamado: vidu, kiel ni helpas niajn civitanojn,
   ilin subtenas ktp.
</p> 
<p>Aliflanke, kiam oficialulo, funkciulo, respondas al demando per „Komenti mi ne volas”, tiam jukas la ĵurnalista nazo: jen iu, kiu volas ion kaŝi ... do kial?</p> 
<p>Mi ne tiel pledas por artikoloj pri konspirteorioj. Ilin ni lasu al la interretaj
   forumoj kaj al tiuj, kiuj havas tro da tempo por teoriumi, maltro da pacienco por
   funde esplori.
</p> 
<p>Tamen ĉu ne estus bone, se iam M<span class="mm">ONATO</span> povus malkaŝi al la mondo grandan publikan skandalon, krimaron kaŝitan, trompadon,
   kaj tiel kontribui al pli malfermita, pli justa mondo? Jen ... premion Pulitzer mi enmense
   vidas, preskaŭ flaras, tuŝas.
</p> 
<p>Intertempe ĉiam eblas revi.</p> 
<h3> … kaj pri flaŭroj</h3> 
<p>Revemuloj estas ankaŭ membroj de la brita konservativa registaro, kiuj subtenas novan
   altrapidecan trajnlinion por kunligi la ĉefurbon Londono kun provincaj grandaj urboj
   en la nordo de Anglio.
</p> 
<p>Britio havas nur unu tian linion (kontraste kun ekzemple la franca reto), nome tiun
   inter la submarkola tunelo kaj Londono. Proponata nun estas nova linio, kiu kontraŭ
   miliardoj da pundoj ebligos ekzemple al komercisto en Manĉestro atingi la ĉefurbon
   eventuale 20 minutojn pli rapide ol nun.
</p> 
<p>Ĉu necese? Britio estas dense loĝata insulo kaj konstrui tian fervojon signifos detrui
   ne nur centojn da hejmoj en la metropoloj, sed ankaŭ atenci florojn kaj flaŭrojn de
   la angla kamparo.
</p> 
<p>Kontraŭuloj atentigas, ke estus pli saĝe elspezi la monon por plibonigi la ekzistantan
   reton favore al ordinaraj vojaĝantoj, nek alte salajrataj nek jam dorlotataj. Cetere
   la nunaj planoj enhavas truon: ne eblos trajni ekzemple el norda Anglio rekte al Bruselo
   aŭ Parizo. Ankoraŭ necesos trajnŝanĝi en Londono.
</p> 
<p>Certe mi, kiu forte subtenas fervojojn, pli entuziasmiĝus pri la projekto, se mi povus
   veturi rekte el mia norda regiono al Belgio aŭ Francio kaj tiel eviti Londonon. Se
   ne, mi konkludos simple, ke la proponata fervojo estas vanteco-projekto, kiu enŝutos
   monon en la poŝojn de konstrufirmaaj amikoj de la registara, konservativa partio.
</p> 
