      <h3>Politiko</h3>
      <h3 class="tem">NIĜERIO</h3>
      <h2>Prezidanto-vico</h2>
      <!-- komenco de la enhavo -->

       

       <p>En januaro 2011 niĝerianoj elektos novan prezidanton. La nuna prezidanto, Goodluck Jonathan [gudlak ĝxónatan], la iama vicprezidanto, transprenis la povon en majo 2010 post la morto de la antaŭa ŝtatestro, Umaru Musa Yar'Adua.</p> <p>Jonathan fariĝis la unua prezidanto el la niĝeria minoritato. Niĝerio havas pli ol 450 etnojn, sed la tri ĉefaj etnoj, aŭ gentoj, Igbo, Haŭsa kaj Joruba, bataladas por regi la landon. Dum la armea regado la generaloj, kiuj fariĝis ŝtatestroj, devenis de la norda, islama, haŭsa parto de Niĝerio. En 1999 ĉio ŝanĝiĝis, kiam prezidantiĝis eksa generalo Olusegun Obasanyo el la okcidenta, joruba parto de Niĝerio.</p> <h3>Nafto-riĉa</h3> <p>La nuna prezidanto, kiu devenas de la nafto-riĉa suda Niĝerio, jam anoncis sian kandidatiĝon. Kontraŭas lin kandidatoj el la nordo, kiuj opinias, ke venis la vico de norda prezidanto. La suduloj replikas, ke Yar'Adua, nordulo, mortis post tri jaroj da prezidanteco kaj do restu ĉe ili la prezidanta posteno.</p> <p>Subtenas la kandidatecon de Jonathan – cetere niĝerianoj taksas lin bonŝanca pro lia persona nomo, Goodluck, kiu signifas „bonŝanco” – minoritataj ŝtatoj kaj el sudo kaj el nordo. Apogas lin jam 40 politikaj partioj el la nunaj 63 en Niĝerio.</p> <p>Kandidatecon anoncis aliaj homoj, i.a. la armea prezidanto Babangida, mem ŝtatestro inter 1985 kaj 1993. Tiu okazigis preskaŭ internan militon en Niĝerio, kiam li renversis la popularan venkon de prezidanto Abiola. Dum la protestoj mortis miloj da niĝerianoj.</p> <div style="text-align:right">Princo Henriko O<span class="mm">GUINYE</span>/pg</div> 
