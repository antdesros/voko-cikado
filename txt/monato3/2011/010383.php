      <h3>Politiko</h3>
      <h3 class="tem">IRLANDO</h3>
      <h2>Mikspoto politika</h2>
      <!-- komenco de la enhavo -->

       

       <p>Dum la historio de la moderna irlanda ŝtato la lando estis alterne regata de unu el du partioj ideologie nedistingeblaj, Nur la nomoj, kaj la fakto, ke ili devenas de la du kontraŭaj flankoj dum la irlanda enlanda milito de 1922-1923, ilin apartigas.</p> <p>La rezultantaj politikaj partioj, nome FF kaj FG, estis kiel du ludantoj de tabloteniso, kiuj frapas la pilkon tien kaj reen, sed gajnas kaj ŝanĝas nenion.</p> <p>Pro tio, ke ambaŭ partioj (sed precipe FF, la tiel nomataj batalantoj de la destino), kun helpo de internaciaj kaj naciaj bankoj, efektive detruis la ekonomion de la Irlanda Respubliko, granda parto de la voĉdonanta publiko seniluziiĝis pri tiuj du dekstremaj grupoj.</p> <h3>Konservativa</h3> <p>Rezulte, el ĉiuj niĉoj kaj anguloj de la politika ĝardeno, subite eksvarmis kiel kuloj novaj partioj kaj frakcioj. La plimulto, sed ne ĉiuj, montras sin pli maldekstrema ol kutime en ĉi tiu tradicie konservativa, katolika, malradikala nacio. Inter tiuj novuloj oni povas listigi kelkajn, nome:</p> <p><i>New Vision</i> [nju viĵon], angle, „nova vizio”: pli-malpli radikala alianco de sendependuloj.<br/> <i>Fis Nua</i> [fiŝ nua], irlandgaele, „nova vizio”: malgraŭ la sama signifo de la du nomoj, <i>Fis Nua</i> neniel rilatas al <i>New Vision</i>, kaj ŝajnas esti pli maldekstrema.<br/> <i>Éirigí</i> [ejrigi], irlandgaele, „leviĝu!”: socialisma, respublikisma partio, kies insigno estas tiu, kiun ĝi mem nomas la „Esperanto-stelo”; la partio pretendas apogi Esperanton kiel internacian helplingvon (kio ne tre klarigus la zamenhofan lingvon al la irlanda publiko, ĉar <i>Éirigí</i> estas rigardata iom ekstremisma.)<br/> ULA, aŭ Unuiĝinta Liva (= maldekstrema) Alianco: loza alianco, kiu inkludas la Socialistan Partion de Irlando, la premgrupon „Popolo anstataŭ profito”, asocion de senlaboruloj, kaj eble kelkajn aliajn grupojn kaj sendependulojn.</p> <p>Vera miksita salato. Ne antaŭvideblas, ĉu iuj aŭ ĉiuj sufiĉe influos la evoluon de Irlando. Oni atendu kaj esperu.</p> <div style="text-align:right">Garvan M<span class="mm">AKAJ</span></div> 
