      <h3>Leteroj</h3>
      <h3 class="tem"></h3>
      <h2>Amuza Gubbins</h2>
      <!-- komenco de la enhavo -->

       

      <p>Tre amuzas min legi la kontribuaĵojn al Monato de Paul Gubbins. Oni ĉiam scias precize la tendencon de lia argumentado pri kiu ajn temo. Unue: neniam li diras ion pozitivan pri okcidenta civilizacio; due: li senhezite kaj blinde laŭdas ĉiun landon kaj kvazaŭ-defendas ĉiun diktatoron, kiu aŭdacis kritiki politikiston el demokrata ŝtato pro ekzemple ignorado de homaj rajtoj aŭ registara korupteco; trie: nepre li insinuas mallaŭdon de sia propra lando, Anglio, lumturo de demokratio inter la nacioj, trezorejo de alta kulturo, rifuĝejo kaj azilejo por milionoj da persekutitoj kaj fuĝintoj dum jarcentoj.</p> <p>Oni aldonu ankaŭ, ke Gubbins, sendube ne pro malhonesto, sed nur pro preteratento, plurfoje falsas aŭ kaŝas faktojn, kiuj ne kongruas kun lia argumento. Ekzemple, en sia artikolo <i>Banana Respubliko</i> (M<span class="mm">ONATO</span> 2005/6, p. 12) li ridinde morale ekvivalentigas la elektosistemon de demokrata Britio kun tiu de Zimbabvo, infero surtera, kie dekoj da miloj malsatmortas pro registaraj mismastrumado kaj celkonscia krueleco, kie despotas miliardulo, kiu murdigas je miloj siajn oponantojn.</p> <p>Gubbins sugestas subtile, ke la laborista partio mem de registara ministro Jack Straw falsis balotilojn, kiam fakte temis pri koruptuloj en lokaj filioj de tiu partio. Kaj, malkiel en Zimbabvo, kie la partio de Mugabe organizas tian amasan fraŭdadon, la afero okazis en Britio kontraŭleĝe kaj kun absolute nenia scio flanke de la koncerna partio. Post la nomita kazo en Birmingham okazis alia, ĉi-foje en filio de la konservativa partio, en Bradford. Notindas, ke en ĉiuj kazoj de fraŭdado pri poŝtaj balotiloj eĉ ne unu el la krimuloj naskiĝis en Britio, sed originis el landoj kie balota fraŭdado estas kutima.</p> <p>En la sama numero de M<span class="mm">ONATO</span> aperis artikolo de Gubbins. Mi agrable ŝokiĝis, ĉar li nenie kritikas sian landon en tiu artikolo, ĝis la lasta alineo! Sed li ja ne povis deteni sin tro longe, sen diri ion negativan pri Britio: „Dum najbaroj planas sian estontecon, britoj planas sian estintecon ...”</p> <p>Mi anticipas kun ĝojo pliajn kontribuaĵojn de la estimata redaktoro – eble pri turisma ekspedicio al Nord-Koreio? Tie, kiel en sia „Taglibro” pri sadama Irako, li povus fervore-favore kompari la publikajn konstruaĵojn kun tiuj en Anglio. Aparte li povus kompari la kolosan multajn milionojn kostintan maŭzoleon de la Granda kaj Kara Gvidanto, Kim il-Sung kun la modestaj tomboj de la gereĝoj kaj politikistoj de Britio.</p> <div style="text-align:right">Paul B<span class="mm">LASKOWICZ</span></div> <div style="text-align:right"><i>Germanio</i></div> 
