      <h3>Politiko</h3>
      <h3 class="tem">JAPANIO</h3>
      <h2>Sendependiĝo - ĉu senbaza?</h2>
      <!-- komenco de la enhavo -->

       

        <p>Raportoj el Skotlando pri la sendependiga referendumo kaptis la atenton de loĝantoj
   de Okinavo, la plej suda insularo de Japanio. Same kiel Skotlando, Okinavo havas sufiĉan
   historian kialon por sendependiĝi. Okinavo (antaŭe Rjukju) estis memstara reĝolando
   inter 1429 kaj 1879, kiam ĝin aneksis Japanio.
</p> 
<p>En aprilo 1945 Okinavo fariĝis la loko, kie batalis japana kaj usona armeoj. Tie mortis
   ĉirkaŭ 200 000 japanoj, el kiuj 94 000 estis okinavaj civitanoj – kvarono de la tuta loĝantaro.
</p> 
<p>Post la fino de la milito Usono okupis Okinavon kaj tie kreis la plej gravan strategian
   bazon en la orienta Azio, precipe por la militoj en Koreio kaj Vjetnamio. En 1972
   Okinavo estis redonita al Japanio, sed restis la militbazoj.
</p> 
<h3>Suferas</h3> 
<p>Nuntempe Okinavo, okupanta malpli ol 1 % de la japana teritorio, posedas preskaŭ tri
   kvaronojn de la usonaj militbazoj en Japanio, kiuj kovras 10 % de la insularo. Pro
   la bazoj daŭre suferas la insularanoj, tamen ilia ekonomio forte dependas de la bazoj.
</p> 
<p>Reliefigas la problemon la aerarmea bazo Futenma, laŭdire la plej danĝera en la mondo,
   en la urbo Ginowan. La bazo, ĉirkaŭata de domoj, multe ĝenas la urbanojn ne nur pro
   la bruo sed ankaŭ pro krimoj de la tieaj militistoj kontraŭ la civitanoj. Krome kraŝis
   helikoptero sur la universitaton.
</p> 
<p>Pro tio en 1999 la gubernio en Okinavo akceptis proponon por konstrui novan bazon
   apud la urbo Nago. Tamen la popolo rifuzas, postulante, ke nova bazo estu konstruita
   ekster Okinavo.
</p> 
<h3>Promesis</h3> 
<p>Post la venko de la Liberal-Demokrata Partio en 2012 reviviĝis planoj por krei talusojn
   kaj V-formajn kurejojn en la maro apud Nago. La registaro en Tokio promesis abundajn
   subvenciojn kaj investojn por Okinavo. Tamen en elektoj en 2014 venkis kandidatoj,
   kiuj kontraŭis la translokadon de la aerbazo Futenma, argumentante, ke Okinavo ne
   toleru la ŝarĝon de tiom da militistaj instalaĵoj.
</p> 
<p>Nun Okinavo kontraŭstaras al la centra registaro de Japanio. La registaro de Abe Shinzo
   komencis puni Okinavon. Nek Abe nek multaj ministroj interparolis kun la nova guberniestro
   de Okinavo, kiam tiu vizitis la ĉefurbon. Krome la registaro konsideras malaltigi
   subvenciojn por la insularo.
</p> 
<p>Siavice okinavanoj opinias: „Se Japanio bezonas usonajn bazojn, japanoj egale surŝultrigu sian porcion de la ŝarĝoj”.</p> 
<h3>Subtenas</h3> 
<p>La nuna situacio kaj la historio de Okinavo povus nutri la emon sendependiĝi disde
   Japanio. Antaŭ kelkaj jaroj, tamen, opiniosondado montris, ke nur 5 % de la popolo
   subtenas sendependigon.
</p> 
<p>Aliflanke, se kreskas malfido inter la insularo kaj Tokio, kaj se daŭre interbatalas
   policanoj kun kontraŭuloj de la nova bazo, povas esti, ke pli kaj pli da okinavanoj
   inklinos al sendependigo. 
</p> 
