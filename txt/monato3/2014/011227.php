      <h3>Noveloj</h3>
      <h3 class="tem"></h3>
      <h2>La piano</h2>
      <!-- komenco de la enhavo -->

       

      <p>Estis trankvila antaŭvespero je la fino de la somero. La suno subiris, kaj ĝia mola
   oranĝkolora lumo surverŝis la urbon. Sur la muroj de la konstruaĵoj, sur la vitrinoj
   de la vendejoj kaj sur la fenestroj ludis kuprokoloraj briloj. Komenciĝis la lasta
   horo de la tago, kiam iom post iom ĉio dronis en inkokolora krepusko: la konstruaĵoj,
   la homoj, la arboj.
</p> 
<p>Anton iris sur unu el la grandaj stratoj inter la luksaj vendejoj. Li ne rapidis,
   ĝuis la trankvilan antaŭvesperon. Junaj belaj virinoj kun enigmaj ridetoj pasis preter
   li, kaj iliaj multekostaj parfumoj diskrete odoris. Anton ŝatis la lastajn somerajn
   tagojn, kiam jam ne estis tiel varme, la suno ne estis blindiga kaj la friska vento
   kiel birdflugilo karesis la vizaĝojn.
</p> 
<p>Subite Anton haltis. Antaŭ li venis la virino, kiun li jam duan aŭ trian fojon vidis
   sur tiu ĉi strato. Ŝi paŝis malrapide kaj enpensiĝinte. Ŝia hela rigardo kvazaŭ flugis
   super la homaj kapoj. Ŝi ne estis juna, eble jam kvindekjara, sed ankoraŭ estis bela,
   la krispa grizeca hararo libere falis sur ŝiajn ŝultrojn. Ŝia korpo estis tenera,
   ŝiaj piedoj – graciaj. Nun ŝi surhavis blankan robon, kiu emfazis ŝian sveltan korpon. Verŝajne
   ofte ŝi iris sur tiu ĉi strato, ĉar ŝi ne rigardis la vendejojn kaj ne atentis pri
   la homoj, kiuj preterpasis. Anton malrapidigis siajn paŝojn. Iam antaŭ tre longe ŝajne
   li vidis tiun ĉi virinon, sed li ne memoris, kie kaj kiam. Tiu ĉi inteligenta vizaĝo,
   la delikataj lipoj kaj la lagobluaj okuloj estis konataj al li. Ŝajne li ne nur vidis,
   sed same sonĝis pri ŝi. Vere la jaroj lasis siajn signojn sur ŝian vizaĝon. Ĉe la
   anguloj de ŝiaj okuloj kaj sur la kolo jam videblis etaj sulkoj, kaj ŝia korpo similis
   al matura sukoplena frukto. „Kiu estas tiu ĉi virino, kiun eble mi konas?” demandis sin Anton.
</p> 
<p>Ŝi preterpasis lin, kaj li kvazaŭ eksentis la susuron de ŝia tenera blanka robo. Stranga
   estas la homa memoro. Ni renkontas iun, vidas lian aŭ ŝian vizaĝon, ekscias la nomon,
   sed post jaroj tiu ĉi vizaĝo malaperas aŭ restas profunde en nia konscio kiel pala
   rememoro, simila al malnova flava fotografaĵo, kaj vane ni penas pli bone trarigardi
   tiun ĉi fotografaĵon, ke ni rememoru, kiam kaj kie ni vidis la homon sur ĝi, kiu li
   estas, kio estas lia nomo, sed vane. Ĉio delonge malaperis. La tempo viŝis ĉion, kiel
   la maraj ondoj viŝas la paŝospurojn sur la akva sablo.
</p> 
<p>Jam du fojojn sur tiu ĉi strato Anton vidis tiun ĉi virinon, kaj ĉiam li streĉe rigardis
   ŝian vizaĝon. Li estis certa, ke iam li vidis ŝin, sed li ne kuraĝis alparoli ŝin,
   nek postsekvi ŝin. Tamen hodiaŭ, kiam ŝi denove preterpasis lin, li turnis sin kaj
   ekiris post ŝi. Ŝi iris trankvile. Hodiaŭ ŝi surhavis malhelbluan robon, kiu simile
   al boata velo aŭ aperis, aŭ malaperis inter la homoj, kiuj iris sur la trotuaro. Anton
   iris post ŝi kaj estis certa, ke ŝi ne turnos sin, nek supozas, ke iu postsekvas ŝin.
   Li daŭre diris al si mem: „Mi verŝajne konas ŝin, sed tre delonge mi ne vidis ŝin. Mi ne memoras ŝian nomon.
   Mi ne kuraĝas alparoli ŝin, eble ŝi eĉ ne deziros alrigardi min. Ŝi opinios, ke mi
   estas iu sintrudemulo.”
</p> 
<p>Post ducent aŭ tricent metroj la virino subite haltis kaj eniris ien. Anton rapidigis
   la paŝojn. Estis danctrinkejo kun granda lumreklamo 
   <i>Danctrinkejo Noktaj Triloj</i>. Dum sekundoj li hezitis ĉu eniri aŭ ne. Li demandis sin, kion serĉas ĉi tie tiu
   ĉi virino? Li restis senmova antaŭ la pordo kaj poste tuj eniris. Ene regis duonlumo.
   Li ĉirkaŭrigardis kaj vidis la virinon iri inter la tablojn. „Eble ŝi serĉas iun, eble ŝi renkontiĝos kun iu ĉi tie, aŭ povas esti, ke ĉiutage ŝi
   venas ĉi tien sola, eksidas ĉe iu tablo kaj mendas iun trinkaĵon”, meditis Anton.
</p> 
<p>Subite la virino malaperis kaj Anton embarasiĝis. Ŝi kvazaŭ dronis sub la tero. Li
   ĉirkaŭrigardis, turnis sin dekstren, poste maldekstren, sed nenie vidis ŝin kaj ne
   povis klarigi al si mem, kiel ŝi tiel neatendite malaperis. Li sentis, ke la homoj,
   kiuj sidas ĉe la tabloj, scivole rigardas lin. Ja, li staris kiel arbo en la centro
   de la danctrinkejo. Li decidis tuj eksidi ĉe iu tablo kaj iom atendi. Eble la virino
   aperos de ie. Li sidiĝis ĉe la plej proksima tablo kaj denove ĉirkaŭrigardis. En la
   danctrinkejo estis ĉefe mezaĝaj homoj. Eble ili ofte venis ĉi tien aŭ ĉi tie ili renkontiĝis.
   Anton hontis kaj sentis konsciencriproĉon. Li aspektis kiel fiulo, kiu deziras kaŝe
   enrigardi en ies vivon kaj serĉi en ĝi delonge kaŝitan sekreton. Li decidis tuj stariĝi
   kaj foriri, sed en tiu ĉi momento en la danctrinkejo aŭdiĝis ia bruo, kaj li rimarkis
   la virinon. Eble ŝi eliris el iu ĉambro. Nun ŝi estis vestita en longa nigra robo,
   kaj ŝiaj nudaj ŝultroj brilis kiel marmoraj.
</p> 
<p>Kiel ombro la virino trapasis inter la tabloj kaj sidiĝis antaŭ la piano, kiu estis
   en la angulo de la danctrinkejo. Anton nun rimarkis ĝin. Kelkajn sekundojn ŝi restis
   senmova antaŭ la piano, dorse al la homoj en la danctrinkejo, kaj poste komencis malrapide
   pianludi. Anton stupore rigardis ŝian dorson, ŝiajn manojn blankajn kiel porcelano
   kaj ŝiajn longajn arĝentecajn harojn, kiuj falis sur ŝiajn ŝultrojn. Li aŭskultis
   la melodion, kaj subite en lia konscio komencis elnaĝi kiel fora insulo en maro ia
   rememoro. Unue ĝi estis pala, malklara, simila al forgesita melodio, sed poste ĝi
   iĝis pli klara, kaj kvazaŭ ĉio okazus en tiu ĉi momento antaŭ liaj okuloj. „Tiu ĉi estas Sandra, Sandra Stanislavova, nun mi memoras ŝin”, flustris li. Ŝajne granda kolora radio ekmoviĝis antaŭ li kaj revenigis lin en la
   infanecon.
</p> 
<p>Tiam lia familio loĝis en malnova kvaretaĝa domo en la centro de la urbo. Sur ĉiu
   etaĝo estis du loĝejoj: unu pli granda kaj la alia – pli malgranda. La familio de Anton loĝis en la pli granda loĝejo sur la kvara etaĝo
   kaj en la alia loĝejo estis luantoj, kiuj ofte estis diversaj. Ordinare ili estis
   junaj homoj, ĉefe studentoj. Anton memoris, kiam en la najbara loĝejo ekloĝis junulino.
   Tiam li estis lernanto kaj foje, kiam li revenis el la lernejo, li vidis la junulinon.
   Ŝi estis bela blondulino kun bluaj okuloj, kaj ŝia eleganta korpo similis al violono.
   Ŝi haltis kaj amike ekridetis al Anton. Li ruĝiĝis kaj rapide ekflustris:
</p> 
<p>„Bonan tagon.”</p> 
<p>„Saluton!” diris ŝi. „Mi estas Sandra, ni estas najbaroj.”</p> 
<p>Anton gapis ŝin sorĉita kaj ŝajnis al li, ke la malpura kaj malbela ŝtuparejo kun
   nelavitaj fenestroj subite eklumas kaj ekbrilas. Post tiu ĉi unua renkontiĝo li ofte
   vidis ŝin. Lia patrino kaj Sandra iĝis amikinoj. Sandra gastis en ilia domo kaj kun
   la patrino de Anton ili trinkis kafon. Kiam Anton vidis la lagobluajn okulojn de Sandra,
   li ruĝiĝis, kvazaŭ iu bruligus fajron antaŭ lia vizaĝo, liaj manplatoj komencis ŝviti,
   kaj lia koro ekbatis kiel la koro de timigita leporo. Foje-foje Anton sonĝis pri Sandra.
   Li sciis, ke ŝi estas pianistino. Kiam ŝi estis en sia hejmo, ŝi pianludis, kaj la
   melodio aŭdeblis en la tuta domo. Anton aŭskultis la muzikon emociita kaj imagis ŝiajn
   longajn tenerajn fingrojn, kiuj ame tuŝas la pianklavojn. Tiam li ne sciis, kion pianludas
   Sandra, de kiu komponisto, sed li sentis la muzikon kiel friskan ventoblovon.
</p> 
<p>Kiam liaj gepatroj parolis pri Sandra, ili diris, ke ŝi estas unu el la plej talentaj
   junaj pianistinoj. Ili menciis, ke ŝi multe koncertas kaj ŝia gloro de talenta pianistino
   jam transflugis la ŝtatajn limojn, kaj Anton fieris, ke li konas ŝin kaj ofte renkontas
   ŝin.
</p> 
<p>Foje Sandra venis en ilian domon. Kiam Anton vidis ŝin, li tuj rimarkis, ke ŝi estas
   ĝoja kaj ŝia ridanta vizaĝo aspektas kiel suna printempa mateno. Sandra ĉirkaŭbrakis
   la patrinon de Anton kaj diris:
</p> 
<p>„Onjo Tina, mia plej granda revo realiĝis! Oni invitas min al Vieno. Mi subskribis
   kontrakton kun Viena Filharmonio, kaj mi pianludos en la urbo de la muziko.”
</p> 
<p>„Sandra,” ekĝojis la patrino, „mi estas tre feliĉa, sed mi bedaŭras, ke longe mi ne vidos vin.”</p> 
<p>„Onjo Tina,” diris Sandra kaj iom embarasite alrigardis la patrinon de Anton, „mi havas grandan peton al vi. La plej kara aĵo, kiun mi posedas, estas mia piano.
   Ĉu mi povas lasi ĝin ĉe vi? Mi devas liberigi la loĝejon.”
</p> 
<p>„Kompreneble”, diris ĥore la gepatroj. „Nia loĝejo estas granda, estos loko por ĝi, kaj ni zorgos pri ĝi kiel pri niaj okuloj.”</p> 
<p>„Mi ege dankas al vi. Kiam mi revenos, mi reprenos ĝin.”</p> 
<p>Sandra forveturis, kaj ŝia granda nigra piano restis en la loĝejo de Anton. Oni metis
   ĝin en angulon de la gastĉambro, kaj tiel ili alkutimiĝis al ĝi, kvazaŭ la piano ĉiam
   estus tie. Kiom da fojoj Anton alrigardis ĝin, li kvazaŭ denove vidis la lagobluajn
   okulojn de Sandra, kiuj lumigis lin, kaj lia koro komencis denove bati kiel kuranta
   ĉevalo tra senlima kampo. Anton ofte demandis sin, kiel Sandra fartas en Vieno, ĉu
   ŝi koncertas tie, kaj kiel pasas ŝiaj tagoj en la nekonata lando. Li senpacience atendis
   ŝian revenon el Vieno, sed pasis unu, du, tri jaroj kaj Sandra ne revenis. Oni ne
   sciis, kio okazis al ŝi. Sandra ne skribis leterojn, nek telefonis. La gepatroj de
   Anton provis ekscii ion pri ŝi, sed ili ne sciis ŝian adreson en Vieno. Ŝia piano
   daŭre estis tie, en la angulo de la gastĉambro, kaj ĝi kviete atendis Sandran. Tamen
   post kelkaj jaroj la familio de Anton devis ŝanĝi la loĝejon. Ili aĉetis novan loĝejon,
   kaj kune kun ĉiuj mebloj ili transportis la pianon en la novan loĝejon. Ĝi jam estis
   kvazaŭa ano de la familio. Daŭre neniu sciis ion pri Sandra. Ŝi tute malaperis kaj
   oni jam ne sciis, ĉu ŝi estas en Aŭstrio aŭ en alia lando.
</p> 
<p>Anton iĝis plenkreska viro. Li ne loĝis kun la gepatroj, multe vojaĝis, kaj iom da
   tempo li loĝis en provinco. Kiam liaj gepatroj jam estis certaj, ke Sandra ne revenos,
   ili donacis la pianon al parenca knabineto, kiu komencis lerni pianludadon. Tiel malaperis
   ankaŭ la nigra piano de Sandra.
</p> 
<p>Nun Anton sidis en la danctrinkejo kaj aŭskultis la pianludadon de Sandra. „Kiam ŝi revenis en Bulgarion?” demandis li sin mem. „Eble ŝi serĉis liajn gepatrojn, sed ne trovis ilin, ĉar ili ne plu loĝis en la domo,
   kiun ŝi konis, kaj verŝajne neniu povis diri al ŝi ilian novan adreson.” Delonge la gepatroj de Anton ne estis vivantaj.
</p> 
<p>Nun Sandra ludis ian malrapidan nostalgian melodion, sed neniu en la danctrinkejo
   aŭskultis ĝin. Preskaŭ ĉiuj babilis, ridis, tintigis la glasojn. En la krepuska vasta
   ejo la viroj amindumis, kaj iliaj ebriaj rigardoj brilis kiel la okuloj de embuskantaj
   lupoj. Neniu interesiĝis pri la muziko de Sandra. Anton eksentis ofendon. Sandra,
   la fama kaj talenta pianistino, kiu iam sukcese koncertis en multaj eŭropaj landoj,
   nun pianludis en tiu ĉi duaranga danctrinkejo al ebriuloj, kiuj ne komprenis la muzikon
   kaj tute ne interesiĝis pri ĝi.
</p> 
<p>„Kio okazis al ŝi?” meditis Anton. „Ĉu ŝi edziniĝis, ĉu ŝi havis infanojn, kial ŝi revenis en Bulgarion? En kiuj landoj
   ŝi loĝis, kaj ĉu ŝi ankoraŭ memoras la gepatrojn de Anton kaj Anton mem? Ĉu ŝi serĉis
   sian pianon, kiu atendis ŝin en la loĝejo de Anton?” Al tiuj ĉi demandoj nur Sandra povis respondi, sed nun ŝi sidis antaŭ tiu ĉi malnova
   piano en tiu ĉi danctrinkejo kaj ludis. Ŝi jam estis maljuna, laca virino, kiu eble
   devis pianludi por vivteni sin.
</p> 
<p>Tiu ĉi ne estis Sandra, kiun Anton konis, la iaman junan virinon kun la lagobluaj
   okuloj. Anton stariĝis kaj ekiris al la pordo. „Ni ne devas reiri en la pasintecon. Ĉio restu tie por ĉiam”, diris li al si mem kaj foriris.
</p> 
