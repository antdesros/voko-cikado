      <h2>Monda kulturo apud la maro</h2>
      <!-- komenco de la enhavo -->

       

       <p>En Irlando, kiel en aliaj landoj, la somero estas la preferata sezono por festivaloj. Unu el la plej sukcesaj estas la tiel nomata „Festivalo de mondaj kulturoj”, kiu okazas ĉiujare en la populara feriejo kaj marhaveno Dúnlaoire [dun lire], sude de Dublino, la irlanda ĉefurbo.</p> <p>Homoj el multaj landoj, kaj anoj de eksterlandaj komunumoj daŭre loĝantaj en Irlando, kuniĝas kun irlandanoj por dividi kaj ĝui riĉan paletron da internacia kulturo. Patroprenas dancistoj, luktartistoj, kantistoj, cirkoj, muzikistoj, ĵonglistoj, aktoroj – finfine, ĉiaj folkloroj kaj tradicioj.</p> <p>Cetere dum la festivalo eblas viziti Zen-ĝardenon aŭ ĉeesti kurseton pri bonsajo-arbetoj; lerni ion pri ĉina kaligrafio aŭ hinda tamburado; aŭskulti la ĉanton de tibetaj lamaoj; spekti dancon kaj aŭdi kantojn el Kubo, Koreio kaj Barato, aŭ rigardi aktoradon de scenoj el la hinda Ramajano.</p> <h3>Esplanado</h3> <p>Laŭ la tuta marborda esplanado vidiĝas budoj, ĉe kiuj oni povas manĝi frandaĵojn el Libano, Palestino, Japanio, Hispanio, Francio, Italio, Germanio, kaj multaj aliaj landoj.</p> <p>Regas ĝoja etoso en la urbo, precipe en la Popola Parko en Dúnlaoire. La evento estas bone organizata kaj ordigata de la <i>Garda</i> (polico), la Civila Defendo (kiu fakte ne defendas kontraŭ neekzistantaj malamikoj, sed sukuras la publikon), volontulaj purigistoj, kaj kontrolistoj.</p> <p>La rezulto estas, ke malgraŭ miloj da junuloj (kaj malpli junaj personoj) oni ne notas la malagrablan spektaklon de ebrio kaj konsumado de narkotaĵoj, kiu ofte konstateblas ĉe eventoj kiaj rokfestivaloj. Anstataŭe, la festivalo distingiĝas per afableco kaj amikeco internacia.</p> <h3>Velŝipoj</h3> <p>Ankaŭ la belo de la urbo kontribuas al la sukceso. Kiam la suno brilas – kio ne ĉiam okazas en Irlando – oni regalas sin, promenante apud la maro, sur kiu videblas buntaj velŝipoj kaj jaĥtoj. Ĉe la horizonto montras sin montoj. De tempo al tempo alvenas aŭ forveturas pramŝipoj el, aŭ al, Kimrio. Dume, sur la stratoj de Dúnlaoire, aŭdiĝas la lingvoj de multe da landoj kaj etnoj: hinda, tibeta, brazila, ĉina, japana ... eĉ Esperanto.</p> <p>La semajnfina festivalo daŭras de vendredo ĝis dimanĉo. Ĉi-jare ni partoprenis la sabatan programon, precipe dediĉitan al la kulturo de Azio. Aparta allogaĵo estis la Cirko Kawa el Barato, kies anoj faris nekredeblajn atingojn: promenadon sur tenda ŝnuro, grimpadon sur longa bambua stango apogita sur la ventro de eta barbulo, kiu kulminis per sensacia rotacio de „homa helikoptero” sur la pinto de la stango.</p> <p>Kvankam partoprenis en la festivalo malpli da homoj ol en la pasinteco – certe pro la ekonomia krizo – la publiko estis gaja kaj kontenta kiel ĉiam. La festivalon oni povas varme rekomendi.</p> <div style="text-align:right">Garvan M<span class="mm">AKAJ</span></div>  
