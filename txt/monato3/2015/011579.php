      <h3>Spirita vivo</h3>
      <h3 class="tem">OPINIO</h3>
      <h2>Jesuo fuĝas en Egiption, kiel ankaŭ aliaj nun fuĝas</h2>
      <!-- komenco de la enhavo -->

       

       <p>„Jozefo prenis la knabeton kaj lian patrinon nokte, kaj migris en Egiptujon.” Jen la enhava kerno – se preterlasi Diajn intervenojn kaj Malnovtestamentajn profetaĵojn – de la versikloj 13-18 en la dua ĉapitro de la evangelio laŭ Sankta Mateo.</p> 
<p>Ekzegezistoj kaj pentristoj el tiu teksto formis en la menso de la kredema popolo
   preskaŭ idilian scenon: Sub steloplena ĉielo Sankta Jozefo migras kviete tra nokta
   pejzaĝo. Post si li gvidas azenon, sur kiu duondorme sidas Sankta Maria, kun en la
   brakoj la beba Jesuo. Fore anĝelo lumigas la vojeton. Sed – se entute –, tamen pli realisme okazis tiel: Jozefo hastis antaŭ la persekutado fare de la tirano
   Herodo. Al ĉelima gardostaranto li kaŝe enmanigis konsiderindan sumon kaj poste malaperis
   anonime en proksima urbo, por tie pene perlabori kiel domkonstruisto la vivbezonaĵojn
   de la familio.
</p> 
<p>Aktuale multege da homoj el la Proksima Oriento kaj Norda Afriko migras en Eŭropon,
   por tie ekloĝadi. Intertempe oni sendistinge nomas ilin „rifuĝantoj”. Inter ili estas sendispute la vere persekutitaj personoj, kiuj meritas klasikan
   azilon. Sed granda kvanto venas pro aliaj kialoj (tie ĉi ne aparte listigendaj). Pri
   tio nun daŭre okazas disputoj en Eŭropo inter homoj / partioj, kiuj volas vaste malfermi
   la propran landon / tutan Eŭropon, kaj aliaj, kiuj pli sobre pripensas la situacion
   rigarde al la eblo ĉiun migremulon akcepti, al la propra religio / kulturo kaj al
   la morgaŭo, kiu alportos eble eksplodan plimultiĝon kaj senfinan grandkvantan alvenon.
   Ĉar tiaj konsideroj estas pli fortaj en religie solidaj regionoj, la malfermemuloj,
   kiuj opinias sin la veraj bonaj homoj, draŝas per la idilia bildo supre montrita certajn
   eŭropajn kristanojn: „Jen eĉ via kara Jesuo devis fuĝi. Kaj vi restas ŝtonkoraj antaŭ la mizero de freŝe
   alvenintaj homoj.”
</p> 
<p>Tamen, la situacio ne estas tiel simpla, kiel volas dedukti el la biblia bildo la
   plej ofte senreligiaj akuzantoj. En la germana Bavario oni devis forpreni la krucojn
   de sur la lernejaj vandoj, sed ĉirkaŭe kreskas moskeoj kaj islama vivostilo. Pollando,
   post longa subpremo, konsolidiĝis kadre de la katolika konfesio, fronte al la rusa
   ortodoksa mistiko kaj la pruse protestanta militemo. Ĉu mirinde, ke ili aktuale volas
   akcepti nur kristanajn azilpetantojn?
</p> 
<p>Kion pri la baltaj ŝtatoj, kiuj ankoraŭ luktas kun la problemoj de la rusa enmigrado
   dum la sovetia tempo? Kion pri Hungario ekzemple, kiu spertis post 1526 preskaŭ plenan
   detruiĝon fare de la osmanaj trupoj; pri Hispanio, kiu bezonis pli ol 700 jarojn por
   repeli la arabajn invadintojn? Serbio ĵus perdis ne nur siajn praserbajn monaĥejojn
   al Kosovo. Kiu rajtas postuli, ke tiuj landoj varme akceptu islamanojn, eble kun militista
   fono ĉe IS, Boko Haram aŭ aliaj grupoj?
</p> 
<p>La germanaj Verduloj hurlas, se homoj en tiu lando preferus akcepti mortminace persekutitajn
   kristanojn el Sirio anstataŭ sendistinge apliki „bonvenigan kulturon” al homoj kun plene aliaj vivmaniero kaj religio. Ja kompreneble, ĉar la partiestro
   kaj multaj ŝtataj „respondeculoj pri integrado” jam estas islamanoj.
</p> 
<p>Kaj nenie leviĝas la demando, kial islamanoj el la koncernaj regionoj ne pretendas
   protekton ĉe la samkredantoj en la riĉegaj Emirlandoj kaj Sauda Arabio. Aŭ kial tiuj
   landoj ne finas la kruelajn agadojn de certaj trupoj en Sirio kaj Irako ne nur kontraŭ
   kurdoj, kristanoj kaj jezidoj, sed ankaŭ kontraŭ islamaj samreligianoj.
</p> 
<p>Jes ja, Jesuo diris: „Kion vi faris al la plej mizera de miaj fratoj, tion vi faris por mi.” Sed Sanka Mateo daŭrige raportas ankaŭ, ke Jozefo remigris post la minaco en sian
   patrujon kun Maria kaj Jesuo. Kaj ili estis nur triope, sen poŝtelefono. Tion prisilentas
   la homoj, kiuj ja asertas: „Ni (!) estas malferma lando kaj bonvenigas ĉiun, kiu volas vivi ĉe ni”, sed ne volas antaŭvidi la konsekvencojn de eksponenta enmigrado, kiuj subfosos la
   leĝan azilrajton kaj okazigos la plenan transformiĝon de la aŭtentaj eŭropaj kulturoj,
   la rapidan renverson de longtempe kreskintaj religiaj strukturoj. Kiam la aktuala
   mono elĉerpiĝos, ne plu estos demando de kulturo kaj religio, sed de sociaj kaj interetnaj
   konfliktoj terene de Eŭropo sub la sanktaj signoj de kruco kaj duonluno.
</p> 
