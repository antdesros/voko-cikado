      <h3>Libroj</h3>
      <h3 class="tem"></h3>
      <h2>Rigardo sur la milito en Alĝerio</h2>
      <!-- komenco de la enhavo -->

       

     <p>Unue, klarigoj pri la titolo de la libro: „Tuikero” estis la propra nomo de alĝeria harkio, amiko de la aŭtoro. „Harkio” estas ĝenerala alnomo de islamaj soldatoj alĝeriaj, kiuj batalis flanke de la francoj, kontraŭ alĝeriaj sendependismaj soldatoj dum la milito inter Francio kaj Alĝerio (1954-62).</p> <p>Roger Condon estis 19-jara konskripto, kiu servis dum 21 monatoj en la franca armeo. La libro temas pri lia nuna memoro pri tiu konskripta sperto, surbaze de liaj notoj el tiu periodo plus nun nur pluenmensaj memoroj. Do aŭtobiografia verko, tre interesa kaj, krome, informa.</p> <p>Malgraŭ la titolo <i>Tuikero la Harkio kaj la cetero</i>, la persono Tuikero preskaŭ ne rolas en la rakonto, kiu konsistas preskaŭ ekskluzive el „la cetero”. Tamen tiu „cetero” estas multenhava, kaj vere leginda.</p> <p>Condon ne malŝparas siajn paĝojn per rakontado pri bataloj, atakoj kaj aliaj fimilitaĵoj (lia takso, krome ankaŭ mia). Anstataŭe li priskribas sian vivon dum tiuj jaroj, la multajn stultajn ordonojn faritajn de armeestroj, komforte sidantaj en Francio tute sen scio pri la realaj kondiĉoj, kiujn frontas la francaj armeanoj en Alĝerio; la neniam oficiale agnoskitan uzadon de metodoj, akceptataj ambaŭpartie, por ebligi ĉiutagan vivon sen danĝera interkonflikto ktp ktp.</p> <p>La fiforlaso de la „harkioj” fare de la franca arme-administrantaro, tiel ke tiuj alĝeriaj franc-helpintaj harkioj suferis persekuton kaj murdadon en Alĝerio, pro tio ke la franca armeo memplaĉe „forgesis” ilin post la fino de la milito, forte skuas la senton de la aŭtoro pri la „civiliziteco” de la francaj agoj kaj principoj.</p> <p>Kvankam, prave, kontraŭmilita, ĉi tiu libro konigas nur la vidpunkton de sia aŭtoro pri la ĉiutaga soldata vivo kaj ne tede ripetadas laŭ la maniero de propagandaj verkoj. Tial kaj por- kaj kontraŭ-milituloj povas ĝui la rakonton kaj kunsenti kun ĝia verkinto.</p> <p>La libro estas tre fortike broŝurita. Oni tre zorge korektocele tralegis ĝin, tiel ke kritikinda estas nur la kvalito de la papero uzita.</p> <div style="text-align:right">Donald B<span class="mm">ROADRIBB</span></div> <h6 class="bib">Roĝero Kondono (Roger Condon): Tuikero la Harkio kaj la cetero. Eld. Eldona Fako Kooperativo de SAT, (loko ne indikita), 2010. 150 paĝoj, kudre broŝurita. ISBN 978-2-918053-04-0, laŭ paĝo 6; 978-2-918053-02-6 laŭ la malantaŭa kovropaĝo.</h6>  
