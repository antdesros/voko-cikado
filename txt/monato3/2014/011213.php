      <h3>Politiko</h3>
      <h3 class="tem">AŬSTRIO</h3>
      <h2>Modelo por Eŭropo</h2>
      <!-- komenco de la enhavo -->

       

       <p>Kompromison atingis la du partioj, kiuj ekde la dua mondmilito dominas la politikan vivon en Aŭstrio, por daŭrigi la tielnomatan grandan koalicion (2008-13).</p> <p>Du monatojn kaj duonon post la parlamentaj elektoj interkonsentis la socialdemokratoj (SPÖ), gvidataj de Werner Faymann, kaj la kristandemokratoj (ÖVP), gvidataj de Michael Spindelegger, plu kunlabori por solidigi la ŝtatajn financojn kaj pligrandigi la ekonomian kreskon kaj okupatecon.</p> <p>La kanceliero Faymann, prezentante la novan koalician registaron, deklaris, ke oni ellaboris ampleksan, detalan laborprogramon, kiu enhavas la „plej bonan” de ambaŭ partioj.</p> <p>Reage al kritikoj pri mankantaj grandaj reformoj diris Faymann: „Oni ne devas ree malkovri la sukcesan landon Aŭstrio. Ni estas modelo por Eŭropo.”</p> <h3>Intencoj</h3> <p>Dum la enoficigo kaj ĵurigo de la nova registaro protestis centoj da studentoj. Ili esprimis sian malkontenton pri la likvido de la memstara ministrejo pri scienco kaj esploroj kaj ĝia aligo al la ministrejo pri ekonomio. Tio estas unu el la nemultaj reform-intencoj de la koalicia registaro.</p> <p>Unu el la celoj de la nova registaro dum la sekvaj kvin jaroj estas atingi ĝis 2016 „strukturan nuldeficiton”. „Tial necesas plialtigi impostojn kaj fari reformojn”, diris Faymann.</p> <p>Oni altigos la tabakimposton, alkoholimposton kaj imposton je la allaso de aŭtoj kaj motorcikloj. Krome la registaro intencas plialtigi ĝis 2018 la nunan mezan aĝon de emeritiĝo de 58,4 al 60 jaroj.</p> <p>La vickanceliero Spindelegger notis, ke ambaŭ partioj konsentis pri ŝparado, inkluzive de publikaj administraj kostoj, kaj ankaŭ stimulos ekonomian kreskon kaj okupatecon.</p> <p>Kritikantoj, inkluzive la opoziciajn Liberecan Partion kaj verdulojn, deklaris, ke Aŭstrio reformu la publikajn financojn kaj pensiojn kaj reduktu la ŝvelintan burokrataron. Tiel eblos garantii la vivnivelon en la lando kun la plej malalta senlaboreco en EU.</p> <h3>La nova registaro</h3> <p>La nova registaro konsistos el 15 homoj: krom kanceliero Faymann, sep estas socialdemokratoj kaj sep kristandemokratoj. La postenon de ministro pri eksteraj aferoj transprenas la 27-jara Sebastian Kurz (ÖVP).</p> <p>Li estis ĝis nun ŝtata sekretario pri integrado kaj ne disponas pri diplomata sperto. Krome li estas la plej juna ministro pri eksteraj aferoj en EU. La ministro pri justico estas la sperta advokato Wolfgang Brandstetter, kaj la ministrino pri junularo kaj familio estas Sophie Karmasin.</p> <p>Kritikantoj, eĉ el la koaliciaj partioj mem, atentigas, ke la nova registaro povas estigi stagnadon en la aŭstra politiko.</p> <div style="text-align:right">Evgeni G<span class="mm">EORGIEV</span></div>  
