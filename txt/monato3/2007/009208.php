       <p>Komence de 2007 en Litovio loĝis 1 807 000 virinoj. Tio signifas, ke virinoj superis virojn je 229 000, do por 100 viroj estis 115 inoj.</p> <p>Ĉu inoj superas virojn nur kvante? Tute ne. Inoj distingiĝas per plia emo al studado; viroj tamen al pli ofta uzo de komputiloj kaj Interreto. Cetere ĉirkaŭ 60 % de la litovaj studentoj kaj 58 % de la doktoriĝintoj estas virinoj.</p> <p>Virinoj koncentriĝas precipe en urboj: en kelkaj kvartaloj estas por 100 viroj 120 inoj. En 2006 la meza aĝo de litova virino estis 40,7 jaroj, de viro 35,9.</p> <div style="text-align:right">last</div> 
